content: str = """
I secondi sono la portata principale: carne, pesce, pollame. Il servizio
deve rispettare la cottura richiesta e presentare con cura.

**Verifica Cottura**
Per carne: chiedere grado di cottura (al sangue, media, ben cotta) al
momento dell'ordine. Riportare esattamente in cucina. Per pesce:
verificare se intero o filetto, e preferenze su pelle e lische.

**Contorni**
I secondi possono includere contorno o essere serviti senza. Offrire
contorni a parte se non inclusi: verdure di stagione, insalata,
patate. In Italia il contorno spesso si ordina separatamente.

**Salse e Condimenti**
Servire salse a parte quando possibile, per permettere all'ospite
di dosare. Non versare salsa sul piatto senza chiedere.

**Tempi**
Il secondo segue il primo dopo una pausa adeguata. L'ospite segnala
quando è pronto; non portare il secondo mentre sta ancora mangiando
il primo.
"""  # noqa: E501

