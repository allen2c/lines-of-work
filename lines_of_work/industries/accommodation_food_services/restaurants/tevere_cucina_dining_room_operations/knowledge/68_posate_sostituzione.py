"""Quando e come sostituire posate e bicchieri durante il servizio."""

title: str = "Sostituzione Posate e Bicchieri"

content: str = """
Le posate si cambiano tra le portate quando necessario. La regola generale:
fornire posate pulite per ogni nuova portata che lo richieda.

**Tra le Portate**
Dopo l'antipasto: raccogliere posate usate, portare posate per il primo.
Dopo il primo: posate per il secondo. Per il dolce: cucchiaino e forchetta
da dessert. Non accumulare posate sul tavolo; ritirare quelle usate prima
di portare le nuove.

**Bicchieri**
Un bicchiere caduto o sporcato va sostituito immediatamente. Per degustazioni
vino, nuovi bicchieri per ogni vino se lo stile lo richiede. Altrimenti,
lo stesso bicchiere può servire per vini simili (es. due rossi).

**Discrezione**
Sostituire senza attirare l'attenzione. Ritirare da destra, servire da
sinistra. Non toccare le posate con le mani dove l'ospite le impugna.
"""  # noqa: E501

