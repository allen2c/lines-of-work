title: str = "FIFO and Product Rotation"

content: str = """
First in, first out (FIFO) ensures that older product is used before newer
product, reducing spoilage and waste.

**Labeling**: Every prepped item and received product receives a date label:
prep date or use-by date. Labels are legible and placed where they remain
visible until the product is used.

**Storage Organization**: New product goes behind or beneath older product.
Stations pull from the front. Walk-in shelving is organized by category and
date. Raw proteins on bottom; ready-to-eat above. Produce has designated
zones; ethylene producers (e.g., apples) separated from sensitive items.

**Rotation Checks**: Daily walk-through verifies that no expired or borderline
product remains. Questionable product is discarded. Short-dated items are
prioritized in prep and specials.
"""  # noqa: E501

