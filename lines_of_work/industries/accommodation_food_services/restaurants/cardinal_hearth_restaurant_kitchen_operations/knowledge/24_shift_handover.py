content: str = """
Smooth handovers between shifts ensure continuity and prevent gaps in prep or
communication.

**What to Communicate**: Prep status — what is done, what is in progress, what
is needed. Par levels — what is running low. 86's and substitutions. Any
equipment issues or maintenance needs. Special events or large reservations
coming up.

**Documentation**: Use prep lists and boards. Update them throughout the shift.
Incoming shift should review before assuming responsibility. Verbal handover
supplements written documentation; do not rely on memory alone.

**Timing**: Allow overlap for handover. Rushed transitions lead to missed
information. The outgoing shift stays until the incoming shift is oriented.
"""  # noqa: E501

