content: str = """
While the kitchen bears primary responsibility for food safety, front-of-house
staff play a critical role in maintaining standards from the pass to the table.
Proper handling prevents contamination, temperature abuse, and allergen exposure.

**Temperature Control**

Temperature is the front line of food safety:

- **Hot Foods**: Must arrive at the table above 135°F (57°C). If plates cool
during service, warming lamps or expedited delivery is required. Do not allow
hot food to sit at the pass.

- **Cold Foods**: Must remain below 41°F (5°C). Salads, ceviches, and cold
appetizers should be delivered promptly without delay at room temperature.

- **Time as Control**: Foods in the "danger zone" (41°F - 135°F) for more than
4 hours must be discarded. In practice, this means service staff must deliver
 promptly and clear finished plates.

**Allergen Management**

Allergen protocols extend beyond the kitchen:

- **Clear Communication**: Ensure kitchen confirms allergen-free preparation;
visually inspect plates for obvious contaminants (nuts on a nut-free order).

- **Dedicated Service**: Use separate trays or delivery methods for allergen-free
plates to prevent cross-contact during transport.

- **Guest Verification**: Confirm with the guest that the dish meets their
restriction when delivering: "This is the shellfish-free paella as requested."

**Proper Handling Techniques**

- **Hand Hygiene**: Wash hands after touching face, hair, or non-food items;
after handling money; after bussing tables; and between handling raw and ready-to-eat foods.

- **Glass and Plate Handling**: Hold glasses by stems or bases, never by rims
where lips will touch. Carry plates without touching food surfaces.

- **Utensil Management**: Do not touch eating surfaces of flatware. If a fork
or knife falls, replace with clean utensils; do not wipe and reuse.

**Illness Protocol**

Staff showing symptoms of foodborne illness (vomiting, diarrhea, jaundice,
or infected wounds) must not handle food or food-contact surfaces. Management
must reassign such staff immediately.

**Cleaning and Sanitizing**

- Use separate cloths for food-contact surfaces vs. non-food areas
- Sanitize tables between seatings with approved disinfectant
- Ensure sanitizer buckets maintain proper concentration per manufacturer specifications
- Replace sanitizing solutions regularly (typically every 2-4 hours or when visibly soiled)

**Documentation**

Health inspections cover front-of-house areas. Maintain records of:
- Sanitizer concentrations
- Temperature logs for holding equipment
- Staff health screening records
- Cleaning schedules and completion

Front-of-house food safety is the final barrier between kitchen preparation
and guest consumption. Vigilance at this stage protects both guest health
and restaurant reputation.
"""

