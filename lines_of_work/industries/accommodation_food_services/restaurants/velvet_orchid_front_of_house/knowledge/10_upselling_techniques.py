content: str = """
Upselling in fine dining differs from casual establishments. The goal is not
merely increasing check averages but enhancing the guest experience. Every
suggestion should feel like personalized service, not sales pressure.

**The Guiding Principle**

Suggest items that legitimately improve the dining experience for this specific
guest. If a guest orders a red meat entree, wine pairing is a natural enhancement.
If celebrating a birthday, a special dessert presentation adds value. The guest
should feel advised, not sold to.

**Natural Upselling Opportunities**

- **Beverage Pairing**: Suggest wine by the glass or bottle based on ordered dishes.
Frame as enhancement: "The sommelier recommends the Pinot Noir with your duck —
shall I bring a glass to accompany the dish?"

- **Supplemental Courses**: Suggest additions that create a more complete experience.
"Many guests enjoy starting with our seafood tower before the tasting menu. Would
you like to see that option?"

- **Premium Alternatives**: Offer upgrades when they provide discernible value.
"Our dry-aged ribeye is available tonight for an additional $25. Given your
appreciation for steak, it might be worth considering."

- **Shareable Enhancements**: Suggest sides or appetizers for the table. "The
truffle fries are excellent for sharing — shall I add those for the table?"

- **Experience Additions**: Special menu tastings, chef's table options, or
wine flights that create memorable moments.

**Language That Sells**

- Use descriptive adjectives: "buttery," "house-made," "locally-sourced"
- Create scarcity appropriately: "We only have two orders of the special remaining"
- Offer rather than ask: "I'll bring the wine list so you can select a pairing"
instead of "Do you want wine?"

**Reading Guest Signals**

Not all guests welcome upselling. Indicators of receptiveness:
- Asking about specials or recommendations
- Expressing openness: "What do you suggest?"
- Celebratory context (dates, anniversaries, business entertainment)
- Previous positive responses to suggestions

Signs to reduce upselling:
- Stated budget constraints
- Decisive ordering without seeking input
- Indicators of price sensitivity (ordering lowest-priced items)
- Time pressure expressed by guest

**The Graceful Decline**

When a guest declines an upsell, accept without pressure or attitude. A simple
"Of course" maintains goodwill. Never imply judgment about the decision.

**Measuring Success**

Track upselling metrics by individual server but prioritize guest satisfaction
scores. A server with lower check averages but higher satisfaction may be building
long-term loyalty that exceeds immediate revenue.
"""

