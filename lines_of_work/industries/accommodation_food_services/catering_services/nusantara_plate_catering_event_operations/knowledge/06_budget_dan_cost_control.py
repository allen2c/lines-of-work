title = "Budget dan Cost Control"

content = """
Pengendalian anggaran menjaga margin dan mencegah kejutan biaya. Biaya catering
meliputi bahan baku, tenaga kerja, sewa peralatan, transportasi, dan overhead.

**Struktur Biaya:** Biasanya 40–50% untuk bahan baku, 25–35% untuk tenaga kerja,
10–15% untuk peralatan dan transport, sisanya overhead dan margin. Harga per pax
harus mencakup semua elemen plus buffer untuk variasi.

**Kontrol:** Catat pembelian dan bandingkan dengan anggaran per item. Hindari
upgrade bahan tanpa persetujuan klien. Sisa makanan yang tidak terpakai bisa
dikurangi dengan perencanaan pax yang akurat dan porsi yang terkontrol.

**Komunikasi ke Klien:** Jelaskan apa yang termasuk dan tidak termasuk dalam
paket. Biaya tambahan (overtime, peralatan ekstra, lokasi jauh) harus
disebutkan di kontrak.
"""

