content = """
Run of show (ROS) adalah dokumen waktu yang merinci urutan kegiatan dari setup
hingga breakdown. Semua pihak — klien, vendor, staf — harus mengacu pada dokumen
yang sama.

**Elemen ROS:** Waktu kedatangan tim, mulai setup, selesai setup, tamu mulai
datang, jam makan (atau beberapa sesi), jeda, acara inti (toast, pemotongan kue),
hingga breakdown dan keberangkatan. Sertakan buffer 15–30 menit untuk transisi.

**Koordinasi:** Bagikan ROS ke klien, venue, dan vendor lain (dekorasi, sound system)
agar sinkron. Perubahan harus dikomunikasikan segera. Pada hari-H, koordinator
harus memegang salinan dan memantau kemajuan secara real-time.

**Keterlambatan:** Jika ada delay, prioritaskan waktu makan — tamu yang kelaparan
adalah prioritas. Sesuaikan urutan lain jika perlu, dan informasikan pihak terkait.
"""

