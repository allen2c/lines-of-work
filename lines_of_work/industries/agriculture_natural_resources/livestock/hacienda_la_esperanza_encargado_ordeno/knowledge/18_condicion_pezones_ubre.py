title = "Condición de los pezones y la ubre"

content = """
- Inspeccionar visualmente cada ubre antes del ordeño: buscar enrojecimiento, hinchazón, heridas, verrugas o pezones dañados.
- Los pezones deben estar limpios y sin grietas. Si hay grietas o callosidades, aplicar ungüento emoliente después del ordeño.
- Si un pezón tiene una lesión abierta, no colocar la pezonera en ese cuarto. Ordeñar manualmente y aplicar tratamiento antiséptico.
- Las ubres con edema (hinchazón) deben ser ordeñadas con cuidado, usando pezoneras de menor vacío si es posible. Reportar al veterinario.
- Registrar cualquier anomalía en la condición de la ubre en el sistema.
"""

