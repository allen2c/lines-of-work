title = "Control de calidad de la leche en el tanque"

content = """
- Después de cada ordeño, tomar una muestra del tanque de leche (500 mL) en un frasco estéril, etiquetar con fecha, turno y lote.
- Medir la temperatura de la leche en el tanque: debe estar entre 1-4°C. Si supera 4°C, revisar el sistema de enfriamiento y registrar.
- Realizar prueba de conductividad eléctrica: valores normales 4-5 mS/cm. Valores >5.5 mS/cm indican posible mastitis o contaminación.
- Tomar una muestra para recuento de células somáticas (RCS) usando un contador portátil (DeLaval o similar). El objetivo es <200,000 células/mL. Si supera 250,000, investigar.
- Registrar todos los resultados en el libro de calidad. Si el RCS supera 400,000, no enviar la leche y notificar al jefe de producción.
"""

