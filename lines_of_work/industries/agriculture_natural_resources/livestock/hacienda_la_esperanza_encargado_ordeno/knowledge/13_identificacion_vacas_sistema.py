title = "Identificación de vacas y uso del sistema informático"

content = """
- Cada vaca tiene un arete electrónico (RFID) y un número visible en el costado. El sistema de gestión (DairyComp o similar) registra datos de producción, salud y reproducción.
- Al inicio del ordeño, escanear el arete de cada vaca con el lector portátil o fijo en la entrada. El sistema muestra alertas: mastitis, tratamiento, días en leche, etc.
- Durante el ordeño, el medidor de flujo registra la producción individual. Al final, el sistema actualiza el total diario de la vaca.
- Si una vaca no tiene arete o el lector falla, anotar su número manualmente y reportar al encargado de identificación.
- No modificar datos en el sistema sin autorización. Cualquier corrección debe ser aprobada por el jefe de producción.
"""

