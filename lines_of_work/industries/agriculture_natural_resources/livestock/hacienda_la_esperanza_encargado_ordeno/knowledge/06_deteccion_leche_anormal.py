title = "Detección de leche anormal y mastitis clínica"

content = """
- Durante el descarte de primeros chorros, observar: coágulos (mastitis), sangre (trauma o infección), leche acuosa o con grumos, color amarillento o rosado.
- Si se detecta anormalidad, realizar la prueba de California Mastitis Test (CMT) en el cuarto afectado: mezclar 2 mL de leche con 2 mL de reactivo, agitar 15 segundos, leer la reacción (negativa, traza, +1, +2, +3).
- Clasificar: +1 o +2 = mastitis subclínica; +3 = mastitis clínica. Registrar en el sistema.
- Para mastitis clínica (+3): aislar la vaca, ordeñarla al final del lote, no mezclar su leche con el tanque general, y aplicar tratamiento según protocolo veterinario (antibiótico intramamario, 3 dosis cada 12 horas).
- Si hay más de 2 vacas con mastitis clínica en un turno, notificar al veterinario.
"""

