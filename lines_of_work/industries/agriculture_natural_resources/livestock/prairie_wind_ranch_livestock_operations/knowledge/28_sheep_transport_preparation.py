title = "Sheep Transport Preparation"

content = """
Sheep require careful handling and preparation for transport to minimize
stress and injury. Wool and breed affect heat tolerance during transit.

**Pre-Transport:** Withhold feed 12 to 24 hours; continue water access.
Do not ship sick, lame, or heavily pregnant ewes. Ensure health papers
and scrapie program compliance. Shear if weather and timing permit to
reduce heat stress; avoid shipping recently shorn sheep in cold conditions.

**Loading Density:** Provide adequate space for sheep to stand and brace.
Overcrowding increases injury and mortality. Consider breed size and
wool condition when determining numbers per compartment.

**Post-Arrival:** Offer water immediately. Introduce feed gradually.
Observe for respiratory disease and stress-related conditions. Isolate
and treat affected animals promptly.
"""  # noqa: E501

