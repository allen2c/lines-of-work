title = "Neonatal Diarrhea Response"

content = """
Neonatal diarrhea (scours) can cause rapid dehydration and death. Early
treatment and supportive care improve survival.

**Signs:** Loose or watery feces, dehydration (sunken eyes, skin tent),
weakness, and reluctance to nurse. Cause may be bacterial, viral, or
parasitic; veterinarian can advise on diagnostics and treatment.

**Supportive Care:** Provide fluids orally or intravenously as directed.
Continue to offer colostrum or milk; electrolytes may be given between
nursing. Keep calves or lambs warm and dry. Isolate to prevent spread.

**Prevention:** Ensure adequate colostrum intake. Maintain clean calving
or lambing areas; rotational use of pastures reduces pathogen buildup.
Vaccinate dams when recommended. Minimize stress during neonatal period.
"""  # noqa: E501

