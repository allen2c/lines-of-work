title = "Lameness Assessment and Initial Response"

content = """
Lameness reduces mobility, feed intake, and welfare. Early identification
and appropriate response improve outcomes.

**Observing Gait:** Watch for shortened stride, weight shifting, reluctance
to bear weight, or abnormal limb placement. Isolate lame animals for closer
examination and to prevent bullying. Document which limb and severity.

**Common Causes:** Foot rot, abscess, injury, joint infection, or structural
issues. Do not trim or treat without identifying the cause; improper
intervention can worsen some conditions. Contact veterinarian for
persistent or severe lameness, or when cause is uncertain.

**Hoop and Prevention:** Maintain dry, clean areas around water and feed.
Trim feet when indicated; use proper restraint and sanitation. Quarantine
and treat new arrivals to reduce introduction of infectious causes.
"""  # noqa: E501

