title = "Animal ID and Traceability Requirements"

content = """
Animal identification supports disease traceability, performance records, and
compliance with movement regulations. Prairie Wind Ranch follows national and
state requirements for cattle and sheep.

**Cattle:** Official ID (e.g., USDA ear tag or RFID) is required for interstate
movement and for animals over 18 months in many programs. Apply tags according
to manufacturer instructions; record tag number with animal description and
ownership. Report lost tags and re-tag as needed.

**Sheep:** Scrapie program requirements apply; consult current regulations for
flock identification and tag specifications. Record all official IDs on
movement documents.

**Record Retention:** Maintain identification records for the duration of
ownership and as required by regulation. Cross-reference with breeding,
health, and movement records.
"""  # noqa: E501

