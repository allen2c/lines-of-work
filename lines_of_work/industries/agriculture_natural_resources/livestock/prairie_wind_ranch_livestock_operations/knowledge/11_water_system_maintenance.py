title = "Water System Maintenance"

content = """
Reliable water access is critical for animal health and performance. Inspect
troughs, tanks, and water lines regularly and address issues promptly.

**Daily Checks:** Verify water flow and levels during routine pasture checks.
Ensure float valves function; repair or replace faulty hardware to prevent
overflow or empty tanks. Remove debris and algae buildup from troughs.

**Seasonal Maintenance:** Before freeze-up, drain or protect exposed lines and
valves. Check automatic waterers for freeze damage after cold events. In summer,
monitor for algae and bacterial growth; clean and disinfect as needed.

**Pasture Development:** When establishing new water points, consider flow rate,
capacity for herd size, and distance from grazing areas. Livestock should not
travel more than one-quarter mile to water under normal conditions.
"""  # noqa: E501

