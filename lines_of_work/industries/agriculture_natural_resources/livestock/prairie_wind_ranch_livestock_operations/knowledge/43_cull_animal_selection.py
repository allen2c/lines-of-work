content = """
Culling removes animals that do not meet production, health, or economic
goals. Consistent criteria support herd and flock improvement.

**Reasons for Culling:** Open or late-bred females, poor mothering,
chronic illness or lameness, low production (weaning weights, wool,
etc.), structural unsoundness, age, and disposition problems. Document
reasons for records and genetic evaluation.

**Process:** Identify culls at appropriate times (post-pregnancy check,
post-weaning, etc.). Ensure animals are fit for transport if sold.
Handle culls separately to avoid mixing with retained breeding stock.
Update records to reflect removals.

**Replacement Planning:** Balance cull rate with replacement availability.
Maintain age structure for consistent production. Select replacements
from best-performing, healthy dams.
"""  # noqa: E501

