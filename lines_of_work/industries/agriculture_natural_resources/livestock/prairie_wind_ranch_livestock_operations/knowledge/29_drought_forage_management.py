title = "Drought Forage Management"

content = """
During drought, forage production declines and quality may change. Early
adjustments protect pasture recovery and animal performance.

**Stocking Adjustments:** Reduce stocking rate before pastures are
overgrazed. Sell or relocate livestock, or wean early to reduce forage
demand. Overgrazing during drought delays recovery for years.

**Supplementation:** Provide hay or other forage to replace grazing shortfall.
Match supplement to forage quality; low-quality dry forage may require
energy and protein supplementation for lactating animals. Ensure mineral
availability.

**Pasture Protection:** Maintain residual height to protect plant crowns.
Consider sacrificing some paddocks for heavy use while resting others.
Avoid grazing regrowth too early after rain; allow establishment.
"""  # noqa: E501

