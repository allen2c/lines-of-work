title = "Forage Height Targets for Grazing"

content = """
Maintaining appropriate forage height before and after grazing supports plant
persistence and animal performance. Heights are measured from ground level to
the top of the leaf canopy.

**Cool-Season Grasses:** Graze when height reaches six to eight inches. Remove
livestock when stubble height remains at three to four inches. Avoid grazing
below two inches, which depletes root reserves.

**Warm-Season Native Grasses:** Begin grazing when height reaches eight to twelve
inches. Leave four to six inches of stubble. These grasses store energy in lower
stems; overgrazing weakens stand longevity.

**Legumes:** Maintain at least three inches of residual to protect crown and
regrowth. Legumes are more susceptible to overgrazing than grasses; mixed swards
may require rotation timing driven by legume tolerance.
"""  # noqa: E501

