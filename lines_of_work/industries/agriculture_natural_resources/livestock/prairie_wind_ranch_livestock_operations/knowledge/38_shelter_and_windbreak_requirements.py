title = "Shelter and Windbreak Requirements"

content = """
Shelter reduces cold stress and can improve performance and welfare during
adverse weather. Requirements vary by species, age, and condition.

**Cattle:** Mature cattle with winter coats and adequate nutrition can
withstand cold if dry and out of wind. Provide windbreaks in open
pastures. Calves and thin animals need more protection. Bedding in
confinement areas reduces stress and mud.

**Sheep:** Sheep with full wool have good cold tolerance when dry.
Windbreaks and shed access during storms reduce losses. Lambs and
newly shorn sheep need protection from wind and wet. Ensure dry
lying areas.

**Design:** Windbreaks should reduce wind speed by 50 percent or more.
Orientation and spacing affect effectiveness. Avoid overcrowding in
shelters; ensure adequate ventilation to prevent moisture buildup.
"""  # noqa: E501

