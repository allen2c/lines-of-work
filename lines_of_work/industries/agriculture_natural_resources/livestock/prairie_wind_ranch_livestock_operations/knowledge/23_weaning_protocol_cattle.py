title = "Weaning Protocol for Cattle"

content = """
Weaning is a high-stress period for calves. Gradual or two-step weaning
can reduce stress and illness compared to abrupt weaning.

**Preparation:** Ensure calves are vaccinated and dewormed per protocol
before weaning. Familiarize calves with water sources and feed if they
will receive supplemental feed. Plan for adequate pen or pasture space.

**Methods:** Fence-line weaning allows calf and dam to see and hear each
other while preventing nursing; stress and bawling are often reduced.
Nose-flap weaning blocks nursing while keeping pairs together. Abrupt
weaning requires excellent facility design and observation.

**Post-Weaning:** Monitor feed and water intake daily. Watch for respiratory
signs; treat promptly per veterinarian guidance. Provide quality forage
or feed and clean water. Minimize handling during the first two weeks.
"""  # noqa: E501

