title = "Cattle Transport Preparation"

content = """
Proper preparation reduces stress and illness during and after transport.
Comply with regulations for animal fitness, rest, and documentation.

**Pre-Transport:** Withhold feed for 12 to 24 hours before loading to reduce
gut fill and manure during transit; allow free access to water until loading.
Ensure animals are fit for travel; do not ship lame, sick, or severely
thin animals. Verify vaccinations and health papers meet destination
requirements.

**Loading:** Use low-stress handling. Do not overcrowd; allow space for
animals to brace during movement. Check trailer for sharp edges, adequate
ventilation, and non-slip flooring.

**Post-Arrival:** Allow rest and access to water before feeding. Observe
closely for respiratory or digestive signs. Provide clean, dry environment.
"""  # noqa: E501

