title = "Handling Facility Safety"

content = """
Working facilities must be designed and maintained to protect both livestock and
personnel. Inspect facilities before each use and address hazards immediately.

**Alley and Chute:** Ensure solid sides to prevent animals from seeing distractions
and balking. Non-slip flooring reduces falls. Curved alleys facilitate flow.
Squeeze chutes must close smoothly and release reliably. Test gates and latches
before loading animals.

**Personnel:** Never stand directly behind an animal in a chute or alley. Use
escape routes and avoid positioning where you could be pinned. Wear appropriate
footwear and avoid loose clothing. Communicate clearly when multiple people
are working.

**Maintenance:** Repair broken boards, sharp edges, and malfunctioning equipment
before use. Keep walkways clear and well-lit.
"""  # noqa: E501

