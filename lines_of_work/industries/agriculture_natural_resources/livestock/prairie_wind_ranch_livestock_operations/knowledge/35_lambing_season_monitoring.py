title = "Lambing Season Monitoring"

content = """
Lambing requires regular observation to assist ewes in difficulty and
to ensure lambs receive colostrum. Frequency depends on lambing pattern
and conditions.

**Check Frequency:** During synchronized lambing, check at least twice
daily; more often during peak. In cold or wet conditions, increase
frequency. Spot-lambing (year-round) flocks may require daily checks
when lambing is expected.

**Observation:** Identify ewes in labor, newly lambed pairs, lambs
that have not nursed, and ewes with retained lambs or distress.
Warm and feed chilled lambs; assist with dystocia per protocol.
Ensure bonding; cross-foster or bottle-raise orphans when possible.

**Record Keeping:** Document birth date, ewe ID, lamb ID, sex, birth
type (single, twin, etc.), and assistance given. Track losses for
flock analysis.
"""  # noqa: E501

