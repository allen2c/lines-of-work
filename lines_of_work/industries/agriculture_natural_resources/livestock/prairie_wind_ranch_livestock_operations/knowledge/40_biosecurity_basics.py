content = """
Biosecurity reduces the introduction and spread of disease. Simple
practices protect herd and flock health.

**Visitors and Vehicles:** Limit farm access. Provide designated parking
and foot baths where appropriate. Require clean clothing and footwear
or farm-provided coveralls and boots for visitors to animal areas.

**New Animals:** Quarantine as described in new-animal protocols.
Purchase from sources with known health status when possible.

**Equipment and Feed:** Avoid sharing equipment with other operations
without cleaning and disinfection. Source feed from reputable suppliers.
Prevent contact between wildlife and feed storage.

**Sick Animals:** Isolate promptly. Use separate equipment for sick
animals or clean and disinfect between uses. Work sick animals last
when possible.
"""  # noqa: E501

