title = "Disease Reporting Requirements"

content = """
Certain livestock diseases must be reported to state or federal authorities.
Prompt reporting supports disease control and protects the industry.

**Reportable Diseases:** List varies by jurisdiction and species. Examples
include anthrax, brucellosis, tuberculosis, scrapie, and foreign animal
diseases. Consult state veterinarian or USDA for current list.

**When to Report:** Report when clinical signs match a reportable disease,
when diagnostics confirm reportable disease, or when unexplained morbidity
or mortality occurs. When in doubt, contact the veterinarian or regulatory
official.

**Process:** Typically report to state veterinarian or USDA Area Veterinarian
in Charge (AVIC). Provide location, species, number affected, clinical signs,
and any diagnostic results. Cooperate with investigation and movement
restrictions.
"""  # noqa: E501

