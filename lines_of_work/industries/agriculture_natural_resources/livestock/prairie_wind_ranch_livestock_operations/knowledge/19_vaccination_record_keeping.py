title = "Vaccination Record Keeping"

content = """
Vaccination records support health management and may be required for sale
or movement. Record all vaccinations at the time of administration.

**Required Fields:** Date, animal or group ID, vaccine product name and
serial/lot number, route (subcutaneous, intramuscular), site if applicable,
and initials of person administering. For breeding stock, maintain individual
records; for groups, document group identification clearly.

**Storage:** Keep records accessible for inspections and for at least three
years after animal disposition. Digital systems should allow search by
animal ID and by date. Back up records regularly.

**Compliance:** Certain vaccines are required for interstate movement or
program participation. Verify current requirements before transport.
"""  # noqa: E501

