title = "Breeding Record Requirements"

content = """
Accurate breeding records support genetic management, pregnancy diagnosis, and
regulatory traceability. Record all breeding events within 48 hours of observation
or artificial insemination.

**Cattle:** Document cow or heifer ID, sire ID (or bull code), breeding date, and
method (natural service or AI). For AI, include sire name and registration number
if applicable. Note any observed returns to estrus for culling decisions.

**Sheep:** Record ewe ID, ram ID, breeding date, and breeding group. Flush dates
and turnout dates inform expected lambing windows. Mark singles versus multiples
at lambing for genetic evaluation.

**Storage:** Maintain records in the ranch system with backups. Records may be
required for animal disease traceability and voluntary programs.
"""  # noqa: E501

