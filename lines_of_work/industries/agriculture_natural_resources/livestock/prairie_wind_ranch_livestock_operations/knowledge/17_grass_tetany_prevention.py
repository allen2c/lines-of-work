title = "Grass Tetany Prevention"

content = """
Grass tetany (hypomagnesemia) occurs when cattle or sheep on lush, fast-growing
cool-season pasture have inadequate magnesium intake. It is most common in
spring and in lactating cows. Prevention is more effective than treatment.

**Risk Conditions:** Young, succulent grasses high in potassium and protein,
low in magnesium; cold, cloudy weather; lactating females. Older cows and
thin animals are at higher risk.

**Prevention:** Provide high-magnesium mineral supplement two to three weeks
before turnout and throughout the risk period. Ensure adequate consumption;
palatability and placement affect intake. Feeding hay before turnout can
reduce risk. Avoid sudden full turnout to lush pasture.

**Signs and Response:** Staggering, muscle tremors, convulsions, collapse.
Emergency treatment requires intravenous magnesium; contact veterinarian
immediately. Affected animals may relapse; provide oral magnesium and
observe closely.
"""  # noqa: E501

