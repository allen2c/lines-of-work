title = "New Animal Introduction and Quarantine"

content = """
Introducing animals from other herds carries disease and parasite risk.
Isolate and observe new arrivals before mixing with the home herd.

**Quarantine Period:** Minimum 30 days in a separate area with no nose-to-nose
contact with resident animals. Longer periods may be warranted for high-risk
acquisitions or when disease outbreaks occur in source regions.

**During Quarantine:** Observe for respiratory disease, diarrhea, lameness,
and general thrift. Administer vaccinations and deworming per ranch protocol.
Collect and submit diagnostic samples if the veterinarian recommends. Check
identification and health papers for completeness.

**Release:** Only mix after quarantine is complete and no illness has been
observed. Document release date and any treatments given.
"""  # noqa: E501

