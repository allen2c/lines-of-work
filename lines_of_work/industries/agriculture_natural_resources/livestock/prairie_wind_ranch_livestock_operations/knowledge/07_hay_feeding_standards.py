title = "Hay Feeding Standards"

content = """
Hay quality and feeding method affect intake, waste, and animal performance.
Match hay type and allocation to physiological stage and environmental conditions.

**Quality Assessment:** Visual inspection for mold, weeds, and leaf retention. If
available, use forage test results for protein and energy. Late-cut hay may
require supplementation for growing or lactating animals.

**Feeding Method:** Unrolled hay or hay rings reduce waste compared to feeding
directly on the ground. Space hay access to reduce competition; provide enough
linear bunk or feeder space so subordinate animals can eat. Limit waste to
under fifteen percent when possible.

**Allocation:** Provide sufficient hay so animals do not run out between feedings.
In extreme cold, increase allocation to meet maintenance requirements. Remove
and replace spoiled or refused hay promptly.
"""  # noqa: E501

