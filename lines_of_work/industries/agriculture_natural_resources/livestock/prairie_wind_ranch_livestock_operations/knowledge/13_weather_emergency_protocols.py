title = "Weather Emergency Protocols"

content = """
Extreme weather requires proactive preparation and post-event response. Document
all incidents and animal losses for insurance and records.

**Blizzards and Extreme Cold:** Ensure adequate shelter or windbreaks and bedding.
Provide extra hay and unfrozen water. Move vulnerable animals (recent newborns,
thin animals) to protected areas when forecast predicts dangerous conditions.
Check all animals within 24 hours after the event.

**Heat Stress:** Provide shade and ample water during heat advisories. Avoid
handling or transport during peak heat. Cattle and sheep pant to cool; open-mouth
breathing and lethargy indicate distress. Reduce stocking density in confined
areas when possible.

**Flooding:** Move livestock to higher ground when flooding is predicted. Do not
drive animals through deep or swift water. Document any losses and report to
authorities as required.
"""  # noqa: E501

