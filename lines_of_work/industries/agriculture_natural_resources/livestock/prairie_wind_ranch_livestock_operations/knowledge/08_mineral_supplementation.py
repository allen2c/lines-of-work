title = "Mineral Supplementation Protocol"

content = """
Mineral blocks or loose mineral formulations provide trace elements and major
minerals that forages may lack. Location and formulation affect consumption
and efficacy.

**Placement:** Place mineral feeders or blocks near water sources and loafing
areas to encourage consistent intake. Provide one feeder per 25 to 30 head for
cattle; adjust for sheep. Ensure sheltered placement during wet or snowy periods.

**Formulation:** Use products formulated for the region and forage base. High
potassium forages may require magnesium supplementation to reduce grass tetany
risk in spring. Salt drives intake; ensure adequate but not excessive consumption.

**Monitoring:** Track consumption rates. Significant over- or under-consumption
indicates formulation or placement issues. Record lot numbers and start dates
for traceability.
"""  # noqa: E501

