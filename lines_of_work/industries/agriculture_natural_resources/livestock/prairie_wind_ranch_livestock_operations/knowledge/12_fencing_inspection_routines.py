title = "Fencing Inspection Routines"

content = """
Perimeter and interior fencing contain livestock and protect pastures from
overuse. Regular inspection prevents escapes and predator entry.

**Weekly Patrol:** Walk or ride fence lines weekly during grazing season. Look
for broken wires, loose posts, sagging sections, and vegetation contact that
may short electric fence. Document locations and schedule repairs.

**Post-Storm Checks:** After high winds, hail, or heavy snow, inspect critical
sections immediately. Fallen trees and drifted snow can compromise fences.
Prioritize perimeter and livestock-handling areas.

**Electric Fence:** Test energizer output weekly. Ensure ground rods are
properly installed and connections are tight. Vegetation under hot wires
reduces voltage; trim or treat as needed. Record voltage readings for
trend analysis.
"""  # noqa: E501

