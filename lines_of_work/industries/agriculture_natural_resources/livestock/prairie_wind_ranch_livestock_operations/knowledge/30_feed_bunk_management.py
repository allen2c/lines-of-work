title = "Feed Bunk Management"

content = """
Proper bunk management ensures consistent intake, minimizes waste, and
supports rumen health when supplemental feed is offered.

**Cleanliness:** Remove old or spoiled feed before adding new. Wet,
moldy, or contaminated feed reduces intake and can cause illness. Clean
bunks regularly; sanitize when disease has occurred.

**Delivery:** Deliver feed at consistent times. Provide sufficient bunk
space so all animals can eat; subordinate animals may be excluded when
space is limited. Adjust amounts based on consumption; slight cleaning
of bunks between feedings is ideal. Avoid running out of feed.

**Monitoring:** Observe consumption patterns. Sudden drops may indicate
health issues, ration problems, or environmental stress. Record refusals
and adjust delivery accordingly.
"""  # noqa: E501

