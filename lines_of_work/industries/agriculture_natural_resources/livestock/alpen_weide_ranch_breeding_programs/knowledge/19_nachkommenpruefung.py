"""Knowledge item: Nachkommenprüfung."""

title = "Nachkommenprüfung"

content = """
Die Nachkommenprüfung bewertet Bullen anhand der Leistung ihrer Nachkommen.
Sie liefert zuverlässige Zuchtwertschätzungen, erfordert aber Zeit und
ausreichende Nachkommenschaft.

**Daten:** Gewichte, Schlachtleistung, Fleischqualität und Kalbefähigkeit der
Kälber werden erfasst. Die Leistung wird dem Vater zugeordnet und mit
Herden- oder Populationsdurchschnitten verglichen.

**Zeitrahmen:** Erste Ergebnisse liegen oft erst nach mehreren Jahren vor.
Genomische Tests ergänzen die Nachkommenprüfung und beschleunigen die
Zuchtwertschätzung bei jungen Bullen.
"""  # noqa: E501

