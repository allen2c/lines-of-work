"""Knowledge item: Biosicherheit bei Zuchttieren."""

title = "Biosicherheit bei Zuchttieren"

content = """
Zuchttiere und Besamungsmaterial können Krankheitserreger einschleppen.
Biosicherheitsmaßnahmen schützen die Herde vor Infektionen.

**Neuzugänge:** Quarantäne, tierärztliche Untersuchung und
impfstoffbedingte Immunität vor Einstallung prüfen.

**Besamung:** Semen von anerkannten Stationen; Hygienevorschriften
bei Handhabung und Applikation einhalten.

**Bullen:** Zuchttauglichkeitsuntersuchung und Gesundheitsstatus
vor Einsatz prüfen.
"""  # noqa: E501

