title = "Futtereffizienz und Selektion"

content = """
Futtereffizienz beeinflusst die Wirtschaftlichkeit von Mutterkuhbetrieben.
Die genetische Verbesserung erfordert Messung und Zuchtwertschätzung.

**Residual Feed Intake (RFI):** Tiere mit niedrigerem RFI fressen weniger
bei gleicher Leistung. RFI ist heritabel und kann in die Selektion
einfließen.

**Praktische Ansätze:** Wo RFI-Messung nicht möglich ist, unterstützen
Wachstum, Körperkondition und Futteraufnahme die indirekte Auswahl.
Weideeffizienz bleibt schwer zu erfassen; Herdenvergleiche helfen.
"""  # noqa: E501

