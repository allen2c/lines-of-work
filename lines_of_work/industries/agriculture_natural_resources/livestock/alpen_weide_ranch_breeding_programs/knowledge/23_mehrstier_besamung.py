"""Knowledge item: Mehrstierbesamung."""

title = "Mehrstierbesamung"

content = """
Bei Natursprung können mehrere Bullen gemeinsam eingesetzt werden.
Die Mehrstierbesamung erhöht die genetische Vielfalt und reduziert
das Risiko bei Einzelbullen.

**Verhältnis:** Ein Bulle pro 20–25 Kühe ist üblich. Bei jungen
Bullen oder schwierigem Gelände weniger Kühe pro Bulle.

**Abstammung:** Ohne DNA-Test ist die Vaterschaft unklar. Für
Herdenbuch und Zuchtwertschätzung ist Einzelbullenbesamung oder
DNA-Abstammungstest erforderlich.
"""  # noqa: E501

