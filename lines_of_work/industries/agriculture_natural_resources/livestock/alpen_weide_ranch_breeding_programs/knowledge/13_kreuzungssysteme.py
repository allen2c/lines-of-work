content = """
Kreuzung nutzt Heterosis (Hybridleistung) und komplementäre Genetik. Rotations-
und Terminalkreuzung sind gängige Systeme.

**Rotationskreuzung:** Zwei oder mehr Rassen wechseln sich als Vatertier ab.
Erhaltung von Heterosis bei maternalen und paternalen Linien. Einfacher zu
managen bei zwei Rassen.

**Terminalkreuzung:** Reinrassige Mutterlinie mit Terminalbullen. Maximale
Heterosis bei Nachkommen; alle Kälber gehen in die Mast. Erfordert Erhaltung
reiner Mutterlinien.

**Dreirassen-Rotation:** Fleckvieh, Charolais, Limousin beispielhaft. Höhere
Heterosis, mehr Komplexität. Dokumentation der Abstammung entscheidend.
"""  # noqa: E501

