"""Knowledge item: Ausmerzungsentscheidungen."""

title = "Ausmerzungsentscheidungen"

content = """
Die Ausmerzung von Kühen und Färsen hält die Herde leistungsfähig und
reduziert Inzucht. Kriterien müssen betriebsspezifisch und konsistent sein.

**Leistung:** Niedrige Trächtigkeitsrate, späte Abkalbung, schlechte
Muttereigenschaften oder geringe Kälberleistung können zur Ausmerzung führen.

**Gesundheit:** Chronische Lahmheit, Euterprobleme oder wiederholte
Behandlungen rechtfertigen die Ausmerzung aus Tierschutzgründen.

**Alter und Genetik:** Ältere Kühe mit abnehmender Leistung oder Tiere,
die nicht zu den Zuchtzielen passen, werden ersetzt.
"""  # noqa: E501

