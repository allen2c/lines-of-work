title = "Nettoyage et entretien des bassins"

content = """
Un entretien régulier des bassins limite l'accumulation de matières organiques,
de biofilm et de pathogènes. Les bassins vides doivent être nettoyés et désinfectés
entre deux lots.

**Nettoyage courant :** Retirer les débris et cadavres quotidiens. Nettoyer les
grilles et crépines pour maintenir le débit. Contrôler l'état des parois (algues,
dépôts).

**Nettoyage entre lots :** Vider le bassin, gratter les dépôts, rincer. Appliquer
une désinfection (chlore, chaux, produits homologués) selon protocole. Rincer
avant remplissage. Documenter la date et le produit utilisé. Respecter les
délais avant réempoissonnement.
"""  # noqa: E501

