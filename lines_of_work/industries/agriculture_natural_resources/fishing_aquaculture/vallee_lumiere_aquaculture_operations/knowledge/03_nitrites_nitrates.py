title = "Nitrites et nitrates en bassin"

content = """
Les nitrites (NO2) et nitrates (NO3) proviennent du cycle de l'azote : dégradation
des matières organiques, excréments, restes d'aliments. Les nitrites sont toxiques
à faible concentration ; les nitrates le sont à des concentrations plus élevées.

**Seuils nitrites :** Pour la truite, maintenir en dessous de 0,1 mg/L. Au-delà
de 0,25 mg/L, stress et possible brunissement du sang (méthémoglobinémie). Action
immédiate : augmenter l'oxygénation, réduire l'alimentation, vérifier le débit.

**Nitrates :** Moins toxiques à court terme. Maintenir en dessous de 50 mg/L pour
la truite. Des valeurs élevées indiquent un bassin sous-dimensionné ou un
renouvellement insuffisant.
"""  # noqa: E501

