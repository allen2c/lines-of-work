title = "Gestion du gel en hiver"

content = """
En hiver, le gel peut obstruer les grilles, bloquer les vannes et réduire l'apport
d'eau. Une couche de glace à la surface limite les échanges gazeux. Les
installations en circuit ouvert sont particulièrement exposées.

**Prévention :** Maintenir un débit suffisant pour éviter la formation de glace
dans les canaux. Vérifier les vannes et les pompes (risque de gel dans les
parties non drainées). Installer des dispositifs anti-gel ou des brise-glace
si nécessaire.

**En cas de gel :** Dégager les grilles et les prises d'eau. Ne pas briser la
glace sur les bassins de manière brutale (stress acoustique pour les poissons).
Si l'oxygène chute, augmenter l'aération ou le débit.
"""  # noqa: E501

