content = """
En circuit ouvert, l'eau provient d'une source (rivière, forage, mer) et est
rejetée après passage dans les bassins. Le renouvellement continu assure
l'oxygène et la dilution des déchets. Les investissements sont plus faibles
mais la dépendance au débit et à la qualité de la source est forte.

**Avantages :** Simplicité, coûts énergétiques modérés, stabilité si la source
est de bonne qualité. Idéal pour les régions où l'eau est abondante et fraîche.

**Contraintes :** Débit minimum requis. Sensibilité aux pollutions en amont,
aux variations de température et de débit (étiage, crues). Rejets à gérer selon
la réglementation. Les pathogènes peuvent entrer avec l'eau d'alimentation.
"""  # noqa: E501

