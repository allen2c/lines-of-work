title = "Grilles et crépines"

content = """
Les grilles et crépines retiennent les poissons tout en laissant passer l'eau.
Elles sont placées à l'entrée et à la sortie des bassins. Une crépine obstruée
réduit le débit et peut provoquer des problèmes d'oxygène.

**Entretien :** Nettoyer les grilles régulièrement (quotidiennement si nécessaire
en période de fort encrassement). Vérifier l'intégrité des mailles (pas de
déchirure permettant l'échappement de poissons).

**Sécurité :** Les grilles doivent être fixes et bien fixées. En circuit ouvert,
une grille de sortie défaillante peut permettre aux poissons de s'échapper vers
le milieu naturel, avec des conséquences écologiques et réglementaires.
"""  # noqa: E501

