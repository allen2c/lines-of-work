title = "Jeûne pré-récolte"

content = """
Le jeûne avant la récolte vide le tube digestif, améliore la qualité du produit
et réduit les rejets pendant le transport et l'abattage. La durée varie selon
la température et l'espèce.

**Recommandations :** Pour la truite, 24 à 48 h à 10-15 °C ; jusqu'à 72 h en
eau froide. Pour le bar, 24 à 48 h. Adapter si la température est plus élevée
(métabolisme plus rapide).

**Organisation :** Planifier la récolte en fonction du jeûne. Informer le
personnel des lots en jeûne pour éviter les distributions accidentelles.
Maintenir une oxygénation suffisante pendant le jeûne.
"""  # noqa: E501

