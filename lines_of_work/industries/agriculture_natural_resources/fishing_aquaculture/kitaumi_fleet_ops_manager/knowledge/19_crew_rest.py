title = "船員の休息時間管理"

content = """
- 労働基準法に基づき、船員の1日あたりの労働時間は12時間以内、連続作業は6時間以内。休息時間は最低8時間（うち連続6時間以上）。
- 管理者は出港前に各船の乗船者数と予定操業時間を確認。長時間操業（12時間超）が予想される場合は、交代要員を手配。
- 帰港後、船長は乗船者の労働時間を記録し、管理者に提出。管理者は月次で労働時間の集計を行い、過労が疑われる船員には休暇を指示。
- 違反が発覚した場合（例：連続16時間操業）、管理者は船長に改善命令。再犯時は組合長報告の上、出港停止3日。
"""

