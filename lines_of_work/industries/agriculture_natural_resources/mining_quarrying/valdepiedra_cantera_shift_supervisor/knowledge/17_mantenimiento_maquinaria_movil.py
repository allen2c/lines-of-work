title = "Mantenimiento de maquinaria móvil"

content = """
- La maquinaria móvil (pala cargadora, excavadora, dúmper, cisterna, perforadora) sigue un
  plan de mantenimiento preventivo por horas de funcionamiento.
- Antes de cada relevo el operador hace la revisión de prearranque: niveles de aceite,
  refrigerante e hidráulico, estado de neumáticos u orugas, luces, avisador de marcha atrás,
  extintor, frenos y ausencia de fugas. Se registra en la lista de chequeo.
- El engrase se realiza según pauta (a menudo diario o cada 8-10 horas en puntos críticos);
  la falta de engrase acelera el desgaste de bulones y articulaciones.
- Los cambios de aceite de motor y filtros se programan por horas (por ejemplo, cada
  250-500 h según fabricante); los aceites usados y filtros se gestionan como residuo
  peligroso.
- Los neumáticos de gran tamaño se vigilan en presión y estado; cortes o presión inadecuada
  causan calentamiento y riesgo de reventón. La manipulación de ruedas la hace personal
  formado con jaula de seguridad.
- Los defectos que afectan a la seguridad (frenos, dirección, avisadores, estructura ROPS/FOPS)
  retiran la máquina de servicio hasta su reparación.
- Los repostajes se hacen con el motor parado, sin fumar y con bandeja anti-derrame; los
  derrames de gasoil se recogen con absorbente y se gestionan adecuadamente.
- El jefe de turno coordina con el taller la reparación de averías y decide, con criterio de
  seguridad, si una máquina puede seguir operando o debe pararse.
"""

