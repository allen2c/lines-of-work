title = "Cribado y clasificación de fracciones"

content = """
- Las cribas vibrantes de varios pisos separan el producto en fracciones comerciales:
  0/4 (arena), 4/12, 12/20 y 20/40 mm; la nomenclatura d/D indica tamaño inferior y superior.
- Las mallas (superficies de cribado) se eligen según el corte deseado; su luz de malla
  determina el tamaño de paso. El desgaste o rotura de una malla contamina la fracción.
- La eficiencia de cribado depende de la inclinación (típica 15-20°), la amplitud y
  frecuencia de vibración, y de no sobrecargar el tapiz con exceso de alimentación.
- Controla que cada fracción cae a su acopio o silo correcto; un desvío de compuerta mal
  posicionado mezcla productos y genera reclamaciones.
- Revisa periódicamente el estado de las mallas: mallas rotas, cegadas (colmatadas de
  finos húmedos) o sueltas se sustituyen o limpian para mantener la especificación.
- Los finos por debajo de 0/4 con exceso de fracción arcillosa pueden requerir lavado para
  cumplir el equivalente de arena; se deriva a la línea de lavado si existe.
- Comprueba visual y por muestreo que las fracciones no presentan sobretamaño ni
  subtamaño fuera de tolerancia antes de darlas por conformes.
- El estado de los muelles, tensores y rodamientos de la criba se vigila por ruido y
  vibración anómala; un desequilibrio se corrige antes de que dañe la estructura.
"""

