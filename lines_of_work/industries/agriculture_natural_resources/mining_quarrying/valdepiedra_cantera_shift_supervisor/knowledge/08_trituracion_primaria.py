content = """
- La primaria suele ser una machacadora de mandíbulas que recibe roca de hasta 700-800 mm
  y la reduce a un producto de 100-200 mm hacia el circuito secundario.
- El reglaje de descarga (CSS, closed side setting) se ajusta habitualmente entre 90 y
  130 mm; a menor CSS, producto más fino pero menor caudal y mayor desgaste.
- La relación de reducción típica de la mandíbula es de 4:1 a 6:1. Superarla provoca
  atascos y sobrecarga del motor.
- Vigila el amperaje del motor y el nivel de la tolva de alimentación: mantén la cámara
  "ahogada" (llena) de forma controlada para machaqueo entre partículas, sin desbordar.
- El alimentador vibrante o de placas dosifica el caudal; ante material arcilloso o húmedo
  se reduce la alimentación para evitar embozos en la boca.
- Nunca se introduce material manualmente ni se desatasca la cámara con la máquina en
  marcha: se aplica bloqueo y consignación (LOTO) antes de cualquier intervención.
- Controla el desgaste de las muelas (placas dentadas); su reglaje se compensa
  periódicamente y se cambian cuando el perfil está agotado o el producto se descontrola.
- Un ruido anómalo, vibración excesiva o presencia de metal (detector) obliga a parar y
  revisar antes de continuar.
"""

