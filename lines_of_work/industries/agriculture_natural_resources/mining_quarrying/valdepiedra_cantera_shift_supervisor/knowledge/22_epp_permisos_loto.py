content = """
- El equipo de protección individual básico en la cantera es: casco, calzado de seguridad,
  ropa de alta visibilidad, protección auditiva en zonas señalizadas y protección respiratoria
  frente al polvo. Se añaden gafas y guantes según la tarea.
- El bloqueo y consignación (LOTO) se aplica antes de cualquier intervención en máquinas:
  parar, aislar todas las energías (eléctrica, hidráulica, neumática, gravedad), bloquear con
  candado personal, señalizar y verificar la ausencia de energía antes de acceder.
- Cada persona que interviene coloca su propio candado; nadie retira el bloqueo de otro. El
  equipo no se reenergiza hasta que todos han retirado sus candados y se confirma zona
  despejada.
- Los trabajos especiales requieren permiso de trabajo por escrito: trabajos en caliente
  (soldadura), en altura, en espacios confinados (silos, tolvas) y con riesgo eléctrico.
- Los espacios confinados exigen medición de atmósfera, ventilación, vigilante exterior y
  medios de rescate antes de la entrada; nunca se entra sin autorización y control.
- Los trabajos en altura se realizan con protección colectiva o, en su defecto, arnés
  anclado a punto resistente; no se accede a estructuras sin protección frente a caídas.
- El jefe de turno verifica que los permisos están emitidos, que se cumplen las condiciones y
  que el personal dispone y usa el EPP adecuado antes de autorizar la tarea.
- Se prohíbe retirar resguardos y anular dispositivos de seguridad; hacerlo detiene el
  trabajo hasta restablecer la protección.
"""

