title = "Trituración secundaria y terciaria (conos)"

content = """
- La secundaria suele ser un triturador de cono que recibe el producto de la primaria
  (100-200 mm) y lo reduce a 30-60 mm; la terciaria afina hasta las fracciones finas.
- El reglaje (CSS) del cono se ajusta en 12-40 mm según la fracción objetivo; una fina
  regulación determina la curva granulométrica y la producción de arena.
- La relación de reducción del cono es de 3:1 a 6:1. Es fundamental mantener la cámara
  alimentada de forma uniforme y centrada para un desgaste regular del manto y la coraza.
- Evita la marcha en vacío o la alimentación intermitente: provoca desgaste irregular y
  producto fuera de forma (lajas). El objetivo es partícula cúbica.
- Vigila la presión del sistema hidráulico de protección: al pasar un intriturable (hierro,
  metal), el cono abre para dejarlo pasar y luego vuelve al reglaje; registra estos eventos.
- Controla la temperatura y el nivel del aceite de lubricación del cono; un fallo de
  lubricación obliga a parar de inmediato para no dañar los cojinetes.
- El circuito trabaja en lazo cerrado con la criba: el rechazo (sobretamaño) retorna al
  cono para volver a triturarse hasta pasar la malla.
- Cualquier intervención en la cámara se hace con la máquina parada, consignada y verificada
  la ausencia de energía residual.
"""

