content = """
- El ruido laboral se controla frente al valor de acción: a partir de 80 dB(A) se facilitan
  protectores auditivos y a partir de 85 dB(A) su uso es obligatorio y se señaliza la zona.
- El valor límite de exposición diaria del trabajador es de 87 dB(A) teniendo en cuenta la
  atenuación de la protección; se prioriza reducir el ruido en origen (aislar, capotar,
  mantener rodamientos y muelas en buen estado).
- Las cabinas de la maquinaria móvil y de la sala de control ofrecen aislamiento acústico;
  se mantienen cerradas durante la operación.
- Las vibraciones de la voladura hacia edificaciones vecinas se controlan mediante la
  velocidad de pico de partícula (PPV). Se fijan límites según normativa y tipo de
  edificación (por ejemplo, del orden de 5-9 mm/s para estructuras sensibles).
- Se registra la vibración de las voladuras con sismógrafos en puntos de control cercanos a
  viviendas, junto con la sobrepresión aérea (onda aérea).
- Si se prevé superar el límite, se ajusta el diseño de voladura: menor carga por
  microrretardo, más secuenciación y menor número de barrenos por disparo.
- Las vibraciones mano-brazo y cuerpo entero de perforistas y conductores se limitan con
  asientos amortiguados, mantenimiento y rotación de tareas.
- Las quejas de vecinos por ruido o vibración se registran y se investigan, ajustando la
  operación o los horarios cuando proceda.
"""

