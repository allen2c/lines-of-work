content = """
- El despacho se ordena según las órdenes de carga del día; se comprueba matrícula,
  transportista, producto y fracción antes de cargar.
- La carga desde silo o acopio con pala se distribuye de forma equilibrada sobre el eje del
  camión para no descentrarlo ni superar las cargas por eje admisibles.
- No se sobrecarga: se respeta la masa máxima autorizada del vehículo. Un camión sobrecargado
  se retira material antes de salir; salir sobrecargado es incumplimiento legal y riesgo vial.
- La caja se cubre con lona o malla antes de circular por vía pública para evitar caída de
  material y emisión de polvo; sin lona no se autoriza la salida.
- El conductor permanece en cabina durante la carga con pala; si baja, se detiene la carga.
  Se mantiene contacto visual entre palista y conductor.
- Tras cargar, el camión pesa en báscula, recoge el albarán y sale por el itinerario
  señalizado, respetando el límite de velocidad interno.
- Se controla la limpieza de ruedas y bajos si existe punto de lavado, para no arrastrar
  barro a la vía pública.
- Cualquier derrame en pista se retira de inmediato; el orden y la limpieza de la zona de
  carga previenen resbalones y atropellos.
"""

