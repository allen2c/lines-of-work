content = """
- El factor de carga (consumo específico de explosivo) se sitúa en 0,25-0,45 kg/m3 según
  la dureza de la roca y la fragmentación deseada. El responsable de voladura fija el valor.
- El retacado (tapón de detritus o grava en la boca del barreno) suele ser de 20-25 veces
  el diámetro, del orden de 2-2,5 m, para confinar los gases y reducir proyecciones.
- Se emplea secuenciación con detonadores de microrretardo para reducir vibraciones y
  mejorar la fragmentación; el tiempo entre barrenos de una misma fila es de 8-25 ms.
- La carga de fondo (mayor densidad) rompe el empotramiento en el piso; la carga de columna
  fragmenta el resto del banco.
- El jefe de turno coordina que la zona esté despejada de personal y maquinaria, y organiza
  la retirada de equipos a distancia segura antes de la conexión final.
- Tras el disparo, se evalúa la fragmentación (tamaño de bloques), la aparición de repiés,
  el desplazamiento de la pila y posibles bolos que requieran taqueo secundario.
- Los bolos que superan la boca del alimentador se fragmentan con martillo hidráulico
  (picador), nunca cargando barrenos improvisados sin control técnico.
- El artillero titulado es el único que carga, conecta y dispara; el jefe de turno no
  manipula explosivos ni accesorios.
"""

