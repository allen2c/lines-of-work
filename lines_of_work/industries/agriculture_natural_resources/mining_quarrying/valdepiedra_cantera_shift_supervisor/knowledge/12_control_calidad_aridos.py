title = "Control de calidad de los áridos"

content = """
- El control de calidad verifica que cada fracción cumple la especificación pactada con el
  cliente y las normas de producto aplicables (marcado de conformidad).
- Granulometría: se determina por tamizado el porcentaje que pasa por cada tamiz. La curva
  debe quedar dentro de los husos de la fracción; el sobretamaño y el subtamaño tienen
  límites (por ejemplo, retenido en el tamiz D no superior al 15 %).
- Equivalente de arena (SE): mide el contenido de finos arcillosos en la arena 0/4. Para
  hormigón se suele exigir SE mayor de 70-75; valores bajos obligan a lavar el árido.
- Coeficiente de Los Ángeles: mide la resistencia al desgaste por impacto y abrasión; para
  capas de rodadura de asfalto se exigen valores bajos (por ejemplo LA menor de 20-25).
- Índice de lajas: controla la forma de la partícula; un exceso de partículas planas y
  alargadas empeora el comportamiento en hormigón y aglomerado.
- El muestreo se hace de forma representativa (en cinta o en acopio con cuarteo) y con la
  frecuencia establecida en el plan de control de producción.
- El producto no conforme se retiene, se identifica y se reclasifica o se recircula; no se
  despacha hasta corregir el proceso (reglaje, malla, lavado).
- El jefe de turno coordina con laboratorio, actúa sobre el proceso ante desviaciones y
  registra los resultados para la trazabilidad.
"""

