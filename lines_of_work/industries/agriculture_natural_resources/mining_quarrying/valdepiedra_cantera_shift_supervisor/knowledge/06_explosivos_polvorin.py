title = "Custodia de explosivos y polvorín"

content = """
- Los explosivos y detonadores se custodian en polvorines autorizados, separados entre sí
  (explosivo y accesorios de iniciación nunca juntos) y con la ventilación y protección
  antirrayos reglamentarias.
- El acceso está restringido al personal autorizado; el jefe de turno controla que solo el
  artillero y personal designado retiren material, siempre con registro documental.
- Se lleva un libro de consumo: entradas, salidas y saldo de cada tipo de explosivo y de
  detonadores. El recuento debe cuadrar en cada voladura; cualquier descuadre se investiga
  de inmediato.
- El transporte interno de explosivos al frente se hace en vehículos adecuados, a velocidad
  reducida, sin mezclar explosivo y detonadores en el mismo habitáculo salvo lo permitido.
- Está prohibido fumar y usar llamas o equipos que generen chispas cerca del explosivo.
- La sobra de explosivo tras una voladura se devuelve al polvorín y se anota; no se abandona
  en el frente ni se destruye por medios improvisados.
- Cualquier robo, pérdida o hallazgo de explosivo sin detonar se comunica sin demora a la
  dirección, al responsable de voladuras y a las autoridades competentes.
- El jefe de turno no manipula ni almacena explosivos: su papel es garantizar que se cumplen
  el control de accesos, el acordonamiento y la trazabilidad documental.
"""

