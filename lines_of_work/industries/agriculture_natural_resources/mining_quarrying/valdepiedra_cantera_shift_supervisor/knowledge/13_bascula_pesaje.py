title = "Pesaje: báscula puente y báscula de cinta"

content = """
- La báscula puente pesa cada camión a la entrada (tara) y a la salida (bruto); la
  diferencia es el peso neto de árido despachado, base del albarán.
- La báscula puente se verifica metrológicamente de forma periódica; su error máximo
  admisible en servicio es del orden del 0,5 % de la carga. Se realizan comprobaciones con
  pesas patrón según el plan de calibración.
- La báscula de cinta (pesómetro) integra el caudal en marcha para conocer la producción en
  tiempo real; su exactitud típica es de ±0,5 % a ±1 %. Se calibra con cadena patrón o
  material de peso conocido.
- Antes de cada turno se comprueba el cero de la báscula de cinta con la banda en marcha y
  vacía; una deriva del cero falsea toda la contabilidad de producción.
- El camión se sitúa completamente sobre la plataforma, sin apoyarse fuera, con el motor al
  ralentí y sin personas ajenas encima durante el pesaje.
- Cada albarán registra matrícula, producto, fracción, tara, bruto, neto, fecha, hora y
  destino; los datos deben ser trazables y coherentes con el pesaje.
- Discrepancias entre báscula de cinta y báscula puente por encima de la tolerancia se
  investigan: posibles causas son suciedad en el pesómetro, descentrado de cinta o
  descalibración.
- El jefe de turno supervisa que no se manipulen los equipos de pesaje y que las incidencias
  se anoten para revisión de mantenimiento y calidad.
"""

