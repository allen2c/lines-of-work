title = "Perforación de barrenos"

content = """
- La altura de banco de referencia es de 12 m (rango 10-15 m). El perforista ejecuta la
  malla según el diseño entregado por el responsable técnico.
- Diámetro de barreno habitual: 89-115 mm. Para el diámetro de 89 mm se usan mallas con
  piedra de 2,8-3,2 m y espaciamiento de 3,3-3,8 m.
- La sobreperforación es de 0,3-0,5 m por debajo de la cota de banco para asegurar un piso
  limpio y evitar repiés (resaltes en la base).
- Verifica la inclinación de los barrenos (típicamente 10-20° respecto a la vertical) con
  inclinómetro; una desviación excesiva altera piedra real y puede causar proyecciones.
- Registra la profundidad real de cada barreno y comunica al responsable de voladura las
  desviaciones, coqueras o presencia de agua, que condicionan el explosivo a emplear.
- Mantén las bocas de los barrenos taponadas y señalizadas para evitar caídas de material
  y accidentes hasta la carga.
- Controla el polvo de perforación mediante captador o inyección de agua; el perforista usa
  protección respiratoria adecuada frente a la sílice.
- No se perfora sobre restos de barrenos antiguos ni en zonas con posibles cargas sin
  detonar: ante la duda, detén y consulta.
"""

