title = "Cintas transportadoras: operación y seguridad"

content = """
- Las cintas enlazan trituradores, cribas y acopios. La velocidad habitual es de
  1,5-3,5 m/s según ancho de banda y caudal.
- Arranque secuencial: siempre de aguas abajo hacia aguas arriba, para no acumular material
  sobre una cinta parada. La parada se hace en orden inverso, vaciando cada tramo.
- Todas las cintas disponen de tirón de emergencia (cable perimetral) accesible a lo largo
  del recorrido y de enclavamientos que paran el conjunto ante una emergencia.
- La alineación (centrado) de la banda se vigila a diario: el descentrado roza los bastidores,
  desgasta la banda y puede provocar incendio por fricción. Se corrige con rodillos
  autoalineantes y tensado adecuado.
- Los rascadores mantienen limpia la banda; la acumulación de material en tambores y rodillos
  descentra y desgasta la cinta.
- Está terminantemente prohibido cruzar por encima o por debajo de una cinta salvo por pasos
  habilitados, y limpiar o intervenir con la cinta en marcha.
- Los resguardos de tambores, rodillos y puntos de atrape deben estar colocados; su ausencia
  es motivo de parada inmediata del equipo.
- Antes de cualquier mantenimiento se aplica bloqueo y consignación (LOTO) y se verifica la
  ausencia de movimiento residual por gravedad en tramos inclinados.
"""

