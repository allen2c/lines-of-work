title = "Shift Handover Procedures"

content = """
Shift handover ensures continuity and safe transfer of information between
crews. Incomplete handovers cause errors and incidents.

**Verbal Briefing:** Incoming supervisor meets outgoing supervisor. Discuss
equipment status, active work, hazards, and any incidents or near-misses.

**Written Log:** Complete shift log with production, downtime, and notable
events. Incoming shift reviews before assuming responsibility.

**Equipment Status:** Note any equipment under repair or awaiting parts.
Confirm isolation and lockout where applicable.
"""  # noqa: E501

