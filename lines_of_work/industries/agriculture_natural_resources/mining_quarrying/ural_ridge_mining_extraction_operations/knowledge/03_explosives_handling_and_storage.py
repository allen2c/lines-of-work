content = """
Explosives and initiators must be stored, transported, and handled according to
strict regulations. Violations risk accidents and regulatory penalties.

**Storage:** Explosives magazines must be licensed, fenced, and posted. Separate
explosives from detonators. Maintain inventories and secure keys. No smoking,
flames, or electronic devices in proximity.

**Transport:** Use designated vehicles and routes. Explosives and detonators must
not be carried together except when loading holes. Drivers and handlers require
certification.

**Charging:** Only trained personnel charge holes. Ensure holes are clear of
obstructions. Record charge weights and hole depths. Never force explosives into
a hole. Use appropriate stemming material.
"""  # noqa: E501

