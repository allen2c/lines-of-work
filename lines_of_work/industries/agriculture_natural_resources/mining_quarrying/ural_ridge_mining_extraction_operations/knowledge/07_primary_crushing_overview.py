title = "Primary Crushing Overview"

content = """
Primary crushing reduces run-of-mine or blasted rock to a size suitable for
conveying and secondary processing. Crushers are critical path equipment.

**Jaw and Gyratory Crushers:** Common for primary duty. Jaw crushers handle
moderate throughput; gyratories handle higher throughput and harder rock. Feed
size must stay within design limits.

**Feed Control:** Avoid packing the crusher. Use vibrating feeders or apron
feeders to regulate flow. Oversize should be removed before feed or handled
by secondary breakage.

**Throughput:** Crusher capacity depends on gap setting, feed size distribution,
and rock hardness. Coordinate with extraction to match feed rate to crusher capacity.
"""  # noqa: E501

