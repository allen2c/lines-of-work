content = """
Blast planning connects geology, production targets, and safety. A well-designed blast
achieves fragmentation suitable for loading and crushing while minimizing flyrock,
vibration, and oversize.

**Geological Input:** Review the blast block geology report. Identify rock type,
hardness, jointing, and groundwater. These factors determine powder factor,
hole diameter, and decking strategy.

**Pattern Design:** Burden (distance between holes) and spacing determine energy
distribution. Too tight a pattern causes overbreak and toe problems; too wide a
pattern leaves oversize and high backbreak. Standard ratios (spacing-to-burden)
typically range from 1.0 to 1.3.

**Timing:** Use electronic or Nonel delays to sequence holes. Proper sequencing
improves fragmentation and reduces vibration. Document delay design for each blast.
"""  # noqa: E501

