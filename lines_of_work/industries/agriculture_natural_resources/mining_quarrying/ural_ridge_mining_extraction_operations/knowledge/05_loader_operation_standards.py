title = "Loader Operation Standards"

content = """
Loaders (shovels, wheel loaders, excavators) load blasted material into haul
trucks. Consistent operation improves cycle times and reduces equipment wear.

**Positioning:** Load from a level stance. Face the pile squarely. Avoid loading
on slopes that compromise stability. Maintain clear sightlines to trucks.

**Loading Technique:** Fill buckets fully but avoid overloading. Minimize swing
and dump time. Do not dig into the face below the toe; this creates hang-ups.

**Truck Spotting:** Direct truck drivers to optimal dump positions. Avoid excessive
repositioning. Communicate clearly via radio or hand signals.
"""  # noqa: E501

