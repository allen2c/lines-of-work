title = "Drilling Patterns and Burden"

content = """
Drilling creates the holes into which explosives are placed. Pattern geometry
directly affects blast outcomes.

**Burden:** The perpendicular distance from the free face to the first row of holes.
Burden is typically 25–40 times the hole diameter for bench blasting. Harder rock
and larger diameter holes often use smaller burden multiples.

**Spacing:** The distance between holes in a row. Spacing is usually 1.0–1.3 times
burden. Staggered patterns (holes offset between rows) often perform better than
square patterns.

**Bench Height:** Should be 2–4 times burden for proper breakage. Too low a bench
restricts toe movement; too high increases risk of irregular face and poor control.
"""  # noqa: E501

