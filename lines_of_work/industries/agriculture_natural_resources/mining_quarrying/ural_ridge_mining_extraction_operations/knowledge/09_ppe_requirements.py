title = "PPE Requirements"

content = """
Personal protective equipment (PPE) is mandatory for all personnel in extraction
areas. PPE does not replace engineering controls but is a required last line of
defense.

**Hard Hat:** Worn at all times in the pit, plant, and shop. Replace after impact.

**Safety Boots:** Steel-toe or equivalent. Must meet site specification for
puncture and slip resistance.

**High-Visibility Vest:** Required in areas with mobile equipment. Ensures
visibility to operators.

**Hearing Protection:** Required in high-noise areas (drills, crushers, haul
routes). Provide appropriate NRR-rated protection.
"""  # noqa: E501

