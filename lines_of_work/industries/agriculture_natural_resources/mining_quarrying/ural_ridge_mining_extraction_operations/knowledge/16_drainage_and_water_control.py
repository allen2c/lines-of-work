title = "Drainage and Water Control"

content = """
Water in the pit affects stability, equipment operation, and environmental
compliance. Proper drainage protects the operation and downstream receiving
waters.

**Pit Drainage:** Pumps and sumps collect pit water. Maintain pump capacity
for storm events. Plan for rising groundwater as mining deepens.

**Sediment Control:** Sediment basins and berms capture runoff before it leaves
the site. Inspect and maintain structures. Discharge only per permit.

**Erosion:** Revegetate disturbed areas where possible. Use erosion control
blankets and check dams on slopes. Monitor for gullies and repair promptly.
"""  # noqa: E501

