title = "Haul Road Design and Maintenance"

content = """
Haul roads connect loading faces to crushers and dumps. Proper design and maintenance
reduce cycle time, tire wear, and accidents.

**Design:** Gradient should not exceed design limits for loaded trucks (typically
8–10% sustained). Provide adequate width for two-way traffic and passing. Plan
drainage to prevent ponding and erosion.

**Maintenance:** Water for dust control according to schedule. Grade roads to
restore crown and remove potholes. Maintain berms on fills and critical curves.
Monitor road condition with regular inspections.

**Dust Control:** Water sprays, chemical dust suppressants, or road surface
treatment. Balance cost with effectiveness and environmental constraints.
"""  # noqa: E501

