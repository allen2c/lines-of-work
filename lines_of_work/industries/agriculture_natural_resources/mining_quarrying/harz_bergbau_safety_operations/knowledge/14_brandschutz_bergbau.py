content = """
Brände in Bergwerken und Steinbrüchen bergen besondere Risiken: eingeschlossene Personen,
Rauchausbreitung, Explosionsgefahr. Brandschutzmaßnahmen sind daher zentral.

**Prävention:** Rauchverbot in explosionsgefährdeten Bereichen. Lagerung brennbarer Stoffe
nach Vorschrift. Regelmäßige Prüfung elektrischer Anlagen. Heißarbeiten nur mit Genehmigung.

**Löschmittel:** Feuerlöscher müssen in ausreichender Anzahl und Art vorhanden sein. Für
elektrische Anlagen sind Kohlendioxid- oder Pulverlöscher geeignet. Löschwasserstellen
müssen zugänglich sein.

**Alarmierung:** Brandmeldeanlagen und manuelle Alarmierung. Fluchtwege und Sammelplätze
müssen allen bekannt sein. Regelmäßige Brandschutzübungen sind Pflicht.
"""  # noqa: E501

