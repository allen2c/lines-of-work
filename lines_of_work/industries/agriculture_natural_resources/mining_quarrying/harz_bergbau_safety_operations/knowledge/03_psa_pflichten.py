title = "Persönliche Schutzausrüstung (PSA) im Bergbau"

content = """
PSA ist die letzte Verteidigungslinie gegen Gefahren, die nicht durch technische
oder organisatorische Maßnahmen beseitigt werden können. Im Bergbau sind bestimmte
PSA-Arten vorgeschrieben.

**Kopfschutz:** Schutzhelme nach EN 397 sind auf dem gesamten Betriebsgelände
Pflicht. Bei Überkopfarbeiten oder in Bereichen mit Absturzgefahr sind Helme mit
Kinnriemen zu verwenden.

**Augen- und Gesichtsschutz:** Schutzbrillen oder -visiere bei Staub, Splittern,
Funkenschlag oder chemischen Spritzern. Bei Schweißarbeiten sind
Schweißerschutzschilde erforderlich.

**Gehörschutz:** In Lärmbereichen ab 85 dB(A) ist Gehörschutz bereitzustellen; ab
90 dB(A) besteht Tragepflicht. Ohrstöpsel oder Kapselgehörschützer je nach
Exposition.

**Hand- und Fußschutz:** Schutzhandschuhe und Sicherheitsschuhe mit Zehenschutz
sind Standard. In Sprengbereichen können zusätzliche Anforderungen gelten.
"""  # noqa: E501

