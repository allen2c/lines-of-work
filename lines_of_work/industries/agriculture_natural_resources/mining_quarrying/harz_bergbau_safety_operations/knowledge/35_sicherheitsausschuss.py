title = "Sicherheitsausschuss — Mitwirkung der Arbeitnehmer"

content = """
Der Sicherheitsausschuss berät den Arbeitgeber in Fragen des Arbeitsschutzes. Er setzt
sich aus Vertretern des Arbeitgebers, der Arbeitnehmer und des Betriebsarztes sowie
der Fachkraft für Arbeitssicherheit zusammen.

**Einrichtung:** In Betrieben mit mehr als 20 Beschäftigten ist ein
Sicherheitsausschuss zu bilden. Im Bergbau gilt dies für ober- und untertägige
Betriebe.

**Aufgaben:** Beratung bei der Planung und Durchführung von Arbeitsschutzmaßnahmen,
Unterstützung bei der Unfallverhütung, Erörterung von Unfallursachen und Vorschlägen
zur Verbesserung. Der Ausschuss hat kein Entscheidungsrecht, aber ein Anhörungsrecht.

**Sitzungen:** Mindestens vierteljährlich. Protokolle werden geführt und den
Aufsichtsbehörden auf Verlangen vorgelegt.
"""  # noqa: E501

