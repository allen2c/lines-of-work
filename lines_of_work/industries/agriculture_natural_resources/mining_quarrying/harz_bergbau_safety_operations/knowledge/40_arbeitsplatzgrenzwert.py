title = "Arbeitsplatzgrenzwerte (AGW) — Staub und Gase"

content = """
Arbeitsplatzgrenzwerte definieren die maximale Konzentration von Stoffen in der
Atemluft, bei der keine gesundheitlichen Schäden zu erwarten sind. Im Bergbau sind
insbesondere Staub und Gase relevant.

**E-Staub:** Allgemeiner Staubgrenzwert. Im Bergbau oft überschritten bei Abbau,
Bohren, Sprengen. Maßnahmen: Absaugung, Nassabbau, Atemschutz.

**A-Staub:** Alveolengängiger Feinstaub. Besonders gefährlich, kann zu Silikose
führen. Grenzwert strenger als E-Staub.

**Gase:** Methan, CO, CO2, NOx. Untertage können sich Gase ansammeln. Kontinuierliche
Überwachung und Wetterführung erforderlich. AGW je nach Stoff unterschiedlich.
"""  # noqa: E501

