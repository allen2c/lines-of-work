content = """
Đất và nước là hai yếu tố đầu vào có thể mang mối nguy ô nhiễm vào rau, nên phải được đánh giá và
quản lý chặt theo VietGAP để bảo đảm an toàn thực phẩm.

**Chất lượng đất:**
- Đánh giá vùng trồng để loại trừ nguy cơ ô nhiễm kim loại nặng (chì, cadimi, thủy ngân, asen) và
  tồn dư hóa chất từ hoạt động trước đó.
- Cách ly khu vực sản xuất với nguồn gây ô nhiễm như bãi rác, khu công nghiệp, chuồng trại chưa xử lý.
- Duy trì độ phì và sức khỏe đất bằng phân hữu cơ hoai mục, luân canh, hạn chế lạm dụng hóa chất.
- Theo dõi pH, độ mặn; cải tạo khi cần (bón vôi cho đất chua).

**Chất lượng nước tưới:**
- Dùng nước sạch, không dùng nước thải, nước ô nhiễm tưới trực tiếp lên phần rau ăn được.
- Kiểm tra định kỳ chỉ tiêu vi sinh (E. coli, coliform) và kim loại nặng theo yêu cầu.
- Bảo vệ nguồn nước khỏi nhiễm bẩn từ phân, thuốc; không rửa bình phun, đổ thuốc thừa vào ao, kênh.

**Xử lý khi phát hiện nguy cơ:**
- Nếu kết quả kiểm tra vượt ngưỡng, tạm dừng sử dụng nguồn nước/khu đất đó, tìm nguyên nhân và biện
  pháp khắc phục; báo cán bộ kỹ thuật trưởng và ban quản lý.
- Lưu hồ sơ kết quả phân tích đất, nước làm bằng chứng cho đánh giá chứng nhận.

Quản lý tốt đất và nước là phòng tuyến đầu tiên bảo đảm rau an toàn từ gốc.
"""

