content = """
Thuốc bảo vệ thực vật (BVTV) chỉ là biện pháp cuối cùng trong IPM và phải được dùng an toàn,
đúng quy định để bảo vệ người, sản phẩm và môi trường.

**Nguyên tắc "4 đúng":**
- **Đúng thuốc:** chỉ dùng thuốc trong danh mục được phép sử dụng trên rau, đúng đối tượng dịch hại;
  không dùng thuốc cấm, thuốc ngoài danh mục.
- **Đúng liều, đúng nồng độ:** pha theo khuyến cáo nhãn thuốc; không tự ý tăng liều.
- **Đúng lúc:** phun khi dịch hại đến ngưỡng, ưu tiên giai đoạn mẫn cảm của sâu bệnh, tránh phun
  khi cây đang thu hoạch hoặc sắp thu hoạch.
- **Đúng cách:** phun đều, đúng bộ phận bị hại, trời lặng gió, không phun khi sắp mưa.

**An toàn khi pha và phun:**
- Mang đầy đủ bảo hộ: khẩu trang/mặt nạ, kính, găng tay, ủng, quần áo dài. Không ăn uống, hút thuốc khi phun.
- Pha thuốc nơi thoáng, xa nguồn nước sinh hoạt; dùng đúng dụng cụ đong; không đổ thuốc thừa xuống ao, kênh.
- Phun xuôi theo chiều gió; cắm biển cảnh báo và cách ly khu vực vừa phun.

**Sau khi phun:**
- Rửa sạch bình, dụng cụ; thu gom bao bì, chai lọ thuốc để nơi quy định, không tái sử dụng, không vứt bừa bãi.
- Tắm rửa, thay quần áo; giặt riêng đồ bảo hộ. Ghi lại tên thuốc, liều, ngày phun, thời gian cách ly.

Bảo quản thuốc trong kho riêng có khóa, xa tầm trẻ em, xa thực phẩm và nguồn nước.
"""

