title: str = "Umidade dos Grãos na Colheita e Armazenagem"

content: str = """
A umidade dos grãos determina a segurança do armazenamento e o custo de
secagem. Colher fora da faixa ideal aumenta perdas e riscos de deterioração.

**Faixa Ideal de Colheita:** 13–15% para colheita direta sem secagem imediata.
Abaixo de 13% aumentam perdas por debulha e quebra; acima de 15% exige
secagem antes do armazenamento.

**Armazenamento Seguro:** Grãos armazenados em silos devem estar entre 12 e
13% de umidade para evitar aquecimento e desenvolvimento de fungos. Monitore
temperatura e umidade no centro da massa armazenada.

**Secagem:** Secadores a fluxo contínuo ou intermitente reduzem umidade.
Evitar temperatura do ar acima de 60 °C para não prejudicar a qualidade
protéica. Resfriar grãos após secagem antes de armazenar.
"""

