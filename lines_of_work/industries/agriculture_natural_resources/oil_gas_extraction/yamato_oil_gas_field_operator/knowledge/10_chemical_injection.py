title = "薬品注入システムの管理"

content = """
- 注入薬品の種類：腐食防止剤、スケール防止剤、消泡剤、メタノール（ハイドレート防止）、乳化破壊剤。各薬品の注入ポイントと目標濃度。
- 注入ポンプの監視：往復動ポンプのストローク数、ダイヤフラムポンプの脈動、流量計の指示値。ポンプの漏洩チェック（パッキン、シール）。
- タンク残量管理：各薬品タンクの液面を毎シフト確認。残量が20%を下回ったら補充依頼。在庫管理システムに入力。
- 注入量調整：分析結果（腐食速度、スケール付着、泡立ち）に基づき、エンジニアの指示で注入量を変更。変更後は24時間監視。
- 安全注意：薬品のMSDSを常備。取り扱い時は保護手袋、保護眼鏡、防護服着用。漏洩時は中和剤または吸着材で処理。
- 定期メンテナンス：注入ポンプの月1回の点検（バルブ、シール、フィルター）。年1回の校正。
"""

