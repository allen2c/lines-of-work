title = "作業許可証（PTW）の運用"

content = """
- PTWの種類：一般作業許可、高温作業許可、危険物取扱許可、閉鎖空間入構許可、電気作業許可。作業内容に応じて適切な許可証を発行。
- 発行手順：作業依頼者が申請書を提出 → 現場監督がリスク評価 → 安全担当者が承認 → 現場オペレーターが現場確認後、作業開始。
- 現場オペレーターの役割：PTWの内容（作業範囲、危険性、保護具、隔離措置、緊急時対応）を作業者に説明。作業開始前に隔離確認（ロックアウト/タグアウト）を実施。
- 作業中の立会い：危険作業（高所、閉鎖空間、活線）は常時立会い。その他は定期的に巡回。作業中断時はPTWを一時停止。
- 作業終了確認：作業者が現場を原状回復したことを確認し、PTWに署名。隔離解除はオペレーターのみが行う。
- PTWの保管：発行から1年間保存。監査時に提示可能な状態。
"""

