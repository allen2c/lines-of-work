title = "ロックアウト/タグアウト（LOTO）手順"

content = """
- 対象：電気スイッチ、バルブ、ポンプ、コンプレッサーなど、誤操作による危険を防止するためのエネルギー隔離。
- 手順：①作業対象の機器を特定 ②通常の停止操作 ③エネルギー源の遮断（電気ブレーカー、バルブ閉） ④ロックアウトデバイス（パッドロック）を装着 ⑤タグ（警告タグ）に作業者名・日時・理由を記入 ⑥隔離確認（テスト操作でエネルギーが供給されないことを確認）。
- 複数作業者：グループLOTOの場合、各作業者が自分のロックを装着。マスターロックは監督者が管理。
- 解除手順：①作業完了確認 ②作業者自身がロックを解除 ③タグを除去 ④エネルギー復旧（制御室と連携） ⑤機器の試運転。
- 禁止事項：他人のロックを無断で解除しない。ロックの代用に結束バンド等を使用しない。タグのみの使用は不可。
- 違反時の措置：LOTO違反は重大な安全違反として報告。再教育と懲戒の対象。
"""

