title = "閉鎖空間入構手順"

content = """
- 定義：タンク内部、ピット、配管内部など、換気が不十分で酸素欠乏や有害ガス滞留の可能性がある空間。
- 入構前の準備：隔離（電気、配管、機械的ロックアウト）、換気（強制換気30分以上）、ガス測定（酸素19.5～23.5%、可燃性ガス<10%LEL、H2S<10ppm、CO<25ppm）。
- 入構許可：PTW（閉鎖空間入構許可）を発行。入構者全員の資格確認（特別教育修了）。監視員を配置。
- 入構中の監視：連続ガスモニタリング、監視員との常時通信（無線またはロープ信号）。30分ごとにガス再測定。
- 緊急時対応：ガス警報または異常時は直ちに退避。救出は訓練を受けた者以外は行わない。外部の救急隊を要請。
- 記録：入構者名簿、ガス測定結果、作業時間を記録。入構後は設備の復旧確認。
"""

