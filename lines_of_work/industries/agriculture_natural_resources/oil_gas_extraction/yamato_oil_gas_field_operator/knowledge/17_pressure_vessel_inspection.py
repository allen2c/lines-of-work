title = "圧力容器の定期点検"

content = """
- 対象：セパレーター、コンプレッサー、タンク、熱交換器など。高圧ガス保安法に基づく法定検査（年1回の内部開放検査、4年ごとの耐圧試験）。
- 内部開放検査：設備を停止し、内容物を除去、洗浄後、内部を目視点検。腐食、亀裂、変形、溶接部の異常を確認。厚さ測定を実施。
- 耐圧試験：設計圧力の1.5倍の水圧試験。昇圧速度、保持時間（10分）、漏洩確認。試験後は乾燥。
- 非破壊検査（NDT）：超音波探傷、磁粉探傷、浸透探傷を必要に応じて実施。結果は記録し、次回検査まで保存。
- 検査記録：検査日、検査者、結果、補修履歴を設備台帳に記入。法定検査は所轄官庁に報告。
- オペレーターの役割：検査前の隔離・洗浄・換気、検査中の立会い、検査後の復旧。検査日程の調整。
"""

