title = "Abandono y sellado de pozos"

content = """
El abandono de pozos requiere procedimientos aprobados para sellar el pozo de forma
permanente. Incluye cementación de intervalos productivos, corte de casing y sellado
superficial. La ANLA y autoridades locales exigen planes y certificación. Un abandono
inadecuado puede causar contaminación y responsabilidad legal.
"""  # noqa: E501

