title = "Prevención y control de parafinas"

content = """
Las parafinas son hidrocarburos de cadena larga que precipitan en tuberías y equipos cuando
la temperatura baja. Causan obstrucciones y reducen la producción. Medidas: calentamiento
de líneas, inyección de inhibidores, pigging regular. Monitorear temperaturas de flujo y
aplicar tratamientos antes de que se acumulen depósitos significativos.
"""  # noqa: E501

