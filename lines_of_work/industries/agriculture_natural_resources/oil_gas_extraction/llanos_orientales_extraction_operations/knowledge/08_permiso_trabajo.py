title = "Permiso de trabajo"

content = """
El permiso de trabajo (PT) autoriza tareas que implican riesgos específicos: trabajo en
caliente, espacios confinados, altura, excavación. El solicitante y el autorizador deben
evaluar riesgos, definir controles y firmar. Sin PT válido no se inicia la tarea. Al
finalizar, cerrar el PT y confirmar que el área quedó segura.
"""  # noqa: E501

