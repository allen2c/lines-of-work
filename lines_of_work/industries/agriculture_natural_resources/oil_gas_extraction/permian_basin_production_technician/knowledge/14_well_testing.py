title = "Well Testing – Separator Test and Sampling"

content = """\
- Conduct a well test every 30 days or when flow changes significantly. Use a test separator (if available) or a portable test unit.
- Run the well through the test separator for 4–8 hours. Record oil, water, and gas rates. Take oil and water samples for BS&W and salinity.
- For gas wells, use a critical flow prover or orifice meter. Record flowing tubing pressure and temperature.
- Compare test results to previous tests; if oil rate drops >20%, check for scale, paraffin, or mechanical issues.
- Report test results to the Foreman and update the well's production allocation in the SCADA system.
- Label and store samples in a cool place; send to lab within 24 hours.
"""

