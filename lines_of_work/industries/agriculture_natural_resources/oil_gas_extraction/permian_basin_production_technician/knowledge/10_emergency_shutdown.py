title = "Emergency Shutdown (ESD) – Manual and Automatic"

content = """\
- Know the location of all ESD buttons on the pad (wellhead, separator, tank battery). Pressing any ESD will close all wing valves and shut down heaters and compressors.
- Automatic ESD triggers: high pressure (>1,200 psi), low pressure (<50 psi), gas detection (H2S or LEL), fire detection.
- After an ESD, do not reset until the cause is identified and corrected. Notify the Foreman and Control Room before resetting.
- To reset, first verify that all equipment is safe (no gas leaks, no fire). Then press the reset button on the ESD panel and manually reopen wing valves one at a time.
- Record the ESD event in the daily report: time, cause, actions taken, and duration of shutdown.
- Test ESD system monthly by simulating a trip (with Foreman approval). Ensure all valves close within 5 seconds.
"""

