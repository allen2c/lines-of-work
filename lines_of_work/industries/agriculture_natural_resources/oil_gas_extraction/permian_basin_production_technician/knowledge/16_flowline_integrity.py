title = "Flowline Integrity – Pressure Testing and Corrosion Monitoring"

content = """\
- Perform a pressure test on new or repaired flowlines: test to 1.5 times maximum operating pressure (e.g., 1,500 psi for a 1,000 psi line). Hold for 30 minutes with <5% pressure drop.
- Install corrosion coupons at key locations (wellhead, separator inlet, tank battery). Retrieve and send to lab every 3 months.
- Monitor flowline pressure drop; an increase of >20 psi over baseline may indicate scale or paraffin buildup.
- Inspect flowline for leaks weekly: look for wet spots, vegetation changes, or odor. Use a gas detector if H2S is present.
- If a leak is suspected, shut in the well and isolate the section. Report immediately to Foreman and environmental team.
"""

