title = "Valve Maintenance – Gate, Ball, Check, and Actuated Valves"

content = """\
- Operate all manual valves (gate, ball) fully open or fully closed; never leave partially open to avoid seat damage.
- Lubricate valve stems quarterly with appropriate grease (e.g., for high-pressure gate valves, use moly-based grease).
- Check actuated valves (e.g., ESD valves, dump valves) for proper operation: cycle them weekly and verify position indicator.
- If a valve is hard to turn, do not force it. Apply penetrating oil and try again. If still stuck, tag it and report for maintenance.
- Inspect check valves for backflow: listen for chatter or check downstream pressure. Replace if leaking.
- Record valve maintenance in the CMMS: valve tag number, date, lubrication, and any issues.
"""

