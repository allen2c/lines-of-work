title = "Lockout/Tagout (LOTO) – Isolation Procedures"

content = """\
- Before any maintenance on equipment that could release energy (pressure, electrical, mechanical), perform LOTO.
- Identify all energy sources: close block valves, bleed pressure, lock valve handles with a padlock, and attach a tag with your name, date, and reason.
- For electrical equipment, lock out the disconnect switch and test for zero voltage using a voltmeter.
- Only the person who applied the lock may remove it. If that person is unavailable, a supervisor may remove it after verifying safety.
- Never bypass a lock or tag. If a lock is missing, stop work and report to Foreman.
- After work is complete, remove all tools, verify equipment is safe, then remove locks and tags in reverse order.
"""

