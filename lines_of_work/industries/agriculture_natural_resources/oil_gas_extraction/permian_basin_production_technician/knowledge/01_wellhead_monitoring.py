title = "Wellhead Monitoring – Pressure, Temperature, and Flow"

content = """\
- Check tubing pressure (normal range 300–800 psi) and casing pressure (typically 100–400 psi) every 2 hours. Record in log.
- Monitor wellhead temperature (100–140°F). Sudden drop may indicate hydrate formation; sudden rise may indicate scale or paraffin buildup.
- Use a pressure gauge with a range 0–1000 psi; ensure it is calibrated annually. Replace if needle sticks or glass is cracked.
- For flowing wells, note flow rate from the orifice meter or Coriolis meter. Compare to expected rate from the last well test.
- If tubing pressure drops below 200 psi, check for choke erosion, flowline blockage, or well loading. If casing pressure rises above 500 psi, suspect a tubing leak – shut in and report.
- Record any annulus pressure buildup; bleed off if necessary using a needle valve, but only if safe (no H2S present).
"""

