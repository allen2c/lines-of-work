title = "变更管理（MOC）流程"

content = """
- 变更类型：工艺参数变更（如温度范围调整）、设备变更（如更换不同型号泵）、操作程序变更（如修改DCS逻辑）、物料变更（如更换原料供应商）。任何变更必须启动MOC流程。
- 申请与评估：操作员发现需要变更时，填写《变更申请单》，说明变更原因、内容、风险评估。由车间主任组织工艺、设备、HSE、仪表等专业人员评审，必要时进行HAZOP分析。
- 审批与实施：评审通过后，经生产副总批准。实施前进行培训（操作员需掌握新参数或新程序），更新操作规程和DCS画面。变更实施后试运行72小时，无异常方可正式投用。
- 关闭与归档：试运行结束后，操作员填写《变更关闭报告》，确认效果。所有MOC文件（申请、评审、培训记录、试运行数据）归档保存至装置生命周期结束。
"""

