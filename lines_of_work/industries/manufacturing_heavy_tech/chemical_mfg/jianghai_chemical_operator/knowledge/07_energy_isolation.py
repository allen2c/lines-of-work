content = """
- 隔离范围：设备检修前需隔离所有能量源，包括电源（断开断路器并上锁）、蒸汽（关闭手动阀并加盲板）、工艺物料（关闭阀门并加盲板或双阀关闭加锁）、氮气（关闭阀门并加盲板）。
- 上锁步骤：操作员填写《能量隔离清单》，由班长确认隔离点。操作员使用个人挂锁（每人一把，钥匙唯一）锁在隔离点，并挂上“禁止操作”警示牌。多人作业时使用集体锁箱。
- 验证程序：上锁后，操作员尝试启动设备（如按启动按钮）确认无动作，并使用检测仪器（如电压表、压力表）确认能量已隔离。验证后通知维修人员开始作业。
- 解锁程序：维修完成后，操作员与维修人员共同检查现场，确认工具和人员已撤离。操作员取下个人挂锁，班长确认所有锁已移除，然后恢复能量供应。严禁他人代取锁。
"""

