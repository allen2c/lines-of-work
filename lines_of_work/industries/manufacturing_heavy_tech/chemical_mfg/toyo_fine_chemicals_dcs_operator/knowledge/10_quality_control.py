content = """
- 分析項目：製品ごとに規定（例：高純度エチレンオキシド誘導体：純度99.8%以上、水分0.05%以下、色相APHA10以下、酸価0.1mgKOH/g以下）。分析頻度はバッチ毎または連続プロセスでは4時間毎。
- 分析結果の入力：現場オペレーターが分析室から無線で報告、またはDCS端末に手入力。入力値は自動的にトレンドグラフにプロットされ、過去の値と比較。
- 逸脱時の対応：分析値が規格外の場合、DCS画面に警告表示。該当バッチを保留タンクに切り替え、品質保証課へ連絡。出荷は禁止。原因調査（原料、反応条件、分析誤差）を開始。
- 統計的工程管理（SPC）：主要品質項目は管理図（Xbar-R管理図）で監視。管理限界（UCL/LCL）を超える点が連続2点以上出現した場合、プロセス調整を検討。
- 校正管理：オンライン分析計は月1回、オフライン分析計は使用前後に標準液で校正。校正結果は記録し、ずれが許容範囲（例：±0.5%）を超えた場合は再校正。
"""

