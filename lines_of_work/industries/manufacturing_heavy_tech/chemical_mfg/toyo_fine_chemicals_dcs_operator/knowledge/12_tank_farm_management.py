content = """
- タンク一覧：原料タンク（ステンレス製、容量50～200m³、窒素シール）、製品タンク（同、加温・保温仕様あり）、中間体タンク（容量20～100m³）。各タンクに液面計（レーダー式）、温度計、圧力計、緊急通気弁。
- 在庫管理：DCS画面で全タンクの液面・温度・圧力をリアルタイム表示。在庫システム（SAP）と連携し、1時間ごとに自動照合。差異が±2%を超えた場合は、現場確認と原因調査。
- 受入操作：原料タンクローリー到着時、DCSで受入ラインのバルブを開き、ポンプ起動。受入量は計量器で確認し、タンク液面上昇と照合。受入後、サンプリングと分析。
- 払出操作：出荷計画に基づき、製品タンクから充填ラインへ移送。充填中は液面低下速度を監視し、目標充填量に達したら自動停止。充填量誤差は±0.5%以内。
- タンク切替：一つのタンクが空またはメンテナンスの場合、別のタンクに切り替え。切替時はバルブ開閉順序をSOPに従い、混入防止のためライン洗浄を実施。
"""

