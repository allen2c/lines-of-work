content = """
- 対象作業：火気使用（溶接、グラインダー）、高温作業、閉所内作業、高所作業、電気作業、危険物取扱い。すべての作業は作業許可書（PTW）が必要。
- PTW発行手順：作業依頼者がPTW申請書を提出→現場監督者がリスクアセスメントを実施→安全衛生課が承認→DCSオペレーターが運転状況を確認し、該当設備の停止・アイソレーションを実施→作業開始。
- DCSオペレーターの役割：PTWに記載された設備の運転停止・ロックアウト/タグアウト（LOTO）をDCSから実行。作業中は該当設備の状態を監視し、無関係な操作を禁止。作業完了後、復旧操作を実施。
- 緊急時のPTW中断：火災報知や重大アラーム発生時、すべてのPTW作業を中断し、作業者を退避させる。DCSオペレーターは無線で現場に退避指示を出す。
- PTW記録：発行番号、作業内容、期間、承認者、完了日時をシステムに記録。監査対応のため、最低3年間保存。
"""

