content = """
- 無線通信：使用チャンネルは工場内共通（CH1: 通常連絡、CH2: 緊急）。呼び出しは「DCSより現場、応答願います」、応答は「現場、どうぞ」。通信は簡潔に、確認は復唱。
- 指示の出し方：DCSオペレーターは、現場オペレーターに具体的な操作（例：「A-101バルブを50%開けてください」「サンプリングポイントP-3でサンプルを採取してください」）を指示。指示後、現場からの完了報告を待つ。
- 現場からの報告：現場オペレーターは、異常音、異臭、振動、リークなどを発見した場合、直ちにDCSに報告。DCSオペレーターは該当箇所のDCSデータを確認し、対応を指示。
- 協力作業：タンク切替やライン洗浄など、DCS操作と現場操作が連動する作業は、両者でタイミングを合わせる。DCSオペレーターは「準備完了」の合図を待ってから操作を実行。
- 安全確認：現場オペレーターが危険区域に入る場合、DCSオペレーターは該当設備の運転状態を安全側に固定し、無線で入域許可を出す。退出時も同様に確認。
"""

