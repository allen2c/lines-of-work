title = "緊急停止（ESD）手順"

content = """
- ESD発動条件：反応器圧力が上限警報（1.2MPa）を超え、かつ上昇傾向が止まらない場合、またはガス検知器が爆発下限界の25%以上を検知した場合。手動ESDボタンはDCS画面右上と現場計器室に設置。
- ESD操作：ESDボタンを押下すると、全原料供給遮断、反応器加熱停止、緊急冷却弁開放、ベントライン開放（フレアスタックへ）が自動実行。DCSは全操作をログに記録。
- ESD後の対応：①現場オペレーターに無線でESD作動を伝達、避難指示。②工場長・安全衛生課へ即時通報。③DCS画面で全機器の状態（閉止、停止）を確認。④原因究明のため、トレンドデータと操作履歴を保存（消去禁止）。
- 復旧手順：原因除去後、安全確認（ガス検知、温度低下）を経て、SOPに従い段階的に復旧。復旧には通常2～4時間。再発防止策をインシデントレポートに記載。
- 訓練：ESD訓練は年2回実施（実機停止を伴わないシミュレーション）。オペレーターは全員、年1回の実技試験に合格すること。
"""

