content = """
- エネルギー隔離は、電気、機械、熱、化学、圧力の5種類を対象とする。
- 電気的隔離：ブレーカーをOFFにし、ロックアウト。確認はテスターで電圧ゼロを確認。
- 機械的隔離：カップリングを外す、ベルトを外す、チェーンを切断。回転体が完全に停止していることを確認。
- 熱的隔離：配管の保温材を除去し、表面温度が40℃以下になるまで冷却。高温流体のバルブを閉鎖し、ドレンで抜く。
- 化学的隔離：配管にブラインド（盲板）を挿入、またはダブルブロック＆ブリードを実施。残留化学物質を洗浄・パージ。
- 圧力隔離：すべての圧力源を遮断し、ブローダウン弁で大気開放。圧力計でゼロを確認。
- 隔離後は、必ず「隔離確認チェックリスト」に記入し、2人でダブルチェック。
"""

