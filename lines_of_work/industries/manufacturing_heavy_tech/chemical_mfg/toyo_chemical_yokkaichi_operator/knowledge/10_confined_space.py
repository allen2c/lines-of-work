title = "閉鎖空間（タンク内）作業手順"

content = """
- 閉鎖空間とは、マンホール径が60cm以下、内部の高さが1.5m以上、換気が不十分な空間（タンク、反応器、ピット）。
- 入槽前の必須確認：酸素濃度19.5～23.5%、可燃性ガス濃度0.1%未満、硫化水素・一酸化炭素濃度が許容濃度以下。
- 作業中は常時換気（送風機）を実施。ガス検知器を携帯し、警報が鳴ったら直ちに退避。
- 入槽者は安全帯（命綱）を着用し、外部に監視員を1名配置。監視員は常に内部と連絡を取り合う。
- 緊急時は、監視員が直ちに救助を要請。無資格者は絶対に内部に入らない。
"""

