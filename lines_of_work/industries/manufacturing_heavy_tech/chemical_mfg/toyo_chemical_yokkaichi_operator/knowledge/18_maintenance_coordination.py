title = "保全作業との連携"

content = """
- 定期保全（TBM）は年1回の全設備停止期間中に実施。運転員は停止・起動の支援と、作業許可書の発行を担当。
- 日常保全（CBM）は、運転中に可能な範囲で実施。運転員は異常兆候（振動、温度上昇、異音）を発見したら保全課に連絡。
- 保全作業中は、運転員が現場で作業の進捗を監視。作業が予定時間を超過する場合は、シフトリーダーに報告。
- 保全完了後は、運転員が試運転（リークテスト、動作確認）を実施。問題なければ通常運転に復帰。
- 保全記録は設備台帳に反映。運転員は点検結果を運転日誌に記録。
"""

