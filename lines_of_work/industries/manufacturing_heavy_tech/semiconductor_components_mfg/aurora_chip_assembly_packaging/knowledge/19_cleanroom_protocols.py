"""Cleanroom protocols for assembly operations."""

title = "Cleanroom Protocols"

content = """
Assembly of semiconductor devices requires controlled environments to minimize
particulate and chemical contamination. Cleanrooms are classified by ISO 14644
(e.g., ISO Class 5, 7) based on particle count per cubic meter. Assembly
areas typically operate at ISO Class 5–7 depending on the process step.

**Gowning:** Proper gowning (hood, coverall, boots, gloves) is mandatory.
Training and audits ensure correct technique. No cosmetics, jewelry, or
exposed skin. **Access:** Airlocks and pressure cascades limit ingress of
contamination. Personnel and material flow are designed to avoid cross-
contamination. **Housekeeping:** Regular cleaning of surfaces, tools, and
floors. Use cleanroom-approved wipers and solutions; no shedding materials.

**Monitoring:** Particle counters and environmental data (temp, humidity)
are logged. Excursions trigger investigation. Contamination events (e.g.,
spill, breach) require containment, cleanup, and assessment of affected product.
"""  # noqa: E501

