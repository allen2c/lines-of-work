title = "Eutectic Die Attach"

content = """
Eutectic die attach uses a low-melting alloy (e.g., Au-Si eutectic at ~363°C, or
Au-Sn) to form a metallurgical bond between the die backside and the substrate.
This method offers superior thermal and electrical performance compared to epoxy
and is common in power devices and high-reliability applications.

**Process:** A preform or pre-plated alloy is placed on the substrate. The die is
positioned and the assembly is heated above the eutectic temperature. The alloy
melts and wets both surfaces, then solidifies upon cooling to form a strong bond.

**Advantages:** Low thermal resistance, high electrical conductivity, and excellent
reliability under thermal cycling. **Challenges:** Higher process temperature (can
stress sensitive dies), tighter process control required, and typically higher
cost. Equipment includes eutectic bonders with precise temperature and force control.
Recipe parameters (temperature, time, force, atmosphere) must be strictly maintained;
deviations can cause voids, non-wetting, or brittle intermetallics.
"""  # noqa: E501

