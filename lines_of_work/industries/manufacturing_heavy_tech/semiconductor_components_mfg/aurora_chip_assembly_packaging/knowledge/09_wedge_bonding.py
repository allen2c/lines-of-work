"""Wedge bonding for fine-pitch and aluminum wire applications."""

title = "Wedge Bonding"

content = """
Wedge bonding uses a wedge-shaped tool to press and ultrasonically weld the wire
to both the die pad and the substrate. Unlike ball bonding, both bonds are
wedge-style; there is no ball formation. Wedge bonding is used for Al wire and
for fine-pitch applications where ball diameter would limit pitch.

**Advantages:** Fine pitch capability, compatibility with bare Al pads (common
in many IC designs), lower material cost when using Al. **Typical applications:**
RF devices, MEMS, sensors, and high-pin-count packages.

**Process controls:** Wedge tool geometry and condition; bond force and ultrasonic
power and time; wire tension and loop height. Wedge tools wear faster than
capillaries in ball bonding; monitor bond strength and replace on schedule.
Loop profiles (height, span) must be designed to avoid shorting, wire sweep
during molding, and mechanical interference. Wire pull is the primary
destructive test for wedge bonds.
"""  # noqa: E501

