content = """
Ball shear and wire pull are destructive tests to verify wire bond integrity.
**Ball shear:** A shear tool moves horizontally against the ball (first bond)
until failure. Force and failure mode (ball lift, crater, wire break) are
recorded. Minimum force is specified per wire diameter (e.g., g/mil). Cratering
(lift of pad/substrate material) indicates excessive force or weak pad
metallization; escalate to process or design. **Wire pull:** A hook engages
the wire loop and pulls vertically until failure. Force and failure mode
(wire break, bond lift) are recorded. Minimum force per JEDEC or customer
spec.

**Sampling:** Typically 2–5 units per lot, multiple bonds per unit. Data
enters SPC. Out-of-spec or adverse failure modes trigger hold and
investigation. Capillary/wedge tool life is often linked to shear/pull
trends; replace tools before degradation.
"""  # noqa: E501

