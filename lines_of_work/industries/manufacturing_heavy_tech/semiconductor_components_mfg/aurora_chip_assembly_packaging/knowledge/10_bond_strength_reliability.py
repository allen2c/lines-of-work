title = "Bond Strength and Reliability"

content = """
Bond integrity is verified through destructive mechanical tests performed on
sample units from production lots. Ball shear applies a lateral force to the
first bond until failure; wire pull applies a vertical or angled force to the
wire until it breaks or lifts.

**Specifications:** JEDEC and customer specs define minimum ball shear force
(e.g., grams-force per mil diameter) and wire pull force. Failure modes (lifted
ball, wire break, cratering) are categorized; some failure modes (e.g., cratering)
indicate process or design problems and may trigger lot hold.

**Sampling:** Typically 2–5 units per lot, with multiple bonds per unit tested.
Frequency may increase for new devices or after process changes. Data is tracked
in SPC; trends or out-of-spec results trigger investigation and containment.
Bond reliability over temperature cycling and humidity is validated during
qualification; production monitoring ensures process stability.
"""  # noqa: E501

