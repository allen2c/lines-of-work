content = """
Common assembly defects include: non-stick (bond not adhering), wire sweep
(wire displaced during molding), delamination (interface separation), voids
(in die attach or mold), cracks (die or package), wire shorts, mark
illegibility, lead defects (bent, dirty), and contamination. Each defect
type has root causes (process, material, equipment, design) and corrective
actions.

**Pareto analysis:** Rank defects by frequency or cost impact. Focus
improvement efforts on the top few defect types that drive the majority of
failures. Track Pareto over time; shifts indicate process drift or new
issues. **Categorization:** Defects are coded for database reporting.
Consistent coding enables trend analysis and benchmarking across lots and
product types.

**Containment:** When defect rates spike, contain affected lots (hold, 100%
inspect, or scrap) before release. Root cause and corrective action (RCCA)
are required before process release.
"""  # noqa: E501

