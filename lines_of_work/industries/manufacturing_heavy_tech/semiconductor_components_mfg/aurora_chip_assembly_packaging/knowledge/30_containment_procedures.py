"""Containment procedures for quality excursions."""

title = "Containment Procedures"

content = """
When a quality excursion is detected (e.g., defect spike, out-of-spec
parameter, material deviation), immediate containment is required. **Hold:**
Stop further processing or shipment of affected lots. Place lots on hold in
the tracking system; assign an owner for investigation. **Identify scope:**
Determine which lots, equipment, or time windows are affected. Use genealogy
and process logs to trace. **100% inspection or sort:** If applicable,
inspect or test 100% of units to sort good from bad. Document results.
**Scrap or rework:** Non-conforming product may be scrapped or reworked per
procedure. Rework requires validation and documentation. **Root cause:**
Investigate why the excursion occurred. Use 5-why, fishbone, or similar.
**Corrective action:** Fix the root cause (process, training, equipment,
material). Verify effectiveness. **Release:** Only release contained lots
after approval from quality or designated authority. Document all actions.
"""  # noqa: E501

