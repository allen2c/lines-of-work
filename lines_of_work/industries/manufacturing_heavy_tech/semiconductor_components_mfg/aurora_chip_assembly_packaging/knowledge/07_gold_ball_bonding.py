"""Gold ball bonding process and best practices."""

title = "Gold Ball Bonding"

content = """
Gold (Au) ball bonding is the traditional and still widely used method for
creating first bonds on die pads. An electric spark (Electronic Flame-Off, EFO)
melts the wire tail to form a free-air ball (FAB). The ball is then placed
onto the Al or Au pad and bonded using a combination of force, ultrasonic
energy, and sometimes heat.

**Advantages:** Mature process, excellent bondability to Al pads, good reliability.
**Limitations:** Gold cost, and Al pad consumption (growth of intermetallics over
time can affect long-term reliability in extreme conditions).

**Process controls:** EFO current and time (ball size); bond force and ultrasonic
settings (first and second bond); capillary tip geometry and replacement schedule.
Ball diameter is typically 2.5–3× wire diameter. Second bonds (stitches) require
different force/US settings. Monitor ball shear and wire pull; replace capillary
when bond strength drifts or defects increase.
"""  # noqa: E501

