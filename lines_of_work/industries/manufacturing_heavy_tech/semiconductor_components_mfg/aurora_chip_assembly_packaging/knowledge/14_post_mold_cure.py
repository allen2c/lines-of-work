title = "Post-Mold Cure"

content = """
Post-mold cure (PMC) is a bake step applied after transfer molding to complete
the polymerization of the mold compound. Incomplete cure leaves residual
reactive groups that can absorb moisture more readily and may cause popcorning
during reflow. PMC also stabilizes the package for subsequent thermal cycles
and electrical test.

**Typical schedule:** 4–8 hours at 150–175°C in air or N2. The exact profile
is specified by the mold compound supplier and must be followed. Ovens must
maintain temperature uniformity (±5°C typical); load configuration and airflow
affect uniformity. Over-cure can cause discoloration or degradation in some
compounds; under-cure risks reliability failure.

**Tracking:** Lot hold time between mold and PMC may be limited by some
specifications. PMC completion must be recorded in the lot genealogy. Packages
are not shipped or exposed to humidity-sensitive handling until PMC is complete.
"""  # noqa: E501

