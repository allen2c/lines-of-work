title = "Liquid Encapsulation"

content = """
Liquid encapsulants are used for packages where transfer molding is unsuitable:
e.g., WLCSP, certain QFN/BGA variants, MEMS, and optical devices. The encapsulant
is dispensed (glob top, underfill, or molded) and cured at lower temperatures
than transfer mold compounds.

**Types:** Glob top covers the die and bonds with a single dispensed dome;
underfill flows into the gap between die and substrate in flip-chip packages;
dam-and-fill uses a dam to contain the liquid before cure. Liquid encapsulants
can be silicone, epoxy, or hybrid chemistries.

**Process controls:** Dispense pattern, volume, viscosity, and pot life; cure
temperature and time; moisture uptake. Liquid materials are more sensitive to
contamination and mixing ratio (for two-part systems). Cure uniformity affects
reliability; incomplete cure leads to moisture absorption and delamination risk.
MSL rating must be established through IPC/JEDEC testing.
"""  # noqa: E501

