content = """
Transfer molding is the dominant process for encapsulating wire-bonded assemblies
in plastic packages. A preheated mold compound (thermoset epoxy) is placed in
a transfer pot; a plunger forces the compound through runners and gates into mold
cavities containing the assembled leadframes or substrates. Heat and pressure
cure the compound, forming a rigid, protective body around the die and bonds.

**Key parameters:** Mold temperature, transfer pressure, clamp force, fill time,
and compound preheat. Improper settings cause voids, wire sweep, incomplete fill,
or flash. Mold design (runner size, gate location, venting) affects fill
uniformity and defect levels. Multiple cavities are filled simultaneously;
balance between cavities is critical for consistency.

**Post-mold cure:** Many compounds require a post-mold cure (PMC) bake (e.g.,
4–8 hours at 150–175°C) to complete cross-linking and stabilize moisture
absorption characteristics. PMC is required before electrical test and before
MSL rating can be applied.
"""  # noqa: E501

