title = "Mold Compound Selection"

content = """
Mold compounds are formulated from epoxy resins, silica fillers, and additives.
Selection depends on package type, reliability requirements, and cost. Key
properties include: coefficient of thermal expansion (CTE), glass transition
temperature (Tg), thermal conductivity, ion content (Na, Cl, K), and filler
loading.

**CTE match:** CTE should be close to die and substrate to minimize stress and
delamination. High filler loading lowers CTE but may increase viscosity and
affect moldability. **Ion content:** Low ion levels reduce corrosion and
electromigration risk in humid environments. **MSL:** Compounds are qualified
for specific Moisture Sensitivity Levels; the package MSL is determined by
the slowest element (compound, die, substrate).

Material changes require full qualification (reliability testing, MSL
re-evaluation). Lot traceability is mandatory; use-by date and storage
conditions must be observed.
"""  # noqa: E501

