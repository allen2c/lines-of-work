title = "Delamination and Voids"

content = """
Delamination is separation at an interface (die-attach, mold-compound to leadframe,
etc.). It can be caused by contamination, poor adhesion, CTE mismatch, moisture,
or process excursion. Delamination can lead to cracking, corrosion, or electrical
failure. C-SAM (scanning acoustic microscopy) and cross-section analysis are used
for detection and diagnosis.

**Voids** in die attach reduce thermal and mechanical performance. Large voids
or void clusters can cause localized stress and reliability failure. C-SAM
quantifies void area; typical specs limit void percentage. Root causes include
dispense issues, placement force, outgassing, or material properties. **Mold
voids:** Air or gas trapped during transfer molding. Causes include insufficient
transfer pressure, vent blockage, or compound viscosity. Mold voids can expose
wires or cause wire sweep. X-ray and C-SAM aid detection. Process optimization
(temperature, pressure, fill time) reduces void formation.
"""  # noqa: E501

