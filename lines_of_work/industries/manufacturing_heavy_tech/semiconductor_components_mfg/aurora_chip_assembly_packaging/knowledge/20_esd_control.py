"""ESD control for assembly and handling."""

title = "ESD Control"

content = """
Electrostatic discharge (ESD) can damage or latent-damage semiconductor devices.
Assembly areas must implement ESD control per ANSI/ESD S20.20 or equivalent:
grounded workstations, conductive floor mats, wrist straps, grounded tools,
and ESD-safe packaging and handling.

**Personnel:** Wrist straps or heel straps verified daily; proper footwear on
conductive floors. No ungrounded personnel in sensitive areas. **Equipment and
surfaces:** Workstations, conveyors, and fixtures must be grounded. Ionizers
may be used to neutralize charge on non-conductive materials but do not
replace grounding. **Packaging:** Trays, tubes, and bags must be ESD-safe
(conductive or dissipative). Avoid plastic that can tribocharge.

**Audits:** Regular verification of grounding continuity and ionizer balance.
ESD events (e.g., visible spark, failed verification) require assessment of
affected product. Training and awareness are ongoing.
"""  # noqa: E501

