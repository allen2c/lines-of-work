"""Die attach fundamentals and process objectives."""

title = "Die Attach Fundamentals"

content = """
Die attach bonds the semiconductor die to a substrate (leadframe, organic substrate,
or ceramic) and provides mechanical stability, thermal path, and often electrical
ground. The bond must be uniform, void-free within acceptable limits, and reliable
over the product lifetime.

**Objectives:**
- Mechanical adhesion sufficient to withstand wire bonding, molding, and thermal
  cycling without delamination.
- Thermal conduction from die to substrate for heat dissipation.
- Electrical connection when the backside of the die is grounded.

**Key parameters:** Bond-line thickness (BLT), coverage area, void percentage, and
cure state. Excessive voids reduce thermal and mechanical performance; insufficient
cure leads to reliability failures. Process control includes dispense pattern,
placement accuracy, and cure profile (temperature and time). All parameters are
documented in the assembly recipe and monitored via SPC.
"""  # noqa: E501

