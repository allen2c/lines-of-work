"""Common assembly failure modes and prevention."""

title = "Common Failure Modes"

content = """
**Non-stick:** Bond does not adhere. Causes: contamination, wrong parameters,
worn tool, incompatible metallization. Prevent: clean surfaces, correct
recipe, tool PM. **Wire sweep:** Shorts or breaks from mold flow. Prevent:
mold design, lower pressure, loop optimization. **Delamination:** Interface
separation. Causes: contamination, moisture, CTE mismatch. Prevent: process
control, material qualification, dry handling. **Voids:** In die attach or
mold. Prevent: dispense/placement control, mold parameters, material
selection. **Cracks:** Die or package crack. Causes: mechanical stress,
thermal stress, handling. Prevent: process optimization, proper tooling,
ESD and mechanical handling. **Corrosion:** In humid environments, especially
with Cu wire. Prevent: MSL compliance, storage, package design. **Mark
failure:** Illegible or missing. Prevent: laser/ink parameters, verification.
Systematic tracking and RCCA reduce recurrence.
"""  # noqa: E501

