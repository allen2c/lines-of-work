title = "Immersion Lithography and NA Enhancement"

content = """
Immersion lithography is a technique used to increase the Numerical Aperture 
(NA) of the scanner's lens system, thereby improving resolution. It 
involves placing a liquid (typically ultrapure water) between the final 
lens element and the wafer surface.

**Refractive Index Advantage:**
The resolution of an optical system is limited by the refractive index (n) 
of the medium between the lens and the wafer. Air has n ≈ 1, while water 
has n ≈ 1.44 at 193nm. By using water, the effective NA can be increased 
beyond 1.0 (hyper-NA), reaching values up to 1.35.

**System Challenges:**
Immersion lithography introduces several technical hurdles:
- **Bubble Formation:** Any bubbles in the water can cause catastrophic 
  defects. The water must be degassed and delivered with extreme care.
- **Water Contamination:** The water must be kept at a constant 
  temperature and free of any impurities that could affect its 
  refractive index or contaminate the wafer.
- **Resist Leaching:** Some components of the photoresist can leach 
  into the water, potentially damaging the lens or causing defects. 
  Specialized "topcoat" layers are often used to prevent this.

**Scanning Speed:**
The water must be maintained as a stable "meniscus" under the lens as 
the wafer stage moves at high speeds. This requires sophisticated 
fluid-handling systems and stage control.
"""

