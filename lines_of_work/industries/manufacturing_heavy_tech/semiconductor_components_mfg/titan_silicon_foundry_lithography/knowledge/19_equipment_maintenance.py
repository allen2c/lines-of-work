content = """
The lithography scanner is a high-maintenance machine that 
requires regular servicing to ensure its performance and 
reliability.

**Preventative Maintenance (PM):**
PM involves regularly scheduled tasks such as cleaning the 
optics, replacing worn parts, and recalibrating the sensors. 
This is essential for preventing unexpected tool failures 
and maintaining process stability.

**Tool Down Scenarios:**
A "tool down" occurs when a scanner becomes non-functional 
due to a hardware failure or a process error. This can 
significantly impact the fab's production schedule and 
requires immediate attention from maintenance and process 
engineers.

**Diagnostic Tools and Logs:**
Scanners generate detailed logs of their performance, which 
can be used to diagnose problems and identify potential 
failures before they occur. Engineers use specialized 
diagnostic software to analyze these logs and troubleshoot 
issues.

**Spare Parts Management:**
Maintaining an inventory of critical spare parts is essential 
for minimizing tool downtime. This includes everything from 
simple sensors to complex lens modules and light sources.

**Vendor Support:**
For major hardware failures or complex process issues, 
engineers often work closely with the scanner vendor's 
support team to resolve the problem.
"""

