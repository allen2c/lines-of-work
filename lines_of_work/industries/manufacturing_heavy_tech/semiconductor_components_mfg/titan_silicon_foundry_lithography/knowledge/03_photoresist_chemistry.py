title = "Photoresist Chemistry and Application"

content = """
Photoresist is a light-sensitive polymer used to form a temporary pattern on 
the wafer. It consists of three main components: a polymer resin, a sensitizer 
(Photoacid Generator or PAG in chemically amplified resists), and a solvent.

**Chemically Amplified Resists (CAR):**
Modern lithography relies on CAR technology. When exposed to light, the PAG 
releases an acid. During the Post-Exposure Bake (PEB), this acid catalyzes a 
chemical reaction in the polymer, changing its solubility. A single photon 
can trigger many reactions, significantly increasing the resist's sensitivity.

**Spin-Coating Process:**
The resist is applied to the wafer using a spin-coater. The wafer is spun at 
high speeds (thousands of RPM) to create a uniform, thin film. The thickness 
is controlled by the spin speed and the resist's viscosity. Uniformity is 
critical, as thickness variations lead to focus errors.

**Positive vs. Negative Resist:**
- **Positive Resist:** The exposed areas become soluble in the developer and 
  are removed. The remaining pattern matches the dark areas of the mask.
- **Negative Resist:** The exposed areas become insoluble (cross-linked) and 
  remain after development. The pattern is the inverse of the mask.
"""

