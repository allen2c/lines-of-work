title = "Photomask Technology and Pellicles"

content = """
The photomask (or reticle) contains the master pattern for a circuit layer. 
It is a high-precision glass or quartz plate with a thin layer of chrome 
or other opaque material.

**Mask Manufacturing:**
Masks are manufactured using electron-beam lithography (EBL) to achieve 
the resolution needed to define features that are often smaller than the 
wavelength of light. The mask design is based on the original circuit 
layout, but modified with Optical Proximity Correction (OPC) features.

**Phase Shift Masks (PSM):**
PSMs use specialized materials to shift the phase of the light passing 
through certain areas of the mask. This creates destructive interference 
at the edges of features, sharpening the printed image and improving 
resolution.

**Pellicles:**
A pellicle is a thin, transparent membrane stretched over a frame and 
attached to the mask. It prevents particles from landing on the mask 
surface. Any particles that land on the pellicle are out of focus 
during exposure and do not print on the wafer. For EUV lithography, 
pellicles must be extremely thin (a few nanometers) to minimize 
absorption.

**Mask Inspection:**
Masks are regularly inspected for defects or contamination using 
high-resolution optical or electron-beam tools. Any defects on the 
mask will be printed on every die, making mask quality paramount.
"""

