content = """
晶圆测试（CP / Wafer Sort）是在切割与封装前对晶圆上的每一颗 die 进行电性及功能测试的工序。
目的是在早期筛除不良 die，避免将已知不良品送入后续封装与成品测试，从而节省成本并加速
良率反馈。

**测试时机与位置：** 晶圆完成前道制程（扩散、光刻、刻蚀、薄膜等）后，在切割（saw）前或
部分制程在切割后、封装前进行。测试在探针台上完成：探针卡上的探针与 die 上的焊盘（pad）
接触，测试机（ATE）通过探针施加激励并量测响应。

**通过/不通过判定：** 每颗 die 根据测试项结果被判定为 pass 或 fail，并可按电气规格
分档（bin），例如速度档、功耗档。测试结果形成 wafer map，用于良率分析、制程改进与
客户交片规格确认。

**与前后道关系：** 晶圆测试数据是制程整合与良率管理的重要输入；与封装后成品测试（FT）
的相关性分析可帮助区分为前道缺陷还是封装/测试引入的问题。
"""

