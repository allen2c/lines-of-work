content = """
分档（binning）是根据测试结果将 die 归类到不同档位，用于区分可出货等级、速度档、
功耗档或不良类型，供后续封装、打标与客户规格对应。

**档位定义：** 由设计或客户规格定义，例如 Bin1 为全规格通过、Bin2 为降速通过、
Bin3 为仅部分功能通过、Fail 为不合格。测试程式中的 bin 码与每项 fail 的对应
关系必须明确且与下游一致。

**Wafer map：** 每颗 die 的 bin 结果可绘制成 wafer map，不同颜色或符号代表不同
档位。通过 wafer map 可快速识别系统性失效（如边缘、特定区域）与随机失效分布。

**数据输出：** 分档结果通常随测试数据一起输出到 MES 或下游系统，供封装排程、
良率报表与客户交片使用。档位统计与规格符合性需定期核对。
"""

