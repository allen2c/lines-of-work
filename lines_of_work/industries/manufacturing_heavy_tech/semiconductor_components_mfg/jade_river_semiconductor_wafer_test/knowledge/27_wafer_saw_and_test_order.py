title = "切割与测试顺序"

content = """
晶圆在制造完成后，测试（CP）与切割（saw）的顺序因产品与厂内
流程而异，影响成本、周期与数据管理。

**先测后切（test-before-saw）：** 整片 wafer 先完成 CP，再根据
测试结果决定是否切割或只切割 pass die。常见于标准流程，便于
整片良率分析与 wafer map 完整。

**先切后测（saw-before-test）：** 先切割成单颗 die 再测试，多用于
特殊封装或无法整片探针接触的产品。需处理 die 搬运、载具与
追溯标识。

**部分产品：** 可能采用部分切割后再测试等混合流程。测试部门
需与制造、封装排程协同，确保 lot 流向、数据与追溯一致，
并满足客户对测试阶段与数据的要求。
"""

