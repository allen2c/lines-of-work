title = "Umgang mit Abweichungen"

content = """
- Definition: Abweichung = jede Abweichung von genehmigten Spezifikationen, Verfahren oder Parametern. Beispiele: Temperaturüberschreitung, Partikelalarm, falsche Materialcharge, Dokumentationslücke.
- Sofortmaßnahmen: Tätigkeit stoppen, Charge im MES auf „gesperrt“ setzen, Schichtleitung informieren. Keine eigenmächtigen Korrekturen außerhalb der SOP. Abweichungsbericht (Deviation Report) ausfüllen mit: Beschreibung, Ursache (sofern bekannt), betroffene Charge, betroffene Prozessschritte.
- Klassifizierung: Kritische Abweichung (z. B. Sterilitätsverlust) → sofortige QS-Eskalation. Major-Abweichung (z. B. Temperatur außerhalb Toleranz >5 Min) → Charge muss möglicherweise verworfen werden. Minor-Abweichung (z. B. Dokumentationsfehler) → Korrektur durch Nachtrag mit Begründung.
- Nachverfolgung: Jede Abweichung erhält eine eindeutige ID. Ursachenanalyse (Root Cause Analysis) innerhalb von 5 Arbeitstagen. CAPA-Maßnahmen (Corrective and Preventive Actions) werden im QM-System verfolgt.
"""

