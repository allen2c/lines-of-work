title = "Inspektion von Behältnissen (Sichtprüfung)"

content = """
- Nach der Abfüllung und Lyophilisation werden alle Behältnisse (Fläschchen, Ampullen) einer 100%igen Sichtprüfung unterzogen. Prüfung auf: Partikel, Risse, Undichtigkeiten, falsche Füllhöhe, Verfärbungen.
- Methoden: Manuelle Sichtprüfung unter Beleuchtung (≥2000 Lux) mit schwarzem/weißem Hintergrund. Automatisierte Prüfung mittels Kamerasystem (z. B. Partikeldetektion, Glasfehlererkennung). Operator führt Stichprobenkontrollen durch (z. B. 10% der Charge).
- Akzeptanzkriterien: Keine sichtbaren Partikel >50 µm, Füllhöhe ±5% vom Soll, keine Risse oder Undichtigkeiten. Bei Auffälligkeiten: Ausschleusen der betroffenen Einheiten, Dokumentation, ggf. Charge sperren.
- Operator dokumentiert Anzahl der Ausschussstücke und deren Fehlerart. Bei erhöhtem Ausschuss (>2%) sofortige Meldung an Schichtleitung.
"""

