title = "Kalibrierung von Messgeräten"

content = """
- Alle Messgeräte (Thermometer, Druckmessumformer, Waagen, pH-Meter, Partikelzähler) müssen regelmäßig kalibriert werden. Kalibrierintervalle gemäß Herstellervorgabe und betrieblicher Risikobewertung (z. B. Waagen monatlich, Thermometer jährlich).
- Vor jeder Nutzung: Sichtprüfung auf Beschädigung, Prüfung des Kalibrierstatus (Aufkleber mit Datum der nächsten Kalibrierung). Bei abgelaufenem Kalibrierdatum: Gerät nicht verwenden, an die Messtechnik übergeben.
- Tägliche Funktionskontrolle: Waagen mit Referenzgewicht (z. B. 100 g) prüfen – Abweichung ≤ 0,1% toleriert. pH-Meter mit Pufferlösungen (pH 4, 7, 10) justieren. Dokumentation der Kontrolle im Logbuch.
- Bei Abweichung: Gerät sperren, Kalibrierung veranlassen, betroffene Messungen der letzten Charge auf Plausibilität prüfen. Ggf. Charge sperren.
"""

