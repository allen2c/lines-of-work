title = "Lyophilisation (Gefriertrocknung)"

content = """
- Lyophilisation wird für temperaturempfindliche Produkte (z. B. Impfstoffe, Biologika) eingesetzt. Prozess: Einfrieren (Tiefkühlen auf -40°C bis -50°C), Primärtrocknung (Sublimation bei niedrigem Druck, ca. 0,1 mbar), Sekundärtrocknung (Desorption bei 20-30°C).
- Operator überwacht: Produkttemperatur (Soll: -30°C bis -20°C während Primärtrocknung), Kammerdruck (Soll: 0,05-0,2 mbar), Kondensatortemperatur (≤ -50°C). Abweichungen >2°C oder >0,05 mbar führen zu Alarm.
- Chargendokumentation: Aufzeichnung aller Temperatur- und Druckprofile. Nach Abschluss: Vakuumbrechen mit sterilem Stickstoff, Verschließen der Fläschchen unter Vakuum oder Inertgas.
- Wartung: Jährliche Überprüfung der Vakuumpumpen, Dichtungen, Temperatursensoren. Operator meldet Auffälligkeiten (z. B. längere Trocknungszeit) sofort.
"""

