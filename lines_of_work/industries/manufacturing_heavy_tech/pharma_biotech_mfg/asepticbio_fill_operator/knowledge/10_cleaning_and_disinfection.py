title = "Cleaning and Disinfection Between Batches"

content = """
- After each batch, perform a full cleaning of all surfaces in the Grade A zone: filling machine, conveyor, stopper bowl, and walls. Use validated disinfectants: 70% IPA for routine disinfection, 0.5% sodium hypochlorite (bleach) for sporicidal action. Rotate weekly to prevent resistance.
- Apply disinfectant with sterile lint-free wipes. Contact time: minimum 3 minutes for IPA, 5 minutes for bleach. Do not wipe off before contact time elapses. Use WFI (water for injection) rinse after bleach to remove residues.
- Document cleaning in the cleaning log: date, time, disinfectant used, operator signature. Supervisor inspects and signs off before next batch.
- For equipment parts (e.g., filling needles), follow validated cleaning procedure: rinse with WFI, soak in 1% NaOH for 30 minutes, rinse again, then autoclave. Never reuse single-use items.
"""

