content = """
- After visual clearance, perform swab testing for product residue if the previous batch contained a potent compound (e.g., cytotoxic, hormone). Use a sterile swab moistened with WFI, wipe a 10 cm x 10 cm area on the filling needle and stopper bowl.
- Send swab to QC for analysis (e.g., HPLC). Acceptance criteria: no detectable residue above 0.1 ppm. If positive, repeat cleaning and re-swab. If still positive, escalate to supervisor.
- For non-potent products, swab testing is not required unless specified in the batch record. However, a visual inspection with a flashlight is mandatory.
- Document swab results in the line clearance log. Do not start the next batch until clearance is approved by supervisor.
"""

