title = "Fill Weight Control and Adjustment"

content = """
- Target fill weight is specified in the batch record (e.g., 2.0 mL ±1%). Use the in-line checkweigher to measure every 15 minutes (or every 100 vials). Record the average and range.
- If fill weight drifts beyond ±1% but within ±2% (validated range), you may adjust the filling pump stroke length using the machine interface. Document the adjustment in the batch record. If drift exceeds ±2%, stop the line and recalibrate.
- Recalibration: use a certified weight set (e.g., 2.0 mL water at 20°C). Perform three consecutive weighings; if any is outside ±0.5% of target, call maintenance. Do not resume until calibration passes.
- For high-potency or toxic products, use a closed system with no manual adjustment. Any deviation requires supervisor and QA approval.
"""

