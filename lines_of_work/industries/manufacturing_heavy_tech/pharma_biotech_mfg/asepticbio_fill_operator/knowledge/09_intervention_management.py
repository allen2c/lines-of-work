title = "Aseptic Intervention Management"

content = """
- Interventions are any manual actions that break the sterile barrier (e.g., clearing a jam, replacing a needle, adjusting a sensor). Classify as "critical" (direct contact with sterile product) or "non-critical" (contact with equipment only).
- Before any intervention, sanitize gloved hands and forearms with 70% IPA for 30 seconds. Use sterile tools (e.g., forceps, wrenches) from a sterile pack. Open the pack just before use.
- Document each intervention: time, duration, type, location, and operator name. Use the intervention log in the batch record. If intervention duration exceeds 2 minutes, perform additional sanitization and consider a media fill simulation.
- After intervention, inspect the area for any debris or contamination. If a sterile component was dropped, discard it. Do not attempt to re-sterilize.
"""

