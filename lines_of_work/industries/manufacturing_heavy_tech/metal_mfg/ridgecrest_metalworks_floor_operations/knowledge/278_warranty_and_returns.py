title = "Warranty Claims and Returned Goods Handling"

content = """
When a customer contacts Ridgecrest Metalworks with a quality complaint or requests to return parts, the response process must be fast, disciplined, and customer-focused. Every warranty claim or return is a failure that must be understood and corrected—not just resolved for the individual complaint, but eliminated so it does not recur.

**Receiving a customer quality complaint.** Customer complaints arrive via phone, email, or formal customer corrective action request (CAR) forms. All complaints are logged in the quality management system by the QA team within 24 hours of receipt. The complaint log includes: customer name, date received, part number and revision, lot/order number, quantity affected, and description of the defect.

**Immediate containment.** The first obligation is to protect the customer from further impact. Upon receiving a complaint:
1. QA quarantines all remaining Ridgecrest stock of the affected part and lot—pull from finished goods immediately.
2. The sales team contacts the customer to confirm the scope and whether any additional parts from the same lot are in the customer's inventory or in transit.
3. If replacement parts are needed urgently, the customer's required quantity is prioritized in the production schedule.

**Returned goods authorization (RGA).** Before the customer ships parts back, they must receive an RGA number from Ridgecrest. The RGA number allows the receiving team to identify, accept, and route returned goods correctly. Without an RGA number, returns go to the general receiving area and risk being lost or inadvertently re-entered into inventory.

**Receiving returned parts.** Returned parts arrive with the RGA paperwork. The receiving team places them directly into the NCR hold area—never into stock. QA inspects the returned parts to verify the reported defect and record actual quantities.

**Corrective action.** After the immediate containment is handled, QA leads an 8D or equivalent root cause analysis:
- Problem statement (precise, measurable).
- Immediate containment actions (already done).
- Root cause investigation (5 Why, fishbone, process analysis).
- Corrective action: process or system change that eliminates the root cause.
- Effectiveness verification: how will we confirm the corrective action worked?
- Customer communication: written response submitted per the customer's required response timeline (typically 30 days for full 8D).

Warranty cost (replacement parts, rework, freight, customer sorting costs) is tracked against each NCR and rolled up monthly as a cost-of-poor-quality (COPQ) metric reviewed by the plant manager.
"""

