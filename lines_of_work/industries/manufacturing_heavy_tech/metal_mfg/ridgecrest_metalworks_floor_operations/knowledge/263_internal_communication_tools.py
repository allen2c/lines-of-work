title = "Internal Communication: Boards, Radios, and Systems"

content = """
Effective internal communication is a prerequisite for coordinated production floor operations at Ridgecrest Metalworks. The facility uses a combination of visual management tools, radio communication, electronic systems, and structured meetings to ensure that relevant information reaches the right people at the right time.

**Visual management boards.** Each cell has a performance board displaying:
- Today's production schedule with planned vs. actual quantities updated hourly by the supervisor or lead.
- Machine status (running / setup / down).
- Safety cross (each day marked green for no incident, red for incident).
- Quality metrics (FPY, open NCRs).
- OEE trend chart (weekly).
- Open action items with owners and due dates.

The facility also has a central operations board in the production meeting area showing the same data aggregated across all cells. Boards must be kept current—stale data is worse than no data because it creates false confidence.

**Two-way radios.** Each cell supervisor carries a company radio during working hours. Radio channels are assigned by function:
- Channel 1: General production/supervisors.
- Channel 2: Maintenance and facilities.
- Channel 3: Warehouse and logistics.
- Channel 4: Management.

Radio discipline: identify yourself and the recipient, keep messages brief, confirm receipt of instructions by repeating back the key information. Never use radio to discuss quality failures, personnel issues, or customer-sensitive information—use phone or in-person for sensitive topics.

**Production management system (PMS).** The facility's production management system (integrated with SAP) provides a real-time dashboard visible on monitors in the production meeting room and on supervisor tablets. Supervisors use it to log downtime, enter production counts, and receive alerts from the planning team.

**Internal telephone and messaging.** Extension list posted at each work cell. Supervisors communicate with planning, QA, and maintenance via phone for non-urgent matters. The company instant messaging platform (Teams) is used by management and office staff—not expected of floor operators but available on facility tablets.

**Bulletin boards.** Posted notices covering: safety alerts, policy updates, training schedules, and employee announcements. Required notices (OSHA posters, EEO posters) are in the main break room. Outdated notices are removed within 5 business days of expiry.

**Escalation matrix.** A laminated card at each supervisor station lists the direct contact number for each escalation: machine breakdown → maintenance, quality hold → QA lead, safety emergency → 911 and EHS manager, material shortage → planner.
"""

