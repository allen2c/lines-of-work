title = "Visual Inspection: Methods and Lighting Requirements"

content = """
Visual inspection (VT) is the foundation of all NDT activity and the first examination applied to any component. At Ridgecrest Metalworks, visual inspection of weldments is performed in accordance with ISO 17637 (welds), and general visual inspection follows written procedures defining the scope, acceptance criteria, and minimum conditions for examination.

Direct visual inspection is performed when the eye can be placed within 600 mm of the surface at an angle of not less than 30 degrees to the surface. Illuminance at the inspection surface must be a minimum of 500 lux per ISO 17637 and EN 13018; 1000 lux is recommended for the detection of fine surface features. A calibrated light meter (lux meter) is used to verify illuminance at the start of each inspection shift or when lighting conditions change.

Aids to visual inspection:
- Magnifying glass (2× to 10×) for examining fine surface features, weld craters, and pits
- Dental mirrors and articulated borescopes for examining restricted or internal areas
- Weld gauges (bridge cam gauge, fillet weld gauge) for measuring weld throat, leg length, and reinforcement height
- Flashlights with focused beams for raking illumination to highlight surface irregularities by creating shadows across them (grazing light technique)

Weld visual inspection (per ISO 17637) checks:
- Surface discontinuities: cracks, porosity, crater cracks, lack of fusion at toes
- Weld geometry: undercutting, overlap, excess reinforcement, underflush
- Spatial characteristics: weld dimension (leg size for fillet welds, reinforcement height for butt welds)
- Alignment and distortion of joined components

Inspectors must have adequate visual acuity: near vision corrected or uncorrected to a minimum Jaeger 1 (or equivalent) at 300 mm, verified at least annually per ISO 9712 or ASNT requirements.

Inspection records must document: part identification, weld/area examined, acceptance criteria applied, illuminance measured, equipment used, technician identity and certification level, date, and accept/reject result. Photographs are taken to document significant findings and limit samples.
"""

