title = "Mold Preparation and Core Making"

content = """
The quality of a sand casting begins with the quality of the mold and cores. Proper mold preparation
ensures dimensional accuracy, adequate permeability to allow gases to escape, sufficient strength to
withstand metallostatic pressure during pouring, and clean internal surfaces free of contaminants
that could cause defects.

Molding Sand Types:

Green Sand: The traditional molding medium; a mixture of silica sand, bentonite clay binder, and
water. The clay becomes plastic when moistened and bonds the sand grains together. Green sand molds
are made directly in the flask, used once, and then recycled through the sand reclaim system. Sand
properties monitored include compactability (moisture indicator), green compression strength, permeability,
and clay content. Regular sand testing is performed by the foundry technician using a universal sand
tester at the start of each shift.

Chemically Bonded Sand (No-Bake / Cold Box): Resin binders (furan, phenolic urethane, or sodium
silicate) are mixed with sand and catalyst to create a self-hardening mixture that achieves high
strength without moisture. No-bake sand is used for large or complex molds where green sand strength
is insufficient. Cold box processes use gaseous catalysts to harden the mold rapidly in the flask
or core box.

Mold Making Process:
The pattern is placed on a match plate or positioned in the drag (bottom) half of the flask. Sand
is rammed around the pattern by hand or using a sand slinger. Sufficient ramming density ensures
strength; over-ramming reduces permeability. The cope (top) half is rammed separately after the
drag is prepared. Parting compound (a fine powder) is applied to the parting surface to prevent
the two halves from bonding.

Core Making:
Cores are made in core boxes using chemically bonded sand to achieve dimensional stability. After
curing, cores are cleaned of loose sand, inspected for dimensional accuracy and surface condition,
and coated with a refractory wash to improve surface finish on the casting's internal surfaces.
Cores are stored in a temperature- and humidity-controlled area to prevent moisture absorption, which
would cause gas evolution and porosity during pouring.

Mold Wash:
Mold cavity surfaces are coated with a refractory wash (zircon, graphite, or alumina-based) to improve
casting surface finish, prevent metal penetration into the sand, and ease casting removal. The wash
must be allowed to dry fully before pouring; residual moisture in the wash is a significant source
of gas porosity defects.
"""

