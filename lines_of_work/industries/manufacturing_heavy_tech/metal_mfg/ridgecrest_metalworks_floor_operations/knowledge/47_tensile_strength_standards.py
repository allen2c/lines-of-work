title = "Tensile Strength and Yield Strength Standards"

content = """
Tensile testing is the definitive mechanical test for verifying that a metal meets its specified
strength requirements. Ridgecrest Metalworks performs tensile testing on incoming materials, cast and
forged products, heat-treated components, and weld procedure qualifications. Understanding how tensile
tests are conducted and how results are interpreted is essential for quality personnel and process engineers.

The Tensile Test:
A machined test specimen of defined geometry (per ASTM E8 for metals, or ASTM A370 for steels) is
mounted in a tensile testing machine and pulled at a controlled rate until fracture. The machine
records force and elongation continuously. From this data, the following are calculated:

- Ultimate Tensile Strength (UTS): Maximum load divided by original cross-sectional area. Represents
  the maximum stress the material can withstand.
- Yield Strength (0.2% Offset): The stress at which the material has undergone 0.2% permanent
  plastic strain. Identified by drawing a line parallel to the linear (elastic) portion of the
  stress-strain curve starting from the 0.2% strain point; the intersection with the curve is the
  yield strength.
- Elongation: Percentage increase in gauge length from original to fracture. Measured by fitting the
  broken pieces together and measuring the final gauge length.
- Reduction of Area: Percentage decrease in cross-sectional area at the fracture location.

Specimen Orientation:
Specimen orientation relative to the material's working direction matters. Longitudinal specimens
(parallel to rolling or forging direction) typically show higher strength and elongation than
transverse specimens. Customer specifications often define the required orientation.

Typical Specification Requirements (Reference Values):
- AISI 1018 cold drawn: UTS 440 MPa min, Yield 370 MPa min, Elongation 15% min
- AISI 4140 Q&T (typical 35 HRC condition): UTS 1000 MPa min, Yield 860 MPa min, Elongation 16% min
- 6061-T6 aluminum: UTS 310 MPa min, Yield 276 MPa min, Elongation 8% min
- 304L stainless (annealed): UTS 485 MPa min, Yield 170 MPa min, Elongation 40% min

Certificate of Conformance (CoC) vs. Actual Test Reports:
For some incoming materials, suppliers provide test reports with actual test values. For others, a
Certificate of Conformance (CoC) attests that the material was tested and meets specification without
providing actual numbers. Customer and internal requirements determine which is acceptable. Critical
components typically require actual test data on the MTR, not just a CoC.
"""

