title = "Waste Classification: Hazardous vs. Non-Hazardous"

content = """
Correct waste classification is the foundation of legal environmental compliance at Ridgecrest Metalworks. Misclassifying hazardous waste as non-hazardous—intentionally or through ignorance—is a federal violation under RCRA that can result in civil penalties of up to $37,500 per day per violation and potential criminal liability.

**RCRA hazardous waste criteria.** A solid waste is classified as hazardous if it meets any of these criteria:
- **Listed waste:** Appears on an EPA listed waste category (F-list, K-list, P-list, U-list). Examples relevant to Ridgecrest: spent solvent-containing cleaning wipes (F003/F005 listed solvents), certain spent pickle liquor from steel surface preparation.
- **Characteristic waste:** Exhibits one of four hazardous characteristics:
  - **Ignitability:** Flash point below 140°F (60°C). Covers most used petroleum solvents and some cutting fluid concentrates.
  - **Corrosivity:** pH ≤ 2 or ≥ 12.5. Covers spent strong acid pickling solutions and alkaline cleaning baths.
  - **Reactivity:** Unstable, explosive, or reacts violently with water. Cyanide-bearing heat treatment salts fall here.
  - **Toxicity:** Contains specific metals or organics above threshold concentrations in leachate testing (TCLP test).

**Common Ridgecrest waste classifications.**

| Waste Stream | Classification |
|---|---|
| Steel scrap (clean, uncoated) | Non-hazardous solid waste / recyclable |
| Mixed metal chips with coolant | Potentially hazardous if coolant is characteristic |
| Spent coolant emulsion | Evaluate by pH, ignitability, and metals content |
| Used mineral oil (non-chlorinated) | Non-hazardous if not mixed with hazardous waste |
| Spent chlorinated solvent | Hazardous (F001/F002 listed) |
| Empty chemical drums (not triple-rinsed) | May retain hazardous waste classification |
| General trash from offices | Non-hazardous municipal solid waste |

**Waste determination process.** When a new waste stream is generated, the EHS manager performs a waste determination using process knowledge and, if needed, analytical testing. The determination is documented. No new waste stream is disposed of until the determination is complete.

Never mix hazardous and non-hazardous waste—this dilution strategy is illegal and converts the entire combined volume to hazardous waste under RCRA.
"""

