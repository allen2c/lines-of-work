title = "On-Time Delivery Tracking and Escalation Triggers"

content = """
On-time delivery (OTD) tracking at Ridgecrest Metalworks is a continuous, real-time process—not just a monthly scorecard calculation. The production team tracks every open customer order against its committed ship date throughout the production cycle, with defined escalation triggers when orders fall at risk.

**Order tracking cadence.**

- **Daily:** The production planner reviews all work orders with due dates within the next 10 business days. Orders on schedule receive no special action. Orders that show risk (behind at any routing step, material pending, machine constraint) are flagged on the planning board.
- **Weekly:** All orders due within 30 days are reviewed in the operations meeting. Status is rated: On Track, At Risk, or Late.
- **Monthly:** The full OTD calculation is run for all shipments made during the month. Results are reported to the customer in cases where customer-specific OTD reporting is required.

**Escalation triggers.**

Trigger Level 1 (Planner action): Work order more than 1 day behind schedule at any operation. Planner adjusts the schedule, identifies recovery option (overtime, alternate machine), and documents the plan.

Trigger Level 2 (Supervisor escalation): Work order projected to be late by 3 or more business days with the current plan. Supervisor and planner jointly develop a recovery plan. Customer account manager is notified.

Trigger Level 3 (Production manager escalation): Work order cannot be recovered to the committed ship date without extraordinary action (emergency subcontracting, air freight, weekend overtime). Production manager approves the recovery cost. Customer is notified with a revised date.

Trigger Level 4 (Plant manager): Customer delivery is going to be missed and the customer has a hard shut-down date or delivery condition—customer's production line depends on this shipment. Plant manager personally contacts the customer. All available resources are mobilized.

**OTD by customer reporting.** Monthly OTD by customer is calculated from SAP shipment records. Any customer with OTD below 90% for two consecutive months receives a formal corrective action response (the 8D or equivalent format), submitted to the customer within 10 business days of month-end.

**No ship without completion.** Orders may not be shipped incomplete or with missing documentation to "count" a delivery as on time. A shipment is on time only when the full ordered quantity of conforming parts, with all required documentation, physically departs the facility on or before the committed date.
"""

