title = "Incident and Near-Miss Reporting Process"

content = """
Timely and accurate incident reporting is fundamental to Ridgecrest Metalworks' safety improvement
process. Every injury, occupational illness, property damage event, environmental release, and
near-miss must be reported, documented, and investigated. Suppressing or discouraging incident reports
is a serious violation of company policy and may constitute an OSHA recordkeeping violation.

Definitions:
- Injury or Illness: Any work-related physical harm, including cuts, burns, fractures, strains,
  respiratory illness, and occupational skin conditions.
- Near-Miss: An unplanned event that did not result in injury, illness, or damage but had the
  potential to do so. Near-misses are early warning signs and are treated with the same urgency
  as actual incidents.
- Property Damage Incident: Any event causing damage to equipment, tooling, facilities, or product
  valued above $500.

Immediate Reporting Steps:
1. Ensure the area is safe and provide first aid to the injured person.
2. Notify the shift supervisor immediately, regardless of injury severity.
3. Do not disturb the incident scene until the supervisor has assessed it and authorized cleanup or
   repair. Exception: if continued hazard exposure exists, make the area safe first.

Documentation Requirements:
The shift supervisor must complete an Incident Report form within 4 hours of the event. The report
captures: date, time, location, names of involved and witness personnel, description of what happened,
the nature of the injury or potential injury, immediate causal factors, and initial corrective actions.

Investigation Process:
Within 24 hours, the Safety Officer initiates a root cause investigation using the 5-Why or fishbone
analysis method. The investigation seeks contributing factors including: inadequate procedures, training
gaps, equipment deficiencies, environmental conditions, and human factor influences. Corrective actions
are assigned with responsible owners and due dates.

OSHA Recordkeeping:
The Safety Officer determines OSHA recordability for each incident per 29 CFR 1904 criteria. Severe
injuries requiring in-patient hospitalization, amputation, or loss of an eye must be reported to OSHA
within 24 hours. A fatality must be reported to OSHA within 8 hours.

Near-miss reporting is actively encouraged through recognition programs. Employees who report near-misses
are thanked publicly at shift meetings; their reports drive preventive actions that protect the entire workforce.
"""

