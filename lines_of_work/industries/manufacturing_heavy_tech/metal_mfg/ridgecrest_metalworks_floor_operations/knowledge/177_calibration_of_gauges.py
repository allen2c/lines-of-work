title = "Gauge Calibration: Frequency, Records, and Traceability"

content = """
Calibration is the process of comparing a measurement instrument against a reference of known accuracy and, where necessary, adjusting or establishing correction factors to ensure the instrument measures within its specified accuracy. At Ridgecrest Metalworks, all instruments used to make product acceptance decisions must be calibrated and within calibration date before use.

Calibration traceability: The calibration chain must be traceable to national or international measurement standards (NIST in the USA, or equivalent national metrology institutes). This means: Ridgecrest's working gauges are calibrated against reference standards; reference standards are calibrated by an accredited calibration laboratory against national standards. Calibration certificates document each step in this chain with expanded measurement uncertainty.

Calibration intervals are defined for each instrument type based on manufacturer recommendations, use frequency, and past performance. Common intervals at Ridgecrest:
- Digital calipers and micrometers: 12 months
- Dial test indicators and comparators: 12 months
- Thread gauges and plug gauges: 6–12 months (or immediately after suspected misuse)
- Torque wrenches: 6 months
- Hardness testers: 12 months (reference block verification daily before use)
- Weighing scales: 12 months
- CMM: 12 months volumetric accuracy (with qualification check before each session)

Calibration labels: Each calibrated instrument carries a label showing the calibration due date and a unique instrument ID. Labels are color-coded by year or quarter to enable rapid visual identification of expired instruments on the shop floor.

Instruments that fail calibration are removed from service immediately, labeled with a red out-of-service tag, and placed in the quarantine area. Products measured with the failed instrument since its last satisfactory calibration are identified and reviewed: a risk assessment determines whether they require re-measurement.

Calibration records (certificates and interval logs) are maintained in the calibration register and retained for a minimum of three years or the life of the gauge, whichever is longer. The calibration register is reviewed annually to identify instruments due for calibration and to analyze past calibration results for trends of early drift or repeated failure.
"""

