content = """
Ridgecrest Metalworks maintains a written respiratory protection program in compliance with OSHA 29
CFR 1910.134. The program governs the selection, use, maintenance, and training requirements for all
respirators used on the production floor. Respiratory protection is used as a supplemental control
when engineering controls alone cannot reduce airborne contaminant concentrations to below permissible
exposure limits.

Respirator Types in Use:

Filtering Facepiece Respirators (N95/P100): Used in areas where particulate hazards exist at levels
requiring protection but below concentrations requiring supplied-air. N95 respirators filter at least
95% of airborne particles. P100 respirators (sometimes called HEPA respirators) filter at least 99.97%
of particles and are used where oil aerosols are present (e.g., machining mist environments) and where
hexavalent chromium or other highly toxic metal fumes are present.

Half-Face and Full-Face Air-Purifying Respirators (APR): Used with combination cartridges for protection
against specific chemical vapors and particulates. Full-face respirators provide additional eye protection.
The correct cartridge type must be matched to the specific contaminant; universal cartridges are not
suitable for all environments.

Supplied-Air Respirators (SAR) and Self-Contained Breathing Apparatus (SCBA): Used in atmospheres
that are Immediately Dangerous to Life or Health (IDLH), oxygen-deficient environments, and certain
confined space entries. SCBA is maintained and worn only by trained emergency response team members.

Medical Evaluation:
Before being fitted for or assigned a respirator, every employee must complete a confidential medical
evaluation questionnaire (OSHA Appendix C to 1910.134) reviewed by the facility's occupational health
provider. Medical clearance is required and is renewed annually or when health status changes.

Fit Testing:
Qualitative or quantitative fit testing must be performed annually for all tight-fitting respirators.
Fit testing is conducted by the Safety Officer or a designated trained tester. Employees must be
fit-tested in the same make, model, and size of respirator they will use on the job.

Limitations of Voluntary Use:
Employees who voluntarily choose to wear filtering facepiece respirators (N95) in environments where
exposure is below action levels must still receive OSHA Appendix D information. The facility must
ensure that voluntary use does not create hazards such as heat stress or impaired communication.
"""

