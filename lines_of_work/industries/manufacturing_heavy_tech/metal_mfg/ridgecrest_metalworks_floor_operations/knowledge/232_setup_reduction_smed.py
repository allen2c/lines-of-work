title = "Setup Reduction Using SMED Methodology"

content = """
Setup time is the time a machine is not producing parts—waiting for changeover from the last job to the first good piece of the next job. At Ridgecrest Metalworks, reducing setup time is a high-priority continuous improvement objective because every hour of setup is an hour of lost production capacity.

**What is SMED?** Single-Minute Exchange of Die (SMED) is a lean methodology developed by Shigeo Shingo that aims to reduce setup time to single-digit minutes (less than 10 minutes). While few metal manufacturing setups reach that target, the methodology consistently delivers 30–70% setup time reductions when properly applied.

**SMED methodology steps.**

1. **Observe and record.** Time-study the current setup process in detail. Video recording is the most effective tool. Identify every individual task: tool retrieval, measurement, adjustment, test cuts, inspection.

2. **Separate internal from external activities.**
   - Internal activities: can only be performed while the machine is stopped (e.g., removing the current die, installing the next die, installing new tooling).
   - External activities: can be performed while the machine is still running the previous job (e.g., retrieving the next job's tooling, pre-staging materials, pre-loading the next program).
   The immediate target is to convert as many internal activities to external as possible.

3. **Convert internal to external.** Examples: Pre-stage all tools at the machine before the current job ends; pre-set tool offsets offline using a tool presetter; prepare fixtures and clamps away from the machine; pre-load CNC programs during the last part of the current run.

4. **Streamline remaining internal activities.** Use quick-connect hydraulic or pneumatic clamps instead of bolted fixtures. Standardize toolholder heights so no re-measurement is needed. Use color-coded tooling or shadow boards so nothing is searched for during setup.

5. **Standardize and document.** Write the reduced setup procedure as a standard setup sheet. Measure and record the new setup time. Post the standard setup sheet at the machine.

**Ridgecrest SMED targets.** Current baseline setup times are tracked by work center. Cells with setup times exceeding 45 minutes for routine changeovers have active SMED kaizen events scheduled. Results are tracked on the cell's performance board.

Operators are the most valuable contributors to SMED events because they perform setups daily. Their observations on where time is lost are the foundation of any improvement effort.
"""

