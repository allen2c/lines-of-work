content = """
The Ridgecrest Metalworks production floor is organized into clearly designated bays and zones, each
engineered for specific process types. This layout minimizes cross-traffic between incompatible materials
and operations, supports efficient material flow, and allows safety controls to be tailored to the hazard
profile of each area.

Bay A — Melt Shop and Foundry: Located at the north end of the facility, Bay A houses the electric arc
furnaces, induction furnaces, and sand casting lines. The area is designed with reinforced concrete flooring
rated for molten metal spills, overhead crane coverage, and dedicated ventilation with high-temperature
fume extraction. Access is restricted to authorized foundry personnel and requires full foundry PPE.

Bay B — Forging and Press Operations: Situated adjacent to the melt shop, Bay B contains hammer forging
cells, hydraulic presses, and induction heating stations. The bay features vibration-isolated foundations
beneath the hammer units and overhead cranes rated for up to 50 tons. Die storage racks are positioned
along the perimeter for quick change-over access.

Bay C — Machining Center: Bay C occupies the central production floor and houses CNC turning centers,
horizontal and vertical machining centers, and manual lathes for prototype and repair work. Climate control
is maintained in this bay to support dimensional accuracy. Coolant sumps and chip conveyors are integrated
into the floor design.

Bay D — Heat Treatment and Surface Finishing: Located at the south end, Bay D houses box and pit furnaces
for annealing, normalizing, quenching, and tempering. Adjacent to the furnaces are shot blasting booths,
chemical cleaning lines, and plating stations. Strict chemical segregation protocols apply in this area.

Bay E — Inspection and Shipping: The final bay serves as the quality inspection station, non-destructive
testing area, and finished goods staging zone. Coordinate measuring machines (CMMs), ultrasonic testing
equipment, and hardness testers are maintained here. Completed parts are documented, packaged, and transferred
to the adjacent shipping dock.

Aisle markings, bay boundaries, and pedestrian walkways are painted in high-visibility yellow throughout
the floor. Overhead signage identifies each zone and the specific PPE required for entry.
"""

