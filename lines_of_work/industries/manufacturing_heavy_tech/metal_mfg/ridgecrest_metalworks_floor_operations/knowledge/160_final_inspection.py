title = "Final Inspection Before Shipment"

content = """
Final inspection is the last verification step before finished goods are released and shipped to the customer. At Ridgecrest Metalworks, no product is dispatched without a completed and signed final inspection record. Final inspection is performed by trained quality inspectors independent of the production team.

The final inspection process begins with reviewing the job traveller to confirm that all required in-process inspection sign-offs are present and that no outstanding NCRs are open against the job. If any control points are unsigned or an unresolved NCR exists, the inspection is suspended and the issue resolved first.

Final inspection scope is defined by the inspection plan for each part number and includes a selection from:

1. Dimensional inspection: Key drawing dimensions are verified using appropriate instruments. The number of characteristics inspected and the sample size are defined in the inspection plan based on criticality. For first-time jobs or changed processes, 100 % inspection of critical dimensions may be required.

2. Visual inspection: All surfaces are examined for machining damage, weld defects, surface finish non-conformances (scratches, pits, corrosion), coating defects, and correct identification markings.

3. Functional checks: Where applicable, threads are gauged (go/no-go), hole patterns are checked with pin gauges or templates, and assembly-critical features are verified.

4. Coating thickness and adhesion: Final paint or plating thickness is measured and recorded. Adhesion spot tests may be performed on witness panels from the batch.

5. Documentation check: Part numbers, customer order references, material certifications, CoCs, and any required test reports are assembled and reviewed for completeness and correctness.

6. Quantity verification: The delivered quantity is counted and matched to the order. Under- and over-shipments are flagged and addressed per customer and commercial requirements.

Upon satisfactory completion of all checks, the inspector signs and stamps the inspection record and traveller, indicating release. Products are labeled with acceptance tags and transferred to the dispatch area. The completed documentation pack (traveller, CoC, MTR) accompanies the shipment. Any deficiencies found at final inspection are processed through the NCR system with root cause analysis to drive process improvement.
"""

