title = "Die Casting: High-Pressure and Gravity Methods"

content = """
Die casting uses reusable metal molds (dies) to produce castings with excellent dimensional accuracy,
fine surface finish, and high production rates. Ridgecrest Metalworks operates die casting for selected
non-ferrous alloy components where volume and dimensional requirements justify the die tooling investment.

High-Pressure Die Casting (HPDC):
In HPDC, molten metal is injected into the die cavity at high pressure (typically 70–1000 MPa) and
high velocity using a cold-chamber or hot-chamber machine. The die is made of H13 hot-work tool steel
and is water-cooled to enable rapid solidification and short cycle times (seconds to minutes).

Cold-Chamber HPDC: Molten metal is ladled into the shot sleeve outside the machine and then pushed
into the die by the plunger. Used for aluminum, copper alloys, and magnesium alloys, which are too
corrosive to run in the submerged (hot-chamber) system.

Hot-Chamber HPDC: The injection mechanism is submerged in the molten metal. Used for zinc (Zamak
alloys) and some low-melting-point alloys. Not suitable for aluminum or copper.

Advantages of HPDC: High production rates (hundreds of shots per hour for small parts), excellent
dimensional repeatability, fine surface finish (Ra 0.8–3.2 µm), and ability to produce thin walls
(1–3 mm). Disadvantages: high die cost, limited to non-ferrous alloys, and susceptibility to gas
porosity from entrapped air during high-velocity fill (limits heat treatability and weld repair).

Gravity Die Casting (Permanent Mold):
Molten metal is poured into a preheated steel or iron die by gravity, without the use of injection
pressure. Lower filling velocity reduces turbulence and gas entrapment compared to HPDC, producing
castings with lower porosity suitable for heat treatment and structural applications. Cycle times are
longer and thin-wall capability is more limited than HPDC.

Ridgecrest uses gravity die casting for aluminum housings and covers where improved mechanical
properties (achieved through subsequent heat treatment) are required.

Die Maintenance:
Die life is measured in shots. H13 steel dies for aluminum can last 100,000–500,000 shots with proper
maintenance. Die maintenance includes: regular cleaning of the cavity, application of die release agent
(lubricant and thermal barrier), monitoring for heat checking (network of surface cracks from thermal
cycling), and repair of worn or damaged features by welding or insertion of replacement inserts.
"""

