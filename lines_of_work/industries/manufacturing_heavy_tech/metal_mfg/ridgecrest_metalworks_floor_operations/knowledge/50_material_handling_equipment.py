content = """
Moving heavy, awkward, and hot metal components safely and efficiently requires a variety of material
handling equipment at Ridgecrest Metalworks. Operators must be familiar with the equipment available
in their work area, its rated capacity, and the requirements for safe operation.

Overhead Cranes:
Bay A and Bay B are each served by overhead bridge cranes. Bay A has a 30-ton and a 15-ton crane
sharing the runway. Bay B has a 50-ton crane for heavy die and billet handling and a 10-ton auxiliary
crane. Only certified crane operators may control the crane's hoist motions. Signaling is by voice,
radio, or standard hand signals as defined in ASME B30.2, Appendix A. Before each lift, the operator
and rigger confirm the load weight, rigging configuration, and travel path.

Jib Cranes and Gantry Cranes:
Workstation jib cranes (1/2 ton to 2 ton) are installed at select machining cells and the grinding
area to lift heavy workpieces onto machine tables. Gantry cranes (portable bridge cranes) are used
in Bay D for moving parts in and out of pit furnaces. All jib and gantry cranes display their rated
capacity on a permanent tag; never lift a load exceeding the rated capacity.

Forklifts and Pallet Jacks:
Several counterbalanced forklifts (3-ton to 7-ton capacity) operate in the material storage area,
receiving dock, and shipping bay. Electric pallet jacks are used for moving pallet loads within the
machining center where floor space and clearances limit forklift operation. Motorized pallet jacks
are stand-up-ride or walkie types and must be operated within their rated load capacities.

Vacuum Lifters and Magnetic Lifters:
Sheet metal and plate stock is moved using vacuum lifting attachments (for non-porous, smooth-surfaced
plate) or switchable-permanent magnet lifters (for ferrous materials only). Operators must check that
the vacuum level or magnet-on status is confirmed before lifting. Lifting slotted, perforated, or
rough-cast surfaces with vacuum lifters is not permitted.

Floor Carts and Die Trucks:
Heavy tooling such as forging dies and large fixtures are transported on dedicated die-change carts
with V-block supports and load locks. These carts are manually pushed on smooth floor areas; motorized
tugger trains are used for moving multiple carts in the inter-bay material flow path. Die carts must
never be left unattended in main aisles or emergency egress paths.
"""

