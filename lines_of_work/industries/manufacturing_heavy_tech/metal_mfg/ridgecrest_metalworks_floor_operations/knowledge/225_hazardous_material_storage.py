title = "Hazardous Material Storage: Chemicals and Gases"

content = """
Ridgecrest Metalworks uses a variety of hazardous materials in daily operations: cutting fluids, rust inhibitors, degreasing solvents, weld shielding gases, and heat treatment salts. Improper storage of these materials creates fire, explosion, toxic exposure, and environmental liability. Compliance with OSHA 29 CFR 1910.106, 1910.119, and local fire codes is mandatory.

**Chemical storage cabinets.** Flammable liquids (flash point below 100°F/38°C) must be stored in listed flammable storage cabinets. These cabinets are yellow, self-closing, and vented. The total volume of flammables outside a cabinet in any work area must not exceed one day's supply (typically 25 gallons). Cabinets must be kept closed when not actively in use.

**Incompatible chemical segregation.** Never store incompatible materials in the same cabinet or on adjacent shelves without physical segregation. Key incompatibilities at Ridgecrest:
- Oxidizers (hydrogen peroxide, nitrates) must be separated from flammables and organics by at least one fire-rated wall or in a dedicated oxidizer cabinet.
- Acids (hydrochloric acid, phosphoric acid for metal cleaning) must not share a cabinet with alkaline solutions or with flammables.
- Cyanide-bearing heat treatment salts must be stored in a locked, dry area with a separate drain containment and labeled with skull-and-crossbones NFPA hazard markers.

**Compressed gas cylinders.** Compressed gas cylinders (argon, CO₂, oxygen, acetylene, nitrogen) are stored:
- Upright with caps on when not in use.
- Chained or strapped to a wall or cylinder cart—never free-standing.
- Separated: oxygen cylinders must be stored at least 20 feet from fuel gas cylinders (acetylene, propane) or separated by a 5-foot tall, 0.5-hour fire-rated barrier.
- Away from heat sources, direct sunlight, and electrical panels.

**Secondary containment.** All liquid chemical storage areas must have secondary containment (a sump or bermed floor) capable of holding 110% of the largest container volume. This prevents spills from reaching floor drains.

**Labeling and SDS.** Every container must be labeled with the chemical name, hazard pictograms, and signal word per GHS/HazCom 2012. The corresponding Safety Data Sheet (SDS) must be accessible within 30 seconds from any point in the facility via the online SDS portal or the physical binder at the safety station.

Maximum quantities stored in any zone are posted at zone entry. Never exceed posted limits.
"""

