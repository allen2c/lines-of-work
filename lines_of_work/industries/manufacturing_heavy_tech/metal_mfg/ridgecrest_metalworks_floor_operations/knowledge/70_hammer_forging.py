title = "Hammer Forging: Drop Hammers and Power Hammers"

content = """
Hammer forging uses the kinetic energy of a falling or driven ram to deform the workpiece through
impact. The high deformation rate characteristic of hammers is beneficial for achieving complete cavity
fill in impression dies and for rapid open-die shaping operations. Ridgecrest Metalworks operates
gravity drop hammers and air-actuated power hammers in Bay B for steel and iron forging operations.

Types of Hammers:

Gravity (Board) Drop Hammers:
The ram is raised by friction boards, straps, or chain drive to a selected height and then released
to fall under gravity. Energy per blow is determined by ram weight multiplied by drop height.
Board hammers are mechanically simple, reliable, and well-suited to impression-die forging of small
to medium steel components. The operator controls blow energy by varying the drop height.

Air/Steam Drop Hammers:
Air or steam pressure is applied above the ram to accelerate it downward beyond free-fall velocity,
increasing blow energy for a given ram weight. The same pressure can be used to lift the ram.
Blow energy is controlled by the operator via the throttle valve. Air hammers offer higher blow
energy than board hammers of equivalent ram weight and provide better control over blow velocity.

Counter-Blow (Counterblow) Hammers:
Two rams strike the workpiece simultaneously from above and below. The opposing blow vectors are
absorbed in the machine frame rather than the foundation, reducing the size of the required foundation
and vibration transmitted to the building. Counter-blow hammers are used for large forgings where
the simultaneous bilateral compression improves symmetry of deformation.

Hammer Operation Safety:

Foundation and Vibration:
Drop hammers produce intense vibration transmitted through the floor. Hammer foundations at Ridgecrest
are designed with vibration-isolation pads and are sized and engineered independently of the building
slab. Periodic inspection of foundation integrity and anchor bolt torque is required.

Scale and Flying Metal:
Hot scale dislodged from billets by hammer blows can project significant distances. The area around
operating hammers must be barricaded with steel deflector shields. Operators and nearby personnel
must wear full foundry PPE including face shields and leather aprons.

Die Locking and Setup:
Hammer die halves are locked to the ram and anvil with keys, T-bolts, or shank clamps per the die
drawing. Loose die components can eject violently during forging. Die installation is verified by the
cell lead before the first blow of each setup.

Slam-Down Prevention:
Operators must never stand between the hammer ram and the anvil during any phase of die change or
setup adjustment, even when the hammer is assumed to be in the held-up position. Mechanical blocks
or safety dogs must be engaged to physically prevent ram drop before any personnel enter the die space.
"""

