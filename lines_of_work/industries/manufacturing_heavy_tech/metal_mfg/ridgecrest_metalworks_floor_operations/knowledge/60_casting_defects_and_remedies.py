title = "Common Casting Defects and Remedies"

content = """
Casting defects are discontinuities in the cast structure that reduce mechanical properties, violate
dimensional requirements, or compromise the appearance of finished components. Recognizing and correctly
diagnosing casting defects is essential for quality technicians, foundry operators, and process engineers
at Ridgecrest Metalworks. Defects must be documented, dispositioned, and root-caused to prevent
recurrence.

Shrinkage Porosity:
Internal cavities or surface depressions formed when liquid metal cannot reach areas that solidify
late in the sequence, leaving voids where liquid feeding is cut off. Detected by radiographic testing
(X-ray) for internal shrinkage; visible as surface sinks on the casting exterior. Remedied by
enlarging risers, adding chills to redirect solidification, or redesigning gates to improve feeding.

Gas Porosity:
Smooth, round voids distributed throughout the casting caused by dissolved gas (hydrogen in aluminum,
nitrogen and hydrogen in steel) coming out of solution during solidification. Distinct from shrinkage
in appearance (rounder pores, more uniformly distributed). Remedied by degassing the melt, reducing
moisture in the mold and charge materials, and controlling pouring temperature.

Sand Inclusions:
Fragments of mold or core sand embedded in the casting surface or interior, caused by mold erosion
from turbulent metal flow, inadequate mold strength, or damaged mold surfaces. Results in hard spots
that damage cutting tools during machining. Remedied by improving mold hardness, slowing pour rate,
and improving gating design to reduce turbulence.

Cold Shuts:
A discontinuity or seam on the casting surface where two streams of metal met but failed to fuse
completely due to insufficient temperature or flow. Appear as a visible line or fold. Caused by low
pour temperature, long flow distances, or thin sections that freeze prematurely. Remedied by increasing
metal temperature or improving gating to reduce travel distance.

Misrun:
An incomplete casting where the metal failed to fill the entire mold cavity before solidifying.
Related to cold shut but more severe. Caused by insufficient metal temperature, slow pouring rate,
poor venting (back-pressure of trapped air slows fill), or inadequate metal volume.

Hot Tearing:
A crack or tear that forms during solidification while the metal is still semi-solid and weak. Caused
by thermal stresses from differential cooling across thick and thin sections or restraint by the mold.
Remedied by improving casting design, adding generous radii at section transitions, and modifying
shakeout timing.
"""

