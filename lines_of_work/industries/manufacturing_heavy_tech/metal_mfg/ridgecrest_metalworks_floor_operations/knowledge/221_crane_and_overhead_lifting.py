title = "Overhead Crane and Rigging Operations"

content = """
Overhead bridge cranes at Ridgecrest Metalworks range from 5-ton to 20-ton capacity and are used to move steel plate, coils, large fabrications, and heavy machine components. Rigging failures can result in catastrophic drops with lethal consequences. Every overhead lift requires qualified personnel, correct rigging selection, and a clear lift zone.

**Qualified operators and riggers.** Crane operators must be trained and designated as qualified per ASME B30.2 and company procedure. Riggers who attach and detach loads must be qualified riggers per ASME B30.9. No one else may perform these roles. Verbal instruction at the crane controls must use standard hand signals (per ASME B30.2) or radio communication with clear call signs.

**Pre-lift planning.** Before any lift:
- Identify the lift weight. Check the work order, material cert, or weigh the load. Never guess.
- Select rigging with a working load limit (WLL) that exceeds the lift weight with appropriate sling angle de-rating applied.
- Inspect all slings, hooks, shackles, and lifting beams. Discard any sling showing cuts exceeding 10% of wire rope diameter, kinked wire rope, or chain links showing elongation, cracks, or wear.
- Clear the travel path of personnel. Establish a drop zone: no one stands under or near a suspended load at any time.

**Sling types and selection.**
- Wire rope slings: high strength, used for heavy plate and coils; susceptible to damage from sharp edges—use edge protection.
- Chain slings: durable and heat resistant; inspect every link for stretch, cracks, and wear; grade 80 or 100 alloy chain only.
- Web slings (nylon): protect finished surfaces; inspect for cuts, holes, acid burns, and UV degradation.
- Spreader bars and lifting beams: used when sling angle would otherwise be too acute or when the load requires distributed lift points.

**Sling angle de-rating.** A sling at 60° from horizontal retains approximately 87% of its single-vertical WLL; at 30°, only 50%. Always calculate based on actual angle. The sling angle must never be less than 30° from horizontal.

**Lift execution.** Lift slowly and test-halt at 6 inches above the floor to confirm balance and rigging security before traveling. Travel at low speed with a designated ground guide when visibility is limited. Lower loads onto adequate dunnage—never directly onto the floor if the rigging cannot be released safely.

Post-use, all rigging gear is returned to the designated storage rack and reinspected before storage.
"""

