title = "Press Forging: Hydraulic and Mechanical Presses"

content = """
Press forging uses a slow, sustained squeezing force — rather than the rapid impact of a hammer —
to deform the workpiece. The controlled, penetrating action of a press produces more uniform deformation
through the cross-section of the part, with less chilling of the surface layer and better dimensional
control than equivalent hammer forging. Ridgecrest Metalworks operates both hydraulic and mechanical
presses in Bay B for a range of forging work.

Hydraulic Presses:
Hydraulic presses use hydraulic cylinders driven by pump-powered fluid pressure to generate force.
Key characteristics:
- Force is constant throughout the full stroke (as opposed to mechanical eccentric presses, which
  have variable force vs. stroke position).
- Stroke length and speed are continuously controllable, making them ideal for isothermal and
  warm forging operations where controlled deformation rate is important.
- Maximum capacities at Ridgecrest: 2,500-ton and 1,000-ton hydraulic presses in Bay B.
- Typical ram speed: 10–50 mm/s for hot forging; slower for difficult-to-forge materials.

Hydraulic presses are the preferred equipment for forging aluminum, titanium, and nickel superalloys,
where temperature sensitivity and precise force control are critical. They are also used for large
open-die cogging operations on steel billets exceeding the hammer equipment's practical weight limit.

Mechanical Presses:
Mechanical presses convert rotational energy (from a flywheel) to linear ram motion via an eccentric
crank or knuckle-joint mechanism. Key characteristics:
- Energy-limited machines: The energy available for each stroke is determined by the flywheel size
  and speed; once the flywheel energy is expended, the press cannot complete the stroke, which can
  result in the ram jamming in the die (sticking out) — a significant operational hazard.
- Stroke is fixed; the die must be designed to achieve complete fill at the press's bottom dead center.
- Higher ram speeds than hydraulic presses; suitable for high-rate production of small to medium
  steel forgings.
- Capacities at Ridgecrest: 800-ton knuckle-joint mechanical press.

Die-Change Safety:
Die changes on press equipment require full LOTO of the press. Dies must be secured on the press
bolster before the LOTO is released. Die weights must be known; overhead crane ratings and rigging
configurations must be verified against the die weight before any die change begins.

First-Piece Inspection:
After each die installation or setup change, a first-piece forging is produced and inspected
dimensionally before releasing the run to full production. First-piece records are attached to the
traveler and retained in the quality system.
"""

