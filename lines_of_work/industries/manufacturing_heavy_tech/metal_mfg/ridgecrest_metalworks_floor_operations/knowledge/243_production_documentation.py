title = "Production Documentation: What to Complete and File"

content = """
Complete and accurate production documentation is a requirement of ISO 9001, customer contracts, and regulatory compliance at Ridgecrest Metalworks. Documentation creates the evidence that parts were manufactured and inspected to specification. In the absence of records, no claim of conformance can be defended.

**Documents generated per work order.**

1. **Shop traveler.** The primary manufacturing record. Must be completed in full at every routing step—operator signature, date, quantity produced, quantity scrapped with codes. No blank fields. Corrections are made by single-line strikethrough with initials; white-out is prohibited.

2. **Inspection records.** First-piece, in-process, and final inspection records on the Ridgecrest standard inspection forms. Each measurement is recorded individually—not just a pass/fail checkbox. The inspector signs and dates each record.

3. **Material test reports (MTRs).** The mill-issued material certifications proving the purchased raw material meets the specified grade and mechanical properties. MTRs are cross-referenced to the heat number on the material label and the work order traveler.

4. **Non-conformance reports (NCRs).** Any part that fails inspection generates an NCR. The NCR documents: what the defect is, where it was found, what quantity is affected, and the disposition (use-as-is, rework, scrap, return to supplier).

5. **Production confirmation printout.** The SAP-generated confirmation showing what quantities were reported at each operation. This is the system-side record that pairs with the physical traveler.

6. **Certificate of conformance (CoC).** A signed document (by QA manager or designee) stating that the parts were manufactured, tested, and inspected per the applicable specifications. Required by many customers before shipment.

**Retention requirements.** All production documents are retained for a minimum of 10 years unless the customer contract or regulatory requirement specifies longer (aerospace and defense customers may require 20+ years or permanent retention for life-of-equipment parts).

**Document control.** Documents must not be altered after closure without a formal corrective record. Lost travelers require a QA investigation and a reconstructed record signed by the supervisor and QA manager before the work order can close.

Electronic records in the ERP are the authoritative version; paper is the backup and contemporaneous record.
"""

