content = """
Hazardous waste documentation at Ridgecrest Metalworks is not optional paperwork—it is the legal record of each pound of hazardous waste generated, how it was stored, where it was shipped, and where it was ultimately disposed. Incomplete or inaccurate waste manifests are a direct RCRA violation.

**The uniform hazardous waste manifest.** The EPA uniform manifest (EPA Form 8700-22) is the tracking document that must accompany every hazardous waste shipment from the moment it leaves the generator facility until it reaches the disposal or treatment facility. The manifest contains:
- Generator name, address, and EPA ID number.
- Transporter name and EPA ID.
- Designated treatment, storage, and disposal facility (TSDF) name and permit number.
- Waste description, DOT proper shipping name, UN/NA number, hazard class.
- Quantity in pounds or gallons.
- Waste characterization codes.
- Emergency response contact information.
- Signatures of generator, transporter, and receiving facility.

**Generator signature obligation.** The generator certification on the manifest states that the waste has been classified correctly, is properly packaged, and that the generator has made a good-faith effort to use a licensed, responsible transporter and TSDF. The EHS manager or their designee signs the manifest—never a contractor or driver.

**Manifest copies and retention.** The generator retains the manifest copy (copy 1). The transporter retains a copy. The TSDF retains a copy and mails a signed confirmation copy back to the generator within 35 days. If the signed copy is not received within 60 days, the generator must file an exception report with the EPA. All manifest records are retained for minimum 3 years (LQG requirements; state rules may require longer).

**Land Disposal Restriction (LDR) notification.** For hazardous wastes subject to land disposal restrictions, the generator must include a written LDR notification with the manifest identifying the waste and confirming it has been treated to meet EPA standards before land disposal. The EHS manager prepares this document.

**Biennial reporting.** Large Quantity Generators submit a biennial hazardous waste report to the EPA every even-numbered year, summarizing total quantities of each hazardous waste generated and the disposal method used.

All waste documentation is filed in the EHS records room and must be accessible for regulatory inspection without advance notice.
"""

