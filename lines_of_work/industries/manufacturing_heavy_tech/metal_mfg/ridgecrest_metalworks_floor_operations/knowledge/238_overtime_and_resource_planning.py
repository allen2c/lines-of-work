title = "Overtime Authorization and Resource Planning"

content = """
Overtime is an important capacity lever for Ridgecrest Metalworks to recover from schedule shortfalls, handle demand spikes, or meet critical customer commitments. However, overtime carries cost, fatigue, and safety risks that require it to be planned and authorized carefully rather than used as a default response to poor scheduling.

**Authorization process.** Overtime must be authorized in writing (or via the HR system) before it is worked—not retroactively. Authorization levels:
- Up to 4 hours OT per person per week: Cell supervisor authority, with planner confirmation that the work is available and necessary.
- 4–12 hours OT per person per week: Production manager approval required.
- More than 12 hours OT per person per week: Operations director approval, with HR notification for fatigue risk assessment.

Weekend overtime requires 48-hour advance notice to all affected employees per the labor agreement, except in declared emergency situations.

**Legal and safety limits.** Federal and state laws limit maximum hours for certain categories of workers. Beyond legal limits, fatigue-related error rates increase significantly in precision metal manufacturing operations. Supervisors must not pressure operators to work unsafe hours. Any operator who self-reports fatigue must be reassigned to a lower-risk task or released for the day.

**Resource planning for overtime.** Before approving overtime, the supervisor must confirm:
- The machine(s) to be used are available and not scheduled for PM.
- Sufficient material is available (issued or staged).
- The work order and traveler documents are ready.
- Required tooling and fixtures are in place.
- A qualified operator is available for that specific machine (certification check).
- Adequate supervision is present—unsupervised OT work on high-risk operations is not permitted.

**Cross-training and flexibility.** The primary strategy for handling demand variation without excessive overtime is cross-training operators across multiple machines and operations. The skills matrix (tracked in the training management system) shows which operators are qualified for which machines. The planner uses this matrix when building OT schedules to select the most versatile available people.

Overtime usage is reported monthly to finance and operations leadership as part of the labor cost variance review.
"""

