content = """
Immediate and appropriate first aid response can prevent a minor injury from becoming a serious medical
emergency. Ridgecrest Metalworks maintains trained first aid responders on every shift, stocked first
aid stations throughout the facility, and automated external defibrillators (AEDs) at key locations.

First Aid Trained Responders:
A minimum of two employees per shift hold current first aid and CPR certification (American Red Cross
or equivalent). Their names and current certifications are posted on the shift supervisor board. First
aid responders are not substitutes for emergency medical services; they provide initial stabilization
until EMS arrives.

First Aid Station Locations:
Stocked first aid kits compliant with ANSI Z308.1 are located at each bay entrance, in the supervisor
office, in each break room, and in the main administrative area. Kits include bandages, sterile gauze,
wound closure strips, antiseptic wipes, burn gel, eye wash solution, and nitrile gloves. Kits are
inspected monthly and restocked as needed.

Emergency Eyewash and Body Wash Stations:
Eyewash and drench shower stations are located within 10 seconds (approximately 55 feet) of all areas
where corrosive chemicals are handled, including the acid cleaning area, plating area, and battery
charging station. Stations are flushed weekly to ensure functionality and freedom from sediment buildup.
In the event of a chemical splash to the eyes: immediately flood the eye with water for a minimum of
15 minutes, holding the eyelid open; do not rub the eye; seek medical evaluation even if irritation
appears to resolve.

AED Locations:
Automated External Defibrillators are located at:
- Bay A entrance (Melt Shop)
- Bay C main aisle (Machining Center)
- Main lobby adjacent to the HR office
- Cafeteria near the main entrance

AEDs are inspected monthly to confirm operational readiness. All certified first aid responders are
trained in AED use. If a person collapses and is unresponsive: call 911 immediately, begin CPR,
and have someone retrieve and deploy the nearest AED without delay.

Burn First Aid (Common in Foundry and Forge):
Cool the burn with running cool (not ice cold) water for at least 10 minutes. Do not apply butter,
oil, or toothpaste. Cover with a sterile non-adherent dressing. For any burn larger than the palm of
a hand, full thickness burns, or burns to the face, hands, or joints, call EMS immediately.
"""

