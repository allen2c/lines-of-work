content = """
Material handling via forklifts and overhead cranes is constant at Ridgecrest Metalworks due to the
weight and size of metal billets, castings, dies, and finished components. Both equipment types
present significant struck-by, crush, and dropped-load hazards that require strict operational controls.

Forklift Safety:
Only trained and annually evaluated operators may operate powered industrial trucks (PITs) at Ridgecrest.
Training covers pre-operation inspection, load capacity limits, stability triangle principles, safe travel
speeds, and pedestrian interaction protocols.

Key operating rules:
- Conduct a documented pre-shift inspection using the Forklift Inspection Checklist before every shift.
  Any deficiency that affects safe operation requires taking the unit out of service immediately.
- Travel with forks lowered to 6–8 inches above the floor, tilted back.
- Never exceed load capacity shown on the nameplate; no added counterweights without engineering approval.
- Slow to a walking pace and sound the horn at all blind intersections and building exits.
- Pedestrians always have the right of way; operators must stop and yield.
- No riders on forklifts unless the unit has a factory-installed passenger seat.
- On inclines, always travel with the load facing uphill.

Overhead Crane Safety:
Overhead cranes in Bay A and Bay B are rated from 10 to 50 tons and require crane operator certification
in accordance with ASME B30.2. Daily pre-shift inspections include checking wire rope condition, hook
latch, electrical controls, limit switches, and brake function.

Key crane rules:
- All crane lifts require a designated rigger and a spotter; communication must be established before
  any lift begins.
- Rigging must be inspected before each use; chain slings, wire rope slings, and synthetic slings must
  be discarded if worn, kinked, or at or beyond their retirement criteria.
- Never lift a load over personnel. Establish a clear drop zone and ensure it is barricaded before lifting.
- Tag lines must be used to control load rotation on any lift exceeding 6 feet of travel height.
- Critical lifts — defined as lifts exceeding 75% of crane rated capacity or involving unusual rigging
  configurations — require a written lift plan approved by the engineering team.

Pedestrians must stay clear of active crane bays. Overhead crane travel aisles are marked with yellow
floor striping; personnel must not stand beneath a suspended load under any circumstances.
"""

