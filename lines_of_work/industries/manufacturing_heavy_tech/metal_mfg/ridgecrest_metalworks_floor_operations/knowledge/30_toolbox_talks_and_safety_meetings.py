content = """
Toolbox talks are brief, focused safety discussions conducted on the production floor before a shift
or before a specific high-risk task begins. At Ridgecrest Metalworks, toolbox talks are a key mechanism
for reinforcing safety standards, communicating new hazard information, and maintaining safety
awareness across all shifts.

Toolbox Talk Requirements:
All production shifts must conduct a toolbox talk at or near the start of the shift. The talk is led
by the cell lead or shift supervisor and lasts 5 to 10 minutes. Topics should be directly relevant
to the work being performed that day or to a recent incident, near-miss, or seasonal hazard. Attendance
is documented on the Toolbox Talk Record form, which is retained in the department safety file for
12 months.

Effective Toolbox Talk Format:
1. State the topic clearly and explain why it is relevant to today's work.
2. Describe the specific hazard, risk, or standard being discussed.
3. Explain the correct procedure, control measure, or required behavior.
4. Ask the group for questions, observations, or relevant experiences.
5. Confirm understanding and close with a key takeaway.

Toolbox talks should be conversational, not lecture-style. The most effective sessions involve
two-way discussion where workers share their own observations and questions. Safety Officer library
of pre-written toolbox talk outlines covers over 80 topics and is accessible on the facility intranet.
Cell leads are encouraged to customize these templates to reflect specific equipment and job conditions.

Monthly Safety Committee Meeting:
The Safety Committee meets monthly and includes the Safety Officer, a representative from each
production bay, the Operations Manager, and HR. Agenda items include review of the monthly safety
audit findings, open corrective actions, near-miss and incident trends, OSHA recordkeeping status,
and planning for upcoming safety training. Meeting minutes are posted on the safety bulletin board
within 3 days of the meeting.

Annual Safety Stand-Down:
Once per year, Ridgecrest conducts a facility-wide Safety Stand-Down day. Production is paused for
a half-day while all employees rotate through safety refresher stations covering emergency procedures,
PPE, LOTO, and current focus hazards identified from the year's incident data.
"""

