title = "Total Productive Maintenance (TPM): Pillars and Floor Roles"

content = """
Total Productive Maintenance (TPM) is a company-wide manufacturing improvement
strategy that aims to maximize equipment effectiveness by engaging all employees —
from operators to senior management — in proactive maintenance activities. TPM
originated at Nippondenso in Japan and was formalized by the Japan Institute of
Plant Maintenance (JIPM). Ridgecrest Metalworks implements TPM to reduce unplanned
downtime, improve product quality, and increase operator ownership of equipment.

**The Eight TPM Pillars:**

1. **Autonomous Maintenance (Jishu Hozen):** Operators perform routine cleaning,
   lubrication, bolt-tightening, and basic inspection. This restores equipment to
   basic condition and prevents accelerated deterioration. Operators complete
   seven steps from initial cleaning to full autonomous management.

2. **Planned Maintenance:** The maintenance team transitions from reactive to
   preventive and predictive maintenance, scheduling tasks during planned windows
   rather than responding only to failures.

3. **Focused Improvement (Kobetsu Kaizen):** Cross-functional teams use structured
   problem-solving (PDCA, WHY-WHY analysis) to eliminate chronic equipment losses
   systematically.

4. **Education and Training:** Skills matrices identify gaps. Operators receive
   hands-on equipment training; maintenance technicians develop multi-skill
   competencies in mechanical, electrical, and hydraulic systems.

5. **Early Equipment Management:** TPM principles are applied at the equipment
   design and commissioning stage to build in reliability, ease of cleaning, and
   maintainability before installation.

6. **Quality Maintenance (Hinshitsu Hozen):** Equipment conditions that cause
   quality defects are identified and controlled. The goal is zero defects through
   equipment condition management rather than post-production inspection.

7. **Safety, Health, and Environment:** All maintenance and autonomous activities
   include risk assessment. Zero accidents is a parallel goal to zero breakdowns.

8. **TPM in Administration:** Improvement of office and support function processes
   to eliminate waste that affects production flow.

**Operator Responsibilities Under TPM:**
- Complete daily cleaning and lubrication according to the One-Point Lesson card
  posted at each machine.
- Tag abnormalities (leaks, unusual sounds, loose guards) using the red/orange
  tag system and notify maintenance immediately.
- Record cleaning times and abnormality findings in the equipment logbook.
- Participate in improvement activities for their assigned equipment area.

**OEE as the TPM Metric:**
TPM uses Overall Equipment Effectiveness (OEE) as its primary measure of success.
The target world-class OEE is ≥85%. Floor teams review OEE data in daily meetings
and use it to prioritize improvement activities.
"""

