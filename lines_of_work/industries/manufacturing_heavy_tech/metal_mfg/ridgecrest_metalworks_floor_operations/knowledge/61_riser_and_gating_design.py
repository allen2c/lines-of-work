title = "Riser and Gating System Design"

content = """
The riser and gating system is the network of channels and reservoirs through which molten metal
flows to fill the mold cavity and feed shrinkage during solidification. Proper design of this system
is critical to casting soundness, yield, and dimensional quality at Ridgecrest Metalworks.

Gating System Components:

Pouring Cup/Sprue Cup: Receives metal from the ladle and provides a seal to prevent air aspiration.
Straight-sided or conical tapered shapes are preferred over parallel-walled sprues to maintain back-
pressure during pour.

Sprue: The vertical channel down which metal flows from the cup to the parting line level. Tapered
(wider at top) to compensate for metal acceleration under gravity, maintaining full flow and minimizing
air aspiration.

Runners: Horizontal distribution channels at the parting line that carry metal from the sprue base
to the in-gates. Runner design affects velocity and turbulence.

In-Gates: The final entry points into the mold cavity. Gate size, number, location, and cross-section
shape control the rate and direction of metal entry. The sum of in-gate cross-sectional areas relative
to the runner and sprue determines whether the system is pressurized (choked at the gate) or
unpressurized. Pressurized systems help filter slag but carry higher velocity into the cavity.

Riser Function:
Risers are reservoirs of molten metal that compensate for the volumetric shrinkage that occurs as
metal solidifies (typically 3–7% for steel, 6% for cast iron, 5% for aluminum). To feed effectively,
the riser must: (1) remain liquid longer than the casting section it feeds (achieved by sizing the
riser's modulus larger than the casting's modulus), (2) be connected to the area of last solidification,
and (3) contain sufficient volume of liquid metal to compensate for casting shrinkage.

Blind Risers vs. Open Risers:
Open risers extend to the top of the cope; they lose heat rapidly from the exposed top surface. Blind
risers are fully enclosed in the mold and can be insulated with exothermic sleeves or insulating
sleeves to extend their liquid life and reduce the riser size needed.

Chills:
Metal chills (internal or external) accelerate solidification in targeted areas, redirecting the
solidification front toward the riser and eliminating isolated hot spots. Internal chills become part
of the casting; external chills are reusable and mounted against the mold cavity wall.
"""

