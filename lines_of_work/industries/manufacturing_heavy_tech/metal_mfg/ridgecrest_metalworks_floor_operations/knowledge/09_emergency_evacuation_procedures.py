title = "Emergency Evacuation Procedures"

content = """
Ridgecrest Metalworks maintains a written Emergency Action Plan (EAP) in compliance with OSHA 29 CFR
1910.38. All employees are trained on evacuation procedures during new hire orientation and during annual
refresher drills. A full facility evacuation drill is conducted at least twice per year.

Evacuation Triggers:
Evacuation is required upon activation of the facility fire alarm system, announcement over the PA
system by authorized personnel, or a direct order from the Shift Supervisor, Safety Officer, or
emergency responders. Do not wait to confirm the nature of the emergency before beginning evacuation.

Evacuation Routes:
Evacuation routes are posted on floor maps displayed at each bay entrance, break room, and emergency
station. Primary and secondary routes are identified. Routes are painted with green directional arrows
on the floor and supplemented by illuminated exit signage. All exit doors are push-bar equipped and
must never be blocked or locked during occupied hours.

Assembly Points:
- North Assembly Point: Parking lot adjacent to Bay A, marked with a green "Assembly Point" sign
- South Assembly Point: Far end of the south employee parking lot, marked similarly
Personnel in Bay A and Bay B evacuate to the North Assembly Point. Personnel in Bay C, D, and E
evacuate to the South Assembly Point unless directed otherwise.

Accountability Procedure:
Each shift supervisor maintains a current headcount roster. Upon arrival at the assembly point,
supervisors immediately conduct a headcount and report to the Emergency Coordinator (typically the
Plant Manager or designee). Missing personnel must be reported to emergency responders — do not
re-enter the building to search for individuals.

Special Considerations for Foundry Personnel:
If molten metal is in process at the time of evacuation, operators must follow the emergency shutdown
sequence (posted at each furnace station) to safely leave the furnace in a hold state before
evacuating. This sequence must be completed in under 60 seconds; if the alarm requires immediate
evacuation, abandon the process and leave.

Persons with Mobility Limitations:
Designated evacuation assistants are assigned to support any personnel who may need assistance
reaching exit stairs or refuge areas. Refuge areas are identified on floor maps and are equipped with
two-way intercoms connected to the security desk.

After the All-Clear:
Personnel must remain at the assembly point until the Emergency Coordinator gives the all-clear.
Re-entry before the all-clear is prohibited. Upon return, the shift supervisor documents the event
in the shift log.
"""

