title = "Pattern Making: Materials and Allowances"

content = """
Patterns are the physical replicas of a casting used to create the mold cavity in sand casting
operations. The pattern is not identical to the finished casting — it incorporates deliberate dimensional
allowances that compensate for the physical changes the metal undergoes from liquid state to cooled
final casting. Pattern quality directly determines casting accuracy and production efficiency.

Pattern Materials:

Wood: Traditional pattern material, still used for prototype and short-run patterns. Inexpensive
and easy to machine; however, susceptible to moisture warping over time. Properly sealed and lacquered
wood patterns are used for low-volume production. Hardwoods (mahogany, cherry) are preferred for
durability.

Urethane Foam and Board: Commonly used for rapid prototype patterns and for large patterns where
timber weight would be prohibitive. High-density urethane boards machine cleanly and are dimensionally
stable. Not suitable for high-volume production use due to wear.

Aluminum: Durable, dimensionally stable, and easy to machine to high accuracy. Aluminum patterns
are used for medium-to-high production volumes where dimensional consistency over thousands of cycles
is required. Aluminum is resistant to moisture unlike wood.

Steel: Used for very high-volume production patterns and match plates where maximum durability and
minimal wear are essential. Heavier and more expensive to machine than aluminum.

Pattern Allowances:

Shrinkage Allowance: Molten metal shrinks as it solidifies and cools. The pattern is made oversized
by the alloy's specific shrinkage rate:
- Gray iron: 10 mm/m (1.0%)
- Steel: 20 mm/m (2.0%)
- Aluminum: 13 mm/m (1.3%)
- Copper alloys: 15–17 mm/m (1.5–1.7%)

Machining Allowance (Finish Stock): Additional material is added to surfaces that will be machined,
providing stock for the cutting tool to achieve the final dimensional and surface finish requirements.
Typical machining allowance: 3–6 mm for small castings, up to 12 mm for large rough castings.

Draft Angle: Tapers applied to vertical surfaces (perpendicular to the parting line) to allow the
pattern to be drawn from the sand without damaging the mold cavity. Typical draft: 1–3 degrees.

Distortion Allowance: For castings known to distort during cooling due to geometric asymmetry, the
pattern is intentionally distorted in the opposite direction to compensate for the expected movement.
"""

