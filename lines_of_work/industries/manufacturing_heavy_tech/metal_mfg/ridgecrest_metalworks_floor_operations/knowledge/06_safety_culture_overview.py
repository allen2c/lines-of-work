content = """
Ridgecrest Metalworks operates under a Zero-Harm safety philosophy, meaning that all injuries, illnesses,
and safety incidents are considered preventable. This is not a compliance slogan but an operational
framework that shapes daily decisions, resource allocation, and performance expectations at every level
of the organization.

The Zero-Harm commitment means that no production target, customer deadline, or cost constraint justifies
exposing workers to uncontrolled hazards. Every employee — from the plant manager to a newly hired floor
operator — has both the right and the responsibility to stop work when they observe an unsafe condition
or behavior. Stop-work authority is non-punitive; any employee who halts an operation due to a genuine
safety concern is protected from retaliation.

Safety culture at Ridgecrest is reinforced through several core practices:

Daily Pre-Shift Safety Briefings: Each shift begins with a 5-minute safety stand-up at the cell level.
The cell lead or shift supervisor discusses the day's hazards, reviews any open safety actions, and
confirms PPE compliance before production begins.

Near-Miss Reporting Culture: Near-misses are treated as precursor events to serious injuries. The facility
tracks near-miss reports as a leading safety indicator and rewards timely, accurate reporting. Suppressing
or failing to report a near-miss is a disciplinary matter.

Behavior-Based Safety Observations: Trained observers conduct regular safety observations on the floor,
recording safe behaviors and unsafe acts without attribution. Aggregate data drives targeted interventions.

Safety Performance Metrics: The facility tracks Total Recordable Incident Rate (TRIR), Lost Time Incident
Rate (LTIR), near-miss frequency, and safety observation completion rates. These metrics are reviewed
weekly by the safety committee and monthly at the all-hands operations meeting.

Management Visibility: Supervisors and managers are expected to conduct regular floor walks with explicit
safety observation components. Visible management engagement with safety reinforces that safety is a
genuine priority rather than a posted policy.

New hire safety orientation covers the Zero-Harm philosophy, emergency procedures, hazard communication,
and PPE requirements before any new employee sets foot on the production floor.
"""

