title = "Loading and Unloading Procedures for Trucks and Containers"

content = """
Loading and unloading operations at the Ridgecrest Metalworks shipping dock involve heavy equipment, elevated surfaces, and shared spaces between pedestrians and powered vehicles. Strict procedure compliance protects personnel, prevents product damage, and keeps the schedule running.

**Pre-arrival preparation.** Before a truck arrives, the dock coordinator confirms dock availability, assigns a specific bay number, and communicates this to the driver upon arrival. Dock doors remain closed until the truck is fully positioned and the dock lock (wheel chock or ICC bar restraint) is engaged. Never open a dock door to an unsecured truck.

**Unloading sequence.**
1. Visually inspect the truck interior before inserting forks or stepping inside.
2. Verify the bill of lading matches the expected PO before any material is moved.
3. Use the correct unloading equipment—steel coils require a coil car or spreader; plate bundles require a forklift with appropriate capacity and forks long enough for the bundle width.
4. Place unloaded material in the designated receiving inspection area, not directly into production racks.
5. Photograph any visible shipping damage before moving the shipment. Note damage on the BOL before the driver departs.

**Loading sequence.**
1. Confirm the packing list matches the picked and staged order.
2. Inspect the truck interior for cleanliness and structural integrity before loading.
3. Heaviest items go in first, placed low and centered over the axles.
4. Secure loads with appropriate blocking, bracing, or strapping per DOT regulations. Metal parts must not be able to shift during transport.
5. Verify load does not exceed the truck's rated payload.
6. Hand the signed BOL and any required shipment documentation to the driver.

**Dock safety rules.** Only qualified, trained personnel operate dock equipment. The dock leveler must be fully lowered after use—never leave it extended. Battery charging areas near the dock require ventilation; no open flames or sparks within 20 feet.

**Container loading.** Ocean freight containers have a maximum gross weight limit (typically 26,500 kg) that must not be exceeded. Floor loading within containers must account for container floor weight ratings—concentrated loads from forks require load-spreading plates.

All departing shipments are recorded in the ERP with posting of outbound delivery and goods issue, generating the final inventory reduction.
"""

