title = "Material Test Reports (MTRs) and Certification"

content = """
Material Test Reports (MTRs), also called Mill Test Reports or Certified Mill Test Reports (CMTRs), are documents issued by the material producer (steel mill, foundry, or processing center) certifying that the material supplied meets the requirements of a specified standard. At Ridgecrest Metalworks, MTRs are required for all structural, pressure-containing, and safety-critical raw materials and are linked to the specific heat (melt) of metal from which the material was produced.

An MTR typically contains:
- The producing mill's name and location
- Material specification (e.g., ASTM A36, ASTM A572 Grade 50, EN 10025 S355JR)
- Product form (plate, bar, tube, structural section) and size
- Heat (melt) number: The unique identifier for the batch of metal produced in one furnace heat. This is the critical traceability link.
- Chemical composition (ladle analysis and, sometimes, product analysis): Elements measured against the maximum and minimum limits of the specification. Carbon, manganese, phosphorus, sulfur, silicon, and alloying elements are reported.
- Mechanical properties: Yield strength, tensile strength, and elongation from tensile testing; Charpy impact values where required by specification; hardness if specified.
- Heat treatment condition (normalized, quenched and tempered, annealed, as-rolled)
- Certification statement that the material meets the named specification, signed by the mill's authorized representative

Receiving inspection at Ridgecrest verifies the MTR before accepting material:
- Does the MTR reference the correct specification and revision?
- Are the chemical analysis values within the specification limits?
- Are the mechanical properties within the specified range?
- Does the heat number on the MTR match the heat number stamped or stenciled on the material?

For critical pressure vessel or structural applications, the MTR must be an original or certified copy. Photocopies of photocopies may be non-traceable and are rejected. Where digital MTRs are provided, they must come from a verified supplier source.

MTRs are filed with the job record and retained for the minimum period specified by the applicable code or customer contract, typically 10 years for structural applications and the life of the pressure vessel for ASME-coded vessels.
"""

