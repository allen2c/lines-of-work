title = "Non-Conformance Reports (NCRs): Raising and Processing"

content = """
A Non-Conformance Report (NCR) is the formal record raised whenever a product, process, or material is found to not meet a specified requirement. At Ridgecrest Metalworks, raising an NCR is not optional — all identified non-conformances must be documented, regardless of the anticipated disposition. NCRs drive accountability, traceability, and systematic improvement.

Who can raise an NCR: Any employee who identifies a non-conformance — operators, inspectors, supervisors, quality engineers, and even receiving personnel. The rule is: if in doubt, raise an NCR and let the quality review process make the disposition decision.

NCR content requirements:
- Unique NCR number (sequential, from the quality system)
- Date raised and raised by
- Part number, job number, and quantity affected
- Description of the non-conformance: what is wrong, where it was found (operation, inspection stage), and how it was detected
- Reference to the requirement not met (drawing dimension, specification, standard)
- Measured value or observation vs. the required value or condition
- Disposition decision (made by quality engineer or designated responsible person)

Disposition options for non-conforming material:
- Use-as-is (concession): The non-conformance is evaluated and determined not to affect form, fit, or function. Customer approval may be required for customer-specific requirements. A concession is documented and the part is released with the NCR attached to the traveller.
- Rework: The part is corrected to bring it within specification (re-machined, re-welded, re-coated). Rework must be documented and the part re-inspected after rework.
- Repair: The part is modified to a non-standard condition that meets the functional requirement but does not strictly conform to the drawing. Requires engineering and customer approval.
- Scrap: The part is irreparably non-conforming and is disposed of. Scrapped parts are physically marked to prevent inadvertent use (stamped, painted red, or physically destroyed).
- Return to supplier: Used for incoming non-conformances where the material or component is returned for replacement.

All NCRs are closed only after the disposition has been implemented and verified. Open NCRs are reviewed at weekly quality meetings. NCR data is analyzed monthly for trends: recurring defect types, problem operations, and high-cost non-conformances drive corrective action priorities.
"""

