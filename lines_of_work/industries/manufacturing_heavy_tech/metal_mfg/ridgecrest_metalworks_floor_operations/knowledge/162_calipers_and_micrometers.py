content = """
Calipers and micrometers are the most frequently used precision measuring instruments on the Ridgecrest Metalworks production floor. Correct technique and proper care are essential to maintaining their accuracy and service life.

Digital vernier calipers (0–150 mm, 0–300 mm, 0–600 mm) measure external dimensions, internal dimensions (using the small upper jaws), depth (using the depth rod), and step heights. Correct use:
- Zero the caliper with jaws fully closed before measuring. If the display does not read 0.00, recalibrate or remove from service.
- Apply moderate, consistent closing force — never force the jaws. Use the thumb wheel where fitted.
- Ensure the jaws are perpendicular to the feature being measured; tilting introduces cosine error.
- For internal measurements with the upper jaws, close the jaws to the bore and read the caliper. Jaw width must be subtracted if the caliper does not auto-compensate.
- Do not measure hot parts; allow to cool to ambient temperature.

Outside micrometers measure external dimensions with higher accuracy (typically 0.001 mm resolution) using the calibrated screw thread of the thimble. Correct use:
- Clean the anvil and spindle faces before use with a clean cloth. Never use emery cloth or abrasives.
- Close the micrometer onto the part using the ratchet (thimble friction stop) to apply standardized measuring force. Never force the thimble without the ratchet.
- Check calibration against the supplied setting gauge (or slip gauges for the range) at the start of each shift. Record any zero error and report deviations > 0.003 mm to quality.
- For bore measurements, inside micrometers or bore gauges (set against an outside micrometer for the nominal dimension) are used.

Care of calipers and micrometers:
- Store in their protective case when not in use. Do not leave on machine tables where chips can damage them.
- After use, wipe clean with a dry or lightly oiled cloth. Do not use compressed air to clean electronic calipers (moisture ingress risk).
- Replace batteries in digital instruments before they run low; low battery causes erratic readings.
- Do not drop instruments. Impact damage can cause jaw distortion or misalignment of the screw thread in micrometers. Any instrument that has been dropped must be checked for zero error and sent for recalibration before further use.
- All instruments carry a calibration due date label. Do not use expired instruments; return them to the calibration store.
"""

