title = "Closed-Die (Impression-Die) Forging"

content = """
Closed-die forging, also known as impression-die forging, involves pressing or hammering a heated
billet into a die set in which the upper and lower dies contain machined cavities (impressions)
that together define the shape of the forged part. The metal fills the die cavity under the forming
force, producing a near-net-shape forging with good dimensional control and excellent grain flow
contouring the part geometry. Ridgecrest Metalworks uses closed-die forging for medium-to-high-volume
production of structural and mechanical components.

Die Design Elements:

Impression Cavity: The negative of the forged shape, incorporating draft angles (typically 5–7 degrees
for steel; 3–5 degrees for aluminum) to allow the forging to be ejected from the die after forming.

Flash Gutter: A groove machined around the perimeter of the die cavity. Excess metal squeezed out
of the cavity during the final blow fills this gutter as flash — a thin fin of metal around the
forging's parting line. Flash allows the dies to close completely and ensures complete fill of the
cavity. After forging, flash is removed in a trimming press using a trim die.

Multiple Impression Dies: For complex shapes, a single die block may contain multiple impressions:
edger/fuller impressions to preform the billet close to the final shape, followed by the blocker
impression (preliminary shape) and finally the finisher impression (final shape). Progressive use
of impressions in sequence reduces die wear and forming force on the final impression.

Forging Sequence:
1. Heat billet to forging temperature in the induction heater or box furnace.
2. Transfer billet to the press or hammer rapidly (target: less than 20 seconds exposure time in
   open air to minimize scale formation and temperature loss).
3. Position in preform impression; apply forming blow(s).
4. Transfer to finisher impression; apply final forming blow(s).
5. Transfer forging to trim press; trim flash in the trim die while still hot.
6. Allow to cool or transfer to heat treatment as specified on the traveler.

Quality Considerations:
Flash line: Inspect that the parting line is within the specified location; mislocated parting indicates
die shift or improper billet positioning.
Underfill: Insufficient metal volume or inadequate forming energy causes incomplete fill of recesses
and contours.
Scale pits: Die scale pressed into the surface from inadequate scale removal before forging causes
surface pitting.
Laps and cold shuts: Folds of metal on the surface caused by incorrect preform design or improper
metal flow; appear as cracks on the surface but with smooth, oxidized internal surfaces.
"""

