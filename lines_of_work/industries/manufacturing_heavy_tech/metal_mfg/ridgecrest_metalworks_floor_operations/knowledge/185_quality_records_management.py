title = "Quality Records: Retention, Access, and Control"

content = """
Quality records are the documented evidence that quality activities — inspections, tests, calibrations, reviews, and approvals — have been performed and that products meet their requirements. At Ridgecrest Metalworks, quality records are controlled under the QMS document control procedure and are managed as a critical business asset.

Types of quality records maintained:
- Inspection travellers and dimensional inspection records
- NCRs, CAPAs, and 8D reports
- Calibration certificates and calibration logs
- Material Test Reports and Certificates of Conformance
- Welder and welding procedure qualification records
- NDT reports and personnel certification records
- First Article Inspection packages
- Customer complaint records and 8D responses
- Internal and external audit reports
- Management review minutes
- Training records and competency assessments
- SPC data and control chart printouts

Retention periods are defined in the quality records retention schedule, which lists each record type and the required retention period. Retention is driven by:
- ISO 9001 requirement: Records must be retained to demonstrate product conformance for the period necessary to meet customer and regulatory requirements
- Customer contractual requirements (e.g., "retain for 15 years after last delivery")
- Regulatory requirements (e.g., pressure vessel records per ASME code; defense records per contract)
- Legal requirements (product liability — records may be needed to defend against claims many years after supply)

Minimum retention periods at Ridgecrest: General production records: 5 years. Structural or pressure vessel records: 10 years. Safety-critical aerospace/defense: life of the end product or 25 years, whichever is longer.

Record storage: Paper records are stored in fireproof, secure cabinets organized by job number and year. Electronic records are stored in the QMS database with regular backups to an offsite location. Access to quality records is controlled: production personnel can view their own job travellers; NCRs and calibration records are accessible to quality and management. External requests for records (e.g., from customers or auditors) are routed through the quality manager.

Record legibility and completeness: Records must be legible, dated, signed by the responsible person, and sufficient to recreate the quality activities performed without ambiguity. Correcting a record error requires a single line through the error, the correction written alongside, and the corrector's initials and date — never use correction fluid (Wite-Out) on quality records.
"""

