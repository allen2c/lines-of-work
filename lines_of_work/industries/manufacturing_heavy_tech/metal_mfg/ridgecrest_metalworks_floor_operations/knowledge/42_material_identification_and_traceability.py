title = "Material Identification and Heat Traceability"

content = """
Material identification and heat traceability are requirements imposed by customer specifications,
quality management system standards (AS9100, ISO 9001), and in many cases by regulatory requirements
for pressure vessels, structural members, and safety-critical aerospace components. Ridgecrest Metalworks
maintains traceability from the incoming mill certificate to the finished part certification package.

Heat Number:
A heat number (also called a melt number) uniquely identifies the batch of molten metal from which
a material was produced. All material from the same heat has nominally the same chemical composition
as verified by ladle analysis at the steel mill. Heat numbers are the primary linking identifier
between the MTR documentation and the physical material.

Methods of Identification on Material:
Heat numbers and grade markings are maintained using the following methods, chosen based on material
form and application:
- Die stamping or vibro-engraving: Permanent, used on bar ends and forged blanks
- Paint stenciling: Used on plates and larger stock; must be replenished if the surface is cleaned
  before the heat number is transferred
- Color coding: Some facilities use color stripe conventions for rapid visual grade sorting; Ridgecrest
  uses color as a supplemental identifier only, never as the sole means of identification
- Tags and travelers: Paper or durable plastic tags travel with the material and are updated at each
  operation to record the current job, operation, and operator

Traveler Document:
A production traveler (route card) accompanies every job lot from material issue through final
inspection. The traveler records the heat number, material specification, operation sequence, inspection
results at each stage, any non-conformances or deviations, and the identity of operators and inspectors
at each step. The traveler is the chain-of-custody document for material traceability.

Traceability for Split Lots:
When a material lot is split into multiple sub-lots (e.g., a plate cut into multiple blanks), each
sub-lot must receive an individual traveler and its own heat number identification before the parent
piece identification is removed. The cutting operation must be documented to show which sub-lots
came from which parent material.

Traceability Records Retention:
Complete traceability records are retained by the quality department for the minimum period required
by the customer purchase order or applicable standard — typically 10 years for aerospace components
and 5 years for general industrial work.
"""

