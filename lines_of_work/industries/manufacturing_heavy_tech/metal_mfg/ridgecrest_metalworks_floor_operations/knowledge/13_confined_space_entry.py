content = """
Ridgecrest Metalworks has identified all permit-required confined spaces within the facility in
accordance with OSHA 29 CFR 1910.146. These spaces include heat treatment pit furnaces (when in
maintenance mode), quench tank interiors, storage tanks in the chemical treatment area, certain
utility vault accesses, and large casting flask assemblies during cleaning operations.

Defining a Permit-Required Confined Space:
A confined space is permit-required if it has one or more of the following characteristics: a known
or potential atmospheric hazard (toxic, flammable, or oxygen-deficient atmosphere), a configuration
that could trap or engulf an entrant, or any other recognized serious safety or health hazard. The
facility's confined space inventory list specifies which spaces are permit-required.

Permit Requirements:
No employee may enter a permit-required confined space without a completed Entry Permit signed by an
Authorized Entrant supervisor. The permit must document: identification of the space, purpose of
entry, duration, authorized entrants, attendants, entry supervisor, known hazards, means of isolating
hazards (including LOTO status), atmospheric testing results, acceptable entry conditions, rescue
and emergency services available, communication procedures, and required PPE and equipment.

Atmospheric Testing Protocol:
Before any entry, a calibrated multi-gas monitor must test the atmosphere for oxygen content (acceptable
range: 19.5%–23.5%), flammable gas/vapor (must be below 10% of LEL), and toxic gases relevant to the
space (carbon monoxide, hydrogen sulfide, as applicable). Testing must be conducted from outside the
space before entry and continuously monitored during the entry.

Roles:
- Authorized Entrant: Trained employee who enters the space. Must know hazards, signs of exposure,
  and how to alert the attendant and self-rescue.
- Attendant: Stationed outside the space at all times during entry. Tracks entrants, monitors
  conditions, maintains communication, and initiates rescue if necessary. The attendant may never
  enter the space to perform rescue.
- Entry Supervisor: Authorizes the permit, confirms conditions are acceptable, and cancels the permit
  if conditions change.

Rescue Plan:
A rescue procedure specific to each confined space must be in place before entry begins. Non-entry
rescue using retrieval lines and harnesses is preferred. If entry rescue is required, only trained
confined space rescue personnel may respond.
"""

