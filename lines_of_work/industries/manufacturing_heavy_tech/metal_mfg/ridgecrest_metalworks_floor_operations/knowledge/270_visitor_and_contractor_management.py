title = "Visitor and Contractor Safety Induction"

content = """
All visitors and contractors who enter the Ridgecrest Metalworks production floor must complete a safety induction before proceeding beyond the reception area. This requirement protects uninformed individuals from floor hazards and protects the company from liability. It also ensures that visitors understand behavior expectations that may differ from their own workplace norms.

**Visitor categories.**
- **Guests and customers:** Typically guided tours or business meetings that include a floor walk. Short induction (10–15 minutes) with mandatory PPE.
- **Contractors performing work:** Machine repair technicians, construction crews, inspection auditors, service representatives. Require full induction appropriate to the scope and duration of their work.
- **Regulatory inspectors:** OSHA, EPA, or customer quality auditors. Accompanied by the EHS manager or plant manager at all times. Standard induction applies.

**Visitor sign-in.** All visitors sign in at the reception desk, providing name, company, purpose of visit, and duration expected. A photo ID is recorded. Visitors receive a dated visitor badge that must be worn visibly at all times on the production floor.

**Safety induction content.** The production floor safety briefing covers:
- Emergency evacuation: exits, muster points, alarm signal (one long horn blast followed by evacuation announcement).
- Required PPE in production areas: safety glasses with side shields (mandatory everywhere on the floor), steel-toed footwear, hearing protection in posted high-noise zones, high-visibility vest.
- Pedestrian path rules: stay in marked pedestrian aisles, do not cross forklift travel lanes, do not approach or touch any machine or material without escort permission.
- No photography without prior written authorization—drawings, materials, and processes may be proprietary.
- No food or drink on the production floor.
- Absolute rule: do not bypass any safety barrier, guard, or warning sign.

**Contractor-specific requirements.**
- Contractors performing physical work must provide their own PPE compliant with Ridgecrest standards or use Ridgecrest-supplied PPE.
- Hot work (welding, cutting, grinding) by contractors requires a hot work permit issued by the EHS manager.
- Contractors working on energized equipment must comply with Ridgecrest's lockout/tagout program and may be required to use Ridgecrest-supplied locks.
- Contractors generating hazardous waste on site must coordinate with the EHS manager for proper disposal—they may not dispose of waste in Ridgecrest containers.

Escort requirements: visitors must be escorted by a Ridgecrest employee at all times unless they have been formally cleared for unescorted access (typically multi-day contractors with documented induction on file).
"""

