title = "Material Test Reports and Mill Certifications"

content = """
Material Test Reports (MTRs), commonly called mill certifications or mill certs, are the primary
documentation instrument for establishing that a raw material meets its specified composition and
mechanical property requirements. At Ridgecrest Metalworks, MTRs are required for all structural,
pressure-retaining, and aerospace-grade materials and are retained as quality records for the life
of the part.

What an MTR Contains:
A compliant MTR will typically include:
- Material manufacturer (mill) name and location
- Material specification (e.g., ASTM A108, AMS 6414)
- Alloy grade designation (e.g., 4340, 316L)
- Heat number (a unique identifier for the melt/heat from which the material was produced)
- Chemical composition test results (ladle analysis and/or product analysis, per specification)
- Mechanical test results: typically tensile strength, yield strength, elongation, and reduction of
  area from a test specimen cut from the heat
- Additional test results as required: Charpy impact values, hardness, grain size, cleanliness rating
- Mill's authorized signature or stamp indicating the document is a true certification

Reviewing an MTR:
The receiving quality technician compares each reported value on the MTR against the limits specified
in the applicable material standard. If any value falls outside the specification range, the material
is rejected at receiving regardless of the mill's apparent compliance stamp. Common errors found in
MTRs include: transposed values, wrong heat number reference, missing required tests, and results
reported to an incorrect version of the specification.

Heat Number Linking:
The heat number on the MTR is stamped, etched, or paint-marked on the physical material. This link
between the paper record and the physical material must be maintained throughout the production process.
When material is cut or processed, the heat number must be transferred to each piece before the
original identification is removed.

MTR Retention:
MTRs are scanned and stored in the electronic document management system with the work order number
and job record. Physical copies are filed by job number. Aerospace jobs require MTR retention for a
minimum of 10 years; general industrial jobs are retained for 5 years unless otherwise specified by
the customer purchase order.

Customer-Specific Requirements:
Some customers specify additional certification requirements beyond the base material standard: source
country restrictions, specific melt practices (vacuum arc remelt, electroslag remelt), specific testing
labs, or dual certifications to both ASTM and AMS. These requirements are captured in the customer
purchase order and must be confirmed at the time of material procurement.
"""

