title = "Spent Coolant and Cutting Fluid Disposal"

content = """
Metalworking coolants and cutting fluids extend tool life, improve part quality, and control machining temperatures. But over time, coolants degrade, become contaminated with tramp oil, metal fines, and bacteria, and must be removed from service. Spent coolant is one of the most significant regulated waste streams at Ridgecrest Metalworks.

**Types of coolant used.** Ridgecrest uses three primary coolant categories:
- **Water-soluble emulsifiable oil coolants (semi-synthetic):** Used in most CNC machining centers. Mixed at 5–12% concentration with water.
- **Straight cutting oils (neat oil):** Used in gear cutting, threading, and broaching. Petroleum-based, not mixed with water.
- **Synthetic coolants:** Fully water-based solutions used for grinding operations.

**Coolant monitoring.** Active coolant in sumps is monitored weekly:
- Concentration (refractometer reading) maintained per the manufacturer's recommended range.
- pH maintained between 8.5 and 9.5 for most emulsion coolants (below 8.5 promotes bacterial growth; above 10.0 causes operator skin irritation).
- Tramp oil level (free-floating oil from machine ways and spindle oil leaks): remove excess with a skimmer or coalescer when tramp exceeds 2% of sump volume.
- Bacterial count: tested monthly using dip slides; action required if bacteria exceed threshold.

**Spent coolant removal.** When a coolant sump must be replaced (due to excessive contamination, pH out of range, or system cleaning), the spent coolant is pumped into a dedicated labeled waste coolant container. The waste coolant container must:
- Be closed when not actively being filled.
- Be labeled "Waste Coolant" with the date accumulation began.
- Have secondary containment below it (drip pan or bermed floor).

**Waste classification.** Spent water-soluble coolant is typically a non-hazardous waste (unless the spent fluid has characteristic properties—pH or metals). It may be eligible for off-site recycling (re-refining or fuel blending) or treated on-site through ultrafiltration before sewer discharge (subject to pretreatment permit limits). Neat cutting oil waste is more likely to require off-site disposal as a regulated waste oil.

**Never pour spent coolant down floor drains.** Floor drains at Ridgecrest connect to the stormwater or industrial pretreatment system. Unauthorized discharges violate the facility's water discharge permit.

Coolant waste manifests are retained with other environmental records.
"""

