content = """
The Corrective and Preventive Action (CAPA) process is the systematic approach used at Ridgecrest Metalworks to eliminate the root cause of existing non-conformances (corrective action) and to proactively prevent potential problems from occurring (preventive action). CAPA is a core ISO 9001 requirement and a critical driver of quality improvement.

Corrective Action (CA) is triggered by:
- Recurring NCRs for the same defect or operation
- Customer complaints
- Internal or external audit findings
- Significant quality escapes (non-conforming product reaching the customer)
- Major or critical NCRs

The CAPA process follows a structured sequence:

1. Problem statement: Define the non-conformance clearly and specifically — what failed, where, when, and to what extent. Avoid vague statements like "machining error." Use data: "Bore diameter out of tolerance on Job 4823, dimension 45.000 +0.025/−0.000, measured at 44.965 on 3 of 10 parts."

2. Containment: Immediately protect the customer from receiving non-conforming product. Quarantine suspect stock, sort or 100 % inspect affected batches, issue stop-ship instructions, and notify the customer if product may have escaped.

3. Root cause analysis (RCA): Determine the fundamental reason why the non-conformance occurred. Tools used at Ridgecrest include:
   - 5-Why analysis: Ask "why?" iteratively until the root cause is reached
   - Fishbone (Ishikawa) diagram: Organize potential causes by category (machine, method, material, measurement, man, environment)
   - Fault tree analysis: Logical deductive analysis for complex failures

4. Corrective action implementation: Develop and implement actions that directly address the root cause. Actions should be specific, assigned to a named owner, and have a defined completion date.

5. Effectiveness verification: After implementation, monitor the process or product to confirm the corrective action has eliminated the root cause and that the non-conformance has not recurred. Evidence of effectiveness is documented.

6. Closure: The CAPA record is formally closed by the quality engineer with a summary of findings, actions, and verification evidence.

Preventive actions arise from risk assessments, process FMEAs, and trend analysis. They address potential failure modes before they manifest as actual non-conformances. CAPA records are reviewed at management review meetings to assess system effectiveness.
"""

