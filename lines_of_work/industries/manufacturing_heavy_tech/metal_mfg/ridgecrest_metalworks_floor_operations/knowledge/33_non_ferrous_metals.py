content = """
Non-ferrous metals processed at Ridgecrest Metalworks include aluminum alloys, copper alloys (brass
and bronze), and in limited quantities titanium and nickel-based superalloys. These materials are
selected for specific property advantages over steel that justify their typically higher cost.

Aluminum Alloys:
Aluminum is the most commonly processed non-ferrous metal at the facility. Its low density (approximately
one-third that of steel), excellent corrosion resistance, and good machinability make it attractive
for aerospace, automotive, and consumer product applications. Aluminum alloys are identified by a
four-digit designation system; common series include 2xxx (copper-alloyed, aerospace structural), 6xxx
(magnesium-silicon, general purpose and extrusions), and 7xxx (zinc-alloyed, highest-strength aerospace
grades). Aluminum machines at high speed with appropriate cutting parameters and requires separate
tooling from steel to prevent chip contamination.

Copper and Copper Alloys:
Copper is valued for its exceptional electrical and thermal conductivity. In its commercially pure form
(C10100 to C12200 series), it is processed for electrical components and heat transfer applications.
Alloys of copper include brass (copper-zinc) and bronze (copper-tin, or copper with other alloying
additions). Brass machines freely and is used for valve bodies, fittings, and decorative hardware.
Phosphor bronze offers higher strength and good wear resistance for bushings and bearings.

Titanium Alloys:
Titanium combines high strength with low density (about 60% of steel's density) and outstanding
corrosion resistance, including resistance to seawater and chlorine environments. Ti-6Al-4V (Grade 5)
is the most commonly processed titanium alloy, used in aerospace frames, fasteners, and medical
implants. Titanium is challenging to machine due to its low thermal conductivity, which concentrates
heat at the cutting edge, and its tendency to work-harden and chemically react with tooling at high
temperatures. Generous coolant flow and low cutting speeds are required.

Nickel Superalloys:
Inconel 718 and similar nickel-based superalloys retain strength at temperatures up to 700°C and above,
making them essential for gas turbine and energy sector components. These materials are extremely
difficult to machine due to high strength, work-hardening tendency, and abrasiveness. Specialized
tooling, rigid setups, and frequent tool monitoring are mandatory.

Non-ferrous metals must be stored separately from ferrous materials to prevent galvanic corrosion
and cross-contamination of chips during machining.
"""

