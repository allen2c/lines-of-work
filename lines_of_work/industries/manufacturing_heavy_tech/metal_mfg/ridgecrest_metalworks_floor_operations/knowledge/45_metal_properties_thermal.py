title = "Thermal Properties of Metals in Processing"

content = """
Thermal properties of metals govern their behavior during casting, forging, heat treatment, welding,
and machining operations. Understanding these properties enables production personnel to make correct
decisions about processing temperatures, heating and cooling rates, and equipment selection.

Melting Point:
The temperature at which a solid metal transitions to a liquid state. Melting point is fundamental
to casting and welding operations. Key reference values:
- Aluminum: ~660°C (1220°F)
- Copper: ~1085°C (1985°F)
- Cast iron: ~1150–1300°C (2100–2370°F) depending on carbon content
- Carbon steel: ~1425–1540°C (2600–2800°F) depending on carbon content
- Nickel superalloys (Inconel 718): ~1260–1336°C (2300–2437°F)
Significant superheat above the liquidus temperature is applied during melting to ensure complete
fluidity and adequate mold filling time before solidification begins.

Thermal Conductivity:
Measures how readily heat flows through a material. High thermal conductivity means heat generated
at the cutting zone or weld pool dissipates quickly into the workpiece and tooling. Copper (398 W/m·K)
and aluminum (205 W/m·K) are highly conductive; titanium (21 W/m·K) and stainless steel (15 W/m·K)
are poor conductors, causing heat concentration at the machining interface and requiring lower cutting
speeds and generous coolant.

Thermal Expansion Coefficient:
Metals expand when heated and contract when cooled. Differential thermal expansion creates internal
stresses during heating and cooling, which is the basis of thermal stress cracking in castings and
the distortion seen in weldments. The coefficient of thermal expansion (CTE) must be considered when
designing close-tolerance assemblies of dissimilar metals that will be used at elevated temperatures.

Specific Heat Capacity:
The energy required to raise a unit mass of material by one degree. High specific heat means more
furnace energy is required to bring the material to processing temperature. Relevant for estimating
heat treatment cycle times and furnace loading limits.

Transformation Temperatures in Steel:
Carbon and alloy steels undergo phase transformations at specific temperature ranges called critical
temperatures (A1, A3, Acm). These temperatures define the boundaries for heat treatment operations:
austenitizing above A3 for full hardening, annealing and normalizing temperature ranges, and the
martensite start (Ms) and finish (Mf) temperatures that govern quench response. These transformation
temperatures shift with alloy content and are charted in TTT (Time-Temperature-Transformation) diagrams
available in the heat treatment department's process reference library.
"""

