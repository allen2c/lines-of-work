content = """
Materials handling encompasses every movement of raw materials, work-in-process (WIP), and finished goods throughout the Ridgecrest Metalworks facility. Efficient materials handling reduces production costs, minimizes damage, and keeps the production schedule on track. Poor materials handling, by contrast, leads to delays, safety incidents, and costly rework.

The core principle is to move material the least number of times over the shortest possible distance while maintaining safety and product integrity. Every unnecessary move is waste—it consumes labor, time, and equipment capacity without adding value to the product.

Equipment categories used on the floor include:

- **Forklifts (counterbalance and reach trucks):** Handle pallets, coils, and heavy steel stock. Rated by load capacity at a specified load center distance. Never exceed the nameplate capacity.
- **Overhead bridge cranes:** Used for lifting heavy plate, bundles, and large fabrications. Typically 5- to 20-ton capacity at Ridgecrest.
- **Pallet jacks (manual and electric ride-on):** For lighter pallet loads within aisles and staging areas.
- **Conveyors and roller tables:** Move parts between machining centers and inspection stations with minimal manual handling.
- **Steel-band and chain slings:** Rigging components used with cranes for irregular loads.

Operators must match equipment to load characteristics. Coiled steel requires saddle cradles or coil mandrels; flat plate requires spreader bars to prevent bending. Bundles of tubing or bar stock must be secured with proper banding before any lift.

All aisle markings on the floor define dedicated travel paths for powered equipment. Pedestrians and forklift routes must remain segregated. Yellow floor lines and bollards enforce this separation.

Pre-use equipment inspections are mandatory before every shift. Any defect—cracked forks, worn tires, inoperative lights, leaking hydraulics—must be tagged out and reported to the maintenance desk before the unit is moved to another task.

Documentation requirements: every material movement that changes inventory location or status must be logged in the ERP system within the same shift. Unofficial "staging" of materials in unmarked areas is prohibited because it breaks inventory visibility and creates trip hazards.
"""

