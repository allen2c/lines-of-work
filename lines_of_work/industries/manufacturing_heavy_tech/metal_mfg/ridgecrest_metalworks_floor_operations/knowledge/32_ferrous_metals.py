content = """
Ferrous metals form the backbone of production at Ridgecrest Metalworks. Their range of achievable
mechanical properties, combined with well-established processing routes and relative cost effectiveness,
makes them the dominant material class across the facility's customer base.

Carbon Steel:
Plain carbon steel is classified by carbon content: low carbon (0.05–0.30% C), medium carbon (0.30–0.60%
C), and high carbon (0.60–1.00% C). Low carbon steel (mild steel) is highly formable and weldable but
has limited strength. Medium carbon steel offers a good balance of strength and toughness after heat
treatment, making it suitable for shafts, gears, and structural components. High carbon steel is harder
and more wear-resistant but less ductile and more difficult to weld.

Alloy Steel:
Alloying elements such as chromium, molybdenum, nickel, and vanadium are added to achieve specific
property improvements. AISI 4140 (chromium-molybdenum) is one of the most commonly processed alloy
steels at Ridgecrest, used extensively for high-strength shafts and forged components due to its excellent
combination of hardenability, strength, and fatigue resistance.

Tool Steel:
Tool steels are highly alloyed, high-carbon steels optimized for hardness, abrasion resistance, and
resistance to deformation at elevated temperatures. They are used at Ridgecrest for die-making and
tooling fabrication. Common grades include H13 (hot-work tool steel for forging dies) and D2 (cold-work
tool steel for blanking and trimming dies).

Cast Iron:
Cast iron has a carbon content greater than 2%, making it unsuitable for hot working but ideal for
casting. The main variants processed at Ridgecrest are:
- Gray iron: Graphite flakes within the matrix; excellent machinability and vibration damping; used
  for machine bases and housings.
- Ductile iron (nodular iron): Magnesium treatment causes graphite to form spheroids; significantly
  better strength and ductility than gray iron; used for automotive and pump components.
- White iron: Very hard and brittle; graphite is retained as iron carbide (cementite); used in
  wear-resistant applications.

Processing Considerations:
Ferrous metals are generally compatible with standard water-soluble coolants in machining. However,
high-carbon and tool steels require careful control of cutting parameters to prevent work hardening,
and cast irons produce abrasive graphite-laden chips that accelerate tool wear.
"""

