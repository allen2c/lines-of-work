title = "Hardness Testing: Rockwell, Brinell, and Vickers"

content = """
Hardness testing is one of the most frequently performed quality verification activities at Ridgecrest
Metalworks. It is fast, relatively inexpensive, and requires only minimal specimen preparation. Hardness
is used to verify heat treatment results, confirm incoming material condition, monitor surface hardening
depth, and screen for potential material mix-ups.

Rockwell Hardness (HR):
Rockwell testing applies a minor preload followed by a major load and measures the depth of permanent
indentation. The result is read directly on the tester dial or display — no measurement of indentation
size is needed. Multiple scales exist for different material hardness ranges:
- HRC (Rockwell C): Brale conical diamond indenter, 150 kg major load. Used for hardened steels and
  high-strength alloys. Applicable range: HRC 20–70.
- HRB (Rockwell B): 1/16-inch ball indenter, 100 kg major load. Used for soft steels, annealed steels,
  aluminum, and copper alloys. Applicable range: HRB 0–100.
- HRA: Diamond indenter, 60 kg load. Used for cemented carbides and very thin hardened materials.

Brinell Hardness (HBW):
Brinell testing presses a 10 mm tungsten carbide ball into the surface under a load of 500–3000 kg
(dependent on the material hardness range) and measures the diameter of the resulting indentation
optically. The Brinell Hardness Number (HBW) is calculated from the indentation diameter. Brinell is
particularly useful for large castings and forgings where the coarse microstructure requires a large
indentation to obtain a representative average. The indentation is large enough to be influenced by
inclusions and microstructural heterogeneity, which can be informative for casting quality assessment.

Vickers Hardness (HV):
The Vickers test uses a square pyramid diamond indenter at loads ranging from grams to 120 kg. The
diagonal length of the square impression is measured. Vickers is highly versatile, applicable from
the softest to hardest metals, and can be used for very thin specimens or layers (microhardness testing).
Case hardening depth profiles (hardness vs. depth below surface) are routinely measured using Vickers
at loads of HV 0.3 to HV 1 in the metallurgical lab.

Cross-Referencing Hardness to Tensile Strength:
ASTM E140 provides standard hardness conversion tables for common steel grades. These conversions
are approximate and apply most accurately to steels with martensitic microstructures; they should
not be used as substitutes for tensile testing on unfamiliar alloys.

All hardness testers at Ridgecrest are calibrated daily using certified reference blocks per ASTM E18
(Rockwell) and ASTM E10 (Brinell) before being used for production testing.
"""

