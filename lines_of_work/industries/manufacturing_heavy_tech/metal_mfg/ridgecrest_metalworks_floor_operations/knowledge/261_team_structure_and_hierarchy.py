content = """
Understanding the organizational structure of the Ridgecrest Metalworks production floor is essential for every employee. Clear reporting lines determine who to go to with problems, who has authority to make decisions, and how information flows up and down the organization.

**Facility leadership structure.**

- **Plant Manager:** Overall facility accountability for safety, quality, delivery, cost, and culture. Final authority on major operational decisions.
- **Production Manager:** Directs all manufacturing operations across shifts. Owns the production schedule adherence and OEE performance metrics. Reports to the Plant Manager.
- **Quality Manager:** Leads the QA team, owns the QMS, and has authority to hold or release any shipment. Has an independent reporting line to the Plant Manager on quality issues.
- **Maintenance Manager:** Directs preventive and corrective maintenance. Owns equipment uptime targets.
- **EHS Manager:** Owns safety and environmental programs. Has direct access to the Plant Manager on safety and compliance matters.

**Production floor hierarchy.**

- **Cell Supervisor (Shift Supervisor):** Responsible for a defined production cell or department during their shift. Owns safety, quality, output, and personnel management for their cell on their shift. This is the primary management contact for all floor operators.
- **Lead Operator (Senior Operator / Team Lead):** Experienced operator designated as the first point of contact for technical and quality questions within the cell. Not a management role but a technical authority. Covers for the supervisor during brief absences.
- **Production Operator:** Runs machines, completes documentation, reports issues to the lead or supervisor.
- **Materials Handler / Warehouse Associate:** Manages material flow to and from the cell. Reports to the warehouse supervisor but coordinates closely with cell supervisors.

**Escalation principle.** Every issue should be raised at the lowest level that can resolve it. Minor technical questions go to the lead operator. Equipment issues go to the supervisor who contacts maintenance. Quality escapes go to the supervisor and QA lead. Safety concerns can go directly to the EHS manager bypassing the production chain—this is a protected right.

**Cross-shift continuity.** The outgoing supervisor is responsible for briefing the incoming supervisor at shift changeover. The handover ensures that responsibilities transfer clearly and no issues fall through the gap between shifts.
"""

