content = """
Hot work at Ridgecrest Metalworks refers to any operation that produces a flame, arc, or spark capable
of igniting combustible materials. This includes welding, cutting (oxy-fuel and plasma), grinding,
brazing, soldering, and heat gun applications performed outside of designated hot work stations.

Hot Work Permit System:
A hot work permit is required for any hot work conducted outside of permanently designated welding
and cutting booths. The permit system ensures that fire hazards are identified and controlled before
work begins. The shift supervisor issues hot work permits after completing a pre-work fire hazard
assessment. Permits are valid for one shift only and must be renewed for each new shift.

Pre-Hot Work Checklist (required for permit issuance):
- All combustibles within a 35-foot radius are removed or covered with fire-resistant blankets.
- Floor drains, sewer openings, and conduit paths in the work area are sealed.
- Flammable gas or liquid containers have been removed or isolated.
- Sprinkler heads near the work area are protected from weld spatter with wet cloth covers.
- A fire watch is assigned and positioned with an appropriate fire extinguisher.
- The fire watch personnel have been briefed on their responsibilities.

Fire Watch Requirements:
A designated fire watch must be present during all permitted hot work and for a minimum of 30 minutes
after work concludes. The fire watch must maintain unobstructed visual contact with the work area,
must not perform any other tasks simultaneously, and must have immediate access to an appropriate
extinguisher. The fire watch monitors for smoldering materials in hidden areas including wall
cavities, insulation, and under floor gratings.

Welding and Cutting in Confined or Restricted Spaces:
Hot work within or directly adjacent to tanks, vessels, or partially enclosed structures requires an
elevated-risk hot work permit approved by the Safety Officer in addition to a confined space entry
permit where applicable. Atmospheric testing for flammable gases is mandatory before and during such work.

Cylinder Storage and Handling:
Oxy-fuel cylinders must be stored upright, chained to a fixed support, valve caps in place when not
in use, and segregated by gas type with a minimum 20-foot separation or a 5-foot fire-rated barrier
between oxygen and fuel gas cylinders. Cylinders must never be stored in unventilated areas or near
heat sources. Leaking cylinders must be moved to an outdoor ventilated area immediately.
"""

