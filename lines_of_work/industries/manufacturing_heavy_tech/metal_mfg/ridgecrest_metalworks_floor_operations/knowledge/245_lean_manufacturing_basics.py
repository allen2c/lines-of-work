title = "Lean Manufacturing Basics: 5S, Waste, and Value Flow"

content = """
Lean manufacturing is the systematic elimination of waste from production processes to maximize value delivered to the customer with minimum resources. At Ridgecrest Metalworks, lean principles are embedded in daily operations through 5S, waste identification, and value stream thinking. Lean is not a project—it is a way of working every day.

**The 8 wastes (DOWNTIME).**
1. **Defects:** Parts that fail inspection and require rework or scrap. The most visible waste—directly destroys material and labor cost.
2. **Overproduction:** Making more than the customer ordered or making parts earlier than needed. Ties up material, space, and labor.
3. **Waiting:** Operators, machines, or materials idle while waiting for the next step. Every second of waiting is lost capacity.
4. **Non-utilized talent:** Operator knowledge, skills, and ideas that go unused. The suggestion system addresses this.
5. **Transportation:** Unnecessary movement of material between locations. Short, direct flow paths reduce this.
6. **Inventory:** Raw material, WIP, and finished goods sitting idle beyond immediate need. Inventory hides problems.
7. **Motion:** Unnecessary operator movement to find tools, materials, or information. 5S directly attacks this waste.
8. **Extra processing:** Performing operations beyond what the customer requires or what the part specification demands.

**5S methodology.** 5S is a workplace organization method applied to every work area:
- **Sort (Seiri):** Remove everything from the area that is not needed for current production. Tag-and-remove campaign.
- **Set in Order (Seiton):** Assign a specific location for every item that remains. Shadow boards for tools; marked floor locations for equipment.
- **Shine (Seiso):** Clean the workspace and equipment to baseline condition. Cleaning reveals abnormalities.
- **Standardize (Seiketsu):** Create standards (visual aids, cleaning schedules, audits) to maintain the first three S's.
- **Sustain (Shitsuke):** Build habits and audits that make 5S a permanent discipline, not a one-time event.

**Value stream thinking.** A value stream map charts every step in the production flow from raw material to customer delivery, showing value-added vs. non-value-added time. Most facilities find that 90–95% of total lead time is non-value-added. Identifying and eliminating the largest non-value-added steps (batch queues, transport delays, inspection waits) compresses lead time dramatically.

Ridgecrest employees at all levels are trained in lean basics and are expected to identify and report waste in their daily work.
"""

