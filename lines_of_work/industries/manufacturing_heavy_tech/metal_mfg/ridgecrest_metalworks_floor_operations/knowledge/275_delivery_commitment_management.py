title = "Delivery Commitment Management and On-Time Performance"

content = """
Delivery commitments are promises made to customers about when their orders will be received. At Ridgecrest Metalworks, on-time delivery (OTD) is one of the top three customer satisfaction drivers alongside quality and responsiveness. Missing a committed delivery date without advance notice is among the most damaging things a supplier can do to a customer relationship.

**How delivery commitments are made.** Customer requested delivery dates are reviewed by the sales and production planning team against available capacity and material availability before a commitment is confirmed. The planner performs a rough-cut feasibility check:
- Is the required raw material in stock or arriving in time?
- Does the available machine capacity support the requested date?
- Are there competing rush orders or constraints that would block the job?

If the requested date is not achievable, the sales team negotiates an alternative with the customer before accepting the order. An accepted order date is a commitment.

**Managing commitments through production.** Once committed, the order's due date is tracked daily in the production schedule. The planner monitors work order progress against the schedule at each operation. Key monitoring triggers:
- Work order falls more than 10% behind schedule at any operation: the planner flags it and reviews recovery options.
- Machine breakdown affecting a critical-path work order: immediate escalation to the production manager for recovery plan.
- Material shortage: planner contacts purchasing for expedited delivery or identifies a substitute material.

**Proactive communication.** If at any point during production it becomes clear that the committed delivery date cannot be met, the customer must be notified as early as possible with:
- A revised realistic delivery date (not an optimistic estimate).
- A brief explanation of the cause.
- What steps Ridgecrest is taking to minimize the impact.

Customers who receive late notice are much more forgiving when notified 2 weeks early rather than 1 day before the expected delivery. Late surprises damage trust; early communication with honesty preserves it.

**On-time delivery metric.** OTD is measured as: (Shipments delivered on or before the customer requested date) / (Total shipments) × 100%. The Ridgecrest OTD target is 95%. Monthly OTD performance by customer is reviewed in the operations and sales alignment meeting, and any customer below 90% receives a formal recovery plan.

**Root cause analysis for late deliveries.** Every late delivery receives a coded root cause classification: scheduling error, material shortage, machine breakdown, quality hold, engineering change, or customer change. Tracking root causes by category allows systematic elimination of the leading causes.
"""

