content = """
Internal environmental audits verify that Ridgecrest Metalworks' environmental management system is functioning as designed, that operational controls are being followed, and that the facility remains in compliance with its permits and regulatory obligations. ISO 14001 requires a program of internal environmental audits conducted at planned intervals, with results reported to management.

**Audit program structure.** The EHS manager schedules internal environmental audits on an annual calendar. Higher-risk areas (hazardous waste accumulation area, wastewater treatment, chemical storage, high-emissions operations) are audited more frequently—at least twice per year. Lower-risk areas (general manufacturing, finished goods) are audited annually.

**Audit scope.** Environmental audits at Ridgecrest cover:
- Regulatory compliance: Are all activities within permit limits? Are records being maintained properly?
- EMS conformance: Are documented procedures being followed? Are operational controls in place?
- Environmental performance: Is the facility meeting its stated environmental objectives and targets?
- Emergency preparedness: Are spill kits stocked and accessible? Are personnel aware of spill response procedures?

**Audit process.**

1. **Planning.** The auditor reviews the applicable regulatory requirements, permits, and previous audit findings before conducting fieldwork. An audit checklist is prepared.

2. **Opening meeting.** Brief meeting with the area supervisor to explain scope, schedule, and protocol. Audits are based on evidence, not impressions.

3. **Field audit.** The auditor walks the area, observes operations, reviews records, inspects containers and storage areas, and interviews operators. Operators should answer questions honestly and directly. "I don't know" is an acceptable answer if followed by identifying who does know.

4. **Evidence collection.** Findings (both conformances and nonconformances) are documented with specific evidence: photo, record reference, or observation. Vague findings ("waste area not properly organized") are not acceptable—specific evidence is required.

5. **Closing meeting.** Findings are presented to the area supervisor and the EHS manager.

**Nonconformances.** An audit nonconformance is documented on an environmental corrective action request (CAR). The area owner responds within 10 business days with a root cause analysis and corrective action plan. The EHS manager verifies effectiveness at the next scheduled audit.

**Preparation tips.** Operators should know: where waste containers are located and how they are labeled; where the SDS binder or app is accessed; what to do in a spill; and what environmental aspects apply to their work area.
"""

