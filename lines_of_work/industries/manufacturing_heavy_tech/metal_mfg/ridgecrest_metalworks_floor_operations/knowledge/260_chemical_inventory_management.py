title = "Chemical Inventory Management and SDS Filing"

content = """
Ridgecrest Metalworks maintains a chemical inventory of all hazardous chemicals present in the facility. This inventory supports OSHA Hazard Communication (HazCom) compliance, EPA EPCRA Tier II reporting, and the facility's emergency response planning. Every chemical used in production, maintenance, or facility operations must appear in the chemical inventory system.

**Chemical approval process.** No new chemical may be brought into the facility without prior approval by the EHS manager. The approval process includes:
1. Submission of the proposed chemical's Safety Data Sheet (SDS) to the EHS manager.
2. EHS review for hazard classification, regulatory status (RCRA hazardous waste implications, air emission impacts, water discharge implications), and required controls.
3. Written approval or rejection with rationale.

This requirement applies to all chemicals—including cleaning supplies, lubricants, adhesives, and marking materials, not just process chemicals.

**Safety Data Sheets (SDS).** Every chemical present in the facility must have a current SDS (GHS format, 16-section) accessible at all times to any employee who works with or near that chemical. OSHA 29 CFR 1910.1200 requires that SDSs be available without barrier to employees in their work area.

At Ridgecrest, SDSs are maintained in:
- The online SDS management system (accessible from any facility computer terminal and from the EHS mobile app).
- Physical binders at the main safety station and at each department's safety station.

**SDS contents to know.** Each SDS section relevant to floor use:
- Section 2: Hazard identification (signal word, hazard statements, pictograms).
- Section 4: First aid measures.
- Section 6: Accidental release / spill measures.
- Section 7: Handling and storage requirements.
- Section 8: PPE and exposure limits.
- Section 13: Disposal considerations.

**Chemical inventory register.** The inventory register lists each chemical by: product name, manufacturer, chemical family, SDS date, location(s) used, maximum quantity on hand, and regulatory status flags (EPCRA reportable, CAA HAP, RCRA hazardous waste precursor). The register is reviewed and updated quarterly.

**EPCRA Tier II reporting.** If any EPCRA Section 302 Extremely Hazardous Substance (EHS) or any OSHA hazardous chemical is present above thresholds, an annual Tier II report must be submitted to the State Emergency Response Commission, Local Emergency Planning Committee, and local fire department by March 1st. The EHS manager prepares this report using the chemical inventory data.

Operators must report any chemical brought to the floor that is not on the approved chemical list to their supervisor immediately.
"""

