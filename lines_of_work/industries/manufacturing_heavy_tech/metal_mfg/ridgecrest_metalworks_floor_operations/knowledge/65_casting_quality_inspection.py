title = "Casting Quality Inspection Methods"

content = """
Casting quality inspection at Ridgecrest Metalworks encompasses visual, dimensional, and non-destructive
evaluation methods applied at multiple stages from green casting through final acceptance. The inspection
plan for each casting is specified in the part's Quality Plan, linked to the customer's drawing notes
and applicable casting quality standards (ASTM A802 for steel, ASTM E446 for radiographic acceptance,
MSS SP-55 for surface quality, etc.).

Visual Inspection:
All castings receive a 100% visual inspection after shakeout cleaning and fettling. Visual inspection
detects surface porosity, cold shuts, misruns, surface cracks, hot tears, sand inclusions, and obvious
dimensional anomalies. Inspectors must have access to adequate lighting (minimum 50 foot-candles) and
reference standards for acceptable vs. rejectable conditions. Visual acceptance criteria for each
casting family are defined in the relevant reference radiograph or comparative photograph standard.

Dimensional Inspection:
Castings are dimensionally inspected against the engineering drawing using calibrated measuring tools:
vernier calipers, height gauges, templates, and coordinate measuring machines (CMM). First-article
castings receive 100% dimensional inspection of all controlled features. Production lots are inspected
per the sampling plan defined in the Quality Plan.

Radiographic Testing (RT):
X-ray or gamma-ray radiography reveals internal discontinuities including shrinkage porosity, gas
porosity, hot tears, and cold shuts within casting sections. The radiographic image (film or digital)
is evaluated by a certified Level II or Level III RT technician against the applicable reference
radiograph standard. RT is mandatory for pressure-retaining castings and aerospace structural castings.

Ultrasonic Testing (UT):
Pulse-echo ultrasonic testing detects internal discontinuities based on reflection of sound waves
at material boundaries. UT can be more sensitive than RT for planar defects (cracks and laminations)
and is used where wall thickness allows adequate sound transmission.

Magnetic Particle Testing (MT) and Liquid Penetrant Testing (PT):
MT is used on ferrous casting surfaces to detect surface and near-surface cracks. PT is used on all
alloy types for surface crack detection where MT is not applicable (non-ferrous or austenitic alloys).
Both methods require qualified technicians and defined acceptance criteria from the applicable standard.

Pressure Testing (Hydrostatic/Pneumatic):
Castings for pressure-retaining service are hydrostatically tested at a specified multiple of the design
pressure to verify leak integrity before delivery. Test pressure, hold time, and acceptance criteria
are per the applicable pressure vessel or piping code (ASME B16.34, ASTM A370, or customer specification).
"""

