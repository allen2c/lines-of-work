title = "Customer Order Review Before Production Start"

content = """
Before production begins on any customer order, a formal order review must be completed to confirm that Ridgecrest Metalworks has all the information, materials, tooling, and capacity needed to produce the parts correctly and on time. This review is part of ISO 9001 clause 8.2—requirements for products and services—and is the last opportunity to catch ambiguities or issues before they become expensive floor problems.

**Who performs the order review.** For standard, repeat orders with unchanged specifications: the production planner verifies the work order against the current approved drawing and releases to the floor.

For new orders, modified orders, or orders with special customer requirements: a formal review meeting is held with participation from sales, production planning, engineering, and QA before the first work order is released.

**Order review checklist.**

1. **Drawing and specification completeness.** Is the latest approved customer drawing or 3D model available in the document control system? Is the revision level on the drawing consistent with the revision level on the customer PO? Are all referenced specifications (material grade, surface finish standard, weld standard, plating spec) available and understood?

2. **Material availability.** Is the required raw material grade, form, and quantity in stock or on order with a confirmed delivery date that allows production to start as scheduled?

3. **Process capability confirmation.** Can Ridgecrest produce the specified tolerances and surface finishes with current equipment and processes? New tolerance requirements tighter than current process capability require engineering review before commitment.

4. **Special requirements.** Does the order require: first article inspection (FAI), statistical process control (SPC) reporting, specific test certificates, witness inspection by the customer, specific packaging and labeling, material trace code marking? All special requirements must be documented on the work order traveler before release.

5. **Customer-supplied material.** If the customer is supplying material for processing, confirm quantity received, condition, and that it matches the drawing material specification.

6. **Delivery terms.** Confirm: required delivery date, incoterms (FOB, DDP, etc.), carrier preferences, and whether a partial delivery is acceptable.

Any open items from the order review are resolved before the work order is released. Open items discovered on the floor after production has started are far more expensive to address than those caught before the first operation.
"""

