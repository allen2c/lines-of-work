title = "Shift Changeover: Face-to-Face Handover Requirements"

content = """
The shift changeover is one of the highest-risk periods in any manufacturing operation. Incomplete information transfer between the outgoing and incoming shifts is a leading cause of accidents, quality errors, and production disruptions. At Ridgecrest Metalworks, a structured, face-to-face handover between shift supervisors is mandatory for every shift transition.

**Why face-to-face is required.** Written shift reports are essential supplements, but they cannot replace direct verbal communication. Face-to-face handover allows the incoming supervisor to ask questions, confirm understanding, and probe for issues that the outgoing supervisor may have omitted. Information that "seemed too minor to write down" is often exactly what the incoming shift needed to know.

**Handover location.** The primary shift handover takes place at the production floor's central communication board. Both supervisors stand in front of the board during the handover, which allows visual reference to the schedule, machine status, and open action items.

**Handover content checklist.**

1. **Safety.** Any incidents, near-misses, or ongoing hazards from the outgoing shift. Status of any lockout/tagout points currently active.

2. **Production status.** Current status of each active work order: operation number, quantity completed, quantity remaining, projected completion vs. due date.

3. **Machine status.** Any machine that is down, in a degraded state, or has a maintenance work order open. Known upcoming PM activities that will affect the incoming shift's schedule.

4. **Quality issues.** Any parts in a hold state, active NCRs awaiting disposition, first-piece inspections pending before a run can continue, or any quality concern observed but not yet formally documented.

5. **Material status.** Any materials expected to arrive during the incoming shift, any materials that are short or on allocation, and any kits partially staged for upcoming work orders.

6. **Personnel.** Any operator absences, cross-training situations (operators working outside their primary cell), or operator capability limitations relevant to the night's assignments.

7. **Open action items.** Every item on the shift log that was not resolved by the outgoing shift, with the current status and recommended next step.

**Handover duration.** The minimum handover is 10 minutes; complex shifts may take 20–25 minutes. Cutting the handover short to leave early is not acceptable. Both supervisors sign the shift report to confirm the handover was completed.

**Operator-level handover.** For critical or high-precision operations, an operator-to-operator verbal handover is also expected at the machine, covering machine condition, job status, and any adjustments made during the shift.
"""

