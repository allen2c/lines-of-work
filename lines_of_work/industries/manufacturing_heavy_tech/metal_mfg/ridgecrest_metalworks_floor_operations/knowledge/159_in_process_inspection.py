title = "In-Process Inspection and Control Points"

content = """
In-process inspection involves checking work at defined stages during manufacturing to catch non-conformances early, before additional value is added to a defective part. At Ridgecrest Metalworks, control points are defined in the process routing (traveller) for each part number. Operators and quality inspectors share responsibility for in-process verification.

Control points are established at:
- First-off inspection: The first part produced after a setup or tool change is fully inspected before the remainder of the batch is run. This confirms that the machine is set correctly before committing the full lot. First-off results are recorded and approved by the operator and, for critical operations, co-signed by the quality team.
- Dimensional checks during the run: At defined frequency (e.g., every 10th part, every 30 minutes, or at tool change intervals), selected dimensions are measured. Results are plotted on control charts where SPC is in use.
- Pre-operation checks: Before a part proceeds to the next operation (e.g., from machining to welding, or from welding to coating), specified characteristics are checked to confirm acceptability. This prevents propagating a defect through additional processes.

Typical in-process checks include:
- Critical dimensions by micrometer, caliper, or gauges
- Thread gauging (go/no-go gauges for pitch diameter verification)
- Weld visual inspection during and after welding (appearance, coverage, absence of visible defects)
- Temperature verification for heat treatment processes
- Coating wet film thickness during painting

When an out-of-tolerance condition is found, the operator must stop and notify the supervisor and quality. Parts produced since the last satisfactory check are quarantined and checked 100 % or dispositioned through the NCR process. The machine or process is adjusted and a new first-off confirmation is taken before production restarts.

Traveller cards accompany each job through the shop floor. At each control point, the inspector or operator signs and dates the traveller, recording the inspection result. A job cannot proceed to the next stage if the preceding sign-off is missing. This provides a paper trail and enforces sequential process discipline.
"""

