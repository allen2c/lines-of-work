content = """
When permanent access structures are unavailable, ladders and portable elevated work platforms are
used at Ridgecrest Metalworks for maintenance, inspection, and overhead rigging tasks. Proper selection,
inspection, and use of these access equipment types are critical for preventing falls.

Ladder Selection:
Portable ladders at Ridgecrest must be rated for industrial use. Step ladders and extension ladders
must meet ANSI A14.1 or A14.2 standards and carry a Type IA duty rating (300 lb capacity) as a
minimum. Ladder material selection matters in the operational environment: fiberglass ladders are
required near electrical hazards; aluminum ladders are suitable in the machining and material storage
areas but are prohibited in the foundry and arc furnace areas due to electrical and heat exposure risk.

Ladder Inspection:
Before each use, visually inspect ladders for: bent or cracked rails or rungs, loose or missing
hardware, damaged or missing non-slip feet, and contamination (oil, grease, or metal chips on rungs).
Defective ladders must be tagged out of service and removed from the floor immediately.

Safe Ladder Use:
- The 4-to-1 rule for extension ladders: for every 4 feet of height, the base must be 1 foot out
  from the support structure (approximately 75-degree angle).
- Extension ladders must extend at least 3 feet above the landing surface at the top.
- Maintain three points of contact at all times while climbing: two hands and one foot, or two feet
  and one hand.
- Do not stand on the top two rungs of a step ladder or the top three rungs of an extension ladder.
- Never carry tools or materials in your hands while climbing; use a tool belt or a hand line.
- Step ladders must be fully open and spread with the spreader locked before use.

Aerial Work Platforms (AWPs):
Scissor lifts and boom lifts are used for maintenance tasks requiring both elevation and a stable
work platform. Operators must be trained and qualified for each AWP type. The platform guardrails
must be in the raised position at all times during elevated operation. When using a boom lift, a full-body
harness must be worn and attached to the manufacturer-designated anchor point inside the platform.

Scaffolding:
Temporary scaffolding erected for extended maintenance tasks must be designed, erected, and inspected
by a qualified scaffold competent person. No personnel may access scaffolding until the competent
person has completed the pre-use inspection and affixed a green tag indicating it is safe for use.
"""

