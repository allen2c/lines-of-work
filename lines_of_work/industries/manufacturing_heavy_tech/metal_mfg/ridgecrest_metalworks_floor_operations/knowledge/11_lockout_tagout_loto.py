title = "Lockout/Tagout (LOTO) Procedures"

content = """
Lockout/Tagout (LOTO) is the primary control method used at Ridgecrest Metalworks to protect workers
from the unexpected energization or release of stored energy during equipment servicing and maintenance.
The LOTO program complies with OSHA 29 CFR 1910.147 (Control of Hazardous Energy).

When LOTO Applies:
LOTO is required any time an employee must place any part of their body into a point of operation or
a zone where unexpected machine motion, energization, or energy release could cause injury. This
includes clearing jams, changing tooling, performing maintenance, cleaning inside equipment, or
adjusting mechanisms that require bypassing guards.

Energy Types Addressed:
LOTO at Ridgecrest covers all relevant energy forms: electrical (motors, heating elements, control
circuits), hydraulic (accumulators, cylinders), pneumatic (air cylinders, actuators), mechanical
(stored spring energy, gravity), thermal (heated furnace elements, steam lines), and chemical
(pressurized chemical lines).

Machine-Specific LOTO Procedures:
Each piece of equipment on the floor has a machine-specific LOTO procedure document posted on or
adjacent to the machine. These documents include photographs of energy isolation points, the specific
sequence of steps to achieve zero energy state, and verification steps. Generic LOTO procedures are
not acceptable for equipment with multiple energy sources.

LOTO Execution Steps:
1. Notify affected employees that the equipment will be taken out of service.
2. Identify all energy sources using the machine-specific LOTO procedure.
3. Shut down the equipment using normal stopping procedures.
4. Isolate all energy sources (de-energize, disconnect, close valves).
5. Apply personal padlock(s) to each energy isolation point.
6. Release or restrain all stored energy (bleed pneumatic lines, block raised components, discharge
   capacitors, allow thermal cooling where applicable).
7. Verify zero energy state by attempting to start the machine and testing with a voltage meter.

Group LOTO:
When multiple workers service the same equipment simultaneously, each worker applies their own
personal padlock to a group lock hasp. No individual may remove another person's lock. The last
worker to leave the job removes the group hasp only after confirming all others have departed.

LOTO training is required before any employee performs maintenance tasks. Annual retraining and an
annual LOTO program audit are conducted by the Safety Officer.
"""

