title = "Raw Material Storage and Segregation"

content = """
Proper raw material storage protects material quality, maintains traceability, and prevents mix-ups
that could result in wrong material being placed into production. Ridgecrest Metalworks maintains
dedicated storage areas organized by material family, form, and grade, with strict segregation rules.

Storage Area Layout:
The raw material storage area is divided into the following segregated zones:
- Ferrous bar and billet storage: Steel racks holding bar stock, rounds, and billets, organized by
  grade and diameter. Rack sections are labeled with alloy grade and allowable PO numbers.
- Plate and sheet storage: Horizontal plate racks supporting flat stock. Plates are organized by
  material specification and thickness.
- Non-ferrous storage: Separate room for aluminum, copper, and other non-ferrous stock. Segregation
  from ferrous materials is essential to prevent galvanic corrosion and chip contamination.
- Special alloys vault: Locked and controlled-access area for aerospace-grade, high-value, or limited-
  supply materials such as titanium, Inconel, and specialty tool steels.

Material Identification Requirements:
Every piece of material in storage must bear visible identification. Bar stock must retain the original
mill bundle tag or have heat numbers stamped or paint-stenciled on each piece after the bundle is
broken. Plate pieces cut from a larger sheet must be identified with the original heat number before
being returned to storage.

Unidentified material found in the storage area must be tagged as Unknown/Suspect and placed in
the NCM hold area pending investigation. It may not be used in production until positively identified
via PMI testing or other acceptable means.

Environmental Controls:
Steel materials must be protected from moisture to prevent rust formation that may be mistaken for
corrosion damage or may require additional cleaning before use. Storage racks maintain material off
the floor; temperature and humidity control is limited in the main storage building, so materials
sensitive to corrosion are wrapped in VCI (Vapor Corrosion Inhibitor) paper when stored for extended
periods.

First-In, First-Out (FIFO):
Material is issued from storage on a FIFO basis to minimize inventory age and prevent the use of
older material when newer stock of the same specification is available. The material control team
uses the ERP system to manage FIFO lot assignment and issue material against production orders.
"""

