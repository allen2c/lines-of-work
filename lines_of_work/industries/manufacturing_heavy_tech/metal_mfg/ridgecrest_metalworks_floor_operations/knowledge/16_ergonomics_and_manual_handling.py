content = """
Musculoskeletal injuries from manual handling, repetitive motion, and awkward postures represent a
significant portion of workplace injuries in metal manufacturing. Ridgecrest Metalworks addresses
ergonomic hazards through workstation design, mechanical assistance, job rotation, and training.

Manual Lifting Guidelines:
Before lifting any object, assess the load weight, shape, grip accessibility, and distance to be
carried. Use mechanical lifting aids for any load over 50 lbs. For loads between 25 and 50 lbs,
use team lifting if the load cannot be managed by mechanical assist.

Safe manual lifting technique:
- Position feet shoulder-width apart, close to the load.
- Bend at the knees, not the waist; keep the back as straight and upright as possible.
- Get a firm grip on the load; use handles or lifting fixtures if available.
- Lift by straightening the legs, keeping the load close to the body.
- Do not twist the torso while carrying a load; pivot by moving the feet instead.
- Lower the load in a controlled manner by bending the knees.

Mechanical Lifting Aids:
Vacuum lifters, magnetic lifters, jib cranes, and ergonomic lift-and-tilt tables are available in
the machining center and assembly areas for handling metal stock and components. Employees should
request additional lifting aids from their supervisor when manual handling of awkward loads is
required. Using available mechanical aids is expected, not optional.

Repetitive Motion and Prolonged Posture:
Grinding, deburring, and inspection tasks can involve repetitive wrist and shoulder motions that
increase injury risk over time. Controls include job rotation so no operator performs the same highly
repetitive task for more than two consecutive hours, scheduled micro-breaks, use of anti-fatigue mats
at standing workstations, and tool selection that minimizes grip force requirements.

Workstation Assessment:
The Safety Officer conducts ergonomic workstation assessments using a standardized ergonomic risk
scoring tool. Employees are encouraged to report discomfort, pain, or awkward working positions before
they develop into injuries. Early reporting enables workstation adjustments before a musculoskeletal
disorder occurs.

Ergonomic incident reports are reviewed quarterly to identify trends and prioritize engineering controls
such as fixture redesign, height-adjustable tables, and conveyor modifications.
"""

