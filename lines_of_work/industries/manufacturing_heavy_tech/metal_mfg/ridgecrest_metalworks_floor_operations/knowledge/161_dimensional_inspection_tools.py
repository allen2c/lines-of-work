title = "Dimensional Inspection Tools and Their Use"

content = """
Accurate dimensional inspection requires selecting the correct tool for the feature being measured and the tolerance involved. At Ridgecrest Metalworks, a range of inspection instruments is maintained, calibrated, and available for production and quality use. Using the wrong tool — or using the right tool incorrectly — produces unreliable results.

General rules for instrument selection:
- The measurement uncertainty of the instrument should not exceed 10–25 % of the feature tolerance (the "10:1 rule" or "4:1 rule" for more demanding applications). For a 0.05 mm tolerance, the instrument should resolve to 0.005 mm (5 µm) or better.
- Temperature effects matter for precision work: metals expand approximately 12 µm/m/°C. Parts and instruments should both be at 20 °C for highest accuracy. In a production environment, parts should be allowed to stabilize before measurement.

Common instruments and their applications:

Steel rules and tape measures: Used for non-critical length checks (± 0.5 mm accuracy). Not suitable for toleranced features.

Vernier calipers: Used for external/internal dimensions, step heights, and depths to ±0.02–0.05 mm (digital electronic calipers typically resolve to 0.01 mm). Suitable for tolerances ≥ 0.1 mm.

Outside/inside micrometers: Used for diameters and thicknesses to ±0.001 mm resolution. Essential for shaft diameters, wall thicknesses, and thread major diameters. Multiple sizes cover 0–25 mm, 25–50 mm, etc.

Depth micrometers and depth gauges: Used for blind hole depths, slot depths, and step heights to ±0.01–0.001 mm.

Dial test indicators (DTIs) and lever-type test indicators: Used for roundness checks, runout measurement, surface flatness, and alignment at ±0.001–0.01 mm resolution. Used in conjunction with a surface plate and height gauge.

Gauge pins and plug gauges (go/no-go): Used to verify hole diameters to attribute (pass/fail). Faster than measurement for high-volume applications.

Thread gauges (go/no-go ring and plug): Verify thread pitch diameter conformance to tolerance class. ISO metric, UNC, UNF, BSP, and other thread forms require form-specific gauges.

Height gauges and surface plates: A precision granite surface plate provides the flat reference datum. Height gauges measure step heights, perpendicularity offsets, and center heights to ±0.01 mm. Granite surface plates must be certified for flatness per grade requirements and kept clean and free of damage.
"""

