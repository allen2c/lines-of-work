title = "Cross-Department Coordination: QA, Maintenance, and Warehouse"

content = """
The production floor at Ridgecrest Metalworks does not operate in isolation. Every shift involves frequent, critical interactions with Quality Assurance, Maintenance, and Warehouse/Materials functions. Understanding the correct protocol for these interactions prevents delays, reduces misunderstandings, and ensures that each department can do its job effectively.

**Coordination with Quality Assurance (QA).**

QA provides inspection services, disposition of non-conforming material, and approval of first-piece and final inspections. Production's responsibilities in this relationship:
- Notify the QA inspector when a first-piece or setup approval is ready—do not begin a production run without QA sign-off on the first piece when required by the traveler.
- When a potential defect is found, stop production and call QA rather than continuing and hoping the issue resolves.
- Provide QA with the part, the drawing, and the relevant traveler section when requesting disposition—not just the part alone.
- Do not pressure QA for a pass result on suspect parts. QA disposition is independent of production schedule pressure.

**Coordination with Maintenance.**

Maintenance responds to breakdown work orders and executes planned PM activities. Production's responsibilities:
- Log downtime accurately in the system so maintenance has data to prioritize and plan.
- When a machine breaks down, call maintenance on radio channel 2 with the machine ID, a description of the symptom, and the current work order status. Stay nearby to answer questions but do not attempt repairs without authorization.
- Do not remove maintenance lockout/tagout locks under any circumstances—only the person who applied the lock removes it.
- Give maintenance access to the machine without delay—do not continue operating a machine with a known fault while waiting for a "convenient" time.
- For PM activities: clear the machine and surrounding area of WIP and tools before the scheduled PM window so maintenance can start immediately.

**Coordination with Warehouse/Materials.**

Warehouse manages material flow to and from production cells. Production's responsibilities:
- Submit material pick requests (or flag the scheduled material pull) at least 2 hours before the material is needed.
- Confirm that the material received at the cell matches the work order BOM before the warehouse associate leaves.
- Notify the warehouse when material is ready for transfer to the next operation or to finished goods—do not let completed work pile up in the cell.
- Return unused material to the warehouse with the correct labels and transaction posted; do not store unauthorized material in the cell.

Cross-department coordination issues should be escalated to the respective department supervisors (production and QA/maintenance/warehouse supervisor) when informal resolution fails.
"""

