title = "Employee Suggestion System and Idea Management"

content = """
The Ridgecrest Metalworks employee suggestion system is the formal channel through which any employee—operator, lead, supervisor, or office staff—can submit an idea for improving safety, quality, productivity, cost, environmental performance, or workplace morale. The system exists because good ideas come from anywhere in the organization, and front-line employees often have the clearest view of operational problems and practical solutions.

**How to submit a suggestion.** Employees submit ideas using the suggestion card (paper form at each cell and in the break room) or electronically through the company intranet portal. The submission includes:
- Employee name (optional—anonymous suggestions are accepted but receive no individual recognition).
- Description of the problem or opportunity.
- Proposed solution or improvement idea.
- Estimated benefit (if known).

**What happens after submission.** Every suggestion is reviewed by the cell supervisor within 5 business days. The supervisor determines one of three outcomes:
1. **Implement immediately.** Small, low-risk improvements within the supervisor's authority are implemented without further review.
2. **Refer for further evaluation.** Ideas requiring engineering review, capital investment, cross-department impact, or policy change are forwarded to the production manager and relevant department heads.
3. **Decline with explanation.** If an idea is not feasible or has already been tried, the submitter receives a written explanation. Unexplained rejections undermine trust in the system.

**Recognition.** All submitted suggestions—whether implemented or not—receive a written response. Implemented suggestions earn recognition:
- Verbal recognition at the daily production meeting.
- Certificate of recognition posted on the employee board.
- For suggestions that generate measurable cost savings or defect reductions, a formal monetary award per the company recognition policy (awarded quarterly).

**System health metrics.** The number of suggestions submitted per employee per month is tracked—not as an individual pressure metric, but as a facility health indicator. Facilities with fewer than 0.5 suggestions per employee per month are considered to have suppressed engagement, typically due to poor supervisor response practices.

**Idea management log.** All suggestions are logged in the improvement management system with: submission date, submitter, description, status, assigned owner, and completion date. The log is reviewed in the monthly operations meeting to ensure no idea ages more than 30 days without a disposition.
"""

