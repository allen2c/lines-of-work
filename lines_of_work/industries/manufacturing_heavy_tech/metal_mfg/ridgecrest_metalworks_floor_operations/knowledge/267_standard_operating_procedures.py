title = "Standard Operating Procedures (SOPs): Use and Updates"

content = """
Standard Operating Procedures (SOPs) are the documented, step-by-step instructions for performing key production tasks consistently and safely. At Ridgecrest Metalworks, SOPs are a controlled quality document—not a suggestion or a guideline. Deviating from an approved SOP without authorization is a nonconformance under ISO 9001.

**Purpose of SOPs.** SOPs exist to:
- Define the one best way to perform a task based on quality, safety, and efficiency requirements.
- Ensure consistency across operators, shifts, and time.
- Provide training material for new employees.
- Serve as an audit evidence document showing that processes are controlled.
- Protect operators—if an accident occurs, following the approved SOP demonstrates due care.

**SOP structure at Ridgecrest.** Each SOP includes:
- Document number, revision level, and effective date.
- Scope (which machines, parts, or operations it covers).
- Required PPE and safety precautions.
- Materials, tools, and equipment required.
- Numbered step-by-step instructions.
- Acceptance criteria and inspection check points.
- References (related drawings, material specs, safety data sheets).
- Revision history.

**Document control.** SOPs are controlled documents. Only the current approved revision may be used in production. The document control system (a module within the QMS) lists the current revision for each SOP. Printed copies are stamped "CONTROLLED" and distributed only through the document controller. Uncontrolled copies (without the stamp) are for reference only and must not be used in production.

Outdated SOP copies must be removed from the work area and destroyed when a new revision is issued. The document controller distributes updated copies and collects outdated ones.

**Using an SOP.** The SOP is reviewed at the start of any job where it is applicable, particularly for infrequent operations, new operators, or jobs following an ECO. An operator who has memorized a procedure is still expected to reference the SOP for critical steps.

**Requesting SOP updates.** If an operator identifies that an SOP is incorrect, incomplete, or has been superseded by a process change, they must not simply work around it. The correct path is to:
1. Notify the supervisor of the discrepancy.
2. The supervisor submits an SOP change request to engineering or quality.
3. The change is reviewed, approved, and implemented through the document control process.
4. The updated SOP is distributed before the old version is removed from service.

SOPs are reviewed and re-approved at minimum every three years, or following any process change.
"""

