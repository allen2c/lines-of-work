title = "Key Mechanical Properties of Metals"

content = """
Mechanical properties define how a metal responds to applied forces and are the primary basis for
material selection in engineering applications. Production personnel at Ridgecrest Metalworks must
understand these properties to correctly interpret material specifications, recognize when a material
may be outside specification, and understand why certain processing steps (such as heat treatment)
are required.

Tensile Strength (Ultimate Tensile Strength, UTS):
The maximum stress a material can withstand before fracturing. Reported in MPa or psi. This is the
"headline" strength value on most material specifications. High tensile strength does not always
indicate high fatigue resistance or good ductility.

Yield Strength (Proof Strength):
The stress at which a material begins to deform plastically — i.e., permanently. Below the yield
strength, deformation is elastic and the material returns to its original shape when the load is
removed. Engineering components are typically designed to operate well below the yield strength.
In the absence of a distinct yield point (as in austenitic stainless steels), yield strength is
reported as the 0.2% offset proof stress.

Elongation and Reduction of Area:
Elongation is the percentage increase in gauge length at fracture; reduction of area is the percentage
decrease in cross-sectional area at the fracture point. Both are measures of ductility. Higher values
indicate more ductile material that will deform visibly before fracturing, providing warning of
overload. Brittle materials have low elongation and fracture with little plastic deformation.

Hardness:
Hardness measures resistance to localized plastic deformation (indentation). Hardness correlates
roughly to tensile strength and is a fast, non-destructive in-process check. Common scales used at
Ridgecrest: Rockwell (HRC for hardened steels, HRB for softer materials), Brinell (HBW for castings
and forgings), and Vickers (HV for thin sections and surface layers).

Toughness:
The ability of a material to absorb energy before fracturing, measured by Charpy or Izod impact tests.
Toughness is particularly important for components subject to impact or shock loading and for materials
used at low temperatures where certain steels undergo a ductile-to-brittle transition.

Fatigue Strength:
The maximum stress amplitude a material can withstand for a specified number of load cycles without
fracture. Fatigue failures initiate at stress concentrations (notches, surface defects, tool marks)
and can occur well below the UTS. Surface finish and residual stress state (from heat treatment and
machining) significantly affect fatigue life.
"""

