title = "Shift Report: Required Fields and Submission"

content = """
The shift report is the formal written record of each production shift at Ridgecrest Metalworks. It provides the incoming shift with a clear picture of what was accomplished, what is in progress, and what problems need attention. It also provides management with a daily record for trend analysis and performance review.

**Who completes it.** The outgoing shift supervisor (or lead operator in smaller cells) is responsible for completing the shift report before leaving the facility. The report must be submitted no later than 15 minutes after shift end. Late or incomplete reports are noted in the supervisor's performance record.

**Required fields.**

1. **Date, shift, and supervisor name.** Basic header information—no blanks.

2. **Production output by work order.** For each active work order, record: work order number, part number, operation number, quantity started, quantity completed (good), quantity scrapped (with scrap code), and cumulative order completion percentage.

3. **Machine status.** List each machine: running, idle, or down. If down, specify downtime start time, downtime code, and current status (awaiting maintenance / maintenance in progress / repaired).

4. **Material issues.** Any material shortages encountered, material quality issues found, or material non-conformances initiated during the shift.

5. **Safety incidents and near-misses.** Any first-aid cases, near-misses, or safety observations must be described. "No incidents" must be explicitly stated if none occurred—a blank field is not acceptable.

6. **Quality holds and NCRs.** Any non-conforming material or parts flagged during the shift, including the NCR number, part affected, quantity, and disposition requested.

7. **Maintenance work performed.** Any maintenance activities completed during the shift, including PM tasks and emergency repairs.

8. **Open issues for incoming shift.** A brief list of items the incoming shift must be aware of or act on: jobs in critical queue, pending decisions, parts awaiting QA clearance.

9. **Shift handover communication.** Confirm that a face-to-face handover was completed with the incoming supervisor (checkbox plus signature of both).

**Submission.** The report is submitted via the production management system (digital form) and a printed copy is left on the supervisor's desk. Supervisors review all overnight shift reports before the 7:00 AM production meeting.
"""

