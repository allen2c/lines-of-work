title = "Continuous Casting of Steel and Non-Ferrous Metals"

content = """
Continuous casting (also called strand casting) is the process by which molten metal is solidified
into a semi-finished billet, bloom, or slab for subsequent rolling, forging, or further processing.
At Ridgecrest Metalworks, continuous casting of steel and aluminum produces the forging feedstock
used in the Bay B forging operations, allowing tight chemical and microstructural control from melt
to billet.

Process Description:
Molten metal from the electric arc furnace or induction furnace is transferred to a tundish — an
intermediate distribution vessel that maintains a consistent metallostatic head pressure and acts as
a buffer between ladle transfers. From the tundish, metal flows through a water-cooled copper mold
at a controlled rate.

The copper mold gives the strand its initial shape and begins solidification at the strand's outer
skin. The strand is continuously extracted downward (or in a curved path for curved casters) and
passes through a secondary cooling zone where water spray systems cool the strand from the outside
as the solidification front advances inward.

Once sufficiently solidified, the strand is torch-cut or shear-cut into predetermined lengths
(billets or blooms) and transferred to the forging or rolling bay.

Advantages Over Conventional Ingot Casting:
Continuous casting eliminates the crop loss associated with the hot top (riser) of ingot casting,
reduces macro-segregation of alloying elements, provides more uniform microstructure along the
strand length, and significantly increases throughput and yield.

Critical Process Parameters:
- Casting speed: Too fast creates insufficient shell thickness at mold exit, risking a breakout (molten
  metal breaking through the solidified shell — a catastrophic and dangerous event).
- Mold cooling and oscillation: The mold oscillates vertically to prevent the solidifying shell from
  sticking to the copper mold wall. Mold taper must be maintained within specification.
- Superheat temperature: The temperature of the melt above the liquidus when poured. High superheat
  increases centerline segregation; low superheat risks premature freezing in the tundish.
- Secondary cooling: Spray water flow rates must be calibrated to the casting speed to achieve target
  strand surface temperature profiles and avoid surface cracking from rapid cooling gradients.

Billet quality is verified by macro-etch testing of transverse sections to reveal the solidification
structure, centerline porosity, and any segregation patterns.
"""

