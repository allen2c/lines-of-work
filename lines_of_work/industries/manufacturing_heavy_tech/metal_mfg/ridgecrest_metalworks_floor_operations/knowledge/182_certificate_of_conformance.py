title = "Certificates of Conformance (CoC): Content and Control"

content = """
A Certificate of Conformance (CoC), also called a Certificate of Compliance, is a document issued by a supplier or manufacturer declaring that a specific batch of product meets all applicable specification and drawing requirements. At Ridgecrest Metalworks, a CoC is produced for every shipment of finished goods to customers who require one, and CoCs are required from subcontractors performing special processes (heat treatment, plating, NDT) on Ridgecrest-supplied parts.

Ridgecrest CoC content requirements:
- Ridgecrest Metalworks name, address, and authorized signatory
- Customer name and purchase order / order reference number
- Part number and drawing revision
- Quantity shipped
- Job or batch number providing production traceability
- Applicable specifications, standards, and drawing revision to which the product conforms
- Statement of conformance (e.g., "We certify that the above products have been manufactured, inspected, and tested in accordance with the stated specifications and meet all applicable requirements")
- Reference to any applicable process certifications (material certs, heat treatment records, NDT reports) included or on file
- Date of issue and authorized signature

The CoC is not a test report — it does not typically list measured values unless specific test data is required by the customer. It is a declaration of conformance, backed by the quality records maintained in the job file. Issuing a false CoC is fraud; the signatory takes personal and company legal responsibility for the declaration.

Incoming CoC review at Ridgecrest: When a subcontractor (e.g., heat treater, plater, NDT provider) returns material with a CoC, the receiving department verifies:
- The CoC references the correct Ridgecrest purchase order and part number
- The process performed matches what was ordered
- The applicable specification and standard listed on the CoC matches what was specified on the purchase order
- The quantity matches the delivery

CoCs are attached to the job traveller and filed with the quality records. For long-term contracts, CoCs may be scanned and stored electronically in the QMS. Any CoC that cannot be verified or that contains errors is quarantined pending resolution with the supplier — material is not released to production without a valid CoC where one is required.
"""

