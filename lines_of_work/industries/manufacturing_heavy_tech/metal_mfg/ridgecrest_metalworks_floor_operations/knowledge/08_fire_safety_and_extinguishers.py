content = """
Metal manufacturing operations introduce multiple fire ignition sources including molten metal, cutting
sparks, lubricating oils, hydraulic fluids, and flammable solvents. Ridgecrest Metalworks maintains a
comprehensive fire prevention and suppression program to control these risks.

Fire Hazard Controls:
Combustible materials including cardboard, oily rags, paper, and flammable liquids must be kept clear
of hot work areas, furnaces, and welding stations by a minimum of 35 feet or isolated behind fire-rated
barriers. Oily rags must be placed in self-closing metal containers and removed from the floor daily.
Hydraulic lines on presses must be inspected regularly for leaks, as high-pressure hydraulic fluid
misting near ignition sources is a fire and explosion risk.

Fire Suppression Systems:
Bay A (Melt Shop) is equipped with a dry chemical suppression system specifically selected for metal
fire risks. Bays B through E are covered by wet-pipe sprinkler systems. All sprinkler heads must remain
unobstructed; stacking materials within 18 inches of a sprinkler head is prohibited.

Fire Extinguisher Types and Locations:
Extinguishers are mounted at no more than 75-foot travel distance throughout the facility and are
color-coded by class:
- Class ABC dry chemical: General production areas (machining, forging)
- Class D dry powder (sodium chloride or copper-based): Melt shop and any area handling magnesium or
  titanium swarf. Class D fires involve combustible metals and must NEVER be fought with water or
  Class ABC extinguishers, which can cause violent reactions.
- CO2 extinguishers: Electrical panels and control rooms

PASS Technique for Extinguisher Use:
Pull the pin — Aim the nozzle at the base of the fire — Squeeze the handle — Sweep side to side.

Employees should only attempt to fight a fire if: (1) the fire is small and contained, (2) the correct
extinguisher type is available, (3) they have a clear exit path behind them, and (4) they have been
trained in extinguisher use. For any fire that is spreading, involves Class D metals, or cannot be
controlled within 30 seconds, evacuate immediately and call emergency services.

Monthly extinguisher inspections are performed by the Safety Officer. Annual maintenance inspections
are performed by a certified fire equipment contractor. Inspection tags must be current on all units;
report any missing or expired tag immediately.
"""

