title = "Heat Stress Management in Foundry and Forge Areas"

content = """
The foundry and forge areas of Ridgecrest Metalworks generate significant radiant heat from furnaces,
molten metal, hot tooling, and heated workpieces. These environments place workers at risk of heat
stress illness, ranging from heat cramps and heat exhaustion to potentially fatal heat stroke. A
comprehensive heat stress management program is in place for all high-heat work areas.

Heat Illness Recognition:
Heat Cramps: Painful muscle cramps, usually in legs or abdomen; caused by salt and water loss through
sweating. Response: rest, cool environment, electrolyte-containing fluids.

Heat Exhaustion: Heavy sweating, weakness, cold and clammy skin, weak pulse, nausea, possible
fainting. Core temperature remains below 104°F. Response: move to a cool area, loosen clothing, apply
cool wet cloths, provide fluids if conscious, seek medical attention.

Heat Stroke: Medical emergency. Confusion, loss of consciousness, hot and dry or sweaty skin, rapid
strong pulse, core temperature above 104°F. Response: call 911 immediately. Cool the person rapidly
by any available means (ice packs, cool water immersion if possible). Do not give fluids to an
unconscious person.

Heat Stress Prevention Program:
Acclimatization: New workers and returning workers after more than 5 days away from heat exposure
must be gradually acclimatized. The NIOSH schedule calls for 20% exposure on day one, increasing
by 20% per day over the following 4 days. Supervisors track acclimatization status for all Bay A
and Bay B workers.

Hydration: Workers in heat-exposed areas should drink 1 cup (8 oz) of cool water every 15–20 minutes.
Chilled water stations are positioned in each bay's rest area. Caffeinated beverages and alcohol
are not substitutes for water and should be avoided during heat-exposed work periods.

Rest Cycles: In WBGT (Wet Bulb Globe Temperature) conditions requiring it, scheduled work-rest
cycles are enforced based on the ACGIH heat stress guidelines relevant to the metabolic work rate
of the task. Supervisors monitor WBGT using fixed monitors installed in Bay A and Bay B.

Buddy System: Workers in high-heat environments must check on each other regularly. Anyone observing
a coworker appearing confused, weak, or stopping sweating in a hot environment must immediately
alert the supervisor and initiate the emergency response.
"""

