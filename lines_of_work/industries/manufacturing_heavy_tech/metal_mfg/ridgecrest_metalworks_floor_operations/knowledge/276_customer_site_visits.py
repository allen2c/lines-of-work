title = "Customer Site Visits and Audit Preparation"

content = """
Customer site visits and supplier audits are a normal part of doing business for a contract metal manufacturer. Customers—especially those in aerospace, defense, energy, and automotive sectors—routinely audit their suppliers to verify quality systems, process controls, and compliance with their specific requirements. Ridgecrest Metalworks treats every customer visit as both a compliance obligation and a business development opportunity.

**Types of customer visits.**

1. **Qualification audits:** Conducted before a new customer places their first order. Customers assess whether Ridgecrest has the quality system, equipment, and process capabilities to supply their parts.

2. **Surveillance audits:** Periodic check-ins by existing customers to verify ongoing compliance with their quality requirements and any customer-specific quality clauses.

3. **Source inspection:** Customer quality representative witnesses specific operations or inspections in real time for high-criticality orders. Common in aerospace and defense. Source inspector must be given advance scheduling notice and unrestricted access to the relevant production records.

4. **Customer plant tours (business development):** Prospective customers evaluate facilities before awarding new business. These visits are informal but still require the plant to demonstrate operational discipline.

**Audit preparation.**

1. **Notify all relevant personnel.** The plant manager briefs supervisors at least 48 hours before the audit. Operators in audited areas are informed so they are not surprised by external visitors.

2. **Document readiness.** Ensure all quality records for the customer's parts are complete, filed, and accessible. Check that current drawing revisions are posted at work cells. Verify that SOPs and work instructions are current and available.

3. **Physical readiness.** The facility should look like it always looks on a normal productive day—not specially cleaned only for visitors. 5S discipline maintained daily means no last-minute scramble. Scrap bins should be properly labeled and not overflowing. Hazardous materials should be properly stored. Aisles should be clear.

4. **Key process demonstrations.** Identify which processes the customer is most likely to observe. Verify operators on those machines are prepared to explain their process, show their setup verification steps, and demonstrate first-piece inspection methods.

**During the audit.** Operators who are questioned should answer honestly and directly. If an auditor asks to see a record, find it and provide it. If an operator does not know an answer, they should say so and identify who can answer. Do not guess, embellish, or try to redirect auditors away from legitimate questions.

Post-audit, any findings are reviewed by the QA manager and production manager. Corrective actions are submitted to the customer per their required response timeline.
"""

