content = """
Personal protective equipment (PPE) at Ridgecrest Metalworks is selected based on documented hazard
assessments for each work area and task type. The facility conducts annual PPE hazard assessments in
accordance with OSHA 29 CFR 1910.132 and updates requirements when new processes or chemicals are introduced.

Facility-Wide Minimum PPE (required in all production bays at all times):
- Safety glasses with side shields (ANSI Z87.1 rated)
- Steel-toed and steel-midsole safety footwear (ASTM F2413)
- High-visibility vest or incorporated hi-vis elements when in active forklift traffic zones

Area-Specific PPE Requirements:

Foundry and Melt Shop (Bay A):
- Face shield (minimum 8-inch drop, heat-resistant) worn over safety glasses
- Aluminized or leather foundry jacket and spats rated for molten metal splash
- Foundry gloves (aluminized or leather welder's style)
- Hearing protection (minimum NRR 25) due to sustained high noise levels
- Safety footwear must have no external lacing that could trap molten metal

Forging and Press Area (Bay B):
- Face shield for press operation and die change activities
- Cut-resistant gloves (minimum ANSI A4) when handling billets and flash
- Leather apron during forge operations
- Hearing protection (NRR 25 minimum)

Machining Center (Bay C):
- Safety glasses (side shields mandatory)
- Cut-resistant gloves when handling raw stock or finished parts with sharp edges; gloves must NOT be
  worn while operating rotating equipment
- Hearing protection when using grinders or high-speed cutters

Heat Treatment and Finishing (Bay D):
- Heat-resistant gloves rated for the applicable furnace temperature range
- Face shield during quench operations
- Chemical-splash goggles and chemical-resistant gloves in the cleaning and plating zones
- Supplied-air or appropriate respirator when working with cleaning chemicals

PPE Inspection: Operators must inspect PPE before each shift. Damaged or expired PPE must be replaced
immediately. PPE is provided at no cost to employees and is available from the safety supply crib.

Contact lenses are permitted on the floor but require safety glasses; contact lens wearers must notify
their supervisor and are prohibited from working in areas with chemical splash or high fume exposure
without additional eye protection.
"""

