title = "Daily Production Meetings: Agenda and Output"

content = """
The daily production meeting is the core coordination mechanism at Ridgecrest Metalworks. It aligns supervisors, planners, maintenance, quality, and materials on the current day's priorities and resolves obstacles before they delay production. The meeting is structured, time-bounded, and action-oriented—not a status report session.

**Meeting schedule.** The primary production meeting runs each morning at 7:00 AM, 15–20 minutes maximum, at the main production board. A second brief touch-up meeting may occur at shift change (typically 3:00 PM) to hand off open issues to the afternoon crew.

**Participants.** Cell supervisors (all shifts affected), production planner, maintenance team lead, QA lead, warehouse/materials lead. Senior management attends periodically but does not run the meeting.

**Standard agenda.**

1. **Yesterday's performance review (3 min).** Review actual vs. planned output per cell. OEE by work center. On-time completions vs. due dates. Any shipment misses from the previous day.

2. **Today's priorities (5 min).** Top 5 work orders by due date urgency. Rush orders and hot jobs flagged. Any customer commitments due today.

3. **Constraints and obstacles (5 min).** Each cell supervisor names any constraint blocking today's schedule: machine down, material short, operator absent, quality hold. Each named constraint gets an owner and a committed resolution time. No unresolved constraint leaves the meeting without an owner.

4. **Maintenance status (3 min).** Open work orders on critical machines. Planned PM activities that will take equipment down today.

5. **Quality issues (2 min).** Any active holds or NCR dispositions affecting today's production flow.

6. **Action log review (2 min).** Open actions from previous meeting—confirm status (done / not done / owner update).

**Meeting discipline.** Start on time. End on time. Side conversations are taken offline. One person speaks at a time. Every constraint gets an owner and a time—"someone will look at it" is not acceptable.

**Output.** The meeting produces a written action log (posted on the board): item, owner, due time. The planner updates the day's schedule based on meeting outcomes and redistributes to supervisors before 8:00 AM.
"""

