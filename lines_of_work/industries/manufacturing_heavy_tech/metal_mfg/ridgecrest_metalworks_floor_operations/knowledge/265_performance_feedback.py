title = "Performance Feedback and Operator Coaching"

content = """
Performance feedback is the systematic process through which supervisors communicate to operators how well they are performing relative to expectations, identify areas for improvement, and recognize strong performance. At Ridgecrest Metalworks, feedback is delivered through multiple channels: daily informal observation, weekly one-on-one check-ins, and formal periodic reviews.

**Principles of effective feedback.**

1. **Specific and behavioral.** Effective feedback describes observable actions, not character. "I noticed you ran the last three press brake setups 30% faster than standard because you pre-staged all the tools before the previous job ended—that's exactly what we need more of" is useful feedback. "Good job lately" is not.

2. **Timely.** Feedback should be delivered close to the observed behavior—positive or corrective. Waiting weeks to address a recurring error allows it to continue and become a habit. Waiting weeks to recognize excellent work diminishes the impact.

3. **Balanced.** Supervisors who only provide corrective feedback create defensive, disengaged operators. Supervisors who never provide corrective feedback allow problems to persist. Both positive recognition and improvement guidance are part of professional growth.

4. **Private for corrections.** Corrective feedback is always delivered in private—never in front of peers, customers, or visitors. Publicly correcting an operator embarrasses them and damages trust. Recognition can be delivered publicly.

**Daily coaching.** The most impactful feedback happens informally on the floor during the shift. A supervisor observing an operator struggling with a setup should approach, observe, and offer assistance or guidance immediately—not note it for a quarterly review. Coaching is teaching: demonstrate the correct method, explain why, have the operator practice, observe.

**Periodic performance reviews.** Formal performance reviews are conducted at 90 days for new employees, and annually thereafter. Reviews at Ridgecrest cover:
- Technical skills and machine qualifications achieved.
- Quality performance (scrap rate, NCR involvement).
- Safety behavior (compliance, near-miss reporting, participation in safety activities).
- Productivity (output vs. standard, setup time trend).
- Teamwork and communication (shift handover quality, cross-department cooperation).
- Initiative and continuous improvement contributions.

**Documentation.** Supervisors maintain brief notes on significant performance observations (both positive and corrective). These notes support the annual review and protect the company in the event of a disciplinary process. Notes are factual and dated.
"""

