content = """
At Ridgecrest Metalworks, operators are qualified to specific machines and operations through a structured training and certification process. No operator is authorized to independently run a machine or perform an operation for which they are not yet certified. This requirement protects part quality, machine integrity, and operator safety.

**Training pathway.** New operators progress through four stages:

1. **Orientation (Week 1–2).** Facility safety overview, emergency procedures, quality system awareness, ISO 9001 and 14001 basics, ERP system overview, PPE requirements. Completed with HR and EHS team. Required before entering any production area.

2. **Observation.** The trainee observes a qualified operator running the target machine for a defined period (typically 4–8 hours depending on complexity). Trainee does not touch controls or handle parts independently.

3. **Supervised practice.** The trainee operates the machine under direct supervision of the qualified operator or supervisor. The supervisor is physically present and can intervene immediately. The trainee completes the required number of setups and production runs defined in the machine certification checklist.

4. **Qualification assessment.** The supervisor or lead operator formally evaluates the trainee against the machine certification checklist, which covers: safety pre-checks, setup procedure, correct program selection, first-piece verification, production run control, downtime logging, and end-of-shift documentation. The trainee must pass all criteria. Partial passes require additional supervised practice.

**Skills matrix.** The skills matrix is a table showing each operator (rows) against each machine and operation qualification (columns), with status: Not Trained, In Training, Qualified, or Qualified Trainer. The skills matrix is maintained in the HR/training management system and printed monthly for posting at each cell's performance board.

**Re-qualification.** Operators who have not run a specific machine for more than 6 months must complete a refresher observation and a supervisor sign-off before running independently again. This also applies after a return from extended leave.

**Specialized certifications.** Certain operations require additional formal certification:
- Forklift and powered industrial vehicle operation (OSHA-required).
- Overhead crane operation and rigging.
- Welding procedure qualifications (per AWS D1.1 or customer-specified procedure).
- Nondestructive testing (Level I or II per SNT-TC-1A).

These certifications are tracked separately and have defined re-certification intervals.

Training records are maintained for each employee's tenure plus 5 years after separation.
"""

