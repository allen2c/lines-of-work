content = """
Effective escalation is critical on a metal manufacturing floor where equipment failures, quality
deviations, and safety events can have significant downstream consequences. Ridgecrest Metalworks
maintains a defined escalation hierarchy to ensure the right people are contacted promptly.

Primary Floor Contacts:
- Shift Supervisor: First point of escalation for all production, quality, and safety issues during
  the shift. Located in the supervisor office adjacent to Bay B or actively walking the floor.
- Cell Lead: Immediate first-line support for operator-level issues within a specific production cell.

Quality Escalation:
- Quality Technician on duty: Contact for in-process inspection questions, interpretation of tolerances,
  and disposition of suspected non-conforming parts. Each shift has at least one quality technician
  assigned to the floor.
- Quality Manager: Escalated contact for systemic quality issues, customer hold notifications, or when
  a non-conformance cannot be dispositioned at the technician level. Available during day shift; on-call
  during second and third shifts.

Maintenance Escalation:
- Maintenance Technician on duty: Contact for equipment breakdowns, unusual machine behavior, or
  requests for unscheduled preventive maintenance. Third shift has a reduced maintenance crew; major
  repairs are typically deferred to first shift unless the equipment is critical-path.
- Maintenance Supervisor: Escalation for multi-machine failures, procurement of emergency parts, or
  decisions to take equipment out of service for extended repair.

Safety Escalation:
- Safety Officer: Contact for any imminent hazard, serious near-miss, or chemical release. The Safety
  Officer has unconditional stop-work authority. During second and third shifts, the shift supervisor
  acts as the safety authority until the Safety Officer is reached.
- Emergency Services: Dial 911 for life-threatening emergencies. The facility's emergency address and
  internal emergency line (ext. 9-911) are posted at every emergency station on the floor.

Management Escalation:
- Operations Manager: Escalation for production crises, major schedule impacts, or customer-critical
  situations that cannot be resolved at the supervisor level. Available by cell phone 24/7.
- Plant Manager: Ultimate escalation authority for facility-wide issues, regulatory notifications,
  or significant safety events requiring public or customer communication.

All escalation contacts and after-hours phone numbers are posted on the shift supervisor board, in each
bay break room, and in the facility emergency response plan binder.
"""

