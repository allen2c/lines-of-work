content = """
Metal manufacturing processes generate sustained high noise levels and significant vibration exposure
through forging, grinding, shot blasting, and press operations. Ridgecrest Metalworks maintains a
hearing conservation program in compliance with OSHA 29 CFR 1910.95 and manages hand-arm vibration
risks through equipment selection and exposure controls.

Noise Monitoring:
Industrial hygiene noise surveys are conducted annually and whenever a new noise-generating process
is introduced. Noise levels in the foundry and forging bays routinely exceed 90 dBA as an 8-hour
time-weighted average (TWA). Areas where the TWA reaches or exceeds 85 dBA are designated as Hearing
Protection Zones and are marked with posted warning signs and yellow floor boundary markings.

Hearing Conservation Program:
Employees with noise exposures at or above 85 dBA TWA are enrolled in the hearing conservation program,
which includes:
- Annual audiometric testing to detect early shifts in hearing threshold
- Hearing protection training and fitting
- Access to employer-provided hearing protection at no cost

Hearing Protection Options:
The facility provides foam earplugs (NRR 29), banded earplugs for frequent donning and doffing, and
earmuff-style protectors (NRR 25) suitable for use in the forging and blast areas. For environments
exceeding 100 dBA, dual protection (earplugs plus earmuffs) is required. The effective protection of
dual hearing protection is approximated by adding 5 dB to the higher-rated device's NRR.

Engineering controls for noise are the primary approach where feasible: acoustic enclosures around
high-noise equipment, vibration-dampening mounts under hammers and presses, and sound-absorbing panels
in the machining center ceiling reduce overall ambient noise levels.

Hand-Arm Vibration:
Hand-arm vibration syndrome (HAVS) can develop from prolonged use of vibrating tools such as grinders,
chipping hammers, and pneumatic scaling tools. Controls include: limiting daily vibration exposure
time, rotating workers between vibrating and non-vibrating tasks, selecting low-vibration tool models,
and requiring anti-vibration gloves when vibration cannot otherwise be reduced. Employees should report
tingling, numbness, or whitening of fingers to occupational health promptly.

Whole-body vibration from forklift operation on uneven floor surfaces is managed by maintaining smooth
floor surfaces in high-traffic areas and ensuring forklift seats have functional suspension.
"""

