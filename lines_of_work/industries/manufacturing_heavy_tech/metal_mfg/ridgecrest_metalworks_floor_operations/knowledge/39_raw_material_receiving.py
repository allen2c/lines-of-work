title = "Raw Material Receiving and Inspection Procedures"

content = """
Raw material receiving and inspection is the first critical quality gate in Ridgecrest Metalworks'
production process. Materials that do not meet specification requirements must be identified and
segregated before they enter the production flow — catching a non-conforming material at receiving
is far less costly than discovering it after machining or heat treatment.

Receiving Workflow:
1. Material is delivered to the receiving dock and off-loaded by the receiving team.
2. The receiving technician checks the delivery against the Purchase Order (PO): confirms quantity,
   description, and heat/lot number match the shipping documentation.
3. Visual inspection is performed: check for physical damage during shipping (dents, cracks, bent
   stock), corrosion or contamination, and condition of packaging for moisture-sensitive materials.
4. Documentation review: mill certificate (Material Test Report, MTR) must accompany all structural
   and aerospace-grade materials. The MTR is reviewed for chemical composition and mechanical test
   results against the applicable material specification.
5. Dimensional check: a sample of incoming material is measured for compliance with specified
   dimensions (bar diameter, plate thickness, etc.) using calibrated measuring instruments.

Material Sampling:
Ridgecrest uses a sampling plan for dimensional and chemical verification of incoming material.
Standard lots use ANSI/ASQ Z1.4 sampling for attribute inspections. For critical aerospace materials,
100% heat number verification is required.

Positive Material Identification (PMI):
For alloy steels, stainless steels, and special alloys, positive material identification using X-ray
fluorescence (XRF) analysis may be required per the customer's purchase order or the facility's internal
receiving procedure. XRF instruments are maintained in the receiving and inspection department and
must be calibrated before use.

Non-Conforming Material Disposition:
Material that fails any receiving inspection criterion must be tagged with a red Non-Conforming
Material (NCM) tag and placed in the designated NCM hold area — a physically segregated zone in the
receiving dock. The quality department is notified immediately. Disposition options include: return
to supplier, supplier concession with documented acceptance, or rejection and replacement.

Material accepted at receiving is tagged with a green Accepted tag and moved to the appropriate
storage area with the MTR documents attached or entered into the traceability system.
"""

