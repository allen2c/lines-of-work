title = "Metal Cleaning and Degreasing Before Processing"

content = """
Metal surfaces must be clean and free of oils, oxides, cutting fluid residues, drawing compounds, and
other contaminants before they undergo heat treatment, welding, plating, inspection, or precision
machining. Inadequate cleaning causes heat treat scaling, weld porosity, plating adhesion failures,
and dimensional errors from contamination on critical surfaces.

Cleaning Processes at Ridgecrest:

Solvent Degreasing:
Hydrocarbon-based solvents and low-VOC alternative solvents (such as acetone, isopropyl alcohol,
or specialized aqueous-alkaline cleaners) are used for wipe-down cleaning of individual parts. Solvent
degreasing rapidly removes oils and cutting fluids from machined surfaces. All solvent cleaning must
be performed with adequate ventilation; flammable solvents must be stored in approved safety cans and
used away from ignition sources.

Aqueous Alkaline Cleaning:
Heated alkaline cleaning solutions (typically at 60–80°C) are used in immersion or spray washer
systems for batch cleaning of machined parts before heat treatment. Alkaline cleaners saponify (break
down) oils and emulsify them for rinsing. Parts must be adequately rinsed with clean water after
alkaline cleaning to prevent alkaline residue from causing staining during heat treatment.

Acid Pickling:
Dilute hydrochloric or sulfuric acid solutions remove scale (iron oxide) and mill scale from steel
surfaces before plating, hot dip galvanizing, or welding. Pickling is performed in the designated
chemical processing area with full corrosive PPE and local exhaust ventilation. Over-pickling (excessive
time in acid) can cause hydrogen embrittlement in high-strength steels; time and temperature must be
controlled per the work instruction.

Abrasive Cleaning:
Shot blasting, glass bead blasting, and aluminum oxide grit blasting remove scale, rust, and surface
defects mechanically. Abrasive blasting creates the surface anchor profile needed for good paint
adhesion. The media type, size, and blast pressure are specified on the part routing to achieve the
required surface profile and cleanliness level per SSPC standards.

Ultrasonic Cleaning:
Used in the inspection and plating prep areas for precision parts where surface contamination must
be removed from blind holes and complex geometries. High-frequency ultrasonic energy in an aqueous
cleaning solution generates cavitation bubbles that mechanically scrub contaminants from surfaces.
"""

