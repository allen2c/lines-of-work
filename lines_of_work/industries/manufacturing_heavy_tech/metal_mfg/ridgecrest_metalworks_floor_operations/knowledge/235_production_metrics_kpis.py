title = "Key Production KPIs: Throughput, Yield, and Cycle Time"

content = """
Key performance indicators (KPIs) allow Ridgecrest Metalworks to measure production health objectively, identify trends, and hold teams accountable to operational targets. The following KPIs are tracked at cell, department, and facility levels.

**Throughput.** Throughput is the number of conforming units produced and shipped per unit of time. It is the ultimate measure of a production system's output. Throughput is measured in parts per shift, per day, and per week, compared against the planned output from the MPS. A throughput below plan indicates a capacity constraint, quality problem, or scheduling issue that must be investigated.

**First-pass yield (FPY).** FPY measures the percentage of parts that complete all operations conforming on the first attempt without rework:
  FPY = (Parts completed with no rework or scrap) / (Total parts started) × 100%
  Cell FPY targets at Ridgecrest are 97% or higher. FPY below 95% triggers a root-cause investigation and corrective action.

**Scrap rate.** Scrap rate = (Scrap pieces × material cost) / Total material cost processed. Scrap is tracked by operation and by defect code. A Pareto of scrap codes drives the improvement agenda.

**Cycle time.** Cycle time is the average time to complete one unit through an operation or through the entire routing. It is compared against the standard cycle time established by industrial engineering. Actual cycle time exceeding standard by more than 10% is a flag for investigation (operator, machine, material, or method issue).

**Manufacturing lead time.** Total elapsed calendar time from work order release to finished goods receipt. Reducing manufacturing lead time improves delivery responsiveness and reduces WIP inventory.

**Schedule adherence.** Percentage of work orders completed in the period they were scheduled:
  Schedule Adherence = (Work orders completed on schedule) / (Total work orders scheduled) × 100%
  Target is 90% or higher weekly.

**On-time delivery (OTD).** Percentage of customer shipments delivered on or before the customer-requested date. OTD is the customer-facing measure of overall production system performance. Ridgecrest's OTD target is 95%.

**Labor efficiency.** Actual labor hours per piece vs. standard labor hours per piece. Efficiency below 85% on a repeated basis indicates a training, method, or tooling issue.

All KPIs are posted on the department visual performance board and reviewed in the daily production meeting.
"""

