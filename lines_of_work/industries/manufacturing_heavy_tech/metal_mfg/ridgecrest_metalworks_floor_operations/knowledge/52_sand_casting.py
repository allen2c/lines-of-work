title = "Sand Casting: Process and Quality Considerations"

content = """
Sand casting is the most versatile and widely used casting process in heavy metal manufacturing, and
it is the primary foundry method at Ridgecrest Metalworks. It is compatible with virtually all castable
alloys, imposes minimal constraints on casting geometry, and can produce parts from ounces to many
tons. The trade-off is moderate dimensional tolerance and surface finish compared to precision casting
methods.

Process Steps:

Pattern Fabrication: A pattern — a replica of the part with dimensional allowances for shrinkage and
machining stock — is made from wood, urethane, aluminum, or steel. The pattern is used to create the
mold cavity impression in sand.

Mold Making: The pattern is placed in a two-part flask (cope and drag). Bonded molding sand (typically
green sand, a mixture of silica sand, clay, and water, or chemically bonded sand using resin binders)
is packed around the pattern and compacted. The pattern is then drawn (removed), leaving the mold cavity.

Core Setting: If the casting has internal voids, hollow sections, or passages, sand cores are placed
in the mold before it is closed. Cores are made from bonded sand in core boxes matching the internal
shape required.

Mold Closure and Venting: The cope and drag are assembled and secured. Vent pins create channels for
gas to escape during pouring, preventing gas porosity defects.

Pouring: Molten metal at the specified pour temperature is transferred from the furnace via ladle and
poured through the sprue opening at a controlled rate to minimize turbulence. Turbulent filling
entrains air and slag, causing surface defects and porosity.

Solidification and Shakeout: After the metal solidifies sufficiently, the flask is opened and the sand
mold is broken away (shakeout). The rough casting is separated from the gating system.

Quality Considerations:
Key quality variables in sand casting include: sand permeability and strength (low permeability traps
gas), moisture content (excess moisture causes steam explosions and porosity), pour temperature
(too low causes misruns; too high increases shrinkage and gas absorption), and gating design (affects
filling velocity and turbulence level). First-article castings undergo full dimensional inspection
and radiographic or ultrasonic testing to verify internal soundness.
"""

