title = "测量系统分析（MSA）简介"

content = """
- MSA评估测量系统的重复性和再现性（GR&R）。首件检验员需了解量具的偏倚、线性、稳定性。
- 重复性：同一检验员用同一量具多次测量同一工件，测量值变异。要求GR&R%≤10%为优秀，10%-30%可接受，>30%需改进。
- 再现性：不同检验员用同一量具测量同一工件，测量值变异。通常与重复性合并计算。
- 偏倚：测量平均值与参考值之差。用标准件校准可消除偏倚，偏倚应≤量具分辨力的1/10。
- 日常检查：每月用标准件验证量具偏倚，记录在《量具校准台账》中。若偏倚超差，立即停用并送修。
"""

