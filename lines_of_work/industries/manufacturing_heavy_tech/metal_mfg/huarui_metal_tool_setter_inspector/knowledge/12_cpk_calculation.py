title = "过程能力指数（Cpk）计算"

content = """
- Cpk衡量过程满足公差的能力。公式：Cpk = min( (USL-μ)/(3σ), (μ-LSL)/(3σ) )，其中USL/LSL为上下公差，μ为过程均值，σ为过程标准差。
- 计算步骤：收集至少25个子组数据（每个子组n=5），计算总均值μ和标准差σ（用R̄/d2估计，d2查表n=5时d2=2.326）。
- 示例：某尺寸公差±0.05mm（USL=0.05，LSL=-0.05），μ=0.01mm，σ=0.012mm，则Cpk=min((0.05-0.01)/(3*0.012), (0.01+0.05)/(3*0.012)) = min(1.11, 1.67)=1.11。
- 判定标准：Cpk≥1.33表示过程能力充足；1.0≤Cpk<1.33需改进；Cpk<1.0过程能力不足，必须调整。
- 首件检验中，若Cpk<1.33，需分析原因（刀具、参数、装夹），调整后重新首件。
"""

