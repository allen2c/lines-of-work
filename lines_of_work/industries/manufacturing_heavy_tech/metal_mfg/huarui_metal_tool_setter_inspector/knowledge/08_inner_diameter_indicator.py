title = "内径量表使用"

content = """
- 内径量表（如三爪内径千分尺或杠杆式内径百分表）用于测量孔径，精度0.01mm或0.001mm。
- 使用前用标准环规（或外径千分尺配合量块）校准零位。将量表放入环规，摆动找到最小值，调整表盘指针归零。
- 测量时，将量表放入孔内，沿轴向和径向摆动，读取最小示值（即实际孔径）。每个截面测量两个垂直方向，取平均值。
- 注意：量表测头需与孔壁垂直，避免倾斜导致读数偏大。测量深度应大于孔深的一半。
- 常见问题：测头磨损、弹簧失效导致回程误差>0.005mm，需更换或送修。使用后清洁测头，轻放。
"""

