title = "切削参数调整（转速、进给、切深）"

content = """
- 转速S（r/min）：根据刀具直径D（mm）和切削速度Vc（m/min）计算：S=1000*Vc/(π*D)。例如：硬质合金铣刀加工钢件，Vc=80-120m/min，D=10mm，则S≈2546-3820r/min。
- 进给F（mm/min）：F=fz*Z*S，其中fz为每齿进给量（mm/z），Z为齿数。精加工fz=0.02-0.05mm/z，粗加工fz=0.08-0.15mm/z。
- 切深Ap（mm）：粗加工Ap=1-3mm，精加工Ap=0.1-0.5mm。首件试切时，建议先取中间值，测量后微调。
- 调整原则：若表面粗糙度差，降低进给或提高转速；若刀具磨损快，降低转速或切深；若振动大，降低切深或调整转速避开共振区。
- 注意：调整后必须重新试切首件，确认尺寸和表面质量。严禁在批量加工中随意更改参数。
"""

