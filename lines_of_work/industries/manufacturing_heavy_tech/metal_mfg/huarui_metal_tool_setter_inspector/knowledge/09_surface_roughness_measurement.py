title = "粗糙度测量"

content = """
- 使用便携式粗糙度仪（如Mitutoyo SJ-210），触针式，测量参数Ra、Rz。Ra常用范围0.2-6.3μm。
- 测量前清洁工件表面，无油污、切屑。选择测量方向垂直于加工纹理（车削沿轴向，铣削沿进给方向）。
- 设置取样长度（λc）：通常为0.8mm（Ra 0.1-2.0μm）或2.5mm（Ra 2.0-10μm）。评定长度取5个取样长度。
- 测量步骤：将粗糙度仪置于工件表面，按下启动键，触针移动，读取Ra值。每个测量点测三次，取平均值。
- 判定：图纸要求Ra≤1.6μm，若实测Ra>1.6μm，需检查刀具磨损、切削参数或冷却液。粗糙度超差需重新加工。
"""

