title = "刀具长度补偿设置"

content = """
- 在FANUC系统（或Siemens）中，刀具长度补偿通过G43 H__指令调用。H值对应刀具长度偏置寄存器。
- 设置方法：将刀具装入主轴，移动机床使刀尖接触工件表面（或对刀块），记录机床坐标Z值，减去工件坐标系Z0值，得到长度补偿值。输入对应H寄存器，单位mm，保留小数点后三位。
- 使用对刀仪时，直接读取长度值，输入H寄存器。注意：对刀仪测量的是刀尖到刀柄基准面的距离，需与机床参考点一致。
- 补偿值验证：试切一个平面，用千分尺测量实际厚度，若偏差>0.01mm，需重新对刀。每把刀首次使用必须验证。
- 多把刀补偿时，确保每把刀的H值对应正确刀号。换刀后需重新确认补偿值，尤其是刀具磨损或重磨后。
"""

