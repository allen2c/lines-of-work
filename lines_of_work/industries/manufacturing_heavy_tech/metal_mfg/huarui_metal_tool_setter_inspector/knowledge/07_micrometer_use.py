title = "千分尺使用与读数"

content = """
- 外径千分尺（分度值0.001mm）测量范围0-25mm、25-50mm等。使用前用标准棒（或量块）校准零位，误差≤0.002mm。
- 测量步骤：清洁工件测量面与千分尺测砧、测微螺杆；旋转棘轮至发出“咔咔”声（测力约5-10N）；读取数值：固定套筒上的毫米刻度+半毫米刻度+微分筒上的0.001mm刻度。
- 示例：固定套筒露出12mm，半毫米刻度可见（0.5mm），微分筒刻度35（0.035mm），总读数12.535mm。
- 注意：千分尺需恒温（20±1°C），手持时避免手温传递。测量时工件应静止，不可旋转。
- 维护：使用后清洁并涂防锈油，放入专用盒内。每年送检一次，校准证书保存备查。
"""

