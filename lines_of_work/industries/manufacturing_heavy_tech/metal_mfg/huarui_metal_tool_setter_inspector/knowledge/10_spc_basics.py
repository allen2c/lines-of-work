content = """
- SPC（统计过程控制）用于监控过程稳定性。首件检验中常用均值-极差控制图（Xbar-R图）。
- 子组大小：通常取n=5（连续5件或同一批次5个测量点）。每批次首件测量一个子组，但首件只有一件，故实际中常将同一批次不同位置或重复测量作为子组。
- 控制限计算：Xbar图的中心线CL=X̄（所有子组均值的均值），UCL=X̄+A2*R̄，LCL=X̄-A2*R̄；R图的CL=R̄，UCL=D4*R̄，LCL=D3*R̄。A2、D3、D4查表（n=5时A2=0.577，D3=0，D4=2.114）。
- 判异准则：点超出控制限；连续7点同侧；连续7点上升或下降；点靠近控制限（2/3点落在±2σ内）。出现任一异常需分析原因。
- 记录：每批次首件数据填入SPC表格，绘制控制图。若过程受控，允许批量生产；否则暂停，调整后重新首件。
"""

