title = "对刀仪使用规范"

content = """
- 对刀仪（如ZOLLER或国产型号）精度为0.001mm，每次使用前需用标准棒（直径20mm，长度100mm）进行零点校准。
- 校准步骤：清洁标准棒与对刀仪测头，将标准棒装入主轴，移动测头接触标准棒端面，按“清零”键。重复三次，偏差≤0.003mm方可使用。
- 测量刀具时，将刀具装入刀柄，测头接触刀尖（或刀片切削刃），读取长度和半径值。每把刀测量两次，取平均值，两次差值>0.005mm需重新测量。
- 对刀仪环境温度要求20±2°C，湿度≤60%，避免气流干扰。若温度波动超过±1°C，需等待30分钟稳定后再使用。
- 每日下班前清洁对刀仪测头及导轨，并记录使用日志。发现测头磨损或损坏立即报修。
"""

