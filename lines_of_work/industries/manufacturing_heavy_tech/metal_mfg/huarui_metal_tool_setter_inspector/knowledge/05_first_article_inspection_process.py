title = "首件检验流程"

content = """
- 接收任务：从工艺工程师处获取《首件检验指导书》，包含图纸、公差要求、测量点位置（至少5个关键尺寸）。
- 准备量具：根据公差选择量具（公差≤0.05mm用千分尺，>0.05mm用卡尺；内径用内径量表；粗糙度用粗糙度仪）。
- 测量顺序：先测量外径/长度等易测尺寸，再测量内径、位置度、形位公差。每个尺寸测量三次，取平均值。
- 记录：在《首件检验记录表》中填写实测值、判定结果（合格/不合格）、测量人、日期。超差尺寸用红笔标注。
- 判定标准：所有尺寸在公差范围内，且表面粗糙度Ra≤1.6μm（图纸要求），则首件合格。若任一尺寸超差，判定不合格，启动升级流程。
"""

