"""TRS et OEE : indicateurs de performance des équipements."""

title = "TRS et OEE"

content = """
Le TRS (Taux de Rendement Synthétique) et l'OEE (Overall Equipment Effectiveness)
mesurent l'efficacité réelle des équipements. TRS = disponibilité × performance ×
qualité. Disponibilité = temps utile / temps requis ; performance = cadence réelle /
cadence nominale ; qualité = pièces conformes / pièces produites.

**Utilisation** : Identifier les pertes (pannes, réglages, rebuts), prioriser les
actions d'amélioration et comparer les équipements. Un TRS industriel cible est
souvent > 75 %.

**Limites** : Ne pas confondre taux élevé et surproduction ; le TRS doit servir
l'objectif client, pas gonfler les stocks.
"""  # noqa: E501

