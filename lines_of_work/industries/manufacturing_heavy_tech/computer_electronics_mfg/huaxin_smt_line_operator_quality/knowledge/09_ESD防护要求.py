title = "静电防护（ESD）操作规范"

content = """
- 个人防护：进入车间必须穿防静电服、防静电鞋（鞋跟电阻≤35MΩ）、佩戴ESD腕带（腕带与皮肤接触良好，阻值0.75-1MΩ）。每班次开始前用腕带测试仪自检，不合格者禁止上岗。
- 工作台：所有工作台面铺设防静电垫（接地电阻≤1MΩ），台面离子风机持续运行（风速1.5-2.5m/s，离子平衡度±10V）。操作员每2小时用静电测试仪检查台面电压（目标＜50V）。
- 物料处理：PCB板、IC、敏感元件必须使用防静电托盘或防静电袋。禁止将未封装元件直接放在普通桌面上。焊接工具（烙铁）必须接地，漏电流＜0.5mA。
- 违规处理：发现ESD违规（如未戴腕带、使用普通塑料袋）立即纠正并记录，累计3次违规需重新培训。
"""

