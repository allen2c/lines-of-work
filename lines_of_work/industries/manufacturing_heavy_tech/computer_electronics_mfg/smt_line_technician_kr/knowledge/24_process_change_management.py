content = """
- ECN(Engineering Change Notice) 또는 ECO(Engineering Change Order)에 따라 공정 변경 수행.
- 변경 유형: 스텐실 설계, 리플로우 프로파일, 솔더 페이스트 종류, 부품 대체, 장비 교체.
- 변경 절차: ECN 접수 → 영향 평가(품질, 수율, 비용) → 초물 검사(FAI) → 고객 승인(필요 시) → 양산 적용.
- 변경 전후 비교: 30개 샘플(변경 전/후)로 불량률, Cpk, 위양성률 비교. 차이가 유의미하면 변경 승인.
- 변경 이력: MES에 변경 번호, 적용 일자, 작업자, 결과 기록. 이전 설정은 6개월간 보관.
- 긴급 변경(불량 대응): 임시 변경 후 24시간 이내 정식 ECN 발행. 승인 없이 임시 변경 지속 금지.
"""

