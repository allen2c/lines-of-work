title = "Planificación de capacidad"

content = """
La planificación de capacidad asegura que el MPS y el MRP sean factibles con los
recursos disponibles (máquinas, mano de obra, turnos).

**Niveles:** Rough-cut valida el MPS con factores de carga por familia; capacity
requirements planning (CRP) valida el plan detallado por centro de trabajo y periodo.
Si la capacidad no es suficiente, se replanifica (retrasos, cambios de mix, overtime)
o se escapa a dirección.

**Indicadores:** Utilización (%), horas disponibles vs cargadas, cola de trabajo.
Los cuellos de botella limitan el throughput global; identificarlos es prioritario para
decisiones de nivelación o inversión.
"""  # noqa: E501

