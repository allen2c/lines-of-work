title = "Impacto de ECO en planificación"

content = """
Un ECO (Engineering Change Order) modifica diseño, BOM o proceso. Planificación debe
gestionar la transición: qué órdenes usan la versión antigua y cuáles la nueva, cuándo
agotar stock de componentes obsoletos, cómo comunicar a compras y planta.

**Efectividad de fechas:** Los ECO suelen tener fecha de implantación. Antes de esa
fecha se usa la BOM/ruta antigua; después, la nueva. Órdenes en curso deben completarse
según la versión con que se liberaron o ajustarse si es factible.

**Riesgos:** Componentes obsoletos que quedan en stock, mezcla de versiones en producto
final, confusión en planta. Planificación coordina con ingeniería para un corte limpio.
"""  # noqa: E501

