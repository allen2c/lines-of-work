title = "Mix de producto y planificación"

content = """
El mix es la proporción de diferentes productos o familias en el plan. Cambiar el mix
afecta la carga por centro de trabajo (cada producto tiene rutas y tiempos distintos)
y los materiales necesarios.

**Desafío:** La demanda no suele ser proporcional a la capacidad. Un mix desequilibrado
puede sobrecargar unos centros y dejar otros ociosos. Planificación debe evaluar el
mix propuesto contra la capacidad y proponer ajustes (cambiar secuencia, adelantar
o retrasar familias, negociar plazos con ventas).

**Herramientas:** Matrices producto–centro, análisis de utilización por familia.
En S&OP se discute el mix agregado; en planificación detallada se afina.
"""  # noqa: E501

