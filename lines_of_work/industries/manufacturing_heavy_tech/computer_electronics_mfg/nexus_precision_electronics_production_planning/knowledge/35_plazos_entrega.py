title = "Plazos de entrega"

content = """
El plazo de entrega (lead time) al cliente es el tiempo desde la confirmación del
pedido hasta la entrega. Incluye fabricación, inspección, embalaje y logística.
Planificación debe respetar estos plazos al comprometer fechas.

**Cálculo:** Sumar lead times de materiales críticos, tiempo de fabricación, buffer
de calidad y logística. En make-to-order puede ser mayor; en make-to-stock depende
del inventario.

**Comunicación:** Ventas debe conocer los plazos estándar y las condiciones para
acelerar (si hay capacidad y materiales). Promesas por debajo del plazo real generan
insatisfacción y presión innecesaria en planta.
"""  # noqa: E501

