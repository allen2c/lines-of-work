title = "Utilización de capacidad"

content = """
La utilización es el ratio entre horas cargadas (trabajo planificado o ejecutado) y
horas disponibles en un centro. Ejemplo: 80h cargadas / 100h disponibles = 80%.

**Interpretación:** Utilización muy baja indica capacidad ociosa o plan desalineado;
muy alta (sobre 90-95% sostenido) indica riesgo de cuello de botella y retrasos.
El objetivo depende del tipo de centro y de la estrategia (flexibilidad vs coste).

**Desglose:** Por centro, por periodo, por familia. Identificar centros con utilización
crónica alta o muy baja ayuda a priorizar mejoras y decisiones de inversión.
"""  # noqa: E501

