title = "Sistemas pull"

content = """
En un sistema pull, la producción se activa por consumo posterior: cuando un proceso
posterior retira material, se dispara la fabricación o suministro del proceso anterior.
El flujo está controlado por la demanda real, no por un plan rígido.

**Kanban:** Tarjetas o señales que autorizan la producción o reabastecimiento cuando
un contenedor se vacía. El número de kanbans limita el WIP.

**Relación con planificación:** El MPS/MRP define qué y cuánto fabricar; pull regula
el cuándo y el ritmo en planta. En entornos híbridos, planificación emite OF pero
el flujo interno puede ser pull. Comprender el modo de operación evita duplicar o
contradecir señales.
"""  # noqa: E501

