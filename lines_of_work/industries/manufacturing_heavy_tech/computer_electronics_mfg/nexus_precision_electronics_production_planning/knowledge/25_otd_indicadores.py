title = "Indicador OTD (On-Time Delivery)"

content = """
OTD mide el porcentaje de entregas que llegan al cliente en la fecha comprometida.
Es un KPI central en planificación y operaciones; afecta la satisfacción del cliente
y la confianza comercial.

**Cálculo:** Entregas a tiempo / Total entregas en el periodo, típicamente por cliente,
por familia o global. Puede medirse en unidades o en líneas de pedido.

**Causas de fallo:** Retrasos de materiales, cuellos de botella, problemas de calidad,
cambios de prioridad mal gestionados. Planificación debe analizar causas raíz y proponer
acciones correctivas. Objetivos OTD suelen estar entre 95-99% según sector.
"""  # noqa: E501

