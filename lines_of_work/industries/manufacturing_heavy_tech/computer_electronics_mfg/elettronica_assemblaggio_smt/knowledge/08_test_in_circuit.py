title = "Test in-circuit (ICT)"

content = """
- L'ICT verifica la continuità elettrica, i valori di resistenza, capacità e diodi, e l'assenza di cortocircuiti.
- Utilizza una sonda a letto di aghi (bed-of-nails) o a pinze mobili (flying probe). Tempo di test: 10-60 secondi per scheda.
- Soglie: resistenza di corto < 10 Ω, resistenza di aperto > 1 MΩ, tolleranza componenti passivi ±5% (resistori) o ±10% (condensatori).
- Se il tasso di fallimento ICT supera il 2%, eseguire un'analisi di pareto per identificare i difetti più frequenti.
"""

