title = "Rückverfolgbarkeit und Chargenlenkung"

content = """
Rückverfolgbarkeit ermöglicht, die Herkunft von Materialien und Bauteilen
bis zu Charge, Charge oder Los zurückzuverfolgen. Bei Reklamationen oder
Rückrufen essentiell.

**Anforderungen:** IATF und OEMs fordern Rückverfolgbarkeit für sicherheits-
und gesetzesrelevante Merkmale. Ausdehnung kundenspezifisch. Mindestens
Los- oder Chargenebene.

**Umsetzung:** Chargen-/Loskennzeichnung am Produkt oder Verpackung.
Zuordnung zu Materialchargen, Produktionsdatum, Schicht. Verknüpfung in
ERP/MES. Schnelle Abfrage bei Reklamation ermöglichen.

**Speicherung:** Aufzeichnungen aufbewahren gemäß Aufbewahrungsfrist.
Bei elektronischer Speicherung: Backup, Zugriff bei Bedarf sicherstellen.

**Rückruf:** Bei Produktrückruf: betroffene Lots identifizieren, Kunden
und Behörden informieren. Rückverfolgbarkeit entscheidend für Umfang.
"""

