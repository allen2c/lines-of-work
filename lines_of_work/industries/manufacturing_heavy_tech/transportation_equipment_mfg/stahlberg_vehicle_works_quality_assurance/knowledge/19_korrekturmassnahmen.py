title = "Korrekturmaßnahmen (CA)"

content = """
Korrekturmaßnahmen beseitigen die Ursache eines aufgetretenen Fehlers, um
Wiederholung zu verhindern. Sie unterscheiden sich von Sofortmaßnahmen,
die nur das Symptom adressieren.

**Auslöser:** Reklamationen, interne Fehlermeldungen, Audit-Findings,
Abweichungen in SPC, Nichtkonformitäten. Jede Abweichung prüfen: Korrektur
erforderlich?

**Prozess:** Problem beschreiben, Ursache ermitteln (5-Why, Ishikawa),
Maßnahmen definieren, umsetzen, Wirksamkeit prüfen. Dokumentation für
Audits und Management-Review.

**Wirksamkeitsprüfung:** Nach Umsetzung über angemessenen Zeitraum prüfen,
ob Problem nicht erneut auftritt. Bei Wiederholung: Ursachenanalyse
vertiefen.

**Verantwortung:** Qualitätsabteilung koordiniert, Fachbereiche setzen um.
Zeitpläne und Fristen einhalten, besonders bei Kundenreklamationen.
"""

