title = "5S — Arbeitsplatzorganisation"

content = """
5S ist eine Methode zur systematischen Ordnung und Standardisierung von
Arbeitsbereichen. Ein aufgeräumter, strukturierter Arbeitsplatz reduziert
Fehlerquellen und unterstützt Qualität sowie Effizienz.

**Seiri (Sortieren):** Unnötige Gegenstände entfernen. Nur was gebraucht wird,
bleibt am Arbeitsplatz. Rote Markierung für unklare Artikel.

**Seiton (Systematisieren):** Jeder Gegenstand hat einen festen Platz.
FIFO-Regale, Schattenborde, Kennzeichnungen. Auffinden in Sekunden.

**Seiso (Sauberhalten):** Regelmäßige Reinigung. Schmutz und Verschleiß
früh erkennen. Reinigung als Inspektionsgelegenheit nutzen.

**Seiketsu (Standardisieren):** Einheitliche Standards über alle Bereiche.
Checklisten, Foto-Standards, Verantwortlichkeiten festlegen.

**Shitsuke (Selbstdisziplin):** 5S zur Gewohnheit machen. Regelmäßige Audits,
 kontinuierliche Verbesserung. 5S ist Grundlage für Lean und Qualität.
"""

