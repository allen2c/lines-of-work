content = """
Die fünf Kerntools (Core Tools) sind die zentralen Methoden des Automotive
Qualitätsmanagements: APQP, FMEA, MSA, SPC und PPAP. Sie werden gemeinsam
eingesetzt und verknüpft.

**APQP:** Rahmen für die gesamte Qualitätsplanung. Steuert den Ablauf und
die Erstellung der anderen Dokumente. Zeitlicher und inhaltlicher Fahrplan.

**FMEA:** Risikoanalyse für Design und Prozess. Liefert Inputs für Control
Plan und Prüfpläne. Bei Änderungen aktualisieren.

**MSA:** Sichert Messsysteme. Vor SPC und Fähigkeitsstudien: Messsystem
muss geeignet sein.

**SPC:** Überwacht Prozesse laufend. Im Control Plan als Steuerungsmethode.
Frühwarnung bei Prozessänderung.

**PPAP:** Freigabe für Serienproduktion. Fasst alle Outputs zusammen.
Ohne PPAP keine Serienbelieferung.
"""

