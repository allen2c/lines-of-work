content = """
Audit-Findings sind festgestellte Abweichungen oder Verbesserungspotenziale.
Klassifizierung steuert Dringlichkeit und Umfang der Reaktion.

**Klassen:** Major (schwerwiegend, Systemlücke), Minor (einzelne Abweichung),
OFI (Opportunity for Improvement, Empfehlung). IATF und VDA haben eigene
Klassifikationsschemata.

**Reaktion:** Major: sofortige Korrekturmaßnahme, Root-Cause-Analyse,
 Follow-up-Audit. Minor: Korrektur innerhalb Frist. OFI: optional, aber
sinnvoll.

**Stellungnahme:** Bei externen Audits oft schriftliche Stellungnahme zu
Findings. Korrekturmaßnahmen beschreiben, Termine nennen. Fristen einhalten.

**Schließung:** Nachweis der Umsetzung. Auditor oder Zertifizierer bestätigt
Schließung. Bei Major: erneutes Audit nötig.
"""

