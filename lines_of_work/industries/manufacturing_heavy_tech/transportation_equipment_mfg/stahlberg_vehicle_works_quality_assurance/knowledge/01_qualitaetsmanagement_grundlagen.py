content = """
Qualitätsmanagement umfasst alle systematischen Tätigkeiten zur Planung,
Lenkung und Verbesserung von Qualität. Im Automotive-Bereich zielt QM darauf,
Produkte und Prozesse so zu gestalten, dass Kundenanforderungen zuverlässig
erfüllt werden.

**Qualitätsbegriff:** Qualität ist die Gesamtheit von Merkmalen eines Produkts
oder einer Tätigkeit bezüglich ihrer Eignung, festgelegte und vorausgesetzte
Erfordernisse zu erfüllen (DIN EN ISO 9000). Im Fahrzeugbau schließt das
Funktionalität, Zuverlässigkeit, Haltbarkeit und Kundenzufriedenheit ein.

**QM-Prinzipien:** Kundenspezifischer Fokus, Führung, Einbindung von Menschen,
Prozessorientierung, Verbesserung, faktenbasierte Entscheidungsfindung,
Beziehungsmanagement. Diese Prinzipien leiten alle QM-Aktivitäten bei Stahlberg.

**PDCA-Zyklus:** Plan-Do-Check-Act strukturiert kontinuierliche Verbesserung.
Planung umfasst Zielsetzung und Maßnahmen, Do die Umsetzung, Check die
Bewertung der Ergebnisse, Act die Anpassung und Standardisierung.

**Integration:** QM ist keine isolierte Abteilung, sondern in alle Prozesse
integriert. Von der Entwicklung über Einkauf und Produktion bis zur Auslieferung.
"""

