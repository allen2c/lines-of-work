content = """
Die Wareneingangsprüfung stellt sicher, dass eingehende Materialien und
Bauteile den Spezifikationen entsprechen, bevor sie in die Produktion gelangen.
Fehlerhafte Ware wird abgewiesen oder gesondert behandelt.

**Prüfumfang:** Abhängig von Lieferantenbewertung, Produktkritikalität und
Historie. Kann von Stichproben über 100-Prozent-Prüfung bis zur Freigabe
ohne Prüfung (bei qualifizierten Lieferanten) reichen.

**Dokumentation:** Prüfpläne definieren Merkmale, Methoden, Stichproben.
Prüfberichte dokumentieren Ergebnisse. Bei Abweichung: Sperrung, 8D,
Rücksendung oder Sonderfreigabe mit Begründung.

**Materialzertifikate:** Für kritische Werkstoffe 3.1 oder 3.2 nach EN 10204.
Prüfung auf Vollständigkeit und Plausibilität der Angaben.

**Durchlaufzeit:** Wareneingang und Prüfung müssen zeitnah erfolgen, um
Produktionsstillstand zu vermeiden. FIFO beim Lagern geprüfter Ware.
"""

