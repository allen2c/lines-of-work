content = """
Fertigungsprüfungen erfolgen während des Produktionsprozesses. Sie erkennen
Abweichungen früh und verhindern, dass Fehler weiterlaufen oder sich
akkumulieren. Reaktionszeiten sind kürzer als bei Endprüfung.

**Prüfpunkte:** Im Control Plan und Prozessablauf definiert. Typisch nach
kritischen Prozessschritten, vor nicht rückgängig machbaren Operationen,
an Prozessübergängen.

**Methoden:** Erstprüfung durch Maschinenbediener, Stichproben durch
Qualitätsprüfer, automatische Prüfung (100-Prozent bei kritischen Merkmalen).
SPC für stetige Merkmale.

**Reaktion:** Bei Abweichung: Prozess stoppen, Ursache klären, Gegenmaßnahmen,
 eventuell Nacharbeit oder Ausschuss. Dokumentation für 8D und FMEA-Update.

**Fertigungsunterbrechung:** Nach Stillstand oder Werkzeugwechsel oft
verstärkte Prüfung bis Prozessstabilität bestätigt ist.
"""

