content = """
Umweltprüfungen simulieren Nutzungsbedingungen (Temperatur, Feuchte,
Vibration, Korrosion) und validieren Produktqualität unter realistischen
Bedingungen. Für Fahrzeugkomponenten häufig gefordert.

**Temperaturprüfung:** Kältetest, Wärmetest, Temperaturwechsel. Prüfkammern,
 definierte Profile. DIN, ISO oder kundenspezifische Spezifikationen.

**Korrosionsprüfung:** Salzsprührnebel (z.B. DIN EN ISO 9227), Kesternich,
Feuchte-Wechselklimate. Bewertung nach definierter Skala.

**Vibrationsprüfung:** Simulation von Fahrzeugvibration. Einachsig oder
mehrachsig. Frequenz und Pegel nach Kundenanforderung.

**Dokumentation:** Prüfprotokoll mit Bedingungen und Ergebnissen.
Für PPAP und Kundenfreigabe. Bei Nichtbestanden: Ursache klären,
Maßnahmen, Wiederholung.
"""

