title = "Takt time"

content = """
Le takt time est le temps disponible pour la production divisé par la quantité demandée
sur la même période. Il définit le rythme auquel le client « tire » la production.

**Formule:** Takt = temps disponible / demande. Exemple: 27 000 s par jour, 90 pièces
demandées → takt = 300 s/pièce.

**Usage:** Aligner les temps de cycle des postes sur le takt pour éviter surproduction
et attentes. Un poste plus lent que le takt devient un goulot à traiter en priorité.
"""  # noqa: E501

