title = "Ordres de fabrication (OF)"

content = """
Un ordre de fabrication (OF) autorise et suit la production d'une quantité donnée
d'un article, sur une période donnée. Il porte une référence, une quantité, des
dates prévues et un statut (planifié, lancé, en cours, terminé).

**Cycle de vie:** Création à partir du plan, lancement sur le terrain, déclaration
d'avancement et de quantités réalisées, clôture avec rapprochement des consommations
et des sorties.

**Traçabilité:** Les OF permettent de relier les lots produits aux matières consommées
et aux opérations réalisées, requis pour la qualité et le recall.
"""  # noqa: E501

