title = "Non-conformités en production"

content = """
Une non-conformité est un écart par rapport aux spécifications ou aux procédures.
En production, elle peut concerner une pièce, un lot ou un process (défaut, paramètre
hors tolérance, procédure non suivie).

**Traitement:** Blocage des pièces concernées, enregistrement (fiche NC), analyse
cause racine et décision (rejet, rework, dérogation). La production et la qualité
collaborent pour éviter la propagation.

**Impact planning:** Les lots bloqués ou en rework peuvent retarder les livraisons;
le contrôle de production doit intégrer ces aléas dans le réplan et les promesses.
"""  # noqa: E501

