title = "Indicateurs OTD (On Time Delivery)"

content = """
L'OTD mesure le pourcentage de livraisons effectuées à la date promise (ou dans la fenêtre
convenue). C'est un indicateur clé pour la performance client et le pilotage de la production.

**Calcul:** Nombre de lignes ou commandes livrées à l'heure / total livré, sur une période
donnée. Les règles (tolérance en jours, prise en compte des reports client) doivent être
définies et stables.

**Pilotage:** Un OTD en baisse déclenche une analyse des causes (retards production,
qualité, approvisionnement) et un plan d'action.
"""  # noqa: E501

