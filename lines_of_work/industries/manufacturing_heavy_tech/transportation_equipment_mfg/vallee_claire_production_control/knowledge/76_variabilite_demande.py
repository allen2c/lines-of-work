title = "Variabilité de la demande"

content = """
La demande varie dans le temps (saisonnalité, tendance, aléas). Une forte
variabilité complique la planification et peut conduire à des surstocks
ou des ruptures si le plan n'est pas ajusté.

**Réponses:** Lissage par stock, capacité flexible (HS, intérim), réduction
des délais de réaction, collaboration avec le commercial pour lisser les
commandes. Le contrôle de production doit disposer de prévisions mises à
jour et de règles pour réagir aux variations (réplan, alertes).
"""  # noqa: E501

