title = "Penanganan Shortage"

content = """
Shortage terjadi ketika bahan tidak tersedia saat dibutuhkan produksi. Langkah
immediate: konfirmasi penyebab (delay shipment, reject, error planning), estimasi
waktu tersedia, dan komunikasi ke production planning.

**Opsi mitigasi**: Expedite shipment dari pemasok, transfer dari lokasi lain, gunakan
safety stock, substitusi dengan part alternatif (jika disetujui engineering), atau
reschedule produksi. Pilih berdasarkan biaya, risiko, dan feasibility.

**Eskalasi**: Shortage yang berpotensi line stop harus di-escalate. Dokumentasikan
setiap shortage untuk root cause analysis dan perbaikan proses.
"""  # noqa: E501

