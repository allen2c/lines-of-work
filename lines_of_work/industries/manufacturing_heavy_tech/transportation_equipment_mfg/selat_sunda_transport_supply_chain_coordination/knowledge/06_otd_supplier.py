"""On-time delivery pemasok dan pengukurannya."""

title = "OTD Pemasok"

content = """
On-time delivery (OTD) mengukur persentase pengiriman yang tiba sesuai tanggal yang
dijanjikan. Untuk manufaktur otomotif, OTD sering dihitung dengan jendela toleransi
(contoh: +/- 1 hari) atau berdasarkan requested delivery date vs actual receipt date.

**Perhitungan**: OTD = (jumlah pengiriman on-time / total pengiriman) × 100%. Catat
setiap kasus late delivery dengan alasan dan dampak. Gunakan data untuk vendor rating
dan diskusi perbaikan dengan pemasok.

**Tindakan**: Keterlambatan berulang memerlukan root cause analysis dan action plan.
Pemasok dengan OTD rendah kronis dapat dijadwalkan untuk audit atau review kontrak.
"""  # noqa: E501

