title = "Sinkronisasi Produksi"

content = """
Sinkronisasi memastikan material tiba di line-side tepat ketika dibutuhkan — tidak
terlalu awal (menumpuk) maupun terlambat (shortage). Ini memerlukan koordinasi erat
antara pengadaan, logistik, dan production planning.

**Praktik**: Gunakan delivery schedule yang selaras dengan production schedule. Untuk
JIT, arrival window bisa per shift atau per jam. Komunikasi real-time dengan pemasok
dan ekspedisi mengenai perubahan jadwal.

**Proaktif**: Jika produksi dipercepat atau ditunda, segera update pengadaan. Jika
material terlambat, cek alternatif (buffer, expedite, substitusi) sebelum line stop.
"""  # noqa: E501

