title = "Identyfikacja i zarządzanie wąskimi gardłami"

content = """
Wąskie gardło to zasób, którego zdolność produkcyjna ogranicza przepustowość całego
systemu. W planowaniu produkcji priorytetem jest maksymalne wykorzystanie wąskich gardeł.

**Identyfikacja:** Analiza obciążenia maszyn, czasów cyklu, kolejek. Wąskie gardło ma
najwyższy współczynnik wykorzystania i najdłuższą kolejkę zleceń.

**Harmonogramowanie:** Zlecenia na wąskim gardle planuje się najpierw. Pozostałe zasoby
dostosowuje się do tego harmonogramu.

**Buforowanie:** Przed wąskim gardłem utrzymuje się bufor materiałowy, aby uniknąć
przestojów z powodu braku pracy. Za wąskim gardłem bufor jest mniejszy.

**Eskalacja:** Gdy wąskie gardło nie może sprostać popytowi, należy eskalować do kierownictwa
produkcji — możliwe rozszerzenie zdolności, outsourcing lub zmiana harmonogramu klienta.
"""

