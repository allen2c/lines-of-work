title = "品質與交期的權衡"

content = """
生產壓力下，品質與交期可能產生衝突。品質單位須堅守原則：品質疑慮未排除
前不允出貨。同時，積極協助縮短品質問題的解決週期，減少對交期的影響。

**不可妥協的情境：** 安全相關缺陷、法規不符合、客戶明確拒收的項目、
製程失控且原因未明、量測系統不可接受時的數據。這些情境下須拒絕放行，
並升級管理層。

**可協商的情境：** 輕微外觀瑕疵經客戶同意可特採、非關鍵尺寸超差經工程
評估可接受、緊急訂單的加嚴檢驗替代正常週期。任何偏離須有書面授權與
記錄。

**預防性作法：** 透過製程能力提升、防錯、供應商管理，減少異常發生，
從根本降低品質與交期衝突的頻率。品質是設計與製程的結果，非檢驗出來的。
"""

