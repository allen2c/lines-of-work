content = """
機械零件常需材料證明以確保材質、機械性質符合規格。採購合約應明確要求
供應商提供 MTR（Material Test Report）或 3.1 證書。

**MTR 內容：** 爐號、批號、化學成分、機械性質（抗拉、降伏、伸長率）、
熱處理狀態。與訂單規格比對，確認符合。

**3.1 證書：** 第三方認證之材料證明，可信度較高。適用於關鍵件、航太、
汽車等產業。

**來料檢驗：** 除文件審查外，可執行硬度抽檢、光譜分析（成分）、尺寸抽檢。
高風險物料提高抽檢比例或全檢。

**不合格處理：** 材料不符時隔離、標識，通知採購與供應商。依程序辦理退貨、
特採或報廢。記錄供供應商評鑑。
"""

