title = "變更管制"

content = """
設計、製程、設備、供應商等變更可能影響產品品質。變更管制確保變更經評估、
驗證、核准後實施，避免未受控變更導致不良。

**變更類型：** 設計變更（圖面、規格）、製程變更（參數、工法）、設備變更、
供應商變更、場所變更等。

**管制流程：** 提出變更申請、影響評估（含 PFMEA、控制計畫）、驗證（試作、
檢驗、試跑）、核准、實施、記錄。客戶要求時須送客戶批准。

**品質參與：** 評估檢驗方法是否仍適用、是否需更新控制計畫、是否需 PPAP
重新提交。變更後首件或首批須加強驗證。
"""

