content = """
量規與專用檢具用於快速判定尺寸是否在公差範圍內，適用於大量生產的製程檢驗。
與通用量具不同，量規通常不讀取數值，僅判定合格/不合格。

**塞規與環規：** 通端（GO）與止端（NO-GO）的設計原理。通端檢驗最大實體尺寸，
止端檢驗最小實體尺寸。磨耗裕度的考量與校驗週期。

**卡規與高度規：** 用於外徑、厚度、高度等特徵的快速檢驗。定位基準的選擇影響
量測重複性。複合檢具將多個特徵整合於單一治具。

**功能性量規：** 模擬裝配條件，一次檢驗多個尺寸的配合性。用於複雜幾何或
關鍵配合件。設計時須考慮累積公差與量規不確定度。

**維護與校驗：** 量規磨耗會導致誤判。定期校驗、使用後清潔、存放環境控制
為維持準確度的必要措施。
"""

