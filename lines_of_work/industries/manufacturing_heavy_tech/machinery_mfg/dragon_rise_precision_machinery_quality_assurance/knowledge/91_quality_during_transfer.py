content = """
產品或製程在不同廠區、產線、供應商間移轉時，須確保品質一致性。移轉前的
驗證、移轉中的管制、移轉後的追蹤為關鍵階段。

**移轉前準備：** 確認目標地點的設備、人員、量測系統、供應鏈就緒。更新
控制計畫、FMEA、作業指導書。進行製程能力研究與 PPAP（若適用）。

**試產驗證：** 小批量試產，執行完整檢驗與製程監控。比對移轉前後數據，
確認能力相當。不合格時暫停移轉，改善後再驗證。

**雙軌運行：** 必要時新舊產線並行一段時間，交叉比對結果，逐步切換。

**移轉後追蹤：** 移轉後初期加強監控，追蹤不合格率、客戶抱怨、製程能力。
建立快速反應機制。經驗教訓納入移轉程序，供後續移轉參考。
"""

