content = """
PPAP（Production Part Approval Process）為汽車產業的零件核准流程，確保供應商
在量產前已驗證製程能力並提交完整證據。

**提交等級：** 共五級，等級三為預設。等級一僅保證書，等級三為完整文件與樣品，
等級四、五增加客戶指定要求。

**必要文件：** 設計紀錄、工程變更文件、客戶工程核准、DFMEA/PFMEA、量測系統分析、
初始製程能力研究、檢驗計畫、樣品、試生產檢驗結果等。

**製程能力：** Cpk、Ppk 需達客戶要求（通常 Cpk≥1.33）。不穩定或能力不足時
需改善後重新提交。

**龍騰精機情境：** 供應汽車零組件客戶時，新零件或變更須完成 PPAP。品質單位
主導文件彙整與提交，協調工程、生產完成各項驗證。
"""

