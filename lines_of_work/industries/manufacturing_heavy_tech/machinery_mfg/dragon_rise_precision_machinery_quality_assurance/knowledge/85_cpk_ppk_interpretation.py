title = "Cpk 與 Ppk 解讀"

content = """
製程能力指數 Cpk 與 Ppk 量化製程滿足規格的能力。Cpk 反映製程在統計管制下
的潛在能力；Ppk 反映製程的實際表現，包含組內與組間變異。

**計算公式：** Cpk = min[(USL-μ)/3σ, (μ-LSL)/3σ]，其中 μ 為平均，σ 為
估計的製程標準差。Ppk 使用總標準差。兩者皆假設常態分布。

**解讀準則：** Cpk ≥ 1.33 表示製程能力充足；1.00–1.33 為邊緣；< 1.00 表示
不合格率過高。汽車產業常要求 Cpk ≥ 1.33 或 1.67。

**Cpk 與 Ppk 差異：** 若 Ppk 明顯低於 Cpk，表示存在組間變異（如不同批次、
班別、機台間的差異），須先改善穩定性再追求能力提升。

**限制：** 非常態分布時指數可能誤導。單邊規格需使用 Cpu 或 Cpl。小樣本時
估計不確定度高，解讀須謹慎。
"""

