content = """
Zdolność to maksymalna ilość produkcji w danym okresie przy założonych warunkach.
W Wisła Stal zdolność wyraża się w godzinach maszynowych lub sztukach na zmianę.

**Obliczenie:** Zdolność = dostępny czas × wykorzystanie × wydajność. Dostępny czas
to godziny pracy minus planowane przestoje (konserwacja, święta). Wykorzystanie i
wydajność wynikają z historycznych danych.

**RCCP (Rough-Cut Capacity Planning):** Szybka weryfikacja zdolności na poziomie
kluczowych zasobów. Wykonywane przed zatwierdzeniem planu produkcyjnego.

**CRP (Capacity Requirements Planning):** Szczegółowe planowanie zdolności na podstawie
MRP. Identyfikacja niedoborów i nadwyżek w poszczególnych okresach.
"""

