title = "Kapazitätsplanung und -abgleich"

content = """
Kapazitätsplanung stellt sicher, dass die verfügbare Fertigungskapazität den
Bedarf deckt. Engpässe müssen rechtzeitig erkannt und behoben werden.

**Kapazitätsarten:** Personalkapazität (Stunden, Schichten), Maschinenkapazität
(Betriebsstunden), Anlagenkapazität. Die geringste Kapazität bestimmt den Engpass.

**Bedarfsermittlung:** Aus Arbeitsplänen und Stücklisten: Bearbeitungszeiten je
Arbeitsgang mal Menge. Rüstzeiten anteilig zuordnen. Pufferzeiten berücksichtigen.

**Kapazitätsabgleich:** Sollkapazität (Bedarf) mit Istkapazität (Verfügbar)
vergleichen. Überlast: Mehrschicht, Überstunden, Fremdvergabe, Terminverschiebung.
Unterlast: Rationalisierung, Auftragsübernahme.

**Rough-Cut Capacity Planning:** Grobe Prüfung kritischer Ressourcen in früher
Planungsphase. Detaillierte Feinplanung erst bei Auftragsfreigabe.

**Dynamische Anpassung:** Planabweichungen (Ausfälle, Nacharbeitsbedarf)
kontinuierlich erfassen. Rolling Planning für Aktualisierung.
"""

