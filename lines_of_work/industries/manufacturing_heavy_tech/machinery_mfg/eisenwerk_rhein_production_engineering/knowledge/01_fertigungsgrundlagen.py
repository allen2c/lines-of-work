content = """
Die Fertigung umfasst alle Verfahren, mit denen aus Rohmaterialien oder Halbzeugen
fertige Produkte entstehen. Im Maschinenbau werden überwiegend trennende, fügende
und umformende Verfahren eingesetzt.

**Trennende Verfahren (Zerspanung):** Material wird durch Schneiden, Fräsen, Drehen,
Bohren oder Schleifen abgetragen. Kennzeichnend ist die Spanbildung. Die Auswahl
abhängt von Form, Material, Toleranz und Oberflächengüte.

**Umformende Verfahren:** Material wird plastisch verformt ohne Spanabnahme:
Biegen, Tiefziehen, Schmieden, Walzen, Strangpressen. Geeignet für Massenfertigung
und hohe Festigkeit bei geringem Verschleiß.

**Fügeverfahren:** Verbindung von Bauteilen durch Schweißen, Löten, Kleben oder
mechanisches Fügen (Schrauben, Nieten). Entscheidungskriterien: Festigkeit,
Temperaturbeständigkeit, Nacharbeitbarkeit.

**Additive Verfahren:** 3D-Druck, Lasersintern. Für Prototypen, Einzelstücke oder
komplexe Geometrien, die konventionell nicht wirtschaftlich herstellbar sind.

**Verfahrensauswahl:** Wirtschaftlichkeit, Losgröße, Material, Genauigkeit,
Oberflächenanforderungen und vorhandene Kapazitäten bestimmen die Auswahl.
"""

