content = """
Änderungsmanagement steuert Änderungen an Produkten und Prozessen
kontrolliert. Vermeidet unbeabsichtigte Nebenwirkungen und sichert
Rückverfolgbarkeit.

**Änderungsanlass:** Kunde, interne Optimierung, Fehlerkorrektur,
Normänderung, Kostensenkung. Dokumentieren und priorisieren.

**Änderungsprozess:** Antrag, Bewertung (Technik, Kosten, Termin),
Freigabe, Umsetzung, Prüfung, Freigabe zur Fertigung.

**Betroffene Bereiche:** Konstruktion, Fertigung, Einkauf, Qualität,
Vertrieb. Abstimmung erforderlich.

**Konfiguration:** Produktkonfiguration (Stückliste, Zeichnung,
Arbeitsplan) versionsgeführt. Änderungen nur über formalen Prozess.

**Schnelländerung:** Bei dringenden Korrekturen verkürzter Prozess.
Risiko dokumentieren, Nacharbeit bei Bedarf.

**Werkzeuge:** PLM-Systeme, ERP-Änderungsmodul, Workflows.
"""

