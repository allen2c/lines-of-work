title = "Messsystemanalyse (MSA)"

content = """
Die Messsystemanalyse prüft, ob ein Messsystem geeignet ist, Merkmale
verlässlich zu erfassen. Ungenauigkeiten im Messsystem führen zu
fehlerhaften Prozessentscheidungen.

**R&R-Studie:** Wiederholbarkeit (Repeatability) und Reproduzierbarkeit
(Reproducibility). Mehrere Prüfer messen gleiche Teile mehrfach.
Varianzzerlegung in Komponenten.

**%R&R:** Anteil der Messstreuung an der Gesamtstreuung. < 10 % gut,
10–30 % bedingt geeignet, > 30 % ungeeignet. AIAG-VDA Richtlinien.

**Richtigkeit:** Abweichung vom Istwert. Kalibrierung mit Normale.
Systematische Abweichung korrigieren.

**Stabilität:** Messsystem über Zeit konstant. Trendanalyse,
Kontrolle durch periodische Prüfung.

**Linearity:** Abweichung über Messbereich konstant. Bei nicht-linear:
Bereich eingrenzen oder Korrektur anwenden.

**Durchführung:** Vor Prozessfähigkeitsuntersuchung. Bei Änderung
des Messsystems oder kritischen Reklamationen.
"""

