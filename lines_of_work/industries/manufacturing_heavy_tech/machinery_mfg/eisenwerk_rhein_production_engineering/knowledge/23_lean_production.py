content = """
Lean Production zielt auf Vermeidung von Verschwendung, kürzere
Durchlaufzeiten und höhere Flexibilität. Ursprung im Toyota Produktionssystem.

**Verschwendungsarten:** Überproduktion, Bestände, Transport, Warten,
Bewegung, Überbearbeitung, Fehler. systematisch abbauen.

**Flussprinzip:** Material und Information fließen ohne Unterbrechung.
Rüstzeiten minimieren für kleine Lose. Engpässe identifizieren und
beseitigen.

**Pull-Prinzip:** Produktion erst bei Bedarf (Kundenauftrag oder
Nachschubsignal). Kanban steuert Materialfluss. Gegenteil: Push.

**Standardisierung:** Standardarbeitsanweisungen, Takt, Ablauf.
Grundlage für Verbesserung und Störungsbehebung.

**Kaizen:** Kontinuierliche Verbesserung durch alle Beteiligten.
Kleine Schritte, regelmäßig. Gemba: Ort des Geschehens.

**Wertstromanalyse:** Abbildung Material- und Informationsfluss.
Verschwendung sichtbar machen, Verbesserungspotenziale finden.
"""

