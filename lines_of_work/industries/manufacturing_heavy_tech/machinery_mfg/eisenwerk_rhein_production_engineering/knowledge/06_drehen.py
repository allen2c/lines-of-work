content = """
Drehen ist das spanabhebende Bearbeiten rotierender Werkstücke. Das Werkstück
dreht sich, das Werkzeug führt die Schnittbewegung aus. Hauptverfahren im
Maschinenbau für rotationssymmetrische Teile.

**Dreharten:** Außenlängsdrehen, Innendrehen, Plan drehen, Gewindeschneiden,
Profildrehen. Unterschiedliche Werkzeughalter und Schneiden je Anwendung.

**Maschinentypen:** Spitzendrehmaschine, Revolverdrehautomat, Dreh-Fräs-Zentrum
(Mill-Turn), Langdrehautomaten. Auswahl nach Stückzahl, Genauigkeit, Komplexität.

**Werkzeuge:** Wendeschneidplatten mit verschiedenen Geometrien, Schneidstoffen
(Hartmetall, Cermet, Keramik) und Beschichtungen. Spanbrecher für Spanabfuhr.

**Bearbeitungsparameter:** Schnittgeschwindigkeit vc, Vorschub f, Schnitttiefe ap.
Abhängig von Werkstoff, Schneidstoff, Kühlung. Optimalbereich für Standzeit und
Oberflächengüte ermitteln.

**Spanbildung:** Kontinuierliche, locker brechende oder Spanbruch fördernde
Spanbildung. Spanform durch Schneidengeometrie und Parameter beeinflussbar.
"""

