title = "Die sieben Arten der Verschwendung"

content = """
Verschwendung (japanisch: Muda) umfasst alles, was nicht zum Kundennutzen
beiträgt. Sieben Kategorien helfen bei systematischer Identifikation.

**1. Überproduktion:** Mehr produzieren als der Kunde sofort benötigt.
Führt zu Beständen, Kapitalbindung, Verderb.

**2. Bestände:** Rohmaterial, Halbzeug, Fertigware über Bedarf.
Verschleiert Probleme, bindet Kapital, Risiko bei Änderung.

**3. Transport:** Unnötiges Bewegen von Material. Kein Mehrwert,
Risiko Beschädigung, Zeitverlust.

**4. Warten:** Menschen oder Maschinen warten. Leerlauf, Engpässe,
ungleichmäßiger Takt.

**5. Bewegung:** Unnötige Wege, Suchen, Bücken. Ergonomie,
Effizienz verbessern.

**6. Überbearbeitung:** Mehr Arbeit als nötig. Zu hohe Toleranzen,
doppelte Prüfungen, unnötige Prozesse.

**7. Fehler:** Ausschuss, Nacharbeit, Reklamationen. Qualitätskosten,
Lieferverzug. Poka-Yoke zur Vermeidung.
"""

