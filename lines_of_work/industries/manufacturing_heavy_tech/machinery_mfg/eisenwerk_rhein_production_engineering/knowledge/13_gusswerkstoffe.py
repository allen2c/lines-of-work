content = """
Guss erzeugt Bauteile durch Erstarren von Schmelze in Formen. Geeignet
für komplexe Geometrien, Integrieren von Funktionen, wirtschaftlich bei
Serienfertigung.

**Gusswerkstoffe:** Grauguss, Temperguss, Stahlguss, Aluminiumguss,
Kupferlegierungen. Eigenschaften: Festigkeit, Dichtheit, Bearbeitbarkeit.

**Sandguss:** Einmalformen aus Sand. Für Einzelstücke und Kleinserien.
Oberfläche und Maßgenauigkeit begrenzt.

**Kokillenguss:** Dauerformen. Höhere Genauigkeit und Oberfläche.
Wirtschaftlich bei größeren Serien.

**Druckguss:** Druck in Metallform. Sehr hohe Produktivität, dünnwandig.
Hauptsächlich Aluminium, Zink, Magnesium.

**Gussqualität:** Lunker, Poren, Risse vermeiden. Gussplanung (Anguss,
Speiser) kritisch. Prüfung: Röntgen, Druckprüfung, Sichtprüfung.

**Nachbearbeitung:** Entgraten, Wärmebehandlung, Oberflächenbehandlung.
"""

