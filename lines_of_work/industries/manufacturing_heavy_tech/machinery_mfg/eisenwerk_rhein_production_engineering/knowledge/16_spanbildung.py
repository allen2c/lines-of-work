content = """
Spanbildung entsteht beim Zerspanen durch plastische Verformung und
Scherung des Materials vor der Schneide. Die Spanform beeinflusst
Oberfläche, Werkzeugbelastung und Prozesssicherheit.

**Spanarten:** Fließspan (lang, bandförmig), Reißspan (zerbrochen),
Lockerspan (locker gebrochen). Abhängig von Werkstoff, Parameter,
Schneidengeometrie.

**Probleme:** Fließspäne können Wickeln, Kratzen, Verklemmung.
Spanbrecher an der Schneide fördern Bruch. Zu hoher Vorschub: dicker
Span, höhere Kräfte.

**Spanraum:** Ausreichend für Spanabfuhr. Kühlschmiermittel spült.
Bei Tiefbohrung: Druckkühlung durch Werkzeug.

**Spanbildungstheorie:** Scherzone, Reibung, Wärme. Grundlage für
Prozessmodellierung und Optimierung.

**Praktische Einstellung:** Parameter und Schneidengeometrie variieren,
bis akzeptable Spanform erreicht. Dokumentieren für Wiederholbarkeit.
"""

