title = "Umformung und Massivumformung"

content = """
Umformung verändert die Werkstoffform durch plastische Verformung ohne
Spanabnahme. Hohe Festigkeit, gute Materialausnutzung, wirtschaftlich
bei mittleren und großen Stückzahlen.

**Kalumformung:** Biegen, Tiefziehen, Prägen bei Raumtemperatur.
Vorteil: Oberfläche bleibt, Nachteil: begrenzte Formänderung.

**Warmumformung:** Schmieden, Walzen bei erhöhter Temperatur. Größere
Formänderung möglich, Oxidation der Oberfläche.

**Massivumformung:** Schmieden (Freiform, Gesenkschmieden), Strangpressen,
Walzen. Für massive Bauteile mit hoher Belastung.

**Blechumformung:** Tiefziehen, Biegen, Stanzen. Für Karosserie, Gehäuse,
Verpackungen.

**Werkzeuge:** Gesenke, Matrizen, Biegewerkzeuge. Verschleiß durch hohe
Kräfte. Schmierung und Werkzeugbeschichtung verlängern Standzeit.

**Qualität:** Rissfreiheit, Maßhaltigkeit, Oberfläche prüfen.
"""

