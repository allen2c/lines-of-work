content = """
Schnittparameter bestimmen Leistung, Oberfläche und Standzeit. Die drei
Hauptgrößen: Schnittgeschwindigkeit vc, Vorschub f, Schnitttiefe ap.

**Schnittgeschwindigkeit vc:** Umfangsgeschwindigkeit der Schneide in m/min.
Abhängig von Werkstoff und Schneidstoff. Zu hoch: Verschleiß, Brand.
Zu niedrig: Produktivitätsverlust.

**Vorschub f:** Weg pro Umdrehung (Drehen) oder Zahn (Fräsen) in mm.
Beeinflusst Oberfläche und Spanlast. Schruppen: höherer Vorschub.
Schlichten: feiner für Rauheit.

**Schnitttiefe ap:** Eingriff in radiale Richtung in mm. Schruppen: größere
Tiefe, wenige Pässe. Schlichten: geringe Resttiefe.

**Zusammenhang:** Materialabtragsrate Q = vc × f × ap. Konstante bei
Optimierung variieren. Empfehlungen aus Handbüchern, Datenbanken,
Eigenversuche.

**Kühlung:** Trocken, Minimalmengenschmierung (MMS), Flutkühlung.
Einfluss auf Parameter und Oberfläche.
"""

