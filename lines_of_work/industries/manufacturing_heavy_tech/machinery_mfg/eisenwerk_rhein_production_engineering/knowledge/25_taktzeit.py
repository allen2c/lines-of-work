title = "Taktzeit und Fließfertigung"

content = """
Taktzeit definiert den Rhythmus, in dem ein Produkt fertiggestellt wird.
Grundlage für Fließfertigung und Auslastungsplanung.

**Definition:** Taktzeit = Verfügbare Zeit / Kundenbedarf. Z.B. 7,2 h
Verfügbarkeit, 360 Stück Bedarf: Taktzeit = 72 Sekunden pro Stück.

**Arbeitszeit pro Station:** Muss ≤ Taktzeit sein. Sonst Rückstau
oder Überlastung. Ausgleich durch Aufteilung oder Parallelarbeit.

**Fließband:** Stationen in Reihenfolge, Material fließt im Takt.
Balance: Arbeitszeiten angleichen, Engpässe vermeiden.

**Verdichtung:** Wenn Arbeitszeit < Taktzeit, Möglichkeit für
Mehrfacharbeitsplätze oder Zusatzaufgaben. Oder Kürzung der
Verfügbarkeit (weniger Schichten).

**Pulsierende Fertigung:** Takt als Steuergröße für Produktion.
Störungen wirken sofort sichtbar.
"""

