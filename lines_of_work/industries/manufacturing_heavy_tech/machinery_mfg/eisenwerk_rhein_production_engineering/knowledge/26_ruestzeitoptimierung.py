title = "Rüstzeitoptimierung (SMED)"

content = """
SMED (Single Minute Exchange of Die) reduziert Rüstzeiten von Stunden
auf Minuten. Ermöglicht kleine Losgrößen ohne Kapazitätsverlust.

**Grundidee:** Rüstvorgänge analysieren, in interne (bei Stillstand)
und externe (bei Lauf) trennen. Externe vorbereiten, interne minimieren.

**Schritte:** 1) Alle Tätigkeiten erfassen. 2) Interne und externe
trennen. 3) Externe zu extern machen (Vorbereitung). 4) Interne
verkürzen (Vereinfachung, Parallelisierung).

**Beispiele:** Werkzeuge vor dem Rüsten bereitlegen. Schrauben durch
Schnellspannsysteme ersetzen. Einstellungen dokumentieren und
wiederholgenau machen. Parallel arbeiten wo möglich.

**Werkzeuge:** Rüst-Checklisten, Rüstwagen, Vorrichtungen.
Standardisierung der Abläufe.

**Nutzen:** Kleinere Lose, weniger Bestand, flexiblere Reaktion
auf Kundenbedarf. Oft 50–90 % Rüstzeitreduktion möglich.
"""

