title = "Prüfmittelüberwachung und Kalibrierung"

content = """
Prüfmittel müssen regelmäßig überwacht werden, um zuverlässige
Messergebnissen zu gewährleisten. Nicht kalibrierte Messmittel
können zu Fehlentscheidungen führen.

**Kalibrierung:** Vergleich mit höherer Norm, Bestimmung der Abweichung.
Kalibrierschein dokumentiert Ergebnis. Intervall je Risiko und Nutzung.

**Eichung:** Amtliche Prüfung für gesetzlich geregelte Messgeräte
(Waagen, Durchflussmesser). Nur durch staatlich anerkannte Stellen.

**Prüfmittelarten:** Messschieber, Mikrometer, Lehren, Messuhren.
Je nach Genauigkeitsanforderung und Einsatz.

**Prüfplan:** Welches Mittel, welches Intervall, welche Methode.
Rückverfolgbarkeit zu nationalen oder internationalen Normalen.

**Kennzeichnung:** Status (kalibriert, abgelaufen, fehlerhaft),
Nächstfälligkeit. Aussonderung bei Überschreitung Toleranz.

**Dokumentation:** Prüfprotokolle, Kalibrierscheine archivieren.
"""

