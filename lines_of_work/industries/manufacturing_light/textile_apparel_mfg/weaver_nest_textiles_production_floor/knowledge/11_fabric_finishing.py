title = "Fabric Finishing and Inspection"

content = """
The final stage of production is finishing and inspection. This ensures the fabric meets 
quality standards and is ready for the customer.

**Off-the-Loom Inspection:** Every roll of fabric is inspected as it is removed from the 
loom. This is the first line of defense against defects. Any issues are noted and 
reported to the weaver and production manager.

**Mending and Burling:** Minor defects like loose ends, slubs, or knots can often be 
corrected by hand. This is a skilled task that requires a deep understanding of the 
weave structure.

**Scouring and Washing:** Removing machine oils, sizing agents, and other impurities from 
the fabric. This is typically done in large vats or continuous washing machines.

**Drying and Heat-Setting:** The fabric is dried and then heated to stabilize its 
dimensions and prevent shrinkage. This is particularly important for synthetic and 
blended fabrics.

**Final Inspection and Grading:** The finished fabric is inspected one last time and 
graded based on the number and severity of remaining defects. Only "A-Grade" fabric is 
shipped to customers.
"""

