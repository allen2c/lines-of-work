title = "Yarn Counts and Fiber Characteristics"

content = """
The choice of yarn is fundamental to the final fabric's properties. Yarn is characterized 
by its fiber content and its thickness, or "count."

**Natural Fibers:** Cotton, wool, silk, and linen are the most common. Each has unique 
properties: cotton is breathable and durable; wool is warm and resilient; silk is lustrous 
and strong; linen is crisp and absorbent.

**Synthetic Fibers:** Polyester, nylon, and acrylic are often used for their strength, 
elasticity, and resistance to wrinkles and shrinkage. Blends of natural and synthetic 
fibers are common to achieve a balance of properties.

**Yarn Count Systems:** There are two main systems: Indirect (e.g., Cotton Count, Worsted 
Count) and Direct (e.g., Denier, Tex). In indirect systems, a higher number means a finer 
yarn. In direct systems, a higher number means a thicker yarn. Understanding these is 
essential for calculating yarn requirements and ensuring the correct fabric weight.
"""

