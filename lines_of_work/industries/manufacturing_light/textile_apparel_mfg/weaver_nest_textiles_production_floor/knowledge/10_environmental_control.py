title = "Environmental Control: Humidity and Temperature"

content = """
The production floor environment significantly impacts fiber behavior and machine 
performance. Consistent environmental control is essential for high-quality weaving.

**Humidity:** Most fibers, especially natural ones like cotton and wool, are hygroscopic, 
meaning they absorb and release moisture from the air. High humidity increases fiber 
strength and elasticity, reducing breakage. Low humidity makes fibers brittle and prone 
to static electricity, leading to "clinging" and poor shed formation.

**Temperature:** While less critical than humidity, extreme temperatures can affect 
machine lubricants and the physical properties of synthetic fibers. A stable temperature 
helps maintain consistent machine timing and operator comfort.

**Monitoring:** The production floor should be equipped with hygrometers and 
thermometers. Automated humidification systems are often used to maintain the ideal 
relative humidity (typically 65-75% for cotton, 75-85% for wool).
"""

