title = "Loom Technical Specifications: Rapier and Projectile"

content = """
Weaver's Nest utilizes a range of modern loom technologies to achieve high-speed and 
versatile production.

**Rapier Looms:** These are highly versatile and can handle a wide range of yarn types 
and colors. They use flexible or rigid rapiers to carry the weft across the shed. Rapier 
looms are ideal for small-batch and complex patterns.

**Projectile Looms:** These use small, bullet-like projectiles to carry the weft across 
the shed. They are very fast and can handle wide fabric widths. Projectile looms are 
ideal for high-volume production of simpler fabrics.

**Technical Specifications:** Each loom has its own set of technical specifications, 
including its maximum speed (measured in picks per minute), its maximum width, and its 
patterning capabilities. Understanding these specifications is essential for selecting 
the right loom for each order.

**Maintenance and Calibration:** Modern looms require regular maintenance and 
calibration to ensure they are running at peak performance. This includes checking the 
timing of the picking and shedding motions, the tension of the warp and weft, and the 
alignment of the reed and harnesses.
"""

