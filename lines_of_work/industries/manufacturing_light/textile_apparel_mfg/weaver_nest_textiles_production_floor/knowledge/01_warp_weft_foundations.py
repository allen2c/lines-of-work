content = """
The core of all woven textiles lies in the intersection of two sets of yarns: the warp and the weft. 
Understanding their relationship is fundamental to production floor management.

**Warp Yarns:** These are the lengthwise yarns that are held in tension on the loom. They must be 
strong and durable, as they undergo significant stress during the weaving process. The warp 
determines the length of the fabric and is prepared through warping and beaming.

**Weft Yarns:** Also known as filling or woof, these yarns are interlaced crosswise through the 
warp. They are typically carried by a shuttle, rapier, or air jet. The weft determines the 
width of the fabric and provides much of its texture and aesthetic character.

**The Interlacing Pattern:** The specific way the weft passes over and under the warp yarns 
defines the weave structure (e.g., plain, twill, satin). This pattern determines the fabric's 
strength, drape, and appearance. Consistent tension in both warp and weft is critical for 
preventing defects like bowing or skewing.
"""

