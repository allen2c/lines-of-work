title = "Quality Control: Common Weaving Defects"

content = """
Maintaining high quality requires constant vigilance. Weavers must be trained to identify 
and address defects as they occur.

**Broken Ends:** A warp yarn that snaps during weaving. This creates a vertical gap in the 
fabric. Modern looms have automatic stop motions that halt the machine when an end breaks, 
allowing the weaver to tie it back together.

**Broken Picks:** A weft yarn that breaks or runs out before completing its pass. This 
creates a horizontal gap or "thin place" in the fabric.

**Slubs and Neps:** Thick or knotted areas in the yarn that create irregular bumps in the 
fabric. While sometimes desired for texture, they are often considered defects in 
high-end fabrics.

**Mispicks:** Errors in the weave pattern, often caused by a malfunctioning harness or 
incorrect drawing-in. These are particularly noticeable in complex patterns like jacquards 
or intricate twills.

**Bowing and Skewing:** Distortions in the fabric where the weft yarns are not perfectly 
perpendicular to the warp. This is usually caused by uneven tension during take-up or 
finishing.
"""

