title = "Loom Dressing: Drawing-In and Tying-In"

content = """
Loom dressing is the process of setting up the warp yarns on the loom. This is a meticulous 
task that directly impacts the weave structure and machine efficiency.

**Drawing-In:** Each individual warp yarn must be threaded through a specific heddle on a 
harness and then through a dent in the reed. The drawing-in plan determines the weave 
pattern. This is typically done manually for new patterns or when changing the fabric 
construction significantly.

**Tying-In:** When a new warp beam is identical to the one just finished, the new yarns can 
be tied to the ends of the old warp. This can be done manually or with a tying-in machine. 
It is much faster than drawing-in but requires the previous setup to be in good condition.

**Reeding:** The final step of dressing, where yarns are pulled through the reed. The reed 
controls the spacing of the warp yarns (ends per inch) and beats the weft into place. Proper 
reeding is essential for maintaining the correct fabric width and density.
"""

