title = "NR-12 — Segurança em Máquinas e Equipamentos"

content = """
A NR-12 estabelece requisitos mínimos de segurança para máquinas e equipamentos.
O cumprimento é obrigatório e alvo de fiscalização do Ministério do Trabalho.

**Proteções:** Máquinas devem ter proteções que impeçam acesso a pontos de
perigo (agulhas, lâminas, partes móveis) durante a operação. Proteções fixas
ou intertravadas. Não remover ou desativar proteções.

**Parada de emergência:** Botão de parada de emergência acessível e identificado.
Testar periodicamente. Acionar em situação de risco imediato.

**Sinalização:** Áreas de risco sinalizadas. Uso de EPIs conforme análise de
risco — luvas, óculos, calçados fechados quando necessário. Treinamento
obrigatório antes de operar nova máquina.
"""  # noqa: E501

