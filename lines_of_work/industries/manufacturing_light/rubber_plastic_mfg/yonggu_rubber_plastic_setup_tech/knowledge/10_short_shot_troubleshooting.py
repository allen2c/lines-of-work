title = "短射（充填不足）排除"

content = """
- 原因分析：短射可能原因包括：射出壓力或速度不足、計量位置過小、料管溫度過低、原料流動性差、模具排氣不良、澆口或流道堵塞、原料乾燥不足。
- 排除步驟：1. 檢查原料是否乾燥完全（含水率是否超標）。2. 增加射出壓力（每次5-10 MPa）或射出速度（每次10 mm/s）。3. 檢查料管溫度是否在原料建議範圍內。4. 檢查模具排氣槽是否堵塞，必要時清理。5. 若為多模穴，檢查各模穴充填是否平衡。
- 進階調整：若調整參數後仍短射，可嘗試增加計量位置（增加料量），或提高模溫以改善流動性。若為薄壁件，可採用高速射出（>100 mm/s）。
- 預防措施：定期清理料管（每週一次），避免碳化物堵塞噴嘴。新模具試模時，先以低速低壓確認流道暢通。
"""

