content = """
- 首件檢驗：每次換模、換料或參數調整後，需進行首件檢驗。取5件成品，量測所有關鍵尺寸（使用游標卡尺、分釐卡或投影儀），檢查外觀（毛邊、縮水、氣痕、顏色），並記錄於首件檢驗表。
- 巡檢頻率：正常生產時，每2小時進行一次巡檢。每次取3件成品，量測關鍵尺寸與外觀，並與首件樣品比對。若尺寸偏移超過公差之50%，需進行參數微調。
- 抽樣標準：依AQL（允收品質水準）標準進行抽樣，通常採用AQL 0.65（正常檢驗）或AQL 1.0（放寬檢驗）。若連續3批不合格，需轉為嚴格檢驗。
- 異常處理：若巡檢發現不良，需立即隔離該批次（從上次合格巡檢時間起算），並通知班長與品保人員進行重檢。
"""

