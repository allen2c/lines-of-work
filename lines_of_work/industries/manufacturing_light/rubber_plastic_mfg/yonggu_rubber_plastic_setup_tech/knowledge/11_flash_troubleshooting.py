title = "毛邊（披鋒）排除"

content = """
- 原因分析：毛邊可能原因包括：鎖模力不足、模具平行度不佳、模具分模面有異物或損傷、射出壓力過高、V-P切換過晚、原料流動性過高。
- 排除步驟：1. 檢查鎖模力是否足夠（可增加5-10%）。2. 檢查模具分模面是否有殘料或刮傷，必要時清潔或送修。3. 降低射出壓力（每次5-10 MPa）或提前V-P切換位置（每次1-2 mm）。4. 檢查模具排氣槽是否過深（標準深度0.02-0.05 mm）。
- 進階調整：若為高流動性原料（如PP、PA），可降低料管溫度（每次5°C）或降低射出速度。若毛邊發生在滑塊或頂針處，需檢查滑塊間隙或頂針配合度。
- 預防措施：定期（每月）檢查模具平行度，並進行保養。新模具上機前，先以藍丹檢查分模面接觸狀況。
"""

