title = "縮水（凹陷）排除"

content = """
- 原因分析：縮水通常發生在成品厚壁區域或肋條根部，原因包括：保壓壓力或時間不足、計量位置過小（料量不足）、冷卻時間不足、模溫過高、澆口過小或過早凝固。
- 排除步驟：1. 增加保壓壓力（每次0.5-1 MPa）或延長保壓時間（每次0.5秒）。2. 增加計量位置（增加料量，每次1-2 mm）。3. 降低模溫（每次5°C）以加速澆口凝固。4. 檢查澆口尺寸是否足夠，必要時請模具維修組擴大。
- 進階調整：若縮水發生在遠離澆口處，可嘗試提高射出速度或增加第二段保壓壓力。對於厚壁件，可採用「漸降保壓」模式（保壓壓力隨時間遞減）。
- 預防措施：設計模具時，確保肋條厚度不超過主壁厚之60%。調整參數時，以成品重量作為判斷依據（重量穩定即表示充填飽滿）。
"""

