title = "翹曲（變形）排除"

content = """
- 原因分析：翹曲主因是冷卻不均或殘留應力，原因包括：模溫不均（模具兩側溫差過大）、冷卻時間不足、射出壓力過高、保壓時間過長、原料結晶度差異（如PA、POM）。
- 排除步驟：1. 檢查模溫機各回路溫度是否一致（溫差應小於5°C），調整冷卻水路流量。2. 延長冷卻時間（每次2-3秒）或降低模溫（每次5°C）。3. 降低射出壓力或保壓壓力（每次5-10 MPa）。4. 對於結晶性原料，調整模溫以控制結晶度（如PA模溫60-80°C可減少翹曲）。
- 進階調整：若翹曲方向固定，可嘗試調整頂出順序或增加頂針數量。對於大型平面件，可使用「保壓反翹」技巧（在保壓階段局部增加壓力）。
- 預防措施：設計模具時，確保冷卻水路均勻分佈，並在成品對稱位置設置相同冷卻條件。
"""

