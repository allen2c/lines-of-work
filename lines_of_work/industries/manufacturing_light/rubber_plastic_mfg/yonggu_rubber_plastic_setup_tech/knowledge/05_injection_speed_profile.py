content = """
- 分段原則：通常分為3-5段。第一段（低速）用於通過澆口，避免噴射（jetting）；第二段（高速）用於快速充填模穴，減少流動阻力；第三段（減速）用於充填末端，防止毛邊或過度保壓。
- 速度設定參考：對於外觀件（如ABS外殼），第一段速度設為20-40 mm/s，第二段60-80 mm/s，第三段30-50 mm/s。對於結構件（如PA66+GF支架），可採用較高速充填（80-100 mm/s）以確保流動性。
- 切換位置：速度切換點通常以螺桿位置（mm）設定。例如：第一段從計量位置到澆口位置，第二段從澆口到模穴80%充填位置，第三段到V-P切換點。
- 調整技巧：若出現噴射痕，應降低第一段速度；若出現結合線明顯，可提高第二段速度或提高模溫；若末端有毛邊，應降低第三段速度或提前切換至保壓。
"""

