title = "射出壓力與保壓壓力設定"

content = """
- 射出壓力：設定值通常為機台最大壓力之50-80%。對於高流動性原料（如PP、PE），壓力可較低（60-80 MPa）；對於高黏度原料（如PC、PMMA），壓力需較高（100-140 MPa）。
- 保壓壓力：通常為射出壓力之50-70%。保壓壓力過低會導致縮水，過高則可能造成毛邊或殘留應力。調整時以0.5 MPa為步進單位。
- V-P切換點：切換點位置通常設定在螺桿計量位置前5-10 mm處，或依成品重量調整。若切換過早（殘留熔膠過多），可能導致短射；切換過晚（殘留熔膠過少），可能導致毛邊或成品過重。
- 保壓時間：保壓時間需足以讓澆口凝固。通常設定為0.5-2秒，可依成品重量變化來判斷：若增加保壓時間成品重量不再增加，即為最佳保壓時間。
"""

