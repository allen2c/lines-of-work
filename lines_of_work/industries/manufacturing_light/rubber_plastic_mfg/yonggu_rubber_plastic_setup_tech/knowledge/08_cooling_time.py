title = "冷卻時間與模溫控制"

content = """
- 冷卻時間計算：冷卻時間約佔整個成型週期之50-80%。基本公式：冷卻時間（秒）= 成品最大壁厚（mm）² × 係數（原料係數：ABS 1.5-2.0，PP 1.0-1.5，PC 2.0-3.0，PA66 0.8-1.2）。
- 模溫設定：ABS模溫40-60°C；PP模溫30-50°C；PC模溫80-120°C；PA66模溫60-90°C。模溫過低會導致縮水或結晶度不足（如PA），模溫過高會延長冷卻時間。
- 模溫機使用：連接模溫機時，需設定進水溫度與出水溫度，溫差應控制在±3°C以內。若溫差過大，表示冷卻水路設計不良或流量不足。
- 優化技巧：在確保成品不變形且頂出順暢的前提下，逐步減少冷卻時間（每次減少1-2秒），觀察成品是否出現翹曲或頂出困難。若出現問題，則回調至前一穩定值。
"""

