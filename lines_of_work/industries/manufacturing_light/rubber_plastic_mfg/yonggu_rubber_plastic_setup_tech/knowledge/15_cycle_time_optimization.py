title = "週期時間優化技巧"

content = """
- 優先順序：優化週期應先從佔比最大的項目著手，通常為冷卻時間（佔50-80%），其次為開關模時間（佔10-20%），最後為射出與保壓時間（佔10-20%）。
- 冷卻時間優化：在成品不變形且頂出順暢的前提下，每次減少冷卻時間1-2秒，觀察成品狀況。若出現翹曲或頂出困難，則回調至前一穩定值。可使用模溫機降低模溫以加速冷卻。
- 開關模速度優化：提高開模速度（每次5-10%），但需確保模具不產生震動。提高關模速度時，需注意低壓保護區域的設定，避免模具碰撞。
- 頂出時間優化：減少頂出次數（如從3次減為2次）或降低頂出延遲時間（每次0.2秒）。確保頂出行程足夠但不過長。
- 射出時間優化：在不產生毛邊的前提下，提高射出速度以縮短充填時間。保壓時間則以澆口凝固為準，避免過長。
"""

