title = "模具上下機標準流程"

content = """
- 上模前準備：確認模具尺寸與機台夾模規格（如哥林柱間距、模具厚度範圍）相符。清潔模具分模面與定位環，檢查滑塊、頂針、斜銷動作是否順暢，並塗抹適量防鏽劑。
- 吊模與鎖模：使用天車將模具吊入機台，對準定位環後緩慢下降。先以低壓（設定值低於50 bar）進行鎖模，確認模具平行度後，再鎖緊夾模螺絲（使用扭力扳手，依模具大小鎖緊至80-120 Nm）。
- 管路連接：依模具設計圖連接模溫機進出水口（注意入水在下、出水在上，避免氣泡），以及油壓/氣壓管路。接好後進行管路排氣，確認無漏水、漏油。
- 參數初始設定：輸入模具編號後，調出歷史參數（若為舊模具）或依原料物性表設定初始值。設定模具保護壓力（通常為20-40 bar）與保護位置（距離模具閉合位置5-10 mm）。
"""

