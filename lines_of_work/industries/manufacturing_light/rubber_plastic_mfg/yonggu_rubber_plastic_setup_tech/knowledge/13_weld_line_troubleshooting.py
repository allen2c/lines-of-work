title = "結合線（熔接線）改善"

content = """
- 原因分析：結合線產生於熔膠流動前端相遇處，原因包括：模溫過低、射出速度過慢、原料流動性差、模具排氣不良、澆口位置不佳。
- 改善步驟：1. 提高模溫（每次5-10°C）以延緩熔膠冷卻。2. 提高射出速度（每次10-20 mm/s）以增加熔膠前端溫度。3. 檢查模具排氣槽是否足夠，必要時在結合線位置增加排氣。4. 若為外觀件，可嘗試提高料管溫度（每次5°C）。
- 進階調整：對於結構件，可增加保壓壓力以壓實結合線區域。若結合線位置固定且影響強度，需與工程師討論修改澆口位置或增加溢料井。
- 預防措施：在模具設計階段，使用模流分析軟體預測結合線位置，並盡量將其移至非外觀面或低應力區域。
"""

