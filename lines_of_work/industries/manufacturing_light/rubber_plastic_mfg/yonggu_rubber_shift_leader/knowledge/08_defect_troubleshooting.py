content = """
- 短射：原因包括射出量不足、料溫過低、射出速度太慢、模具排氣不良。對策：增加計量位置、提高料溫5-10°C、加快射出速度、清理排氣槽。
- 毛邊：原因包括鎖模力不足、射出壓力過高、模具分模面磨損。對策：增加鎖模力（不超過機台最大鎖模力90%）、降低射出壓力、檢查模具分模面有無異物或損傷。
- 氣痕/銀紋：原因包括原料未乾燥、料管溫度過高、射出速度太快。對策：確認乾燥時間與溫度、降低料溫5-10°C、降低射出速度。
- 縮水/凹陷：原因包括保壓不足、冷卻時間不夠、產品壁厚不均。對策：增加保壓壓力或時間、延長冷卻時間、修改模具壁厚（需設計單位評估）。
- 變形：原因包括冷卻不均、模具溫度不平衡、產品幾何不對稱。對策：調整模溫機各區溫度（溫差控制在5°C內）、延長冷卻時間、增加頂針數量或位置。
"""

