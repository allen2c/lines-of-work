content = """
- 步驟1：確認新模具與機台噸數匹配（模具重量不得超過機台最大模板承重），檢查模具外觀有無損傷。
- 步驟2：關閉機台電源與模溫機，等待料管溫度降至安全範圍（低於100°C）後，拆除舊模具。
- 步驟3：使用天車或吊環吊起模具，對準機台定位環，緩慢合模至模具接觸，鎖緊模夾（扭力值依機台手冊，一般80-120Nm）。
- 步驟4：連接冷卻水管與熱澆道接線，確認無漏水、無短路。設定模溫機溫度（依原料與模具設計）。
- 步驟5：執行模具開合動作，檢查滑塊、頂針與安全門感應器是否正常。進行試模，調整參數至穩定。
- 步驟6：填寫模具更換記錄表，包含模具編號、更換時間、操作人員簽名。舊模具送回模具倉庫或維修區。
"""

