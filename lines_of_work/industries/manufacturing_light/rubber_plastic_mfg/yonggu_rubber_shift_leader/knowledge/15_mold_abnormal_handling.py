title = "模具異常處理流程"

content = """
- 發現模具異常（如龜裂、變形、咬傷、滑塊卡死、頂針斷裂），立即停機，掛上「模具異常-禁止使用」牌。
- 填寫「模具維修申請單」，內容包含：模具編號、異常描述、發現時間、機台編號、操作人員簽名。附上異常照片（至少2張不同角度）。
- 通知模具維修組（電話或ERP系統），維修組需在1小時內到場評估。評估結果分為：現場維修（1小時內可完成）、送修（需拆模）、報廢。
- 若需送修，領班協助拆模並運送至模具維修區。維修期間，生管需調整排程，使用備用模具或轉移訂單。
- 模具維修完成後，領班需進行試模確認（至少20模次），確認無異常後方可恢復生產。維修記錄需更新於模具履歷表。
"""

