title = "品質異常處理流程"

content = """
- 發現品質異常時，立即標示並隔離不良品，填寫「品質異常單」（紙本或ERP系統），內容包含：機台編號、模具編號、原料批號、異常描述、數量、發現時間。
- 通知品保單位（巡檢員或品保工程師）進行判定。品保需在30分鐘內到場，判定結果分為：報廢、重工、特採（需客戶同意）。
- 若異常原因為模具或機台問題，領班需在1小時內提出初步對策（如調整參數、更換零件），並記錄於異常單。
- 同一模具連續發生5次以上相同異常，或不良率超過5%，升級至品保工程師與模具設計單位，進行根本原因分析。
- 異常單需於24小時內結案，結案時需填寫改善措施與預防對策，並由課長與品保主管簽核。
"""

