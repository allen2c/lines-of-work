content = """
5S (Sort, Set in Order, Shine, Standardize, Sustain) menciptakan lingkungan kerja yang
tertata, aman, dan efisien. Housekeeping yang buruk meningkatkan risiko insiden dan
menyembunyikan masalah.

**Sort (Seiri):** Pisahkan barang yang diperlukan dan tidak. Buang atau relokasi barang
tidak terpakai. Kurangi clutter di lantai produksi.

**Set in Order (Seiton):** Setiap alat dan material punya tempat. Label dan shadow board
untuk peralatan. Barang sering dipakai dekat titik penggunaan.

**Shine (Seiso):** Bersihkan area secara teratur. Pembersihan mengungkap kebocoran,
keausan, dan kerusakan. Libatkan operator dalam pembersihan area kerjanya.

**Standardize dan Sustain:** Dokumentasikan standar 5S. Audit berkala dan perbaikan berkelanjutan.
Kepemimpinan harus konsisten mendukung praktik 5S.
"""  # noqa: E501

