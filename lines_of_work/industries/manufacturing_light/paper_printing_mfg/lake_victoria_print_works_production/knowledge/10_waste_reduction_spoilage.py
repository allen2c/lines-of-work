title = "Waste Reduction and Spoilage Management"

content = """
Print production generates waste through setup, running adjustments, and defects.
Minimizing waste reduces cost and environmental impact while improving efficiency.

**Setup Waste:** Every job requires make-ready before acceptable sheets are
produced. Optimize by grouping similar jobs, training operators, and maintaining
equipment. Document typical setup waste by job type for planning.

**Running Waste:** Allow overrun for bindery and customer overage. Excessive
overrun increases waste. Track actual vs. planned spoilage to identify
improvement opportunities.

**Defect Waste:** Quality issues during the run produce scrap. Root cause
analysis of recurring defects (e.g., hickeys, misregister) helps prevent future
loss. Address substrate, environment, and equipment factors.

**Recycling and Disposal:** Paper waste is recyclable. Coordinate with waste
handlers. Ink and chemistry require appropriate disposal per regulations. Track
waste streams for reporting and improvement.
"""

