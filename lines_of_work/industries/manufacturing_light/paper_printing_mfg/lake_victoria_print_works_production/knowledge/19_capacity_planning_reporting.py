title = "Capacity Planning and Production Reporting"

content = """
Capacity planning aligns available resources with demand. Production reporting
provides visibility for management decisions and customer communication.

**Capacity Metrics:** Track available press hours, finishing hours, and labor
by shift. Account for planned maintenance and holidays. Use historical uptime
and efficiency for realistic capacity estimates.

**Demand Planning:** Aggregate orders and forecasts to assess load. Identify
periods of overbooking or underutilization. Adjust scheduling, overtime, or
customer commitments accordingly. Communicate capacity constraints to sales.

**Production Reports:** Shift reports document jobs run, quantities produced,
waste, and downtime. Summarize for daily or weekly management review. Track
efficiency (actual output vs. theoretical) and spoilage rates.

**Trend Analysis:** Use historical data to improve estimates and identify
seasonal patterns. Support budgeting, staffing, and investment decisions.
Share relevant metrics with operations team for continuous improvement.
"""

