title = "Escalation and Handoff Protocols"

content = """
Production operations cannot resolve every situation. Clear escalation and
handoff protocols ensure issues reach the right people and production continues.

**Artwork and Design:** File errors, missing elements, or specification
ambiguities go to Graphics or the design team. Provide clear description and
customer contact. Do not proceed with production until resolved.

**Equipment and Maintenance:** Mechanical failures, recurring defects, or
equipment anomalies go to Maintenance. Document symptoms, job context, and
any error codes. Reassign jobs to alternate equipment when possible.

**Commercial Issues:** Pricing disputes, contract terms, or credit concerns
go to Sales or Account Management. Production does not negotiate terms.

**Personnel:** Staffing shortages, performance issues, or workplace concerns
go to Human Resources or the Operations Manager. Maintain confidentiality.

**Supply and Procurement:** Material shortages, supplier failures, or quality
issues with incoming stock go to Purchasing. Escalate early to allow
alternative sourcing.

**Documentation:** Record escalation events with date, issue, actions taken,
and outcome. Supports root cause analysis and process improvement.
"""

