content = """
Quality control at Lake Victoria Print Works occurs at defined checkpoints to
catch defects early and prevent costly reprints. Clear criteria support
consistent decisions.

**Pre-Press Checkpoint:** Validate files for correctness before plate making or
digital release. Errors caught here cost least. Sign-off required before
production release.

**Press Start Checkpoint:** First sheets from press are reviewed for color,
register, and clarity. Compare to approved proof. Adjust or remake plates as
needed. Do not proceed to full run until start sheets are approved.

**Running Checkpoint:** Periodic pulls during the run to detect drift (color,
register, hickeys, etc.). Frequency depends on run length. Document any
adjustments and resultant waste.

**Finishing Checkpoint:** Verify cutting, folding, binding, and packing against
specifications. Check for damage, misalignment, or missing elements. Random
sampling for longer runs.

**Acceptance Criteria:** Define defect levels: critical (unacceptable),
major (negotiable), minor (acceptable within limits). Document and communicate
thresholds. When in doubt, hold and escalate rather than ship nonconforming product.
"""

