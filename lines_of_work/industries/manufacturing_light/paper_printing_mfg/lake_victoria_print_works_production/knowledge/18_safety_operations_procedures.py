content = """
Print production involves machinery, chemicals, and moving materials. Safety
procedures protect personnel and prevent incidents.

**Machine Safety:** Presses, cutters, and binders have pinch points and moving
parts. Follow lockout-tagout procedures for maintenance. Guards must be in
place during operation. Train operators on emergency stops and safe practices.

**Chemical Handling:** Inks, solvents, and fountain solution require appropriate
handling. Use personal protective equipment as specified. Store chemicals
correctly; ensure adequate ventilation. Maintain safety data sheets (SDS)
and train staff on hazards.

**Material Handling:** Paper and finished product can be heavy. Use proper
lifting techniques or equipment. Stack materials safely to prevent collapse.
Keep aisles clear.

**Emergency Procedures:** Know locations of fire extinguishers, first aid kits,
and emergency exits. Establish evacuation procedures and assembly points.
Report incidents and near-misses for corrective action.
"""

