title = "Troubleshooting Common Print Defects"

content = """
Print defects have identifiable causes. Systematic troubleshooting resolves
issues and prevents recurrence.

**Hickeys:** Small, circular defects often caused by paper dust, dried ink, or
contamination on the blanket or plate. Inspect and clean blankets, filters, and
paper. Check ink freshness.

**Misregister:** Color layers or pages out of alignment. Causes include paper
stretch, plate movement, or press adjustment. Verify paper conditioning, plate
mounting, and press settings. Check for humidity-related paper expansion.

**Mottling:** Uneven ink lay or color variation. Can result from ink-water
imbalance, uneven roller pressure, or substrate variation. Adjust ink and water;
check substrate consistency.

**Scumming:** Non-image areas picking up ink. Usually too little dampening or
plate problems. Adjust water form roller; inspect and replace plates if worn.

**Picking:** Paper surface lifting and transferring to blanket. Substrate too
weak for ink tack; reduce tack or change paper. Check for coating defects.

**Document Defects:** Record defect type, location, and suspected cause. Share
with maintenance and procurement when recurring. Train operators on recognition
and initial response.
"""

