content = """
O corte define o formato final. Máquinas guilhotina ou de corte e vinco
(corte e vincadeira) são usadas. O vinco prepara o papel para dobrar
sem rachar na linha de dobra.

**Tipos de Dobra:** Dobra paralela (dupla, tripla), dobra em sanfona,
dobra em cruz. Cada tipo exige imposição específica e influencia
o prazo. A gramatura do papel afeta a qualidade da dobra; papéis
muito grossos podem rachar sem vinco prévio.

**Colagem:** Cola branca ou hot melt para encartes, catálogos e
blocos. A colagem lateral (encarte) ou lombada depende do número
de páginas e formato. Lombada canoa (colagem da capa) é comum em
revistas e catálogos.

**Laminação:** Película plástica sobre a impressão (fosca ou brilho)
protege e dá acabamento. Aumenta custo e prazo.
"""  # noqa: E501

