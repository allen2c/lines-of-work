content = """
Cores especiais (Pantone, etc.) exigem chapas adicionais e
passagens extras na impressão. Cada cor especial aumenta
o setup e o tempo de máquina.

**Quando Vale a Pena:** Para marcas e identidade visual onde
a cor exata é crítica. Para tiragens muito pequenas, o custo
pode ser proibitivo; avaliar simulação em CMYK.

**Conversão para CMYK:** Se o cliente abrir mão da cor exata,
a gráfica ou o designer pode converter Pantone para CMYK.
A cor resultante pode variar um pouco. Sempre avisar o
cliente sobre a diferença.

**Cores Metálicas e Fluorescentes:** Exigem tintas específicas
e podem ter disponibilidade limitada. Consultar estoque
e fornecedores.
"""  # noqa: E501

