title = "Opacidade do Papel"

content = """
A opacidade indica quanto o papel impede a passagem de luz.
Papel com baixa opacidade permite que a impressão de uma
face transpareça para a outra (show-through). Em papéis
finos (75–90 g/m²) impressos frente e verso, isso pode
prejudicar a legibilidade.

**Soluções:** Aumentar gramatura, usar papel com maior
opacidade, reduzir área de impressão em uma das faces
ou evitar fundos muito escuros em ambas as faces.

**Especificação:** Fabricantes informam opacidade (ex: 90%).
Para miolos de catálogos e revistas, opacidade mínima
é desejável para conforto de leitura.
"""  # noqa: E501

