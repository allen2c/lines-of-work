content = """
Cancelamentos podem ocorrer por desistência do cliente ou
por necessidade da gráfica (ex: impossibilidade técnica).
A política deve definir consequências em cada cenário.

**Antes da Produção:** Se o pedido ainda não entrou em
produção, o cancelamento é mais simples. Pode haver retenção
de valor por custos já incorridos (provas, pré-impressão).
Comunicar claramente.

**Durante a Produção:** Cancelamento após início gera
custos de material e tempo. A política pode prever multa
ou cobrança parcial. Avaliar caso a caso com gerência.

**Após Conclusão:** Pedido concluído não é cancelável;
pode haver devolução em situações excepcionais, conforme
acordo.

**Documentação:** Registrar cancelamento, motivo e
acordos em escrito.
"""  # noqa: E501

