title = "Direção da Fibra do Papel"

content = """
O papel tem direção de fibra (geralmente indicada na embalagem:
parallel ou perpendicular ao sentido da bobina/folha). A fibra
influencia a dobra: dobrar contra a fibra tende a rachar;
com a fibra, dobra mais limpa.

**Vinco:** O vinco prepara o papel para dobrar. Em papéis
acima de 150 g/m², o vinco é recomendado. Sem vinco,
a dobra pode rachar ou ficar irregular.

**Orientação na Imposição:** O pré-impressão considera
a direção da fibra ao impor as páginas, para que as
dobras ocorram no sentido correto.

**Informação ao Cliente:** Para projetos com muitas dobras
ou papel grosso, a gráfica deve orientar sobre orientação
e vinco.
"""  # noqa: E501

