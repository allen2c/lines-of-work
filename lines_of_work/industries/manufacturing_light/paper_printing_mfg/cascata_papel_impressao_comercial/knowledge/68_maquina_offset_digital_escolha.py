content = """
A escolha entre offset e digital depende de tiragem,
prazo, especificação e custo. Regra de bolso: digital
para tiragens curtas (até 500–1000); offset para médias
e longas.

**Digital:** Sem chapas, setup rápido, personalização,
reimpressão sob demanda. Custo unitário alto em tiragens
grandes. Algumas cores e substratos têm limite.

**Offset:** Custo unitário baixo em tiragens maiores.
Qualidade estável, ampla variedade de papéis e acabamentos.
Setup e chapas têm custo fixo.

**Híbrido:** Alguns trabalhos usam offset para miolo
e digital para capa personalizada, ou vice-versa.
"""  # noqa: E501

