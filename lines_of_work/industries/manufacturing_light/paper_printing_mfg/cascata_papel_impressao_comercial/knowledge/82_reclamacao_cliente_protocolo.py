content = """
Ao receber uma reclamação: ouvir com atenção, não interromper,
anotar detalhes (pedido, problema, expectativa). Pedir amostras
ou evidências quando pertinente. Informar que a situação será
analisada e que haverá retorno em prazo definido (ex: 24–48h).

**Não:** Responsabilizar o cliente imediatamente, minimizar
o problema, prometer solução sem verificar viabilidade.

**Sim:** Reconhecer o incômodo, investigar com produção e
pré-impressão, propor solução concreta (reimpressão, desconto,
entrega complementar) conforme política e responsabilidade.

**Documentar:** Registrar a reclamação, a análise e a
resolução. Serve para melhoria e para referência em
caso de disputa.
"""  # noqa: E501

