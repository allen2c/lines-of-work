content = """
A encadernação mantém as páginas unidas e determina a abertura e
durabilidade do miolo. As opções variam por número de páginas,
formato e uso.

**Grampeado (saddle stitch):** Grampos no vinco central. Para revistas,
catálogos e encartes de até 64–96 páginas (dependendo da gramatura).
Custo menor, prazo mais curto.

**Colado (perfect binding):** Lombada colada, capa colada no miolo.
Para catálogos, livros e manuais com maior número de páginas. Permite
impressão na lombada.

**Espiral / Wire-O:** Fios metálicos ou plásticos passando por furos.
Páginas viram 360°. Comum em cadernos e manuais de uso frequente.

**Capa Dura:** Miolo colado em capa rígida. Para publicações de
maior valor e durabilidade. Custo e prazo maiores.
"""  # noqa: E501

