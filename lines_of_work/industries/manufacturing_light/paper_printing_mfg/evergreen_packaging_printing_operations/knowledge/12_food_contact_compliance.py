content = """
Packaging for food products must comply with strict regulations ensuring
that materials do not transfer harmful substances to food or affect safety
and quality. Understanding these requirements is essential for compliant
production.

**FDA Regulations (US Market):** The FDA regulates food contact substances
under 21 CFR. Indirect food additives (substrates, inks, coatings) must
comply with specific sections. Substances on the GRAS list (Generally
Recognized As Safe) require no pre-approval. Food Contact Notifications
(FCN) establish safety for new substances. Facilities must maintain
substance documentation demonstrating compliance.

**EU Framework Regulation 1935/2004:** European regulations require that
food contact materials do not endanger human health or change food composition
detrimentally. The Good Manufacturing Practices Regulation (2023/2006)
mandates quality control systems. Specific measures address plastics (10/2011),
recycled plastics (1616/2022), and coatings. Migration testing evaluates
chemical transfer under defined conditions.

**Migration Concepts:** Overall Migration Limits (OML) restrict total substance
transfer, typically 10 mg/dm² or 60 mg/kg. Specific Migration Limits (SML)
control individual substances of concern. Testing uses food simulants
(representing aqueous, acidic, alcoholic, fatty foods) under time/temperature
conditions modeling actual use. Testing validates that packaging meets
regulatory thresholds.

**Heavy Metal Restrictions:** Lead, cadmium, mercury, and hexavalent chromium
face strict limits in most jurisdictions. Testing protocols (EN 12472 for
metals and alloys) define extraction and measurement methods. Compliance
often requires third-party laboratory certification.

**Compliance Documentation:** Technical files must document substance
composition, migration testing, and declarations of compliance. Records
enable traceability from raw materials through finished packaging. Brand
owners increasingly require full supply chain transparency and testing
certification from packaging suppliers.
"""

