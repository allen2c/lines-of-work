content = """
Corrugated board specifications communicate structural requirements through
standardized notation systems understood across the supply chain. Proper
specification ensures boxes meet performance requirements without excess cost.

**ECT vs. Mullen Specifications:** Two primary systems define board strength.
Edge Crush Test (ECT) measures vertical stacking performance in pounds per
inch. Common ECT ratings include 23 ECT (light duty), 26 ECT (standard), 32
ECT (heavy duty), 44 ECT (very heavy), and 48+ ECT for industrial applications.
Mullen (burst strength) measures puncture resistance in pounds per square inch.
ECT has largely replaced Mullen for most applications because it directly
predicts stacking performance in warehouse environments.

**Standard Specification Format:** Board grades are expressed as liner weight/
medium weight/flute type. For example, "35/26 C" indicates 35 lb/1000 sq ft
outer liner, 26 lb medium, C-flute. Double wall specifications chain grades:
"35/26 C/35/26 B" describes a BC double wall with specified liners and mediums.

**Linerboard Types:** Kraft linerboard offers maximum strength and printability.
Recycled linerboard reduces environmental impact and cost but may have lower
burst strength. White top linerboard provides a premium printing surface for
high-graphics retail packaging. Mottled white offers a middle ground with some
recycled content and acceptable print quality.

**Basis Weight Considerations:** Linerboard weights range from 23 lb to 90+
lb per thousand square feet. Heavier liners increase burst strength and print
smoothness. Medium weights similarly vary, with heavier mediums improving
cushioning and stacking performance.

**Custom vs. Stock Specifications:** Stock board grades offer cost efficiency
and quick availability. Custom specifications optimize performance for specific
applications but require longer lead times and minimum order quantities. Most
packaging operations maintain inventory of common grades (26/26 C, 35/26 C)
while ordering custom specifications for high-volume or specialized needs.
"""

