content = """
Preventive maintenance preserves printing press performance, prevents
breakdowns, and extends equipment life. Structured maintenance programs
balance scheduled downtime against reliability requirements.

**Maintenance Categories:** Breakdown maintenance responds to failures,
often causing unplanned downtime and collateral damage. Preventive
maintenance performs scheduled service before failures occur. Predictive
maintenance uses monitoring (vibration, temperature, oil analysis) to
predict failures and time interventions optimally. Total productive
maintenance engages operators in routine care and improvement activities.

**Daily Maintenance:** Operators perform cleaning, lubrication, and inspection
at shift start and end. Tasks include: wiping rollers and cylinders; checking
ink fountain levels; inspecting belts and chains; cleaning sensors and
cameras. Daily care prevents minor issues from escalating and maintains
baseline performance.

**Periodic Maintenance:** Weekly, monthly, and quarterly tasks address
wear components and calibration. Anilox roller cleaning restores cell
volume. Blanket and plate cylinder maintenance ensures consistent impression.
Registration system calibration maintains accuracy. Drying and curing system
checks verify proper operation. Scheduled maintenance windows, often on weekends
or holidays, minimize production impact.

**Annual Overhaul:** Major maintenance rebuilds critical assemblies: gearbox
inspection, bearing replacement, frame alignment verification. Overhauls may
require weeks of downtime and external specialist support. Planning ensures
parts availability and minimal production disruption.

**Documentation and Analysis:** Maintenance records track service history,
spare parts consumption, and failure patterns. Analysis identifies recurring
issues guiding improvement or replacement decisions. Mean time between
failures (MTBF) and mean time to repair (MTTR) metrics quantify reliability
and maintenance efficiency.
"""

