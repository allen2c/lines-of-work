content = """
Optical margin alignment, also known as "hanging punctuation," is a
sophisticated typographic technique used to create a more visually consistent
edge for justified text. It involves slightly shifting certain characters,
such as punctuation marks (commas, periods, quotation marks) and some letters
(like 'A' or 'V'), outside the main text block margin.

Punctuation marks and certain character shapes have less visual weight than
full-bodied letters like 'H' or 'M.' When these characters align exactly
with the margin, they can create the illusion of an uneven or "bumpy" edge.
By allowing these elements to "hang" slightly into the margin, the overall
text block appears much straighter and more harmonious to the eye.

At Vellum Craft Press, we apply optical margin alignment to all our high-end
layouts. This technique is particularly effective for titles, pull quotes,
and long-form body text. It is a subtle detail that many readers might not
consciously notice, but it contributes significantly to the overall sense
of professional polish and craftsmanship that defines our work.
"""

