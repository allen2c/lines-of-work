content = """
Margins are not merely the empty space around a block of text; they are
active participants in the overall composition of a page. Well-proportioned
margins provide "breathing room" for the eye, prevent the content from
feeling cramped, and offer a comfortable area for the reader to hold the
book without obscuring the text.

Historically, the proportions of the page and its margins have been
informed by the Golden Ratio (approximately 1.618). This mathematical
constant is found throughout nature and has been used by architects and
artists for millennia to create aesthetically pleasing proportions. In book
design, the Van de Graaf canon is a classic method for determining page
proportions and margin widths that result in a perfectly balanced layout.

Typically, the inner margin (gutter) is the narrowest to account for the
binding, while the outer margin is wider. The top margin is often slightly
smaller than the bottom margin, which provides a weighted base for the
page content. These proportions ensure that the text block sits harmoniously
on the paper, creating a sense of classical elegance and structural
integrity that is a hallmark of Vellum Craft Press's design philosophy.
"""

