content = """
Book design is a specialized field within typography with its own set of
well-established conventions and standards. These conventions have evolved
over centuries to create a predictable and comfortable reading experience
for the audience.

The structure of a book typically includes the front matter (title page,
copyright page, table of contents, etc.), the main body (divided into
chapters or sections), and the back matter (index, bibliography, etc.).
Each of these components has its own specific layout requirements. For
example, the main body text should have consistent margins, running heads,
and folios (page numbers) that guide the reader through the content.

At Vellum Craft Press, we adhere to these classical book design conventions
while also incorporating modern refinements. We pay close attention to the
placement of page numbers, the formatting of headings, and the treatment of
captions and pull quotes. By following these established standards, we create
books that are not only beautiful but also intuitive and easy to navigate
for the reader.
"""

