title = "Handling Widows and Orphans"

content = """
In typography, widows and orphans are short lines or single words that appear
at the beginning or end of a column or page, isolated from the rest of the
paragraph. These "typographic errors" disrupt the visual flow of the text
and create awkward gaps that can distract the reader.

An orphan is a single line or word at the end of a paragraph that appears at
the top of a new column or page. A widow is a single line or word at the
beginning of a paragraph that appears at the bottom of a column or page.
At Vellum Craft Press, we consider these to be unacceptable in our final
layouts and take several steps to eliminate them.

The most common way to fix widows and orphans is to adjust the tracking or
word spacing of the paragraph to either pull the isolated line back onto the
previous page or push an additional line forward to join it. In some cases,
we might also work with the editorial team to slightly rewrite a sentence
to change its length. This attention to detail ensures that every page has
a solid and professional appearance, with no distracting gaps or
isolated fragments of text.
"""

