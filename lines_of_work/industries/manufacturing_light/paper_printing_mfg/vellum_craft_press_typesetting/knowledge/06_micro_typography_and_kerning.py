content = """
Micro-typography is the art of fine-tuning the spacing and appearance of
individual characters and words to create a harmonious and readable page.
The most critical aspect of micro-typography is kerning, which is the
adjustment of the space between specific pairs of letters to ensure
consistent visual spacing.

While most modern fonts include built-in kerning pairs, they are often
insufficient for high-end typesetting, especially at larger sizes or in
titles. A skilled typesetter will manually adjust the spacing between
characters to eliminate awkward gaps or cramped combinations. This is
particularly important for character pairs with diagonal strokes (like 'A'
and 'V') or those with large counters (like 'T' and 'o').

At Vellum Craft Press, we aim for "even typographic color," meaning the overall
density of the text block should be uniform across the page. This is
achieved through careful kerning and attention to other micro-typographic
details like letterspacing (tracking) and word spacing. The goal is to
create a seamless reading experience where the eye is never distracted by
inconsistent spacing or awkward character combinations.
"""

