content = """
Ligatures are special characters that combine two or more letters into a
single, unified glyph. This technique was originally developed by scribes
and later by early printers to prevent certain character combinations from
clashing or to improve their legibility.

At Vellum Craft Press, we use ligatures to add a touch of historical
precision and elegance to our layouts. Standard ligatures, such as 'fi,'
'fl,' and 'ff,' are commonly used in body text to prevent the overlapping
of characters. Discretionary ligatures, such as 'st' or 'ct,' are more
decorative and are typically reserved for titles or special editions.

Beyond ligatures, high-end fonts often include an extensive set of alternate
glyphs, such as swashes (ornamental flourishes on letters), small caps,
and old-style numerals (figures with varying heights and descenders). The
skillful selection and application of these alternate characters are
essential for creating layouts that are both visually rich and typographically
sophisticated. We use these features to add a layer of depth and personality
to our work that is simply not possible with standard commercial typefaces.
"""

