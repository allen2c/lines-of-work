title = "订单接收与评审流程"

content = """
- 销售部每日16:00前将新订单（含图纸、BOM、交期要求）录入ERP系统，状态为"待评审"。
- 生产计划员次日9:00前完成评审：核对图纸完整性、BOM物料编码、工艺路线（开料→铣型→组装→打磨→涂装→包装）。
- 若图纸或BOM缺失，退回销售部补充，并记录退回原因与时间。
- 评审通过后，在ERP中将订单状态改为"已确认"，并生成生产任务单（含唯一任务编号、预计开工/完工日期）。
- 对于定制订单（非标尺寸、特殊木材），需额外与技术部确认工艺可行性，耗时不超过1个工作日。
"""

