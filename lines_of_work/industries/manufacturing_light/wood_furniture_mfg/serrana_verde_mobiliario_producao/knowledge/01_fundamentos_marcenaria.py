content = """
A marcenaria é a arte e técnica de trabalhar a madeira para produzir peças
funcionais e estéticas. Na Serrana Verde, combinamos técnicas tradicionais
com equipamentos industriais. O processo básico envolve corte, desbaste,
montagem e acabamento.

O corte define as dimensões brutas da peça. O desbaste (plaina, tupia)
aprofina e modela. A montagem une as peças por colagem, parafusos ou
encaixes. O acabamento protege e embeleza a superfície.

Compreender a direção das fibras (veio da madeira) é crucial: cortes
perpendiculares ao veio são mais resistentes; cortes paralelos podem
rachar. O teor de umidade da madeira afeta estabilidade dimensional.
"""

