content = """
L'applicazione a spruzzo è il metodo più diffuso nel reparto per fondi e finiture, perché consente uniformità e produttività. Legnovivo utilizza pistole a bassa pressione ed elevato trasferimento (HVLP), sistemi airless e airmix, scelti in funzione del prodotto e della geometria del pezzo.

Buone pratiche applicative:
- si regolano pressione dell'aria, portata del prodotto e ampiezza del ventaglio in base alla vernice e alla superficie;
- si mantiene una distanza e una velocità di passata costanti, con sovrapposizione delle passate di circa il 50% per evitare strisce e zone scoperte;
- la pistola si muove parallela alla superficie, non "a ventaglio" attorno al polso, che genera spessori disomogenei;
- si controllano la grammatura applicata e il numero di mani per rispettare lo spessore previsto dal ciclo;
- si spruzza sempre con l'impianto di aspirazione attivo e i DPI respiratori indossati.

Un'applicazione scorretta genera colature (troppo prodotto), buccia d'arancia (prodotto troppo viscoso o pressione errata), overspray secco e disuniformità di brillantezza. Il responsabile addestra gli operatori alla tecnica, verifica la taratura delle pistole e promuove il recupero dell'overspray e la riduzione degli sprechi, che incidono su costi e ambiente. Le pistole vanno pulite a fine turno per evitare intasamenti degli ugelli.
"""

