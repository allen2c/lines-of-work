title = "Parametri della cabina di verniciatura"

content = """
La cabina di verniciatura è l'ambiente controllato in cui si applica il prodotto e in cui si captano nebbie e vapori. La sua corretta gestione influisce su qualità, sicurezza e consumi. Legnovivo dispone di cabine pressurizzate con filtrazione dell'aria in ingresso e abbattimento dell'overspray a velo d'acqua o a filtri a secco.

Parametri controllati dal responsabile prima e durante le lavorazioni:
- portata e velocità dell'aria: un flusso corretto allontana i vapori dall'operatore e trasporta l'overspray verso i filtri, senza però essere così forte da seccare la vernice prima della distensione;
- filtrazione in ingresso: i filtri dell'aria immessa trattengono la polvere che altrimenti si depositerebbe sulla superficie fresca;
- temperatura e umidità relativa: valori adeguati (spesso intorno a 20 °C e umidità moderata) garantiscono una corretta essiccazione, soprattutto con vernici all'acqua;
- pulizia delle pareti e delle griglie e stato dei filtri di abbattimento, che vanno sostituiti quando saturi.

Filtri saturi riducono l'aspirazione, aumentano il rischio di accumulo di overspray combustibile e peggiorano la qualità. Il responsabile tiene un registro dei controlli e delle sostituzioni, verifica gli allarmi di depressione e assicura che la cabina non venga mai utilizzata con l'impianto di ventilazione fermo o in avaria.
"""

