content = """
Formaldehyde is used in some leather finishing and resin applications. It is restricted due
to skin sensitisation and carcinogenicity concerns. Limits vary by market and product type.

**Regulatory Limits:** EU Oeko-Tex and many brands limit free and released formaldehyde
(e.g. 16–75 mg/kg depending on product class). Japan has JIS L 1041. US CPSIA does not
set a general limit but some states and brands do.

**Testing:** Methods typically extract or release formaldehyde and quantify by colorimetric
or chromatographic means. Results are reported as mg/kg. Ensure the test method matches
the regulatory or customer requirement.

**Reduction Strategies:** Use low-formaldehyde or formaldehyde-free resins; optimise
curing; ensure adequate ventilation and post-curing. Document resin specifications and
batch testing.
"""

