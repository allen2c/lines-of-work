title = "Raw Material Traceability and Sourcing"

content = """
Traceability of raw hides and fur is essential for regulatory compliance and customer assurance.
Supply chain due diligence requires documented origin, species identification, and handling history
from slaughter or harvest through to finished product.

**Documentation Requirements:** Maintain records of supplier name, location, batch or lot number,
date of receipt, and species. For fur, CITES permits and origin certificates must be retained.
For leather, slaughterhouse or tannery-of-origin documentation supports claims of legality and
animal welfare.

**Risk Assessment:** Evaluate suppliers for compliance with local animal welfare laws, anti-
illegal-logging or wildlife regulations, and conflict-mineral–type due diligence where applicable.
High-risk origins may require additional verification or third-party audits.

**Retention:** Retain traceability records for the minimum period required by law (often 5–10
years) and by customer contracts. Records must be retrievable for audits and incident
investigations.
"""

