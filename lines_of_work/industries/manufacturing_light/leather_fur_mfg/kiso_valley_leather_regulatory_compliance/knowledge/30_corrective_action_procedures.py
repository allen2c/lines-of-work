"""Corrective action procedures for non-conformances."""

title = "Corrective Action Procedures"

content = """
不適合が発見された場合の是正措置は、再発防止の観点から体系的に実施する。ISO 9001、
ISO 14001等のマネジメントシステムでは、是正処置プロセスが要求される。

**手順：** 不適合の記録 → 原因分析（5 Why、フィッシュボーン等）→ 是正措置の決定 →
実施 → 効果確認 → 記録の保管。

**原因分析：** 表面的な対処ではなく、根本原因を特定する。なぜその不適合が発生したか、
なぜ検出されなかったかを問う。

**効果確認：** 是正措置の実施後、同様の不適合が再発していないことを確認する。監査で
是正要求が出た場合は、証拠を提出して閉じる。
"""

