"""Azo dye restrictions in leather and fur products."""

title = "Azo Dye Restrictions"

content = """
Azo dyes that can release certain aromatic amines are restricted or banned in many jurisdictions
because some of these amines are carcinogenic. Leather and fur dyed with such colorants must
not be placed on the market.

**Regulatory Scope:** EU REACH Annex XVII restricts 22 aromatic amines. Similar lists exist
in China, Japan, and other markets. The restriction applies to textiles and leather that may
release these amines under reductive conditions.

**Testing:** Standard methods simulate reductive cleavage and quantify released amines.
Compliance is typically verified by testing dyed materials or finished products. Suppliers
of dyes and dyed materials should provide declarations of conformity.

**Mitigation:** Use dyes from suppliers that certify compliance with the relevant RSL.
Maintain a positive list of approved dyes and periodically verify with testing. Document
all dye formulations and test results.
"""

