content = """
制限物質リスト（RSL）は、皮革・毛皮製品に含有してはならない、または含有量が制限される
化学物質の一覧です。REACH Annex XVII、顧客RSL、業界イニシアチブ（OEKO-TEX、ZDHC等）
が主要な参照源です。

**REACH Annex XVII：** アゾ染料、六価クロム、カドミウム、ニッケル、フタル酸エステル、
PAH、PFOS等が制限または禁止されています。皮革製品は「長時間皮膚に接触する製品」として、
ニッケル放出量等の基準が適用されます。

**顧客RSL：** ブランドや小売業者は、REACHを上回る独自のRSLを設定することがあります。
アゾ染料の分解生成物、PCP、ホルムアルデヒド、六価クロム等の許容値が厳格化される
傾向にあります。顧客からRSLを入手し、仕様書に組み込みます。

**OEKO-TEX、ZDHC：** 繊維・皮革業界向けの認証プログラムでは、MRSL（製造工程制限物質
リスト）とRSLを定めています。認証取得を目指す場合は、これらの基準に適合した化学品の
選定と工程管理が必要です。
"""

