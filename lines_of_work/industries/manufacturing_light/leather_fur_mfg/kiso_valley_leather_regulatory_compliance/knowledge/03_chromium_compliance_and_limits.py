title = "Chromium Compliance and Limits"

content = """
Chromium is widely used in leather tanning (chromium III). The regulatory concern is chromium
VI (hexavalent chromium), which can form under certain conditions and is a known carcinogen.

**Regulatory Limits:** EU REACH restricts Cr VI to 3 mg/kg in leather articles that come into
contact with skin. Similar limits apply in other markets. Some brands require even lower
thresholds or zero detection.

**Formation and Prevention:** Cr VI can form from Cr III through oxidation (heat, pH, UV).
Prevention measures include proper post-tanning washing, use of reducing agents, pH control,
and storage conditions. Finished leather should be tested before release.

**Testing:** Standard methods include ISO 17075 or equivalent. Testing frequency should be
risk-based: every batch for high-risk processes, periodic for stable processes. Retain
test reports for audits and customer requests.
"""

