content = """
皮革・毛皮製品の製品安全基準は、化学物質、物理的危険、可燃性等をカバーします。主要な
規格として、ISO、ASTM、JIS、ENが参照されます。

**化学的安全性：** 皮革製品では、六価クロム、アゾ染料、ホルムアルデヒド、重金属、フタル酸
エステル等が規制対象です。試験方法はISO 17072（金属含有量）、ISO 17226（ホルムアルデヒド）、
IUC 20（六価クロム）等に規定されています。

**物理的安全性：** 子供向け製品では、小部品の窒息リスク、鋭利な縁、紐の長さ等が規制されます。
CPSIA、EN 71シリーズ、ST 2016（日本）を参照します。

**可燃性：** 衣料品やインテリア用途の皮革・毛皮には、可燃性試験が要求される場合があります。
実務では、顧客の技術仕様書および輸出先の規制を確認し、必要な試験を第三者機関に依頼して
実施し、証明書を取得します。
"""

