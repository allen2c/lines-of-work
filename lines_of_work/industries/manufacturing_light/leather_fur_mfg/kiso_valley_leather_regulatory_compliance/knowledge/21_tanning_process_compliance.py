content = """
鞣製工程の規制適合は、皮革製造における化学物質使用と排出の管理に直結する。クロム鞣し、植物鞣し、
その他の鞣法それぞれに、使用可能な化学物質、濃度、廃液処理に関する要件がある。

**クロム鞣し：** 三価クロムは一般的に許容されるが、六価クロムへの酸化を防ぐ工程管理が必要。
REACH Annex XVIIでは六価クロムの皮革中濃度に制限がある。鞣し後の還元・洗浄を適切に行い、
保管条件（湿度・温度）を管理して六価クロムの生成を抑制する。

**植物鞣し：** タンニン等の天然由来物質を使用する場合、重金属や残留農薬の混入に注意。原料の
産地証明と成分分析が求められる場合がある。

**工程記録：** 各ロットの鞣剤種類、使用量、浴比、温度、時間を記録し、トレーサビリティと
監査証拠を確保する。規制変更時には工程の再評価が必要となる。
"""

