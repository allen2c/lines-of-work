content = """
皮革・毛皮の品質グレーディングは、国際規格および業界慣行に基づいて行われます。取引
および製品適合性の判断において、共通の基準を持つことが重要です。

**皮革のグレード：** IULTCS（国際皮革技術者・化学者協会）や各国の業界団体が基準を
定めています。傷、色むら、厚みの均一性、引張強度、伸び率等を評価します。牛革では
フルグレイン、トップグレイン、コレクテッドグレイン等の区分も品質表示に用いられます。

**毛皮のグレード：** 毛の密度、光沢、柔らかさ、色の均一性、傷の有無等で評価されます。
ミンク、フォックス等の養殖毛皮では、産地や農場による格付けが取引慣行として存在します。

**実務上の活用：** 入荷検査、工程間検査、出荷検査において、グレード基準に基づく
合否判定を行います。顧客が独自のグレードを指定する場合は、その基準を文書化し、
検査員に周知します。不適合時の扱い（返品、値引き、特採）も事前に取り決めておきます。
"""

