content = """
Erdgas ist der einzige Brennstoff des Rheinstrom Kraftwerks. Die Lieferung erfolgt
über die Ferngasleitung der Open Grid Europe. Am Werkszaun: Druck etwa 25 bar.

**Gasdruckreduzierstation (GPRS):**
Reduzierung auf den für die Gasturbinen erforderlichen Druck (typisch 18–20 bar).
Zwei Stränge für Redundanz. Druckregelung, Filter, Heizung (falls erforderlich).

**Gasturbinen-Gasversorgung:**
Vom GPRS zu den Brennkammern. Gasdruck und -temperatur werden überwacht. Bei
Unterdruck oder Temperaturabweichung Anfahr- oder Lastaufnahme blockieren.

**Sicherheit:**
Gasleckage-Detektion im GPRS und an kritischen Punkten. Notabsperrung bei Detektion.
Regelmäßige Dichtheitsprüfungen und Übungen zur Notabschaltung.
"""

