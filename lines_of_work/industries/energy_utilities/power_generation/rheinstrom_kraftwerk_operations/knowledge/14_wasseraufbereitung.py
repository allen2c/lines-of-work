content = """
Speisewasser und Kühlwasser müssen bestimmte Qualitätsanforderungen erfüllen, um
Korrosion, Ablagerungen und biologisches Wachstum zu vermeiden.

**Speisewasser:**
Entgastes, entsalztes Wasser. Leitfähigkeit, pH-Wert, Sauerstoff und Inhibitoren
überwachen. Grenzwerte einhalten, sonst Kessel gefährdet.

**Kühlwasser:**
Kühlturm-Wasser: pH-Wert, Härte, Chlorung. Biozid-Dosierung nach Vorschrift.
Regelmäßige Kontrollen und Probenahmen.

**Chemikalien:**
Lagerung und Handhabung gemäß Sicherheitsdatenblättern. Dosierungen protokollieren.
Überdosierung vermeiden.
"""

