title = "冷態啟動步驟（停機超過72小時）"

content = """
- 鍋爐上水至汽包水位 -100 mm（正常水位以下），啟動強制循環泵（若為直流爐則建立啟動流量）。
- 點火前爐膛通風吹掃5分鐘（風量 > 30%額定），確認無殘餘可燃氣體。
- 依序點燃第一層油槍（2支對角），控制爐膛出口煙溫升速率 < 50°C/h，避免汽包熱應力過大。
- 當主蒸汽壓力達 0.5 MPa 時，開啟過熱器疏水閥；壓力達 1.0 MPa 時，投入高壓旁路系統。
- 汽機暖機：主蒸汽溫度需高於汽機金屬溫度 50°C以上，以 300 rpm 低速暖機 30 分鐘，監測振動及脹差。
- 升速至 3000 rpm 後進行閥門全行程測試，確認調門、主汽門動作正常。
- 並網前調整發電機電壓與系統電壓一致，合閘後立即帶初始負載 30 MW，避免逆功率。
"""

