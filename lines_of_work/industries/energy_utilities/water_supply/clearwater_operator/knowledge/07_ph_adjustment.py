title = "pH Adjustment (Lime and CO₂)"

content = """
- Raw water pH typically 6.8–7.2; target finished water pH 7.5–8.0 for corrosion control and optimal disinfection.
- Add hydrated lime slurry (5% solution) to raise pH; feed rate 5–15 mg/L as Ca(OH)₂.
- Add carbon dioxide (CO₂) to lower pH if needed; feed rate 2–10 mg/L.
- Monitor pH online at rapid mix, after lime addition, and at clearwell outlet.
- If pH exceeds 8.5, reduce lime feed by 10% and add CO₂; if pH below 6.5, increase lime feed by 10%.
"""

