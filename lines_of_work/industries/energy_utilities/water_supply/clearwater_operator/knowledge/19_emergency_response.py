title = "Emergency Response Procedures"

content = """
- Chlorine leak: evacuate area, activate scrubber, call 911, notify supervisor; use SCBA to isolate valve.
- Power outage: backup generator starts automatically; verify SCADA and pumps are online; check battery backups on critical instruments.
- Major water main break: reduce plant flow to prevent tank overflow; coordinate with distribution to isolate break.
- Chemical spill: contain with absorbent pads, notify hazmat team, document spill volume and location.
- All emergencies must be logged in the incident report system within 1 hour.
"""

