title = "Proteção catódica"

content = """
A proteção catódica reduz a corrosão em dutos enterrados aplicando corrente ou
ânodos que polarizam o metal. Sistemas por corrente impressa (retificadores) ou
por ânodos galvânicos são usados conforme projeto.

**Monitoramento:** Potenciais de proteção são medidos nos pontos de teste conforme
programa. Valores fora da faixa indicada exigem análise (falha de ânodo, interferência,
revestimento danificado).

**Manutenção:** Retificadores são vistoriados periodicamente; ânodos galvânicos
têm vida útil limitada e são substituídos quando o potencial deixa de ser atendido.
Registros são mantidos para auditoria e integridade.
"""

