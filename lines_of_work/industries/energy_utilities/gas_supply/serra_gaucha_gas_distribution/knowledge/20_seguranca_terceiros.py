title = "Segurança de terceiros e contratados"

content = """
Terceiros e contratados que atuam em instalações ou na faixa de dutos devem
cumprir os requisitos de segurança da concessionária: indução, PTW, EPI e
supervisão quando aplicável.

**Indução:** Antes do trabalho, o contratado recebe informações sobre riscos,
procedimentos de emergência e regras do local. A participação é registrada.

**Supervisão:** Atividades de maior risco podem exigir acompanhamento por
técnico da concessionária. A PTW define a necessidade e o responsável.
"""

