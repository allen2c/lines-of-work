content = """
Trabalhos a quente (solda, corte, esmeril, uso de chama) em áreas com presença
ou proximidade de gás exigem permissão específica e medidas de controle para
evitar ignição.

**Requisitos:** Área livre de gás (teste com detector), extintor e vigilante
de incêndio. Superfícies combustíveis devem ser protegidas ou umedecidas.
Após o trabalho, inspecione a área por tempo definido para detectar brasas
ou aquecimento tardio.

**Aprovação:** A permissão é emitida pelo responsável pelo local após
verificação das condições. Validade é limitada ao turno ou ao período
estabelecido.
"""

