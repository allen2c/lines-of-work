content = """
O gás natural distribuído deve atender especificações de qualidade (poder
calorífico, composição, ponto de orvalho, etc.) definidas por contrato
e norma. Desvios podem afetar equipamentos dos clientes e a segurança.

**Monitoramento:** Amostragem e análise são feitas conforme programa ou
em eventos específicos. Resultados são registrados e comparados aos limites.

**Não conformidade:** Se a qualidade estiver fora da especificação, a origem
(de transporte, rede ou cliente) deve ser investigada. Comunicação ao
transportador ou ao cliente segue procedimento e contrato.
"""

