title = "Manutenção corretiva"

content = """
A manutenção corretiva responde a falhas ou defeitos identificados em inspeção,
alarme ou reclamação. Deve ser planejada com segurança (LOTO, PTW) e executada
conforme procedimento.

**Priorização:** Falhas que afetam segurança ou continuidade do fornecimento
têm prioridade. Outras são agendadas conforme criticidade e disponibilidade
de recursos.

**Execução:** Use o procedimento específico do equipamento ou do tipo de falha.
Após o reparo, teste e documente. Se a causa raiz for relevante, registre para
análise e melhoria preventiva.
"""

