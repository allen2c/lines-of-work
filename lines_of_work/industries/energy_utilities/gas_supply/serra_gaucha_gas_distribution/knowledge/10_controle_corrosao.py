title = "Controle de corrosão"

content = """
A corrosão em dutos e equipamentos é mitigada por revestimento, proteção catódica e
monitoramento. O controle reduz o risco de vazamento e prolonga a vida dos ativos.

**Revestimento:** Revestimento externo dos dutos é especificado e aplicado na construção.
Em reparos ou exposições, a qualidade do revestimento deve ser mantida ou restaurada.

**Proteção catódica:** Anodos e retificadores são inspecionados e ajustados conforme
programa. Leituras de potencial são registradas; tendências fora do esperado disparam
ação corretiva.

**Atmosfera e internos:** Equipamentos em atmosfera agressiva ou em contato com
fluidos são tratados conforme norma (revestimento, material especificado ou inibidor).
"""

