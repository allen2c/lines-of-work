content = """
Grid codes define the technical and operational requirements that generators must meet to
connect to and operate on the transmission system. In the Gulf region, each country
(UAE, KSA, Oman, Bahrain, Qatar, Kuwait) has its own grid code, though efforts toward
GCC harmonization are underway.

**Core Requirements:** Grid codes typically specify voltage and frequency limits, fault-ride-through
(FRT) capability, reactive power range, ramp rates, and power factor requirements. Compliance
is verified during commissioning and monitored continuously during operation.

**Harmonization:** The GCC Interconnection Authority promotes alignment across member states.
Operators of plants feeding multiple markets or regional interconnections must satisfy the
most stringent of applicable codes.

**Enforcement:** Non-compliance can result in curtailment, penalties, or disconnection. Crystal
Dunes Solar maintains documentation for each host grid and conducts periodic audits to ensure
ongoing compliance.
"""  # noqa: E501

