title = "Curtailment Management and Documentation"

content = """
Curtailment is the reduction of generation below the available resource (sunlight) due to
grid constraints, TSO instructions, or other operational reasons. It directly impacts
revenue and must be managed and documented with care.

**Causes:** Common causes include grid congestion, voltage limits, frequency response
requirements, maintenance on adjacent assets, and TSO-directed reductions for system
balance. In the Gulf, dust events and grid upgrades can also trigger curtailment.

**Process:** When curtailment is required, the grid operations team issues setpoints to
the plant controller, records the instruction source and duration, and logs the lost
generation for reporting and potential compensation claims.

**Reporting:** All curtailment events are reported to management, regulators, and relevant
stakeholders. Accurate records support performance guarantees, insurance, and regulatory
compliance.
"""  # noqa: E501

