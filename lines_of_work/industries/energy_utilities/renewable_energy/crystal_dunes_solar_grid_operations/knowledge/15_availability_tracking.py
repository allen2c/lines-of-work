title = "Availability and Performance Tracking"

content = """
Availability measures the fraction of time the plant is capable of generating (excluding
planned outages and external curtailment). Performance tracking compares actual output
to expected output under given conditions.

**Metrics:** Common metrics include technical availability (excluding grid curtailment),
commercial availability (including grid curtailment), capacity factor, and performance
ratio (PR). Each serves a different purpose for reporting and contracts.

**Grid-Related Losses:** Curtailment, grid outages, and interconnection faults reduce
availability or performance. These are tracked separately from plant-side losses
(equipment faults, soiling, maintenance).

**Reporting:** Availability and performance data feed into regulatory reports, O&M
contracts, and investor updates. Consistent definitions and audit trails are essential.
"""  # noqa: E501

