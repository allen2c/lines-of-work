title = "Black Start Capability and Restoration"

content = """
Black start is the ability to start a generator without external grid power. Most solar
plants do not have black start capability; they require the grid to be energized for
inverter operation. This is acceptable for the vast majority of grid codes.

**Restoration Role:** After a widespread outage, the TSO restores the grid in a defined
sequence. Solar plants typically reconnect when the grid is re-energized at their POC.
Operations coordinate with the TSO on reconnection timing and ramp-up.

**Hybrid Plants:** Plants with storage or diesel backup may have limited black start
capability for local loads. This is plant-specific and does not apply to standard
utility-scale solar.

**Documentation:** The plant's restorability and dependencies are documented for TSO
planning. No false claims of black start capability should be made.
"""  # noqa: E501

