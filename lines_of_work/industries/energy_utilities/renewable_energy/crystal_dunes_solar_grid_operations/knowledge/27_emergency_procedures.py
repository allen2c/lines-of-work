content = """
Emergency procedures define the response to critical grid events: partial or total
blackout, frequency excursions, voltage collapse, or TSO-directed emergency actions.
Safety and grid stability override all other considerations.

**Immediate Actions:** Follow TSO instructions without delay. If no instruction is
received and the plant detects abnormal grid conditions (e.g., frequency outside
dead band), predetermined tripping or curtailment may be required. Procedures specify
conditions and actions.

**Communication:** Notify the TSO and internal management as soon as practicable.
Maintain communication throughout the event and during restoration.

**Post-Event:** Do not reconnect until authorized. Document the event and participate
in root cause analysis. Update procedures if lessons learned indicate improvements.
"""  # noqa: E501

