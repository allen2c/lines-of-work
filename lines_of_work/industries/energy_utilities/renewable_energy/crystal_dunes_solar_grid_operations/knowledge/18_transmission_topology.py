title = "Transmission Topology and POC Location"

content = """
The point of connection (POC) is the electrical boundary where the plant connects to
the transmission system. Understanding the topology around the POC is essential for
voltage control, fault response, and coordination with the TSO.

**Upstream Assets:** The POC is typically at a substation or switching station. The
transmission line voltage level (e.g., 132 kV, 400 kV) and the neighboring network
topology affect voltage stability and fault behavior.

**Downstream:** From the POC, the plant's collector system connects to inverters and
arrays. Internal faults and maintenance on the collector system can affect grid
interface availability.

**TSO Awareness:** Grid operations maintains up-to-date knowledge of planned outages,
new connections, and topology changes that may affect the plant's operating context.
"""  # noqa: E501

