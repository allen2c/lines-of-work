title = "Performance Ratio (PR) Berechnung"

content = """
- PR = (tatsächlicher Ertrag / (Globalstrahlung * installierte Leistung)) * 100. Werte auf Monatsbasis.
- Bei PR < 75 % für PV: Prüfe auf Verschmutzung, Verschattung, Moduldefekte oder Wechselrichterwirkungsgrad.
- Für Wind: PR = (tatsächlicher Ertrag / (Windenergiepotenzial * Anlagenverfügbarkeit)) – Ziel > 90 %.
- Dokumentiere die PR-Werte monatlich und leite bei negativem Trend Maßnahmen ein.
"""

