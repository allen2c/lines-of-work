title = "Forma e contenuto dell'offerta di acquisto"

content = """
L'offerta di acquisto è il primo impegno formale dell'acquirente. Deve essere
chiara e completa per evitare equivoci e contenziosi.

**Contenuto tipico:** Identificazione delle parti (acquirente e venditore),
descrizione dell'immobile (come da visura o da preliminare), prezzo offerto,
modalità di pagamento (anticipo, saldo al rogito, eventuale mutuo), importo e
destinazione della caparra, termine per la stipula del preliminare o del
rogito, eventuali condizioni (es. concessione mutuo, esito perizia).

**Forma:** L'offerta è di norma scritta; può essere sottoscritta dall'acquirente
e presentata al venditore (direttamente o tramite agenzia). Se il venditore
accetta senza modifiche, si può procedere al preliminare; se propone
modifiche, si instaura una trattativa (controfferte).

**Revoca:** Fino all'accettazione, l'offerta può essere revocata; dopo
l'accettazione si forma il contratto (preliminare) e le parti sono vincolate
nei limiti di legge. L'agente non deve fare pressioni per far accettare
offerte inadeguate né nascondere controfferte.
"""  # noqa: E501

