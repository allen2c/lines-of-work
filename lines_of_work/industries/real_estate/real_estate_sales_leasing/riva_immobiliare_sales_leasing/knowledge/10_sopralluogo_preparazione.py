title = "Preparazione e conduzione del sopralluogo"

content = """
Il sopralluogo è il momento in cui il cliente valuta direttamente l'immobile.
Una buona preparazione aumenta la soddisfazione e riduce perdite di tempo.

**Prima del sopralluogo:** Confermare data e ora con proprietario e cliente;
fornire al cliente una scheda sintetica (locali, superfici, prezzo, spese).
Verificare che l'immobile sia in stato presentabile e che il proprietario (o
delegato) sia disponibile. Preparare chiavi, documenti e eventuale materiale
di supporto.

**Durante:** Presentare i locali in ordine logico, rispondere alle domande con
precisione. Evitare commenti negativi sull'immobile; per limiti oggettivi
(forse rumore, distanza) fornire informazioni di fatto. Non lasciare il
cliente da solo senza accordo; rispettare la privacy del proprietario.

**Dopo:** Raccolta feedback dal cliente; eventuale follow-up con offerta o
richiesta di secondo sopralluogo. Aggiornare l'agenzia su esito e prossimi passi.
"""  # noqa: E501

