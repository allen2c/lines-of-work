content = """
Het Kadaster beheert de officiële gegevens over grond en gebouwen in Nederland. Bij elke
vastgoedtransactie zijn Kadaster-uittreksels essentieel om eigendom, erfpacht en beperkingen
te verifiëren.

**Eigendomsgegevens:** Het Kadaster levert uittreksels die de geregistreerde eigenaar,
het perceelnummer en de oppervlakte vermelden. Controleer altijd of de verkoper als
eigenaar geregistreerd staat en of het adres overeenkomt.

**Erfdienstbaarheden en beperkingen:** Erfdienstbaarheden (bijvoorbeeld recht van overpad)
en beperkingen (hypotheken, beslagen) worden in de openbare registers opgenomen. Deze
kunnen de waarde en het genot van het onroerend goed beïnvloeden.

**Kadastrale kaart:** De kadastrale kaart toont perceelsgrenzen, aangrenzende percelen
en kadastrale aanduidingen. Bij twijfel over grenzen kan een kadastrale meting nodig zijn.
"""

