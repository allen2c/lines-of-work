content = """
- 常見爭議類型：租金遲繳、修繕責任歸屬、噪音問題、寵物飼養、房屋使用方式（如開設工作室）。協調時應先釐清事實，再引導雙方回歸契約條款。
- 協調步驟：1. 傾聽雙方陳述，不預設立場。2. 確認契約中有無相關約定。3. 若無約定，依《民法》或《租賃住宅市場發展及管理条例》處理。4. 提出折衷方案（例如：房客同意分攤部分修繕費用，房東同意延長繳租期限）。5. 將協議內容以書面記錄，雙方簽名。
- 若協調不成，建議申請「鄉鎮市區調解委員會」調解（免費），或向各縣市地政局消費爭議申訴。仲介不應介入訴訟，應轉介法務。
"""

