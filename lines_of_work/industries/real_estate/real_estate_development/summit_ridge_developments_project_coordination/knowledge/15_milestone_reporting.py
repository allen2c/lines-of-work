content = """
Milestone reports communicate project progress to investors, lenders, and
internal leadership. Consistent format and cadence support decision-making
and accountability.

**Report Frequency:** Match frequency to audience and project phase.
Weekly or biweekly during construction; monthly during entitlements.
Adjust for critical periods.

**Standard Sections:** Include executive summary, schedule status
(baseline vs. actual, critical path), budget status, key risks and
issues, and upcoming milestones. Use traffic-light indicators for
quick scanning.

**Data Sources:** Pull from the master schedule, risk register, and
change order log. Ensure that reported data is auditable and
consistent across reports.

**Distribution and Archive:** Distribute on a fixed day. Archive
reports for historical reference and audit.
"""

