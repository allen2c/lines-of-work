"""Environmental impact assessment requirements for development projects."""

title = "Environmental Impact Assessment"

content = """
Environmental assessments evaluate potential impacts of a proposed development on
natural resources, traffic, noise, air quality, and community character. Requirements
vary by jurisdiction and project scale.

**Thresholds and Triggers:** Jurisdictions define thresholds that trigger full
environmental impact reports versus mitigated negative declarations or exemptions.
Project size, location, and sensitivity of resources affect applicability.

**Scope and Alternatives:** Define the project description, alternatives analyzed,
and mitigation measures. Traffic, hydrology, biological, and cultural resources
are common impact areas. Engage qualified consultants for technical sections.

**Public Review:** Draft documents are circulated for public and agency comment.
Responses must be incorporated or addressed. Final approval typically requires
findings of no significant impact or adoption of mitigation measures.

**Ongoing Compliance:** Approved mitigation measures become conditions of approval.
Track and document implementation throughout construction and operations.
"""

