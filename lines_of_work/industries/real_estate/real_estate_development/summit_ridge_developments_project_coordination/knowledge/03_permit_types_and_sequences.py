title = "Permit Types and Sequences"

content = """
Real estate development requires numerous permits and approvals, often in a prescribed
order. Understanding the sequence prevents costly rework and delays.

**Zoning and Land Use:** Rezoning, conditional use permits, and variances typically
come first. These establish the allowable use, density, and building envelope. Public
hearings and planning commission approvals are common.

**Environmental:** Depending on jurisdiction, environmental assessments or impact
reports may be required before or alongside building permits. Wetlands, endangered
species, and contamination may trigger additional studies.

**Site Development:** Grading permits, stormwater management, and utility permits
often precede or parallel building permit submittal. Coordination with public works
and utility providers is essential.

**Building Permits:** Structural, mechanical, electrical, and plumbing permits are
typically bundled in a building permit application. Plan review cycles vary by
jurisdiction; resubmittals can extend the timeline significantly.

**Occupancy and Inspections:** Final inspections and certificate of occupancy
follow construction completion. Some jurisdictions require temporary occupancy
permits for phased occupancy.
"""

