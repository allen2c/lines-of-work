title = "Third-Party Inspection Requirements"

content = """
Special inspections by qualified third parties are
often required for structural, fire, or
geotechnical work. Coordination ensures compliance.

**Code Requirements:** Building codes specify when
special inspections are required. Typically
structural steel, concrete, masonry, and
soils. Review with the architect and engineer.

**Selection:** Owner or design professional
selects the special inspector. Ensure
qualifications meet code and jurisdiction
requirements.

**Schedule:** Inspections occur at defined
points in construction. Coordinate with
contractor and inspector. Document results.

**Reports:** Retain inspection reports for
permit closeout and record. Address
deficiencies before covering work.
"""

