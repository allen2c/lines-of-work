"""Delay analysis for schedule impact assessment."""

title = "Delay Analysis"

content = """
When delays occur, analysis helps attribute cause and quantify impact.
Methods vary in complexity and defensibility for claims or disputes.

**As-Planned vs. As-Built:** Compare the baseline schedule to actual
execution. Identify when delays occurred, which activities were
affected, and whether delays were concurrent with other delays.

**Critical Path Impact:** Determine whether delayed activities were
on the critical path at the time of delay. Delays to non-critical
activities may not extend the project if float existed.

**Causes:** Categorize delays as owner-caused, contractor-caused,
or neutral (weather, force majeure). Document contemporaneous
records to support attribution.

**Recovery Planning:** Use delay analysis to inform recovery
options. Identify activities that can be accelerated or
re-sequenced to recover lost time.
"""

