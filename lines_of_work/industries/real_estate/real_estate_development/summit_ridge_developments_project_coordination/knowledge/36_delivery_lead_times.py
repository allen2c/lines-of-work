content = """
Long-lead items can drive project schedule. Early identification
and procurement prevent delays and allow schedule optimization.

**Critical Items:** Structural steel, elevators, HVAC equipment,
and specialty finishes often have extended lead times.
Consult contractors and suppliers during design.

**Procurement Schedule:** Align procurement with design and
permit milestones. Submittal approval may be required
before order. Factor in fabrication and shipping.

**Substitution and Value Engineering:** Consider alternatives
with shorter lead times when design allows. Document
comparisons and approvals.

**Tracking:** Monitor order status and delivery dates.
Alert the team to slips. Coordinate delivery with
construction sequence and site access.
"""

