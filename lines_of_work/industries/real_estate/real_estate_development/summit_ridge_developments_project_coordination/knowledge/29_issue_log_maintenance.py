title = "Issue Log Maintenance"

content = """
An issue log tracks active problems that require resolution.
Unlike risks, issues have already occurred and need immediate
attention.

**Issue Definition:** Document the issue description, discovered
date, impact (schedule, cost, quality), owner, and target
resolution date. Prioritize by impact.

**Status Tracking:** Update status as work progresses: open,
in progress, resolved, or closed. Add resolution notes when
closing.

**Escalation:** Define escalation triggers (e.g., unresolved
after 2 weeks, high impact). Route escalated issues to
appropriate leadership.

**Integration:** Link issues to the risk register when
a risk has materialized. Reference issues in status
reports and meeting agendas.
"""

