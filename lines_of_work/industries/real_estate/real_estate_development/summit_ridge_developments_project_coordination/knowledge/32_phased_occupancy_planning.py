"""Phased occupancy planning for multi-phase projects."""

title = "Phased Occupancy Planning"

content = """
Multi-building or multi-phase projects may allow occupancy of
completed portions before full project completion. Planning
enables smooth handover and revenue recognition.

**Sequence Definition:** Define which buildings or phases
receive certificates of occupancy first. Consider utilities,
access, and tenant requirements.

**Temporary Occupancy:** Some jurisdictions issue temporary
certificates with conditions. Document conditions and
expiration. Plan for final CO before temp expires.

**Tenant Coordination:** Coordinate with tenants on move-in
dates, access, and punch list. Ensure that common areas
and building systems serving early phases are complete.

**Schedule Integration:** Model phased occupancy in the
master schedule. Align construction sequencing and
inspections with occupancy targets.
"""

