title = "Master Project Schedule Structure"

content = """
The master project schedule is the central planning document that sequences all activities
from land acquisition through certificate of occupancy. A well-structured schedule enables
stakeholders to see dependencies, critical path, and resource allocation at a glance.

**WBS Hierarchy:** Organize work into a work breakdown structure with clear phases:
pre-development, design, permit and entitlement, construction, and closeout. Each phase
contains deliverables and milestones that feed into the next.

**Activity Definition:** Each activity has a unique ID, description, duration, predecessors,
and assigned responsibility. Use consistent naming and avoid overlapping or redundant
activities. Link activities logically so that predecessors drive successor start dates.

**Milestone Placement:** Define milestones for key decision points and gate approvals.
Examples include land closing, design completion, permit issuance, groundbreaking, and
substantial completion. Milestones have zero duration and serve as schedule anchors.

**Baseline and Updates:** Establish a baseline schedule after stakeholder approval.
Track actual progress against baseline and document variance. Update the schedule
periodically and communicate changes to the team.
"""

