"""Certificate of occupancy requirements and process."""

title = "Certificate of Occupancy Requirements"

content = """
A certificate of occupancy authorizes legal use and occupancy of a building.
Municipalities issue it after confirming that construction complies with
approved plans and code.

**Pre-requisites:** All building, mechanical, electrical, plumbing, and fire
inspections must be passed. Fire department and other agencies may have
separate sign-off requirements. As-built documentation is often required.

**Temporary vs. Final CO:** Temporary certificates allow occupancy before all
work is complete, typically with conditions and expiration dates. Final CO
requires full completion and punch list resolution.

**Phased Occupancy:** For multi-building or multi-phase projects, certificates
may be issued per building or phase. Coordinate inspection sequencing and
documentation to support phased handover.

**Handover:** Secure the certificate before tenant or purchaser occupancy.
Retain copies in project files and provide to property management.
"""

