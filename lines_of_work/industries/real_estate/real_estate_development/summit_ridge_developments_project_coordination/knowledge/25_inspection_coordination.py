title = "Inspection Coordination"

content = """
Building, fire, and specialty inspections occur throughout
construction. Coordination ensures that work is ready when
inspectors arrive and that results are documented.

**Inspection Types:** Footing, foundation, framing, rough
mechanical/electrical/plumbing, insulation, and final.
Some jurisdictions require third-party special inspections.

**Scheduling:** Request inspections per jurisdiction process—
often 24–48 hours in advance. Plan for inspector availability
and weather.

**Preparation:** Confirm that work is complete and accessible.
Have approved plans and permits on site. Address known
deficiencies before the inspection.

**Documentation:** Obtain inspection cards or reports. File
in project records. Address failed inspections promptly
and request reinspection.
"""

