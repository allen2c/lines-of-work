"""Document control best practices for development projects."""

title = "Document Control Best Practices"

content = """
Project document control ensures that stakeholders work from current, authorized
documents and that changes are traceable. Poor document control causes rework,
disputes, and schedule delays.

**Naming Conventions:** Use consistent file naming with project code, discipline,
document type, revision, and date. Example: SRD-2024-A-001-SITE-P01-Rev2-20240315.

**Revision Tracking:** Maintain a revision log for each document. Record revision
number, date, description of changes, and approver. Supersede previous versions
in the repository but retain for audit.

**Access and Permissions:** Grant access based on role. Construction teams need
current plans; consultants may need full history. Restrict edit permissions to
authorized personnel.

**Distribution and Retrieval:** Use a shared repository with clear folder structure.
Notify stakeholders when new revisions are posted. Ensure mobile and field access
for construction teams.
"""

