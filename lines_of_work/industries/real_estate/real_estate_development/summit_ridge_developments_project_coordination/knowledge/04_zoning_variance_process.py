"""Zoning variance process and procedural requirements."""

title = "Zoning Variance Process"

content = """
A zoning variance grants relief from specific dimensional or use requirements when
strict compliance would cause undue hardship. The process is formal and often
subject to public notice and hearing.

**Hardship Standard:** Applicants must demonstrate that the property has unique
physical constraints or circumstances not shared by neighboring properties, and
that the variance is the minimum relief necessary. Financial hardship alone rarely
qualifies.

**Application and Fees:** Submit a completed application with site plans, elevation
drawings, and a narrative explaining the hardship. Pay application and noticing fees.
Deadlines for hearing schedules vary by jurisdiction.

**Public Notice:** Jurisdictions require mailed notice to adjacent property owners
and published notice in local media. Establish the notice date and confirm receipt
before the hearing.

**Hearing and Decision:** Present the case to the zoning board or hearing officer.
Address staff recommendations and public comments. Decisions may be subject to
appeal within a specified period.
"""

