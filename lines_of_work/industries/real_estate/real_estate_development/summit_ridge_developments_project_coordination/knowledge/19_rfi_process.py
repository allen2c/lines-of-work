"""Request for information (RFI) process."""

title = "Request for Information Process"

content = """
RFIs formalize questions about design, contract documents, or field
conditions. A clear process ensures timely responses and documentation.

**RFI Initiation:** The contractor or subcontractor submits an RFI
with a unique number, subject, reference to drawings or specs, and
specific question. Attach relevant documentation.

**Routing and Response:** Route RFIs to the appropriate architect or
engineer. Establish response time expectations (e.g., 5–10 business
days). Track overdue responses.

**Response Quality:** Responses should be clear and actionable.
"See contract" or "per plan" are insufficient when the question
addresses an ambiguity. Document design clarifications that affect
scope.

**Log and Archive:** Maintain an RFI log with number, date, status,
and summary. Archive all RFIs and responses. Flag RFIs that may
lead to change orders.
"""

