"""Submittal tracking for design and construction."""

title = "Submittal Tracking"

content = """
Submittals are shop drawings, product data, and samples submitted for
review and approval before fabrication or installation. Tracking ensures
timely review and prevents schedule impacts from late approvals.

**Submittal Log:** Maintain a log with submittal ID, description,
responsible party, submit date, review status, and return date.
Link to the schedule activity that depends on approval.

**Review Workflow:** Define review routing: architect, engineer,
owner. Establish turnaround expectations. Track overdue reviews
and escalate as needed.

**Approval Conditions:** Document "approved as noted" or "revise
and resubmit" with clear comments. Ensure that conditions are
addressed before fabrication.

**Schedule Integration:** Identify long-lead submittals that
drive procurement and construction sequence. Prioritize
review of critical path items.
"""

