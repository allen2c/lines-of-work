"""Stakeholder meeting protocols for development coordination."""

title = "Stakeholder Meeting Protocols"

content = """
Effective meetings keep architects, engineers, contractors, consultants, and
municipal staff aligned. Consistent protocols prevent miscommunication and
ensure follow-through.

**Agenda Distribution:** Send agendas at least 48 hours before meetings. Include
objectives, discussion topics, and decisions required. Attach relevant documents
for advance review.

**Roles and Facilitation:** Designate a facilitator to keep discussions on track
and a note-taker to document decisions, action items, and owners. Rotate roles when
appropriate to build ownership.

**Action Items:** Record each action with an owner and due date. Review open
items at the start of the next meeting. Escalate overdue items before they become
critical.

**Meeting Notes:** Distribute notes within 24–48 hours. Use a standard format:
attendees, summary, decisions, action items, next meeting date. Archive in the
project document repository.
"""

