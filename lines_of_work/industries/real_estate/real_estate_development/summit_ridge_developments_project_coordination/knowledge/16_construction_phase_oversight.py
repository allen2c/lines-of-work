"""Construction phase oversight for development coordinators."""

title = "Construction Phase Oversight"

content = """
Development coordinators support construction execution by monitoring
progress, facilitating communication, and ensuring alignment with
project objectives. They do not replace the general contractor's
responsibility for means and methods.

**Progress Monitoring:** Compare actual progress to the schedule.
Attend progress meetings and walk the site periodically. Document
observations and flag variances.

**Design Clarification:** Facilitate resolution of design questions
and RFIs. Ensure that architects and engineers respond in a timely
manner. Document decisions that affect scope or schedule.

**Change Management:** Track change order requests and their impact.
Support the development team in evaluating and approving changes.
Maintain records for cost and schedule reconciliation.

**Quality and Safety:** Observe that work conforms to approved plans.
Report safety concerns to the contractor. Do not direct contractor
personnel; coordinate through the superintendent.
"""

