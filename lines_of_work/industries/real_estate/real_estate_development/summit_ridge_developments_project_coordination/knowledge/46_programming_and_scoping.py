"""Programming and scoping for development projects."""

title = "Programming and Scoping"

content = """
Programming defines the project's functional and spatial
requirements. Accurate scoping prevents design changes
and cost growth later.

**Stakeholder Input:** Gather input from end users,
tenants, and operations. Document requirements in
a program document. Obtain sign-off before design.

**Space Program:** Define areas by use, square
footage, and adjacencies. Include circulation,
mechanical, and support spaces.

**Budget Alignment:** Ensure program fits budget.
Identify value engineering opportunities early.
Document trade-off decisions.

**Change Control:** Treat program changes as scope
changes. Route for approval. Update design and
budget accordingly.
"""

