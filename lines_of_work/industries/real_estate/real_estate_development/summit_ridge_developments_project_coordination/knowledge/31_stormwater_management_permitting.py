title = "Stormwater Management Permitting"

content = """
Stormwater regulations protect water quality and control runoff.
Development typically requires stormwater permits at multiple
stages.

**Construction General Permit:** Often required before grading.
Requires stormwater pollution prevention plan (SWPPP), erosion
control, and Best Management Practices. Inspections during
construction are common.

**Post-Construction:** Permanent stormwater facilities must
meet design standards. As-built certification and long-term
maintenance plans may be required. Some jurisdictions require
operation and maintenance agreements.

**NPDES and MS4:** National and municipal separate storm
sewer system requirements vary by region. Consult local
regulations and engineers early.

**Monitoring:** Some permits require monitoring or reporting.
Track compliance deadlines and retain records.
"""

