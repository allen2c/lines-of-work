title = "Risk Register Maintenance"

content = """
A risk register tracks potential threats to project success and planned
responses. Regular maintenance keeps the team focused on proactive
mitigation.

**Risk Identification:** Capture risks from all phases: permitting,
design, construction, and market. Include likelihood and impact
assessments. Solicit input from consultants and contractors.

**Response Strategies:** For each risk, define mitigation (reduce
likelihood), contingency (plan if it occurs), or acceptance.
Assign an owner for each response.

**Ongoing Review:** Update the register in project meetings. Add new
risks as they emerge. Close or revise risks that have materialized
or been mitigated.

**Escalation Triggers:** Define when risks are escalated: e.g., high
impact and medium likelihood, or when mitigation is not progressing.
"""

