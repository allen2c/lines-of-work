title = "Utility Coordination"

content = """
Development requires coordination with water, sewer, gas, electric,
and communications providers. Lead times and easement requirements
vary significantly.

**Early Engagement:** Contact utilities during due diligence to
confirm availability, capacity, and connection requirements.
Identify need for off-site improvements or upgrades.

**Design Coordination:** Provide utility companies with site plans
and project timelines. Obtain service letters or preliminary
designs. Incorporate utility requirements into grading and
foundation design.

**Construction Sequencing:** Schedule utility work to align with
site development. Underground utilities typically precede
paving. Overhead electric may require pole placement and
inspections.

**Documentation:** Secure easements, permits, and as-built
documentation. Retain for title and operations.
"""

