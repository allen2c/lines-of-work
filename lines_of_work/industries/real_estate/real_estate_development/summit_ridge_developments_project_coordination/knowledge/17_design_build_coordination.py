"""Design-build coordination responsibilities."""

title = "Design-Build Coordination"

content = """
In design-build delivery, the contractor leads design and construction
under a single contract. The owner and development coordinator focus on
defining requirements and monitoring compliance rather than managing
design directly.

**Requirements Definition:** Clearly document owner's requirements,
program, standards, and performance criteria. These become the
basis for contractor design and execution.

**Design Review Milestones:** Establish review points for design
development. Review for compliance with requirements and code.
Avoid directing means and methods; focus on outcomes.

**Interface with Permitting:** Ensure that design progresses in
time to support permit submittals. Coordinate between contractor
design team and permit expediting.

**Change and Conformance:** Changes to requirements trigger
contract changes. Verify that constructed work conforms to
approved design and permit conditions.
"""

