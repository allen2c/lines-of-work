title = "Escrow and Financial Closeout"

content = """
Financial closeout reconciles final costs, releases
retainage, and closes project accounts. Coordination
with finance and legal is critical.

**Final Application for Payment:** Contractor submits
final application with lien waivers, as-built
documents, and warranty certificates. Verify
completeness before processing.

**Retainage Release:** Retainage is typically released
after substantial completion and punch list
resolution. Some contracts allow partial
release before final.

**Final Change Order:** Close any pending change orders.
Reconcile contingency and budget. Obtain final
approvals.

**Project Closeout:** Close project accounts, archive
financial records, and distribute final reports
to stakeholders.
"""

