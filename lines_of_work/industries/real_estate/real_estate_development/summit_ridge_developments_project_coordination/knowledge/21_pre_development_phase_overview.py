title = "Pre-Development Phase Overview"

content = """
The pre-development phase spans from site identification through
entitlement and design completion. Success here sets the foundation
for construction and delivery.

**Site Due Diligence:** Conduct title, survey, environmental, and
geotechnical reviews. Identify easements, encumbrances, and
constraints. Resolve issues before closing or condition removal.

**Entitlements:** Secure zoning, conditional use, variances, and
environmental approvals. Manage public hearings and agency
coordination. Track permit expirations and conditions.

**Design Development:** Progress from concept through schematic,
design development, and construction documents. Align design with
entitlement conditions and budget.

**Financing and Partnerships:** Finalize construction financing,
equity, and development agreements. Ensure that loan conditions
align with project schedule and milestones.
"""

