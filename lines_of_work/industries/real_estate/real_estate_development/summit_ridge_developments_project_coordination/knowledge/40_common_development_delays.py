content = """
Understanding common delay sources enables proactive
mitigation. Many delays are predictable and manageable.

**Permit Delays:** Incomplete submittals, plan corrections,
and agency backlog extend permit timelines. Submit
complete packages; build buffer for review cycles.

**Design Delays:** Late owner decisions, scope creep, and
coordination gaps slow design. Lock program early;
maintain disciplined change process.

**Construction Delays:** Weather, labor, material
availability, and unforeseen conditions affect
construction. Contingency and early procurement
help mitigate.

**Financing Delays:** Loan conditions, appraisal, and
documentation can delay funding. Align financing
timeline with project milestones.
"""

