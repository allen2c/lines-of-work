"""Contingency management for schedule and budget."""

title = "Contingency Management"

content = """
Contingency reserves buffer against unknown conditions and
changes. Managing contingency prevents over-allocation and
ensures reserves are available when needed.

**Schedule Contingency:** Add time to critical path activities
or milestones for permit delays, weather, and design
clarifications. Typical range 10–20 percent of critical path.

**Budget Contingency:** Owner and contractor contingencies
cover different risks. Track drawdown and remaining
balance. Require approval for contingency use.

**Release Criteria:** Define when contingency can be used:
e.g., change orders, unforeseen conditions. Document
releases and rationale.

**Reporting:** Include contingency status in periodic
reports. Flag when remaining contingency is low
relative to risk.
"""

