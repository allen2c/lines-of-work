"""Consultant coordination across design and permitting phases."""

title = "Consultant Coordination"

content = """
Development projects rely on architects, engineers, traffic consultants,
environmental specialists, and others. Effective coordination ensures
that deliverables are integrated and submitted on time.

**Scope and Schedule Alignment:** Confirm each consultant's scope, deliverables,
and schedule. Ensure that dependencies between disciplines are reflected in
milestone dates. Avoid gaps or overlaps in responsibility.

**Communication Channels:** Establish regular touchpoints for design
coordination. Use shared document platforms and issue tracking. Escalate
conflicts promptly when consultants disagree on interfaces.

**Submittal Coordination:** Consolidate permit applications where possible.
Coordinate review cycles so that one delay does not cascade. Track
submittal status centrally.

**Payment and Incentives:** Align consultant payments with milestone
completion. Consider incentives for early delivery when schedule is
critical.
"""

