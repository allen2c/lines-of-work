content = """
Projects with affordable housing components face additional
compliance requirements. Coordination with housing agencies
and monitoring is essential.

**Restrictive Covenants:** Recorded covenants often restrict
rents, occupancy, and resale. Understand term and
recertification requirements.

**Verification and Certification:** Tenants may need income
verification. Units may require periodic certification.
Document processes and deadlines.

**Agency Reporting:** Housing agencies may require annual
reports, audits, or site visits. Maintain records of
compliance and communications.

**Penalties:** Non-compliance can trigger fines, loss of
subsidies, or mandatory sale. Escalate compliance
issues immediately.
"""

