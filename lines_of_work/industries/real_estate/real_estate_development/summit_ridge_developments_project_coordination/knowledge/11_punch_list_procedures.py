"""Punch list procedures for project closeout."""

title = "Punch List Procedures"

content = """
The punch list catalogs incomplete or deficient work remaining at substantial
completion. Systematic procedures ensure timely resolution and a clean closeout.

**Generation:** Conduct walkthrough with owner, contractor, and design team.
Document items by location, description, and responsible party. Photograph
items where helpful. Use a shared format such as a spreadsheet or field app.

**Prioritization:** Classify items as critical (affects occupancy or safety),
high (visible or functional impact), or low (cosmetic). Address critical
items before certificate of occupancy. Establish target completion for others.

**Tracking and Resolution:** Assign owners and due dates. Re-inspect upon
completion and close items when work is verified. Track open count in
weekly status reports.

**Final Sign-off:** Obtain owner and contractor sign-off on punch list
completion. Retain records for warranty and dispute resolution.
"""

