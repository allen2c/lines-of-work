"""Permit condition tracking and compliance."""

title = "Permit Condition Tracking"

content = """
Approvals often include conditions that must be satisfied before
or during construction. Tracking ensures compliance and prevents
revocation or enforcement action.

**Condition Types:** Pre-construction (e.g., bond, erosion control
plan), during construction (inspections, monitoring), and
post-construction (as-built certification, mitigation).

**Log and Assign:** Create a conditions log with condition text,
responsible party, due date, and status. Assign owners for
each condition.

**Verification:** Obtain and file documentation demonstrating
compliance. Letters from engineers, inspection reports, and
agency sign-offs are common.

**Expiration and Extension:** Some conditions have expiration
dates. Request extensions before expiration if construction
is delayed. Document extension approvals.
"""

