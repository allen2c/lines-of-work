"""Change order process in development and construction."""

title = "Change Order Process"

content = """
Change orders modify the scope, schedule, or contract price after execution. A
disciplined process ensures that changes are documented, approved, and
incorporated into schedules and budgets.

**Initiation:** Document the requested change with a clear description, reason,
and proposed impact on scope, schedule, and cost. Identify whether the change
originates from owner, design, or field conditions.

**Pricing and Approval:** Obtain cost and schedule impact from the contractor or
consultant. Route for approval per the project's authority matrix. Some changes
require multiple approval levels.

**Execution and Documentation:** Issue a formal change order referencing the
contract, change number, and approved amount. Update the contract sum and
schedule. File supporting correspondence and drawings.

**Tracking:** Maintain a change order log with number, description, status,
approved amount, and cumulative impact. Report change order totals in
periodic project reviews.
"""

