title = "Matriz de autoridad y aprobaciones"

content = """
La matriz de autoridad define quién puede aprobar qué (órdenes de modificación,
cambios de alcance, prórrogas, gastos extraordinarios) según umbrales. Evita
bloqueos y asegura que las decisiones se tomen al nivel adecuado. La coordinación
de obra la conoce para canalizar las solicitudes correctamente.

**Umbrales típicos:** Órdenes de modificación por importe (hasta X el jefe de
proyecto, por encima dirección de desarrollo o inversores); prórrogas de plazo
(automáticas hasta N días, por encima con aprobación); cambios de especificación
que afecten a estándares (siempre con aprobación del promotor). Definir por
proyecto o por política de la empresa.

**Flujo:** Cuando una decisión supera el umbral del coordinador de obra, se
eleva a quien corresponda con la información necesaria (impacto en plazo,
coste, alcance). Documentar la aprobación antes de comprometer al proyecto.

**Actualización:** Revisar la matriz cuando cambien los equipos o los acuerdos
con inversores o financiación.
"""

