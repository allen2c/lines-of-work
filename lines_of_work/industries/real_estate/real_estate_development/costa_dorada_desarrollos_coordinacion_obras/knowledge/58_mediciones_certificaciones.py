title = "Mediciones y certificaciones de obra"

content = """
Las certificaciones de obra son el soporte para los pagos al contratista.
Reflejan el trabajo ejecutado y aprobado hasta una fecha. La coordinación
de obra no las elabora (eso corresponde a dirección de obra o al
contratista según el tipo de contrato), pero debe alinear sus fechas con
el cronograma de desembolsos y con el informe de hitos.

**Proceso:** El contratista presenta mediciones; la dirección de obra
revisa y emite certificación; el promotor aprueba y tramita el pago
según los plazos del contrato. Cualquier retraso en la certificación
retrasa el pago y puede afectar a la liquidez del contratista y al
avance de la obra.

**Hitos de desembolso:** Si la financiación liga desembolsos a certificaciones
o hitos físicos, coordinar con dirección financiera las fechas de
certificación y de solicitud de desembolso. Incluir en el cronograma
maestro los hitos de certificación previstos.
"""

