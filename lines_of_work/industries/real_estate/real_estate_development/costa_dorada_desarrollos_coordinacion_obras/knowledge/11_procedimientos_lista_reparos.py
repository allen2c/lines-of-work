# Knowledge: procedimientos de lista de reparos.

title = "Procedimientos de lista de reparos"

content = """
La lista de reparos (punch list) recoge los defectos o elementos pendientes de acabado
que deben corregirse antes de dar por cerrada la obra o antes de la recepción. Su
seguimiento es parte del cierre del proyecto.

**Elaboración:** Tras la inspección de fin de obra, dirección de obra y promotor
recorren las unidades o zonas y anotan partidas no conformes: acabados, funcionamiento
de instalaciones, pequeños daños. Se asigna responsable (contratista o subcontrata)
y, si procede, plazo.

**Seguimiento:** Registrar estado de cada partida (abierta, en curso, cerrada). Las
inspecciones de cierre verifican que los reparos estén resueltos. Los no resueltos
pueden retrasar la recepción o la retención de garantías según contrato.

**Cierre:** Cuando todos los reparos están cerrados o se acuerda un cierre condicional
con reservas documentadas, se procede a la recepción y, en su caso, a la solicitud
de licencia de primera ocupación.
"""

