# Knowledge: tipos de licencias y secuencia.

title = "Tipos de licencias y secuencia"

content = """
En España y en muchos países de Latinoamérica el desarrollo requiere varias licencias y
autorizaciones en un orden determinado. Conocer la secuencia evita bloqueos y retrasos.

**Licencia urbanística:** Clasificación y calificación del suelo, plan parcial o
reforma interior si aplica, y licencia de obras (mayor o menor según normativa). Puede
incluir informe de compatibilidad urbanística. La obtención suele ser previa a la
licencia de obra de construcción.

**Licencia de obra:** Autorización para ejecutar la obra según proyecto. Puede ser
desglosada por fases (cimentación, estructura, cerramientos) según la administración.
Vigilar condiciones y plazos de ejecución.

**Licencias ambientales y otras:** Evaluación de impacto ambiental, autorización de
vertidos, afección a dominio público hidráulico, servidumbres, etc. Dependen del emplazamiento
y del tipo de proyecto. Algunas son previas a la licencia de obra.

**Secuencia práctica:** Suelo y normativa → proyecto básico y de ejecución → licencia
urbanística/obra → eventuales condicionados (ambiental, servicios) → inicio de obra
según plazos legales.
"""

