# Knowledge: planificación de ocupación escalonada.

title = "Planificación de ocupación escalonada"

content = """
En proyectos de varias fases o edificios con muchas unidades, la ocupación puede
ser escalonada: se entregan bloques o fases en distintas fechas. Esto afecta al
cronograma, a las inspecciones y a la licencia de primera ocupación (si la
administración permite ocupación parcial).

**Definición de fases:** Establecer qué unidades o zonas se entregan en cada fase
y en qué orden. Considerar dependencias técnicas (instalaciones comunes, accesos)
y comerciales (ventas, alquileres). Documentar en el plan de proyecto y en el
cronograma maestro.

**Licencia y recepción:** Verificar si la normativa permite licencia de primera
ocupación parcial o si hay que esperar a la obra completa. Planificar recepciones
parciales con el contratista (actas, listas de reparos por fase) y las inspecciones
correspondientes.

**Seguridad y uso:** Hasta el cierre total, coordinar medidas de seguridad y
separación entre zonas en obra y zonas ya entregadas. Comunicar a usuarios y
a gestión patrimonial las fechas y condiciones de cada fase.
"""

