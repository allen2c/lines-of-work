title = "Normativa de accesibilidad"

content = """
La normativa de accesibilidad (estatal y autonómica en España; equivalente
en Latinoamérica) establece requisitos de diseño y de uso para que los
edificios sean accesibles. El incumplimiento puede impedir la licencia
de primera ocupación o dar lugar a sanciones. La coordinación de obra
debe asegurar que el proyecto y la obra cumplan y que la documentación
acreditativa esté disponible al cierre.

**Requisitos típicos:** Accesos, itinerarios, puertas, aseos, aparcamientos
reservados, señalización. Varían según uso (vivienda, comercial, público)
y según normativa. El proyecto debe incorporarlos; la dirección de obra
verifica su ejecución. Cualquier cambio que afecte a accesibilidad debe
ser aprobado técnicamente.

**Documentación:** Certificado de accesibilidad o documento equivalente
que exija la administración para la licencia de primera ocupación o
cedula de habitabilidad. Incluir en el expediente de cierre y en la
entrega a gestión patrimonial. La coordinación de obra vela por que
se solicite y se archive a tiempo.
"""

