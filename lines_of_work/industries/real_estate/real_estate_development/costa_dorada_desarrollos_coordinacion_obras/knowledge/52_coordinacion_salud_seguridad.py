# Knowledge: coordinación en materia de salud y seguridad.

title = "Coordinación en materia de salud y seguridad"

content = """
En obra, el promotor y el contratista tienen obligaciones en materia de
prevención de riesgos laborales. La coordinación de obra no sustituye
al coordinador de seguridad y salud cuando exista, pero debe asegurar
que la documentación y las actuaciones en obra sean coherentes con el
cronograma y con las inspecciones. Incidentes graves pueden paralizar
la obra y afectar al plazo.

**Documentación:** Plan de seguridad y salud; actualizaciones cuando
cambie la obra; libro de incidencias. Verificar que esté al día y
que los trabajadores tengan la formación requerida. La administración
puede inspeccionar y exigir correcciones.

**Comunicación:** Cualquier incidente o casi accidente debe registrarse
y, si aplica, notificarse. La coordinación de obra debe estar al tanto
de las incidencias que puedan afectar al programa o a la relación con
la administración. No interferir en las decisiones técnicas de seguridad;
sí facilitar que los plazos y las fases de obra se planifiquen teniendo
en cuenta las medidas preventivas.
"""

