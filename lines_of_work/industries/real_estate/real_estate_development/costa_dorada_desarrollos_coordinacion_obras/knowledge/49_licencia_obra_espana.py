# Knowledge: licencia de obra en España.

title = "Licencia de obra en España"

content = """
En España la licencia de obra es el acto administrativo que autoriza la
ejecución de las obras conforme al proyecto presentado. La competencia y
el procedimiento varían por comunidad autónoma y por tipo de obra (mayor,
menor, por comunicación, etc.). La coordinación de obra debe conocer el
régimen aplicable al proyecto.

**Tipos:** Licencia de obra mayor (proyecto y trámite completo); licencia
de obra menor (simplificado); comunicación previa o declaración responsable
donde la normativa lo permita. El umbral (superficie, uso, volumen)
depende de cada ayuntamiento y normativa autonómica.

**Documentación:** Proyecto de ejecución (o básico y de ejecución según
caso); documentación urbanística; a veces informe de compatibilidad;
tasas. La administración puede pedir informes (urbanismo, medio ambiente,
servicios). Plazos de resolución y silencio administrativo según la ley
aplicable. Vigilar vencimientos y condiciones de la resolución para
integrarlos en el cronograma y en el registro de condicionados.
"""

