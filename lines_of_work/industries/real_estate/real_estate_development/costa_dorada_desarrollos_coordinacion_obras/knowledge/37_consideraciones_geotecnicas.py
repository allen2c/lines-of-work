# Knowledge: consideraciones geotécnicas.

title = "Consideraciones geotécnicas"

content = """
El informe geotécnico y el proyecto de cimentación condicionan la viabilidad y
el plazo de la obra. La coordinación de obra debe conocer las restricciones
geotécnicas para planificar y detectar desviaciones (hallazgos en excavación,
condicionados).

**Contenido del informe:** Tipo de terreno, nivel freático, capacidad de carga,
riesgos (deslizamientos, expansividad, karst). Recomendaciones de cimentación
y de contención. El proyecto de cimentación debe ajustarse al informe; si no,
puede exigirse informe complementario o variación.

**Durante la obra:** Si la excavación revela condiciones distintas a las
previstas, debe paralizarse la afectada y consultar con el geólogo y la
dirección de obra. Puede ser necesario proyecto de modificación y, en su caso,
orden de modificación. Documentar hallazgos y decisiones.

**Cronograma:** La fase de cimentación suele ser crítica. Cualquier retraso
por geotecnia afecta a todo el programa; incluir margen y seguimiento cercano
en esta fase.
"""

