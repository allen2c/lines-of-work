# Knowledge: arqueología preventiva.

title = "Arqueología preventiva"

content = """
En solares con potencial arqueológico o en zonas protegidas, la administración
puede exigir un seguimiento arqueológico durante la excavación o las
obras. Los hallazgos pueden paralizar la obra o condicionar el proyecto.
La coordinación de obra debe conocer si el proyecto está sujeto a esta
exigencia y planificar plazos y actuaciones con el arqueólogo y la
administración.

**Cuándo aplica:** En áreas de protección arqueológica, en entornos
BIC o cuando la evaluación previa lo recomiende. La licencia de obra
o el condicionado de la EIA pueden imponer un plan de seguimiento.

**Desarrollo:** El arqueólogo supervisa las excavaciones según el plan
aprobado. Si hay hallazgos, la administración puede ordenar parada,
documentación o modificación del proyecto. Los plazos no son predecibles;
incluir contingencia en el cronograma para esta fase. Documentar todos
los informes y actas para el archivo del proyecto.
"""

