# Knowledge: proceso de solicitud de información (RFI).

title = "Proceso de solicitud de información (RFI)"

content = """
Las solicitudes de información (Request for Information, RFI) son las consultas
formales del contratista cuando el proyecto es ambiguo, incompleto o contradictorio.
La respuesta debe ser clara y documentada para evitar reclamaciones y retrasos.

**Cuándo se usa:** El contratista envía una RFI cuando necesita aclaración para
ejecutar (detalle constructivo, especificación, conflicto entre planos). No debe
usarse para cambios de alcance; esos se gestionan como órdenes de modificación.

**Registro y numeración:** Cada RFI tiene número único, fecha, descripción del
tema, referencia a planos o pliego, y estado. La dirección de obra o el proyectista
asignan la respuesta; si afecta a otra especialidad, se deriva internamente.

**Plazos:** Definir en contrato o protocolo el plazo máximo de respuesta (p. ej.
7–15 días según complejidad). Incumplimientos repetidos deben escalarse. La
coordinación de obra hace seguimiento para que las RFI no queden bloqueadas.
"""

