title = "Fase de predesarrollo"

content = """
La fase de predesarrollo abarca desde la identificación del solar hasta la obtención
de licencias y la finalización del proyecto de ejecución. Un buen cierre de esta
fase sienta las bases para la obra y la entrega.

**Due diligence del solar:** Revisar titularidad, catastro, cargas, servidumbres,
condiciones ambientales y geotécnicas. Resolver incidencias antes del cierre de la
compra o del levantamiento de condiciones.

**Licencias y condicionados:** Obtener licencia urbanística, de obra y las ambientales
o sectoriales que correspondan. Gestionar audiencias públicas y coordinación con
administraciones. Controlar vencimientos y condicionados.

**Proyecto y financiación:** Avanzar desde el concepto al proyecto básico y de
ejecución, alineado con condicionados y presupuesto. Cerrar financiación de obra
y acuerdos de inversión, asegurando que las condiciones encajen con el cronograma.
"""

