# Knowledge: expectativas de informe a inversores.

title = "Expectativas de informe a inversores"

content = """
Los inversores y la financiación suelen exigir informes periódicos del estado
del proyecto. La coordinación de obra aporta los datos de avance, hitos y
riesgos que alimentan estos informes y debe conocer el formato y la periodicidad
exigidos.

**Contenido típico:** Resumen ejecutivo; avance físico y financiero; hitos
cumplidos y próximos; desviaciones de plazo y coste; principales riesgos e
incidencias; previsiones de cierre. Puede exigirse documentación de apoyo
(certificaciones, actas).

**Periodicidad y formato:** Mensual o trimestral según el acuerdo. Algunos
inversores usan plantillas propias; otros aceptan el formato interno del
promotor. Cumplir plazos de envío para mantener la confianza.

**Sensibilidad:** No incluir información confidencial que no deba salir del
promotor. Los datos de coordinación de obra son factuales; las interpretaciones
o estrategias las comunica dirección de desarrollo o relaciones con inversores.
"""

