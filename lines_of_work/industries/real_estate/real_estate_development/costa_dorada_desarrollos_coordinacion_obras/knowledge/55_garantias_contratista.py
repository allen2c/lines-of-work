# Knowledge: garantías del contratista.

title = "Garantías del contratista"

content = """
El contrato de obra suele establecer garantías (buen fin, defectos ocultos,
mantenimiento) con plazos y condiciones. La coordinación de obra debe
conocer los plazos de garantía y asegurar que la documentación y las
actas de recepción permitan reclamar si surge un defecto. No gestiona
las reclamaciones técnicas; sí el registro y la trazabilidad.

**Tipos:** Garantía de buen fin (periodo desde recepción, típicamente
uno o dos años); garantía por defectos ocultos (plazos legales más largos);
compromisos de mantenimiento de determinadas partidas. Los plazos se
cuentan desde la recepción (total o parcial según lo pactado).

**Documentación:** Actas de recepción, listas de reparos cerradas,
certificados de dirección de obra. Conservar en el archivo del proyecto.
Cuando expire la garantía, el expediente debe estar completo por si
hay reclamación posterior. Transmitir a gestión patrimonial los plazos
y contactos del contratista para el periodo de garantía.
"""

