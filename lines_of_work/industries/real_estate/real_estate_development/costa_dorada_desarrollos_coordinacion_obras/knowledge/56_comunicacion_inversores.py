# Knowledge: comunicación con inversores durante la obra.

title = "Comunicación con inversores durante la obra"

content = """
Durante la fase de obra los inversores esperan información periódica y
transparente sobre avance, incidencias y previsiones. La coordinación
de obra aporta los datos; la comunicación formal la lleva normalmente
dirección de desarrollo o relaciones con inversores. Conocer los canales
y el tono evita mensajes contradictorios o informaciones prematuras.

**Contenido que aporta coordinación:** Avance físico; hitos cumplidos
y próximos; desviaciones de plazo y causas; riesgos que afecten al
cierre; necesidades de decisión (prórroga, variación). Todo factual
y documentado. No incluir interpretaciones sobre estrategia comercial
o financiera salvo que se solicite expresamente.

**Frecuencia y formato:** Según acuerdo con inversores: informes
mensuales, llamadas trimestrales, alertas ante incidencias graves.
Respetar los plazos de envío y el formato acordado. Si hay retraso
importante o riesgo de incumplimiento de hito, comunicar con antelación.
"""

