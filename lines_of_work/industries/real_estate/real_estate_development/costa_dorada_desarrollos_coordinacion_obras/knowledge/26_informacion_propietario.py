# Knowledge: información suministrada por el promotor.

title = "Información suministrada por el promotor"

content = """
El promotor (propietario del proyecto) debe suministrar al equipo de proyecto y al
contratista la información necesaria para ejecutar el proyecto: datos del solar,
condicionados, decisiones de alcance, aprobaciones. Los retrasos en esta información
pueden dar lugar a reclamaciones de plazo o coste.

**Contenido típico:** Titularidad y límites del solar; informes geotécnicos y
estudios previos; condicionados de la licencia y de la EIA; proyecto de referencia
o brief; decisiones de acabados o especificaciones; aprobación de submittals y
órdenes de modificación. Todo debe estar documentado y versionado.

**Plazos:** Establecer en contrato o protocolo los plazos en que el promotor
proporcionará la información. La coordinación de obra hace seguimiento y escalada
si hay demoras que afecten al cronograma.

**Cambios:** Cualquier cambio de alcance o de criterio debe comunicarse por escrito
y evaluarse en términos de plazo y coste antes de su incorporación.
"""

