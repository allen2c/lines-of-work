# Knowledge: auditorías internas de proyecto.

title = "Auditorías internas de proyecto"

content = """
Algunos promotores realizan auditorías internas de sus proyectos (cumplimiento
de procesos, control de costes y plazos, calidad documental). La coordinación
de obra facilita el acceso a la información y a los interlocutores, y
aplica las mejoras que se deriven de las conclusiones.

**Alcance:** Revisión de cronograma y de hitos; revisión de registro de
riesgos e incidencias; muestreo de documentación (actas, certificaciones,
OM); cumplimiento de condicionados y de procedimientos internos. El
auditor suele ser un equipo distinto al del proyecto para garantizar
independencia.

**Preparación:** Tener el expediente ordenado y accesible. Designar un
punto de contacto y reservar tiempo del equipo para reuniones y
entregas de información. No ocultar incidencias; la auditoría sirve
para mejorar, no para culpar.

**Seguimiento:** Las recomendaciones pueden traducirse en cambios de
proceso o de herramientas. La coordinación de obra puede liderar o
apoyar la implementación en su ámbito.
"""

