# Knowledge: pliego de condiciones y su uso en coordinación.

title = "Pliego de condiciones y su uso en coordinación"

content = """
El pliego de condiciones del contrato de obra define obligaciones, plazos,
procedimientos de certificación, variaciones, recepción y garantías. La
coordinación de obra debe conocer las cláusulas que afectan al cronograma,
a las comunicaciones y a la documentación para actuar conforme al contrato
y para detectar desviaciones o reclamaciones.

**Cláusulas relevantes:** Plazo de ejecución y prórrogas; obligación de
entregar programa de obra y actualizaciones; plazos de respuesta a RFI
y submittals; procedimiento de órdenes de modificación; certificaciones
y pagos; recepción y lista de reparos; garantías y seguros. Tener a mano
un resumen o índice para consultas rápidas.

**Cumplimiento:** Cuando el contratista o el promotor no cumplan plazos
o procedimientos del pliego, documentar y, si aplica, notificar por escrito.
Las desviaciones repetidas pueden dar lugar a reclamaciones; la trazabilidad
protege al promotor. En caso de duda sobre interpretación, consultar con
dirección de desarrollo o asesoría jurídica.
"""

