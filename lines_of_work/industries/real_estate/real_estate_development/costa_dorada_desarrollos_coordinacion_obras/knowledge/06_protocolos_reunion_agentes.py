title = "Protocolos de reunión con agentes"

content = """
Las reuniones con arquitectos, ingenieros, contratistas, administración e inversores
deben seguir un protocolo claro para que queden registradas las decisiones y las acciones.

**Convocatoria:** Incluir orden del día, participantes, lugar o enlace y duración
prevista. Enviar con antelación suficiente y adjuntar documentación relevante cuando
proceda.

**Desarrollo:** Un moderador (habitualmente coordinación de obra) guía el orden del día,
controla el tiempo y asegura que cada tema tenga conclusión o siguiente paso. Anotar
acuerdos, responsables y plazos.

**Acta:** Redactar acta en un plazo breve (24–48 h). Incluir asistentes, puntos tratados,
decisiones adoptadas y acciones con responsable y fecha. Distribuir a participantes y
archivar en el expediente del proyecto. Las actas son referencia en caso de discrepancias.
"""

