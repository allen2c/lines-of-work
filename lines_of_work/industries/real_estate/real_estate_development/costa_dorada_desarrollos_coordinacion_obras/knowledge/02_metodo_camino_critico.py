title = "Método del camino crítico"

content = """
El método del camino crítico (CPM) identifica la secuencia de actividades que determina
la duración mínima del proyecto. Cualquier retraso en una actividad crítica retrasa el
fin del proyecto.

**Cálculo:** A partir de las duraciones y predecesoras se calculan fechas early start/finish
y late start/finish. La holgura (slack) es la diferencia entre late y early. Las actividades
con holgura cero forman el camino crítico.

**Uso en coordinación:** Priorizar seguimiento y recursos en las actividades críticas.
Vigilar entregas de proyecto, licencias y partidas de obra que estén en el camino crítico.
Identificar cuando una actividad no crítica pasa a ser crítica por retrasos.

**Recuperación:** Para recuperar plazo se actúa sobre actividades críticas: compresión
de plazos (más recursos), solapamiento controlado o revisión de alcance. Documentar
cambios y su impacto en coste y riesgo.
"""

