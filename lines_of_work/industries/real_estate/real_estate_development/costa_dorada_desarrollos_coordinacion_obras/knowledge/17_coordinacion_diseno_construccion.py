# Knowledge: coordinación diseño–construcción.

title = "Coordinación diseño–construcción"

content = """
En proyectos con enfoque diseño–construcción o con participación temprana del
contratista, la coordinación entre proyecto y obra es continua. El objetivo es
que el proyecto sea construible, ajustado a plazo y coste, y sin conflictos en
ejecución.

**Entradas del contratista:** Si el contratista aporta propuestas de valor (alternativas
constructivas, suministros), deben integrarse en el proyecto con revisión de la
dirección facultativa y aprobación del promotor. Documentar acuerdos y variaciones
de alcance o precio.

**Planificación conjunta:** El cronograma de obra debe ser coherente con el de proyecto
y licencias. Secuencia de entregas de proyecto (por fases o por partidas) y de
ejecución. Evitar que la obra espere proyecto o que el proyecto se retrase por
prioridades de obra no alineadas.

**RFI y cambios en obra:** Las dudas o hallazgos en obra que exijan cambio de proyecto
se gestionan por el canal definido (RFI, orden de modificación). La coordinación
de obra facilita que las respuestas lleguen a tiempo al contratista.
"""

