# Knowledge: coordinación con consultores externos.

title = "Coordinación con consultores externos"

content = """
Arquitectos, ingenieros de estructuras e instalaciones, geólogos, consultores ambientales
y otros participan en el proyecto en distintas fases. La coordinación de obra asegura
que sus entregas estén alineadas con el cronograma y que no haya huecos ni duplicidades.

**Roles y entregas:** Definir en encargo o contrato qué entrega cada consultor, en qué
fecha y con qué formato. Integrar esas fechas en el cronograma maestro como hitos
o dependencias.

**Revisión e integración:** Los proyectos de las especialidades deben integrarse en el
proyecto de ejecución. Coordinar revisiones conjuntas para detectar interferencias
o incompatibilidades antes de obra. Las RFI (solicitudes de información) durante la
obra se canalizan al consultor correspondiente con plazo de respuesta.

**Comunicación:** Incluir a los consultores necesarios en reuniones de seguimiento
y en la distribución de actas y de actualizaciones del cronograma. Documentar acuerdos
y cambios de alcance que afecten a su trabajo.
"""

