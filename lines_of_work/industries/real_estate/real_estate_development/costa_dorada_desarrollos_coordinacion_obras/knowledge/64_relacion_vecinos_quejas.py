# Knowledge: relación con vecinos y gestión de quejas.

title = "Relación con vecinos y gestión de quejas"

content = """
Las obras pueden generar molestias a vecinos (ruido, polvo, tráfico,
vibraciones). Una gestión proactiva de la comunicación y de las quejas
reduce el riesgo de denuncias, requerimientos de la administración o
paralizaciones. La coordinación de obra puede canalizar la comunicación
o apoyar al responsable designado por el promotor.

**Comunicación previa:** Informar a vecinos y comunidad de las fechas
previstas, horarios y medidas de mitigación antes del inicio de obra.
Dejar un canal de contacto (correo, teléfono) para consultas y quejas.

**Registro de quejas:** Registrar cada queja con fecha, contenido y
respuesta. Si la queja es fundada, adoptar medidas y comunicarlas.
Si no lo es, responder con educación y documentación. Evitar confrontación;
la administración suele valorar que el promotor haya respondido.

**Escalado:** Quejas repetidas o que impliquen riesgo legal o de
paralización deben escalarse a dirección de desarrollo y, si procede,
a asesoría jurídica. No comprometer al promotor sin autorización.
"""

