# Knowledge: coordinación de servicios y suministros.

title = "Coordinación de servicios y suministros"

content = """
Las acometidas de agua, electricidad, gas, telecomunicaciones y saneamiento deben
planificarse y coordinarse con las compañías y con la administración para que estén
disponibles cuando la obra lo requiera. Un retraso en acometidas puede retrasar
pruebas e instalaciones interiores.

**Solicitudes y plazos:** Cada suministro tiene plazos de estudio, proyecto y
ejecución por parte de la compañía. Solicitar con antelación según la información
que proporcionen las propias compañías. Incluir hitos de solicitud y de conexión
en el cronograma.

**Obra y permisos:** La ejecución de las redes puede requerir licencia de obra
o autorización de ocupación de vía. Coordinar con la obra general para no interferir
con la circulación o con otras partidas. Algunas conexiones solo pueden hacerse
en determinadas fases del edificio.

**Documentación:** Conservar proyectos de acometida, certificados de conexión y
contratos con las compañías para el archivo del proyecto y para la entrega a
gestión patrimonial o compradores.
"""

