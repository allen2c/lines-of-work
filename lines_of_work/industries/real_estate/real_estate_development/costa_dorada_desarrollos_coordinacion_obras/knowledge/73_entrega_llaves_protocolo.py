# Knowledge: protocolo de entrega de llaves.

title = "Protocolo de entrega de llaves"

content = """
La entrega de llaves es el acto formal en que el promotor entrega el
inmueble al comprador o al inquilino. Suele ir acompañada de una
inspección conjunta y de la firma de acta de entrega. La coordinación
de obra puede no estar presente en cada entrega, pero debe asegurar
que el proceso esté definido y que la documentación de soporte (licencia,
certificados, llaves, manuales) esté disponible para el equipo comercial
o de gestión patrimonial que la realiza.

**Contenido del acta:** Descripción del inmueble entregado; estado
de las instalaciones y de los elementos comunes si aplica; relación
de llaves, mandos y documentación entregada; reservas o incidencias
si las hay; firma de las partes. Conservar copia en el archivo del
proyecto y en el expediente del comprador o inquilino.

**Preparación:** Antes de la fecha de entrega, verificar que la
licencia de primera ocupación o cédula de habitabilidad esté en vigor,
que las listas de reparos estén cerradas en la unidad y que la
documentación (manuales, garantías) esté preparada. Coordinar con
gestión patrimonial o ventas las fechas y los materiales de entrega.
"""

