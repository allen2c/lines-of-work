# Knowledge: retrasos comunes en el desarrollo inmobiliario.

title = "Retrasos comunes en el desarrollo inmobiliario"

content = """
Conocer las causas más frecuentes de retraso ayuda a planificar contingencias
y a actuar con antelación. Muchas son previsibles y mitigables con buen
seguimiento y coordinación.

**Administración:** Resolución de licencias más lenta de lo previsto; recursos
de terceros; condicionados que exigen proyecto complementario o nueva aprobación.
Mitigación: solicitar con antelación, mantener relación con la administración,
incluir margen en el cronograma.

**Proyecto y consultores:** Revisiones largas; RFI sin respuesta a tiempo;
incompatibilidades entre especialidades detectadas tarde. Mitigación: plazos
claros de revisión, reuniones de coordinación, detección temprana de conflictos.

**Obra:** Retrasos en suministros; condiciones geotécnicas distintas; condiciones
meteorológicas; capacidad del contratista. Mitigación: lead times realistas,
seguimiento del programa, reserva de contingencia. Documentar causas para
análisis de retrasos y para lecciones aprendidas.
"""

