# Knowledge: supervisión de la fase de obra.

title = "Supervisión de la fase de obra"

content = """
La coordinación de obra no sustituye a la dirección de obra ni a la dirección de
ejecución, pero vela por que el cronograma, los hitos y la información fluyan entre
promotor, dirección facultativa y contratista. La supervisión se centra en el avance
y en los bloqueos, no en el control técnico de calidad de las unidades de obra.

**Seguimiento del avance:** Comparar lo ejecutado con el cronograma (programación del
contratista y cronograma maestro). Identificar desviaciones y causas (suministros,
proyecto, permisos, recursos). Reuniones de obra periódicas con actas y acciones.

**Información y RFI:** Asegurar que las solicitudes de información del contratista
se respondan en plazo por la dirección de obra o los consultores. Un retraso en las
respuestas suele traducirse en retraso en obra.

**Interfaces:** Coordinar interfaces entre lotes (estructura, instalaciones, acabados)
y entre contratista principal y subcontratistas o suministradores. Los cuellos de
botella en una partida afectan a las siguientes.
"""

