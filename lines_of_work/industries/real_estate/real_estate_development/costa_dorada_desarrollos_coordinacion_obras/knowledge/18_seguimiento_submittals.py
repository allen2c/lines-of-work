# Knowledge: seguimiento de submittals y aprobaciones.

title = "Seguimiento de submittals y aprobaciones"

content = """
Los submittals son entregas del contratista (catálogos, muestras, planos de detalle,
datos de producto) que la dirección de obra o el proyectista deben revisar y aprobar
antes de que se incorporen a la obra. Un retraso en las aprobaciones retrasa la
ejecución.

**Registro:** Mantener un listado de submittals con identificador, descripción,
fecha de envío, responsable de revisión y estado (pendiente, aprobado, aprobado
con observaciones, rechazado). Asignar plazos de revisión en el contrato o en
protocolo interno.

**Flujo:** Contratista envía → dirección de obra o proyectista revisa → respuesta
por escrito (aprobado, aprobado con condiciones, rechazado). Si hay condiciones,
el contratista reenvía y se repite el ciclo. No autorizar uso en obra hasta
aprobación formal.

**Impacto en cronograma:** Las partidas que dependen de un submittal no pueden
iniciarse hasta su aprobación. Incluir en la programación los plazos de revisión
y planificar los envíos con antelación.
"""

