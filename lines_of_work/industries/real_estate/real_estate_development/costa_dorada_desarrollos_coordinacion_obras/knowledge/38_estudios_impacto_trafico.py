title = "Estudios de impacto de tráfico y movilidad"

content = """
En proyectos de cierta entidad o en ubicaciones sensibles, la administración
puede exigir un estudio de impacto de tráfico o de movilidad que justifique
la generación de tráfico y las medidas correctoras. La aprobación puede ser
condición de la licencia urbanística o de obra.

**Contenido típico:** Generación de viajes del proyecto; estado actual de la
red; impacto previsto; medidas de mitigación (accesos, señalización, mejoras
viarias, promoción de transporte público). El estudio lo elabora un consultor
especializado y lo revisa la administración (tráfico, movilidad).

**Integración en el cronograma:** Incluir la elaboración del estudio y su
aprobación en la fase de predesarrollo. Las condiciones de la resolución
(obras de urbanización, compromisos de movilidad) deben incorporarse al
proyecto y al registro de condicionados.

**Durante la obra:** Algunas condiciones se cumplen en fase de urbanización
o de entrega. Dar seguimiento hasta su cierre.
"""

