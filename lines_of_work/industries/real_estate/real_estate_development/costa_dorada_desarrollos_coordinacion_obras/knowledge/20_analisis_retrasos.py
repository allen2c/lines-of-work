# Knowledge: análisis de retrasos.

title = "Análisis de retrasos"

content = """
Cuando el proyecto se retrasa respecto al cronograma, es necesario analizar causas
y responsabilidades para tomar decisiones (recuperación de plazo, prórroga, reclamaciones)
y para informar a inversores y financiación.

**Registro de hechos:** Documentar fechas reales de hitos (entrega de proyecto,
obtención de licencia, inicio de obra, hitos de construcción) y comparar con
las previstas. Registrar eventos que hayan causado retraso: demora administrativa,
orden de modificación, falta de suministro, condiciones meteorológicas, etc.

**Atribución:** Determinar si el retraso es imputable al promotor, a la administración,
al contratista o a fuerza mayor. Esto afecta a prórrogas contractuales y a posibles
reclamaciones. Cuando haya controversia, el análisis debe ser sólido y documentado.

**Recuperación:** Si el retraso es recuperable, valorar medidas (aceleración,
reprogramación, reducción de alcance) y su coste. Actualizar el cronograma maestro
y comunicar a los agentes.
"""

