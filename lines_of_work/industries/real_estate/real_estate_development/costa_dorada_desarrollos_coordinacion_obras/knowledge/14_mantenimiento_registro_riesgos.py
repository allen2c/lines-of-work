title = "Mantenimiento del registro de riesgos"

content = """
El registro de riesgos recoge los riesgos identificados que pueden afectar al plazo,
coste o alcance del proyecto, junto con su probabilidad, impacto y medidas de mitigación.
Revisarlo con periodicidad evita sorpresas y permite actuar a tiempo.

**Identificación:** Los riesgos pueden ser técnicos (geotecnia, diseño), normativos
(licencias, condicionados), de suministro (materiales, mano de obra), contractuales
o externos (mercado, fuerza mayor). Incluir tanto riesgos del promotor como del
contratista cuando afecten al proyecto global.

**Valoración y prioridad:** Asignar probabilidad e impacto (por ejemplo alta/media/baja)
y priorizar los de mayor exposición. Revisar cuando haya cambios de alcance, de
cronograma o de contexto.

**Mitigación y escalado:** Para cada riesgo prioritario, definir responsable y acción
(evitar, reducir, transferir o aceptar). Si un riesgo se materializa o supera umbrales,
escalar a dirección de desarrollo y actualizar el registro.
"""

