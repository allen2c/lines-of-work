title = "Entrega y revisión del cronograma del contratista"

content = """
El contratista entrega su programa de obra (cronograma detallado) según lo establecido
en el pliego o contrato. La dirección de obra y la coordinación del promotor lo
revisan para asegurar coherencia con el cronograma maestro y con los hitos del
proyecto.

**Contenido:** Actividades desglosadas por partida o zona, duraciones, dependencias,
recursos asignados y fechas de hitos. Suele exigirse en formato que permita análisis
de ruta crítica (p. ej. Gantt con predecesoras). Puede requerirse programa por
fases (mensual o trimestral) según el contrato.

**Revisión:** Comprobar que el programa sea coherente con el proyecto, con la
licencia y con las interfaces con suministros y subcontratistas. Verificar que
los hitos clave (entrega de fases, fin de obra) coincidan o sean compatibles con
el cronograma maestro. Las observaciones se devuelven al contratista por escrito.

**Actualizaciones:** El contrato puede exigir actualizaciones periódicas del
programa (p. ej. mensuales). Comparar con el avance real y con el cronograma
maestro en cada ciclo de seguimiento.
"""

