title = "Estructura del cronograma maestro"

content = """
El cronograma maestro es el documento central que ordena todas las actividades desde la
adquisición del suelo hasta la licencia de primera ocupación. Una estructura clara permite
ver dependencias, ruta crítica y asignación de recursos.

**Estructura por fases:** Organizar el trabajo en fases: predesarrollo, proyecto, licencias,
obra y cierre. Cada fase incluye entregables e hitos que alimentan la siguiente.

**Definición de actividades:** Cada actividad tiene ID, descripción, duración, predecesoras
y responsable. Usar nomenclatura coherente y evitar solapamientos. Enlazar actividades de
forma lógica para que las predecesoras determinen el inicio de las sucesoras.

**Hitos:** Definir hitos en decisiones clave y aprobaciones (cierre de compra, fin de
proyecto, obtención de licencia, inicio de obra, fin de obra). Los hitos tienen duración
cero y actúan como anclas del cronograma.

**Línea base y actualizaciones:** Fijar una línea base tras la aprobación de los agentes.
Registrar el avance real frente a la base y documentar desviaciones. Actualizar el
cronograma con periodicidad acordada y comunicar cambios al equipo.
"""

