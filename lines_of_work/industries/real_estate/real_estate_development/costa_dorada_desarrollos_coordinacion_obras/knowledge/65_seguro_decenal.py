title = "Seguro decenal"

content = """
En España y en varios países el promotor o el constructor deben suscribir
un seguro de daños materiales que cubra lesiones en la obra (estructurales,
de cerramiento, etc.) durante un periodo legal (p. ej. 10 años). La
coordinación de obra debe asegurar que la póliza esté vigente y que
la documentación se archive para el cierre y para la venta. En Latinoamérica
la figura puede variar (garantía, seguro).

**Obligación:** Surge al finalizar la obra; la póliza debe estar en vigor
antes de la recepción o de la licencia de primera ocupación según la
normativa. El tomador suele ser el promotor; los beneficiarios pueden
incluir compradores o entidades financieras. Verificar requisitos en el
contrato de obra y en la normativa aplicable.

**Documentación:** Conservar póliza, justificantes de pago y comunicaciones
con la aseguradora en el expediente del proyecto. Entregar copia a gestión
patrimonial o al comprador. La coordinación de obra vela por que no falte
en el handover.
"""

