# Knowledge: archivo de obra y conservación documental.

title = "Archivo de obra y conservación documental"

content = """
El archivo del proyecto reúne la documentación generada durante el
desarrollo y la obra para uso interno, para garantías, para la
administración y para la venta o cesión del activo. La coordinación
de obra vela por que el archivo esté ordenado, completo y accesible
durante la vida del proyecto y en el handover.

**Estructura:** Por fases (predesarrollo, proyecto, licencias, obra,
cierre) o por tipo (contratos, planos, actas, certificados). Nomenclatura
consistente e índice actualizado. Definir responsable del archivo
(en muchos casos la coordinación de obra) y dónde se almacena
(repositorio digital, carpeta física según política).

**Conservación:** Los plazos legales de conservación varían (contratos,
facturas, licencias, garantías). Mantener el archivo al menos durante
el periodo de garantías y según obligaciones legales y fiscales. Al
transferir el activo, entregar copia o acceso según acuerdos y conservar
lo que corresponda al promotor.
"""

