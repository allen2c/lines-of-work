title = "Cumplimiento de vivienda protegida y convenios"

content = """
Cuando el proyecto incluye vivienda protegida (VPO, VPPB, etc.) o está sujeto a
convenios urbanísticos o de cesión, el cumplimiento de los requisitos es obligatorio
y puede condicionar la licencia de primera ocupación y la venta. La coordinación
de obra debe tener visibilidad de estos requisitos.

**Requisitos típicos:** Superficies, tipologías y calidades definidas en el convenio
o en la normativa; plazos de adjudicación o de venta; documentación a presentar
a la administración; inspecciones específicas. Registrar todos en el plan de
cumplimiento y en el registro de condicionados.

**Seguimiento:** Incluir en las revisiones de obra y en el cierre que las unidades
o zonas afectadas cumplan lo pactado. Cualquier desviación debe ser aprobada por
la administración o por el convenio antes de variar el proyecto.

**Documentación:** Conservar certificados, actas y comunicaciones que acrediten
el cumplimiento para la administración y para el archivo del proyecto.
"""

