title = "Evaluación de impacto ambiental"

content = """
La evaluación de impacto ambiental (EIA) es un procedimiento administrativo que valora
los efectos previsibles de un proyecto sobre el medio ambiente. En desarrollo inmobiliario
a menudo es preceptiva para obras de cierta entidad o en zonas sensibles.

**Cuándo aplica:** Depende de la legislación autonómica y estatal (o equivalente en
Latinoamérica). Suele ser obligatoria para grandes superficies, suelo no urbanizado,
cercanía a espacios protegidos o cuando la administración lo determine en screening.

**Fases:** Estudio de impacto ambiental (elaborado por el promotor o consultor), período
de información pública, informe del órgano ambiental y declaración de impacto (favorable,
favorable con condiciones o desfavorable). Las condiciones se incorporan al proyecto
y al seguimiento.

**Integración en el cronograma:** La EIA puede ser anterior o paralela a la licencia
urbanística según el marco legal. Incluir plazos de exposición pública y de resolución
en el cronograma maestro para no retrasar el inicio de obra.
"""

