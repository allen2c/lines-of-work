# Knowledge: documentación de cierre del proyecto.

title = "Documentación de cierre del proyecto"

content = """
Al cierre del proyecto debe reunirse y organizarse la documentación que acredita la
ejecución conforme a proyecto y licencia, y que será necesaria para la operación,
la venta o el alquiler y las garantías. La coordinación de obra vela por que no
faltan entregables clave.

**Documentación técnica:** Proyecto as-built o actualizado; certificados de
dirección de obra; actas de replanteo e inspección; certificados de instalaciones;
documentación de accesibilidad y eficiencia energética; manuales de uso y
mantenimiento cuando se exijan.

**Licencias y contratos:** Licencia de obra, licencia de primera ocupación o cédula
de habitabilidad; contratos de suministro y de obra; pólizas de seguro vigentes;
liquidaciones y certificados de pago final.

**Archivo:** Organizar el archivo según la política de la empresa (por proyecto,
por tipo de documento, con índice). Entregar copia o acceso a gestión patrimonial
o al comprador según acuerdos. Conservar el original según obligaciones legales
y de garantía.
"""

