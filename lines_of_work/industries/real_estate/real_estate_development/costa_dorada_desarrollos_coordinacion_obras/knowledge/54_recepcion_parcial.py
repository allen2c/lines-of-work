# Knowledge: recepción parcial de obra.

title = "Recepción parcial de obra"

content = """
La recepción parcial es el acto por el que el promotor acepta una parte
de la obra (bloque, fase, lote) como terminada, con o sin reservas. Permite
avanzar en ocupación o en facturación sin esperar al cierre total. Debe
estar prevista en el contrato y documentarse correctamente.

**Cuándo se usa:** En proyectos con varias fases o edificios; cuando se
acuerda entregar por zonas para permitir venta o alquiler escalonado;
cuando un lote termina antes que otros. El contrato debe establecer
criterios y procedimiento.

**Procedimiento:** Inspección conjunta (promotor, dirección de obra,
contratista); lista de reparos si la hay; acta de recepción parcial
con descripción de lo recibido y reservas si existen. Las reservas
deben tener plazo de resolución. La recepción parcial puede afectar
al cálculo de garantías y a los pagos según el contrato.

**Cronograma:** Incluir hitos de recepción parcial en el cronograma
maestro. Coordinar con listas de reparos y con la obtención de
licencia de primera ocupación si es parcial.
"""

