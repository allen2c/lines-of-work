title = "Marco normativo en Latinoamérica"

content = """
Costa Dorada Desarrollos opera también en mercados de Latinoamérica. El
marco normativo (licencias, habitabilidad, impuestos, garantías) varía
por país e incluso por ciudad. La coordinación de obra debe conocer los
requisitos del proyecto concreto y planificar según la jurisdicción
correspondiente. Este conocimiento es de referencia; cada proyecto
debe validar con asesoría local.

**Licencias y permisos:** Pueden existir licencia de construcción,
permiso de uso, habilitación municipal, permisos ambientales y sectoriales.
Los plazos y la documentación varían. Incluir en el cronograma los hitos
de solicitud y resolución según la normativa local.

**Recepción y garantías:** Las figuras de recepción provisional y
definitiva, los plazos de garantía y los seguros pueden diferir de
España. El contrato de obra y el asesor local definen los procedimientos.
La coordinación de obra aplica los mismos principios (documentar, seguir
plazos, archivar) adaptados al marco local.
"""

