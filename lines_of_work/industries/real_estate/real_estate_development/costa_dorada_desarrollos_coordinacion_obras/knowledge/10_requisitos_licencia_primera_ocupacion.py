title = "Requisitos de licencia de primera ocupación o cédula de habitabilidad"

content = """
La licencia de primera ocupación (LPO) o la cédula de habitabilidad, según la comunidad
autónoma, acredita que la obra se ha ejecutado conforme al proyecto y a la licencia
y que el edificio reúne condiciones para su uso. Es necesaria para dar de alta suministros
y, en muchos casos, para vender o alquilar.

**Requisitos habituales:** Obra terminada según proyecto; certificado de dirección de
obra; certificado de instalaciones (eléctrica, fontanería, etc.) cuando aplique;
documentación de accesibilidad y de eficiencia energética según normativa; pago de tasas.
Algunas administraciones exigen inspección in situ.

**Plazos:** Solicitar con antelación; los plazos de resolución pueden ser largos. En
silencio administrativo, verificar si es positivo o negativo en la normativa aplicable.

**Condicionados:** La resolución puede imponer condiciones (obras complementarias,
documentación adicional). Registrar y dar seguimiento hasta el cierre. Sin LPO o cédula
en vigor, el uso legal del edificio puede quedar comprometido.
"""

