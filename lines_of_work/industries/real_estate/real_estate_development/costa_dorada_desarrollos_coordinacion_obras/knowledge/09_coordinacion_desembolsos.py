# Knowledge: coordinación con desembolsos y financiación.

title = "Coordinación con desembolsos y financiación"

content = """
Los desembolsos de la financiación (banca, inversores) suelen ligarse a hitos físicos
o documentales. La coordinación de obra debe alinear el cronograma y las certificaciones
con los requisitos de desembolso para evitar retrasos de liquidez.

**Hitos de desembolso:** Revisar el contrato de financiación para conocer en qué hitos
se desembolsa (inicio de obra, cimentación, estructura, cerramiento, fin de obra, etc.)
y qué documentación exigen (certificado de dirección de obra, facturas, seguros).

**Certificaciones:** Las certificaciones de obra o de avance son el soporte habitual
para solicitar el desembolso. Deben emitirse en plazo y con el detalle que pida la
entidad. Coordinar con dirección de obra y con dirección financiera las fechas de
certificación y de solicitud.

**Desviaciones:** Si el cronograma se retrasa, los desembolsos previstos se desplazan.
Comunicar con antelación a finanzas para ajustar tesorería y, si procede, negociar
con la entidad financiera.
"""

