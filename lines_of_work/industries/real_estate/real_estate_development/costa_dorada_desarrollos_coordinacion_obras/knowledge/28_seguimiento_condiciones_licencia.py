# Knowledge: seguimiento de condiciones de la licencia.

title = "Seguimiento de condiciones de la licencia"

content = """
La resolución de la licencia de obra (y de otras licencias) suele incluir condiciones
que deben cumplirse durante la obra o antes de la ocupación. Su incumplimiento puede
impedir la licencia de primera ocupación o dar lugar a requerimientos o sanciones.

**Registro:** Listar todas las condiciones de la licencia con referencia al texto
de la resolución. Asignar responsable de cumplimiento (promotor, dirección de obra,
contratista) y fecha límite si la hay. Estado: pendiente, en curso, cumplida, no
aplicable.

**Durante la obra:** Condiciones típicas: señalización del solar, horarios de obra,
gestión de residuos, control de ruidos y vibraciones, informe arqueológico, replanteos
y comunicaciones a la administración. Revisar el registro en las reuniones de obra
y en los informes de hitos.

**Cierre:** Antes de solicitar la licencia de primera ocupación, verificar que todas
las condiciones estén cumplidas y documentadas. La administración puede comprobar
su cumplimiento en la inspección final.
"""

