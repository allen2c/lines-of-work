# Knowledge: mantenimiento del registro de incidencias.

title = "Mantenimiento del registro de incidencias"

content = """
El registro de incidencias recoge los hechos que afectan o pueden afectar al proyecto
(plazo, coste, calidad o seguridad) y el estado de su resolución. Es distinto del
registro de riesgos: las incidencias son hechos ya ocurridos o en curso.

**Qué registrar:** Retrasos en entregas, no conformidades en obra, accidentes o
casi accidentes, conflictos entre partes, incumplimiento de condicionados, hallazgos
en obra que exijan cambio de proyecto, retrasos administrativos. Incluir fecha,
descripción, impacto y responsable de seguimiento.

**Seguimiento:** Cada incidencia debe tener un estado (abierta, en resolución,
cerrada) y, si aplica, una acción correctora o escalado. Revisar en reuniones de
seguimiento y en informes de hitos. Las incidencias cerradas se archivan con la
decisión o el cierre documentado.

**Escalado:** Si una incidencia no se resuelve en el plazo esperado o supera umbrales
de impacto, escalar a dirección de desarrollo y, si procede, a asesoría jurídica o
a la aseguradora.
"""

