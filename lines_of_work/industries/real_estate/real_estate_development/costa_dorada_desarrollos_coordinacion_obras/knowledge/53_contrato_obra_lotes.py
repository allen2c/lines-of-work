# Knowledge: contrato de obra por lotes y coordinación.

title = "Contrato de obra por lotes y coordinación"

content = """
Cuando la obra se contrata por lotes (estructura, instalaciones, acabados,
etc.) con distintos contratistas, la coordinación entre lotes es crítica.
Los retrasos o las no conformidades de un lote afectan a los siguientes.
La coordinación de obra vela por que las interfaces estén definidas y
por que el programa conjunto sea coherente.

**Definición de interfaces:** En proyecto y en contrato deben quedar claros
los límites entre lotes (quién hace qué, hasta dónde, en qué fecha).
Evitar huecos o solapamientos. Las reuniones de coordinación entre
contratistas (o con el contratista principal y subcontratistas) deben
estar en la agenda periódica.

**Programa integrado:** Un único cronograma que refleje todos los lotes
y sus dependencias. Actualizarlo con el avance real de cada uno. Si
un lote se retrasa, comunicar de inmediato a los afectados y evaluar
impacto en el cierre.

**Conflictos:** Las discrepancias entre lotes (daños, responsabilidad)
deben escalarse a dirección de obra y, si procede, a dirección de
desarrollo. Documentar acuerdos y variaciones.
"""

