# Knowledge: licencia de actividad y apertura.

title = "Licencia de actividad y apertura"

content = """
Para usos no puramente residenciales (comercial, oficinas, hostelería, etc.)
suele ser necesaria una licencia de actividad o de apertura que autorice
el uso conforme a la normativa urbanística y de seguridad. La coordinación
de obra debe conocer si el proyecto está sujeto a este requisito y planificar
su obtención antes de la puesta en marcha.

**Cuándo aplica:** Depende del uso y de la normativa local. Actividades
clasificadas (por ruido, riesgo, aforo) suelen requerir licencia o
comunicación. La licencia de primera ocupación no sustituye la de actividad
cuando esta es preceptiva.

**Documentación y trámite:** Proyecto de actividad, memoria, planos,
certificados de instalaciones y de prevención de incendios según corresponda.
Plazos de resolución según la administración. Incluir en el cronograma
de cierre si la actividad debe estar operativa en una fecha concreta.

**Condicionados:** La resolución puede imponer condiciones de uso o de
mantenimiento. Transmitirlas a gestión patrimonial o al operador del
local.
"""

