# Knowledge: eficiencia energética y certificación.

title = "Eficiencia energética y certificación"

content = """
El certificado de eficiencia energética es obligatorio en España para
edificios de nueva construcción y para venta o alquiler. La coordinación
de obra debe asegurar que el proyecto cumple los requisitos y que el
certificado se obtiene y se archiva para la licencia de primera ocupación
o para la entrega. En Latinoamérica existen figuras equivalentes según
el país.

**Proyecto:** El proyecto debe incorporar las exigencias de la normativa
(aislamiento, instalaciones, ventilación). La calificación energética
se calcula con herramientas reconocidas. Incluir en el cierre que la
obra ejecutada coincide con lo proyectado en este aspecto.

**Certificado:** Un técnico competente emite el certificado con la
calificación (A–G) y las recomendaciones de mejora. Se registra en
el organismo autonómico correspondiente. Conservar copia en el expediente
y entregar a comprador o inquilino cuando corresponda. La coordinación
de obra vela por que la fecha de emisión no retrase la licencia de
primera ocupación o la venta.
"""

