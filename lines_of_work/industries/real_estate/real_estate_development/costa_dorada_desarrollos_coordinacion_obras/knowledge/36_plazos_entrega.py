# Knowledge: plazos de entrega y lead times.

title = "Plazos de entrega y lead times"

content = """
Muchas partidas del proyecto dependen de plazos de entrega (lead times) de
materiales, equipos o subcontratistas. No incorporarlos al cronograma genera
retrasos evitables. La coordinación de obra debe asegurar que la programación
los tenga en cuenta.

**Materiales y equipos:** Pedidos con varios meses de antelación (estructura
metálica, ascensores, fachada, etc.). Verificar con el contratista y con los
suministradores los plazos reales y las fechas de pedido necesarias para
cumplir el programa. Incluir hitos de pedido en el cronograma.

**Subcontratistas:** La disponibilidad de especialistas (instalaciones, acabados)
puede condicionar las fechas. Coordinar con el contratista la reserva de fechas
y la secuencia de lotes para no bloquear la obra.

**Riesgo:** En épocas de alta demanda o de escasez de suministro, los lead times
pueden alargarse. Revisar periódicamente y actualizar el cronograma si los
plazos cambian.
"""

