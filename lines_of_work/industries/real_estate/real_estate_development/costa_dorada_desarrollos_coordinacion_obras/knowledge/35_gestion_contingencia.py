# Knowledge: gestión de la contingencia de plazo y coste.

title = "Gestión de la contingencia de plazo y coste"

content = """
La contingencia es el margen reservado para absorber imprevistos de plazo o coste
sin alterar el compromiso formal con inversores o financiación. Su uso debe
documentarse y comunicarse para mantener la credibilidad del plan.

**Contingencia de plazo:** Días o semanas incorporados al cronograma maestro para
retrasos menores (administrativos, meteorología, suministros). Cuando se consumen,
actualizar el cronograma y comunicar. Si los retrasos superan la contingencia,
es necesario prórroga formal, recuperación de plazo o revisión de hitos con
inversores o banca.

**Contingencia de coste:** Partida presupuestaria para variaciones y imprevistos.
Las órdenes de modificación o los gastos no previstos se imputan a la contingencia
hasta su agotamiento. Por encima, se requiere aprobación de dirección o de
inversores según umbrales.

**Registro:** Llevar un seguimiento del consumo de contingencia (plazo y coste)
y de las causas. Incluir en informes de hitos para que dirección e inversores
tengan visibilidad.
"""

