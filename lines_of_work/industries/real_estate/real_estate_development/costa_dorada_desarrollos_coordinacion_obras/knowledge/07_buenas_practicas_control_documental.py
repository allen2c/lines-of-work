title = "Buenas prácticas de control documental"

content = """
El control documental garantiza que el equipo disponga de la versión vigente de planos,
licencias, órdenes de modificación y correspondencia, y que quede trazabilidad de los
cambios.

**Estructura y nomenclatura:** Carpeta o repositorio por proyecto con subcarpetas por
tipo (proyecto, licencias, obra, contratos, comunicaciones). Nomenclatura consistente:
código de proyecto, tipo de documento, versión y fecha (ej. CD-01-PROY-A01-2024-03).

**Versiones:** Cada nueva versión de un documento relevante se registra con número o
fecha. No sobrescribir versiones anteriores; conservar historial según política de la
empresa y obligaciones legales.

**Acceso y difusión:** Definir quién puede editar, quién puede consultar y cómo se
distribuyen las actualizaciones a contratistas y consultores. En obra, asegurar que
solo se usen planos y revisiones aprobadas.
"""

