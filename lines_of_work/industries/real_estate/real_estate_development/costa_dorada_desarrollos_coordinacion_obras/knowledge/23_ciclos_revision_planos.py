# Knowledge: ciclos de revisión de planos.

title = "Ciclos de revisión de planos"

content = """
Los planos del proyecto pasan por ciclos de revisión (internos, con consultores,
con el promotor) antes de considerarse cerrados para licencia y obra. Planificar
estos ciclos evita retrasos en la obtención de la licencia o en el inicio de obra.

**Niveles de revisión:** Revisión interna del equipo de proyecto; revisión por
especialidades (estructura, instalaciones); revisión por el promotor (alcance,
estándares); revisión por la administración (si aplica en la fase de licencia).
Cada ciclo puede devolver comentarios que exijan una nueva versión.

**Plazos y responsabilidad:** Asignar plazos de respuesta por cada ciclo y un
responsable que agrupe comentarios y cierre la versión. Evitar ciclos indefinidos;
fijar un número máximo de rondas o una fecha de cierre para la versión de licencia.

**Trazabilidad:** Guardar versiones y registro de comentarios principales para
poder justificar cambios ante la administración o en caso de reclamación.
"""

