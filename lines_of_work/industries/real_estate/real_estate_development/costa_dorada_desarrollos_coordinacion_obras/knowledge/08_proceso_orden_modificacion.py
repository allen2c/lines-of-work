# Knowledge: proceso de orden de modificación.

title = "Proceso de orden de modificación"

content = """
Las órdenes de modificación (o órdenes de cambio) formalizan alteraciones del alcance,
plazo o precio respecto al contrato o al proyecto base. Un proceso claro evita reclamaciones
y mantiene el control del cronograma y del presupuesto.

**Origen:** Puede ser solicitud del promotor, propuesta del contratista o resultado
de incidencias (condicionados, hallazgos en obra). Cualquier cambio que afecte alcance,
plazo o coste debe documentarse.

**Flujo:** Propuesta o solicitud → valoración técnica y económica por la dirección
facultativa o el contratista según corresponda → aprobación por el promotor (según
umbrales internos) → emisión de orden de modificación firmada → incorporación al
cronograma y al presupuesto. Las OM se numeran y se archivan.

**Impacto en cronograma:** Si la modificación implica más plazo, actualizar el
cronograma maestro y comunicar a los agentes. Las prórrogas de licencia de obra
pueden requerir solicitud a la administración.
"""

