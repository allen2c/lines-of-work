# Knowledge: programación y definición de alcance.

title = "Programación y definición de alcance"

content = """
La programación (programming) en desarrollo inmobiliario es la fase en que se
define el alcance del proyecto: usos, superficies, tipologías, estándares.
Un alcance bien definido y estable evita cambios costosos durante el proyecto
y la obra. La coordinación de obra trabaja sobre ese alcance ya fijado, pero
debe entender su origen.

**Contenido:** Brief del promotor; superficies y distribución por uso;
requisitos normativos y de convenio; estándares de calidad; restricciones
de presupuesto y plazo. Todo ello se refleja en el proyecto básico y de
ejecución. Cualquier cambio de alcance debe pasar por el proceso de
orden de modificación y aprobación.

**Congelación de alcance:** En un momento dado se «congela» el alcance para
licencia y contrato de obra. Los cambios posteriores son variaciones
gestionadas formalmente. La coordinación de obra vela por que la ejecución
se ajuste al alcance contratado y por que las variaciones queden documentadas.
"""

