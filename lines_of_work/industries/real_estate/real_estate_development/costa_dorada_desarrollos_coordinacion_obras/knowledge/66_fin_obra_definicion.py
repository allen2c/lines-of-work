# Knowledge: definición de fin de obra.

title = "Definición de fin de obra"

content = """
El «fin de obra» o «terminación de la obra» es el hito a partir del cual
se cuentan plazos de recepción, garantías y obligaciones de seguro. Debe
estar definido en el contrato y documentado para evitar disputas. La
coordinación de obra alinea el cronograma y las actas con esta definición.

**Definición contractual:** Suele ser la fecha en que el contratista
comunica que la obra está terminada conforme al proyecto y lista para
recepción, o la fecha de entrega de llaves según el tipo de contrato.
Puede haber recepciones parciales con distintas fechas de «fin» por
zona. Revisar el pliego para no confundir fin de obra con recepción
definitiva.

**Documentación:** Acta de fin de obra o comunicación formal del
contratista; acta de recepción (provisional o definitiva) en la que
el promotor acepta el estado. Las listas de reparos pueden retrasar
la recepción definitiva pero no necesariamente el «fin de obra» si
el contrato lo distingue. Conservar en el archivo.
"""

