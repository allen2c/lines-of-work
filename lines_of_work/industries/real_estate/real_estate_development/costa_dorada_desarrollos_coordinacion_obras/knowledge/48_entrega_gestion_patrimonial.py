# Knowledge: entrega a gestión patrimonial.

title = "Entrega a gestión patrimonial"

content = """
Al cierre del proyecto, la documentación y la información necesarias para la
gestión del activo (venta, alquiler, mantenimiento) se entregan al equipo
de gestión patrimonial o al comprador. La coordinación de obra asegura que
el handover sea completo y ordenado.

**Contenido típico:** Planos as-built; manuales de uso y mantenimiento de
instalaciones; certificados y licencias (primera ocupación, instalaciones);
pólizas y garantías vigentes; contactos de contratistas y suministradores
para garantía; claves y accesos si aplica. Lista cerrada según acuerdo
interno o con comprador.

**Formato y soporte:** Entregar en el formato acordado (carpeta digital,
repositorio, copia física si se pacta). Incluir índice o checklist firmado
como acta de entrega. La gestión patrimonial debe poder operar el edificio
y atender a propietarios o inquilinos sin depender del equipo de desarrollo.

**Plazos:** La entrega suele coincidir con la recepción o con el cierre
de venta. Planificar con antelación para que no falte documentación en
el último momento.
"""

