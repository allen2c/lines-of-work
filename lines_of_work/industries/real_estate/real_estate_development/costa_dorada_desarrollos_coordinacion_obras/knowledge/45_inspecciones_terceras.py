# Knowledge: inspecciones de terceros.

title = "Inspecciones de terceros"

content = """
Además de la dirección de obra y de la administración, pueden intervenir
inspecciones de terceros: aseguradora, entidad de control, certificadora,
o auditoría interna. La coordinación de obra facilita el acceso y la
documentación que soliciten.

**Tipos:** Inspección de la aseguradora (seguro decenal, RC) en fases
definidas del proyecto; entidad de control para certificación de
instalaciones o de eficiencia energética; auditoría interna de procesos
o de cumplimiento. Cada una tiene alcance y periodicidad propios.

**Preparación:** Conocer fechas y requisitos con antelación. Asegurar
que la obra esté en condiciones (accesos, limpieza, documentación)
y que los interlocutores designados estén disponibles. Las no conformidades
o observaciones deben registrarse y cerrarse según los plazos acordados.

**Documentación:** Conservar actas e informes de inspecciones de
terceros en el expediente del proyecto. Pueden ser exigidos para
pólizas, certificados o venta.
"""

