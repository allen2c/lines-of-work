# Knowledge: rutinas de reunión con el contratista.

title = "Rutinas de reunión con el contratista"

content = """
Las reuniones periódicas con el contratista (y con la dirección de obra) mantienen
alineados el avance, los problemas y las acciones. Una rutina estable evita que se
pierdan temas y que las decisiones se retrasen.

**Frecuencia y asistencia:** Semanal durante obra es lo habitual; en fases de
proyecto o de licencias puede ser quincenal. Asisten promotor (coordinación de
obra), dirección de obra y contratista; se invita a consultores o subcontratistas
cuando el orden del día lo requiera.

**Orden del día:** Avance respecto al programa; incidencias y RFI abiertas;
submittals pendientes; riesgos y próximos hitos; acciones con responsable y
fecha. Dejar un espacio para temas urgentes sin que domine la reunión.

**Acta y seguimiento:** Redactar acta en 24–48 h con acuerdos y acciones.
Distribuir a los asistentes y archivar. Las acciones se revisan en la siguiente
reunión; las no cerradas se escalan si es necesario.
"""

