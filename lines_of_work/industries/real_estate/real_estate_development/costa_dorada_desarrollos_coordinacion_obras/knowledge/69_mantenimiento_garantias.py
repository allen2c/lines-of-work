# Knowledge: mantenimiento de garantías y retenciones.

title = "Mantenimiento de garantías y retenciones"

content = """
Durante y tras la obra pueden existir retenciones en los pagos (por
garantía de buen fin o por reparos pendientes) y garantías bancarias
o pólizas. La coordinación de obra debe tener visibilidad de los
plazos de liberación y de la documentación necesaria para no retener
de más ni liberar antes de tiempo.

**Retenciones contractuales:** El contrato puede establecer una
retención (p. ej. 5%) hasta la recepción definitiva o hasta el fin
del periodo de garantía de buen fin. La liberación requiere acta de
recepción y, si aplica, certificado de fin de garantía. Documentar
y coordinar con dirección financiera.

**Garantías bancarias:** Si el contratista ha aportado garantía
bancaria (avance, cumplimiento), su liberación sigue lo pactado en
el contrato. Conservar copia de la garantía y de las comunicaciones
con el banco. No liberar sin autorización interna y sin comprobar
que no hay reclamaciones pendientes.
"""

