# Knowledge: captura de lecciones aprendidas.

title = "Captura de lecciones aprendidas"

content = """
Al cierre del proyecto (o en hitos intermedios) se recogen lecciones aprendidas:
qué funcionó bien, qué falló y qué se haría distinto. Sirven para mejorar
procesos y evitar repetir errores en futuros proyectos. La coordinación de
obra puede liderar o aportar a esta captura.

**Qué capturar:** Retrasos y sus causas; incidencias recurrentes; buenas
prácticas de coordinación con administración o contratista; herramientas o
formatos que hayan funcionado; recomendaciones para cronograma, licencias
o contratos. Enfoque en hechos y acciones, no en culpas.

**Cuándo:** Al cierre del proyecto, con reunión o cuestionario al equipo
(clave: dirección de obra, contratista si participa, consultores). Opcionalmente
en hitos (fin de predesarrollo, fin de obra) para no perder información.

**Uso:** Almacenar en repositorio accesible (base de conocimiento, wiki).
Revisar al planificar proyectos similares. No sustituye el archivo del
proyecto ni la documentación contractual.
"""

