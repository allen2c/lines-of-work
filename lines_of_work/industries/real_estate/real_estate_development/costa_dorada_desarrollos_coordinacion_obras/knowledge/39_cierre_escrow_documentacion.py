# Knowledge: cierre de escrow y documentación de entrega.

title = "Cierre de escrow y documentación de entrega"

content = """
En algunos proyectos la documentación clave (planos as-built, certificados,
licencias) se deposita en escrow o se entrega formalmente al promotor o al
comprador en un acto de cierre. La coordinación de obra vela por que el
conjunto esté completo y conforme antes de la fecha acordada.

**Contenido del escrow o entrega:** Según contrato o acuerdo: proyecto as-built,
certificados de dirección de obra e instalaciones, licencia de primera ocupación,
pólizas vigentes, manuales, garantías. Listar en un checklist y verificar cada
ítem antes del cierre.

**Plazos:** La fecha de cierre suele estar fijada en el contrato de compraventa
o de financiación. Retrasos en la obtención de la licencia o en la liquidación
de la lista de reparos pueden retrasar el cierre; comunicar con antelación a
las partes.

**Acta de entrega:** Documentar qué se entrega, a quién y en qué soporte.
Firmar acta por las partes. Conservar copia en el archivo del proyecto.
"""

