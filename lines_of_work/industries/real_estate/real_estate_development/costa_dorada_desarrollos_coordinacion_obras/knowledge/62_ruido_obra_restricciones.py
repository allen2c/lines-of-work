# Knowledge: ruido y restricciones durante la obra.

title = "Ruido y restricciones durante la obra"

content = """
Las obras están sujetas a restricciones de horario y de nivel de ruido
por normativa municipal y por condicionados de la licencia. El incumplimiento
puede dar lugar a denuncias, requerimientos o paralización. La coordinación
de obra debe conocer las restricciones y velar por su cumplimiento en
la programación y en la comunicación con el contratista.

**Restricciones típicas:** Horarios de trabajo (p. ej. no noches ni
domingos); límites de nivel sonoro; prohibición de maquinaria ruidosa
en determinadas fases sin medidas correctoras. La licencia de obra o
la ordenanza municipal suelen fijarlas. Incluir en el registro de
condicionados y en el plan de obra.

**Comunicación:** Informar al contratista por escrito de las restricciones.
En las reuniones de obra recordar la importancia del cumplimiento. Si
hay quejas vecinales, documentar y, si procede, adoptar medidas
adicionales (barreras, horarios más restrictivos) para evitar
escalada con la administración.
"""

