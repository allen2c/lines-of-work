title = "Subcontratistas y aprovisionamiento"

content = """
El contratista principal puede apoyarse en subcontratistas y en suministros
externos. La coordinación de obra no gestiona sus contratos, pero debe
tener visibilidad de que los recursos estarán disponibles según el programa
y de que no hay bloqueos por falta de aprovisionamiento o de subcontrata.

**Programa:** El programa del contratista debe reflejar las fechas en
que cada subcontrata o suministro es necesario. Revisar en las reuniones
periódicas que no haya huecos (pedidos no hechos, subcontratas no
confirmadas). Los lead times largos deben estar incorporados al cronograma.

**Incidencias:** Si un subcontratista o un suministro se retrasa, el
contratista debe comunicarlo y proponer medidas. La coordinación de
obra registra el impacto en el cronograma y, si procede, en el informe
de hitos. Las reclamaciones entre contratista y subcontratista son
ajenas al promotor salvo que afecten al plazo o al alcance contratado.
"""

