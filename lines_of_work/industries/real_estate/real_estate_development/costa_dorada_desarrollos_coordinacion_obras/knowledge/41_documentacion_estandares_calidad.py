# Knowledge: documentación de estándares de calidad.

title = "Documentación de estándares de calidad"

content = """
Los estándares de calidad del promotor (acabados, especificaciones, marcas)
deben estar documentados y comunicados al equipo de proyecto y al contratista
para que la obra se ejecute conforme a lo esperado. La coordinación de obra
asegura que esta documentación esté disponible y actualizada.

**Contenido:** Pliegos de condiciones; especificaciones técnicas; catálogos
o muestras aprobados; criterios de recepción por partida. Todo lo que defina
«qué es conformidad» debe estar por escrito y referenciado en el contrato.

**Revisión y aprobación:** Los estándares se aprueban antes del inicio de
obra o en fases tempranas. Cualquier cambio debe comunicarse por escrito y
evaluarse en plazo y coste. Los submittals del contratista se comparan con
estos estándares.

**Archivo:** Conservar versiones vigentes en el expediente del proyecto y
en el repositorio accesible al equipo. Incluir en el cierre del proyecto
para gestión patrimonial y para garantías.
"""

