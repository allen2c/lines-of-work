# Knowledge: liquidación del contrato de obra.

title = "Liquidación del contrato de obra"

content = """
La liquidación del contrato es el acto por el que se determina el importe
definitivo de la obra (mediciones finales, variaciones, deducciones).
Suele ir ligada a la recepción definitiva y al pago del saldo. La
coordinación de obra no la elabora, pero debe conocer el proceso y los
plazos para que el cierre del proyecto no se retrase por desajustes
en la liquidación.

**Contenido:** Mediciones finales de todas las partidas; aplicación
de órdenes de modificación; deducciones por penalizaciones, reparos
o retenciones pactadas. Dirección de obra y contratista suelen
elaborar la propuesta de liquidación; el promotor la aprueba. Puede
haber discrepancia y negociación; documentar acuerdos por escrito.

**Plazos:** El contrato puede fijar un plazo desde la recepción para
presentar la liquidación y para su aprobación. Incluir en el cronograma
de cierre. La coordinación de obra vela por que las partes dispongan
de la información necesaria (certificaciones, OM, actas) para no
bloquear la liquidación.
"""

