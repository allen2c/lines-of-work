# Knowledge: verificación del seguro de obra.

title = "Verificación del seguro de obra"

content = """
El contratista debe mantener pólizas en vigor (responsabilidad civil, accidentes,
seguro decenal si aplica) según el pliego o el contrato. La coordinación de
obra o la dirección de obra suelen verificar que los seguros estén al día
antes del inicio y en renovaciones periódicas. Un seguro caducado puede
implicar paralización o responsabilidad para el promotor.

**Qué verificar:** Que las pólizas estén vigentes; que las coberturas y
capitales cumplan lo contratado; que el promotor figure como tomador o
beneficiario cuando corresponda; que las fechas de renovación estén
controladas. Solicitar copia de póliza y de justificantes de pago.

**Periodicidad:** Verificación al inicio de obra y antes de cada periodo
de renovación (anual). Si el contratista no presenta la documentación
a tiempo, escalar y, si es necesario, retener pagos hasta regularización.

**Archivo:** Conservar justificantes en el expediente del proyecto.
Requeridos para desembolsos de financiación y para el cierre.
"""

