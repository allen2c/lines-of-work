# Knowledge: cédula de habitabilidad.

title = "Cédula de habitabilidad"

content = """
La cédula de habitabilidad (o figura equivalente en cada comunidad autónoma)
acredita que una vivienda reúne las condiciones mínimas de habitabilidad
(superficie, altura, ventilación, instalaciones). En muchas comunidades
es requisito para el alta de suministros y para la venta o el alquiler.
Puede ser obligatoria en lugar de o además de la licencia de primera
ocupación, según la normativa.

**Solicitud:** Tras la finalización de la obra, se presenta la solicitud
con la documentación que exija la administración (proyecto, certificados,
licencia de obra, etc.). La resolución puede ser automática con
documentación completa o requerir inspección. Los plazos varían.

**Vigencia:** La cédula puede tener vigencia indefinida o limitada (p. ej.
diez años), tras la cual debe renovarse. En ventas o alquileres suele
exigirse vigente. Incluir en el archivo del proyecto y en la entrega
a gestión patrimonial o comprador. La coordinación de obra vela por
que se solicite en tiempo y por que se archive el justificante.
"""

