# Knowledge: coordinación de inspecciones.

title = "Coordinación de inspecciones"

content = """
Las inspecciones de la administración (obra, instalaciones, accesibilidad, etc.)
y las inspecciones de dirección de obra o de control de calidad deben programarse
y documentarse. Una inspección no superada retrasa el avance o la recepción.

**Tipos:** Inspecciones de la administración (obligatorias por normativa en determinados
puntos de la obra); inspecciones de la dirección de obra (seguimiento y conformidad);
inspecciones de terceros (seguros, certificaciones). Cada una tiene requisitos de
aviso, documentación y acta.

**Programación:** Incluir en el cronograma las fechas previstas de inspección según
el avance de la obra. Solicitar cita con la antelación que exija la administración.
Asegurar que la obra esté en condiciones de ser inspeccionada (accesos, limpieza,
documentación en obra).

**Seguimiento:** Registrar resultado (conforme, con reservas, no conforme) y acciones
derivadas. Las no conformidades deben cerrarse antes de seguir con la siguiente
fase o antes de la recepción, según corresponda.
"""

