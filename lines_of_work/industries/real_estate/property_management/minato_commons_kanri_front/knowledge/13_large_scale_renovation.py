content = """
- 大規模修繕は外壁・屋上防水・鉄部塗装・給排水管等を計画的に更新する工事で、概ね12年周期を目安に実施する。
- 進め方は一般に、(1)専門委員会（修繕委員会）の設置、(2)建物診断・劣化調査、(3)設計・仕様の検討、(4)施工会社の選定、(5)総会での工事・予算の決議、(6)施工・監理、(7)完了検査・アフター点検、の順で進む。
- 発注方式には「設計監理方式（設計事務所が監理）」「責任施工方式」があり、透明性・コスト・専門性の観点から理事会に選択肢を示す。
- 施工会社選定は、公募・相見積り・ヒアリングを経て、価格だけでなく実績・体制・保証を総合評価する。
- 工事費・予算・工期・仮設計画（足場・工事期間中の生活影響）は総会で決議し、区分所有者へ丁寧に周知する。
- フロント担当は事務局として、住民説明会の運営、工事中の問い合わせ対応、進捗・支払管理を支援する。
"""

