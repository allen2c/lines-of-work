content = """
- 敷地内の駐車場・駐輪場・バイク置場は共用部分で、使用細則に基づき区分所有者へ有償で使用許可（使用契約）する。
- 空き区画の管理・募集、使用料の徴収、契約者名簿の更新はフロント担当が行い、公平性のため空き待ちは申込順（キャンセル待ちリスト）で管理する。
- 機械式駐車場（パレット式・タワー式）は、定期点検（一般に月次保守）と法定の安全対策が必要で、故障時は速やかに保守会社へ手配する。
- 契約者以外の無断駐車・放置車両・放置自転車には、警告書の掲示・貼付、期限を定めた撤去予告など段階的に対応する。所有者不明の車両は法的手続きを踏まえ慎重に扱う。
- 転居・売却で区画が不要になった場合は、解約手続きと精算を行い、次の希望者へ割り当てる。
- 駐車場使用料の会計上の取扱い（管理費会計か修繕積立金会計か）は規約・総会決議に従って処理する。
"""

