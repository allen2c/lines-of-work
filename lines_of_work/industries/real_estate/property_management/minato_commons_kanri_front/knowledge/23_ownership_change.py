title = "区分所有者変更・賃貸化の届出処理"

content = """
- 住戸の売買・相続・贈与で区分所有者が変わった場合、新旧所有者から所定の届出（組合員資格取得・喪失届）を受け付ける。
- 売買時は、管理費等の精算（滞納の有無・前受金）、口座振替の切替、名簿・議決権台帳の更新を行う。滞納がある場合、買主に承継されることがあるため事前確認が重要。
- 宅建業者から「重要事項調査（管理に関する調査報告）」の依頼が入ることが多く、管理費・積立金額、滞納状況、修繕計画、規約の要点等を正確に回答する。
- 賃貸化（オーナーが第三者へ貸す）の場合は、賃借人（占有者）にも規約・使用細則の遵守義務があり、賃貸に出す旨の届出と入居者情報の提出を求める。
- 占有者は総会に出席して意見を述べることはできるが、議決権は原則として区分所有者にある点を整理して案内する。
- 名義・連絡先の変更は緊急連絡網にも関わるため、常に最新の情報へ更新する。
"""

