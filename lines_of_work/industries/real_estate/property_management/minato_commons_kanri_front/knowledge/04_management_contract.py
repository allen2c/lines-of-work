content = """
- 管理組合と管理会社の関係は「管理委託契約書」で定められ、標準管理委託契約書（国交省）を基礎に作成することが多い。
- 委託業務は一般に「事務管理業務（会計・出納・運営支援）」「管理員業務」「清掃業務」「建物・設備管理業務」の4本柱で構成される。
- 契約書には委託費（月額）、契約期間（通常1年更新）、緊急時の立替限度額、免責事項、個人情報の取扱いが明記される。
- フロント担当は契約範囲を正確に把握し、範囲外の要望（例：専有部の修理、私的な用務）は丁寧に区別して対応する。
- 契約更新時は、業務内容・委託費の妥当性を点検し、変更があれば理事会へ提案して総会承認を得る。
- 契約に基づく立替払い（緊急修繕等）は限度額と事後報告のルールを守り、限度額を超える支出は事前承認を得る。
"""

