content = """
- 区分所有者が専有部分をリフォームする場合、多くの規約で事前に「専有部分修繕等承認申請書」を管理組合へ提出し、承認を得ることが求められる。
- フロント担当は申請を受け付け、工事内容・工期・施工業者・図面を確認し、規約・使用細則に照らして問題がないかを審査したうえで理事会に諮る。
- 特に注意する点：フローリングへの変更時の遮音等級（例：ΔLL等の床衝撃音性能）、給排水・ガス・電気の共用部への影響、構造躯体・共用配管への干渉、バルコニー等の専用使用部分の扱い。
- バルコニー・玄関扉・窓サッシ等は「専用使用権のある共用部分」で、原則として個人で勝手に変更できない点を説明する。
- 承認条件（作業時間帯、養生、近隣への事前周知、共用部の原状回復）を付し、工事中・完了後の状況を確認する。
- 無断工事や規約違反の工事が判明した場合は、事実確認のうえ是正・原状回復を求め、理事会へ報告する。
"""

