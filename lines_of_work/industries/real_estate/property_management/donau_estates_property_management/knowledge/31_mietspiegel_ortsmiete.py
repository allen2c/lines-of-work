title = "Mietspiegel und ortsübliche Miete"

content = """
Der Mietspiegel dokumentiert die ortsübliche Vergleichsmiete für Wohnraum in einer Gemeinde.
Er wird von Gemeinden oder Interessenverbänden erstellt und dient als Referenz bei
Mietstreitigkeiten und Mieterhöhungen. Die ortsübliche Miete (§ 558 BGB) ist die
übliche Entgelt für vergleichbare Wohnungen. Bei Mieterhöhungen darf die Nettokaltmiete
innerhalb von drei Jahren nicht mehr als 20 Prozent steigen. Der Mietspiegel hilft,
diese Grenze zu prüfen. Qualifizierte Mietspiegel berücksichtigen Lage, Ausstattung,
Baujahr und Wohnfläche. Ein einfacher Mietspiegel listet nur Durchschnittswerte.
"""  # noqa: E501

