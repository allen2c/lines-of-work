"""Knowledge item: Wohnungsbesichtigung Mieter."""

title = "Besichtigungsrecht und Zugang zur Wohnung"

content = """
Der Vermieter hat ein begrenztes Recht, die Wohnung zu besichtigen, z.B. zur
Instandhaltung, Wertermittlung oder Vermarktung. Die Besichtigung muss angemessen
angekündigt werden (üblich 2–4 Wochen) und zu vernünftigen Zeiten stattfinden. Der
Mieter muss den Zugang dulden, kann aber Terminvorschläge machen. Bei dringenden
Reparaturen ist die Ankündigungsfrist verkürzt. Respektvoller Umgang beugt Konflikten vor.
"""  # noqa: E501

