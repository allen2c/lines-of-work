title = "Mahnwesen und Zahlungsverzug"

content = """
Bei Zahlungsverzug des Mieters sind formale Schritte erforderlich. Zunächst eine
freundliche Erinnerung, dann eine Mahnung mit Fristsetzung. Ab dem zweiten
Monat ohne Zahlung kann eine Kündigung erwogen werden. Die Mahnung muss
schriftlich erfolgen und eine angemessene Zahlungsfrist setzen (üblich 14 Tage).
Verzugszinsen können ab dem Zeitpunkt des Verzugs berechnet werden. Bei
wiederholtem Verzug sollte ein Gespräch geführt werden; Sonderzahlungsvereinbarungen
dokumentieren. Bei anhaltendem Verzug: rechtliche Beratung einholen und ggf.
Kündigung nach § 543 Abs. 2 Nr. 3 BGB prüfen.
"""  # noqa: E501

