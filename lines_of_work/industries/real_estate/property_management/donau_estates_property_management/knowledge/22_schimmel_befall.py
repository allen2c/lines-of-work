title = "Schimmelbefall"

content = """
Schimmel entsteht oft durch Feuchtigkeit, mangelnde Lüftung oder bauliche Mängel.
Die Ursachenklärung ist entscheidend: Lüftungsverhalten des Mieters vs. bauliche
Defizite (Wärmebrücken, undichte Stellen). Die Verwaltung dokumentiert den Befund,
beauftragt ggf. Gutachten und koordiniert Sanierung. Bei Mitverschulden des Mieters
kann die Miete gemindert oder Schadensersatz geltend gemacht werden. Regelmäßige
Begehungen und Mieterberatung zu Lüftung reduzieren Streitfälle.
"""  # noqa: E501

