"""Knowledge item: Betriebskostenvorauszahlung."""

title = "Betriebskostenvorauszahlung und Anpassung"

content = """
Die monatliche Nebenkostenvorauszahlung wird im Mietvertrag festgelegt und basiert auf
Schätzwerten. Nach der Jahresabrechnung erfolgt Erstattung oder Nachforderung. Bei
dauerhafter Abweichung von mehr als 10 Prozent kann der Vermieter die Vorauszahlung
angemessen anpassen (§ 560 BGB). Die Anpassung bedarf der Schriftform. Transparente
Kommunikation mit dem Mieter über die Gründe verhindert Konflikte.
"""  # noqa: E501

