title = "Indexmiete und Staffelmiete"

content = """
**Indexmiete:** Die Miete wird an einen Verbraucherpreisindex (z.B. VPI) gekoppelt.
Bei Änderung des Index kann die Miete angepasst werden. Vorteil: planbare Entwicklung
für beide Seiten. Die Anpassung erfolgt in der Regel jährlich. Der Vertrag muss die
Indexbindung klar benennen.

**Staffelmiete:** Feste Erhöhungen werden vertraglich für mehrere Jahre festgelegt.
Typisch sind 2–3 Prozent pro Jahr über z.B. fünf Jahre. Nach Ablauf der Staffel
endet die Bindung; weitere Erhöhungen nur nach § 558 BGB. Beide Modelle müssen
bei Vertragsschluss vereinbart werden und sind für Bestandsmieten nicht nachträglich
einführbar.
"""  # noqa: E501

