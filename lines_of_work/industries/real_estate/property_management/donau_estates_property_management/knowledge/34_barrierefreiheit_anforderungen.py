title = "Barrierefreiheit und Anforderungen"

content = """
Barrierefreie Wohnungen erfüllen Anforderungen für Menschen mit Einschränkungen:
breitere Türen, bodengleiche Duschen, Griffe, ausreichend Bewegungsfläche.
Bei Neubau und größeren Umbauten gelten landesrechtliche Vorgaben (z.B. LBO).
Eigentümer können Förderungen für barrierereduzierende Maßnahmen nutzen.
Bei Bestandsimmobilien besteht kein allgemeiner Umbauzwang; bei Nachfrage
sollte die Machbarkeit geprüft werden. Dokumentation von bereits vorhandenen
barrierefreien Einheiten unterstützt die Vermarktung und erfüllt soziale
Verantwortung.
"""  # noqa: E501

