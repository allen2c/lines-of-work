title = "Mieterinsolvenz"

content = """
Bei Insolvenz des Mieters prüft die Verwaltung, ob Mietforderungen angemeldet
werden müssen. Die Miete bleibt grundsätzlich fällig; der Insolvenzverwalter
kann den Vertrag gemäß § 108 InsO kündigen. Die Kaution kann zur Befriedigung
herangezogen werden. Die Verwaltung dokumentiert alle Schritte, meldet Forderungen
fristgerecht und koordiniert mit Eigentümer und ggf. Rechtsabteilung. Schnelles
Handeln sichert Ansprüche.
"""  # noqa: E501

