"""Knowledge item: Mietminderung Recht."""

title = "Mietminderung und Mängel"

content = """
Bei erheblichen Mängeln der Mietsache hat der Mieter ein gesetzliches Recht auf Mietminderung
(§ 536 BGB). Der Umfang richtet sich nach der Schwere des Mangels; Faustregeln und
Mietminderungstabellen dienen als Orientierung. Der Mieter muss den Mangel anzeigen und dem
Vermieter Gelegenheit zur Beseitigung geben. Unberechtigte Mietminderung kann zur Kündigung
wegen Zahlungsverzugs führen. Dokumentation aller Mängelanzeigen und Fristen ist zentral.
"""  # noqa: E501

