title = "Schufa-Auskunft und Bonitätsprüfung"

content = """
Die Schufa-Auskunft gibt Auskunft über die Kreditwürdigkeit eines Bewerbers.
Vermieter können eine Auskunft anfordern, der Bewerber muss zustimmen. Wichtige
Indikatoren: negative Einträge, offene Forderungen, Zahlungsverzug. Eine
pauschale Ablehnung allein wegen Schufa-Einträgen kann rechtlich problematisch
sein; Einzelfallprüfung ist geboten. Alternative oder ergänzend: Gehaltsabrechnung,
Arbeitsvertrag, Auskunft des vorherigen Vermieters. Daten müssen DSGVO-konform
verarbeitet und nach Abschluss des Verfahrens gelöscht werden.
"""  # noqa: E501

