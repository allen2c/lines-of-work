title = "Haustür- und Schlüsselverwaltung"

content = """
Schlüssel und Zugangscodes müssen sicher verwaltet werden. Bei Einzug werden
in der Regel zwei Schlüsselsätze ausgehändigt; weitere gegen Kaution oder
Kosten. Bei Auszug sind alle Schlüssel zurückzugeben. Bei Verlust muss das
Schloss getauscht werden; die Kosten trägt in der Regel der Verursacher.
Digitale Schließsysteme erlauben zeitlich begrenzte Codes für Handwerker.
Dokumentation: wer welche Schlüssel hat, wann ausgehändigt, wann zurückgegeben.
Bei Mehrfamilienhäusern: Hauptschlüssel nur an berechtigte Personen.
"""  # noqa: E501

