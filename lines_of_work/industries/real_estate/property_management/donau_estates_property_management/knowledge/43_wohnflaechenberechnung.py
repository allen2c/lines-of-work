title = "Wohnflächenberechnung"

content = """
Die Wohnfläche beeinflusst Miete, Nebenkostenumlage und Mietspiegel. Maßgeblich
ist die Wohnflächenverordnung (WoFlV) bzw. DIN 277. Voll angerechnet werden
Räume mit mindestens 2 m Höhe; Flächen unter 1 m Höhe zählen nicht. Balkone,
Terrassen und Dachterrassen werden mit 25–50 % angerechnet. Bei Abweichungen
zwischen Vertrag und tatsächlicher Fläche kann der Mieter Mietminderung oder
Nebenkostenerstattung verlangen. Regelmäßige Überprüfung bei Übergabe und bei
Abrechnungsstreitigkeiten empfohlen.
"""  # noqa: E501

