"""Knowledge item: Wohnungswechsel bei bestehendem Mieter."""

title = "Wohnungswechsel bei bestehendem Mieter"

content = """
Ein Wohnungswechsel innerhalb desselben Objekts oder Portfolios erfordert klare Absprachen.
Der alte Mietvertrag wird gekündigt, der neue geschlossen. Übergabeprotokoll für die alte
Wohnung und Einzugprotokoll für die neue sind erforderlich. Kaution kann bei gleichem
Vermieter übertragen werden. Mietdauer und Kündigungsschutz beginnen neu.
"""  # noqa: E501

