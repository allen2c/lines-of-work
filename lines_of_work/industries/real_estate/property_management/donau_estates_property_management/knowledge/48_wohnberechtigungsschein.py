"""Knowledge item: Wohnberechtigungsschein."""

title = "Wohnberechtigungsschein (WBS)"

content = """
Der Wohnberechtigungsschein berechtigt zur Anmietung von gefördertem Wohnraum.
Er wird von der Kommune ausgestellt, wenn Einkommens- und Wohnraumbedarfskriterien
erfüllt sind. Vermieter von Sozialwohnungen müssen bei Neuvermietung einen
gültigen WBS verlangen. Bei Verlust des WBS während der Mietzeit oder bei
Förderungsende kann die Miete angepasst werden. Der Verwalter muss die
Gültigkeit prüfen und mit der fördernden Stelle kommunizieren.
"""  # noqa: E501

