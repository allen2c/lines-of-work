title = "Wohnungsbesichtigung"

content = """
Besichtigungen dienen der Vermietung oder der Kontrolle des Zustands. Der
Vermieter hat ein Recht auf Besichtigung, muss aber angemessene Termine
anbieten und den Mieter rechtzeitig informieren (§ 535 BGB). Üblich sind
Termine mit 14 Tagen Vorlauf. Bei Verdacht auf Vertragsverletzung (z.B.
unerlaubte Untervermietung) kann eine Besichtigung angeordnet werden.
Während der Besichtigung: Zustand dokumentieren, Fotos mit Einwilligung.
Bei Vermietung: Interessenten sachlich über Objekt und Umgebung informieren,
keine diskriminierenden Kriterien anwenden.
"""  # noqa: E501

