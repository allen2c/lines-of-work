"""Knowledge item: Wohnungsgeberbestätigung."""

title = "Wohnungsgeberbestätigung"

content = """
Die Wohnungsgeberbestätigung ist eine Meldebestätigung, die der Vermieter
dem Mieter bei Einzug ausstellt. Sie bestätigt den Einzug in die Wohnung
und ist für die Anmeldung beim Einwohnermeldeamt erforderlich. Ohne sie
kann der Mieter sich nicht ordnungsgemäß anmelden. Die Bestätigung muss
Name und Adresse des Mieters, Einzugsdatum und Unterschrift des Vermieters
enthalten. Bei Verweigerung ohne Grund kann der Mieter Schadensersatz
verlangen. Die Ausstellung erfolgt in der Regel bei Schlüsselübergabe.
"""  # noqa: E501

