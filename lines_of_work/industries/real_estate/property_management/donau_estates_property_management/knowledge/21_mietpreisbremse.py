title = "Mietpreisbremse"

content = """
Die Mietpreisbremse (§ 556d BGB) begrenzt die Mieterhöhung bei Neuvermietung in
Gebieten mit angespanntem Wohnungsmarkt. Die ortsübliche Vergleichsmiete darf
nicht um mehr als 10 Prozent überschritten werden. Die Länder bestimmen die
betroffenen Gebiete per Rechtsverordnung. Bei Verstößen kann der Mieter die
Überzahlung zurückfordern. Die Verwaltung muss vor Neuvermietung prüfen, ob die
Mietpreisbremse gilt, und die ortsübliche Vergleichsmiete ermitteln (z.B. über
den Mietspiegel oder Gutachten). Bestehende Verträge sind nicht rückwirkend
betroffen.
"""  # noqa: E501

