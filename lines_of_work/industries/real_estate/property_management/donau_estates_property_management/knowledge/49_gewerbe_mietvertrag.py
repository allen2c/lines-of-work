title = "Gewerbemietvertrag — Besonderheiten"

content = """
Gewerbemieten unterliegen nicht dem Wohnraummietrecht; es gilt das allgemeine
Mietrecht (§§ 535 ff. BGB) mit kürzeren Kündigungsfristen und mehr
Vertragsfreiheit. Typische Klauseln: Indexmiete, Staffelmiete, Umbau- und
Nutzungsrechte, Konkurrenzschutz. Bei Insolvenz des Mieters hat der Vermieter
Sonderrechte (z.B. Kündigung ohne Wartezeit unter Umständen). Gewerbeobjekte
erfordern oft höheren Koordinationsaufwand (Lüftung, Brandschutz, Barrierefreiheit).
Verträge sollten mit Anwalt geprüft werden.
"""  # noqa: E501

