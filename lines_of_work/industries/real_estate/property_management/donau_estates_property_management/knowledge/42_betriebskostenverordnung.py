content = """
Die Betriebskostenverordnung (BetrKV) listet die umlagefähigen Nebenkosten auf.
Nur dort genannte Positionen dürfen auf Mieter umgelegt werden. Typische
Kategorien: Grundsteuer, Wasserversorgung, Entwässerung, Heizung, Aufzug,
Straßenreinigung, Gartenpflege, Beleuchtung, Schädlingsbekämpfung,
Gebäudeversicherung, Gemeinschaftsantenne, Müllabfuhr. Nicht umlagefähig sind
z.B. Verwaltungskosten des Vermieters, Darlehenszinsen, Instandhaltungsrücklagen.
Die Abrechnung muss nachvollziehbar und mit Belegen versehen sein.
"""  # noqa: E501

