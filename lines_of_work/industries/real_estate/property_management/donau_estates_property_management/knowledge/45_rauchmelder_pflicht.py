"""Knowledge item: Rauchmelderpflicht."""

title = "Rauchmelderpflicht"

content = """
In den meisten Bundesländern sind Rauchmelder in Wohnungen Pflicht. Typischerweise
in Schlafzimmern, Kinderzimmern und Fluren. Die Installation obliegt dem Vermieter,
Wartung und Batteriewechsel oft dem Mieter (je nach Landesrecht und Vertrag).
Bei Nichtvorhandensein oder Defekt kann der Mieter Mietminderung geltend machen.
Der Verwalter muss Bestand und Funktionsfähigkeit dokumentieren und bei
Mängeln umgehend Abhilfe schaffen. Prüfintervalle und Verantwortlichkeiten
sollten im Mietvertrag geregelt sein.
"""  # noqa: E501

