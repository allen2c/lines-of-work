"""Knowledge item 81: Zählerstandsablesung."""

title = "Zählerstandsablesung"

content = """
Zählerstände (Strom, Gas, Wasser, Wärme) müssen bei Ein- und Auszug sowie für die
Nebenkostenabrechnung erfasst werden. Ablesung durch Mieter, Hausmeister oder
Energieversorger. Bei digitalen Zählern können Fernablesungen möglich sein. Die
Verwaltung muss Ablesetermine koordinieren und Werte dokumentieren. Fehlerhafte
Ablesungen führen zu falschen Abrechnungen und Nachforderungen.
"""  # noqa: E501

