"""Knowledge item 82: Zweckentfremdung von Wohnraum."""

title = "Zweckentfremdung von Wohnraum"

content = """
In vielen Städten ist die Zweckentfremdung von Wohnraum (z.B. dauerhafte Ferienvermietung,
Leerstand, gewerbliche Nutzung ohne Genehmigung) verboten oder genehmigungspflichtig.
Verstöße können Bußgelder und Zwangsvermietung nach sich ziehen. Die Verwaltung muss
Nutzungsänderungen prüfen und bei Verdacht melden. Untervermietung und Airbnb-Nutzung
sind oft eingeschränkt.
"""  # noqa: E501

