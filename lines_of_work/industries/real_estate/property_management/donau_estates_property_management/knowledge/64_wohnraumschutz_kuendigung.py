"""Knowledge item: Wohnraumschutz Kündigung."""

title = "Wohnraumschutz und Kündigungsbeschränkungen"

content = """
Das Wohnraumschutzgesetz schränkt die Kündigung von Wohnraum ein. Eigenbedarf, Verwertung
und sonstige Gründe unterliegen engen Fristen und Nachweispflichten. Bei Mietern mit
besonderem Kündigungsschutz (z.B. langjährige Mietdauer, Alter, Behinderung) gelten
verlängerte Kündigungsfristen. Die ordentliche Kündigung durch den Vermieter ist bei
unbefristeten Verträgen nur unter bestimmten Voraussetzungen zulässig.
"""  # noqa: E501

