"""Knowledge item: Wohnungsverwaltung bei Genossenschaften."""

title = "Wohnungsverwaltung bei Genossenschaften"

content = """
Genossenschaftswohnungen unterliegen besonderen Regeln. Mitglieder haben Nutzungsrechte,
keine klassischen Mietverträge. Die Verwaltung koordiniert mit dem Vorstand, verwaltet
Genossenschaftsanteile und Wohnberechtigungen. Kündigungen und Nachfolgeregelungen
folgen der Satzung. Die Abrechnung von Umlagen und Instandhaltung kann von der
Standard-Immobilienverwaltung abweichen.
"""  # noqa: E501

