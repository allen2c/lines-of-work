"""Knowledge item 77: Wohnungsqualität und Standards."""

title = "Wohnungsqualität und Standards"

content = """
Die Wohnungsqualität umfasst bauliche, hygienische und sicherheitstechnische Anforderungen.
Mindeststandards ergeben sich aus Landesbauordnung, Mietrecht und Wohnraumförderung. Zu prüfen
sind: Heizung, Belüftung, Schallschutz, Feuchtigkeit, Brandschutz, Barrierefreiheit. Bei
Mängeln hat der Mieter Rechte auf Mietminderung oder Instandsetzung. Die Verwaltung muss
regelmäßig den Zustand dokumentieren und Sanierungsbedarf melden.
"""  # noqa: E501

