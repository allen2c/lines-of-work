content = """
- 事前準備：対象物件の地図、公図、周辺の用途地域図を印刷。調査チェックリスト（外観・周辺環境・交通・駐車場等）を用意。
- 現地到着後、まず物件の外観を全方位から写真撮影（4方向以上）。周辺道路の幅員、前面道路の種別（公道・私道）を確認。
- 建物の場合：構造（RC・S・木造等）、階数、屋根材、外壁材、経年劣化状況（ひび割れ、錆、雨漏り跡）を記録。共用部（エントランス、廊下、エレベーター）も確認。
- 土地の場合：形状（整形・不整形）、高低差、接道状況、境界標の有無、隣地との境界確定状況を確認。日当たり、眺望も重要。
- 周辺環境：最寄り駅までの距離（徒歩分数）、バス停、スーパー、病院、学校等の有無。騒音・臭気・治安等のマイナス要因も記録。
"""

