content = """
- 住宅估价优先采用市场比较法。可比实例应选取同小区或邻近小区、相同户型（如三室两厅）、相似楼层（高层、中层、低层）、相似装修标准。
- 楼层修正：多层（6层以下）一般中间层最高，顶层和底层较低，修正幅度-5%~+5%；高层（7层以上）中间偏高，顶层和底层较低，修正幅度-3%~+8%。
- 朝向修正：南北通透最优，朝南次之，朝东/西再次，朝北最差，修正幅度-2%~+5%。
- 景观修正：有江景、湖景、公园景观可上浮5%-15%，有噪音源（高架、铁路）下浮5%-10%。
- 装修修正：精装比毛坯高10%-20%，简装高5%-10%，需根据实际成新度调整。
"""

