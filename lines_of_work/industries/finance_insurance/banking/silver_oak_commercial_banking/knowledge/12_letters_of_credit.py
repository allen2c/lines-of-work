title = "Letters of Credit: Standby and Commercial"

content = """
A Letter of Credit (LC) is a guarantee from Silver Oak Bank that a client's
payment to a third party will be received on time and for the correct amount. If
the client is unable to pay, the bank is required to cover the full or remaining
amount of the purchase.

**Types of Letters of Credit:**
1. **Commercial LC:** Used primarily in international trade. The bank pays the
   seller (beneficiary) upon presentation of shipping documents (e.g., Bill of
   Lading) that prove the goods were sent.
2. **Standby LC (SBLC):** Acts as a 'safety net.' The bank only pays if the
   client fails to perform on a contract (e.g., failing to pay rent or complete a
   construction project).

**Risk and Fees:**
LCs are 'Off-Balance Sheet' obligations for the bank until they are drawn upon.
However, they consume the client's credit capacity just like a loan. The bank
charges an issuance fee (typically 1-3% per annum) based on the risk and the
amount of the guarantee.
"""

