title = "Working Capital Financing: Revolving Lines of Credit"

content = """
A Revolving Line of Credit (RLOC) is a flexible financing tool designed to bridge
the gap between a business's operating expenses and its collection of accounts
receivable. Unlike a term loan, an RLOC allows the borrower to draw, repay, and
re-borrow funds as needed.

**Key Features:**
- **Borrowing Base:** The maximum amount available is often tied to a percentage
  of eligible assets (e.g., 80% of accounts receivable under 90 days, 50% of
  finished goods inventory).
- **Interest Only:** Typically, borrowers pay interest monthly on the outstanding
  balance, with the principal due at maturity (usually 12-24 months, subject to
  annual renewal).
- **Clean-up Clause:** Some agreements require the balance to be reduced to zero
  for a specific period (e.g., 30 consecutive days) each year to prove the
  facility is truly for short-term working capital.

**Usage:**
Appropriate uses include purchasing inventory, funding payroll during seasonal
lulls, or taking advantage of early-payment discounts from suppliers. It should
not be used for long-term asset purchases like machinery or real estate.
"""

