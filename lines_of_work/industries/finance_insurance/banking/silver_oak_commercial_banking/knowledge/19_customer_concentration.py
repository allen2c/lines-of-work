title = "Customer Concentration Risk"

content = """
Customer concentration risk occurs when a significant portion of a borrower's
revenue is derived from a single customer or a small group of customers. At
Silver Oak Bank, we view concentrations exceeding 20-25% of total revenue as a
major risk factor.

**The Danger of Concentration:**
If the borrower's major customer goes bankrupt, terminates the contract, or
demands significant price concessions, the borrower's cash flow could collapse,
leading to a loan default.

**Mitigating the Risk:**
- **Accounts Receivable Aging:** We closely monitor the payment history of the
  major customer.
- **Contract Review:** We analyze the terms of the contracts with major
  customers, looking for 'Termination for Convenience' clauses.
- **Borrowing Base Adjustments:** The bank may 'cap' the amount of eligible
  receivables from a single customer (e.g., no more than 25% of the total
  borrowing base can come from Customer X).
- **Secondary Sources:** We place higher emphasis on personal guarantees and
  tangible collateral when high concentration exists.
"""

