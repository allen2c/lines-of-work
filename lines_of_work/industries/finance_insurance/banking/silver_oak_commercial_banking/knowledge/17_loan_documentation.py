title = "Loan Documentation and Closing Procedures"

content = """
The closing process is the final step in extending credit, where the bank's
approved terms are translated into legally binding documents. Precision in
documentation is vital to ensure the bank's interests are protected.

**Core Loan Documents:**
1. **Promissory Note:** The borrower's legal promise to repay the debt,
   specifying the amount, interest rate, and repayment schedule.
2. **Loan Agreement:** The master document containing all terms, conditions,
   covenants, and representations.
3. **Security Agreement:** Grants the bank a security interest in the business
   assets (collateral).
4. **Guaranty Agreement:** Executed by the principals to personally guarantee
   the debt.

**Pre-Closing Checklist:**
Before funding, the bank must confirm:
- **Insurance:** Evidence of adequate property, liability, and (if applicable)
  flood insurance with the bank listed as 'Loss Payee' or 'Mortgagee.'
- **Good Standing:** A certificate from the Secretary of State confirming the
  borrower is a validly existing entity.
- **Authority:** Corporate resolutions authorizing the signers to bind the
  company to the debt.
"""

