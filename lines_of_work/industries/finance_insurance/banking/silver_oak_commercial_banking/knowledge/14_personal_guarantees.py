title = "Personal Guarantees and Principal Support"

content = """
In middle-market commercial banking, Silver Oak Bank often requires the owners
(principals) of a business to provide a Personal Guarantee (PG). This aligns the
interests of the owners with the bank and provides an additional layer of
repayment.

**Types of Guarantees:**
1. **Unlimited Personal Guarantee:** The guarantor is liable for the full amount
   of the business's debt.
2. **Limited Guarantee:** Liability is capped at a specific dollar amount or
   percentage of the loan.
3. **Validity Guarantee ('Bad Boy' Acts):** Only triggered if the borrower
   commits fraud, misrepresentation, or other specific 'bad acts.'

**Personal Financial Statements (PFS):**
Guarantors must provide an annual PFS and tax returns so the bank can assess their
'Global Cash Flow' and net worth. A guarantee is only as strong as the
guarantor's liquid assets outside of the business being financed.
"""

