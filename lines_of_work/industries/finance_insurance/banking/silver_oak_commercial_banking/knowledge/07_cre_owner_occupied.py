content = """
Owner-occupied Commercial Real Estate (CRE) loans are extended to businesses that
occupy at least 51% of the property being financed. These loans are viewed
favorably by Silver Oak Bank as the business's operations provide the primary
repayment source.

**Underwriting Criteria:**
- **Loan-to-Value (LTV):** Typically capped at 75-80% of the appraised value.
- **Debt Service Coverage:** The property's Net Operating Income (NOI) or the
  business's global cash flow must meet the 1.25x DSCR threshold.
- **Appraisal and Environmental:** A professional MAI appraisal and a Phase I
  Environmental Site Assessment (ESA) are mandatory to mitigate collateral and
  liability risks.

**Benefits to the Borrower:**
- **Equity Building:** Instead of paying rent to a third party, the business
  builds equity in a tangible asset.
- **Tax Advantages:** Potential deductions for mortgage interest and depreciation
  (consult a tax professional).
- **Stability:** Protection against rising rents and the risk of lease non-renewal.
"""

