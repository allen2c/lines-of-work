content = """
As a regulated financial institution, Silver Oak Bank must adhere to strict Anti-
Money Laundering (AML) and Know Your Customer (KYC) regulations, primarily driven
by the Bank Secrecy Act (BSA) and the USA PATRIOT Act.

**KYC Requirements:**
Before onboarding a new commercial client, we must verify:
- **Identity of Beneficial Owners:** Any individual owning 25% or more of the
  entity must provide government-issued ID and personal details.
- **Business Legitimacy:** Review of Articles of Incorporation, Operating
  Agreements, and proof of physical address.
- **Nature of Business:** Understanding the source of funds and the expected
  transactional activity.

**Ongoing Monitoring:**
We utilize automated systems to flag 'Suspicious Activity,' such as large,
unexplained cash deposits or frequent transfers to high-risk jurisdictions. If
activity deviates significantly from the client's established profile, a
Suspicious Activity Report (SAR) may be filed with FinCEN.

Failure to maintain robust AML/KYC programs can result in massive regulatory
fines and irreparable reputational damage to the bank.
"""

