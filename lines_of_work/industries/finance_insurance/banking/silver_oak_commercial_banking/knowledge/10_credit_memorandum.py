content = """
The Credit Memorandum (or 'Credit Memo') is the definitive internal document used
to propose and approve a commercial loan. It synthesizes all analysis into a
compelling narrative for the Credit Committee.

**Key Sections of a Credit Memo:**
1. **Executive Summary:** The 'Bottom Line'—who is the borrower, how much are they
   asking for, and what is the proposed structure?
2. **Business Description:** Overview of the company's history, products,
   markets, and competitive advantages.
3. **Financial Analysis:** Detailed review of historical performance, including
   trend analysis of margins, liquidity, and leverage.
4. **Collateral Assessment:** Valuation of the assets securing the loan and the
   bank's lien position (e.g., 1st Deed of Trust, UCC-1 on all assets).
5. **Risk Factors and Mitigants:** Honest identification of what could go wrong
   (e.g., 'Customer Concentration') and how the bank is protected (e.g., 'Personal
   Guarantees from high-net-worth principals').

A well-written memo is objective, data-driven, and anticipates the questions a
skeptical Credit Committee might ask.
"""

