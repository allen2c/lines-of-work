content = """
Commercial credit analysis is the systematic evaluation of a business's ability to
repay its debt obligations. At Silver Oak Bank, we focus on the 'Five Cs of Credit'
to ensure a holistic assessment of risk.

**Character:** The borrower's reputation, experience, and integrity. We review
management's track record and industry standing.

**Capacity:** The primary source of repayment, typically cash flow from operations.
We analyze historical and projected EBITDA (Earnings Before Interest, Taxes,
Depreciation, and Amortization) and Debt Service Coverage Ratios (DSCR).

**Capital:** The owner's 'skin in the game.' We look for a healthy debt-to-equity
ratio, ensuring the business is not over-leveraged.

**Collateral:** Secondary sources of repayment. This includes business assets like
accounts receivable, inventory, equipment, and real estate. We apply appropriate
advance rates (e.g., 80% on eligible AR, 50% on inventory) to determine the
borrowing base.

**Conditions:** External factors such as industry trends, economic environment, and
regulatory changes that may impact the borrower's performance.
"""

