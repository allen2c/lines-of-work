content = """
Silvergate Community Bank offers a range of lending products to meet personal
and business financing needs. While loan origination is handled by specialized
officers, customer service plays a critical role in initial education and
referral.

**Mortgage Loans:** Fixed-rate mortgages available in 15-year and 30-year terms
with competitive rates. Adjustable-rate mortgages (ARMs) offer lower initial
rates with periodic adjustments. Minimum down payment 5% for qualified buyers;
PMI required below 20% equity. First-time homebuyer programs available with
reduced down payment requirements. Pre-approval letters issued within 48 hours
of complete application.

**Home Equity Products:** Home Equity Loans provide lump-sum financing with
fixed rates and terms from 5-15 years. Home Equity Lines of Credit (HELOC)
offer revolving access with variable rates tied to prime rate. Both require
sufficient home equity (typically 80% loan-to-value maximum combined). Interest
may be tax-deductible when used for home improvements — customers should
consult tax advisors.

**Personal Loans:** Unsecured installment loans for debt consolidation, major
purchases, or unexpected expenses. Amounts from $2,500 to $25,000 with terms
of 12-60 months. Rates based on creditworthiness, with preferred rates for
auto-debit from Silvergate checking accounts. No collateral required but
strong credit history expected.

**Auto Loans:** Financing for new and used vehicle purchases. New car rates
as low as 4.49% APR; used car rates vary by vehicle age. Terms from 36-72
months. Pre-approval available to strengthen customer's negotiating position at
dealerships. Auto-debit discount available.

**Business Term Loans:** Fixed-rate financing for equipment purchases, expansion,
or working capital. Amounts from $10,000 to $250,000. Terms from 2-7 years
matched to asset life. Requires business financial statements and personal
guarantee from owners of 20% or more.

**Business Lines of Credit:** Revolving credit for managing cash flow, inventory
purchases, or seasonal fluctuations. Credit limits from $10,000 to $100,000.
Interest-only payments during draw period. Annual review and renewal required.

**Small Business Administration (SBA) Loans:** Government-guaranteed loans for
small businesses that may not qualify for conventional financing. Longer terms and
lower down payments than conventional loans. Requires additional documentation
and longer processing times. Silvergate is a preferred SBA lender.

**Referral Protocol:** When customers express interest in loans, gather initial
information about loan purpose, requested amount, and timeline. Explain basic
product features and direct to appropriate Loan Officer. Provide direct contact
information and request Loan Officer follow up within 24 hours. Never quote
specific rates or approval likelihood — these require formal underwriting.
"""  # noqa: E501

