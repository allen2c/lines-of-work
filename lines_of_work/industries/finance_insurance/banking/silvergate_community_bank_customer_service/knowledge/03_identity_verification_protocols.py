title = "Identity Verification Protocols"

content = """
Protecting customer accounts begins with rigorous identity verification. Whether
a customer visits the branch, calls the service center, or uses digital channels,
establishing identity is the first and most critical security step.

**In-Person Verification:** When a customer visits a branch, request primary
government-issued photo ID. Verify the photo matches the person presenting it.
Check that the ID is current and not expired. For address verification, confirm
the physical address on ID matches account records or documentation provided.

**Telephone Authentication:** Before discussing account-specific information,
authenticate callers using a multi-factor approach. Verify full Social Security
Number or Tax ID, account number, and at least one recent transaction detail or
account balance. For sensitive transactions, require additional verification
through security questions established at account opening.

**Digital Banking Access:** New device registrations trigger multi-factor
authentication. Customers receive one-time passcodes via SMS or email. If a
customer cannot access their registered device or email, verify identity through
out-of-wallet questions or require branch visit with photo ID for high-risk
requests like password resets or wire transfers.

**Suspicious Indicator Recognition:** Be alert for red flags: reluctance to provide
ID, presentation of damaged or altered documents, inconsistencies between
documents and stated information, nervous behavior, or urgency that seems
artificial. When doubts arise, involve a supervisor without alarming the customer.

**Third-Party Access:** When someone other than the account holder requests
information or transactions, verify the third party's authority. Require
proper documentation: power of attorney, court orders, or authorized signer
status. Never disclose account information to spouses, family members, or
business partners without verified authorization on file.

**Documentation Standards:** Record verification details in customer notes
including ID type and expiration date, without storing full ID numbers. Note any
verification challenges or exceptions granted. Consistent documentation supports
fraud investigations and compliance audits.
"""  # noqa: E501

