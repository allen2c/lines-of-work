title = "Fee Structure and Policies"

content = """
Transparent fee communication builds trust and helps customers avoid
unexpected charges. Understanding fee structures enables representatives to
explain costs, recommend fee avoidance strategies, and make appropriate
waivers when warranted.

**Monthly Maintenance Fees:** Essential Checking: $0 with eStatements or $8
without. Premier Checking: $12 unless $1,500 minimum daily balance or $5,000
combined deposits maintained. Silver Savings: $5 unless $300 minimum balance
or automatic transfer. Money Market: $15 unless $2,500 minimum balance.

**Overdraft Fees:** Standard overdraft fee is $35 per item, maximum 4 fees
daily. No fee if overdrawn less than $5. Extended overdraft fee of $7 per day
applies after 5 consecutive overdrawn days. Courtesy overdraft coverage requires
opt-in for ATM and debit transactions; checks and ACH always covered if account
is in good standing.

**Stop Payment Fees:** $30 for Essential Checking customers; waived for Premier
Checking. Fee applies per stop payment request regardless of number of checks
covered. Stop payments remain active for 6 months and can be renewed with
additional fee.

**Cashier's Check Fees:** $10 for Essential Checking; free for Premier. Fee
waived for all customers when the check is required to repair a bank error or
for documented emergency situations.

**Wire Transfer Fees:** Domestic outgoing wires: $25 online, $35 in-branch.
Domestic incoming: $15. International outgoing: $45 online, $50 in-branch.
International incoming: $20. Business accounts have separate wire fee schedules.

**Excess Transaction Fees:** Savings and Money Market accounts limited to 6
convenient withdrawals per statement cycle under Regulation D. Each additional
withdrawal incurs $15 fee. ATM and branch withdrawals are not subject to this
limit. Fee applies per item over limit.

**Foreign Transaction Fees:** Debit card purchases outside the United States
incur 3% foreign transaction fee. ATM withdrawals internationally incur $5 per
transaction plus 3% currency conversion. These fees help cover international
processing costs and currency exchange risk.

**Fee Waiver Authority:** Customer Service Representatives may waive fees up
to $100 per customer per month for documented service failures, first-time
occurrences, or customers with long relationships and clean history. Waivers
over $100 require Branch Manager approval. Document waiver reasons in customer
notes for consistency and audit trail.
"""  # noqa: E501

