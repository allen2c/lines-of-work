title = "Fraud Prevention and Awareness"

content = """
Fraud prevention is a shared responsibility between the bank and customers.
Educating customers about common schemes and protective measures reduces losses
and strengthens the banking relationship.

**Common Fraud Types:**
- **Phishing:** Fraudulent emails or texts impersonating the bank to steal
  login credentials. Often contain urgent language and suspicious links.
- **Vishing:** Voice calls claiming to be from the bank fraud department
  requesting verification information.
- **Card Skimming:** Devices attached to ATMs or gas pumps capturing card data.
- **Check Fraud:** Counterfeit, forged, or altered checks.
- **Wire Fraud:** Social engineering schemes tricking customers into sending
  funds to fraudulent recipients.
- **Account Takeover:** Criminals gaining access to online banking to change
  contact information and drain funds.

**Red Flags for Representatives:** Be alert when customers mention: urgent
requests from "bank security," instructions to keep transactions secret,
unusual wire instructions, or pressure to act immediately. These may indicate
the customer is being victimized in real-time.

**Prevention Education:** Advise customers to: never click links in unsolicited
emails claiming to be from the bank; verify caller identity by hanging up and
calling the bank directly using the number on their card; cover the PIN pad
when entering numbers; review account statements promptly; and set up account
alerts for immediate transaction notification.

**Elder Financial Abuse:** Older customers are frequent targets of financial
exploitation. Indicators include new "best friends" accompanying transactions,
reluctance to discuss finances, unexplained account activity, or sudden
changes to account ownership or beneficiaries. Report suspicions to Adult
Protective Services and escalate to Compliance.

**Business Email Compromise:** Business customers should be warned about schemes
where fraudsters impersonate executives or vendors to initiate fraudulent wires.
Recommend verification protocols: confirm wire instructions via phone using
known numbers, not email replies; establish dual authorization for large wires;
and be suspicious of urgent requests outside normal patterns.

**When to Escalate to Fraud Department:** Immediately refer any suspected
fraud case involving identity theft, account takeover, unauthorized wire
transfers, or when the customer is actively communicating with a suspected
fraudster. Do not delay — fraud investigations are time-sensitive.

**Fraud Claim Process:** When fraud is confirmed, the Fraud Department handles
the investigation rather than Customer Service. Customer Service's role is
initial documentation, account protection (card blocks, password resets), and
warm handoff to Fraud specialists. Provide the customer with the Fraud
department's direct number and expected contact timeframe.
"""  # noqa: E501

