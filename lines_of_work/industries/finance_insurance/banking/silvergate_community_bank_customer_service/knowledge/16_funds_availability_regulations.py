title = "Funds Availability Regulations"

content = """
Federal Regulation CC governs when deposited funds must be made available
to customers. Understanding these rules enables accurate communication about
hold periods and exceptions that permit longer delays.

**Standard Availability Schedule:**
- **Cash Deposits:** Available immediately
- **Wire Transfers:** Available immediately
- **ACH Credits:** Available by opening of business on the next business day
- **Checks:** $225 available next business day; remainder according to type

**Check Hold Categories:**
- **Local Checks:** Checks drawn on banks within the same Federal Reserve
check processing region — first $225 next business day, remaining funds
second business day.
- **Non-Local Checks:** Checks drawn on distant banks — first $225 next
business day, remaining funds fifth business day.

**Next-Day Availability Items:** The following deposits must be available next
business day: cashier's checks, certified checks, government checks, and
on-us checks (drawn on Silvergate). These high-certainty items carry minimal
risk.

**Exception Holds:** Under specific circumstances, Silvergate may extend
holds beyond the standard schedule. Exception holds require specific case
reasons:
- **New Account:** First 30 days after account opening: up to 9 business days
for non-next-day items.
- **Large Deposit:** Deposits exceeding $5,525 in a single day: up to 7
business days for amount over $5,525.
- **Redeposited Check:** Checks returned unpaid and redeposited: up to 7
business days.
- **Reasonable Doubt:** Reasonable cause to doubt collectibility: up to 7
business days.
- **Emergency Conditions:** Computer failures, natural disasters: extends
all holds.

**Hold Notice Requirements:** When placing a hold, provide written notice at
the time of deposit if possible, or by next business day. Notice must state
the amount held, when funds will be available, and the reason for the hold.
Exception holds require specific language about the exception invoked.

**Mobile Deposit Availability:** Mobile check deposits follow standard
availability rules with an additional $225 available next business day as a
bonus for using remote deposit. Large deposits or new accounts may trigger
extended holds.

**Customer Communication:** Explain holds as risk management practices, not
personal judgments. Emphasize that holds protect all customers by ensuring
checks clear before funds are spent. For customers with urgent needs,
suggest alternative deposit methods like wire transfers or cashier's checks
that have immediate availability.

**ATM Deposits:** Night drop and ATM deposits before cutoff time (varies by
location) receive same-day credit with standard availability schedules.
Deposits after cutoff are credited next business day.
"""  # noqa: E501

