title = "Wire Transfer Procedures"

content = """
Wire transfers move funds quickly between financial institutions domestically
and internationally. Due to their speed and finality, wire transfers require
rigorous verification and security protocols.

**Domestic Wires (Fedwire):** Same-day transfer of funds between U.S. financial
institutions. Cutoff time for same-day processing: 3:00 PM ET for online wires,
4:00 PM ET for branch wires. Require recipient's full name, account number,
bank name, and ABA routing number. Domestic wires typically settle within
minutes to hours.

**International Wires (SWIFT):** Cross-border transfers processed through the
SWIFT network. Require beneficiary name and address, account number (IBAN for
many countries), SWIFT/BIC code for the receiving bank, and intermediary bank
information if required. International wires take 1-5 business days depending
on destination and currency.

**Verification Requirements:** Before processing any outgoing wire, verify
customer identity through standard authentication. Confirm wire details by reading
them back to the customer. For large wires or new payees, ask purpose of wire
and relationship to beneficiary. Document customer's verbal confirmation.

**Online Wire Limits:** Personal accounts: $25,000 per wire, $50,000 daily limit.
Business accounts: limits vary by relationship and account type. Customers may
request temporary limit increases with advance notice and additional verification.

**Wire Amendment and Cancellation:** Domestic wires may be cancelled if the
request is received before cutoff and the wire has not yet been processed.
After processing, recall requests can be submitted but success depends on
recipient bank cooperation. International wires are generally irreversible
once sent. Always emphasize this finality with customers.

**Incoming Wire Instructions:** Provide customers with incoming wire instructions
including Silvergate's ABA routing number (011000015) and their full account
number. International wires additionally require Silvergate's SWIFT code
(SLVGUS33) and beneficiary address. Confirm customers understand that sender
must initiate the wire from their institution.

**Red Flags for Wire Fraud:** Be suspicious of: wires to new recipients with
urgent timing; requests to keep wire secret from family or colleagues; changes
to previously provided wire instructions via email; wires to countries with high
fraud risk; and customers who seem coached or confused about wire purpose. When
red flags present, escalate to supervisor before processing.

**Fees:** Domestic outgoing: $25 online, $35 branch. International outgoing:
$45 online, $50 branch. Incoming wires: $15 domestic, $20 international.
Fees are non-refundable even if wire is cancelled or recalled.
"""  # noqa: E501

