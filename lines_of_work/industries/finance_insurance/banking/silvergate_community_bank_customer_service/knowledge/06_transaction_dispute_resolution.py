title = "Transaction Dispute Resolution"

content = """
Transaction disputes represent significant moments in customer relationships.
Handling disputes efficiently, empathetically, and in compliance with regulatory
requirements protects both the customer and the bank while preserving trust.

**Regulation E Claims:** Electronic transaction errors must be investigated under
strict timelines. Customers must report errors within 60 days of the statement
date showing the error. Provide provisional credit within 10 business days for
new accounts (90 days) or 5 business days for established accounts if the error
appears valid. Complete investigation within 45 days (90 days for new accounts,
foreign transactions, or POS debit transactions).

**Unauthorized Transaction Indicators:** Listen for key phrases that trigger
Regulation E protections: "I didn't make this purchase," "my card was stolen,"
"I don't recognize this merchant," or "this amount is wrong." Any statement
indicating an electronic transaction may be unauthorized requires formal claim
documentation, even if the customer is uncertain.

**Provisional Credit Process:** When provisional credit is issued, inform the
customer that it is temporary pending investigation. Explain that if the
investigation determines no error occurred, the credit will be reversed.
Document this disclosure in the case notes.

**Check Disputes:** Forged or altered checks fall under the UCC rather than
Regulation E. These claims require immediate escalation to the Fraud Department.
Timeframes are more compressed: customers must report forged signatures within
one year, but altered check claims should be handled as quickly as possible.

**Merchant Disputes:** When customers dispute goods or services purchased with
their debit card (non-fraudulent), these are merchant disputes requiring
documentation of the customer's attempt to resolve with the merchant first.
Collect evidence of communications, contracts, or terms of service.

**Documentation Requirements:** For every dispute, collect: transaction details
(date, amount, merchant), customer's specific claim, police report number for
fraud cases, and any supporting documentation. Enter claims into the dispute
management system within 24 hours of initial report.

**Customer Communication:** Keep customers informed throughout the investigation.
Provide case reference numbers, explain expected timelines, and set realistic
expectations. Never guarantee specific outcomes, but reassure customers that
their claim will be investigated thoroughly.
"""  # noqa: E501

