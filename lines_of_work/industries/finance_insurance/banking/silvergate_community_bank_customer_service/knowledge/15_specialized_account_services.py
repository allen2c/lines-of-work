content = """
Beyond standard deposit accounts, Silvergate offers specialized accounts for
unique customer situations. Understanding these products enables appropriate
recommendations and accurate service.

**Trust Accounts:** Accounts managed by trustees for the benefit of named
beneficiaries. Require trust documentation including the trust agreement and
certificate of trust. Trustees must provide identification and proof of
trustee authority. Account title must include the trust name and trustee
identification (e.g., "Smith Family Trust, John Smith, Trustee").

**Estate Accounts:** Accounts opened by estate executors or administrators for
managing deceased persons' assets. Require death certificate, letters
testamentary or letters of administration from probate court, and executor
identification. Estate accounts are temporary accounts that close when
probate concludes and assets are distributed.

**Guardian and Conservator Accounts:** Accounts for managing the affairs of
minors or incapacitated adults. Require court orders appointing the guardian
or conservator. Accounts are subject to court supervision and may require
periodic accounting to the court. Restrictions may apply to withdrawals.

**Representative Payee Accounts:** Accounts for recipients of Social Security
or SSI benefits who cannot manage their own funds. Payees must use funds
solely for the beneficiary's needs and account for expenditures to the SSA
upon request. Annual reporting required.

**Power of Attorney:** When an agent acts under a power of attorney, verify
the document is in effect (durable or springing), has not been revoked, and
grants the specific authority requested. Record the power of attorney in
account documentation. Note that powers of attorney terminate at the
principal's death.

**UTMA/UGMA Custodial Accounts:** Adult-managed accounts for minors. Assets
belong to the minor but are controlled by the custodian until the minor
reaches age of majority (18 for UGMA, 21 for UTMA in most states). Once the
minor reaches the designated age, they have full rights to the assets and
the account converts to a standard account in their name.

**Special Needs Accounts (ABLE):** Tax-advantaged savings accounts for
individuals with disabilities diagnosed before age 26. Allow savings without
jeopardizing means-tested government benefits like Medicaid and SSI. Annual
contribution limits apply. Accounts managed by states but offered through
Silvergate as an authorized agent.

**Customer Service Considerations:** Specialized accounts often have legal
restrictions that standard accounts do not. When in doubt about authority,
always request documentation and escalate to Branch Manager. Never provide
information to parties without verified legal authority, even if they claim
family relationship to the account holder.
"""  # noqa: E501

