title = "Customer Complaint Handling"

content = """
Complaints represent opportunities to demonstrate commitment to service and
to recover customer relationships. Handling complaints with empathy, efficiency,
and appropriate authority preserves the bank's reputation and customer loyalty.

**LEARN Framework for Complaint Resolution:**
- **L**isten: Allow the customer to explain fully without interruption. Take
  notes on key facts and emotions expressed.
- **E**mpathize: Acknowledge feelings and the impact of the situation. Express
  genuine understanding of their frustration.
- **A**pologize: Offer a sincere apology for the experience, regardless of fault.
  "I'm sorry this happened to you" validates their feelings.
- **R**esolve: Take action to correct the situation within your authority.
  Explain what you can do, not what you cannot.
- **N**otify: Follow up to ensure satisfaction and document the resolution.

**Common Complaint Categories:**
- **Fee Disputes:** Customers disputing overdraft fees, service charges, or
  late fees. Review fee waiver authority and customer relationship history.
- **Service Failures:** Long wait times, unreturned calls, or errors. Focus on
  service recovery gestures.
- **Transaction Issues:** Failed deposits, posting errors, or disputes. Verify
  facts and escalate to appropriate department.
- **Product Misunderstanding:** Customers claiming they were not informed of
  terms. Review account opening documentation and disclosures provided.

**Empowerment and Authority:** Customer Service Representatives can:
- Waive fees up to $100 per customer monthly
- Process service corrections immediately
- Issue goodwill gestures (fee waivers, upgraded services)
- Escalate to Branch Manager for complex or high-value situations

**When to Escalate:** Escalate complaints involving: legal threats or regulatory
referrals; repeated service failures by the same department; requests exceeding
authority limits; customers who remain dissatisfied after initial resolution;
or sensitive situations involving elderly or vulnerable customers.

**Documentation Requirements:** Record complaint details in CRM including:
date/time, customer name, complaint category, factual summary, actions taken,
and resolution offered. Document fee waivers with reason code. Complete complaint
records support trend analysis and regulatory reporting.

**Service Recovery Principles:** Address the emotional impact before the
financial. Waiving a fee does not fully resolve the customer's frustration
about how they were treated. Combine tangible recovery (fee waiver, credit)
with intangible recovery (sincere apology, personal commitment to improvement).

**Follow-Up:** For significant complaints, schedule follow-up contact to verify
satisfaction. A brief call or email shows the complaint was taken seriously
and helps rebuild trust. Document follow-up in customer notes.
"""  # noqa: E501

