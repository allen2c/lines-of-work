content = """
Banking operates within a complex regulatory framework designed to protect
customers, prevent financial crime, and ensure system stability. Customer
service representatives must understand key compliance obligations that
impact daily interactions.

**Bank Secrecy Act (BSA) and Anti-Money Laundering (AML):** The BSA requires
banks to report suspicious activity and maintain records of certain
transactions. Currency Transaction Reports (CTRs) document cash transactions
exceeding $10,000. Suspicious Activity Reports (SARs) document potential
money laundering, structuring, or other suspicious behavior.

**Customer Identification Program (CIP):** Federal law requires verification
of customer identity before opening accounts. CIP requirements include:
name, date of birth, address, and identification number. Document
verification must occur at account opening with acceptable primary documents.

**Customer Due Diligence (CDD):** Beyond CIP, the CDD Rule requires
identification and verification of beneficial owners (25%+ ownership) and
control persons for business accounts. This information must be kept current
and updated when changes occur.

**Regulation E:** Governs electronic fund transfers including debit cards,
ATM transactions, and online banking. Defines customer rights regarding
unauthorized transactions, error resolution, and disclosure requirements.
Regulation E claims must follow strict investigation timelines.

**Regulation CC:** Governs funds availability, determining when deposited
funds must be made available for withdrawal. Specifies standard availability
schedules and exception holds that permit longer delays.

**Regulation DD:** Truth in Savings requires clear disclosure of account
terms, fees, and interest rates. Disclosures must be provided at account
opening and upon request.

**Fair Lending and Equal Credit Opportunity:** Prohibits discrimination in
lending based on protected characteristics (race, color, religion, national
origin, sex, marital status, age, receipt of public assistance). All
customers must receive consistent treatment and information.

**USA PATRIOT Act:** Enhanced anti-terrorism financing measures including
customer identification, information sharing with law enforcement, and
enhanced scrutiny of foreign accounts and transactions.

**Office of Foreign Assets Control (OFAC):** Prohibits transactions with
individuals, entities, and countries on sanctions lists. Systems screen
customers and transactions against OFAC lists; matches require freezing
and reporting.

**Privacy (GLBA):** Gramm-Leach-Bliley Act requires protection of customer
non-public personal information. Customers must receive privacy notices and
have opt-out rights for certain information sharing. Never disclose account
information to unauthorized third parties.

**Customer Service Compliance Role:** While Compliance Department handles
complex regulatory matters, Customer Service serves as the frontline for
detecting and reporting suspicious activity, verifying identity, and
following established protocols that keep the bank and customers safe.
"""  # noqa: E501

