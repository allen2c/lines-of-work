title = "Statement and Document Access"

content = """
Account statements and related documents provide customers with records of
their banking activity. Supporting customer access to these documents is
essential for reconciliation, tax preparation, and dispute resolution.

**Statement Delivery Options:**
- **eStatements:** Electronic statements delivered via online banking and
  email notification. Available immediately when generated. Free for all
  accounts. Environmentally friendly and more secure than mail.
- **Paper Statements:** Mailed to address on file, usually arriving 3-5
  business days after generation. $3 monthly fee for Essential Checking;
  waived for Premier Checking and Money Market accounts.

**Statement Content:** Standard statements include: beginning and ending
balances; all transactions with dates, descriptions, and amounts; interest
earned or paid; fees charged; and running balance. Images of cleared checks
included with checking account statements (first 10 images free, additional
$1 each for Essential; unlimited free for Premier).

**Statement Cycles:** Most personal accounts generate monthly statements on
the account opening anniversary date. Some savings accounts offer quarterly
statements. Business accounts default to monthly but can select daily or
weekly reporting for high-volume accounts.

**Online Access:** Customers can access statements online for 18 months
through online banking. Older statements can be requested through customer
service with research fees waived for requests under 2 years old; fees may
apply for older records or extensive research.

**Check Image Retrieval:** Cleared check images are available online for
7 years. Customers can print, save, or email images directly from online
banking. Original checks are retained for limited periods before destruction
per retention policies.

**Account History:** Transaction history is available online for 18 months
in detailed or summary formats. CSV exports available for import into
accounting software or spreadsheets. Older history requires research request.

**Research and Retrieval Fees:** Standard account research (transaction
history, statement copies, check images) is free for requests covering less
than 2 years. Requests spanning 2-5 years: $25 per hour with 1-hour minimum.
Requests over 5 years: $50 per hour. Statement copies: $5 each for recent
statements, included in research fees for older records.

**Tax Document Access:** 1099-INT forms for interest income and 1098 forms
for mortgage interest are mailed by January 31 and available online. Verify
mailing address is current before year-end to ensure proper delivery.

**Statement Reconciliation Support:** When customers report discrepancies
between their records and statements, walk through the reconciliation process:
verify outstanding checks, review automatic payments, confirm deposit dates,
and check for unrecorded fees. Escalate to Research if the discrepancy
suggests a bank error.
"""  # noqa: E501

