content = """
Opening a new account at Silvergate Community Bank requires careful attention to
regulatory requirements, documentation, and customer needs assessment. A properly
executed account opening establishes the foundation for a lasting banking
relationship.

**Customer Identification Program (CIP):** Federal law requires verification of
the customer's identity before opening any account. Acceptable primary documents
include unexpired government-issued photo ID (driver's license, passport, state
ID, military ID). For non-US citizens, valid passport with current visa or
permanent resident card required. Documents must be current and not expired.

**Minimum Required Information:** Collect full legal name, date of birth, physical
address (no PO boxes), mailing address if different, primary phone number, email
address, occupation/employer name, and Social Security Number or Tax ID. For
business accounts, additional documentation includes articles of incorporation,
EIN confirmation, and authorized signatory identification.

**Beneficial Ownership Verification:** For business accounts, identify and verify
the identity of all individuals owning 25% or more of the business entity, plus
one control person. This CDD Rule requirement strengthens anti-money laundering
compliance for corporate accounts.

**Account Selection Guidance:** Interview the customer about their banking needs
before recommending products. Assess typical monthly transaction volume, average
balance expectations, ATM usage patterns, and interest preferences. Recommend
appropriate products without pressuring for upgrades.

**Disclosure Review:** Present and explain the Truth in Savings disclosure,
fee schedule, and account agreement. Highlight key terms including overdraft
policies, funds availability, and early withdrawal penalties for CDs. Document
customer acknowledgment.

**Initial Deposit Processing:** Accept cash, check, or electronic transfer for
opening deposit. Verify funds availability for checks over $5,000. Issue initial
debit card and temporary checks if requested. Schedule follow-up appointment for
debit card PIN setup when required.
"""  # noqa: E501

