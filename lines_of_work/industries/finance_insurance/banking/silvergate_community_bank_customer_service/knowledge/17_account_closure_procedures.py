title = "Account Closure Procedures"

content = """
Account closures, whether initiated by customer or bank, require careful
handling to ensure complete settlement, regulatory compliance, and preservation
of the relationship where possible.

**Customer-Initiated Closures:** When a customer requests to close an account,
first explore the reason. Many closures can be prevented by addressing service
issues, recommending alternative products, or explaining features the customer
may not be using. If the customer proceeds, follow proper closure procedures.

**Pre-Closure Checklist:** Before closing any account:
- Verify account ownership and identity
- Confirm all checks have cleared (recommend 10-day waiting period for active
  accounts)
- Stop all automatic payments, direct deposits, and recurring transfers
- Cancel account-linked services (debit card, online bill pay, etc.)
- Check for outstanding loans or pledged collateral
- Verify no pending transactions or holds

**Balance Settlement:** Process final balance as customer directs: cash
withdrawal, cashier's check, wire transfer, or transfer to another account.
Document the disposition method in the closure record. For large cash
withdrawals, complete CTR (Currency Transaction Report) if applicable.

**Final Documentation:** Provide customer with written confirmation of account
closure including final balance and date of closure. For loans or credit
products tied to the account, provide payoff letters or closure confirmations
as appropriate.

**Bank-Initiated Closures:** Silvergate may close accounts for reasons
including: zero balance dormancy (typically 6-12 months); suspicious activity
suspected; excessive overdrafts; or abusive customer behavior. Account closure
notices are sent via mail to the address on file, providing 30 days notice
when possible.

**Dormant and Escheatment Accounts:** Accounts with no customer-initiated
activity for extended periods become dormant. After the statutory dormancy
period (varies by state, typically 3-5 years), unclaimed funds must be
transferred to the state's unclaimed property division. Customers can reclaim
escheated funds from the state.

**Reactivating Dormant Accounts:** Customers may reactivate dormant accounts
by visiting a branch with identification, contacting customer service, or
making a deposit or withdrawal. Dormant account fees may have accumulated;
consider waiving these for customers reactivating and intending to resume
active use.

**Relationship Preservation:** When a long-standing customer closes an account,
express appreciation for their past business and leave the door open for
future relationships. Note: customers who close accounts often return if their
new banking relationship does not meet expectations.

**Documentation:** Record closure reason (customer request, bank decision,
dormancy), final balance, how funds were disbursed, and any conversations
about the closure. Complete records support audit requirements and customer
inquiries that may arise years later.
"""  # noqa: E501

