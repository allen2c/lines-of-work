title = "Garantias: Aval, Fiança e Cessão"

content = """
Para operações de maior risco ou valor, o banco pode exigir garantias
adicionais: aval (terceiro que se compromete solidariamente), fiança ou
cessão de direitos (ex.: recebíveis). O analista deve conhecer os tipos
e quando são exigidos.

**Aval:** O avalista responde pela dívida como se fosse o próprio devedor.
Em caso de inadimplência, o banco pode cobrar do avalista sem precisar
acionar o devedor primeiro. O avalista deve comprovar capacidade de
pagamento e aceitar a obrigação de forma livre e esclarecida.

**Fiança:** Similar ao aval, com nuances jurídicas. A fiança pode ser
limitada ou ilimitada conforme o contrato.

**Cessão de Recebíveis:** O cliente cede ao banco o direito a receber
de terceiros (NF-e, duplicatas, cheques). O banco antecipa o valor e
cobra do devedor. Comum em capital de giro para empresas.

**Registro:** Garantias devem ser formalizadas em contrato e registradas.
Verificar conformidade legal e capacidade do garantidor.
"""  # noqa: E501

