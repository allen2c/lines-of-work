content = """
Negativação é o registro em bureaus de crédito (Serasa, SPC, Boa Vista) de
dívidas não pagas. O registro prejudica o score e a capacidade do consumidor
de obter novos créditos. A inclusão e a exclusão seguem regras legais.

**Inclusão:** O credor pode negativar após o inadimplemento e a notificação
do devedor. O registro deve conter valor, data e identificação correta. Prazos
máximos de permanência variam (geralmente 5 anos para consumidores).

**Exclusão:** Após quitação, o consumidor tem direito à liberação em até 5
dias úteis. O banco deve comunicar a quitação aos bureaus. Se houver erro
(pagamento indevido, valor incorreto), o cliente pode contestar e o banco
deve retificar.

**Boa Prática:** O Cerrado Horizonte Bank notifica o cliente antes de
negativar e mantém processo ágil de liberação após quitação. Evitar
negativação por valores irrelevantes ou em disputa sem análise prévia.
"""  # noqa: E501

