title = "Seguro Prestamista"

content = """
O seguro prestamista (ou seguro de vida em grupo vinculado ao crédito) cobre o
saldo devedor em caso de morte ou invalidez do tomador. É comum em operações
de maior valor e prazo, como financiamento imobiliário e empréstimos consignados.

**Características:** Prêmio pode ser único (pago na contratação) ou mensal
(incluído na parcela). A seguradora paga ao banco o saldo devedor na data do
sinistro, e a dívida do cliente ou de seus herdeiros é considerada quitada.

**Aceitação:** O cliente pode recusar o seguro quando a oferta for opcional,
conforme CDC. Em alguns produtos (ex.: SFH), o seguro pode ser obrigatório.
Informar claramente o custo e a cobertura.

**Sinistro:** Em caso de óbito ou invalidez, os beneficiários ou o próprio
cliente devem acionar a seguradora com documentação (atestado de óbito, laudo
médico). O banco auxilia no encaminhamento quando solicitado.
"""  # noqa: E501

