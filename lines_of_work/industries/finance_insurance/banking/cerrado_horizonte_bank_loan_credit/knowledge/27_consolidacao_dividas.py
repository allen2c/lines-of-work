content = """
Consolidação (ou refinanciamento de dívidas) ocorre quando o cliente contrata
um empréstimo para quitar várias dívidas de uma vez, unificando em uma única
parcela. O objetivo é reduzir o custo total ou simplificar o fluxo de pagamentos.

**Análise:** Avaliar a operação como crédito pessoal. Verificar se a nova
parcela única é compatível com a renda e o índice de endividamento. O cliente
deve comprovar as dívidas existentes (boletos, extratos) para que o banco
possa destinar os recursos corretamente.

**Benefício:** Se a taxa do empréstimo for menor que a média ponderada das
dívidas atuais, o cliente reduz o custo. Se o prazo for alongado, a parcela
mensal diminui, mas o custo total pode aumentar.

**Cuidados:** Garantir que o cliente entenda que está assumindo nova dívida
e que deve evitar contrair novas dívidas paralelas. Oferecer orientação
financeira quando o padrão de endividamento for preocupante.
"""  # noqa: E501

