title = "Reclamações ao Banco Central"

content = """
O Banco Central mantém canal de reclamações (Sistema de Ouvidorias do
Sistema Financeiro) onde clientes podem registrar insatisfação com
instituições reguladas. O banco deve responder em prazo e resolver ou
justificar a posição.

**Fluxo:** O cliente registra a reclamação no portal do Bacen. O banco
recebe a notificação e tem prazo para responder (geralmente 30 dias).
A resposta é enviada ao Bacen e ao cliente. O Bacen monitora índices
de reclamação por instituição.

**Tipos Comuns:** Demora na liberação após quitação, cobrança indevida,
falta de transparência em taxas, tratamento inadequado. Muitas reclamações
podem ser evitadas com comunicação clara e procedimentos corretos.

**Procedimento Interno:** A ouvidoria ou o compliance distribui a
reclamação à área responsável. O analista de crédito pode ser acionado
para esclarecer decisões ou fornecer informações. Responder com precisão
e documentação que comprove a conduta do banco.
"""  # noqa: E501

