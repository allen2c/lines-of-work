title = "Reg E Error Resolution"

content = """
- Customers have 60 days from the statement date to report an unauthorized EFT or error.
- Upon receiving a claim, investigate within 10 business days. If unresolved, provisionally credit the customer's account within 10 days (up to $50 for claims under $50, or the full amount for larger claims).
- Complete investigation within 45 days (90 days for new accounts or foreign transactions). Notify customer of results in writing.
- If the error is confirmed, correct the account; if not, reverse the provisional credit and explain.
"""

