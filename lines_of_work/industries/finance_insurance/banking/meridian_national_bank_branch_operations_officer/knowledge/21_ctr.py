title = "Currency Transaction Reporting (CTR) Thresholds"

content = """
- File a CTR for any cash transaction (deposit, withdrawal, exchange) exceeding $10,000 in a single business day.
- Aggregate multiple cash transactions by the same person or entity if they appear to be structured to avoid the $10,000 threshold.
- Exemptions: certain businesses (e.g., gas stations, grocery stores) can be designated as exempt after due diligence.
- CTRs must be filed electronically via the BSA E-Filing System within 15 days of the transaction.
"""

