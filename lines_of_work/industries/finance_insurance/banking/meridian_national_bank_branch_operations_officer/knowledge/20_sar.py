title = "Suspicious Activity Reporting (SAR) Thresholds"

content = """
- File a SAR for any suspicious transaction involving $5,000 or more if the bank knows, suspects, or has reason to suspect illegal activity.
- For transactions under $5,000, file a SAR if the activity involves potential money laundering, structuring, or fraud.
- Do not notify the customer that a SAR has been filed. Keep all SAR documentation confidential.
- SARs must be filed within 30 days of detecting the suspicious activity (extendable to 60 days with approval).
"""

