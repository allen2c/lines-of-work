title = "Regulación bancaria en Latinoamérica"

content = """
Las instituciones financieras en Latinoamérica operan bajo marcos regulatorios que varían
por país pero comparten principios comunes. El banco central de cada jurisdicción
supervisa la solvencia, liquidez y prácticas de las entidades.

**Reservas y capital:** Los bancos deben mantener reservas mínimas y ratios de capital
adecuados. Las operaciones de crédito están limitadas por estos requisitos y por
políticas internas de riesgo.

**Transparencia:** Las tasas, comisiones y condiciones deben divulgarse claramente al
cliente antes de la contratación. El costo efectivo total (CET) es obligatorio en
muchas jurisdicciones.

**Reportes:** Las operaciones de crédito se reportan a centrales de riesgo. El cliente
debe ser informado sobre el uso de sus datos y sus derechos de consulta y corrección.
"""  # noqa: E501

