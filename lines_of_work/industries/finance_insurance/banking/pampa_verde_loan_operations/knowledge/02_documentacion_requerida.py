title = "Documentación requerida para solicitudes de crédito"

content = """
Cada producto crediticio exige documentación específica. La ausencia o
inconsistencia de documentos es causa suficiente para suspender o rechazar
la solicitud hasta su regularización.

**Identificación:** DNI o cédula de identidad vigente. Para extranjeros
residentes, documento que acredite residencia legal y permiso de trabajo
cuando aplique.

**Comprobantes de ingresos:** Últimos tres recibos de sueldo, declaración
jurada de ingresos para trabajadores independientes, o estados financieros
para empresas. Los ingresos deben ser verificables y estables.

**Comprobante de domicilio:** Factura de servicios, contrato de alquiler o
escritura de propiedad con antigüedad no mayor a noventa días.

**Productos específicos:** Crédito hipotecario requiere tasación del inmueble
y documentación notarial. Crédito vehicular exige factura del vehículo.
Crédito PYME requiere balances y documentación societaria.
"""  # noqa: E501

