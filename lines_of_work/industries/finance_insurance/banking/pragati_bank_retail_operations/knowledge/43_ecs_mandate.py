title = "ECS (Electronic Clearing Service) Mandate"

content = """
ECS is used for recurring debits (e.g. loan EMI to another bank,
insurance premium) or credits (e.g. dividend, salary). The customer
gives a mandate to the bank to allow such debits or to receive
credits.

**ECS Debit:** The customer signs a mandate form authorising a
beneficiary (e.g. lender, insurer) to debit the account through
NPCI/bank. The mandate is registered with the bank. On each due
date, the amount is debited automatically. Sufficient balance
must be maintained.

**ECS Credit:** For receiving credits (e.g. salary), the customer
provides account details to the employer or payer. Credits are
processed as per the cycle.

**Stop ECS:** To stop future ECS debits, the customer must inform
both the bank and the beneficiary. The bank will reject further
debits once the mandate is cancelled.

**Pragati Bank:** The retail agent can explain that ECS is used
for recurring payments and that mandate forms are available at
the branch. For stop or dispute, the customer should contact
the branch and the beneficiary.
"""  # noqa: E501

