title = "Senior Citizen Interest Rates and Schemes"

content = """
Banks offer higher interest rates and special schemes for senior
citizens (typically 60 years and above) on FDs and sometimes on
savings accounts.

**FD rates:** Senior citizens usually get an additional rate (e.g.
0.25% to 0.50% p.a.) over the card rate for the same tenure.
Some banks have dedicated senior citizen FD schemes with further
benefits.

**Tax benefit:** Interest income from deposits is taxable. Senior
citizens may have a higher TDS threshold (e.g. Rs 50,000 per year
for interest from deposits). Form 15H can be submitted to avoid
TDS if total income is below the taxable limit.

**Other benefits:** Some banks offer preferential queue at branches,
lower minimum balance for savings accounts, or special health or
pension products. Exact benefits vary by bank.

**Pragati Bank:** The retail agent can inform that senior citizens
get additional FD rate and can direct them to the branch or website
for current rates and scheme details.
"""  # noqa: E501

