title = "Withdrawal Limits for Savings Account"

content = """
Withdrawals from a savings account can be made at the branch (counter
or ATM) or through electronic channels. Limits may apply by channel
and by product.

**Branch:** For cash withdrawal at the counter, banks may set a limit
(e.g. above a certain amount, prior notice or identity may be required).
Large withdrawals may need to be arranged in advance for cash availability.

**ATM:** Daily and per-transaction ATM withdrawal limits apply as per
the card and bank policy. These are displayed in the app and tariff.

**Electronic (NEFT/IMPS/UPI):** Per-transaction and per-day limits are
set for each channel. They are visible in net banking and the app.

**No limit on number of withdrawals:** RBI has removed the cap on
number of free savings account withdrawals. Banks may charge for
beyond a certain number of branch transactions as per their tariff.

**Pragati Bank:** The retail agent can direct customers to the
tariff and app for current withdrawal limits by channel.
"""  # noqa: E501

