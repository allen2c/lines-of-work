title = "Common Documents for Loan Applications"

content = """
Loan applications require identity, address, income, and asset/liability
proof. The exact list depends on the loan type and the bank's policy.

**Identity and address:** Aadhaar, PAN, Passport, Voter ID, or
Driving Licence. Address proof as per KYC norms.

**Income (salaried):** Salary slips (usually last 3–6 months), Form 16,
bank statement of the salary account (6 months to 1 year). Some banks
ask for employment certificate.

**Income (self-employed):** IT returns (2–3 years), profit and loss
and balance sheet, bank statements, GST registration if applicable.
Business proof may be required.

**Property (for secured loans):** Property documents, valuation report,
and encumbrance certificate as per the bank's checklist.

**Existing loans:** Details of existing EMIs and lenders for
calculation of eligibility.

**Pragati Bank:** The retail agent can give a general list and
direct the customer to the branch or loan section for the
exact document checklist for the desired loan product.
"""  # noqa: E501

