title = "Chequebook Request and Usage"

content = """
A chequebook allows the account holder to make payments by writing a cheque
or to withdraw cash at the branch. It is issued for savings and current
accounts where the product permits cheque facility.

**Request:** Chequebook can be requested at the branch, through net banking,
or via the mobile app. First chequebook may be free; subsequent ones may
attract a charge as per the bank's tariff. Delivery is by post or collection
from the branch.

**Filling a cheque:** Write the date, payee name, amount in words and figures,
and sign as per the specimen on record. Avoid overwriting; cancelled cheques
should be marked "CANCELLED" and retained.

**Validity:** A cheque is typically valid for three months from the date of
issue (as per RBI). Stale cheques may not be honoured.

**Stop payment:** If a cheque is lost or should not be paid, the customer
can instruct the bank to stop payment. There may be a charge and a validity
period for the stop instruction.

**Bouncing:** Issuing a cheque without sufficient balance can lead to
bouncing and may attract penalties and legal consequences under the
Negotiable Instruments Act.
"""  # noqa: E501

