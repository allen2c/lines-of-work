title = "UPI Linking and Usage"

content = """
Unified Payments Interface (UPI) allows customers to link their bank account
to a UPI app and make instant payments, collect money, or pay bills without
entering account details each time.

**Linking process:** Download any RBI-approved UPI app, select "Add bank account",
choose Pragati Bank, and verify with the registered mobile number. An OTP or
debit card details may be required for first-time linking. Only the mobile
number registered with the bank can be used.

**Security:** Never share UPI PIN or OTP with anyone. The bank or UPI apps
never ask for PIN or OTP on phone or email. For payments, the customer
enters the UPI PIN on the app; no agent or representative should ask for it.

**Limits:** Per-transaction and per-day limits are set by the bank and the
UPI app. Limits are displayed in the app and may vary for different
transaction types.

**Failed or disputed transactions:** Customers can raise a complaint through
the UPI app or contact the bank's customer care with transaction reference.
Banks follow RBI guidelines for resolution timelines.
"""  # noqa: E501

