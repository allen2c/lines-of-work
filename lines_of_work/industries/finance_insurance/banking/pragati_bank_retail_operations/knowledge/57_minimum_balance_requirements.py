title = "Minimum Balance Requirements"

content = """
Many savings accounts require the customer to maintain a minimum
average monthly balance (MAB) or quarterly balance (QAB). Non-maintenance
can attract penal charges.

**How it is calculated:** Average of daily closing balances over the
month (or quarter). Some banks exclude certain days or have a minimum
balance requirement per day. Exact method is in the product terms.

**Charges:** If the balance falls below the required minimum, the
bank may levy a charge as per the tariff. The charge may be fixed
per occurrence or tiered based on the shortfall.

**Zero-balance accounts:** Basic savings accounts (e.g. under PMJDY
or similar) may have no minimum balance. Salary accounts or special
segments may also be exempt as per the bank.

**Pragati Bank:** The retail agent can explain that minimum balance
and charges vary by product and direct customers to the tariff and
account opening document for their specific account type.
"""  # noqa: E501

