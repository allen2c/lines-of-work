content = """
Net banking (internet banking) allows customers to access their accounts
and conduct transactions through the bank's website from a computer or
browser on a phone.

**Registration:** Visit the bank's official website and look for "Register"
or "First-time user". Enter account number, registered mobile number,
and other details. Verify with OTP or debit card. Set user ID and password.
Some banks require a visit to the branch or ATM for activation.

**Login:** Use the user ID and password. Two-factor authentication (OTP
or similar) may be required for transactions. Do not share credentials
with anyone.

**Features:** Balance, statements, fund transfer, bill pay, FD/RD, stop
cheque, card block, and other services as offered by the bank.

**Security:** Use a strong password, avoid public computers for banking,
and ensure the URL is the bank's official site (https). Phishing sites
often mimic banks; never enter credentials on links received via email
or SMS.
"""  # noqa: E501

