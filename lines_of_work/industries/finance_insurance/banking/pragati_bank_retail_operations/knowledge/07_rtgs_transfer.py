title = "RTGS (Real Time Gross Settlement)"

content = """
RTGS enables real-time transfer of large-value funds between banks. It is
used when immediate credit is required and amounts are typically high.

**Minimum amount:** RBI has prescribed a minimum of Rs 2 lakh per
transaction for RTGS. There is no maximum limit.

**Timings:** RTGS is available 24x7. Settlement is on a real-time basis
subject to the beneficiary bank's acceptance. Cut-off times for same-day
credit may apply as per RBI/bank guidelines.

**Charges:** Banks may levy charges for RTGS as per their tariff. For
digital channels, many banks offer RTGS at reduced or nil charges.

**Requirements:** Beneficiary account number, IFSC, account type, and
beneficiary name. Accuracy is critical; wrong details can delay or
reject the transaction.

**Use case:** RTGS is suitable for high-value transfers such as
property payments, large business payments, or inter-bank transfers
where immediate credit is important.
"""  # noqa: E501

