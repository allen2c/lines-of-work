title = "TDS on Interest (Savings and Deposits)"

content = """
Interest earned on savings accounts and fixed/recurring deposits is taxable
under the Income Tax Act. Banks deduct TDS when interest exceeds the
prescribed threshold in a financial year.

**Savings account:** TDS is deducted if interest in a financial year
exceeds Rs 40,000 (Rs 50,000 for senior citizens from FY 2018-19). The
rate is 10% if PAN is linked; otherwise 20%. Form 15H/15G can be
submitted to avoid TDS if the customer's total income is below the
taxable limit.

**Fixed and recurring deposits:** TDS applies when interest income
from all FDs/RDs with the bank (and often across banks) exceeds the
threshold. Same rates and Form 15H/15G apply.

**Certificate:** Bank issues Form 16A or equivalent for TDS deducted.
The customer uses it for filing income tax return.

**Pragati Bank:** Customers can submit Form 15H/15G at the branch or
through the channel prescribed by the bank. PAN must be linked to
avoid higher TDS and for correct reporting.
"""  # noqa: E501

