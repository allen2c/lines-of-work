title = "Kisan Credit Card (KCC) Overview"

content = """
Kisan Credit Card is a credit facility for farmers to meet their short-term
agricultural and allied needs. It is supported by the government and
implemented by banks including Pragati Bank.

**Eligibility:** Farmers, including individual cultivators, tenant farmers,
and sharecroppers, as well as joint liability groups and self-help groups
of farmers, may be eligible. Landholding and cropping pattern are considered.

**Limit:** The limit is based on the scale of finance for the crop and
holding, as per RBI and bank norms. It can cover crop production,
maintenance of farm assets, and post-harvest expenses. A sub-limit
for consumption need may be allowed.

**Drawal and repayment:** The farmer can withdraw as per need within the
limit. Repayment is typically aligned with harvest; interest subvention
may apply as per government scheme.

**Documents:** Land records, identity and address proof, and other
documents as required by the bank. Branch or agricultural officer
can provide the exact list.

**Pragati Bank:** KCC is offered at designated branches. The retail
agent can direct customers to the branch or agricultural desk for
eligibility and application.
"""  # noqa: E501

