title = "Account Statement Request"

content = """
Customers can obtain account statements for a chosen period for
reconciliation, tax, or loan applications. Statements can be
downloaded online or requested at the branch.

**Digital:** Through net banking or the mobile app, the customer
can download statements for a date range (e.g. last 1 month to
7 years depending on bank retention). Format is usually PDF.
No charge for a certain number of downloads per year in many banks.

**Branch:** Request a printed statement at the branch. There may
be a charge for statements beyond the free limit (e.g. first 2–4
per year free). Statement is provided after identity verification.

**Period:** Banks retain transaction data for a defined period
(e.g. 7–10 years). Older statements may not be available.

**Loan applications:** Lenders often ask for 6–12 months of bank
statements. The customer can download and submit them or authorise
the bank to share with the lender as per process.

**Pragati Bank:** The retail agent can direct customers to the
app or net banking for self-service statement download and to
the branch for printed statements and charges.
"""  # noqa: E501

