title = "Form 15H and 15G to Avoid TDS on Interest"

content = """
Form 15H (for senior citizens) and Form 15G (for others) are
self-declarations that the taxpayer's total income is below the
taxable limit. When submitted to the bank, the bank may not deduct
TDS on interest income to the extent covered by the form.

**Form 15H:** For individuals who are 60 years or older. They
declare that their total income is below the taxable limit and
that the tax on their estimated total income is nil.

**Form 15G:** For individuals and HUFs below 60. Same declaration
that tax on total income is nil. Cannot be used if the person
has already submitted 15G to the same bank for the same financial
year and the total interest from that bank exceeds the threshold.

**Validity:** These forms are valid for one financial year. They
must be submitted at the start of the year or before interest
accrues. Banks may allow submission through the branch or online
as per process.

**Pragati Bank:** The retail agent can explain that 15H/15G can
avoid TDS if income is below taxable limit and direct customers
to the branch or website for the form and submission process.
"""  # noqa: E501

