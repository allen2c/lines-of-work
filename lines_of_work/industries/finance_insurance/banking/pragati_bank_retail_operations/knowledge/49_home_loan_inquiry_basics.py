content = """
Home loans are long-term loans for purchase, construction, or
renovation of residential property. They are secured by the
property.

**Eligibility:** Based on age, income, existing obligations, and
credit score. Banks use fixed obligation to income ratio (FOIR)
or similar norms. Tenure can be up to 20–30 years.

**Amount:** Depends on property value, bank's LTV (loan-to-value)
norms, and eligibility. Stamp duty and registration are usually
not financed.

**Interest:** Floating or fixed. Floating rates are linked to
RBI repo or the bank's external benchmark. Processing fee and
other charges apply as per tariff.

**Documents:** Identity, address, income proof, property documents,
sale agreement, and NOC from society or builder as required.

**Pragati Bank:** The retail agent can provide basic information
and direct customers to the home loan desk or branch for
detailed eligibility, rates, and documentation. The agent does
not approve or reject loans.
"""  # noqa: E501

