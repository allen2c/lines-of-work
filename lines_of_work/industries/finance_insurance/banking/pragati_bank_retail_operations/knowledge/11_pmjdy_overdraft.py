title = "PMJDY Overdraft Facility"

content = """
Overdraft under PMJDY allows eligible account holders to withdraw a small
amount over and above the balance in their account, subject to scheme and
bank rules.

**Eligibility:** Typically, accounts that have been operated satisfactorily
for a defined period (e.g. six months) and meet other criteria set by the
bank and the scheme may be considered for overdraft. One OD per household
is usually allowed as per scheme guidelines.

**Limit:** The overdraft limit is capped by the scheme (e.g. Rs 10,000).
The actual limit granted may be lower based on the bank's assessment.

**Interest:** Interest is charged on the amount utilised and for the period
of use. The rate is as per bank policy and scheme terms.

**Repayment:** The overdraft should be repaid so that the account returns
to positive balance. Regularisation is important to avoid continued charges
and to remain eligible for future facility.

**Pragati Bank:** Branch staff can confirm current eligibility criteria,
limit, and interest for PMJDY overdraft.
"""  # noqa: E501

