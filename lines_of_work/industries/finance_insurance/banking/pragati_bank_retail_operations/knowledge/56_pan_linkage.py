title = "PAN Linkage with Bank Account"

content = """
Linking PAN (Permanent Account Number) with the bank account is
mandatory for certain transactions and to avoid higher TDS on
interest and other payments.

**Requirement:** RBI and tax rules require PAN for opening accounts,
for high-value transactions, and for correct TDS deduction. Without
PAN, TDS on interest may be deducted at 20% instead of 10%.

**How to link:** Submit PAN at the branch or through net banking/app
if available. The bank verifies with the income tax database and
updates the account. Confirmation is sent.

**TDS:** For interest on deposits, if PAN is linked and Form 15H/15G
is submitted where applicable, TDS may be nil or at 10%. Non-linkage
can lead to higher TDS and possible notice from the tax department.

**Update:** If PAN is corrected or updated with the tax department,
inform the bank so that records match.

**Pragati Bank:** The retail agent can advise customers to link PAN
to avoid higher TDS and to comply with KYC. Linkage can be done at
the branch or through the prescribed channel.
"""  # noqa: E501

