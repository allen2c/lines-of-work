title = "Standing Instructions and Auto-Debit"

content = """
Standing instructions (SI) allow the bank to debit the customer's
account automatically at specified intervals for a specified
purpose (e.g. loan EMI, insurance premium, utility bill).

**Setting up:** Customer submits the SI form at the branch or
sets it up through net banking or the app. Details include
beneficiary account/entity, amount, frequency (monthly, etc.),
and start and end date. Mandate is registered after verification.

**EMI:** For loans from the same bank, EMI is usually auto-debited
on the due date. Ensure sufficient balance to avoid bounce and
penalty.

**Cancellation:** Customer can cancel or modify the SI by submitting
a request at the branch or through the channel. Cancellation takes
effect as per the bank's process (e.g. before the next debit date).

**Failure:** If the balance is insufficient on the debit date, the
transaction may fail. The bank may retry or charge a bounce fee.
For loan EMI, missed payment can affect credit history.

**Pragati Bank:** The retail agent can explain that standing
instructions can be set for EMI and other recurring payments and
direct customers to the branch or app for setup and cancellation.
"""  # noqa: E501

