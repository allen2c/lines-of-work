content = """
Some bank products (e.g. PMJDY RuPay card) come with accidental
or life insurance. In case of claim, the nominee or family needs
to approach the bank and the insurer.

**Accidental cover (e.g. PMJDY):** Nominee or legal heir should
contact the branch with the death certificate, claim form, and
any other document required by the insurer. The bank will forward
to the insurer. Claim is settled as per the scheme terms.

**Life cover:** Similar process—claim form, death certificate,
and nominee details. The insurer may ask for additional documents.
Settlement is as per the policy.

**Account balance:** The balance in the deceased's account is
released to the nominee or legal heir as per succession. Nomination
simplifies the process. Without nomination, succession certificate
or legal representation may be required.

**Pragati Bank:** The retail agent can explain that insurance
claims are processed in coordination with the insurer and that
the nominee should contact the branch with the death certificate
and claim form. The branch will guide on the exact process.
"""  # noqa: E501

