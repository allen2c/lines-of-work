title = "TAN and TDS Certificate for Interest"

content = """
Banks deduct TDS on interest income when it exceeds the threshold
and issue a TDS certificate (e.g. Form 16A). The bank's TAN (Tax
Deduction and Collection Account Number) is used for this.

**When TDS is deducted:** Interest on deposits (savings, FD, RD)
exceeds Rs 40,000 (Rs 50,000 for senior citizens) in a financial
year. Rate is 10% if PAN is linked, 20% if not. Form 15H/15G
can avoid TDS if the customer's total income is below the
taxable limit.

**Certificate:** Bank issues Form 16A or equivalent showing TDS
deducted. The customer uses it while filing the income tax return.
TDS is reflected in Form 26AS (now AIS) for the customer.

**Pragati Bank:** The retail agent can explain that TDS certificate
is available from the bank and that customers should ensure PAN
is linked and submit Form 15H/15G if eligible to avoid or reduce TDS.
"""  # noqa: E501

