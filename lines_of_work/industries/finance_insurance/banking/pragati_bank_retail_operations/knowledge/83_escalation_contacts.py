content = """
For unresolved issues or escalation, customers should know the
correct contact points within the bank and with the regulator.

**Branch:** First point for account-related issues, documentation,
and local service. Branch manager can address many complaints.

**Customer care helpline:** 24x7 or during working hours. Number
is on the website, passbook, and card. Note the complaint or
reference number when you call.

**Nodal officer:** For unresolved complaints, the bank appoints
a nodal officer. Contact details are on the website and in the
branch. The nodal officer must respond within the timeline
prescribed by the bank.

**Banking Ombudsman:** If the bank does not resolve the complaint,
the customer can approach the RBI Banking Ombudsman. Details are
on the RBI website. The Ombudsman can award compensation and
direct the bank to take action.

**Pragati Bank:** The retail agent can direct customers to the
helpline and branch for first-level resolution and to the website
for nodal officer and escalation. For Ombudsman, the customer
can refer to RBI's official portal.
"""  # noqa: E501

