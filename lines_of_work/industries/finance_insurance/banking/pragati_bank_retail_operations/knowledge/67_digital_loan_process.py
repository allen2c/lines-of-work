title = "Digital Loan Application Process"

content = """
Many banks offer digital (online) application for personal loans,
some retail loans, and other products. The process is paperless
or minimal-paper and can be faster.

**Steps:** Customer visits the bank's website or app, selects the
loan product, and enters basic details (income, employment, amount
required). Consent for credit bureau check is taken. Documents may
be uploaded. Verification may be done through video or in-person
as per the bank. Approval and disbursement can be quick for
eligible applicants.

**Eligibility:** Same as offline—income, age, credit score, and
documentation. The system may give an indicative eligibility or
rate; final approval is with the bank.

**Safety:** Use only the bank's official website or app. Do not
share OTP or passwords with anyone. Verify the URL and app source.

**Pragati Bank:** The retail agent can inform that some loans can
be applied online and direct customers to the official website or
app for the digital loan section. The agent does not process or
approve loans.
"""  # noqa: E501

