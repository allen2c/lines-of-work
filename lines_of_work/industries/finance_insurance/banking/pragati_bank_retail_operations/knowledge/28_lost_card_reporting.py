title = "Lost or Stolen Card Reporting"

content = """
If a debit or ATM card is lost or stolen, the customer must report it
immediately to limit liability for unauthorised transactions.

**Reporting channels:** Call the bank's 24x7 helpline (number on the
website and back of the card), use the mobile app or net banking to
block the card, or visit the branch. Blocking via app or helpline is
fastest.

**Liability:** As per RBI guidelines, if the loss is reported within
a few days, the customer's liability for unauthorised transactions may
be limited. Delay in reporting can increase liability. The exact timeline
and cap are in the bank's policy and RBI circulars.

**After blocking:** Request a replacement card. The old card will not
work. There may be a fee for re-issue. The new card will have a new
number and may require PIN set/activation.

**Pragati Bank:** Helpline number and in-app block option are communicated
at account opening and on the website. The retail agent can remind
customers to block immediately and then request a replacement.
"""  # noqa: E501

