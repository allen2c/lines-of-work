title = "Minor Accounts"

content = """
Banks allow accounts in the name of minors (below 18 years) operated by
a guardian. This helps in building savings and inculcating banking habits.

**Guardian:** Usually a parent or court-appointed guardian operates the
account. Guardian's KYC and proof of relationship (e.g. birth certificate)
are required. The account can be "either or survivor" or "former or survivor"
with the minor and guardian as per bank product.

**Withdrawal:** Withdrawals are permitted by the guardian for the minor's
benefit. When the minor attains majority, the bank may require the account
to be converted to a regular account in the minor's name with fresh KYC.

**Interest and tax:** Interest is taxable in the hands of the minor; clubbing
provisions may apply. TDS and Form 15H/15G rules are similar to other
individual accounts.

**Pragati Bank:** Minor account variants and documentation can be confirmed
at the branch.
"""  # noqa: E501

