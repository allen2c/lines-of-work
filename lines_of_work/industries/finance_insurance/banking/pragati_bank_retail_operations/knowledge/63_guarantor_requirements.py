title = "Guarantor Requirements for Loans"

content = """
For some loans, banks require a guarantor—a person who undertakes
to repay the loan if the borrower defaults. Guarantee can be
limited or unlimited as per the agreement.

**When required:** For education loans, personal loans to those with
limited income or credit history, or when collateral is insufficient.
Exact requirement depends on the product and the bank's policy.

**Eligibility of guarantor:** Usually a person with stable income
and good credit history. The guarantor's income and obligations
may be assessed. Relationship with the borrower (e.g. parent, relative)
is common but not always mandatory.

**Rights and duties:** The guarantor is liable to the bank if the
borrower defaults. The guarantor can seek recovery from the borrower
as per law. Guarantee is a serious commitment; the guarantor should
read the deed carefully.

**Pragati Bank:** The retail agent can explain that some loans
require a guarantor and direct customers to the loan section for
who can be a guarantor and what documents are needed.
"""  # noqa: E501

