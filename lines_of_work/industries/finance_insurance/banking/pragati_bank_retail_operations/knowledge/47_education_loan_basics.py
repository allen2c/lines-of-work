title = "Education Loan Basics (Overview)"

content = """
Education loans help students and parents finance higher education
in India or abroad. Banks offer them under priority sector and
non-priority sector as per RBI norms.

**Eligibility:** Indian nationals pursuing approved courses in
recognised institutions. Co-borrower (parent or guardian) is usually
required. Age and course criteria apply as per the bank.

**Amount and margin:** Banks finance up to a certain percentage of
the course fee and living expenses; the rest is margin money from
the borrower. Margin may be lower for studies in India and for
weaker sections.

**Repayment:** Moratorium period (course period + 6–12 months after
job start) is common. Repayment starts after moratorium. Interest
may be charged during moratorium or subsidised as per scheme.

**Security:** For loans above a threshold, banks may require
collateral or third-party guarantee. Below that, only co-borrower
guarantee may be needed.

**Pragati Bank:** The retail agent can give a brief overview and
direct customers to the education loan desk or branch for
eligibility, documentation, and interest rates.
"""  # noqa: E501

