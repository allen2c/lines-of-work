title = "Passbook and Account Statement"

content = """
Passbooks and account statements provide a record of all transactions in
the account. They are essential for reconciliation and proof of transactions.

**Passbook:** A passbook is a physical booklet issued for savings accounts
(and some current accounts). The customer gets it updated at the branch
or at a passbook-printing kiosk. It shows date, narration, debit, credit,
and balance. Loss of passbook should be reported; a duplicate can be
issued on request with applicable charges.

**Account statement:** An electronic or printed statement lists transactions
over a chosen period. It can be downloaded from net banking or mobile
banking, or requested at the branch. Banks may charge for physical
statements beyond a certain number per year.

**Period:** Statements can usually be requested for the last 1 to 7 years
depending on bank policy and system retention.

**Discrepancy:** If the customer finds any wrong or unknown transaction,
they must report it to the branch or customer care immediately. The bank
will investigate as per RBI guidelines and its internal policy.
"""  # noqa: E501

