title = "OTP and Two-Factor Authentication"

content = """
OTP (One Time Password) is used by banks to verify the customer's
identity during login or transactions. It is a key part of two-factor
authentication (2FA).

**Usage:** OTP is sent to the registered mobile number (and sometimes
email) when the customer logs in, adds a payee, or confirms a
transaction. The OTP is valid for a short period (e.g. 2–10 minutes)
and must not be shared.

**Security:** Never share OTP with anyone, including people claiming
to be from the bank, technical support, or law enforcement. Do not
enter OTP on any website other than the bank's official app or
net banking page.

**If OTP not received:** Check network, ensure the mobile number is
correct and registered, and request resend after the cooldown. If
the number has changed, update it at the branch with KYC.

**Pragati Bank:** The retail agent can remind customers that OTP
is confidential and that the bank never asks for OTP on call or
email. For OTP issues, the customer can use the app or contact
the helpline.
"""  # noqa: E501

