title = "Demand Draft (DD) Process"

content = """
A demand draft is a negotiable instrument issued by the bank on
its own branch or another bank, directing the paying branch to
pay a specified amount to the payee or bearer. It is used for
safe transfer of money, especially when the payee is in another
city.

**How to get:** Customer fills the DD application form at the
branch, submits the amount plus charges, and provides payee name
and the paying branch (or city). The bank issues the DD. The
customer can send it by post or hand it over to the payee.

**Charges:** Banks charge for DD issuance; the amount may depend
on the DD value and whether it is on the same branch or another
bank. Charges are in the tariff.

**Payment:** The payee presents the DD at the paying branch with
identity proof. The amount is paid after verification. Lost DD
can be reported; duplicate may be issued after verification and
indemnity as per bank policy.

**Pragati Bank:** The retail agent can explain that DD can be
obtained at the branch and direct customers to the tariff for
charges and to the branch for the exact process.
"""  # noqa: E501

