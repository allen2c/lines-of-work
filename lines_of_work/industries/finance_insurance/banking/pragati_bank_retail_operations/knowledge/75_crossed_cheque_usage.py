title = "Crossed Cheque and Account Payee"

content = """
A crossed cheque has two parallel lines on the face. It cannot be
encashed over the counter; it can only be credited to a bank account.
This reduces the risk of misuse if the cheque is lost or stolen.

**Account payee:** Adding "Account Payee" or "A/C Payee" between
the lines (or "A/c payee only") restricts the payment to the
account of the payee named on the cheque. It should not be
transferred to another person. This is a common practice for
payments to suppliers, rent, etc.

**General crossing:** Only two parallel lines without "Account
Payee" means the cheque can be credited to any bank account of
the payee or their endorsee, but not encashed over the counter.

**Bearer cheque:** An uncrossed cheque can be encashed by the
bearer at the counter. Bearer cheques are riskier and are often
discouraged for large amounts.

**Pragati Bank:** The retail agent can explain that crossing
and "Account Payee" make the cheque safer and that customers
should use them for non-cash payments.
"""  # noqa: E501

