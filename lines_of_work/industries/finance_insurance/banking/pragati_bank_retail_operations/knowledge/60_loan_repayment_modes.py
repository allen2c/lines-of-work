title = "Loan Repayment Modes"

content = """
Banks offer multiple options for repaying loan EMIs and other dues.
Choosing a convenient mode ensures timely payment and avoids bounce
and penalty.

**ECS/NACH:** Mandate is given to debit the linked bank account
on the due date. Most common for salaried borrowers. Ensure
sufficient balance.

**Standing instruction (SI):** For loans from the same bank, SI
can be set to debit the savings account on the due date.

**Post-dated cheques (PDC):** Borrower issues cheques for each
EMI; the bank presents them on the due dates. Less common now
due to ECS/NACH.

**Manual payment:** Customer can pay at the branch (cash or
cheque) or through net banking/mobile banking by adding the
loan account as a payee and transferring the EMI amount before
the due date.

**Pragati Bank:** The retail agent can explain that EMI can be
paid via ECS, SI, or manual transfer and direct customers to
the branch or loan section for setting up the preferred mode.
"""  # noqa: E501

