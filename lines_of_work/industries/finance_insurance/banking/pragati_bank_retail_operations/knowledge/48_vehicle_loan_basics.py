content = """
Vehicle loans finance the purchase of new or used cars, two-wheelers,
or commercial vehicles. The loan is secured by the vehicle.

**Eligibility:** Salaried or self-employed individuals meeting age,
income, and credit criteria. The vehicle is hypothecated to the bank
until full repayment.

**Loan amount and margin:** Banks finance a percentage of the
on-road price (e.g. up to 85–90% for new cars). The rest is down
payment. Used vehicle loans may have a lower loan-to-value ratio.

**Tenure:** Typically 1 to 7 years for cars; two-wheeler loans
are shorter. Longer tenure means lower EMI but higher total interest.

**Documentation:** Identity, address, income proof, quotation or
invoice from dealer, and vehicle registration papers after purchase.
Insurance (comprehensive) is usually mandatory and may be arranged
through the bank.

**Pragati Bank:** The retail agent can give a brief overview and
direct customers to the vehicle loan section or branch for
eligibility, rates, and process.
"""  # noqa: E501

