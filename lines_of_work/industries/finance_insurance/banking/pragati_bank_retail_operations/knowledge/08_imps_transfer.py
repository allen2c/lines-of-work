title = "IMPS (Immediate Payment Service)"

content = """
IMPS is an instant inter-bank electronic funds transfer service that
works 24x7, including on bank holidays. It is useful for urgent
transfers of smaller amounts.

**Limit:** Per-transaction and per-day limits are set by each bank and
may vary by channel (mobile, net banking, ATM). Typical limits range
from Rs 1 lakh to Rs 5 lakh per transaction; daily limits may be higher.

**Timings:** IMPS is available round the clock. Credit is usually
instant to the beneficiary account.

**Charges:** Banks may charge a nominal fee for IMPS; the amount
varies by bank and channel. Check the current tariff at the branch
or on the bank's website.

**Requirements:** For transfer using MMID (Mobile Money Identifier):
mobile number and MMID of the beneficiary. For account-based
transfer: account number and IFSC. Beneficiary must be registered
for IMPS where applicable.

**Use case:** IMPS is ideal when NEFT batch delay is not acceptable
and the amount is within the limit. It is widely used for person-to-person
transfers and bill payments.
"""  # noqa: E501

