title = "Gold Loan Basics"

content = """
Gold loans are secured loans where the customer pledges gold ornaments or
coins with the bank. The loan amount is a percentage of the gold's value
(as per bank's valuation and LTV norms).

**Eligibility:** Any individual who owns gold and can provide identity
and address proof can apply. Minimum and maximum age criteria may apply.
Gold must meet the bank's purity and weight criteria.

**Valuation:** The bank gets the gold valued (in-house or through an
approved valuer). Loan amount is a percentage of the value (e.g. 75–80%)
as per RBI and bank policy.

**Tenure and interest:** Tenure can range from a few months to a few years.
Interest may be charged on a reducing or flat basis. The gold is returned
only after full repayment and closure of the loan.

**Storage:** Gold is stored in the bank's strong room. The customer
receives a receipt. Insurance is typically taken by the bank.

**Default:** If the loan is not repaid, the bank can auction the gold
after notice as per the loan agreement and legal requirements.

**Pragati Bank:** Branch staff can explain current LTV, tenure, rates,
and documentation for gold loans.
"""  # noqa: E501

