title = "Debit Card PIN Generation and Change"

content = """
The debit card PIN is required for ATM withdrawals and often for POS
transactions. It must be kept confidential and can be set or changed
through secure channels.

**First-time PIN:** When a new card is issued, the bank may send a PIN
mailer by post, or allow PIN generation at the ATM (insert card, choose
"Generate PIN", authenticate with OTP or other method), or through the
app. Follow the instructions that come with the card.

**Change PIN:** At an ATM: insert card, select "Change PIN", enter
current PIN and new PIN twice. Via app or net banking: go to card
services, select change PIN, authenticate with OTP, and set new PIN.
Banks encourage periodic PIN change for security.

**Forgot PIN:** If the PIN is forgotten, the customer can generate a
new PIN at the ATM (card and registered mobile required) or through
the app, as per bank process. No one from the bank will ask for the
PIN over phone or email.

**Pragati Bank:** Exact steps for PIN generation and reset are in the
welcome kit and on the bank's website or app.
"""  # noqa: E501

