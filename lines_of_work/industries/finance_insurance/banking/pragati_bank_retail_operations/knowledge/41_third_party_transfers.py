title = "Third-Party Transfers and Payee Registration"

content = """
Transferring funds to another person's account (third-party transfer)
requires the beneficiary to be added as a payee in most channels.
This reduces the risk of wrong credit.

**Adding a payee:** In net banking or the app, go to "Add beneficiary"
or "Manage payees". Enter the beneficiary's account number, IFSC,
name, and optionally a nickname. The bank may send an OTP or require
a small test transfer to confirm. Some banks impose a cooling-off
period before the first transfer to a new payee.

**Limits:** Per-transaction and daily limits apply as per the channel
(NEFT, IMPS, etc.) and bank policy.

**Safety:** Always verify the account number and IFSC. Wrong details
can credit the wrong account; recovery is difficult. Never share
OTP or passwords with anyone claiming to be from the bank.

**Pragati Bank:** The retail agent can explain that third-party
transfer requires payee registration and direct customers to the
app or net banking for the exact steps and limits.
"""  # noqa: E501

