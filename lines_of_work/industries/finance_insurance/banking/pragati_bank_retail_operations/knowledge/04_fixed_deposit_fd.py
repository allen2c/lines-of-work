title = "Fixed Deposit (FD) Overview"

content = """
A Fixed Deposit (FD) is a lump-sum deposit for a fixed tenure at a predetermined
interest rate. It offers safety and predictable returns for retail customers.

**Tenure:** Banks offer FDs from 7 days to 10 years; Pragati Bank's range is
as per the current product schedule. Longer tenures often carry higher rates
up to a limit; senior citizens may get an additional rate as per policy.

**Interest:** Interest is fixed at the time of booking. It can be paid at
maturity, monthly, or quarterly depending on the variant. Interest income
is taxable; TDS applies if interest exceeds the threshold in a financial year.

**Premature withdrawal:** Allowed subject to bank rules; a penalty (lower
interest rate for the period) is usually applied. Some tax-saving FDs have
a lock-in and no premature withdrawal.

**Loan against FD:** Customers can take a loan against the FD at a margin;
interest on the loan is typically a few points above the FD rate. This avoids
breaking the FD and losing interest.

**Renewal:** On maturity, the FD can be renewed at prevailing rates or
withdrawn. Auto-renewal options are available; customers should confirm
terms at the branch.
"""  # noqa: E501

