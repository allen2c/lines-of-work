title = "Nomination Facility for Bank Accounts"

content = """
Nomination allows the account holder to designate a person(s) to receive
the balance in the account and the contents of a safe deposit locker (if
any) in the event of the holder's death. It simplifies claim settlement.

**Who can nominate:** Individual account holders and joint account holders
(in accordance with the mode of operation) can make or change a nomination.
For joint accounts, the nomination is typically from the surviving holder(s).

**Number of nominees:** One or more nominees can be appointed. If there are
multiple nominees, the proportion of share should be specified; otherwise
equal share is assumed.

**Minor nominee:** A minor can be nominated, but a guardian must be
appointed to receive the amount on the minor's behalf until he or she
attains majority.

**Change or cancellation:** Nomination can be made, changed, or cancelled
any time by submitting the prescribed form at the branch. No fee is
usually charged.

**Pragati Bank:** Nomination form is available at the branch and on the
website. Filling it at the time of account opening or later is recommended.
"""  # noqa: E501

