title = "Premature Withdrawal of Fixed Deposit"

content = """
Premature withdrawal means closing an FD before the maturity date. Banks
allow it subject to a penalty, which is usually a reduction in the
interest rate payable.

**Penalty:** The bank may pay interest at a rate lower than the contracted
rate (e.g. 1% or 2% below the contracted rate, or the rate applicable
for the period the FD actually ran, whichever is lower). Some banks have
a minimum period (e.g. 3 months) before premature withdrawal is allowed.

**Tax-saving FDs:** These have a lock-in of 5 years. Premature withdrawal
is not permitted; only in case of death or other exceptional circumstances
may the bank allow it as per policy.

**Process:** Customer submits a request at the branch or through the
channel allowed by the bank. Identity verification is done. The principal
plus penalised interest (if any) is credited to the linked account or
paid as per instruction.

**Pragati Bank:** Exact penalty and process for premature withdrawal
are in the FD terms; branch staff can explain.
"""  # noqa: E501

