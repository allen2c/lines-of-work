title = "Nominee Update Process"

content = """
Customers can add, change, or cancel the nomination for their
account or deposit at any time by submitting the prescribed form.

**Form:** The bank's nomination form is available at the branch
and on the website. It requires the nominee's name, relationship,
address, and optionally share (if multiple nominees). The form
must be signed by the account holder(s) as per the mode of operation.

**Submission:** Submit the filled form at the branch with valid
identity. No fee is usually charged. The bank will update the
record and may give an acknowledgment.

**Multiple nominees:** If there are more than one, the proportion
of share should be specified; otherwise equal share is assumed.
A minor can be nominated with a guardian appointed to receive
the amount until the minor attains majority.

**Cancellation:** To cancel the nomination, submit a cancellation
form. To change, submit a new nomination form; it supersedes
the previous one.

**Pragati Bank:** The retail agent can direct customers to the
branch or website for the nomination form and explain that
nomination can be updated anytime.
"""  # noqa: E501

