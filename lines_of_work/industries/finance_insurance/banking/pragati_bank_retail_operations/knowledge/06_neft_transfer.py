title = "NEFT (National Electronic Funds Transfer)"

content = """
NEFT is a nationwide electronic funds transfer system operated by RBI in
batch settlements. It allows individuals and entities to transfer funds
between banks in India.

**Timings:** NEFT is available 24x7. Batches are settled at half-hourly
intervals. Credits are typically received within the same batch or the
next, subject to cut-off times and beneficiary bank.

**Limit:** There is no minimum or maximum limit set by RBI for NEFT.
Individual banks may set their own limits for retail channels; Pragati
Bank's limits are displayed in net banking, mobile banking, and at the branch.

**Charges:** RBI has mandated that no charges be imposed by banks for
NEFT for digital channels. Branch-initiated NEFT may attract charges
as per the bank's tariff.

**Requirements:** Beneficiary's account number, IFSC code of the branch
where the account is held, account type, and beneficiary name. Incorrect
IFSC or account number can cause failure or miscredit; customers must
verify details before confirming.
"""  # noqa: E501

