title = "Cash Deposit Limits and Reporting"

content = """
Banks are required to comply with anti-money laundering (AML) and
tax reporting norms. Large cash deposits may attract reporting or
documentation.

**No general prohibition:** There is no blanket limit on cash deposit
for a customer's own account. However, frequent or large cash
transactions may be monitored as per bank policy and regulatory
guidelines.

**Tax compliance:** Income Tax Act requires that cash deposits
totalling more than a specified amount in a financial year (e.g.
Rs 10 lakh or more in savings account) may be reported by the bank
or reflected in the annual information return. The customer should
ensure that the source of funds is legitimate and declared in tax
returns if applicable.

**KYC:** Updated KYC is essential. For very large cash transactions,
the bank may ask for source of funds or additional documents as
per AML policy.

**Pragati Bank:** The retail agent can inform customers that there
is no general cap on cash deposit but that tax and AML norms apply,
and direct them to the branch for large or frequent cash deposits.
"""  # noqa: E501

