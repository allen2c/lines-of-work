title = "Succession Certificate and Claim in Case of Death"

content = """
When an account holder dies, the balance and other assets are
released to the nominee or legal heir. If there is a valid
nomination, the nominee can claim after producing the death
certificate and completing the bank's process.

**With nomination:** Nominee submits the death certificate,
claim form, and identity proof. The bank verifies and releases
the balance to the nominee. This does not override the legal
rights of other heirs; it only enables the bank to discharge
its liability.

**Without nomination:** If there is no nomination or the nominee
is not claiming, the legal heir(s) may need to produce a
succession certificate, probate, or court order, or an indemnity
as per the bank's policy. This can take time and may require
legal assistance.

**Joint account:** For "Either or Survivor" or "Former or Survivor",
the survivor can operate and later close the account or transfer
to their name. Nomination by the survivor may be required for
claim after the survivor's death.

**Pragati Bank:** The retail agent can explain that nomination
simplifies claim and that in the absence of nomination, legal
heirs may need a succession certificate or similar. The branch
can provide the exact checklist for claim.
"""  # noqa: E501

