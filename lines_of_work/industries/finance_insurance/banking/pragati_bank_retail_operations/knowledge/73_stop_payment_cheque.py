title = "Stop Payment of Cheque"

content = """
Stop payment is an instruction to the bank not to pay a specific
cheque that the customer has issued. It is used when the cheque
is lost, or the payment should not be made for some reason.

**Process:** Customer gives a written instruction or uses the
app/net banking to stop the cheque. Details required: cheque
number, date, amount, and payee name. The instruction is valid
for a limited period (e.g. 6 months or 1 year); after that it
may need to be renewed. A charge may apply as per the tariff.

**Effect:** When the cheque is presented, the bank will dishonour
it with the reason "Stop payment by drawer." The customer should
inform the payee separately to avoid inconvenience.

**Cannot stop:** Once the cheque has been paid, stop payment cannot
be applied. If the cheque was lost and encashed by someone else,
the customer must report to the bank and may need to file a
complaint.

**Pragati Bank:** The retail agent can explain that stop payment
can be given at the branch or through the app and that a fee may
apply. Exact validity period is in the bank's policy.
"""  # noqa: E501

