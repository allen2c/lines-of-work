title = "Dormant and Inactive Account Reactivation"

content = """
Accounts with no customer-initiated transactions for a long period
(e.g. 12–24 months) may be classified as dormant or inactive. Banks
do this to manage risk and reduce fraud on unused accounts.

**Consequences:** Dormant accounts may have restricted operations—
e.g. no ATM or net banking until reactivation. Credits (salary, interest)
may still be allowed. The exact restrictions are in the bank's policy.

**Reactivation:** Customer must visit the branch with identity proof
and request reactivation. The bank may verify KYC and ask for a
fresh signature. After verification, the account is reactivated
and full access is restored.

**Charges:** Some banks charge for reactivation; others do not. Check
the tariff.

**Prevention:** Conduct at least one transaction (e.g. balance inquiry,
small transfer) periodically to keep the account active. Ensure
contact details are updated so that the bank can reach the customer.

**Pragati Bank:** The retail agent can explain that dormant accounts
need branch visit for reactivation and can advise customers to
transact periodically to avoid dormancy.
"""  # noqa: E501

