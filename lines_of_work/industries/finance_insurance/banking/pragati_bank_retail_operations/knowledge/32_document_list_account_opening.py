title = "Document Checklist for Account Opening"

content = """
A clear checklist of documents helps customers come prepared for
account opening and avoids multiple visits.

**Identity proof (any one):** Aadhaar, PAN, Passport, Voter ID,
Driving Licence, or NREGA job card with photo. Aadhaar and PAN
are most commonly used and suffice when linked.

**Address proof (any one):** Aadhaar (if address is current),
utility bill (not older than 3 months), Passport, rent agreement,
or certificate from a recognised authority.

**Photograph:** Recent passport-size photograph as per bank
specifications.

**For joint account:** KYC documents of all account holders and
signed mandate.

**For current account:** In addition, business registration proof,
GST certificate if applicable, and authority documents (board
resolution, partnership deed, etc.) as required by the bank.

**Pragati Bank:** The branch can provide a printed checklist or
link to the document list on the website. The retail agent can
summarise the list and advise customers to carry originals and
copies.
"""  # noqa: E501

