title = "Savings Account Opening Process"

content = """
Opening a savings account at Pragati Bank is the first step for most retail customers.
The process is designed to be simple while meeting RBI KYC norms.

**Eligibility:** Any individual who is a resident of India and is of majority age
(18 years or above) can open a savings account. Minors may open accounts with a
guardian; separate norms apply.

**Documents required:** Valid identity proof (Aadhaar, PAN, Passport, or Voter ID)
and address proof (Aadhaar, utility bill, or rent agreement). Recent passport-size
photographs are also required. For joint accounts, KYC documents of all holders
are needed.

**In-branch process:** The customer fills the account opening form, submits
documents for verification, and provides a specimen signature. The branch
conducts in-person verification (IPV) and may request biometric in line with
bank policy. Once verified, the account is opened and the customer receives
account number, passbook or welcome kit, and debit card (if applied for).

**Minimum balance:** Pragati Bank offers variants with different minimum balance
requirements; some accounts (e.g. under PMJDY) have zero balance facility.
"""  # noqa: E501

