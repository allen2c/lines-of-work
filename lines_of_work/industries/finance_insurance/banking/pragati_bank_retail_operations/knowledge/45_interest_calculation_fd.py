title = "Interest Calculation for Fixed Deposits"

content = """
FD interest can be calculated on a simple or compound basis depending
on the product. The frequency of compounding (quarterly, half-yearly,
or at maturity) affects the effective return.

**Simple interest:** Interest = Principal × Rate × Time. Used in some
short-term or special schemes.

**Compound interest:** Interest is calculated on principal plus
accrued interest at each compounding interval (e.g. quarterly).
Effective annual yield is higher than the stated rate when
compounding is more than once a year.

**Payout options:** Interest can be paid at maturity, monthly, or
quarterly. For monthly/quarterly payout, the amount is credited to
the linked account; for cumulative FD, interest is reinvested and
paid at maturity.

**TDS:** If interest exceeds the threshold in a financial year, TDS
is deducted. Form 15H/15G can be submitted to avoid TDS if total
income is below taxable limit.

**Pragati Bank:** The exact formula and payout options are in the
FD terms. The retail agent can direct customers to the branch or
website for interest calculation examples.
"""  # noqa: E501

