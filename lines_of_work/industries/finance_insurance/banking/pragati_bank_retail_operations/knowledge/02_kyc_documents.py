title = "KYC Documents for Bank Accounts"

content = """
Know Your Customer (KYC) is mandated by RBI to prevent fraud and money laundering.
Banks must collect and periodically update KYC details for all account holders.

**Identity proof (any one):** Aadhaar, PAN, Passport, Voter ID, Driving Licence,
or NREGA job card with photograph. For individuals, Aadhaar and PAN are most
commonly used and often sufficient when linked.

**Address proof (any one):** Aadhaar, utility bill (not older than three months),
rent agreement, Passport, or certificate from a recognised authority. Aadhaar
serves as both identity and address proof when it contains the current address.

**Photograph:** Recent passport-size photograph as per bank specifications.

**For joint accounts:** KYC documents of all account holders are required.
For accounts of minors, guardian's KYC and proof of relationship are needed.

**Periodic KYC update:** Banks may request re-submission of documents or
re-verification at intervals. Customers are notified by the branch or through
SMS/email. Non-compliance can lead to restrictions on the account.
"""  # noqa: E501

