title = "Positive Pay for Cheques"

content = """
Positive Pay is a fraud-prevention measure where the account holder
pre-registers cheque details (number, date, amount, payee name) with
the bank before the cheque is presented. The bank matches the
presented cheque with the registered details before payment.

**Process:** Customer submits cheque details through net banking,
mobile app, or at the branch before or when issuing the cheque.
When the cheque is presented for payment, the bank verifies against
the registered data.

**Mandatory for high value:** RBI has mandated Positive Pay for
cheques of Rs 50,000 and above for some banks. Exact threshold
and implementation are as per RBI circulars and bank policy.

**Benefit:** Reduces the risk of payment against forged or altered
cheques. Customers should register details accurately; mismatch
can lead to dishonour.

**Pragati Bank:** The retail agent can explain that Positive Pay
is used for cheque safety and direct customers to the app or
branch for registration and threshold.
"""  # noqa: E501

