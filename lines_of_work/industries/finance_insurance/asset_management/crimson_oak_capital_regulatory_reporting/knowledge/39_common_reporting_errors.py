title = "Common Reporting Errors"

content = """
Certain errors recur across regulatory submissions. Awareness helps prevent them.

**Data Quality:** Duplicate records, missing fields, incorrect units (e.g., thousands vs
millions), wrong currency. Implement validation rules and reconciliation checks.

**Timing:** Using incorrect reference dates, submitting after the deadline, or using
stale data. Maintain a calendar of deadlines and reference dates.

**Classification:** Wrong instrument type, jurisdiction, or entity classification.
Reference data and mapping logic must be kept up to date.

**Rounding and Aggregation:** Inconsistent rounding can cause totals to not tie. Define
rounding rules and apply them consistently.
"""  # noqa: E501

