title = "Best Execution Reporting"

content = """
Best execution reporting demonstrates that firms have taken sufficient steps to obtain
the best possible result for clients when executing orders. Regulators require evidence
of policy, monitoring, and periodic review.

**Policy Requirements:** Firms must have a written best execution policy covering
execution venues, factors considered, and how client orders are handled. The policy
must be disclosed to clients and reviewed annually.

**Monitoring and Evidence:** Firms must monitor execution quality across venues and
retain records. MiFID II requires RTS 27 and RTS 28 reports for execution quality
and venue selection. Asset managers must document how they achieve best execution.

**Client Reporting:** Clients must receive information on execution quality. Firms
should explain how they assess and improve execution outcomes.
"""  # noqa: E501

