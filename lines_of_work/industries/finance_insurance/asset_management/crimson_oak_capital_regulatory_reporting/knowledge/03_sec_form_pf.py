title = "SEC Form PF"

content = """
Form PF is required for SEC-registered investment advisers that advise one or more private
funds and have at least $150 million in private fund assets under management. It provides
the SEC and FSOC with systemic risk visibility.

**Filing frequency:** Large hedge fund advisers ($1.5B+ AUM) file quarterly. Large liquidity
fund advisers file monthly. All other qualifying advisers file annually.

**Sections:** The form collects data on fund strategy, leverage, counterparty exposure,
liquidity, investor concentration, and performance. Large private equity advisers report
on portfolio company financing and strategy.

**Confidentiality:** Form PF information is generally confidential. It is shared with the
CFTC for dually registered advisers and used for regulatory purposes. Advisers must maintain
accurate books and records supporting the filing.
"""  # noqa: E501

