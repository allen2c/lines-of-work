title = "Regulatory Liaison"

content = """
Regulatory liaison is the formal channel between the firm and regulators. The liaison
coordinates examinations, responds to information requests, and maintains ongoing
dialogue on interpretation and implementation.

**Designated contact:** A single point of contact or small team should be designated
for each major regulator. This ensures consistent messaging and avoids conflicting
responses. The liaison must have authority to commit the firm and access to senior
management when needed.

**Examination coordination:** When regulators schedule visits or request documents,
the liaison manages logistics, timelines, and internal coordination. All responses
should be reviewed for accuracy and completeness before submission.

**Proactive engagement:** For significant rule changes or interpretive questions,
the liaison may engage regulators in advance to clarify expectations. Such
engagement should be documented and shared with compliance and legal.
"""  # noqa: E501

