title = "Audit Trail Requirements"

content = """
Regulators require firms to maintain audit trails that capture the full lifecycle
of transactions and decisions. Audit trails support investigations, examinations,
and dispute resolution.

**What to Capture:** Transaction details, timestamps, user identifiers, system
changes, and approvals. Records must be tamper-evident and retained for the
required period (often seven years or more).

**Access and Retrieval:** Audit data must be retrievable within a reasonable
timeframe. Regulators may request specific records during examinations. Firms
must demonstrate that audit trails are complete and reliable.

**Retention:** Retain records per regulatory requirements. Ensure migration
and archiving preserve integrity. Deletion before the retention period expires
is a compliance breach.
"""  # noqa: E501

