title = "Position Limits"

content = """
Position limits restrict the size of positions in certain derivatives and commodities to
prevent manipulation and excessive speculation. MiFID II and CFTC rules impose limits.

**Scope:** Commodity derivatives, especially agricultural and energy. Spot-month and
single-month limits. Aggregation across related accounts.

**Exemptions:** Hedging exemptions may apply. Bona fide hedging must be documented.
Pass-through and other carve-outs exist but require careful analysis.

**Reporting:** Breach of limits triggers reporting. Firms must monitor positions against
limits and maintain audit trails. Cross-border aggregation adds complexity.
"""  # noqa: E501

