title = "Training and Competency for Regulatory Reporting"

content = """
Staff involved in regulatory reporting must have appropriate training and
competency. Regulators expect documented training programs and evidence of
understanding.

**Role-based training:** Tailor training to responsibilities. Data preparers need
different depth than reviewers or approvers. New joiners should complete
training before handling reports.

**Refresher training:** Conduct periodic refreshers when rules change or
annually. Track completion and address gaps.

**Competency assessment:** For critical roles, consider assessments or
certifications. Document competency requirements in role descriptions.
"""  # noqa: E501

