title = "Transitional Provisions in Regulatory Rules"

content = """
New or amended rules often include transitional provisions: phased implementation,
grandfathering, or temporary exemptions. Understanding these is critical to
avoid premature or delayed compliance.

**Phased implementation:** Some rules apply in stages (e.g., by size or date).
Confirm which phase applies to the firm and when each phase takes effect.

**Grandfathering:** Existing arrangements may be exempt from new requirements
under certain conditions. Document eligibility and monitor for changes that
could end grandfathering.

**Grace periods:** Short-term exemptions or extended deadlines may apply. Do not
rely on grace periods for long-term planning; use them to complete implementation.
"""  # noqa: E501

