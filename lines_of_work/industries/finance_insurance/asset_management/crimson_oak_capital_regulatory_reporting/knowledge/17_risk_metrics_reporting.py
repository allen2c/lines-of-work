content = """
Regulatory reports require standardised risk metrics: VaR, leverage, concentration,
sensitivity measures. Consistency and accuracy are critical.

**VaR:** Value at Risk at specified confidence levels. Historical, parametric, or
Monte Carlo. Back-testing requirements. Disclosure in Annex IV and Form PF.

**Leverage:** Gross and net leverage. Commitment method vs notional. On- and
off-balance-sheet. AIFMD and Form PF have specific definitions.

**Concentration:** Single-name, sector, geography. Limits and breaches. Reporting
of top exposures.

**Data quality:** Risk metrics depend on accurate position and pricing data. Garbage
in, garbage out. Validate inputs and outputs.
"""  # noqa: E501

