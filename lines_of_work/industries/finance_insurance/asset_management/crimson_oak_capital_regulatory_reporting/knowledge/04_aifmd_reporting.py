title = "AIFMD Reporting"

content = """
The Alternative Investment Fund Managers Directive (AIFMD) governs EU managers of alternative
investment funds. Reporting under AIFMD is submitted to national competent authorities
(NCAs) and shared with ESMA.

**Annex IV:** The main reporting template. Frequency depends on AUM: above EUR 500M (leveraged)
or EUR 1B (unleveraged) requires quarterly reporting; below those thresholds, annual.

**Content:** Annex IV covers leverage, liquidity, risk metrics, concentration, and valuation.
Managers must report on principal markets, main instruments, and investor information.

**Depositary:** AIFMD requires a depositary for each AIF. The depositary holds assets and
oversees the manager. Reporting coordination with the depositary is often necessary.

**Third-country managers:** Non-EU managers marketing in the EU under national private
placement regimes may have reduced or different reporting obligations depending on the
member state.
"""  # noqa: E501

