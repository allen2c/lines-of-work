title = "Data Quality for Regulatory Reporting"

content = """
Regulatory reporting depends on accurate, complete, and timely data. Poor data
quality leads to incorrect submissions, regulatory findings, and reputational risk.

**Data Governance:** Establish ownership, definitions, and lineage for regulatory
data. Source systems must be reliable and consistent. Data dictionaries and
mapping documents support traceability.

**Validation Rules:** Implement pre-submission validation for format, completeness,
and business rules. Reject or flag records that fail validation before submission.
Maintain an audit trail of corrections and overrides.

**Reconciliation:** Reconcile regulatory outputs to source systems and management
reports. Investigate and resolve discrepancies. Document reconciliation procedures
and frequency.
"""  # noqa: E501

