title = "Internal Controls for Regulatory Reporting"

content = """
Internal controls ensure that regulatory reports are accurate, complete, and timely.
Controls should be designed to prevent errors, detect them when they occur, and
correct them before submission.

**Segregation of duties:** Data preparation, review, and submission should involve
different individuals where practicable. This reduces the risk of undetected errors
or intentional misstatement.

**Reconciliation:** Reported figures should be reconciled to source systems and
management accounts. Discrepancies must be investigated and resolved before filing.

**Review and approval:** A designated approver should sign off on each report before
submission. The approver must have sufficient knowledge to assess reasonableness.
"""  # noqa: E501

