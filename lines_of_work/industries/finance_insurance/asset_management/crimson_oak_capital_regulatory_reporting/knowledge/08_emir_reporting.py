title = "EMIR Reporting"

content = """
EMIR (European Market Infrastructure Regulation) requires reporting of derivatives
contracts to trade repositories. The obligation applies to EU counterparties and
non-EU entities with a sufficient EU nexus.

**What to report:** All OTC derivatives and exchange-traded derivatives must be
reported. Fields include counterparty data, contract details, valuation, and
collateral. Both counterparties typically report; duplication is resolved via
reconciliation.

**Timing:** T+1 for most reports. Backloading of historic contracts was required
for contracts outstanding at implementation. Life-cycle events (modifications,
terminations) must be reported.

**Trade repositories:** Reports go to an authorised or recognised trade repository.
ESMA maintains a list. Data is available to NCAs and ESMA for supervision.
"""  # noqa: E501

