title = "Governance for Regulatory Reporting"

content = """
Governance defines accountability and oversight for regulatory reporting. The board
or a committee should receive regular updates on reporting obligations, deadlines,
and any material issues or breaches.

**Ownership:** A senior manager should be formally accountable for regulatory
reporting. This person ensures adequate resources, processes, and controls are
in place.

**Committee oversight:** A compliance or risk committee typically reviews reporting
metrics, breach summaries, and regulatory developments. Minutes should document
decisions and follow-up actions.

**Escalation to board:** Material breaches, enforcement actions, or systemic
deficiencies should be escalated to the board with a clear remediation plan.
"""  # noqa: E501

