title = "Third-Party Data Providers for Regulatory Reporting"

content = """
Regulatory reporting often relies on third-party data: market data, reference data,
pricing, and regulatory feeds. Quality and timeliness of this data directly affect
reporting accuracy.

**Reference Data:** ISINs, LEIs, classifications, and entity hierarchies. Errors here
propagate through reports. Validate and reconcile against authoritative sources.

**Pricing:** Fair value and mark-to-market for illiquid or complex instruments. Document
pricing policies and challenge third-party prices when appropriate.

**Regulatory Feeds:** Some regulators provide reference data or validation services.
Integrate these into the reporting workflow to catch errors before submission.
"""  # noqa: E501

