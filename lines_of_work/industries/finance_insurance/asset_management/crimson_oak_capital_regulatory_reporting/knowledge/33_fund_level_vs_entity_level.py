title = "Fund-Level vs Entity-Level Reporting"

content = """
Regulatory reporting occurs at different levels. Fund-level reporting focuses on the
investment vehicle (e.g., a UCITS fund, a hedge fund). Entity-level reporting focuses
on the management company or investment adviser.

**Fund-Level:** NAV, holdings, leverage, liquidity, investor breakdown. Regulators assess
fund risk and investor protection. Required for UCITS, AIFMD, Form PF (private funds).

**Entity-Level:** AUM, client count, organizational structure, conflicts of interest,
compliance programs. Regulators assess firm-level risk and conduct. Required for Form ADV,
MiFID II authorization, and similar.

**Mapping:** A single management company may run multiple funds. Entity-level data is
reported once; fund-level data is reported per fund. Ensure consistency between the two.
"""  # noqa: E501

