title = "Remediation of Regulatory Findings"

content = """
When regulators or internal audit identify deficiencies, remediation must be
prompt, thorough, and documented. Incomplete remediation can lead to enforcement
or repeated findings.

**Root cause analysis:** Identify why the deficiency occurred. Address underlying
process, system, or control gaps, not just symptoms.

**Remediation plan:** Define actions, owners, and deadlines. Track progress and
escalate delays. Obtain sign-off when complete.

**Verification:** Confirm that remediation effectively addresses the finding.
Consider independent review for material issues.
"""  # noqa: E501

