title = "Escalation Procedures for Regulatory Reporting"

content = """
When reporting issues arise, clear escalation procedures ensure timely resolution and
appropriate communication.

**Thresholds:** Define what constitutes a minor error vs a material breach. Material
breaches may require immediate notification to the regulator and senior management.

**Roles:** Identify who is responsible for triage, remediation, and regulatory
communication. Document escalation paths and contact details.

**Timelines:** Set internal deadlines well before regulatory deadlines. If a submission
will be late, assess whether advance notification to the regulator is required.

**Documentation:** Record all escalations, decisions, and remedial actions for audit
and regulatory examination.
"""  # noqa: E501

