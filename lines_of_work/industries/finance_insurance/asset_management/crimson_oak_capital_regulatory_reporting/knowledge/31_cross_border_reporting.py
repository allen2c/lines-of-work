title = "Cross-Border Reporting"

content = """
Asset managers operating across jurisdictions must comply with reporting obligations in each
relevant country. Cross-border reporting involves identifying which regimes apply based on
fund domicile, investor residence, and trading venue location.

**Jurisdictional Triggers:** A fund domiciled in the EU with US investors may trigger both
EU (AIFMD, UCITS) and US (Form PF, Form ADV) reporting. Trading on US exchanges or
holding US securities adds further obligations.

**Consolidation Challenges:** Data may need to be aggregated differently for each regulator.
De-duplication and mapping of entities across regimes is essential to avoid double-counting
or gaps.

**Delegation and Sub-Managers:** When investment management is delegated to a sub-manager
in another jurisdiction, responsibility for reporting may be shared. Clear agreements and
data-sharing protocols are required.
"""  # noqa: E501

