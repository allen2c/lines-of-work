title = "Client Asset Segregation"

content = """
Client asset segregation ensures that client funds and securities are held separately
from the firm's own assets. Breach of segregation rules is a serious regulatory
and operational failure.

**Custody Rules:** Firms holding client assets must comply with custody and
safeguarding rules. Assets must be identifiable and protected from firm insolvency.
Segregation may be statutory or omnibus depending on jurisdiction.

**Reconciliation:** Daily reconciliation between internal records and custodian
statements is mandatory. Discrepancies must be investigated and resolved promptly.
Regulators expect documented procedures and escalation paths.

**Reporting:** Segregation status, custody arrangements, and any breaches must be
reported. CASS (UK) and similar regimes require specific disclosures and attestations.
"""  # noqa: E501

