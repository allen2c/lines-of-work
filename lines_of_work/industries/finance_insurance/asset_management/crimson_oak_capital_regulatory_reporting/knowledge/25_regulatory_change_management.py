content = """
Regulatory change management is the process of identifying, assessing, and implementing
new or amended regulatory requirements. Asset managers face a constant stream of
regulatory updates across multiple jurisdictions.

**Horizon Scanning:** Monitor regulatory publications, consultation papers, and
industry updates. Establish relationships with legal and compliance teams and
external advisers. Track implementation deadlines and dependencies.

**Impact Assessment:** For each change, assess applicability, scope, and effort.
Determine which systems, processes, and reports are affected. Prioritize by
deadline and materiality.

**Implementation:** Document changes, update procedures, train staff, and validate
before go-live. Post-implementation review confirms that requirements are met
and controls are effective.
"""  # noqa: E501

