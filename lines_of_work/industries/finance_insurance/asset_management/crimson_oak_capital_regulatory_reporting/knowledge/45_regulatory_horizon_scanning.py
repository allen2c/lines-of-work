title = "Regulatory Horizon Scanning"

content = """
Horizon scanning identifies upcoming regulatory changes that may affect reporting
obligations. Early awareness allows the firm to plan, budget, and implement changes
before deadlines.

**Sources:** Regulator websites, consultation papers, industry associations,
legal and compliance bulletins, and peer networks. Assign ownership for monitoring
each jurisdiction.

**Assessment:** For each proposed change, assess impact on the firm: new reports,
modified reports, system changes, or process changes. Prioritize by significance
and deadline.

**Action plan:** Develop implementation timelines, assign owners, and track progress.
Engage with regulators during consultation periods where appropriate.
"""  # noqa: E501

