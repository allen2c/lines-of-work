title = "Regulatory Framework Overview for Asset Managers"

content = """
Asset managers operate under a complex web of regulations that vary by jurisdiction and fund type.
Understanding the framework is essential for accurate and timely reporting.

**SEC (US):** The Securities and Exchange Commission oversees investment advisers and funds under
the Investment Advisers Act and Investment Company Act. Form ADV, Form PF, and Form N-PORT are
among the key reporting instruments for registered advisers and private funds.

**ESMA (EU):** The European Securities and Markets Authority coordinates EU-wide rules. AIFMD
(Alternative Investment Fund Managers Directive) and UCITS govern fund managers. MiFID II
covers markets and transparency. EMIR and SFTR address derivatives and securities financing.

**FCA (UK):** Post-Brexit, the Financial Conduct Authority maintains UK-specific rules aligned
with EU frameworks where appropriate. CASS (Client Assets) and SM&CR (Senior Managers) apply.

**Cross-border:** Managers with global operations must map obligations across jurisdictions,
identify overlaps, and avoid gaps. Delegation and outsourcing add complexity.
"""  # noqa: E501

