title = "Consolidated Reporting"

content = """
Consolidated reporting aggregates data across multiple entities, funds, or sub-funds into
a single regulatory submission. This is common for group structures, umbrella funds, and
master-feeder arrangements.

**Group-Level Aggregation:** Parent entities may be required to report on a consolidated
basis for capital, liquidity, or systemic risk purposes. Subsidiaries and branches must
feed data into the group reporting process.

**Umbrella Funds:** A single legal entity with multiple sub-funds may report at the
umbrella level or per sub-fund depending on the regime. NAV, positions, and exposures
must be correctly attributed.

**Master-Feeder:** Feeder funds invest in a master fund. Reporting may be at the master
level, with feeder-level disclosures for investor-facing documents. Avoid double-counting
assets.
"""  # noqa: E501

