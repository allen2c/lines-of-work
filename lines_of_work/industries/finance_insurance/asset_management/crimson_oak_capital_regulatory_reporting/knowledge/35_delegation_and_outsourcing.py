title = "Delegation and Outsourcing in Regulatory Reporting"

content = """
Asset managers may delegate or outsource parts of the reporting process. Regulators
expect the delegating entity to retain responsibility and oversight.

**Responsibility:** The regulated entity remains accountable. Delegation does not
transfer liability. The firm must have adequate controls and monitoring of the delegate.

**Due Diligence:** Before delegating, assess the delegate's capability, resources, and
compliance. Document the assessment and review it periodically.

**Data Access:** The delegate must have access to accurate, timely data. Define SLAs,
escalation paths, and audit rights. Ensure the delegate can meet regulatory deadlines.
"""  # noqa: E501

