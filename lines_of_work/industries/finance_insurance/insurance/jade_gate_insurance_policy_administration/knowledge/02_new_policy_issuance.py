content = """
新单承保是保单管理中的首要环节，指核保通过后由保单管理部门完成系统录入、校验与出单的流程。确保录入信息与核保结论、投保单一致，是后续批改与理赔的基础。

**承保前校验**：核对投保单、核保意见书、保费到账凭证。确认被保险人身份、保险标的、保险金额、保险期间、特别约定等与核保结论一致。发现不符时及时与核保沟通，不得擅自修改。

**系统录入**：按照系统字段要求逐项录入，注意险种、条款版本、费率适用是否正确。录入后执行系统校验规则，排除逻辑错误与必填项遗漏。

**出单与送达**：生成正式保单（电子或纸质），加盖承保章或电子签章。按约定方式送达投保人，并保留送达记录。批量业务需按渠道要求分批出单、对账。
"""

