content = """
续保指保险期间届满前或届满时，经双方同意延续原保单保障的行为。续保处理需遵循公司规则与监管要求，确保续保清单准确、提醒及时、出单合规。

**续保清单生成**：系统根据到期日、续保规则自动生成续保清单。需排除已明确不续保、标的灭失、拒保等情形。清单需经复核，避免漏续或错续。

**续保提醒**：在保单到期前规定天数（如 30、60 天）向投保人发送续保提醒，说明续保条件、预估保费及续保方式。提醒记录需留存备查。

**续保出单**：收到投保人续保意愿及保费后，按新单承保流程录入并出单。续保保单号可沿用或按规则重新编排，需在系统中建立与原保单的关联，便于历史追溯。
"""

