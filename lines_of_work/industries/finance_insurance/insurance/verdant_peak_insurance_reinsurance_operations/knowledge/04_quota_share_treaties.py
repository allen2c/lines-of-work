title = "Quota Share Treaties"

content = """
A quota share treaty cedes a fixed percentage of every risk within scope. If the treaty is 50% quota share, the cedent cedes 50% of premium and 50% of every loss.

**Simplicity:** Quota share is straightforward to administer. Premium and loss allocation require no complex calculations. Bordereaux list each policy with the ceded percentage.

**Uses:** Quota share provides surplus relief, capacity for growth, and profit-sharing with reinsurers. It is common for new lines or when the cedent seeks to reduce net retentions across the board.

**Limitations:** Cedent cedes both small and large losses proportionally. There is no severity protection. Profit commission may offset cost for profitable portfolios.
"""  # noqa: E501

