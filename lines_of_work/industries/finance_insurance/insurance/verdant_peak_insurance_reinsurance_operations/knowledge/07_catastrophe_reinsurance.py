title = "Catastrophe Reinsurance"

content = """
Catastrophe (cat) reinsurance protects insurers against losses from natural or man-made catastrophes: earthquakes, hurricanes, floods, terrorism, or other events causing widespread damage.

**Per Occurrence Basis:** Cat covers typically attach per occurrence. A single event triggers the cover regardless of how many policies or claims are affected.

**Attachment and Limit:** The cedent retains losses up to the attachment point. The reinsurer pays from attachment to the treaty limit. Reinstatement provisions may allow restoration of coverage after a loss.

**Pricing:** Cat premium is often a percentage of subject premium (property premium in affected zones) or based on catastrophe models. Pricing varies significantly by geography and exposure.

**Placement:** Cat programs often involve multiple reinsurers and layers. Placement may be treaty, facultative, or a combination. Timeline and capacity can be tight at renewal.
"""  # noqa: E501

