title = "Occurrence vs Claims-Made"

content = """
Insurance and reinsurance may be written on occurrence or claims-made bases. The basis affects when losses attach and how reinsurance responds.

**Occurrence:** Coverage attaches when the loss occurs, regardless of when the claim is reported. Reinsurance follows the same basis. Long-tail losses may arise years after policy expiry.

**Claims-Made:** Coverage attaches when the claim is made (reported) during the policy period. Tail coverage may extend the reporting period. Reinsurance is typically claims-made to match.

**Matching:** Cedents and reinsurers align bases to avoid coverage gaps. Mismatch can create disputes over whether a loss falls within the reinsurance period.
"""  # noqa: E501

