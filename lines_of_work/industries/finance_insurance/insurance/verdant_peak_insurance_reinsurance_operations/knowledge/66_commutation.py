title = "Commutation"

content = """
Commutation is the early termination of reinsurance obligations through a lump-sum payment. It settles all future premium and loss obligations between the parties.

**When Used:** Run-off situations, dispute resolution, or simplification of portfolios. Both parties must agree. Actuarial valuation determines the commutation amount.

**Process:** Actuaries value outstanding liabilities. Parties negotiate the commutation payment. Agreement is documented. Payments are made and obligations are released.

**Benefits:** Cedents eliminate future administration and credit risk. Reinsurers achieve finality. Both gain certainty.
"""  # noqa: E501

