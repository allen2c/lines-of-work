title = "Bordereau Preparation"

content = """
A bordereau is a detailed schedule of cessions submitted to reinsurers. It forms the basis for premium and loss accounting and must be accurate and timely.

**Content:** Premium bordereaux list policies or risks with premium, cession percentage or amount, and ceded premium. Loss bordereaux list claims with loss amounts, cession, and ceded loss.

**Frequency:** Treaty terms specify submission frequency: monthly, quarterly, or annually. Late submission may breach contract and delay settlements.

**Format:** Standard formats exist (e.g., LIRMA, Lloyd's). Cedents may use proprietary formats if agreed. Data quality is paramount; errors cause reconciliation issues and may affect cover.
"""  # noqa: E501

