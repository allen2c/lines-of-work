title = "Proportional vs Non-Proportional Reinsurance"

content = """
Reinsurance structures fall into two broad categories: proportional and non-proportional. Understanding the distinction is essential for program design and administration.

**Proportional Reinsurance:** Cedent and reinsurer share premium and losses in a fixed proportion (e.g., 80% ceded, 20% retained). Quota share and surplus treaties are proportional. The reinsurer participates in every loss, regardless of size.

**Non-Proportional Reinsurance:** The reinsurer pays only when losses exceed an attachment point. Excess of loss and stop-loss treaties are non-proportional. Premium is typically a percentage of subject premium or a fixed amount, not a share of each policy premium.

**Choosing the Structure:** Proportional reinsurance provides capacity and surplus relief; non-proportional protects against severity and catastrophe. Most programs combine both.
"""  # noqa: E501

