title = "Property Claims Handling"

content = """
Property claims handling encompasses the assessment and settlement of damages to real and personal property, requiring knowledge of construction, repair costs, and property valuation principles.

**Property Inspection:** Arrange for qualified inspection of damaged property, including structural assessment, systems evaluation, and content inventory. Document pre-loss condition and current damage state with detailed photography.

**Scope of Damage:** Identify all damaged components including structural elements, mechanical systems, finishes, and personal property. Distinguish between direct physical damage and consequential losses.

**Repair vs. Replacement:** Determine whether repair or replacement represents the most appropriate recovery method based on damage extent, property age, and cost-effectiveness considerations.

**Cost Estimation:** Obtain multiple repair estimates from licensed contractors when damage exceeds predetermined thresholds. Evaluate estimate completeness, accuracy, and compliance with local building codes.

**Depreciation Application:** Apply appropriate depreciation factors to determine actual cash value settlements. Consider property age, condition, and market factors affecting depreciation calculations.

**Code Upgrade Requirements:** Account for building code changes requiring upgrades beyond pre-loss condition. Negotiate fair contribution toward code compliance costs when mandated by law.

**Additional Living Expenses:** Evaluate temporary housing needs and associated costs when property damage renders premises uninhabitable. Verify reasonable duration and expense levels based on repair timelines.
"""

