title = "Liquidity Risk"

content = """
Liquidity risk is the inability to execute trades or meet obligations
without excessive cost. Trading desks manage liquidity in normal and
stress conditions.

**Market Liquidity** varies by security, time, and size. Bid-ask
spreads and depth indicate liquidity. Illiquid positions are harder
to unwind.

**Funding Liquidity** addresses the ability to meet cash obligations.
Margin calls and settlement require adequate funding.

**Stress** can cause liquidity to evaporate. Contingency plans assume
reduced liquidity and plan position reduction or hedging.

**Monitoring** of liquidity metrics supports proactive management.
Early warning indicators help avoid forced selling.
"""

