title = "Common Failure Modes"

content = """
Common failure modes in trading operations include recurring
errors and control weaknesses. Awareness supports prevention
and rapid response.

**Fat Finger** errors involve wrong quantity, price, or symbol.
Pre-trade checks and order size limits mitigate risk.

**Settlement Fails** from incorrect instructions or insufficient
position are common. Straight-through processing and
reconciliation reduce occurrences.

**Limit Breaches** occur when monitoring fails or limits are
inadequate. Real-time systems and escalation support control.

**Communication** failures with clients or counterparties
cause confusion. Confirmation and status updates reduce risk.

**Post-Incident Review** identifies root causes and improves
controls for future prevention.
"""

