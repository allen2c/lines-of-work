content = """
Portfolio risk metrics quantify exposure to market movements and support limit
enforcement and reporting. Traders and risk managers use these metrics daily.

**Value at Risk (VaR)** estimates potential loss over a horizon at a given confidence
level. Historical, parametric, and simulation methods each have strengths and limitations.

**Stress Testing** applies historical or hypothetical scenarios to assess portfolio
behavior under extreme conditions. Complement VaR by capturing tail risk.

**Greeks** measure sensitivity of options and derivatives to underlying price, volatility,
time, and rates. Delta, gamma, theta, and vega inform hedging decisions.

**Concentration Risk** measures overexposure to single names, sectors, or regions.
Diversification limits reduce concentration risk.

**Leverage Ratios** track gross and net exposure relative to capital. Regulators and
internal policies impose leverage constraints.
"""

