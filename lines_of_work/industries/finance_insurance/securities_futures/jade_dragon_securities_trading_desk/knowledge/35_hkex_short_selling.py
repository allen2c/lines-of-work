title = "HKEX Short Selling Rules"

content = """
Short selling on HKEX is subject to designated securities lists and
reporting requirements. Compliance prevents regulatory and operational
issues.

**Designated Securities** are approved for short selling. The list is
published and updated. Short selling non-designated securities is
prohibited.

**Reporting** of short positions may be required. Thresholds and
timing follow SFC and HKEX rules.

**Borrow Requirement** means short sellers must have arranged borrow
before selling. Naked short selling is generally prohibited.

**Tick Rule** or similar restrictions may apply in some markets.
Hong Kong rules should be verified for current requirements.
"""

