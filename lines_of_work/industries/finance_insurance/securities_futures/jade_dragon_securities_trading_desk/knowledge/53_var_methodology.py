title = "VaR Methodology"

content = """
Value at Risk (VaR) methodology quantifies potential loss over a
horizon at a confidence level. VaR supports risk limits and
reporting.

**Historical VaR** uses historical returns to estimate distribution.
Simpler but may not capture regime changes.

**Parametric VaR** assumes normal or other distribution. Variance-
covariance approach is common. May underestimate tail risk.

**Monte Carlo VaR** simulates scenarios. Flexible for complex
portfolios and non-linear payoffs. Computationally intensive.

**Limitations** include model risk and failure to capture
extreme tail events. Stress testing complements VaR.
"""

