title = "Best Execution Standards"

content = """
Best execution requires firms to take all sufficient steps to obtain the best possible
result for client orders, considering price, costs, speed, and likelihood of execution.

**Policy** must be documented and disclosed to clients. The policy describes how the
firm achieves best execution and the venues used.

**Factors** include price, costs, speed, likelihood of execution, settlement, and order
size. Relative importance varies by order type and client circumstances.

**Venue Selection** considers liquidity, fees, technology, and execution quality.
Regular venue analysis supports informed routing decisions.

**Execution Quality Reports** measure performance against benchmarks. Firms use these
reports for continuous improvement and client disclosure.

**Client Instructions** may override normal routing when clients specify venues or
constraints. Document instructions and ensure they align with best execution.
"""

