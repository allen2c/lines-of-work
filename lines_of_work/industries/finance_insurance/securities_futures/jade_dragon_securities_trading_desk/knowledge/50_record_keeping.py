title = "Record Keeping"

content = """
Record keeping preserves evidence of trading decisions, client
instructions, and regulatory compliance. Requirements mandate
retention periods and accessibility.

**Trade Records** include order details, execution reports,
and time stamps. Records must support audit and regulatory
examination.

**Retention** periods vary by record type. Hong Kong and SFC
rules specify minimum retention. Extending retention may support
dispute resolution.

**Format** may be electronic. Systems must ensure integrity,
completeness, and searchability.

**Destruction** at end of retention requires controlled process.
Premature destruction can constitute violation.
"""

