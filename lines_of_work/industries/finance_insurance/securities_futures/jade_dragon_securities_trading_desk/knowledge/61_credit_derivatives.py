title = "Credit Derivatives"

content = """
Credit derivatives transfer credit risk between parties. CDS and
related products are used for hedging and speculation in credit
markets.

**Credit Default Swaps (CDS)** provide protection against default.
Buyer pays premium; seller pays upon credit event. Single-name
and index CDS exist.

**Basis Risk** between cash bonds and CDS affects hedging
effectiveness. Basis can be positive or negative.

**Documentation** under ISDA governs CDS contracts. Credit events
and settlement mechanics are standardized.

**Trading** of CDS occurs OTC. Liquidity varies by reference
entity. Central clearing applies to certain index products.
"""

