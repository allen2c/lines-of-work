title = "Inventory Management"

content = """
Inventory management balances the need to facilitate client flow with
risk limits. Prudent management prevents excessive directional exposure.

**Position Limits** cap inventory by security, sector, and overall.
Breaches trigger alerts and potential position reduction.

**Hedging** reduces risk when inventory deviates from target. Delta
hedging for options and portfolio hedging for cash positions apply.

**Financing Costs** of holding inventory affect profitability. Overnight
and term funding rates apply to long positions; borrowing costs to short.

**Client Facilitation** may require taking risk to complete client
orders. Risk-reward assessment guides facilitation decisions.
"""

