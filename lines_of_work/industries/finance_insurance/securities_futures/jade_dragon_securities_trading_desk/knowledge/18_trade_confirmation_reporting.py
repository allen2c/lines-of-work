content = """
Trade confirmation and reporting ensure clients receive accurate transaction
details and regulators receive required disclosures. Timeliness and accuracy
are essential.

**Confirmations** should be sent promptly after execution. They include
security, quantity, price, fees, settlement date, and counterparty.
Electronic confirmations reduce errors and speed delivery.

**Regulatory Reporting** to SFC and HKEX includes transaction reports
and large position disclosures. Format and timing follow prescribed
specifications.

**Client Reporting** may include execution quality, portfolio summaries,
and performance attribution. Customization depends on client agreements.

**Record Keeping** requirements mandate retention of trade records for
prescribed periods. Electronic storage must be searchable and secure.
"""

