title = "Block Trades"

content = """
Block trades involve large order sizes that may move the market if executed
in the open order book. Alternative execution methods protect clients.

**Negotiated Trades** occur off-exchange or via block crossing networks.
Price is agreed between buyer and seller, often at a discount or premium
to last sale.

**Dark Pools** aggregate block liquidity. Algorithms and smart order
routing can source block liquidity without signaling intent.

**Risk** of information leakage requires careful handling. Leaking order
intent can move prices against the client.

**Regulatory** treatment may differ for blocks. Reporting and disclosure
rules apply. SFC and HKEX have specific block trade provisions.
"""

