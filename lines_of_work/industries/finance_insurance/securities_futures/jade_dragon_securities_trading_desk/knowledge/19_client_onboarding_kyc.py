title = "Client Onboarding and KYC"

content = """
Client onboarding and know-your-customer (KYC) procedures establish the
relationship and verify client identity. Trading desk personnel must
understand restrictions that apply to each client.

**Account Opening** requires identity verification, suitability
assessment, and agreement documentation. Incomplete onboarding blocks
trading.

**Restricted Lists** may prohibit trading in certain securities for
specific clients. Compliance maintains and distributes lists.

**Trading Permissions** define what products and strategies each client
may use. Permissions must align with client sophistication and objectives.

**Ongoing Due Diligence** includes periodic KYC refresh and monitoring
for unusual activity. AML and sanctions screening are integral.
"""

