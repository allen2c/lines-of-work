title = "Margin Lending"

content = """
Margin lending allows clients to borrow against securities to finance
additional purchases. Trading desks must understand margin implications
for execution and risk.

**Initial Margin** is required to open positions. HKEX and internal
rules define margin rates by security and portfolio.

**Maintenance Margin** must be maintained to avoid liquidation.
Mark-to-market triggers margin calls when positions move against the client.

**Haircuts** vary by security. Less liquid or more volatile securities
receive larger haircuts, reducing borrowing capacity.

**Liquidation** occurs when margin calls are not met. Understanding
liquidation procedures protects clients and the firm.
"""

