title = "Settlement Fail Management"

content = """
Settlement fail management addresses trades that do not settle on
due date. Prompt resolution minimizes cost and operational impact.

**Causes** include insufficient securities, funding shortfalls,
instruction errors, and counterparty issues. Root cause analysis
informs prevention.

**Buy-In** procedures allow the receiving party to purchase
securities in the market when delivery fails. Cost passes to
the failing party.

**Extension** may be agreed for short periods. Fails persist
when underlying issues are not resolved.

**Reporting** of fails to regulators may be required. Persistent
fails attract regulatory attention and potential penalties.
"""

