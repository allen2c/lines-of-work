title = "Counterparty Risk"

content = """
Counterparty risk is the risk that a trading partner defaults before
fulfilling obligations. Derivatives and securities lending create
counterparty exposure.

**Exposure** measures mark-to-market and potential future exposure.
Netting agreements reduce gross to net exposure.

**Collateral** secures exposure. Margin and independent amount
reduce default loss. Collateral must be sufficient and enforceable.

**Credit Limits** cap exposure to each counterparty. Limits reflect
credit quality and relationship importance.

**Central Clearing** reduces bilateral counterparty risk for
cleared products. CCP becomes counterparty to both sides.
"""

