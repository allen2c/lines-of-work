title = "Insider Dealing Prevention"

content = """
Insider dealing prevention protects the firm and individuals from
illegal use of material non-public information. Chinese walls and
restricted lists are key controls.

**Material Information** can move prices if public. Examples include
earnings, mergers, and regulatory decisions.

**Chinese Walls** separate non-public information from trading.
Physical and system access controls enforce separation.

**Restricted Lists** prohibit or restrict trading in securities
where the firm has non-public information. Compliance maintains
and distributes lists.

**Training** ensures personnel understand prohibitions and
procedures. Acknowledgment of policies is typically required.
"""

