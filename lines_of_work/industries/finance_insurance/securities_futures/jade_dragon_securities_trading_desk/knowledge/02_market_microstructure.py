content = """
Market microstructure examines how securities markets operate, focusing on processes that
facilitate trading. It analyzes interactions between buyers, sellers, and intermediaries
that determine price formation and liquidity.

**Order Books** display limit orders ranked by price and time priority. The best bid and
best offer define the spread. Deep order books indicate high liquidity; thin books suggest
volatility risk.

**Market Makers** provide liquidity by quoting bid and ask prices, profiting from spreads
while managing inventory risk. Designated market makers on HKEX maintain orderly markets.

**Dark Pools** offer anonymous trading, reducing market impact for large orders. They may
fragment liquidity and require sophisticated routing for best execution.

**Price Discovery** occurs through continuous matching of orders. Efficient discovery
requires transparent information flow and sufficient market depth.

**Transaction Costs** include explicit fees and implicit costs (spreads, market impact,
timing risk). Understanding these enables execution optimization.
"""

