content = """
Circuit breakers and trading halts pause trading during extreme volatility
or for material announcements. Understanding triggers and procedures
supports client communication and risk management.

**Price Limit** triggers may halt individual securities when prices move
beyond thresholds. Hong Kong and Connect have different rules.

**Market-Wide** circuit breakers pause the entire market during extreme
moves. They allow information dissemination and reduce panic.

**Trading Halts** for announcements stop trading in specific securities.
Resumption follows exchange procedures and announcement release.

**Contingency** plans should address client orders during halts.
Communication and order handling procedures must be clear.
"""

