title = "AML in Trading"

content = """
Anti-money laundering (AML) in trading involves monitoring for
suspicious activity and maintaining customer due diligence.
Trading desks support AML through accurate record-keeping and
escalation of suspicious patterns.

**Customer Due Diligence** establishes client identity and
purpose. Enhanced due diligence applies to higher-risk clients.

**Transaction Monitoring** identifies unusual patterns: structuring,
rapid movement, or inconsistent with known profile. Alerts
trigger investigation.

**Suspicious Activity Reporting** to the Joint Financial Intelligence
Unit (JFIU) in Hong Kong is mandatory when suspicion exists.

**Red Flags** in trading include unusual settlement, third-party
payments, and high-risk jurisdictions.
"""

