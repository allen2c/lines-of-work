title = "Custody Arrangements"

content = """
Custody arrangements determine where securities are held and how
settlement occurs. Trading desks must understand custody for
settlement and corporate action processing.

**Direct Custody** holds securities in the client's name at the
custodian. Most transparent structure for beneficial ownership.

**Omnibus** accounts commingle client positions. Sub-ledgers track
individual entitlements. Operational efficiency versus transparency.

**Sub-Custodians** in different markets hold local securities.
Global custodians coordinate sub-custodian networks.

**Stock Connect** uses specific custody structures. Northbound
and Southbound have different sub-custodian requirements.
"""

