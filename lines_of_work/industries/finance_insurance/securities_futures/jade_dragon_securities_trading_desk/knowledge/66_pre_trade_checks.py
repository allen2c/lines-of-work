title = "Pre-Trade Checks"

content = """
Pre-trade checks validate orders before submission to prevent
rule violations, limit breaches, and operational errors. Automated
checks support consistent enforcement.

**Restricted List** blocks orders in securities where the firm
has non-public information. Compliance maintains the list.

**Credit Limits** prevent exposure beyond counterparty limits.
Margins and collateral inform available capacity.

**Position Limits** block orders that would breach internal or
regulatory limits. Real-time position tracking enables checks.

**Market Hours** and holiday checks prevent submission when
markets are closed. Symbol and quantity validation prevent
obvious errors.
"""

