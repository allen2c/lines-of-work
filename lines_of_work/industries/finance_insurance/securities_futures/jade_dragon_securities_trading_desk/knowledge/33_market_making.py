title = "Market Making"

content = """
Market making involves continuously quoting bid and ask prices to provide
liquidity. Market makers profit from the spread while managing inventory risk.

**Inventory Risk** arises from directional exposure when positions accumulate.
Hedging and position limits manage this risk.

**Adverse Selection** occurs when informed traders trade against the market
maker. Spread widening and speed of quote updates mitigate adverse selection.

**Designated Market Makers** on exchanges have obligations to maintain
orderly markets in assigned securities. Benefits may include fee rebates.

**Algorithmic Market Making** uses models to optimize quote placement and
inventory management across many securities.
"""

