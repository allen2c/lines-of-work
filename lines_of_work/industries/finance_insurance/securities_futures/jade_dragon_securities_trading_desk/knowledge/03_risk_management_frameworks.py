title = "Risk Management Frameworks"

content = """
Risk management frameworks establish limits, monitoring, and escalation procedures to
protect the firm and clients from trading-related losses.

**Position Limits** cap exposures to individual securities, sectors, or counterparties.
Automated systems enforce limits and trigger alerts when thresholds are approached.

**Market Risk** measures exposure to price movements. VaR, stress testing, and scenario
analysis quantify potential losses under adverse conditions.

**Credit Risk** addresses counterparty default. Margining, collateral agreements, and
exposure limits mitigate credit exposure from derivatives and securities lending.

**Operational Risk** covers settlement failures, technology outages, and process errors.
Business continuity plans and disaster recovery procedures are essential.

**Liquidity Risk** ensures the firm can meet obligations. Contingency funding and
liquidity buffers support resilience during stress.
"""

