content = """
La conformità regolamentare per il trading di titoli in Italia e nell'UE richiede
aderenza a Consob, Banca d'Italia, MiFID II, MAR e regolamenti di mercato. Le violazioni
comportano sanzioni, sospensioni e responsabilità penali.

**MiFID II** armonizza i mercati finanziari UE: best execution, trasparenza pre e post
trade, reporting delle transazioni (RTS 22), registrazione delle chiamate, separazione
dei costi di ricerca. Gli intermediari devono dimostrare politiche e procedure documentate.

**Consob** supervisiona i mercati italiani, le società quotate e gli intermediari.
Autorizzazione, requisiti di capitale, condotta e trasparenza verso i clienti sono
oggetto di vigilanza continua.

**Market Abuse Regulation (MAR)** vieta insider trading, manipolazione di mercato e
comunicazioni false. Gli intermediari devono avere sistemi di vigilanza, policy di
wall crossing e procedure di segnalazione.

**Best execution** richiede politiche scritte, valutazione periodica dei venue, e
documentazione che dimostri l'ottenimento del miglior risultato per il cliente considerando
prezzo, costi, velocità e probabilità di esecuzione.

**Transaction reporting** a Consob e ESMA deve essere completo, tempestivo e accurato.
Errori o ritardi possono generare sanzioni amministrative.
"""  # noqa: E501

