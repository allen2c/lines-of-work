title = "conferma negoziazione e reporting"

content = """
La conferma e il reporting delle negoziazioni garantiscono accuratezza, trasparenza
e conformità normativa. Ogni operazione deve essere confermata, registrata e
comunicata secondo i requisiti applicabili.

**Conferma al cliente:** La conferma deve essere inviata tempestivamente (spesso
entro T+1) con tutti i dettagli: strumento, quantità, prezzo, commissioni,
data di regolamento, controparte. Il cliente deve poter verificare e contestare
eventuali errori.

**Reconciliation:** La riconciliazione tra i sistemi della desk, del clearing
e del cliente identifica discrepanze. Le differenze devono essere risolte
prima del regolamento.

**Regulatory reporting:** MiFID II, EMIR e normative locali richiedono la
segnalazione delle negoziazioni alle autorità e ai trade repositories. I
tempi e i formati sono prescritti; i ritardi comportano sanzioni.

**Best execution reporting:** Le istituzioni devono rendere pubblici i report
sulla qualità di esecuzione, inclusi i costi e le venue utilizzate. I clienti
devono poter valutare se hanno ricevuto best execution.

**Record retention:** I registri delle negoziazioni devono essere conservati
per periodi stabiliti dalla normativa (tipicamente 5-7 anni) e devono essere
accessibili alle autorità in caso di ispezione.
"""  # noqa: E501

