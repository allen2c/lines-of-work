content = """
L'onboarding del cliente e la verifica Know Your Customer (KYC) sono prerequisiti
obbligatori prima di concedere l'accesso alla negoziazione. Consob e le normative
antiriciclaggio impongono controlli rigorosi sull'identità, la provenienza dei
fondi e l'adeguatezza del profilo di rischio.

**Verifica dell'identità:** Richiedere documenti d'identità validi (carta d'identità,
patente, passaporto) e verificare la corrispondenza con i dati dichiarati. Per
persone giuridiche, ottenere atti costitutivi, visura camerale e poteri di
rappresentanza. Conservare copie conformi per almeno dieci anni.

**Provenienza dei fondi:** Documentare l'origine dei fondi depositati mediante
dichiarazioni, estratti conto o certificazioni. Per importi significativi,
richiedere documentazione aggiuntiva. Segnalare operazioni sospette all'UIF.

**Profilo di investitore:** Classificare il cliente come retail, professionista
o controparte qualificata secondo MiFID II. Valutare conoscenze, esperienza,
situazione finanziaria e obiettivi di investimento. Aggiornare periodicamente.

**Adeguatezza e appropriatezza:** Per prodotti complessi, verificare
l'adeguatezza della raccomandazione. Per servizi di consulenza, valutare
l'appropriatezza rispetto al profilo. Documentare le valutazioni effettuate.

**Aggiornamenti periodici:** Rivedere i dati KYC a scadenza regolare (es. annuale)
e in caso di variazioni significative. Sollecitare documentazione mancante prima
di sospendere l'accesso.
"""  # noqa: E501

