title = "sorveglianza e monitoraggio delle negoziazioni"

content = """
La sorveglianza delle negoziazioni rileva manipolazioni di mercato, insider trading e
comportamenti anomali, garantendo conformità normativa e integrità del mercato.

**Market manipulation** include spoofing (ordini fittizi per influenzare il prezzo),
layering, wash trading e pump-and-dump. I sistemi di sorveglianza analizzano pattern
di ordini e esecuzioni per identificare attività sospette.

**Insider trading** si verifica quando informazioni materiali non pubbliche influenzano
le decisioni di trading. Il monitoraggio confronta timing di trade con eventi societari
e comunicazioni interne.

**Best execution surveillance** verifica che gli ordini clienti siano eseguiti secondo
politiche di best execution, confrontando prezzi ottenuti con benchmark di mercato.

**Pre-trade controls** bloccano ordini che violano limiti, restrizioni o blacklist
prima dell'invio al mercato.

**Post-trade analysis** esamina esecuzioni completate per anomalie, errori o deviazioni
dalle istruzioni clienti.

**Alert management** richiede triage, indagine e documentazione degli alert generati.
I falsi positivi devono essere ridotti senza compromettere il rilevamento.
"""  # noqa: E501

