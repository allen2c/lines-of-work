title = "standard di miglior esecuzione"

content = """
Gli standard di miglior esecuzione richiedono che le controparti ottengano il risultato
più favorevole per il cliente considerando prezzo, costo, velocità e probabilità di
esecuzione. La MiFID II e normative analoghe impongono obblighi stringenti.

**Politica di ordine** documenta come il desk seleziona venue, algoritmi e parametri
per raggiungere il best execution. Deve essere approvata, pubblicata e rivista periodicamente.

**RPA (Reasonable Proportion of Arrangements)** richiede di eseguire ordini clienti
su un numero sufficiente di venue per garantire risultati competitivi.

**Commission disclosure** impone trasparenza sui costi di esecuzione, commissioni
e conflitti di interesse.

**Benchmark** per valutare la qualità: VWAP, TWAP, implementation shortfall, arrival
price. Il confronto con benchmark oggettivi dimostra l'adempimento.

**Client-specific factors** includono istruzioni esplicite, urgenza, dimensione ordine
e natura del titolo. La politica deve bilanciare questi fattori.

**Documentazione** di ogni decisione di esecuzione per ordini significativi supporta
la dimostrabilità della conformità in caso di audit.
"""  # noqa: E501

