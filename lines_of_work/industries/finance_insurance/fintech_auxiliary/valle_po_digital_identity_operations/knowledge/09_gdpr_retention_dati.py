title = "GDPR e Retention dei Dati per Verifica Identità"

content = """
Il GDPR regola il trattamento dei dati personali. La verifica dell'identità
implica dati sensibili (documenti, biometria) che richiedono particolare attenzione.

**Base giuridica:** L'esecuzione di obblighi contrattuali e legali (AML/KYC)
costituisce base per il trattamento. Il consenso può essere richiesto per
trattamenti aggiuntivi.

**Minimizzazione:** Raccogliere solo i dati strettamente necessari. Non
conservare copie complete di documenti oltre il necessario se la normativa
consente alternative.

**Retention:** I dati di verifica devono essere conservati per almeno 5 anni
dalla fine del rapporto (AML). Oltre, valutare necessità e cancellare.

**Diritti dell'interessato:** Accesso, rettifica, cancellazione (con eccezioni
per obblighi legali), portabilità dove applicabile. Rispondere entro 30 giorni.

**Sicurezza:** Crittografia, accesso limitato, log delle operazioni. Violazioni
notificare al Garante entro 72 ore.
"""  # noqa: E501

