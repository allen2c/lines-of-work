content = """
Il beneficiario effettivo (UBO) è la persona fisica che controlla o beneficia
effettivamente di un'entità legale, anche attraverso strutture societarie.

**Soglie tipiche:** 25% di proprietà o controllo del capitale/voti, o influenza
significativa sulla gestione. In Italia e UE, la soglia è spesso il 25%.

**Strutture complesse:** Catene di holding, trust, fondazioni. Tracciare fino
alle persone fisiche. Può richiedere documentazione multipla e mappe societarie.

**Registro UBO:** In Italia, il Registro della Trasparenza (e altri registri
pubblici) possono essere consultati per verificare le dichiarazioni.

**Documentazione:** Atto costitutivo, statuto, dichiarazione dei beneficiari
effettivi, documenti di identità degli UBO. Per PEP tra gli UBO, applicare EDD.
"""  # noqa: E501

