title = "Procedure durante Downtime del Servizio"

content = """
Durante manutenzione programmata o outage non programmato, i clienti devono
essere informati e avere alternative dove possibile.

**Comunicazione:** Notifica in anticipo per manutenzione. Durante outage,
aggiornamenti su status page e canali dedicati. Stima tempi di risoluzione
quando disponibile.

**Alternative:** Per verifiche urgenti, valutare video-identificazione manuale
o rinvio. Non promettere tempi che non si possono garantire.

**Post-mortem:** Per outage significativi, report interno e comunicazione
ai clienti impattati con cause e misure preventive.
"""  # noqa: E501

