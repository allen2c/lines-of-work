title = "Digital Signature and E-Sign Compliance"

content = """
Amber River accepts electronic signatures for loan agreements and disclosures in
accordance with the ESIGN Act and UETA (Uniform Electronic Transactions Act).

**Legal Validity:** Electronic signatures have the same legal effect as wet
signatures when the signer intends to sign and the process captures that intent.
We use a compliant e-sign platform that records IP address, timestamp, and
document version at the time of signing.

**Consent Requirements:** Before signing, borrowers must affirmatively consent
to receive documents electronically and confirm they can access and retain
them. Consent is captured separately from the loan agreement.

**Document Delivery:** Signed documents are delivered via secure portal and
email. Borrowers can download PDF copies at any time. We retain the signed
version for the full retention period required by regulation.

**Revocation:** Borrowers may withdraw electronic consent and request paper
documents. We will provide paper copies within a reasonable timeframe and
may require a new application if the loan has not yet been disbursed.

**State Variations:** A small number of states have additional e-sign
requirements. Our platform enforces state-specific rules automatically.
"""  # noqa: E501

