title = "Disbursement Procedures"

content = """
Disbursement is the process of transferring approved loan funds to the
borrower. Amber River offers ACH and check options with clear timelines.

**ACH Disbursement:** The default and fastest method. Funds are sent via
Automated Clearing House to the borrower's verified bank account.
Typical timing: 1–3 business days after loan documents are signed and
funded. The account must be in the borrower's name and pass verification.

**Check Disbursement:** Available for borrowers who cannot receive ACH
or prefer a paper check. Checks are mailed to the address on file.
Delivery typically takes 5–7 business days. Checks must be deposited
within the validity period (e.g., 90 days).

**Funding Conditions:** Disbursement occurs only after: (1) signed
loan agreement, (2) completed identity verification, (3) no rescission
period or rescission period has passed, (4) no fraud flags. We do not
disburse to third parties or unverified accounts.

**Tracking:** Borrowers receive email confirmation when disbursement
is initiated. They can track status in the Amber River portal. For
ACH, the funds appear in the recipient account per their bank's
processing schedule.
"""  # noqa: E501

