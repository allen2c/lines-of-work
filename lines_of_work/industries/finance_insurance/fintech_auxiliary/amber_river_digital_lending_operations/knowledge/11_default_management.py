title = "Default Management and Delinquency Escalation"

content = """
When a borrower fails to make scheduled payments, the account enters delinquency.
Amber River follows a structured escalation path before considering default or
charge-off.

**Delinquency Stages:** Accounts are typically classified as 30, 60, 90, or
120+ days past due. Each stage triggers specific outreach, fee assessments,
and potential credit bureau reporting. State laws may limit fees or require
additional notices.

**Outreach Sequence:** Initial contact attempts include automated reminders,
SMS, email, and phone. Borrowers are offered repayment plans, forbearance,
or modification options before escalation to collections. All contact must
comply with FDCPA (Fair Debt Collection Practices Act) and state rules.

**Repayment Plans:** Borrowers may qualify for short-term plans that spread
missed payments over future installments. Plans require documented agreement
and adherence. Default on a plan may accelerate escalation.

**Charge-Off and Collections:** After sustained non-payment (typically 120+
days), the account may be charged off and transferred to internal or external
collections. Charge-off does not extinguish the debt; the borrower remains
obligated. Legal action may follow per state law and internal policy.

**Credit Bureau Reporting:** Delinquency and charge-off status are reported
to credit bureaus per FCRA requirements. Borrowers have the right to dispute
inaccurate information.
"""  # noqa: E501

