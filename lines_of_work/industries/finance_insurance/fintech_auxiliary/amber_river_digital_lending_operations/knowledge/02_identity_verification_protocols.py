title = "Identity Verification Protocols"

content = """
Identity verification ensures that the applicant is who they claim to be,
reducing fraud and synthetic identity risk in digital lending.

**Document Verification:** Government-issued photo ID (driver's license or
passport) is required. The document must be current and unexpired. We verify
that the name, date of birth, and address match the application.

**Selfie Match:** For enhanced verification, applicants may be asked to
submit a selfie that is compared against the photo on the ID. Liveness
detection prevents use of photos or videos of another person.

**Address Verification:** Proof of address may be required via utility bill,
bank statement, or lease document dated within the last 90 days. P.O. boxes
are not accepted as primary address.

**Database Checks:** We cross-reference applicant data against identity
verification services and fraud databases. Mismatches or flags require
manual review before the application can proceed.

**Failure Handling:** If verification fails, applicants receive a clear
explanation and may be invited to retry with different documents. Repeated
failures may result in application closure with guidance to contact support.
"""  # noqa: E501

