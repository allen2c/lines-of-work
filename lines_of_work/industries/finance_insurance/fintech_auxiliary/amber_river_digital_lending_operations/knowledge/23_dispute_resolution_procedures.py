title = "Dispute Resolution Procedures"

content = """
When a borrower disputes a charge, balance, or reporting, follow the
documented dispute resolution procedures to ensure fair and compliant
handling.

**Billing Disputes:** If a borrower disputes a fee or charge, gather
documentation, verify the charge against the loan agreement and
applicable policies, and provide a written response within 30 days
per Regulation Z. If the dispute is valid, reverse the charge and
notify the borrower. If not, explain the basis in plain language.

**Credit Bureau Disputes:** Borrowers may dispute information reported
to credit bureaus. We must investigate within 30 days and correct
or verify the information. Provide the borrower a written summary
of the investigation outcome.

**Identity Theft:** If a borrower reports identity theft or
unauthorized account activity, follow the identity theft protocol:
place a fraud alert, freeze the account if appropriate, and provide
the required FTC Identity Theft Report acknowledgment and
investigation timeline.

**Escalation:** Disputes involving legal interpretation, regulatory
ambiguity, or potential litigation are escalated to Legal and
Compliance. Do not make unilateral determinations on such matters.
"""  # noqa: E501

