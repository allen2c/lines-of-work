title = "Legal Referral Criteria"

content = """
Certain situations require escalation to Legal or external counsel
rather than routine operations handling.

**Regulatory or Compliance:** Interpretation of TILA, ECOA, state
usury laws, or licensing requirements. Disputes over disclosure
accuracy or timing. Regulatory exam or enforcement inquiries.

**Litigation Risk:** Borrower threatens or initiates legal action.
Demands for arbitration or small claims. Subpoenas or discovery
requests. Class action indicators.

**Fraud or Identity Theft:** Confirmed or suspected synthetic
identity, document forgery, or impersonation. May involve
law enforcement referral and identity theft affidavit.

**Bankruptcy and Insolvency:** Bankruptcy filing, deceased
borrower estate, or business dissolution. Requires precise
legal handling for claims and discharge.

**Contract Disputes:** Disagreement over loan terms, modification
agreements, or settlement terms. Ambiguity in documentation.
"""  # noqa: E501

