content = """
The Equal Credit Opportunity Act (ECOA) prohibits discrimination in credit
transactions on the basis of race, color, religion, national origin, sex,
marital status, age, or receipt of public assistance.

**Prohibited Factors:** Credit decisions cannot be based on protected
characteristics. Lenders must evaluate applicants solely on creditworthiness
factors such as income, employment, credit history, and debt obligations.

**Adverse Action Notices:** When an application is denied, the borrower
must receive an adverse action notice within 30 days. The notice must
state the principal reasons for denial or the right to request them.
Reasons must be specific and factual, not generic.

**Marital Status:** Creditors cannot require a spouse's signature or
income for an individual applicant who qualifies on their own. Spousal
guarantees may be requested only when permitted by law.

**Language and Accessibility:** We provide materials in multiple languages
where required and ensure accessibility for applicants with disabilities.
Operations staff must not make assumptions based on protected characteristics.
"""  # noqa: E501

