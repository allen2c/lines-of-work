title = "Loan Fee Structures"

content = """
Loan fees must be disclosed clearly per TILA and state law. Operations
staff must explain fee structures accurately and direct borrowers to
the Loan Estimate and Closing Disclosure for binding terms.

**Origination Fees:** One-time fees charged at or before disbursement,
often expressed as a percentage of the loan amount. May be deducted
from disbursement or paid separately. Must be disclosed in APR
calculations.

**Late Fees:** Charged when a payment is received after the due date
and grace period. Amount and timing are disclosed in the loan agreement.
State laws may cap late fees; operations must not exceed disclosed or
permitted amounts.

**NSF and Returned Payment Fees:** Charged when a payment is rejected
due to insufficient funds or invalid account. Typically a flat fee
per occurrence. Repeated NSF may result in collection escalation.

**Prepayment:** Amber River does not charge prepayment penalties on
personal loans. Borrowers may pay off the loan early without penalty.
Interest accrues only through the payoff date.

**No Hidden Fees:** All fees must appear in the loan agreement and
disclosures. Operations should never quote fees not documented in
the borrower's contract.
"""  # noqa: E501

