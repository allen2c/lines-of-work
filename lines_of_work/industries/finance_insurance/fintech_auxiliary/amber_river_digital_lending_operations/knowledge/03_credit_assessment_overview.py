title = "Credit Assessment Overview"

content = """
Credit assessment at Amber River combines traditional credit bureau data with
alternative data to evaluate borrower creditworthiness. Operations staff
explain the process but do not make credit decisions.

**Credit Bureau Pull:** We obtain credit reports from one or more major
bureaus (Experian, Equifax, TransUnion). The pull may result in a soft or
hard inquiry depending on application stage. Applicants are informed of
inquiry type in disclosures.

**Key Factors:** Credit score, payment history, debt-to-income ratio,
employment stability, and existing obligations are considered. Each factor
contributes to the overall risk assessment. We do not disclose the exact
weighting or algorithm.

**Alternative Data:** Bank account transaction data, rent payment history,
or utility payments may supplement traditional credit data for applicants
with thin files. Consent is required for alternative data use.

**Decision Outcomes:** Applications result in approve, decline, or request
for additional information. Decline reasons are provided per ECOA
requirements. Operations staff can explain general factors but cannot
override or appeal decisions — that is an underwriting function.
"""  # noqa: E501

