title = "TILA and APR Disclosure Requirements"

content = """
The Truth in Lending Act (TILA) requires clear, standardized disclosure of
loan terms so borrowers can compare offers and make informed decisions.

**APR Disclosure:** The Annual Percentage Rate (APR) must be disclosed
prominently. APR reflects the cost of credit including interest and
certain fees, expressed as a yearly rate. It allows comparison across
lenders and loan types.

**Loan Estimate:** Within three business days of application, borrowers
receive a Loan Estimate with principal, interest rate, APR, total
payments, and fees. This is a preliminary disclosure; terms may change
if application information changes.

**Closing Disclosure:** Before consummation, a final Closing Disclosure
confirms the terms. Borrowers must have a reasonable opportunity to
review before signing. E-signature constitutes acceptance.

**Right of Rescission:** For certain loans (e.g., refinancing of primary
residence), borrowers have a three-business-day right to cancel. We
cannot disburse until the rescission period expires.

**Fee Disclosure:** All fees that are part of the credit cost must be
included in APR calculation or itemized. No hidden fees are permitted.
"""  # noqa: E501

