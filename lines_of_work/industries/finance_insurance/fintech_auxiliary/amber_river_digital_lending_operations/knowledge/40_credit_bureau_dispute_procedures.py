title = "Credit Bureau Dispute Procedures"

content = """
Borrowers have the right to dispute inaccurate information reported
to credit bureaus. Amber River must investigate and respond per
FCRA requirements.

**Borrower Initiation:** Disputes may come directly from the borrower
or through a credit bureau. We receive a dispute notification with
the nature of the claim. Investigation must begin within 30 days.

**Investigation Steps:** Review loan documents, payment history, and
account records. Determine whether the reported information is
accurate. If inaccurate, correct the reporting. If accurate,
provide explanation to the bureau and borrower.

**Response Timeline:** FCRA requires completion within 30 days of
receipt. Extensions are limited. Failure to comply can result
in regulatory action and civil liability.

**Corrective Reporting:** If we find an error, we submit a
correction to the bureau(s). Borrower receives written
notice of the result. Re-insertion of deleted information
has specific notice requirements.
"""  # noqa: E501

