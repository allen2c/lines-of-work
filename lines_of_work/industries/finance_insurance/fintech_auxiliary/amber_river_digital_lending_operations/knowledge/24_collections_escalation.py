title = "Collections Escalation"

content = """
Operations staff assist borrowers with repayment questions and direct
them to appropriate resources. When an account becomes delinquent,
escalation to Collections follows defined criteria.

**Delinquency Thresholds:** Accounts 1–30 days past due remain in
operations for standard outreach (payment reminders, due date
clarification). At 31 days past due, the account is escalated to
Collections for structured contact and repayment plan offers.

**Operations Scope:** Operations may explain late fees, grace periods,
and payment options. We do not negotiate repayment plans, accept
partial payments as settlement, or make promises about credit
reporting. Those decisions belong to Collections.

**Handoff Protocol:** When escalating, provide Collections with a
summary of borrower contact history, stated reason for delinquency,
and any hardship indicators. Ensure the borrower receives clear
information about who will contact them next and how.

**FDCPA Compliance:** Collections contacts are subject to the Fair
Debt Collection Practices Act. Operations staff must not use
language or tactics that could be construed as collection activity
beyond informational assistance. When in doubt, escalate.
"""  # noqa: E501

