title = "Servicing Transfer Protocols"

content = """
When loan servicing is transferred to another entity, borrowers must
receive advance notice and clear instructions. Operations staff
should be prepared to explain transfers and direct borrowers to
the correct servicer.

**Notice Requirements:** Per CFPB rules, borrowers receive a
transfer notice at least 15 days before the transfer date. The
notice includes the new servicer's name, address, phone, and
effective date. A welcome notice from the new servicer follows.

**During Transition:** Payments sent to the old servicer before
the transfer date are typically forwarded. Payments sent after
the transfer date to the old servicer may be forwarded or
returned; borrowers should be directed to pay the new servicer.

**Account Access:** Online account access may change. Borrowers
must register with the new servicer's portal. Auto-pay and
recurring payments may need to be re-established.

**Disputes and Complaints:** If a borrower has an open dispute
or complaint, it should be transferred with the loan. Operations
should document and escalate any transfer-related issues.
"""  # noqa: E501

