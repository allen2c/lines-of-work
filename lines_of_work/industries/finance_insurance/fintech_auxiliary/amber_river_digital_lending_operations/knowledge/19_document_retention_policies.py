content = """
Amber River retains loan-related documents in accordance with
regulatory requirements and internal policy. Operations staff
must understand retention periods and how to support audits.

**Retention Periods:** Loan applications, signed agreements,
disclosures, and correspondence are typically retained for at
least the life of the loan plus seven years, or as required by
state and federal law. Some documents may have longer retention
(e.g., tax-related, litigation hold).

**What Is Retained:** Application data, identity verification
records, income documentation, credit reports, loan agreements,
disclosure acknowledgments, payment history, and customer
communications. Electronic records are stored in secure
systems with appropriate access controls.

**Audit and Examination Support:** Regulators and auditors may
request documents to verify compliance. We maintain organized
records and can produce requested materials within required
timeframes. Operations may be asked to explain processes or
provide sample files.

**Borrower Requests:** Borrowers may request copies of their
loan documents. We provide them in accordance with applicable
privacy and record-retention rules. There may be a fee for
duplicate copies in some cases.

**Destruction:** Documents are securely destroyed after the
retention period expires, unless subject to a litigation
hold or other legal requirement.
"""  # noqa: E501

