title = "Third-Party Verification and Income Documentation"

content = """
Amber River verifies applicant income and employment through
third-party sources when possible. This supports accurate
credit decisions and reduces fraud risk.

**Employment Verification:** We may use third-party services
to confirm employment status, employer name, job title, and
income. Applicants authorize this verification as part of
the application. Some employers participate in automated
verification; others require manual contact or document
upload.

**Income Documentation:** When third-party verification is
unavailable or insufficient, we accept pay stubs, tax returns,
bank statements, or other documentation. Requirements vary
by product and applicant type (W-2 employee, self-employed,
etc.). We explain requirements during the application process.

**Bank Account Verification:** We verify bank account ownership
through micro-deposits, instant verification (when the applicant
links their account via a supported aggregator), or bank
statement upload. This ensures disbursement and repayment
can be processed correctly.

**Privacy and Consent:** Applicants must consent to third-party
verification. We use only authorized vendors and comply with
privacy laws. Verification results are used solely for
underwriting and account servicing.

**Failures and Manual Review:** When verification fails or
returns incomplete data, we may request additional
documentation or route the application for manual review.
Operations guides applicants through alternative verification
paths.
"""  # noqa: E501

