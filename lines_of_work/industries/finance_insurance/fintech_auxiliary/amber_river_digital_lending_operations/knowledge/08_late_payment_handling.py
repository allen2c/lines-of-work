title = "Late Payment Handling"

content = """
Late payment handling balances borrower support with the need to maintain
repayment discipline and protect the portfolio.

**Late Fee Assessment:** Late fees are assessed per the loan agreement,
typically after a grace period (e.g., 10–15 days past due). The fee amount
and cap are disclosed at origination. State laws may limit late fees.

**Communication:** Borrowers receive reminder notices before and after
the due date. Notifications are sent by email and, if enrolled, SMS.
Tone is helpful, not punitive — we want to facilitate payment.

**Delinquency Stages:** Accounts are classified as current, 1–30 days
past due, 31–60 days, 61–90 days, and 90+ days. Escalation and
collections procedures vary by stage. Operations staff handle early
delinquency; collections handles escalated accounts.

**Payment Arrangements:** For borrowers experiencing temporary hardship,
we may offer payment plans or short-term forbearance. These require
documentation and approval. Operations can explain options and
initiate requests but cannot approve them unilaterally.

**Credit Reporting:** Delinquency may be reported to credit bureaus
per our reporting policy. Borrowers are informed of reporting
practices in the loan agreement.
"""  # noqa: E501

