title = "Co-Borrower Processing"

content = """
Joint applications with co-borrowers combine both parties' income and
credit for qualification; both are equally liable for repayment.

**Application Flow:** Both applicants complete identity verification,
provide income documentation, and authorize credit checks. Either party
can typically view the application status; both must e-sign to consummate.

**Qualification:** Combined income and debt-to-income ratios are used.
The lower of the two credit scores may be used for pricing in some
configurations. Co-borrowers strengthen approval odds for marginal
applicants.

**Repayment Responsibility:** Both parties are jointly and severally
liable. Payment can be made by either; delinquency affects both
credit reports. Disputes between co-borrowers do not relieve
either of the obligation.

**Removal or Change:** Co-borrower release is rare and product-specific.
Refinancing as a single borrower is an option if the remaining
borrower qualifies independently.
"""  # noqa: E501

