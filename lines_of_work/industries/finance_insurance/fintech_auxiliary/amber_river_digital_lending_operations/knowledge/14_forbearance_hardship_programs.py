title = "Forbearance and Hardship Programs"

content = """
Amber River offers forbearance and hardship programs to borrowers
experiencing temporary financial difficulty. These programs provide
short-term relief while preserving the loan obligation.

**Forbearance:** A temporary pause or reduction of payments. Interest
typically continues to accrue during forbearance. The forbearance
period is limited (e.g., 3–6 months) and must be requested and
approved. At the end of forbearance, the borrower resumes payments;
missed amounts may be added to the loan balance or spread over
future payments, per program terms.

**Eligibility:** Borrowers must demonstrate hardship (job loss, medical
expense, natural disaster, etc.) and ability to resume payments after
the forbearance period. We may require documentation. Approval is
subject to underwriting review; not all requests are granted.

**Hardship Repayment Plans:** Alternative to full forbearance. Reduced
payments for a set period, with the shortfall added to the loan or
extended term. Requires documented agreement and adherence.

**Application Process:** Borrowers contact operations support to
request forbearance or a hardship plan. We collect information,
explain options, and route to the appropriate team for decision.
Timely application is important; we encourage borrowers to reach
out before missing payments.

**Communication:** Approved forbearance or hardship terms are
documented in writing. Borrowers receive clear information about
resumption of payments and any impact on credit reporting.
"""  # noqa: E501

