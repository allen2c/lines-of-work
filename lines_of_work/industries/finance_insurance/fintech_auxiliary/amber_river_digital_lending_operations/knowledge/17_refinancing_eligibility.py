title = "Refinancing Eligibility and Process"

content = """
Refinancing allows existing borrowers to replace their current loan
with a new loan, often at a lower rate or with different terms.
Eligibility depends on credit, payment history, and product availability.

**Eligibility Criteria:** Borrowers typically must have made a minimum
number of on-time payments (e.g., 6–12 months), have no current
delinquency, and meet current credit and income standards. Refinancing
is subject to underwriting; approval is not guaranteed.

**Benefits:** Refinancing may reduce APR, lower monthly payments,
or shorten the term. Borrowers should compare total cost of the
new loan versus remaining payments on the current loan. We provide
clear disclosures for refinance offers.

**Process:** Borrowers apply through the same channel as new loans.
We pull updated credit and income information. If approved, the
new loan pays off the existing loan; any surplus may be disbursed
to the borrower. The original loan is closed.

**Timing:** Refinancing is typically available after the initial
funding period. Some products may have prepayment penalties;
we disclose these at origination. Borrowers should contact
operations or apply online to check eligibility.

**Limitations:** Refinancing may not be offered in all states or
for all product types. We do not guarantee refinance availability.
"""  # noqa: E501

