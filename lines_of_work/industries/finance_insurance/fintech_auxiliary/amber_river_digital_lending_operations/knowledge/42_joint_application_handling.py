title = "Joint Application Handling"

content = """
Joint applications allow two or more borrowers to apply together, sharing
responsibility for the loan. Both applicants' credit, income, and
employment are evaluated.

**Eligibility:** Joint applicants must each meet identity verification,
income, and credit criteria. The combined application may qualify for
a larger loan amount or better rate than either applicant alone.

**Liability:** Joint applicants are typically jointly and severally
liable — each is responsible for the full loan balance. If one
borrower defaults, the other remains obligated. This should be
clearly disclosed at application and closing.

**Disbursement:** Funds may be disbursed to a single designated account
or split per applicant preference. Both applicants must consent to
the disbursement method and destination.

**Repayment:** Either joint applicant may make payments. Account
statements and correspondence can be sent to one or both, per
preference. Both have equal access to account information.

**Relationship Verification:** Spouses, domestic partners, or
co-owners may apply jointly. Business partners applying for
business loans follow separate business lending procedures.
"""  # noqa: E501

