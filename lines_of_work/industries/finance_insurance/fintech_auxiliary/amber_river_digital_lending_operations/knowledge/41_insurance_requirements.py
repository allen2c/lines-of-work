title = "Insurance Requirements for Secured Lending"

content = """
When loans are secured by collateral such as vehicles or property, lenders typically
require borrowers to maintain adequate insurance to protect the lender's interest.

**Collateral Protection:** For auto loans, comprehensive and collision coverage
must name the lender as loss payee. For real estate, hazard insurance with
lender as mortgagee is required. Gaps in coverage may trigger force-placed
insurance at the borrower's expense.

**Force-Placed Insurance:** If a borrower allows required insurance to lapse,
the lender may purchase coverage on the borrower's behalf. Premiums are
added to the loan balance. Borrowers should be directed to reinstate their
own coverage promptly to avoid higher force-placed rates.

**Proof of Insurance:** Borrowers must provide declarations pages showing
coverage limits, effective dates, and lender as loss payee. Digital
submission via portal or email is accepted. Annual renewal verification
is typically required.

**Unsecured Loans:** Personal unsecured loans do not require collateral
insurance. Borrowers may have optional credit life or disability insurance
offered at origination; these are voluntary and do not affect loan approval.
"""  # noqa: E501

