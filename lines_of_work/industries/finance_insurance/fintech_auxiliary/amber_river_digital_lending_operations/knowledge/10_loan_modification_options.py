title = "Loan Modification Options"

content = """
Loan modifications help borrowers who experience financial hardship
maintain their obligations while avoiding default. Modifications
require underwriting or specialized team approval.

**Forbearance:** Temporary suspension or reduction of payments for a
defined period (e.g., 3–6 months). Interest may continue to accrue.
At the end of forbearance, the borrower resumes payments; missed
amounts may be added to the loan or paid in a lump sum.

**Payment Reduction:** Lowering the monthly payment for a period by
extending the term or adjusting the rate. Used when income has
decreased but the borrower can still make reduced payments.

**Term Extension:** Extending the loan term to lower monthly payments.
Total interest paid may increase. Requires recalculation and
amended disclosure.

**Rate Adjustment:** In limited circumstances, a temporary or permanent
rate reduction may be offered. Typically reserved for demonstrated
hardship and strong prior payment history.

**Documentation:** Borrowers must provide evidence of hardship (e.g.,
job loss, medical bills, natural disaster). We do not approve
modifications without documented need. Operations staff collect
documents and submit for review.
"""  # noqa: E501

