content = """
When a borrower files for bankruptcy, Amber River follows legal and
regulatory requirements for claims, automatic stay compliance, and
communication.

**Automatic Stay:** Upon filing, the automatic stay prohibits collection
activity, including calls, letters, and ACH debits. Violations can
result in sanctions. Operations must flag accounts immediately upon
bankruptcy notice.

**Claim Filing:** In Chapter 7 and 13, we file a proof of claim with
the court. Amount, classification (secured vs unsecured), and
documentation must be accurate. Deadlines are strictly enforced.

**Communication:** Post-filing communication is limited. We may respond
to borrower inquiries but must not engage in collection. Discharge
notices require account closure and credit bureau update.

**Recovery:** Discharge extinguishes personal liability for most
unsecured debt. Non-dischargeable claims (e.g., fraud) are rare;
legal counsel determines applicability.
"""  # noqa: E501

