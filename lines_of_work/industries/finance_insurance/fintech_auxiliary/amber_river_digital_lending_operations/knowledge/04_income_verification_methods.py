title = "Income Verification Methods"

content = """
Income verification confirms that applicants have sufficient, stable income
to support loan repayment. Multiple verification methods are accepted
depending on employment type and documentation availability.

**Pay Stub Verification:** For W-2 employees, recent pay stubs (typically
the last two) are required. Stubs must show employer name, pay period,
gross income, and year-to-date totals. Handwritten or altered stubs are
not accepted.

**Bank Statement Verification:** Bank statements may be used to verify
income deposits, especially for self-employed or gig economy workers.
Statements should cover at least two months and show consistent deposit
patterns. We look for regular income, not one-time transfers.

**Tax Return Verification:** For self-employed applicants, tax returns
(typically the last two years) and corresponding schedules are used.
1099 forms may supplement W-2 income for side income verification.

**Employment Verification:** We may contact employers to confirm
employment status, tenure, and income. Verbal or written verification
letters from HR are accepted. Applicants must authorize contact.

**Alternative Income:** Alimony, child support, disability, retirement
income, or rental income may be considered if documented. Not all
income types qualify for all loan products.
"""  # noqa: E501

