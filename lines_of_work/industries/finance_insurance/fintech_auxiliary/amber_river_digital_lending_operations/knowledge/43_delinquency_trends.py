title = "Delinquency Trends and Early Warning"

content = """
Monitoring delinquency trends helps identify portfolio risk and operational
gaps before accounts reach severe default. Operations staff should recognize
early warning patterns and escalate appropriately.

**Early Delinquency (1–30 days):** Often due to timing, forgotten due dates,
or temporary cash flow. Many resolve with a single reminder or autopay setup.
High early delinquency rates in a segment may indicate underwriting drift or
economic stress.

**Roll Rates:** The proportion of 30-day delinquent accounts that roll to
60 days, and 60 to 90, indicates severity. Rising roll rates suggest
systemic issues — economic downturn, servicing gaps, or product design.

**Segment Analysis:** Delinquency by product type, origination vintage,
geography, or risk tier reveals concentration risk. New products or
geographic expansion may show higher early delinquency until seasoning.

**Operational Response:** Operations should track first-payment default
rates, repeat delinquency, and cure rates. Unusual spikes warrant
escalation to underwriting, collections, or risk management. Document
trends for audit and regulatory reporting.
"""  # noqa: E501

