title = "Credit Bureau Reporting and Dispute Procedures"

content = """
Amber River reports loan account information to the major consumer credit
bureaus (Experian, Equifax, TransUnion) in compliance with the Fair Credit
Reporting Act (FCRA).

**What Is Reported:** Account opening date, loan type, credit limit or
original balance, current balance, payment history (on-time, late, or
delinquent), account status (open, closed, charged off), and date of last
activity. Negative information typically remains for seven years.

**Reporting Schedule:** Updates are submitted monthly, typically within
30 days of the billing cycle close. New accounts are reported upon
disbursement. Closed accounts are reported with final status.

**Borrower Disputes:** If a borrower disputes the accuracy of reported
information, they may submit a dispute directly to the bureau or to Amber
River. We must investigate within 30 days and correct or verify the
information. Borrowers can also add a statement of dispute to their file.

**Adverse Action Notices:** When a credit decision is based in whole or
part on information from a credit report, we provide an adverse action
notice with the bureau name and the consumer's right to a free copy of
the report.

**Accuracy Standards:** We maintain procedures to ensure reported data
is accurate and complete. Systematic errors may require corrective
reporting to all affected borrowers.
"""  # noqa: E501

