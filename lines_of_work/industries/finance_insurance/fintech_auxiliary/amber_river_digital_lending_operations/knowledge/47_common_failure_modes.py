title = "Common Failure Modes in Digital Lending"

content = """
Operations staff should recognize common failure modes and know how
to prevent or resolve them. Proactive identification reduces
borrower friction and operational cost.

**Document Upload Failures:** Blurry images, wrong document type,
or file size limits can delay applications. Guide borrowers to
use the mobile app for clearer captures, verify document type
before upload, and retry with supported formats.

**Identity Verification Timeouts:** ID verification may fail due
to lighting, document quality, or system latency. Allow retry
within 24 hours; escalate if repeated failures suggest fraud
or system issues.

**ACH Rejections:** Disbursement or payment ACH may reject due
to invalid account, closed account, or insufficient funds.
Verify account details with the borrower; for payments, collect
alternative payment method and assess late fee if applicable.

**E-Sign Failures:** Signing links expire; some borrowers have
browser or device compatibility issues. Resend links as needed;
ensure consent and audit trail are preserved.

**Communication Gaps:** Borrowers may miss emails or assume
approval without reading conditions. Confirm critical
steps (e.g., e-sign, disbursement) via multiple channels
when possible. Document all outreach attempts.
"""  # noqa: E501

