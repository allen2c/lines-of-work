content = """
Amber River assigns applicants to risk tiers based on credit profile,
income stability, and other factors. Operations staff explain outcomes
but do not make tier assignments.

**Tier Structure:** Tiers range from prime (lowest risk, best rates)
to near-prime and subprime (higher risk, higher rates or denial).
The underwriting model determines tier; operations cannot override.

**Rate and Fee Impact:** Tier affects APR, origination fees, and
loan limits. When explaining offers to borrowers, use the terms
presented in their disclosure. Do not speculate on what they
might qualify for with different circumstances.

**Reconsideration:** Borrowers may request reconsideration after
denial or if their circumstances change. Those requests go to
Underwriting. Operations can explain the process and timeline
but cannot expedite or influence the decision.

**Transparency:** We disclose the key factors that influenced
the decision in adverse action notices. Operations staff may
explain those factors in plain language but must not add or
contradict the stated reasons.
"""  # noqa: E501

