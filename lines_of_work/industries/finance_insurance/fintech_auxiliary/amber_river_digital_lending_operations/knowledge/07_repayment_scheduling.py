content = """
Repayment scheduling defines when and how borrowers make loan payments.
Amber River offers flexible due dates and multiple payment methods.

**Due Dates:** Payments are typically due monthly on a date chosen by
the borrower at origination (e.g., the 15th of each month). The first
payment is usually due one full billing cycle after disbursement.

**Payment Amount:** Fixed monthly payments include principal and interest.
The amount is disclosed in the loan agreement and does not change unless
the loan is modified. Extra principal payments reduce total interest.

**Payment Methods:** ACH debit (autopay), one-time ACH, debit card, or
check by mail. Autopay is encouraged and may qualify for a rate
discount. Payments must be initiated before the due date to avoid
late fees.

**Grace Periods:** A short grace period (e.g., 10–15 days) may apply
before late fees are assessed. Interest continues to accrue during
the grace period. Operations staff should clarify the specific
grace period for each loan product.

**Holiday and Weekend Processing:** Payments received on non-business
days are processed the next business day. Timely initiation before
the due date is the borrower's responsibility.
"""  # noqa: E501

