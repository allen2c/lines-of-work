title = "Friendly Fraud Indicators"

content = """
- Friendly fraud occurs when a legitimate cardholder disputes a transaction they actually authorized. Common indicators:
- **Behavioral**: Cardholder made multiple purchases from the same merchant, used the same device/IP, or has a history of disputes.
- **Communication**: Cardholder contacted the merchant before the dispute (e.g., asking for a refund, then filing a chargeback).
- **Timing**: Dispute filed immediately after delivery or after the refund window expired.
- **Evidence**: The cardholder’s email or chat logs show they received the product/service and were satisfied.
- **Action**: If friendly fraud is suspected, recommend representment even if evidence is borderline. Flag the case for the fraud team to monitor the cardholder.
"""  # noqa: E501

