title = "AML Screening Process"

content = """
- Run the applicant's full name, date of birth, and SSN/ITIN through AML-Watch against OFAC, UN sanctions, EU sanctions, and PEP lists.
- A "hit" (exact or fuzzy match above 85% confidence) must be reviewed manually. Do not proceed with the loan until cleared.
- For PEP matches, determine if the person is a domestic PEP (high-risk) or foreign PEP (very high-risk). Foreign PEPs require enhanced due diligence (EDD) – collect source of funds and wealth.
- If the screening returns a "possible match" (70-84% confidence), escalate to the compliance team for further investigation within 1 hour.
- Log the screening result and any EDD documents in the AML-Watch case file. Retain records for at least 5 years.
"""

