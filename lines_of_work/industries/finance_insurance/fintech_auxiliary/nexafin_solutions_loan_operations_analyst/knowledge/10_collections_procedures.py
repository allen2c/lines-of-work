title = "Collections Procedures"

content = """
- For accounts 15+ days past due, compile a summary: borrower contact history, payment capacity (income, expenses), and any hardship notes. Send to the collections team via the Collections module.
- Do not contact the borrower directly unless explicitly authorized by the collections manager. All communication must be logged.
- If the borrower requests a payment plan, verify that the proposed plan meets NexaFin's minimum payment threshold (at least 50% of the regular monthly payment).
- For accounts 60+ days past due, recommend repossession or legal action only after senior management approval.
- Track collection outcomes (promises to pay, broken promises, settlements) in the system. Update loan status accordingly.
"""

