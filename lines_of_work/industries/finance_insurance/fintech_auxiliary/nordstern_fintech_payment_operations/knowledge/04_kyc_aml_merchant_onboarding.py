content = """
Merchant onboarding requires verification of identity, business legitimacy, and
risk profile in line with anti-money laundering (AML) and know-your-customer
(KYC) obligations under EU law.

**Entity Verification:** For businesses, we require: company registration
documents, proof of address, beneficial ownership information (UBO), and
directors or authorized signatories. For sole traders, identity document
and business registration.

**Business Model Assessment:** We evaluate the nature of goods or services,
expected transaction volumes, geographic reach, and customer base. High-risk
categories (e.g., gambling, crypto, adult content, high-value goods) undergo
enhanced due diligence.

**Ongoing Monitoring:** After onboarding, we monitor for unusual transaction
patterns, sanctions list matches, and adverse media. Merchants must notify
us of material changes to business model, ownership, or geographic reach.

**Document Retention:** All onboarding documents are retained for at least
five years after the business relationship ends, per EU AML directives.

**Timeline:** Standard onboarding completes within 5–10 business days for
low-risk merchants. Complex or high-risk cases may take longer. Merchants
can check application status via the partner portal or by contacting
operations support.
"""  # noqa: E501

