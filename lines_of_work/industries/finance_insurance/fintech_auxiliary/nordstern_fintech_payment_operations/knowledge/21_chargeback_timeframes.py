title = "Chargeback Timeframes"

content = """
Card scheme and SEPA rules define strict timeframes for chargebacks and
representment. Missing deadlines results in automatic loss.

**Card Chargeback Windows:** Cardholders typically have 120 days from
transaction date (or expected delivery for goods) to dispute. Some reason
codes have shorter or longer windows. Issuers must submit chargebacks to
the scheme within these windows.

**Representment Deadlines:** When a chargeback is received, merchants
have a limited time (often 7–21 days depending on reason code) to submit
representment evidence. Extensions are rarely granted. Submit complete
evidence; incomplete submissions are rejected.

**Pre-Arbitration and Arbitration:** If representment is rejected, some
schemes allow pre-arbitration. Full arbitration involves scheme fees and
is typically reserved for high-value or precedent-setting cases.

**SEPA Direct Debit Returns:** Unauthorized debits: 8 weeks from debit
date. Unauthorized with mandate dispute: up to 13 months. Authorized
disputes: 8 weeks. Mandate-related: 13 months. Merchants must retain
mandate evidence for the full 13 months.

**Operations Role:** We provide timeframe guidance. The Fraud and
Chargeback team manages actual submissions — escalate immediately
when chargebacks are received.
"""  # noqa: E501

