title = "Merchant Fee Structures"

content = """
Nordstern pricing varies by payment type, volume, and risk. Merchants receive
individual pricing in their agreement.

**Card Fees:** Typically a percentage of transaction value plus a fixed fee
per transaction. Interchange (paid to card issuers) and scheme fees (Visa,
Mastercard) form the base; Nordstern adds a margin. Rates differ by card
type (debit vs credit, domestic vs cross-border) and channel (e-commerce,
POS, mobile).

**SEPA Fees:** SCT and SCT Inst are usually charged per transaction or
per batch. SCT Inst commands a premium over standard SCT.

**Monthly and Annual Fees:** Some plans include platform or account fees.
High-volume or enterprise merchants may negotiate custom pricing.

**Chargeback and Retrieval Fees:** Excessive chargebacks can trigger
additional fees. Retrieval requests (pre-chargeback document requests)
may also incur fees. Merchants should aim to resolve disputes before
chargebacks.

**Pricing Changes:** Fee changes follow the notice period in the merchant
agreement. Merchants can contact their account manager or sales for
pricing questions; operations provides clarity on what appears on statements.
"""  # noqa: E501

