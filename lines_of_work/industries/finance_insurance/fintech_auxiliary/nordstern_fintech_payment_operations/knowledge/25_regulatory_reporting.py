title = "Regulatory Reporting"

content = """
Nordstern as a payment institution fulfills regulatory reporting obligations
to supervisors (e.g., BaFin). Merchants may have their own obligations.

**Nordstern Obligations:** We report transaction volumes, incident data,
and other metrics to regulators. This is internal; merchants do not
submit reports to Nordstern for regulatory purposes unless specifically
requested (e.g., AML questionnaire).

**Sanctions Screening:** We screen parties against sanctions lists per
EU and national requirements. Matches result in blocked transactions
and escalation. Merchants cannot override sanctions screening.

**Transaction Monitoring:** Unusual patterns may trigger regulatory
reporting (e.g., suspicious activity reports). We do not disclose
whether a specific case was reported; such information is confidential.

**Merchant Reporting:** Merchants in regulated industries (e.g., gambling,
crypto) may have separate reporting obligations. Nordstern does not
advise on merchant-specific regulatory compliance; merchants should
consult their legal advisors.
"""  # noqa: E501

