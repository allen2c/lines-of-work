title = "Fraud Prevention for Digital Payments"

content = """
Digital payment fraud takes many forms. Merchants and Nordstern share
responsibility for detection and prevention.

**Transaction Fraud:** Unauthorized use of stolen or compromised payment
credentials. Mitigations: 3D Secure (SCA), velocity checks, device
fingerprinting, AVS and CVV verification. High-risk industries should
implement additional rules and machine learning models.

**Account Takeover:** Fraudsters gain access to merchant or customer accounts
to initiate payments or change payout details. Strong authentication, anomaly
detection, and payout account verification (e.g., micro-deposits) reduce risk.

**Friendly Fraud:** Cardholder disputes legitimate transactions (e.g., family
member used card, regret purchase). Mitigated by clear billing descriptors,
order confirmation emails, and responsive refund processes.

**Merchant Fraud:** Fraudulent merchants using the platform to process
illegitimate transactions. Underwriting, transaction monitoring, and
chargeback ratio thresholds help identify and terminate bad actors.

**Operations Role:** Payment operations does not investigate fraud directly.
We provide awareness guidance and escalate suspected fraud to the Fraud team.
Merchants reporting unauthorized transactions or account compromise should
be directed to the Fraud and Chargeback team immediately.
"""  # noqa: E501

