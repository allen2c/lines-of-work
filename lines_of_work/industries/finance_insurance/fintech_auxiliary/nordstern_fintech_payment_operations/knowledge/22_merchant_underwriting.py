content = """
Merchant underwriting assesses risk before and during the business relationship.
Nordstern's Underwriting team makes approval decisions; Operations provides
information about the process.

**Application Review:** New applications are evaluated for: business model
legitimacy, expected transaction volumes, product or service type, geographic
markets, and applicant history. High-risk categories undergo enhanced review.

**Ongoing Monitoring:** After approval, we monitor chargeback ratios, dispute
patterns, refund rates, and transaction velocity. Deteriorating metrics can
trigger account review, reduced limits, or termination.

**Reserve Accounts:** High-risk or new merchants may be subject to rolling
reserves — a percentage of processing held for a period to cover chargebacks.
Reserve terms are specified in the merchant agreement.

**Termination:** We may suspend or terminate accounts for: excessive
chargebacks, fraud indicators, AML/KYC concerns, terms violations, or
regulatory requirements. Merchants receive notice per agreement terms.

**Operations Role:** We do not approve or reject applications. We can
explain typical timelines, document requirements, and escalation paths
to Underwriting for status inquiries.
"""  # noqa: E501

