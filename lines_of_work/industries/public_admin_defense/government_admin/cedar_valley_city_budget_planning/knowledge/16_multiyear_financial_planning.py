title = "Multi-Year Financial Planning"

content = """
A multi-year financial plan projects revenues, expenditures, and
reserves beyond the current budget year. It informs sustainable
decisions.

**Purpose:** Identify structural gaps or surpluses before they
crisis. Show the long-term impact of current choices: new hires,
debt issuances, or deferred maintenance.

**Assumptions:** Document economic, demographic, and policy
assumptions. Use conservative revenue growth and account for
known cost drivers (contracts, benefits, mandates). Update
annually.

**Scenarios:** Run baseline, optimistic, and pessimistic cases.
Sensitivity to key variables (e.g., sales tax growth, pension
contributions) helps council understand risk.

**Horizon:** Five years is common; ten may be used for capital
or pension. Balance detail with uncertainty—longer horizons
have wider error bands.
"""  # noqa: E501

