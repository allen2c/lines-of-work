title = "Department Budget Request Process"

content = """
Department heads submit budget requests through a structured process. Clear
guidance ensures consistency and comparability.

**Request Package:** Each department provides a narrative justification,
line-item detail for personnel and operating, and any capital requests.
Requests should tie to strategic goals and explain increases over baseline.

**Baseline vs. Additions:** Distinguish between maintaining current service
levels and proposed enhancements. New positions, programs, or expansions
require explicit justification and cost-benefit discussion.

**Review Standards:** Finance reviews for completeness, mathematical accuracy,
and alignment with adopted priorities. Requests exceeding guidance may require
escalation or reduction.

**Timeline:** Departments receive submission deadlines and format templates.
Late submissions complicate consolidation and may receive lower priority.
Extensions should be rare and documented.

**Communication:** Provide feedback to departments on assumptions, gaps,
and any requested revisions. Transparency builds trust and improves quality.
"""  # noqa: E501

