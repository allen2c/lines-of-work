title = "Fund Accounting Basics"

content = """
Municipal governments use fund accounting to segregate resources by purpose and
legal restrictions. Each fund is a self-balancing set of accounts.

**General Fund:** The primary operating fund for general government services
such as public safety, parks, and administration. It is funded chiefly by
property and sales taxes and unrestricted intergovernmental revenue.

**Enterprise Funds:** Self-supporting activities that charge fees, such as
water, sewer, and sometimes solid waste or transit. Revenues should cover
operating and capital costs; subsidies may be intentional policy choices.

**Special Revenue Funds:** Earmarked for specific purposes (e.g., street
maintenance from gas tax, grants for community programs). Revenues and
expenditures are restricted by law or grant agreement.

**Capital Projects and Debt Service Funds:** Track construction of major
facilities and repayment of bonds. Transfers from the general fund or
dedicated revenue support debt service.

**Internal Service Funds:** Recharge costs to other departments (e.g., fleet,
IT, risk management). Rates are set to recover costs and maintain equity.
"""  # noqa: E501

