title = "Variance and Mid-Year Adjustments"

content = """
Actual results will differ from the budget. Variance reporting and
mid-year adjustments keep the city on track.

**Variance Reports:** Monthly or quarterly reports compare actual revenues
and expenditures to budget. Significant variances—typically over 5–10%
or a threshold amount—require explanation. Investigate root causes:
timing, volume, rate changes, or errors.

**Favorable vs. Unfavorable:** For revenues, favorable means exceeding
budget; for expenditures, favorable means under budget. Be consistent
in definitions so readers interpret correctly.

**Supplemental Appropriations:** When unforeseen needs arise—disasters,
court orders, critical vacancies—council may adopt a supplementary
appropriation. Document the need, source of funds (reserves, revenue
overperformance, reallocation), and council action.

**Transfers:** Some adjustments can be handled by intradepartmental or
interfund transfers within council-delegated authority. Know the limits;
excess transfers require council approval.
"""  # noqa: E501

