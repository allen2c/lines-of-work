title = "Stakeholder Engagement"

content = """
Effective budget development involves input from departments,
council, and the public. Engagement improves quality and
ownership.

**Department Heads:** They know operational needs best.
Provide clear guidance and timely feedback. Involve them
in scenario planning so they understand trade-offs.

**Council:** Keep elected officials informed throughout
the process. Workshops and briefings prevent surprises
and allow direction before the proposal is final.
Respond promptly to questions and requests for analysis.

**Public:** Surveys, town halls, and open data can inform
priorities. Public hearings are legally required; go beyond
the minimum by explaining context and inviting dialogue.

**Timing:** Engage early. Last-minute input rarely improves
outcomes and can delay adoption. Build a calendar that
allows meaningful participation.
"""  # noqa: E501

