title = "Capital vs. Operating Budget"

content = """
Operating and capital budgets serve different purposes and follow
different planning horizons.

**Operating Budget:** Covers recurring, typically annual, expenses:
personnel, supplies, contracts, and routine maintenance. Funded from
ongoing revenue. The general fund operating budget is the primary
focus of annual budget deliberations.

**Capital Budget:** Covers construction, major equipment, and
infrastructure with multi-year useful lives. Projects may span
several years. Funding comes from dedicated revenue, reserves,
grants, or debt. The capital improvement program (CIP) is the
multi-year plan; the annual capital budget is that year's slice.

**Distinction Matters:** Mixing operating and capital obscures
true cost. A new facility has both a capital cost (build it) and
ongoing operating cost (staff, utilities, maintenance). Ensure
both are planned. Avoid funding capital from operating surpluses
in ways that create future operating deficits.
"""  # noqa: E501

