title = "Grant Funding Integration"

content = """
Grants can fund specific programs or capital projects. Integrating
them into the budget requires care to avoid over-commitment.

**Restricted vs. Unrestricted:** Grant revenue is usually restricted
to the grant's purpose. Budget it in the appropriate fund and
program. Do not use restricted grants to offset general fund gaps
unless explicitly allowed.

**Matching Requirements:** Many grants require local match (cash or
in-kind). Ensure the match is budgeted and available. Unfunded
matches create compliance risk.

**Timing:** Grant cycles often do not align with the fiscal year.
Recognize revenue when earned; spend within grant periods. Track
drawdown and reporting deadlines.

**Sustainability:** Grants are often time-limited. Plan for
continuation or wind-down. Avoid building ongoing programs on
one-time grants without a transition plan.
"""  # noqa: E501

