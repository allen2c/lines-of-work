title = "Citizen Communication Best Practices"

content = """
Citizens deserve clear, accessible information about how their
tax dollars are used. Effective communication builds trust and
supports informed participation.

**Plain Language:** Avoid jargon. Use 'property tax' instead of
'ad valorem levy' when addressing the public. Define terms when
necessary. Short sentences and active voice improve comprehension.

**Visual Aids:** Charts showing revenue mix, expenditure by
department, and year-over-year trends help readers grasp the
big picture. Ensure graphics are legible and labeled.

**Key Messages:** Lead with what matters: total budget, tax
impact, major changes, and how to engage. Put detail in
appendixes or linked documents for those who want it.

**Multiple Channels:** Publish the budget on the city website,
summarize in newsletters or social media, and make printed
copies available at libraries or city hall. Meet citizens
where they are.
"""  # noqa: E501

