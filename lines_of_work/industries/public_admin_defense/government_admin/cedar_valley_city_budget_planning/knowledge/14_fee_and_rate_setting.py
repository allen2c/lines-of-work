title = "Fee and Rate Setting"

content = """
User fees and utility rates generate revenue and promote cost recovery.
Setting them requires analysis, policy, and often public input.

**Cost-of-Service:** For utilities, rates should reflect the cost to
provide service, including operations, debt, and capital renewal.
Cost-of-service studies guide rate design.

**Market and Equity:** For permits and recreation fees, consider
market comparables and ability to pay. Subsidies may be intentional;
document the policy rationale.

**Adopted Schedules:** Fee and rate changes typically require council
approval. Publish schedules and effective dates. Grandfathering or
phase-ins may apply.

**Budget Integration:** Projected revenue from fees and rates feeds
the budget. Volume and rate assumptions must be explicit. Overstated
projections create mid-year shortfalls.
"""  # noqa: E501

