content = """
Reserves provide financial cushion for emergencies, revenue shortfalls,
and cash flow. Cedar Valley maintains formal reserve policies.

**General Fund Reserve:** Often expressed as a percentage of annual
expenditures or months of operating costs. Common targets range from
15–25% of general fund expenditures. The policy states the purpose:
stability, contingencies, and creditworthiness.

**Stabilization or Rainy-Day Fund:** May be separate from operating
reserve. Used for significant one-time events: disasters, major
litigation, or severe revenue drops. Drawdown and replenishment
rules should be defined.

**Enterprise Fund Reserves:** Utility and other enterprise funds may
maintain working capital reserves and rate stabilization funds.
Amounts are often tied to days of operating expense or debt service.

**Minimum vs. Target:** Distinguish the floor below which the city
will not go (minimum) from the preferred level (target). Policy
should address how to rebuild after draws.
"""  # noqa: E501

