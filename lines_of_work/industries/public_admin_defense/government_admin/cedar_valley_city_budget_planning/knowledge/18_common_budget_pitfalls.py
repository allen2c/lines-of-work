title = "Common Budget Pitfalls"

content = """
Several recurring mistakes undermine budget credibility and
sustainability. Avoid them through discipline and process.

**Over-Projecting Revenue:** Optimistic revenue assumptions
create mid-year shortfalls. Use conservative forecasts;
surprises should be on the upside.

**One-Time for Ongoing:** Using reserves or one-time grants
to fund recurring expenses creates a structural gap in
future years. Identify and plan for the cliff.

**Underestimating Personnel Cost:** Step increases, benefit
inflation, and fully loaded new positions are often
underestimated. Model all cost elements.

**Siloed Planning:** Capital without operating, or vice versa,
leads to unfunded needs. Coordinate across budget types.

**Poor Documentation:** Changes without clear rationale
undermine auditability and public trust. Document
assumptions, amendments, and approvals.
"""  # noqa: E501

