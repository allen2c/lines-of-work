title = "Municipal Budget Cycle Overview"

content = """
The municipal budget cycle typically follows an annual calendar aligned with the
fiscal year. Cedar Valley operates on a July 1 – June 30 fiscal year.

**Planning Phase (Jan–Feb):** The city manager issues guidance to departments
on strategic priorities and fiscal targets. Departments begin drafting requests
and revenue estimates. Economic and demographic assumptions are refined.

**Request Phase (Mar–Apr):** Departments submit detailed budget requests with
justifications. Finance consolidates requests and projects revenues. Preliminary
gaps or surpluses are identified.

**Development Phase (May–Jun):** The budget team produces a proposed budget,
including the general fund, enterprise funds, and capital plan. Council workshops
and public hearings are scheduled. The manager's proposed budget is published.

**Adoption Phase (Jun):** Council holds public hearings, deliberates, and adopts
the budget by ordinance before the new fiscal year begins. Amendments are
documented and incorporated.

**Execution and Monitoring (Jul–Jun):** Departments operate within adopted
allocations. Monthly or quarterly variance reports track performance. Mid-year
adjustments may be requested for unforeseen circumstances.
"""  # noqa: E501

