content = """
Municipal expenditures fall into personnel, operating, capital, and debt
service. Understanding these helps in allocation and variance analysis.

**Personnel (Salaries and Benefits):** Typically the largest share of general
government spending. Includes wages, health insurance, retirement, and other
benefits. Step increases, COLAs, and vacancy assumptions affect projections.

**Operating:** Non-personnel day-to-day costs: utilities, supplies, contracts,
maintenance, and professional services. Many are relatively fixed; others
scale with activity.

**Capital Outlay:** One-time or multi-year investments in facilities,
equipment, and infrastructure. Capital projects may be funded from current
budget, reserves, or debt. Distinguish from operating to avoid double-counting.

**Debt Service:** Principal and interest on bonds and notes. Schedule is
fixed by bond documents. Refinancing or new debt alters future years.

**Transfers:** Allocations to enterprise or special funds, or contributions
to reserves. Ensure transfers are matched by fund purposes.
"""  # noqa: E501

