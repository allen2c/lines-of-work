title = "Debt Management Overview"

content = """
Debt is a tool for financing capital projects. Prudent management
protects credit and limits future burdens.

**Types of Debt:** General obligation (GO) bonds are backed by the
full faith and credit of the city; revenue bonds are secured by
enterprise fund receipts. Lease-purchase and notes may be used for
smaller or shorter-term needs.

**Debt Capacity:** Rating agencies and covenants often limit debt
as a percentage of assessed value or revenue. Track ratios and
project future issuance against capacity.

**Debt Service Schedule:** Principal and interest payments are
fixed by bond documents. Budget for the full schedule; refinancing
or defeasance can reduce costs but must be done prudently.

**Best Practices:** Match debt term to asset life where practical.
Avoid over-relying on debt for recurring needs. Maintain clear
disclosure for investors and the public.
"""  # noqa: E501

