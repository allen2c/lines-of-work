title = "Strategic Planning Alignment"

content = """
Budget decisions should advance council-adopted strategic goals. Alignment
ensures resources follow priorities rather than inertia.

**Strategic Plan Reference:** Cedar Valley's strategic plan identifies
priorities such as public safety, economic development, infrastructure,
and community livability. Budget requests should explicitly link to these
goals. Tag each major initiative to its strategic objective.

**Priority-Based Budgeting:** When resources are constrained, allocations
are guided by priority ranking. Higher-priority services receive
protection; lower-priority or non-strategic items are first candidates
for reduction.

**Performance Measures:** Where possible, tie budget lines to outcome or
output measures. This supports accountability and future budget
justification. Avoid measures that cannot be reliably tracked.

**Trade-off Analysis:** When presenting options to council, frame choices
in terms of strategic impact. 'If we fund X, we cannot fully fund Y'
helps elected officials make informed trade-offs.
"""  # noqa: E501

