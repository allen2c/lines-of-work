content = """
- 予算案は1月定例議会に提出。まず本会議で市長が施政方針を述べ、予算案を上程。
- 常任委員会（総務、文教、民生、建設等）で分割審査。財政課担当者は各委員会に出席し、質疑応答。
- 予算特別委員会が設置される場合、全事業を横断的に審査。議員から修正案（例：特定事業の増額・減額）が提出される可能性。
- 修正案が提出された場合、財政影響試算（財源不足が生じる場合は基金取崩しや事業削減案）を速やかに作成し、市長部局としての対応方針を決定。
- 修正案が可決された場合、予算案を修正し再議決。否決された場合は原案のまま採決。
"""

