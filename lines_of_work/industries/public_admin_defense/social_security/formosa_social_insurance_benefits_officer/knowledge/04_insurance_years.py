title = "保險年資計算"

content = """
- 保險年資以「月」為單位計算，同一被保險人於不同雇主或自營作業期間之年資合併計算。中斷期間不計入，但已累計年資不因中斷而消失。
- 部分工時勞工之月投保薪資按實際工作時數比例計算，但年資仍以月為單位累計。每月工作時數未達基本工資對應時數者，該月年資仍計1個月。
- 軍職、公教人員轉任勞工時，其原有年資得依《軍公教保險年資轉換辦法》申請併計，但需檢附原服務機關證明。
- 年資計算截止日：老年給付以退保日為準；失能給付以診斷確定日為準；遺屬給付以被保險人死亡日為準；生育給付以分娩日為準。
- 系統自動計算年資，但承辦員需人工核對有無漏計或重複。若發現異常，應向保險費收繳單位查詢。
"""

