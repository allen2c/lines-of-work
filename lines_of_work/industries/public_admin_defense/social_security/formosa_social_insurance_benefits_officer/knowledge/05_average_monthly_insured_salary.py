content = """
- 平均月投保薪資（AMIS）為計算多項給付金額之基礎。計算方式：取被保險人事故發生前最近6個月（或加保期間最高60個月，依給付項目而定）之月投保薪資加總後除以月份數。
- 老年給付（年金）採用「最高60個月」之平均；老年一次金、失能、遺屬、生育、傷病給付採用「最近6個月」之平均。失業給付採用「最近6個月」之平均。
- 若被保險人於事故發生前6個月內有投保薪資調整，以調整後之月投保薪資為準。調整前之月份仍以原薪資計算。
- 計算期間若包含未加保月份（如退保後再保），該月份不計入，但需往前補足月份數。例如最近6個月中有1個月未加保，則往前推至第7個月。
- 系統自動計算AMIS，承辦員需確認計算期間正確，並核對投保薪資分級表。若發現薪資申報不實，應通報稽核單位。
"""

