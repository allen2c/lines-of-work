title = "與其他社會福利協調"

content = """
- 社會保險給付與國民年金（National Pension）不得重複請領。例如已領取社會保險老年年金者，不得再請領國民年金老年給付。但可請領差額（若社會保險年金低於國民年金）。
- 勞工退休金（勞退）為雇主提撥之退休金，與社會保險老年給付獨立，可同時請領。承辦員需告知申請人兩者差異。
- 低收入戶或中低收入戶之社會福利補助（如生活扶助、醫療補助）不因領取社會保險給付而受影響，但需主動通報社政單位。
- 職業災害給付與勞動部職災補償（如職災勞工保護法）有競合關係，應優先適用社會保險給付，不足部分再由勞動部補足。
- 承辦員於審核時，需查詢社福整合系統，確認申請人有無其他給付紀錄，避免重複或超額給付。
"""

