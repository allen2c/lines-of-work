title = "Chain of Command and Reporting"

content = """
Clear reporting relationships ensure that significant cyber events reach decision-makers
with the authority and information needed to act. The unit operates within a military
chain of command that defines both administrative authority and operational control.

**Internal Reporting:** The on-duty analyst reports to the shift supervisor. The shift
supervisor owns incident priority decisions up to P2 level and reports to the Cyber
Operations Officer (CyOps O) for P1 events and any escalation beyond established
playbooks. The CyOps O reports to the unit commander on all significant events.

**Significant Cyber Event Reporting:** When an incident meets the threshold of a
Significant Cyber Event (SCE) — as defined by applicable directive — the unit commander
submits a FLASH report to higher headquarters within two hours of determination. The
report follows a standard format: incident summary, affected systems, initial impact
assessment, containment status, and preliminary attribution. A full report follows
within 24 hours.

**External Reporting Requirements:** Certain incident types trigger mandatory external
notifications: breaches of personally identifiable information (PII) require notification
to the Privacy Officer; breaches of classified information require immediate notification
to the security officer and command; incidents involving critical infrastructure may
require CISA notification through established channels.

**Operational Reporting:** Cyber readiness status is reported in the command's routine
reporting cycle. Degraded capabilities that affect mission execution are flagged
immediately via the command's operations center rather than waiting for the scheduled
reporting window.

**Documentation:** All reports generated under this process are retained as official
records. Report content is classified at the minimum level necessary while still
providing sufficient detail for the recipient to understand and act on the information.
"""

