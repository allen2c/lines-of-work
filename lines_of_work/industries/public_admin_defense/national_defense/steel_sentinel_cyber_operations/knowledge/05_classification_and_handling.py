title = "Classification and Information Handling"

content = """
All information produced, processed, or stored by Steel Sentinel Command is subject to
classification rules that govern how it must be marked, stored, transmitted, and
destroyed. Mishandling classified information is a serious regulatory offense with
potential criminal liability.

**Classification Levels:** Information is classified at one of three levels — Confidential,
Secret, or Top Secret — based on the damage its unauthorized disclosure would cause to
national security. Compartmented information (e.g., SCI, SAP) requires additional
handling controls beyond the base classification level.

**Marking Requirements:** Every document, file, and communication containing classified
information must bear a classification marking on the top and bottom of each page.
Derivative classifiers must cite the source authority (Classification Guide or original
document) in the derivative classification block.

**System Accreditation:** Classified information may only be processed on systems holding
a current ATO at the appropriate classification level. Connecting a classified removable
medium to an unaccredited system constitutes a spillage event requiring immediate
reporting and remediation.

**Spillage Response:** If classified data is found on a system accredited at a lower
classification, immediately disconnect the system from the network, restrict access to
the area, and notify the Information System Security Officer (ISSO). Do not attempt to
delete the data without documented authorization. Remediation follows approved spillage
cleanup procedures.

**Transmission:** Classified information may only be transmitted over networks accredited
for that level (e.g., SIPRNet for Secret). Using commercial email, personal devices, or
unclassified messaging platforms to transmit classified content is prohibited regardless
of operational urgency.

**Destruction:** Classified hardcopy is destroyed using NSA-approved cross-cut shredders
or burn bags. Classified electronic media is sanitized using NSA-approved overwrite tools
or physical destruction per applicable regulations before disposal.
"""

