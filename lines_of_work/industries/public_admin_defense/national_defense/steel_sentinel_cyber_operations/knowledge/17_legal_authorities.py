title = "Legal Authorities for Cyber Operations"

content = """
All cyber operations — defensive and offensive — derive their authority from a
defined legal framework. Operating without a clear legal basis exposes the unit
and its personnel to criminal liability and undermines the legitimacy of mission
outcomes. The unit's staff judge advocate (SJA) provides authoritative legal
guidance; when in doubt, stop and seek JAG review.

**Domestic Legal Framework:** Defensive activities on military networks are authorized
under the commander's inherent authority to protect assigned forces and systems,
supplemented by applicable statutes and DoD Directives. Access to non-military networks
— even for purely defensive purposes — requires statutory authority (e.g., voluntary
agreements with network owners or specific legislative grants).

**International Law:** In theater, the law of armed conflict (LOAC) applies to cyber
activities that rise to the level of an "attack" under international humanitarian law.
Proportionality, distinction, and military necessity govern the use of any cyber
capability that produces physical effects or substantial disruption. Legal review is
mandatory before employing any capability with potential escalatory consequences.

**Status of Forces Agreements (SOFAs):** When the unit operates from foreign territory,
the applicable SOFA defines what activities are legally permissible and which require
host-nation consent. Activities not covered by the SOFA require explicit host-nation
authorization and are coordinated through the appropriate diplomatic channel.

**Privacy Protections:** Domestic law restricts government collection of U.S. persons'
communications and data even when monitoring for cybersecurity purposes. Minimization
procedures govern how incidentally collected U.S. person information is handled, stored,
and reported. These requirements are non-negotiable regardless of operational pressure.

**Authorization Reviews:** New or novel capabilities, techniques, or response actions
that have not been previously reviewed require a legal opinion before employment.
Existing legal opinions may not cover all variations of a technique; when uncertainty
exists, seek a new opinion rather than relying on a prior opinion that may not apply.
"""

