title = "Operational Security Principles"

content = """
Operational security (OPSEC) is the process of denying adversaries the ability to
collect, analyze, and exploit information that could be used to degrade unit capability
or mission effectiveness. In cyber operations, OPSEC failures can tip off adversaries
that they have been detected, reveal the unit's detection methods, or expose future
operational plans.

**Five-Step OPSEC Process:** (1) Identify critical information — what would harm the
mission if an adversary knew it. (2) Analyze threats — who could exploit that information
and how. (3) Analyze vulnerabilities — where could the information be exposed.
(4) Assess risk — likelihood and impact of each exposure. (5) Apply countermeasures
proportional to the risk.

**Critical Information Categories:** For a cyber operations unit, critical information
typically includes: current collection and detection capabilities, active investigation
targets and status, tool and technique signatures used in defense, incident indicators
not yet shared, and deployment schedules for cyber protection teams.

**Communication Discipline:** Sensitive operational details are not discussed on
unencrypted channels, in public spaces, or in any forum where the audience is not
verified. Even within the secure facility, need-to-know is enforced — not every team
member requires visibility into every investigation.

**Social Media and Open-Source Exposure:** Personnel are prohibited from posting
information about unit missions, technical methods, system vulnerabilities, or
investigation details on social media, conference presentations, or public forums
without prior review and approval. Adversaries actively monitor open-source channels
for indicators of defensive capability and investigative focus.

**Physical OPSEC:** Classified displays are positioned to prevent observation from
windows or common areas. Visitor access to operations spaces is controlled and
visitors are escorted. Printed classified material is never left unattended on desks.
"""

