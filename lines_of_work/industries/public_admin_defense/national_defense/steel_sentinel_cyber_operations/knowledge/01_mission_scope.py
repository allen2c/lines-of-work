title = "Mission Scope and Operational Boundaries"

content = """
Steel Sentinel Command's cyber operations function holds a narrowly defined mission:
defend assigned military networks, information systems, and critical infrastructure
control systems from unauthorized access, disruption, degradation, destruction, and
exploitation. The unit operates as a defensive cyber operations (DCO) element, distinct
from offensive cyber operations (OCO) authorities held at higher echelons.

**Assigned Assets:** The unit's area of responsibility (AOR) covers garrison networks at
designated installations, deployable mission command systems, logistics data enclaves,
and designated industrial control systems supporting critical infrastructure. A formal
memorandum of agreement (MOA) with each supported command defines the precise boundary
of what is "in scope."

**Out-of-Scope Systems:** Any system not listed in the current AOR document is out of
scope for active monitoring, scanning, or response without explicit written authorization
from the system owner and the unit commander. Responding to an out-of-scope incident
requires an emergency temporary authorization or a formal tasking order from higher
headquarters.

**Authority to Operate:** All defensive activities must be covered by an active
Authorization to Operate (ATO) under the Risk Management Framework (RMF) or a temporary
interim authority. Operating on systems without a current authority constitutes an
unauthorized action reportable under applicable regulations.

**Mission Priorities:** In order of precedence — (1) protect command-and-control systems
supporting ongoing operations; (2) protect intelligence and classified data repositories;
(3) protect logistics and sustainment networks; (4) protect unclassified administrative
networks. Resource allocation decisions made under time pressure follow this hierarchy.
"""

