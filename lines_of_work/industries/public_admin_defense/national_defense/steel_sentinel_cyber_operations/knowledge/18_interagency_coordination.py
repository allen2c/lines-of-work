title = "Interagency and Partner Coordination"

content = """
Effective cyber defense at the national level requires deliberate coordination across
government agencies, military services, coalition partners, and in some cases the
private sector. No single organization has the complete picture or the full range of
authorities needed to defend complex environments.

**CISA Coordination:** The Cybersecurity and Infrastructure Security Agency (CISA)
serves as the national coordinator for critical infrastructure cyber defense. The unit
shares unclassified threat indicators with CISA through the Automated Indicator Sharing
(AIS) program and receives CISA advisories and emergency directives. When an incident
affects critical infrastructure beyond the military enclave, CISA is notified promptly.

**NSA Collaboration:** The NSA Cybersecurity Directorate produces authoritative
advisories on nation-state threats targeting defense networks. The unit maintains
an established liaison relationship for timely receipt of classified threat intelligence
products and can request technical support for complex incident investigations.

**Service and Joint Coordination:** When incidents cross organizational boundaries —
affecting systems owned by a different military service or joint command — the unit
initiates coordination through the appropriate service CERT or the JFHQ-DODIN.
Deconfliction prevents duplicate response efforts and ensures a single authoritative
incident owner.

**Coalition Partners:** Under applicable information sharing agreements (ISAs), the unit
may share threat information with designated coalition partner cyber organizations.
All sharing with foreign partners is classified at the releasability level of the ISA
(e.g., REL TO FVEY) and reviewed to ensure no sources-and-methods equities are exposed.

**Private Sector:** When incidents involve commercial infrastructure or service providers,
coordination occurs through legal counsel to ensure appropriate information handling.
Voluntary information sharing arrangements must be in place before sensitive information
is disclosed to commercial entities.
"""

