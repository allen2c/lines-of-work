title = "Training and Readiness Standards"

content = """
Individual and collective proficiency in cyber defense directly determines the unit's
ability to detect and respond to threats effectively. The unit maintains a structured
training program that covers foundational skills, certifications, and mission-specific
exercises throughout the year.

**Required Certifications:** Personnel in cyber operations roles must hold current
certifications aligned to their position: at minimum DoDD 8140/8570 baseline
certification (e.g., CompTIA Security+ for IAT Level II, CISSP for IAM Level III).
Technical analysts pursuing advanced roles are expected to obtain practitioner-level
certifications such as CEH, GCIH, GREM, or equivalent as specified in the unit's
manning document.

**Annual Training Requirements:** All personnel complete annual cybersecurity awareness
training, insider threat awareness training, and classification management refresher
training. These are tracked in the unit's learning management system and must be
current before personnel can maintain access to classified systems.

**Hands-On Skills Maintenance:** Technical analysts participate in at least four
structured hands-on training exercises per year: two tabletop exercises (scenario-
based discussion of incident response) and two practical exercises using training
range environments that replicate unit infrastructure without operational risk.

**After-Action Learning:** Every significant incident, exercise, and red team event
generates a lessons-learned product. Analysts are expected to review and internalize
these products. Key findings are incorporated into playbook updates within 30 days.

**Deployment Readiness:** Personnel deploying with a cyber protection team (CPT) to
support a combatant command must complete a deployment readiness checklist covering
equipment, access credentials, classification authority, and applicable theater-specific
legal authorities before departure.
"""

