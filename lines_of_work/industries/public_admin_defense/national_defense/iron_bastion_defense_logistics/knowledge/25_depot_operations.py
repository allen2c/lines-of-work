title = "Depot Operations and Distribution"

content = """
Depots are strategic nodes in the defense supply chain where bulk inventory is stored,
processed, and distributed. Effective depot operations are critical for system-wide
readiness.

**Receiving:** Inspect and reconcile inbound shipments against advance shipping notices.
Update system records. Route to storage or cross-dock based on demand priority.

**Storage:** Organize storage by turnover (fast movers accessible) and compatibility
(hazardous, temperature-sensitive). Maintain proper environmental controls. Use
location management for accurate picking.

**Order Fulfillment:** Pick, pack, and ship outbound orders per priority and mode.
Verify picks against orders. Stage for carrier pickup or internal transport.

**Returns and Disposition:** Process returns, excess, and condemned materiel per
policy. Document disposition decisions. Recover value where possible through
reutilization or sale.
"""  # noqa: E501

