content = """
Transportation planning ensures materiel moves from source to destination safely, on time,
and within cost constraints. Iron Bastion Command coordinates with commercial carriers,
military transportation assets, and host-nation logistics when applicable.

**Mode Selection:** Select air, sea, rail, or truck based on urgency, volume, destination
access, and cost. Air is used for time-critical shipments; sea and rail for bulk.
Defense-unique requirements (e.g., convoy escort) may constrain options.

**Routing and Scheduling:** Plan routes that minimize transit time and cost while
avoiding unacceptable risk. Consider customs clearance, rest stops, and delivery windows.
Update plans when disruptions occur.

**Documentation:** Ensure all cargo documentation (bills of lading, customs forms,
hazardous materials declarations) is complete and accurate. Incorrect documentation
causes delays and potential penalties.

**In-Transit Visibility (ITV):** Track shipments from origin to destination using
approved tracking systems. Report delays or exceptions promptly to maintain visibility
for command and the requisitioner.
"""  # noqa: E501

