content = """
Hazardous materials (HAZMAT) require special handling, documentation, and transportation
compliance. Defense logistics often involves fuels, chemicals, munitions, and other
regulated substances.

**Classification:** Identify the hazard class (flammable, corrosive, oxidizer, etc.) and
UN number. Use Safety Data Sheets (SDS) and regulatory references. Misclassification
creates safety and legal risk.

**Packaging and Marking:** Use approved packaging and marking per applicable regulations
(DOT, IATA, IMDG). Ensure placards and labels are correct and visible. Inspect before
shipment.

**Documentation:** Complete shipping papers, declarations, and emergency contact
information. Carriers may refuse improperly documented HAZMAT. Retain copies for
regulatory compliance.

**Training:** Personnel who handle, pack, or ship HAZMAT must complete required training
and certification. Refresher training is mandatory at defined intervals. Document
training completion.
"""  # noqa: E501

