title = "Surge Capacity and Demand Spikes"

content = """
Surge capacity is the ability to increase supply chain throughput when demand spikes due
to contingency operations, exercises, or other events.

**Pre-Positioning:** Maintain buffer stock for items likely to surge. Position at the
right echelon—too far forward increases holding cost; too far back increases response
time.

**Vendor Capacity:** Understand suppliers' ability to ramp production or delivery.
Pre-negotiate surge terms where possible. Identify alternate sources for capacity
constraints.

**Transportation:** Secure access to additional transportation capacity—charter
aircraft, additional truck capacity, or expedited services. Relationships with carriers and
forwarders facilitate rapid booking.

**Prioritization:** When capacity is constrained, use clear prioritization rules.
Mission-critical units and deployed forces typically receive first allocation.
Document and communicate the logic.
"""  # noqa: E501

