title = "Inventory Management Principles"

content = """
Effective inventory management ensures the right materiel is available at the right place
and time without excessive holding costs. Iron Bastion Command applies core principles to
balance readiness with efficiency.

**Demand Classification:** Items are classified by consumption patterns (fast-moving,
slow-moving, or non-moving) and criticality. High-value, mission-critical items receive
closer monitoring and higher safety stock levels.

**Stock Positioning:** Inventory is positioned across echelons (strategic depots, tactical
forward stock, unit-level) based on lead times, consumption rates, and transportation
constraints. The goal is to minimize total system inventory while meeting service levels.

**Replenishment Logic:** Reorder points and order quantities are calculated using lead
time, demand variability, and desired service levels. Economic Order Quantity (EOQ)
and similar models inform purchase sizes when applicable.

**Cycle Counting:** Physical counts are conducted on a scheduled basis to reconcile
system records with actual stock. Discrepancies trigger investigation and corrective
action. High-value or sensitive items may require more frequent counts.
"""  # noqa: E501

