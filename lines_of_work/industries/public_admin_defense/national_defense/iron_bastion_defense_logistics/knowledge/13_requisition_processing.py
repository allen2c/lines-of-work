title = "Requisition Processing Workflow"

content = """
Requisition processing transforms user needs into funded, executable purchase actions. A
disciplined workflow prevents errors and ensures auditability.

**Intake:** Requisitions enter through the approved system. Validate that the requester
is authorized, funding is cited, and the requirement is clearly described. Reject incomplete
requisitions with a clear explanation.

**Validation:** Verify that the item or service is allowable, the quantity is justified,
and the delivery date is achievable. Check for duplicative requests. Consult technical
experts when specifications are complex.

**Sourcing:** Identify the appropriate source—existing contract, new purchase, or
interagency transfer. Route to the contracting officer when a new contract is needed.

**Order Placement:** Generate the purchase order or transfer document. Confirm funding,
delivery address, and inspection requirements. Transmit to the vendor and track to
completion.
"""  # noqa: E501

