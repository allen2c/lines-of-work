title = "Controlled Unclassified Information (CUI)"

content = """
Controlled Unclassified Information (CUI) is information that requires safeguarding
under federal law or policy but is not classified. Defense logistics often involves
CUI such as procurement plans and supply chain data.

**Categories:** CUI is organized by category (e.g., procurement-sensitive, export
control). Each category may have specific handling requirements. Consult the CUI
registry for authoritative guidance.

**Marking:** Mark documents and systems containing CUI with the appropriate CUI
marking. Recipients must understand the handling requirements.

**Transmission:** Transmit CUI only through approved channels (encrypted email, secure
file transfer). Do not use unsecured commercial systems for CUI.

**Vendor Handling:** When sharing CUI with vendors, ensure they have appropriate
agreements (e.g., NDA, CUI handling agreement) and secure systems. Limit sharing to
what is necessary.
"""  # noqa: E501

