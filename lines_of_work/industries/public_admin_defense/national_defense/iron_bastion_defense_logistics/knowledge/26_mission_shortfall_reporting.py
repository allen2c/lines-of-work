title = "Mission Shortfall Reporting"

content = """
Mission shortfall reporting communicates supply chain gaps that impact readiness or
mission execution. Timely, accurate reporting enables command intervention and
resource allocation.

**Identification:** Monitor fill rates, backorders, days of supply, and critical item
status. Flag items that fall below thresholds or that support high-priority missions.
Use automated alerts where possible.

**Assessment:** Assess impact—which units or missions are affected, severity, and
timeline. Distinguish between temporary delay and structural shortfall.

**Reporting Format:** Use standard formats and classification levels. Include item,
quantity short, impact, and recommended action. Brief in person when impact is severe.

**Escalation:** Route shortfalls to the appropriate level—local resolution first,
then higher headquarters. Document escalation path and response. Follow up until
resolved or accepted as risk.
"""  # noqa: E501

