title = "Cycle Counting and Reconciliation"

content = """
Cycle counting is a continuous inventory accuracy process that counts a subset of items
on a rotating schedule. It replaces or supplements full physical inventories.

**Schedule:** Count high-value and fast-moving items more frequently. Low-value,
slow-moving items may be counted annually. Publish the schedule so all parties know
when counts occur.

**Procedure:** Counters use system-generated count sheets. Count actual quantity,
record on the sheet, and enter into the system. A second party may verify for
high-value items. Document any variances.

**Reconciliation:** Investigate variances beyond the tolerance threshold. Common causes
include receiving errors, picking errors, theft, and system data entry mistakes. Correct
the record and address root cause where possible.

**Reporting:** Track count accuracy (percentage of items within tolerance) over time.
Improving accuracy reduces stockouts and excess orders driven by bad data.
"""  # noqa: E501

