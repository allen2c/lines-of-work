title = "Contingency Planning"

content = """
Contingency planning prepares the supply chain for disruptive events—natural disasters,
conflict, supplier failure, or transportation disruption. Plans enable rapid response.

**Scenario Development:** Identify likely disruptions based on geography, dependency on
single sources, and threat assessments. Plan for each high-probability scenario.

**Alternate Sources:** Document backup suppliers for critical items. Qualify alternates
before a crisis. Consider domestic versus international options and lead time implications.

**Surge Capacity:** Define processes to ramp up procurement and transportation when
demand spikes. Pre-negotiate surge clauses with key vendors where feasible.

**Communication:** Establish notification procedures and decision authorities. Ensure
command, vendors, and partners know whom to contact and when to escalate.

**Exercises:** Test contingency plans through tabletop or live exercises. Update plans
based on lessons learned.
"""  # noqa: E501

