title = "Supplier Quality Standards"

content = """
Supplier quality directly affects mission readiness. Substandard materiel can cause
equipment failure, safety incidents, and operational delay. Iron Bastion Command
enforces quality standards throughout the supply chain.

**Specifications:** Ensure purchase orders and contracts reference correct technical
specifications. Ambiguity leads to disputes and nonconforming deliveries.

**Inspection:** Inspect incoming materiel against the order and specifications. Document
acceptance or rejection. Rejected items are returned or corrected per contract terms.

**Corrective Action:** When quality issues arise, require the supplier to investigate
root cause and implement corrective action. Track repeat offenders; consider removal from
preferred supplier lists for persistent failure.

**Certifications:** Some items require supplier certifications (e.g., ISO, NADCAP).
Verify certifications are current before award and periodically thereafter.
"""  # noqa: E501

