title = "Logistics Readiness Metrics"

content = """
Logistics readiness metrics quantify the supply chain's ability to support mission
requirements. Iron Bastion Command reports these metrics to enable informed decisions.

**Fill Rate:** Percentage of requisitions filled within the target time. High fill rates
indicate healthy stock positioning; low rates may signal shortfalls or process issues.

**Days of Supply:** On-hand inventory expressed in days of consumption at current rates.
Used to identify items at risk of stockout or excess.

**Backorder Status:** Number and value of unfilled requisitions. Aging backorders indicate
persistent shortfalls requiring escalation.

**Transportation Metrics:** On-time delivery rate, average transit time, and exception
rate. These reflect carrier performance and process effectiveness.

**Reporting Cadence:** Establish a regular cadence for metric compilation and briefing.
Trend analysis over time is more informative than point-in-time snapshots.
"""  # noqa: E501

