title = "Demand Forecasting"

content = """
Demand forecasting projects future requirements to enable proactive inventory positioning
and procurement. Accurate forecasts reduce stockouts and excess inventory.

**Historical Analysis:** Review consumption history for each item. Identify trends,
seasonality, and outliers. Use clean data; exclude one-time events or data errors unless
they indicate a pattern.

**Mission Alignment:** Correlate demand with planned operations, exercises, and
contingencies. Adjust forecasts when mission plans change. Work with operational
planners to incorporate their input.

**Methods:** Apply appropriate techniques—moving averages, exponential smoothing, or
regression—based on data quality and volume. For low-demand or new items, use judgment
and comparative analysis. Document assumptions.

**Review and Update:** Forecasts age quickly. Establish a schedule to review and update
forecasts. Incorporate actual consumption and revised mission plans. Communicate
significant changes to procurement and transportation planners.
"""  # noqa: E501

