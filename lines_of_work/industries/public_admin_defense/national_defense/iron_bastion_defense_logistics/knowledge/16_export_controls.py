title = "Export Controls and Defense Trade"

content = """
Export controls regulate the transfer of certain goods, technology, and services to
foreign persons or destinations. Defense-related items are subject to the International
Traffic in Arms Regulations (ITAR) or Export Administration Regulations (EAR).

**Classification:** Determine whether an item is controlled and under which regime. Use
the US Munitions List (USML) or Commerce Control List (CCL). When uncertain, seek
export control counsel.

**Licenses and Exemptions:** Many defense exports require a license from the Department
of State or Commerce. Some transactions qualify for license exemptions—document the
basis carefully.

**Due Diligence:** Screen parties and destinations against denied-party lists. Do not
proceed with restricted transactions without proper authorization.

**Logistics Implications:** Ensure carriers and freight forwarders understand export
control requirements. Documentation must support the legal basis for the shipment.
"""  # noqa: E501

