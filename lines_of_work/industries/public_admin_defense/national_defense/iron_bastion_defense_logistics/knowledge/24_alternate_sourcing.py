title = "Alternate Sourcing Strategies"

content = """
Alternate sourcing reduces risk of single-point-of-failure and supports contingency
planning. Multiple sources provide flexibility when primary sources are constrained.

**Dual Sourcing:** Maintain two or more qualified sources for critical items. Split
allocations to keep both warm. Document qualification for each source.

**Geographic Diversity:** Consider sources in different regions to mitigate
geo-specific disruptions (natural disaster, conflict, trade restrictions).

**Domestic vs. International:** Domestic sources may offer faster delivery and avoid
export control complexity. International sources may offer cost or capability advantages.
Balance based on item criticality and mission needs.

**Qualification:** Qualifying alternate sources takes time. Do not wait for a crisis—
qualify alternates during steady state. Document technical and quality requirements.
"""  # noqa: E501

