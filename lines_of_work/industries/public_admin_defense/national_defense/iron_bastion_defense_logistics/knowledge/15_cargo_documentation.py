title = "Cargo Documentation Requirements"

content = """
Accurate cargo documentation is essential for customs clearance, carrier acceptance,
and chain-of-custody accountability. Errors cause delays and compliance failures.

**Bill of Lading (BOL):** The primary contract between shipper and carrier. It describes
the cargo, origin, destination, and terms. Ensure description matches actual contents and
weight. Obtain signed copies from the carrier at pickup.

**Packing List:** Itemized list of contents, quantities, and weights. Used for
receiving verification and customs. Must match the BOL and physical shipment.

**Customs Documentation:** For international shipments, provide commercial invoice,
certificate of origin, and any required permits or licenses. Work with customs brokers
when complexity exceeds in-house capability.

**Retention:** Retain all shipping documents for the prescribed period (typically several
years). These support audits, claims, and dispute resolution.
"""  # noqa: E501

