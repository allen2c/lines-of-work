content = """
- 廃棄対象：有効期限切れ弾薬、劣化・変形した弾薬、使用不能となった武器、除籍が承認された装備品。
- 手続きの流れ：
  1. 廃棄対象品目をリスト化し、上官の承認を得る。
  2. 防衛省（または方面隊）の廃棄計画に基づき、廃棄実施部隊（爆薬処理班等）に依頼。
  3. 弾薬の廃棄は、原則として爆破解体または焼却処分。自衛隊の演習場内の指定場所で実施。
  4. 武器の廃棄は、切断・破壊後、金属くずとして業者引き渡し。シリアル番号を消去し、除籍報告書を提出。
- 不発弾の処理：
  - 発見時は直ちにその場を立ち入り禁止にし、不発弾処理班（EOD）に通報。
  - 処理班到着まで、安全距離（小銃弾で100m以上、迫撃砲弾で300m以上）を確保。
  - 処理完了後、報告書を作成し、原因調査を実施。
- 帳簿処理：廃棄完了後、弾薬台帳・武器台帳から除籍。廃棄証明書を保管。
"""  # noqa: E501

