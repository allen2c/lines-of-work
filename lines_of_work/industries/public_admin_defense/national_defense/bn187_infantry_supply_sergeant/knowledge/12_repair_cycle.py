title = "Repair Cycle and Turn-In for Maintenance"

content = """
- Unserviceable equipment (condition code B) that is economically repairable is turned in to the supporting maintenance facility (e.g., Field Maintenance Shop – FMS). Use DA Form 2407 (Maintenance Request) along with the turn-in.
- The supply sergeant coordinates with the battalion maintenance officer (BMO) to prioritize repairs. Items with a high mission impact (e.g., radios, weapons) are expedited. The turn-in is processed in ULLS-G as a “transfer to maintenance.”
- When the item is repaired and returned, the maintenance facility issues a DA Form 2408-1 (Equipment Inspection and Maintenance Record) and a new condition code. The supply sergeant updates ULLS-G and reissues the item to the original hand receipt holder.
- If an item is condemned (condition code C), it is turned in to the SSA for disposal. The supply sergeant must ensure the FLIPL is complete before disposal. Condemned items are stored separately pending turn-in.
"""

