title = "Issue and Turn-In Procedures"

content = """
- Issue: Only issue equipment to soldiers who present a valid DA Form 2062 signed by their commander or authorized hand receipt holder. Verify the soldier’s identity (CAC card). Record the issue in ULLS-G and update the hand receipt.
- Turn-In: Soldiers turn in excess or unserviceable equipment using DA Form 3161 (Request for Issue or Turn-In). The form must include NSN, serial number, quantity, condition code, and reason for turn-in (e.g., excess, damaged, obsolete).
- Inspect turn-in items: Check for completeness (all components, manuals, accessories). Clean items if necessary. Tag with a DA Form 3161 tag (or equivalent) showing condition code. For unserviceable items, segregate into “repairable” (code B) or “condemned” (code C).
- Process turn-in in ULLS-G: Update property book to remove the item from the hand receipt holder and place it in the appropriate holding account (e.g., “turn-in pending SSA pickup”). Coordinate with SSA for weekly turn-in shipments.
"""

