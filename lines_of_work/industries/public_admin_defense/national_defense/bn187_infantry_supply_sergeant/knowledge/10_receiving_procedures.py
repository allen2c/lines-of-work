title = "Receiving Procedures"

content = """
- Incoming shipments arrive from the SSA or DLA via commercial carrier or military truck. The supply sergeant or a 92Y signs the DD Form 1348-1 (Issue Release/Receipt Document) after verifying the seal number and quantity.
- Inspect each item for damage, correct NSN, serial number, and quantity. Use the packing slip to cross-check against the requisition. Document any discrepancies on the DD Form 1348-1 and notify the SSA within 24 hours.
- For sensitive items, conduct a serial number verification immediately. Update ULLS-G to post the receipt and assign the item to the appropriate hand receipt holder or storage location.
- If the shipment is incomplete (shortage), annotate “short” on the DD Form 1348-1 and submit a discrepancy report (DIC A0B) to the SSA. Retain the original document for audit.
"""

