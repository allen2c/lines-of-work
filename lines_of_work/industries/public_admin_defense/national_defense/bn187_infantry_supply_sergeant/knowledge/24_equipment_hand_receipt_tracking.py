title = "Sub-Hand Receipt Tracking and Reconciliation"

content = """
- The supply sergeant maintains a master log of all sub-hand receipts (DA Form 2062) issued to company commanders, platoon leaders, and key NCOs. The log includes: hand receipt number, holder’s name, date issued, date of last inventory, and status (active/closed).
- Quarterly reconciliation: The supply sergeant generates a list of all active hand receipts from ULLS-G and compares it to the master log. Any hand receipt not inventoried in the last 90 days is flagged and a reminder sent to the holder.
- When a hand receipt holder transfers, the supply sergeant initiates a clearance process. The holder must turn in all items or transfer them to a new holder. The hand receipt is closed and filed.
- The supply sergeant also tracks “floating” hand receipts (e.g., for items loaned to other units). These are annotated with a special code and reconciled monthly. Any item loaned for more than 30 days requires a formal memorandum of agreement.
"""

