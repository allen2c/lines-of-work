title = "Emergency and Deployment Operations"

content = """
- During a deployment or contingency, the supply sergeant coordinates with the S4 to pre-position equipment and supplies. A deployment load plan is created listing all items to be taken, with hand receipts for each soldier.
- The supply sergeant ensures all sensitive items are inventoried and packed in sealed containers. A deployment hand receipt is issued to each soldier for their assigned equipment. The master hand receipt log is updated.
- In the event of a natural disaster or base emergency, the supply sergeant secures the warehouse and arms room, then reports to the battalion command post. Key items (weapons, NVGs, radios) are moved to a secure location if time permits.
- Post-deployment, the supply sergeant conducts a 100% inventory of all returned equipment within 7 days. Any losses are documented and FLIPL initiated. The property book is reconciled with the pre-deployment record.
"""

