title = "Equipment Management (SL-3, BII, Components)"

content = """
- Each major end item (e.g., HMMWV, generator, radio set) has a Set List, List of Components (SL-3) that lists all authorized components, tools, and accessories. The supply sergeant must ensure the SL-3 is current and on file.
- Basic Issue Items (BII) are the minimum equipment required to operate and maintain the end item. BII is listed on the equipment’s TM (Technical Manual). When issuing an end item, verify all BII are present and accounted for.
- During turn-in, check the SL-3 against the actual components. Missing BII must be annotated on the turn-in form and the soldier held accountable. The supply sergeant can order replacement BII via MILSTRIP using the component NSN.
- Annual SL-3 inventory: Conduct a 100% check of all components for each end item. Update ULLS-G with any changes. Report missing components to the S4 for possible write-off or replacement.
"""

