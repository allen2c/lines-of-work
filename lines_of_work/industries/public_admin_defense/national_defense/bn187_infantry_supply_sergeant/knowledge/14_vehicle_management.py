title = "Vehicle Management (PLL and Dispatch)"

content = """
- The supply sergeant manages the Prescribed Load List (PLL) for the battalion’s vehicles (HMMWV, LMTV, etc.). PLL items are repair parts that are stocked to support immediate maintenance needs. The PLL is reviewed monthly and adjusted based on usage.
- Vehicle dispatch: The supply sergeant issues vehicle keys and dispatch documents (DD Form 1970) to drivers. Verify the driver’s license and training. Record mileage and fuel level on dispatch.
- When a vehicle is turned in for maintenance, the supply sergeant removes the PLL items that are not part of the vehicle (e.g., tools) and updates the property book. The vehicle’s hand receipt is transferred to the maintenance facility.
- Annual vehicle inventory: Conduct a 100% check of all vehicles, including serial numbers, condition, and PLL components. Report any missing items to the S4.
"""

