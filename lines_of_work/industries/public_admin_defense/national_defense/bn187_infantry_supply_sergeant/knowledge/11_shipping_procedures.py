title = "Shipping Procedures"

content = """
- Outgoing shipments include turn-ins to the SSA, lateral transfers to other units, and items sent for repair. Prepare a DD Form 1348-1 for each shipment, listing NSN, serial number, quantity, and condition code.
- Pack items according to military packaging standards (MIL-STD-2073). Use appropriate cushioning, moisture barrier, and outer container. Label with the consignee’s UIC, address, and a barcode label.
- For sensitive items, use a locked container and require a signature from the carrier. The shipping document must include a “Sensitive Item” flag. Notify the SSA of the shipment via email or ULLS-G.
- Retain a copy of all shipping documents in the supply section’s outgoing log. Track shipments using the carrier’s tracking number. If a shipment is lost, initiate a tracer within 48 hours.
"""

