title = "Property Book Management"

content = """
- The property book is maintained in ULLS-G. All transactions (gains, losses, adjustments) must be posted within 24 hours of the event. The property book is the single authoritative record of all equipment assigned to the battalion.
- Each item is assigned a unique serial number (if applicable) and a National Stock Number (NSN). The property book tracks location, hand receipt holder, condition code (A – serviceable, B – unserviceable, C – condemned, etc.), and acquisition cost.
- Monthly reconciliation: Compare ULLS-G output to hand receipt logs and physical inventory counts. Any discrepancy over $100 must be investigated and documented on DA Form 3161 (Request for Issue or Turn-In) or DA Form 2062 (Hand Receipt).
- Annual 100% inventory is mandatory. The battalion commander appoints an inventory officer (usually a captain) to oversee the process. Results are reported on DA Form 1687 (Notice of Delegation of Authority – Receipt for Supplies) and signed by the commander.
"""

