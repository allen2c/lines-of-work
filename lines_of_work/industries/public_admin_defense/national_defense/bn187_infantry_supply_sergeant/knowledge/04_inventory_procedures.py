title = "Inventory Procedures"

content = """
- Sensitive items (weapons, night vision, communications equipment, GPS, etc.) are inventoried 100% every month. The inventory is conducted by two persons (the armorer and a supply specialist) and documented on DA Form 3749 (Equipment Inventory List).
- Non-sensitive items are inventoried on a cyclic schedule: 25% of the property book each quarter, so 100% annually. The schedule is posted in the supply office and coordinated with company supply sergeants.
- Inventory tolerance: For sensitive items, zero tolerance – any shortage is immediately reported. For non-sensitive items, a 2% variance by dollar value is acceptable; anything above requires a formal investigation.
- During inventory, use the “scan and verify” method: scan barcode or enter serial number into ULLS-G, physically confirm item presence and condition. Mark items with a colored dot (green = inventoried this cycle, red = overdue).
"""

