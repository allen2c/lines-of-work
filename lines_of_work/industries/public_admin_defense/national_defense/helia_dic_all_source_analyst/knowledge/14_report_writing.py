title = "Intelligence Report Writing Standards"

content = """
- BLUF format: 3–5 sentences capturing the bottom line, the key evidence, and the confidence call; never bury the lead.
- Section structure: BLUF, Key Judgments, Evidence (with line numbers and Admiralty codes), Gaps, Watch Items, Confidence Summary.
- Confidence levels: LOW (single source, weak corroboration, aged), MODERATE (partial corroboration, mixed reporting), HIGH (multi-INT, recent, high corroboration).
- Word economy: active voice, present tense for current state, future tense for projected activity; avoid jargon the consumer may not know.
- Length: routine INTREP ≤ 2 pages, priority estimate ≤ 6 pages, full strategic estimate ≤ 12 pages including annexes.
- Every product carries the analyst's name, branch, date-time group, classification line, CAPCO markings, and ICD-206 line numbers.
"""  # noqa: E501

