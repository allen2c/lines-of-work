content = """
當事人提出之實物證據（非書面文件）需經書記官收受、編號及妥善保管，
直至案件終結或法院命返還為止。

**證物收受程序：**
1. 當事人提出實物證據（如：契約原本、照片、電子裝置、現金、毒品樣品等），
   書記官填寫「證物清單」，記載收受日期、數量、外觀描述及提出人姓名。
2. 證物清單一式兩份，一份附入卷宗，一份交當事人收執。
3. 貴重物品、現金應立即交法院庫房保管，書記官不得留置。

**證物存放分類：**
- 一般證物（文件、照片）：存入卷宗相關封套，隨卷保管。
- 體積較大之物品：存放證物室，以案號及證物編號標示。
- 毒品、武器等危險物品：依法移交警察機關或管制單位保管，法院存查紀錄。
- 電子設備：存放有防靜電保護之專用儲存區。

**借閱與返還：**
法官或書記官得依訴訟需要調閱證物。案件終結後，書記官通知當事人
限期領回證物；未於期限內領回者，依法移送拍賣或銷毀。
刑事定罪案件之扣押物，依沒收宣告或裁定另行處置。

**鏈條監管（Chain of Custody）：**
任何人取用或移動證物，均應在「證物移動記錄表」上簽名，
確保證物之完整性，防止被指控變造或污染，維護裁判正確性。
"""

