title = "少年事件處理原則"

content = """
少年事件依《少年事件處理法》以保護優先、教育代替懲罰為核心理念，
本院少年法庭負責審理，書記官在辦理少年案件時須嚴格遵守保密及保護規定。

**少年事件種類：**
- **虞犯事件**：少年有觸犯刑法之虞，如慣常逃家、結交不良份子，
  由少年調查官調查後，聲請法院以保護處分處理。
- **少年刑事案件**：少年（十四歲至十八歲）犯罪，法院評估是否以刑事程序或保護程序處理。
- **兒童保護事件**：未滿十二歲之兒童觸法或受虐，以保護為主要手段。

**嚴格保密義務：**
少年之姓名、照片及其他足以識別身分之資訊，不得以任何方式公開或提供給媒體。
書記官不得接受媒體詢問少年案件，應一律轉院長室公關窗口處理。
違反者依《少年事件處理法》第八十三條可能構成刑事責任。

**少年調查程序：**
收件後由書記官轉交少年調查官，調查官進行環境調查及心理評估，
形成調查報告供法官參考。書記官協助安排調查官訪談及相關報告之卷宗歸檔。

**保護處分種類：**
法院得裁定之保護處分包括：訓誡、假日生活輔導、保護管束、感化教育等。
書記官在裁定後通知執行機關（如少年輔育院、緩起訴監護機關）並追蹤執行回報。

**與學校之協調：**
少年案件法院得通知學校，書記官依裁定辦理，但通知函之內容以必要範圍為限，
不得過度揭露案件細節，以維護少年在校之隱私及社會復歸機會。
"""

