content = """
電話詢問是民眾常用的查詢管道，書記官在電話服務中須兼顧效率、
禮貌與個人資料保護，無法當面核對證件使電話服務具有較高風險。

**接聽禮節：**
電話應在三聲內接聽，以「松山地方法院書記廳，您好，請問有什麼可以協助您？」
或類似標準語開頭。通話過程中保持語氣平穩清晰，避免使用法律術語而不加解釋。

**身分核驗：**
電話詢問案件進度前，書記官應核對詢問人與案件之關係：
- 詢問者自稱為當事人：核對案號加姓名。
- 詢問者自稱為律師：請提供律師字號或委任狀上之律師姓名。
- 無法核驗身分者：說明無法在電話中提供案件資訊，請親自至窗口辦理。

**可電話告知之事項：**
- 下一次庭期時間與地點
- 案件目前所在程序階段
- 繳費方式及繳費地點
- 辦公時間及洽辦窗口

**不可電話告知之事項：**
- 裁判書全文（需親自領取或透過電子送達系統）
- 對造當事人聯絡資訊
- 法官姓名（部分保護實務上不主動告知）
- 尚未確定之案件結果或法官之心證

**電話紀錄：**
重要電話詢問（如當事人反映未收到通知、聲稱送達有誤）應填寫電話紀錄表，
記載來電時間、詢問事項及處理結果，存入相關案件備查。

**轉接與回電：**
如無法立即回答，應請對方留下聯絡資料，承諾查詢後回電，
並在當日辦公時間結束前完成回電，不可無限期拖延。
"""

