content = """
服務台窗口是法院與民眾直接接觸的第一線，書記官的服務態度與效率
直接影響民眾對司法機關之信任。本院採標準化服務規範，確保服務品質一致。

**接待禮節：**
書記官於民眾到達窗口時，應主動以禮貌問候語接待，
確認其來訪目的，並在力所能及範圍內提供協助。
不得因案件類型或當事人身分而有差別待遇。

**等候管理：**
服務台採取號碼牌制度，依序叫號。高峰時段（通常為上午開院後一小時）
應增開窗口，避免等候時間過長。書記官應定期向等候民眾說明預估等候時間。

**服務項目分流：**
書記官應能快速判斷民眾需求，並引導至正確窗口：
- 收件、案件查詢 → 民事科、刑事科或家事科窗口
- 費用繳納 → 出納窗口
- 卷宗閱覽 → 卷宗室
- 訴訟程序諮詢 → 訴訟輔導科
- 法律扶助 → 法扶基金會駐院服務台（如有設置）

**抱怨及不滿情緒處理：**
遇民眾情緒激動或表達強烈不滿，書記官應保持冷靜，
以理解及尊重的態度回應，說明程序限制，不與當事人爭辯。
如情況無法自行處理，通知主任書記官或法警協助，必要時請庭長介入。

**保持中立：**
書記官在服務台不得對法院裁判發表個人看法，
不得暗示案件結果，亦不得對任何一方當事人表達同情或偏袒。
任何時候均應保持中立、專業之立場。
"""

