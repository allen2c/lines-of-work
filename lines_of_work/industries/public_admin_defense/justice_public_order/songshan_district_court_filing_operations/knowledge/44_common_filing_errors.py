title = "常見收件與文書錯誤及預防"

content = """
書記官作業中的常見錯誤，若未及時發現，可能導致訴訟程序延誤或當事人權益受損。
掌握常見錯誤類型有助於強化個人查核習慣。

**收件階段常見錯誤：**
- **漏蓋收件章**：書狀未蓋收件章即流入作業程序，導致無法確認確切收件日期。
  預防：建立「蓋章→登錄→開立收據」之固定程序。
- **繕本份數計算錯誤**：多方訴訟時繕本份數計算失誤，導致一造未收到書狀副本。
  預防：收件時逐一清點繕本，對照當事人名單。
- **規費計算錯誤**：依錯誤基準（如以訴訟標的計算時使用不正確金額）計算規費。
  預防：一律使用司法院試算系統，不依記憶計算。

**分案階段常見錯誤：**
- **關聯案件未適用同一法官**：多起相關案件分由不同法官審理，增加矛盾裁判風險。
  預防：收件時主動查詢同一當事人之繫屬案件。
- **案件性質辨別錯誤**：將非訟事件登錄為訴訟事件，或反之，導致適用錯誤程序。
  預防：遇不確定之案件性質，先詢問主任書記官再登錄。

**送達階段常見錯誤：**
- **地址錯誤**：當事人於書狀載明之地址已非現住址，導致送達不到。
  預防：收件時確認地址完整，並利用戶政系統查詢（如有授權）。
- **回證追蹤疏漏**：送達完成但未在系統記錄送達日期，影響後續庭期安排。
  預防：每日核查未回收之回證清單，定期催查。

**判決書作成常見錯誤：**
- **當事人姓名誤植**：繕本上姓名有誤，影響文件效力。
  預防：正本製作後由另一書記官逐字核對。
- **上訴期間告知錯誤**：告知錯誤之上訴期間，侵害當事人訴訟權。
  預防：對照書狀類型確認正確期間，並在送達附記書上明確記載。
"""

