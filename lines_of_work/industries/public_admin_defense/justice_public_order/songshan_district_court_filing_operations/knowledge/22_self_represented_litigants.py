title = "自行訴訟當事人協助"

content = """
未委任律師之自行訴訟當事人（俗稱「自訴人」或「訴訟人本人」）往往缺乏法律知識，
書記官應在程序上給予必要協助，但須嚴守「告知程序、不提供法律意見」之界線。

**書記官可提供之協助：**
- 告知應向哪個窗口辦理、需要準備哪些文件。
- 提供書狀範本（司法院網站或本院服務台均有提供）。
- 說明訴訟程序步驟（如收件後流程、開庭方式）。
- 告知裁判費計算方式及繳費方式。
- 告知上訴期間、聲請救助之程序。
- 轉介法律扶助基金會或本院訴訟輔導科。

**書記官不可提供之事項：**
- 建議是否應提起訴訟或上訴（屬法律判斷）。
- 協助撰寫或修改書狀內容。
- 告知案件勝訴可能性或法官傾向。
- 提供法律解釋或對法律條文進行說明（超出行政範疇）。

**訴訟輔導科：**
本院設有訴訟輔導科，由資深書記官提供較深入之程序說明，
並協助自行訴訟當事人閱覽格式化書狀範本。書記官應主動引導
有需求之民眾前往訴訟輔導科，而非自行逐一解釋法律程序細節。

**無障礙服務：**
對於行動不便、視障、聽障或語言障礙之當事人，書記官應安排合適之溝通輔助，
如通譯、手語翻譯或書面說明，確保程序公平近用。
"""

