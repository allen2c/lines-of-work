title = "案件進度查詢服務"

content = """
民眾、當事人及律師得向本院查詢案件進度，書記官於提供資訊時
須兼顧服務效率與個人資料保護原則。

**查詢管道：**
1. **服務台窗口**：攜帶身分證明及相關案號或當事人姓名，當場查詢。
2. **電話查詢**：撥打書記廳直線電話，書記官電話確認身分後告知進度。
3. **線上查詢**：司法院訴訟進度查詢網站（https://e-court.judicial.gov.tw）
   提供當事人自助查詢，無需洽書記官即可取得基本進度資訊。
4. **律師查詢**：委任律師可至法院調閱卷宗或使用律師登錄系統查詢。

**可提供之資訊：**
- 最近一次庭期及地點
- 下一次預定開庭日期
- 案件目前所在程序階段（如：準備程序、言詞辯論、判決審核）
- 判決書是否已作成（可告知案號及作成日期，但不當場提供全文）

**不可提供之資訊：**
- 法官對案件之心證或初步看法
- 尚未公告之判決內容
- 對造當事人之聯絡資料或住所
- 非查詢人所涉案件之進度（需確認查詢人為當事人或其代理人）

**身分確認原則：**
電話查詢時，書記官應核對查詢人之姓名與案件當事人名單是否相符，
並詢問案號或另一足以確認身分之資訊，如無法確認身分，僅能請對方親自至窗口辦理。
"""

