content = """
開庭排程係由書記官協調法官、當事人（或其律師）及法庭空間，
安排庭期的行政作業，影響訴訟程序之效率與當事人之期日安排。

**排程職責分工：**
法官秘書負責與法官協調可用庭期，書記官負責向當事人傳達庭期、
管理法庭室使用登記，以及製發開庭通知書。

**開庭通知書製作：**
通知書應載明：案號、當事人姓名、開庭目的（如準備程序或言詞辯論）、
日期、時間、地點（法庭室號）、注意事項（如需攜帶證物）。
通知書應於開庭日七日前送達（緊急情形除外），不足七日者應先電話通知。

**法庭室管理：**
本院各法庭均登載於法庭室排程系統，書記官於確認庭期後鎖定法庭，
避免雙重安排。法庭清潔、音響設備及錄音錄影系統由法院管理員負責，
書記官於開庭前確認設備正常。

**庭期變更：**
當事人或律師可於開庭七日前申請庭期變更，由法官裁量是否准許。
書記官收到申請後轉呈法官，並通知所有當事人調整結果。
無正當理由拖延訴訟之申請，法官得予駁回。

**複數事件同日安排：**
部分法官同日安排多件庭期，書記官應預留合理間隔（至少三十分鐘），
並設置候庭區讓民眾等候，維持法庭秩序及程序流暢。
"""

