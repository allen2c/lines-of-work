content = """
家事事件涉及婚姻、親子及家庭關係，兼具高度隱私性及複雜情感因素，
本院家事庭依《家事事件法》處理，書記官應以特別謹慎之態度辦理。

**家事事件類型：**
- **婚姻事件**：離婚（裁判離婚、調解離婚）、婚姻無效或撤銷。
- **親子事件**：確認親子關係、終止收養、監護權、探視權安排。
- **監護及輔助宣告**：對無行為能力人或限制行為能力人之監護宣告。
- **繼承事件**：遺囑認證、繼承人陳報遺產及拋棄繼承。
- **家庭暴力保護令**：通常保護令、暫時保護令之聲請。

**程序特色：**
家事事件法採「統合處理原則」，同一家庭相關事項得在同一程序中一併處理，
如離婚同時處理財產分配、未成年子女監護及扶養費。
書記官收件時須確認所有相關聲明事項均已記載於書狀，以利整合處理。

**保護令緊急程序：**
緊急保護令（EPO）聲請須於四小時內裁定，書記官立即轉交值日法官，
並通知警政機關備勤。一般暫時保護令則應於七十二小時內裁定。

**調查報告之保密：**
家事調查官所作之訪視調查報告，屬高度敏感文件。書記官以獨立封套保存，
非經法官核准，不得開示予任何一造當事人，以保護未成年人及脆弱家庭成員。

**拋棄繼承：**
繼承人應於知悉繼承後三個月內至法院辦理拋棄繼承聲請，
書記官收件後確認申請人身分及附具被繼承人死亡證明、繼承系統表等文件。
"""

