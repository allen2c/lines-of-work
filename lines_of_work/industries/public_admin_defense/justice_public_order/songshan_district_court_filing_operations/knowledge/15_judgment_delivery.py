title = "裁判書送達與上訴期間告知"

content = """
裁判書（判決書或裁定書）之送達係啟動上訴期間計算的法律行為，
書記官須準確執行並詳細記錄，以免因程序瑕疵引發爭議。

**裁判書製作：**
法官或受命法官作成裁判後，由法官秘書或書記官依裁判原本製作正本，
正本加蓋法院印及法官章，正本與原本核對無誤後方可送達。

**送達對象：**
民事判決正本送達兩造當事人（或其訴訟代理人）；
刑事判決正本送達被告、辯護律師及檢察官；
裁定依性質送達相關人員。

**上訴期間：**
- 民事通常訴訟：判決送達後二十日內提起上訴（小額程序為十日）。
- 刑事：判決送達後二十日內提起上訴。
- 裁定之抗告：裁定送達後十日內提起抗告。

**書記官告知義務：**
裁判書送達時，應附記上訴（或抗告）期間及管轄上訴法院（本院第二審為臺灣高等法院）。
親自到庭領取裁判書之當事人，書記官應口頭告知期間，並請其在收據上簽名確認。

**確定判決之後續：**
上訴期間屆滿而無上訴，案件確定。書記官在系統標注「確定」，
並依規定將判決書上傳至司法院裁判書查詢系統（在去識別化後公開）。
確定判決為執行名義，當事人可據以聲請強制執行。
"""

