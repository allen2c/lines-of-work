content = """
訴訟救助制度保障資力不足之當事人不因無力繳納費用而喪失訴訟權，
書記官應主動告知符合資格者，並協助辦理相關程序。

**訴訟救助（暫免裁判費）：**
依《民事訴訟法》第一百零七條，當事人無資力支出訴訟費用且其訴訟顯非無理由者，
得聲請法院准予訴訟救助。法院核准後，暫免繳納裁判費、執行費等；
勝訴後費用由敗訴方負擔，或由國庫墊付後日後求償。

**聲請程序：**
1. 當事人填寫訴訟救助聲請書，載明資力狀況及訴訟標的。
2. 附具財力證明：低收入戶或中低收入戶證明書、稅務資料、薪資單、存款餘額等。
3. 書記官收件後陳核法官，法官於收件後裁定准否（通常三至七日內）。
4. 核准者列入卷宗並通知當事人，暫停費用追繳程序。

**法律扶助基金會轉介：**
無資力者亦可向財團法人法律扶助基金會申請免費法律代理服務。
書記官可提供基金會聯絡資料及申請表格，但不代為審核申請資格。

**刑事案件：**
刑事被告之辯護費用由國選辯護人制度（《刑事訴訟法》第三十一條）保障，
符合一定條件者由法院指定公設辯護人或義務辯護律師，書記官應告知被告此項權利。

**注意事項：**
書記官在說明救助制度時，應保持中立立場，不對案件勝敗表示意見，
亦不評論當事人是否應聲請救助。
"""

