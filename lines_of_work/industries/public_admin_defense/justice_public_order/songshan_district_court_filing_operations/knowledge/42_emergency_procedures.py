content = """
書記廳的緊急應變程序涵蓋自然災害、火災、停電及人身安全事件，
書記官應熟知緊急應變計畫，確保民眾與同仁安全，並保護關鍵卷宗。

**火災應變：**
發現火情立即按下火警警報，大聲通知周遭人員疏散，
撥打119後依疏散路線引導民眾離開法院。書記官不得為搶救卷宗而延遲疏散。
卷宗保全依事先備份之電子卷宗為主，紙本卷宗之損毀應事後依保險及補救程序處理。

**地震應變：**
地震發生時，書記官應蹲下、掩護並抓住固定物（DROP-COVER-HOLD）。
地震後確認無人受傷，在建築管理人員宣布安全前不得重新進入辦公室。
評估結構安全後，依書記廳長指示決定是否繼續收件或暫停服務。

**停電應變：**
備用電源（UPS）應於停電後維持電腦系統至少三十分鐘，供書記官儲存未完成之作業。
停電期間啟動紙本備援程序，手動收件並填寫備援記錄。
停電若影響庭審，由法警及書記官協助當事人至安全區域等待。

**系統重大故障：**
JIS或電子卷宗系統當機時，書記廳長決定是否暫停收件或繼續紙本作業。
已收件之紙本資料應妥善保管，待系統恢復後補登。

**公共衛生事件：**
遇傳染病疫情，依行政院或衛生主管機關之指示調整服務方式，
如改採預約制、口罩佩戴要求或電子送達優先。書記官配合院方公告執行。
"""

