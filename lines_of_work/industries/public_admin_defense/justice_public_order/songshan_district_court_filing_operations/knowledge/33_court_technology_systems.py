title = "法院資訊系統操作"

content = """
書記廳日常作業高度依賴司法院建置之資訊系統，書記官應熟練操作各項系統，
並了解各系統之資料範疇與存取權限。

**書記廳管理系統（JIS — Judicial Information System）：**
核心業務系統，用於案件收件登錄、分案、庭期排程、文書管理及結案。
書記官以個人帳號登入，操作記錄全程留存稽核，任何修改均需輸入變更原因。

**電子卷宗系統：**
掃描文件上傳後，於此系統管理電子卷宗，供法官線上閱卷。
書記官負責確認文件上傳完整性及分類正確性。

**司法院訴訟費用試算系統：**
線上計算民事裁判費及其他規費，輸入案件性質及標的金額後，
系統輸出費用金額及繳費說明，書記官可直接列印繳費通知單。

**電子送達平台：**
向同意電子送達之律師或法人傳送文書，書記官確認收件人已設定電子信箱，
上傳文件後系統記錄傳送時間，收件人確認閱覽後自動產生回執。

**裁判書查詢系統：**
供民眾查詢已公開之裁判書，書記官協助民眾操作時應確認查詢者僅能存取公開資訊，
不得協助查詢受限制之裁判書。

**系統故障處理：**
遇系統當機或維護期間，書記廳啟動紙本備援程序：手寫收件章、人工登記收件清冊。
系統恢復後，書記官補登所有紙本收件資料，確保電子記錄完整。
"""

