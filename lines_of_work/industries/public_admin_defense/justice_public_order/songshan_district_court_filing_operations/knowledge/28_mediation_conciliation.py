title = "調解程序"

content = """
調解係於訴訟繫屬前或訴訟中，由調解委員協助當事人達成和解，
以解決紛爭、減少訴訟負擔的機制。本院設置民事調解委員會。

**強制先行調解之事件（依《民事訴訟法》）：**
- 因道路交通事故發生之損害賠償訴訟
- 勞動事件（部分類型）
- 鄰里糾紛（相鄰關係）
- 其他法律規定應先聲請調解之事件

**調解聲請程序：**
當事人填寫調解聲請書，記明對造資料、爭議事實及請求事項，
繳納調解費（費用低於訴訟費）後，書記官登錄並排定調解期日。

**調解委員安排：**
本院依調解事件性質（如：交通、消費、醫療、家事）選擇專業調解委員，
書記官依規則排定委員並通知雙方當事人，庭期安排於調解室。

**調解成立：**
雙方達成合意，書記官製作調解筆錄，載明和解條件，
由雙方當事人及調解委員簽名，法官核定後具有與確定判決相同之效力，
可作為執行名義。

**調解不成立：**
調解不成立時，書記官發給調解不成立證明書，當事人於三個月內向本院
提起訴訟，視為自聲請調解時起訴（時效起算利益保留）。

**訴訟中調解（和解）：**
訴訟繫屬中法官得試行調解，由法院書記官作成和解筆錄，效力同確定判決。
"""

