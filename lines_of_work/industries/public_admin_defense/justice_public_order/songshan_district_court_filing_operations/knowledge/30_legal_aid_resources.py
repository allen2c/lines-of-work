content = """
書記官雖不提供法律意見，但有責任告知當事人可尋求協助之相關資源，
尤其對弱勢族群及無力自行訴訟者，轉介適當資源是服務品質的重要一環。

**財團法人法律扶助基金會（法扶）：**
提供無資力者免費或低費之法律諮詢及訴訟代理服務。
各地分會可線上、電話或現場申請，審核通過後由律師代理訴訟。
書記官可提供法扶的聯絡資料與分支機構位置，但不代為申請。

**司法院訴訟輔導科：**
本院設訴訟輔導科，提供訴訟程序說明、書狀範本說明及表格填寫輔助，
不涉及具體法律意見。適合自行訴訟且需要程序引導之民眾。

**各地律師公會法律服務中心：**
台北市及新北市律師公會設有免費法律諮詢門診，民眾可預約諮詢。
書記官可提供台北律師公會聯絡方式。

**民事調解機制：**
鄉鎮市區公所調解委員會可受理一般民事及輕微刑事事件，
手續簡便、費用低廉，適合金額不大或希望維持關係的糾紛。

**社工及家暴資源：**
涉及家庭暴力、兒童保護或弱勢族群之案件，書記官應提供
家庭暴力防治中心（1388）及兒童保護熱線（113）等緊急資源，
並在必要時協助聯繫社工人員。

**法律知識平台：**
司法院官網、法務部全國法規資料庫及各法律扶助機構均提供法律知識文章，
書記官可引導民眾自行查閱，無需由書記官代為解釋。
"""

