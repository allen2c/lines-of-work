title = "緊急事件處理程序"

content = """
部分事件具有時效急迫性，書記官須識別並優先處理，
以避免當事人因程序延誤而遭受無法回復之損害。

**緊急保全事件：**
假扣押、假處分聲請事關債權保全，法院應於收件後儘速裁定。
書記官收到聲請書後，立即通知值日法官，並依緊急分案規則處理，
不得等待一般輪值分案流程。

**緊急保護令：**
家庭暴力案件之緊急保護令聲請（依《家庭暴力防治法》），
法院應於四小時內核發或駁回。書記官須立即轉由家事庭值日法官處理，
並通知警察機關配合執行。夜間及假日由院值班人員受理。

**搜索票及逮捕票：**
由檢察官或司法警察官聲請，書記官轉交值日刑事法官核准，
緊急情形可先以電話或通訊方式通知，事後補呈書面。

**羈押聲請：**
被告移送到院後，法官須於二十四小時內決定是否裁定羈押。
書記官應確保聲押書狀及相關卷宗即時送達主任法官，並安排訊問庭室。

**辦公時間外之受理：**
本院設有夜間及假日值班書記官，負責接收緊急文書、
協助值日法官處理緊急聲請，並維護值班記錄。

**優先處理標誌：**
書記官於系統登錄緊急案件時，應標記「緊急」旗標，
確保電腦排程及人工分發均優先處理。
"""

