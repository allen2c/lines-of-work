content = """
司法互助指本院與其他法院或機關相互協助辦理訴訟程序中特定行政事項，
常見形式為囑託送達、囑託調查及國際司法互助。

**囑託送達（國內）：**
本院案件之當事人住所在其他縣市，本院得囑託當地法院代為送達。
書記官製作囑託送達公函，連同待送文書及送達回證，
寄交受囑託法院書記廳，並在系統追蹤回證。

**囑託調查（國內）：**
本案涉及外縣市之證人或需在外縣市進行勘驗，本院得囑託該地法院代為詢問或勘驗。
書記官製作囑託調查公函，載明調查事項及問題，並附具相關卷宗資料副本。

**境外送達（國際）：**
向境外（含中國大陸）當事人送達，依下列管道：
- 與我國有司法互助條約之國家：透過外交部及條約規定之管道辦理。
- 未簽訂條約之國家：公示送達或以外交途徑嘗試送達。
- 中國大陸地區：透過海峽交流基金會辦理，費時較長。

**外國法院之囑託（來文）：**
外國法院寄來之司法互助請求書，由外交部轉交司法院，再分配至管轄法院。
書記官收到後轉法官核示，依許可範圍代為執行送達或調查，
完成後製作回覆公文寄回外國法院（透過外交部）。

**追蹤管理：**
囑託事件需妥善追蹤，書記官建立追蹤清單，定期確認受囑託法院之辦理進度，
逾期未回函者主動催查，確保不影響本案程序。
"""

