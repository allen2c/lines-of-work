content = """
裁判費（又稱訴訟費用）係民事訴訟人向國家繳納之規費，由書記官依法計算並告知，
當事人未繳納者，法院得裁定駁回其書狀。

**民事財產訴訟（給付之訴）：**
依《民事訴訟費用法》第四條規定，按訴訟標的金額或價額計算：
- 未逾十萬元：徵收一千元
- 逾十萬元至一百萬元部分：每萬元徵收一百元（最低一千元）
- 逾一百萬元至一千萬元部分：每萬元徵收九十元
- 逾一千萬元部分：每萬元徵收八十元
不足一萬元者，以一萬元計算。

**小額訴訟：**
訴訟標的十萬元以下，一律徵收一千元。

**非財產訴訟（確認身分之訴等）：**
徵收三千元（定額）。

**假扣押、假處分聲請：**
按請求保全之金額徵收二十分之一之費用，最低一千元。

**非訟事件（支付命令）：**
按請求金額徵收三百分之一，最低五百元。

**計算工具：**
書記官應使用司法院訴訟費用試算系統確認金額，避免人工計算失誤。
試算結果以系統輸出為準，告知當事人後填發繳費單。

**分期或緩繳：**
有資力不足情形者，當事人得向法院聲請訴訟救助（暫免繳裁判費），
書記官應告知此項權利並提供聲請書格式。
"""

