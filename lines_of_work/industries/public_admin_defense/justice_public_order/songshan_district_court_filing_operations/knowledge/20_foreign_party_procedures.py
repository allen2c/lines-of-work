title = "涉外當事人訴訟程序"

content = """
涉外當事人（外籍人士、外國法人或境外居住之國人）在臺提起或參與訴訟，
需就語言、送達及文件驗證等方面進行特別處理。

**語言與翻譯：**
不通中文之外籍當事人得向法院聲請配置通譯。書記官於分案後通知法官秘書
安排通譯，通譯費由聲請方墊付（視訴訟結果可請求對造分擔）。
書狀須附具中文譯本，翻譯機構之資格由法院書記廳確認。

**住所地確認：**
境外住所難以直接核實，書記官應要求當事人提供在臺之送達代收人資料，
或以律師事務所地址為送達地址。如無在臺地址，應告知當事人
後續文書將依條約或外交途徑送達，程序較長。

**外國判決認可：**
持有外國法院確定判決者，如需在臺強制執行，須先向本院聲請承認外國判決，
書記官收受聲請書後，確認附具外國判決正本及中文譯本（公認翻譯）、
外國法院出具之確定證明書等文件。

**中國大陸、香港、澳門地區：**
依《臺灣地區與大陸地區人民關係條例》等特別規定辦理，
送達途徑係透過財團法人海峽交流基金會，程序與一般境外送達不同。
書記官應查閱最新作業規範，避免使用已廢止之程序。

**領事認證文件：**
外國公文書（如公司登記文件、授權書）在臺使用前通常需經駐外館處認證
或海牙公約驗證（Apostille），書記官於收件時應確認文件已依規定認證。
"""

