title = "分案登錄程序"

content = """
分案係將收妥之案件依一定規則分配給特定法官或合議庭，
確保案件負擔公平分配並避免利益衝突。

**輪分規則：**
本院採電腦輔助輪分機制，依收件順序及法官候分序列自動分配，
不允許人為任意指定法官。民事、刑事、家事、少年案件分別在各庭之法官間輪分。

**分案例外：**
下列情形得例外處理：
- 同一當事人已有繫屬中案件：優先分配同一法官（關聯案件原則）。
- 法官迴避：當事人聲請法官迴避或法官自行迴避時，重新輪分下一位法官。
- 特殊庭別：少年案件由具少年事件處理經驗之法官審理。

**系統操作：**
書記官於書記廳管理系統（JIS）輸入案件基本資料（案件性質、訴訟標的金額、
當事人資料等），系統自動計算輪分結果並顯示分配法官。
書記官確認後鎖定分案結果，系統同步通知法官秘書。

**分案完成後：**
系統產製正式案號，書記官列印案件封面（Case Cover Sheet），
附於卷宗最前頁，並將卷宗移交對應法官之秘書或書記官。

**錯誤分案之更正：**
如發現分案錯誤（如應適用關聯原則而未適用），書記官應立即向庭長報告，
由庭長決定是否移案，不得自行更動系統分案結果。
"""

