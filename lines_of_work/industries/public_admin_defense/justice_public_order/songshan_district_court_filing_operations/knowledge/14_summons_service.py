title = "文書送達程序"

content = """
送達係將法院文書合法交付當事人或其代理人，係訴訟程序之關鍵環節，
送達是否合法直接影響程序效力。

**送達方式（依優先順序）：**
1. **郵務送達**：最常見方式，委由郵政機關以掛號方式送達，
   郵差填寫送達回證後回寄法院。
2. **電子送達**：對已同意電子送達之律師或當事人，透過司法院電子送達系統傳送，
   收件人確認閱覽後即完成送達。
3. **執達員送達**：由法院執達員（法警）親自送達，用於掛號無法送達之案件。
4. **囑託送達**：向外縣市當事人送達，囑託當地法院代為辦理。
5. **外交途徑送達**：向境外（包括中國大陸地區）當事人送達，透過外交部辦理。

**送達不能之處理：**
郵務送達無法完成（無人收受、地址不詳、拒絕收受）時，郵差應在現場留置通知單，
當事人可在指定日期前至郵局領取。逾期仍未領取，書記官轉而安排執達員送達。

**公示送達：**
當事人之住所、居所或事務所不明，或依前述方式均無法送達時，
得聲請（或依職權）公示送達。書記官協助準備公示送達聲請或裁定，
裁定核准後於法院公告欄及司法院網站公告，滿二十日後發生送達效力。

**送達回證管理：**
書記官應逐一追蹤送達回證之回收，並在系統標注送達日期。
未收回回證之案件列入追蹤清單，定期催查，確保所有文書均完成合法送達後方進行下一步程序。
"""

