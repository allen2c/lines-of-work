content = """
時效及除斥期間是訴訟中常見的程序問題，雖然判斷是否已過時效屬法律見解，
書記官仍應具備基礎認識，以利向當事人說明盡速就法律諮詢的重要性。

**請求權消滅時效（民法）：**
- 一般請求權：十五年（《民法》第一百二十五條）
- 商事債權、利息、租金等：五年（第一百二十六條）
- 侵權行為損害賠償請求權：知悉時起二年，最長十年

**時效中斷：**
提起訴訟、聲請調解或仲裁，均可中斷時效，使時效重新起算。
書記官在收件時，雖不審查時效是否已過，仍可提醒有期限顧慮之當事人
儘速完成收件程序，因為收件日期即為起訴時間點。

**除斥期間（不可中斷）：**
除斥期間與時效不同，期間屆滿後，形成權本身消滅，不得行使，
亦無中斷或不完成規定。例如：
- 撤銷婚姻之訴：特定事由知悉後六個月內（《民法》第九百九十條）
- 遺囑認領：不受時效規定，但建議盡早辦理

**上訴期間的嚴格性：**
上訴及抗告期間為法定不變期間，逾期不得上訴（除聲請回復原狀）。
書記官在裁判書送達時應清楚告知上訴期間，以保障當事人訴訟權。

**回復原狀聲請：**
因不可歸責當事人之事由致逾法定不變期間，得於障礙消滅後一個月內
聲請回復原狀，由法官裁量是否准許。書記官受理聲請後轉法官核示。
"""

