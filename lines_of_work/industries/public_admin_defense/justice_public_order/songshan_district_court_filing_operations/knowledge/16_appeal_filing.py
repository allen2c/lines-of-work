content = """
上訴及抗告係當事人對下級法院裁判不服，請求上級審法院重新審查之救濟途徑。
書記官須正確識別書狀性質，並確認是否在法定期間內提出。

**上訴書狀審查重點：**
1. **期間合規**：確認書狀收件日期是否在判決送達日後之法定期間內。
   民事、刑事均為二十日；上訴期間末日如逢假日，順延至次一上班日。
2. **書狀格式**：民事上訴狀應載明原判決及上訴聲明，並具體指摘原判決違背法令之處。
   刑事上訴狀須載明上訴理由（實質理由得於收受判決後二十日內補提）。
3. **上訴費用**：民事第二審上訴費用按上訴聲明金額計算，比例高於第一審，
   書記官應告知並發給繳費單；刑事上訴不預繳費用。
4. **繕本份數**：上訴狀繕本應按他造人數提出。

**抗告書狀：**
對裁定不服提起抗告，期間為十日。抗告書狀載明原裁定案號及抗告理由，
書記官確認期間後，連同原裁定卷宗報請庭長，轉報上級審（臺灣高等法院）。

**第三審（最高法院）：**
民事第三審上訴須以違背法令為由，且訴訟標的逾一定金額；
刑事第三審上訴限定事由（通常需具刑事辯護律師協助）。
書記官在第一審僅負責收受書狀，不審查第三審上訴理由之實質是否充分。

**超期上訴之處理：**
超過法定期間之上訴書狀仍應收受登錄，由法官裁定是否准許回復原狀（如有正當理由），
書記官不得逕行拒收。
"""

