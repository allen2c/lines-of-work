title = "案件收件程序"

content = """
收件程序是案件進入法院系統的第一道關卡，正確的收件審核可避免後續程序延誤。
書記官於受理書狀時，應依下列步驟完成收件作業：

**第一步：格式審查**
確認書狀是否具備法定格式要件：當事人姓名（或名稱）、住所、訴訟代理人資料、
聲明事項、事實及理由、年月日、簽名或蓋章。民事書狀另須記明第一審管轄法院。

**第二步：管轄確認**
核對書狀所載案件是否屬本院管轄範圍。如有管轄疑義，先行受理並標注，
由分案法官或庭長判斷是否移送管轄法院，書記科不得逕予退件。

**第三步：附件清點**
對照書狀所載附件清單，逐一核對正本、繕本份數及相關證物是否齊備。
民事事件依對造當事人人數加一份備份，刑事自訴案件附件依規定辦理。

**第四步：規費確認**
查明是否已繳納裁判費或聲請費。未繳者於收件時告知計算方式，
並發給繳費單據，限期補繳；逾期未補繳者，書記官函請法官命補正。

**第五步：收件登錄**
書狀審查無誤後，於系統登錄收件資料，產製收件章（記載日期及流水號），
蓋於書狀封面及附件騎縫處，並交付收據予當事人或其代理人。

**退件情形：**
僅限書狀非屬司法文書（如純行政申請）、或顯非本院管轄且法規明定不得收受者，
方可當場說明並請其改投適當機關，不得逕行退件而未告知理由。
"""

