title = "案號編配制度"

content = """
案號是案件於本院之唯一識別碼，由書記官在系統完成收件後自動產製，
具有法律效力，不得事後修改（如有誤須依補正程序辦理）。

**案號結構：**
標準格式為「法院代碼 + 年度 + 案件類型代碼 + 流水號」。
例如：「松院民字第○○○○○號」（民事通常訴訟）、「松院刑字第○○○○○號」（刑事公訴）。

**常見案件類型代碼（民事）：**
- 民字：民事通常訴訟
- 簡字：民事簡易訴訟
- 小字：小額訴訟
- 裁字：民事裁定事件
- 調字：調解事件
- 執字：強制執行事件
- 全字：假扣押、假處分保全事件

**常見案件類型代碼（刑事）：**
- 訴字：刑事公訴通常程序
- 自字：刑事自訴
- 易字：刑事簡易程序
- 聲字：刑事聲請事件（含羈押、搜索票等）

**年度起算：**
案號年度依中華民國年度計算（如民國一一四年）。新年度案件於每年一月一日起重新
計算流水號，並以新年度代碼起訴。跨年度案件保留原始年度案號，不更改。

**多被告或多原告案件：**
同一案件僅有一個案號，所有當事人均納入同一卷宗。如後續追加當事人，
以補正方式更新案件資料，案號不變。

**查詢方式：**
民眾可至本院服務台、司法院訴訟進度查詢網站，或撥打本院電話，
以當事人姓名或案號查詢案件進度，書記官應協助操作並保護無關第三人資料。
"""

