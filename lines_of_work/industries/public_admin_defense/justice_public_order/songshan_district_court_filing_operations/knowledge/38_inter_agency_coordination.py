content = """
法院在訴訟程序中需與多個外部機關協調配合，書記官負責對外公文往返、
資料調閱及程序協調，確保司法行政與其他機關業務的順暢銜接。

**與松山地方檢察署之協調：**
- 刑事公訴案件起訴書由檢察署移送本院，書記官確認收件並分案。
- 判決確定後，書記官發文通知檢察署辦理執行（發監、罰金執行等）。
- 不起訴處分之再議或交付審判案件，書記官依規定程序轉送相關資料。

**與警察機關之協調：**
- 緊急保護令核發後，書記官立即以電話通知松山分局，並補發書面裁定。
- 執行搜索票或逮捕票，書記官協助核發裁定文件。
- 證人或被告到庭保護，書記官預先通知院警（法警隊）。

**與法務部行政執行署之協調：**
- 民事執行涉及行政義務（如稅款）移交行政執行署，書記官移交相關執行名義及資料。

**與地政機關之協調：**
- 假扣押裁定核發後，書記官發文通知地政事務所辦理假扣押登記，凍結不動產。
- 判決確定之所有權移轉，書記官出具必要之法院證明文件。

**與社政機關之協調：**
- 家事案件需家庭暴力防治中心或社工協助時，書記官發公函請求指派社工訪視，
  並安排社工出席庭期提供調查報告。
- 少年案件如需安置，書記官協助聯繫主管機關及安置機構。

**公文格式規範：**
對外公文依公文程式條例及本院公文格式手冊製作，
書記官在主任書記官或庭長核簽後方可發文。
"""

