content = """
案件終結後，書記官依規定辦理卷宗歸檔，並按保存年限管理卷宗，
期滿後依法定程序銷毀，以有效管理庫房空間並符合《公文書檔案管理法》規定。

**歸檔時機：**
案件終結（包括判決確定、和解成立、調解成立、駁回確定、撤回起訴等）後，
書記官製作「結案報告」並在系統標注結案狀態，卷宗封存後移送庫房。

**保存年限分類：**
- **民事判決案件**：確定後保存十年。
- **民事非訟案件（調解、強制執行）**：結案後保存五年。
- **刑事判決案件**：依刑度定期；死刑、無期徒刑案件永久保存；有期徒刑依刑期長短為五年至二十年不等。
- **少年案件**：少年成年後滿七年銷毀（有助社會復歸）。
- **家事案件**：依性質為十至三十年不等（婚姻案件較長）。

**電子卷宗保存：**
電子掃描版本之保存年限與紙本相同，儲存於司法院集中儲存系統，
設有定期備份機制。電子版本到期後由資訊部門執行刪除作業，書記廳配合確認。

**銷毀程序：**
保存期滿之卷宗由書記廳彙整清冊，陳報庭長及院長室核准後，
移送廢紙處理廠依保密方式銷毀（紙本以碎紙方式）。含有個人資料之文件
應確保銷毀過程無法復原，書記官保存銷毀紀錄備查。

**民眾申請補卷：**
案件已歸檔未銷毀之情形，當事人得申請調閱，書記官從庫房調卷，
並在調卷記錄中登載。調卷後應確認歸還並重新歸庫。
"""

