content = """
L'emploi stable réduit significativement le risque de récidive. Le service de
probation collabore avec les dispositifs d'insertion et les employeurs.

**Orientation :** Diriger vers Pôle emploi, les missions locales, les structures
d'insertion par l'activité économique (SIAE). Vérifier que la personne accomplit
les démarches demandées.

**Contraintes :** Certaines condamnations interdisent l'exercice de professions
particulières (enseignement, garde d'enfants, finance). Informer la personne
et l'employeur potentiel des restrictions.

**Documentation :** Consigner les démarches, les entretiens d'embauche, les
périodes d'emploi et les motifs de cessation. L'emploi peut justifier une
réduction de la fréquence des contrôles.
"""

