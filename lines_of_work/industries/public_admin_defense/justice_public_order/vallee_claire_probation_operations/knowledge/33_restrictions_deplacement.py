"""Knowledge item: restrictions de déplacement et autorisations de voyage."""

title = "Restrictions de déplacement et autorisations de voyage"

content = """
Les conditions de probation peuvent limiter les déplacements du probationnaire,
notamment en cas de risque de fuite, d'infractions liées au territoire ou de
mesures de protection des victimes.

**Interdiction de quitter le territoire :** Certaines peines interdisent toute
sortie du territoire national. L'agent vérifie que le probationnaire n'a pas
quitté le pays et documente toute demande exceptionnelle (urgence familiale,
obligation professionnelle) pour transmission au magistrat.

**Restriction géographique :** Des conditions peuvent limiter les déplacements
à une région, un département ou une commune. Les déplacements hors zone doivent
être autorisés à l'avance.

**Procédure d'autorisation :** Pour toute demande de voyage (professionnel,
familial, médical), le probationnaire adresse une demande écrite avec justificatifs.
L'agent évalue le risque, consulte le dossier et transmet sa recommandation au
tribunal ou au magistrat compétent.

**Contrôle :** En cas de surveillance électronique mobile, les déplacements sont
traçables. Les violations (déplacement non autorisé) sont documentées et peuvent
donner lieu à procédure de révocation.
"""

