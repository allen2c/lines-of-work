"""Standard obligations and conditions imposed during probation."""

title = "Obligations et interdictions de la mise a l'epreuve"

content = """
Les obligations et interdictions sont fixees par le tribunal ou le JAP. Elles doivent
etre proportionnees, realistes et liees aux objectifs de reinsertion et de prevention
de la recidive. L'agent de probation veille a leur comprehension et a leur respect.

**Obligations frequentes :** Se presenter aux convocations du service de probation ;
justifier de son activite (emploi, formation, recherche d'emploi) ; subir les
traitements imposes (toxicomanie, alcoolisme) ; indemniser les victimes ; effectuer
des travaux d'interet general ; respecter des mesures d'eloignement ou d'interdiction
de contact.

**Interdictions frequentes :** Se rendre dans certains lieux ; exercer certaines
activites ; rencontrer certaines personnes (victimes, coauteurs) ; posseder ou
porter une arme ; conduire un vehicule ; quitter le territoire.

**Information de la personne :** Toutes les obligations doivent etre remises par ecrit
et expliquees oralement. La personne signe un document attestant qu'elle a compris
les consequences d'un manquement.
"""

