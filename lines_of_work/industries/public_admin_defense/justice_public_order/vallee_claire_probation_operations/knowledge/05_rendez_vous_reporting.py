content = """
La frequence des rendez-vous est determinee par le niveau de surveillance et les
besoins identifies. Un premier entretien a lieu dans les quinze jours suivant la
notification de la decision. Un planning de suivi est etabli et communique a la
personne.

**Frequence type :** Niveau faible : un entretien par mois ; niveau moyen : deux
entretiens par mois ; niveau eleve : hebdomadaire ou plus. Des rendez-vous
supplementaires peuvent etre imposes en cas de situation instable.

**Contenu des entretiens :** Verification du respect des obligations ; evaluation
de la situation (logement, emploi, sante) ; orientation vers des dispositifs
d'accompagnement ; recueil d'informations pour le rapport au JAP.

**Absence non justifiee :** Toute absence doit etre signalee. Apres deux absences
non justifiees, un rapport est adresse au JAP. Des mesures de controle renforce
ou une convocation devant le JAP peuvent etre decidees.
"""

