title = "Procédures en cas de violation des obligations"

content = """
Une violation est un manquement aux conditions imposées par le juge. La réponse
doit être proportionnée, documentée et conforme à la procédure.

**Gravité :** Distinguer les manquements mineurs (retard isolé, oubli de
document) des violations graves (absences répétées, reprise d'activité délictuelle,
violation d'interdiction de contact). Les secondes justifient une saisine
immédiate du juge.

**Procédure :** Rédiger un rapport circonstancié. Saisir le juge de l'application
des peines (JAP) ou le procureur selon les cas. Proposer une mesure adaptée :
avertissement, renforcement des conditions, convocation devant le JAP, révocation.

**Délais :** Respecter les délais de convocation et de transmission des pièces.
La personne doit être informée des faits qui lui sont reprochés et pouvoir
présenter ses observations.
"""

