title = "Probation et mesures éducatives pour mineurs"

content = """
La justice des mineurs obéit à des principes spécifiques : primauté de l'éducatif,
réduction de la responsabilité en fonction de l'âge, protection renforcée de la
vie privée. Les mesures de probation pour mineurs sont adaptées à ces principes.

**Mesures possibles :** Remise aux parents, admonestation, placement sous surveillance
éducative, activité de jour, stage de formation, réparation. L'emprisonnement est
une mesure de dernier recours pour les mineurs.

**Accompagnement :** Le conseiller travaille en étroite collaboration avec la
famille et les services éducatifs. Les rendez-vous peuvent avoir lieu dans des
locaux adaptés. La scolarité et l'insertion professionnelle sont des priorités.

**Confidentialité :** Les dossiers des mineurs sont protégés par des dispositions
particulières. Les informations ne peuvent être communiquées que dans le cadre
strict des procédures légales.
"""

