"""Knowledge item: violence conjugale."""

title = "Cas de violence conjugale et conditions spécifiques"

content = """
Les personnes condamnées pour violence conjugale ou intrafamiliale font l'objet de
conditions de probation renforcées. L'agent de probation doit évaluer le risque de
récidive et les besoins de protection des victimes avant toute décision.

**Conditions obligatoires :** interdiction de contact avec la victime sauf ordonnance
du tribunal, participation à un programme de traitement des auteurs de violences,
respect d'une zone d'exclusion autour du domicile et du lieu de travail de la victime.
La surveillance peut inclure un bracelet électronique avec alerte de proximité.

**Évaluation du risque :** utiliser les outils validés (SARA, ODARA) pour évaluer la
dangerosité. Documenter toute tentative de contact ou de harcèlement. Les victimes
doivent être informées de leur droit à la notification et aux mesures de protection.

**Coordination :** travailler en étroite collaboration avec les services d'aide aux
victimes, les refuges et les forces de l'ordre. Toute violation des conditions doit
être signalée immédiatement au parquet et au tribunal.
"""

