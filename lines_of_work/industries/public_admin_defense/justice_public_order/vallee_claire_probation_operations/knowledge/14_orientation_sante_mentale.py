title = "Orientation vers les services de santé mentale"

content = """
Une proportion significative des personnes sous probation présente des troubles
psychologiques ou psychiatriques. L'orientation vers des soins adaptés fait partie
intégrante de la réinsertion et de la réduction des risques de récidive.

**Dépistage :** L'évaluation initiale inclut des questions sur l'historique
psychiatrique, les traitements en cours et les symptômes actuels. En cas de doute,
une évaluation spécialisée est demandée.

**Conditions de soins :** Le juge peut imposer une obligation de soins psychiatriques
ou psychologiques. Le probationnaire doit fournir une attestation de suivi régulier.
Les refus de soins sont documentés et peuvent conduire à un signalement.

**Liaison avec les soignants :** Avec accord du probationnaire, le service peut
échanger avec le médecin ou le psychologue traitant pour s'assurer de la continuité
des soins. La confidentialité médicale reste la règle ; seules les informations
pertinentes pour la surveillance sont partagées.
"""

