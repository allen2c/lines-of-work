"""Knowledge item: interdiction de contact avec les victimes."""

title = "Interdiction de contact avec les victimes"

content = """
L'interdiction d'entrer en contact avec la ou les victimes est une mesure
de protection courante, notamment en matière de violences, de harcèlement
ou d'infractions sexuelles. L'agent doit en assurer le respect strict.

**Définition du contact :** Le jugement précise généralement l'interdiction
de contact direct (physique, verbal) et indirect (courrier, téléphone, réseaux
sociaux, intermédiaires). En cas d'ambiguïté, interpréter de manière extensive
dans l'intérêt de la victime.

**Mesures d'éloignement :** Des conditions peuvent imposer une distance minimale
(par exemple 500 mètres) ou une interdiction de se rendre dans certains lieux
(foyer de la victime, lieu de travail, établissement scolaire des enfants).

**Vérification :** Lors des entretiens, l'agent interroge le probationnaire
sur tout contact, volontaire ou fortuit. Les déclarations de la victime (si
elle signale des manquements) sont recueillies et croisées avec le dossier.

**Violation :** Tout contact non autorisé constitue une violation grave. La
révélation immédiate au parquet et au tribunal est obligatoire. Documenter
précisément les faits, les témoignages et les éléments de preuve.
"""

