title = "Travail d'intérêt général (TIG)"

content = """
Le travail d'intérêt général consiste en des activités non rémunérées au profit
d'associations ou de collectivités. Il peut être ordonné comme condition de probation
ou comme peine alternative à l'emprisonnement.

**Organisation :** Le probationnaire est orienté vers une structure agréée (association
d'insertion, mairie, établissement public). Un contrat définit la nature des tâches,
la durée totale et le calendrier. Le service de probation vérifie l'effectivité
du travail.

**Suivi :** Des attestations de présence sont demandées à chaque session. En cas
d'absence non justifiée, un avertissement est délivré. Les manquements répétés
peuvent donner lieu à un rapport de violation.

**Adaptation :** Les tâches sont adaptées aux capacités physiques et aux compétences
du probationnaire. L'objectif est une contribution utile à la collectivité tout
en favorisant la responsabilisation.
"""

