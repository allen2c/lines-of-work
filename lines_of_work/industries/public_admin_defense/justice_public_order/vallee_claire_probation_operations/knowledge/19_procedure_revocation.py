title = "Procédure de révocation de probation"

content = """
La révocation met fin à la probation et peut entraîner l'exécution de la peine
d'emprisonnement initialement suspendue. Elle intervient en cas de violation
grave des conditions ou de commission d'une nouvelle infraction.

**Violations pouvant conduire à la révocation :** Nouvelle condamnation pénale,
absence répétée aux rendez-vous sans justification, non-respect des obligations
de soins ou d'emploi, violation des interdictions (contact avec la victime, zone
d'exclusion), falsification ou mensonge caractérisé.

**Procédure :** Le conseiller rédige un rapport de violation détaillé. Le parquet
décide de la suite à donner. Une audience de révocation est fixée. Le probationnaire
est convoqué et peut être assisté d'un avocat. Le juge entend les parties et
décide de révoquer ou de maintenir la probation, éventuellement avec des
conditions renforcées.

**Mesures alternatives :** Le juge peut choisir un avertissement, une prolongation
de la probation ou des conditions supplémentaires plutôt qu'une révocation complète.
"""

