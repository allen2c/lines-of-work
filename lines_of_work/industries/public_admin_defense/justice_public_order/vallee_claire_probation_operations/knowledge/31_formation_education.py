"""Knowledge item: formation et éducation des probationnaires."""

title = "Formation et éducation des probationnaires"

content = """
La formation et l'éducation constituent des leviers essentiels de la réinsertion.
Les conditions de probation peuvent imposer l'obtention d'un diplôme, une formation
professionnelle ou une scolarité adaptée.

**Obligations scolaires pour les mineurs :** Les mineurs sous probation doivent
généralement maintenir une scolarité régulière. L'agent vérifie les attestations
d'assiduité et les bulletins. Les absences non justifiées peuvent constituer
une violation.

**Formation professionnelle :** Pour les adultes, les conditions peuvent inclure
une formation qualifiante (permis de conduire, CAP, BEP, etc.). L'agent oriente
vers les organismes partenaires (Pôle emploi, missions locales, centres de formation)
et suit l'avancement.

**Éducation civique et sensibilisation :** Certaines peines prévoient des stages
de citoyenneté ou de sensibilisation (sécurité routière, violences, etc.).
L'agent s'assure de l'inscription et de la participation effective.

**Documentation :** Conserver les attestations de formation, diplômes obtenus
et certificats de stage dans le dossier. Ces éléments sont pris en compte pour
les rapports au tribunal et les demandes de libération anticipée.
"""

