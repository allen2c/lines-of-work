"""Legal framework governing probation in France."""

title = "Cadre legal de la probation"

content = """
La probation en France repose sur plusieurs textes fondateurs. Le Code penal definit
les peines pouvant etre prononcees en milieu ouvert : sursis avec mise a l'epreuve,
controle judiciaire, liberation conditionnelle. Le Code de procedure penale regit les
modalites d'execution et les pouvoirs du juge de l'application des peines (JAP).

**Sursis avec mise a l'epreuve :** Le tribunal peut prononcer une peine d'emprisonnement
tout en en suspendant l'execution, sous reserve du respect d'obligations et d'interdictions
pendant une periode determinee. En cas de manquement, la peine peut etre executee.

**Liberation conditionnelle :** Une personne detenue peut etre liberee avant le terme
de sa peine si elle presente des garanties serieuses de reinsertion. Elle reste sous
surveillance jusqu'a la fin de la periode d'epreuve.

**Role du JAP :** Le juge de l'application des peines decide des modalites de la mise
a l'epreuve, peut modifier les obligations, prononcer une revocation en cas de
manquement grave, ou accorder une liberation conditionnelle.
"""

