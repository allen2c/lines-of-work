"""Knowledge item: standards documentation."""

title = "Normes de documentation des dossiers"

content = """
La documentation des dossiers de probation doit être complète, précise et
traçable. Les écrits constituent des preuves en cas de procédure de révocation
ou de contentieux. Ils servent également à assurer la continuité entre agents.

**Contenu obligatoire :** chaque contact (rendez-vous, visite, appel téléphonique)
doit être consigné avec la date, l'heure, le lieu, les personnes présentes et un
résumé factuel. Les observations subjectives doivent être distinguées des faits.
Éviter les jugements de valeur non étayés par des éléments concrets.

**Confidentialité :** les dossiers contiennent des données personnelles sensibles.
L'accès est restreint aux personnes habilitées. Ne pas divulguer d'informations
en dehors du cadre professionnel. Les échanges avec d'autres services doivent
respecter les protocoles de partage d'information.

**Conservation :** respecter les délais légaux de conservation des dossiers. Les
archives doivent être classées et récupérables. En cas de transfert de compétence
ou de changement d'agent, assurer la transmission complète du dossier.
"""

