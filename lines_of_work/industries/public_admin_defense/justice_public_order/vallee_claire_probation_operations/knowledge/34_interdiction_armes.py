"""Knowledge item: interdiction de détenir des armes."""

title = "Interdiction de détenir des armes"

content = """
L'interdiction de détenir ou de porter des armes est une condition fréquente
en probation, notamment pour les infractions à caractère violent, les délits
d'armes ou les situations à risque.

**Portée de l'interdiction :** Vérifier dans le jugement si l'interdiction
couvre les armes à feu, les armes blanches, les armes de catégorie D ou
l'ensemble des armes. Certaines professions (sécurité, armée) peuvent faire
l'objet de dérogations sous conditions.

**Contrôle :** Lors des visites à domicile, l'agent peut procéder à des
vérifications. La présence d'armes non déclarées constitue une violation
grave. En cas de doute, saisir les autorités compétentes (police, gendarmerie).

**Remise des armes :** Si le probationnaire détenait des armes avant la condamnation,
il doit les remettre aux autorités. L'agent vérifie l'attestation de remise
ou de vente et la conserve au dossier.

**Signalement :** Toute infraction à l'interdiction (acquisition, détention,
port) doit être signalée immédiatement au parquet et au tribunal. La violation
peut entraîner la révocation de la probation.
"""

