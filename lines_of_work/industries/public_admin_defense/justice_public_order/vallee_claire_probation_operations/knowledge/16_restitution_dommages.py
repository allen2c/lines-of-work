"""Knowledge: restitution et dommages-intérêts."""

title = "Restitution et paiement des dommages-intérêts"

content = """
Les condamnations peuvent inclure une obligation de verser des dommages-intérêts
à la victime ou des restitutions. Le service de probation supervise le respect
de ces obligations financières et peut faciliter les modalités de paiement.

**Suivi des échéances :** Les montants et calendriers sont extraits du jugement.
Un plan de paiement peut être établi en accord avec la victime ou le greffe.
Les versements sont enregistrés et la victime est informée des paiements reçus.

**Difficultés de paiement :** En cas d'incapacité temporaire (chômage, maladie),
une demande de rééchelonnement peut être présentée au juge. La bonne foi et les
efforts du probationnaire sont pris en compte. L'absence totale d'effort peut
conduire à une violation.

**Priorisation :** Lorsque plusieurs dettes existent (amende, dommages-intérêts,
pension alimentaire), les priorités sont définies par la loi ou le juge. Le
probationnaire doit être informé de l'ordre des priorités.
"""

