"""Knowledge item: éthique professionnelle et déontologie."""

title = "Éthique professionnelle et déontologie"

content = """
L'agent de probation exerce une mission de service public à l'interface
entre l'autorité judiciaire, le probationnaire et la société. L'éthique
professionnelle guide chaque décision et chaque interaction.

**Impartialité :** Traiter chaque dossier avec objectivité, sans discrimination
liée à l'origine, aux convictions ou à la situation sociale. Les recommandations
au tribunal doivent reposer sur des faits et une analyse professionnelle.

**Intégrité :** Ne pas accepter de cadeaux, avantages ou pressions susceptibles
d'influencer le jugement. Refuser toute relation personnelle avec un probationnaire
ou son entourage qui pourrait compromettre l'exercice des fonctions.

**Secret professionnel :** Les informations recueillies dans le cadre des
entretiens sont couvertes par le secret professionnel. Les exceptions
(obligation de signalement, révélation au tribunal) sont strictement encadrées.

**Déontologie :** Respecter les codes de déontologie du service et les
recommandations de l'administration. En cas de conflit entre des instructions
et l'éthique, saisir la hiérarchie et, le cas échéant, les instances de
contrôle.
"""

