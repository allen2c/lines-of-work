"""Knowledge item: parole vs probation."""

title = "Différence entre probation et libération conditionnelle"

content = """
La probation et la libération conditionnelle (parole) sont deux régimes distincts de
surveillance communautaire. La probation s'applique en lieu et place d'une peine
d'emprisonnement ou en complément d'une peine partiellement exécutée. La libération
conditionnelle intervient après une période d'incarcération, sur décision d'une
commission ou du tribunal.

**Probation :** ordonnée directement par le tribunal au moment du prononcé de la
peine. Le condamné n'a pas nécessairement purgé de temps en détention. L'agent de
probation relève du service de probation rattaché au tribunal. La révocation
conduit généralement à l'exécution de la peine initiale.

**Libération conditionnelle :** accordée après examen du dossier par une commission
ou un juge. Le condamné a purgé une partie de sa peine en établissement pénitentiaire.
L'agent de probation ou de libération conditionnelle assure le suivi jusqu'à la fin
de la période de surveillance. La révocation entraîne le retour en détention pour
purger le reliquat de peine.

**Implications pratiques :** les deux régimes partagent des outils et des procédures,
mais les critères de révocation et les autorités compétentes diffèrent. Bien
identifier le régime applicable à chaque dossier.
"""

