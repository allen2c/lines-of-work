"""Knowledge item: reunification familiale."""

title = "Réunification familiale et relations familiales"

content = """
Le maintien ou la restauration des liens familiaux est un facteur de réinsertion
et de réduction de la récidive. L'agent de probation évalue la situation familiale
et accompagne le condamné dans le respect des conditions pouvant affecter ces
relations (interdiction de contact, zone d'exclusion, droits de visite).

**Évaluation :** identifier les personnes significatives, la qualité des relations
avant l'incarcération ou la condamnation, les besoins des enfants, les risques
(violence domestique, exposition des mineurs à des comportements inadaptés). Les
services de protection de l'enfance peuvent être associés si nécessaire.

**Conditions restrictives :** lorsque le tribunal impose une interdiction de contact
avec certains membres de la famille (conjoint, enfants), l'agent doit expliquer
clairement les limites et les conséquences des violations. Les demandes de
modification (droits de visite supervisés) passent par le tribunal.

**Soutien :** orienter vers des services de médiation familiale, de parentalité
ou de thérapie familiale lorsque cela favorise la réinsertion et ne contrevient
pas aux conditions. Documenter les progrès et les difficultés.
"""

