title = "Sécurité des agents de probation"

content = """
Les agents de probation effectuent des visites à domicile et des rencontres en
situation potentiellement à risque. La sécurité personnelle est une priorité
opérationnelle. Chaque service doit disposer de protocoles clairs et d'équipements
adaptés.

**Avant les visites :** consulter le dossier pour identifier les antécédents de
violence, les armes, les troubles mentaux ou la toxicomanie. Pour les cas à risque
élevé, effectuer les visites en binôme ou avec l'appui des forces de l'ordre.
Informez toujours un collègue ou la hiérarchie de votre itinéraire et de l'heure
prévue de retour.

**Pendant les visites :** rester vigilant aux signaux d'alerte (agitation, accès
aux armes, présence de tiers hostiles). Ne jamais pénétrer dans un lieu si vous
estimez que votre sécurité est compromise. Les entretiens peuvent se dérouler dans
un lieu neutre (bureau, centre de probation) si nécessaire.

**En cas d'incident :** disposer d'un moyen de communication d'urgence. Documenter
tout incident et le signaler à la hiérarchie. Les formations régulières aux
techniques de désescalade et à la gestion des situations conflictuelles sont
obligatoires.
"""

