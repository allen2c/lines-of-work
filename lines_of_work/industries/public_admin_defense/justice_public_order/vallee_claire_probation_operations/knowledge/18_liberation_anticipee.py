title = "Libération anticipée et clôture de la probation"

content = """
Une probation peut être clôturée avant le terme prévu si le probationnaire a
respecté toutes les conditions et démontré une réinsertion satisfaisante. La
décision relève du juge, sur proposition du service de probation.

**Critères d'éligibilité :** Au moins la moitié de la durée de probation écoulée,
aucune violation significative, respect des obligations financières ou plan en
cours, insertion professionnelle ou efforts démontrés, avis favorable du service.

**Procédure :** Le conseiller prépare un rapport de synthèse et une proposition
de libération anticipée. Le dossier est transmis au parquet et au juge. Une audience
peut être tenue. La décision est notifiée au probationnaire.

**Fin normale :** À l'échéance, si toutes les conditions ont été remplies, la probation
prend fin automatiquement. Un certificat de bonne conduite peut être délivré sur
demande pour faciliter l'insertion professionnelle.
"""

