title = "Transfert interjuridictionnel et accords de coopération"

content = """
Lorsqu'un condamné sous probation souhaite résider ou se déplacer dans une autre
juridiction (région, pays), des procédures de transfert ou de supervision
délégée s'appliquent. Les accords de coopération encadrent ces situations.

**Transfert de supervision :** le condamné demande un transfert vers sa nouvelle
résidence. La juridiction d'origine transmet le dossier à la juridiction d'accueil.
Celle-ci accepte ou refuse après vérification de sa compétence et de ses moyens.
En cas d'acceptation, la juridiction d'accueil assure le suivi et rend compte à
l'origine. Les conditions restent celles fixées par le tribunal d'origine, sauf
adaptation locale autorisée.

**Déplacements temporaires :** pour des séjours courts (voyage, formation), une
autorisation préalable peut être requise. La juridiction d'accueil peut accepter
d'assurer une supervision temporaire. Documenter les autorisations et les
coordonnées des correspondants.

**Accords internationaux :** entre États, des conventions bilatérales ou
multilatérales (ex. Convention de Strasbourg) régissent les transferts. Les
délais et les formalités varient. Consulter les services compétents.
"""

