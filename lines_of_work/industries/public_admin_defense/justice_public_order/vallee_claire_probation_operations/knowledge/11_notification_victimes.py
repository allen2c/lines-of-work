title = "Notification des victimes et droits à l'information"

content = """
Les victimes d'infractions ont le droit d'être informées de l'évolution de la situation
du condamné sous surveillance. Le service de probation Vallee Claire applique les
procédures de notification prévues par la loi.

**Droits des victimes :** Demande d'information sur la libération, les déplacements
ou les conditions de surveillance. Inscription au registre des victimes pour recevoir
les alertes automatiques. Droit de faire des déclarations lors des audiences de
révocation ou de modification de peine.

**Procédures internes :** Vérifier que la victime a été identifiée dans le dossier
d'enquête. Documenter toute demande de non-contact ou de zone d'exclusion. Transmettre
les demandes de notification au système central dans les délais impartis.

**Confidentialité :** Les coordonnées des victimes ne sont jamais communiquées au
condamné. Les échanges se font par courrier sécurisé ou plateforme dédiée.
"""

