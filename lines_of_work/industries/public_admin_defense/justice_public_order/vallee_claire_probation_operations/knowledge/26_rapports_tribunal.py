"""Knowledge item: rapports tribunal."""

title = "Rapports au tribunal et audiences"

content = """
L'agent de probation rédige des rapports à l'intention du tribunal à différentes
étapes : rapport pré-sentenciel, rapport de suivi, rapport en vue de révocation
ou de libération anticipée. La qualité et l'objectivité de ces rapports influencent
directement les décisions judiciaires.

**Rapport pré-sentenciel :** présente la situation personnelle, familiale,
professionnelle et sociale du prévenu, son passé judiciaire, les circonstances de
l'infraction et une évaluation des risques et des besoins. Propose une orientation
(probation, emprisonnement, mesures alternatives) sans préjuger de la décision du
tribunal. Les sources d'information doivent être citées.

**Rapport de suivi :** décrit la conformité aux conditions, les progrès réalisés,
les difficultés rencontrées et les recommandations (maintien, modification des
conditions, révocation). Être factuel et équilibré. En cas de violation, exposer
les faits et les éléments de preuve.

**Audiences :** l'agent peut être convoqué à comparaître. Préparer la présentation
orale, apporter le dossier et les pièces utiles. Répondre aux questions avec
précision. Ne pas dépasser le cadre de sa compétence technique.
"""

