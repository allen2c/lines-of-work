"""Knowledge item: planification reinsertion."""

title = "Planification de la réinsertion"

content = """
La réinsertion est un processus progressif qui commence avant la fin de la peine
et se poursuit après la libération. L'agent de probation contribue à l'élaboration
et au suivi du plan de réinsertion en coordination avec les services pénitentiaires
et les partenaires communautaires.

**Éléments du plan :** logement, emploi ou formation, santé (physique et mentale),
liens familiaux, gestion des dettes et des obligations financières, participation
à des programmes de traitement si nécessaire. Chaque domaine fait l'objet d'objectifs
mesurables et de jalons temporels.

**Anticipation :** pour les personnes en libération conditionnelle, le plan est
préparé en détention. L'agent de probation participe aux commissions de préparation
à la libération, rencontre le détenu et identifie les ressources disponibles dans
la communauté d'accueil. La continuité des soins (santé mentale, addiction) est
prioritaire.

**Suivi post-libération :** les premières semaines sont critiques. Renforcer la
fréquence des contacts, faciliter l'accès aux services et identifier rapidement
les obstacles. Ajuster le plan en fonction des difficultés rencontrées.
"""

