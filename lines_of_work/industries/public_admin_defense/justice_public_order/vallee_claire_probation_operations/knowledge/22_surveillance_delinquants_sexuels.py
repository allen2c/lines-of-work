title = "Surveillance des délinquants sexuels"

content = """
La surveillance des auteurs d'infractions à caractère sexuel exige des protocoles
spécifiques et une formation spécialisée. Les conditions imposées par le tribunal
incluent généralement l'interdiction de contact avec des mineurs, l'inscription au
registre des délinquants sexuels et la participation à un programme de traitement.

**Évaluation du risque :** utiliser les outils actuariels (Static-99R, STABLE-2007)
pour classer le niveau de risque. Les cas à haut risque font l'objet de visites
domiciliaires plus fréquentes et d'une surveillance renforcée. Vérifier la conformité
avec les restrictions d'emploi et de résidence.

**Conditions courantes :** interdiction d'accès aux lieux fréquentés par des mineurs
(écoles, parcs, centres de loisirs), restrictions d'utilisation d'Internet, obligation
de déclarer toute relation amoureuse impliquant des enfants. Les déplacements à
l'étranger peuvent être soumis à autorisation préalable.

**Traitement obligatoire :** orientation vers des programmes spécialisés en
réhabilitation des délinquants sexuels. Documenter la participation et les progrès.
Toute récidive ou comportement préoccupant doit être signalé sans délai.
"""

