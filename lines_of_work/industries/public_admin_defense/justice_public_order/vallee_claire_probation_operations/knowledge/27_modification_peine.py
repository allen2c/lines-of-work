"""Knowledge item: modification peine."""

title = "Modification des conditions de probation"

content = """
Les conditions de probation peuvent être modifiées en cours d'exécution sur demande
du condamné, de l'agent de probation ou du parquet. Le tribunal compétent statue
après audition des parties. Les modifications doivent être proportionnées et
justifiées par l'évolution de la situation.

**Demandes d'allègement :** réduction de la fréquence des rendez-vous, assouplissement
des restrictions de déplacement, suppression d'une condition devenue inutile. Le
condamné doit démontrer une bonne conformité et des progrès suffisants. L'agent
émet un avis motivé.

**Demandes de renforcement :** ajout de conditions (traitement, surveillance
électronique, zone d'exclusion) en cas de comportement préoccupant ou de
nouveaux éléments de risque. Documenter les faits justifiant la demande. Le
tribunal peut ordonner une audience contradictoire.

**Procédure :** la demande est déposée au greffe. Les parties sont convoquées.
L'agent prépare un rapport à jour. La décision est notifiée au condamné et au
service de probation. Les nouvelles conditions prennent effet à la date indiquée.
"""

