"""Knowledge item: obligations financières et suivi des paiements."""

title = "Obligations financières et suivi des paiements"

content = """
Les probationnaires peuvent être soumis à plusieurs obligations financières :
amende, frais de justice, restitution aux victimes, pension alimentaire ou
contribution aux frais de probation. Le suivi rigoureux de ces obligations
fait partie des missions de l'agent.

**Types d'obligations :** Identifier clairement dans le jugement ou l'ordonnance
les montants, échéances et bénéficiaires. Les amendes et frais de justice sont
versés au Trésor public ; les restitutions et pensions alimentaires aux créanciers
désignés.

**Plan de paiement :** Évaluer la capacité de paiement du probationnaire et,
si nécessaire, proposer un échéancier adapté. Un plan irréaliste favorise
l'échec et les contentieux.

**Suivi et relances :** Documenter chaque paiement reçu (date, montant, référence).
En cas de retard, procéder aux relances selon la procédure interne. Les manquements
répétés peuvent être signalés au tribunal.

**Modification :** Si la situation financière change (perte d'emploi, maladie),
le probationnaire peut demander une révision au tribunal. L'agent prépare un
rapport circonstancié à l'appui de la demande.
"""

