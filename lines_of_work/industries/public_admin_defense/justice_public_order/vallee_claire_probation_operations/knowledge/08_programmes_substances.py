title = "Programmes de traitement des addictions"

content = """
Les personnes sous probation avec une obligation de soins pour addiction sont
orientées vers des structures agréées. Le suivi est coordonné entre le service
de probation et le centre de traitement.

**Types de programmes :** Ambulatoire, résidentiel, groupes de parole. Le choix
dépend de la gravité, des antécédents et des ressources disponibles.

**Contrôles :** Vérifier l'assiduité aux rendez-vous. Les résultats des tests
de dépistage (alcool, stupéfiants) sont communiqués au service selon les
protocoles établis. Une abstention prolongée peut soutenir une demande de
levée anticipée.

**Manquements :** Les absences répétées ou les tests positifs constituent des
violations. Documenter chaque incident. Une rechute isolée peut conduire à un
renforcement du suivi plutôt qu'à une révocation si le traitement est poursuivi.
"""

