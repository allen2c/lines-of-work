"""Knowledge item: confidentialité et protection des données personnelles."""

title = "Confidentialité et protection des données personnelles"

content = """
Les dossiers de probation contiennent des données personnelles sensibles :
antécédents judiciaires, santé, situation familiale, adresses. Leur protection
est une obligation légale et déontologique.

**Cadre juridique :** Le Règlement général sur la protection des données (RGPD)
et la loi française relative à l'informatique et aux libertés s'appliquent.
Les données sont collectées pour l'exécution d'une mission d'intérêt public.

**Accès aux dossiers :** Seuls les personnels habilités et les autorités
judiciaires compétentes peuvent accéder aux dossiers. Toute communication
à des tiers (employeurs, bailleurs, etc.) requiert le consentement du
probationnaire ou une base légale explicite.

**Conservation et archivage :** Les dossiers sont conservés selon les délais
réglementaires. À l'issue, les données sont supprimées ou anonymisées sauf
exceptions prévues par la loi.

**Incidents :** Toute perte, divulgation ou accès non autorisé doit être
signalée au responsable de la protection des données et à la CNIL si
nécessaire. Documenter l'incident et les mesures correctives.
"""

