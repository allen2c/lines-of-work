title = "涉及特殊车辆的事故处理"

content = """
- 军车：查验军人证件、车辆行驶证、驾驶证，记录部队番号。事故处理需通知驻地部队保卫部门，由军地双方共同处理。不得扣留军车，但可暂扣驾驶员证件。
- 警车/消防车/救护车：执行任务时享有优先通行权，但发生事故仍需处理。确认是否在执行紧急任务（警灯警报开启），若在执行任务，记录任务性质，通知其单位领导。非执行任务时按普通车辆处理。
- 校车：查验校车标牌、驾驶人资格。事故后优先疏散学生，通知学校及教育部门。不得扣留校车，但可暂扣驾驶人证件。
- 外籍车辆：查验护照、国际驾照或翻译件。事故处理需通知出入境管理部门，涉及外交豁免的，报告上级并联系外事办。
"""

