content = """
- 追尾事故：后车负全部责任（除非前车倒车、溜车、强行变道）。
- 变道事故：变道车辆负全部责任（未确保安全距离）。
- 转弯事故：转弯车辆让直行车辆先行，转弯车负全责；但直行车辆未减速避让的，可能负次要责任。
- 掉头事故：掉头车辆让正常行驶车辆，掉头车负全责。
- 倒车/溜车事故：倒车/溜车方负全责。
- 无信号灯路口：让右方道路来车先行，未让行方负全责；支路让干路先行。
- 责任划分比例：全部责任100%，主要责任70%，同等责任50%，次要责任30%。简易程序只认定全部责任或同等责任，不划分主次。
"""

