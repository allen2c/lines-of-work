title = "Gestion des RFI (Request for Information)"

content = """
Une RFI est une demande d'information ou de clarification adressée au maître
d'œuvre ou à un autre corps de métier lorsque les documents du projet sont
insuffisants ou contradictoires. Le coordinateur MEP rédige, suit et archive
les RFI relatives aux lots techniques.

**Processus :** Identification du besoin, rédaction de la RFI (question claire,
référence aux documents, contexte), envoi au destinataire, suivi du délai de
réponse, intégration de la réponse dans les documents de conception, mise à
jour de la maquette BIM si nécessaire.

**Bonnes pratiques :** Une RFI par sujet, formulation précise, pièces jointes
numérotées. Éviter les RFI multiples qui retardent le projet ; regrouper les
questions liées lorsqu'elles concernent le même lot. Le coordinateur tient un
registre des RFI et vérifie que toutes les réponses ont été prises en compte.
"""  # noqa: E501

