title = "Réserves de passage dans la structure"

content = """
Les réserves de passage sont des ouvertures prévues dans les éléments
structurels (dalles, poutres, murs) pour le passage des réseaux MEP. Elles
sont dimensionnées par le BET structure sur la base des esquisses fournies
par les lots MEP. Un oubli ou une dimension insuffisante entraîne un
retard ou un surcoût en chantier.

**Typologie :** Traversées de dalle (fourreaux, coffres), traversées de poutre
(évidements), réservations dans les murs (gaines, passages de colonnes).
Chaque réserve doit être identifiée, cotée et reportée sur les plans structure.

**Délais :** Les lots MEP doivent transmettre leurs besoins en réserves avant
le lancement des plans structure définitifs. Le coordinateur centralise les
demandes, vérifie les conflits entre lots et transmet un document consolidé
au BET structure.
"""  # noqa: E501

