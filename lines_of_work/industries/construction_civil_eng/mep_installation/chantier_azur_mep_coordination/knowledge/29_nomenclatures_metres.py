title = "Nomenclatures et métrés"

content = """
Les nomenclatures listent les équipements et matériaux par lot. Les métrés
donnent les quantités (longueurs de câbles, surfaces de gaines, nombre
de bouches). Le coordinateur vérifie que les nomenclatures et métrés sont
alignés sur les plans et le CCTP. Une incohérence peut entraîner des
erreurs de dimensionnement ou des litiges contractuels.

**Contenu typique :** Référence produit, désignation, quantité, unité,
remarques. Les variantes et options doivent être clairement identifiées.

**Traçabilité :** Chaque mise à jour des plans doit être répercutée dans
les nomenclatures. Le coordinateur utilise des checks croisés (comptage
de bouches sur plan vs nomenclature) pour détecter les écarts.
"""  # noqa: E501

