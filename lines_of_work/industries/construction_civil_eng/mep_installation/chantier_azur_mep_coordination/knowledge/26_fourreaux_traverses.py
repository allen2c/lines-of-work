title = "Fourreaux et traversées"

content = """
Les fourreaux sont des gaines prévues pour le passage futur de câbles ou
de conduites (traversée de dalle, traversée de mur). Ils évitent de percer
la structure en phase exécution. Le coordinateur s'assure que tous les
fourreaux nécessaires sont demandés au BET structure et que leurs dimensions
sont suffisantes.

**Dimensionnement :** Le fourreau doit permettre le passage du câble ou de
la conduite, plus l'isolant et un espace pour le tirage. Un coefficient
de remplissage maximal (souvent 40 %) est appliqué pour les câbles.
Plusieurs fourreaux peuvent être regroupés en gaine technique.

**Traçabilité :** Chaque fourreau est identifié sur les plans (numéro,
diamètre, lot concerné). Le coordinateur vérifie que les nomenclatures
et les plans d'exécution mentionnent les fourreaux à mettre en place.
"""  # noqa: E501

