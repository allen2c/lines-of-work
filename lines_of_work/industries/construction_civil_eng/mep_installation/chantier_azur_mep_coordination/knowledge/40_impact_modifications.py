title = "Impact des modifications sur la coordination"

content = """
Toute modification d'un lot (évolution du programme, changement de
fournisseur, correction d'erreur) peut impacter les autres lots et
la coordination. Le coordinateur évalue l'impact et diffuse les
informations aux lots concernés. Les modifications non tracées peuvent
créer des conflits ou des non-conformités en chantier.

**Évaluation d'impact :** Qui est impacté ? Quels documents doivent être
modifiés ? Y a-t-il un impact sur les réservations structure ? Sur le
planning ?

**Processus de modification :** Demande documentée, validation par le
responsable de lot, mise à jour de la maquette et des plans, revue de
coordination, diffusion. Les modifications en phase exécution sont
gérées par des procédures spécifiques (avenant, PI).
"""  # noqa: E501

