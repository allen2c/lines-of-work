title = "Aditivos contratuais"

content = """
Aditivos alteram o contrato após a assinatura: quantitativo, preço, prazo,
escopo. Devem ser formalizados por escrito e aprovados conforme política
de governança. Aditivos informais geram risco jurídico e de auditoria.

**Tipos:** Aditivo de preço (reajuste, alteração de quantitativo); aditivo
de prazo (prorrogação); aditivo de escopo (inclusão ou exclusão de itens).
Lei 8.666 e 14.133 limitam percentuais e condições para aditivos em contratos
públicos.

**Processo:** Solicitação motivada, análise técnica e comercial, aprovação
hierárquica, elaboração do termo aditivo, assinatura pelas partes. Registrar
no sistema de contratos e vincular ao contrato original.

**Variação de quantidade:** Pequenas variações (até 25% em alguns casos)
podem ser previstas no contrato. Acima disso, novo processo licitatório ou
justificativa de dispensa.
"""  # noqa: E501

