content = """
A avaliação sistemática de fornecedores apoia decisões de recontratação e
qualificação. Indicadores típicos incluem pontualidade, qualidade e
conformidade documental.

**Pontualidade:** Percentual de entregas no prazo. Calcular como (entregas
no prazo / total de entregas) x 100. Considerar tolerância (ex. 3 dias) conforme
política interna.

**Qualidade:** Número de não conformidades por contrato ou por valor adquirido.
Classificar por gravidade (crítica, maior, menor).

**Conformidade documental:** Entregas com documentação completa e correta.
Não conformidades documentais atrasam aprovações e podem impactar a obra.

**Uso:** Consolidar em relatório trimestral ou anual. Fornecedores com
desempenho insatisfatório repetido devem ser reavaliados ou excluídos do cadastro.
"""  # noqa: E501

