content = """
A forma de pagamento impacta o fluxo de caixa e o risco das partes. Contratos
bem estruturados equilibram segurança do fornecedor e preservação de capital
da contratada.

**À vista:** Pagamento único após entrega e aceitação. Reduz risco de
inadimplência para o fornecedor, mas concentra saída de caixa. Menos comum
em grandes contratos.

**Parcelado:** Pagamento em etapas (ex. 30% antecipado, 70% na entrega) ou
conforme cronograma de entregas parciais. Antecipação pode exigir garantia
(seguro garantia, caução) para proteger a contratada.

**Prazo:** Pagamento em 30, 60 ou 90 dias após fatura. Melhora o fluxo de
caixa da contratada. O fornecedor pode incluir custo financeiro no preço.
Manter controle rigoroso de vencimentos para evitar atrasos e multas.
"""  # noqa: E501

