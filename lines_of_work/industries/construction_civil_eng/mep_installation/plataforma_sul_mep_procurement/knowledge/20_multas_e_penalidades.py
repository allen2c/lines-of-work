content = """
Contratos de aquisição costumam prever multas por atraso na entrega ou por
não conformidade. A aplicação deve seguir o disposto no contrato e ser
documentada para cobrança ou compensação.

**Multa por atraso:** Geralmente expressa em percentual ao dia sobre o valor
do atraso, com limite (ex. 10% do valor do contrato). Calcular a partir da
data prevista até a entrega efetiva. Requisitar formalmente ao fornecedor.

**Multa por não conformidade:** Pode incluir custos de substituição, frete e
retrabalho. Contratos bem redigidos definem esses critérios antecipadamente.

**Abatimento e compensação:** Em alguns casos, a multa é abatida do pagamento.
Manter registro das ocorrências e das comunicações para eventual disputa ou
arbitragem. Escale ao gerente de compras antes de aplicar multas elevadas.
"""  # noqa: E501

