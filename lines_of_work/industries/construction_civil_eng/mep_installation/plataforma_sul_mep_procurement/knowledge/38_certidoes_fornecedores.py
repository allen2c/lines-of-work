title = "Certidões exigidas de fornecedores"

content = """
Antes de contratar, a empresa exige certidões que comprovem a regularidade
fiscal e trabalhista do fornecedor. A ausência ou negativa pode impedir a
contratação ou gerar responsabilidade solidária.

**Certidão Conjunta (Negativa de Débitos):** Certidão positiva com efeito
de negativa ou certidão de quitação de tributos federais (Receita Federal).
Atesta regularidade fiscal.

**Certidão Trabalhista:** Negativa de débitos trabalhistas (CNDT). Obrigatória
para contratação com órgãos públicos e boa prática para empresas privadas.

**Certidão FGTS:** Regularidade com o Fundo de Garantia. Complementa a
verificação trabalhista. Validade: verificar prazo de cada certidão (30, 90,
360 dias) e solicitar renovação antes do vencimento.
"""  # noqa: E501

