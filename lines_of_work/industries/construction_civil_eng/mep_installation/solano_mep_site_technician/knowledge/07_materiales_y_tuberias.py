title = "Materiales y Selección de Tuberías"

content = """
La elección del material de tubería impacta directamente en la durabilidad, el costo,
el método de instalación y el cumplimiento normativo del sistema. El técnico MEP debe
conocer las propiedades, limitaciones y técnicas de unión de cada material para ejecutar
correctamente las especificaciones del proyecto.

**Cobre:** Material tradicional para agua potable y agua caliente. Alta resistencia a la
corrosión y vida útil superior a 50 años en condiciones normales. Tipos más comunes: Tipo
L (paredes medias, uso residencial y comercial) y Tipo K (paredes gruesas, enterrado y
alta presión). La unión por soldadura capilar (sweating) requiere limpieza, fundente y
aportación de estaño-plata o estaño-cobre a temperatura controlada.

**PVC (Cloruro de Polivinilo):** Ampliamente utilizado en drenaje sanitario y pluvial.
Clasificación: PVC-P (plastificado) para drenaje; PVC-U (no plastificado) para presión.
Rígido, liviano y económico. No es apto para agua caliente continua por encima de 60 °C.
Unión por solvente (cemento solvente) o espiga-campana con empaque de hule.

**CPVC (Cloruro de Polivinilo Clorado):** Similar al PVC pero con mayor tolerancia térmica
(hasta 93 °C), apto para distribución de agua caliente. Unión exclusiva con adhesivo para
CPVC certificado; no es intercambiable con el adhesivo de PVC.

**PPR (Polipropileno Random Copolímero):** Resistente a temperatura y presión. Unión por
termofusión mediante manguito: la tubería y el accesorio se calientan simultáneamente con
mandril y matriz a temperatura de fusión (~260 °C) y se ensamblan en tiempo controlado.
Extremadamente resistente a incrustaciones y a la corrosión química.

**Acero negro y galvanizado:** Usado en instalaciones de gas, redes contra incendio y vapor.
Unión roscada (NPT) o soldada (SMAW, MIG). El galvanizado ofrece protección contra oxidación;
no debe soldarse sin eliminación previa del recubrimiento en la zona de unión.

**HDPE (Polietileno de Alta Densidad):** Ideal para instalaciones enterradas, alta
resistencia al impacto y a la corrosión. Unión por termofusión a tope o electrofusión.

**Acero inoxidable (304/316):** Para sistemas de alta pureza (farmacéutica, alimentos) o
ambientes agresivos. Unión por soldadura TIG, prensado o roscado.
"""  # noqa: E501

