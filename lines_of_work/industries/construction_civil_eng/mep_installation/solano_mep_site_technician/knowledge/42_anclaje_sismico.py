title = "Anclaje Sísmico de Instalaciones MEP"

content = """
En zonas de actividad sísmica, los sistemas MEP pueden convertirse en riesgo si no están
correctamente anclados. Un terremoto puede desplazar o derribar equipos pesados, romper
tuberías de gas o agua, dañar tableros eléctricos y cortar el suministro de servicios
esenciales en el momento en que más se necesitan.

**Normativa de referencia:** ASCE 7 (Minimum Design Loads for Buildings and Other Structures)
define los requisitos de diseño sísmico para sistemas no estructurales, incluyendo
instalaciones MEP. Los códigos locales en países sísmicos (Chile, Perú, México, Colombia,
Ecuador) complementan o adaptan estos requisitos.

**Sistemas y equipos que requieren anclaje sísmico:**
- Equipos mecánicos con masa > 45 kg (bombas, compresores, unidades manejadoras de aire,
  chillers, torres de enfriamiento, generadores).
- Tableros eléctricos, transformadores y UPS.
- Tuberías de plomería y HVAC de diámetro ≥ 2½" (63 mm) en plomería, y ductos de sección
  mayor a 0,84 m² en HVAC.
- Tanques de presión, cisternas elevadas y depósitos de combustible.

**Tipos de soporte sísmico:**
- *Bracing lateral y longitudinal:* Varillas o perfiles de acero en diagonal, conectados
  a la estructura del edificio, que impiden el movimiento lateral y longitudinal de las
  tuberías durante el sismo.
- *Bases antivibratorias sísmicas:* Combinan aislamiento de vibración con anclaje sísmico
  mediante snubbers (amortiguadores) que limitan el desplazamiento sin transmitir vibración
  en operación normal.

**Cruces de juntas de expansión sísmica:** Los edificios tienen juntas de expansión que
permiten el movimiento relativo entre secciones durante un sismo. Las tuberías y ductos
que cruzan estas juntas deben usar conexiones flexibles con longitud adecuada para absorber
el desplazamiento esperado sin romperse.

**Instalación de anclajes:** Los anclajes a concreto deben usar taquetes certificados
para carga sísmica (expansión mecánica tipo epóxico o adhesivo). La resistencia de cada
anclaje se verifica con el catálogo del fabricante, según el diámetro, la profundidad
de empotramiento y la resistencia del concreto.

**Inspección y documentación:** Los soportes sísmicos son elementos de seguridad; su
instalación debe inspeccionarse y documentarse en el Plan de Puntos de Inspección. La
separación máxima entre soportes se define en el cálculo sísmico del proyecto.
"""  # noqa: E501

