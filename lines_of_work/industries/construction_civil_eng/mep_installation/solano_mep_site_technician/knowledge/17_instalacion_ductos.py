title = "Instalación de Ductos de Climatización"

content = """
La red de ductos distribuye el aire tratado desde la unidad manejadora hasta los espacios
del edificio y retorna el aire a la unidad para su recirculación o expulsión. Una red bien
diseñada e instalada garantiza confort térmico uniforme y eficiencia energética.

**Tipos de ductos:**
- *Chapa galvanizada:* El material más común en sistemas de climatización comercial e
  industrial. Se fabrica en tramos rectangulares o circulares según especificación. Norma
  de fabricación: SMACNA HVAC Duct Construction Standards.
- *Ducto flexible (flex duct):* Tramos cortos entre el plenum o caja de volumen variable
  (VAV) y la rejilla de distribución. Máximo 1,5 m; el exceso de longitud y los codos
  ajustados aumentan la resistencia aerodinámica y el ruido.
- *Ducto de fibra de vidrio (ductboard):* Paneles rígidos de fibra de vidrio con barrera
  de aluminio; aislamiento térmico y acústico integrados. Usado en sistemas de baja presión.
- *Ducto de polipropileno o PVC:* Para extracción en cocinas industriales o zonas con
  presencia de aceites y grasas; resistente a la corrosión química.

**Soportes y colgantes:** La SMACNA especifica el tipo y espaciado de colgantes según el
tamaño del ducto. Los colgantes de varilla roscada y abrazadera son los más comunes. El
ducto no debe apoyarse sobre tuberías de otros sistemas.

**Sellos y estanqueidad:** Las uniones entre tramos y accesorios se sellan con mastique
(mastic) o cinta de aluminio metalizado de calidad SMACNA (no cinta de tela). Los sistemas
clasificados en clase 2 (hasta 500 Pa) o clase 3 (hasta 1.000 Pa) exigen pruebas de
estanqueidad cuantitativas.

**Aislamiento de ductos de suministro:** Los ductos que recorren espacios no climatizados
(sótanos, áticos, cielos rasos calientes) deben aislarse para evitar ganancia de calor y
condensación. Espesor típico: 25 mm de lana mineral o polietileno espumado, con barrera
de vapor en climas cálidos húmedos.

**Balanceo del sistema:** Tras la instalación, se ajustan los dampers y rejillas para
distribuir el caudal de aire según el diseño. El técnico usa un anemómetro o caudalímetro
para medir el flujo en cada terminal y ajustar hasta alcanzar los caudales de proyecto.
"""  # noqa: E501

