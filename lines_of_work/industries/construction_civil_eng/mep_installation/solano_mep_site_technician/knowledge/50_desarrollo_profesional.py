content = """
El campo de las instalaciones MEP evoluciona continuamente: nuevas tecnologías (VRF de
última generación, BIM, IoT en edificios, refrigerantes de bajo GWP), nuevas normativas
y creciente complejidad de los proyectos. El técnico MEP que invierte en su formación
continua amplía sus oportunidades profesionales y aporta mayor valor a Constructora Solano.

**Certificaciones técnicas de referencia:**
- *Electricidad:* Licencia de electricista oficial/maestro según el reglamento nacional
  (México: NOM-029-STPS; Colombia: RETIE; Perú: CNE). NICET (National Institute for
  Certification in Engineering Technologies) para sistemas contra incendios.
- *HVAC y Refrigeración:* Certificación de técnico en refrigeración (EPA 608 como referencia
  internacional; equivalentes nacionales). ASHRAE Learning Institute para cursos de HVAC,
  eficiencia energética y calidad del aire.
- *BIM:* Autodesk Certified Professional en Revit MEP; certificaciones de Navisworks.
- *Comisionamiento:* NEBB (National Environmental Balancing Bureau) para TAB (Testing,
  Adjusting and Balancing); AABC para comisionamiento de sistemas HVAC.
- *Gestión de proyectos:* PMI Scheduling Professional (PMI-SP) o CAPM para técnicos
  que aspiran a roles de supervisión y coordinación.

**Rutas de desarrollo en Constructora Solano:** El técnico MEP puede progresar hacia:
(1) Técnico senior especializado (plomería, electricidad o HVAC); (2) Supervisor de
instalaciones MEP; (3) Coordinador de proyecto MEP; (4) Ingeniero residente MEP (con
título universitario); (5) Gerente de proyectos.

**Formación en obra:** La experiencia en proyectos de diversa complejidad es irreemplazable.
Solicitar rotación entre especialidades (eléctrica, plomería, HVAC) y entre tipos de
proyecto (residencial, comercial, industrial) es la manera más efectiva de ampliar
el conocimiento práctico.

**Actualización normativa:** Las normas ASHRAE, IEC, NFPA y NEC se actualizan con
frecuencia. Suscribirse a los boletines de actualización y participar en talleres de
actualización normativa es parte de la responsabilidad profesional del técnico.

**Cultura de seguridad como competencia:** La capacitación continua en seguridad (OSHA 30H
para construcción, primeros auxilios, rescate en espacios confinados, trabajo en altura)
no sólo protege al técnico; lo posiciona como líder de seguridad en su cuadrilla.
"""  # noqa: E501

