content = """
El técnico MEP en obra utiliza un conjunto variado de herramientas manuales, eléctricas y
de medición. El conocimiento y manejo correcto de cada herramienta reduce el tiempo de
instalación, mejora la calidad del trabajo y previene accidentes.

**Herramientas de plomería:**
- *Cortatubo:* Para cortar tuberías de cobre, acero o PVC con corte limpio y perpendicular.
- *Escariador y biselador:* Eliminan las rebabas internas del corte antes de soldar o cementar.
- *Curvadora de tubería:* Hidráulica o manual, para doblar cobre o hierro sin ovalizar.
- *Máquina roscadora:* Rosca extremos de tuberías de acero para uniones NPT.
- *Fusionadora de PPR:* Calienta el accesorio y la tubería de PPR para la unión por termofusión.
- *Bomba de prueba hidrostática:* Manual o eléctrica; genera presión para prueba de estanqueidad.

**Herramientas eléctricas:**
- *Taladro percutor e hilti (rotomartillo):* Para perforaciones en concreto, mampostería
  y acero estructural.
- *Curvadora de conduit eléctrico:* Manual (hasta 1") o hidráulica/eléctrica (hasta 4").
- *Pistola jalacables (wire puller):* Facilita el tendido de conductores en conduit largo.
- *Multímetro digital:* Medición de tensión, corriente, resistencia, continuidad y diodo.
- *Pinza amperimétrica:* Medición de corriente sin interrumpir el circuito.
- *Megóhmetro (megger):* Prueba de resistencia de aislamiento en conductores y motores.
- *Telurómetro:* Medición de resistencia del sistema de tierra.
- *Analizador de redes eléctricas:* Monitoreo de parámetros eléctricos en tableros (tensión,
  corriente, factor de potencia, armónicos, energía consumida).

**Herramientas de HVAC:**
- *Manifold de servicio:* Para medir presiones de alta y baja en el circuito frigorífico.
- *Bomba de vacío:* Evacúa humedad y gases no condensables del circuito frigorífico antes
  de la carga de refrigerante.
- *Recuperadora de refrigerante:* Extrae el refrigerante del sistema antes de intervenciones.
- *Anemómetro:* Mide la velocidad del aire en rejillas y difusores para balanceo del sistema.
- *Manómetro diferencial:* Mide la caída de presión en filtros, serpentines y válvulas.

**Mantenimiento de herramientas:** Las herramientas deben revisarse antes de su uso,
mantenerse en buen estado y almacenarse correctamente. Las herramientas defectuosas o
con cables dañados deben retirarse de servicio inmediatamente y reportarse al almacén.
"""  # noqa: E501

