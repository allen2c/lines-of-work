title = "Instalación de Conduit y Bandejas Portacables"

content = """
El conduit y las bandejas son los medios de soporte y protección de conductores eléctricos.
Una instalación bien ejecutada facilita el tendido, protege los cables de daños mecánicos y
ambientales, y simplifica futuras ampliaciones o mantenimientos.

**Planeación de rutas:** Antes de iniciar el trazado, el técnico debe estudiar el plano de
instalación eléctrica y los planos de demás sistemas MEP para identificar posibles conflictos.
Las rutas de conduit deben aprovechar ductos estructurales, espacios entre losas y ejes
de distribución, evitando cruces innecesarios con estructura y otros sistemas.

**Doblado de conduit metálico:** Se usa curvadora de pie o hidráulica. El radio de curvatura
mínimo depende del diámetro del conduit (típicamente 6 veces el diámetro interior). Los
dobleces deben quedar en el mismo plano para mantener el tendido alineado. El ángulo de
90° debe compensarse con el fenómeno de "spring back" del metal.

**Soportes y anclajes:** El NEC y los códigos locales especifican la distancia máxima entre
soportes: típicamente 3 m para conduit de 1" y menores; hasta 3,7 m para diámetros mayores.
Los soportes deben fijarse a estructura (losa, viga, muro) con taquete o anclaje de expansión
adecuado al tipo de sustrato (concreto, mampostería, acero).

**Uniones y accesorios:** Las uniones entre tramos de conduit (coples, niples) deben quedar
ajustadas y sin espacio para mantener la continuidad mecánica y la protección IP del
sistema. En exteriores o ambientes húmedos, se usan accesorios con empaque hermético.

**Tendido de cables:** El jalado de cables se realiza con grasa de jalado (wire pulling
lubricant), especialmente en conduits largos con múltiples curvas. La tensión máxima de
jalado no debe superar 0,5 kN para cables de calibres hasta 2/0 AWG. El porcentaje de
llenado del conduit no debe superar el 40% para uno, 31% para dos y 40% del área interna
para tres o más conductores (NEC, Tabla C).

**Sellos (sealtite):** En conduits que penetran muros cortafuego, áreas presuprizadas o
zonas clasificadas como peligrosas (hazardous locations), se requieren sellos cortafuego
o explosión-proof según el caso.

**Bandejas portacables:** Deben soportarse con distancia máxima de 1,5 m (bandejas de
malla) o hasta 3 m (bandejas de escalera) según especificación del fabricante. Deben
estar puestas a tierra en toda su longitud mediante conector de bonding o conductor de
tierra paralelo.
"""  # noqa: E501

