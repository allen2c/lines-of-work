content = """
Las instalaciones MEP deben cumplir un conjunto de normas y reglamentos que varían según
el país, el tipo de edificación y el uso al que se destina. Constructora Solano aplica
tanto las normas nacionales vigentes en cada mercado como referencias internacionales
reconocidas como estándares técnicos de calidad.

**Normas eléctricas de referencia:**
- *NEC (National Electrical Code, NFPA 70):* Referencia ampliamente adoptada en América
  Latina para el diseño e instalación de sistemas eléctricos.
- *IEC 60364:* Estándar internacional sobre instalaciones eléctricas de baja tensión,
  especialmente usado en proyectos con influencia europea.
- Reglamentos nacionales locales (p.ej., NOM-001-SEDE en México, RETIE en Colombia).

**Normas de plomería y drenaje:**
- Códigos de plomería nacionales que regulan diámetros mínimos, pendientes de drenaje,
  ventilaciones, distancias máximas entre soportes y materiales permitidos.
- Normas de la IAPMO (International Association of Plumbing and Mechanical Officials)
  usadas como referencia en varios países latinoamericanos.

**Normas HVAC:**
- *ASHRAE Standards* (principalmente 15, 34, 55, 62.1, 90.1): gobiernan seguridad en
  sistemas de refrigeración, clasificación de refrigerantes, calidad del aire interior,
  confort térmico y eficiencia energética.
- SMACNA (Sheet Metal and Air Conditioning Contractors National Association): estándares
  de fabricación e instalación de ductos.

**Normas de seguridad y prevención de incendios:**
- NFPA 13 (sistemas de rociadores), NFPA 72 (alarmas de incendio), NFPA 101 (código de
  seguridad en la vida).

**Aplicación práctica:** El técnico MEP no necesita memorizar todos los códigos, pero sí
debe saber dónde encontrar la referencia correcta, cuándo consultarla y cuándo escalar
a ingeniería si la norma no es clara. La ignorancia de la normativa no exime de la
responsabilidad legal ni técnica sobre la instalación ejecutada.
"""  # noqa: E501

