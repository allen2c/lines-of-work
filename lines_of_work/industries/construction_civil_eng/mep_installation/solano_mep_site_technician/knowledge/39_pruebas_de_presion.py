title = "Pruebas de Presión en Sistemas Hidráulicos"

content = """
Las pruebas de presión verifican la integridad de las redes hidráulicas antes de que queden
ocultas detrás de acabados o enterradas. Son obligatorias por la normativa de construcción
y constituyen el último control de calidad antes de la puesta en servicio del sistema.

**Tipos de prueba:**
- *Prueba hidrostática:* Se llena el sistema con agua, se purga el aire y se eleva la
  presión con bomba de prueba. Es el método estándar para tuberías a presión; el agua
  no es comprimible, lo que hace la prueba más segura (un fallo no produce expansión
  explosiva como en prueba neumática).
- *Prueba neumática:* Se usa gas (aire comprimido, nitrógeno) cuando el sistema no puede
  mojarse. Mayor riesgo: si hay falla, la energía almacenada en el gas comprimido se libera
  explosivamente. Se realiza sólo bajo procedimiento estricto, con personal a distancia
  segura y protección física.

**Presión de prueba:** Generalmente 1,5 veces la presión máxima de trabajo (PMAO) del
sistema, o el valor que especifique la norma aplicable (p.ej., ASME B31.9 para sistemas
de construcción). La presión se aplica gradualmente: primero al 50% de la presión de
prueba, inspección visual, luego hasta el 100%.

**Duración de la prueba:** Mínimo 2 horas para sistemas de agua fría y caliente sanitaria.
En redes contra incendio (NFPA 13): 200 psi durante 2 horas. Para sistemas de refrigeración,
la norma ASHRAE 15 especifica las presiones y duración según el tipo de refrigerante.

**Registro de la prueba:** Usar un manómetro de glicerina calibrado en rango adecuado
(la presión de prueba debe estar entre el 25% y el 75% de la escala del manómetro).
Registrar la presión inicial, la temperatura del agua, las lecturas intermedias y la presión
final. La firma del técnico y del supervisor valida el certificado de prueba.

**Criterio de aceptación:** Ausencia de caída de presión mayor a 0,1 bar (o el valor
especificado en el proyecto) durante la duración de la prueba, y ausencia de humedad
visible en cualquier unión, accesorio o equipo.

**Fallas durante la prueba:** Si se detecta una caída de presión, localizar la fuga
inspeccionando visualmente todas las uniones (agua jabonosa ayuda a detectar fugas
pequeñas). Despresurizar antes de cualquier intervención en la red. Reparar, dejar
curar el material de unión y repetir la prueba desde el inicio.
"""  # noqa: E501

