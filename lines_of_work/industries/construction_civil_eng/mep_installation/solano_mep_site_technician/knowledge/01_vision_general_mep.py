title = "Visión General de los Sistemas MEP"

content = """
Las instalaciones MEP (Mecánicas, Eléctricas y de Plomería) constituyen la infraestructura
funcional de cualquier edificio. Sin ellas, una estructura no es habitable ni operable.
Comprender la interdependencia entre los tres sistemas es fundamental para ejecutar obra
con calidad y sin conflictos entre oficios.

**Sistemas Mecánicos (M):** Incluyen climatización (HVAC), ventilación, extracción de aire,
sistemas de frío industrial, salas de máquinas y equipos de manejo de aire. Su diseño
busca garantizar confort térmico, calidad del aire interior y eficiencia energética.

**Sistemas Eléctricos (E):** Comprenden suministro de energía en baja y media tensión,
tableros de distribución, alumbrado, tomacorrientes, sistemas de respaldo (UPS, generadores),
pararrayos y sistemas de comunicación y datos. Deben cumplir la normativa vigente y garantizar
continuidad operativa.

**Sistemas de Plomería (P):** Abarcan agua fría y caliente sanitaria, drenaje pluvial y
sanitario, sistemas de extinción de incendios (rociadores, gabinetes), gas combustible y
sistemas de vacío o aire comprimido donde aplique.

**Integración en obra:** Los sistemas MEP comparten el espacio físico de la edificación con
la estructura y los acabados. Una coordinación deficiente genera cruces, colisiones y
retrabajos costosos. La metodología BIM (Building Information Modeling) permite detectar
conflictos en fase de diseño y organizar la secuencia de instalaciones en obra.

**Secuencia típica de instalación:** En general, la estructura precede a los sistemas MEP;
luego se instalan sistemas principales (tuberías de mayor diámetro, tableros, ductos
principales), seguidos de ramales y circuitos secundarios, y finalmente terminales y
dispositivos. Los acabados ocurren al final, una vez completada la instalación y aprobadas
las pruebas.

El técnico MEP debe conocer esta jerarquía para planificar su trabajo diario, anticipar
interferencias y comunicarse efectivamente con los demás oficios presentes en obra.
"""  # noqa: E501

