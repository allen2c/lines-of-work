title = "Estimación de Materiales y Metrados en MEP"

content = """
La estimación precisa de materiales (metrado) es la base para planificar las compras,
controlar los costos y evitar interrupciones de obra por falta de insumos. El técnico
MEP con experiencia puede realizar metrados de campo y verificar las cantidades estimadas
por el área de ingeniería o costos.

**Metrado de tuberías:** Se mide la longitud total de cada diámetro y material a instalar
en metros lineales. Se suman las longitudes de tramos rectos y se agregan los factores
de desperdicio y corte (típicamente 5–10% según complejidad del trazado). Los accesorios
(codos, tees, reducciones, válvulas) se cuantifican por unidad según el esquema isométrico
o el plano de planta.

**Metrado de ductos:** El ducto rectangular se mide en kg o en m² de chapa desarrollada.
El ducto circular se mide en metros lineales por diámetro. Los accesorios (curvas, tees,
transiciones, compuertas) se cuantifican por unidad. El fabricante de ducto provee el
peso por metro para cada espesor de chapa.

**Metrado eléctrico:** Se cuantifica el conduit en metros lineales por tipo y diámetro,
los conductores en metros lineales por calibre y tipo, las cajas de registro por unidad,
los tableros y sus componentes internos (interruptores, barras), y las luminarias y
tomacorrientes por unidad.

**Metrado de equipos:** Los equipos se cuantifican por unidad: bombas, fan coils, chillers,
unidades de HVAC. Para cada equipo se verifica que los materiales de conexión (válvulas,
juntas flexibles, manómetros, termómetros) estén también incluidos en el metrado.

**Software de metrado:** Herramientas como Bluebeam Revu, AutoCAD MEP o módulos de Revit
permiten extraer cantidades directamente del modelo o los planos PDF. Para obras donde
no hay modelo BIM, el metrado se realiza manual o con hojas de cálculo prediseñadas.

**Control de material en obra:** El técnico MEP registra el material que ingresa a obra,
verifica que coincide con la orden de compra (cantidad, diámetro, material, certificado
de calidad del fabricante para materiales críticos) y controla el consumo por frente
mediante el sistema de vales de almacén. El exceso o déficit de material respecto al
metrado se analiza para identificar errores de estimación o desperdicios evitables.
"""  # noqa: E501

