content = """
La gestión del cronograma en instalaciones MEP requiere comprender las dependencias entre
actividades propias y las de otros oficios, anticipar cuellos de botella en materiales y
personal, y comunicar el avance con precisión al director de obra.

**Programa maestro y programa detallado:** El programa maestro del proyecto (Gantt general)
muestra los hitos y fases principales. El técnico MEP trabaja con el programa detallado
de su especialidad, que desglosa actividades por piso, zona y sistema. Este programa se
actualiza semanalmente con el avance real.

**Método de programación:** Los proyectos de Constructora Solano usan la Ruta Crítica
(CPM) para proyectos de mayor complejidad, y el Lookahead de 3 semanas para la
planificación operativa diaria. El Lookahead identifica las actividades de las próximas
3 semanas, sus prerequisitos y los recursos necesarios para ejecutarlas sin interrupciones.

**Restricciones de planificación:** Una restricción es cualquier condición que impide
iniciar una actividad planificada. Las restricciones más comunes en MEP son:
- Planos no aprobados o RFIs pendientes de respuesta.
- Materiales no llegados a obra o en cuarentena de calidad.
- Frente de trabajo no liberado por otro oficio (estructura sin colar, acabados sin secar).
- Personal no disponible (ausentismo, capacitación pendiente).
- Permiso de trabajo no emitido.

**Indicadores de avance:** El avance se expresa como porcentaje del trabajo completado
respecto al alcance total, medido en metros lineales de tubería instalada, metros cuadrados
de ducto montado o cantidad de puntos eléctricos terminados. El Índice de Desempeño de
Cronograma (SPI = EV/PV) indica si el proyecto está adelantado, a tiempo o atrasado.

**Recuperación de atrasos:** Si el avance está por debajo del plan, el técnico MEP propone
al director de proyecto medidas de recuperación: horas extra, incorporación de cuadrillas
adicionales, replanificación de secuencias. Cada medida tiene un costo que debe
evaluarse antes de implementarla.

**Interfaz con proveedores de larga entrega:** Equipos como chillers, transformadores,
generadores y tableros de media tensión tienen tiempos de fabricación de 8 a 16 semanas.
El técnico MEP coordina con el área de compras para que los pedidos se colocan con la
anticipación suficiente y alerta tempranamente si hay riesgo de retraso en la entrega.
"""  # noqa: E501

