title = "Coordinación entre Oficios en Obra"

content = """
En un proyecto de construcción, los sistemas MEP comparten el espacio físico del edificio
con la estructura, los acabados y otros oficios especializados. La falta de coordinación
es una de las principales causas de retrabajos, atrasos y sobrecostos. El técnico MEP
tiene un papel activo en facilitar esta coordinación.

**Reuniones de coordinación:** Se realizan semanalmente (o con mayor frecuencia en etapas
críticas) entre los responsables de cada especialidad y el director de obra. El técnico
MEP presenta el avance de su frente, los conflictos detectados y las solicitudes de acceso
a zonas compartidas.

**Jerarquía de instalaciones:** Cuando el espacio es insuficiente para todos los sistemas,
se aplica una jerarquía de prioridad típica: (1) estructura, (2) drenajes por gravedad
(pendiente fija), (3) tuberías de mayor diámetro, (4) ductos de mayor sección, (5) bandejas
eléctricas, (6) conduit y tuberías menores. Esta jerarquía puede variar según el proyecto.

**Planos de coordinación MEP (Coordination Drawings):** En proyectos complejos, los equipos
de cada especialidad generan planos de coordinación en BIM (Revit, Navisworks) donde se
superponen todos los sistemas para detectar colisiones antes de instalar. El técnico debe
trabajar conforme a los planos de coordinación aprobados, no sólo los planos de diseño.

**Pases y mangas:** Las tuberías y ductos que cruzan losas, vigas y muros requieren
mangas o pases estructurales reservados durante la etapa de estructura. El técnico MEP
debe verificar la ubicación y diámetro de las mangas antes de iniciar la instalación.
Un pase mal ubicado implica perforación posterior, que requiere aprobación estructural.

**Protocolo de interferencias:** Cuando se detecta una colisión no resuelta en planos,
el técnico la reporta al supervisor MEP, quien genera un RFI o una solicitud de cambio
de ruta. No se modifica la ruta de instalación sin autorización por escrito del ingeniero
de diseño, ya que puede afectar el balance hidráulico o el dimensionamiento eléctrico.

**Terminaciones compartidas:** Los acabados de cielorrasos, pisos y muros dependen de
que los sistemas MEP estén completamente instalados, probados y aprobados. El técnico
MEP debe comunicar con anticipación cuándo su frente estará liberado para que el equipo
de acabados pueda planificar su ingreso.
"""  # noqa: E501

