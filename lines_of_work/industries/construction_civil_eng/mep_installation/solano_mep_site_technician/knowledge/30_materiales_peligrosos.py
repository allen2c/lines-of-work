title = "Manejo de Materiales Peligrosos en Obra MEP"

content = """
Las instalaciones MEP implican el uso de sustancias que pueden representar riesgos para
la salud y el ambiente si no se manejan correctamente: refrigerantes, solventes, adhesivos,
fluxes de soldadura, aceites, limpiadores ácidos y pinturas. El manejo adecuado protege
al personal, cumple la normativa ambiental y previene responsabilidades legales.

**Hoja de Datos de Seguridad (HDS/SDS):** Toda sustancia química usada en obra debe contar
con su HDS (antes llamada MSDS). La HDS describe la composición, los riesgos físicos y de
salud, los procedimientos de manejo seguro, el EPP requerido y las medidas ante derrames
o exposición accidental. Las HDS deben estar disponibles en el área de uso y en el almacén.

**Almacenamiento:** Los productos químicos deben almacenarse en un cuarto ventilado,
separados por compatibilidad (oxidantes lejos de combustibles, ácidos lejos de bases).
Los cilindros de gas comprimido (acetileno, oxígeno, argón) deben sujetarse con cadena
y mantenerse separados y tapados cuando no están en uso.

**Refrigerantes:** No liberar refrigerantes a la atmósfera. Usar recuperadora certificada.
Los cilindros de refrigerante son recipientes a presión; no exponer al calor ni golpear.

**Solventes y adhesivos (cemento de PVC, flux de cobre):** Inflamables y con vapores
tóxicos. Usar en zonas ventiladas, lejos de fuentes de ignición. Cubrir envases cuando
no se usan para reducir la evaporación.

**Aceites y lubricantes:** No verter en drenajes. Recolectar en contenedores y disponer
con gestor autorizado de residuos peligrosos.

**Limpiadores ácidos (para tuberías de cobre o equipos):** Cáusticos; pueden quemar
piel y ojos. Usar gafas herméticas, guantes resistentes a ácidos y delantal. Neutralizar
antes de disponer.

**Gestión de residuos peligrosos:** Constructora Solano contrata gestores autorizados
para la disposición final de residuos peligrosos generados en obra. El técnico MEP debe
segregar correctamente los residuos (envases de refrigerante, trapos con solvente,
baterías, lámparas fluorescentes) y depositarlos en los contenedores identificados.

**Derrames:** Ante un derrame, contener con material absorbente (arena, vermiculita, aserrín),
recoger en bolsa hermética y disponer como residuo peligroso. Nunca lavar hacia el drenaje.
"""  # noqa: E501

