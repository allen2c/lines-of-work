title = "Métodos de Cableado Eléctrico en Edificaciones"

content = """
El método de cableado define cómo se instalan y protegen los conductores eléctricos en
el interior de una edificación. La elección depende del tipo de local, las condiciones
ambientales, las exigencias del código aplicable y los requerimientos de mantenimiento.

**Conduit metálico rígido (RMC/IMC):** Ofrece la protección mecánica más alta. Se usa en
ambientes industriales, áreas expuestas, sótanos húmedos y donde el conduit también
actúa como conductor de tierra. La instalación requiere doblado con curvadora hidráulica
o eléctrica para mantener el radio mínimo de curva sin ovalizar la sección.

**Conduit metálico flexible (FMC/LFMC):** Se usa en conexiones a equipos que vibran
(motores, compresores, unidades manejadoras de aire) y en tramos cortos donde el conduit
rígido no puede llegar. El LFMC tiene recubrimiento de PVC resistente a la humedad.

**Conduit de PVC (ENT/Rígido):** Económico y de fácil instalación en losas y paredes
embutidas. Resistente a la corrosión, pero no provee protección mecánica intrínseca; en
zonas expuestas se requiere complemento con canal o protección adicional. No apto para
temperaturas superiores a 75 °C.

**Bandeja portacables (cable tray):** Sistema abierto de soporte para múltiples circuitos
en áreas técnicas, plantas de proceso y espacios de servicio. Permite fácil inspección y
modificación. Tipos: escalera, malla, canal. Los conductores en bandeja deben seleccionarse
con la reducción de capacidad correspondiente por agrupamiento.

**Cable multipolar (NM-B, tipo SHF, TTU):** En algunos tipos de construcción se permiten
cables sin conduit, empotrados en losas o grapados a superficies. El tipo de cable debe
ser compatible con la temperatura y la categoría de instalación definida en el código.

**Calibre de conductores:** Determinado por la corriente máxima de diseño, la caída de
tensión admisible y la temperatura de operación. En sistemas trifásicos, el conductor
neutro puede reducirse si las cargas son balanceadas y no hay armónicos significativos;
siempre se consulta el código antes de aplicar esta reducción.

**Identificación de conductores:** Los colores normalizados deben respetarse: fase L1
(negro o café), L2 (rojo), L3 (azul en muchos países), neutro (blanco o gris), tierra
(verde o verde/amarillo). La consistencia cromática previene errores en intervenciones
de mantenimiento.
"""  # noqa: E501

