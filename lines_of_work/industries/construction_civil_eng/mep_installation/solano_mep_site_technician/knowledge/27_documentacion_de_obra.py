title = "Documentación de Obra en Proyectos MEP"

content = """
La documentación técnica de obra es el registro formal del trabajo ejecutado. Tiene valor
legal, facilita el mantenimiento futuro y es requisito indispensable para la entrega al
cliente. Un proyecto bien documentado protege a la empresa en caso de disputas y simplifica
intervenciones de terceros durante la operación del edificio.

**Planos as-built (conforme a obra):** Son los planos de diseño modificados para reflejar
exactamente cómo quedó instalado cada sistema: ubicaciones exactas de tuberías, cotas
reales de instalación, diámetros y materiales usados, posición de válvulas y registros,
y cualquier cambio aprobado respecto al diseño original. Se actualizan continuamente
durante la obra y se entregan al cliente en formato digital al finalizar.

**Bitácora de obra:** Registro diario de actividades, mano de obra empleada, avance
por frentes, condiciones climáticas que afectaron el trabajo, visitas de inspección y
decisiones tomadas. Es un documento legal que puede usarse en arbitrajes o reclamaciones.

**Registros de pruebas y ensayos:** Certificados de prueba hidrostática, resistencia de
aislamiento, balanceo de aire (TAB), mediciones de tierra, calibraciones de instrumentos
y ensayos de materiales. Se archivan por sistema y por lote de trabajo.

**No Conformidades (RNC):** Documento que registra cada desviación detectada, su resolución
y el cierre verificado. El índice de no conformidades y su tasa de cierre es un indicador
de calidad del proyecto.

**Solicitudes de información (RFI):** Preguntas formales al equipo de diseño cuando la
información de planos es insuficiente o contradictoria. Cada RFI tiene número, fecha de
emisión, fecha de respuesta comprometida y respuesta formal archivada. Las RFIs modifican
el alcance sólo cuando se formalizan como cambios de diseño aprobados.

**Órdenes de cambio (Change Orders):** Documento que autoriza una modificación al alcance,
cronograma o costo del contrato. Ningún cambio de alcance se ejecuta sin orden de cambio
firmada por el cliente y el director de proyecto de Constructora Solano.

**Manuales de operación y mantenimiento (O&M):** Se elaboran para cada sistema MEP;
incluyen fichas técnicas de equipos, procedimientos de arranque y paro, programa de
mantenimiento preventivo, lista de repuestos críticos y datos de contacto del fabricante.
"""  # noqa: E501

