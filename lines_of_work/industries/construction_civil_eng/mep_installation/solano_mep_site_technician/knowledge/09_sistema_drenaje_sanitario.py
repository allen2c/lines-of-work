title = "Sistema de Drenaje Sanitario y Ventilación"

content = """
El sistema de drenaje sanitario conduce las aguas residuales desde los aparatos sanitarios
hasta la red municipal de alcantarillado o al sistema de tratamiento del predio. Un diseño
deficiente provoca obstrucciones, malos olores, reflujos y riesgos para la salud pública.

**Componentes principales:**
- *Ramales de aparato:* Tuberías de pequeño diámetro que conectan cada aparato (lavabo,
  inodoro, fregadero) con el bajante o colector horizontal.
- *Bajantes (columnas de aguas negras):* Tuberías verticales de mayor diámetro que reciben
  los ramales de cada nivel y descargan al colector principal.
- *Colectores horizontales:* Tuberías a nivel de sótano o enterradas que conducen el flujo
  hasta el punto de entrega en la red exterior.
- *Cajas de inspección/registros:* Puntos de acceso para mantenimiento y desobstrucción,
  ubicadas en cambios de dirección, confluencias y a intervalos de distancia máxima
  según normativa (típicamente cada 15–20 m en horizontal).
- *Sello hidráulico (sifón):* Dispositivo en forma de U que retiene agua para bloquear
  el paso de gases malolientes y patógenos desde la red de drenaje al interior del edificio.

**Ventilación de la red:** Todo sistema de drenaje sanitario requiere red de ventilación
para equilibrar presiones. Sin ventilación adecuada, los sellos hidráulicos se vacían por
sifonamiento y los gases fétidos ingresan al ambiente. La ventilación puede ser primaria
(prolongación del bajante a la azotea), secundaria (ramales de ventilación conectados a
cada sifón) o mediante válvulas de admisión de aire (AAV) donde no es posible llevar
tubería al exterior.

**Pendientes mínimas:** Los drenajes horizontales requieren pendiente mínima de 1 a 2%
(1–2 cm por metro). Pendientes menores provocan sedimentación; pendientes excesivas
pueden dejar la tubería sin líquido de arrastre.

**Diámetros mínimos:** El inodoro requiere ramal de mínimo 100 mm (4"); otros aparatos
dependen del número de unidades de descarga (fixture units) que contribuyen al ramal.

**Trampa de grasas:** Cocinas industriales y restaurantes deben instalar trampa de grasas
antes de descargar al drenaje sanitario, para retener aceites que solidifican en la red.
"""  # noqa: E501

