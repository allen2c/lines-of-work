content = """
La comunicación efectiva con el cliente y las demás partes interesadas del proyecto es
una competencia profesional que el técnico MEP desarrolla con la experiencia. Una
comunicación clara, oportuna y documentada previene malentendidos, gestiona expectativas
y construye la confianza que Constructora Solano necesita para crecer con clientes recurrentes.

**Representación de Constructora Solano:** En todo momento, el técnico MEP representa
a la empresa ante el cliente y las autoridades. Su conducta, vocabulario y presentación
personal reflejan la imagen de la empresa. Se espera puntualidad, presentación adecuada,
lenguaje técnico preciso y respeto en todas las interacciones.

**Comunicación de avance:** Las actualizaciones de avance al cliente se realizan en el
formato acordado en el contrato (informe semanal escrito, reunión de seguimiento mensual,
dashboard en línea). El técnico MEP provee al director de proyecto la información de campo
necesaria para elaborar estos reportes con precisión.

**Gestión de solicitudes del cliente en campo:** Si el representante del cliente en obra
solicita un cambio o hace una observación técnica, el técnico MEP escucha y registra la
solicitud, pero no la implementa sin autorización del director de proyecto. Los cambios de
alcance sin orden de cambio formal generan trabajos sin remuneración para la empresa.

**Comunicación ante problemas:** Cuando se detecta un problema que afectará el cronograma,
el costo o la calidad, el técnico MEP lo comunica de inmediato al director de proyecto,
con los datos del problema y las opciones de solución que tiene a su alcance. No se ocultan
ni minimizan los problemas; la transparencia temprana permite tomar decisiones antes de
que el impacto sea mayor.

**Reuniones de obra:** El técnico MEP asiste a las reuniones de coordinación de obra con
preparación: lleva el avance actualizado de su frente, la lista de restricciones activas
y las solicitudes que necesita de otros oficios o del cliente. Participa con precisión y
brevedad, sin desviar la discusión hacia temas que pueden resolverse bilateralmente.

**Entrega al cliente:** En la entrega final del proyecto, el técnico MEP presenta los
sistemas instalados al personal de mantenimiento del cliente, explica su operación básica,
señala las válvulas, interruptores y puntos de acceso principales, y deja el dossier
técnico organizado y completo. Una buena entrega es la última oportunidad de demostrar
profesionalismo y dejar una impresión positiva que genere nuevos proyectos.
"""  # noqa: E501

