title = "Fallas Comunes en Instalaciones MEP y sus Causas"

content = """
Conocer las fallas más frecuentes en instalaciones MEP permite al técnico aplicar controles
preventivos durante la ejecución y diagnosticar problemas con mayor rapidez durante las
pruebas o la operación. La mayoría de las fallas en campo tienen causas que pudieron
evitarse con una correcta instalación desde el inicio.

**Fallas en fontanería:**
- *Fugas en uniones soldadas de cobre:* Causadas por limpieza insuficiente, temperatura
  incorrecta de soldadura o flujo contaminado. La fuga suele aparecer durante la prueba
  hidrostática.
- *Obstrucción en drenajes:* Pendiente insuficiente, residuos de obra (cemento, cascajo)
  que se solidifican en la tubería. Prevención: taponar temporalmente los ramales durante
  la obra y limpiar antes de las pruebas.
- *Golpe de ariete:* Ruido y vibración producidos por cierre brusco de válvulas. Solución:
  instalar cámaras anti-golpe de ariete en puntos críticos.
- *Corrosión galvánica:* Contacto directo entre cobre y acero negro sin aislamiento, en
  presencia de agua. Prevención: usar niples dieléctricos en las uniones bimetálicas.

**Fallas en electricidad:**
- *Sobrecalentamiento en conexiones:* Conexiones flojas o subdimensionadas producen
  resistencia adicional y calor. Detectables en termografía infrarroja.
- *Caída de tensión excesiva:* Conductores subdimensionados para longitudes largas.
  Verificar con cálculo antes de instalar.
- *Fuga a tierra en cables dañados:* Rotura del aislamiento por arrastre sin protección,
  aplastamiento con herramientas pesadas o exposición a temperaturas extremas.
- *Inversión de fases:* Error de conexión en motores trifásicos que provoca giro en
  sentido contrario. Se detecta en la primera prueba de sentido de giro.

**Fallas en HVAC:**
- *Corto de refrigerante:* Fuga en unión de cobre del circuito frigorífico; pérdida
  progresiva de capacidad de enfriamiento.
- *Condensado sin drenaje:* Bandeja de fan coil o UMA sin pendiente correcta; acumulación
  de agua que desborda al cielorraso.
- *Ducto sin sellar:* Fugas de aire por uniones mal selladas; reducción del caudal
  entregado a las zonas y aumento del consumo energético.
- *Filtros colmatados:* No se toman en cuenta para el comisionamiento; el sistema parte
  con filtros nuevos pero deben ser monitoreados desde la primera operación.

**Prevención sistemática:** El Plan de Puntos de Inspección y las revisiones de calidad
durante la instalación son la herramienta más efectiva para detectar y corregir estas
fallas antes de que se conviertan en problemas operativos.
"""  # noqa: E501

