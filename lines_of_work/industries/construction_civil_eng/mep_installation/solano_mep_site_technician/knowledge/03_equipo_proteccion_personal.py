content = """
El Equipo de Protección Personal (EPP) es la última barrera entre el trabajador y el
riesgo residual que no ha podido eliminarse mediante controles de ingeniería u organización.
Su uso correcto y consistente es obligatorio en todas las áreas de trabajo de Constructora
Solano.

**EPP básico requerido en toda la obra:**
- Casco de seguridad clase E (resistente a impactos y conductividad eléctrica).
- Calzado de seguridad con punta de acero y suela antideslizante.
- Chaleco reflectante de alta visibilidad.
- Gafas de seguridad o protección ocular adecuada a la tarea.
- Guantes apropiados según la actividad (corte, soldadura, manejo de ácidos, etc.).

**EPP específico por actividad MEP:**
- *Electricidad:* Guantes dieléctricos certificados, calzado dieléctrico, protector facial
  con arco eléctrico (Arc Flash) y ropa ignífuga donde aplique.
- *Soldadura y oxicorte:* Careta de soldador, guantes de cuero de manga larga, delantal
  de cuero, polainas y protección respiratoria según material.
- *HVAC y refrigerantes:* Gafas herméticas y guantes criogénicos al manipular refrigerantes;
  mascarilla con filtro orgánico al usar solventes o selladores.
- *Trabajos en altura:* Arnés de seguridad tipo paracaídas certificado, cuerda de vida
  y absorvedor de impacto; conectado a una línea de anclaje aprobada.

**Mantenimiento del EPP:** El EPP dañado, vencido o de dudosa integridad debe retirarse
de servicio de inmediato y reportarse al almacén para su reemplazo. Nunca se deben
realizar reparaciones improvisadas en arneses, cascos o guantes dieléctricos.

**Responsabilidad del técnico:** Cada técnico es responsable de inspeccionar su EPP
antes de cada jornada, usarlo correctamente durante toda la actividad de riesgo y
almacenarlo en condiciones adecuadas al final del turno.
"""  # noqa: E501

