title = "Sellados Cortafuego en Pases MEP"

content = """
Cuando tuberías, ductos o bandejas eléctricas atraviesan muros o losas de compartimentación
cortafuego, se crea una abertura que compromete la integridad del compartimento. Si no
se sella correctamente, un incendio puede propagarse de un compartimento a otro a través
de estos pases, neutralizando la función de la compartimentación y poniendo en riesgo vidas.

**Principio de compartimentación:** Los edificios se dividen en compartimentos o sectores
de incendio mediante muros y losas con una resistencia al fuego determinada (R30, R60,
R120, R240 en minutos). Los pases MEP deben mantener esta resistencia sin excepción.

**Tipos de productos de sellado cortafuego:**
- *Morteros intumescentes:* Se aplican alrededor de la tubería o cable en el pase.
  Al exponerse al calor, se expanden y sellan el espacio incluso si la tubería plástica
  se funde y desaparece.
- *Collares cortafuego (fire collars):* Se instalan alrededor de tuberías plásticas (PVC,
  CPVC, PPR). Contienen material intumescente que, al activarse, sella el hueco dejado
  por la tubería fundida.
- *Bloques y almohadas cortafuego:* Rellenos de material intumescente usados para sellar
  bandejas con múltiples cables. Fácil instalación y modificación posterior.
- *Masilla cortafuego (fire sealant):* Para sellar huecos irregulares o de pequeño tamaño
  alrededor de cables y conduits.
- *Placas de yeso o tableros intumescentes:* Para cubrir y proteger aberturas de mayor
  dimensión (ductos de HVAC de gran sección).

**Dampers cortafuego en ductos HVAC:** Los ductos que atraviesan muros cortafuego deben
incorporar compuertas cortafuego (fire dampers) que se cierran automáticamente al detectar
temperatura elevada (fusible de 72 °C standard). Los smoke dampers se cierran al detectar
humo (señal del sistema de detección).

**Requisitos de instalación:** El sellado cortafuego debe aplicarse según las instrucciones
del fabricante del producto certificado (Underwriters Laboratories, ETAG o equivalente
local). La combinación de producto, substrato y diámetro/tipo de tubería debe estar
cubierta por una evaluación de ensayo. No se improvisan mezclas ni se sustituyen productos
sin evaluación equivalente.

**Documentación y acceso:** La ubicación de todos los pases sellados debe registrarse en
los planos as-built. Los puntos de inspección de dampers cortafuego deben quedar accesibles
mediante registros en el cielorraso o en el muro. Las pruebas de dampers se realizan
anualmente como parte del mantenimiento preventivo del sistema de protección contra incendios.
"""  # noqa: E501

