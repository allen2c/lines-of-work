title = "Electrical Scope Pricing"

content = """
Electrical estimating in renovation covers new circuits, outlets, switches, lighting,
and panel work. Licensed electricians are required; labor rates reflect skilled trade.

**Circuit and Outlet Count:** Price by the point (outlet, switch) or by circuit.
Include GFCI and AFCI where code requires. Dedicated circuits for appliances are
separate line items.

**Lighting:** Recessed cans, pendants, and fixtures vary by type and quantity. Include
rough-in, wiring, trim, and bulb. Smart switches and dimmers add cost.

**Panel and Service:** Panel upgrades, subpanels, and service changes are significant
costs. Coordinate with utility and permit requirements. Often requires separate quote.

**Low-Voltage:** Data, AV, and low-voltage lighting may be a separate trade. Include
or exclude clearly; scope creep is common when not defined.
"""  # noqa: E501

