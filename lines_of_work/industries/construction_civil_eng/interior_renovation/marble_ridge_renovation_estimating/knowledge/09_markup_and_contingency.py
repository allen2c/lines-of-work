title = "Markup and Contingency"

content = """
Markup covers overhead and profit; contingency covers unknowns. Both must be applied
consistently and communicated clearly to the client.

**Overhead:** Overhead includes office costs, insurance, bonding, equipment, and
administrative support. It is typically applied as a percentage of direct cost (materials
and labor) or embedded in labor rates.

**Profit:** Profit margin varies by project size, risk, and competitive pressure.
Document the margin applied and ensure it aligns with company policy and market conditions.

**Contingency:** Contingency is a reserve for unforeseen conditions, design
clarifications, and latent defects. Typical ranges: 5–10% for well-defined scope,
10–15% for moderate uncertainty, 15–25% for preliminary budgets or high uncertainty.

**Presentation:** Separate contingency from base scope in the estimate. Explain that it
is a reserve, not a guarantee of expenditure; unused contingency may be released.
"""  # noqa: E501

