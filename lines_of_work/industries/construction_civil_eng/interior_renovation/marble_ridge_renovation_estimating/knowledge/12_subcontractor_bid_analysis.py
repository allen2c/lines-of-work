title = "Subcontractor Bid Analysis"

content = """
When work is subcontracted, the estimator must solicit, compare, and incorporate
subcontractor bids. Incomplete or inconsistent bids lead to scope gaps and disputes.

**Bid Solicitation:** Provide clear scope documents and plans. Specify inclusions,
exclusions, and schedule. Allow adequate time for subs to quote accurately.

**Apples-to-Apples Comparison:** Ensure all bids cover the same scope. Missing items
in one bid may make it appear low. Create a bid comparison matrix by trade and line item.

**Qualification:** Low price is not the only criterion. Verify licensing, insurance,
and references. Past performance and reliability matter for schedule and quality.

**Incorporation:** When a sub bid is selected, incorporate it into the estimate with
clear documentation. Add appropriate markup for general conditions and risk. Note
payment terms and retention.
"""  # noqa: E501

