content = """
Quantity takeoff is the foundation of accurate estimating. Every cost estimate begins
with measuring and counting the materials and labor required to complete the scope.

**Plan Reading:** Extract linear, square, and count quantities from plans, elevations, and
sections. Use consistent units (square feet for flooring, linear feet for trim, each for
fixtures). Verify scale and confirm that plans reflect the current design intent.

**Measurement Rules:** Follow industry standards for measuring: net quantities for some
materials (e.g., paint coverage), gross with waste for others (e.g., flooring, tile).
Document which rule applies to each takeoff line.

**Assemblies vs. Components:** Decide whether to estimate by assembly (e.g., "kitchen
cabinetry" as a lump) or by component (boxes, doors, hardware). Component-level takeoffs
support change orders and value engineering; assembly-level is faster for preliminaries.

**Site Verification:** When plans are incomplete or conditions are uncertain, note
assumptions and recommend site verification before contract. Flag areas where as-built
conditions may differ materially from plans.
"""  # noqa: E501

