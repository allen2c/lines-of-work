title = "Regional Cost Adjustments"

content = """
Construction costs vary by geography. National or reference-location data must be
adjusted for the project's market.

**Location Factors:** Published indices (e.g., RSMeans city indices) provide multipliers
relative to a base city. Apply the factor to relevant cost categories.

**Labor Rate Variation:** Wages differ by state, metro area, and union density. Use
local wage data or loaded rates from recent projects. Do not assume national averages.

**Material Availability:** Remote or rural locations may have higher delivery costs or
limited supplier options. Coastal or urban areas may have premium hauling and parking.

**Documentation:** Record the source of location factors and the base location. Clients
in unfamiliar markets may question adjustments; transparent methodology builds trust.
"""  # noqa: E501

