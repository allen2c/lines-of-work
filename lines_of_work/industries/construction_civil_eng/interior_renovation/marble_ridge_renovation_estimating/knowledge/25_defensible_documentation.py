title = "Defensible Documentation"

content = """
Estimates may be challenged by clients, auditors, or in disputes. Documentation that
supports each line and assumption protects the estimator and the firm.

**Source Trail:** For each significant cost, record the source: quote date, supplier,
project reference. Retain backup for major bids and subcontractor quotes.

**Assumption Log:** Maintain a log of assumptions (scope, access, exclusions). When
conditions change, the log supports revisions and change orders.

**Revision History:** When estimates are revised, note the date, reason, and changes.
Preserve prior versions when contractually or legally relevant.

**Quality Review:** Implement a review process: self-check, peer review, or supervisory
sign-off. Catch errors before they become contract terms.
"""  # noqa: E501

