content = """
Waste factors account for cutting loss, breakage, and pattern matching. Underestimating
waste leads to shortfalls and emergency purchases at premium prices.

**Flooring Waste:** Hardwood and laminate typically require 5–10% waste for layout and
cuts. Tile and stone may require 10–15% for pattern, diagonals, or large-format breaks.
Complex patterns or herringbone layouts can exceed 15%.

**Trim and Millwork:** Linear materials like baseboard, crown, and casing need 5–8% for
miter cuts and length optimization. Prefinished trim may require more to avoid visible
splices.

**Drywall and Paint:** Drywall waste ranges from 5–10% depending on layout. Paint coverage
varies by substrate, sheen, and number of coats; use manufacturer specs and add 5–10%
for touch-ups and cut-in loss.

**Documentation:** Record the waste factor applied to each line. Clients and auditors
may question large allowances; defensible percentages from historical data support the estimate.
"""  # noqa: E501

