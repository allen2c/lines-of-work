title = "Change Order Pricing"

content = """
Change orders arise when scope changes after contract award. Estimates must be timely,
defensible, and consistent with the original bid methodology.

**Time and Materials vs. Lump Sum:** T&M change orders bill actual hours and materials
at agreed rates. Lump sum change orders require a full estimate; document assumptions
and exclusions.

**Documentation:** Record the change in scope, the basis of the estimate (takeoff,
quotes, rates), and any conditions (e.g., "assumes access during normal hours"). Obtain
written approval before proceeding.

**Overhead and Profit:** Change orders typically include markup for overhead and profit.
Apply the same rate as the original contract unless otherwise agreed.

**Pricing Consistency:** Use the same unit costs and labor rates as the base contract
when possible. New or different materials require fresh research and client approval.
"""  # noqa: E501

