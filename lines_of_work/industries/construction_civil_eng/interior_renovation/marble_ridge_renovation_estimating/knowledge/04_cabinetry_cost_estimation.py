title = "Cabinetry Cost Estimation"

content = """
Cabinetry estimating combines box count, door style, hardware, and installation labor.
Custom vs. semi-custom vs. stock affects both material cost and lead time.

**Box Count and Configuration:** Count base and wall cabinets by size. Note fillers,
adjustable shelves, and specialty units (lazy susans, pull-outs). Include toe kicks
and end panels.

**Door and Drawer Fronts:** Door style (raised panel, flat, glass) and material (wood,
MDF, thermofoil) drive cost. Soft-close hinges and drawer slides add per-unit cost.

**Countertops:** Countertops are often a separate line but must be coordinated with
cabinetry for template and install timing. Include backsplash and edge treatment.

**Installation Labor:** Cabinet install is typically priced per linear foot or as a
lump sum. Include leveling, scribing, and hardware mounting. Appliance cutouts and
trim may be additional.
"""  # noqa: E501

