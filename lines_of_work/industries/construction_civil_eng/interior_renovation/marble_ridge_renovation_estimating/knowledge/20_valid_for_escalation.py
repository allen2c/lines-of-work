title = "Valid-For Periods and Escalation"

content = """
Material and labor costs change over time. Estimates must state how long quoted prices
remain valid and how escalation is handled beyond that period.

**Valid-For Period:** Typical valid-for periods are 30, 60, or 90 days. Beyond that,
prices may be subject to revision. State the period clearly on the proposal.

**Escalation Clauses:** For long-lead or phased projects, contract language may allow
price adjustment for materials or labor beyond a threshold. Document the formula and
trigger.

**Material Lead Times:** Some materials have long lead times; price at order may differ
from price at estimate. Consider locking in critical items or including escalation
reserves.

**Communication:** Inform clients when the valid-for period is approaching. A signed
contract or purchase order before expiry typically locks pricing.
"""  # noqa: E501

