title = "Preliminary Budget Methods"

content = """
Early-stage estimates often lack full scope and details. Order-of-magnitude and
parametric methods provide ballpark figures for feasibility and planning.

**Cost per Square Foot:** Historical $/sf for similar project types (e.g., kitchen
remodel, full home renovation) gives a quick range. Specify what is included (e.g.,
"soft costs excluded").

**Parametric Models:** Use key drivers (e.g., number of bathrooms, linear feet of
cabinetry) with unit rates to build a top-down estimate. Refine as scope is defined.

**Ranges and Confidence:** Present preliminary estimates as ranges (e.g., $X–$Y) with
notes on assumptions. Avoid single-point numbers that imply false precision.

**Progression:** As design develops, move from parametric to assembly to component-level
estimates. Document the estimating class (e.g., Class 5 to Class 2) and expected accuracy.
"""  # noqa: E501

