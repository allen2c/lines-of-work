content = """
Permit costs and fees are often overlooked in preliminary estimates. Interior renovation
may require building, electrical, plumbing, and sometimes mechanical permits depending
on scope and jurisdiction.

**Building Permit:** Typically based on valuation or square footage. Major renovations
trigger full permit review; cosmetic work may be exempt in some localities.

**Trade Permits:** Electrical, plumbing, and HVAC work often require separate permits.
Include permit fees and inspection costs. Some jurisdictions bundle; others charge per trade.

**Impact and Hookup Fees:** Utility connections, impact fees, or development charges may
apply for substantial work. Research local requirements for the project address.

**Exclusions:** Clearly state which permits are included and which are owner-responsible
(e.g., HOAs, historical review). Document assumptions when scope is uncertain.
"""  # noqa: E501

