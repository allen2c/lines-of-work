title = "Complexity Factors in Estimating"

content = """
Complexity affects labor productivity and sometimes material usage. Failure to account
for complexity leads to underestimated durations and costs.

**Design Complexity:** Intricate patterns, custom details, and nonstandard dimensions
increase labor. Apply complexity multipliers when work deviates from typical conditions.

**Coordination and Phasing:** Renovations in occupied homes, coordination with multiple
trades, and phasing for owner occupancy add time and cost. Include in general conditions.

**Existing Conditions:** Unknown or poor existing conditions (rotted framing, inadequate
structure) require contingency. Historic or specialty construction may need specialized
labor and materials.

**Documentation:** When applying complexity factors, note the rationale. "1.3x labor
for confined crawl space access" is defensible; arbitrary markups are not.
"""  # noqa: E501

