title = "Fixture and Finish Allowances"

content = """
Allowances are dollar amounts set aside for client-selected items (fixtures, finishes,
appliances) when the exact selection is unknown at bid time. Proper use prevents
surprises and disputes.

**Allowance Definition:** The allowance covers the specified item only (e.g., "kitchen
faucet") at a defined quality level. Overages are change orders; underages may or may
not be credited depending on contract terms.

**Setting the Amount:** Base allowances on typical mid-range selections. Too low leads
to constant change orders; too high inflates the bid. Document what the allowance
includes (product only vs. product plus install).

**Client Communication:** Explain that the allowance is an estimate. Client choices
above the allowance require additional payment. Provide guidance on selections that
fit within the allowance.

**Variance Handling:** Define in the contract how allowance surpluses or shortfalls
are handled at final selection.
"""  # noqa: E501

