title = "Unit Cost Development"

content = """
Unit costs ($/sf, $/lf, $/ea) are the building blocks of estimates. They must be
current, sourced, and appropriate for the project's quality level.

**Source Documentation:** Record the source of each unit cost: supplier quote, price
list, historical project. Outdated or unsourced costs undermine estimate credibility.

**Quality and Scope:** A "mid-grade" flooring price means nothing without defining the
tier. Match the unit cost to the specification. Avoid mixing economy and premium
references in the same line.

**Bulk and Volume:** Some materials have volume discounts. Note whether the price
assumes minimum order or project quantity. Small projects may not qualify for bulk rates.

**Regional Variation:** Material and labor costs vary by region. Adjust national or
reference data for local conditions. Use local supplier quotes when available.
"""  # noqa: E501

