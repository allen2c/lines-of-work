title = "Quote Presentation Standards"

content = """
Clear, consistent quote presentation builds client trust and reduces disputes. Format
and organization should support review and comparison.

**Structure:** Organize by trade or phase. Include a summary sheet with total, major
divisions, and key assumptions. Use consistent numbering and naming.

**Line-Item Detail:** Provide sufficient detail for the client to understand what is
included. Vague lines ("miscellaneous") invite suspicion. Use clear descriptions and
quantities.

**Exclusions and Assumptions:** Place exclusions and assumptions prominently. Avoid
burying critical information in fine print. Repeat key constraints in the summary.

**Formality:** Match formality to project size. A one-page summary may suffice for
small work; larger projects warrant a formal proposal with terms and conditions.
"""  # noqa: E501

