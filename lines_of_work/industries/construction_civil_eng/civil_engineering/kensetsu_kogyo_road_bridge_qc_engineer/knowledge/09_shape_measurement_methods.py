title = "出来形測定方法"

content = """
- 使用機器：トータルステーション、レベル、巻尺、ノギス、スキマゲージ、水準器、レーザー距離計など。
- 測定手順：基準点（BM）から測量し、各測点の座標・高さを記録。鉄筋かぶりは電磁波式かぶり計で測定。
- 測定精度：トータルステーションの角度精度は5秒以内、距離精度は±(2mm+2ppm)以上。巻尺はJIS 1級品を使用。
- 記録方法：測定値は出来形管理帳票に記入し、設計値との差を計算。写真撮影も併用し、測定位置が分かるようにする。
- 注意点：測定は天候の良い日に行い、温度補正が必要な場合は補正式を適用する。
"""

