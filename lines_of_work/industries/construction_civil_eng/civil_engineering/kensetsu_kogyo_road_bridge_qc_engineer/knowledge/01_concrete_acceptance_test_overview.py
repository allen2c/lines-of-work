title = "コンクリート受入試験の概要"

content = """
- コンクリート受入試験は、生コンクリートが配合計画書およびJIS A 5308（レディーミクストコンクリート）に適合しているかを確認するために、現場到着後速やかに実施する。
- 試験項目：スランプ、空気量、塩化物含有量、コンクリート温度、圧縮強度用供試体採取。必要に応じて塩化物イオン拡散係数やアルカリ骨材反応試験も行う。
- 試験頻度：同一配合・同一製造所からの連続打設の場合、原則として1日1回以上、または150m³ごとに1回以上。橋梁の重要部材では全荷を対象とする場合もある。
- 不合格となった荷は、荷卸しを拒否し、生コン工場に返送する。再試験は認められない。
- 試験結果は「コンクリート受入試験報告書」に記録し、打設日、配合記号、荷番、試験値、判定、試験者署名を明記する。
"""

