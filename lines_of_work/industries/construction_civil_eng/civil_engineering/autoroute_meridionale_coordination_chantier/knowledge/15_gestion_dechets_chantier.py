title = "Gestion des déchets de chantier"

content = """
Les déchets de chantier (gravats, enrobés, terres, bois, ferraille)
doivent être triés, évacués et traités conformément à la réglementation.
Les terres excavées peuvent être réutilisées sur site ou ailleurs sous conditions.

**Tri à la source :** Bennes dédiées, traçabilité. Déchets dangereux
(amiante, hydrocarbures) gérés séparément.

**Bordereau de suivi :** BSD pour les déchets dangereux. Conservation
des preuves d'élimination ou de valorisation. Conformité ICPE si
installation de stockage temporaire sur site.
"""

