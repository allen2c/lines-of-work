title = "Sous-traitance"

content = """
La sous-traitance permet de faire réaliser tout ou partie des travaux
par une autre entreprise. Le donneur d'ordre reste responsable vis-à-vis
du maître d'ouvrage. Convention écrite obligatoire.

**Agrément :** Le maître d'ouvrage doit agréer le sous-traitant. Refus
d'agrément possible si capacités insuffisantes ou conflit d'intérêts.

**Coordination :** Interface entre lots, planning partagé, points
réguliers. PPSPS et coordination SPS incluant les sous-traitants.
"""

