title = "Poussières et silice"

content = """
Les poussières de chantier, notamment celles contenant de la silice
cristalline (perçage, sciage béton ou pierre), exposent au risque
de silicose. Réduction à la source (arrosage, aspiration) et
protection individuelle.

**Mesures techniques :** Travail à l'humide, captage à la source,
ventilation. Éviter le balayage à sec.

**EPI :** Masques FFP2 ou FFP3 selon l'évaluation. Contrôle
d'étanchéité du masque. Formation à la pose et à l'entretien.
"""

