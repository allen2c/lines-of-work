title = "Eurocode 3 — Stahlbau"

content = """
DIN EN 1993 (Eurocode 3) gilt für die Bemessung von Stahlbauten und
Stahlverbundkonstruktionen. Brücken und Stützbauwerke aus Stahl
unterliegen zusätzlich den Zusätzlichen Technischen Vertragsbedingungen
für Ingenieurbauten (ZTV-ING).

**Querschnittsklassen:** Die Einstufung in Klasse 1 bis 4 bestimmt,
ob plastische Querschnittsausnutzung zugelassen ist. Für Ermüdungsbeanspruchungen
sind die Kerbfallklassen der Anschlussdetails maßgebend.

**Stabilität:** Knicken, Biegedrillknicken und Beulen von Blechen
sind nach den relevanten Teilnormen (EC3-1-1, 1-5) nachzuweisen.
Imperfektionen und Ersatzstabverfahren sind zu berücksichtigen.

**Schweißverbindungen:** Schweißnahtnachweise nach EC3-1-8.
Qualitätsanforderungen an Schweißnähte (Visuell, ZfP) und
Dokumentation der Schweißerqualifikation sind Pflicht.
"""

