title = "Korrosionsschutz von Stahlbauwerken"

content = """
Stahlbauwerke benötigen Schutz vor Korrosion durch Beschichtung,
Feuerverzinkung oder Duplex-Systeme. Die ZTV-ING Teil 3
und die ISO 12944 legen Anforderungen fest.

**Beschichtungssysteme:** Grundbeschichtung, Zwischenbeschichtung,
Deckbeschichtung. Korrosivitätskategorien (C2–C5) und
Schutzziele. Trockenschichtdicke und Haftung.

**Oberflächenvorbehandlung:** Strahlen bis Reinheitsgrad Sa 2½,
Rauhtiefe. Kontrolle vor Auftrag. Untergrundprüfung.

**Instandhaltung:** Erneuerung nach Erreichen der
Mindestschichtdicke oder bei Durchrostung. Dokumentation
der Oberflächenzustände bei Hauptprüfungen.
"""

