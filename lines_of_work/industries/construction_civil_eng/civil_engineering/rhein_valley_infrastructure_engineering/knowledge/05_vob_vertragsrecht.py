title = "VOB — Vergabe- und Vertragsordnung für Bauleistungen"

content = """
Die VOB gliedert sich in drei Teile: VOB Teil A (Vergabeverfahren),
VOB Teil B (Allgemeine Vertragsbedingungen für die Ausführung von
Bauleistungen) und VOB Teil C (Allgemeine Technische Vertragsbedingungen).

**VOB/B:** Regelt die vertraglichen Beziehungen zwischen Auftraggeber
und Auftragnehmer. Relevante Themen: Abnahme, Mängelhaftung, Leistungsänderungen,
Mehr- und Minderleistungen, Vertragsstrafen. Die Abnahme ist ein
rechtserheblicher Vorgang und muss protokolliert werden.

**VOB/C:** Enthält technische Regeln für Erdarbeiten, Betonarbeiten,
Straßenbau und andere Gewerke. ATV DIN 18331 (Erdarbeiten), DIN 18300
(Erdbau), ZTV E-StB und weitere Normen werden einbezogen.

**Ausschreibung:** Leistungsbeschreibungen müssen eindeutig, vollständig
und widerspruchsfrei sein. Leistungsverzeichnisse (LV) und
Positionstexte sind maßgeblich für die Auslegung.
"""

