title = "型枠工事の実務"

content = """
- 型枠材：合板（ラワン合板12mm、コンクリート型枠用合板18mm）、鋼製型枠（パネル式）。使用回数管理（合板は5回程度で交換）。
- 組立て：墨出しに合わせて型枠を建て込む。コーナー部は専用金物（コーナークリップ）。継ぎ目はテープでシール。
- 支保工：パイプサポート（調整式）、水平つなぎ、筋かい。支柱間隔は設計値。高さ3.5m超は2段水平つなぎ。
- 剥離剤：油性または水性。均一に塗布、過剰はコンクリート表面の汚れ原因。塗布後1時間以内にコンクリート打設。
- インサート：配管スリーブ、アンカーは型枠に固定。位置確認。貫通部は防水テープ。
- 脱型：強度確認後、側面型枠から。下面型枠は支保工を残して脱型。再支保工が必要な場合あり。
- 点検：建て込み後、鉛直・水平・断面寸法を計測。許容差内であること。
"""

