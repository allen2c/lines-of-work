content = """
- 準備：打設前の型枠・鉄筋検査合格、生コン手配、ポンプ車配置、作業員配置（打設班、締固め班、仕上げ班）。
- 打設：柱→梁→スラブの順。柱は1mごとに締固め。梁は側面から打設、スラブは一方向に連続。打継ぎ部はモルタル敷き。
- 締固め：内部振動機を垂直に挿入、引き抜きはゆっくり。型枠に接触させない。再振動禁止。
- 仕上げ：スラブ上面はレベラーで高さ調整、金ごて仕上げ。打設後2～4時間で仕上げ完了。
- 養生：打設後すぐに湿潤養生開始。7日間継続。
- 脱型：強度確認後、側面型枠→下面型枠（支保工は残す）。スラブ下面は強度100%まで支保工保持。
- 検査：脱型後、表面状態、ひび割れ、出来形測定。必要に応じて補修。
"""

