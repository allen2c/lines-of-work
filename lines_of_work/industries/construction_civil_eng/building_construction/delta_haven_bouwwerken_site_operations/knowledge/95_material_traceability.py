title = "Material Traceability"

content = """
Material traceability links installed
materials to sources and test
results. Essential for quality,
warranty, and compliance. Maintain
through procurement and construction.

**Certification:** Request mill
certificates, test reports, and
batch numbers. File with delivery
records. Cross-reference to
installation locations.

**Recording:** Log materials to
location. Batch concrete to pour.
Tag reinforcement and structural
steel. Document in as-built.

**Recall:** If defect discovered,
traceability enables identification
of affected work. Plan remediation.
"""

