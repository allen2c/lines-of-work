title = "Quality Control Procedures"

content = """
Quality control verifies that work meets specifications
before concealment or handover. Inspection and test plans
define what to check, when, and by whom.

**Inspection Points:** Define hold points where work
must be approved before proceeding. Do not bypass
without formal concession.

**Testing:** Conduct specified tests (e.g., concrete
strength, pressure tests) and document results.
Investigate failures and correct before acceptance.

**Records:** Maintain inspection and test records for
handover and future reference. Traceability supports
warranty and dispute resolution.
"""

