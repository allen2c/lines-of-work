title = "Roofing Systems and Waterproofing"

content = """
Roofing systems protect the building from weather and contribute to
energy performance. Flat and pitched roofs each have distinct
installation and waterproofing requirements. In the Dutch climate,
durability and drainage are critical.

**Flat Roofs:** Built-up or single-ply membranes require clean substrates,
proper lap joints, and termination details. Ensure adequate falls for
drainage and inspect penetration flashings.

**Pitched Roofs:** Tiles, slates, or metal panels require correct fixings,
underlay, and valley details. Verify wind zone and exposure class for
fixing specifications.

**Waterproofing Below Grade:** Tanking or drainage systems protect
basements. Ensure continuity with above-grade damp proofing and
integrity of joints and penetrations.
"""

