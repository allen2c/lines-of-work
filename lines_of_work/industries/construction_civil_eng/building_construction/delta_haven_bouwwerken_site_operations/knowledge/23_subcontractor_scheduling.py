title = "Subcontractor Scheduling"

content = """
Subcontractor scheduling ensures trades execute in the
correct sequence with adequate access and prerequisites.
Poor sequencing causes delay, rework, and conflict.

**Sequence:** Follow the construction program. Identify
dependencies and lead times. Buffer for weather and
uncertainty where appropriate.

**Interface Management:** Define handover criteria between
trades. Inspect completed work before follow-on starts.
Document any deficiencies.

**Communication:** Daily or weekly coordination meetings
align priorities. Update schedule when conditions change.
"""

