title = "Defect Management"

content = """
Defects are non-conformances that must be
corrected. Systematic identification and
resolution ensure quality before handover.

**Identification:** Inspect work at defined
stages. Log defects with location, description,
and responsibility. Assign priority.

**Resolution:** Correct within agreed timescales.
Re-inspect after correction. Close defect when
satisfactory.

**Recurrence:** Analyse defect patterns. Address
root causes to prevent repetition. Share
lessons with team.
"""

