title = "Commissioning Preparation"

content = """
Commissioning verifies systems perform
as designed. Preparation during
construction simplifies commissioning
and accelerates handover.

**Documentation:** Maintain installation
records. Document test results. Provide
equipment data for commissioning.

**Access:** Ensure access to equipment
and points. Complete work before
commissioning starts. Resolve defects
promptly.

**Schedule:** Coordinate with commissioning
team. Identify prerequisites. Plan
sequential systems (e.g., electrical
before HVAC). Avoid rework from
commissioning findings.
"""

