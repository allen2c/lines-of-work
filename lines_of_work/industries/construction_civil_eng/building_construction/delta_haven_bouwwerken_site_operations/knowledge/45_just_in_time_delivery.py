title = "Just-in-Time Delivery"

content = """
Just-in-time delivery reduces storage and
handling by delivering materials when needed.
Requires coordination and reliable supply.

**Planning:** Coordinate delivery with
construction schedule. Account for lead
times and buffer for uncertainty. Communicate
requirements to suppliers.

**Site Readiness:** Ensure access and
staging space when delivery arrives. Avoid
delays that hold up delivery or work.

**Risks:** Late delivery delays work.
Plan contingencies for critical items.
Maintain supplier relationships.
"""

