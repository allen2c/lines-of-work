title = "Hot Weather Precautions"

content = """
Hot weather accelerates concrete setting and
evaporation. Rapid hydration can cause cracking;
rapid evaporation weakens surface. Plan for
summer conditions.

**Placement:** Cool mix water or use retarders.
Place during cooler hours. Protect from sun
and wind during placement.

**Curing:** Start curing immediately. Use
evaporation retardants or wet curing. Extend
curing for mass concrete to control temperature.

**Worker Safety:** Provide shade, water, and
rest breaks. Watch for heat stress.
"""

