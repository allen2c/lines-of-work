title = "Dust and Silica Control"

content = """
Dust, especially silica from cutting and
grinding, causes respiratory disease.
Control exposure through engineering
and administrative measures.

**Engineering Controls:** Use wet cutting,
LEV, or enclosed processes. Select
low-dust materials where possible.

**Administrative Controls:** Limit exposure
duration. Rotate workers. Provide training
on hazards and controls.

**PPE:** When controls insufficient, use
respiratory protection. Ensure fit
testing and training. Maintain equipment.
"""

