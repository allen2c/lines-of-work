"""Material certification and traceability requirements."""

title = "Material Certification"

content = """
Materials used in construction must be traceable to certificates of conformity
(verklaring van overeenstemming, VvO) or equivalent. Quality assurance verifies
that delivered materials match the specified grades and have valid documentation.

**Structural Materials:** Concrete, reinforcement steel, and structural steel
require mill certificates or VvO. Verify grade, dimensions, and batch against
order and design. Store certificates with the project file.

**Envelope Materials:** Insulation, membranes, and cladding must have CE marking
and performance declarations. Verify thermal resistance (R-value) and fire
classification against specification.

**MEP Materials:** Pipes, cables, and equipment require type approval and
installation certificates. Verify compatibility with Dutch norms (NEN).

**Storage and Handling:** Check that materials are stored per manufacturer
instructions. Reject damaged or contaminated materials. Document any
substitutions and obtain approval before use.
"""

