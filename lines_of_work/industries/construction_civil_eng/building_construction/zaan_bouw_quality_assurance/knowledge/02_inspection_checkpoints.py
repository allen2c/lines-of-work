title = "Inspection Checkpoints"

content = """
Quality inspections occur at defined stages before work is covered or handed over.
Each checkpoint has specific criteria and documentation requirements. Missing a
checkpoint can result in rework or authority rejection.

**Foundation:** Verify pile records, reinforcement placement, concrete grade,
and waterproofing before backfill. Document with photographs and checklists.

**Structure:** Inspect reinforcement lap lengths, cover, and connections before
concrete pour. Verify formwork alignment and support. Post-pour: check curing
and surface quality.

**Envelope:** Inspect insulation continuity, vapour barrier installation, and
flashing details before cladding. Verify airtightness and thermal bridge
treatment.

**MEP Rough-In:** Inspect conduit and duct routing, fire-stopping, and
penetration seals before closing. Verify test results for pressure and
electrical circuits.

**Finishes:** Inspect substrate preparation, material compatibility, and
installation per specification before handover.
"""

