"""Fire safety compliance and compartmentation verification."""

title = "Fire Safety Compliance"

content = """
Fire safety requirements under the Bouwbesluit and NEN 6068 must be verified
during construction. Compartmentation, fire resistance of structural elements,
and means of escape require documented inspection before occupation.

**Compartmentation:** Verify fire-resistant walls, floors, and doors are
installed per design. Check penetrations are properly sealed with approved
fire stops. Document fire resistance ratings and certification.

**Structural Fire Resistance:** Confirm concrete cover, intumescent coatings,
or other protection meets specified fire resistance. Verify connections and
joints. Retain manufacturer certificates for passive fire protection systems.

**Escape Routes:** Verify escape route dimensions, signage, and emergency
lighting. Check that finishes in escape routes meet reaction-to-fire
requirements. Coordinate with authority inspection schedule.
"""

