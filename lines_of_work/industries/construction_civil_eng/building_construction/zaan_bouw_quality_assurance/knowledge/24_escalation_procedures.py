"""Escalation procedures for quality issues and non-conformances."""

title = "Escalation Procedures for Quality Issues"

content = """
Quality issues that cannot be resolved at site level must be escalated
promptly. Delayed escalation can lead to rework, schedule impact, and
regulatory exposure.

**Level 1 — Site Resolution:** Minor deviations within tolerance may be
resolved by the site quality coordinator with subcontractor agreement.
Document the resolution and any concessions.

**Level 2 — Project Manager:** Non-conformances affecting specification,
safety, or regulatory compliance require project manager involvement.
Prepare a clear summary with photos, references, and proposed resolution.

**Level 3 — Technical Director:** Design changes, authority disputes, or
repeated failures require technical director review. Include full history
and impact assessment.
"""  # noqa: E501

