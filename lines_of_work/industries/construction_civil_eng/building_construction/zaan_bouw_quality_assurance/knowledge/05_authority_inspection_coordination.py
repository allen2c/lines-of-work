content = """
The omgevingsdienst (environmental and building authority) conducts mandatory
inspections at key stages. Quality assurance coordinates scheduling, preparation,
and follow-up to ensure smooth approval and avoid delays.

**Inspection Stages:** Foundation, structure, fire safety installations, and
completion. Schedule inspections well in advance; authorities have limited
capacity.

**Preparation:** Ensure all required documentation is ready: drawings, certificates,
inspection reports, and as-built records. Walk the site beforehand to confirm
work is complete and accessible.

**During Inspection:** Accompany the inspector, answer questions, and document
findings. Note any conditions or follow-up required.

**Follow-Up:** Address any remarks or rejections promptly. Reschedule if
necessary. Obtain written approval before proceeding. File approval documents
in the project record.
"""

