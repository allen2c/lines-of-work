"""Quality requirements for heritage and monument-protected buildings in the Zaan region."""

title = "Heritage and Monument Quality Requirements"

content = """
The Zaanstreek region contains protected heritage structures and monument
buildings. Projects involving such structures require additional quality
controls and coordination with cultural heritage authorities.

**Monument Permit:** Verify that the required monument permit (omgevingsvergunning
monument) is in place before work begins. Conditions attached to the permit
define allowable interventions and preservation requirements.

**Material and Method Compatibility:** Restoration and renovation work must
use materials and methods compatible with historic fabric. Document all
deviations from original construction and obtain approval where required.

**Inspection Coordination:** Heritage authority inspections may be required
at specific stages. Schedule and prepare documentation in advance.
"""  # noqa: E501

