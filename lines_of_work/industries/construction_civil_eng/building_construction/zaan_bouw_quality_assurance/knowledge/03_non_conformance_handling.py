"""Non-conformance handling and documentation procedures."""

title = "Non-Conformance Handling"

content = """
When work does not meet specification or regulation, it must be documented and
resolved before proceeding. Non-conformances (NCs) are logged, classified, and
tracked to closure.

**Identification:** Record the location, trade, description, and reference to
specification or drawing. Attach photographs. Assign a unique NC number and
severity (critical, major, minor).

**Critical NCs:** Block further work until resolved. Examples: structural
defects, fire safety deviations, safety-critical installations. Escalate
immediately to project manager and relevant trade supervisor.

**Major NCs:** Must be corrected before the next phase or handover. Document
corrective action and re-inspect before closing.

**Minor NCs:** May be corrected during snagging. Track to ensure closure before
final handover.

**Closure:** Verify corrective work, sign off the NC, and file in the project
quality record.
"""

