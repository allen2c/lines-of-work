title = "Concrete Quality Control"

content = """
Concrete quality control ensures structural integrity and durability. Zaan Bouw
projects in the Zaanstreek region require documented compliance with NEN-EN 206
and project-specific mix designs. Quality assurance covers ordering, delivery,
placement, and curing.

**Delivery Verification:** Check delivery tickets for mix designation, batch
number, and conformity. Verify slump and temperature at discharge. Reject loads
that exceed allowable deviation or show segregation. Retain samples for
compressive strength testing per statistical plan.

**Placement and Compaction:** Ensure adequate vibration and consolidation.
Verify cover to reinforcement and joint preparation. Document ambient conditions
and any interruptions. Cold weather and hot weather concreting require specific
precautions per NEN 8005.

**Curing and Protection:** Protect fresh concrete from desiccation, freezing,
and mechanical damage. Curing duration and method must align with specification.
Record curing regime for traceability.
"""

