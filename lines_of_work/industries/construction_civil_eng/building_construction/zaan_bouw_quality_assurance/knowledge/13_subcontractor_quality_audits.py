"""Subcontractor quality audit procedures."""

title = "Subcontractor Quality Audits"

content = """
Periodic audits of subcontractor work verify that quality systems are in place
and that workmanship meets specification. Audits are planned, not ad hoc.

**Audit Scope:** Review method statements, material certifications, and
inspection records. Observe work in progress and interview site personnel.
Check compliance with VCA and project-specific requirements.

**Findings:** Document non-conformities with evidence. Require corrective
action plans with deadlines. Follow up to verify closure. Escalate persistent
issues to the project manager.
"""

