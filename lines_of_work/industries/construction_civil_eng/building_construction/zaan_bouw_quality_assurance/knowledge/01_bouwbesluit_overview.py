content = """
The Bouwbesluit (Building Decree) is the primary Dutch regulation governing building
construction. It sets technical requirements for safety, health, usability, energy
efficiency, and environmental protection. Quality assurance must ensure all work
conforms to the applicable articles.

**Scope:** The Bouwbesluit applies to new construction, renovation, and change of use.
Requirements vary by building function (residential, office, industrial) and are
expressed as functional demands (prestatie-eisen) rather than prescriptive rules.

**Key Areas:** Fire safety (brandveiligheid), structural stability, accessibility,
acoustic performance, thermal insulation, and moisture control. Each area has
verification routes: calculation, testing, or reference to approved standards.

**Verification:** Document compliance through technical specifications, test reports,
and inspection records. The building authority (omgevingsdienst) verifies during
construction and at completion.
"""

