title = "Quality Plan Structure"

content = """
The project quality plan defines inspection and test plans, hold points,
and documentation requirements. It is agreed at project start and updated
when scope changes.

**Inspection and Test Plans (ITP):** Per discipline, list activities,
inspection frequency, acceptance criteria, and records. Define hold points
where work may not proceed without QA release.

**Roles and Responsibilities:** Clarify who performs inspections, who
reviews, and who signs off. Avoid conflicts of interest.

**Review Cycle:** Update the quality plan when the design or construction
sequence changes. Communicate changes to all parties.
"""

