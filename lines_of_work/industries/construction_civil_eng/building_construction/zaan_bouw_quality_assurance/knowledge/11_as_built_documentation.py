"""As-built documentation standards for quality records."""

title = "As-Built Documentation"

content = """
As-built documentation records the actual constructed condition of the building
for handover, maintenance, and future modifications. In Dutch practice, the
opdrachtgever receives as-built drawings, O&M manuals, and warranty documentation.

**Drawing Updates:** Mark-up or revise design drawings to reflect installed
conditions. Note deviations from design that were approved during construction.
Include MEP routing, structural connections, and finishes.

**Photographic Records:** Document critical interfaces, concealed work before
closure, and completed installations. Timestamp and geolocate where relevant.
Store in a structured archive for future reference.

**O&M Manuals:** Compile manufacturer data sheets, maintenance schedules, and
operating instructions for installed systems. Ensure Dutch-language versions
where required by contract.
"""

