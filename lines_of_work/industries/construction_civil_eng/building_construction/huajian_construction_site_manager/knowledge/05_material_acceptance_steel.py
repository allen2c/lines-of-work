title = "钢筋进场验收标准"

content = """
- 资料核对：每批钢筋需提供质量证明书、出厂检验报告，核对炉批号、规格、数量是否与送货单一致，资料不全拒收。
- 外观检查：钢筋表面不得有裂纹、结疤、折叠，锈蚀程度不得影响截面（轻微浮锈可接受，严重锈蚀需除锈后复检）。
- 尺寸抽检：直径6-12mm钢筋，每捆抽检5根，直径偏差≤±0.3mm；直径14-25mm钢筋，偏差≤±0.4mm。不合格批次退场。
- 取样送检：每60吨为一批次，不足60吨按一批计，随机抽取5根，每根取1个拉伸试样和1个弯曲试样，送检合格后方可使用。
- 堆放要求：按规格、批次分区堆放，离地≥200mm（垫木），标识牌清晰（规格、产地、检验状态），未检区与合格区严格分开。
"""

