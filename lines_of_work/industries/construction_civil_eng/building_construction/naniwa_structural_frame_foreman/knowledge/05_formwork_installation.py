content = """
- 型枠は、コンクリートの側圧・重量・打設時の衝撃・作業荷重に耐える強度と剛性を確保する。せき板・桟木・端太・締付金物（フォームタイ、セパレータ）・支保工を計画通りに配置する。
- 建込み前に墨（通り芯・逃げ墨・レベル）を確認し、柱・壁の位置と建入れ（垂直）を出す。下げ振り・トランシット・水平器で精度を確認する。
- 締付け不足は「はらみ・のろ漏れ・寸法不良」、締めすぎは「セパレータ破断・かぶり不足」を招く。ピッチと本数を図面・要領通りに守る。
- 支保工（パイプサポート、大引・根太、水平つなぎ）は座屈と沈下を防ぐため、根がらみ・敷板・水平つなぎを確実に設ける。梁下・スラブ下の許容荷重を超えない。
- 打設前に清掃（ごみ・木くず・雪氷除去）、はく離剤塗布、開口・スリーブ・箱抜き・インサートの位置固定を確認する。桟木の緩みや隙間を再点検する。
- 脱型は所定の強度発現とコンクリートの材齢・気温を確認してから行い、支保工の存置期間を守る。転用材は損傷・変形を点検し不良品を除く。
"""

