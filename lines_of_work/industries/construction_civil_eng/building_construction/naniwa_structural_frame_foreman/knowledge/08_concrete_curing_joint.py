content = """
- 打設後は硬化に必要な温度・湿度を保つ湿潤養生を行う。散水、養生マット、養生シート、膜養生剤などで乾燥・急激な水分逸散を防ぐ。乾燥はひび割れと強度不足の原因となる。
- 養生期間は所要強度の発現とセメント種類・気温で決める。普通ポルトランドセメントで平均気温15℃以上なら湿潤養生5日以上が目安。強度確認前に大きな荷重や衝撃を与えない。
- 寒中コンクリート（打設後の平均気温4℃以下が予想される時期）は初期凍害を防ぐため、給熱養生・断熱養生・シート囲いで保温し、コンクリート温度を確保する。凍結融解を受けさせない。
- 暑中コンクリート（日平均気温25℃超）は水分の急激な蒸発とスランプ低下、コールドジョイントに注意し、打設後速やかに養生を開始する。
- 打継ぎ位置は構造上応力の小さい位置（梁・スラブはスパン中央付近、柱・壁はスラブ天端）に設ける。せん断力の大きい位置を避ける。
- 打継ぎ面はレイタンス・ぜい弱部・ごみを除去して健全なコンクリートを露出させ、十分に吸水させてから新たに打ち込む。水平打継ぎ部は特に清掃を徹底する。
"""

