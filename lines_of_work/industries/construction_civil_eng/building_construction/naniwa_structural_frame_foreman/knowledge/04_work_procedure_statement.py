title = "作業手順書と施工要領"

content = """
- 危険を伴う作業や品質に直結する作業には、事前に作業手順書（施工要領書）を用意する。型枠建込み、支保工、鉄筋組立、コンクリート打設、揚重、解体などが対象。
- 作業手順書には、作業の順序、使用する機械・工具・材料、各手順の急所（安全・品質・やりやすさ）、想定される危険と対策、資格要件、指揮者・合図者を明記する。
- 手順書は現場の実情に合わせて作成し、机上の一般論で済ませない。前工程・後工程との取り合い、他業者との輻輳を考慮する。
- 作業前に手順書を用いて作業員へ「作業手順の周知（ツールボックスミーティング）」を行い、理解を確認する。手順変更が生じたら口頭で終わらせず、その場で共有し記録に残す。
- 元請から求められる場合は、施工要領書を事前に提出し承認を得る。承認された手順から逸脱しない。逸脱が必要なら再協議する。
- 実施後は手順の問題点を振り返り、次回に反映して手順書を改訂する。
"""

