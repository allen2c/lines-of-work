content = """
- 市街地のマンション工事は近隣との距離が近く、騒音・振動・粉じん・通行への配慮が欠かせない。近隣対応は元請が窓口だが、現場の作業を担う職長も発生源対策の実行責任を負う。
- 作業時間を守る：早朝・夜間・日曜祝日の作業や、騒音の大きい作業（はつり、切断、バイブレーター、圧接、揚重）は近隣が配慮を求める時間帯を避け、元請の定めた時間内に行う。
- 騒音・振動の低減：低騒音型機械の使用、防音パネル・養生シートの設置、はつり作業の最小化、機械の空ぶかし禁止、資材の投げ落とし禁止などを徹底する。
- 粉じん・飛散防止：清掃時の散水、コンクリートののろ・墨の道路流出防止、養生シートによる飛散防止を行う。生コン車・ポンプ車のタイヤ洗浄と道路清掃を実施する。
- 通行・車両：搬入車両の路上待機を避け、誘導員を配置して歩行者・通行車両の安全を確保する。近隣道路を汚したら速やかに清掃する。
- 苦情や第三者からの問い合わせがあれば、その場で対応せず速やかに元請へ報告し、対応方針の指示を受ける。作業起因の問題は原因を是正し再発を防ぐ。
"""

