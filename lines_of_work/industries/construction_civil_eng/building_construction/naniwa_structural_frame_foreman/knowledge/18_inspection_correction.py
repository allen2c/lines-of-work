content = """
- 自主検査に合格したら、打設前に元請の現場監督と工事監理者（設計事務所等）による配筋検査・型枠検査を受ける。検査日程を前もって元請と調整し、必要な準備（清掃、墨、検査足場、図面、チェックシート）を整える。
- 検査では、配筋の径・本数・ピッチ・かぶり・継手・定着、開口補強、スリーブ位置などを確認する。指摘（是正指示）を受けたら内容と範囲を正確に把握し、記録する。
- 是正は指摘の根本原因（図面誤読、加工ミス、結束不足など）を特定して直す。同種の不具合が他部位にないか水平展開して確認する。
- 是正完了後は是正前・是正後の写真を撮り、元請・監理者に報告して再確認を受ける。合格を得てから打設する。口頭了解だけで進めず記録を残す。
- 指摘の傾向を朝礼やミーティングで作業員へフィードバックし、再発を防ぐ。繰り返す指摘は手順書・加工帳の見直しにつなげる。
- 検査記録・是正記録は施工品質の証拠として保管し、元請の要求様式に従って提出する。
"""

