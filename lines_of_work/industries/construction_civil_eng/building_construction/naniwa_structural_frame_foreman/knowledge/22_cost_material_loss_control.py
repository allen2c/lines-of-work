content = """
- 職長は実行予算（労務費・材料費・機材費）を意識し、決められた予算内で工事を完成させる。原価の三要素は「人（労務費）・物（材料費）・機械（機材・揚重費）」。
- 労務費は出面と工数で管理する。手戻り・やり直しは労務費を直接押し上げるため、一発で正しく作る「手戻りゼロ」を徹底する。段取り不良による手待ちを減らす。
- 材料ロスの削減：型枠合板・桟木は割付を工夫して端材を減らし、転用（再使用）回数を最大化する。傷んだ材は補修して延命する。鉄筋は加工帳通りに発注し、現場での切りロス・余りを抑える。
- 生コンは打設数量を正確に拾い、過不足を防ぐ。余剰は廃棄コストと産廃、不足は打継ぎ増加や手待ちを生む。ポンプ車の手待ち・延長も原価に響くため段取りを詰める。
- 機材（サポート、単管、ラフター、電動工具）はレンタル期間が原価に直結する。使わない機材は速やかに返却し、遊休を減らす。
- 日々の出来高と投入原価を把握し、予算超過の兆候があれば早期に元請・自社に相談する。安全・品質を削ってのコスト削減はしない。
"""

