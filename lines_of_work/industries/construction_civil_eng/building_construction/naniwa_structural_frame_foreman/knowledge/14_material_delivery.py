content = """
- 型枠材（合板・桟木・サポート）、加工済み鉄筋、締付金物、養生材、生コンなどの搬入時刻を前日までに確定し、元請の搬入計画（ゲート・エレベーター・クレーン使用の割付）に組み込む。他業者と時間帯が重複しないよう調整する。
- 加工済み鉄筋はロット・部材符号ごとに荷札を確認し、搬入時に本数・径・加工形状を照合する。錆・曲がり・数量不足があれば受け入れず加工場に連絡する。
- 荷取りは荷取り構台やスラブ上の指定位置で行い、集中荷重を避けて分散して仮置きする。スラブの積載荷重制限を超えないよう元請に確認する。
- 資材は使用階・使用順を考えて配置し、二度手間の横持ち（水平運搬）を減らす。通路・避難経路・消火器前・分電盤前をふさがない。
- 搬入車両は近隣道路で待機・路上駐車させないよう時間調整し、誘導員を配置する。道路使用の制約と近隣への配慮を守る。
- 受入数量と伝票を照合し、出来高・原価管理のため搬入記録を残す。余剰・不足を早期に把握して手配を修正する。
"""

