title = "朝礼・KY活動の進め方"

content = """
- 朝礼は全作業員が参加し、当日の作業内容・工程・入場業者・重機稼働・立入禁止区域を共有する。元請主催の合同朝礼のあと、自社の職種別ミーティングを行う。
- KY（危険予知）活動は「現状把握（どんな危険があるか）→本質追究（危険のポイント）→対策樹立（どうする）→目標設定（私たちはこうする）」の4ラウンド法で進める。
- その日の具体作業に即した危険を挙げる。例：型枠建込みなら「脚立からの墜落」「材の落下」「釘の踏み抜き」など。抽象的な標語で終わらせない。
- 指差し呼称でチーム目標を唱和し、全員の意識を合わせる。KY記録用紙に危険ポイントと対策、参加者、当日の作業リーダーを記入して元請に提出する。
- 体調確認・アルコールチェックを行い、体調不良者は無理をさせず配置換えまたは休ませる。熱中症リスクが高い日はWBGT値と対策を朝礼で周知する。
- 新規入場者や当日だけの応援職人にも必ず作業内容と危険を個別に説明してから作業に就かせる。
"""

