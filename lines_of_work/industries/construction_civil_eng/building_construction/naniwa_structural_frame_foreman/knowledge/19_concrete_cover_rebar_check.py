title = "かぶり厚さと配筋精度の確保"

content = """
- かぶり厚さは、鉄筋表面からコンクリート外面までの最短距離で、耐久性・耐火性・防錆・付着に直結する。最小かぶりは部位で異なる（例：土に接しない柱・梁は一般に3cm以上、土に接する部分やスラブ下端など部位ごとに必要値を確認）。
- かぶりはスペーサーで確保する。床・スラブはバーサポートやサイコロ、壁・柱はドーナツ型やプラスチックスペーサーを適切なピッチで配置する。スペーサー不足でかぶり不足やせき板への鉄筋接触が起きる。
- 過大なかぶりは断面の有効高さ減少やひび割れを招くため、必要かぶり＋施工誤差の範囲に収める。設計かぶりと最小かぶりの違いを理解する。
- 配筋のピッチ・本数は打設時の振動で乱れやすい。結束を確実にし、上端筋の受け（馬・スペーサー）を入れてたわみを防ぐ。打設直前・打設中に配筋の乱れを点検する。
- 継手・定着の位置と長さは応力の観点で重要。梁主筋の定着、柱梁仕口の納まり、開口補強の位置を図面通りに施工し、勝手な省略や位置ずらしをしない。
- かぶり測定はスペーサー間・要所で実測し、写真で記録する。不足箇所は打設前に是正する。
"""

