title = "Fill Rate dan Ketersediaan Stok"

content = """
Fill rate adalah persentase permintaan yang dapat dipenuhi dari stok
tersedia. Fill rate tinggi berarti pelanggan jarang mengalami stockout
atau penggantian barang; fill rate rendah merugikan penjualan dan
reputasi.

**Perhitungan:** Fill rate = (pesanan terpenuhi lengkap / total pesanan) × 100%.
Atau pada level SKU: (unit terpenuhi / unit diminta) × 100%.

**Penyebab Fill Rate Rendah:** Peramalan demand yang buruk, lead time
supplier panjang, overselling karena sinkronisasi stok tertunda, atau
selisih inventori (fisik vs sistem).

**Improvement:** Perbaiki peramalan, tingkatkan safety stock untuk
produk populer, percepat replenishment, dan pastikan akurasi data
inventori melalui stock opname berkala.
"""  # noqa: E501

