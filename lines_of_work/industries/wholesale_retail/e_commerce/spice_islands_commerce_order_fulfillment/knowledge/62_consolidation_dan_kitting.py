title = "Consolidation dan Kitting"

content = """
Consolidation menggabungkan beberapa pesanan atau beberapa item ke
satu paket atau satu shipment. Kitting adalah perakitan beberapa
komponen menjadi satu unit sebelum pengiriman.

**Consolidation:** Ketika pelanggan membuat beberapa pesanan dalam
waktu singkat ke alamat yang sama, pertimbangkan consolidate ke
satu paket untuk menghemat ongkos kirim dan mengurangi kemasan.
Beberapa sistem mendeteksi dan menawarkan opsi ini.

**Kitting:** Untuk bundle atau set produk (mis. starter kit),
komponen dirakit di gudang dan diberi satu SKU kit. Mempercepat
picking untuk pesanan bundle dan memastikan kelengkapan.

**Trade-off:** Consolidation menunda pengiriman untuk pesanan
pertama sampai pesanan terakhir siap. Kitting menambah langkah
di gudang tetapi menyederhanakan inventory untuk bundle.
"""  # noqa: E501

