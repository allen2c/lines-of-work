title = "Pickup Point dan Locker"

content = """
Pickup point (POP) dan locker memungkinkan pelanggan mengambil paket
di lokasi yang nyaman ketimbang menunggu di rumah. Mengurangi attempt
gagal dan biaya last-mile untuk kurir.

**Pickup Point:** Warung, minimarket, atau toko mitra kurir yang
menerima paket. Pelanggan memilih POP saat checkout; kurir mengantar
ke POP; pelanggan mengambil dengan nomor resi dan identitas.

**Locker:** Kotak pengambilan otomatis di lokasi strategis. Kurir
memasukkan paket ke locker; pelanggan mendapat kode untuk membuka.
Biasanya gratis atau dikenakan biaya kecil.

**Keunggulan:** Pelanggan yang sibuk atau tanpa alamat tetap dapat
menerima paket. Kurir efisien karena pengantaran ke satu titik.
Spice Islands Commerce dapat menawarkan opsi POP/locker jika tersedia
di wilayah pelanggan.
"""  # noqa: E501

