title = "Climbing Rope Storage and Handling Standards"

content = """
Climbing ropes are critical safety equipment and require specialized care during
the fulfillment process at Glacier Outfitters. Improper storage or handling can
compromise the integrity of the rope and put the climber at risk.

**Storage Requirements:** Ropes must be stored in a cool, dry, and dark area
of the warehouse, away from direct sunlight and any potential sources of
chemical contamination (such as batteries or cleaning supplies). Keep ropes off
the concrete floor and on dedicated shelving to prevent moisture absorption.

**Handling Precautions:** When picking or packing a rope, avoid dragging it
across the floor or over sharp edges. Use both hands to lift the rope and avoid
stepping on it. Ropes should be stored and shipped in their original factory-
coiled condition to prevent tangling or kinking.

**Inspection for Damage:** Before shipping a rope, perform a visual and
tactile inspection to ensure there are no nicks, cuts, or soft spots in the
sheath or core. If any damage is suspected, the rope must be immediately
removed from sellable inventory and tagged for a more thorough review by a
qualified technician.

**Traceability and Documentation:** Every rope is assigned a unique batch or
serial number. This information must be recorded for every order to ensure full
traceability in the event of a safety recall or warranty claim. Never remove or
obscure any of the manufacturer's safety labels or warnings.
"""

