title = "Lithium Battery Shipping Regulations for Outdoor Electronics"

content = """
Many of Glacier Outfitters' most popular products—including headlamps, GPS
units, satellite communicators, and portable power banks—contain lithium
batteries. These batteries are classified as hazardous materials and are
subject to strict shipping regulations globally.

**UN3481 vs. UN3480:** Lithium-ion batteries "contained in equipment" (e.g., a
headlamp with an internal battery) are classified differently than "standalone"
lithium batteries (e.g., a replacement power pack). Each category has specific
labeling and packaging requirements that must be followed without exception.

**Regulatory Compliance:** We must adhere to the latest IATA (for air transport)
and DOT (for ground transport) regulations. This includes ensuring that battery-
powered equipment is securely packed to prevent accidental activation and
protect against short circuits during transit.

**Labeling and Documentation:** Every package containing lithium batteries must
be marked with the appropriate lithium battery warning label and include a
statement on the shipping manifest or invoice. Failure to provide this
information can lead to significant shipping delays, fines, and safety risks.

**Carrier Restrictions:** Some carriers have their own internal policies and
limitations on the quantity and power capacity of lithium batteries they will
accept, particularly for air services. It is essential to verify these
restrictions before selecting a shipping method for an order containing battery-
powered gear.
"""

