title = "Staffing and Scheduling"

content = """
Labor is a major cost; scheduling must align with traffic and task requirements. Understaffing
hurts service; overstaffing hurts margin.

**Traffic-Based Scheduling:** Use historical traffic patterns (by day, time, season) to
schedule. Peak hours get more coverage; slow periods get minimum coverage with flexibility.

**Task Allocation:** Balance selling time with non-selling tasks: replenishment, recovery,
receiving. Plan tasks for low-traffic periods when possible.

**Break Compliance:** Ensure breaks comply with labor law. Document and track. Avoid
scheduling gaps that leave the floor uncovered during peak times.

**Call-Offs and Swaps:** Have a process for call-off coverage. Encourage advance swaps.
Chronic call-off patterns should be addressed with management.
"""  # noqa: E501

