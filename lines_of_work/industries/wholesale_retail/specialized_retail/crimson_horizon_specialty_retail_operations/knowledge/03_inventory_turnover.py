title = "Inventory Turnover and Stock Levels"

content = """
Inventory turnover measures how often stock sells and is replaced within a period. Higher
turn typically indicates better alignment with demand and lower carrying cost.

**Target Turns:** Specialty retail targets vary by category. Apparel often aims for 4–6
turns annually; electronics and lifestyle goods may target 6–8. Use benchmark data and
internal history to set targets.

**Reorder Points:** Set reorder points based on lead time, safety stock, and sales velocity.
Reorder when on-hand plus in-transit falls below the point that ensures no stock-out before
the next delivery.

**Safety Stock:** Maintain buffer for demand variability and supply delays. Too little
causes stock-outs; too much ties up capital and increases markdown risk.

**Cycle Counts:** Conduct regular cycle counts to keep system accuracy high. Discrepancies
should be investigated and root causes addressed.
"""  # noqa: E501

