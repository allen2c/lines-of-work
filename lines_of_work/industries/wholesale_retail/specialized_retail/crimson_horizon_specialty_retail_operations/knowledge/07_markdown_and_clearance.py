title = "Markdown and Clearance Procedures"

content = """
Controlled markdowns protect margin while moving slow-moving inventory. Ad-hoc discounts
erode profitability and train customers to wait for sales.

**Markdown Calendar:** Follow the central markdown calendar. Initial markdowns are typically
20–30%; subsequent steps increase the discount. Timing aligns with season end or product
lifecycle.

**Clearance Zones:** Designate clearance areas with clear signage. Mix categories if needed
to create a "treasure hunt" feel. Keep these areas tidy to avoid a bargain-basement impression.

**Donation and Disposal:** For items that do not sell through clearance, follow company
policy for donation, liquidation, or disposal. Document and report per procedure.

**No Unauthorized Discounts:** Staff may not offer ad-hoc discounts without manager approval.
Exception handling must be logged.
"""  # noqa: E501

