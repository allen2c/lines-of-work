title = "Omnichannel Awareness"

content = """
Many customers research online and buy in-store, or vice versa. Staff should support a
seamless experience across channels.

**Inventory Visibility:** Know if items are available in-store, at other stores, or
online. Use systems to check and offer alternatives when out of stock.

**Order Fulfillment:** If the store fulfills online orders, follow pick-pack-ship
procedures. Accuracy and speed matter.

**Returns:** Accept online returns in-store when policy allows. Process per procedure;
do not treat them differently from in-store purchases.

**Consistent Messaging:** Brand and policy should be consistent across channels. Avoid
contradicting what the customer saw online.
"""  # noqa: E501

