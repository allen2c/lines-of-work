title = "Promotional Execution"

content = """
Promotions drive traffic and clear inventory. Proper execution ensures the promotion
delivers as intended without margin erosion.

**Setup Timing:** Promotional displays and signage should be in place before the promotion
starts. Price changes must be complete in POS before opening.

**Communication:** Staff must know promotion details: dates, eligibility, exclusions.
Train before the promotion begins so they can answer customer questions.

**Inventory Readiness:** Ensure adequate stock for promoted items. Under-stocking creates
frustration; over-stocking may require deeper markdowns later.

**Post-Promotion:** Remove promotional signage and revert prices when the promotion ends.
Evaluate sell-through and margin impact for future planning.
"""  # noqa: E501

