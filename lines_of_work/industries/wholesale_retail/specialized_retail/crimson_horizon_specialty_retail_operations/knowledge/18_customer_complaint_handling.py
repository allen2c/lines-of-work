title = "Customer Complaint Handling"

content = """
Complaints are opportunities to retain customers when handled well. A consistent approach
resolves issues and protects the brand.

**Listen First:** Let the customer finish. Do not interrupt or defend. Acknowledge
frustration before moving to solution.

**Empowerment:** Resolve within policy when possible. Manager approval for exceptions.
Document the resolution for consistency and learning.

**Fair Outcomes:** Replace defective product, refund when appropriate, offer gesture of
goodwill within limits. Do not over-compensate to avoid escalation; it sets bad precedent.

**Escalation:** If the customer insists on more than policy allows, or becomes abusive,
escalate to manager. Know when to involve higher authority.
"""  # noqa: E501

