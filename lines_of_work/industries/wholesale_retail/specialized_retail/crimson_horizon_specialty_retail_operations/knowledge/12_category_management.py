title = "Category Management Basics"

content = """
Category management treats product categories as business units. Data-driven decisions
improve assortment and performance.

**Category Roles:** Define categories as destination, routine, seasonal, or convenience.
Different roles drive different metrics: traffic, margin, inventory turns.

**Performance Metrics:** Track sales, margin, turnover, and sell-through by category.
Benchmark against plan and prior year. Identify winners and losers.

**Space Allocation:** Allocate space by category role and productivity. High-performing
categories earn more space; underperformers get reduced or replaced.

**Vendor Collaboration:** Work with key vendors on assortment and promotion. Share data
within confidentiality limits to improve mutual outcomes.
"""  # noqa: E501

