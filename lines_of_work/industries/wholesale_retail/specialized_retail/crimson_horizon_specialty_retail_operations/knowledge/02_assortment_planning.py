title = "Assortment Planning Principles"

content = """
Assortment planning determines what products we carry, in what quantities, and when. It directly
impacts sales, margin, and inventory health.

**Demand Alignment:** Plan assortments around historical sell-through, seasonality, and local
demographics. Use data from POS and loyalty programs to refine allocations by store.

**Margin Mix:** Balance high-margin items that drive profitability with traffic drivers that
build basket size. Avoid over-indexing on either; a healthy mix sustains both revenue and margin.

**Lifecycle Management:** Introduce new products with clear exit criteria. Markdown and
clearance should be planned, not reactive. Products that underperform get reduced or
discontinued on a schedule.

**Localization:** Allow store managers input on local favorites. Central planning sets the
frame; store input fine-tunes the mix.
"""  # noqa: E501

