title = "Cash Handling Procedures"

content = """
Cash handling errors create shrink and audit issues. Strict procedures protect both the
company and employees.

**Drawer Management:** Each transaction goes through assigned drawer. No shared drawers
without manager override. Starting fund is verified at shift start.

**Cash Drops:** Make drops when drawer exceeds threshold. Document drops; do not leave
excess cash in drawer.

**Deposits:** Prepare deposit with tape and documentation. Verify deposit matches sales.
Two-person verification when required by policy.

**Shortages and Overages:** Investigate and document. Patterns may indicate theft or
procedure gaps. Do not use personal funds to cover shortages.
"""  # noqa: E501

