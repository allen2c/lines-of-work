title = "Visual Merchandising Standards"

content = """
Visual merchandising creates the first impression and guides shopping behavior. Consistent
execution across stores reinforces brand and drives conversion.

**Zoning:** Place high-margin and impulse items at eye level and near checkout. Traffic
drivers belong in high-visibility areas. Complementary products should be adjacencies.

**Planogram Compliance:** Follow planograms for fixture layout. Deviations require approval
and should be documented. Planograms are built from data, not preference.

**Signage:** Use clear, legible signage for pricing and product information. Promotional
signage must be current; remove expired offers immediately.

**Presentation Quality:** Products should be faced, folded, or displayed according to brand
standards. Damaged or soiled goods must not appear on the floor.
"""  # noqa: E501

