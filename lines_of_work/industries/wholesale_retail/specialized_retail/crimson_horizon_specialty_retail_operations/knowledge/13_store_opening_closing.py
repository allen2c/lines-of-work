title = "Store Opening and Closing Procedures"

content = """
Consistent opening and closing routines ensure security, accuracy, and readiness. Deviations
create risk and operational gaps.

**Opening:** Verify alarm disarmed, lights on, and systems up. Complete cash drawer setup
and register checks. Walk the floor for cleanliness and readiness. Address any issues
before doors open.

**Closing:** Begin closing tasks before locked-door time. Count drawers and prepare deposit.
Reconcile sales and complete required reports. Secure high-value merchandise. Set alarm
and lock all exits. Document any incidents.

**Key Control:** Limit key access to authorized personnel. Log key sign-out and return.
Never leave keys unsecured.

**Alarm and Safe:** Know the alarm code and safe procedures. Report false alarms and
suspicious activity immediately.
"""  # noqa: E501

