title = "Receiving and Replenishment"

content = """
Accurate receiving ensures inventory records match physical stock. Replenishment keeps the
sales floor full and reduces lost sales.

**Receiving Protocol:** Verify shipment against purchase order and packing slip. Count
quantities and inspect for damage. Record variances and discrepancies. Sign only when
accurate.

**Putaway:** Store products in designated locations. Backstock should be organized for
efficient picking. Use locator system if available.

**Replenishment Triggers:** Replenish from backstock when on-floor quantity falls below
minimum. Prioritize high-velocity and promotional items. Replenishment is ongoing, not
scheduled.

**Vendor Returns:** Process defective or incorrect shipments per vendor return policy.
Document and ship within required window.
"""  # noqa: E501

