content = """
Retaining skilled workers reduces turnover cost
and maintains operational continuity at Indus
Bazaar.

**Competitive Compensation:** Ensure wages and
benefits are competitive. Review regularly.
Address compression and inequity. Consider
incentives tied to performance.

**Career Development:** Offer advancement
paths. Cross-train for variety. Support
skill development. Recognize growth.
Create internal promotion opportunities.

**Work Environment:** Safe, respectful
workplace. Adequate equipment and
facilities. Reasonable workload and
scheduling. Address concerns promptly.
Involve staff in improvements.

**Recognition:** Acknowledge contributions.
Celebrate achievements. Provide feedback.
Create positive culture. Avoid taking
people for granted.

**Exit Analysis:** Conduct stay and exit
interviews. Identify patterns. Address
root causes. Track turnover by role and
tenure. Measure impact of retention
efforts.
"""

