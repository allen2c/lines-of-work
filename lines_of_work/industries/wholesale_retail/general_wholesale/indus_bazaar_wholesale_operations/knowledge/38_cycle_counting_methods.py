title = "Cycle Counting Methods"

content = """
Cycle counting verifies inventory accuracy through ongoing counts
rather than full physical inventories. Different methods suit
different needs at Indus Bazaar.

**ABC Cycle Counting:** Count A items most frequently (e.g., monthly),
B less often (quarterly), C annually. Aligns effort with impact.
Requires ABC classification maintenance.

**Control Group Counting:** Count a small set of items repeatedly
to identify root causes of variance. Useful for process improvement
and training.

**Opportunistic Counting:** Count items when they are handled —
during picking, replenishment, or receiving. Low incremental cost.
May miss seldom-touched items.

**Random Sampling:** Select random locations or SKUs. Provides
statistical accuracy estimate. Less targeted than ABC.

**Process:** Document count procedures. Record variances. Investigate
root causes. Adjust records and processes. Track accuracy trends.
"""

