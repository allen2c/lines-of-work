content = """
Less-than-truckload (LTC) and full-truckload (FTL) serve
different shipment profiles. Choosing correctly optimizes
cost and service for Indus Bazaar.

**FTL:** One shipment or consolidation of multiple orders
fills a truck. Direct point-to-point. Lower cost per unit
for large shipments. Best for high volume, dedicated
shipments, or time-critical loads.

**LTL:** Shipment shares space with other freight. Carrier
consolidates at terminals. Pay for space used. Best for
smaller shipments, multiple destinations, or when full
truck is not economical.

**Break-Even:** Calculate when FTL becomes cheaper than LTL.
Depends on weight, distance, and rates. Consider handling
and transit time differences. Re-evaluate as volumes change.

**Hybrid:** Use both. Route and shipment size determine
choice. Pool smaller shipments for FTL when possible.
Negotiate with carriers for volume discounts.
"""

