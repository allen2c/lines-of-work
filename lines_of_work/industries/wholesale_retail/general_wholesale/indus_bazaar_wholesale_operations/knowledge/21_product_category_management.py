content = """
Product category management organizes inventory and operations by product
type at Indus Bazaar. Categories drive assortment, pricing, and handling.

**Category Definition:** Group products by type, supplier, or end use.
Examples: textiles, apparel, electronics, home goods. Define hierarchies
for reporting and planning.

**Assortment Planning:** Balance breadth and depth within categories.
Include fast movers and niche items. Align with customer demand and
supplier capabilities. Review and prune slow movers periodically.

**Handling Requirements:** Categories may have different storage, picking,
or shipping needs. Textiles require different handling than electronics.
Design processes and layouts accordingly.

**Supplier Alignment:** Some categories align with dedicated suppliers.
Leverage supplier expertise for assortment and promotion. Coordinate
new product introductions and discontinuations.

**Performance by Category:** Track sales, margin, turnover, and service
by category. Use insights for assortment and space allocation decisions.
"""

