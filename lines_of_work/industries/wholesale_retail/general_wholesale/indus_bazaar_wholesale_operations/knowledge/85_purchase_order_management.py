content = """
Purchase orders formalize procurement
commitments. Effective management ensures
accurate fulfillment for Indus Bazaar.

**PO Creation:** Authorized by policy.
Include supplier, items, quantities,
prices, dates, and terms. Transmit to
supplier. Obtain acknowledgement.
Maintain in system. Version control
for amendments.

**PO Receiving:** Match receipts to PO.
Quantity and quality verification.
Record variances. Partial receipts
where allowed. Close or cancel when
complete. Reconcile with invoices.

**Amendments:** Change orders for
quantity, date, or other terms.
Document and communicate. Update
systems. Maintain audit trail.
Obtain approval per policy.

**Tracking:** Monitor open POs.
Expected receipt dates. Aging and
overdue. Expedite or defer as needed.
Report to procurement and management.
Support cash flow planning.
"""

