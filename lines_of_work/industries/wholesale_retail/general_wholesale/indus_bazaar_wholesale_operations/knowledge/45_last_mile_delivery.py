title = "Last-Mile Delivery"

content = """
Last-mile delivery is the final leg to the customer. It often
represents a significant portion of cost and drives customer
experience for Indus Bazaar.

**Challenges:** High cost per stop. Time windows and access
restrictions. Failed deliveries and redelivery. Urban congestion
and parking. Customer availability.

**Options:** Own fleet, dedicated carrier, or crowdsourced delivery.
Parcel lockers and pickup points. Each has trade-offs in cost,
control, and flexibility.

**Time Windows:** Define and communicate delivery expectations.
Offer flexibility where feasible. Use tracking and notification
to reduce failed first attempts.

**Returns:** Plan for reverse logistics. Provide clear instructions
and labels. Consider pickup services for large or high-value
returns. Integrate with returns processing.

**Performance:** Track on-time delivery, damage, and customer
feedback. Work with carriers to improve. Have escalation and
redress processes.
"""

