title = "Vendor-Managed Inventory"

content = """
Vendor-managed inventory (VMI) shifts replenishment responsibility
to the supplier. Indus Bazaar may participate as customer or
supplier depending on role.

**As Customer:** Provide supplier with inventory visibility (often
via EDI or portal). Supplier monitors levels and ships to maintain
target. Reduces internal planning and ordering. Requires trust and
clear agreements.

**As Supplier:** Monitor customer inventory. Plan replenishment
shipments to maintain agreed levels. Requires access to data and
logistics capability. Can deepen relationship and lock in volume.

**Benefits:** Reduces stockouts, improves turnover, and can lower
total supply chain cost. Shifts some risk and responsibility.
Requires system integration and collaboration.

**Challenges:** Data accuracy, conflicting objectives, and
accountability for errors. Define roles, targets, and escalation
clearly. Monitor performance and adjust.

**Agreements:** Document targets, replenishment logic, ownership
of inventory, and liability. Review and revise periodically.
"""

