title = "Order Picking Strategies"

content = """
Order picking is the most labor-intensive activity in wholesale operations.
Selecting the right strategy improves accuracy, throughput, and cost.

**Discrete Picking:** One picker completes one order at a time. Best for
large orders, fragile items, or when order accuracy is paramount. Higher
travel time per unit picked.

**Batch Picking:** One picker collects multiple orders in a single pass.
Items are sorted to orders at a consolidation area. Reduces travel and
improves picker productivity. Requires robust sortation and verification.

**Zone Picking:** Divide the warehouse into zones; each picker works one zone.
Orders pass through zones via conveyor or cart. Enables parallel processing
and specialization. Requires coordination and handoff procedures.

**Wave Picking:** Release orders in waves by carrier, region, or priority.
Combines batch and zone concepts. Optimizes truck loading and shipment
consolidation. Common for daily cutoffs and same-day dispatch.

**Selection Criteria:** Order profile, product mix, facility layout, and
labor availability determine the best approach. Many operations use hybrid
or tiered strategies by order type.
"""

