content = """
Lot and serial tracking enables traceability for regulated, high-value,
or recall-prone products at Indus Bazaar.

**Lot Tracking:** Track inventory by production or receipt lot.
Enables recall, expiry management, and quality investigation.
Assign lot codes at receiving. Capture at all movements.

**Serial Tracking:** Track individual units by serial number.
Required for some electronics, machinery, or warranty management.
Capture serials at receiving and shipping. Maintain in system.

**FEFO and Expiry:** For perishables or dated products, use
first-expired-first-out (FEFO) logic. System should support
expiry dates and direct picking accordingly.

**Recall Process:** When a recall occurs, identify affected lots
or serials quickly. Quarantine inventory. Notify customers and
support retrieval. Document all actions.

**System Support:** WMS or ERP must support lot and serial
capture, storage, and inquiry. Integrate with receiving and
shipping workflows.
"""

