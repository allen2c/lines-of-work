title = "Emergency Preparedness"

content = """
Emergency preparedness protects people and assets during disruptive
events. Plan for natural disasters, fire, power outage, and security
incidents.

**Emergency Plan:** Document procedures for evacuation, shelter-in-place,
and communication. Designate roles and backups. Coordinate with local
authorities and emergency services. Conduct drills regularly.

**Business Continuity:** Identify critical processes and systems.
Plan for alternate sites, backup power, or workarounds. Define recovery
time objectives. Test recovery procedures periodically.

**Communication:** Establish notification chains for staff, management,
and key partners. Use multiple channels. Keep contact information current.

**Inventory Protection:** Assess risks to stored goods — fire, flood,
theft. Mitigate through building design, insurance, and security.
Document and photograph for claims.

**Supplier and Customer Communication:** Notify partners of disruptions
and expected recovery. Manage expectations. Prioritize critical orders
when capacity is limited.
"""

