title = "Consignment Inventory"

content = """
Consignment inventory remains owned by the supplier until used or
sold. Indus Bazaar may hold consignment stock to reduce carrying
cost and risk.

**Structure:** Supplier delivers goods; title transfers at sale
or use. Indus Bazaar provides storage and handling. Payment
occurs per agreement — at sale, at use, or periodically.

**Benefits:** Reduces buyer capital and obsolescence risk.
Supplier gains placement and sales opportunity. Can improve
availability and responsiveness.

**Challenges:** Coordination of ownership, insurance, and
damage liability. Inventory accuracy and reporting. Clear
agreements prevent disputes.

**Process:** Track consignment separately in systems.
Report usage or sales to supplier. Reconcile periodically.
Manage space allocation and replenishment triggers.

**Agreements:** Document ownership, payment terms, liability,
and reporting. Define audit rights and dispute resolution.
"""

