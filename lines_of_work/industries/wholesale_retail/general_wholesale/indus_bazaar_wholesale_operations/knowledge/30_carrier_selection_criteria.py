title = "Carrier Selection Criteria"

content = """
Carrier selection affects cost, speed, and reliability for Indus
Bazaar shipments. Match carriers to shipment profiles and needs.

**Service Level:** Transit time, frequency, and geographic coverage.
Some carriers specialize in regional routes; others in international.
Choose based on customer expectations and product characteristics.

**Capacity and Reliability:** Carrier ability to handle volume,
especially during peak. On-time performance and damage rates.
Track and use performance data in selection.

**Cost:** Compare rates by lane, weight, and volume. Consider
accessorial charges. Negotiate contracts for consistent volume.
Balance cost with service.

**Special Requirements:** Temperature-controlled, hazardous,
or high-value shipments need specialized carriers. Verify
capability and certification.

**Relationship Management:** Develop relationships with key
carriers. Communicate volume forecasts. Resolve issues
collaboratively. Maintain alternatives for backup.
"""

