title = "Drop Shipping"

content = """
Drop shipping allows Indus Bazaar to sell products that ship
directly from supplier to customer. No warehouse storage of
the product.

**Process:** Customer orders through Indus Bazaar. Order is
transmitted to supplier. Supplier ships to customer. Indus
Bazaar handles billing and customer service.

**Benefits:** No inventory carrying cost for drop-ship items.
Expanded assortment without space. Lower capital requirement.
Useful for long-tail or test products.

**Challenges:** Less control over fulfillment and quality.
Supplier reliability affects customer experience. Coordination
of order flow, tracking, and returns. Margin may be lower.

**Integration:** Systems must support order routing to suppliers,
tracking aggregation, and customer communication. Supplier must
provide ASN and tracking data. Handle partial orders and
backorders.

**Supplier Selection:** Evaluate supplier fulfillment capability,
accuracy, and communication. Monitor performance. Have
alternatives for critical items.
"""

