title = "Quality Control Procedures"

content = """
Quality control protects Indus Bazaar's reputation and reduces returns
and claims. Consistent procedures apply at receiving and before shipment.

**Incoming Inspection:** Verify product conformity to purchase specifications.
Sample or full inspection based on risk and lot size. Reject or quarantine
nonconforming goods. Document findings for supplier feedback and claims.

**In-Process Checks:** Monitor handling and storage conditions. Ensure
product integrity during picking and packing. Inspect packaging materials
and labels.

**Outbound Verification:** Confirm order accuracy before shipment. Weight
and count checks where applicable. Final visual inspection for damage.
Prevent incorrect or defective orders from reaching customers.

**Documentation:** Record inspection results. Retain samples when necessary.
Maintain traceability for recalls or disputes. File records per retention
requirements.

**Continuous Improvement:** Analyze defect patterns. Root-cause failures.
Work with suppliers and internal teams to reduce defects over time.
"""

