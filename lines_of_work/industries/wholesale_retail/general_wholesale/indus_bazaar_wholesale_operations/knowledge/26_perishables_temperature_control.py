title = "Perishables and Temperature Control"

content = """
Perishable products require temperature-controlled storage and
handling. Cold chain integrity protects product quality and safety.

**Temperature Zones:** Different products need different
temperatures — ambient, chilled, frozen. Designate zones
and monitor continuously. Alarm on deviation.

**Receiving:** Verify temperature upon receipt. Reject or
quarantine out-of-range shipments. Document temperatures.
Minimize time out of temperature during handling.

**Storage:** Maintain consistent temperature in storage.
Organize for FIFO or FEFO. Avoid blocking airflow. Plan
for power backup where critical.

**Picking and Shipping:** Minimize exposure during picking
and packing. Use insulated packaging and cold packs for
shipment. Coordinate with carriers for timely pickup.

**Documentation:** Record temperatures and hold times.
Maintain records for audits and traceability. Comply with
food safety or pharmaceutical regulations where applicable.
"""

