title = "Incoterms Overview"

content = """
Incoterms are international standards that define buyer and seller
responsibilities for delivery, cost, and risk. Correct use prevents
disputes for Indus Bazaar.

**Group E — EXW:** Seller makes goods available at their premises.
Buyer bears all cost and risk from pickup. Minimal seller obligation.

**Group F — FCA, FAS, FOB:** Seller delivers to carrier or port.
Risk transfers at specified point. Buyer arranges main carriage.

**Group C — CFR, CIF, CPT, CIP:** Seller pays freight to destination
but risk transfers earlier. Seller bears freight cost, not risk during
transit.

**Group D — DAP, DPU, DDP:** Seller delivers to destination. DDP
includes import duties; seller handles clearance. Maximum seller obligation.

**Selection:** Choose based on who controls logistics, who bears risk,
and commercial practice. Document clearly in contracts and invoices.
"""

