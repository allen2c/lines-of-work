title = "Demand Forecasting Techniques"

content = """
Demand forecasting supports inventory planning and procurement at Indus
Bazaar. Accurate forecasts reduce stockouts and excess inventory.

**Historical Analysis:** Use sales history to identify trends, seasonality,
and patterns. Simple methods include moving averages and exponential
smoothing. More sophisticated approaches use statistical models or
machine learning where data quality permits.

**Seasonality:** Many wholesale categories exhibit seasonal peaks — festivals,
back-to-school, and weather-related demand. Incorporate seasonal factors
into forecasts. Plan inventory and capacity for peak periods.

**Collaboration:** Gather input from sales, customers, and suppliers. Combine
quantitative forecasts with qualitative judgment. Document assumptions.

**Forecast Accuracy:** Track forecast error (e.g., MAPE, bias). Use accuracy
metrics to improve methods and calibrate safety stock. Review and adjust
forecasts regularly.

**Replenishment Link:** Connect forecasts to reorder points, order quantities,
and lead times. Ensure the planning system uses consistent demand signals.
"""

