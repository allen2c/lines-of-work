title = "Labeling Standards"

content = """
Consistent labeling enables identification, tracking, and
compliance throughout the supply chain for Indus Bazaar.

**Barcode Standards:** Use GS1 or equivalent standards for
product and logistics identifiers. GTIN for products;
SSCC for logistic units. Ensure scanner compatibility.

**Label Content:** Include human-readable and machine-readable
information. Product code, description, quantity, lot/serial,
expiry if applicable. Add handling and hazard symbols when
required.

**Label Placement:** Apply labels where they will be scanned
— typically on the outside of cartons or pallets. Avoid
obstruction by wrap or other labels. Ensure durability.

**Carrier Labels:** Apply carrier-specific labels for
shipments. Follow carrier format and placement rules.
Generate from integrated systems when possible.

**International:** Some markets require specific formats
or language. Verify destination requirements. Use
approved printers and materials.
"""

