title = "Variance Resolution"

content = """
Inventory variances occur when system records differ from physical
counts. Systematic resolution maintains accuracy and identifies
root causes at Indus Bazaar.

**Investigation:** When variance is found, investigate before
adjusting. Possible causes: receiving errors, picking errors,
theft, damage, system errors, or transaction timing. Review
transactions, video, and process adherence.

**Adjustment:** Once cause is understood, adjust records as
appropriate. Document reason codes. Follow approval workflows
for significant adjustments. Retain audit trail.

**Root Cause Prevention:** Address process or system gaps.
Improve training, procedures, or system controls. Share learnings
with relevant teams.

**Reporting:** Track variance rates, types, and trends. Report
to management. Use for performance evaluation and improvement
initiatives.

**Theft and Loss:** Significant or patterned variances may
indicate theft. Escalate to loss prevention or management.
Document and protect evidence.
"""

