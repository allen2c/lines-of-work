title = "Packing and Shipping Procedures"

content = """
Packing and shipping ensure orders leave Indus Bazaar intact and on time.
Consistent procedures protect product quality and support carrier handoffs.

**Packing Standards:** Use appropriate cartons, dunnage, and void fill.
Match packing to product fragility and shipment mode. Avoid over-packing
(waste) and under-packing (damage). Label all cartons clearly with
shipment and content identifiers.

**Labeling:** Apply carrier labels, shipping labels, and internal tracking
codes per carrier and destination requirements. Ensure labels are legible
and positioned for automated sortation where applicable.

**Weight and Dimension:** Capture weight and dimensions for freight billing
and manifest accuracy. Use scales and dimensioners for consistency.
Incorrect data causes billing disputes and delivery delays.

**Manifesting:** Generate packing lists, bills of lading, and electronic
manifests as required. Transmit data to carriers for pickup scheduling and
tracking. Retain copies for records.

**Handoff:** Stage shipments by carrier and route. Verify counts at handoff.
Obtain proof of pickup. Document any exceptions for claims or follow-up.
"""

