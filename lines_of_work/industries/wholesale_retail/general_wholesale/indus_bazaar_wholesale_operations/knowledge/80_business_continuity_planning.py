title = "Business Continuity Planning"

content = """
Business continuity planning prepares for
extended disruption. Ensures Indus Bazaar
can recover critical operations.

**Risk Assessment:** Identify threats —
natural disaster, fire, system failure,
supply disruption, pandemic. Assess
likelihood and impact. Prioritize
preparation. Update periodically.

**Critical Functions:** Identify what
must continue. Define recovery time
and point objectives. Map dependencies.
Plan alternatives — alternate site,
workarounds, manual procedures. Document
in plan.

**Recovery Plan:** Step-by-step procedures.
Roles and responsibilities. Contact lists.
Resource needs. Communication plans.
Test regularly. Update after tests and
actual events.

**Supply Chain:** Diversify suppliers and
carriers. Maintain backup relationships.
Understand single points of failure.
Plan for key supplier or customer
disruption. Coordinate with partners.
"""

