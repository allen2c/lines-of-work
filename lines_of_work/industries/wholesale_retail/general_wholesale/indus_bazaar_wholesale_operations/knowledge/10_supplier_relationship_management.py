content = """
Supplier relationships underpin Indus Bazaar's ability to deliver reliably.
Strong partnerships improve quality, lead time, and responsiveness.

**Selection Criteria:** Evaluate suppliers on quality, delivery performance,
pricing, payment terms, and responsiveness. Consider capacity for growth and
geographic proximity. Diversify critical suppliers to manage risk.

**Communication:** Maintain regular contact with key suppliers. Share
forecasts and demand signals. Discuss capacity, lead times, and potential
disruptions. Resolve issues promptly and professionally.

**Performance Tracking:** Monitor on-time delivery, quality acceptance
rates, and claim resolution. Provide feedback. Address persistent
shortfalls through escalation or supplier development.

**Contracts and Terms:** Document agreements on price, lead time, payment
terms, and quality requirements. Clarify liability for defects and
delays. Review and renew periodically.

**Collaboration:** Explore vendor-managed inventory, consignment, or
collaborative planning where mutually beneficial. Align incentives for
long-term partnership.
"""

