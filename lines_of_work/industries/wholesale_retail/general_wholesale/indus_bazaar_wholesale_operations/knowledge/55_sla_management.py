content = """
Service level agreements (SLAs) define expectations
with customers and suppliers. Managing to SLAs
maintains relationships and accountability for
Indus Bazaar.

**Internal SLAs:** Define targets for order
processing, picking, packing, and shipment.
Align with customer expectations. Measure
and report performance. Drive improvement
when missed.

**Customer SLAs:** Formal or informal agreements
on fill rate, delivery time, and accuracy.
Document clearly. Communicate proactively
when at risk. Track and report. Address
persistent shortfalls.

**Supplier SLAs:** Expect on-time delivery,
quality, and compliance from suppliers.
Include in contracts. Monitor and feed
back. Use in supplier evaluation and
selection.

**Carrier SLAs:** Delivery performance,
damage rates, and claims resolution.
Negotiate where possible. Track and
escalate. Consider penalties or
incentives in contracts.
"""

