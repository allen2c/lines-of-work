content = """
Trained staff are essential for operational excellence at Indus Bazaar.
Invest in onboarding, skill development, and retention.

**Onboarding:** New hires need orientation to safety, procedures, and
equipment. Assign mentors. Confirm understanding before independent
work. Document training completion.

**Procedure Training:** Ensure staff know receiving, putaway, picking,
packing, and shipping procedures. Include system use (WMS, scanners).
Update training when processes change.

**Equipment Certification:** Forklift and other equipment operators
must complete certified training. Refresher training per regulatory
or policy requirements. Document certifications.

**Safety Training:** Regular safety briefings and drills. Hazard
communication, emergency procedures, and PPE use. Report and investigate
incidents to prevent recurrence.

**Career Development:** Offer advancement paths and skill-building
opportunities. Recognize performance. Reduce turnover through engagement
and fair treatment.
"""

