content = """
Key performance indicators (KPIs) measure operational effectiveness at
Indus Bazaar. Consistent metrics support accountability and improvement.

**Order Fulfillment:** Order fill rate (percentage of orders shipped complete
and on time), line fill rate, and perfect order rate. Target levels depend
on customer expectations and competitive standards.

**Throughput:** Orders per hour, lines per hour, and units per labor hour.
Compare across shifts, teams, and periods. Identify best practices and
training needs.

**Accuracy:** Picking accuracy, receiving accuracy, and inventory record
accuracy. Track errors by type and root cause. Reduce defects through
process and system improvements.

**Cycle Time:** Time from order release to shipment. Break down by stage:
pick, pack, manifest, load. Address bottlenecks.

**Inventory:** Inventory turnover, days of supply, and carrying cost.
Balance service levels with capital and storage expense.

**Cost:** Cost per order, cost per line, and cost per unit shipped.
Benchmark against historical and industry norms.
"""

