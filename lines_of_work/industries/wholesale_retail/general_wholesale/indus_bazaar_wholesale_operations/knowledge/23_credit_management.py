title = "Credit Management"

content = """
Credit management balances sales opportunity with financial risk at
Indus Bazaar. B2B trade often relies on credit terms and trust.

**Credit Evaluation:** Assess new customers for creditworthiness.
Use references, financial data, and trade reports. Set initial
limits. Review periodically.

**Terms and Limits:** Define payment terms (net 30, net 60, etc.)
and credit limits by customer or segment. Document in agreements.
Apply consistently at order release.

**Collections:** Monitor aging. Send reminders before due date.
Escalate overdue accounts per policy. Balance relationship
preservation with cash flow needs.

**Holds and Blocks:** Place orders on hold when credit is
exceeded or payment is overdue. Communicate clearly. Release
upon payment or management approval.

**Dispute Resolution:** Address billing disputes promptly.
Document resolutions. Update processes to prevent recurrence.
"""

