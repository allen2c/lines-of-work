title = "Returns Management"

content = """
Returns management handles unwanted or defective goods returned by
customers. Effective processes recover value and maintain customer
relationships.

**Receiving Returns:** Inspect returned goods for condition and
completeness. Verify against return authorization or RMA. Document
reason codes. Quarantine for disposition decision.

**Disposition:** Decide restock, refurbish, liquidate, or scrap.
Restockable items return to inventory after inspection. Defective
items go to repair or supplier return. Obsolete or damaged items
may be liquidated or discarded.

**Inventory Impact:** Update inventory records promptly. Track return
rates by product and customer. Identify root causes — quality,
incorrect orders, or policy abuse.

**Supplier Returns:** Process supplier returns per agreements. Document
defects. Coordinate credits and replacement. Use returns data in
supplier performance reviews.

**Customer Communication:** Provide clear return policies and
instructions. Acknowledge returns and process credits or replacements
promptly. Manage expectations.
"""

