content = """
Receiving and putaway form the entry point for all goods at Indus Bazaar.
Accurate, efficient processes ensure inventory integrity and downstream
fulfillment quality.

**Receiving Protocol:** Verify inbound shipments against advance shipping
notices (ASN) and purchase orders. Inspect for damage, count discrepancies,
and correct SKU. Document variances before accepting liability. Use barcode
or RFID scanning where available to automate validation.

**Inspection Standards:** Visual inspection for damage, contamination, or
incorrect labeling. Sampling plans for large lots. Quarantine suspect goods
until disposition. Document all findings for supplier claims when applicable.

**Putaway Rules:** Direct items to storage locations based on velocity,
product characteristics, and slotting strategy. Fast movers near shipping;
bulk in reserve. Follow FIFO or FEFO for perishables. Assign locations via
WMS or equivalent system to maintain traceability.

**Documentation:** Complete receiving records, update inventory systems
promptly, and file bills of lading and packing lists. Retain documentation
for the periods required by local regulations and audit requirements.
"""

