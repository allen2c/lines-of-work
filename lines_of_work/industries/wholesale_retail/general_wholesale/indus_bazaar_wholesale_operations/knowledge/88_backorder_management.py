content = """
Backorders occur when demand exceeds
available inventory. Managing them
effectively balances customer
expectations and operations for
Indus Bazaar.

**Capture:** Record backordered
items and quantities. Link to
original order. Set expected
availability date. Communicate
to customer. Offer alternatives
when possible — substitution,
partial ship, or cancel.

**Fulfillment:** When stock arrives,
allocate to backorders. Priority
by order date, customer, or
policy. Release for picking.
Ship promptly. Update customer.
Close backorder in system.

**Communication:** Proactive
updates on status. Revise dates
when supply changes. Manage
expectations. Escalate when
extended. Document interactions.
Reduce frustration and cancellation.

**Metrics:** Track backorder
count, value, and age. Fill rate
for backorders. Customer
cancellation rate. Use for
inventory and procurement
improvement.
"""

