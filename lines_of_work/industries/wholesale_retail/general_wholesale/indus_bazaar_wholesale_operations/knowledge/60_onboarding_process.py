title = "Onboarding Process"

content = """
Effective onboarding integrates new hires
quickly and safely into Indus Bazaar
operations.

**Pre-Day One:** Prepare workspace,
equipment, and access. Assign mentor or
buddy. Share schedule and expectations.
Send welcome information.

**Orientation:** Company and site overview.
Safety orientation and compliance
requirements. Policies and procedures.
Benefits and HR essentials. Tour of
facility. Introduce key people.

**Role-Specific Training:** Procedures
and systems for the role. Shadow
experienced workers. Supervised practice.
Check-off before independent work.
Document completion.

**First Weeks:** Gradual increase in
independence. Regular check-ins with
supervisor and mentor. Address questions.
Gather feedback on onboarding experience.

**Evaluation:** Assess readiness at 30,
60, 90 days. Identify gaps. Improve
onboarding based on feedback and
retention data. Reduce time to
productivity.
"""

