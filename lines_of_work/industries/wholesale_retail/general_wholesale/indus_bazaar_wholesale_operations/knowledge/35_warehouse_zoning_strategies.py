content = """
Zoning organizes warehouse space by function or product
characteristics. Effective zoning improves flow and efficiency
at Indus Bazaar.

**Functional Zones:** Receiving, storage, picking, packing,
shipping, and returns. Each has distinct layout and equipment
needs. Design flow to minimize cross-traffic.

**Velocity Zones:** Fast movers in easily accessible locations.
Slow movers in bulk or reserve areas. Slot products by turnover
to reduce travel time.

**Product Zones:** Group by category, size, or handling needs.
Textiles, electronics, and hazmat may require separate areas.
Enables specialized processes and equipment.

**Temperature Zones:** Ambient, chilled, and frozen. Isolate
and monitor. Ensure proper handling at zone boundaries.

**Dynamic Zoning:** Re-slot periodically as demand changes.
Use slotting software or analysis to optimize. Balance
stability with responsiveness.
"""

