content = """
Quality control at Khlong Siam Merchants protects brand reputation and reduces
returns and claims. Procedures apply at receiving and before shipment.

**Inbound QC:** Inspect samples or full lots against specifications. Reject
or quarantine non-conforming goods. Document findings and communicate with
suppliers. Track rejection rates by supplier and category.

**Outbound QC:** Verify picked orders against documentation. Spot-check
packaging integrity. Ensure labels and expiry dates are correct. Random
audits support continuous improvement.

**Non-conformance handling:** Define disposition (return, rework, use-as-is
with customer approval). Root cause analysis for recurring issues.
"""

