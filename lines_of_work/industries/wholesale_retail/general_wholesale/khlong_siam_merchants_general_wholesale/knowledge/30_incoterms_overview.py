content = """
Incoterms (International Commercial Terms) define buyer and seller
obligations for delivery, risk, and cost. Khlong Siam Merchants uses
them in international contracts.

**Key terms:** EXW (seller's premises), FCA (carrier), CPT/CIP (carriage
paid/insured), DAP/DPU/DDP (destination). Each specifies who pays
transport, insurance, and customs.

**Selection:** Choose terms that match operational reality. FOB for
maritime shipments. DAP or DDP when seller delivers to buyer's door.
Consider who controls logistics and risk at each stage.

**Updates:** Incoterms are periodically revised. Use current edition
(2020) and specify in contracts. Train staff on implications.
"""

