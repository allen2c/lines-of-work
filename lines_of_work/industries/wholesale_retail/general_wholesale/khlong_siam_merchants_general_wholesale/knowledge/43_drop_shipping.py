content = """
Drop shipping allows Khlong Siam Merchants to fulfill orders without holding
stock. Supplier ships directly to the end customer.

**Process:** Customer orders from Khlong Siam. Khlong Siam transmits order
to supplier. Supplier ships to customer. Khlong Siam handles billing and
customer service. Inventory never touches Khlong Siam warehouse.

**Considerations:** No inventory cost for drop-ship items. But less control
over delivery and quality. Supplier must be reliable. Customer experience
must be seamless (branding, communication). Margin may be lower.
"""

