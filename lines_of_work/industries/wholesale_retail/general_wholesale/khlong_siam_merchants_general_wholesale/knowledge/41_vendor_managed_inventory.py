title = "Vendor Managed Inventory"

content = """
Vendor managed inventory (VMI) shifts replenishment responsibility to the
supplier. Khlong Siam Merchants may participate as buyer or, in some cases,
as the managing party for downstream customers.

**How it works:** Supplier monitors buyer's inventory (via data sharing or
site access). Supplier initiates replenishment to maintain target levels.
Reduces buyer's planning burden and can improve fill rates.

**Requirements:** Trust, data accuracy, clear targets, and aligned incentives.
Define roles, metrics, and escalation. System integration for visibility.

**Benefits:** Reduced stockouts, lower administrative cost, potential for
better inventory turns. Challenges include loss of control and data sharing.
"""

