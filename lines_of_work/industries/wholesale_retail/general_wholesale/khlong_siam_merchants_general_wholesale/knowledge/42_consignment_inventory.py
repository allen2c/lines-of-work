title = "Consignment Inventory"

content = """
Consignment inventory remains owned by the supplier until sold or used.
Khlong Siam Merchants may hold consignment stock for certain customers or
categories.

**Structure:** Supplier places goods at Khlong Siam or customer location.
Ownership transfers on sale, use, or after a period. Khlong Siam (or
customer) provides storage and handling; supplier retains risk.

**Tracking:** Must accurately track consignment quantities separate from
owned inventory. Reconcile periodically. Bill or report sales per agreement.

**Use cases:** New product trials, slow-moving specialty items, or
partnerships where supplier wants placement without immediate sale.
"""

