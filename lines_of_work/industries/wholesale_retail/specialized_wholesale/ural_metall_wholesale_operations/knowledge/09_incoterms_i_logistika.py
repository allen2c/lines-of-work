content = """
При отгрузке в регионы и страны ЕАЭС используются Incoterms для определения
ответственности за риски и расходы. «Урал Металл» чаще всего работает на условиях:

**EXW (Ex Works) — самовывоз:** Клиент забирает товар со склада в Екатеринбурге.
Все риски и расходы по погрузке и транспорту — на клиенте. Документы выдаются
на складе при отгрузке.

**FCA (Free Carrier) — до терминала перевозчика:** Компания доставляет до склада
или терминала в указанном городе. Погрузка на транспорт клиента или перевозчика
— по согласованию. Риски переходят в точке передачи.

**DAP (Delivered at Place) — доставка до места:** Товар доставляется до адреса
клиента без разгрузки. Разгрузка — обязанность клиента. Используется при
договорной доставке силами компании или субподрядчика.

При экспорте в ЕАЭС — учёт таможенных процедур и документации. Операции обязаны
знать базовые положения выбранного Incoterms и корректно отражать их в договоре
и накладных.
"""

