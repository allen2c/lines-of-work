content: str = """
A armazenagem de café cru exige controle de umidade, ventilação e proteção contra
pragas para preservar a qualidade até a expedição.

**Umidade:** O café deve ser armazenado com umidade entre 10% e 12%. Acima de
12%, há risco de fermentação, mofo e proliferação de fungos. Abaixo de 10%,
pode haver perda de peso e alteração de sabor.

**Ventilação:** O armazém deve ter circulação de ar adequada para evitar pontos
quentes e condensação. Pilhas não devem encostar diretamente em paredes.

**Organização por lotes:** Mantenha lotes separados por origem, tipo, bebida e
data de recebimento. Use etiquetas visíveis em cada pilha. O princípio FIFO
(first in, first out) reduz risco de perda por envelhecimento.

**Pragas:** Controle de roedores e insetos (broca, traça) é obrigatório.
Inspecione regularmente e aplique fumigação quando autorizada e registrada.
"""

