title: str = "Armazenagem de Açúcar — Procedimentos"

content: str = """
O açúcar cristal e VHP são higroscópicos: absorvem umidade do ambiente, o que
pode causar empedramento, perda de fluidez e deterioração.

**Condições ideais:** Umidade relativa do ar inferior a 60%. Temperatura estável,
evitando variações bruscas que favorecem condensação. Piso seco e impermeável.

**Empilhamento:** Sacos devem ser dispostos em paletes, sem contato direto com
o piso. Evite sobrecarga que cause deformação dos sacos e risco de ruptura.

**Segregação:** Mantenha lotes separados por tipo (cristal, VHP), fornecedor e
data de recebimento. Rastreabilidade é exigida por clientes industriais e
exportadores.

**Inspeção:** Verifique periodicamente sinais de umidade, empedramento ou
contaminação. Açúcar úmido deve ser segregado e reportado ao controle de
qualidade imediatamente.
"""

