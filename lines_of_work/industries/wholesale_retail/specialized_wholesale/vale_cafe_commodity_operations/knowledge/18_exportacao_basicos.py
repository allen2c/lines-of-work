content: str = """
A exportação de café e açúcar envolve documentação específica e conformidade
com exigências do comprador e do país de destino.

**Documentos comuns:** Comercial Invoice, Packing List, Bill of Lading (conhecimento
de embarque), Certificate of Origin, Certificate of Analysis (laudo), e
documentos sanitários quando exigidos (fitossanitário, sanitário).

**Incoterms:** FOB (Free on Board) e CIF (Cost, Insurance and Freight) são
frequentes. O vendedor FOB entrega no porto de embarque; o comprador assume
frete e risco a partir daí. CIF inclui frete e seguro até o porto de destino.

**Desembaraço:** O desembaraço aduaneiro de exportação é feito pela empresa
ou por despachante. A declaração de exportação (DU-E) e demais documentos
devem estar corretos para evitar retenção da carga.

**Prazo e documentação:** Compradores internacionais costumam pagar contra
apresentação de documentos (letter of credit ou D/P). Garanta que a documentação
esteja completa e em conformidade para evitar atrasos no pagamento.
"""

