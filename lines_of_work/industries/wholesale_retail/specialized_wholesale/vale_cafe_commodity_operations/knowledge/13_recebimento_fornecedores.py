content: str = """
O recebimento deve garantir que o produto entregue está conforme contrato e
em condições adequadas antes de ser aceito no estoque.

**Inspeção visual:** Verifique a integridade da carga. Sacos rasgados, úmidos
ou com sinais de pragas devem ser segregados e anotados. Caminhões inadequados
(limpos, cobertos) podem motivar recusa da carga.

**Documentação:** Confira a nota fiscal do fornecedor. Quantidade, produto e
lote devem bater com o esperado. Divergências devem ser anotadas e o fornecedor
contatado antes de finalizar o recebimento.

**Amostragem e análise:** Para café, realize amostragem conforme procedimento
e envie para classificação se a compra foi por tipo/bebida. Para açúcar,
solicite certificado de análise ou realize análise de polarização/ICUMSA.
Desvios significativos podem gerar devolução ou deságio.

**Registro:** Registre a entrada no sistema com lote, data, fornecedor e
resultado da inspeção. Arquive a NF e os laudos.
"""

