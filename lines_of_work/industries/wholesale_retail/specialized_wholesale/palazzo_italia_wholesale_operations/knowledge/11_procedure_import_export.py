title = "Procedure import-export"

content = """
Le operazioni con paesi extra-UE richiedono pratiche doganali e documentazione specifica.
Palazzo Italia gestisce sia import dai fornitori che export verso clienti esteri.

**Import:**
Le merci in ingresso dall'estero devono essere dichiarate in dogana. La dichiarazione
include classificazione tariffaria, valore, origine e eventuali dazi. L'IVA viene assolta
all'importazione o in regime di sospensione per merci in magazzino doganale.

**Export:**
Per le vendite verso paesi terzi è necessario compilare la dichiarazione di esportazione.
La merce deve uscire dal territorio UE; la prova di esportazione (es. EAD o equivalente)
consente l'operazione non imponibile.

**Spedizioni UE:**
Le vendite intracomunitarie non richiedono pratica doganale ma documentazione commerciale
completa e identificazione IVA del cliente.
"""

