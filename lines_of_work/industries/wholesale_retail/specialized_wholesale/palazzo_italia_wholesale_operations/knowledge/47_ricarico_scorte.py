content = """
Il replenishment consiste nel riordinare articoli quando lo stock scende sotto soglie
predefinite. Evita rotture di stock e garantisce disponibilità.

**Punti di riordino:**
Definiti per articolo in base a lead time di fornitura, domanda media e scorte di
sicurezza. Formula tipica: punto di riordino = domanda durante lead time + scorta sicurezza.

**Gestione:**
L'ERP può generare suggerimenti di acquisto. L'addetto acquisti verifica e conferma
agli ordini. Per articoli stagionali, il replenishment ha finestre limitate.
"""

