content = """
Gli Incoterms (International Commercial Terms) definiscono obblighi, costi e rischi tra
venditore e acquirente. Palazzo Italia deve applicarli correttamente nelle vendite internazionali.

**Termini comuni:**
- EXW (Ex Works): il cliente ritira a magazzino, sostiene tutti i costi e rischi
- FCA: venditore consegna al vettore nominato dal cliente
- DAP: venditore paga trasporto fino a destinazione, sgravio a bordo mezzo
- DDP: venditore paga tutto, inclusi dazi e tasse fino a destinazione

**Scelta e comunicazione:**
La condizione va indicata in offerta, ordine e fattura. Per DDP il prezzo deve includere
stima di dazi e IVA di destinazione. Errori possono generare contenziosi.
"""

