title = "Gestione ordini di acquisto"

content = """
Gli ordini di acquisto (OA) verso i fornitori devono essere tracciati dalla emissione
alla ricezione della merce.

**Emissione:**
L'OA riporta codice fornitore, articoli, quantità, prezzo, date di consegna e condizioni.
Va inviato al fornitore e registrato in ERP. La conferma del fornitore va archiviata.

**Stato e follow-up:**
Stati tipici: emesso, confermato, parzialmente ricevuto, ricevuto. Per ritardi vanno
effettuati solleciti. Le modifiche vanno concordate e documentate.

**Riconciliazione:**
A ricevimento completato, verificare che quantità e valori corrispondano. Eventuali
differenze vanno affrontate con il fornitore prima della chiusura contabile.
"""

