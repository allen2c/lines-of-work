content = """
फार्मास्युटिकल वितरण एक कार्यशील पूंजी (Working Capital) गहन व्यवसाय है।
आपूर्तिकर्ताओं को भुगतान करने और ग्राहकों से प्राप्ति के बीच के अंतर को बैंक
सुविधाओं से प्रबंधित किया जाता है।

**कार्यशील पूंजी की आवश्यकता:**
- ग्राहकों को 15-30 दिन क्रेडिट।
- आपूर्तिकर्ताओं को 30-45 दिन में भुगतान।
- स्टॉक का मूल्य: ₹1.5-2 करोड़।
- इस अंतर के लिए बैंक ऋण आवश्यक।

**बैंक सुविधाएँ:**

*Cash Credit (CC) Limit:*
- Stock और Debtors के आधार पर।
- ₹2 करोड़ CC Limit — SBI से।
- ब्याज दर: MCLR + Spread (~10-12% PA)।
- स्टॉक और Book Debts का मासिक विवरण बैंक को।

*Letter of Credit (LC):*
- बड़े आपूर्तिकर्ताओं के लिए।
- आयात LC: विदेशी खरीद के लिए।

*Term Loan:*
- वेयरहाउस विस्तार: ₹50 लाख।
- वाहन: HP (Hire Purchase)।
- Collateral: संपत्ति/FD।

**GST रिफंड:**
Export पर IGST रिफंड — GSTR-1 और GSTR-3B के बाद GST पोर्टल से।

**फाइनेंशियल मॉनिटरिंग:**
- मासिक P&L और Balance Sheet।
- Cash Flow Statement: साप्ताहिक।
- Debtors Aging Report: साप्ताहिक।
- CC Limit उपयोग: 80% से अधिक न हो।

**CA/Tax Advisor:**
- आरोहा फार्मा एक अनुभवी CA (Chartered Accountant) से सेवाएँ लेती है।
- Income Tax, TDS, GST — अनुपालन के लिए।
"""

