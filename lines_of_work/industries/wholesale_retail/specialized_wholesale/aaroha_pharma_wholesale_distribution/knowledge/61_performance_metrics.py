content = """
प्रदर्शन मेट्रिक्स (KPI — Key Performance Indicators) व्यावसायिक स्वास्थ्य का
वस्तुनिष्ठ मूल्यांकन करते हैं और सुधार के क्षेत्रों की पहचान करते हैं।

**वित्तीय KPI:**
- **मासिक बिक्री:** लक्ष्य बनाम वास्तविक (₹)।
- **सकल मार्जिन:** (बिक्री — COGS) / बिक्री × 100।
- **Order Fill Rate:** पूरे होने वाले ऑर्डर / कुल ऑर्डर × 100 (लक्ष्य: 95%+)।
- **Debtors Turnover:** बिक्री / औसत देनदार (उच्चतर = बेहतर)।
- **Days Sales Outstanding (DSO):** औसत संग्रह दिवस (लक्ष्य: <25 दिन)।

**परिचालन KPI:**
- **On-Time Delivery Rate:** समय पर डिलीवरी / कुल डिलीवरी (लक्ष्य: 92%+)।
- **Order Processing Time:** ऑर्डर से डिलीवरी तक (लक्ष्य: शहर में 4 घंटे)।
- **Inventory Turnover:** COGS / औसत इन्वेंटरी (उच्चतर = बेहतर, लक्ष्य: 8-12x/वर्ष)।
- **Stock-out Rate:** स्टॉक-आउट SKU / कुल SKU (लक्ष्य: <2%)।
- **Return Rate:** रिटर्न मूल्य / बिक्री मूल्य × 100 (लक्ष्य: <1%)।

**ग्राहक KPI:**
- **New Customers/Month:** नए ग्राहक (लक्ष्य: 10-15/माह)।
- **Customer Retention Rate:** सक्रिय ग्राहकों का प्रतिशत।
- **NPS (Net Promoter Score):** ग्राहक संतुष्टि सर्वेक्षण से।

**नियामक KPI:**
- **Zero Drug License Violations:** कोई उल्लंघन नहीं।
- **GST Filing Compliance:** समय पर फाइलिंग (100%)।
- **ADR Reports Filed:** रिपोर्ट की गई ADR संख्या।

**डैशबोर्ड:**
सभी KPI ERP के मासिक डैशबोर्ड में प्रबंधकों को उपलब्ध। विचलन पर तत्काल
चर्चा और सुधारात्मक कार्रवाई।
"""

