content = """
UP FDA और CDSCO अधिकारी किसी भी समय थोक वितरण परिसर का निरीक्षण कर सकते हैं।
हमेशा निरीक्षण के लिए तैयार रहने की संस्कृति व्यावसायिक सुरक्षा की गारंटी है।

**निरीक्षण में जाँची जाने वाली प्रमुख बातें:**

*लाइसेंस और दस्तावेज:*
- मूल Wholesale Drug License और नवीनतम नवीनीकरण।
- Qualified Person (Registered Pharmacist) की उपस्थिति और डिग्री।
- NDPS लाइसेंस (यदि लागू)।

*वेयरहाउस और भंडारण:*
- तापमान और आर्द्रता रिकॉर्ड।
- क्वारंटाइन क्षेत्र की उचित व्यवस्था।
- एक्सपायर्ड दवाओं का उचित पृथक्करण।
- कोल्ड स्टोरेज तापमान लॉग।

*खरीद और बिक्री रिकॉर्ड:*
- Form 20-B के तहत Purchase Register।
- Sales Register।
- Schedule H/H1 Sales Register।
- NDPS Register।

*कर्मचारी प्रशिक्षण:*
- प्रशिक्षण रिकॉर्ड।
- Pharmacist का उपस्थिति लॉग।

**निरीक्षण के दौरान व्यवहार:**
- शांत और सहयोगपूर्ण रहें।
- अनुरोधित दस्तावेज तुरंत प्रस्तुत करें।
- जो न जानते हों वह न बताएँ — "मैं जाँचकर बताता हूँ" कहें।
- किसी भी अनुचित माँग पर वरिष्ठ अधिकारी को सूचित करें।

**निरीक्षण के बाद:**
- निरीक्षण रिपोर्ट की प्रति प्राप्त करें।
- किसी कमी के लिए निर्धारित समय में CAPA लागू करें।
- CAPA रिपोर्ट FDA को भेजें।
"""

