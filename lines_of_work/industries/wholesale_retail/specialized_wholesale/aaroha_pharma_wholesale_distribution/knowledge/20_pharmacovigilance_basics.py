title = "फार्माकोविजिलेंस — प्रतिकूल दवा प्रतिक्रिया रिपोर्टिंग"

content = """
फार्माकोविजिलेंस (Pharmacovigilance) वह विज्ञान और गतिविधि है जिसके माध्यम से
दवाओं के प्रतिकूल प्रभावों (Adverse Drug Reactions — ADR) की पहचान, मूल्यांकन
और रोकथाम की जाती है। थोक वितरक के रूप में आरोहा फार्मा की इसमें भूमिका है।

**ADR रिपोर्टिंग की जिम्मेदारी:**
- ड्रग एंड कॉस्मेटिक्स एक्ट के तहत, दवा वितरक को संदिग्ध ADR की जानकारी मिलने
  पर निर्माता और CDSCO को सूचित करना अनिवार्य है।
- PVPI (Pharmacovigilance Programme of India) का राष्ट्रीय समन्वय केंद्र
  IPC (Indian Pharmacopoeia Commission), गाजियाबाद में है।

**ADR की पहचान:**
ग्राहक (केमिस्ट, डॉक्टर, मरीज) से रिपोर्ट प्राप्त होने पर:
1. घटना का विवरण लें: दवा का नाम, बैच नंबर, प्रतिक्रिया का प्रकार।
2. प्रतिक्रिया की गंभीरता: हल्की/मध्यम/गंभीर/जानलेवा।
3. मरीज की उम्र, लिंग, सहवर्ती दवाएँ।

**रिपोर्टिंग प्रक्रिया:**
1. Yellow Form भरें (PVPI का Suspected ADR Reporting Form)।
2. निर्माता को तुरंत (24 घंटे में गंभीर, 15 दिन में हल्की) सूचित करें।
3. CDSCO/PVPI पोर्टल पर ऑनलाइन रिपोर्ट करें।
4. आंतरिक रिकॉर्ड बनाएँ।

**वितरक की सीमित भूमिका:**
वितरक दवा की नैदानिक प्रभावशीलता पर टिप्पणी नहीं करते। ADR सूचना केवल
निर्माता और CDSCO तक पहुँचाना हमारी जिम्मेदारी है। चिकित्सीय निर्णय डॉक्टर
का अधिकार है।

**जागरूकता:**
ग्राहकों (केमिस्टों) को ADR रिपोर्टिंग के लिए प्रोत्साहित करें। इससे दवा सुरक्षा
में सुधार होता है।
"""

