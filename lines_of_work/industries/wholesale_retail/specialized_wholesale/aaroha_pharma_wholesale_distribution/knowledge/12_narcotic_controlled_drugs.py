content = """
नारकोटिक ड्रग्स एंड साइकोट्रॉपिक सब्सटेंस (NDPS) एक्ट 1985 के तहत नशीली और
मनोविकार दवाओं का वितरण अत्यंत नियंत्रित है। इन दवाओं के दुरुपयोग की उच्च
संभावना के कारण विशेष सावधानी अपरिहार्य है।

**NDPS लाइसेंस की शर्तें:**
- अलग NDPS वितरण लाइसेंस (राज्य सरकार से)।
- नामित "Responsible Person" — सक्षम प्राधिकरण द्वारा अनुमोदित।
- लाइसेंस नवीनीकरण: वार्षिक।

**प्रमुख नशीली दवाएँ (उदाहरण):**
- Morphine (दर्द प्रबंधन, कैंसर पीड़ित)।
- Codeine-based Cough Syrups।
- Diazepam, Alprazolam (चिंता निवारक)।
- Tramadol (दर्दनिवारक)।
- Buprenorphine (ओपियोइड लत उपचार)।

**रिकॉर्ड रखरखाव (अनिवार्य):**
- अलग NCB (Narcotic Control Bureau) रजिस्टर।
- प्रत्येक लेनदेन में: तारीख, ग्राहक का नाम, डॉक्टर का नाम, मात्रा, बैच नंबर।
- मासिक रिपोर्ट UP Drug Authority को।
- स्टॉक का भौतिक सत्यापन: तिमाही।

**आपूर्ति प्रतिबंध:**
- केवल अस्पताल/नर्सिंग होम/पंजीकृत चिकित्सक को आपूर्ति।
- खुदरा केमिस्ट को NDPS दवाएँ: केवल Retail NDPS License होने पर।
- प्रत्येक बिक्री के साथ नुस्खे की प्रमाणित प्रति फाइल करें।

**सुरक्षित भंडारण:**
- NDPS दवाएँ: लॉक्ड स्टील कैबिनेट में अलग से।
- चाबी: केवल Responsible Person के पास।
- सीसीटीवी निगरानी: 24x7।

**उल्लंघन के परिणाम:**
NDPS उल्लंघन कठोर दंड के अधीन है, जिसमें 10-20 वर्ष का कारावास और भारी जुर्माना
शामिल हो सकता है। किसी भी संदिग्ध गतिविधि की तुरंत पुलिस को सूचना दें।
"""

