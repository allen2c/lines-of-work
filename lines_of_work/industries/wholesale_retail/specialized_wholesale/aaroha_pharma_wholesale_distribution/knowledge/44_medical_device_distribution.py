content = """
चिकित्सा उपकरणों (Medical Devices) का बाजार भारत में तेजी से बढ़ रहा है। 2023 से
भारत में Medical Devices Rules 2017 के तहत उपकरणों का पंजीकरण अनिवार्य कर दिया
गया है।

**चिकित्सा उपकरण नियामक ढाँचा:**
- **Medical Devices Rules, 2017:** CDSCO द्वारा प्रशासित।
- **वर्गीकरण:** Class A (कम जोखिम) से Class D (उच्च जोखिम)।
- उपकरण वितरण के लिए Drug License की तरह Dealer's License।

**आरोहा फार्मा का MD पोर्टफोलियो:**
- Class A: BP Monitor, Thermometer, Pulse Oximeter, Glucometer।
- Class B: Surgical Instruments, Examination Gloves।
- Class C: Diagnostic Reagents (ELISA Kits)।
- सर्जिकल सप्लाई: Syringe, IV Set, Bandages, Gauze।

**भंडारण आवश्यकताएँ:**
- सर्जिकल उपकरण: सूखा, धूल-मुक्त वातानुकूलित क्षेत्र।
- Sterile उत्पाद: शीशा टूटने से बचाएँ।
- इलेक्ट्रॉनिक उपकरण: तापमान और आर्द्रता नियंत्रण।
- शेल्फ लाइफ: प्रत्येक उत्पाद अलग — लेबल जाँचें।

**CDSCO पंजीकरण:**
वितरित उत्पाद CDSCO पंजीकृत होने चाहिए। नया उत्पाद जोड़ने से पहले CDSCO
Registration Number जाँचें।

**Import Medical Devices:**
विदेशी उपकरणों के लिए CDSCO Import Certificate और BIS Certification (यदि
अनिवार्य हो)।

**Post-Market Surveillance:**
उपकरण में खराबी की रिपोर्ट CDSCO को Medical Device Adverse Event Reporting
System (MedWatch) के माध्यम से करें।
"""

