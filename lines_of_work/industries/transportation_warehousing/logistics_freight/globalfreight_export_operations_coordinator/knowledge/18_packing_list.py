title = "梱包明細書（Packing List）"

content = """
- 梱包明細書は、貨物の梱包内容を詳細に記載した書類。通関や検品に使用される。記載事項：荷主・荷受人情報、船積み情報（船名・B/L番号など）、各梱包のマーク・番号、品名、数量、重量（正味・総重量）、容積、梱包材の種類（木箱、段ボール、パレットなど）。
- 木製梱包材を使用する場合、IPPC（国際植物防疫条約）に基づく熱処理または燻蒸処理の証明（ISPM15）が必要。該当する場合は、処理マークを梱包材に表示し、証明書を添付する。
- 梱包明細書は、インボイスと整合している必要がある。品名、数量、重量が一致しないと、税関で指摘される。
- 複数の梱包がある場合、各梱包の内容を明確に記載する。特に、混載貨物の場合は、どの荷主の貨物がどの梱包に入っているか区別する。
- 梱包明細書のフォーマットは、顧客や通関業者の要求に応じて調整する。通常はExcelまたはPDFで作成する。
"""  # noqa: E501

