title = "### 緊急時対応マニュアル（自然災害・テロ・感染症）"

content = """
- **自然災害（地震・台風・豪雨）**：
  - **初動対応**：ドライバーの安全確認、車両の安全な場所への移動。
  - **運行中止**：災害発生時は**即時運行中止**し、最寄りの安全な場所へ避難。
  - **荷主への連絡**：遅延や中止の連絡を**30分以内**に実施。
  - **復旧手順**：災害収束後、道路状況を確認し、運行再開の可否を判断。
- **テロ・ハイジャック**：
  - **ドライバーへの指示**：車両を安全な場所に停車し、**110番**へ通報。
  - **社内報告**：テロ発生から**10分以内**に運行管理者へ報告。
  - **荷主への連絡**：積載物の安全確認と遅延の連絡。
- **感染症（COVID-19など）**：
  - **ドライバーの健康管理**：発熱や咳などの症状がある場合は**運行不可**。
  - **衛生対策**：マスク着用、手洗い・消毒の徹底。
  - **運行調整**：感染拡大時は**運行本数を削減**し、密を避ける。
"""  # noqa: E501

