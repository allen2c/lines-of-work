title = "High-Value Cargo"

content = """
Consignments of high declared value or sensitive nature (e.g. electronics,
precious goods) need extra care in assignment, routing, and communication.
Do not disclose value or nature beyond what is needed for operations.

**Assignment:** Use trusted drivers and, where policy requires, escorted or
tracked runs. Avoid unnecessary stops and use secure parking. Minimise the
number of people who know the consignment details.

**Documentation:** Waybill may show a generic description rather than full
detail. E-way and invoice may still reflect value for tax; ensure consistency
without advertising the cargo. At delivery, verify consignee identity
strictly.

**Incident:** Theft or loss of high-value cargo must be reported immediately
to operations and police. Preserve evidence and do not discuss liability
publicly. Insurance and claims will be handled by the designated team.
"""  # noqa: E501

