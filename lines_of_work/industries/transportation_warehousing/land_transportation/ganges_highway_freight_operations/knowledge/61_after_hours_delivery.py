content = """
Some customers require delivery outside normal business hours or on weekends.
Confirm with the consignee that they will accept at the requested time and
that the driver has the correct contact and gate instructions.

**Scheduling:** Book the delivery slot with the consignee. If they are not
available, do not send the vehicle; failed delivery and detention cost time
and money. Have a backup contact or reschedule procedure.

**Driver Safety:** Night or early-morning delivery in isolated areas may
pose safety concerns. Assess the location and, if needed, arrange for
security or daytime delivery. Do not compromise driver safety for
customer convenience.

**Charges:** After-hours or weekend delivery may attract extra charges as per
contract. Ensure the customer is aware and that the charge is applied in
billing.
"""  # noqa: E501

