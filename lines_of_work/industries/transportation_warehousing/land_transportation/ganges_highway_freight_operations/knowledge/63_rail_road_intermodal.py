title = "Rail-Road Intermodal"

content = """
Some long-haul movements use rail for the trunk leg and road for pickup and
delivery. Handover at the rail terminal must be documented so that responsibility
and condition are clear.

**At Rail Terminal:** When we deliver to the rail head or collect from it,
the waybill and condition should be verified and signed by the rail or
terminal operator. Retain the interchange receipt. Any damage or shortage
noted at interchange should be recorded and reported.

**Documents:** Rail may have its own consignment note or bill. Ensure our
waybill and the rail document are consistent. For the road leg, we need
the correct e-way bill and any permit for the origin and destination of
that leg.

**Timing:** Rail schedules may be less flexible than road. Plan pickup and
delivery at the terminal to match the train. Delays at the terminal can
affect the overall delivery commitment.
"""  # noqa: E501

