title = "Shortage or Discrepancy at Loading"

content = """
If the quantity or condition of goods at loading does not match the order or
waybill, the discrepancy must be recorded and the consignor and operations
informed before the vehicle leaves. Do not accept a load that is significantly
short or damaged without acknowledgment.

**Verification:** Count or weigh as per the loading protocol. If there is a
shortage, note it on the loading slip and get the loader or consignor to
acknowledge. Operations may contact the customer to confirm whether to
proceed with the short load or wait for correction.

**Damage at Loading:** If goods are already damaged at loading, record and
photograph. The consignor may replace the item or note the damage on the
waybill so that the consignee is not held responsible. Do not accept
responsibility for pre-load damage.

**Documentation:** All discrepancies must be on record. Failure to document
can lead to dispute at delivery and claims against the carrier.
"""  # noqa: E501

