title = "Vehicle Cleanliness and Cargo Compatibility"

content = """
The cargo compartment must be clean and suitable for the type of cargo. Food,
pharmaceutical, or sensitive goods may require a clean or sanitised load space.
Previous cargo must not contaminate the next load.

**Before Loading:** Inspect the vehicle for residues, odour, or damage. If the
last load was incompatible (e.g. chemicals before food), the vehicle may need
cleaning or approval before the next assignment. Document the pre-load condition
if the customer requires it.

**Compatibility:** Do not load incompatible goods together (e.g. food with
chemicals). For part-loads, ensure that consolidation does not create
contamination or reaction risk. When in doubt, check with operations or
refuse the mix.

**After Unloading:** Remove debris and sweep or wash as per policy. This
prepares the vehicle for the next trip and avoids claims of contamination.
"""  # noqa: E501

