title = "Contractor and Subcontracted Vehicle Standards"

content = """
When Ganges Highway uses subcontracted or owner-driver vehicles, they must meet the
same standards as owned fleet for documents, safety, and compliance. Failure to
enforce this exposes the company to risk.

**Document Check:** Before engaging a vehicle, verify registration, fitness,
insurance, and permit. Ensure the driver has a valid license and, if required,
dangerous goods or other training. Keep a record of the check.

**E-Way and Waybill:** The subcontracted driver must carry the correct e-way bill
and waybill and follow the same procedures at check-posts and delivery. Brief them
in a language they understand and confirm they will not bribe or evade checks.

**Liability:** Contract terms should clearly allocate responsibility for compliance,
damage, and claims. Operations should know whom to contact for the subcontracted
unit in case of incident.
"""  # noqa: E501

