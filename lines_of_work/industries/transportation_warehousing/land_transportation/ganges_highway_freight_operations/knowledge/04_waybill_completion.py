title = "Waybill Completion and Handover"

content = """
A waybill (or consignment note) is the primary record of the goods carried, the parties,
and the terms of carriage. Correct completion and handover protect the carrier and the
customer.

**Contents:** Typically include consignor and consignee details, description and quantity
of goods, place of loading and delivery, date, and condition of goods at handover.
Signatures or acknowledgements at loading and delivery provide evidence of receipt and
condition.

**Domestic vs CMR:** For domestic movement, a simple waybill format is often used. For
international road carriage (e.g. to Nepal or Bangladesh), the CMR consignment note may
apply; completion must follow CMR requirements to benefit from liability limits and
dispute resolution.

**Handover:** At delivery, the consignee or authorised person should sign the waybill.
Retain a copy for company records and any proof-of-delivery scan or photo as per
customer contract.
"""  # noqa: E501

