title = "Cargo Theft Prevention"

content = """
Cargo theft is a risk on long-haul routes. Basic precautions reduce the likelihood
and help recovery if theft occurs. Drivers and operations must be alert to suspicious
activity and follow secure parking and routing guidelines.

**Parking:** Use designated, well-lit parking or company-approved halt points when
stopping overnight. Avoid isolated stretches. Lock the cabin and secure the load;
use seals where applicable.

**Route:** Vary routes and timing where possible to avoid predictability. Share
movement details only with authorised persons. Report any tailing or unusual
interest in the vehicle to operations.

**Incident:** If theft or attempted theft occurs, contact police and operations
immediately. Preserve the scene and do not disturb evidence. Note vehicle number,
description of persons or vehicles involved, and location. Insurance and claims will
require a formal report.
"""  # noqa: E501

