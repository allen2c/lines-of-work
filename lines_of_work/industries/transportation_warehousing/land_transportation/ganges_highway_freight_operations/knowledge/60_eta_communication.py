title = "ETA Communication to Customer"

content = """
Customers expect to know when the consignment will arrive. Provide realistic
ETAs and update them when the situation changes. Avoid over-promising or
going silent when there is a delay.

**Initial ETA:** After dispatch, give an estimated time of arrival based on
distance, typical speed, and known check-posts. Build in a buffer for
queues and rest. State clearly if the ETA is approximate.

**Updates:** When the vehicle is delayed (breakdown, check-post, traffic),
revise the ETA and inform the customer. One proactive call is better than
the customer calling to ask. If the delay is significant, offer options (e.g.
next-day delivery) if possible.

**Delivery Window:** Some customers have a specific time window. Confirm the
window with the consignee and plan the driver's schedule so that they can
meet it. If we cannot meet the window, inform the customer in advance so
they can adjust.
"""  # noqa: E501

