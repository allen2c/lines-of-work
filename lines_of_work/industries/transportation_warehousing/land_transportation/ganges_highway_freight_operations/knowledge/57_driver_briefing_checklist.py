title = "Driver Briefing Checklist"

content = """
Before every trip, the driver should be briefed using a standard checklist so
that nothing is missed. Use Hindi or the driver's working language and confirm
understanding.

**Route and Stops:** Origin, destination, loading and delivery addresses, and
any intermediate stops. Mention known check-posts and weighbridges. Provide a
map or directions if the driver is not familiar with the route.

**Documents:** Confirm the driver has the trip folder with waybill, e-way bill,
permits, and contacts. Remind them to keep documents accessible and not to
lose or hand over originals except at delivery.

**Cargo and Handling:** Description of cargo, any special handling (e.g. keep
dry, no stacking), and securing requirement. For dangerous goods, confirm
training and permit. For high-value or sensitive cargo, remind them of
security and discretion.

**Contacts:** Operations number, and customer or agent contact for delivery.
Confirm the driver will call on departure and at key milestones and will
report any delay or incident immediately.
"""  # noqa: E501

