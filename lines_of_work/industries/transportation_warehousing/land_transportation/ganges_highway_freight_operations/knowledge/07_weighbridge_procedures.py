title = "Weighbridge Procedures"

content = """
Weighbridges are used to verify that vehicles comply with axle-load and gross vehicle
weight limits. Cooperation with weighbridge staff and correct procedure reduce delays
and avoid penalties.

**Approach:** Drivers should follow signs and queue in an orderly manner. Overloading
must be corrected before proceeding; offloading or redistribution may be required.

**Documentation:** Some weighbridges issue a weight slip or stamp the waybill. Retain
these for records and for resolving any later dispute about weight at check-posts.

**Avoiding Tampering:** Tampering with the vehicle or the weighbridge to show a lower
weight is illegal. Ganges Highway does not condone such practices and will treat them
as a serious breach of policy.
"""  # noqa: E501

