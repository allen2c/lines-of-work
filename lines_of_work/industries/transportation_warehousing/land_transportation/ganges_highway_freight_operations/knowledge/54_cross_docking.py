title = "Cross-Docking and Transhipment"

content = """
When cargo is transhipped from one vehicle to another (e.g. at a hub or
cross-dock), documentation and condition check must be done at the handover
point to avoid loss or dispute.

**Handover:** The receiving driver or warehouse should verify quantity and
condition against the waybill. Note any shortage or damage at the time of
transfer. Both parties sign the transfer sheet or waybill. The receiving
unit assumes responsibility from that point.

**Documents:** E-way bill may need to be updated or a new leg created when
the vehicle or consignment is split. Follow GST and company procedure. The
receiving vehicle must carry the correct documents for the next leg.

**Time:** Minimise dwell time at the cross-dock to avoid detention and
pilferage. Plan arrival and departure so that the receiving vehicle can
load and leave without undue delay.
"""  # noqa: E501

