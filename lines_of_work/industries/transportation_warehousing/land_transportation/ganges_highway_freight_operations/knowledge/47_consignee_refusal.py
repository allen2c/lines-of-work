title = "Consignee Refusal and Failed Delivery"

content = """
When the consignee refuses to accept the consignment or the delivery cannot be
completed (closed premises, wrong address, or dispute), the driver must not leave
the goods unattended. Follow company procedure for failed delivery.

**Documentation:** Record the reason for non-delivery, the time, and any
communication with the consignee or consignor. Get a written refusal or note
from the consignee if possible. Photograph the premises or situation if
relevant. Report to operations immediately.

**Disposition:** Operations will contact the consignor for instructions: hold
at a nearby location, return to origin, or redirect. Do not return or
redirect without explicit instruction. Secure the cargo until disposition is
decided.

**Cost:** Detention and return cost may be chargeable to the customer as per
contract. Log all time and cost for billing.
"""  # noqa: E501

