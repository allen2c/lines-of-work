content = """
Cargo that exceeds normal length, width, height, or weight limits requires
special permits and often escort vehicles. Do not dispatch such cargo without
valid permits and route clearance.

**Classification:** Over-dimensional (OD) or project cargo is classified by
dimension and weight. Permit type and cost vary by state and corridor. The
commercial or project team usually obtains the permit; operations must ensure
it is in hand and communicated to the driver before the trip.

**Route and Escort:** Permits may specify the route and timing. Escort vehicles
may be mandatory. The driver must follow the permitted route and not deviate
without authorisation.

**Documents:** The driver must carry the permit and any escort instructions.
At check-posts, present the permit when asked. Expired or incorrect permits
can lead to detention and penalty.
"""  # noqa: E501

