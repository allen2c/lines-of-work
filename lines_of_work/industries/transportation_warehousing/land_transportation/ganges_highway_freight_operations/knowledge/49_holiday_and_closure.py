title = "Holiday and Facility Closures"

content = """
Loading and delivery facilities may be closed on public holidays or for local
events. Check before committing to a pickup or delivery date. Plan routes
considering check-post and border holiday closures.

**Calendar:** Maintain a list of national and state holidays that affect
highways, borders, or customer facilities. Some customers have reduced hours
on the eve of holidays. Confirm with the customer when in doubt.

**Queue and Delay:** Around major holidays, traffic and check-post queues can
be longer. Build buffer into ETAs and inform customers if delay is likely.
Do not promise delivery on a day when the consignee is closed unless they
have confirmed receipt.

**Driver Leave:** Respect driver requests for leave on festivals. Plan
rosters in advance so that capacity is known and commitments are realistic.
"""  # noqa: E501

