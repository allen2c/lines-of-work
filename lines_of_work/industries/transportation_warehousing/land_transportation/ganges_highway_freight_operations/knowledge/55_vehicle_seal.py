title = "Seal Usage and Verification"

content = """
Seals are used to detect tampering with the load. When a seal is applied at
loading, the number is recorded; at delivery, the consignee verifies that the
seal is intact and matches. Broken or mismatched seals must be reported.

**Application:** Apply the seal according to the customer's or procedure's
requirement. Record the seal number on the waybill and, if required, on the
e-way or customer portal. The driver should not open the seal en route except
in an emergency (e.g. inspection by authority); if opened, record and report.

**Verification at Delivery:** The consignee checks the seal before unloading.
If the seal is broken or number does not match, they may refuse the consignment
or note a discrepancy. Document and report. Do not remove or replace the seal
without authorisation.
"""  # noqa: E501

