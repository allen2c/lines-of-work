title = "Pallet and Unit Load Standards"

content = """
Many consignments are palletised or unitised. Correct handling preserves the load
and ensures compatibility with loading docks and equipment at origin and destination.

**Pallet Type:** Standard pallets (e.g. 1200x1000 mm) are common. Verify that
the vehicle and loading equipment can handle the pallet size and weight. Overhang
or unstable stacking can cause collapse in transit.

**Securing:** Palletised loads should be strapped or wrapped and secured to
prevent movement. Use corner protectors and anti-slip mats where needed. Inspect
after loading and after any stop where the load might have shifted.

**Handling:** Use forklift or pallet jack as appropriate. Do not drag or drop
pallets. At delivery, ensure the consignee can unload with their equipment; if
not, agree on method and record any damage during unloading.
"""  # noqa: E501

