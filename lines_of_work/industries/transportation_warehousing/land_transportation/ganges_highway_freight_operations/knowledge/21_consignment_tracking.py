title = "Consignment Tracking"

content = """
Tracking allows operations and customers to know the status and location of a
consignment. Update systems at key milestones so that ETAs and exceptions are visible.

**Milestones:** Typical updates include: assigned, picked up, in transit (with
location or checkpoint), at border or weighbridge, out for delivery, delivered.
Custom requirements may add more checkpoints.

**Channel:** Drivers may report via phone, SMS, or app. Operations enters data into
the TMS or customer portal. Automated GPS or telematics can supplement manual updates.
Ensure updates are timely so that customer commitments are met.

**Exceptions:** Delays, breakdowns, or detentions should be logged with reason and
revised ETA. Customers appreciate proactive communication when something goes wrong.
"""  # noqa: E501

