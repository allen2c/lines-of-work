title = "Customer Priority and Commitments"

content = """
Some consignments have committed delivery dates or are marked high priority.
Dispatch and operations should allocate resources and communicate in a way that
honours these commitments where possible without compromising safety or compliance.

**Commitments:** Contractual or quoted delivery dates should be visible in the
booking. Plan the assignment so that the vehicle and driver can realistically meet
the date, allowing for rest, check-posts, and contingencies.

**Priority:** When capacity is tight, priority rules (e.g. committed date, customer
tier, or cargo type) may be applied. Follow the company's priority policy and
document why a consignment was delayed if it could not be served as requested.

**Communication:** If a commitment is at risk, inform the customer early with a
revised ETA and reason. Do not promise a new date that cannot be met.
"""  # noqa: E501

