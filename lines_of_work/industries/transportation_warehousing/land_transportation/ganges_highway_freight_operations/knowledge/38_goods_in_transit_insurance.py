title = "Goods-in-Transit Insurance"

content = """
Goods-in-transit insurance covers loss or damage to cargo during carriage. The
policy may be held by the carrier, the customer, or both. Operations should know
the scope and how to report a claim.

**Coverage:** Typically covers theft, accident, and specified perils. Exclusions
(e.g. inherent vice, improper packing) may apply. Ensure that the consignment
and handling are within the policy terms.

**Claim Reporting:** Report incidents promptly to operations and the designated
insurance contact. Provide waybill, POD, photos, and incident details. Late
reporting can void or reduce the claim.

**Liability:** Insurance does not automatically admit carrier liability. Liability
is determined by contract and law. The claims team will coordinate with the
insurer and the customer.
"""  # noqa: E501

