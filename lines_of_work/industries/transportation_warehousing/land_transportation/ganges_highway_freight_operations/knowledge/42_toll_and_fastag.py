title = "Toll and FASTag"

content = """
National and state highways often have toll plazas. FASTag and other electronic
payment methods reduce delay and improve tracking. Ensure vehicles are equipped
and accounts are funded.

**FASTag:** Link the tag to the correct vehicle and ensure the linked account
has sufficient balance for the planned route. Low balance can lead to rejection
at the plaza and manual payment and delay.

**Reconciliation:** Toll transactions are reconciled against trip and route.
Unexplained or duplicate charges should be reported to the toll operator and
internally. Misuse of tags or accounts is a disciplinary matter.

**Manual Toll:** Where electronic toll is not available, the driver may pay in
cash. Retain receipts for reimbursement and trip costing. Do not skip tolls;
evasion is illegal and can result in penalty.
"""  # noqa: E501

