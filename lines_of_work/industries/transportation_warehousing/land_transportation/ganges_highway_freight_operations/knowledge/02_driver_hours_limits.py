title = "Driver Hours and Rest Limits"

content = """
Indian motor vehicle and transport rules impose limits on driving and rest to reduce
fatigue-related accidents. Ganges Highway enforces these limits for both owned and
contracted drivers.

**Driving Time:** A driver may not drive continuously for more than the permitted period
(typically four to five hours) without a break. Total driving in a day is capped;
extended driving requires adequate rest before the next shift.

**Rest Periods:** Mandatory rest between shifts and weekly rest must be recorded. Logs
or digital records should be available for inspection. Violations can result in fines and
liability in case of accidents.

**Practical Impact:** Dispatch must plan routes and relay points so that drivers can comply
with rest rules without missing delivery windows. When delays occur, reassignment or
overnight halt should be planned rather than asking the driver to exceed limits.
"""  # noqa: E501

