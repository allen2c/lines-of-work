title = "Third-Party and Customer Agents"

content = """
We sometimes work with third-party logistics providers or customer-nominated
agents for clearance, warehousing, or last-mile. Coordination must be clear
so that handover and documents are correct.

**Scope:** Define who does what: e.g. we do line haul and hand over at the
border to the customer's clearing agent, or we deliver to a 3PL warehouse
that then delivers to the consignee. Document the handover point and
responsibility.

**Documents:** At handover to the agent or 3PL, ensure waybill and e-way are
exchanged or updated as required. Get a signed receipt or transfer note. Retain
for our records and for claim or audit.

**Communication:** Share only necessary information (consignment ID, vehicle
number, ETA, documents). Do not share other customers' data or our internal
costs. Escalate if the agent or 3PL requests something outside the agreed scope.
"""  # noqa: E501

