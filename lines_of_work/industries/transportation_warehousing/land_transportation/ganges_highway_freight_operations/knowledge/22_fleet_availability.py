title = "Fleet Availability"

content = """
Dispatch depends on accurate knowledge of which vehicles and drivers are available
and when. Maintenance, repairs, and prior assignments must be reflected in planning.

**Vehicle Status:** Track which trucks are in use, under repair, or idle. Allocate
only vehicles that are legally and physically fit for the trip. Avoid overcommitting
the same vehicle to overlapping trips.

**Driver Status:** Driver roster, rest periods, and leave affect availability. Respect
driver hours and rest rules; do not assign a driver who has not had sufficient rest
since the last run.

**Contracted Fleet:** When using owner-drivers or subcontracted trucks, confirm
availability and terms before promising a slot to the customer. Document the
assignment and ensure they meet company standards for documents and safety.
"""  # noqa: E501

