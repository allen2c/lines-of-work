title = "Customer Complaint Handling"

content = """
When a customer complains about delay, damage, or service, acknowledge promptly
and follow the company's complaint-handling procedure. Do not argue or promise
compensation without authority.

**Acknowledgment:** Confirm receipt of the complaint and give a reference
number or ticket ID. Set an expectation for when they will receive a
substantive response (e.g. within 24 or 48 hours). Escalate to the
designated person for complaint resolution.

**Investigation:** Gather facts: waybill, POD, driver report, and any photos.
Check whether the complaint is valid and what might have gone wrong. Document
findings. Do not admit liability or promise refund or compensation; that is
for the claims or commercial team.

**Closure:** Once the responsible team has decided the outcome, communicate
to the customer and close the ticket. If the complaint reveals a process
gap, feed it back for improvement.
"""  # noqa: E501

