title = "Types de Wagons Fret — Codification UIC"

content = """
L'Union internationale des chemins de fer (UIC) et l'ERA definissent des codes
standardises pour les wagons de fret. Chaque wagon porte une plaque avec son
numero d'immatriculation et des codes indiquant son type, sa charge utile, et
ses equipements.

**Codes courants:** E (wagen couvert), F (wagon plat), S (wagon special
citernes, etc.). Les numeros a 12 chiffres identifient de maniere unique chaque
wagon. La fiche wagon (fiche RIV ou accord bilateral) indique les caracteristiques
techniques et les restrictions d'usage.

**Implications operationnelles:** Le choix du wagon doit correspondre au type de
marchandise (vrac, conteneurs, citernes, marchandises dangereuses). Un mauvais
choix peut entrainer un refus de chargement ou une non-conformite RID. Vallee
Rhone Fret gere des pools de wagons et des locations aupres de VFL, Ermewa, GATX.
"""  # noqa: E501

