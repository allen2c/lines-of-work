title = "Transport Intermodal Conteneurise"

content = """
Le transport intermodal combine rail, route et parfois fluvial ou maritime.
Les conteneurs 20', 40', 45' sont charges sur wagons plats (type F) ou
wagons porte-conteneurs, transportes par train, puis decharges en terminal
pour reprise par camion ou navire.

**Avantages:** Efficacite energetique, reduction des emisions, decongestion
des axes routiers. Les operateurs comme Vallee Rhone Fret proposent des
services porte-a-porte en combinant rail et pre/post acheminement routier.

**Exigences:** Alignement des creneaux train/terminal/camion. Traçabilite des
conteneurs (numero, statut, position). Conformite aux normes de manutention et
de securite des chargements (arrimage).
"""  # noqa: E501

