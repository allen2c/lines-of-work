title = "Procedure en Cas de Deraillement"

content = """
Un deraillement est un evenement grave exigeant une reaction structuree.
L'objectif prioritaire est la securite des personnels et du public, puis la
progression de l'enquête et la reprise du trafic.

**Actions immediates:** Alerter les secours (pompiers, SAMU si blesses), le
gestionnaire d'infrastructure (SNCF Reseau), la police si necessaire, et la
direction des operations. Ne pas deplacer les wagons sans instruction. Isoler
la zone et empecher l'acces non autorise.

**Marchandises dangereuses:** Si des wagons RID/ADR sont impliques, appliquer
les consignes de la fiche d'urgence du RID. Informer les services specialises
(SPE, etc.) et respecter les perimetres de securite. Documenter l'ensemble
des faits pour l'enquête et les assureurs.
"""  # noqa: E501

