title = "Locomotives et Puissance de Traction Fret"

content = """
Les trains de fret necessitent une puissance de traction adaptee au poids du
convoi, au profil de la ligne, et aux reglementations. Les locomotives sont
louees ou possedees par les operateurs (Fret SNCF, Captrain, DB Cargo, etc.).

**Types:** Locomotives electriques (25 kV, 1,5 kV, 3 kV selon les pays),
diesels pour les lignes non electrifiees. Puissances typiques 4 à 6 MW pour
les convois lourds.

**Compatibilite:** La locomotive doit etre adaptee au courant disponible sur
le parcours. Les changements de systeme electrique (ex. frontieres) exigent
un changement de locomotive ou des machines multi-courant. La planification
inclut la disponibilite des engins aux points de relais.
"""  # noqa: E501

