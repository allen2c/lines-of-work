title = "Operations Portuaires Marseille-Fos"

content = """
Marseille-Fos est le premier port francais et une porte majeure pour le fret
mediterranean. Les trains de marchandises accedent aux terminaux portuaires
via les embranchements depuis la ligne Miramas-Fos et les installations
du Grand Port Maritime de Marseille (GPMM).

**Types de trafic:** Conteneurs (trafic majeur), vrac liquide et solide,
vehicules, ro-ro. Chaque quai et terminal a ses propres contraintes
d'emplacement, de grue, et de calendrier. Les trains doivent etre composes
selon l'ordre de dechargement demande par le terminal.

**Coordination:** Le GPMM et les gestionnaires de terminaux communiquent les
creneaux disponibles. Un retard d'arrivee du train peut decaler l'accostage
des navires et generer des surcoûts (demurrage). La ponctualite est donc
critique pour maintenir la fluidite portuaire.
"""  # noqa: E501

