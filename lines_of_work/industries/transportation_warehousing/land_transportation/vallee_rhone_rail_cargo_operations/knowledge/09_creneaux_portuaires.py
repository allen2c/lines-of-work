title = "Creneaux Portuaires et Contraintes Horaires"

content = """
Les ports et terminaux intermodaux fonctionnent avec des creneaux horaires
stricts. L'arrivee d'un train hors creneau peut entrainer un report au lendemain
ou des frais supplementaires d'ouverture de poste.

**Typologie des creneaux:** Creneaux jour (6h-18h), creneaux etendus (24h),
creneaux week-end et jours feries (souvent surcoûtes). Les terminaux
conteneurs imposent souvent des slots de 2 a 4 heures pendant lesquels le
train doit etre presente et traite.

**Planification:** La demande de sillon SNCF Reseau doit etre alignee sur le
creneau portuaire. En cas de retard en cours de route, une coordination
rapide avec le terminal permet parfois un report dans le meme jour ou une
reprogrammation minimisee. La communication proactive est essentielle.
"""  # noqa: E501

