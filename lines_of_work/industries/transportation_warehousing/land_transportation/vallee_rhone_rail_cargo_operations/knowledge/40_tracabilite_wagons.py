title = "Traçabilite des Wagons et Systemes d'Information"

content = """
La traçabilite des wagons repose sur des systemes d'information partages
(RIV, RailTrace, ERTMS, systemes prives). Chaque evenement (prise en charge,
passage en gare, livraison) est enregistre.

**Donnees:** Position, contenu, statut (en route, en gare, en maintenance),
date/heure des evenements. Les retards et incidents sont documentes pour
les clients et les assureurs.

**Obligations:** Fournir les donnees a jour, signaler les ecarts (wagon
bloque, perdu, endommage). Les rapports de positionnement sont utilises
pour la planification et la facturation. Une traçabilite defectueuse
degrada la confiance et peut generer des litiges.
"""  # noqa: E501

