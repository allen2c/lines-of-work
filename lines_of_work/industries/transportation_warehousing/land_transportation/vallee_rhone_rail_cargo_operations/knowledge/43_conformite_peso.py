"""Knowledge item: conformite Peso."""

title = "Conformite Poids et Gabarit (PESO)"

content = """
Les wagons et convois sont soumis a des limites de poids par essieu (PESO)
et de gabarit (profils). Le depassement peut endommager l'infrastructure et
compromettre la securite.

**Limites:** SNCF Reseau definit les charges maximales par ligne selon la
categorie de voie. Les wagons lourds (vrac, citernes) doivent etre repartis
pour respecter les limites par essieu. Le gabarit (largeur, hauteur) est
verifie aux points critiques (tunnels, ouvrages).

**Controle:** Pesee avant depart pour les chargements sensibles. Refus de
circulation si non-conformite. Les operations doivent connaitre les
contraintes des lignes empruntees et les communiquer aux chargeurs.
"""  # noqa: E501

