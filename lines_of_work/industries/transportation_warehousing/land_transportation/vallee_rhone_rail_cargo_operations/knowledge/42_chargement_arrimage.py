"""Knowledge item: chargement et arrimage."""

title = "Chargement et Arrimage des Marchandises"

content = """
Le chargement et l'arrimage des marchandises sur les wagons doivent repondre
aux normes de securite. Un chargement mal arrime peut se deplacer en cours
de route et provoquer un deraillement ou une chute.

**Normes:** Arrimage resistant aux accelerations longitudinales et
transversales. Utilisation de cales, sangles, et dispositifs conformes.
Verification avant le depart par le chargeur et le transporteur.

**Responsabilite:** Le chargeur est generalement responsable du chargement
conforme. Le transporteur peut refuser un wagon dont le chargement lui
parait douteux. En cas d'accident, une expertise determinera les
responsabilites. Les operations doivent documenter tout refus ou reserve.
"""  # noqa: E501

