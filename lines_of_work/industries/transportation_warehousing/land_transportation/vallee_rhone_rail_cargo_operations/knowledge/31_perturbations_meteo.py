title = "Perturbations Meteorologiques"

content = """
Les conditions meteorologiques extremes (neige, vent fort, canicule, crues)
peuvent affecter la circulation ferroviaire. SNCF Reseau impose des
restrictions de vitesse ou des interruptions en fonction des alertes.

**Neige et gel:** Risque de blocage des appareils de voie, de formation de
givre sur les catenaires. Vitesses reduites, voire suspension sur certains
troncons. Les trains de fret peuvent etre reroutes ou reportes.

**Vent:** Sur les zones exposees (viaducs, littoral), des limitations de vent
s'appliquent. Un depassement du seuil entraine l'arret des circulations jusqu'a
amelioration.

**Communication:** Les consignes de restriction sont diffusees par SNCF
Reseau. Vallee Rhone Fret adapte la planification et informe les clients
des retards probables.
"""  # noqa: E501

