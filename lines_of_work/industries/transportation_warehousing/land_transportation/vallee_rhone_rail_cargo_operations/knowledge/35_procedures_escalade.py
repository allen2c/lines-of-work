title = "Procedures d'Escalade"

content = """
Certaines situations depassent le perimetre des operations courantes et
exigent une escalation vers la direction, le juridique, ou les autorites.
Les procedures internes definissent les seuils et les circuits.

**Criteres:** Incident grave (deraillement, blessure, fuite matiere dangereuse),
litige contractuel majeur, pression mediatique ou politique, decision
strategique (refus de creneau, modification d'itineraire couteuse).

**Circuit:** Alerte du responsable operations, qui decide de l'escalade vers
la direction generale, le juridique, ou la communication. Un rapport incident
est redige et diffuse selon les regles. Les operations ne prennent pas de
decision engageant la responsabilite legale ou financiere sans validation.
"""  # noqa: E501

