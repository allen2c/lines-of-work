content = """
La demande de sillons suit un calendrier et des formats definis par SNCF Reseau
et l'ARAFER. Les operateurs deposent leurs demandes via les outils nationaux et
europeens.

**Calendrier Path Request:** Demande annuelle puis ajustements trimestriels et
mensuels. Les sillons confirmes sont bloques ; les modifications ulterieures
sont possibles mais soumises a disponibilite. En periode de travaux,
l'infrastructure peut proposer des itineraires de substitution.

**Coût des sillons:** Les redevances d'utilisation du reseau (RPU) sont
facturees par SNCF Reseau. Le montant depend du tonnage, de la distance, et
des segments empruntes. Une annulation ou une modification tardive peut
entrainer des frais. La planification optimise ces couts.
"""  # noqa: E501

