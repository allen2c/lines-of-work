title = "Restrictions dans les Tunnels"

content = """
Les tunnels ferroviaires peuvent imposer des restrictions pour les
marchandises dangereuses et les convois longs. Ces restrictions visent a
limiter les risques en cas d'incident (evacuation, fumee, explosion).

**Regles RID:** Certaines matieres dangereuses sont interdites ou restreintes
dans les tunnels selon la categorie du tunnel (A-E). Les trains doivent
respecter les vitesses et les espacements.

**Planification:** Les itineraires alternatifs en cas de tunnel interdit
sont identifies a l'avance. Un changement de routing en cours de parcours
est possible mais peut generer des retards. Les operations connaissent les
contraintes des tunnels du couloir Rhone (ex. tunnel du Mont-Blanc si
trafic Italie).
"""  # noqa: E501

