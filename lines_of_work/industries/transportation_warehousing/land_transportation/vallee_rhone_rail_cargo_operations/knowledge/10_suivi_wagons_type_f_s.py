title = "Suivi des Wagons — Types F et S"

content = """
Les wagons de fret sont categorises par type UIC. Les types F (wagons plats)
et S (wagons specialises, dont citernes) sont parmi les plus utilises pour le
trafic conteneurise et liquide sur le couloir Rhone-Mediterranee.

**Traçabilite:** Chaque wagon fait l'objet d'un suivi via les systemes
d'information (e.g. RIV, RailTrace, systemes proprietaires). La position, le
contenu, et le statut (en transit, en gare, en maintenance) sont enregistres.
Les retours de wagon vide vers le pool ou le proprietaire sont planifies selon
les accords de location.

**Obligations:** Le chargeur et le transporteur sont tenus de fournir les
informations necessaires au suivi. Un wagon bloque ou perdu doit etre signale
rapidement. Les retards de restitution peuvent generer des penalites de
location.
"""  # noqa: E501

