"""Knowledge item: handoff commercial."""

title = "Handoff vers le Commercial"

content = """
Les operations ne prennent pas de decisions commerciales : tarification,
nouvelles offres, negociations contractuelles. Ces sujets sont transferes
au commercial.

**Quand transférer:** Demande de devis, negociation de creneaux speciaux,
volumes nouveaux, modification de contrat. Les operations fournissent
la faisabilite technique et les contraintes (sillons, terminaux) mais
ne fixent pas les prix.

**Procedure:** Identifier le commercial en charge du client ou du segment.
Transmettre le contexte et les besoins. Le commercial reviendra vers les
operations pour valider la faisabilite avant de s'engager contractuellement.
"""  # noqa: E501

