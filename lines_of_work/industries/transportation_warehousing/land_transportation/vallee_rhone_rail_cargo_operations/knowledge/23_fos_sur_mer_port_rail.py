content = """
Fos-sur-Mer est la zone portuaire majeure du Grand Port Maritime de Marseille.
Les terminaux conteneurs, vrac et hydrocarbures sont connectes au reseau
ferroviaire par des embranchements dedies.

**Lignes d'acces:** Depuis Miramas et la ligne Lyon-Marseille, les trains
accedent aux differents bassins. Chaque terminal a ses propres voies,
signalisations et contraintes de manoeuvre. Les conducteurs doivent connaitre
les itineraires et les consignes de securite specifiques.

**Trafic:** Trains block conteneurs vers/depuis l'arriere-pays et
l'international. Trains vrac pour ciment, minerais, produits petroliers.
La priorite est donnee aux creneaux navires : un train en retard peut rater
le chargement ou le dechargement prevu.
"""  # noqa: E501

