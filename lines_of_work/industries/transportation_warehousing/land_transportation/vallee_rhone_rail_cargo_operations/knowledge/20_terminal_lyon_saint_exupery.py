title = "Terminal Intermodal Lyon Saint-Exupery"

content = """
Le terminal intermodal de Lyon Saint-Exupery (Sernam) est un point nodal pour
le trafic conteneurise entre le Nord, l'Est et le Sud de la France. Il dessert
l'aeroport et les zones logistiques proches.

**Capacites:** Traitement de conteneurs 20', 40', 45', reefer. Creneaux
d'arrivee et de depart coordonnes avec les autres flux (routier, avion). Les
trains en provenance ou a destination du port de Fos, de l'Alsace, ou de
Lyon-Vaise passent ou s'arretent selon les besoins.

**Coordination:** Vallee Rhone Fret planifie les circulations en fonction des
creneaux Sernam. Un retard peut decaler le dechargement et impacter les
clients. La communication quotidienne avec le gestionnaire du terminal est
essentiale.
"""  # noqa: E501

