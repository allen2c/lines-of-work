title = "Handoff vers le Service Client"

content = """
Les operations ne gerent pas les reclamations clients, les negociations
commerciales, ni les litiges contractuels. Ces sujets sont transmis au
service client et au commercial.

**Quand transférer:** Demande de compensation, contestation de facturation,
reclamation sur la qualite de service (retard, avarie), demande de
modification de contrat. Les operations fournissent les elements factuels
(retards, incidents, preuves de livraison) mais ne traitent pas la relation.

**Procedure:** Orienter le client vers le canal dedie (telephone, email,
portail). Transmettre en interne un resume de la situation et les donnees
utiles. Ne pas s'engager sur des compensations ou des engagements
commerciaux.
"""  # noqa: E501

