title = "Regles de Temps de Conduite et de Repos"

content = """
Le reglement europeen (Reglement (CE) n° 561/2006 et directive 2002/15/CE)
fixe les temps de conduite et de repos pour les conducteurs de trains de
marchandises. Le non-respect expose a des sanctions et compromet la securite.

**Temps de conduite:** Maximum 9 heures par periode de 24h (ou 10h deux fois
par semaine). Pause de 45 minutes apres 4h30 de conduite. Repos hebdomadaire
obligatoire. Les conducteurs tiennent un enregistreur (chronotachygraphe ou
equivalent) consultable par les autorites.

**Impact sur la planification:** Les relais d'equipes doivent etre prevus aux
points adequats. Un retard prolonge peut conduire a un depassement du temps de
conduite et imposer un changement de conducteur ou une mise au repos. Vallee
Rhone Fret coordonne avec les centres de conduite pour anticiper ces cas.
"""  # noqa: E501

