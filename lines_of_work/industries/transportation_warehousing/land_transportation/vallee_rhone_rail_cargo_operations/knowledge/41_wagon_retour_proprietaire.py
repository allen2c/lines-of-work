"""Knowledge item: wagon retour proprietaire."""

title = "Retour des Wagons au Proprietaire"

content = """
Les wagons loues ou empruntes aux pools doivent etre restitues selon les
delais contractuels. Un retard de restitution genere des penalites
(demurrage) et degrade la disponibilite du parc.

**Procedures:** Planifier le retour vide vers le depot ou le pool indique.
Respecter l'itinerarie et le creneau annonces. En cas d'impossibilite
(retard, incident), prevenir le proprietaire et negocier une extension si
possible.

**Documents:** Attestation de restitution, etat du wagon (propre, endommage).
Un wagon rendu en mauvais etat peut faire l'objet de facturation
supplementaire. La traçabilite de l'etat a la restitution est importante.
"""  # noqa: E501

