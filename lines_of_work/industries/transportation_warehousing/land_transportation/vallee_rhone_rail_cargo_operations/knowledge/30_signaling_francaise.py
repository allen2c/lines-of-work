title = "Signalisation Ferroviaire en France"

content = """
La signalisation ferroviaire francaise (SNCF Reseau) comprend plusieurs
systemes : block manuel, block automatique lumineux (BAL), block automatique
a permissivite restreinte (BAPR), et sur les LGV des systemes de controle
continu (ETCS niveau 2).

**Aspect des signaux:** Carre (arret), sifflet (ralentissement), vert (voie
libre). Les signaux d'entree et de sortie des gares, les limitrophes de
vitesse, et les signaux de protection sont a respecter strictement.

**Contexte fret:** Sur les lignes classiques, le fret partage les voies avec
les trains voyageurs. Les sillons sont attribues en fonction des priorites
et des capacites. Le conducteur doit reagir aux signaux et aux instructions
du poste de commande.
"""  # noqa: E501

