title = "Pools de Wagons Europeens"

content = """
Les wagons de fret sont souvent geres en pool : plusieurs societes partagent
un parc commun pour optimiser l'utilisation. En Europe, les pools couvrent les
wagons couverts, plats, citernes, et specialises.

**Fonctionnement:** Un wagon est attribue a une mission, circule, puis est
restitue au pool ou a un autre operateur selon les accords. Les societes
(Ermewa, VFL, GATX, etc.) assurent la maintenance et l'allocation. Les
penalites de retention (demurrage) sanctionnent les retours tardifs.

**Implications pour Vallee Rhone Fret:** La planification doit integrer les
delais de restitution. Un wagon vide bloqué en terminal ou sur une ligne
secondaire genere des coûts et degrade le taux de rotation. La coordination
avec les pools est quotidienne.
"""  # noqa: E501

