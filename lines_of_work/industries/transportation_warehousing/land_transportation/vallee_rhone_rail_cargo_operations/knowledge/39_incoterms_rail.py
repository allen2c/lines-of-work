title = "Incoterms et Responsabilites Rail"

content = """
Les Incoterms definissent les responsabilites entre vendeur et acheteur
(chargement, assurance, risque, douane). Pour le transport ferroviaire, les
plus courants sont FCA, CPT, CIP, DAP, DDP.

**Implications operations:** Selon l'Incoterm, le chargeur ou le destinataire
organise le pre/post acheminement. Les operations doivent savoir qui est
interlocuteur pour les creneaux, les documents, et les incidents.

**Exemple:** En CPT, le transporteur (rail) assure jusqu'a un point convenu ;
le risque passe au chargeur des la remise au premier transporteur. La
traçabilite et la preuve de livraison sont essentielles pour les litiges.
"""  # noqa: E501

