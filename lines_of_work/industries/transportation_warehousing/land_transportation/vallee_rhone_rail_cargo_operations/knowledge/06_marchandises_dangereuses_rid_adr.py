title = "Marchandises Dangereuses — RID et ADR"

content = """
Le transport des marchandises dangereuses par rail est reglemente par le RID
(ferroviaire) et l'ADR (routier, reference pour l'harmonisation). Les deux
textes reposent sur le systeme de classification ONU et les numeros UN.

**Classes de danger:** 9 classes (explosifs, gaz, liquides inflammables,
solides inflammables, matieres oxidantes, toxiques, radioactifs, corrosifs,
divers). Chaque matiere a un numero UN et des dispositions d'emballage,
d'etiquetage et de chargement specifiques. Les wagons citernes et wagons
specialises doivent etre homologues pour la classe concernee.

**Separation et placement:** Le RID impose des regles de separation entre
certaines classes et entre wagons dangereux et espaces occupes. Le placement
dans le convoi (distance des locomotives, position en queue) doit etre verifie
avant chaque circulation. Toute anomalie exige l'arret et la correction.
"""  # noqa: E501

