"""Knowledge item: transport combine."""

title = "Transport Combine Rail-Route"

content = """
Le transport combine associe le rail pour la longue distance et la route pour
le pre- et post-acheminement. Il reduit les emissions et la congestion
routiere tout en offrant une couverture geographique etendue.

**Organisation:** L'operateur ferroviaire coordonne avec les transporteurs
routiers pour les prises en charge et livraisons en gare ou en terminal.
Les creneaux camion doivent s'aligner sur les horaires des trains.

**Documents:** Une lettre de voiture unique (CMR ou CIM selon les segments)
ou des documents enchaine peuvent etre utilises. La traçabilite door-to-door
est assuree par l'operateur chef de file.
"""  # noqa: E501

