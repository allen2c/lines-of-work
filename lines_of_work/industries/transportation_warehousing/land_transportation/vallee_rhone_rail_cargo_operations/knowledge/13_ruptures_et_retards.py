title = "Ruptures de Convoi et Gestion des Retards"

content = """
Les ruptures de convoi (decrochage de wagons, probleme de frein) et les retards
causes par incidents d'infrastructure ou de material roulant exigent une reaction
rapide pour limiter l'impact sur la circulation et les clients.

**Rupture en ligne:** Le conducteur signale la rupture. Une locomotive de
secours ou un depannage peut etre requis. SNCF Reseau adapte le graphique.
Les trains en attente sur la ligne sont informes. Les clients destines et les
terminaux d'arrivee doivent etre prevenus du retard estime.

**Communication:** Un retard significatif (> 2h) declare en amont permet aux
terminaux de reprogrammer. Un retard non signale degrade la confiance et peut
entrainer des refus de creneau. La chaine d'information interne (dispatch,
commercial, clients) doit etre fluide.
"""  # noqa: E501

