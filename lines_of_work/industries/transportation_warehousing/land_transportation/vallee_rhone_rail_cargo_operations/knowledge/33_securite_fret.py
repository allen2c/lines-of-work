title = "Securite du Fret Ferroviaire"

content = """
La securite du fret implique la protection des biens, la prevention du vol et
du vandalisme, et la conformite aux reglementations (RID, CIM). Les sites
sensible (marchandises dangereuses, hautes valeurs) font l'objet de mesures
renforcees.

**Surveillance:** Cameras, gardiennage, clotures. Les acces aux terminaux et
embranchements sont controles. Les wagons immobilises en pleine voie sont
particulierement exposés.

**Coordination:** En cas d'incident de securite (vol, degradation), la police
et l'infrastructure sont alertees. Les assurances exigent une declaration
rapide. Vallee Rhone Fret participe aux procedures de securite partagees
avec SNCF Reseau et les terminaux.
"""  # noqa: E501

