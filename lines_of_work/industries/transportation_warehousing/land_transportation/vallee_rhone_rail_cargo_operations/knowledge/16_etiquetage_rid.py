title = "Etiquetage et Placage RID"

content = """
Les wagons transportant des marchandises dangereuses doivent porter des etiquettes
et plaques conformes au RID. Ces marquages informent les secours et les
personnels de l'identification du danger.

**Etiquettes de danger:** Nombre UN, nom de la matiere, etiquettes de classe
(de forme et couleur normalisees). Placage sur les cotes du wagon pour
visibilite maximale. Les grandes citernes peuvent exiger plusieurs etiquettes.

**Plaque orange:** Indique le numero UN (partie superieure) et le code
danger/matiere (partie inferieure). Doit etre lisible et correctement
positionnee. Un defaut d'etiquetage peut entrainer un refus de circulation
par les controles ou une non-conformite en cas d'accident.
"""  # noqa: E501

