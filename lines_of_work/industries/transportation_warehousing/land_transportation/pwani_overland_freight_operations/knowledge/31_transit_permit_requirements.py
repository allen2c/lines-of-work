"""Transit permit requirements for East African corridors."""

title = "Transit Permit Requirements"

content = """
Transit permits allow goods to move through a country without customs clearance for local
consumption. EAC member states have bilateral and multilateral transit arrangements.
Pwani Overland must ensure valid transit permits before dispatch on transit routes.

**When Required:** Transit cargo moving through Kenya, Tanzania, Uganda, Rwanda, or
Burundi to a final destination in another country requires a transit permit or bond.
The permit is typically obtained by the clearing agent or shipper before movement.

**Validity:** Permits have time limits and designated routes. Deviation or overstay
can result in penalties. Drivers must follow the route and present documents at each
checkpoint as required.

**Bond and Guarantee:** Transit often requires a customs bond or guarantee. The
carrier may be liable if goods are diverted or documents are not properly completed.
Ensure the bond is in force for the entire transit period.
"""  # noqa: E501

