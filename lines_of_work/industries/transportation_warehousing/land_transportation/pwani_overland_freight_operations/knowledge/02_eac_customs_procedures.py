content = """
The East African Community (EAC) customs union facilitates trade between Kenya, Tanzania,
Uganda, Rwanda, Burundi, and South Sudan. Pwani Overland must comply with EAC customs
procedures at all border crossings.

**Single Customs Territory:** Goods cleared at one EAC border may transit to another
member state with minimal formalities when proper transit documents are used. The
Electronic Cargo Tracking System (ECTS) monitors transit movements.

**Required Documents:** Commercial invoice, packing list, certificate of origin (where
applicable), and transport documents. Pre-clearance and Single Customs Document (SCD)
can reduce border dwell time when properly completed.

**Border Coordination:** Drivers must present documents at the designated customs office.
Delays occur when documents are incomplete or when physical inspection is required.
Plan for inspection time and ensure driver has agent or customer contact at the border.
"""  # noqa: E501

