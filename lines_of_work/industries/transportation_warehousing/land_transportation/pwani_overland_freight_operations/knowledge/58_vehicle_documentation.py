title = "Vehicle Documentation Requirements"

content = """
Each vehicle must carry valid documentation for the jurisdictions it operates in.
Missing or expired documents can lead to detention, fines, and delayed deliveries.

**Standard Documents:** Registration, insurance, roadworthiness certificate (where
required), and operator license. For cross-border, add transit permits, customs
documents, and any bilateral permits. Driver license and identity documents are
also required.

**Validity:** Check expiry dates before dispatch. Renewals should be scheduled
well in advance. Operations or fleet management typically maintains a calendar
for renewals; drivers should report any document that is about to expire.

**Inspection:** During checks, authorities may request originals. Keep documents
in a secure, accessible location in the cab. Copies may not be accepted for
certain documents.
"""  # noqa: E501

