title = "CMR Waybill Basics"

content = """
The CMR (Convention relative au contrat de transport international de marchandises par
route) is the international convention governing road freight across borders. Pwani
Overland uses CMR waybills for consignments moving between EAC member states and beyond.

**When Required:** CMR applies to international road transport where the place of
taking over and delivery are in different countries, at least one of which is a
contracting state. Kenya, Tanzania, and Uganda are parties; Rwanda and Burundi have
varying adoption.

**Contents:** The waybill must include sender and consignee details, place and date
of taking over, description of goods, and applicable charges. Incomplete or incorrect
waybills can cause border delays and liability disputes.

**Carrier Liability:** CMR defines limits of liability for loss, damage, or delay. The
carrier must preserve evidence (photos, inspection reports) when damage is discovered.
"""  # noqa: E501

