title = "Cargo Insurance Basics"

content = """
Cargo insurance covers loss or damage to goods in transit. The shipper or consignee
typically arranges insurance; the carrier may offer contingent liability or
supplemental cover. Understand the scope of cover and exclusions.

**Carrier Liability:** Under CMR and local law, the carrier has limited liability
unless loss or damage is due to wilful misconduct or gross negligence. Declared
value or special agreements may increase liability. Document conditions at pickup
and delivery to support or defend claims.

**Claims Process:** When damage or loss occurs, report promptly. Preserve evidence,
obtain written statements, and follow the insurer's claims procedure. Delayed
reporting can prejudice the claim.

**Exclusions:** War, strikes, inherent vice, and improper packaging may be excluded.
Verify what is covered before accepting high-value or sensitive cargo.
"""  # noqa: E501

