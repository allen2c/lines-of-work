title = "Customer Handover Procedures"

content = """
Delivery handover is the final step in the freight chain. A proper handover
protects both Pwani Overland and the customer and provides proof of delivery.

**Verification:** Confirm the consignee identity and that they are authorised
to receive. Check delivery address matches the waybill. Count or verify packages
where practical.

**Documentation:** Obtain a signed delivery note or proof of delivery. Note any
shortage, damage, or discrepancy. Retain copies for company records and potential
claims.

**Communication:** Report successful delivery to dispatch. If the consignee
refuses or is absent, follow company procedure for redelivery or return. Do not
leave cargo unattended without explicit authorisation.
"""  # noqa: E501

