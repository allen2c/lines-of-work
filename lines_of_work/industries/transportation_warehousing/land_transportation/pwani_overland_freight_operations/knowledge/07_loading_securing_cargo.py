title = "Loading and Securing Cargo"

content = """
Proper loading and securing prevent damage and accidents. Pwani Overland drivers
and loading staff must follow standard procedures for all consignments.

**Loading Sequence:** Heavy items at the bottom, fragile items protected. Distribute
weight evenly to avoid axle overload and instability. Use dunnage and blocking where
needed.

**Securing:** Straps, ropes, or chains must hold the load firmly. Check that nothing
can shift during braking or cornering. Re-secure after weighbridge or inspection
stops if the load was disturbed.

**Documentation:** Record any damage observed at loading. Photograph the loaded
vehicle before departure. Note special handling requirements (e.g. this side up) on
the waybill. Discrepancies between paperwork and actual load must be resolved before
dispatch.
"""  # noqa: E501

