"""Shortage and overage handling at delivery."""

title = "Shortage and Overage"

content = """
When the delivered quantity does not match the waybill or packing list, record
the discrepancy immediately. Shortage and overage affect claims and customer
relations.

**At Delivery:** Count or verify quantity with the consignee. If shortage or
overage is found, note it on the delivery receipt and obtain the consignee's
signature. Do not leave without documenting the variance.

**Causes:** Loading error, pilferage, damage, or documentation error. Investigation
may be needed. Preserve evidence: photos, statements, and records of the loading
and transit chain.

**Reporting:** Inform dispatch and the consignor promptly. They will decide
whether to file a claim, adjust documentation, or investigate further.
"""  # noqa: E501

