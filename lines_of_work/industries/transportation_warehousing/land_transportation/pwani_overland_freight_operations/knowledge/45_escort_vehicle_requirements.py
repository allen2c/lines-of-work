title = "Escort and Pilot Vehicle Requirements"

content = """
Oversized or overweight loads may require escort or pilot vehicles. Pwani Overland must
arrange these when permits specify such requirements.

**When Required:** Permits for abnormal loads typically state escort requirements.
Failure to provide escort can result in detention and fines.

**Escort Duties:** Escort vehicles warn other traffic and assist at narrow sections.
They must be equipped and driven by qualified personnel.

**Coordination:** Ensure escort is available at the start of the journey and at
handover points if the route crosses jurisdictions.
"""  # noqa: E501

