title = "Turkish Customs Entry Procedures"

content = """
Goods entering Turkey for import or transit must be declared at a customs office.
Import declarations require a customs broker unless the importer is registered.
Required documents typically include the commercial invoice, packing list, bill of
lading or CMR, certificate of origin if applicable, and any permits or licenses.
Customs duties and VAT are calculated based on the declared value and tariff
classification. Transit cargo requires a transit declaration or TIR carnet.
Preferential trade agreements (EU, EFTA, UK) may reduce or eliminate duties when
origin criteria are met. Delays can occur if documents are incomplete or if
physical inspections are ordered. Advance electronic submission speeds processing.
"""  # noqa: E501

