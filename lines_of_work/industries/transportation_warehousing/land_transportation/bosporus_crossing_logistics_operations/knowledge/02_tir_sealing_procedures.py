title = "TIR Sealing Procedures"

content = """
TIR sealing ensures that cargo compartments are secured and cannot be opened without
customs oversight. At the first customs office of departure, officers inspect the
load and apply official seals to the vehicle or container. These seals must remain
intact until the goods reach the customs office of destination or the final exit
point. If a seal is broken or damaged during transit, the driver must report
immediately to the nearest customs office. Tampering invalidates the TIR guarantee
and may result in duties and penalties. Bosporus Crossing drivers are trained to
verify seal integrity at each stop and to never remove seals without customs
authorization. Seal numbers must be recorded on the TIR carnet vouchers.
"""  # noqa: E501

