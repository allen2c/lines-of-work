title = "Hazardous Materials (HAZMAT) Handling"

content = """
The transportation of hazardous materials (HAZMAT) by rail is subject to strict
federal regulations to ensure the safety of crews, the public, and the
environment. Dispatchers play a vital role in monitoring and managing HAZMAT
shipments across the network.

**Documentation and Placarding:** Every HAZMAT shipment must be accompanied by
detailed shipping papers, including the proper shipping name, UN number, and
hazard class. Railcars must be appropriately placarded to indicate the nature of
the hazard (e.g., flammable, corrosive, toxic).

**Train Placement Rules:** Federal law dictates the placement of HAZMAT cars
within a train consist. For example, certain hazardous cars must be separated
from locomotives, occupied cabooses, and other incompatible hazardous materials
by a specific number of "buffer cars." Dispatchers must verify that every
train's consist complies with these "cover" requirements before authorizing
movement.

**Emergency Response:** In the event of a derailment or leak involving HAZMAT,
dispatchers must immediately notify emergency responders and provide accurate
information from the train's manifest. They coordinate the isolation of the
affected area and assist in the implementation of the railroad's emergency
response plan.
"""

