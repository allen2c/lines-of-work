title = "Train Consist Building and Weight Distribution"

content = """
A train consist is the complete list of locomotives and railcars that make up a
train. Building an optimal consist requires careful consideration of car types,
cargo, weight distribution, and destination sequencing.

**Weight Distribution and Safety:** Proper weight distribution is critical for
preventing derailments, especially on steep grades and sharp curves. Heavy cars
should generally be placed at the front of the train, with lighter cars toward
the rear. Dispatchers and yardmasters must verify that every consist complies
with the railroad's "train makeup" rules, which specify the maximum allowable
weight and length for various train types.

**Block Building:** Cars are organized into "blocks" based on their destination
and priority. Efficient block building minimizes the need for intermediate
switching at yards, reducing dwell time and improving overall network speed.
Dispatchers coordinate with yardmasters to ensure that trains are built in the
correct "block order" to facilitate efficient set-outs and pickups along the
route.

**Special Handling Requirements:** Certain cars require special handling, such
as HAZMAT shipments, oversized loads, or cars with mechanical restrictions.
Dispatchers must ensure that these cars are properly placed within the consist
and that the crew is aware of any special instructions or speed restrictions.
"""

