content = """
Positive Train Control (PTC) is a federally mandated safety technology designed
to prevent train-to-train collisions, over-speed derailments, and unauthorized
movements into work zones. It acts as a digital safety net that can
automatically apply brakes if a crew fails to respond to a restrictive signal.

**Core Components:** PTC relies on a combination of GPS, onboard computers,
wayside interface units, and a robust data communication network. The system
constantly monitors the train's location, speed, and the status of upcoming
signals and switches.

**Safety Functions:** PTC enforces temporary speed restrictions, protects
roadway workers, and prevents movements through improperly aligned switches. If
the system detects a potential violation, it issues a warning to the crew. If no
action is taken, PTC initiates a full service or emergency brake application to
bring the train to a safe stop.

**Dispatch Integration:** Dispatchers must ensure that PTC-equipped locomotives
are properly initialized and that all track data is current. Any failures in the
PTC system must be reported immediately, as they may require reduced speeds or
other operational restrictions under federal law.
"""

