title = "Railroad Yard Switching and Classification"

content = """
Railroad yards are the "sorting centers" of the rail network, where incoming
trains are disassembled and outgoing trains are built. Switching and
classification are the core processes used to organize railcars by destination
and priority.

**Classification Methods:** In a "hump yard," cars are pushed over a small hill
(the hump) and allowed to roll by gravity into various classification tracks.
In a "flat yard," locomotives move cars back and forth to sort them. Dispatchers
coordinate with yardmasters to ensure that cars are classified efficiently and
that "consists" (train lists) are built according to the network's operating
plan.

**Switching Operations:** Switching involves moving cars between tracks to build
trains, serve local industries, or move bad-order cars to repair tracks.
Dispatchers must manage yard congestion and ensure that switching movements do
not interfere with the arrival or departure of through-trains.

**Dwell Time and Throughput:** Yard dwell time—the average time a car spends in
a yard—is a critical metric for network efficiency. Dispatchers monitor yard
levels and adjust train schedules to prevent bottlenecks, ensuring a steady
throughput of cars and maximizing the utilization of yard assets.
"""

