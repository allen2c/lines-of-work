content = """
The air brake system is the primary means of controlling the speed and stopping
of a train. Dispatchers must understand the fundamentals of air brake operation
and the requirements for air brake tests to ensure safe train movement.

**Air Brake Fundamentals:** The air brake system uses compressed air to apply
brake shoes to the wheels of locomotives and railcars. The system is designed to
be "fail-safe," meaning that a loss of air pressure will result in an automatic
brake application. Dispatchers must be aware of the impact of train length and
weight on braking performance, especially on steep grades.

**Air Brake Tests:** Federal regulations require that air brake tests be
conducted at various points in a train's journey, such as when a train is
initially built, when cars are added or removed, or when a locomotive is
changed. These tests ensure that the air brake system is functioning properly
and that all cars have effective brakes. Dispatchers must coordinate with crews
and yardmasters to ensure that all required tests are completed before
authorizing movement.

**Dynamic Braking:** Most modern locomotives are equipped with dynamic braking,
which uses the locomotive's traction motors as generators to provide braking
force. Dynamic braking is particularly effective on long, steep grades, as it
reduces the wear on air brake shoes and helps maintain a steady speed.
Dispatchers must ensure that crews are utilizing dynamic braking effectively to
optimize train handling and safety.
"""

