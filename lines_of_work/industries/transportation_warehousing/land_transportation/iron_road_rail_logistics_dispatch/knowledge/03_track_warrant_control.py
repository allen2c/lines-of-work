title = "Track Warrant Control (TWC) Procedures"

content = """
Track Warrant Control (TWC) is a method of authorizing train movements on lines
not equipped with CTC or other signal systems. It relies on verbal or written
authorities issued by a dispatcher to a train crew, specifying the limits and
conditions of their movement.

**Issuing Warrants:** Dispatchers issue track warrants via radio or telephone.
The crew must record the warrant on a standard form and repeat it back to the
dispatcher to ensure accuracy. The dispatcher then confirms the warrant with a
unique number and a time of issuance.

**Limits and Authorities:** A track warrant defines specific geographic limits,
such as mileposts or station names. It may grant authority to "proceed" from
one point to another, "work between" two points, or "occupy" a specific track
for a set duration. Only one train can hold authority for a specific block at a
time, unless "joint authority" is explicitly granted.

**Reporting Clear:** Once a train has exited the limits of a track warrant, the
crew must "report clear" to the dispatcher. This releases the track for use by
other trains. Dispatchers must maintain a rigorous log of all active and
released warrants to prevent overlapping authorities and ensure network safety.
"""

