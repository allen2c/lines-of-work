title = "Intermodal Drayage and Last-Mile Logistics"

content = """
Drayage is the transport of freight over a short distance, typically between an
intermodal terminal and a customer's facility or another transport hub. It is a
critical component of the "last-mile" logistics process.

**Drayage Providers and Coordination:** Railroads coordinate with a network of
independent drayage providers to move containers and trailers between terminals
and customers. Dispatchers monitor the arrival of intermodal trains and
coordinate with drayage providers to ensure that containers are picked up and
delivered on schedule. Efficient drayage operations are essential for
maintaining terminal throughput and meeting customer expectations.

**Gate Operations and Truck Turn Time:** Intermodal terminals use "gate"
systems to manage the entry and exit of drayage trucks. Dispatchers monitor gate
activity and truck "turn time"—the time a truck spends inside the terminal.
Long turn times can lead to congestion at the gates and delays in freight
delivery. Dispatchers work with terminal managers to optimize gate operations
and minimize turn times.

**Intermodal Equipment Management:** Drayage also involves the management of
intermodal equipment, such as chassis and trailers. Dispatchers must ensure
that sufficient equipment is available at the terminal to meet the needs of
drayage providers. They also coordinate the repair and maintenance of this
equipment to ensure safe and reliable operation.
"""

