title = "Hours of Service (HOS) Regulations"

content = """
Hours of Service (HOS) regulations are federal laws designed to prevent
fatigue-related accidents by limiting the number of hours rail crews can work.
Dispatchers are responsible for monitoring crew on-duty times and ensuring that
relief crews are staged to prevent violations.

**On-Duty Limits:** For most freight crews, the maximum on-duty period is 12
consecutive hours. Once this limit is reached, the crew must be relieved and
provided with a minimum of 10 hours of undisturbed rest. Dispatchers must track
the "time on duty" for every crew in their territory and plan for relief well
in advance of the 12-hour mark.

**Deadheading and Relief:** If a train cannot reach its destination before the
crew's HOS expires, the dispatcher must arrange for a "relief crew" to be
transported to the train's location. The original crew then "deadheads" (is
transported) to their home terminal or a rest facility. Deadhead time counts
toward the total on-duty period.

**Operational Impact:** HOS violations are serious regulatory offenses and can
result in significant fines for both the railroad and the individual crew
members. Dispatchers must balance the need for train movement with the absolute
requirement for HOS compliance, often making difficult decisions to hold or
re-route trains to ensure crews remain within their legal limits.
"""

