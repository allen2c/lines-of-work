title = "**海關查驗作業流程**"

content = """
- 海關查驗分為**文件查驗**與**實物查驗**，比例約為**3:1**。
- **文件查驗**：
  - 海關審核報單及附件是否齊全、資料是否一致。
  - 如發現疑問，會要求補件（如提供原產地證明、測試報告）。
  - 補件時限：**3個工作天**，逾期可能產生滯報金（每日TWD 200）。
- **實物查驗**：
  - 海關通知查驗後，**1個工作天內**需安排查驗時間，並通知倉儲部門配合。
  - 查驗地點：通常在貨櫃場或保稅倉庫。
  - 查驗內容：核對貨物實際狀況（如品名、數量、包裝）是否與報單一致。
  - 如發現異常（如品名不符、數量短少），海關可要求補稅、退運或罰款。
- 查驗費用：由報關行或進口人負擔，費用依貨物類別而定（如一般貨物TWD 1,500/櫃，危險品TWD 3,000/櫃）。
"""  # noqa: E501

