content = """
- 補稅原因：
  - 低報價值（如發票金額低於市場價格）。
  - 稅則適用錯誤（如誤用零稅率而非一般稅率）。
  - 貨物數量短少（如報單數量與實際不符）。
- 補稅流程：
  1. **海關通知**：
     - 海關發出「補稅通知書」，註明補稅原因及金額。
     - 通知書通常附有「繳款書」，需在**15天內**繳納。
  2. **確認補稅原因**：
     - 核對海關指摘內容（如價值、數量、稅則）是否合理。
     - 如有異議，可在**15天內**提出異議申請（需提供證明文件）。
  3. **繳納稅款**：
     - 如確認補稅合理，需通知客戶繳納差額（超過TWD 10,000需主管核准）。
     - 繳款後，將繳款證明提交海關。
  4. **後續處理**：
     - 如客戶拒絕繳納，需通知業務部門協商解決（如調整合約或退運貨物）。
     - 如逾期未繳，海關可處以滯納金（每日0.5%）。
- 注意事項：
  - 補稅金額超過TWD 50,000，需提供額外證明（如合約、市場價格資料）。
  - 如補稅涉及違規（如低報價值），海關可處以罰鍰（新台幣6,000元以上）。
"""  # noqa: E501

