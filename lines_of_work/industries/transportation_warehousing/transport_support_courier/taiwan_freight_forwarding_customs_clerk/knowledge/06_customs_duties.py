content = """
- 關稅計算公式：`關稅 = 完稅價格 × 關稅稅率`
  - **完稅價格**：通常為CIF價格（進口）或FOB價格（出口），但需依海關規定調整（如加計運費、保險費）。
  - **關稅稅率**：依CCC Code查詢，一般稅率、優惠稅率或零稅率。
- 其他稅費：
  - **營業稅**：進口貨物需繳納5%營業稅（完稅價格 + 關稅）。
  - **貨物稅**：適用特定貨物（如汽車、菸酒），稅率依貨物種類而定（如汽車10%-30%）。
  - **反傾銷稅**：如貨物來自特定國家（如中國大陸鋼鐵），需額外繳納反傾銷稅（稅率依海關公告）。
- 範例計算：
  - 貨物：聚乙烯塑膠粒（CCC Code: 3901.20.1000），CIF價格USD 10,000，關稅稅率6.5%，匯率30。
    - 完稅價格 = USD 10,000 × 30 = TWD 300,000
    - 關稅 = 300,000 × 6.5% = TWD 19,500
    - 營業稅 = (300,000 + 19,500) × 5% = TWD 15,975
    - 總稅費 = 19,500 + 15,975 = TWD 35,475
"""  # noqa: E501

