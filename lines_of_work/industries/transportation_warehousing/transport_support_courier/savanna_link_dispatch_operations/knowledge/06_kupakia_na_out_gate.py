title = "Kupakia na Out-Gate"

content = """
Out-gate ni hatua ya mwisho ya kifurushi kwenye kituo. Vifurushi vinapakiwa kwenye magari
ya mstari kulingana na njia na ratiba.

**Utaratibu:**
- Baada ya upangaji, vifurushi vinapakiwa kwenye kontena au palleti zilizowekwa alama.
- Kila kontena/paleti ina stika ya njia (mfano: NBI-MSA, NBI-DAR, NBI-KLA).
- Mgari wa mstari unafika kwenye eneo la kupakia; mfanyakazi anapakia na kuchoma out-gate.
- Mfumo unarekodi muda wa kuondoka na nambari ya mgari.

**Mambo muhimu:**
- Usiweke vifurushi vya njia tofauti kwenye kontena moja.
- Thibitisha idadi ya vifurushi inalingana na hati ya mgari.
- Skana out-gate ni lazima kwa kila kifurushi; bila hiyo hali haitasasishwa kwa mpokeaji.
"""  # noqa: E501

