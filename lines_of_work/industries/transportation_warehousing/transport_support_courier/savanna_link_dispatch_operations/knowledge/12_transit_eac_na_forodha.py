title = "Transit ya EAC na Sheria za Forodha"

content = """
Jumuiya ya Afrika Mashariki (EAC) ina mikataba ya usafiri wa bidhaa bila ukodi kati ya
nchi wanachama. Transit inahitaji hati sahihi.

**Aina za hati:**
- CMR (Convention relative au contrat de transport international de marchandises par route):
  hati ya usafirishaji wa barabara.
- Airway bill: kwa usafiri wa ndege.
- Bill of Lading: kwa usafiri wa meli.

**Masuala ya forodha:**
- Vifurushi vya transit vinapaswa kuwa na stika wazi ya "Transit".
- Idara ya forodha inaweza kukagua nasibu; hakikisha hati ziko tayari.
- Bidhaa zilizosanifikirishwa (local content) zinaweza kupata mapungufu ya ukodi.

**Mafunzo:** Wafanyakazi wa kituo wanafundishwa mambo ya kimsingi ya forodha; masuala magumu
yanaelekezwa kwa idara ya forodha.
"""  # noqa: E501

