title = "Vitu Vilivyokatazwa"

content = """
Savanna Link haikubali vifurushi vyenye vitu vilivyokatazwa. Usafirishaji wa vitu kama hizo
unakiukwa sheria na kunakabiliwa na adhabu.

**Vitu vilivyokatazwa kabisa:**
- Dawa za kulevya, silaha, vilipuko
- Vitu vyenye moto au kutokwa kwa kemikali
- Pesa taslimu, hati muhimu za kifedha
- Nyama, mazoweo, na bidhaa zingine za kilimo zisizopata kibali cha karantini
- Vitu vinavyokiuka haki za miliki (vitoleo bandia)

**Vitu vinavyohitaji kibali maalum:**
- Dawa za wagonjwa (lazima iwe na risiti ya daktari)
- Viti vya umeme (lazima viwe na certification)
- Kemikali za viwandani (lazima ziwe na MSDS na uhalali wa usafirishaji)

**Utaratibu:** Endapo kifurushi kinashukiwa kuwa na kitu kilichokatazwa, katalia haraka na
wasili kwa idara ya usalama na forodha.
"""  # noqa: E501

