"""Knowledge item: mkusanyiko wa operesheni."""

title = "Mkusanyiko wa Operesheni za Kituo cha Kuunganisha"

content = """
Kituo cha kuunganisha cha Savanna Link ni kiungo kinachounganisha mtandao wa kukusanya na
usafirishaji wa mstari. Kituo hicho huko Nairobi kinashughulikia usafiri wa ndani na hariri;
kituo cha Mombasa kinahudumia usafiri wa bandari na kimataifa.

**Kazi kuu za kituo:**
- Kupokea vifurushi kutoka magari ya usafirishaji (in-gate)
- Kupanga kulingana na msimbo wa posta na mwelekeo (ndani / EAC / nje ya EAC)
- Udhibiti wa ubora na usawazishaji na hati ya usafirishaji
- Kupakia magari ya mstari kulingana na njia (out-gate)
- Ufuatiliaji wa wakati halisi na sasisho la hali kwenye mfumo

**Ratiba ya kazi:**
- Shift ya mchana: 06:00–14:00 (maandalizi, matengenezo)
- Shift ya jioni: 14:00–22:00 (wakutaji wa mitaa, wave ya kwanza ya upangaji)
- Shift ya usiku: 22:00–06:00 (usafirishaji wa mstari, upangaji mkuu)

**Vipimo muhimu:**
Muda unaotarajiwa wa kifurushi kwenye kituo: < masaa 4. Usahihi wa upangaji: ≥ 99.9%.
"""  # noqa: E501

