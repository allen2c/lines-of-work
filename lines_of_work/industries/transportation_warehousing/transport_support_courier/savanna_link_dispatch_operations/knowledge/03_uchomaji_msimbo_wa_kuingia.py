"""Knowledge item: uchomaji msimbo wa kuingia."""

title = "Uchomaji Msimbo wa Kuingia (In-Gate)"

content = """
In-gate ni hatua ya kwanza ya kifurushi kwenye kituo. Kila kifurushi kinapaswa kuchomwa
kwenye mfumo kabla ya kuendelea na upangaji.

**Utaratibu:**
- Mgari wa usafirishaji unaingia kwenye eneo la kupokea; mkusanyaji anatoa vifurushi.
- Kila kifurushi kinachomwa kwa skana ya mkononi au skana ya meza.
- Mfumo unarekodi muda, mgari, na mkutaji. Hali inasasishwa kuwa "Imepokelewa kwenye kituo".
- Vifurushi visivyoweza kuchomwa vinakaa kwenye mstari wa reject; ukaguzi wa mkono unafanywa.

**Mambo ya kuzingatia:**
- Thibitisha kuwa msimbo wa mfuko unalingana na hati ya usafirishaji.
- Vifurushi vilivyoharibika au vilibadilishwa visiwekwe kwenye mfumo—wasili kwa msimamizi.
- Kilele cha usindikaji: ondoa vifurushi haraka kutoka magari ili kuepuka foleni.
"""  # noqa: E501

