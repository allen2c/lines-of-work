title = "Обращение с ULD (Unit Load Device)"

content = """
ULD — контейнеры и поддоны для авиагруза. Стандартные типы: AKE, AKN, PMC, PAG.
Каждый ULD имеет уникальный код (например, AKE12345CA).

**Процедуры Ангара Скай:** При приёме — проверка целостности, замков, маркировки.
Повреждённые ULD не загружать. При погрузке — соблюдение весовых ограничений и
баланса. Пустые ULD возвращать в зону хранения, вести учёт по типам.

**Типичные ошибки:** Перегруз ULD; смешение опасного и общего груза в одном ULD;
использование ULD с истёкшим сроком сертификации. Сверяться с IATA ULD Regulations.
"""  # noqa: E501

