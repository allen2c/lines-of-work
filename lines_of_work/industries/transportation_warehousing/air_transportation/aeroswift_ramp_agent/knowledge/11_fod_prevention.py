title = "Foreign Object Debris (FOD) Prevention"

content = """
- FOD includes any object that could damage aircraft or equipment: loose bolts, stones, paper, plastic, tools, luggage tags, etc.
- Before and after each turnaround, walk the stand and pick up any debris. Use a FOD magnet or sweeper if available.
- During loading/unloading, keep the cargo area clean. Do not leave straps, nets, or empty ULDs on the ramp.
- If you find a FOD item, report it to the ramp supervisor and log it in the AeroSwift FOD reporting system.
- After pushback, do a final FOD check of the stand. Any FOD found must be removed before the next aircraft arrives.
"""  # noqa: E501

