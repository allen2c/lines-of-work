title = "Potable Water Service"

content = """
- The potable water truck supplies fresh water to the aircraft. The ramp agent ensures the truck is positioned at the correct water service panel (usually near the rear).
- The water crew connects the hose and fills the tanks. The ramp agent verifies that the water is potable (from a certified source).
- After filling, the hose is disconnected and the panel closed. The truck must be moved away before pushback.
- If the water is contaminated or the truck is not certified, reject the service and notify the ramp supervisor.
- Potable water service is usually done after lavatory service to avoid cross‑contamination.
"""  # noqa: E501

