title = "Procedura obsługi zwrotów"

content = """
Obsługa zwrotów w Wisła Hub przebiega przez strefę RMA (Return Merchandise
Authorization). Każdy zwrot jest skanowany przy przyjęciu i kierowany do
odpowiedniego workflow w zależności od stanu i kategorii produktu.

**Przyjęcie:** Skanowanie etykiety zwrotnej identyfikuje zamówienie. System
porównuje zawartość z deklaracją klienta. Rozbieżności odnotowuje się w RMA.
Towary uszkodzone lub niezgodne z opisem trafiają do kwarantanny.

**Ocena stanu:** Każda jednostka jest klasyfikowana: nadaje się do ponownej
sprzedaży (A), do odsprzedaży ze zniżką (B), do naprawy lub recyklingu (C).
Produkty elektroniczne wymagają testu funkcjonalnego przed klasyfikacją.

**Przekierowanie:** Jednostki A wracają do lokalizacji kompletacyjnych. B do
strefy produktów outlet. C do naprawy lub utylizacji według polityki klienta.
"""

