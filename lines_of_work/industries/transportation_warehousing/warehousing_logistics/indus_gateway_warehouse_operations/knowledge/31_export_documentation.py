"""Export documentation procedures for Indus Gateway."""

title = "Export Documentation"

content = """
Export shipments require complete documentation for customs clearance at destination.
Standard documents include commercial invoice, packing list, bill of lading or airway
bill, certificate of origin (where applicable), and any product-specific certificates.

**Commercial Invoice:** Must show buyer, seller, goods description, quantity, unit price,
total value, Incoterm, and payment terms. Used for customs valuation and duty assessment.

**Packing List:** Item-level detail: carton count, dimensions, weight, marks and numbers.
Enables customs and consignee to verify physical contents against declaration.

**Certificate of Origin:** GSP forms (Form A, EUR.1) reduce or eliminate duties for
eligible products in EU and other preference markets. Indus Gateway coordinates with
chambers of commerce for issuance.

**Retention:** Keep copies of all export documents for minimum five years per Pakistan
customs requirements.
"""  # noqa: E501

