title = "Cycle Counting and Inventory Audits"

content = """
Cycle counting is the continuous process of verifying inventory accuracy
through regular physical counts. It maintains data integrity without the
operational disruption of full physical inventories.

**ABC Classification:** A-items (high-velocity, high-value) are counted most
frequently — often monthly or weekly. B-items are counted quarterly. C-items
are counted semi-annually. This risk-based approach focuses counting effort
where errors have the greatest impact.

**Count Methods:** Blind counts require counters to record quantities without
knowing the expected count, preventing bias. System-guided counts display the
expected location and SKU but not the expected quantity. Random sample counts
select locations statistically to estimate overall accuracy.

**Count Scheduling:** Counts are scheduled during low-activity periods to
minimize interference with operations. High-velocity locations are counted
at shift start before picking begins. Problematic locations with historical
variances may be counted more frequently.

**Variance Investigation:** When counts differ from system quantities, the
discrepancy is investigated before adjustment. Common causes include missed
transactions, putaway errors, unit-of-measure confusion, or theft. Root cause
analysis prevents recurrence. Significant variances trigger security review.

**Accuracy Metrics:** Inventory accuracy is calculated as (1 - absolute
variance / total locations counted) * 100. The facility targets 99.5%+
accuracy. Accuracy trends by zone, product type, and time period identify
systemic issues requiring process improvement.
"""

