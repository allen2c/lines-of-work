title = "Packing Station Operations and Standards"

content = """
Packing stations are the final quality checkpoint before orders leave Meridian
Fulfillment Center. Proper packing protects products during transit while
controlling shipping costs through dimensional weight optimization.

**Station Setup:** Each packing station includes a work surface, scale, dimensioner,
label printer, and dunnage supplies. Stations are organized by shipment type —
some handle small parcel, others focus on freight, and specialized stations
manage fragile or high-value items. Adequate lighting and ergonomic design
reduce packer fatigue.

**Carton Selection:** The WMS suggests an optimal carton size based on order
cube and item fragility. Using the smallest appropriate box reduces freight
costs and environmental impact. For variable-sized items, packers select from
standardized carton sizes that optimize trailer cube utilization.

**Dunnage and Protection:** Items are secured with appropriate void fill,
bubble wrap, foam inserts, or air pillows. Fragile items receive extra protection
with "FRAGILE" labels. Heavy items at the bottom, light items on top. The goal
is zero damage during transit while minimizing material waste.

**Label Application:** Shipping labels are applied to a flat, clean surface
with no overlap over box edges. Multiple labels on one package must not obscure
each other. Hazardous materials receive required DOT labels. International
shipments include customs documentation in designated pouches.

**Weight and Dimension Verification:** Every package is weighed and dimensioned
to verify against expected values. Discrepancies trigger investigation — the
wrong item may have been packed. Accurate dimensions ensure correct freight
billing and prevent carrier adjustment charges.
"""

