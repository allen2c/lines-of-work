title = "Kitting and Light Assembly Services"

content = """
Value-added services like kitting and assembly transform individual
components into finished products or sellable sets, extending Meridian's
fulfillment capabilities beyond basic storage and shipping.

**Kitting Operations:** Kitting combines multiple SKUs into a single
sellable unit or gift set. The WMS maintains a bill of materials for each
kit SKU. Pickers gather components to a kitting station where they are
assembled, labeled with the new kit SKU, and prepared for sale. Kitting
enables product bundling and promotional sets.

**Assembly Services:** Light assembly may include attaching tags, inserting
batteries, applying decals, or basic product configuration. Assembly
stations are equipped with tools, work instructions, and quality checklists.
Assembled units are tested if functionality is involved. Defective assemblies
are disassembled and components returned to stock or discarded.

**Work Order Management:** Value-added work is triggered by WMS work orders
that specify the operation, components required, and output expectations.
Labor is tracked against work orders for costing. Work order completion
updates inventory, consuming components and creating finished goods.

**Quality Control:** Kitted and assembled items undergo inspection before
release. Component counts are verified against the bill of materials.
Appearance and functionality meet standards. Defective work is reworked
or scrapped with component loss documented.

**Packaging Integration:** Finished kits and assemblies flow directly to
packing or are stored in forward locations if demand is immediate.
Custom packaging may be applied for kits. Gift messaging and promotional
inserts are added as specified in the work order.
"""

