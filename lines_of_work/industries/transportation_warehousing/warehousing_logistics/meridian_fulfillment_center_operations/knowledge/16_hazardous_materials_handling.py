title = "Hazardous Materials Storage and Handling"

content = """
Hazardous materials (hazmat) require specialized handling to ensure safety
and regulatory compliance. Meridian Fulfillment Center maintains strict
protocols for these regulated items.

**Hazmat Classification:** Items are classified by DOT hazard classes
including flammables, corrosives, oxidizers, and toxic substances. Safety
Data Sheets (SDS) are on file for all hazmat SKUs. Segregation rules prevent
incompatible materials from being stored together. Classification determines
packaging, labeling, and shipping requirements.

**Segregated Storage:** Hazmat is stored in designated cages or rooms with
appropriate fire suppression, ventilation, and containment. Flammables are in
flammables cabinets. Corrosives are in acid and base segregated areas.
Quantities are limited per fire code requirements. Access is restricted to
trained personnel only.

**Handling Procedures:** Hazmat picks use specialized equipment and PPE.
Spill kits are positioned near hazmat storage. Workers are trained on
evacuation procedures and emergency response. Incompatible items are never
packed together regardless of shipping efficiency. Damaged hazmat containers
are treated as potential spills.

**Shipping Compliance:** Hazmat shipments require proper classification,
packaging, labeling, and documentation. Shipper's Declarations for Dangerous
goods accompany international shipments. Ground hazmat requires specific
marking and driver notification. Only certified hazmat carriers are used.
Shipping papers must accurately describe contents and emergency response info.

**Regulatory Compliance:** The facility maintains hazmat registration with
DOT and complies with OSHA Hazard Communication standards. Training records
are maintained for all hazmat handlers. Emergency response plans are filed with
local authorities. Regular audits verify compliance with 49 CFR and other
applicable regulations.
"""

