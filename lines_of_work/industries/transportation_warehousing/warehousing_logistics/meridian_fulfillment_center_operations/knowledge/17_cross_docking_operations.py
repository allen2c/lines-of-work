title = "Cross-Docking and Flow-Through Operations"

content = """
Cross-docking moves goods directly from receiving to shipping without
intermediate storage. This flow-through strategy reduces handling costs
and delivery time for time-sensitive shipments.

**Cross-Dock Criteria:** Products suitable for cross-docking have predictable
demand, high velocity, and pre-allocated customer orders. Perishables with tight
delivery windows, promotional items for immediate sale, and pre-sold B2B shipments
are prime candidates. Random or unpredictable demand items require storage
buffer.

**Pre-Arrival Preparation:** Cross-dock shipments are pre-allocated to
outbound orders before the inbound truck arrives. The WMS generates pick
lists directing items to specific outbound staging lanes or directly into
outbound trailers. Space is reserved in the cross-dock area.

**Rapid Processing:** Inbound trailers are unloaded directly to the cross-dock
floor or outbound trailers. Items may be sorted by destination or customer
en route. Speed is critical — cross-dock items should move through the
facility in hours, not days. Delays defeat the purpose and create congestion.

**Consolidation Strategies:** Multiple inbound shipments may consolidate
into single outbound loads. LTL (Less Than Truckload) inbound becomes
full truckload outbound to the same region. This freight optimization
reduces transportation costs while maintaining delivery speed.

**Performance Metrics:** Cross-dock success is measured by dwell time
(hours in facility), handling cost per unit, and on-time shipment rate.
Failed cross-docks that require putaway to storage represent process
breakdowns. Continuous improvement targets faster flow and higher cross-dock
eligibility rates.
"""

