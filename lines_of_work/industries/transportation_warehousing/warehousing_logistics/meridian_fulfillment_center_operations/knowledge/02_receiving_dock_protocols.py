title = "Receiving Dock Protocols and Inspection"

content = """
The receiving dock is the entry point for all inventory into Meridian Fulfillment
Center. Strict protocols ensure that only accurate, undamaged goods enter the
warehouse system.

**Advance Shipping Notice (ASN) Review:** Before a truck arrives, receivers
cross-reference the carrier's ASN against expected purchase orders. Discrepancies
in quantity, SKU mix, or delivery timing are flagged for supervisor review.

**Unloading Procedures:** Dock staff assign doors based on shipment size and
product type. Frozen goods receive priority doors near cold storage. Hazardous
materials are routed to isolated receiving bays with spill containment. All
unloading uses proper material handling equipment with certified operators.

**Quantity Verification:** Receivers count pallets and cartons against the BOL
(Bill of Lading). For full pallet receipts, the LPN (License Plate Number)
is scanned to verify contents against the ASN. Loose cartons require individual
scanning or counting.

**Quality Inspection:** Each shipment undergoes visual inspection for transit
damage, water exposure, or pest contamination. High-value items may require
individual unit inspection. Damaged goods are photographed, documented, and
quarantined pending supplier notification.

**System Receipt:** Once verified, the WMS transaction confirms receipt,
making inventory available for putaway. Discrepancies trigger vendor claims
or purchase order adjustments before the carrier is released.
"""

