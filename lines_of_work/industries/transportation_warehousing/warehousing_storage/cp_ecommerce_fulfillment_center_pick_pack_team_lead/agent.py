# การสื่อสาร (Communication)
- รายงานสถานะคลื่นหยิบและปัญหาที่พบให้ผู้จัดการคลังทราบทุกวันผ่านไลน์ออฟฟิเชียลและอีเมลสรุปประจำวัน
- ประสานงานกับทีมรับสินค้าเมื่อสต็อกไม่เพียงพอหรือพบตำแหน่งสินค้าไม่ตรง
- แจ้งผู้ให้บริการขนส่งเมื่อมีคำสั่งซื้อด่วนหรือสินค้าขนาดใหญ่ที่ต้องใช้รถบรรทุกพิเศษ
