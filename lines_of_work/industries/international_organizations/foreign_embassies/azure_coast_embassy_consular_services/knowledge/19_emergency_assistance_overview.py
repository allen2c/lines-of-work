content = """
The Embassy provides emergency assistance to Azure Coast citizens facing serious
crises abroad. Services are available 24/7 for life-threatening situations.

**Emergency Services Scope**:

**Arrest and Detention**: When citizens are arrested or detained by local
authorities, the Embassy provides:
- Notification to family (with consent)
- Access to local legal resources and attorney lists
- Monitoring of detention conditions
- Advocacy for fair treatment under Vienna Convention on Consular Relations
- Delivery of essential supplies (medications, reading materials)

Note: Embassy cannot provide legal advice, pay legal fees, or secure release.

**Medical Emergencies**: For hospitalization or serious illness:
- Notification to family and insurance providers
- Coordination with medical facilities
- Facilitation of medical evacuation if necessary
- Assistance with medical payment arrangements
- Delivery of emergency medications

**Death Abroad**: When citizens die abroad:
- Notification to next of kin
- Guidance on local death registration procedures
- Coordination with funeral services
- Assistance with repatriation of remains or local burial
- Documentation for insurance and estate purposes

**Missing Persons**: Report citizens missing abroad:
- Coordinate with local law enforcement
- Maintain communication with family
- Disseminate information through appropriate channels
- Assist with travel arrangements for searching family members

**Crisis Response**: Natural disasters, political unrest, terrorism:
- Security updates to citizens in affected areas
- Evacuation coordination when necessary
- Emergency financial assistance (loans for return travel)
- Communication facilitation when normal channels disrupted

**Emergency Contact**: 24/7 hotline +1-555-AZCOAST-911
"""

