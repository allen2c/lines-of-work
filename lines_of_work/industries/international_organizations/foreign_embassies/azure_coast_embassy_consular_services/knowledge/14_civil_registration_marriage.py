title = "Civil Registration: Marriage and Divorce"

content = """
The Embassy records marriages and divorces of Azure Coast citizens occurring
abroad, maintaining accurate civil status records in the national registry.

**Marriage Registration**:

**Eligibility**: Marriages between Azure Coast citizens, or between Azure Coast
citizen and foreign national, solemnized abroad may be registered.

**Required Documents**:
- Original foreign marriage certificate with apostille or embassy legalization
- Certified translation if not in English or Azure Coast official language
- Both spouses' valid Azure Coast passports or citizenship proof
- Proof of termination of previous marriages (death certificate or divorce decree)
  if applicable

**Registration Process**:
1. Submit documents at Embassy (appointment required)
2. Consular officer verifies marriage validity under Azure Coast law
3. Documents forwarded to Civil Registry in Azure Coast
4. Registration confirmed within 4-6 weeks
5. Certificate of Marriage Registration Abroad issued

**Marriage Validity Requirements**: Under Azure Coast law, marriages must meet
these standards:
- Both parties minimum 18 years old (or 16 with judicial authorization)
- Free consent of both parties
- Not within prohibited degrees of consanguinity
- Not bigamous
- Solemnized by authorized official

**Divorce Registration**:

**Foreign Divorce Recognition**: Divrees obtained abroad recognized in Azure
Coast if:
- Divorce valid where granted
- Both parties had opportunity to participate in proceedings
- Azure Coast citizen's rights protected under Azure Coast law

**Required Documents**:
- Final divorce decree with apostille or legalization
- Certified translation if necessary
- Evidence both parties received notice of proceedings
- Passport or citizenship documentation

**Effect of Registration**: Registered marriages and divorces have same legal
effect in Azure Coast as those solemnized or granted domestically. Registration
required for name changes, subsequent marriages, and legal proceedings in
Azure Coast courts.
"""

