title = "Arrested Citizen Assistance"

content = """
When Azure Coast citizens are arrested or detained abroad, the Embassy provides
consular assistance while respecting local legal processes and the citizen's
autonomy.

**Initial Response**:
Upon notification of arrest (by authorities, family, or citizen):
1. Verify citizen's identity and detention status
2. Offer consular assistance (citizen may decline)
3. If accepted, establish regular contact with detainee
4. Notify family if detainee consents

**Vienna Convention Rights**: Under the Vienna Convention on Consular Relations,
detaining authorities must:
- Inform foreign detainees of right to contact their embassy
- Allow consular access to detained nationals
- Facilitate communication between detainee and consular officers

**Services Provided**:

**Legal Information**: Provide lists of local attorneys and legal aid resources.
Cannot recommend specific lawyers or provide legal advice.

**Welfare Checks**: Visit detainees regularly to assess:
- Physical health and medical needs
- Treatment conditions
- Access to legal representation
- Family contact needs

**Communication Assistance**: Facilitate contact with:
- Family members (with consent)
- Legal representatives
- Employers for work-related absences

**Documentation**: Issue statements of nationality for court proceedings.
Authenticate documents for legal use.

**Medical Needs**: Ensure detainees receive:
- Prescribed medications
- Medical attention for health issues
- Mental health support when indicated

**Limitations**: The Embassy cannot:
- Pay legal fees or fines
- Secure release from detention
- Override local legal processes
- Provide legal representation
- Guarantee specific treatment standards

**Family Communication**: Keep families informed within bounds of privacy and
security considerations. Coordinate family visits when permitted.

**Trial Monitoring**: Attend court proceedings when open to public. Report
procedural irregularities to appropriate authorities.
"""

