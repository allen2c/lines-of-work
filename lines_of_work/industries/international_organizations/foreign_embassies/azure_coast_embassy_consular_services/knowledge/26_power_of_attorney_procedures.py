title = "Power of Attorney Procedures"

content = """
Powers of attorney executed abroad require specific procedures to be valid for
use in Azure Coast. The Embassy assists with preparation and authentication.

**Power of Attorney Types**:

**General Power of Attorney**: Grants broad authority to handle affairs in
Azure Coast. Typically used when grantor cannot personally manage affairs due
to absence, illness, or incapacity.

**Special/Limited Power of Attorney**: Grants specific authority for defined
purposes: property sale, court representation, banking transactions, business
registration.

**Durable Power of Attorney**: Remains valid if grantor becomes incapacitated.
Must explicitly state durability intention.

**Requirements for Validity**:

**Execution**:
- Grantor personally appears before consular officer
- Valid identification presented
- Mental capacity confirmed (officer observes for signs of impairment)
- Signature witnessed by consular officer
- Notarial certificate issued

**Content Requirements**:
- Grantor's full name, identification number, address
- Agent's full name, identification number, address
- Specific powers granted (vague grants may be rejected)
- Effective date and expiration (or statement of indefinite duration)
- Durability clause if applicable
- Governing law statement (Azure Coast law recommended)

**Authentication**: For use in Azure Coast:
- Notarization by consular officer sufficient
- No additional authentication required

**For use in third countries**: May require apostille if country is Hague
Convention member.

**Revocation**: Powers of attorney may be revoked by:
- Subsequent written revocation notarized and delivered to agent
- Destruction of original (if no copies distributed)
- Grantor's death or incapacity (unless durable)
- Expiration of stated term
- Completion of specified purpose

**Precautions**: Grantors should:
- Choose trusted agents with full understanding of responsibilities
- Limit powers to necessary scope
- Understand that agents may bind grantor to contracts and obligations
- Maintain copy of executed document
- Inform relevant institutions of attorney arrangement

**Fee**: $30 per signature notarized. Complex document review may require
additional appointment.
"""

