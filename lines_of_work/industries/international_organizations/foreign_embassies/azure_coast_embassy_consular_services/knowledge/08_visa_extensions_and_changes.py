title = "Visa Extensions and Status Changes"

content = """
Visa holders requiring extended stay or status modification must apply through
proper channels before current authorization expires. Overstaying results in
penalties and future entry restrictions.

**Extension Eligibility**: Extensions granted only for circumstances beyond
applicant's control or legitimate continuation of original purpose. Extensions
not granted for:
- Tourist visa holders seeking extended tourism beyond initial period
- Changing to work purpose without work permit
- Circumventing standard visa limits

**Valid Extension Grounds**:
- Medical emergency requiring continued treatment
- Force majeure preventing departure (natural disasters, flight cancellations)
- Academic program extension with institutional confirmation
- Business negotiations exceeding initial timeline with employer justification

**Application Timing**: Extension requests must be submitted at least 14 days
before current visa expiration. Late applications considered only with compelling
justification and may incur overstay penalties.

**Required Documentation**:
- Completed extension application form
- Current visa and passport copy
- Detailed explanation letter with supporting evidence
- Proof of continued financial capacity
- Fee payment ($100 USD processing fee plus any applicable penalties)

**Status Changes**: Changing from one visa category to another generally
requires leaving Azure Coast and applying for new visa from abroad. In-country
status changes available only for:
- Student to work visa (with valid work permit)
- Dependent to student visa
- Exceptional humanitarian circumstances

**Processing**: Extension applications reviewed by Department of Immigration
in Azure Coast, not at Embassy. Processing time 2-4 weeks. Applicants remain
in legal status during pending extension review if application submitted before
expiration.

**Overstay Consequences**: Unauthorized overstay results in fines ($10 USD per
day), deportation, and entry bans ranging from one to ten years depending on
duration and circumstances.
"""

