title = "Death Abroad Procedures"

content = """
When Azure Coast citizens die abroad, the Embassy assists families with the
complex administrative and logistical requirements for disposition of remains
and estate matters.

**Initial Response**:

**Notification**: Upon learning of death:
- Confirm identity through passport or identification
- Notify next of kin with sensitivity and compassion
- Provide factual information about circumstances when known
- Offer immediate assistance and guidance

**Documentation Requirements**:
- Local death certificate (original, with apostille for use in Azure Coast)
- Cause of death statement (for insurance claims)
- Consular Report of Death Abroad (prepared by Embassy)

**Disposition Options**:

**Repatriation of Remains**: Return body to Azure Coast for burial
- Coordinate with funeral homes at origin and destination
- Arrange international mortuary transport
- Handle customs and health clearance
- Cost: $5,000-$15,000 USD (typically covered by insurance or family)

**Local Burial/Cremation**: Burial in country of death
- Coordinate with local funeral services
- Ensure compliance with local regulations
- Facilitate family travel for services if desired
- Arrange shipment of cremated remains to Azure Coast if preferred

**Decision Timeline**: Families must decide within 3-5 days in most cases due
to local storage limitations. Embassy provides guidance but respects family
preferences and religious considerations.

**Estate Matters**: Embassy provides limited assistance:
- Secure deceased's passport and valuables for return to family
- Provide lists of local attorneys for estate administration
- Authenticate documents for estate proceedings
- Cannot administer estates or distribute assets

**Financial Assistance**: No government funds available for funeral expenses.
Emergency loans may be available for destitute families in exceptional cases.

**Insurance Coordination**: Assist families with:
- Understanding travel insurance coverage
- Filing death benefit claims
- Obtaining required documentation for insurers
"""

