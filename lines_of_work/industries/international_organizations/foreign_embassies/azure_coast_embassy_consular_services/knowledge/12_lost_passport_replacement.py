title = "Lost or Stolen Passport Replacement"

content = """
Lost or stolen passports require immediate reporting and replacement procedures
to prevent identity fraud and facilitate travel document reissuance.

**Immediate Actions**:
1. Report loss/theft to local police immediately
2. Obtain police report with case number
3. Report to Embassy as soon as possible
4. Monitor for identity theft or fraudulent use

**Embassy Reporting**: Contact consular section during business hours or
emergency line after hours. Provide:
- Circumstances of loss/theft
- Last known location of passport
- Police report number
- Current contact information and location

**Replacement Requirements**:

**Documentation**:
- Police report (original or certified copy)
- Completed passport application form
- Two passport photographs
- Proof of Azure Coast citizenship (birth certificate, citizenship certificate,
  or photocopy of lost passport if available)
- Government-issued photo ID (driver's license, national ID if available)
- Statement explaining circumstances of loss

**Verification Process**: The Embassy verifies applicant identity through:
- Database check of passport records
- Biometric comparison if previous passport on file
- Citizenship documentation review
- Interview with consular officer

**Processing Options**:

**Standard Replacement**: 10-15 business days. Full validity passport issued
($150 USD fee).

**Emergency Travel Document**: For urgent travel within 48 hours. Limited
validity (typically 30 days or single journey). Requires proof of emergency
(medical, death, legal requirement). Fee: $80 USD. Cannot be used for destinations
requiring visas.

**Fraud Prevention**: Lost passport numbers are entered into international
databases. Found passports must be surrendered; using a recovered passport after
reporting loss constitutes fraud.

**Multiple Losses**: Pattern of repeated lost passports triggers enhanced scrutiny
and possible validity restrictions.
"""

