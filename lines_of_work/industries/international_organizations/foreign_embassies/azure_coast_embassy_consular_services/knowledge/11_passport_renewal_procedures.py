title = "Passport Renewal Procedures"

content = """
Passport renewal is available to citizens holding valid or recently expired
Azure Coast passports. The streamlined process requires fewer documents than
first-time applications.

**Eligibility**: Renewal available if:
- Current passport valid or expired less than 3 years
- Passport issued after age 16 (adult renewal)
- Personal data unchanged (name, nationality status)
- Previous passport not significantly damaged

**Application Methods**:

**In-Person**: Schedule appointment online. Required for:
- First adult passport renewal
- Expired passports over 1 year
- Damaged but usable passports
- Applicants requiring same-day processing

**By Mail**: Available for straightforward renewals. Requirements:
- Current passport issued after 2015
- Passport expired less than 1 year or still valid
- No name or data changes
- Clear passport photo (not worn)

**Required Documents**:
- Current/expired passport (original submitted, returned canceled)
- Completed passport application form (signed)
- Two recent passport photographs (specifications below)
- Fee payment ($100 USD standard, $180 expedited)
- Self-addressed prepaid return envelope (mail applications)

**Photograph Specifications**:
- 35mm x 45mm
- White or light gray background
- No glasses, head coverings (except religious with statement)
- Neutral expression, mouth closed
- Full face visible, centered
- Printed on matte or glossy photo paper

**Processing**: Standard 15-20 business days. Expedited 5-7 business days.
Mail applications add transit time to processing.

**Return**: New passport and canceled old passport returned separately for
security. Sign new passport immediately upon receipt.
"""

