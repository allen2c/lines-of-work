title = "Appointment Scheduling and Office Procedures"

content = """
The Embassy operates on an appointment system for most services, ensuring
efficient service delivery and minimizing wait times.

**Appointment Booking Methods**:

**Online System**: Primary booking method at embassy.azurecoast.gov/appointments
- Available 24/7
- Real-time availability display
- Immediate confirmation with reference number
- Rescheduling and cancellation options

**Telephone**: Call center (+1-555-AZCOAST) during business hours
- Monday-Friday, 9:00 AM - 4:00 PM local time
- Automated system available after hours for basic inquiries

**Email**: For complex cases requiring pre-consultation
- consular@embassy.azurecoast.gov
- Response within 2 business days

**Service Categories Requiring Appointments**:
- All passport services
- Visa applications
- Birth, marriage, death registration
- Citizenship matters
- Notarial services (more than 3 documents)

**Walk-In Services** (limited, first-come first-served):
- Document collection
- General inquiries
- Emergency assistance coordination

**Office Hours**:
- Public counter: Monday-Friday, 9:00 AM - 12:00 PM
- Document collection: Monday-Friday, 2:00 PM - 4:00 PM
- Emergency line: 24/7 for citizen arrests, deaths, hospitalizations

**Preparation Guidelines**:
- Arrive 15 minutes before appointment
- Bring all required documents per checklist
- Bring exact change if paying cash
- No electronic devices permitted in consular section
- Security screening required for all entrants

**Rescheduling and Cancellation**:
- Reschedule up to 24 hours before appointment without penalty
- Late cancellations/no-shows may result in 30-day booking restriction
- Emergencies accommodated with documentation

**Accessibility**: Facilities wheelchair accessible. Sign language interpreters
available with 48-hour advance notice. Language assistance available for major
languages.

**Holiday Closures**: Embassy closed on Azure Coast national holidays and host
country public holidays. Check website for current holiday schedule.
"""

