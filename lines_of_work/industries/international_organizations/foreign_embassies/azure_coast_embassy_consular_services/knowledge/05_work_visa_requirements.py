title = "Work Visa Requirements"

content = """
Work visas require pre-approval from the Azure Coast Ministry of Labor before
Embassy processing. The employer initiates the process; the employee completes
visa formalities after work permit approval.

**Work Permit Approval**: Original or certified copy of Ministry of Labor work
permit authorization. Permit specifies employer, position, and validity period.
Work permit must be obtained by employer in Azure Coast before visa application.

**Employment Contract**: Signed contract between employer and employee, notarized
in Azure Coast. Contract must specify salary (meeting minimum wage requirements),
benefits, working conditions, and duration.

**Qualification Verification**: Diplomas, professional certificates, and credentials
relevant to the position. Documents must be apostilled and translated if necessary.
Some professions require additional licensing or registration in Azure Coast.

**Employer Documentation**: Company registration certificate, tax clearance, and
proof of compliance with local employment quotas. Large employers may have
streamlined processes for frequent hires.

**Medical Examination**: Full medical exam at designated panel physician.
Includes drug screening, infectious disease tests, and general health assessment.
Results valid for six months.

**Security Clearance**: Background verification coordinated by Ministry of Labor.
Processing time varies by nationality and position sensitivity.

**Application Process**:
1. Employer obtains work permit (4-8 weeks)
2. Employee submits visa application with permit reference
3. Embassy verifies permit authenticity
4. Visa issued with duration matching work permit

**Family Accompaniment**: Work visa holders may sponsor dependent visas for
spouse and minor children. Dependents may not work without separate authorization.

**Renewal and Transfer**: Extensions require renewed work permit. Changing
employers requires new work permit and visa application.
"""

