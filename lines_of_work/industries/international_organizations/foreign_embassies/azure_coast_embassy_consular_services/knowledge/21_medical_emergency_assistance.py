title = "Medical Emergency Assistance"

content = """
Medical emergencies abroad require rapid, coordinated response. The Embassy
assists citizens facing hospitalization, serious illness, or injury while abroad.

**Emergency Response Protocol**:

**Notification**: Upon receiving medical emergency report:
1. Verify citizen identity and medical facility
2. Offer immediate assistance
3. With consent, contact designated emergency contacts
4. Coordinate with medical providers

**Hospital Liaison**: Embassy officers can:
- Visit hospitalized citizens
- Communicate with medical staff regarding treatment and prognosis
- Clarify medical information for family members
- Ensure cultural and linguistic needs are addressed

**Medical Evacuation**: When local treatment insufficient:
- Assess medical necessity with attending physicians
- Coordinate with insurance providers
- Arrange air ambulance or medical escort services
- Facilitate entry to Azure Coast for emergency care if appropriate

**Financial Assistance**: Limited emergency funds available:
- Medical evacuation loans for destitute citizens
- Repatriation loans for discharged patients unable to travel independently
- Must sign promissory note agreeing to repay

**Documentation Services**:
- Authenticate medical documents for insurance claims
- Issue statements for travel insurance providers
- Coordinate death certificate issuance if needed

**Prescription Medications**: For travelers who lost or depleted medications:
- Contact prescribing physicians for replacement prescriptions
- Facilitate local pharmacy filling when possible
- Advise on equivalent medications available locally

**Mental Health Crises**: For psychiatric emergencies:
- Coordinate with local mental health services
- Facilitate family involvement in treatment decisions
- Assist with voluntary or involuntary commitment procedures per local law

**Medical Tourism**: Note: Embassy does not recommend or vet medical providers.
Citizens seeking elective treatment abroad assume responsibility for provider
selection and risk assessment.

**Advance Directives**: Encourage citizens to carry copies of advance medical
directives and medical power of attorney when traveling.
"""

