title = "Civil Registration: Birth Registration"

content = """
Birth registration establishes Azure Coast nationality for children born abroad
to at least one Azure Coast citizen parent. Registration must occur within
specified timeframes to ensure citizenship rights.

**Eligibility**: Child eligible for Azure Coast citizenship if:
- Born abroad to at least one Azure Coast citizen parent, OR
- Born in Azure Coast to foreign parents with specific residency requirements

**Registration Timeframes**:
- Ideal: Within 30 days of birth
- Acceptable: Within 1 year without additional requirements
- Late registration (1-18 years): Possible with additional documentation
- Adults: Registration through citizenship verification process

**Required Documentation**:

**Parents**:
- Both parents' valid Azure Coast passports or citizenship certificates
- Parents' marriage certificate (if married)
- If unmarried: Affidavit of paternity acknowledgment (father) or sole custody
documentation (mother)

**Child**:
- Foreign birth certificate (original with apostille or legalization)
- Certified translation of birth certificate if not in English or Azure Coast
official language
- Child's passport if already issued by birth country

**Application Process**:
1. Schedule appointment for birth registration
2. Submit documentation for review
3. Consular officer interviews parents to confirm eligibility
4. Registration processed in Azure Coast civil registry system
5. Certificate of Birth Abroad issued
6. Passport application may proceed simultaneously

**Citizenship Determination**: Children born abroad acquire citizenship at birth
if at least one parent was citizen at time of child's birth. Registration confirms
and documents this automatic acquisition.

**Dual Nationality**: Azure Coast permits dual nationality. Children acquiring
other nationalities at birth retain Azure Coast citizenship without renunciation
requirement.

**Subsequent Passport**: Once registered, child eligible for Azure Coast passport.
Minor passports valid 5 years, require both parents' consent.
"""

