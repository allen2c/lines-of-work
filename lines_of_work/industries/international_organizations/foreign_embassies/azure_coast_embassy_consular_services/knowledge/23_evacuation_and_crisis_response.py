title = "Evacuation and Crisis Response"

content = """
During crises affecting Azure Coast citizens abroad (natural disasters, political
unrest, terrorism, pandemics), the Embassy coordinates protective response
including evacuation when necessary.

**Crisis Assessment**:
Embassy continuously monitors:
- Natural disaster warnings and impacts
- Political stability and civil unrest
- Security threat assessments
- Public health emergencies
- Transportation disruptions

**Communication During Crises**:
- Activate emergency notification systems (email, SMS, social media)
- Update travel advisories on Embassy website
- Establish hotlines for citizen inquiries
- Coordinate with local authorities and other diplomatic missions

**Evacuation Decisions**: Evacuation authorized when:
- Imminent threat to citizen safety exists
- Local infrastructure collapsed or unavailable
- Medical care inaccessible
- No other adequate protection available

Evacuation is last resort; sheltering in place or departure via commercial
channels preferred when feasible.

**Evacuation Types**:

**Assisted Departure**: Facilitate commercial transport
- Coordinate with airlines for additional capacity
- Provide ground transport to airports
- Issue emergency travel documents as needed
- Cost borne by travelers

**Emergency Evacuation**: Government-organized transport
- Charter aircraft or sea vessels when commercial options unavailable
- Priority given to vulnerable populations (medical needs, minors, elderly)
- Evacuees sign promissory notes for repayment of transport costs

**Preparation for Evacuation**:
- Register in advance at travel.azurecoast.gov for priority notification
- Maintain valid travel documents
- Keep emergency contact information updated
- Understand that pets typically cannot be evacuated
- Pack essential medications and documents

**Wardens System**: Embassy maintains volunteer wardens in major cities to
assist with local communication and coordination during crises.

**Post-Evacuation**: Citizens evacuated to safe locations receive:
- Temporary accommodation assistance
- Guidance on onward travel to Azure Coast
- Limited emergency financial assistance for destitute travelers
"""

