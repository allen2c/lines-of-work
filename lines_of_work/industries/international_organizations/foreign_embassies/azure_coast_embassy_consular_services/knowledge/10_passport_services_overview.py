title = "Passport Services Overview"

content = """
The Embassy provides comprehensive passport services to Azure Coast citizens
residing abroad or traveling temporarily. Services include new passports,
renewals, replacements for lost or damaged documents, and emergency travel
documents.

**Passport Types**:

**Regular Passport (Blue)**: Standard 10-year validity for adults, 5-year for
minors. 48 pages. Available to all citizens.

**Official Passport (Green)**: Issued to government employees traveling on
official business. Valid for duration of assignment or 5 years, whichever is
shorter. Requires authorization from Ministry of Foreign Affairs.

**Diplomatic Passport (Red)**: Issued to diplomats, high-ranking officials, and
their dependents. Valid 5 years. Requires diplomatic appointment confirmation.

**Temporary Passport (Emergency)**: Limited validity travel document for urgent
travel when standard passport unavailable. Valid for single journey or maximum
30 days. Not valid for visa-required destinations.

**Service Availability**:
- First-time adult applications (by appointment)
- Passport renewals (by appointment or mail)
- Lost/stolen passport replacement (emergency appointment)
- Damaged passport replacement (by appointment)
- Minor passport applications (both parents required)
- Name changes and data corrections (by appointment)

**Processing Times**:
- Standard processing: 15-20 business days
- Expedited processing: 5-7 business days (additional fee)
- Emergency travel document: Same day if approved

**Validity Rules**: Adult passports valid 10 years from issue. Minor passports
valid 5 years. Passports valid for travel until expiration date; some destinations
require 6 months validity beyond arrival.

**Security Features**: Current generation passports include biometric chip,
watermarks, holographic images, and UV-reactive elements to prevent forgery.
"""

