content = """
Citizenship services include verification, documentation, renunciation, and
restoration of Azure Coast nationality status for eligible individuals.

**Citizenship Verification**: Individuals claiming citizenship by birth,
descent, or naturalization may request verification letters for employment,
education, or legal purposes.

**Required for Verification**:
- Valid Azure Coast passport or identity document, OR
- Birth certificate showing Azure Coast birthplace, OR
- Birth certificate showing Azure Coast citizen parent with parent's citizenship
  documentation, OR
- Naturalization certificate

**Renunciation of Citizenship**: Azure Coast citizens may voluntarily renounce
nationality, typically to acquire citizenship of countries prohibiting dual
nationality.

**Renunciation Requirements**:
- Age 18 or older
- Mental capacity to understand consequences
- Another citizenship already acquired or guaranteed upon renunciation
- No pending criminal proceedings in Azure Coast
- No outstanding military service obligations
- No significant tax arrears

**Renunciation Process**:
1. Schedule appointment and declare intent
2. Complete renunciation declaration form
3. Swear oath of renunciation before consular officer
4. Documents forwarded to Ministry of Interior for approval
5. Certificate of Loss of Nationality issued upon approval
6. Passport surrendered for cancellation

**Note**: Renunciation is irrevocable. Restoration requires subsequent
naturalization with same requirements as foreign nationals.

**Citizenship Restoration**: Former citizens who lost nationality through
renunciation or other means may apply for restoration under limited circumstances.

**Restoration Eligibility**:
- Renunciation before age 25 may be reconsidered
- Loss due to parent's actions during minority
- Exceptional service to Azure Coast interests

**Statelessness Prevention**: The Embassy prevents statelessness by ensuring
renouncing citizens possess alternative nationality and by registering births
of citizen children abroad.
"""

