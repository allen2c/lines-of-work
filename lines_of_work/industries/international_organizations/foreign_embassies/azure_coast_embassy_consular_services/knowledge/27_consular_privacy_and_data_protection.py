title = "Consular Privacy and Data Protection"

content = """
The Embassy maintains strict confidentiality and data protection standards
for all consular services, balancing transparency with privacy requirements.

**Privacy Principles**:

**Confidentiality**: All consular interactions are confidential. Information
shared with consular officers protected under:
- Azure Coast data protection laws
- Diplomatic privilege and immunities
- Professional ethics standards

**Information Collection**: The Embassy collects only information necessary
for consular services:
- Identity verification data
- Service-specific requirements
- Emergency contact information
- Financial information for fee processing

**Data Storage and Security**:
- Physical documents stored in secure, access-controlled facilities
- Electronic data encrypted and access-restricted
- Personnel with consular data access subject to confidentiality agreements
- Records maintained per Azure Coast archival requirements

**Information Sharing**:

**Within Consular Section**: Information shared among officers as necessary
to provide seamless service and maintain accurate records.

**With Other Embassy Sections**: Limited sharing for:
- Security threat assessment (with appropriate authorization)
- Coordination of high-level visitor arrangements
- Emergency response coordination

**With Azure Coast Authorities**: Consular reports transmitted to:
- Ministry of Foreign Affairs (citizen registration)
- Civil Registry (birth, marriage, death records)
- Ministry of Interior (passport and citizenship matters)
- Law enforcement (only with consent or legal compulsion)

**With Third Parties**: Information disclosed only with:
- Applicant/citizen explicit written consent
- Legal compulsion (court order, treaty obligation)
- Emergency situations involving imminent harm

**Consent Requirements**: For family notification, legal representative
contact, or medical information sharing, the Embassy obtains explicit consent
except where individual unable to provide consent (medical emergency, death).

**Data Subject Rights**: Individuals may:
- Request access to their consular records
- Request correction of inaccurate information
- Request deletion of unnecessary retained data
- File complaints regarding data handling

**Retention Periods**:
- Passport records: 25 years
- Visa applications: 10 years
- Civil registration: Permanent
- Emergency assistance: 7 years

**Breach Notification**: In event of data breach affecting personal information,
affected individuals notified promptly per Azure Coast requirements.
"""

