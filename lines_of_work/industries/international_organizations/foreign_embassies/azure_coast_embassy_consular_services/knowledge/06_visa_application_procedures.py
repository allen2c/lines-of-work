title = "Visa Application Procedures"

content = """
The Azure Coast Embassy maintains a structured visa application process designed
to ensure efficient processing while maintaining security standards.

**Step 1: Online Application**: All applicants must complete the electronic
application form at visa.azurecoast.gov. The system generates a unique application
reference number and barcode page required for submission.

**Step 2: Document Preparation**: Gather all required documents according to
visa category checklist. Ensure documents meet format requirements: valid
issuance dates, proper certifications, clear photocopies.

**Step 3: Appointment Scheduling**: Book appointment online or through call center.
Walk-in applicants without appointments are served only if slots available.
Appointment confirmation required for entry to consular section.

**Step 4: In-Person Submission**: Attend appointment with:
- Printed application form with barcode
- All original documents and photocopies
- Passport photos meeting specifications
- Payment receipt or prepared payment
- Valid identification

Biometric data (fingerprints and photo) collected during submission.

**Step 5: Processing**: Standard processing times vary by category:
- Tourist: 5-10 business days
- Business: 7-14 business days
- Student: 10-20 business days
- Work: 15-30 business days

Expedited processing available for urgent cases with additional fee.

**Step 6: Collection**: Applicants receive notification when passport ready for
collection. Must present collection slip and identification. Authorized
representatives require power of attorney and their ID.

**Tracking**: Application status available online using reference number.
Updates provided at key stages: received, under review, decision made, ready for
collection.

**Refusals**: Written explanation provided for all refusals citing specific
regulatory grounds. Appeal procedures explained upon request.
"""

