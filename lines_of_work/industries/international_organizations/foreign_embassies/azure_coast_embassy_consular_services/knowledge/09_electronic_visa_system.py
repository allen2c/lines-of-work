title = "Electronic Visa (e-Visa) System"

content = """
The Azure Coast e-Visa system provides streamlined visa processing for eligible
nationalities seeking short-term tourist or business visits.

**Eligible Nationalities**: Citizens of 85 designated countries may apply for
e-Visa. Eligibility based on bilateral agreements, security assessments, and
immigration compliance history. Check current eligibility at evisa.azurecoast.gov
before applying.

**e-Visa Types**:
- **e-Tourist**: Single entry, 30 days maximum stay
- **e-Business**: Single entry, 30 days maximum stay

**Application Process**:
1. Complete online form at evisa.azurecoast.gov
2. Upload passport copy (bio-data page) and digital photograph
3. Pay fee ($60 USD) by credit or debit card
4. Receive application confirmation with reference number
5. Receive decision via email within 3 business days

**Required Documents**:
- Valid passport (minimum 6 months validity beyond intended stay)
- Digital passport photograph (specifications on website)
- Valid email address for communications
- Credit/debit card for payment

**Advantages**:
- No Embassy visit required
- Faster processing than paper applications
- 24/7 application availability
- Direct delivery to email

**Limitations**:
- Single entry only
- 30-day maximum stay (not extendable)
- No status changes permitted within Azure Coast
- No work authorization
- Must enter through designated ports of entry (international airports and
  major seaports only)

**Entry Requirements**: Print e-Visa approval and present with passport at
immigration. Electronic verification available at border but printed copy
recommended.

**Rejection**: Rejected e-Visa applicants may apply for regular visa at Embassy
with additional documentation addressing rejection grounds.

**System Availability**: e-Visa system undergoes periodic maintenance. Apply
at least one week before travel to accommodate technical issues.
"""

