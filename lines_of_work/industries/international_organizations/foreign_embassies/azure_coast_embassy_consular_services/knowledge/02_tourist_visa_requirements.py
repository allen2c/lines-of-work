title = "Tourist Visa Requirements"

content = """
Tourist visa applications require comprehensive documentation to establish the
applicant's identity, financial capacity, and genuine intent to visit temporarily.

**Passport Requirements**: Valid for at least six months beyond intended departure
from Azure Coast, with at least two blank visa pages. Previous passports should be
included if they contain relevant travel history.

**Application Form**: Completed online at visa.azurecoast.gov, printed and signed.
All fields must be completed; incomplete forms are rejected without review.

**Photographs**: Two recent passport-sized photos (35mm x 45mm), white background,
no glasses, neutral expression. Photos must be less than six months old and not
used in previous visas.

**Travel Itinerary**: Confirmed round-trip flight reservation. Do not purchase
non-refundable tickets before visa approval. Hotel reservations or invitation
letter from host in Azure Coast with their ID copy.

**Financial Proof**: Bank statements for last three months showing sufficient funds
(minimum $50 USD per day of stay). Employment verification letter or business
registration if self-employed. Sponsorship letter with sponsor's financial
documents if applicable.

**Travel Insurance**: Mandatory coverage of minimum $30,000 USD for medical
emergencies and repatriation. Policy must be valid for entire Azure Coast stay.

**Purpose Statement**: Brief letter explaining travel purpose, intended activities,
and confirmation of intent to return home. Vague or generic statements raise
concerns about genuine tourist purpose.

**Fee Payment**: Non-refundable $80 USD, payable by money order, cashier's check,
or credit card (in person only). Cash not accepted by mail.

Additional documents may be requested based on applicant nationality, travel
history, or individual circumstances.
"""

