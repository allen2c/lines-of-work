title = "Visa Categories Overview"

content = """
The Azure Coast Embassy issues several visa categories based on the purpose and
duration of the intended stay. Understanding the correct category is essential for
a successful application.

**Tourist Visa (TV)**: For leisure travel, sightseeing, visiting friends or family.
Standard validity is 90 days single or multiple entry. Processing time: 5-10 business
days. Fee: $80 USD.

**Business Visa (BV)**: For commercial activities, meetings, conferences, trade fairs.
Requires invitation letter from Azure Coast registered company. Validity: 90 days to
1 year multiple entry. Processing: 7-14 days. Fee: $120 USD.

**Student Visa (SV)**: For enrolled students at accredited Azure Coast educational
institutions. Requires official acceptance letter and proof of financial means.
Validity: Duration of study program. Processing: 10-20 days. Fee: $100 USD.

**Work Visa (WV)**: For employment with Azure Coast companies. Requires work permit
approval from Ministry of Labor prior to application. Validity: Linked to employment
contract, up to 2 years renewable. Processing: 15-30 days. Fee: $200 USD.

**Transit Visa (TRV)**: For passing through Azure Coast to a third destination.
Maximum stay 72 hours. Requires confirmed onward ticket and visa for destination
if required. Processing: 3-5 days. Fee: $40 USD.

**Diplomatic/Official Visa (DV/OV)**: For holders of diplomatic or official passports
traveling on government business. Processed through diplomatic channels. No fee.

**Electronic Visa (e-Visa)**: Available for tourist and business purposes for eligible
nationalities. Single entry, 30 days. Applied online at evisa.azurecoast.gov.
Processing: 3 business days. Fee: $60 USD.

Selecting the wrong category is a common reason for application rejection. Applicants
must demonstrate that their stated purpose aligns with the visa category requested.
"""

