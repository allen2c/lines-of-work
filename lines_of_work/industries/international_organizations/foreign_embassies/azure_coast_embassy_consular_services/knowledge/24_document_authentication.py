title = "Document Authentication Services"

content = """
Document authentication services verify the authenticity of Azure Coast documents
for use abroad, and foreign documents for use in Azure Coast. Services include
apostilles, certifications, and legalizations.

**Apostille Service**: Under the Hague Apostille Convention (1961), apostilles
authenticate documents for use in other Convention member countries.

**Documents Eligible for Apostille**:
- Birth, marriage, death certificates
- Court orders and judgments
- Notarial acts
- Government-issued documents with official signatures
- Educational records (with verification from issuing institution)

**Apostille Process**:
1. Submit original document or certified copy
2. Embassy verifies signature of issuing authority against registry
3. Apostille certificate attached to document
4. Document valid for use in all Hague Convention countries

**Processing**: 3-5 business days. Fee: $50 per document.

**Certification (Non-Hague Countries)**: For countries not party to Hague
Convention, documents require full legalization chain:
1. Notarization (if private document)
2. Authentication by host country Foreign Ministry
3. Legalization by Azure Coast Embassy

**Certification Process**:
- Submit document with host country authentication
- Embassy verifies Foreign Ministry signature
- Legalization certificate attached
- Document valid for use in destination country

**Certified True Copies**: Embassy certifies copies of Azure Coast documents
as true reproductions of originals. Process:
- Present original document
- Submit copy to be certified
- Officer compares and certifies accuracy
- Fee: $20 per document

**Educational Documents**: Diplomas and transcripts require:
- Verification from issuing educational institution
- Notarization of verification
- Apostille or legalization of notarized verification

**Commercial Documents**: Business contracts, corporate records require:
- Notarization
- Chamber of Commerce certification
- Apostille or legalization

**Validity**: Authenticated documents typically valid indefinitely, though some
jurisdictions may require recent issuance (within 6-12 months).
"""

