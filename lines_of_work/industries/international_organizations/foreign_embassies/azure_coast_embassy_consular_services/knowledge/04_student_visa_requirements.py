title = "Student Visa Requirements"

content = """
Student visas are issued to foreign nationals enrolled in accredited educational
institutions in Azure Coast. The application process requires coordination between
the student, the educational institution, and the Embassy.

**Acceptance Letter**: Official admission letter from accredited Azure Coast
university, college, or language school. Letter must specify program duration,
tuition fees, and enrollment status (full-time required).

**Financial Capacity**: Comprehensive evidence of ability to cover tuition and
living expenses:
- Bank statements showing sufficient funds (minimum $15,000 USD per academic year)
- Scholarship award letter if applicable
- Sponsorship affidavit with sponsor's financial documents and relationship proof
- Proof of prepaid tuition or housing if applicable

**Academic Credentials**: Transcripts and diplomas from previous educational
institutions. Documents not in English or Azure Coast official language require
certified translation.

**Health Requirements**: Medical examination by approved physician within 30
days of application. Required tests include chest X-ray for tuberculosis and
blood screening for communicable diseases. Vaccination records must be current.

**Criminal Background Check**: Police clearance certificate from all countries
of residence in past five years. Certificates must be apostilled or legalized
according to origin country requirements.

**Health Insurance**: Proof of comprehensive health insurance valid in Azure
Coast for entire study period. Minimum coverage $50,000 USD including medical,
accident, and repatriation.

**Statement of Purpose**: Essay explaining educational goals, choice of Azure
Coast institution, and post-graduation plans. Must demonstrate genuine student
intent, not immigration motive.

**Visa Duration**: Issued for program length plus 30 days. Extensions require
continued enrollment and satisfactory academic progress.
"""

