"""Knowledge item: Antragsformulare richtig ausfüllen."""

title: str = "Antragsformulare richtig ausfüllen"

content: str = """
Antragsformulare für Visa und konsularische Leistungen müssen vollständig, lesbar
und wahrheitsgemäß ausgefüllt werden. Unvollständige oder fehlerhafte Angaben
führen zu Verzögerungen oder Ablehnung.

**Vollständigkeit:** Jedes Pflichtfeld muss ausgefüllt sein. Bei nicht zutreffenden
Feldern „N/A" oder „nicht zutreffend" eintragen, nicht leer lassen. Fehlende
Unterschriften oder fehlende Datumsangaben führen zur Zurückweisung.

**Lesbarkeit:** Druckbuchstaben oder maschinenschriftlich. Keine Durchstreichungen
oder Radierungen. Bei Korrekturen initialisieren und Datum setzen.

**Wahrheitsgemäßigkeit:** Falsche Angaben können zur Ablehnung, Sperre und
strafrechtlichen Verfolgung führen. Änderungen der Lebensumstände nach Einreichung
müssen umgehend mitgeteilt werden.
"""

