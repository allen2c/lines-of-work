title = "Transitvisum und Flughafentransit"

content = """
Reisende, die durch einen Staat nur durchreisen, ohne einzureisen, benötigen
unter Umständen ein Transitvisum. Die Regelungen unterscheiden sich nach
Staat und Nationalität des Reisenden.

**Flughafentransit (A-Regime):** Einige Staaten verlangen auch für den
Aufenthalt in der Transitzone ein Visum. Betrifft oft bestimmte Nationalitäten.
Ohne Visum darf die Transitzone nicht verlassen werden.

**Transit durch Schengen:** Bei Einreise in den Schengen-Raum (auch nur
Transit) gilt das Schengen-Visum. Bei reinem Flughafentransit ohne
Grenzübertritt kann für manche Nationalitäten ein Flughafentransitvisum
(Airport Transit Visa) nötig sein.

**Landtransit:** Bei Durchreise per Bahn oder Auto gelten die normalen
Einreisebestimmungen des Durchreisestaats.

Reisende sollten vor Buchung prüfen, ob für ihre Nationalität und Route
ein Transitvisum erforderlich ist. Fehlende Papiere führen zur
Zurückweisung an der Grenze.
"""  # noqa: E501

