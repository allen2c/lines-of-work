"""Knowledge item: Fotoanforderungen für Dokumente."""

title: str = "Fotoanforderungen für Dokumente"

content: str = """
Passfotos für Visa und Reisepässe müssen bestimmte technische und inhaltliche
Anforderungen erfüllen. Nicht konforme Fotos führen zur Ablehnung des Antrags.

**Format:** Biometrisches Format (35 x 45 mm), neutraler Hintergrund (hellgrau
oder weiß), gleichmäßige Beleuchtung. Das Gesicht muss 70–80 Prozent des Fotos
einnehmen.

**Aktualität:** Fotos dürfen nicht älter als sechs Monate sein. Bei Kindern
unter sechs Jahren maximal drei Monate. Änderungen des Aussehens (Bart, Brille,
Kopfbedeckung außer aus religiösen Gründen) erfordern neue Fotos.

**Qualität:** Scharf, ohne Reflexionen, ohne Schatten. Keine digitalen Retuschen.
Bei Brillenträgern: keine Reflexionen auf den Gläsern, Augen müssen sichtbar sein.
"""

