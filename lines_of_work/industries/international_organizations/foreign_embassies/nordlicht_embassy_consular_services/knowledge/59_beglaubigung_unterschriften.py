"""Knowledge item: Beglaubigung von Unterschriften."""

title: str = "Beglaubigung von Unterschriften"

content: str = """
Die Botschaft beglaubigt Unterschriften auf Verträgen, Vollmachten und
Anträgen, sofern der Unterzeichner persönlich erscheint und sich
ausweist. Die Beglaubigung bestätigt die Identität des Unterzeichners,
nicht den Inhalt des Dokuments. Bestimmte Dokumente (z.B. notarielle
Urkunden) werden nicht beglaubigt.
"""  # noqa: E501

