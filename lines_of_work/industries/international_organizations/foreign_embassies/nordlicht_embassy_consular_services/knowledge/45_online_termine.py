title: str = "Online-Terminbuchung"

content: str = """
Die meisten konsularischen Leistungen erfordern einen vorher vereinbarten Termin.
Die Terminbuchung erfolgt über das Online-System der Botschaft.

**Verfügbarkeit:** Termine werden in der Regel zwei bis vier Wochen im Voraus
freigeschaltet. Bei hoher Nachfrage (z.B. vor Ferienzeiten) sind Termine schnell
vergeben. Regelmäßige Aktualisierung der Seite wird empfohlen.

**Pro Antragsteller ein Termin:** Jede Person, die persönlich erscheinen muss,
benötigt einen eigenen Termin. Begleitpersonen von Minderjährigen oder
Hilfsbedürftigen können im selben Termin mit aufgenommen werden.

**Absage und Verschiebung:** Bei Verhinderung den Termin rechtzeitig stornieren,
damit andere Antragsteller die Kapazität nutzen können. Wiederholtes unentschuldigtes
Fernbleiben kann zu Sperren führen.
"""

