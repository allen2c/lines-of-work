title = "Unfall oder schwere Erkrankung im Ausland"

content = """
Bei Unfällen oder schweren Erkrankungen von Staatsangehörigen im Ausland kann
die konsularische Vertretung unterstützen. Die Hilfe umfasst die Benachrichtigung
von Angehörigen, die Vermittlung von Ärzten oder Krankenhäusern und die
Koordination bei Rücktransporten.

**Benachrichtigung:** Auf Wunsch des Betroffenen oder mit dessen Einwilligung
informiert die Vertretung Angehörige im Heimatland. Dafür ist eine
Vollmacht oder die ausdrückliche Zustimmung des Betroffenen erforderlich.

**Medizinische Vermittlung:** Die Vertretung führt Listen von Ärzten und
Kliniken, die Staatsangehörige des Heimatlandes behandeln. Sie vermittelt
keine medizinische Behandlung, sondern gibt Empfehlungen.

**Rücktransport:** Die Kosten für einen medizinischen Rücktransport trägt
der Betroffene oder dessen Krankenversicherung. Die Vertretung kann bei der
Organisation helfen und mit Versicherungen oder Rettungsdiensten vermitteln.
"""  # noqa: E501

