"""Knowledge item: Antragseinreichung und Abholung."""

title: str = "Antragseinreichung und Abholung"

content: str = """
Anträge können persönlich, per Post oder über beauftragte Dritte eingereicht werden.
Die persönliche Vorsprache ist bei Erstvisa und in sensiblen Fällen erforderlich.
Bei Abholung muss der Antragsteller oder ein bevollmächtigter Vertreter mit
gültigem Ausweis erscheinen. Vollmachten müssen notariell beglaubigt sein.
"""  # noqa: E501

