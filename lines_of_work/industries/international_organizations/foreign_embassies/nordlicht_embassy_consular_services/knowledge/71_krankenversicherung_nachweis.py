title: str = "Krankenversicherungsnachweis für Visa"

content: str = """
Für Schengen- und nationale Visa ist in der Regel eine
Reisekrankenversicherung mit Mindestdeckung erforderlich. Die Police
muss für den gesamten Aufenthaltszeitraum gültig sein und Rückführung
abdecken. Die Botschaft prüft die Angaben und lehnt unzureichende
Nachweise ab. EU-Bürger nutzen die Europäische Krankenversicherungskarte.
"""  # noqa: E501

