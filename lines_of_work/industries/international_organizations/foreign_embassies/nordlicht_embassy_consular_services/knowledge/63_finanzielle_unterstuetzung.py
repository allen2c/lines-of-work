"""Knowledge item: Finanzielle Unterstützung in Notfällen."""

title: str = "Finanzielle Unterstützung in Notfällen"

content: str = """
In echten Notfällen (Verlust aller Mittel, Krankheit, Katastrophe) kann
die Botschaft Darlehen für die Rückreise gewähren. Voraussetzung ist
die Staatsangehörigkeit und die Zahlungsunfähigkeit. Das Darlehen muss
nach der Rückkehr zurückgezahlt werden. Keine Unterstützung bei
freiwilligem Aufenthalt ohne Mittel.
"""  # noqa: E501

