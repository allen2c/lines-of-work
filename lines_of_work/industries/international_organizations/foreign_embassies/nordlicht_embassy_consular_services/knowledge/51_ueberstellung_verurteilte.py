title = "Überstellung verurteilter Staatsangehöriger"

content = """
Staatsangehörige, die im Ausland eine Freiheitsstrafe verbüßen, können unter
bestimmten Voraussetzungen in das Heimatland überstellt werden. Das Verfahren
richtet sich nach bilateralen oder multilateralen Abkommen.

**Voraussetzungen:** Beidseitige Zustimmung, Reststrafe mindestens 6 Monate,
keine ausstehenden Verfahren im Gastland. Der Verurteilte muss der Überstellung
zustimmen.

**Rolle der Botschaft:** Vermittlung zwischen den Justizbehörden beider Länder,
Information des Verurteilten über das Verfahren. Die Botschaft entscheidet
nicht über die Überstellung; diese obliegt den zuständigen Ministerien.
"""  # noqa: E501

