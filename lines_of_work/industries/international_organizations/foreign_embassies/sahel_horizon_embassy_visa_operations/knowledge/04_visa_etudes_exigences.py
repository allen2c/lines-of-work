title = "Exigences du visa études"

content = """
Le visa études est délivré aux personnes admises dans un établissement
d'enseignement reconnu pour suivre une formation à temps plein.

**Admission académique** : Certificat d'inscription ou lettre d'admission
officielle de l'établissement, précisant la formation, la durée et le
calendrier. Les établissements privés doivent être agréés par le Ministère
de l'Éducation nationale.

**Moyens de subsistance** : Justification des ressources pour la durée du
séjour (bourse, garantie bancaire, attestation de prise en charge par un
tuteur ou une institution). Le montant minimal est défini par circulaire
et révisé annuellement.

**Assurance santé** : Couverture des frais médicaux et d'hospitalisation
pour toute la période d'études. Les mutuelles étudiantes reconnues sont
acceptées sous réserve de conformité territoriale.

**Cas des mineurs** : Autorisation parentale notariée si le mineur voyage
seul, procuration du tuteur légal si hébergement chez un tiers.

Le visa études peut inclure une autorisation de travail à temps partiel
selon la réglementation en vigueur ; cette disposition doit être explicitement
mentionnée sur le visa.
"""  # noqa: E501

