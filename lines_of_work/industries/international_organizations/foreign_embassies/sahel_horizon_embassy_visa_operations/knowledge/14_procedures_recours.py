content = """
En cas de refus de visa, le demandeur dispose de voies de recours pour
contester la décision. Les délais et modalités sont stricts.

**Recours gracieux** : Adressé à l'autorité ayant pris la décision
(Consul ou Ambassadeur). Doit être envoyé dans un délai fixé par la
notification (généralement un à deux mois). Pièces à joindre : copie
de la décision de refus, argumentation, éventuels documents complémentaires.

**Recours hiérarchique** : Dirigé vers le Ministère des Affaires étrangères
ou l'autorité compétente en matière d'immigration. Mêmes exigences de délai
et de motivation.

**Recours contentieux** : Devant le tribunal administratif compétent.
Délai de deux mois à compter de la notification. L'assistance d'un avocat
est recommandée. La décision du tribunal peut annuler le refus et enjoindre
à réexaminer le dossier.

**Nouvelle demande** : En parallèle ou après épuisement des recours, une
nouvelle demande peut être déposée si la situation a évolué (nouveaux
documents, changement de circonstances). Les motifs du refus précédent
doivent être pris en compte pour éviter une répétition.

Aucun recours ne suspend la décision de refus. Le demandeur ne peut
entrer sur le territoire qu'après obtention d'un visa ou d'une décision
favorable en recours.
"""  # noqa: E501

