title = "Frais consulaires et modes de paiement"

content = """
Les frais consulaires sont perçus pour chaque demande de visa. Ils couvrent
les coûts de traitement et ne sont pas remboursés en cas de refus, sauf
disposition légale ou règlementaire contraire.

**Barème** : Variable selon la catégorie de visa (court séjour, long séjour,
transit, etc.). Les tarifs sont fixés par arrêté et publiés sur le site
de l'ambassade et du Ministère des Affaires étrangères.

**Exemptions** : Enfants en dessous d'un âge défini, titulaires de passeports
diplomatiques ou de service, certaines catégories de chercheurs ou d'artistes
selon accords bilatéraux, participants à des événements officiels. Une
attestation ou une liste d'exemption doit être produite.

**Modes de paiement** : Espèces (monnaie locale ou euros selon le poste),
carte bancaire internationale, virement selon les modalités affichées.
Les chèques ne sont en général pas acceptés.

**Prestataire** : Lorsque le dépôt est délégué à un prestataire, des frais
de service additionnels peuvent s'appliquer. Ils sont distincts des frais
consulaires et doivent être clairement indiqués.

**Reçu** : Un reçu officialisant le paiement des frais consulaires est
délivré au dépôt. À conserver pour tout suivi ou réclamation.
"""  # noqa: E501

