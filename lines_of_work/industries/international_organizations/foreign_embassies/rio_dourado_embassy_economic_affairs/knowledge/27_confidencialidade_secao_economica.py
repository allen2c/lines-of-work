title = "Confidencialidade na Seção Econômica"

content = """
A Seção Econômica trata a informação recebida de empresas e de entidades com
confidencialidade, em conformidade com as práticas diplomáticas e com a protecção
de dados. Dados comerciais sensíveis (estratégia, preços, contratos) não são
partilhados com terceiros sem consentimento; a informação agregada ou de carácter
genérico pode ser usada em relatórios internos ou em respostas a pedidos oficiais
que não identifiquem a empresa.

**Prática:** ao solicitar apoio (advocacy, contactos, informação), as empresas
podem indicar o que consideram confidencial. A Seção evita divulgar detalhes que
possam prejudicar a posição competitiva do requerente; em contrapartida, espera-se
que os pedidos sejam formulados com clareza e que a documentação necessária seja
fornecida quando aplicável.
"""

