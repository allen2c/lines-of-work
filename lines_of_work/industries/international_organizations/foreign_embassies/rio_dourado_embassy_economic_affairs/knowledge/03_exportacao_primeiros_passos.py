content = """
Empresas do Rio Dourado que queiram exportar para o país de acolhimento devem, em primeiro
lugar, verificar requisitos legais e técnicos no destino (normas, certificações, registos)
e no Rio Dourado (licenças, documentação aduaneira). A Seção Econômica pode orientar sobre
onde encontrar essa informação e sobre contactos úteis; não emite licenças nem substitui
consultoria jurídica ou aduaneira.

**Passos recomendados:** (1) definir o produto ou serviço e o mercado-alvo; (2) consultar
exigências regulatórias e técnicas no país de destino; (3) confirmar exigências no Rio Dourado
para exportação; (4) identificar distribuidores, importadores ou parceiros (a Embaixada pode
sugerir fontes e eventos); (5) preparar documentação e logística com apoio de despachantes
ou consultores quando necessário.
"""

