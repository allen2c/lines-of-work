title = "Saúde e Regulamentação na Exportação"

content = """
A exportação de produtos que afectem a saúde (medicamentos, dispositivos médicos,
cosméticos, suplementos) está sujeita a regulamentação específica no país de
destino: autorizações, ensaios, rotulagem. A Seção Econômica pode indicar as
autoridades reguladoras e os tipos de requisitos gerais; não presta assessoria
técnica nem valida dossiês de registo.

**Informação típica:** agência de medicamentos ou equivalente; exigências de
certificação para dispositivos médicos; normas de rotulagem e de alegações
nutricionais. Para cumprimento e submissão de dossiês, as empresas devem
recurrer a consultores regulatórios e às autoridades competentes.
"""

