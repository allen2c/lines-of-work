content = """
Barreiras não tarifárias são medidas que, sem ser tarifas, restringem ou condicionam o
comércio: quotas, licenças, normas técnicas, sanitárias ou fitossanitárias, procedimentos
aduaneiros complexos, restrições cambiais. A Seção Econômica pode explicar que tipos de
barreiras existem no país de acolhimento para determinados sectores e onde obter detalhes
(oficiais de comércio, organismos de normalização, ministérios sectoriais).

**Papel da Embaixada:** informar e, em casos de barreiras que violem acordos ou tratem
empresas do Rio Dourado de forma discriminatória, apoiar advocacy junto das autoridades
locais ou no quadro de comités bilaterais. A remoção ou alteração da medida cabe sempre às
autoridades competentes; a Embaixada não derroga regulamentos.
"""

