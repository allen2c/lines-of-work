content = """
O Rio Dourado identifica setores prioritários para a promoção de exportações, em função da
capacidade produtiva nacional, da procura no mercado de acolhimento e da política comercial
bilateral. A lista pode incluir agronegócio, bens transformados, equipamentos, serviços (turismo,
logística, digital) e nichos de alto valor. A Seção Econômica pode informar quais setores são
prioritários no país de acolhimento e que tipo de apoio (missões, feiras, informação) está
disponível.

**Uso prático:** empresas desses setores tendem a ter maior acesso a eventos e materiais de
promoção. Empresas de outros setores continuam a poder solicitar informação geral e
encaminhamento; a priorização afecta recursos e calendário, não a elegibilidade para
orientação básica.
"""

