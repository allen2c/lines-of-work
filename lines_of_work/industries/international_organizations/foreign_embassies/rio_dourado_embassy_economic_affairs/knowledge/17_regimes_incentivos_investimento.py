title = "Regimes de Incentivos ao Investimento"

content = """
Tanto o Rio Dourado como o país de acolhimento podem oferecer incentivos fiscais,
financeiros ou regulatórios para atrair investimento em sectores ou regiões
específicos. A Seção Econômica pode descrever em termos gerais os regimes
existentes e indicar onde obter informação oficial (agência de investimentos,
ministério da economia, portais governamentais).

**Conteúdo típico:** tipos de incentivos (redução de IRC, créditos fiscais,
subsídios à criação de emprego, simplificação de licenciamento); condições de
elegibilidade; prazos e procedimentos de candidatura. A elegibilidade e o
benefício concreto dependem da análise das autoridades; a Embaixada não aprova
nem concede incentivos.
"""

