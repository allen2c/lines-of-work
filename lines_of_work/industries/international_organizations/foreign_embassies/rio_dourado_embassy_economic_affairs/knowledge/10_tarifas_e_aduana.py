content = """
As tarifas e os procedimentos aduaneiros do país de acolhimento (e do Rio Dourado na
exportação) são definidos por lei e regulamento; a Embaixada não fixa nem aplica tarifas.
A Seção Econômica pode indicar fontes oficiais (administração aduaneira, ministério do
comércio) e, quando disponível, resumos de alíquotas e regimes especiais (drawback, zonas
francas, regimes suspensivos).

**O que a Seção pode fazer:** explicar onde encontrar informação tarifária e de
classificação pautal; referir acordos que alterem alíquotas; indicar contactos da
administração aduaneira para dúvidas procedimentais. Para classificação pautal específica
ou litígios aduaneiros, as empresas devem recorrer a despachantes oficiais ou advogados
especializados.
"""

