title = "Gestion des risques dans les projets de coopération"

content = """
Les projets de coopération sont exposés à des risques multiples : sécuritaires,
politiques, financiers, climatiques, sanitaires. Une gestion proactive des
risques limite les dérives et préserve la continuité des activités.

**Identification** : Cartographie des risques en amont (analyse contextuelle,
retours d'expérience de projets similaires). Mise à jour régulière lors des
réunions de pilotage.

**Mitigation** : Mesures préventives (diversification des zones d'intervention,
renforcement des capacités locales, clauses de force majeure dans les contrats).
Pour les risques élevés, plans de contingence et scénarios de repli.

**Monitoring** : Indicateurs d'alerte (évolution du taux de change, incidents
sécuritaires, changements réglementaires). Escalade rapide vers le bailleur
et l'Ambassade en cas de matérialisation.

L'Ambassade suit l'évolution du contexte pays et peut recommander des
ajustements ou des suspensions temporaires si la situation le justifie.
"""  # noqa: E501

