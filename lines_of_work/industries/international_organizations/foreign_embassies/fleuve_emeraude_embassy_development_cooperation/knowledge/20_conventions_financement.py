title = "Conventions de financement et engagements contractuels"

content = """
Une convention de financement lie le bailleur au bénéficiaire. Elle définit
les objectifs, le montant, la durée, les conditions de décaissement et les
obligations de chaque partie. Sa lecture attentive est indispensable avant
toute signature.

**Clauses essentielles** : Objet et zone géographique, indicateurs de résultat,
calendrier des décaissements, procédures de rapportage, règles d'achat,
conditions de suspension ou de résiliation.

**Modifications** : Les avenants formalisent les changements (prolongation,
réallocation budgétaire, révision des indicateurs). Ils requièrent l'accord
écrit du bailleur.

**Force majeure** : Les conventions prévoient généralement les modalités
de gestion des événements imprévisibles (catastrophe naturelle, conflit,
pandémie). La notification rapide et la documentation sont cruciales.

L'Ambassade ne signe pas les conventions mais peut conseiller les porteurs
sur l'interprétation des clauses et les démarches auprès du bailleur.
"""  # noqa: E501

