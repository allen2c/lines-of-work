title = "Procédures d'achats et marchés publics"

content = """
Les achats dans le cadre des projets de coopération sont soumis à des règles
strictes définies par le bailleur. Le non-respect peut entraîner des refus
de remboursement ou des sanctions.

**Seuils et procédures** : En dessous d'un certain montant, un achat direct
ou une consultation restreinte peut suffire. Au-delà, appel d'offres ouvert,
publication des critères d'attribution, délais minimaux.

**Éligibilité des fournisseurs** : Certains bailleurs excluent les fournisseurs
de pays non éligibles ou imposent des préférences pour les acteurs locaux.
Les listes noires (corruption, fraude) sont vérifiées systématiquement.

**Documentation** : Devis comparatifs, procès-verbaux de sélection, contrats
signés. Les originaux sont conservés pour les audits.

L'Ambassade ne gère pas directement les marchés mais peut orienter les
porteurs de projet vers les procédures applicables et signaler les
incohérences détectées lors des missions de suivi.
"""  # noqa: E501

