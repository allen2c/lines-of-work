title = "Post-Event Follow-Up"

content = """
Post-event follow-up ensures closure, documents lessons learned, and
maintains relationships.

**Thank-You Notes**: Send written thanks to principal guests and key
participants within 48–72 hours. Personalize where possible. Mention
specific highlight or outcome.

**Debrief**: Conduct internal debrief with protocol and operations staff.
What went well? What would we do differently? Document for future
reference. Share lessons with relevant colleagues.

**Records**: Archive guest list, seating plan, program, photographs,
and vendor contracts. Update contact database. Retain records per
embassy retention policy.

**Relationship Building**: Use the event as a springboard for follow-up
engagement. Schedule follow-up meetings if discussed. Nurture new
contacts made at the event.
"""

