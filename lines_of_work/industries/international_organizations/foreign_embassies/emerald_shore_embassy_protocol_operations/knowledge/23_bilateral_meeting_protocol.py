title = "Bilateral Meeting Protocol"

content = """
Bilateral meetings between delegations require careful choreography of
attendance, seating, and documentation.

**Composition**: Agree delegation size and composition in advance. Principal
plus key aides. Interpreters if needed. Note-taker. Balance rank across
the table.

**Seating**: Principals opposite each other or at head of table. Delegations
arrayed by rank along each side. Host typically has back to window or
preferred position.

**Sequence**: Welcoming remarks, substantive discussion, closing. Host usually
opens and closes. Agree whether meeting is on or off the record before
beginning.

**Documentation**: Designate note-taker. Distribute summary or readout to
participants for clearance. Do not attribute specific quotes without
approval. Retain records per embassy procedures.
"""

