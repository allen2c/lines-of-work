content = """
Gift-giving in diplomatic contexts conveys respect and strengthens relationships.
Protocol governs appropriateness, timing, and presentation.

**Appropriateness**: Gifts should reflect national culture or bilateral ties
without excessive value. Avoid gifts that could be perceived as bribes.
Cultural items, art, books, and handicrafts are common. Ensure gifts are
suitable for the recipient's position and culture.

**Timing**: Gifts are typically presented at the opening or conclusion of
official meetings. At state visits, formal exchange may occur at arrival
ceremony or dinner. Document gifts for records and tax purposes where required.

**Presentation**: Present gifts with both hands when culturally appropriate.
Offer a brief explanation of significance. Recipients may open immediately
or defer according to custom.

**Reciprocity**: Be prepared to receive gifts of equivalent dignity. Thank the
donor promptly. Record all official gifts for inventory and compliance.
"""

