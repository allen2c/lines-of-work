title = "Precedence and Ranking"

content = """
Precedence determines the order in which officials are seated, introduced,
or listed at diplomatic events. Incorrect precedence can cause serious offense.

**Heads of State and Government**: Generally ranked by date of assumption of
office (chronological order). At bilateral events, host and guest alternate or
host yields to guest of honor. At multilateral summits, alphabetical order by
country name (in host language) is common.

**Ministers and Senior Officials**: Ranked by portfolio importance in national
tradition, then by seniority within portfolio. Foreign ministers typically
rank high. Defense, finance, and interior ministers follow in order customary
to the host state.

**Ambassadors**: Among ambassadors accredited to the same country, precedence
follows the date of presentation of credentials (doyen or dean). The longest-
serving ambassador is the dean of the diplomatic corps. Within an embassy,
staff rank by position and seniority.

**Bilateral Custom**: Some countries have agreed bilateral precedence that
deviates from general rules. Always verify before major events. Host country
protocol offices maintain definitive lists.
"""

