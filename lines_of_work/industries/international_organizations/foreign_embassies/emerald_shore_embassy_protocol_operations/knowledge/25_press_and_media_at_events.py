title = "Press and Media at Events"

content = """
Media coverage of diplomatic events requires coordination between protocol
and communications staff. Clear rules prevent disruption and manage message.

**Pool Arrangements**: For restricted access events, organize media pool.
Select representatives from major outlets. Pool shares content with others.
Brief pool on timing and restrictions.

**Photo Opportunities**: Designate moments for photography (handshake, signing,
group photo). Limit duration. Ensure good sightlines. Avoid blocking principals.

**Statement and Briefing**: Determine whether event is on or off the record.
Arrange press availability if desired. Prepare talking points for spokespersons.
Coordinate with host country communications.

**Sensitive Events**: For events involving high-level or sensitive participants,
tighten access. Consider embargo on coverage until after departure.
"""

