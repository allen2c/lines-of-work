title = "Cultural Sensitivity at Events"

content = """
Diplomatic events bring together diverse cultures. Sensitivity to custom and
tradition prevents offense and builds goodwill.

**Research**: Before events involving specific communities, research dietary
laws, religious observance, dress expectations, and social norms. Consult
community representatives when appropriate.

**Accommodation**: Provide halal, kosher, vegetarian, or vegan options as
needed. Respect prayer times in scheduling. Avoid scheduling major events
during religious holidays of participants when possible.

**Seating and Gifts**: Some cultures restrict mixed-gender seating or
regulate gift-giving. Adapt arrangements discreetly. Do not single out
individuals in ways that cause embarrassment.

**Communication**: Use appropriate forms of address. Avoid humor that may
not translate or could offend. When in doubt, err on the side of formality.
"""

