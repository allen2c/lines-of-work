title = "Business Card Etiquette"

content = """
Business card exchange in diplomatic settings follows cultural norms and
supports networking and follow-up.

**Presentation**: Offer cards with the right hand (or both hands in some Asian
contexts), face up so the recipient can read it. Receive cards with similar
respect. Do not write on cards in the donor's presence unless invited.

**Content**: Cards should include name, title, embassy or organization, and
contact details. Use the language of the host country or English for
international events. Ensure titles are current.

**Timing**: Exchange cards at the opening of meetings or when introducing
oneself at receptions. Carry an adequate supply. Refill discreetly when
depleted.

**Follow-up**: Use exchanged cards for thank-you notes and future
correspondence. Maintain a system for organizing cards and updating contact
information.
"""

