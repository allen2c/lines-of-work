title = "Photo Opportunities"

content = """
Photo opportunities (photo ops) provide visual record of diplomatic encounters.
Choreography ensures usable images and avoids awkward moments.

**Planning**: Determine desired shots in advance: handshake, signing, group
photo. Identify location, backdrop (flags, lectern), and lighting.
Coordinate with photographers.

**Sequence**: Brief principals on where to stand and when to smile. Allow
10–30 seconds per setup. Signal when done. Escort media out before
substantive discussion resumes.

**Pool Coverage**: When space is limited, use pool. Pool photographer
distributes images. Ensure pool includes major wire services and host
country media.

**Candid Shots**: For less formal moments, permit photography with clear
boundaries. Designate areas as no-photo if needed for sensitive discussions.
"""

