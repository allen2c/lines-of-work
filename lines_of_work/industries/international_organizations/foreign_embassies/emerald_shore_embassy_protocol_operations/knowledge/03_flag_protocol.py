title = "Flag Protocol"

content = """
The correct display of national flags is a core element of diplomatic protocol.
Improper flag use can convey disrespect and must be avoided.

**Display Order**: When multiple flags are shown, the host country flag may
appear first (on the left when viewed, or in the center), or flags may alternate
host-guest. At multilateral events, alphabetical order by country name is standard.

**Position of Honor**: The position of honor is typically to the right of the
observer (left when facing the flags). For bilateral events, the guest country
flag often occupies the position of honor when displayed with the host flag.

**Same Height and Size**: Flags of equal rank must be the same size and flown
at the same height. Never display one nation's flag larger or higher than
another's at the same occasion.

**Half-Mast**: Half-mast signifies mourning. The flag is lowered to one-half
to two-thirds of the staff. Duration and occasions follow national and host
country practice. Coordinate with host protocol on multilateral mourning.

**Retirement**: Damaged or faded flags should be retired with dignity, typically
by burning in a private ceremony.
"""

