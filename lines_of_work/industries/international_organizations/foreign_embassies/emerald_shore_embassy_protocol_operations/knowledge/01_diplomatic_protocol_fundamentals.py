content = """
Diplomatic protocol is the set of rules, customs, and courtesies that govern
official interactions between states and their representatives. It ensures
dignity, predictability, and mutual respect in international relations.

**Purpose**: Protocol reduces friction by establishing clear expectations. When
all parties follow the same conventions, attention focuses on substance rather
than form. Breaches of protocol can cause offense even when unintended.

**Sources**: Protocol derives from the Vienna Convention on Diplomatic Relations
(1961), the Vienna Convention on Consular Relations (1963), multilateral practice,
bilateral agreements, and host country law. When conventions conflict, bilateral
accord and host preference often prevail.

**Principles**: Reciprocity (treat others as they treat you), precedence (order
of rank and seniority), and consistency (apply rules uniformly) guide protocol
decisions. Cultural adaptation is appropriate where it does not compromise
core requirements.

**Application**: Protocol applies to ceremonies, seating, forms of address,
invitations, gifts, flags, anthems, and the conduct of official visits. Protocol
officers advise on these matters and coordinate their implementation.
"""

