title = "Multilateral Summit Protocol"

content = """
Multilateral summits involve many delegations and require coordination with
host country and other participants. Consensus on procedural matters is
essential.

**Precedence**: At summits, alphabetical order by country name (in host
language) is common. Alternately, order by date of participation or other
agreed formula. Confirm with host protocol.

**Seating**: Round or oval tables allow equal prominence. Name plates in
agreed order. Interpreters in booths. Ensure sightlines for all principals.

**Documents**: Circulate drafts in advance. Agree on adoption procedure.
Have clean copies for signing. Distribute final text to all participants.

**Bilateral Pull-Asides**: Summits often include margins for bilateral
meetings. Schedule through host protocol. Ensure rooms and timing are
coordinated.
"""

