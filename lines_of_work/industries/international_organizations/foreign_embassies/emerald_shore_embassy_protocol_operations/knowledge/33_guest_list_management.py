title = "Guest List Management"

content = """
Guest list management ensures the right people are invited and that
capacity and protocol are respected.

**Criteria**: Define invitee categories: principals, delegation, colleague
diplomats, host country officials, community, media. Balance representation
with capacity. Consider bilateral sensitivities.

**Tracking**: Maintain master list with names, titles, contact information,
and RSVP status. Use spreadsheet or database. Update regularly. Assign
unique identifier for place cards and seating.

**Confirmations**: Track yes, no, and pending. Follow up with non-respondents.
Confirm dietary and accessibility needs. Finalize numbers for catering and
seating 48–72 hours before.

**Overflow and Waitlist**: For oversubscribed events, maintain waitlist.
Notify if space opens. Avoid inviting more than capacity—overcrowding
compromises the event.
"""

