title = "National Anthem Protocol"

content = """
National anthems are played at official ceremonies, state visits, and formal
receptions. Proper sequence and conduct during anthem performance are essential.

**Sequence**: At bilateral events, the guest country anthem is typically played
first, followed by the host country anthem. At multilateral events, anthems may
be played in alphabetical order by country, or the host anthem may frame the
sequence (first and last).

**Duration**: Full anthems are usual for arrivals, wreath-laying, and state
dinners. Abbreviated versions (first verse or chorus) may be used when many
anthems are played. Confirm with host protocol.

**Conduct**: During the playing of any national anthem, all present should stand
facing the flag or music source. Military personnel in uniform salute; civilians
place hand over heart if customary in their country. Remain silent and attentive.

**Timing**: Anthems are played at arrival ceremonies (when the dignitary descends
from aircraft or vehicle), during wreath-laying, at the opening of state dinners,
and at the conclusion of official visits. Avoid overlapping with speeches or
movement.
"""

