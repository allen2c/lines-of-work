title = "Table Seating at Dinners"

content = """
Table seating at diplomatic dinners follows precedence while encouraging
engagement. Careful planning prevents offense and supports dialogue.

**Shape**: Round tables encourage conversation; long tables emphasize hierarchy.
U-shaped or E-shaped layouts allow host to see all guests. Choice depends
on guest count and room dimensions.

**Placement**: Principal guest to host's right. Spouses may sit next to
principals or be placed by their own rank. Alternate host and guest
delegations. Avoid seating adversaries or individuals with known tensions
adjacent.

**Escort Cards**: For large dinners, provide escort cards at reception
directing guests to tables. Place cards at each seat. Verify spelling
and titles meticulously.

**Last-Minute Changes**: Have a protocol officer available to adjust for
no-shows or late additions. Avoid obvious demotion of a guest due to
changes.
"""

