title = "Invitation Etiquette"

content = """
Invitations to diplomatic events convey formality and set expectations. Proper
wording and timing support successful events.

**Wording**: Formal invitations use the third person: "The Ambassador of Emerald
Shore requests the pleasure of the company of..." Include full title and name
of the invitee. Specify date, time, place, dress code, and RSVP details.

**Timing**: Send invitations 3–6 weeks before major events. For events involving
high-level visitors, earlier notice may be required. Follow up with reminders
1–2 weeks before.

**RSVP**: Request reply by a specific date. Include contact details (email,
phone). Track responses and follow up with non-respondents. Confirm final
numbers with caterers and venues 48–72 hours before.

**Plus-One and Spouses**: State clearly whether spouses or partners are
invited. For seated dinners, confirm exact numbers to avoid overflow or empty
seats. "And guest" is less formal than naming the spouse.
"""

