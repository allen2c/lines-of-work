title = "Diplomatic Immunity Basics"

content = """
Diplomatic immunity, codified in the Vienna Convention on Diplomatic Relations,
protects diplomats and mission premises from host country jurisdiction. Protocol
officers must understand its scope without offering legal advice.

**Scope**: Diplomatic agents enjoy inviolability of person and immunity from
criminal jurisdiction. Premises, archives, and communications are inviolable.
Administrative and technical staff have more limited immunity.

**Limits**: Immunity does not excuse illegal conduct. Sending states may waive
immunity to permit prosecution. Diplomat status does not extend to private
acts outside official functions in some interpretations.

**Practical Implications**: Protocol events on mission premises are generally
under mission control. Off-premises events follow host country law. Coordinate
with host authorities on any enforcement-sensitive matters. Do not assume
immunity covers commercial or personal disputes.
"""

