title = "Arrival and Receiving Lines"

content = """
Receiving lines allow the host to greet each guest personally. Proper
organization ensures smooth flow and adequate time for each encounter.

**Host Order**: The principal host stands first, often with spouse. Protocol
officers or aides may stand at the end to facilitate introductions. Order
follows precedence within the host delegation.

**Guest Flow**: Guests proceed in order of precedence unless otherwise arranged.
Introduce each guest to the host by full name and title. Brief the host on
notable guests if helpful.

**Timing**: Allow 30–60 seconds per guest for brief exchange. For large events,
consider multiple receiving stations or a receiving line only for principal
guests, with general mingling thereafter.

**Positioning**: Place the receiving line near the entrance so guests pass
through naturally. Ensure adequate space to prevent crowding. Have staff
direct traffic and manage late arrivals.
"""

