title = "Security Coordination"

content = """
Protocol officers coordinate with security services to ensure events proceed
safely without excessive disruption to the program.

**Roles**: Protocol manages program, timing, and guest access. Security manages
threat assessment, screening, and response. Clear delineation prevents overlap
and conflict.

**Advance Coordination**: Share guest lists, venue plans, and program with
security in advance. Accommodate screening requirements (metal detectors,
bag checks) in schedule and layout. Designate secure areas if needed.

**During Events**: Maintain communication channel with security lead. Avoid
ad hoc changes that bypass security. Escort guests through screening when
appropriate.

**Off-Premises**: For events at external venues, host country security may
take lead. Embassy coordinates with host protocol. Ensure embassy staff
have necessary clearances and access.
"""

