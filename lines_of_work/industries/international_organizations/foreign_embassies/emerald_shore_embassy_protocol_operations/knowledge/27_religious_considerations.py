title = "Religious Considerations"

content = """
Religious observance affects scheduling, catering, and conduct at diplomatic
events. Thoughtful accommodation respects participants and avoids exclusion.

**Prayer**: Be aware of prayer times for Muslim participants. Provide quiet
space for prayer if requested. Avoid scheduling key moments during prayer
when participants may need to excuse themselves.

**Dietary Laws**: Halal, kosher, and other religious dietary requirements
must be accommodated. Use certified caterers when required. Label clearly.
Do not assume—confirm with guests or their staff.

**Holidays**: Avoid major events during major religious holidays of
participants when practicable. If events must proceed, acknowledge the
occasion sensitively.

**General Principle**: When in doubt, ask. Most communities appreciate
sincere effort to accommodate, even if execution is imperfect.
"""

