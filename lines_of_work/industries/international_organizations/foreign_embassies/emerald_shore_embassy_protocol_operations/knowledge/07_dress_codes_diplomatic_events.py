content = """
Dress codes for diplomatic events ensure appropriate formality and cultural
alignment. State the code clearly on invitations and in pre-event communications.

**White Tie**: Most formal. Men: tailcoat, white bow tie, white waistcoat.
Women: full-length evening gown. For state dinners and royal occasions.

**Black Tie**: Formal evening wear. Men: dinner jacket (tuxedo), black bow tie.
Women: formal evening wear, typically full-length. Common for embassy dinners
and receptions.

**Lounge Suit / Business Attire**: Day events. Men: dark suit, tie. Women:
business suit or equivalent. For working lunches, signing ceremonies, and
office calls.

**National Dress**: Increasingly accepted and often encouraged at cultural
events. When "national dress" is invited, clarify that traditional formal
attire of any country is welcome. Avoid imposing one nation's definition.
"""

