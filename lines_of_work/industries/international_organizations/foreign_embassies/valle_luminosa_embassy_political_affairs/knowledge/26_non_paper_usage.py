title = "Non-Paper Usage"

content = """
A non-paper is an unsigned written communication conveying a position
without formal commitment. It allows flexibility while creating a record.

**When to Use**: Demarche follow-up, exploratory dialogue, complex
positions requiring written clarity, or when verbal communication may be
misremembered.

**Format**: Headed "Non-Paper" or "Aide-Memoire." No letterhead or
signature. Date optional. Leave with counterpart; do not publish.

**Content**: Clear, structured, factual. Avoid inflammatory language.
Non-papers can be disavowed but are often cited; draft carefully.

**Limitation**: Non-papers do not bind the sending state. For binding
commitments, use formal instruments.
"""

