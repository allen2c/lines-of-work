title = "Legislative Tracking"

content = """
Legislative tracking is the systematic monitoring of bills, amendments, and
parliamentary proceedings that affect bilateral interests or the sending
state's nationals and businesses.

**Identification**: Establish criteria for which legislation merits tracking:
trade, investment, sanctions, immigration, human rights, defence, or
sector-specific regulation. Coordinate with Economic and Consular sections.

**Process**: Understand the host parliament's procedures—first reading,
committee stage, plenary vote, and upper chamber where applicable. Track
timetables and key decision points.

**Reporting**: Note bill number, title, sponsor, current stage, substantive
content, and likely outcome. Flag items requiring demarche or capital
guidance.

**Contacts**: Maintain relations with parliamentarians, committee staff,
and legislative research services to obtain timely information and context.
"""

