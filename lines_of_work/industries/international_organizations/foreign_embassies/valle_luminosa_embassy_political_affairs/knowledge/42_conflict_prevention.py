title = "Conflict Prevention and Early Warning"

content = """
Embassies can contribute to conflict prevention by identifying early
warning signs and reporting them to capital for possible diplomatic
action.

**Indicators**: Escalating rhetoric, mobilisation, polarisation,
institutional weakening, or historical grievance activation. Context
matters; not all indicators lead to conflict.

**Reporting**: Flag concerns clearly. Assess likelihood and timeframe.
Recommend preventative steps where appropriate. Avoid alarmism;
credibility depends on calibration.

**Coordination**: Conflict prevention often involves multiple actors.
Coordinate with like-minded missions, regional organisations, and
capital. Avoid unilateral action that could complicate collective
effort.
"""

