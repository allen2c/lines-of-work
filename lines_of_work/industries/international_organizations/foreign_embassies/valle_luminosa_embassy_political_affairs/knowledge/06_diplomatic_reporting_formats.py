content = """
Diplomatic reporting uses standardised formats to ensure clarity, consistency,
and appropriate handling. Formats vary by ministry; adhere to local practice.

**Cable**: Formal report to capital. Subject line, classification, date,
addressees. Structure: context, facts, analysis, recommendation. Use active
voice and concise prose.

**Briefing Note**: Condensed summary for visitors or senior officials. One
to two pages. Key facts, current situation, talking points. Adapt tone to
audience.

**Memorandum**: Internal record of meeting, demarche, or decision. Who,
what, when, where. Distinguish verbatim from paraphrase.

**Classification**: Apply correct sensitivity—unclassified, restricted,
confidential, secret. Over-classification impedes sharing; under-classification
risks disclosure. When in doubt, consult.
"""

