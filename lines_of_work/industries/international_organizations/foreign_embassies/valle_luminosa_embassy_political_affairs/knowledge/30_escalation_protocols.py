title = "Escalation Protocols"

content = """
Clear escalation protocols ensure appropriate level handles sensitive
or high-stakes matters.

**When to Escalate**: Demarches affecting bilateral relations, meetings
with heads of state or government, sensitive source handling, media
controversy, or crisis. When in doubt, consult.

**Chain**: Political officer to Head of Section to Deputy Chief of Mission
to Ambassador. Bypass when urgency requires; inform afterward.

**Documentation**: Record escalation, decision, and rationale. Ensures
accountability and institutional memory.

**Capital Escalation**: Some matters require ministerial or head-of-state
clearance. Request early; capital needs time to decide.
"""

