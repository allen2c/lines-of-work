title = "Political Counsellor Role"

content = """
The Political Counsellor (or Head of Political Section) leads the embassy's
political work, reporting to the Ambassador and Deputy Chief of Mission.

**Responsibilities**: Oversee political reporting, coordinate demarches,
manage section resources, represent the embassy in political meetings,
and advise the Ambassador on political developments.

**Delegation**: Assign reporting and meeting responsibilities to officers.
Review drafts before transmission. Ensure quality and consistency.

**Capital Interface**: Primary channel for political dialogue with desk.
Convey mission views and requests. Interpret capital guidance for section.
"""

