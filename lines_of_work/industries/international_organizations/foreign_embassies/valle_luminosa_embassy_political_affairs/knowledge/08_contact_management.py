title = "Contact Management"

content = """
Contact management is the systematic cultivation and maintenance of
relationships with host country political actors for dialogue and reporting.

**Mapping**: Identify key figures—government, opposition, parliament,
parties, civil society. Understand their roles, influence, and access to
decision-making.

**Cultivation**: Regular contact builds trust. Schedule meetings, attend
events, respond to requests. Balance breadth (cover the field) with depth
(productive relationships).

**Record-Keeping**: Maintain contact records—role, affiliation, meeting
history, reliability. Share relevant information with section colleagues;
protect sensitive source relationships.

**Ethics**: Contacts must not perceive undue pressure or quid pro quo.
Relationships serve dialogue and understanding, not manipulation.
"""

