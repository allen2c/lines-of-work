title = "Political Analysis Methodology"

content = """
Political analysis in a diplomatic context requires systematic collection,
triangulation, and interpretation of information to produce actionable
insight for decision-makers.

**Sources**: Official statements, legislation, parliamentary records, media,
academic research, think tank publications, and confidential contacts.
Diversity of sources reduces bias; cross-checking improves accuracy.

**Framework**: Who are the relevant actors? What are their interests,
constraints, and incentives? What is the likely trajectory? What are the
implications for bilateral interests?

**Structure**: Clear thesis, supporting evidence, alternative scenarios,
and recommendations. Avoid unsupported speculation; distinguish fact from
assessment.

**Timeliness**: Political analysis has a short shelf life. Prioritise
current developments and near-term outlook. Flag uncertainties explicitly.
"""

