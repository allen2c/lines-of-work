title = "Bilateral Visit Programme Design"

content = """
Bilateral visits—heads of state, ministers, senior officials—require
careful programme design to maximise substantive outcomes.

**Objectives**: Clarify visit objectives with capital. Programme should
advance each. Balance symbolism and substance.

**Meetings**: Match level and portfolio. Allow time for substantive
discussion. Include opposition or civil society where appropriate and
cleared.

**Logistics**: Coordinate with Protocol, Security, and host authorities.
Confirm venues, times, and participants. Build in buffer.

**Materials**: Prepare briefings, talking points, and gifts. Deliver to
principal in advance. Brief on cultural and protocol considerations.
"""

