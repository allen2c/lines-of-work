title = "Demarche Procedures"

content = """
A demarche is a formal diplomatic communication conveying the sending state's
position or request to the host government. Demarches require authorisation
and precise execution.

**Authorisation**: Demarches are initiated by the Ministry. The embassy may
request a demarche or receive instructions to deliver one. Do not demarche
without clearance.

**Delivery**: Arrange meeting with appropriate official. Deliver points
verbally and, if instructed, leave non-paper. Record delivery and response.

**Non-Paper**: A written aide-memoire left with the host. Not signed;
conveys position without formal commitment. Use when written record is
useful.

**Follow-Up**: Report host response to capital. Track whether host follows
through on commitments. Escalate if response is inadequate.
"""

