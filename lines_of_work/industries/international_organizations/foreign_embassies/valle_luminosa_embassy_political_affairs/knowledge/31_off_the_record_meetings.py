title = "Off-the-Record Meeting Practices"

content = """
Off-the-record (Chatham House or similar) meetings enable frank exchange
without formal attribution. Appropriate handling protects sources and
maintains trust.

**Ground Rules**: Establish at outset—no attribution, no direct quotation,
or other agreed limits. All participants must respect. If unclear, confirm
before sharing.

**Reporting**: Content may be reported but not attributed to specific
individuals. Use "according to participants" or "in diplomatic circles."
Do not identify source in cable distribution.

**Value**: Off-the-record allows testing ideas, exploring flexibility,
and receiving candid assessment. Abuse destroys access.
"""

