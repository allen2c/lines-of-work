title = "Working with Interpreters"

content = """
Interpreters enable dialogue across language barriers. Effective use
ensures accurate communication and builds trust.

**Preparation**: Brief interpreter on subject matter, key terms, and
sensitivities. Provide materials in advance when possible.

**Conduct**: Speak in short segments. Pause for interpretation. Maintain
eye contact with counterpart, not interpreter. Avoid sidebar comments
that might confuse.

**Confidentiality**: Interpreters are bound by confidentiality. Treat
sensitive meetings accordingly. Use cleared interpreters when required.

**Quality**: Professional interpreters are trained for diplomacy. Avoid
using untrained staff for substantive meetings.
"""

