title = "Source Reliability and Verification"

content = """
Political reporting depends on reliable sources. Unverified information
undermines credibility and can cause policy errors.

**Assessment**: Evaluate sources by access, track record, and potential
bias. Classify reliability in internal notes. Do not cite unreliable
sources in formal reporting without caveat.

**Verification**: Cross-check significant claims with additional sources.
Distinguish confirmed fact from single-source report. When verification
is impossible, state limitations.

**Handling**: Protect sensitive sources. Do not expose contacts through
careless attribution or distribution. Follow need-to-know principles.

**Correction**: If reporting proves wrong, correct promptly. Credibility
depends on accuracy over time.
"""

