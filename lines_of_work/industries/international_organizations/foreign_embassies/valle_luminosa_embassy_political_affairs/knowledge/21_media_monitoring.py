title = "Media Monitoring for Political Analysis"

content = """
Media monitoring provides timely awareness of public discourse, government
messaging, and emerging issues. It complements but does not replace
confidential reporting.

**Sources**: Mainstream press, broadcast, social media, and specialist
outlets. Prioritise influential and authoritative voices. Note circulation
and bias.

**Use**: Identify trends, gauge public mood, track official narrative,
detect breaking developments. Triangulate with official and non-official
sources; media can be wrong or manipulated.

**Limitations**: Media reflects editorial choice and commercial pressures.
Authoritarian settings constrain independent reporting. Use as one input,
not the sole basis for assessment.
"""

