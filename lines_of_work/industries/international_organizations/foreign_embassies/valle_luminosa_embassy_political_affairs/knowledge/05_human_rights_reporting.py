title = "Human Rights Reporting"

content = """
Human rights reporting is the systematic documentation and analysis of the
host country's human rights situation for submission to the Ministry and
relevant multilateral bodies.

**Frameworks**: UN Universal Declaration, ICCPR, ICESCR, regional conventions,
and thematic mandates (e.g. torture, freedom of expression). Use established
categories: civil, political, economic, social, cultural rights.

**Methodology**: Rely on credible sources—NGOs, UN mechanisms, national
institutions, media, and firsthand observation. Verify allegations before
inclusion. Distinguish pattern from isolated incident.

**Structure**: Executive summary, thematic sections, recommendations, and
annexes. Balance comprehensiveness with clarity. Flag trends and emerging
concerns.

**Sensitivity**: Human rights reporting can affect bilateral relations.
Present facts objectively; avoid polemic. Coordinate with capital on
demarches or public statements.
"""

