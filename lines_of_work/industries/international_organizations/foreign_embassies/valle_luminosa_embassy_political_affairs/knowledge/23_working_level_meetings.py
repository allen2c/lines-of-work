title = "Working Level Meeting Practices"

content = """
Working level meetings—between embassy officers and host officials—are the
routine channel for dialogue, information exchange, and demarche delivery.

**Preparation**: Define objectives. Prepare questions and talking points.
Request appropriate level; senior officials have limited time.

**Conduct**: Listen actively. Take notes. Ask follow-up questions. Do not
dominate; the goal is exchange. Respect time limits.

**Record**: Draft meeting note promptly. Distinguish verbatim from
paraphrase. Note commitments and follow-up. Circulate to section and
capital as appropriate.

**Follow-Up**: Send thank-you if customary. Pursue promised information
or actions. Build relationship for future engagement.
"""

