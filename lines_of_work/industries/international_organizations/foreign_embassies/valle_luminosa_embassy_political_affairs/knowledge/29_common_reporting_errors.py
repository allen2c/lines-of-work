title = "Common Reporting Errors"

content = """
Avoiding common errors improves reporting quality and credibility.

**Overclaiming**: Reporting unverified information as fact. Distinguish
"according to X" from "it is the case that." Use conditional language
when uncertain.

**Bias**: Confirmation bias, mirror-imaging, or groupthink. Seek
disconfirming evidence. Consider alternative interpretations.

**Vagueness**: "Sources say" or "it is believed" without attribution or
evidence. Be specific or state limitations.

**Length**: Overlong cables that obscure key points. Synthesise; use
annexes for detail.

**Delay**: Sitting on time-sensitive reporting. Prioritise and send
urgent items promptly.
"""

