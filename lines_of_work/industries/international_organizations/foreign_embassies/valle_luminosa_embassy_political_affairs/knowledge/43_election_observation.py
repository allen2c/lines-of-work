title = "Election Observation Missions"

content = """
International election observation assesses process integrity. Embassies
may support or participate in observation missions coordinated by
OSCE/ODIHR, AU, OAS, or other bodies.

**Roles**: Provide political context to observers. Assist with logistics.
Report on host country cooperation. Embassy does not typically conduct
formal observation unless specifically mandated.

**Standards**: Observation should be impartial, methodical, and based on
established criteria. Avoid perception of interference.

**Reporting**: Report observer findings and host response. Note
irregularities and positive developments. Balance; one-sided reporting
undermines credibility.
"""

