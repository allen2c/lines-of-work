title = "Briefing Note Format"

content = """
Briefing notes prepare principals for meetings, visits, or decisions.
Concision and clarity are essential.

**Structure**: Executive summary (2–3 sentences), key facts, current
situation, options or recommendations, annexes if needed. One to two
pages maximum for most briefings.

**Audience**: Adapt depth to principal. Ministers need different level
than desk officers. Avoid jargon; define terms when necessary.

**Timeliness**: Deliver in advance of meeting. Principals need time to
absorb. Update if circumstances change.
"""

