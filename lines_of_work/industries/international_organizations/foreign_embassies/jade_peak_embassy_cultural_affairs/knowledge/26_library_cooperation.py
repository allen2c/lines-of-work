content = """
Libraries—university, public, and specialized—are important partners for
cultural outreach. The Embassy supports library cooperation through
donations, programming, and linkage with Jade Peak library networks.

**Donations:** The Embassy may donate books, periodicals, or multimedia
materials to qualified libraries. Donations are at Embassy discretion;
libraries submit requests with justification and commitment to catalog,
shelve, and promote the collection. Priority may go to institutions
with existing Chinese or Asian studies focus.

**Programming:** Author talks, reading clubs, exhibitions, and workshops
can be co-hosted with libraries. The Embassy provides content expertise;
libraries provide venue and audience reach.

**Professional Exchange:** Librarians may participate in exchange
programs to visit Jade Peak libraries, learn cataloging practices,
or develop acquisition strategies. Inquiries through standard exchange
program channels.

**Digital Resources:** Access to databases or e-resources from Jade
Peak may be available through institutional agreements. Libraries
should inquire about current offerings and eligibility.
"""

