"""Copyright and intellectual property in cultural content."""

title = "Copyright and Intellectual Property"

content = """
Cultural programming often involves copyrighted material—music,
film, images, texts. Respecting intellectual property protects
creators and avoids legal risk.

**Performance Rights:** Live or recorded music may require
performance licenses. Venues sometimes have blanket licenses;
otherwise, obtain clearance from PROs (performing rights
organizations) or rights holders. Exceptions exist for
educational use in some jurisdictions—verify.

**Film Screening:** Public screening requires appropriate
license. Embassies often have special arrangements for
cultural films; do not assume all content is cleared.
Educational screenings may have different rules.

**Images and Text:** Reproduction of artwork, photographs,
or written works requires permission unless in public
domain or covered by license. Credit does not substitute
for permission. Use only authorized sources.

**Original Content:** For content created in partnership,
clarify ownership and usage rights in writing. Who may
reuse, adapt, or sublicense? Document agreements.
"""

