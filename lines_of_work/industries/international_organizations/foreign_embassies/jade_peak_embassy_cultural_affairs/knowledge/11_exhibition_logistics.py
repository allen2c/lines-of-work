content = """
Planning and executing cultural exhibitions requires careful coordination of
transport, insurance, installation, and programming. The Embassy provides
guidance and sometimes direct support for exhibitions it endorses.

**Timeline:** Allow 12–18 months for international exhibitions involving object
loans. Key milestones: concept approval, loan agreement, customs and transport
arrangements, installation, programming, deinstallation. Rushing any phase
risks damage, delays, or compliance issues.

**Insurance:** Lenders typically require wall-to-wall coverage at agreed values.
Borrowers must provide proof of insurance before objects travel. The Embassy
can advise on standard requirements but does not underwrite policies.

**Transport:** Fine arts shippers with experience in international cultural
property movement are essential. Climate-controlled packaging, customs
declarations, and CITES compliance (for certain materials) must be addressed.
The Embassy assists with import/export documentation where diplomatic
facilitation is appropriate.

**Installation:** Technical specifications (light levels, climate, security)
must match lender requirements. Condition reports at handover and return are
mandatory. Skilled installers are required for delicate objects.
"""

