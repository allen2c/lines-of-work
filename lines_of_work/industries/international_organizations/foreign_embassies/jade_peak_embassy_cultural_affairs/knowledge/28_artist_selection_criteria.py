title = "Artist Selection Criteria"

content = """
Artists invited for residencies, tours, or Embassy-supported performances
are selected through a combination of artistic merit, program fit, and
practical considerations. Understanding these criteria helps artists
and presenters prepare strong applications or proposals.

**Artistic Merit:** Demonstrated excellence through exhibition record,
performances, publications, or peer recognition. Emerging artists may
be considered if potential is clear. Quality of past work matters more
than quantity.

**Program Fit:** Alignment with current priorities—cultural exchange,
audience development, bilateral themes. Work that engages with Jade
Peak culture or fosters dialogue is valued. Purely commercial or
narrowly self-promotional projects are less likely to be supported.

**Feasibility:** Can the project be realized within available resources
and timeline? Technical requirements, travel logistics, and scheduling
must be manageable. Artists with proven ability to work in unfamiliar
contexts are preferred.

**Diversity:** The Embassy seeks geographic, discipline, and demographic
diversity across its programs. This does not mean lowering standards
but ensuring broad representation over time.
"""

