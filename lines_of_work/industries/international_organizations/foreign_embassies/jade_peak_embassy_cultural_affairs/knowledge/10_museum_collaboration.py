content = """
The Embassy facilitates collaboration between host-country museums and cultural
institutions in Jade Peak. This includes loan exhibitions, traveling shows,
curatorial exchanges, and joint research projects.

**Exhibition Loans:** Objects from Jade Peak collections may be loaned for
temporary exhibitions abroad. Loan requests require formal proposal from the
borrowing institution, including exhibition concept, conservation requirements,
insurance, and security arrangements. Lead time is typically 18–24 months.

**Traveling Exhibitions:** The Embassy occasionally coordinates or supports
exhibitions that tour multiple venues. Host institutions apply through a
competitive process. Selected venues receive exhibition package, interpretive
materials, and sometimes programming support.

**Curatorial Exchange:** Short-term placements for curators to conduct research,
develop exhibitions, or build institutional relationships. Funded through
exchange programs or bilateral agreements.

**Conservation and Research:** Collaborative projects on conservation technique,
material analysis, or provenance research may be supported where they advance
shared knowledge and strengthen institutional ties.
"""

