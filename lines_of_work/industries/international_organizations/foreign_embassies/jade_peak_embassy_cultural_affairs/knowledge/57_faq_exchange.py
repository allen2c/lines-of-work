content = """
**Who is eligible for youth exchange?** Typically citizens of
the host country, aged 16–25, enrolled in or recently
graduated from education. Some programs have language
requirements; others are open to beginners.

**When do applications open?** Varies by program. Youth
exchange often opens in fall for summer programs. Artist
residency opens annually. Check the Embassy website for
current deadlines.

**Does the Embassy help with visas?** We provide support
letters for endorsed programs. Visa application and
adjudication are handled by the Consular Section.
Direct visa questions there.

**Can I do an internship at the Embassy?** Internship
opportunities are limited and vary by post. Inquiries
go through the human resources or designated internship
contact. Cultural Section internships, when available,
are typically unpaid and for academic credit.

**How competitive are the programs?** Competition varies.
Strong applications demonstrate clear motivation,
relevant experience, and fit with program goals.
Apply to multiple programs if possible.
"""

