content = """
Cultural festivals—whether focused on Jade Peak or broadly Asian—offer platforms
for visibility and engagement. The Embassy participates in or supports festivals
through programming, resources, and partnership.

**Embassy-Organized Events:** Lunar New Year celebrations, National Day receptions,
and thematic festivals (e.g., calligraphy, tea culture) are held at the Embassy
or partner venues. These are by invitation or pre-registration. Public events
are advertised through Embassy channels and partner networks.

**Partner Festival Support:** The Embassy may support external festivals through
artist referrals, loan of exhibition materials, funding, or co-branding. Support
is contingent on program quality, alignment with objectives, and capacity.
Proposals should be submitted 4–6 months in advance.

**Community Festivals:** Diaspora and community organizations often organize
cultural festivals. The Embassy may provide materials, speakers, or symbolic
support. We do not control or endorse all community events but seek
constructive engagement where appropriate.

**Evaluation:** Supported events are evaluated for attendance, media coverage,
partner satisfaction, and alignment with strategic goals. Feedback informs
future programming decisions.
"""

