"""Crisis communication for cultural programs."""

title = "Crisis Communication"

content = """
When a cultural program attracts controversy, negative coverage, or
safety concerns, clear communication protocols protect reputations
and ensure appropriate response.

**Escalation:** Staff who receive complaints, media inquiries, or
reports of problems should escalate to the Cultural Attaché or
Press Office. Do not respond ad hoc to sensitive matters. Document
what was said, by whom, and when.

**Spokesperson:** Only designated personnel speak to media on
sensitive matters. Others refer inquiries to the Press Office.
Stay within your mandate; do not speculate on political or
policy questions.

**Internal Coordination:** Cultural Section, Press Office, and
leadership coordinate on messaging. Consistency matters.
Correct misinformation promptly and calmly.

**Learn and Adapt:** After a crisis, conduct a debrief. What
worked? What would we do differently? Update procedures
and training. Crises are inevitable; preparedness reduces harm.
"""

