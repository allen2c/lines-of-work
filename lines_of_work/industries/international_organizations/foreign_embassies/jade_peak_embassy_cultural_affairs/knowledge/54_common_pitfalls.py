"""Common pitfalls in cultural programming."""

title = "Common Pitfalls"

content = """
Awareness of common mistakes helps avoid them. These pitfalls
recur across programs; vigilance improves outcomes.

**Underestimating Timeline:** International projects need
lead time. Visa processing, contract negotiation, and
logistics take longer than expected. Start early.

**Unclear Partnerships:** Roles, responsibilities, and
resources are not specified. Mid-project confusion leads
to conflict. Document agreements at the outset.

**Stereotyping or Exoticizing:** Reducing culture to
clichés—mystical, ancient, inscrutable—undermines
authentic exchange. Present with nuance and respect.

**Ignoring Audience:** Programs designed for designers,
not audiences, miss the mark. Know who you serve and
what they need.

**Overpromising:** Grant applications overstate outcomes;
reporting underdelivers. Funders lose trust. Be realistic
in proposals; honest in reporting.

**Neglecting Evaluation:** Without assessment, you cannot
improve or demonstrate impact. Build evaluation into
design; allocate time and resources.
"""

