"""Types of cultural exchange programs offered by the Embassy."""

title = "Exchange Program Types"

content = """
The Jade Peak Embassy administers several categories of cultural exchange programs,
each designed for a specific audience and objective.

**Youth Exchange:** Short-term programs (2–4 weeks) for students aged 16–25. Focus
on language immersion, homestay experience, and cultural workshops. Typically runs
during summer or winter breaks. Partners include host-country schools and youth
organizations.

**Artist Residency:** 4–12 week stays for visual artists, musicians, or writers.
Participants receive studio space, stipend, and exhibition or performance opportunity.
Selection is competitive; applications open annually.

**Academic Visitor:** Invitations for scholars, researchers, or educators to visit
Jade Peak for lectures, research collaboration, or curriculum development. Duration
varies from one week to three months. Host institutions in Jade Peak must extend
the formal invitation.

**Professional Exchange:** Programs for cultural administrators, museum curators, or
arts educators. Typically 1–2 weeks of site visits, workshops, and networking.
Supports capacity-building and institutional partnerships.

**Teacher Exchange:** Short-term placements for language teachers to observe
classroom practice, share methodologies, and develop materials. Requires
coordination with host-country education authorities.
"""

