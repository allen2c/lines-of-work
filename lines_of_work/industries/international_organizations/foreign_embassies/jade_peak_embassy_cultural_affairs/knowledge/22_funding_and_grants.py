title = "Funding and Grants"

content = """
Cultural exchange and education projects often require external funding. The
Embassy administers limited direct grants and can advise on other sources.

**Embassy Grants:** Small grants (amounts vary) may be available for projects
that align with cultural diplomacy objectives. Typical uses: event support,
materials, partial travel. Application process is competitive; not all
requests can be funded. Deadlines are announced periodically.

**Bilateral Funds:** Some agreements establish dedicated funds. Eligibility
and application procedures are set by the implementing bodies in each country.
The Embassy can provide contact information and general guidance.

**External Funders:** Foundations, arts councils, corporate sponsors, and
university grants may support cultural projects. Partners are encouraged
to pursue multiple funding streams. The Embassy does not endorse specific
grant applications but may provide letters of support for qualified projects.

**Budget Requirements:** Grant applications typically require detailed
budgets, timelines, and outcome descriptions. Overhead limits and reporting
requirements vary. Consult specific program guidelines.
"""

