"""Budget management for cultural projects."""

title = "Budget Management"

content = """
Sound budget management ensures projects deliver as planned and
meet funder expectations. The Embassy expects transparency and
fiscal responsibility from grantees and partners.

**Budget Development:** Itemize by category (personnel, travel,
materials, venue, etc.). Be realistic—underestimating leads to
shortfalls; overestimating can raise questions. Include
contingency (5–10%) where appropriate.

**Restrictions:** Grant agreements may restrict certain expenses
(equipment, overhead, alcohol). Know the rules before spending.
Seek approval for budget modifications when required.

**Documentation:** Retain receipts, invoices, and timesheets.
Audits may occur; good records protect both grantee and funder.

**Timeline:** Some funders release funds in installments tied
to milestones. Plan cash flow accordingly. Late reporting can
delay subsequent installments.
"""

