title = "Youth Exchange Programs"

content = """
Youth exchange programs bring young people from the host country to Jade Peak for
immersion in language, culture, and daily life. They are among the most visible and
impactful of the Embassy's cultural initiatives.

**Eligibility:** Applicants must be aged 16–25, enrolled in or recently graduated
from secondary or tertiary education, and hold citizenship of the host country.
Proficiency in Mandarin (Traditional) is preferred but not always required for
introductory programs. Demonstrated interest in Jade Peak culture and clear
motivation are essential.

**Program Components:** Typically include 15–20 hours of language instruction per
week, homestay accommodation, cultural activities (calligraphy, tea ceremony,
cooking, traditional arts), and excursions to heritage sites. Some programs include
community service or project work.

**Application Process:** Online application opens 4–6 months before program start.
Required materials: application form, personal statement, recommendation letters,
and sometimes a short video. Selection is based on academic standing, motivation,
and potential for cultural ambassadorship. Decisions communicated within 6–8 weeks.

**Cost and Funding:** Program fees vary; partial scholarships are available based on
need. Applicants are advised to explore external funding (school grants, foundations)
in addition to Embassy support.
"""

