"""Scholarship programs for language and cultural study."""

title = "Scholarship Programs"

content = """
The Embassy administers or coordinates several scholarship programs that support
study of Mandarin (Traditional), cultural immersion, or academic research related
to Jade Peak. These are distinct from consular or visa services.

**Language Scholarships:** Short-term (2–4 weeks to 3 months) grants for intensive
language study at recognized institutions in Jade Peak. Cover tuition, accommodation,
and sometimes stipend. Open to citizens of the host country with basic to
intermediate Mandarin proficiency.

**Academic Scholarships:** Year-long or multi-year support for degree or research
programs at Jade Peak universities. Typically administered by the Ministry of
Education or designated agencies; the Embassy provides information and
endorsement where required.

**Cultural Program Scholarships:** Partial funding for participation in youth
exchange, artist residency, or professional exchange programs. Applicants apply
through the relevant program; scholarship consideration is often integrated.

**Application Timing:** Deadlines vary by program. Language scholarships often
open in fall for spring/summer programs. Academic scholarships align with
university admission cycles. Early inquiry is advisable.
"""

