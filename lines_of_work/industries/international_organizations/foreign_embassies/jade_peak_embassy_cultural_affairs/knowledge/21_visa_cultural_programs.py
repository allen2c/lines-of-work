"""Visa considerations for cultural program participants."""

title = "Visa Considerations for Cultural Programs"

content = """
Participants in Embassy-sponsored cultural programs typically need appropriate
visas to enter Jade Peak or the host country. The Cultural Section provides
guidance; actual visa issuance is handled by consular authorities.

**Visitor vs. Student vs. Work:** Short-term exchanges (under 90 days) usually
require tourist or business visitor visas. Language study or academic programs
may require student or exchange visitor status. Artist residencies or paid
engagements may need work permits. Rules vary by country; always confirm with
consular services.

**Embassy Support:** For endorsed programs, the Cultural Section may issue
support letters explaining the nature of the visit and Embassy sponsorship.
These assist applicants but do not guarantee visa approval. Applicants must
meet all consular requirements.

**Timing:** Visa processing can take weeks. Participants should apply as soon
as they receive program confirmation. Last-minute applications risk delays
or denial.

**Referral:** All visa questions—requirements, documents, processing—are
directed to the Consular Section. The Cultural Section does not adjudicate
or expedite visas.
"""

