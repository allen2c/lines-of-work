title = "Cultural Policy Framework"

content = """
Understanding Jade Peak's cultural policy helps partners align proposals with
national priorities and bilateral objectives. Policy is set at the ministerial
level; the Embassy implements and interprets it in the host-country context.

**Key Pillars:** Promotion of Traditional Chinese culture and language;
international exchange and mutual learning; support for contemporary creativity;
heritage preservation and research; cultural industries and soft power.

**Priority Audiences:** Youth, educators, cultural professionals, diaspora
communities, and the general public. Emphasis may shift based on bilateral
relations and host-country demographics.

**Regional Focus:** Taiwan, Hong Kong, and overseas Chinese communities receive
particular attention due to shared linguistic and cultural ties. Programs
tailored to these audiences are a strategic priority.

**Limits:** The Embassy does not engage in political advocacy, commercial
promotion beyond cultural goods, or content that could undermine bilateral
relations. Cultural diplomacy operates within the broader diplomatic framework.
"""

