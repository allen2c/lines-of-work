title = "Media Relations for Cultural Content"

content = """
Positive media coverage amplifies the reach of cultural programs. The
Cultural Section coordinates with the Press Office on cultural-themed
media outreach. Clear protocols ensure appropriate messaging and
attribution.

**Press Releases:** Major events warrant press releases. Content includes
date, venue, description, key participants, and contact for credentials.
Releases are distributed through Embassy channels and, when appropriate,
to cultural and arts press.

**Interviews:** Artists, scholars, or Cultural Section staff may give
interviews. Requests are vetted for fit and timing. Spokesperson
guidance applies; interviewees stay within their expertise and avoid
political topics unless specifically authorized.

**Photography and Filming:** Events may be photographed or filmed for
promotional use. Attendees are notified. Credentialed media may cover
public events subject to space and logistics. Embargoes or
restrictions are communicated in advance when applicable.

**Crisis or Controversy:** If a cultural program attracts negative
attention, the Press Office leads response. Cultural Section provides
facts and context. Do not ad-lib to media on sensitive matters.
"""

