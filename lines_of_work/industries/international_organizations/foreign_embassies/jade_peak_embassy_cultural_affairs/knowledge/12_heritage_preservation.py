content = """
The Embassy supports partnerships focused on tangible and intangible heritage
preservation. This includes built heritage, traditional crafts, performing arts,
and oral traditions. Programs aim to build capacity and share best practices.

**Tangible Heritage:** Collaboration on conservation of historic buildings,
archaeological sites, or museum collections. May involve training workshops,
expert exchanges, or joint research. All work must adhere to international
charters (e.g., Venice Charter) and local regulations.

**Intangible Heritage:** Safeguarding of traditional crafts, music, dance,
folklore, and ritual practices. UNESCO's Intangible Cultural Heritage framework
often informs project design. Documentation, transmission to younger generations,
and sustainable livelihoods for practitioners are common goals.

**Funding:** Heritage projects may be funded through bilateral agreements,
trust funds, or grants from cultural agencies. The Embassy can help identify
opportunities and connect partners with relevant authorities in Jade Peak.

**Ethical Considerations:** Community consent, respect for living traditions,
and avoidance of harm to source communities are paramount. Projects should
benefit heritage bearers, not only researchers or institutions.
"""

