content = """
Academic visitor programs facilitate scholarly exchange between the host country
and Jade Peak. They support research collaboration, guest lecturing, curriculum
development, and institutional partnership building.

**Invitation-Based Model:** Unlike open-call programs, academic visits require a
formal invitation from a Jade Peak institution (university, research center,
museum). The Embassy assists in matching scholars with appropriate hosts and
may provide logistical or partial funding support.

**Typical Duration:** One week to three months. Short stays often focus on
workshops or lecture series; longer stays support research projects or
collaborative writing.

**Funding:** Host institutions in Jade Peak may cover accommodation and local
expenses. The Embassy may contribute travel stipends or administrative support
for high-priority projects. Applicants should confirm funding with both the
inviting institution and the Embassy.

**Application:** Scholars initiate contact through their home institution or
directly with potential hosts in Jade Peak. Once an invitation is secured,
the Embassy can advise on visa procedures, orientation materials, and
logistics. Early coordination (3–6 months before travel) is recommended.
"""

