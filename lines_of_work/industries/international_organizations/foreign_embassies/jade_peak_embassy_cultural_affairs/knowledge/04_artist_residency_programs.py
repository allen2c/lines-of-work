title = "Artist Residency Programs"

content = """
The Embassy's artist residency program offers international artists the opportunity
to live and work in Jade Peak for 4–12 weeks, with studio space, stipend, and
exhibition or performance support.

**Eligibility:** Open to visual artists, musicians, composers, writers, and
occasionally filmmakers. Applicants must have a demonstrated professional practice,
exhibition or publication record, and relevance to cultural exchange objectives.
Citizenship of the host country is required. Collaborative projects involving
Jade Peak artists are encouraged.

**Selection Criteria:** Artistic merit, alignment with program goals, feasibility
of proposed project, and potential for lasting impact. A peer-review panel advises
the Cultural Attaché. Geographic and discipline diversity are considered.

**Support Provided:** Accommodation, studio or rehearsal space, monthly stipend,
round-trip airfare, and assistance with exhibition or performance logistics. The
Embassy does not provide materials, instruments, or production budgets beyond the
standard package.

**Application Cycle:** Opens annually, typically in the fall. Deadline is usually
3–4 months before the intended residency period. Notification within 8–10 weeks.
"""

