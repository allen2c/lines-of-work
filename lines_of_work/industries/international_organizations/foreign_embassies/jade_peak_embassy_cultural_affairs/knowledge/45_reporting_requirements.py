"""Reporting requirements for supported projects."""

title = "Reporting Requirements"

content = """
Projects that receive Embassy support typically have reporting
obligations. Understanding and meeting these maintains good
standing and supports future funding.

**Interim Reports:** For multi-month projects, interim reports
may be required at designated milestones. Include progress,
expenditures, challenges, and any changes to plan.

**Final Reports:** Due within 30–60 days of project completion.
Content: narrative summary, outputs, outcomes, budget
reconciliation, photos or documentation. Follow the template
provided in the grant agreement.

**Financial Reporting:** Detail how funds were spent. Receipts
or invoices may be required for amounts above a threshold.
Unspent funds typically revert; clarify terms in advance.

**Extensions:** If reporting deadlines cannot be met, request
an extension in writing before the due date. Extensions are
at Embassy discretion. Chronic late reporting affects future
eligibility.
"""

