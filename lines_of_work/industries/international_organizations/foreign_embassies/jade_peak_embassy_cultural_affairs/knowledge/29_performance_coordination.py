title = "Performance Coordination"

content = """
Coordinating performances—music, dance, theatre—requires attention to
technical specs, scheduling, visas, and promotion. The Embassy supports
but does not fully produce most performance events.

**Technical Rider:** Artists or their representatives must provide
detailed technical requirements: stage dimensions, sound, lighting,
backline, dressing rooms. Incomplete riders delay planning and can
cause last-minute problems. Provide 2–3 months before performance.

**Venue Coordination:** The Embassy may connect artists with venues
or co-present. Venues have their own technical capacity; match
artists to appropriate spaces. Site visits or technical rehearsals
are recommended when possible.

**Visas and Travel:** Performance tours require appropriate visas.
Work visas or cultural exchange designations may apply. Allow
adequate processing time. The Embassy provides support letters for
endorsed projects.

**Promotion:** Joint promotion by Embassy and presenter maximizes
audience. Provide artist bios, photos, and program notes in advance.
Media interviews may be arranged through the Press Office if
appropriate.
"""

