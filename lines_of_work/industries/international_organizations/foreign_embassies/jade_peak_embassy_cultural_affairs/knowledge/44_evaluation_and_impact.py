content = """
Evaluating cultural programs helps improve quality, demonstrate
accountability, and make the case for continued support. The
Embassy expects grantees and partners to report on outcomes.

**Outputs vs. Outcomes:** Outputs are countable (attendees,
events, materials distributed). Outcomes are changes (knowledge
gained, attitudes shifted, partnerships formed). Both matter;
outcomes require more effort to measure but carry more weight.

**Data Collection:** Surveys (pre/post, exit), attendance records,
feedback forms, media mentions, and qualitative interviews.
Keep it proportionate—simple programs need simple evaluation.
Complex initiatives may warrant external evaluation.

**Reporting:** Grantees submit reports per grant agreement.
Format typically includes narrative, outputs, outcomes,
challenges, and lessons learned. Timeliness matters.

**Learning:** Evaluation should inform future programming.
Share findings internally and, when appropriate, with
partners. Celebrate successes; address failures honestly.
"""

