"""Legal considerations for cultural programming."""

title = "Legal Considerations"

content = """
Cultural programs may touch on contracts, liability, intellectual
property, and regulatory compliance. Partners should be aware
of key considerations; the Embassy does not provide legal
advice but can flag common issues.

**Contracts:** Agreements with venues, artists, vendors, or
co-partners should be in writing. Specify scope, payment,
cancellation, and liability. Consult local legal counsel
for complex arrangements.

**Liability:** Events carry risk (injury, property damage).
Insurance, waivers, or venue agreements may allocate risk.
Understand who is responsible for what. The Embassy's
liability in supported projects is typically limited.

**Regulations:** Permits (alcohol, amplification, occupancy),
copyright (music, film, images), and employment (volunteers
vs. paid) may apply. Compliance is the partner's responsibility.

**Diplomatic Immunity:** Embassy personnel and premises
enjoy certain immunities. This does not extend to
partner organizations. Partners operate under host-country
law.
"""

