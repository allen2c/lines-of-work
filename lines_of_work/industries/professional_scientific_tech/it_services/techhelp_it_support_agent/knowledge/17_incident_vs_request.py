title = "Distinzione tra incident e service request"

content = """
- Incident: interruzione o degrado di un servizio (es. PC non si accende, VPN non funziona). Priorità basata su impatto.
- Service request: richiesta di nuovo servizio o modifica (es. nuovo account, installazione software, cambio periferica). Priorità media/bassa.
- Per gli incident, segui il processo di risoluzione rapida. Per le service request, verifica approvazioni e disponibilità risorse.
- Non trasformare una service request in incident; se c'è urgenza, rivaluta la priorità con il manager.
"""  # noqa: E501

