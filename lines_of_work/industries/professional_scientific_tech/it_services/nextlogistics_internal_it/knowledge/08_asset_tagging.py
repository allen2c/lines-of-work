title = "IT 資産タグ管理規定"

content = """
*   全ての IT 資産（PC、サーバ、ネットワーク機器）に固有の資産タグを貼付します。
*   タグには QR コードが含まれており、スキャンすると資産管理システムの詳細ページへ遷移します。
*   資産の移動（部署変更など）发生时、タグをスキャンして所在情報を更新する義務があります。
*   年 1 回の棚卸し時に、全資産のタグ現物確認を行い、不一致場合は調査報告書を提出します。
*   タグが剥離した場合、情報システム部へ再発行を依頼し、旧タグは無効化処理を行います。
"""  # noqa: E501

