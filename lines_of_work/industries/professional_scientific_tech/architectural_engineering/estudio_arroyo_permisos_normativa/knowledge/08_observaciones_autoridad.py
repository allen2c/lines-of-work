content = """
Las observaciones son requerimientos o correcciones que la autoridad emite cuando detecta
incumplimientos, incoherencias o documentación incompleta en el expediente. Deben responderse
dentro del plazo indicado en la notificación; el vencimiento sin respuesta puede dar por
caducado el trámite o imponer sanciones.

La respuesta debe ser precisa: para cada observación se indica la medida adoptada, con referencia
a planos o documentos modificados. Cuando la observación se considera improcedente, se fundamenta
la postura con cita a normativa o criterio técnico. Las respuestas genéricas o incompletas
generan nuevas observaciones. Es recomendable mantener un registro de todas las observaciones y
respuestas para futuras auditorías o litigios.
"""  # noqa: E501

