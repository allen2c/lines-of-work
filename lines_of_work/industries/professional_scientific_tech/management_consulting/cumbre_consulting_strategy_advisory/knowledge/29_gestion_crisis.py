content = """
Una crisis corporativa es cualquier evento que amenaza de manera significativa la reputación,
las operaciones, las finanzas o la seguridad de una organización y que requiere una respuesta
extraordinaria fuera de los protocolos operativos normales. La capacidad de gestionar crisis
con eficacia distingue a las organizaciones resilientes de las que se ven sobrepasadas por
los eventos.

**Tipos de Crisis Comunes**

Las crisis más frecuentes en el contexto latinoamericano incluyen: crisis de reputación
(escándalo de corrupción, fraude, conducta inapropiada de un directivo), crisis operativa
(falla en productos o servicios que afecta masivamente a clientes), crisis laboral (huelga,
accidente grave, denuncias de condiciones laborales), crisis regulatoria (sanciones o
investigaciones de autoridades) y crisis ambiental (derrame, contaminación, impacto en
comunidades).

**Preparación: El Plan de Crisis**

Las organizaciones bien preparadas tienen un plan de crisis que define: el equipo de gestión
de crisis (quién toma decisiones, quién comunica externamente, quién coordina internamente),
los escenarios de crisis más probables con protocolos de respuesta predefinidos, los
portavoces autorizados y sus mensajes clave, y los canales de comunicación interna y externa
que se activarán en cada escenario.

**Las Primeras 24 Horas**

Las primeras horas de una crisis son las más críticas. Las prácticas recomendadas son: reunir
los hechos antes de comunicar (pero no esperar indefinidamente a tenerlos todos), emitir una
primera comunicación que reconozca la situación sin especular sobre causas o responsabilidades,
activar el equipo de crisis y establecer una cadencia de actualización. El silencio
prolongado crea vacíos que se llenan con rumores.

**Comunicación en Crisis**

Los principios de comunicación en crisis son: transparencia (no ocultar ni minimizar), empatía
(reconocer el impacto en las personas afectadas), responsabilidad (comprometerse con acciones
concretas, no solo palabras) y consistencia (un único mensaje coordinado desde todos los
voceros). Mentir o contradecirse durante una crisis multiplica el daño reputacional.
"""

