content = """
El design thinking es un enfoque centrado en el ser humano para la resolución de problemas
complejos, que prioriza la empatía profunda con el usuario, la generación divergente de
ideas y la validación iterativa antes de comprometerse con una solución. En consultoría de
gestión, complementa el rigor analítico cuantitativo con métodos cualitativos de comprensión
del comportamiento humano.

**Las Cinco Etapas del Design Thinking**

El proceso de design thinking comprende cinco etapas no lineales: (1) Empatizar: comprender
profundamente al usuario o cliente a través de observación directa, entrevistas y inmersión
en su contexto; (2) Definir: sintetizar los hallazgos de la empatía en un enunciado del
problema centrado en las necesidades del usuario (Point of View); (3) Idear: generar una
gran cantidad de ideas divergentes sin filtrar prematuramente; (4) Prototipar: construir
representaciones rápidas y de bajo costo de las soluciones más prometedoras; y (5) Testear:
validar los prototipos con usuarios reales y aprender para iterar.

**Aplicaciones en Consultoría de Gestión**

El design thinking es especialmente valioso en: rediseño de la experiencia del cliente
(cuando los modelos de análisis cuantitativos no capturan los factores emocionales y
conductuales de la decisión), diseño de nuevos productos o servicios, rediseño de procesos
que involucran alta interacción humana, y desarrollo de estrategias de cambio organizacional
donde la adopción depende del comportamiento de personas.

**Herramientas de Design Thinking**

Las herramientas más utilizadas incluyen: mapas de empatía (qué piensa, siente, ve, escucha
y hace el usuario), personas (perfiles representativos basados en investigación real), mapas
de customer journey, "¿Cómo podríamos...?" como estructura de ideación, storyboards para
comunicar la solución y minimum viable products (MVP) para validar hipótesis con el mínimo
de recursos.

**Limitaciones y Complementariedad**

El design thinking no reemplaza el análisis cuantitativo ni la planificación estratégica;
los complementa. Sus principales limitaciones son: requiere acceso real a usuarios (no es
un ejercicio de escritorio), puede producir ideas que no son escalables o financieramente
viables si no se integran con el análisis de negocio, y el proceso iterativo puede ser
difícil de acotar en entornos con plazos cortos. Cumbre Consulting integra design thinking
con rigor analítico en proyectos donde la dimensión humana es crítica.
"""

