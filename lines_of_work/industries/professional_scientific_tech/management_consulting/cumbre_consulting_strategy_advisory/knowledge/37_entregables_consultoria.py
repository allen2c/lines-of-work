title = "Tipos de Entregables en Proyectos de Consultoría"

content = """
Los entregables son los productos concretos que Cumbre Consulting produce para sus clientes
durante y al cierre de un compromiso. Su calidad y claridad son la evidencia tangible del
valor generado. Cada tipo de entregable tiene un propósito específico y estándares de
calidad propios.

**Diagnóstico y Análisis**

El informe de diagnóstico documenta el estado actual de la organización, identificando
fortalezas, debilidades y brechas respecto a las mejores prácticas o la situación competitiva.
Incluye: metodología utilizada, fuentes de información, hallazgos principales (con evidencia),
análisis de causas raíz y una priorización de las áreas de mayor impacto. Es el punto de
partida de cualquier engagement estratégico.

**Documentos de Estrategia**

El documento de estrategia articula la posición estratégica de la empresa, las iniciativas
prioritarias y el plan de acción. Tiene dos versiones: una versión de directorio (diez a
quince diapositivas, orientada a decisiones) y una versión de trabajo (documento completo
con análisis, supuestos y planes detallados). La versión de directorio debe poder leerse
sin la versión de trabajo; la versión de trabajo documenta el razonamiento detrás de cada
recomendación.

**Hojas de Ruta y Planes de Implementación**

La hoja de ruta (roadmap) es el plan temporal de las iniciativas estratégicas: qué se hace,
en qué secuencia, con qué recursos y con qué hitos de control. El nivel de detalle varía
según el horizonte: los primeros seis meses son específicos y accionables; los siguientes
años son indicativos y sujetos a revisión. Los planes de implementación individuales por
iniciativa especifican actividades, responsables, fechas y presupuesto.

**Modelos de Decisión y Valoración**

Los modelos financieros (Excel o herramientas equivalentes) son entregables que el cliente
puede usar para analizar escenarios, actualizar supuestos y simular el impacto de decisiones.
Deben ser auditables, bien documentados y entregados con una sesión de capacitación para
que el equipo del cliente los pueda operar de manera autónoma.

**Manuales y Documentación de Procesos**

En proyectos de mejora de procesos o transformación operativa, los manuales documentan los
nuevos procesos, roles, responsabilidades y políticas. Son entregables de transferencia de
conocimiento que aseguran sostenibilidad sin dependencia permanente de la firma consultora.
"""

