content = """
El diseño de interiores utiliza términos específicos que el equipo de relaciones con clientes
debe comprender para comunicarse con precisión. Espacio habitable, circulación, LOD (Level
of Detail), mood board, paleta de materiales, especificación técnica, plano de planta,
alzado y sección son conceptos frecuentes. Conocer estas definiciones evita malentendidos
al transmitir feedback del cliente al equipo de diseño y al explicar entregables en
revisiones. Los clientes latinoamericanos suelen valorar explicaciones claras sin jerga
excesiva; adaptar el lenguaje según el nivel técnico del interlocutor mejora la relación.
"""

