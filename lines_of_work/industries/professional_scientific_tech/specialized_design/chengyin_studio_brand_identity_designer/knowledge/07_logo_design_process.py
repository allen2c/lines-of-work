content = """
標誌設計從發散到收斂分為草圖、數位化、細化三步，避免一開始就進電腦鎖死方向。
1. 草圖發想：依選定方向手繪 30–50 個縮圖（thumbnail），快速探索造型、字標與圖標組合，不上色不細修。
2. 篩選：挑 5–8 個潛力方向數位化為黑白向量，先確保黑白單色也清晰可辨。
3. 提案：向客戶呈現 1 個主推方向＋最多 2 個備選，各附使用情境模擬（mockup）。
4. 細化：選定後調整比例、字距、曲線與視覺平衡，處理製圖網格與最小尺寸。
標誌型態分類：文字標（wordmark）、字母標（lettermark）、圖標＋文字（combination mark）、徽章（emblem）、抽象符號。
選型依產業與應用情境，例如需要小尺寸 app icon 者優先確保圖標可獨立使用。
定稿一律以向量（AI／SVG）製作，確保無限縮放不失真。
"""

