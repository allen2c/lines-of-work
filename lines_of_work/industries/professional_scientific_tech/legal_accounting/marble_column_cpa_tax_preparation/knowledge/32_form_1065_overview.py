title = "Form 1065 Partnership Return Overview"

content = """
Form 1065 is the federal information return for partnerships. Partnerships
do not pay federal income tax; items flow through to partners on Schedule K-1.
Marble Column CPA prepares 1065 and ensures K-1 allocation is correct.

**Filing Requirement:** Domestic partnerships must file 1065. Due date is
March 15 (or 15th of third month after fiscal year end). Extensions apply to
the entity and to K-1 issuance.

**Schedules:** Schedule K summarizes partnership income, deductions, credits.
Schedule K-1 allocates to each partner. Schedule M-1/M-2 reconcile book to
tax. Form 1065-B for electing large partnerships. Disregarded entities may
report on owner's return in some cases.

**Basis and Capital:** Track partner capital accounts and basis. Negative
capital may have implications for loss deductibility and distribution
treatment. Document substantial built-in loss when applicable.
"""  # noqa: E501

