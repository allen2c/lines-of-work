title = "Bonus Depreciation Rules"

content = """
Bonus depreciation allows an immediate deduction of a percentage of the adjusted
basis of qualified property in the year placed in service. The percentage has
phased down under TCJA; Marble Column CPA verifies current law for each
tax year.

**Eligibility:** Property must have a recovery period of 20 years or less,
be water utility property, or be certain computer software or qualified
improvement property. Original use must generally be with the taxpayer, or
property must be qualified used property meeting specified criteria.

**Coordination:** Bonus applies to basis after Section 179. Remaining basis
is depreciated under MACRS. Electing out of bonus is permitted; consider when
depreciation would be more valuable in future years (e.g., expected higher
marginal rates or NOL utilization).

**Qualified Improvement Property:** Post-TCJA fix made QIP 15-year property
eligible for bonus. Verify that software correctly applies the fix for
post-2017 improvements.
"""  # noqa: E501

