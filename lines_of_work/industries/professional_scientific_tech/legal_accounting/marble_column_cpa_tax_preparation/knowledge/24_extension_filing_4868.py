title = "Extension Filing (Form 4868)"

content = """
Form 4868 extends the time to file individual income tax returns by six months
(to October 15). It does not extend the time to pay. Marble Column CPA files
extensions when needed and ensures clients understand payment obligations.

**Filing:** Can be filed electronically or on paper. Extension is automatic
when properly filed by the original due date. No reason need be stated.

**Payment:** Tax liability must be paid by the original due date to avoid
interest and most penalties. Underpayment of estimated tax penalties may apply.
Extension to file does not extend payment deadline. Recommend paying estimated
balance when filing extension.

**State Extensions:** Many states automatically extend when federal extension
is filed; others require separate state extension. Verify each state's rules.
Some states have different extension periods. Document in multistate files.
"""  # noqa: E501

