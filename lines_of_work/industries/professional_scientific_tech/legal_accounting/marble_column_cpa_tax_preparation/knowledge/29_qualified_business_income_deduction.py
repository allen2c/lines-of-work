title = "Qualified Business Income Deduction (Section 199A)"

content = """
Section 199A allows up to 20% deduction of qualified business income (QBI) from
pass-through entities and sole proprietorships. Marble Column CPA applies
wage/property limits, phaseouts, and specified service trade or business
(STSB) exclusions. Rules are complex; verify calculations.

**QBI:** Net amount of qualified items of income, gain, deduction, and loss from
a qualified trade or business. Excludes investment-type income, reasonable
compensation, and guaranteed payments. Aggregation or separate treatment
elections may apply.

**W-2 Wage and Property Limit:** Deduction generally cannot exceed the greater
of 50% of W-2 wages or 25% of W-2 wages plus 2.5% of unadjusted basis of
qualified property. Phase-in for STSB and threshold amounts.

**STSB:** Specified service trades (health, law, accounting, consulting, etc.)
face phaseout of the deduction when taxable income exceeds threshold. Document
the calculation and any aggregation decisions.
"""  # noqa: E501

