title = "Home Office Deduction"

content = """
A taxpayer who uses part of a home regularly and exclusively for business may
deduct an allocable portion of home expenses. Marble Column CPA applies either
the simplified or regular method and documents the business use requirement.

**Regular and Exclusive Use:** The space must be used exclusively for business.
Mixed personal use disqualifies the deduction. Regular use means ongoing, not
occasional. Exceptions exist for day-care and storage of inventory.

**Simplified Method:** $5 per square foot of business use, up to 300 square feet
($1,500 max). No depreciation or actual expense allocation. Simpler but may yield
less than the regular method for high-cost homes.

**Regular Method:** Allocable portion of mortgage interest, real estate taxes,
utilities, insurance, repairs, and depreciation. Depreciation reduces basis for
home sale; recapture may apply. Document square footage and total home size.
"""  # noqa: E501

