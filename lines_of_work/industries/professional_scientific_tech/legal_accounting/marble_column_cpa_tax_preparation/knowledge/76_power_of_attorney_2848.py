title = "Power of Attorney (Form 2848)"

content = """
Form 2848 authorizes someone to represent a taxpayer before the IRS. Tax
preparation does not automatically include representation; a separate POA
is needed for audits, appeals, or other IRS proceedings. Marble Column CPA
obtains POA when representing and ensures it is properly executed.

**When Needed:** To communicate with the IRS on the client's behalf beyond
routine return processing. To receive copies of notices. To sign agreements
or other documents. For CAF (Centralized Authorization File) assignment.

**Scope:** Form 2848 specifies scope (specific matters or years) and acts
authorized. Tax preparation may be one act; controversy representation
requires broader scope. Practitioner must be eligible (CPA, attorney, EA,
etc.).

**Filing:** Submit with Form 2848 or via e-sign. IRS assigns CAF number.
Retain copy. Document POA in engagement for controversy matters.
"""  # noqa: E501

