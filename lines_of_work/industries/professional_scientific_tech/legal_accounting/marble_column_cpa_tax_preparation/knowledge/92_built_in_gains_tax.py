title = "Built-In Gains Tax for S Corporations"

content = """
C corporations that elect S status may owe built-in gains (BIG) tax on
disposition of appreciated assets within the recognition period (generally
5 years, longer for some). Marble Column CPA calculates BIG tax when
applicable and reports on Form 1120-S.

**Trigger:** Sale or distribution of asset with built-in gain (FMV exceeds
basis) at time of S election, within recognition period. Gain is
taxed at entity level (corporate rate). Pass-through to shareholders is
separate. LIFO recapture tax may also apply.

**Calculation:** Identify built-in gain assets. On disposition, recognize
BIG. Tax at corporate rate. Report on Form 1120-S. Reduce gain by NOL
or credit carryforwards from C corp years if applicable.

**Documentation:** Track recognition period and built-in gain assets.
Document BIG tax calculation. Advise on timing of dispositions to minimize
BIG tax when planning opportunities exist.
"""  # noqa: E501

