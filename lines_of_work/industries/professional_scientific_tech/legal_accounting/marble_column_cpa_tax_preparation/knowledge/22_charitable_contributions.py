title = "Charitable Contributions Deduction"

content = """
Cash and property contributions to qualifying organizations may be deductible
subject to AGI limits. Marble Column CPA requires substantiation and applies
current limits and ordering rules. No deduction without proper documentation.

**Qualified Organizations:** 501(c)(3) and similar entities. Verify status via
IRS EO Select Check or acknowledgment. Political contributions are not deductible.

**Cash Contributions:** Deduction up to 60% of AGI (subject to current law).
Retain bank record or written acknowledgment from charity. No deduction for
cash over $250 without contemporaneous written acknowledgment.

**Property:** Deduct FMV for appreciated capital gain property held long-term,
subject to 30% AGI limit unless reduced. Ordinary income property deducted at
cost. Form 8283 required for non-cash over $500. Appraisal required for
property over $5,000 (except publicly traded securities). Document valuation.
"""  # noqa: E501

