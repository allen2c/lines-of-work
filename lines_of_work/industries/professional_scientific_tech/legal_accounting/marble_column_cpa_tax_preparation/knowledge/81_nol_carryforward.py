title = "Net Operating Loss Carryforward"

content = """
Net operating losses (NOLs) may be carried forward (and in limited cases
back) to offset taxable income. Post-TCJA rules limit NOL deduction to 80%
of taxable income and generally eliminate carryback for most taxpayers.
Marble Column CPA applies current NOL rules.

**Calculation:** NOL is excess of deductions over gross income with
modifications. Certain items are added back or excluded. NOL from pass-through
entities flows to owners. Document NOL computation.

**Carryforward:** 80% limit applies to post-2017 NOLs. Pre-2018 NOLs may
have different rules. 20-year carryforward for pre-2018 NOLs. CARES Act
and subsequent legislation may have temporarily changed rules; verify for
each year.

**Ordering:** Apply NOL in proper order (e.g., pre-2018 NOLs may be used
before post-2017). Track NOL by year of origin. Document schedule.
"""  # noqa: E501

