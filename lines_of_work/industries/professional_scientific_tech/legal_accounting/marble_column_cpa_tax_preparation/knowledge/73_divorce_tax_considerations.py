title = "Divorce Tax Considerations"

content = """
Divorce affects filing status, dependency, alimony, property division, and
retirement accounts. Marble Column CPA addresses these on the return and
coordinates with divorce decrees and settlement agreements.

**Filing Status:** As of December 31. Married persons may file jointly or
separately; divorced by year-end files as single or head of household.
Children of divorced parents: custodial parent generally claims dependent
unless release (Form 8332) provided.

**Property Division:** Generally not taxable when pursuant to decree.
Transfers of property between spouses incident to divorce are typically
tax-free under Section 1041. Recipient takes transferor's basis. Document
the transfer and basis allocation.

**Retirement Accounts:** QDRO (qualified domestic relations order) required
for division of qualified plans. IRA transfer pursuant to decree may be
tax-free. Advise on timing and form of transfer to avoid unintended tax.
"""  # noqa: E501

