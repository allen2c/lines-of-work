title = "Cryptocurrency and Virtual Currency Reporting"

content = """
Virtual currency is property for tax purposes. Sales, exchanges, and
payments using crypto generate taxable events. Marble Column CPA asks
about crypto transactions and reports them correctly. Question 1 on
Form 1040 requires a yes/no response.

**Taxable Events:** Sale or exchange of crypto for dollars or other property,
use of crypto to pay for goods or services, exchange of one crypto for another.
Each is a disposition; gain or loss is recognized. Cost basis and fair market
value at transaction determine gain or loss.

**Reporting:** Report on Schedule D or Form 8949. Maintain records of
acquisition date, cost basis, and disposition. Exchanges may produce
multiple transactions. Mining and staking are income at fair market value.

**Information Reporting:** Brokers may issue 1099 forms for certain
transactions. Proposed regulations expand reporting; monitor updates.
Document client's crypto activity and reporting approach in file.
"""  # noqa: E501

