title = "Material Participation in Real Estate"

content = """
Material participation determines whether rental activities are passive or
nonpassive for real estate professionals. The Section 469(h) tests apply.
Marble Column CPA applies the tests and documents participation.

**Tests:** 500+ hours in the activity; or 100+ hours and no one else
participates more; or 500+ hours in multiple activities; or 100+ hours
in each of several activities aggregating 500+; or participation in
substantially all activities; or 100+ hours plus other tests; or
qualification in 5 of 10 prior years. Logs must support the hours.

**Grouping:** Taxpayer may group or not group rental activities. Grouping
affects material participation determination. Once made, grouping is
binding. Document grouping elections.

**Records:** Contemporaneous time logs, calendars, and other records support
the hours. Estimates prepared at year-end are weaker. Advise clients to
maintain logs throughout the year.
"""  # noqa: E501

