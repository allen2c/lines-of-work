content = """
The 1099 series reports various types of income other than wages. Payers must
furnish these forms to recipients and file copies with the IRS. Marble Column
CPA ensures all 1099 income is reported and properly classified.

**Common Types:** 1099-NEC (nonemployee compensation, replaces 1099-MISC for
services), 1099-INT (interest), 1099-DIV (dividends), 1099-B (broker transactions),
1099-R (retirement distributions), 1099-G (government payments, including refunds),
1099-K (payment card and third-party network transactions), 1099-S (real estate
sales), 1099-MISC (rent, royalties, other).

**Reconciliation:** The IRS receives copies of 1099s. Underreporting triggers
CP2000 notices. Reconcile 1099 amounts to the return; explain differences (e.g.,
basis adjustment, return of capital, reporting error) in the file.

**1099-K Considerations:** Reporting thresholds for 1099-K have changed. Gross
payment amounts may not equal taxable income; deduct fees, returns, and
non-taxable amounts. Document the calculation.
"""  # noqa: E501

