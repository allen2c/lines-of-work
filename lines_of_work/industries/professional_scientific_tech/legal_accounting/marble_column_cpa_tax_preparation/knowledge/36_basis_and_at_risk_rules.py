title = "Basis and At-Risk Rules"

content = """
Basis limits loss deductions from pass-through entities and other activities.
At-risk rules further limit deductions to amounts the taxpayer has at risk.
Marble Column CPA tracks basis and at-risk amounts and applies limitations.

**Basis:** In partnerships, basis includes contributed property (at FMV or
adjusted basis per rules), share of income, and increases for recourse
liabilities. Decreases for distributions, share of losses, and liability
decreases. Cannot go below zero for loss purposes.

**At-Risk:** Deductible losses limited to amount at risk. At risk includes
cash and basis of property contributed, income share, and recourse debt for
which taxpayer is liable. Nonrecourse debt generally not at risk except for
qualified nonrecourse financing in real estate. Excess losses carry forward.

**Documentation:** Maintain basis and at-risk schedules for each activity.
Reconcile to prior year and K-1. Document carryovers when limited.
"""  # noqa: E501

