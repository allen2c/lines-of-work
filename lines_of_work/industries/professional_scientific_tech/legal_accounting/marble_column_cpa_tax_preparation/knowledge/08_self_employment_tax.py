title = "Self-Employment Tax (Social Security and Medicare)"

content = """
Self-employment tax is the equivalent of FICA for employees, paid by sole
proprietors and partners on net earnings from self-employment. Marble Column CPA
calculates and reports this on Schedule SE and ensures clients understand the
combined burden.

**Rate:** 15.3% (12.4% Social Security on earnings up to the wage base, 2.9%
Medicare on all earnings; additional 0.9% Medicare on earnings above threshold
for high earners). The employee and employer portions are both paid by the
self-employed individual.

**Net Earnings:** Generally 92.35% of net profit from Schedule C (or Schedule
F, or net SE income from partnerships). This approximates net of the employer
half of FICA. Reduction for unreimbursed employee business expenses is no
longer available for most taxpayers.

**Deduction:** One-half of SE tax is deductible in calculating AGI. This
reduces income tax but not SE tax itself. Estimated tax payments must account
for both income tax and SE tax to avoid underpayment penalties.
"""  # noqa: E501

