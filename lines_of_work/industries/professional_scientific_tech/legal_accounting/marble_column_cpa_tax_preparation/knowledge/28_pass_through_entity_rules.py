title = "Pass-Through Entity Tax Rules"

content = """
Partnerships and S corporations are generally pass-through entities: income
and loss flow to owners, who report on their individual returns. Marble
Column CPA applies flow-through rules, basis limitations, and entity-level
elections correctly.

**Flow-Through:** Income retains its character (ordinary, capital, passive,
etc.). Deductions and credits flow through. Estimated tax on pass-through
income is the owner's responsibility. Entity may make elections (e.g., Section
179, depreciation) that affect owner amounts.

**Basis Limitations:** Loss deduction limited to basis in interest and
(at-risk rules) amount at risk. Passive activity rules further limit
deduction of passive losses against nonpassive income. Excess amounts
carry forward. Document basis schedules in files.

**State Considerations:** Some states tax partnerships or S corps at entity
level. Pass-through entity tax (PTET) elections allow entity-level state
tax payment to provide SALT cap workaround for owners. Check state rules.
"""  # noqa: E501

