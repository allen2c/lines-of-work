title = "Alimony Treatment (Post-2018 Law)"

content = """
For divorce or separation instruments executed after December 31, 2018 (or
modified after that date to opt in), alimony is not deductible by the payor
and not includible by the payee. Pre-2019 instruments generally retain
prior law (deductible/includible) unless modified.

**Identification:** Determine instrument date and whether modified. Modified
instruments may adopt new treatment if expressly stated. Document the
instrument and modification in the file.

**Pre-2019 Instruments:** Payor deducts alimony; payee includes. Must be
cash payments under divorce or separation instrument. Designated as alimony.
Front-loading recapture rules applied to pre-2019. Verify ongoing
compliance with requirements.

**State Conformity:** States may differ on alimony treatment. Check state
law for each filing. Document state treatment in multistate files.
"""  # noqa: E501

