title = "Gig Economy and Platform Income"

content = """
Income from gig work, freelancing, and online platforms is taxable. Marble
Column CPA reports this income on Schedule C or appropriate schedule and
ensures expenses are properly deducted. 1099-K and 1099-NEC may be issued.

**Classification:** Generally self-employment income. Report gross receipts,
less allowable expenses. Platform fees and payment processor fees reduce
gross. Maintain records of income and expenses; platform summaries may
supplement but do not replace records.

**Deductions:** Home office, equipment, vehicle (actual or mileage), supplies,
subscriptions, and other ordinary and necessary business expenses. Personal
use must be allocated. Document business purpose.

**Tax Obligations:** Self-employment tax applies. Estimated tax payments
may be needed. Consider quarterly filing. Advise clients on record-keeping
and estimated tax to avoid surprises at year-end.
"""  # noqa: E501

