title = "Education Credits (AOTC and Lifetime Learning)"

content = """
The American Opportunity Tax Credit (AOTC) and Lifetime Learning Credit (LLC)
reduce tax for qualifying education expenses. Marble Column CPA determines
which credit applies and ensures expenses are properly substantiated.

**AOTC:** Up to $2,500 per eligible student, 100% of first $2,000 of qualified
expenses plus 25% of next $2,000. Refundable up to 40%. Four years of postsecondary
education per student. Student must be pursuing a degree, half-time or more,
and have no felony drug conviction. Phaseout by AGI.

**LLC:** Up to $2,000 per return (not per student), 20% of up to $10,000
qualified expenses. Nonrefundable. No limit on years. Covers undergraduate,
graduate, and job-related courses. Phaseout by AGI.

**Qualified Expenses:** Tuition and required fees. Room and board generally
excluded. Books and supplies qualify for AOTC if required. 1098-T reports
amounts; reconcile and coordinate with 529 or other education benefits.
"""  # noqa: E501

