title = "Installment Sale Reporting"

content = """
The installment method allows reporting gain from certain sales as payments
are received rather than in the year of sale. Marble Column CPA elects or
applies the method correctly and tracks basis recovery.

**Eligibility:** Generally available for real property and casual sales of
personal property when at least one payment is received after the year of
sale. Cannot use for inventory, marketable securities (with exceptions), or
revolving credit plans.

**Computation:** Gross profit ratio = gross profit / contract price. Each
payment includes interest (reportable) and principal. Principal is allocated
between recovery of basis and gain per the ratio. Form 6252 reports.

**Disposition of Installment Obligation:** Sale or other disposition of the
note accelerates remaining gain. Document the installment sale and any
dispositions in the client file.
"""  # noqa: E501

