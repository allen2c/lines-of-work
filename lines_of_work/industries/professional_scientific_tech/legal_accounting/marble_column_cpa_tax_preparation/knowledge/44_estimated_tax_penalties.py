title = "Estimated Tax Penalties"

content = """
Underpayment of estimated tax results in a penalty under Section 6654
(individuals) or 6655 (corporations). Marble Column CPA calculates the
penalty or helps clients avoid it through adequate withholding and
estimated payments.

**Safe Harbors:** Pay 90% of current-year tax or 100% (110% for high income)
of prior-year tax through withholding and estimated payments. Farmers and
fishermen have separate rules. Annualized method can reduce penalty when
income is uneven.

**Form 2210:** Use to calculate penalty or to request waiver. Waiver may
apply for retirement, disability, or casualty. First-time filers or
significant income changes may support waiver. Document the rationale.

**Avoidance:** Increase withholding (can be done late in the year) or make
larger estimated payments. Withholding is treated as spread evenly over the
year even if withheld late; estimated payments are credited when made.
"""  # noqa: E501

