title = "Schedule C Sole Proprietorship"

content = """
Schedule C reports profit or loss from a trade or business operated as a sole
proprietor. Marble Column CPA ensures income and expenses are correctly
classified and substantiated. Net profit flows to Form 1040 and is subject to
self-employment tax.

**Income:** Report gross receipts. Exclude sales tax collected for remittance.
Income is taxable when received (cash method) or when earned (accrual method)
depending on accounting method elected.

**Expenses:** Must be ordinary and necessary for the business. Common categories
include supplies, advertising, car and truck (actual or mileage), insurance,
interest, office expense, contract labor, repairs, depreciation, and home office.
Personal expenses are not deductible. Commingled accounts require allocation.

**Home Office:** Deduction allowed when space is used regularly and exclusively
for business. Simplified method: $5 per square foot up to 300 sq ft. Regular
method: allocable portion of home expenses. Document use in client file.
"""  # noqa: E501

