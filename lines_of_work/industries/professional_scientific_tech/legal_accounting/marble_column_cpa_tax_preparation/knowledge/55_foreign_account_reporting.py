title = "Form 8938 Specified Foreign Financial Assets"

content = """
Form 8938 reports specified foreign financial assets when thresholds are
exceeded. It accompanies the tax return. Marble Column CPA identifies
clients with reportable assets and ensures the form is filed when required.

**Thresholds:** Higher for residents (e.g., $50,000 single, $100,000 MFJ on
last day or $75,000/$150,000 anytime) and lower for nonresidents. Different
thresholds for married filing separately.

**Specified Assets:** Financial accounts at foreign institutions, interests in
foreign entities (if not reported on other forms), and certain foreign
instruments. Exclusions apply (e.g., accounts reported on FBAR only in some
contexts; see instructions for coordination).

**Penalties:** Failure to file: $10,000, with additional penalties for
continued failure after notice. Reasonable cause defense available. Document
inquiry and any determination that filing is not required.
"""  # noqa: E501

