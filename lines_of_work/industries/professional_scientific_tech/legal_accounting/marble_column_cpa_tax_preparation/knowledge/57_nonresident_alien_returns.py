title = "Nonresident Alien Tax Returns"

content = """
Nonresident aliens (NRAs) with U.S.-source income may need to file Form
1040-NR. Different rules apply than for U.S. persons. Marble Column CPA
handles straightforward NRA returns and refers complex cases to international
tax specialists.

**Filing Requirement:** Generally required when NRA has U.S.-source income
that is not fully withheld or when filing to claim treaty benefits or
refunds. No joint return with nonresident spouse; married filing separately
unless both elect to be taxed as residents.

**Taxation:** NRAs typically taxed only on U.S.-source income. FDAP income
(interest, dividends, etc.) may be subject to 30% withholding unless
treaty reduces. Effectively connected income (ECI) taxed at graduated
rates. Different deduction and credit rules apply.

**Documentation:** W-8BEN for treaty benefits. Form 1040-NR schedules.
Document residency determination and treaty application in file.
"""  # noqa: E501

