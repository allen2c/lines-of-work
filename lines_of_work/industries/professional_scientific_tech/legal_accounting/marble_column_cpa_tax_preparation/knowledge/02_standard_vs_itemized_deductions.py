title = "Standard Deduction vs Itemized Deductions"

content = """
Taxpayers may claim either the standard deduction or itemized deductions, but not
both. The choice depends on which yields the larger reduction in taxable income.
Marble Column CPA compares both methods for every individual return.

**Standard Deduction:** A fixed amount that varies by filing status. Additional
amounts apply for taxpayers age 65 or older or blind. The standard deduction is
indexed for inflation and is generally advantageous for taxpayers without
significant deductible expenses.

**Itemized Deductions (Schedule A):** Include medical expenses (subject to AGI
floor), state and local taxes (SALT) capped at $10,000, mortgage interest
(subject to acquisition debt limits), charitable contributions, casualty and
theft losses (federally declared disaster only), and miscellaneous deductions
as permitted.

**Decision:** When itemized deductions marginally exceed the standard deduction,
consider bunching charitable contributions or other controllable deductions into
alternate years to maximize benefit. Document the comparison in the client file.
"""  # noqa: E501

