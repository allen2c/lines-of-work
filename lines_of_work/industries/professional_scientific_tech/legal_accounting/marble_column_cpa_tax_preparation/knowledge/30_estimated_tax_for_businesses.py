title = "Estimated Tax for Businesses"

content = """
Partnerships, S corporations, and sole proprietors may need to make estimated
tax payments. Marble Column CPA calculates required payments and advises on
timing. Underpayment penalties apply to entities and owners.

**Partnerships and S Corps:** Generally no entity-level estimated tax for
federal income tax (income flows through). May have state-level requirements.
Estimated tax is the owners' responsibility based on their share of income.

**Sole Proprietors:** Combine personal and business income. Pay estimated tax
on self-employment income and other income not subject to withholding.
Include self-employment tax in the calculation.

**Payment Dates:** Same quarterly dates as individuals. Use Form 1040-ES for
individual estimated tax. Some entities use Form 1120-W for corporate
estimated tax when applicable. Document the calculation in the file.
"""  # noqa: E501

