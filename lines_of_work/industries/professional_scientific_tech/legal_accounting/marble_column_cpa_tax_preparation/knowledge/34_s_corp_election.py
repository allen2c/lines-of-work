title = "S Corporation Election"

content = """
Corporations may elect S status to be treated as pass-through entities. The
election has eligibility requirements and timing rules. Marble Column CPA
advises on election and ensures proper Form 2553 filing.

**Eligibility:** Domestic corporation, 100 or fewer shareholders, one class of
stock, shareholders must be individuals, estates, certain trusts, or
tax-exempt organizations. No nonresident alien shareholders.

**Election:** File Form 2553. Must be filed by the 15th day of the third
month of the tax year for which effective, or anytime in the preceding tax
year. Late elections may be granted with IRS consent; attach explanation.

**Effective Date:** Generally first day of the tax year when timely filed.
Revocation can be voluntary or inadvertent (e.g., invalid shareholder).
Termination triggers five-year waiting period before re-election. Document
election and any consent in corporate records.
"""  # noqa: E501

