title = "Backup Withholding"

content = """
Backup withholding (24%) may apply to reportable payments when payee fails
to provide TIN or IRS notifies of underreporting. Marble Column CPA
advises clients who receive notices or have backup withholding on 1099s,
and ensures withheld amounts are reported as tax paid on the return.

**When Applied:** Missing or incorrect TIN, payee fails to certify
non‑foreign status, IRS notifies payer of underreporting, payee fails to
certify no underreporting. Common for 1099-NEC, 1099-INT, 1099-DIV, etc.

**Reporting:** Amount withheld is reported on the 1099. Taxpayer claims
withholding as payment on the return. Reconcile to Form 1099. Refund or
credit if overpayment.

**Resolution:** Provide correct TIN and certification to stop backup
withholding. Resolve IRS underreporting notice. Document resolution and
any refund claim in file.
"""  # noqa: E501

