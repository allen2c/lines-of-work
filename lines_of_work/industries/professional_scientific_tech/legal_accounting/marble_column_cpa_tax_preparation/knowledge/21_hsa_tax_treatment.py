title = "Health Savings Account Tax Treatment"

content = """
HSAs offer triple tax benefit: deductible contributions, tax-free growth, and
tax-free distributions for qualified medical expenses. Marble Column CPA
verifies eligibility and coordinates HSA with HDHP coverage and other
tax-advantaged arrangements.

**Eligibility:** Must be covered by a high-deductible health plan (HDHP) and
have no other disqualifying coverage (e.g., general purpose FSA, Medicare).
Cannot be claimed as a dependent. Last-month rule may allow full-year
contribution if eligible on December 1.

**Contribution Limits:** Annual limits for self-only and family coverage; catch-up
for 55+. Employer contributions count toward limit. Excess contributions are
taxable and subject to penalty unless corrected.

**Distributions:** Qualified medical expenses are tax-free. Nonqualified
distributions are taxable plus 20% penalty (exceptions for age 65+, death,
disability). Coordination with itemized medical deduction; cannot double-dip.
"""  # noqa: E501

