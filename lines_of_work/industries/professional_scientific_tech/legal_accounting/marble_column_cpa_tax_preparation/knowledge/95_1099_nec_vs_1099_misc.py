title = "1099-NEC vs 1099-MISC"

content = """
Form 1099-NEC reports nonemployee compensation. Form 1099-MISC reports
other types of payments (rent, royalties, prizes, etc.). Marble Column
CPA reconciles both forms and reports income correctly. Payers must use
the correct form; misclassification can cause confusion.

**1099-NEC:** Nonemployee compensation of $600 or more. Payments for
services performed by someone who is not an employee. Replaces former
use of 1099-MISC Box 7. Due January 31. Copy B to payee, Copy A to IRS.

**1099-MISC:** Rent (Box 1), royalties (Box 2), other income (Box 3),
fishing boat proceeds, medical payments, substitute payments, crop
insurance, other. Box 7 no longer used for NEC. Different boxes for
different payment types. Verify each box.

**Reconciliation:** Report 1099-NEC on Schedule C or appropriate schedule.
1099-MISC amounts may go to Schedule E, other income, or elsewhere
depending on box. Document classification in file.
"""  # noqa: E501

