title = "Gift and Estate Tax Basics for Tax Preparation"

content = """
Gift and estate taxes are transfer taxes; income tax return preparation
touches on these when clients report gifts of property, inheritances, or
trust distributions. Marble Column CPA reports income correctly and refers
complex transfer tax matters to specialists.

**Income Tax on Gifts:** Donor generally does not recognize income on making
a gift. Donee takes donor's basis for gain (and for loss, lesser of basis
or FMV). Gift tax is the donor's responsibility; no income to donee from receipt.

**Inherited Property:** Generally receives stepped-up (or stepped-down) basis
as of date of death (or alternate valuation). Income in respect of a decedent
(IRD) has no step-up. Report inheritance properly for basis and K-1 income.

**Referral:** Estate tax returns, gift tax returns, and complex trust matters
are outside tax preparation scope. Refer to estate planning or controversy
specialists. Document the referral in the file.
"""  # noqa: E501

