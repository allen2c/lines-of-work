title = "Self-Employment Tax for Partners"

content = """
Partners are subject to self-employment tax on their share of partnership
income from activities in which they materially participate. Guaranteed
payments for services are always subject to SE tax. Marble Column CPA
reports SE income on Schedule K-1 and ensures partners pay SE tax.

**General Rules:** Net earnings from self-employment include the partner's
distributive share of income from a trade or business (other than
investment-type income) and guaranteed payments for services. Limited
partners may have exceptions for certain income.

**Exceptions:** Certain investment income, rental income (unless real estate
professional), and capital gains are generally excluded. Remuneration as
employee of the partnership is W-2 wages, not SE income. Document
treatment in K-1 and partner file.

**Schedule SE:** Partner files Schedule SE with Form 1040. One-half of SE
tax is deductible. Coordinate with estimated tax.
"""  # noqa: E501

