title = "FBAR and Foreign Account Reporting"

content = """
U.S. persons with foreign financial accounts exceeding the reporting threshold
must file FinCEN Form 114 (FBAR). Additional reporting may apply (Form 8938 for
specified foreign assets). Marble Column CPA asks about foreign accounts and
refers for preparation when thresholds are met.

**FBAR Threshold:** Aggregate value of foreign accounts exceeding $10,000 at
any time during the year triggers the filing requirement. Includes bank
accounts, brokerage accounts, and certain other financial interests. Signature
authority over others' accounts may require reporting.

**Due Date:** April 15 (automatic extension to October 15). Filed
electronically through FinCEN BSA E-Filing. No extension form; extension
is automatic.

**Penalties:** Non-willful violations: up to $10,000 per violation. Willful:
up to $100,000 or 50% of account value. Voluntary disclosure programs may
reduce exposure. Refer to international tax specialist when complexity exists.
"""  # noqa: E501

