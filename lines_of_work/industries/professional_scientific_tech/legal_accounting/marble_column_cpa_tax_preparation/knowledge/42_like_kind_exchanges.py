title = "Like-Kind Exchanges (Section 1031)"

content = """
A properly structured like-kind exchange defers gain when business or
investment real property is exchanged for like-kind replacement property.
Marble Column CPA ensures qualified intermediary and timing rules are met.
Personal use property does not qualify.

**Qualified Use:** Both relinquished and replacement property must be held
for productive use in a trade or business or for investment. Real property
in the U.S. is like-kind to other U.S. real property. Post-2017, personal
property generally does not qualify.

**Timing:** Identify replacement property within 45 days; close within 180
days of transfer of relinquished property. Strict deadlines; document dates.

**Boot:** Cash or non-like-kind property received is taxable boot. Mortgage
relief may be boot. Partial exchange treatment when boot is received.
Coordinate with qualified intermediary and document exchange agreement.
"""  # noqa: E501

