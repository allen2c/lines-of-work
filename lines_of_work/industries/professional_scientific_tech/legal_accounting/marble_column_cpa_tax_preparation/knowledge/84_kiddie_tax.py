title = "Kiddie Tax Rules"

content = """
The kiddie tax taxes a child's unearned income at the parent's marginal
rate (or trust and estate rates under current law) when the child is under
a specified age or a full-time student. Marble Column CPA applies the
kiddie tax when applicable.

**Applicability:** Child under 18 at year-end, or 18–23 and full-time
student with earned income less than half of support. Child's unearned
income above threshold ($2,500 for 2024, verify current year) is subject.

**Calculation:** Unearned income above threshold taxed at parent's rate (or
formula based on trust brackets). Form 8615 or 8814. Parent may elect to
include child's income on parent's return (Form 8814) in some cases if
requirements met.

**Planning:** Custodial accounts (UGMA/UTMA) generate kiddie tax. 529 and
Coverdell may be alternatives for education. Document kiddie tax
calculation and any elections in file.
"""  # noqa: E501

