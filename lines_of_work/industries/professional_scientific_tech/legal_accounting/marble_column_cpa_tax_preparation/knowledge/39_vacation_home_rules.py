title = "Vacation Home and Mixed-Use Property Rules"

content = """
When a property is used both for rental and personal use, deduction limits
apply. Marble Column CPA allocates expenses and applies the days-used
limitation. Maintain a log of rental and personal use days.

**Days Test:** If personal use exceeds the greater of 14 days or 10% of
rental days, the property is a vacation home and deductions are limited to
rental income (with some ordering rules). No loss deduction; expenses in
excess of rental income are not deductible (but may carry forward in some cases).

**Allocation:** When limits apply, allocate interest and taxes first to
rental use, then other expenses. Personal-use days get no deduction. Document
the day count and allocation worksheet in the file.

**Minimal Personal Use:** If personal use is 14 days or less and 10% or less
of rental days, standard Schedule E rules apply; loss may be deductible
subject to passive rules.
"""  # noqa: E501

