title = "Home Sale Exclusion (Section 121)"

content = """
Taxpayers may exclude up to $250,000 ($500,000 for married filing jointly)
of gain on the sale of a principal residence if ownership and use tests are
met. Marble Column CPA verifies eligibility and reports the sale correctly.

**Ownership and Use:** Must own and use the home as principal residence for
at least two of the five years preceding the sale. The two years need not be
continuous. Only one sale every two years qualifies for full exclusion.

**Partial Exclusion:** If tests not met due to change in employment, health,
or unforeseen circumstances, a reduced exclusion may apply. Document the
qualifying reason.

**Reporting:** Form 1099-S may be issued. Report the sale on Form 8949 and
Schedule D. Excluded gain is not reported as income. Retain records of
purchase, improvements, and sale for basis support.
"""  # noqa: E501

