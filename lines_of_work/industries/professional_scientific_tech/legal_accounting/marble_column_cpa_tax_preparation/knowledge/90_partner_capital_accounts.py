title = "Partnership Capital Accounts"

content = """
Partners have capital accounts that track their equity. Tax capital
accounts (vs. GAAP) must be maintained for allocation and distribution
purposes. Marble Column CPA reconciles capital accounts and ensures
allocations have substantial economic effect.

**Tax Capital Account:** Increased by contributions and allocations of
income; decreased by distributions and allocations of loss. Must be
maintained in accordance with Section 704(b) regulations. Deficit
restoration obligation or qualified income offset may be required for
loss allocations.

**Liquidating Distributions:** Generally follow capital account
balances. Disproportionate distributions may have different tax treatment.
Document distribution provisions in partnership agreement.

**Negative Capital:** May indicate allocation issues or need for
contribution. Review when negative. Document in partnership file.
"""  # noqa: E501

