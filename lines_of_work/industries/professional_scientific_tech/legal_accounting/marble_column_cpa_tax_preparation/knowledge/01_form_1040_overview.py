title = "Form 1040 Overview for Individual Tax Returns"

content = """
Form 1040 is the primary federal income tax return for individuals. It consolidates
income, adjustments, deductions, credits, and tax liability into a unified filing.
Marble Column CPA uses the current-year Form 1040 and applicable schedules as
published by the IRS.

**Structure:** The form flows from gross income through adjusted gross income
(AGI), then to taxable income after standard or itemized deductions. Tax is
calculated, credits are applied, and the result is reconciled with withholding
and estimated payments to determine refund or amount due.

**Key Schedules:** Schedule 1 reports additional income and adjustments to income.
Schedule 2 covers additional taxes. Schedule 3 reports nonrefundable and refundable
credits. Supporting schedules (A, B, C, D, etc.) attach as needed for specific
income types or deductions.

**Filing Requirements:** Filing thresholds depend on filing status, age, and income
sources. Even when not required, filing may be beneficial to claim refundable
credits such as the earned income tax credit.
"""  # noqa: E501

