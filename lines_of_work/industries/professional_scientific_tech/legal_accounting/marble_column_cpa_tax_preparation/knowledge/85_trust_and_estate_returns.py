title = "Trust and Estate Return Overview"

content = """
Trusts and estates file Form 1041. Income is taxed to the trust/estate or
distributed to beneficiaries. Marble Column CPA prepares straightforward
1041s and refers complex estates and trusts to specialists. K-1 (Form
1041) flows to beneficiaries.

**Filing Requirement:** Domestic trusts and estates with income above
threshold or gross income above $600 (or $100 for grantor-type). Due date
generally April 15 for calendar-year; extension available.

**Distribution Deduction:** Trusts and estates may deduct income required to
be distributed and other amounts properly paid. Beneficiaries report
distributable net income (DNI) on their returns. Excess deductions may
pass through to beneficiaries in year of termination.

**Referral:** Complex estates, multiple beneficiaries, discretionary
distributions, and specialty trusts (CRT, CLT, etc.) warrant referral.
Document scope and referral in file.
"""  # noqa: E501

