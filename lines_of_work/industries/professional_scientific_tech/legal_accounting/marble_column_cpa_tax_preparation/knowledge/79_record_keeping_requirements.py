title = "Record Keeping Requirements"

content = """
Taxpayers must maintain records to support return positions. Marble Column
CPA advises clients on what to keep and for how long. Adequate records are
essential for audit defense and amended returns.

**What to Keep:** Receipts, invoices, bank statements, 1099s, W-2s, K-1s,
prior returns, supporting documents for deductions and credits. For
property, acquisition and improvement records for basis. For business,
expense documentation with business purpose.

**Length:** Generally three years from filing or due date, whichever later.
Seven years for bad debt or worthless security. Indefinitely for property
(basis) and certain carryovers. Document destruction policy if applicable.

**Form:** Paper or electronic. Must be retrievable. Organize by year and
category. Advise clients to retain records before discarding; document
advice in engagement letter or organizer.
"""  # noqa: E501

