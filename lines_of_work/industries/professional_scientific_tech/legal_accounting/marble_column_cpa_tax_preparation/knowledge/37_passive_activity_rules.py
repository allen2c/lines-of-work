title = "Passive Activity Loss Rules"

content = """
Passive activity losses generally cannot offset nonpassive income. Losses
shelter passive income and unused losses carry forward. Marble Column CPA
classifies activities as passive or nonpassive and applies the $25,000
real estate professional exception when available.

**Passive Activities:** Include rental activities (with exceptions) and
businesses in which the taxpayer does not materially participate. Material
participation requires 500+ hours, or 100+ hours if no one else participates
more, or other tests.

**Rental Real Estate Exception:** Up to $25,000 of passive loss may offset
nonpassive income for active participants in rental real estate, phased out
by AGI. Real estate professionals can treat rental activities as
nonpassive if they materially participate.

**Carryforward:** Suspended losses carry forward and offset future passive
income or are released on disposition. Document activity classification and
suspended loss amounts in client files.
"""  # noqa: E501

