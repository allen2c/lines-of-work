title = "Partnership Allocation Rules"

content = """
Partnership allocations must have substantial economic effect under Section 704(b)
or meet the alternate test. Special allocations require partnership agreement
support. Marble Column CPA ensures allocations are properly documented and
reported.

**Substantial Economic Effect:** Allocation must affect partner capital
accounts, liquidating distributions must follow capital accounts, and there
must be a deficit restoration obligation or qualified income offset. Partners
must maintain capital accounts per tax rules.

**Special Allocations:** Depreciation, gain/loss, credits may be specially
allocated if they have substantial economic effect. Allocations of
nonrecourse deductions have specific rules. Document agreement terms.

**Partnership Agreement:** Must reflect allocation structure. Amendments
during the year may have timing issues. Review agreement when preparing
return and K-1s. Flag non-standard allocations for review.
"""  # noqa: E501

