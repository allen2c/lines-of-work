content = """
Depreciation allocates the cost of tangible property over its useful life.
Marble Column CPA applies the correct method, recovery period, and convention
for each asset. Errors in depreciation affect current and future years.

**MACRS:** Modified Accelerated Cost Recovery System is the default for most
tangible property placed in service after 1986. Property is classified (3-year,
5-year, 7-year, etc.). Half-year or mid-quarter convention typically applies.

**Section 179:** Allows expensing (up to annual limit) of qualified property
placed in service during the year. Subject to taxable income limitation and
phaseout. Reduces basis before bonus depreciation.

**Bonus Depreciation:** Permits immediate deduction of a percentage of remaining
basis. Percentage has phased down; verify current law. Applies to new and
certain used property. Coordinate with Section 179 and document elections.
"""  # noqa: E501

