title = "Section 179 Expensing Election"

content = """
Section 179 allows taxpayers to expense (deduct in the year placed in service)
the cost of qualified property instead of depreciating it. Marble Column CPA
applies annual dollar limits and taxable income limitations when recommending
the election.

**Qualified Property:** Tangible personal property, certain qualified real
property (improvements to building interiors, roofs, HVAC, etc.), and off-the-shelf
computer software. Property must be used predominantly in the active conduct of
a trade or business. Suitability for Section 179 does not mean bonus depreciation
is unavailable; coordinate both.

**Limitations:** Dollar limit is set annually (indexed). Phaseout begins when
qualified property placed in service exceeds a threshold. Deduction cannot exceed
taxable business income (computed before the Section 179 deduction); excess
carries forward. Aggregation rules apply to related persons.

**Election:** Made on the return; no separate form for most filers. Revocation
requires IRS consent. Document the calculation and carryforward in the file.
"""  # noqa: E501

