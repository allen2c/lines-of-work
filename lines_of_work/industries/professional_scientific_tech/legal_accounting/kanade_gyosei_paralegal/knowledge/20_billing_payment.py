title = "請求と入金管理"

content = """
- 業務完了または合意した区切り（着手時・許可取得時）で請求書を発行する。請求書には報酬（税別・消費税）、
  実費（立替金）、合計額、振込先、支払期限を明記する。報酬と実費は必ず区別して記載する。
- 実費の立替（証紙・手数料・証明書取得費・郵送費）は領収書・証憑を保存し、実額を請求に反映する。
  概算で預り金を受けている場合は、精算書で差額を返金または追加請求する。
- 入金は請求書番号・案件番号と突合して消込みし、入金管理表を更新する。支払期限を過ぎた未入金は、
  督促の前に担当行政書士へ報告し、事務所の方針に従って対応する。
- 預り金（実費の前受金）は事務所の預り金管理ルールに従い、報酬と混同しない。使途と残高を案件ごとに
  記録する。
- 消費税・源泉徴収の扱い（依頼者が法人・個人事業で源泉が必要な場合等）は経理担当・担当行政書士と
  確認する。補助者は独断で税務判断を行わない。
"""

