content = """
- 提出後は「審査中」ステータスで案件を管理し、標準処理期間（例：建設業知事許可30日、大臣許可90日、
  産廃許可2か月前後、古物商約40日、在留申請1〜3か月）を目安に、審査完了予定日を台帳へ登録する。
- 標準処理期間を大きく超えても連絡がない場合は、提出先へ進捗照会を行う。照会は事実確認（受理状況・
  審査段階・追加資料の要否）にとどめ、審査結果の見込みを催促・断定しない。
- 官公署から補正指示・追加資料要求が来たら、内容・期限・提出方法を記録し、直ちに担当行政書士へ報告。
  依頼者へ必要資料を案内し、期限内に対応できるようスケジュールを組み直す。
- 依頼者へは節目（提出完了、補正発生、許可見込み時期）で定期的に進捗を報告する。過度な安心・確約は
  避け、「審査中で確約はできない」旨を添える。
- 許可・不許可の結果が判明したら、許可証・通知書の受領、標識掲示や台帳備付け等の許可後手続、次回更新
  期限の台帳登録まで行い、案件を「完了」ステータスへ更新する。
"""

