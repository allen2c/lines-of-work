content = """
- 特殊車両通行許可は、道路法の一般的制限値（車両総重量20トン、幅2.5m、高さ3.8m、長さ12m等）を超える
  車両が道路を通行する際に必要。トレーラーやクレーン車、建設機械の運搬などで頻出する。
- 申請は「オンライン申請システム（特殊車両通行許可のオンライン申請）」を利用するのが主流で、経路を
  自動算定できる。手数料は1経路あたり200円（申請車両台数×経路数で計算、複数の道路管理者にまたがる
  場合に課される）。国道のみ等の場合は不要のこともある。
- 主な必要情報：車両諸元（車種、軸数、寸法、重量）、車検証、通行経路、積載貨物の内容。車両の型式や
  軸重により、経路ごとに通行条件（時間帯・誘導車の要否）が付されることがある。
- 標準処理期間は経路や道路管理者の数により数週間〜1か月程度。個別審査が必要な経路はより時間を要する
  ため、工事・輸送の予定日から逆算して申請する。
- 補助者の作業：車検証・車両諸元の整理、経路の入力、包括申請（同一車種の複数台）の可否確認、許可証
  （通行条件書を含む）の受領と交付。許可証は運行時に携行が必要と案内する。
"""

