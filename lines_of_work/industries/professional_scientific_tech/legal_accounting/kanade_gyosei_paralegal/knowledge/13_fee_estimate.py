title = "報酬見積の作成"

content = """
- 見積書は「報酬（事務所の役務対価、税別）」と「実費（法定手数料・証紙・証明書取得費・郵送費・交通費）」
  を明確に分けて作成する。実費は依頼者が負担する立替金であり、報酬とは性質が異なる。
- 分野ごとの報酬水準は事務所の報酬規程に基づく（例：建設業許可・知事新規は報酬10万〜15万円＋実費
  9万円、産廃収集運搬・新規は報酬10万〜13万円＋実費8.1万円、古物商は報酬4万〜5万円＋実費1.9万円）。
  金額は事務所規程が優先し、補助者が独断で値引き・上乗せしない。
- 実費の代表例：建設業許可の申請手数料90,000円、産廃許可の手数料81,000円、古物商19,000円、
  登記事項証明書480〜600円/通、住民票・課税証明書等300円前後/通、郵送・レターパック代など。
- 見積は必ず担当行政書士の承認を得てから依頼者へ提示する。追加業務が発生した場合は、着手前に追加
  見積を提示して合意を得る。後出し請求はしない。
- 見積書には有効期限、着手金・成功時精算の別、キャンセル時の取扱いを明記し、控えを案件ファイルに保存する。
"""

