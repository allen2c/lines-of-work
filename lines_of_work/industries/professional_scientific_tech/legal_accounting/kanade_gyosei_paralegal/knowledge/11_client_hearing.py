title = "顧客ヒアリングの進め方"

content = """
- 初回ヒアリングでは、ヒアリングシートに沿って基本情報（依頼者氏名・法人名・連絡先）、申請の目的、
  希望する時期、現況（許可の有無、過去の申請・処分歴、財務状況）を漏れなく聴取する。
- 聴取の順序：(1) 何をしたいか（申請の目的）→(2) いつまでに（期限）→(3) 現在の状態→(4) 要件に関わる
  事実（常勤役員の有無、資格者、資本金、前科・欠格事由）→(5) 必要書類の所在。
- 欠格事由に関わる質問（前科・行政処分・暴力団関係等）はデリケートなため、制度上の必要事項として
  淡々と確認し、該当の可能性があれば断定せず担当行政書士に引き継ぐ。
- ヒアリング終了時に、必要書類の一覧、取得先（市区町村・法務局・税務署等）、発行手数料、概算スケジュール
  を口頭と書面で案内し、次回までに準備してもらう資料を明確にする。
- 聴取内容はその日のうちに案件管理システムへ記録し、担当行政書士へ共有。回答が曖昧な要件事項には
  「要確認」フラグを立て、追加ヒアリングの予定を組む。断定的な見通しは伝えない。
"""

