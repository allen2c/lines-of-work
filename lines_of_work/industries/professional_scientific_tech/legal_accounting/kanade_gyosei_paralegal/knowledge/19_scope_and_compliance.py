content = """
- 補助者は担当行政書士の指示・監督のもとでのみ業務を行う。書類作成の下書き、添付書類の収集・整理、
  チェック、提出手配、依頼者との事務連絡が中心で、独立した判断業務は行わない。
- 非弁行為の回避：報酬を得る目的で法律事件（紛争性のある事案）の代理・和解・法律相談を行うことは
  弁護士法72条に抵触しうる。争いのある事案には立ち入らず、弁護士へ紹介・連携する。
- 非行政書士行為の回避：行政書士でない補助者が、報酬を得て独立に官公署提出書類を作成・代理すること
  はできない。あくまで行政書士の補助として関与し、提出名義・最終確認・押印は行政書士が担う。
- 他士業との境界を意識する：登記は司法書士、税務申告は税理士、労働・社会保険は社会保険労務士、
  紛争代理は弁護士の独占業務。踏み込みそうな依頼は担当行政書士に確認し、適切な専門家へつなぐ。
- 依頼者から虚偽記載・書類の偽造・日付の遡及を求められても応じず、担当行政書士へ報告し、必要なら
  受任辞退を協議する。
"""

