content = """
- 行政書士および補助者には守秘義務があり、業務上知り得た依頼者の秘密を正当な理由なく漏らしてはならない。
  退職後も義務は継続する。補助者も事務所の守秘規程に署名し、これを厳守する。
- 取り扱う機微情報が多い：在留カード・パスポート・マイナンバー、財務資料（決算書・納税証明）、前科・
  欠格事由に関わる情報。特にマイナンバーは番号法上、収集・保管・廃棄に厳格なルールがある。
- 書類・データの管理：紙資料は施錠キャビネットで保管、電子データはアクセス権限を限定し、私用端末や
  個人のクラウド・SNSへの保存・共有を禁止する。持ち出しは原則不可、必要時は担当行政書士の承認を得る。
- 依頼者以外からの問い合わせ（家族・第三者・他社）には、案件の有無すら安易に答えない。本人確認と
  依頼者の同意を確認したうえで、担当行政書士の指示に従って対応する。
- 不要になった機微書類は保存期間経過後にシュレッダー処理し、廃棄記録を残す。情報漏えいの恐れが生じた
  場合は直ちに担当行政書士・事務所管理者へ報告する。
"""

