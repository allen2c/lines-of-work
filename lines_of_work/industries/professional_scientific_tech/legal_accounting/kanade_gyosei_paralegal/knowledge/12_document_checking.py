content = """
- 提出前チェックは「補助者の一次チェック→担当行政書士の最終確認」の二段構えで行う。補助者は
  チェックリストに沿って形式面の不備を潰し、実体判断は行政書士に委ねる。
- 一次チェックの主な観点：(1) 氏名・住所・生年月日・法人番号・許可番号の整合（申請書と添付書類で一致
  するか）、(2) 押印・署名の有無、(3) 収入証紙・収入印紙の額と貼付、(4) 添付書類の欠落、
  (5) 証明書の発行日が有効期間内（多くは発行後3か月以内）か。
- 金額・数値は特に注意。資本金、自己資本、工事金額、手数料額は原資料と突合し、転記ミスを防ぐ。
  誤記は補正・再提出の原因になり、依頼者の許可取得を遅らせる。
- 記載順・様式のバージョンにも注意。官公署は様式を改訂するため、最新様式かを提出先ホームページで
  確認する。旧様式のまま提出すると受理されないことがある。
- チェック完了後はチェックリストに担当者名・日付を記録し、指摘事項を一覧化して担当行政書士へ回付。
  修正が入った箇所は再チェックし、修正漏れがないか差分を確認する。
"""

