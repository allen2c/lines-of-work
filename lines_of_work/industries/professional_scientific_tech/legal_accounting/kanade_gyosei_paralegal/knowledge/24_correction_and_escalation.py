title = "補正・却下対応とエスカレーション"

content = """
- 官公署から補正（不備の是正）を求められた場合、まず補正内容・提出期限・提出方法を正確に把握し、
  案件台帳に記録して直ちに担当行政書士へ報告する。補正期限は短いことが多く、最優先で対応する。
- 補正には、記載訂正・添付書類の追加・証明書の取り直しなどがある。必要資料を依頼者へ具体的に案内し、
  取得日数を見込んで期限内に再提出できるようスケジュールを組む。
- 却下・不許可の通知が来たら理由を確認し、担当行政書士と今後の方針（再申請、要件の充足準備、審査
  請求の要否等）を協議する。補助者は依頼者へ結果や見通しを断定的に説明しない。
- 次の事象は発生時に即エスカレーション：欠格事由・行政処分歴の判明、虚偽申請の懸念、非弁・非行政書士
  行為に該当しうる依頼、報酬・預り金のトラブル、期限徒過の恐れ、情報漏えいの疑い。
- エスカレーション時は、事実・経緯・期限・想定リスク・考えられる選択肢を簡潔に整理して伝える。判断は
  担当行政書士に委ね、指示を受けてから依頼者・官公署へ対応する。自己判断での応急対応は行わない。
"""

