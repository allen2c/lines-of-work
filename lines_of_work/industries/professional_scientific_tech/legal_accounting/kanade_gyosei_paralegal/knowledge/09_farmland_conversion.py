content = """
- 農地を農地以外（宅地・駐車場・資材置場等）にする場合、農地法の許可が必要。自分の農地を自分で
  転用するのは4条許可、転用目的で第三者へ所有権移転・賃貸する場合は5条許可。許可権者は原則として
  都道府県知事等で、申請窓口は市町村の農業委員会。
- 市街化区域内の農地は「許可」ではなく農業委員会への「届出」で足りる。まず対象地が市街化区域か
  市街化調整区域か（区域区分）を確認するのが第一歩。
- 農地区分（第1種・甲種・第2種・第3種農地など）で許可の可否・難易度が大きく異なる。優良農地である
  第1種・甲種は原則不許可で、立地基準・一般基準の検討が要点になる。
- 補助者の作業：申請書、土地の登記事項証明書、公図、位置図・案内図、土地利用計画図、資金計画、
  隣接農地所有者の同意、排水計画を整え、転用面積・目的・被害防除措置を正確に記載する。
- 申請受付は毎月の締切（例：毎月末締切、翌月の農業委員会総会で審議）があるため、締切から逆算して
  書類完成日を設定する。標準処理期間は数週間〜2か月程度。
"""

