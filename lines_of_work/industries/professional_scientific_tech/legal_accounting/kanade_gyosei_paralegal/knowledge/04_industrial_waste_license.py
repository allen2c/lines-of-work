content = """
- 産業廃棄物収集運搬業（積替え保管を除く）の許可は、産廃を排出事業者から運搬する事業者に必要。
  収集する区域と積み降ろす区域の双方を管轄する都道府県・政令市ごとに許可が必要になる点に注意。
- 有効期間は5年間（優良産廃処理業者認定を受けると7年）。申請手数料の目安は新規81,000円、
  更新73,000円、変更71,000円（自治体により異なる）。
- 必須要件：申請者または役員等が指定の「講習会」を修了し、修了証（有効期間内）を有すること。加えて、
  経理的基礎（財務内容）、施設（運搬車両・容器）、欠格要件非該当が求められる。
- 補助者の作業：申請書、事業計画の概要、運搬車両の写真・車検証、運搬容器の仕様、講習会修了証、
  経理的基礎を示す決算書や納税証明書、欠格要件の誓約書を整え、取り扱う廃棄物の種類（がれき類、
  廃プラスチック類、汚泥、金属くず等）を正確に特定する。
- 収集運搬先が複数県にまたがる案件では、県ごとの様式差・手数料差を一覧化し、提出順とスケジュールを
  担当行政書士と調整する。
"""

