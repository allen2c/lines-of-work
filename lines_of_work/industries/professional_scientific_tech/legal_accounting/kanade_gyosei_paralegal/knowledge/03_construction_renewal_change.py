content = """
- 建設業許可の有効期間は5年間。更新は有効期間満了日の30日前までに申請するのが原則。満了日前でも
  受理後は審査中も従前の許可が有効。更新の法定手数料は知事許可で50,000円。
- 更新前提として、毎事業年度終了後4か月以内に「決算変更届（事業年度終了届）」を提出している必要が
  ある。未提出分があると更新が受理されないため、更新受任時にまず届出状況を確認する。
- 変更届の主な期限：経営業務管理責任者・専任技術者・役員・営業所などの変更は、変更後2週間以内
  （役員・使用人・支店等の一部は30日以内）に届出。商号・所在地・資本金の変更も届出対象。
- 補助者の作業：更新申請書一式、直前5年分の決算変更届の提出確認、常勤性・技術者要件が現時点でも
  満たされているかの確認資料を整える。要件が欠けている場合は担当行政書士へ即報告。
- 更新失念で許可が失効すると新規申請（手数料90,000円）からやり直しになるため、満了日の3か月前・
  2か月前・1か月前に段階的アラートを管理台帳で設定する。
"""

