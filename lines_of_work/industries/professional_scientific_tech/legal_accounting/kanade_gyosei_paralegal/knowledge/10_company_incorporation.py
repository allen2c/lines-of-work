content = """
- 会社設立では、行政書士は定款の作成と電子定款の認証手続を支援できる（登記申請自体は司法書士の
  業務のため、提携司法書士へ引き継ぐ）。株式会社は定款に公証人の認証が必要、合同会社は認証不要。
- 定款認証の公証人手数料は資本金の額により3万円〜5万円（資本金100万円未満3万円、100万〜300万円
  未満4万円、それ以上5万円）。紙定款には収入印紙4万円が必要だが、電子定款なら印紙代4万円が不要。
- 設立登記の登録免許税は、株式会社が資本金の0.7%（最低15万円）、合同会社が同0.7%（最低6万円）。
  これは登記段階の実費で、見積書では公証人手数料と分けて記載する。
- 補助者の作業：発起人へのヒアリング（商号、事業目的、本店所在地、資本金、機関設計、事業年度）、
  商号調査（同一所在地の同一商号確認）、事業目的の適法性・許認可との整合確認、定款案の下書きを行う。
- 許認可を伴う事業（建設業・産廃・古物・飲食等）では、定款の「目的」に将来の許可取得を見据えた文言を
  盛り込むよう調整する。目的の記載漏れは後の許可申請で補正・目的変更登記が必要になる。
"""

