title = "Revenue Recognition Testing"

content = """
- Understand the client's revenue streams and contracts. For each significant revenue type, identify the five-step model under ASC 606 (or IFRS 15).
- Select a sample of revenue transactions (minimum 25 for high-risk, 10 for low-risk) and test:
  - Contract identification and performance obligations.
  - Transaction price and variable consideration estimates.
  - Timing of revenue recognition (point in time vs. over time).
- For service revenue, test that revenue is recognized as services are performed (e.g., using percentage-of-completion or straight-line).
- Compare revenue to prior year and budget; investigate any unexplained growth >10% or decline >15%.
- Document any revenue-related audit adjustments and discuss with the manager.
"""

