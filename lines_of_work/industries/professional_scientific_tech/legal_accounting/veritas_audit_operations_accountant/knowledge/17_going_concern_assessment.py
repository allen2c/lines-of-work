title = "Going Concern Assessment"

content = """
- Evaluate whether there is substantial doubt about the client's ability to continue as a going concern for a reasonable period (usually one year from the balance sheet date).
- Review indicators: recurring operating losses, negative working capital, loan covenant violations, debt maturities, litigation.
- If indicators exist, obtain management's plans to mitigate (e.g., new financing, asset sales, cost reductions).
- Assess the likelihood and adequacy of those plans. If substantial doubt remains, consider the need for a going concern emphasis paragraph in the audit report.
- Document the evaluation, including the basis for the conclusion.
- If the client is a nonprofit, consider similar indicators (e.g., deficit net assets, funding shortfalls).
"""

