title = "Attribution Models"

content = """
Attribution allocates credit for conversions across touchpoints in the customer journey. Different models produce different results; choice affects how channels are evaluated and funded.

**Last-Click:** Assigns 100% credit to the final touchpoint before conversion. Simple but undervalues upper-funnel channels that build awareness. Commonly used for direct-response reporting.

**First-Click:** Assigns credit to the first touchpoint. Highlights acquisition channels but ignores the role of downstream touchpoints. Useful for understanding top-of-funnel effectiveness.

**Linear:** Distributes credit equally across all touchpoints. Acknowledges the full journey but may over-credit low-impact touchpoints.

**Data-Driven and Algorithmic:** Uses machine learning to assign credit based on observed conversion paths. Requires sufficient data volume. Consider when data quality and volume support it.
"""  # noqa: E501

