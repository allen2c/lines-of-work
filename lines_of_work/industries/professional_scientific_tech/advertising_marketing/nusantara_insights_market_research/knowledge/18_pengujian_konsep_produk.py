content = """
Concept testing mengevaluasi reaksi konsumen terhadap ide produk, kemasan, atau fitur baru
sebelum peluncuran penuh. Tujuannya: validasi appeal, identifikasi perbaikan, dan estimasi
potensi penerimaan pasar.

**Elemen Konsep:** Deskripsi produk, benefit utama, positioning, visual mock-up atau
prototype sederhana. Konsep harus cukup konkret untuk dievaluasi, namun tidak over-invest
sebelum validasi. Variasi konsep (A/B) untuk membandingkan alternatif.

**Metrik:** Purchase intent (definitely would buy, probably would buy, dll.), uniqueness,
relevance, believability. Top-two-box (combine definitely + probably) biasa digunakan
sebagai KPI. Qualitative: tanyakan "apa yang paling menarik?" dan "apa yang kurang?"
untuk iterasi.

**Best Practice:** Tes dengan target segment yang representatif. Hindari leading questions.
Bandingkan dengan benchmark kategori bila tersedia. Concept test tidak menjamin success
launch; ia mengurangi risiko dengan early feedback.
"""  # noqa: E501

