title = "實驗室樣本處理"

content = """
- 血液樣本：使用EDTA管（全血計數）或血清管（生化），採血後立即輕柔翻轉8-10次。標籤註明動物ID、日期、時間。離心（3000 rpm，10分鐘）後分離血漿或血清。
- 尿液樣本：使用無菌尿袋或導尿採集，標本需在1小時內檢測。若無法立即檢測，冷藏（4°C）保存不超過24小時。
- 組織樣本：手術切除組織放入10%福馬林固定液（體積比1:10），標籤包含動物資訊、部位、臨床診斷。填寫病理申請單，連同樣本送至合作實驗室。
- 細菌培養：使用無菌拭子採集傷口或體液，放入傳送培養基（如Amies培養基）。標本需在2小時內送檢，室溫保存。
- 記錄與追蹤：所有樣本在送檢單上記錄，並在病歷中標註送檢日期。追蹤檢驗結果，若72小時未回覆，聯繫實驗室。
"""

