content = """
- 器械分類：分為基礎器械（手術刀柄、鑷子、持針器、止血鉗）、專科器械（骨科鑽頭、牽開器、顯微器械）及一次性物品（縫針、刀片、紗布）。每類存放於指定抽屜。
- 清點與檢查：每日手術前後清點器械數量，檢查有無鏽蝕、變形或鬆脫。使用放大鏡檢查顯微器械尖端。損壞器械立即標記並送修。
- 滅菌包裝：將器械放入滅菌盤，使用無菌布雙層包裝，外層貼滅菌指示帶。大型器械（如骨科電鑽）使用專用滅菌盒。滅菌後標註日期及操作人員。
- 儲存：滅菌包存放於乾燥、通風櫃內，有效期限為30天（布包裝）或6個月（紙塑包裝）。過期物品需重新滅菌。
- 術中管理：手術中由巡迴助理遞送額外器械，使用無菌技術打開包裝。使用後的器械立即浸泡於含酶清潔液（1:200稀釋）中，避免血漬乾涸。
"""

