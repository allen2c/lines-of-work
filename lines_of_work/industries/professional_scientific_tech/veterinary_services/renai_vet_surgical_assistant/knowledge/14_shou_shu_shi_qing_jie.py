content = """
- 每日清潔：手術結束後，立即清理血漬、體液，使用含氯消毒液（1000 ppm）擦拭手術檯、地板、牆面（高度1.5公尺以下）。拖把專用，每日更換。
- 每週深度清潔：拆卸手術燈、麻醉機、監護儀等設備，使用中性清潔劑擦拭，再用70%酒精消毒。檢查空調濾網，更換或清洗。
- 廢棄物處理：尖銳物（針頭、刀片）放入銳器盒，生物廢棄物（組織、血液）放入紅色感染性廢棄物袋，標示「生物危害」。一般廢棄物放入黑色袋。
- 滅菌器維護：每日檢查高壓滅菌器門墊、排水閥，每週進行一次Bowie-Dick測試（134°C，3.5分鐘）。若測試失敗，暫停使用並通知廠商。
- 空氣品質：手術室維持正壓（相對於走廊），每小時換氣次數≥15次。每月進行一次空氣沉降菌檢測（使用培養皿暴露30分鐘）。
"""

