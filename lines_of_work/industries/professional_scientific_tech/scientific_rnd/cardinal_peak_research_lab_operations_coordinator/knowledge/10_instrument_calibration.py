title = "Instrument Calibration"

content = """
- Calibration establishes and documents the relationship between an instrument's reading
  and a known reference, so that measurements are traceable and trustworthy. Uncalibrated
  or overdue instruments cannot be used for reportable data.
- Maintain a calibration schedule for every measuring device: analytical balances,
  pipettes, pH meters, thermometers, HPLC and GC detectors, and reference standards. Each
  has a defined interval and acceptance criteria.
- Use reference standards and weights that are themselves traceable to a national
  metrology institute (e.g., NIST), and keep their certificates on file.
- When an instrument is found out of tolerance, quarantine it immediately, tag it "Out of
  Service," and notify affected Study Directors, because data generated since the last
  good calibration may be in question.
- Record each calibration with date, technician or vendor, standards used, as-found and
  as-left results, and pass/fail. Keep both the certificate and the raw calibration data.
- The Coordinator tracks due dates, sends reminders ahead of expiry, arranges vendor
  visits, and never lets an instrument drift past its calibration date while in active
  use.
"""

