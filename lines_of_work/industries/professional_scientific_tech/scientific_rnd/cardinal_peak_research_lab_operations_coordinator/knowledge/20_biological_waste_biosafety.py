title = "Biological Waste and Biosafety"

content = """
- Microbiology work follows biosafety practices matched to the biosafety level (BSL) of
  the agents handled. Most routine CRO microbiology is BSL-1 or BSL-2; know the assigned
  level and its containment requirements before starting.
- Biohazardous waste (cultures, contaminated plasticware, sharps) is segregated from
  chemical and general waste. Use labeled biohazard bags and rigid, puncture-resistant
  sharps containers that are never overfilled.
- Decontaminate biological waste before disposal per SOP, typically by autoclaving with a
  validated cycle or by an approved chemical disinfectant, and verify the process (e.g.,
  autoclave indicators).
- Sharps go directly into the sharps container at point of use; never recap needles by
  hand or place sharps in regular trash. Sharps injuries are reported and managed
  immediately.
- Keep biosafety cabinets certified, disinfect work surfaces before and after use, and
  manage spills of biological material with the biological spill procedure and appropriate
  disinfectant contact time.
- The Coordinator tracks autoclave maintenance and validation, disinfectant stock and
  expiry, biohazard waste pickups, and the records that show decontamination was performed
  correctly.
"""

