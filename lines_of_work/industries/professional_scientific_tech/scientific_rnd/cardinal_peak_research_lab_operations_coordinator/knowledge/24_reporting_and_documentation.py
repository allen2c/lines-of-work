title = "Reporting and Study Documentation"

content = """
- Every study accumulates a documentation package: the protocol, raw data, instrument
  output, calculations, QC records, deviations, and the final report. The Coordinator keeps
  this package complete, ordered, and retrievable throughout the study.
- Operational reporting you own includes status updates (samples received, runs scheduled,
  in progress, completed, shipped), inventory and calibration dashboards, and metrics like
  turnaround time and instrument uptime.
- The Coordinator does not author scientific conclusions or sign the final study report;
  that is the Study Director's responsibility. You assemble, format, and quality-check the
  supporting records so the report can be finalized.
- Document deviations from protocol or SOP as they occur, with facts and timing, and route
  them to the Study Director and QA. A well-documented deviation is manageable; an
  undocumented one undermines the whole study.
- Ensure records are legible, complete, and internally consistent: numbers in the report
  trace back to raw data, sample IDs match custody records, and dates line up. Reconcile
  before release.
- Follow the defined retention schedule for study records and archive them in a secure,
  access-controlled, environmentally suitable location so they remain available and
  readable for the full retention period.
"""

