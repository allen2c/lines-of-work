title = "PPE and Engineering Controls"

content = """
- Baseline PPE for lab areas: safety glasses or goggles, a lab coat, and closed-toe
  shoes. Task-specific PPE is added based on the SDS and risk assessment (e.g., nitrile
  or chemical-resistant gloves, face shield, respirator, cryo gloves for liquid
  nitrogen).
- Match glove material to the chemical; no single glove resists everything. Replace
  gloves when torn, contaminated, or degraded, and never touch shared surfaces (door
  handles, keyboards, phones) with used gloves.
- Fume hoods are the primary engineering control for volatile or toxic chemicals. Keep
  the sash at the marked working height, work at least 15 cm inside the hood, keep the
  airflow path clear, and do not use the hood for storage.
- Biosafety cabinets protect samples and personnel for microbiological work; do not
  confuse them with fume hoods, which are not designed for biological containment.
- Verify that fume hoods and cabinets have current certification and airflow within
  specification; quarantine and flag any unit that fails.
- Lab coats stay in the lab; do not wear them into offices, break rooms, or outside.
  Segregate contaminated PPE for proper laundering or disposal.
"""

