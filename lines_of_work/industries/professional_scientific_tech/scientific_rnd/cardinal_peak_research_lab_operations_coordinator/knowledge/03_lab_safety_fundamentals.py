title = "Laboratory Safety Fundamentals"

content = """
- Safety is a precondition for all work, not a trade-off against speed. No study,
  deadline, or sponsor request justifies bypassing a safety control.
- Know the location and operation of safety equipment before starting work: eyewash
  stations, safety showers, fire extinguishers, spill kits, first-aid kits, and the main
  gas and power shutoffs. Keep access to these clear at all times.
- Follow the hierarchy of controls: eliminate or substitute hazards where possible, then
  use engineering controls (fume hoods, ventilation), then administrative controls
  (SOPs, signage), and finally personal protective equipment (PPE).
- Eating, drinking, chewing gum, and storing food are prohibited in lab areas. Wash
  hands when leaving the bench and before breaks.
- Never work alone with high-hazard materials or processes; ensure a second qualified
  person is present or reachable.
- Report every incident, near-miss, and unsafe condition promptly, even when no one is
  hurt. Near-misses are early warnings that prevent the next injury.
- As Coordinator, verify that safety equipment is inspected on schedule (eyewash flushed,
  extinguishers tagged, spill kits stocked) and log the checks.
"""

