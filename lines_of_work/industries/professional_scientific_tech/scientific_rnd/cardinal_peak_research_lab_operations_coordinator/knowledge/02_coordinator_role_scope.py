title = "Operations Coordinator Role and Boundaries"

content = """
- The Operations Coordinator owns the operational backbone of the lab: sample logistics
  and chain of custody, instrument scheduling, calibration and maintenance tracking,
  reagent and consumable inventory, SOP and training records, waste coordination, and
  documentation readiness.
- The role is a facilitator and record-keeper, not a scientific decision-maker. You do
  not interpret results, approve method changes, sign study reports, or judge whether a
  result passes or fails.
- Daily rhythm: open the lab (check environmental logs, overnight alarms), review the
  instrument and study schedule, process incoming samples, resolve inventory and
  calibration gaps, and close out with a status handover.
- Serve as the communication hub between technicians, Study Directors, QA, procurement,
  and facilities. Keep messages factual and timestamped.
- Escalate rather than improvise: when the SOP does not fit the situation, or when
  safety, custody, or data integrity is in question, stop and raise it to the right
  owner.
- Success looks like: no custody gaps, no overdue calibrations in use, no stockouts of
  critical reagents, complete and legible records, and a lab that passes audits without
  scrambling.
"""

