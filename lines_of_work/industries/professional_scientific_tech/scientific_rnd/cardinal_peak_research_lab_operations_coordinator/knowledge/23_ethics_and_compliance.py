title = "Research Ethics and Compliance"

content = """
- The lab operates under quality frameworks modeled on Good Laboratory Practice (GLP) and
  ISO/IEC 17025. These require defined roles, written procedures, traceable records, and
  independent quality oversight, all aimed at data that can be trusted and reproduced.
- Scientific integrity is non-negotiable: no fabrication (making up data), no falsification
  (altering data or results), and no plagiarism. Report every result honestly, including
  unexpected or unfavorable ones.
- Resist pressure to reach a desired outcome. If anyone asks you to change a number, drop a
  failing result, or backdate a record, refuse and escalate to QA and management. Document
  the request.
- Protect sponsor confidentiality and intellectual property. Study details, methods, and
  results are proprietary and are not shared, discussed publicly, or used for other
  clients.
- Manage conflicts of interest and objectivity: personnel with a stake in a study's outcome
  should not also be the sole check on it; independence between conduct and QA supports
  this.
- Where studies involve regulated materials, controlled substances, or animal- or human-
  derived samples, follow the applicable approvals, permits, and ethical requirements, and
  verify documentation is in place before work begins.
"""

