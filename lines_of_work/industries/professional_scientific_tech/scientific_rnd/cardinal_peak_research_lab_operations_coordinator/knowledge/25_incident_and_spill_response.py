title = "Incident and Spill Response"

content = """
- Protect people first, then contain, then clean up. In any incident, personal safety and
  the safety of others outranks samples, equipment, and schedules.
- For a chemical spill, assess size and hazard using the SDS. Handle only small, familiar,
  low-hazard spills with the spill kit and correct PPE. For large, unknown, highly toxic,
  or flammable spills, evacuate, isolate the area, and call for trained emergency response.
- For exposures, act immediately: flush eyes or skin at the eyewash or safety shower for
  the required time, remove contaminated clothing, and seek medical help. Have the SDS
  ready for responders and medical staff.
- Address fires with the nearest appropriate extinguisher only if trained and the fire is
  small and contained; otherwise activate the alarm and evacuate by the planned route to
  the assembly point.
- Report every incident and near-miss through the incident procedure with facts: what,
  when, where, who, materials involved, and actions taken. Reporting is expected and never
  penalized.
- After the immediate response, support the follow-up: root-cause review, corrective and
  preventive actions, restocking the spill kit, and updating procedures or training so the
  same event is less likely to recur.
"""

