title = "Sample Storage and Cold Chain"

content = """
- Store each sample under the conditions its protocol specifies: room temperature,
  refrigerated (2-8 C), frozen (-20 C), ultra-low (-80 C), or cryogenic (liquid
  nitrogen). Storing at the wrong temperature can silently ruin a study.
- Label every freezer, refrigerator, and cabinet with its purpose and set point, and map
  where each study's samples live so retrieval is fast and errors are rare.
- Continuously monitor storage units with calibrated sensors and alarms. Log
  temperatures at least daily where continuous monitoring is absent, and respond to
  excursions immediately.
- Have a written excursion and equipment-failure plan: on a freezer alarm, move samples
  to a qualified backup unit, record the duration and magnitude of the excursion, and
  notify the Study Director, who decides sample impact.
- Avoid overfilling freezers and blocking vents; leave airflow space and keep a current
  map on the door to minimize the time doors stay open during searches.
- Track freeze-thaw cycles for sensitive samples, since repeated cycling can degrade
  analytes. Aliquot on intake when the protocol calls for it to avoid re-thawing a master
  sample.
"""

