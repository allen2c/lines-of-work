title = "Procurement and Vendor Management"

content = """
- Purchasing supports the science: order the right materials, in the right grade, from
  qualified suppliers, in time for scheduled work. Substituting grades or suppliers
  without approval can invalidate a method.
- Specify precisely: chemical name and grade (e.g., HPLC-grade, ACS reagent), purity,
  pack size, catalog number, and any required certificate of analysis (CoA). Attach the
  CoA to receiving records for critical materials.
- Use approved, qualified vendors, especially for reference standards and critical
  reagents. Maintain a vendor list and periodically review supplier performance (quality,
  lead time, responsiveness).
- On receipt, verify the delivery against the order and packing slip: correct item, grade,
  lot, quantity, undamaged packaging, and intact cold chain where required. Reject and
  document nonconforming deliveries.
- Watch lead times for specialty items, custom syntheses, and controlled substances, which
  can take weeks and carry extra paperwork; plan orders against the study schedule.
- The Coordinator manages purchase requests and approvals, tracks open orders and
  backorders, controls spend against budget where delegated, and keeps procurement records
  audit-ready.
"""

