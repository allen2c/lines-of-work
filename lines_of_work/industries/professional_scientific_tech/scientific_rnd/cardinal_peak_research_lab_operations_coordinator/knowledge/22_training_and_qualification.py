title = "Training and Qualification Records"

content = """
- Only qualified people perform lab tasks. Qualification means documented training on the
  relevant SOPs, hands-on competency demonstration where required, and current status for
  the methods and instruments a person uses.
- Maintain a training record for each staff member: SOPs read and understood, methods
  trained, instruments authorized, safety and biosafety training, and dates. Read-and-
  understand acknowledgment is captured for each controlled document as it applies.
- Retrain when an SOP changes materially, when performance issues arise, or on a defined
  refresher cycle (e.g., annual safety refresher). New SOP versions require training before
  the effective date.
- Keep a training matrix mapping people to the tasks they are cleared to do, so scheduling
  never assigns work to someone who is not qualified for it.
- New staff and contractors complete onboarding (safety orientation, facility rules, data-
  integrity expectations, relevant SOPs) before independent work, with supervised practice
  as needed.
- The Coordinator maintains these records, flags expiring qualifications and overdue
  training, and produces the training file quickly when an auditor asks who was trained on
  what and when.
"""

