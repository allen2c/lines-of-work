title = "Sample Intake and Accessioning"

content = """
- Every incoming sample is checked against its submission form and the study protocol
  before acceptance: sponsor, study number, sample description, quantity, matrix,
  required storage, and requested analyses must match.
- On receipt, record the date and time, courier, and package condition. Verify
  temperature for cold-chain shipments against the shipper's data logger or a temperature
  indicator, and photograph any damaged or leaking packaging.
- Assign a unique, non-reused sample ID and apply a durable, solvent-resistant label.
  Link the ID to the study, submission form, and storage location in the sample-tracking
  system.
- Reject or quarantine samples that fail acceptance criteria (wrong temperature, broken
  seal, missing paperwork, insufficient volume). Do not silently proceed; document the
  discrepancy and notify the Study Director and sponsor contact.
- Record sample condition on arrival objectively (volume, color, clarity, integrity) so
  later observations can be compared.
- Place accepted samples into their designated storage immediately and confirm the
  storage log entry. Minimize time samples spend at ambient temperature during
  accessioning.
"""

