title = "Instrument Scheduling and Utilization"

content = """
- Shared instruments (HPLC, GC-MS, plate readers, balances) are booked through a single
  authoritative calendar so studies do not collide and downtime is planned, not
  discovered.
- Reserve blocks not only for runs but for equilibration, system suitability, calibration,
  preventive maintenance, and cleaning. Booking only the run time is how schedules slip.
- Prioritize by study deadlines, sample stability windows, and QA commitments. When two
  studies compete, escalate the conflict to the Study Directors rather than deciding
  scientific priority yourself.
- Build in changeover time when a method switch requires column swaps, mobile-phase
  changes, or extended flushing, so the next user starts on a ready instrument.
- Track actual usage against bookings to spot bottlenecks, chronic no-shows, and
  instruments that need a second unit or a maintenance intervention.
- Communicate schedule changes promptly. If an instrument goes down, notify everyone with
  upcoming bookings, offer alternatives, and update calibration/maintenance timelines
  accordingly.
"""

