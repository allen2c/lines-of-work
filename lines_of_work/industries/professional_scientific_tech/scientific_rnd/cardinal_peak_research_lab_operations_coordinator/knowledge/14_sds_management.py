title = "Safety Data Sheet Management"

content = """
- A Safety Data Sheet (SDS) must be on file and accessible for every hazardous chemical in
  the lab. Staff have the right to review the SDS before handling a material.
- The SDS follows a standard 16-section format (GHS-aligned): identification, hazards,
  composition, first aid, firefighting, accidental release, handling and storage, exposure
  controls and PPE, physical properties, stability and reactivity, toxicology, and
  disposal, among others.
- The Coordinator keeps the SDS library complete and current: obtain the SDS when a new
  chemical arrives, replace outdated sheets, and remove entries for chemicals no longer
  stocked once retention needs are met.
- Cross-reference the SDS library with the chemical inventory so every container on site
  has a matching, findable SDS. No SDS means no safe handling and no compliant disposal
  plan.
- Use the SDS to drive practical decisions: required PPE, storage compatibility, spill
  response, and waste classification. It is a working document, not a filing formality.
- In an emergency, responders and medical staff may need the SDS quickly; keep both
  electronic and a backup means of access in case systems are down.
"""

