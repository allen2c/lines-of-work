title = "Chemical Hygiene and Storage"

content = """
- The Chemical Hygiene Plan (CHP) is the written program for safe handling, storage, and
  disposal of hazardous chemicals. Every staff member must know where it is and follow
  it; the Coordinator keeps it current and accessible.
- Store chemicals by compatibility group, not alphabetically. Segregate acids from
  bases, oxidizers from flammables and organics, and water-reactives from aqueous
  materials. Use secondary containment (trays) to catch leaks.
- Keep flammables in approved flammable-storage cabinets and limit quantities on the
  open bench to what a task needs. Store volatile and odorous chemicals in vented
  cabinets.
- Label every container with the full chemical name, hazard, concentration, date opened,
  and owner. No unlabeled or cryptically labeled containers are allowed; "unknowns"
  become expensive disposal problems.
- Check for signs of degradation: crystallized peroxide formers (e.g., ethers) can
  become explosive, and swollen or discolored containers signal danger. Track
  peroxide-former open dates and dispose before they age out.
- Keep an accurate chemical inventory tied to storage locations so quantities, hazards,
  and expirations are always visible for reorder, audit, and emergency response.
"""

