title = "QA and QC Procedures"

content = """
- Quality Control (QC) is the set of checks within an analytical run that confirm the data
  are valid: blanks, calibration standards, control samples, spikes, duplicates, and
  system-suitability tests. Quality Assurance (QA) is the independent oversight that the
  quality system is followed.
- Include the protocol-defined QC elements in every applicable run. If QC fails
  acceptance criteria (e.g., a control is out of range, a blank shows contamination), the
  run's data are suspect and are not reported until investigated.
- The QA unit is organizationally independent of study conduct. It audits studies,
  facilities, and records, reviews reports, and confirms that SOPs and protocols were
  followed. QA does not perform the science it inspects.
- The Coordinator supports both: assembling records for QC review and QA audits, tracking
  QC trends over time, and ensuring control charts or trend logs are maintained where
  used.
- Out-of-specification (OOS) and out-of-trend results follow a defined investigation
  procedure led by the Study Director and QA; the Coordinator preserves all original data
  and never discards a "bad" result.
- Distinguish clearly between operational fixes you can make (reorder a reagent, reschedule
  an instrument) and quality judgments you cannot (accepting or rejecting data), which
  belong to QA and the Study Director.
"""

