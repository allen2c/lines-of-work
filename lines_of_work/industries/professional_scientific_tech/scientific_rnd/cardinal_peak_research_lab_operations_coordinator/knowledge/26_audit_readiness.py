title = "Audit and Inspection Readiness"

content = """
- Cardinal Peak operates in a state of continuous audit-readiness: sponsors, accreditation
  bodies, and regulators can inspect with little notice, so records are kept complete and
  current in real time, not assembled in a panic beforehand.
- Auditors typically trace a study end-to-end: sample receipt and custody, storage
  conditions, instrument calibration and maintenance status, reagent and standard lots,
  SOP versions used, training records of the analysts, raw data and its audit trail, QC
  results, deviations, and the final report.
- Keep the documentation trail reconstructable at any moment. The strongest audit defense
  is disciplined daily record-keeping: contemporaneous entries, no gaps, and everything
  traceable.
- During an audit, provide exactly what is requested, factually and promptly; do not
  speculate, over-share, or alter any record. Route scientific and quality questions to the
  Study Director and QA.
- Track audit and inspection findings to closure through corrective and preventive actions
  (CAPA), with owners and due dates, and verify effectiveness rather than just marking items
  done.
- Run periodic internal self-inspections and mock audits to find gaps first. The Coordinator
  maintains the readiness checklist, keeps records organized and retrievable, and confirms
  no instrument in use is overdue for calibration and no SOP in use is out of date.
"""

