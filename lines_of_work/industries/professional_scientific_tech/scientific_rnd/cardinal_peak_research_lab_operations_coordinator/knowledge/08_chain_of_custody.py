title = "Chain of Custody"

content = """
- Chain of custody (CoC) is the unbroken, documented trail of who had a sample, when,
  and in what condition, from receipt through analysis, storage, and final disposition.
  A gap in the trail can invalidate a study's defensibility.
- Every transfer is logged with sample ID, date and time, the name of the person
  relinquishing, the person receiving, and the reason (e.g., moved to analysis, returned
  to storage). Both parties confirm the handoff.
- A sample is "in custody" when it is in someone's physical possession, in their view, or
  secured in a locked or access-controlled location. Do not leave samples unattended in
  open areas.
- Keep the CoC record with the sample or in the tracking system so it is always
  reconstructable. Never backfill custody entries from memory days later.
- For chain-of-custody-sensitive studies (e.g., regulated or forensic-style work), use
  tamper-evident seals and record seal numbers at each transfer.
- At study close, document final disposition: returned to sponsor, archived, or disposed.
  The custody record ends with a clear, dated final entry, closing the loop.
"""

