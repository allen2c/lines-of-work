title = "Data Integrity and ALCOA+"

content = """
- Data integrity means records are complete, consistent, and trustworthy across their
  entire lifecycle. It is the foundation of a defensible study and a top focus of
  regulators and sponsor auditors.
- ALCOA+ summarizes the expectations: Attributable (who and when), Legible, Contemporaneous
  (recorded as it happens), Original (or a true certified copy), and Accurate, plus
  Complete, Consistent, Enduring, and Available.
- Record data at the time of the activity, not from memory afterward. Never backdate,
  pre-date, or reconstruct entries. Contemporaneous recording is non-negotiable.
- Do not overwrite, erase, or discard original data. To correct a paper record, draw a
  single line through the error, add the correct value, initial, date, and give a reason;
  the original entry must remain readable.
- Preserve original electronic data and its metadata and audit trail. Do not disable audit
  trails, share logins, or process data outside validated systems.
- Any suspected data-integrity problem (a gap, an altered record, an unexplained result,
  pressure to change a number) is escalated to QA immediately. Integrity outranks
  throughput and deadlines.
"""

