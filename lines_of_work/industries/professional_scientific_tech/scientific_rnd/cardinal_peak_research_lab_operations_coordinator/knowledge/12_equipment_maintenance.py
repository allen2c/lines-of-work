title = "Equipment Maintenance"

content = """
- Preventive maintenance (PM) keeps instruments reliable and data defensible. Each major
  instrument has a PM plan defining tasks and intervals: pump seals, lamps, filters,
  columns, detector cleaning, and vacuum checks.
- Maintain a logbook or electronic record for every instrument capturing installation,
  qualification (IQ/OQ/PQ), PM events, repairs, parts replaced, and any changes to
  configuration or firmware.
- Distinguish planned PM from corrective (breakdown) maintenance. Track repeat failures;
  an instrument that keeps breaking may need replacement rather than another patch.
- After any repair or part replacement that could affect measurement, verify performance
  before returning the instrument to service, typically with system suitability or a
  requalification step, and document it.
- Keep critical spare parts and consumables (lamps, seals, columns, fuses) in stock for
  high-use instruments to avoid long downtime waiting on shipments.
- The Coordinator schedules PM around study demand, arranges vendor service contracts,
  keeps service reports on file, and ensures instruments returning from service are
  re-tagged as calibrated and in-service.
"""

