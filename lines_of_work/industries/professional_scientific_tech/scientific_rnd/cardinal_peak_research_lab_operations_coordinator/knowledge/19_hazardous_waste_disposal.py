title = "Hazardous Waste Disposal"

content = """
- Chemical waste is segregated by compatibility and hazard class: halogenated versus
  non-halogenated solvents, acids, bases, oxidizers, and toxics are kept apart. Mixing
  incompatible wastes can cause heat, gas, or explosion.
- Use approved, compatible waste containers kept closed except when adding waste, with
  secondary containment. Label each container with its contents, hazards, and accumulation
  start date from the first drop of waste added.
- Do not overfill; leave headspace for expansion, and cap and remove full containers
  promptly. Track accumulation time limits so containers are shipped before regulatory
  deadlines.
- Maintain a waste inventory and use the SDS and waste profiles to classify each stream
  correctly. Never pour hazardous chemicals down the drain unless explicitly permitted for
  that specific material.
- The Coordinator schedules pickups with the licensed disposal vendor, retains manifests
  and certificates of disposal, and reconciles what left the lab against the records.
- Cradle-to-grave responsibility means the lab remains accountable for its waste even after
  it leaves; accurate documentation and a reputable vendor are essential.
"""

