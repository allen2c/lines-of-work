title = "SOP Management and Version Control"

content = """
- Standard Operating Procedures (SOPs) are the controlled, approved instructions for how
  work is done. Only the current approved version may be used at the bench; superseded
  versions are archived, not left in circulation.
- Each SOP carries a unique ID, a version or revision number, an effective date, an
  author, and an approver. Uncontrolled printouts are stamped or dated and reconciled so
  stale copies do not accumulate.
- The Coordinator maintains the master SOP list, tracks review cycles (typically an
  annual or biennial review per document), and flags SOPs that are overdue for review.
- Changes follow change control: draft, review, approval, training, and effective date.
  Staff who perform the procedure are trained on the new version before it takes effect,
  and training is recorded.
- When a procedure at the bench differs from the SOP, that is a discrepancy to escalate,
  not a shortcut to normalize. Either the SOP is updated through change control, or the
  practice is corrected.
- Retire obsolete SOPs by marking them clearly, removing them from active locations, and
  retaining an archived copy for the required retention period to preserve history for
  audits.
"""

