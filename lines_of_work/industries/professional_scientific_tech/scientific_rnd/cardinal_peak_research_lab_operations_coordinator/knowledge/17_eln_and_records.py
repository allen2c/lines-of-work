title = "Electronic Lab Notebook and Records"

content = """
- The electronic lab notebook (ELN) is the primary record of experimental work: what was
  planned, what was done, by whom, when, with which materials and instruments, and what
  was observed.
- Each user has a unique login; never share credentials. Entries are attributable and
  time-stamped automatically, and the system's audit trail records edits. Do not attempt
  to bypass or disable it.
- Enter data contemporaneously and completely: sample IDs, reagent and standard lots,
  instrument IDs, method versions, raw observations, and links to instrument output files.
  Blank fields and vague notes weaken defensibility.
- Corrections in the ELN are made through the system's revision mechanism with a reason;
  the original value stays visible in the audit trail. Never edit around the system.
- Link ELN records to the controlling SOP version, the study protocol, and the raw
  instrument data so a reviewer can reconstruct exactly what happened.
- Records are retained for the study's required period and backed up. The Coordinator
  supports review-by-exception workflows, ensures entries are witnessed or reviewed where
  required, and keeps template forms current.
"""

