content = """
- **Wrong item in a customer's order**: Immediately stop, set aside, and notify the front desk. Do not attempt to swap.
- **Machine error code**: Look up the code in the machine manual. If it's a simple reset (e.g., overload), correct and restart. If persistent, tag machine and escalate.
- **Chemical spill**: Contain with absorbent pads, wear gloves, and follow SDS. Do not wash down the drain.
- **Customer complaint about shrinkage**: Check the care label and the machine settings used. If the label allowed the temperature used, it's a manufacturing issue – inform the supervisor.
- **Lost item**: Search the entire work area, including behind machines. If not found within 30 minutes, fill out a lost-item report.
- **Always document errors** in the shift log with date, time, and action taken.
"""

