title = "Folding Standards"

content = """
- **T-shirts**: Fold in thirds lengthwise, then in half – place with collar facing up. Use a cardboard insert for uniform shirts.
- **Dress shirts**: Button top button, fold sleeves behind, fold in half vertically, then in half horizontally. Use tissue paper for collars.
- **Pants**: Fold along crease (if present), then in half. For jeans, fold in half lengthwise, then in thirds.
- **Towels**: Fold in thirds lengthwise, then in half – stack neatly.
- **Sheets**: Fold in half lengthwise, then in quarters, then in thirds – use a folding board for consistency.
- **Delicates**: Place in tissue paper and then in a poly bag; do not fold tightly to avoid creases.
"""

