title = "Parfum de finition"

content = """
Certains pressings proposent une finition parfumée (spray ou additif en fin de cycle) pour
donner une odeur agréable au linge. Si le client le demande, noter « avec parfum de finition »
et vérifier que le service est disponible et tarifé selon les options du pressing.

À l'inverse, des clients demandent explicitement « sans parfum » (allergie, sensibilité).
Cette demande doit être clairement indiquée pour que la production n'utilise pas de
parfum ni d'adoucissant parfumé. Confirmer au client que sa préférence sera respectée.
"""

