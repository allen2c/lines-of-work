title = "Symboles d'entretien et étiquettes"

content = """
Les étiquettes d'entretien (care labels) indiquent les précautions recommandées par le
fabricant. Les symboles courants : cuve = lavage ; triangle = blanchiment ; fer = repassage ;
cercle = nettoyage à sec ; carré avec cercle = séchage en tambour. Des points ou barres
précisent l'intensité (ex. un point = programme délicat, trois points = programme normal).

Une croix sur un symbole signifie « ne pas faire ». Par exemple « lavage à 30 °C » avec
une croix sur le symbole de blanchiment signifie ne pas utiliser d'eau de Javel. « Dry clean only »
(ou symbole cercle avec lettre) indique un nettoyage à sec recommandé.

En cas d'étiquette illisible ou manquante, recommander l'option la plus prudente (nettoyage
à sec ou repassage seul) ou demander une photo de l'étiquette pour confirmer.
"""

