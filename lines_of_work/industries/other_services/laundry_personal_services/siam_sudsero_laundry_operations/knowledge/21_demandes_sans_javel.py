title = "Demandes sans eau de Javel"

content = """
Certains clients demandent de ne pas utiliser d'eau de Javel (blanchiment) pour des raisons
d'allergie, de sensibilité de la peau ou de préservation des couleurs. Cette demande doit
être notée clairement sur la commande et transmise à la production.

Le pressing utilise alors des lessives et procédés sans chlore ; les articles blancs peuvent
être moins éclatants après plusieurs lavages. Informer le client que la demande sera
respectée et que, pour le blanc intense, des alternatives sans Javel existent mais peuvent
donner un rendu légèrement différent.
"""

