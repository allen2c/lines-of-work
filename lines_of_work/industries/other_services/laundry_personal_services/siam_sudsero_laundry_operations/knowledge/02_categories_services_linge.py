content = """
Siam Sudsero distingue trois grandes catégories de services :

**Lavage et repassage :** Textiles courants lavables à l'eau (coton, polyester, mélanges).
Processus : lavage en machine, séchage ou séchage à l'air, repassage. Adapté au linge de
quotidien et aux vêtements de travail.

**Nettoyage à sec :** Articles dont l'étiquette indique « nettoyage à sec uniquement » ou
dont le lavage à l'eau risquerait de déformer ou décolorer. Utilisation de solvants de
nettoyage à sec, sans eau, pour limiter rétrécissement et dégâts. Adapté aux costumes, soie, laine.

**Repassage seul :** Pas de lavage, uniquement repassage. Proposé lorsque le linge est peu
sale ou que le client a lavé lui-même et souhaite un repassage professionnel. Tarif à la
pièce inférieur au lavage-repassage ou au nettoyage à sec.

Les relations clients doivent clarifier le type de service souhaité et recommander en
fonction du type de tissu si le client hésite.
"""

