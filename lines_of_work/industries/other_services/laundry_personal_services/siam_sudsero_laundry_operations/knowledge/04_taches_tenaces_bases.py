content = """
Les taches courantes (café, vin, graisse, encre) réagissent différemment selon le type de
textile et le moment du traitement. En règle générale : ne pas frotter fort pour éviter
d'incruster la tache ; traiter le plus tôt possible ; éviter la chaleur sur les taches
organiques avant lavage.

Pour les clients qui demandent un retrait de tache, indiquer que le pressing peut traiter
de nombreuses taches en prétraitement, mais que certaines (encre permanente, décolorations
anciennes) peuvent rester visibles ou laisser un halo. Les articles délicats (soie, teintures
vives) sont traités avec des protocoles spécifiques ; en cas de doute, proposer un essai
sur une zone peu visible ou recommander le nettoyage à sec standard sans garantie sur la tache.
"""

