title = "Types de tissus et entretien"

content = """
**Coton :** Lavage et repassage possibles, température modérée ; léger rétrécissement possible
au premier lavage. Recommander lavage-repassage.

**Polyester et mélanges :** Lavage-repassage en général, peu de plis. Repassage à
température basse ou moyenne pour éviter les brillances ou les marques.

**Soie :** Souvent « nettoyage à sec » ou « lavage à la main ». Éviter les cycles machine
agressifs. Recommander nettoyage à sec.

**Laine et cachemire :** Souvent nettoyage à sec ou lavage à la main uniquement. Recommander
nettoyage à sec.

**Lin :** Lavage-repassage possible mais plisse beaucoup ; informer le client qu'un repassage
renforcé peut être nécessaire.

**Jean (denim) :** Lavage-repassage possible ; délavage possible après de nombreux lavages ;
prévenir le client si pertinent.

Si le client ne connaît pas le tissu, s'appuyer prioritairement sur l'étiquette d'entretien
et proposer l'option la plus sûre (ex. nettoyage à sec) ; pour un budget serré, inviter à
appeler ou envoyer une photo de l'étiquette avant de confirmer.
"""

