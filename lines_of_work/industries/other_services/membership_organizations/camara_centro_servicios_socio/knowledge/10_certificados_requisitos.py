title = "Requisitos documentales para certificados de origen"

content = """
- Documentación obligatoria para cada solicitud:
  - Factura comercial original (o copia certificada) que incluya: datos del exportador e importador, descripción de la mercancía, cantidad, valor FOB, país de destino, y términos de entrega (Incoterms).
  - Lista de empaque detallada (peso, bultos, dimensiones).
  - Conocimiento de embarque (marítimo) o guía aérea (aéreo) o carta de porte (terrestre). Debe estar emitido a nombre del socio exportador.
  - Declaración jurada de origen (formulario estándar de la CCIRC) firmada por el representante legal.
- Requisitos adicionales según el acuerdo comercial:
  - Para MERCOSUR: el certificado debe indicar el porcentaje de contenido regional (mínimo 60%).
  - Para Chile: se requiere además un certificado de libre venta si el producto es alimenticio.
  - Para México: el valor FOB no debe exceder $10.000 para trámite simplificado.
- El Coordinador verifica que todos los documentos estén en español o con traducción simple. Si falta algún dato, se rechaza la solicitud y se notifica al socio con los motivos.
"""  # noqa: E501

