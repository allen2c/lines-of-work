title = "Pasos para la planificación de eventos"

content = """
- Definir objetivo, fecha tentativa y presupuesto aprobado por la gerencia.
- Reservar sede con al menos 3 meses de anticipación para eventos grandes, 1 mes para webinars.
- Contratar proveedores (catering, sonido, streaming) y firmar contratos con cláusulas de cancelación.
- Diseñar la agenda y confirmar ponentes con 60 días de antelación.
- Preparar materiales digitales (presentaciones, folletos) y físicos (credenciales, carpetas).
- Configurar la página de inscripción en el CRM y definir precios (early bird, regular, grupos).
- Realizar una prueba técnica 1 semana antes (para eventos híbridos o virtuales).
- El día del evento, coordinar registro, moderación y atención a asistentes.
"""

