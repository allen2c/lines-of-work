title = "Reparación de cortacésped"

content = """
- Tipos: manual (empuje), autopropulsado, con bolsa, triturado. Motores de 4 tiempos (Briggs & Stratton, Honda, Kohler) o eléctricos.
- Fallas comunes: no arranca, pierde potencia, vibra, corta mal, no se desplaza (autopropulsado), ruedas sueltas.
- Cuchilla: desmonta y afila con esmeril o lima. El filo debe tener un ángulo de 30°. Verifica el balance: la cuchilla debe estar equilibrada (usa un equilibrador de cuchillas). Si está doblada o agrietada, reemplázala.
- Sistema de autopropulsión: revisa el cable de transmisión (debe estar tenso), la polea y la correa. Si la correa está desgastada o resbalosa, reemplázala. Mide la tensión: debe haber 10-15 mm de juego al presionar la correa.
- Ruedas: verifica que los cojinetes giren suavemente. Si están duros, lubrica con grasa multiusos. Si tienen juego excesivo, reemplaza los bujes.
- Bolsa de césped: revisa que no tenga agujeros y que el enganche esté firme. Si está rota, sugiere reemplazo.
- Altura de corte: ajusta las ruedas a la misma posición. Verifica que el mecanismo de ajuste no esté atascado.
"""

