title = "Prueba y reemplazo de bujías"

content = """
- Retira la bujía con una llave de bujía del tamaño adecuado (generalmente 5/8" o 13/16"). Inspecciona visualmente: electrodo desgastado, carbonilla seca (mezcla rica), depósitos aceitosos (anillos desgastados), aislante agrietado.
- Limpia la bujía con un cepillo de alambre de latón si está ligeramente sucia. No uses chorro de arena porque daña el aislante.
- Mide la separación entre electrodos con una galga de bujías. Separación típica: 0.6-0.8 mm para motores pequeños. Ajusta doblando suavemente el electrodo lateral.
- Prueba de chispa: conecta la bujía al cable de alta tensión, apoya la rosca contra el bloque del motor (tierra) y tira del arrancador. Debe verse una chispa azul brillante. Si es débil o naranja, reemplaza la bujía.
- Si no hay chispa, verifica el cable de bujía (continuidad), la bobina de encendido (resistencia primaria 0.5-2 ohmios, secundaria 5-15 kohmios) y el interruptor de parada (debe estar en posición de funcionamiento).
- Reemplaza la bujía cada 100 horas de uso o anualmente. Usa la bujía recomendada por el fabricante (ej. NGK BPMR7A para Stihl MS 250).
"""

