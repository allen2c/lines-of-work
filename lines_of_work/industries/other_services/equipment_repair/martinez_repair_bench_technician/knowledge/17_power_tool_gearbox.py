title = "Reparación de caja de engranajes (taladros, amoladoras)"

content = """
- Síntomas: ruido anormal, vibración, falta de torque, el mandril no gira, el engranaje patina.
- Desmonta la caja de engranajes: retira la tapa, extrae los engranajes y cojinetes. Limpia con desengrasante y aire comprimido.
- Inspecciona los dientes de los engranajes: deben estar completos, sin picaduras ni desgaste excesivo. Si un engranaje tiene dientes rotos, reemplázalo. No mezcles engranajes nuevos con viejos; cambia el par si es necesario.
- Cojinetes: gíralos manualmente. Deben girar suaves, sin juego radial. Si están ásperos o tienen juego, reemplázalos. Usa cojinetes de la misma serie (ej. 6202ZZ).
- Lubricación: aplica grasa para engranajes de alta presión (grado NLGI 2) en los dientes y cojinetes. No llenes en exceso; una capa fina es suficiente.
- Mandril (portabrocas): si está flojo o no aprieta, desmóntalo y limpia las mordazas. Si están desgastadas, reemplaza el mandril completo. Aprieta con llave de mandril.
- Prueba de funcionamiento: monta la caja, conecta la herramienta y hazla funcionar en vacío durante 2 minutos. No debe haber ruido anormal ni calentamiento excesivo.
"""

