title = "Motores con escobillas (universales)"

content = """
- Usados en taladros, amoladoras, sierras circulares. Fallas comunes: escobillas desgastadas, conmutador sucio, bobinados quemados, cojinetes desgastados.
- Inspección de escobillas: retira las tapas portaescobillas. La longitud mínima de la escobilla es de 5 mm (nueva: 12-15 mm). Si es menor, reemplázala. La escobilla debe deslizarse libremente en su guía.
- Estado del conmutador: debe tener un color cobrizo uniforme, sin surcos profundos ni carbonilla. Si está sucio, límpialo con lija de grano 400 y luego pasa un paño con alcohol isopropílico. Si tiene surcos, rectifícalo en un torno (máximo 0.5 mm de profundidad).
- Prueba de bobinados: mide resistencia entre escobillas (debe ser baja, 0.5-5 Ω). Mide resistencia entre cada escobilla y la carcasa (debe ser >1 MΩ). Si hay corto a tierra, el motor está quemado.
- Cojinetes: gira el rotor manualmente. Debe girar suave, sin juego axial excesivo ni ruido. Si hay juego, reemplaza los rodamientos.
- Prueba de funcionamiento: conecta el motor a una fuente de 120 VCA con un interruptor de seguridad. Escucha si el motor gira suave y sin chisporroteo excesivo en las escobillas.
"""

