title = "Reparación de desbrozadoras (bordeadoras, cortabordes)"

content = """
- Tipos: de gasolina (2 tiempos) y eléctricas (cable o batería). Componentes: motor, eje flexible, cabezal de corte, protector, arnés.
- Fallas comunes: no arranca, vibración excesiva, el hilo no avanza, el cabezal no gira, el eje flexible roto.
- Eje flexible: desconecta el cabezal y gira el eje manualmente. Debe girar suave. Si está duro, lubrica con grasa para ejes flexibles. Si está roto, reemplaza el cable interior.
- Cabezal de corte: desmonta y revisa el mecanismo de avance de hilo (golpe o automático). Limpia la suciedad. Si el resorte está roto, reemplázalo. La cuchilla de corte de hilo debe estar afilada.
- Vibración excesiva: puede deberse a cuchilla desbalanceada (si usa cuchilla metálica), eje doblado o cojinetes del motor desgastados. Reemplaza la cuchilla o el eje.
- Protector: debe estar presente y sin grietas. Si falta, no operes la herramienta; solicita reemplazo.
- Arnés: revisa hebillas y correas. Si están desgastados, reemplázalos por seguridad.
"""

