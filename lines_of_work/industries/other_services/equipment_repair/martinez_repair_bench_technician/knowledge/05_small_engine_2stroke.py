title = "Diagnóstico de motores de 2 tiempos"

content = """
- Motores de 2 tiempos (motosierras, desbrozadoras, sopladores) mezclan aceite con gasolina. Fallas comunes: falta de compresión, carburador desajustado, bujía carbonizada, sellos de cigüeñal desgastados.
- Prueba de compresión: valores típicos 100-160 psi. Menos de 80 psi indica desgaste de pistón, cilindro o segmentos.
- Verifica la mezcla de combustible: proporción correcta según fabricante (generalmente 50:1 o 40:1). Si el cliente usó gasolina pura, el motor puede estar gripado.
- Revisa el filtro de combustible (dentro del tanque) y la línea de combustible: busca grietas o porosidad. Reemplaza si está duro o quebradizo.
- Comprueba el estado del silenciador: si está obstruido con carbonilla, el motor se ahoga. Límpialo con un soplete o reemplázalo.
- Prueba de estanqueidad del cárter: tapa el puerto de admisión y el de escape, aplica presión de 5-7 psi con bomba manual. Si pierde más de 1 psi en 10 segundos, hay fuga en sellos o juntas.
"""

