title = "Prueba de compresión en motores pequeños"

content = """
- Herramienta: compresómetro con adaptador de rosca para bujías (generalmente M14x1.25 o M10x1.0). Asegúrate de que el motor esté frío y la bujía retirada.
- Abre el acelerador al máximo (puedes bloquearlo con una liga) para permitir la entrada de aire. Tira del arrancador de 4 a 6 veces hasta que la aguja se estabilice.
- Registra la lectura máxima. Interpretación:
  - >120 psi: compresión excelente.
  - 90-120 psi: aceptable, puede funcionar pero con pérdida de potencia.
  - 60-90 psi: baja, requiere revisión de anillos, cilindro o válvulas.
  - <60 psi: motor necesita reconstrucción o reemplazo.
- Si la compresión es baja, realiza una prueba húmeda: añade 5 ml de aceite de motor por el agujero de la bujía y repite la prueba. Si sube >10 psi, el desgaste es en anillos. Si no sube, el problema es en válvulas o junta de culata.
- Para motores de 2 tiempos, la compresión baja también puede deberse a sellos de cigüeñal desgastados. Realiza prueba de estanqueidad del cárter (ver conocimiento 05).
"""

