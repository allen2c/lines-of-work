title = "Diagnóstico de motores de 4 tiempos"

content = """
- Los motores de 4 tiempos (cortacésped, generadores, motobombas) tienen válvulas, árbol de levas y cárter de aceite. Fallas comunes: falta de compresión, bujía sucia, carburador obstruido, aceite bajo o contaminado.
- Prueba de compresión: retira la bujía, inserta el compresómetro, tira del arrancador 4-5 veces. Lectura normal: 90-150 psi (según modelo). Por debajo de 60 psi indica desgaste de anillos o válvulas.
- Verifica nivel y estado del aceite: debe estar entre las marcas de la varilla, color ámbar claro. Si es negro o tiene olor a gasolina, cámbialo.
- Revisa el filtro de aire: si está sucio, límpialo o reemplázalo. Un filtro obstruido causa mezcla rica y ahogamiento.
- Comprueba el juego de válvulas (si aplica): para motores con ajuste, usa galgas. Juego típico admisión 0.10-0.15 mm, escape 0.15-0.20 mm.
- Si el motor humea negro: mezcla rica (carburador sucio, filtro de aire obstruido). Si humea azul: aceite quemado (anillos o guías de válvula). Si humea blanco: agua en combustible o junta de culata dañada.
"""

