title = "Limpieza y ajuste de carburador"

content = """
- Desmonta el carburador: retira el filtro de aire, desconecta las líneas de combustible y el cable del acelerador. Anota la posición de los tornillos de ajuste (L, H, ralentí) antes de desarmar.
- Desarma completamente: retira la cuba, el flotador, el inyector principal, el inyector de ralentí, el tornillo de mezcla y el difusor. Coloca las piezas en una bandeja ordenada.
- Limpia con limpiador de carburadores en aerosol (marca CRC o similar) y aire comprimido a 40-60 psi. No uses alambre metálico en los inyectores; usa un alambre de cobre fino o un limpiador ultrasónico si está muy obstruido.
- Revisa el flotador: debe flotar y no tener fugas. La altura del flotador debe ser la especificada (generalmente 15-18 mm desde la base de la cuba con el flotador invertido).
- Reemplaza la junta de la cuba y el empaque del cuerpo si están dañados. Usa juntas originales o de calidad equivalente.
- Ajuste básico: aprieta los tornillos L y H hasta el fondo suavemente, luego afloja L 1.5 vueltas y H 1 vuelta. Ajusta el ralentí para que el motor funcione estable sin la herramienta de corte girando.
- Después del ajuste, prueba en caliente: acelera a fondo; si el motor duda, abre H 1/8 de vuelta. Si el ralentí es inestable, ajusta L 1/8.
"""

