content = """
- 湯温は40～42℃に設定。体温計で確認し、やけど防止のため43℃以上は使用しない。湯量は全身が浸かる程度（約50リットル）だが、体位変換時にこぼれないよう注意。
- 洗浄順序：頭部→顔面→上肢→胸部→腹部→下肢→背部。頭部は後頭部まで丁寧に洗い、耳の後ろや首のしわも清拭。洗髪はシャンプーを使用し、すすぎ残しがないように。
- 爪切り：手足の爪を短く整え、やすりで滑らかに。爪の間の汚れはブラシで除去。入れ歯がある場合は外して洗浄し、専用容器に保管。
- 体位変換：仰臥位から側臥位へ、または座位にする場合、関節の硬直に注意。肩・股関節は無理に動かさず、温タオルで10分程度温めてから行う。変換中は必ず2人以上で対応。
- 湯灌後は清潔なバスタオルで水分を拭き取り、保湿ローションを塗布して皮膚の乾燥を防ぐ。特に顔・手・足は念入りに。湯灌の所要時間は通常30～45分。
"""  # noqa: E501

