content = """
Client safety governs all spa operations. Understanding contraindications prevents harm
and ensures treatments provide intended benefits without adverse effects. Thorough
screening and vigilant observation protect both clients and practitioners.

**Universal Contraindications:**

*Fever and Acute Illness:* Any client with fever, contagious disease, or acute infection
must reschedule. Treatment during illness stresses the body, spreads contagion, and produces
poor results.

*Open Wounds and Skin Infections:* Active herpes, impetigo, fungal infections, or open
wounds preclude treatment on affected areas. Body wraps and immersion treatments are
absolutely contraindicated.

*Severe Cardiovascular Conditions:* Uncontrolled hypertension, recent heart attack,
severe varicose veins, or thrombosis contraindicate circulatory-enhancing treatments.
Physician clearance required for cardiac clients.

*Pregnancy Considerations:* First trimester generally contraindicates many treatments
beyond basic services. Always require physician clearance. Avoid abdominal massage, certain
essential oils, and heat therapies. Side-lying positioning necessary after first trimester.

**Massage-Specific Contraindications:**

Recent surgery sites, acute injuries with inflammation, bleeding disorders, blood thinner
medication, osteoporosis with fragility, and certain cancer presentations require
modification or avoidance. Deep tissue and intense modalities demand particular scrutiny.

**Skincare Treatment Contraindications:**

Isotretinoin use within past six months absolutely contraindicates peels, waxing, and
exfoliating treatments. Retinoid use requires temporary discontinuation before certain
services. Recent cosmetic procedures like Botox or fillers require waiting periods.
Photosensitizing medications increase sun sensitivity post-treatment.

**Emergency Preparedness:**

All staff maintain current CPR and first aid certification. Emergency contact numbers
posted prominently including local emergency services and poison control. First aid kits
stocked and accessible in each treatment area. Choking rescue equipment available for
relaxation areas serving food and beverages.

**Hygiene Standards:**

Universal precautions treat all bodily fluids as potentially infectious. Single-use
items disposed of properly. Linens changed between every client with hospital-grade
laundering. Equipment sanitized following manufacturer guidelines and health department
regulations.

**Documentation Requirements:**

Record all contraindications, modifications, and client health updates in permanent
files. Note any adverse reactions immediately with detailed circumstances. Incident
reports filed for any unexpected events regardless of severity. Insurance and legal
protection requires thorough documentation.

**When to Refuse Service:**

Trust instincts when something feels wrong. Better to reschedule or decline than risk
client harm. Document refusal reasons professionally without judgment. Offer alternatives
when possible or referrals to appropriate healthcare providers.
"""  # noqa: E501

