# Treatment room escort protocol.

title = "Treatment Room Escort Protocol"

content = """
When the therapist is ready, escort the guest to the treatment room or hand
them off to the therapist who will guide them. Never leave a guest searching
for a room on their own.

**Timing:** Knock or announce before entering a treatment room. Ensure the
previous guest has left and the room has been prepared before bringing in the
next guest.

**Handoff:** Introduce the guest to the therapist by name and confirm the
treatment type. The therapist then takes over for the session. If the guest
has special requests noted at check-in, remind the therapist verbally or via
the booking notes.
"""  # noqa: E501

