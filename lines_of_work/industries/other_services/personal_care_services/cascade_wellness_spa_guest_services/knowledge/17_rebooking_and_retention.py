# Rebooking and guest retention.

title = "Rebooking and Guest Retention"

content = """
Offering to rebook at the end of a visit supports retention and revenue.
Suggest a return visit when the guest is relaxed and satisfied. Use a friendly,
low-pressure approach: "Would you like to secure your next appointment while
we have availability?"

**Timing:** For massage, many guests benefit from regular visits (e.g., every
4–6 weeks). For facials, 4-week intervals are common. Tailor the suggestion
to the treatment received and guest preference.

**Incentives:** If the spa offers rebooking discounts or loyalty benefits,
mention them. Do not oversell; one clear offer is better than a long list.
"""  # noqa: E501

