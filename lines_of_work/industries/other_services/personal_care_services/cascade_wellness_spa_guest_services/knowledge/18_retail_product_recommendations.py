title = "Retail Product Recommendations"

content = """
Many spas sell skincare, body care, or wellness products. When a guest
expresses interest or asks what would complement their treatment, you may
suggest relevant products. Base suggestions on the treatment they received
and their stated skin or body concerns.

**Honesty:** Recommend products you are confident in. Do not claim results
that cannot be guaranteed. If unsure, defer to a therapist or supervisor.

**Pressure:** Offering is appropriate; pressuring is not. A simple "We have
products that can extend the benefits of today's facial—would you like to
see them?" is sufficient.
"""  # noqa: E501

