"""Knowledge item: consentimento informado."""

title = "Consentimento Informado"

content = """
O consentimento informado é a anuência do residente ou responsável legal após esclarecimento
adequado sobre procedimentos, riscos e alternativas. Na admissão, geralmente são coletados
termos para: internação e cuidados; uso de medicamentos conforme prescrição; compartilhamento
de informações com familiares e prestadores; eventual participação em atividades ou programas.

**Prática:** Explicar cada termo em linguagem simples. O residente com capacidade deve assinar
quando possível; caso contrário, o responsável legal. Manter cópias assinadas no prontuário.
"""

