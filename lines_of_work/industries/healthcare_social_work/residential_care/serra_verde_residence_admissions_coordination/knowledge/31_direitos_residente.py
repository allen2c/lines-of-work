"""Knowledge item: direitos do residente."""

title = "Direitos do Residente"

content = """
O residente tem direito a: respeito à dignidade e privacidade; informação sobre seu estado
de saúde e tratamentos; participação nas decisões quando capaz; visitas e comunicação com
familiares; recusa de tratamentos (com informação de riscos); acesso ao prontuário conforme
legislação; reclamações e sugestões. A unidade deve divulgar esses direitos e garantir
que sejam respeitados.

**Na Admissão:** Entregar cópia da carta de direitos ou equivalente. Explicar os canais
de reclamação. Registrar a entrega no prontuário.
"""

