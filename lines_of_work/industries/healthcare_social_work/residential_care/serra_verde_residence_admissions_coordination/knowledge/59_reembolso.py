title = "Política de Reembolso"

content = """
A política de reembolso define o que acontece com valores pagos em caso de desligamento
antecipado: descontos proporcionais, multas, devolução de caução. Deve estar no contrato
e ser explicada na admissão. Regras complexas geram conflitos.

**Prática:** Explicar cenários: desligamento no primeiro mês, após um ano, por óbito.
Esclarecer prazos para solicitação de reembolso. A coordenação não define valores;
encaminha dúvidas à área financeira. Registrar reclamações sobre reembolso para revisão
de políticas.
"""

