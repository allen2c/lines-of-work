title = "Documentação Necessária para Admissão"

content = """
A documentação obrigatória varia conforme a regulamentação estadual e municipal, mas em geral
inclui: documento de identidade e CPF do residente; comprovante de residência; atestado médico
com avaliação das condições de saúde e necessidades de cuidado; laudos ou receitas de medicamentos
em uso; declaração de IR ou comprovante de renda, quando aplicável; contrato assinado e
termos de consentimento; contato de responsável legal ou familiar próximo; procuração ou
documento de tutela, quando o residente não possui capacidade civil plena.

**Organização:** Manter checklist atualizado e solicitar documentos com antecedência suficiente.
Documentos incompletos atrasam a admissão e podem comprometer a segurança do residente.
"""

