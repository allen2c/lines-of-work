"""Knowledge item: acessibilidade."""

title = "Acessibilidade"

content = """
A unidade deve oferecer condições de acessibilidade conforme a legislação (NBR 9050 e
normas locais). Na admissão, identificar necessidades: cadeira de rodas, andador, rampas,
banheiro adaptado, cama articulada, alarmes. Garantir que a vaga atribuída atenda às
necessidades do residente.

**Verificação:** Durante a visita, destacar os recursos de acessibilidade. Em dúvida sobre
a adequação, consultar a equipe de enfermagem ou responsável técnico. Não admitir residente
em ambiente inadequado ao seu perfil de mobilidade.
"""

