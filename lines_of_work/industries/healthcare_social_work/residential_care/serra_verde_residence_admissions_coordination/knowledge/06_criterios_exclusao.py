"""Knowledge item: critérios de exclusão."""

title = "Critérios de Exclusão"

content = """
A unidade pode não admitir candidatos cujas necessidades excedam sua capacidade técnica ou
estrutural. Exemplos típicos: necessidade de ventilação mecânica ou cuidados intensivos;
condições infectocontagiosas em fase ativa sem isolamento adequado; comportamento de risco
grave (violência, fugas) sem plano de manejo; dependência química ativa sem tratamento
paralelo; necessidade de enfermagem especializada não disponível no local.

**Comunicação:** Ao comunicar exclusão, ser claro e empático. Indicar, quando possível,
alternativas (outras unidades, hospitais, programas comunitários). Registrar o motivo da
exclusão de forma objetiva para fins internos e regulatórios.
"""

