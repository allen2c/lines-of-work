"""Knowledge item: visão geral do processo de admissão."""

title = "Visão Geral do Processo de Admissão"

content = """
O processo de admissão em residência para idosos envolve etapas que asseguram o encaminhamento
adequado do residente e a documentação legal necessária. A coordenação eficiente reduz
ansiedade das famílias e garante que a unidade receba residentes cujas necessidades estejam
alinhadas à sua capacidade de cuidado.

**Etapas Principais:** (1) Consulta inicial por telefone, e-mail ou visita; (2) Triagem de
elegibilidade com base em necessidades de cuidado e capacidade de pagamento; (3) Visita guiada
à unidade; (4) Avaliação clínica e funcional pela equipe de enfermagem, quando exigido; (5)
Reunião com família para esclarecimento de contrato e valores; (6) Assinatura de documentos e
coleta de prontuário; (7) Agendamento da mudança e orientação inicial.

**Responsabilidades:** A coordenação de admissões conduz as etapas não clínicas. Decisões
médicas ou de adequação clínica competem à diretoria de enfermagem. Manter registros
precisos de cada etapa é essencial para auditoria e melhoria contínua.
"""

