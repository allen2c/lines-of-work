title = "Declaração de IR ou Comprovante de Renda"

content = """
Para verificação de capacidade de pagamento ou elegibilidade a subsídios, a unidade pode
solicitar declaração de IR ou comprovantes de renda. A coleta deve respeitar a LGPD e
ser justificada pela finalidade.

**Procedimento:** Solicitar apenas quando necessário. Explicar a finalidade. Manter os
documentos em sigilo. Residentes com BPC ou benefícios governamentais podem apresentar
comprovante do benefício em vez de declaração de IR.
"""

