"""Knowledge item: perfil do residente ideal."""

title = "Perfil do Residente Ideal"

content = """
Cada unidade tem um perfil de residente que melhor se adequa à sua estrutura e equipe.
Definir esse perfil ajuda na triagem e na comunicação com famílias. Exemplos: faixa etária;
níveis de dependência atendidos; condições que a unidade não atende (ex.: ventilação
mecânica, diálise); perfil socioeconômico compatível com a mensalidade.

**Uso:** Usar o perfil para qualificar consultas e evitar expectativas irreais. Não
discriminar por critérios ilegais; o perfil é técnico e operacional.
"""

