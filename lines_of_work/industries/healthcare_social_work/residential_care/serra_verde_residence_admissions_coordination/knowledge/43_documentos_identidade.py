title = "Documentos de Identidade"

content = """
Documentos obrigatórios para admissão: RG e CPF do residente (ou certidão de nascimento
para menores); documento do responsável financeiro e do contato de emergência quando
distintos. Cópias devem ser legíveis e atualizadas.

**Verificação:** Conferir se o nome nos documentos coincide com o do prontuário e contrato.
Em caso de documentos em nome de terceiros (tutela), verificar a documentação judicial.
Manter cópias no prontuário ou em sistema seguro.
"""

