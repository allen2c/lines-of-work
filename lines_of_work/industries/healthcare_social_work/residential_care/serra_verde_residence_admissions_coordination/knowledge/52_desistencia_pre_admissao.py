"""Knowledge item: desistência pré-admissão."""

title = "Desistência Pré-Admissão"

content = """
Famílias podem desistir após consulta, visita ou mesmo após assinatura de documentos e
antes da mudança. Registrar a desistência com data e motivo (quando informado). Manter
o registro para análises (preços, processo, concorrência).

**Procedimento:** Confirmar a desistência por escrito ou e-mail. Se houver depósito ou
sinal, verificar a política de reembolso e aplicar conforme contrato. Não pressionar;
a decisão é da família. Manter porta aberta para retorno futuro, se a família desejar.
"""

