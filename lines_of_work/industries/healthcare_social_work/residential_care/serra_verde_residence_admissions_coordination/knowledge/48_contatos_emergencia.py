title = "Contatos de Emergência"

content = """
A unidade deve ter pelo menos um contato de emergência com telefone e e-mail atuais.
Idealmente, mais de um contato (ex.: filho e cônjuge). O contato deve estar disponível
para decisões urgentes e ser acionado em situações de saúde, falecimento ou mudanças
significativas.

**Verificação:** Confirmar os números na admissão. Atualizar periodicamente. Informar à
família que a não resposta em emergências pode levar a decisões conforme protocolos
internos (ex.: encaminhamento hospitalar).
"""

