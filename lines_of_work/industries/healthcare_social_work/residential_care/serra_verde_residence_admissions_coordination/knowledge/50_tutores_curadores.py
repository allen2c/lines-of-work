"""Knowledge item: tutores e curadores."""

title = "Tutores e Curadores"

content = """
Tutela e curadoria são institutos judiciais que atribuem a um responsável a representação
legal do indivíduo (menor ou incapaz). O documento judicial define o responsável e os
limites de atuação. A unidade deve receber cópia da decisão e identificar o representante.

**Diferença:** Tutela aplica-se a menores; curadoria a maiores incapazes. O curador de
idoso pode ter poderes para decisões de saúde, patrimônio e residência. Respeitar estritamente
os termos da decisão judicial.
"""

