title = "Reingresso"

content = """
Reingresso é o retorno de um residente que já esteve na unidade e foi desligado (alta,
transferência, falecimento de cuidador que o levou para casa). A política de reingresso
deve definir: se é permitido; se há prioridade em relação a novos candidatos; se exige
nova avaliação completa; condições de pagamento.

**Procedimento:** Verificar histórico do residente no prontuário. Aplicar triagem e
avaliação como para novo candidato, salvo exceções documentadas. Documentar o reingresso
para estatísticas e acompanhamento.
"""

