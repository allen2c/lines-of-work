"""Knowledge item: transferência de outras instituições."""

title = "Transferência de Outras Instituições"

content = """
A admissão por transferência de outra residência ou hospital exige documentação adicional:
relatório de alta ou de desligamento da instituição anterior; histórico de cuidados e
medicamentos; motivo da transferência; contato da instituição de origem para eventual
troca de informações.

**Fluxo:** Validar que o prontuário anterior está completo antes da mudança. Coordenar com
a instituição de origem o horário e a forma de entrega do residente. Garantir continuidade
de medicamentos e cuidados. Registrar a origem da transferência para estatísticas e auditoria.
"""

