title = "Período de Adaptação"

content = """
O período de adaptação é o intervalo inicial em que o residente e a família se ajustam à
nova rotina. A duração varia conforme a unidade; muitas adotam 30 ou 90 dias. Durante esse
período, pode haver reavaliação do nível de cuidado, ajuste de medicação e apoio emocional
intensificado.

**Coordenação:** Informar a família sobre a existência e duração do período de adaptação.
Esclarecer que ajustes de nível de cuidado podem alterar valores. Manter comunicação próxima
com a equipe de cuidados para identificar dificuldades e encaminhá-las adequadamente.
"""

