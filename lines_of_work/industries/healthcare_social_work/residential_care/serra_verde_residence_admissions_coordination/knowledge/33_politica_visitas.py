title = "Política de Visitantes"

content = """
A política de visitas define horários, áreas permitidas, número de visitantes e regras
especiais (crianças, visitas em quartos). Em períodos de surto ou pandemia, restrições
podem ser temporárias. A política deve ser comunicada na admissão e disponível por escrito.

**Flexibilidade:** Avaliar exceções (visita em fim de vida, situação familiar urgente) em
conjunto com a coordenação ou enfermagem. Documentar exceções concedidas e o motivo.
"""

