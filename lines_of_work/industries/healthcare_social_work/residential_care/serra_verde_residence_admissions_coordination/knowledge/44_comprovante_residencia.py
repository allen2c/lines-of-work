title = "Comprovante de Residência"

content = """
O comprovante de residência (conta de luz, água, contrato de aluguel) é exigido por
algumas unidades para fins administrativos ou regulatórios. Em casos de transferência
hospitalar ou pessoa em situação de rua, pode ser dispensado ou substituído por
declaração.

**Procedimento:** Solicitar quando aplicável. Aceitar formatos usuais (conta em nome do
residente ou de familiar no mesmo endereço). Para residentes sem endereço fixo prévio,
documentar a situação e encaminhar à administração para decisão.
"""

