title = "Avaliação de Necessidades do Residente"

content = """
A avaliação de necessidades define o nível de cuidado adequado e identifica requisitos
específicos (mobilidade, alimentação, medicação, acompanhamento). É realizada pela equipe
de enfermagem, com suporte da coordenação de admissões na coleta de informações preliminares.

**Dimensões Avaliadas:** Capacidade de realização de atividades da vida diária (AVDs);
necessidade de auxílio para mobilidade ou uso de equipamentos; regime medicamentoso;
condições cognitivas; risco de queda; necessidades nutricionais; acompanhamento por
especialistas ou terapia.

**Papel da Coordenação:** Fornecer à equipe de enfermagem os dados disponíveis das consultas,
acompanhar o agendamento da avaliação e comunicar o resultado à família. Não realizar
avaliações clínicas; apenas facilitar o fluxo.
"""

