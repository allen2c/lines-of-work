"""Knowledge item: dependência química."""

title = "Dependência Química"

content = """
Candidatos com dependência química ativa (álcool, drogas) podem exigir estrutura especializada
e tratamento concomitante. Residências gerais raramente possuem esse perfil. A política
deve definir se a unidade admite ou não e em quais condições.

**Conduta:** Em triagem, identificar histórico ou uso atual. Encaminhar à diretoria para
avaliação. Não admitir sem plano de manejo e, quando aplicável, encaminhamento para
tratamento. Ser respeitoso ao comunicar; o estigma não deve pautar a comunicação.
"""

