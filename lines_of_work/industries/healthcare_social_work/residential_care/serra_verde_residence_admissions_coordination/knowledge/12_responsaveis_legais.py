title = "Responsáveis Legais"

content = """
Quando o residente não possui capacidade civil plena (interdição, curadoria), um responsável
legal assume decisões em seu nome. A unidade deve receber cópia do documento judicial que
institui a tutela, curadoria ou interdição, identificando o responsável.

**Documentos:** Procuração pública, quando aplicável; decisão judicial de tutela ou curadoria;
termo de responsabilidade assinado pelo responsável. Em caso de dúvida sobre quem pode
assinar contratos ou consentimentos, encaminhar à administração ou assessoria jurídica.
"""

