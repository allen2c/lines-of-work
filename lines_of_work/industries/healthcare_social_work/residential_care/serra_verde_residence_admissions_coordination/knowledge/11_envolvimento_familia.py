title = "Envolvimento da Família"

content = """
A família é parceira essencial na decisão e na adaptação. Durante a admissão, identificar
quem é o interlocutor principal, quem assina contratos e quem será contato de emergência.
Respeitar dinâmicas familiares diversas; em alguns casos, a decisão é compartilhada entre
vários membros.

**Comunicação:** Manter a família informada em cada etapa. Esclarecer que a unidade espera
visitas e participação conforme a política interna. Documentar quem foi presente em reuniões
e assinaturas. Em caso de conflito entre familiares, encaminhar à administração ou assessoria.
"""

