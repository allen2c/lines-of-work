title = "Histórico Médico"

content = """
O histórico médico deve incluir: diagnósticos atuais e anteriores; cirurgias; alergias;
condições crônicas; acompanhamento por especialistas; exames recentes relevantes; contato
do médico de referência. A unidade precisa desse contexto para garantir continuidade de
cuidados e segurança.

**Fontes:** Prontuário da instituição anterior, alta hospitalar, declaração do médico
assistente. Solicitá-los com antecedência. Histórico incompleto pode retardar a admissão
ou exigir complementação após a entrada.
"""

