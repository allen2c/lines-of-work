title = "Queixas e Reclamações"

content = """
A unidade deve ter fluxo para registro e tratamento de queixas. Na admissão, informar onde
e como a família pode registrar reclamações (livro de ocorrências, e-mail, canal específico).
Cada queixa deve ser registrada, avaliada e respondida em prazo definido.

**Conduta:** Não minimizar ou ignorar reclamações. Encaminhar ao setor responsável (qualidade,
compliance, administração). Manter sigilo quando solicitado. Em situações graves (violência,
negligência), seguir protocolo de notificação e escalação.
"""

