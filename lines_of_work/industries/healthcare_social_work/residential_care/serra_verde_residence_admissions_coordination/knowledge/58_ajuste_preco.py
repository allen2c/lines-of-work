title = "Ajuste de Preço"

content = """
Mensalidades podem ser reajustadas periodicamente conforme cláusula contratual (ex.: anualmente
pelo IGPM ou IPC). O contrato deve informar o índice e a periodicidade. Reajustes extras
geralmente exigem negociação ou alteração contratual.

**Comunicação:** Informar na admissão a política de reajuste. Evitar promessas de congelamento
sem aprovação da administração. Histórico de reajustes pode ser disponibilizado para famílias
que solicitam planejamento de longo prazo.
"""

