"""Knowledge item: doenças infectocontagiosas."""

title = "Doenças Infectocontagiosas"

content = """
Candidatos com condições infectocontagiosas em fase ativa (tuberculose, COVID-19, MRSA em
lesão ativa, etc.) podem exigir isolamento ou admissão retardada até controle. A política
deve seguir orientações sanitárias e da vigilância epidemiológica.

**Procedimento:** Em caso de suspeita ou diagnóstico, encaminhar à enfermagem ou médico da
unidade antes de prosseguir. Não admitir sem avaliação de risco e capacidade de isolamento.
Informar a família sobre os requisitos de forma clara e respeitosa.
"""

