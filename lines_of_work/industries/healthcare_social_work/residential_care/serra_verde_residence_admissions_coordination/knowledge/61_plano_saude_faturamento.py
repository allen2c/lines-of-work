title = "Plano de Saúde e Faturamento"

content = """
Quando a unidade possui acordo com planos de saúde, o faturamento é feito diretamente com
o plano. A família deve fornecer dados do beneficiário e autorização. Valores não cobertos
são de responsabilidade da família. A verificação de cobertura deve ser feita antes da
admissão.

**Procedimento:** Encaminhar à área de faturamento para verificação. Não garantir cobertura
sem confirmação. Informar que a falta de pagamento pelo plano pode resultar em cobrança
da família. Manter registro de residentes com plano para acompanhamento.
"""

