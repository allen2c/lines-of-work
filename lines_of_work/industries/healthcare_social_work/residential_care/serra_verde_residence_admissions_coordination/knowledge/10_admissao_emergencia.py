title = "Admissão de Emergência"

content = """
Admissões de emergência ocorrem quando a família ou um hospital precisa de encaminhamento
urgente, por exemplo após alta hospitalar sem retorno ao lar ou situação de risco no domicílio.
O processo deve ser ágil, mas sem abrir mão dos requisitos mínimos de documentação e
avaliação clínica.

**Procedimentos:** Priorizar a triagem e a avaliação clínica. Coletar documentos em paralelo
quando possível. Definir um fluxo simplificado com checklist mínimo. A diretoria de
enfermagem deve aprovar a admissão antes da chegada. Documentos pendentes devem ser
completados em prazo definido (ex.: 72 horas).
"""

