title = "Depósito e Caução"

content = """
Algumas unidades exigem depósito ou caução como garantia para o cumprimento do contrato.
O valor e as condições de devolução devem estar claros no contrato. A caução pode ser
devolvida ao final do contrato, descontadas eventuais pendências.

**Na Admissão:** Informar valor, forma de pagamento e condições de devolução. Esclarecer
em quais situações a caução pode ser retida (dano ao patrimônio, inadimplência). Registrar
o recebimento e a destinação dos valores.
"""

