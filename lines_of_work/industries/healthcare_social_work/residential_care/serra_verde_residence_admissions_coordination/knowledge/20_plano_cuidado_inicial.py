title = "Plano de Cuidado Inicial"

content = """
O plano de cuidado define as intervenções diárias necessárias para cada residente: higiene,
alimentação, mobilização, administração de medicamentos, acompanhamento de sinais vitais,
sessões de terapia. É elaborado pela equipe de enfermagem e cuidados com base na avaliação
funcional e no histórico médico.

**Na Admissão:** O plano inicial pode ser provisório, com revisão após o período de
adaptação. A coordenação não elabora o plano; comunica à família que a equipe fará essa
definição e apresentará os cuidados oferecidos.
"""

