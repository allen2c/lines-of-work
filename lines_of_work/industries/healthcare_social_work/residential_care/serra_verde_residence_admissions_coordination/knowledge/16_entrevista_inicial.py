title = "Entrevista Inicial"

content = """
A entrevista inicial é o primeiro contato aprofundado com a família ou o residente. Objetivos:
compreender a situação que motivou a busca; esclarecer expectativas; apresentar a unidade e
os serviços; verificar elegibilidade preliminar; coletar dados para a avaliação clínica.

**Roteiro:** Histórico de cuidados recentes; medicamentos em uso; mobilidade e cognição;
preferências (quarto, dieta, horários); capacidade de pagamento; quem será o contato principal.
Registrar as informações em formulário padronizado. A entrevista não substitui a avaliação
médica ou de enfermagem.
"""

