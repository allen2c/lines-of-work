title = "Checklist de Admissão"

content = """
O checklist garante que nenhuma etapa obrigatória seja esquecida. Deve incluir: documentos
de identidade e comprovantes; atestado médico e laudos; receitas e lista de medicamentos;
contrato e termos assinados; avaliação clínica aprovada; quarto atribuído; data e horário
da mudança confirmados; orientação agendada; contatos de emergência verificados.

**Uso:** Preencher o checklist em cada admissão. Assinar e datar cada item. O checklist
completo deve anteceder a mudança. Itens pendentes devem ter prazo e responsável definidos.
"""

