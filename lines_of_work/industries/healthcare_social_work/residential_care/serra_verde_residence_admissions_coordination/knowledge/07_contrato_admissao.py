"""Knowledge item: contrato de admissão."""

title = "Contrato de Admissão"

content = """
O contrato de admissão estabelece as obrigações da unidade e da família ou responsável
financeiro. Deve ser redigido em linguagem acessível e esclarecer: valor da mensalidade e
itens inclusos; taxas adicionais (medicamentos, procedimentos especiais); política de
reajuste; prazo de aviso para rescisão; política de reembolso em caso de desligamento;
obrigações da família (visitas, fornecimento de itens pessoais, comunicados); responsabilidade
por custos não cobertos por convênio ou subsídio.

**Explicação:** Ler e explicar cláusulas principais em reunião presencial. Permitir tempo
para dúvidas e consulta a advogado, se a família desejar. Contratos incompreendidos geram
conflitos posteriores.
"""

