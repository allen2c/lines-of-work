"""Knowledge item: convênios e seguros."""

title = "Convênios e Seguros"

content = """
Algumas unidades aceitam convênios de planos de saúde ou seguros para cobrir parte dos
custos. A coordenação deve verificar: se a unidade possui contrato com o plano; quais
itens são cobertos; qual o fluxo de faturamento; responsabilidade da família por custos
não cobertos.

**Procedimento:** Solicitar número do plano e dados do beneficiário. Encaminhar à área
financeira ou de faturamento para verificação de cobertura. Não garantir reembolso ou
cobertura sem confirmação prévia. Esclarecer que a família permanece responsável por
valores não pagos pelo convênio.
"""

