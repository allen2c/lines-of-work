"""Knowledge item: mudança de nível de cuidado."""

title = "Mudança de Nível de Cuidado"

content = """
O nível de cuidado pode aumentar (piora funcional) ou diminuir (melhora) ao longo do tempo.
A reavaliação periódica identifica a necessidade de mudança. O valor da mensalidade
geralmente varia com o nível.

**Na Admissão:** Informar que o nível é reavaliado periodicamente e que mudanças podem
alterar o valor. O período de adaptação pode incluir reavaliação. A coordenação de
admissões não decide mudanças de nível; isso compete à equipe de enfermagem e à administração.
"""

