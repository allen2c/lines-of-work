title = "Demências e Cuidados"

content = """
Residentes com demência (Alzheimer, vascular, etc.) exigem ambiente estruturado, equipe
treinada e protocolos de segurança (wandering, agitação). A unidade deve informar se
possui ala ou programa específico para demência e qual o nível de comprometimento que
consegue atender.

**Na Triagem:** Identificar se o candidato apresenta demência e o estágio. Casos avançados
podem exigir cuidados além da capacidade da unidade. Encaminhar à avaliação clínica antes
de assumir compromissos. Nunca minimizar a complexidade do cuidado em demências.
"""

