title = "Alta Hospitalar e Encaminhamento"

content = """
Hospitais frequentemente encaminham pacientes para residências após alta. O fluxo inclui:
contato do setor de alta ou assistente social; triagem de elegibilidade; avaliação clínica
e documentação; reserva de vaga; coordenação da transferência. Prazos costumam ser curtos.

**Prática:** Manter relacionamento com hospitais da região. Ter processo ágil para consultas
de alta. Garantir que a documentação mínima (alta médica, medicamentos, contato) seja
recebida antes da mudança. Coordenar com a equipe de enfermagem para recepção adequada.
"""

