title = "Comunicação com Família"

content = """
Manter a família informada em cada etapa reduz ansiedade e fortalece a confiança.
Definir canal preferencial (telefone, WhatsApp, e-mail) e frequência de atualizações.
Registrar todas as interações com data, resumo e encaminhamentos.

**Tom:** Empático, claro e profissional. Evitar jargões. Em más notícias (ex.: inadmissão),
comunicar com delicadeza e oferecer alternativas quando possível. Nunca divulgar informações
de outros residentes ou detalhes clínicos sem autorização.
"""

