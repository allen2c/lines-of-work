"""Knowledge item: subsídio governamental."""

title = "Subsídio Governamental"

content = """
Programas como o BPC (Benefício de Prestação Continuada) ou verbas municipais/estaduais
podem cobrir parte dos custos para famílias de baixa renda. A elegibilidade é definida
por critérios oficiais; a unidade não decide quem recebe o benefício.

**Fluxo:** Identificar se a família possui ou pretende solicitar subsídio. Encaminhar ao
serviço social ou parceiros para orientação sobre documentação e prazos. Informar que a
admissão pode ser condicionada à aprovação do subsídio. Manter registro de residentes
que dependem de verbas governamentais.
"""

