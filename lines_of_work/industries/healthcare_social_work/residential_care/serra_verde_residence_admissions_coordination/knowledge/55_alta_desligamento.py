"""Knowledge item: alta e desligamento."""

title = "Alta e Desligamento"

content = """
O desligamento pode ser por: alta voluntária (pedido da família); transferência para
hospital ou outra instituição; óbito; rescisão por inadimplência ou descumprimento de
contrato; decisão da unidade por não atender mais às necessidades. A coordenação de
admissões não gerencia desligamentos; isso compete à coordenação de cuidados e administração.

**Informação:** Na admissão, esclarecer as condições de rescisão previstas no contrato:
prazo de aviso, multas, reembolso de valores. A família deve conhecer os cenários de saída.
"""

