"""Knowledge item: procuradores."""

title = "Procuradores"

content = """
Procuração permite que alguém represente o residente em atos específicos (assinatura de
contrato, decisões financeiras). A procuração deve ser pública ou reconhecida e conter
os poderes conferidos. Procuradores com poderes gerais podem assumir responsabilidades
amplas; procuraduras específicas limitam o escopo.

**Validação:** Verificar se a procuração está vigente e cobre os atos necessários.
Em caso de interdição, a procuração pode ser inválida; a tutela ou curadoria passa a
vigorar. Em dúvida, encaminhar à assessoria jurídica.
"""

