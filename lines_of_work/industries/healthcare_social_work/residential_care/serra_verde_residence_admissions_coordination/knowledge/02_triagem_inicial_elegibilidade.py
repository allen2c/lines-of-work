"""Knowledge item: triagem inicial e elegibilidade."""

title = "Triagem Inicial e Elegibilidade"

content = """
A triagem inicial filtra consultas para identificar candidatos que potencialmente se encaixam
nos critérios da unidade. Evita expectativas irreais e direciona famílias a alternativas
quando a unidade não é a opção adequada.

**Critérios Típicos:** Idade mínima conforme regulamentação; necessidades de cuidado dentro
dos níveis oferecidos (vida assistida, cuidados intermediários ou enfermagem); ausência de
condições que exijam unidade hospitalar ou especializada; capacidade de pagamento ou elegibilidade
a subsídios; disponibilidade de vaga na categoria de cuidado necessária.

**Condutas:** Durante a triagem, evitar promessas de vaga antes da avaliação completa. Registrar
todas as consultas com data, contato, origem e resultado da triagem. Casos limítrofes devem
ser encaminhados à diretoria de enfermagem antes de qualquer compromisso.
"""

