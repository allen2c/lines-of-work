title = "Critérios de Aceite"

content = """
Os critérios de aceite devem estar documentados na política interna da unidade e alinhados
à regulamentação. Em geral incluem: elegibilidade etária e de cuidado; documentação completa;
assinatura do contrato e termos; avaliação clínica favorável quando exigida; capacidade de
pagamento ou subsídio aprovado; concordância da família ou responsável; vaga disponível no
nível de cuidado adequado.

**Transparência:** Informar claramente os critérios às famílias durante a consulta. Critérios
ambíguos geram expectativas frustradas e reclamações. Quando houver exceções possíveis, definir
quem pode aprovar e em que condições.
"""

