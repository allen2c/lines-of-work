title = "LGPD e Proteção de Dados"

content = """
A Lei Geral de Proteção de Dados (LGPD) exige que a unidade trate dados pessoais com
transparência, finalidade e segurança. Na admissão, coletar apenas dados necessários;
informar a finalidade do tratamento; obter consentimento quando exigido; garantir acesso,
correção e exclusão conforme a lei.

**Na Prática:** Incluir cláusula de tratamento de dados no contrato ou termo separado.
Armazenar documentos em ambiente seguro. Não compartilhar dados com terceiros sem base legal
ou consentimento. Em caso de dúvida, consultar o encarregado de dados (DPO) ou assessoria.
"""

