"""Knowledge item: recusa de admissão."""

title = "Recusa de Admissão"

content = """
A unidade pode recusar admissão quando: o candidato não atende aos critérios de elegibilidade;
não há vaga no nível de cuidado necessário; a documentação está incompleta ou inconsistente;
há risco à segurança de outros residentes ou da equipe. A recusa deve ser fundamentada e
registrada.

**Comunicação:** Comunicar com empatia e clareza. Oferecer alternativas (outras unidades,
lista de espera, programas comunitários). Evitar linguagem que estigmatize. Não divulgar
motivos que exponham dados sensíveis além do necessário.
"""

