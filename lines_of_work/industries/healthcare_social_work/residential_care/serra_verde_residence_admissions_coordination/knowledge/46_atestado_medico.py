title = "Atestado Médico"

content = """
O atestado médico deve conter: avaliação das condições de saúde; necessidades de cuidado;
medicamentos em uso; restrições ou recomendações; contato do médico. Em algumas unidades,
o atestado é substituído ou complementado por avaliação da própria equipe.

**Validade:** Verificar se o atestado está atualizado (geralmente até 30 ou 60 dias).
Atestados antigos podem ser aceitos para triagem inicial, mas um atestado recente deve
anteceder a mudança. Em caso de dúvida sobre o conteúdo, encaminhar à enfermagem.
"""

