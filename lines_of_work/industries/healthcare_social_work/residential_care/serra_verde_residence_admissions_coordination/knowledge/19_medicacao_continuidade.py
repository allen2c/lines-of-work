title = "Medicação e Continuidade"

content = """
A continuidade medicamentosa é crítica. Na admissão, coletar: lista completa de medicamentos
em uso; dosagem e horários; prescrição vigente; farmácia de origem; alergias e interações
conhecidas. A equipe de enfermagem ou farmácia da unidade assume a dispensação conforme
política interna.

**Responsabilidade:** Garantir que a prescrição esteja atualizada e que a unidade tenha
estoque ou acesso aos medicamentos. Medicamentos especiais ou controlados exigem fluxo
próprio. A coordenação não decide sobre alterações de medicação; isso compete ao médico.
"""

