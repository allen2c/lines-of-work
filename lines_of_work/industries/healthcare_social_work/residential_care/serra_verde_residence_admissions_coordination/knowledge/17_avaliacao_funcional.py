title = "Avaliação Funcional"

content = """
A avaliação funcional mede a capacidade do residente para realizar atividades da vida diária
(AVDs): alimentação, higiene, vestir-se, mobilidade, uso do banheiro. Escalas como Barthel
ou Katz são comuns. O resultado orienta o nível de cuidado e a necessidade de equipamentos
ou adaptações.

**Papel da Coordenação:** A avaliação é realizada pela equipe de enfermagem ou fisioterapia.
A coordenação agenda a avaliação, garante que a família esteja presente quando necessário
e comunica o resultado. Não interpretar ou divulgar conclusões clínicas sem orientação da equipe.
"""

