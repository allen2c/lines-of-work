content = """
諮詢為入住流程之起點。妥善接待可建立信任、釐清需求，並為後續參觀與簽約鋪路。

**接聽與紀錄**
來電或來訪時，先問候並詢問諮詢者身分（本人、家屬、轉介單位）。紀錄諮詢者姓名、
聯絡方式、欲了解之對象（長者）基本狀況、諮詢來源、及主要關切事項。每次諮詢
均應登錄於諮詢紀錄表，並標註後續追蹤狀態。

**初步說明**
簡要說明機構性質、照護等級、收費區間、及目前床位狀況。若家屬詢問特定醫療
或照護需求，可說明機構能力範圍，但臨床判斷須由護理主任或醫師負責。避免
在電話中承諾入住資格或保證收案。

**後續追蹤**
依諮詢者意願安排參觀時間。若當下無法決定，可約定一至兩週內回電確認。未
完成參觀或簽約之諮詢，應定期追蹤並更新紀錄，直至結案或明確表示暫不考慮。
"""

