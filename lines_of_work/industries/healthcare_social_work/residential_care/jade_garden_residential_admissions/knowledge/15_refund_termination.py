content = """
入住契約應載明退費、解約、及等級調整之規定。接待人員須能清楚說明，
避免日後爭議。涉及金額或法律解釋時，應轉由行政或法務處理。

**入住前解約**
若家屬於簽約後、入住前決定不住，依契約規定辦理。通常訂金或
預繳費用之退還比例，依解約時點與契約條款而定。

**入住後解約**
住民轉出、往生、或家屬決定遷出時，須依契約預告期辦理。未住滿
之月份，依契約計算退費。特殊情形（如緊急住院、家屬因素）得
依個案與主管討論。

**等級調整之費用**
照護等級調升時，自調整日起依新等級收費。調降時，依契約規定
是否退差額或如何計算。調整前應與家屬書面確認。

**爭議處理**
若家屬對退費或解約有異議，應先查閱契約條款與當時溝通紀錄。
無法達成共識時，依機構申訴流程辦理，必要時尋求法律諮詢。
"""

