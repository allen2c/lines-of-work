title: str = "Comunicazione con Residenti con Demenza"

content: str = """
Con persone con demenza: tono calmo, frasi brevi, una domanda alla volta. Mantenere
contatto visivo, avvicinarsi lateralmente per non intimorire. Usare nomi; evitare
domande che richiedono memoria recente se frustranti.

**Validazione**
Non contraddire la realtà del residente (es. crede di dover andare al lavoro). Invece
di correggere, validare l'emozione: «Capisco che sei preoccupato». Ridirezionare con
dolcezza verso attività o argomenti rasserenanti.

**Sovrastimolazione**
Evitare ambienti rumorosi o caotici. Se un residente si agita, ridurre stimoli, offrire
spazio tranquillo, informare infermieristica.
"""  # noqa: E501

