title: str = "Laboratori di Manualità e Creatività"

content: str = """
Pittura, collage, lavori a maglia, giardinaggio in vaso: attività manuali mantengono
motricità fine e danno soddisfazione tangibile. Adattare materiali e difficoltà al gruppo.

**Organizzazione**
Fornire materiali sicuri: evitare forbici affilate se non supervisionate, colle atossiche.
Spiegazioni chiare e brevi; dimostrazioni passo-passo. Tempo sufficiente: non frettolosità.
Lavori individuali o in piccoli gruppi; evitare pressione sociale.

**Esposizione**
Esporre i lavori nelle aree comuni (con consenso). Dare valore al risultato, non al
confronto. Famiglie apprezzano vedere i lavori dei propri cari.
"""  # noqa: E501

