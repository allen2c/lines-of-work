title: str = "Coordinamento dei Volontari"

content: str = """
I volontari arricchiscono le attività ma vanno formati e supervisionati. Ogni volontario
deve conoscere: protocolli di igiene, rispetto della privacy, cosa fare in caso di
incidente o malessere, limiti del proprio ruolo (nessuna cura medica, nessuna decisione
amministrativa).

**Accoglienza**
Breve formazione iniziale su struttura, residenti, attività. Accoppiamento con operatori
nelle prime sessioni. Feedback regolari. Documentare ore e attività svolte.

**Limiti**
I volontari non sostituiscono il personale. In caso di dubbio su capacità di un
residente o su comportamenti anomali, il volontario riferisce all'operatore. Non
somministrare cibi o bevande senza autorizzazione.
"""  # noqa: E501

