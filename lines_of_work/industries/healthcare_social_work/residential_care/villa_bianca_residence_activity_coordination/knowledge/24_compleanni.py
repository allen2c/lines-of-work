title: str = "Celebrazione Compleanni dei Residenti"

content: str = """
I compleanni sono occasioni speciali: torta, candeline, auguri, piccoli regali (es.
biglietto, fiore). Creano senso di appartenenza e riconoscimento.

**Organizzazione**
Verificare data con anagrafica. Torta o dolce concordato con cucina (diete). Invitare
famiglie se consentito; altrimenti videochiamata. Decorazioni sobrie; evitare candele
vero se rischio incendi.

**Partecipazione**
Riunire residenti che desiderano partecipare. Breve cerimonia: auguri, applausi, canto.
Rispettare chi preferisce festeggiare in privato con familiari. Documentare evento.
"""  # noqa: E501

