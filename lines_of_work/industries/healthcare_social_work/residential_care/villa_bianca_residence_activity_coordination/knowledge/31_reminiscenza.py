title: str = "Terapia della Reminiscenza"

content: str = """
La reminiscenza è una tecnica che stimola il ricordo di esperienze passate attraverso
oggetti, foto, musica, domande guide. È particolarmente utile con persone con demenza:
attiva memoria episodica, rafforza identità, riduce ansia.

**Pratica**
Sessioni in piccolo gruppo o individuali. Stimoli: foto d'epoca, oggetti quotidiani,
canzoni, domande su lavoro, famiglia, viaggi. Ascolto attivo; validare emozioni.
Non correggere "errori" di memoria; il valore è nel racconto, non nella precisione.

**Materiali**
Raccogliere foto e oggetti donati dalle famiglie (con consenso). Creare "scatole
dei ricordi" tematiche. Documentare temi emersi per personalizzare future sessioni.
"""  # noqa: E501

