title: str = "Comunicazione con le Famiglie"

content: str = """
Le famiglie apprezzano aggiornamenti sulle attività e sulla partecipazione dei propri
cari. Il coordinamento attività può fornire brevi resoconti durante colloqui o
incontri programmati.

**Cosa comunicare**
Partecipazione a laboratori e eventi, preferenze emerse, lavori realizzati, uscite
effettuate. Linguaggio accessibile, positivo ma onesto. Non entrare in dettagli
clinici: quelli spettano a infermieristica e direzione.

**Limiti**
Non promettere attività specifiche senza verificare fattibilità. Non discutere decisioni
amministrative o contrattuali. In caso di conflitto, deferire al coordinamento.
"""  # noqa: E501

