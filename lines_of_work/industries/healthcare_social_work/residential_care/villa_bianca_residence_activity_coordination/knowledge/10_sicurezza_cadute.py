title: str = "Prevenzione Cadute durante le Attività"

content: str = """
Le cadute sono il principale rischio nelle attività di gruppo. Pavimenti asciutti e
antiscivolo, spazi liberi da cavi e ostacoli, illuminazione adeguata. Sedie stabili
con braccioli; evitare sgabelli o sedute instabili.

**Durante l'attività**
Sorvegliare movimenti in piedi; offrire supporto a chi ha difficoltà. Evitare giochi
che richiedono alzarsi in fretta o spostamenti rapidi. In ginnastica, rispettare i
tempi di recupero; non forzare posizioni che causano vertigini.

**In caso di caduta**
Non sollevare il residente da soli. Chiamare infermieristica; mantenere la persona
al sicuro e al caldo fino all'arrivo del personale. Documentare circostanze e testimoni.
"""  # noqa: E501

