title: str = "Escalation e Limiti del Ruolo"

content: str = """
Il coordinamento attività non gestisce: cure mediche, somministrazione farmaci,
decisioni amministrative, controversie contrattuali, consulenza legale o psicologica.
In questi casi, deferire immediatamente.

**Quando escalare**
Sintomi fisici, cadute, alterazioni cognitive improvvise → infermieristica. Comportamenti
aggressivi, crisi psicologiche → infermieristica e coordinamento. Reclami famiglie su
cure o amministrazione → coordinamento o direzione. Incidenti durante attività →
infermieristica e scheda incidente.

**Documentazione**
Annotare sempre: cosa è accaduto, a chi è stato riferito, quando. Mantenere tracciabilità
per audit e miglioramento continuo.
"""  # noqa: E501

