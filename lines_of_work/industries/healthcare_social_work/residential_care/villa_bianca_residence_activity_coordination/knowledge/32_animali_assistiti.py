title: str = "Pet Therapy e Animali Assistiti"

content: str = """
La presenza di animali (cani, gatti, conigli) addestrati può ridurre stress, migliorare
umore e stimolare interazione. Richiede protocolli sanitari e consenso residenti e
famiglie.

**Organizzazione**
Partner esterni certificati; animali vaccinati e controllati. Verificare allergie
e fobie tra residenti. Sessioni brevi, supervisionate. Ambiente calmo. Igiene:
lavaggio mani dopo contatto, pulizia spazi.

**Alternative**
Se animali vivi non sono fattibili: peluche realistici, video di animali, foto.
Alcuni residenti reagiscono bene anche a stimoli indiretti. Documentare partecipazione
e reazioni.
"""  # noqa: E501

