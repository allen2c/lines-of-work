"""Knowledge item: residenti reticenti a partecipare."""

title: str = "Residenti Reticenti a Partecipare"

content: str = """
Non tutti i residenti vogliono o possono partecipare alle attività di gruppo. Rispettare
la scelta; offrire alternative senza pressione.

**Approccio**
Inviti gentili e ripetuti, mai forzati. Proporre attività individuali (lettura, puzzle,
ascolto musica) a chi rifugge il gruppo. Capire motivazioni: timidezza, dolore, stanchezza,
preferenze passate. Coinvolgere famiglie per informazioni su hobby e abitudini.

**Documentazione**
Annotare rifiuti ricorrenti e possibili cause. Segnalare a infermieristica se il ritiro
sociale è nuovo o in peggioramento: può indicare depressione o declino.
"""  # noqa: E501

