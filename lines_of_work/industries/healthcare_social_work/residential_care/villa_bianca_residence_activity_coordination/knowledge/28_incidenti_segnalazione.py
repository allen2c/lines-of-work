title: str = "Gestione Incidenti e Segnalazione"

content: str = """
Qualsiasi incidente durante le attività (caduta, malessere, ferita, conflitto) va
documentato e segnalato secondo protocolli. Non sottovalutare; non nascondere.

**Procedure**
Assistenza immediata al residente; chiamata infermieristica se necessario. Compilare
scheda incidente: data, ora, luogo, circostanze, testimoni, azioni intraprese.
Comunicare al coordinamento e, se richiesto, alla famiglia. Conservare documentazione.

**Prevenzione**
Analizzare incidenti ricorrenti per individuare cause (spazi, attività, orari) e
adottare correttivi. Formazione continua su sicurezza.
"""  # noqa: E501

