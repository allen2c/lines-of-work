title = "Grievance Handling Procedures"

content = """
Residents and families may raise grievances about care, staff, or facility
operations. Care coordination receives, documents, and routes grievances
according to policy.

**Receiving a Grievance**
Listen without defensiveness. Document the date, complainant, concern, and
requested resolution. Acknowledge receipt and provide a timeline for response.

**Investigation**
Route to the appropriate department (nursing, dietary, administration).
Ensure the investigation is timely and thorough. Maintain confidentiality.

**Response**
Provide a written response within the policy timeframe. Explain findings
and any actions taken. Offer to discuss further if needed.

**Escalation**
Serious allegations, regulatory concerns, or unresolved grievances go to
the director or quality committee. Document all steps for compliance.
"""

