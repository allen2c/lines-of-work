title = "Continence Care Protocols"

content = """
Continence care supports residents with bladder or bowel incontinence while
preserving dignity and preventing complications. Care coordination ensures
protocols are followed and documented.

**Assessment**
Document continence status in the care plan. Identify causes (functional,
cognitive, medical) and tailor interventions. Reassess periodically.

**Interventions**
Scheduled toileting, prompted voiding, and use of absorbent products as
appropriate. Ensure skin care to prevent breakdown. Coordinate with
nursing for catheter care if applicable.

**Documentation**
Record episodes, interventions, and outcomes. Note changes that may
indicate infection or other complications. Report to the physician when
indicated.

**Dignity**
Approach continence care with respect and privacy. Avoid language that
shames. Support resident preferences when possible.
"""

