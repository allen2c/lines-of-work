content = """
Mobility assistance helps residents move safely—transferring, walking, or
using wheelchairs. Proper technique prevents falls and staff injury.

**Assessment**
Document mobility level and required assistance. Identify equipment needs
(walker, wheelchair, lift). Update when condition changes.

**Transfer Techniques**
Use proper body mechanics and assistive devices. Two-person assists when
required. Never rush or leave a resident unsupported during a transfer.

**Walking Assistance**
Provide appropriate support (gait belt, arm, walker). Clear the path.
Monitor for fatigue or instability.

**Equipment**
Ensure wheelchairs, walkers, and lifts are in good condition. Report
malfunctions. Coordinate with therapy for equipment recommendations.
"""

