title = "Cultural and Religious Accommodation"

content = """
Residents come from diverse backgrounds. Care coordination includes accommodating
cultural and religious practices within facility capabilities and regulations.

**Dietary Accommodation**
Halal, kosher, vegetarian, or other dietary requirements must be communicated to
the kitchen. Coordinate with the dietary team and document preferences in the care plan.

**Religious Practices**
Support access to clergy, prayer times, religious texts, and observance of holy days.
Facilitate visits from religious representatives when requested.

**Cultural Sensitivity**
Respect preferences around modesty, gender of caregivers, family involvement in
care decisions, and communication styles. Document special requests in the care plan.

**Limits**
Accommodations must not compromise safety or regulatory requirements. Escalate
requests that conflict with facility policy or resident safety.
"""

