title = "Night Shift Handoff Procedures"

content = """
The transition from day to night shift requires a structured handoff so
the night team has full context. Care coordination supports this process.

**Handoff Content**
Summarize each resident's status: changes in condition, new orders,
incidents, family concerns, and tasks pending. Highlight residents
requiring extra attention overnight.

**Format**
Use a standardized handoff form or electronic system. Verbal handoff
supplements written information. Allow time for questions.

**Night-Specific Concerns**
Note residents with sleep disturbances, nocturnal confusion, or
scheduled night care. Ensure emergency contacts and escalation
paths are clear.

**Documentation**
Document that handoff occurred. Night staff should document
significant events for the next shift.
"""

