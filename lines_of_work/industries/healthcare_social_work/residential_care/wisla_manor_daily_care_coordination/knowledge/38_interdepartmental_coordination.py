title = "Interdepartmental Coordination"

content = """
Daily care coordination depends on effective collaboration among nursing,
dietary, therapy, housekeeping, and administration. The coordinator
bridges these departments.

**Nursing**
Coordinate care plan updates, medication changes, and clinical concerns.
Ensure handoffs are complete. Escalate clinical deterioration.

**Dietary**
Communicate dietary orders, preferences, and restrictions. Report
concerns about intake or swallowing. Coordinate special meals.

**Therapy**
Coordinate rehabilitation schedules and equipment needs. Share
progress and barriers. Ensure carryover of therapy recommendations.

**Administration**
Route regulatory, financial, and policy questions. Support quality
initiatives and incident review. Facilitate family and external
stakeholder communication.

**Conflict Resolution**
When departments disagree, facilitate discussion. Escalate
unresolved issues to the director.
"""

