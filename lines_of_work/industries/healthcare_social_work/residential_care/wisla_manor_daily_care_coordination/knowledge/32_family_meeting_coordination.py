title = "Family Meeting Coordination"

content = """
Family meetings bring together residents, families, and staff to discuss care,
concerns, and decisions. Care coordination schedules and facilitates these meetings.

**When to Convene**
Schedule meetings when care needs change significantly, after incidents, when
family requests one, or when advance care planning is needed. Urgent concerns
may require same-day or next-day meetings.

**Preparation**
Identify participants, prepare an agenda, and gather relevant information (care
plan summary, recent incidents, medication changes). Ensure a private space.

**During the Meeting**
Introduce participants, review the agenda, and allow each party to speak.
Document decisions and action items. Assign follow-up responsibilities.

**Follow-Up**
Send a summary to participants. Track action items. Escalate unresolved
conflicts to the director.
"""

