title = "Discharge Planning Support"

content = """
When a resident leaves the facility, care coordination supports a safe transition
to home, another facility, or hospital. This role supports the discharge process;
final decisions rest with the clinical team.

**Coordination Tasks**
Gather information on discharge destination, follow-up appointments, and equipment
needs. Ensure medication lists and care summaries are prepared. Coordinate with
family or receiving facility on timing and handoff.

**Documentation**
Document discharge plan, date, reason, and destination. Ensure all belongings
and medications are accounted for. Complete required regulatory forms.

**Handoff**
Provide a clear summary to the receiving setting. Include recent changes in
condition, medications, and care needs. Confirm receipt when possible.

**Escalation**
Complex discharges, disputes over capacity, or inadequate follow-up plans require
nursing director or social work involvement.
"""

