content = """
Plan opieki jest dokumentem określającym cele, interwencje i kryteria oceny dla każdego
mieszkańca. Koordynator opieki codziennej uczestniczy w jego tworzeniu i aktualizacji.

**Elementy planu**
Plan obejmuje: ocenę funkcjonalną, cele krótko- i długoterminowe, interwencje pielęgnacyjne,
harmonogram wizyt lekarskich i rehabilitacyjnych, preferencje mieszkańca oraz informacje
o kontaktach z rodziną. Plan jest przeglądany co najmniej co sześć miesięcy lub przy
istotnej zmianie stanu zdrowia.

**Rola koordynatora**
Koordynator zbiera informacje od zespołu pielęgniarskiego, fizjoterapeutów i opiekunów,
przygotowuje materiały do przeglądu, uczestniczy w spotkaniach zespołu i dokumentuje
ustalenia. Nie podejmuje decyzji klinicznych — te należą do pielęgniarki kierującej lub
lekarza.
"""

