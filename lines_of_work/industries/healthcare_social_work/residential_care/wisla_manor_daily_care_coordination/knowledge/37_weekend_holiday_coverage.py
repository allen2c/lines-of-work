title = "Weekend and Holiday Coverage"

content = """
Care coordination continues on weekends and holidays, often with reduced
administrative staff. Priorities shift to essential operations and
escalation readiness.

**Staffing**
Identify the on-call coordinator or designated lead for weekends and
holidays. Ensure coverage for urgent issues. Document who to contact.

**Prioritized Tasks**
Focus on resident safety, incident response, family emergencies, and
critical communication. Defer non-urgent administrative tasks to the
next business day.

**Physician and Pharmacy Access**
Know which physicians are on call. Ensure pharmacy access for
urgent medication needs. Document after-hours contacts.

**Family Communication**
Inform families of holiday hours and how to reach staff for
urgent matters. Set expectations for response times.
"""

