title = "Procedury przekazania zmianowego"

content = """
Przekazanie zmianowe zapewnia ciągłość opieki między dyżurami. Koordynator opieki
codziennej nadzoruje jakość przekazań i uzupełnia dokumentację.

**Zawartość przekazania**
Przekazanie obejmuje: stan zdrowia mieszkańców wymagających uwagi, zdarzenia z poprzedniej
zmiany, zalecenia lekarskie z dnia, zmiany w planach opieki, sprawy wymagające
kontynuacji oraz informacje o wizytach rodzin. Krytyczne zdarzenia są przekazywane
ustnie i odnotowane w dzienniku.

**Format i czas**
Przekazanie odbywa się na początku każdej zmiany, w wyznaczonym miejscu. Czas trwania
nie powinien przekraczać 20–30 minut. Koordynator upewnia się, że wszystkie istotne
informacje zostały przekazane i że nowa zmiana nie ma pytań.
"""

