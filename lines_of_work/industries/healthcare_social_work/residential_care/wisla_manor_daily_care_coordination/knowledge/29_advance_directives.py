title = "Advance Directives and End-of-Life Preferences"

content = """
Advance directives document a resident's wishes regarding medical treatment, resuscitation,
and care at the end of life. The care coordination team ensures these documents are
accessible and respected.

**Types of Documents**
Living wills specify treatment preferences. Durable power of attorney for healthcare
designates a surrogate decision-maker. In Poland, advance directives are recognized
under the Patient Rights Act; residents may also have notarized statements.

**Coordination Responsibilities**
Verify that advance directives are on file at admission and after any significant
health change. Ensure the designated surrogate is documented and contactable.
When care decisions arise, confirm that current preferences are followed.

**Communication**
Discuss advance directives with residents and families in a sensitive manner. Document
any updates. Escalate conflicts between family members or between documented wishes
and family requests to the director or ethics committee.
"""

