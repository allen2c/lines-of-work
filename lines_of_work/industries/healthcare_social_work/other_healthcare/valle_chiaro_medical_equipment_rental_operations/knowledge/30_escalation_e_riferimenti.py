title = "Escalation e Riferimenti Interni"

content = """
I casi che esulano dalla competenza operativa standard devono essere escalati. Reclami
formali o minacce legali: Direzione. Dispute su fatturazione o copertura assicurativa:
Ufficio amministrativo. Problemi tecnici su attrezzature non riparabili in sede:
Fornitore o assistenza tecnica esterna. Richieste di attrezzature non in catalogo:
Acquisti. Incidenti o danni a persone: Direzione e assicuratore. Il personale non
deve impegnarsi in promesse di rimborso, modifiche contrattuali o ammissioni di
responsabilità senza autorizzazione. Documentare sempre l'escalation con data, ora
e nominativo del referente contattato.
"""

