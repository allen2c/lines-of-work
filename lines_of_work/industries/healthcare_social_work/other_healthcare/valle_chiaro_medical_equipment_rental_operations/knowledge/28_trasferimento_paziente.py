title = "Trasferimenti e Cambi di Indirizzo"

content = """
Se un paziente si trasferisce durante il noleggio (cambio di residenza, ricovero
temporaneo, trasferimento in struttura), Valle Chiaro deve essere informata con
anticipo. Il noleggio può essere trasferito al nuovo indirizzo previa verifica di
fattibilità logistica e, se necessario, aggiornamento della prescrizione. Il costo
del trasferimento può essere addebitato secondo il listino. In caso di ricovero
prolungato, il noleggio può essere sospeso e ripreso alla dimissione, evitando
il pagamento del periodo di inutilizzo. Documentare sempre il motivo e la data
di sospensione.
"""

