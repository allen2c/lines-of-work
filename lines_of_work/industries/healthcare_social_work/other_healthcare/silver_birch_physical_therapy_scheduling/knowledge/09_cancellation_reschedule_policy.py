title = "Cancellation and Reschedule Policy"

content = """
A clear cancellation and reschedule policy protects clinic capacity and sets
patient expectations. Communicate the policy at scheduling and in confirmation.

**Advance Notice:** Require at least 24 hours' notice for cancellations or
reschedules when possible. Late cancellations (within 24 hours) may be subject
to a fee or marked as a no-show depending on clinic policy and payer rules.

**Patient-Initiated Changes:** When a patient requests a change, offer alternative
slots within the same therapist's schedule when continuity matters. If no slot
exists, offer another therapist or add to the wait list for preferred times.

**Clinic-Initiated Changes:** When the clinic must cancel (therapist illness,
equipment issue), contact the patient as soon as possible. Offer the next available
slot, preferably with the same therapist. Apologize for the inconvenience.

**Documentation:** Log all cancellations and reschedules with reason and date.
Use this data to identify patterns (e.g., frequent no-shows, peak cancellation
times) for process improvement.
"""  # noqa: E501

