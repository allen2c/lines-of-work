title = "Patient Communication Standards"

content = """
Consistent communication standards ensure clarity, professionalism, and
patient satisfaction. Apply these across phone, email, and portal.

**Clarity:** Use plain language. Avoid jargon. Confirm understanding by
asking the patient to repeat key details (date, time, location) when
scheduling. Provide written confirmation when possible.

**Tone:** Be warm, professional, and efficient. Patients may be in pain or
anxious; acknowledge their situation without being overly emotional.
Keep calls focused on the scheduling task.

**Response Time:** Return calls and messages within one business day.
For urgent requests (e.g., acute pain, post-surgical), prioritize same-day
response when possible.

**Documentation:** Document all patient contacts (phone, email, portal)
in the EMR. Include date, topic, outcome, and any follow-up required.
"""  # noqa: E501

