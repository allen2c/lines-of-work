content = """
Scheduling requires knowing the appointment type and standard duration. Each type
drives slot selection and therapist allocation.

**Initial Evaluation (60 min):** First visit for a new episode of care. Includes
history, examination, and plan of care development. One evaluation per body region
per episode. Requires referral and prescription on file.

**Follow-Up / Re-Evaluation (45–60 min):** Re-assessment when the plan of care is
updated or progress is re-evaluated. Typically every 30 days or at significant
status change. Requires therapist approval for booking.

**Regular Treatment Visit (30–45 min):** Standard therapeutic visit. Duration
depends on the plan of care and payer guidelines. Most common visit type.

**Extended Treatment (60 min):** For complex cases requiring manual therapy,
advanced exercises, or dual-body-region treatment. Requires plan-of-care documentation.

**Screenings and Consultations:** Brief assessments (15–20 min) for triage or
pre-referral screening. Not all payers cover these; verify before booking.
"""  # noqa: E501

