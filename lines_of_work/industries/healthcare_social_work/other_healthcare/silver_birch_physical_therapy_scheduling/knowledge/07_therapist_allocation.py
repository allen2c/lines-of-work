title = "Therapist Allocation and Matching"

content = """
Allocating patients to the right therapist ensures quality care and continuity.
Consider specialty, acuity, and patient preference when assigning.

**Specialty Matching:** Therapists have areas of focus (spine, sports, pelvic health,
etc.). Match new patients to a therapist whose specialty aligns with the diagnosis
or referral. For general orthopaedic cases, any PT may be appropriate.

**Continuity of Care:** Once a patient begins treatment with a therapist, maintain
that assignment unless the plan of care requires a handoff, the patient requests
a change, or capacity constraints make it unavoidable. Continuity improves outcomes
and patient satisfaction.

**Language and Accessibility:** When a patient requires a therapist who speaks their
language or has specific accessibility accommodations, prioritize that in allocation.
Document the need in the scheduling system.

**Load Balancing:** Distribute new patients across therapists to avoid overload.
Consider each therapist's schedule density, vacation, and caseload mix when offering
appointment options.
"""  # noqa: E501

