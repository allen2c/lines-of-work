title = "Multilingual Patient Handling"

content = """
Patients who prefer a language other than English need accommodation to
access care and communicate effectively. Match them to appropriate
resources.

**Language Preference:** Ask the patient's preferred language for
scheduling and clinical communication. Document in the patient record.
Match to a therapist who speaks that language when available.

**When No Match Exists:** If no therapist speaks the patient's language,
inform the patient and offer alternatives: a telephonic interpreter,
video interpretation, or referral to a clinic with in-language staff.
Do not schedule without a plan for interpretation.

**Scheduling Communication:** Use interpretation services when scheduling
by phone if the patient does not speak English. Provide written materials
in the patient's language when available (e.g., intake forms, visit
instructions).
"""  # noqa: E501

