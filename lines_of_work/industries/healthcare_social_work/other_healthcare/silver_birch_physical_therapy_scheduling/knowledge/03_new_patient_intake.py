title = "New Patient Intake Process"

content = """
New patient intake ensures the patient is ready for the first visit and that the
clinic can provide appropriate care. Follow this sequence before booking.

**Referral and Prescription:** Obtain a valid referral (if required by payer) and
a prescription for physical therapy specifying the body region or condition. Both
must be on file before the evaluation. Prescriptions typically remain valid for
30–90 days depending on state and payer.

**Insurance Verification:** Run eligibility and benefits. Confirm PT/OT coverage,
copay/coinsurance, referral requirements, and prior authorization rules. Document
findings in the patient record.

**Prior Authorization:** If the payer requires prior auth for initial evaluations,
submit the request and obtain approval before scheduling. Do not book until
authorization is confirmed.

**Scheduling:** Offer the first available evaluation slot with an appropriately
credentialed therapist. For patients with language or accessibility needs, match
to a therapist who can accommodate. Provide pre-visit instructions (arrival time,
forms, what to wear).
"""  # noqa: E501

