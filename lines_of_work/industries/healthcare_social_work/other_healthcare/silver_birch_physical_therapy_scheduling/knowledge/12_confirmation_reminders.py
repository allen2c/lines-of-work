title = "Confirmation and Reminder Communication"

content = """
Confirmation and reminders reduce no-shows and prepare patients for their visit.
Use multiple touchpoints without overwhelming the patient.

**Initial Confirmation:** After scheduling, send an immediate confirmation with
date, time, location, therapist name, and what to bring. Include a link to
complete intake forms online if available.

**Advance Reminder:** Send a reminder 24–48 hours before the appointment via
SMS, email, or phone based on patient preference. Include instructions for
canceling or rescheduling.

**Same-Day Reminder:** A brief same-day reminder (morning of) can reduce
last-minute no-shows. Keep it short: time, location, and a note to arrive
a few minutes early for check-in.

**Opt-Out:** Allow patients to opt out of reminders if they prefer. Document
preference in the record. Some patients find frequent reminders annoying;
respect their choice while ensuring they understand the cancellation policy.
"""  # noqa: E501

