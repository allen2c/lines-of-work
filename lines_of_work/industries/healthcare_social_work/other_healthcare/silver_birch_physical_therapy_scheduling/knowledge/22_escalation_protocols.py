title = "Escalation Protocols"

content = """
Escalation ensures issues beyond the scheduler's scope are resolved by
the right person. Escalate promptly; do not delay or attempt to resolve
out of scope.

**Clinical Questions:** Direct to the treating therapist or clinic director.
Examples: interpretation of referral, appropriateness of visit frequency,
or change in patient condition affecting scheduling.

**Authorization Denials:** Notify clinic director and billing. Do not
promise visits that are not authorized. Billing may pursue an appeal;
scheduling follows the outcome.

**Patient Complaints:** Log the complaint and notify the clinic director
within one business day. Do not argue or make promises about resolution.
The director will follow up with the patient.

**Capacity or Access Crises:** When backlog is severe, demand spikes, or
a therapist is unexpectedly out, escalate to the clinic director for
decisions on extended hours, overflow, or patient communication.
"""  # noqa: E501

