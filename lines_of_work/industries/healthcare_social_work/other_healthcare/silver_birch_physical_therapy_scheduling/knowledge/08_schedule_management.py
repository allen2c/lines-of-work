title = "Schedule Management"

content = """
Effective schedule management maximizes capacity while respecting therapist
availability, visit duration, and clinical needs.

**Slot Types:** Configure slots in the scheduling system by appointment type and
duration. Initial evaluations use 60-minute slots; follow-ups use 30- or 45-minute
slots. Avoid booking a 30-minute slot for a 60-minute appointment.

**Therapist Calendars:** Maintain accurate therapist availability. Block time for
documentation, meetings, and lunch. Update the system when a therapist is out sick
or on vacation. Sync with the EMR when possible.

**Buffer Time:** Allow buffer between visits when therapists need to document,
clean equipment, or prepare for the next patient. Back-to-back bookings without
buffer lead to delays and documentation backlog.

**Double-Booking Policy:** Clarify whether therapists may see two patients in
overlapping slots (e.g., one in exercises, one in manual therapy). If allowed,
document the policy and ensure the system supports it correctly.
"""  # noqa: E501

