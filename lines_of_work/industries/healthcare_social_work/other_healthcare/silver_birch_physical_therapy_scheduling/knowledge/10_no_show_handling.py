title = "No-Show Handling"

content = """
No-shows reduce clinic capacity and disrupt schedules. A consistent process
minimizes impact and encourages future attendance.

**Definition:** A no-show is when a patient does not arrive for a scheduled
appointment and does not cancel in advance. Distinguish from late cancellations
(within 24 hours), which may have a different policy.

**Immediate Actions:** Mark the slot as no-show in the scheduling system. Release
the slot for wait-list or same-day add-ons if applicable. Document the no-show
in the patient record.

**Patient Contact:** Attempt to contact the patient (phone, portal message) to
understand the reason and reschedule. Avoid punitive language; focus on
re-engagement. Some patients have transportation, health, or work barriers.

**No-Show Policy:** Apply the clinic's no-show policy consistently. Options
include a fee, discharge after a certain number, or requiring a deposit for
future visits. Ensure the policy is documented and communicated in writing.
"""  # noqa: E501

