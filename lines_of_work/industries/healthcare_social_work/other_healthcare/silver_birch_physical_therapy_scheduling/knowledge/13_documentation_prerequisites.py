title = "Documentation Prerequisites Before Visit"

content = """
Documentation must be complete before the visit to avoid claim denials and
turned-away patients. Verify prerequisites at scheduling and again before
the appointment date.

**Required Documents:** Referral (if required by payer), prescription, prior
authorization (if required), and completed intake forms. Some payers require
a treatment plan or progress note for continued care.

**Timing Checks:** Ensure referral and prescription are current (within
validity period). Ensure prior authorization has not expired and visit count
has not been exceeded. If any document will expire before the visit, obtain
renewals in advance.

**Pre-Visit Audit:** Run a report or manual check 24–48 hours before the visit.
Contact the patient or referring provider if documents are missing. If
documentation cannot be obtained in time, reschedule and explain the reason
to the patient.
"""  # noqa: E501

