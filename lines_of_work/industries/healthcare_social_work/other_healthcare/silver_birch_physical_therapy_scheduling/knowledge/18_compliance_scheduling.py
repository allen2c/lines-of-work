title = "Compliance and Scheduling"

content = """
Scheduling must align with regulatory and payer requirements. Non-compliant
scheduling leads to audit findings, recoupments, and patient liability.

**Visit Frequency:** Do not schedule more frequently than the plan of care
or prior authorization allows. Document the basis for frequency (POC, auth)
in the record.

**Modifier Usage:** Some visit types require modifiers (e.g., -59 for distinct
procedure). Scheduling staff do not assign modifiers, but they should ensure
the visit type matches what was authorized and what the therapist will perform.

**Documentation Trail:** Maintain a clear trail from scheduled visit to
referral, prescription, authorization, and POC. Auditors will request
this. Incomplete documentation at scheduling is a compliance risk.

**Escalation:** When uncertain about compliance, consult the clinic director
or billing before scheduling. Do not assume; verify.
"""  # noqa: E501

