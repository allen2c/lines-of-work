title = "Insurance Verification"

content = """
Insurance verification confirms coverage and requirements before scheduling. Incomplete
verification leads to surprises at the front desk and potential claim denials.

**Eligibility Check:** Confirm the patient is eligible on the date of service.
Verify the plan covers physical therapy and occupational therapy. Note any
exclusions or limitations.

**Benefits:** Document copay, coinsurance, deductible status, and visit limits.
Communicate expected out-of-pocket costs to the patient at scheduling. Some plans
have annual visit caps; track usage against the cap.

**Referral and PA Rules:** Determine whether a referral and prior authorization
are required. Document the source (benefits document, payer phone call, portal)
and the specific requirement.

**Secondary Insurance:** When the patient has secondary coverage, verify both
plans and document coordination-of-benefits order. Schedule according to primary
plan rules; secondary may be billed after primary adjudication.
"""  # noqa: E501

