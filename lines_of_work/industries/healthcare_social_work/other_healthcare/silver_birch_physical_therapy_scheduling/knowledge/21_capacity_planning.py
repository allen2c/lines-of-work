title = "Capacity Planning"

content = """
Capacity planning aligns demand with available slots and therapist hours.
Proactive planning reduces backlog and improves access.

**Demand Forecasting:** Use historical data (new referrals, follow-up
volume, seasonality) to predict demand. Account for therapist vacations,
holidays, and planned absences.

**Slot Allocation:** Reserve evaluation slots for new patients to maintain
access. Balance with follow-up capacity. Too many evals without follow-up
slots creates bottleneck; too few evals underutilizes therapists.

**Backlog Management:** When demand exceeds capacity, quantify the backlog
(weeks to next available, number of patients waiting). Escalate to clinic
director for solutions: extended hours, temporary staff, or wait-list
communication.

**Metrics:** Track key metrics: no-show rate, cancellation rate, average
days to first appointment, and utilization. Use for continuous improvement.
"""  # noqa: E501

