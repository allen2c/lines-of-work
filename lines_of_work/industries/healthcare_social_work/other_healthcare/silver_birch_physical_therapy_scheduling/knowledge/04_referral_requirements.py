content = """
Referral and prescription requirements vary by payer, state, and visit type. Incorrect
handling leads to denied claims and patient dissatisfaction.

**Direct Access:** Some states permit self-referral for a limited number of visits
or evaluation only. Verify state law and payer policy. When direct access applies,
document the legal basis and any visit caps.

**Referral vs. Prescription:** A referral authorizes evaluation; a prescription
specifies the treatment. Some payers require both. The prescription must include
the body region or diagnosis and be signed by a qualified provider. Prescriptions
are typically valid for 30–90 days.

**Timing:** Ensure the referral and prescription are dated within the payer's
acceptance window. Stale prescriptions may be rejected. Obtain updated documents
before the first visit and when the plan of care is renewed.

**Documentation:** Store all referrals and prescriptions in the EMR with clear
dates and provider information. Link them to the scheduled visit for audit readiness.
"""  # noqa: E501

