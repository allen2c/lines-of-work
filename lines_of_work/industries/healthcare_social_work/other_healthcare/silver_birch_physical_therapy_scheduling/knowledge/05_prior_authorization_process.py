title = "Prior Authorization Process"

content = """
Prior authorization (PA) is required by many payers before PT/OT visits. Scheduling
a visit without required PA can result in claim denial and patient liability.

**When PA Is Required:** Common triggers include visit counts (e.g., first 12 visits),
specific procedures, or high-cost services. Check the patient's benefits or call
the payer to confirm. Document the PA requirement and approval in the record.

**Submission:** Submit PA requests with diagnosis codes, procedure codes, and
clinical justification per payer requirements. Use electronic portals when available.
Note the submission date and reference number.

**Approval and Limits:** Upon approval, record the authorization number, effective
dates, visit limit, and any procedure-specific caps. Schedule only within these
limits. When approaching the limit, coordinate with the therapist for a renewal
or new PA before the next visit.

**Denials:** If PA is denied, do not schedule visits that would be uncovered.
Notify the therapist and clinic director. Appeal processes vary by payer; involve
billing and clinical leadership as needed.
"""  # noqa: E501

