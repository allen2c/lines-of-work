title = "Coordinación multi-sillón y disponibilidad"

content = """
La clínica opera con varios sillones dentales atendidos por dentistas e higienistas. Cada
profesional tiene su propia agenda; el sistema de citas debe respetar la capacidad real
por sillón y por tipo de procedimiento.

**Asignación por procedimiento:** Las limpiezas y revisiones rutinarias pueden realizarlas
higienistas; los tratamientos restauradores, extracciones y procedimientos complejos requieren
dentista. No asignar cita de higienista para procedimiento que exige dentista.

**Tiempos estándar:** Cada tipo de cita tiene un bloque de tiempo asignado. No sobrecargar
la agenda; dejar margen entre citas para imprevistos, desinfección y documentación.

**Sustituciones:** Si un profesional falta, la coordinación debe redistribuir las citas
entre los disponibles o contactar a los pacientes para reprogramar. Priorizar urgencias
y citas de seguimiento post-quirúrgico.
"""

