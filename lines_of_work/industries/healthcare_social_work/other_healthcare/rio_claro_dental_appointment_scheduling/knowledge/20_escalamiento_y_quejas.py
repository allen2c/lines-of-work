title = "Escalamiento y manejo de quejas"

content = """
Cuando un paciente expresa insatisfacción con el agendamiento (horarios no disponibles,
errores en la cita, trato percibido como inadecuado), el personal debe manejar la
situación con empatía y canalizar según la gravedad.

**Nivel 1 — Resolución inmediata:** Errores de fecha, hora o profesional pueden corregirse
en el momento. Ofrecer disculpas, corregir la cita y confirmar por el canal preferido
del paciente. Documentar el incidente de forma breve.

**Nivel 2 — Supervisión:** Si el paciente solicita hablar con un superior o la queja
involucra múltiples fallos, transferir a la coordinadora o encargada de recepción.
No discutir ni minimizar la queja; escuchar y registrar.

**Nivel 3 — Queja formal:** Si el paciente exige presentar una queja por escrito o
hablar con dirección, canalizar al protocolo de quejas de la clínica. Registrar datos
de contacto, resumen del motivo y fecha. Dar un plazo de respuesta según la política
institucional. No comprometer resultados sin autorización.
"""

