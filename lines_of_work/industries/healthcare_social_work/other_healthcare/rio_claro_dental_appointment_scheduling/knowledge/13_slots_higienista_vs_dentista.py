title = "Slots de higienista vs dentista"

content = """
La clínica distingue entre citas con higienista dental y citas con dentista. La asignación
correcta evita retrasos y garantiza que el paciente reciba el servicio adecuado.

**Higienista:** Profilaxis (limpieza), aplicación de flúor, educación en higiene oral,
radiografías de rutina, y evaluación inicial para derivar al dentista si se detectan
problemas. Duración típica: 45–60 minutos.

**Dentista:** Examen completo, obturaciones, extracciones, endodoncias, coronas, prótesis,
tratamiento de encías avanzado, y cualquier procedimiento que requiera diagnóstico o
intervención quirúrgica. Duración variable según complejidad.

**Flujo combinado:** En algunas clínicas, el paciente puede tener limpieza con higienista
seguida de revisión con dentista en la misma visita. En ese caso, reservar bloques
consecutivos y coordinar la secuencia con ambos profesionales.
"""

