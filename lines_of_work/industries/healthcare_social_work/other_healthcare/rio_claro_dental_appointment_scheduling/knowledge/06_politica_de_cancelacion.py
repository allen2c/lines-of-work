title = "Política de Cancelación y Reprogramación"

content = """
Una política clara de cancelación protege la disponibilidad de la clínica y permite reasignar
horarios a pacientes en lista de espera.

**Cancelación con anticipación:** Se solicita aviso con al menos 24 horas de anticipación.
Esto permite ofrecer el slot a otro paciente. Cancelaciones repetidas sin aviso pueden
resultar en depósito para futuras citas.

**Reprogramación:** El paciente puede solicitar cambio de fecha u hora. Se busca el primer
horario disponible que se ajuste a sus preferencias. No se penaliza la reprogramación
cuando se avisa con tiempo suficiente.

**No presentación (no-show):** Si el paciente no asiste ni avisa, se registra como no-show.
Después de dos o tres no-shows consecutivos, la clínica puede requerir depósito o pago
anticipado para nuevas citas.

**Cancelación por la clínica:** Si la clínica debe cancelar (emergencia, equipo dañado),
notificar al paciente lo antes posible y ofrecer reprogramación prioritaria.
"""

