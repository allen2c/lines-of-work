content = """
Los recordatorios reducen no-shows y permiten al paciente confirmar o reprogramar con tiempo.
La clínica utiliza SMS, WhatsApp o llamada según preferencia del paciente.

**Momento del envío:** 24 a 48 horas antes de la cita. Un recordatorio demasiado temprano
se olvida; uno muy tarde no da tiempo para reprogramar.

**Contenido del mensaje:** Incluir nombre del paciente, fecha, hora, dirección de la clínica,
nombre del dentista y tipo de procedimiento. Incluir instrucciones para confirmar, cancelar
o reprogramar (por ejemplo, responder "C" para confirmar).

**Confirmación activa:** Cuando el paciente confirma, registrar en el sistema. Si no responde,
considerar una llamada de seguimiento el día anterior, especialmente para citas de mayor
duración o primera vez.

**Idioma:** Enviar el recordatorio en el idioma preferido del paciente cuando la clínica
ofrezca servicio bilingüe.
"""

