title = "Urgencia Dental vs Cita de Rutina"

content = """
Distinguir entre urgencias y citas de rutina permite priorizar correctamente y evitar
sobrecarga del sistema con casos que no requieren atención inmediata.

**Urgencias:** Dolor agudo, traumatismo, sangrado que no cesa, infección con hinchazón,
pérdida de un diente por golpe. Estas situaciones requieren evaluación el mismo día.
La clínica reserva slots de urgencia o inserta al paciente en huecos disponibles.

**Semi-urgente:** Dolor moderado, fractura de diente sin dolor intenso, prótesis rota.
Puede programarse en 24–48 horas según disponibilidad.

**Rutina:** Limpieza, revisión, obturación programada, seguimiento de tratamiento. Se
agenda en el próximo horario disponible según preferencia del paciente.

**Triaje por teléfono:** El personal de recepción debe hacer preguntas breves para
clasificar: ¿Hay dolor? ¿Sangrado? ¿Traumatismo reciente? Con esa información se decide
si se ofrece slot de urgencia o cita regular.
"""

