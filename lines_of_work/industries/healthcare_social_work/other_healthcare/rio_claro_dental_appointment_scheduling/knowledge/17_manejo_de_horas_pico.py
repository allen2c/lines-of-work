content = """
Ciertos horarios concentran mayor demanda: antes y después del trabajo, fines de semana,
y vacaciones escolares. La clínica debe anticipar estas oleadas y optimizar la oferta.

**Bloques premium:** Reservar algunos horarios para pacientes que requieren citas en
horarios limitados (trabajadores con horario rígido, padres con hijos en escuela).
Ofrecer estos slots con prioridad a quienes los soliciten explícitamente.

**Lista de espera:** Para horarios muy solicitados, mantener una lista de espera. Cuando
se libere una cita por cancelación, contactar a los primeros en la lista en orden
cronológico de solicitud.

**Extensión de horario:** En períodos de alta demanda, evaluar ampliar el horario de
atención (mañanas tempranas o tardes extendidas) si la capacidad del equipo lo permite.
Comunicar los cambios con anticipación a los pacientes habituales.
"""

