title = "Registro y Actualización de Datos del Paciente"

content = """
Todo paciente nuevo debe completar una ficha de registro antes de la primera cita. Los datos
se actualizan en cada visita para mantener información vigente.

**Datos obligatorios:** Nombre completo, documento de identidad, fecha de nacimiento, teléfono,
correo electrónico, dirección y persona de contacto en caso de emergencia.

**Historial médico:** Alergias, medicamentos actuales, condiciones crónicas (diabetes, hipertensión,
enfermedades cardíacas) y embarazo si aplica. Estos datos influyen en la seguridad del tratamiento.

**Consentimiento:** Firma de consentimiento informado para tratamiento y uso de datos según
normativa de protección de datos. Para menores, firma del tutor legal.

**Actualización periódica:** Revisar y actualizar datos al menos una vez al año o cuando el
paciente reporte cambios. Datos desactualizados pueden causar errores en recordatorios o
contacto de emergencia.
"""

