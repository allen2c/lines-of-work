title = "Protección de datos y privacidad"

content = """
La información del paciente (nombre, teléfono, correo, historia clínica, datos de pago)
está protegida por normativas de privacidad y confidencialidad médica. El personal de
agendamiento debe manejarla con cuidado.

**Acceso:** Solo el personal autorizado puede consultar o modificar datos del paciente.
No compartir información con terceros sin consentimiento explícito, salvo excepciones
legales (orden judicial, autoridades sanitarias).

**Comunicación:** Al llamar o enviar mensajes, confirmar la identidad del paciente o
su representante antes de revelar detalles de citas o tratamientos. No dejar información
sensible en buzones de voz sin verificar que es el destinatario correcto.

**Almacenamiento:** Los sistemas de citas deben cumplir con los requisitos de seguridad
de la clínica. No usar correos personales o dispositivos no autorizados para manejar
datos de pacientes.
"""

