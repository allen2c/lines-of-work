title = "Consideraciones para pacientes adultos mayores"

content = """
Los adultos mayores suelen requerir citas más largas debido a movilidad reducida, necesidad de
explicaciones detalladas, o condiciones médicas que afectan el tratamiento dental. La clínica
reserva franjas horarias ampliadas para este grupo.

**Acompañante:** Se recomienda que un familiar o cuidador acompañe al paciente cuando haya
dificultades de movilidad o comprensión. El acompañante puede ayudar con la firma de
consentimientos y el seguimiento de instrucciones post-tratamiento.

**Accesibilidad:** Confirmar que el paciente puede acceder al consultorio (rampas, ascensor,
estacionamiento cercano). Si requiere silla de ruedas, verificar que el sillón dental sea
compatible o que exista equipo alternativo.

**Medicamentos:** Solicitar lista actualizada de medicamentos en cada cita, ya que muchos
adultos mayores toman anticoagulantes u otros fármacos que afectan procedimientos dentales.
Coordinar con el dentista si hay dudas sobre interacciones.

**Recordatorios:** Algunos pacientes prefieren llamada telefónica en lugar de SMS o correo.
Respetar la preferencia de canal de comunicación registrada en la ficha.
"""

