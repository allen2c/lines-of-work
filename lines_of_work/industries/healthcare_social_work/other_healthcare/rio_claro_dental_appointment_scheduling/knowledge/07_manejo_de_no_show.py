title = "Manejo de Ausencias sin Aviso (No-Show)"

content = """
Las ausencias sin aviso reducen la utilización de la clínica y afectan a otros pacientes
que podrían haber usado ese horario. Un protocolo consistente mitiga el impacto.

**Registro:** Anotar cada no-show en la ficha del paciente con fecha y tipo de cita.
Mantener un registro que permita identificar patrones.

**Primera ocurrencia:** Llamar o enviar mensaje al paciente para confirmar si hubo error
o emergencia. Ofrecer reprogramación. No aplicar sanción en la primera vez.

**Segunda y tercera ocurrencia:** Informar al paciente sobre la política. Puede requerirse
depósito reembolsable o pago anticipado para la siguiente cita.

**Pacientes crónicos:** Si un paciente acumula múltiples no-shows, la clínica puede optar
por no programar más citas o solo citas de último momento (mismo día) según disponibilidad.

**Lista de espera:** Cuando hay alta demanda, ofrecer slots liberados por cancelación o
no-show a pacientes en lista de espera, priorizando urgencias.
"""

