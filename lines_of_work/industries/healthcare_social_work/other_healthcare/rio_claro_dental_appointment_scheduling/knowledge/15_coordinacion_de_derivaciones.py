title = "Coordinación de derivaciones"

content = """
Cuando un paciente requiere tratamiento especializado (ortodoncia, cirugía maxilofacial,
implantes, periodoncia avanzada), la clínica deriva a un especialista externo o a un
colega dentro de la red. El agendamiento debe facilitar esta transición.

**Documentación:** Obtener del dentista la referencia por escrito con diagnóstico resumido,
procedimiento sugerido y urgencia. Incluir radiografías o estudios si el especialista
los requiere antes de la primera cita.

**Contacto con especialista:** Llamar o enviar la referencia según el canal que el
especialista indique. Confirmar que recibieron la información y que tienen disponibilidad.
Registrar nombre del especialista, fecha de derivación y estado (pendiente, agendado, completado).

**Seguimiento:** Si el paciente no agenda con el especialista en un plazo razonable,
contactar para recordar la derivación y ofrecer apoyo para agendar. Documentar en la
historia clínica.
"""

