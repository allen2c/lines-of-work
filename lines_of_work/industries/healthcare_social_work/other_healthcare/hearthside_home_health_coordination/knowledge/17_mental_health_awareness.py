title = "Mental Health Awareness and Psychosocial Support"

content = """
Mental health conditions are prevalent among home health patients and significantly affect recovery
outcomes, treatment adherence, and quality of life. Depression affects approximately 30–40% of home
health patients; anxiety disorders, cognitive impairment, and social isolation are also common. Care
coordinators are not mental health clinicians, but they play an important role in identification,
documentation, referral, and follow-up.

**Common Mental Health Presentations**
- *Depression:* Persistent low mood, loss of interest in activities, changes in appetite or sleep,
  withdrawal from family, expressions of hopelessness. Often under-reported by elderly patients who
  normalize sadness as a natural consequence of illness.
- *Anxiety:* Excessive worry about health status, reluctance to be left alone, repeated calls to
  the agency for reassurance, resistance to care plan changes.
- *Cognitive Impairment:* Confusion about medications, inability to recall care plan instructions,
  disorientation, or personality change. May indicate dementia, delirium (reversible), or metabolic
  disturbance.
- *Social Isolation:* Patient lives alone, has no regular social contact, lacks transportation,
  or has expressed loneliness. Isolation worsens depression and slows physical recovery.

**OASIS Cognitive and Behavioral Items**
The OASIS assessment includes standardized items addressing cognitive functioning and behavioral
symptoms. These items should accurately reflect the patient's observed status — not best-case
performance — so that the care plan appropriately addresses cognitive or emotional needs.

**Coordinator Role**
- If a clinician, family member, or the patient directly expresses concern about mental health,
  document it and refer to the MSW for a psychosocial assessment.
- If a patient expresses passive suicidal ideation or hopelessness in conversation with the
  coordinator, do not minimize the comment. Escalate immediately to the clinical supervisor and
  ensure the clinician or MSW contacts the patient on the same day.
- For patients with known dementia, confirm that the care plan includes safe medication management,
  that the caregiver has been educated on dementia-specific communication strategies, and that
  the patient's decision-making capacity has been documented.

**Community Resources**
Coordinators should maintain a current list of mental health resources in the service area: crisis
hotlines, community mental health centers, geriatric psychiatry services, senior centers, and peer
support programs. The MSW is the primary coordinator of these referrals, but having the list
accessible allows coordinators to respond immediately to urgent family requests.
"""

