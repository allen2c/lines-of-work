title = "Telehealth and Remote Patient Monitoring in Home Health"

content = """
Telehealth and remote patient monitoring (RPM) have expanded significantly in home health, offering
agencies the ability to supplement in-person visits with virtual check-ins and continuous biometric
data collection. When implemented thoughtfully, these tools improve early detection of deterioration
and reduce avoidable hospitalizations.

**Telehealth Video Visits**
Video visits allow clinicians to conduct visual assessments — wound inspection, gait observation,
mental status screening, or medication review — without traveling to the patient's home. Video visits
can supplement but generally do not replace in-person skilled visits for billing under Medicare home
health benefits (check current CMS guidance for the latest telehealth flexibility rules). They are most
valuable for: interim check-ins between scheduled visits, education sessions with patients or families
who have video access, and supervisor consultations when a field clinician needs a second opinion.

**Remote Patient Monitoring Devices**
RPM devices transmit biometric readings from the patient's home to a clinical monitoring hub in
near-real time. Common devices in home health include:
- *Blood pressure monitors:* Detect hypertensive or hypotensive trends in cardiac and renal patients.
- *Weight scales:* Flag rapid fluid gain in heart failure patients (a gain of 2–3 lbs in 24 hours).
- *Pulse oximeters:* Monitor oxygen saturation in COPD, heart failure, or post-COVID patients.
- *Glucometers with wireless connectivity:* Track blood glucose trends in diabetic patients.

**Alerts and Response Protocols**
RPM generates value only if alert protocols are clearly defined and consistently acted upon.
Coordinators should ensure that: alert thresholds are set based on the physician's orders for each
patient, an on-call clinician is designated to review alerts outside business hours, and the response
protocol (call the patient, schedule a visit, notify the physician) is documented in the care plan.

**Patient Eligibility and Onboarding**
Not all patients are appropriate for telehealth or RPM. Assess: digital literacy, availability of
internet access or cellular connectivity, physical ability to operate the device, and caregiver support
if the patient cannot manage the equipment independently. Provide hands-on device orientation at the
first visit and document the training in the care record.

**Coordination Responsibilities**
Track device assignment and return. Ensure malfunctioning devices are replaced promptly. Run a
weekly alert-response audit to confirm that all out-of-range readings were reviewed and acted on.
"""

