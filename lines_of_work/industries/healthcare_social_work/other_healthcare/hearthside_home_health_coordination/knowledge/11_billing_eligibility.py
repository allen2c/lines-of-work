title = "Medicare Home Health Billing and Eligibility Requirements"

content = """
Medicare is the dominant payer for home health services in the United States, and understanding
its eligibility criteria, coverage conditions, and billing rules is essential for the coordination
team to protect the agency from payment denials and compliance sanctions.

**Four Conditions of Participation**
Medicare Part A and Part B home health benefits require all four conditions to be met simultaneously:
1. **Homebound status:** The patient must be confined to the home, with leaving requiring considerable
   and taxing effort due to illness or injury.
2. **Skilled care need:** At least one service must require the skill of a licensed clinician — nursing,
   physical therapy, or speech-language pathology.
3. **Physician certification:** A physician must certify the plan of care and sign the 485 form.
4. **Medicare-certified agency:** Care must be delivered by an agency that holds active Medicare
   certification and meets Conditions of Participation.

**Patient-Driven Groupings Model (PDGM)**
Since 2020, Medicare reimburses home health under the Patient-Driven Groupings Model, which classifies
each 30-day billing period into a payment group based on: referral source (community vs. institutional),
timing within the episode (early vs. late), clinical grouping (based on primary diagnosis), functional
impairment level (from OASIS functional items), and comorbidity adjustment. Coordinators should
understand that OASIS accuracy directly determines the payment group — inaccurate coding can result
in systematic underpayment or overpayment, both of which carry compliance risk.

**Non-Covered Services**
Medicare does not cover: 24-hour home care, custodial-only care (when no skilled need exists), meal
delivery, homemaker services without a skilled service in the episode, or personal care unrelated to
the medical condition. Coordinators must avoid promising these services to patients or families.

**Billing Timelines**
Claims for the first 30-day period may be submitted after the period ends and required documentation
is complete. RAPs (Requests for Anticipated Payment) were eliminated in 2022. Ensure all visit notes,
OASIS data, and the signed 485 are locked in the EMR before billing submits the claim.
"""

