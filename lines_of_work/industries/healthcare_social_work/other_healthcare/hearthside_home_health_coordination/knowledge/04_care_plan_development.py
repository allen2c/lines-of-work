title = "Individualized Care Plan Development and Maintenance"

content = """
The care plan is the central coordinating document for a home health episode. It translates the
physician's orders and the OASIS clinical findings into a specific, time-bound set of services,
goals, and interventions tailored to the patient's conditions and living situation.

**Core Components of the Care Plan**
A complete care plan includes: primary and secondary diagnoses, identified patient problems and
functional deficits, measurable outcome goals with target timeframes, assigned disciplines and their
visit frequencies, specific nursing and therapy interventions, medication list and management needs,
safety measures, patient and caregiver education topics, and anticipated discharge destination.

**Developing the Plan**
After the OASIS start-of-care assessment, the coordinating clinician drafts the care plan within
24–48 hours. The care coordinator convenes an interdisciplinary team review when multiple disciplines
are involved, ensuring that nursing, therapy, and aide services are sequenced logically. Goals should
be SMART — Specific, Measurable, Achievable, Relevant, and Time-bound — so that progress can be
objectively evaluated at recertification.

**Physician Approval**
No care plan is implemented without a signed Plan of Care (CMS Form 485). The coordinator sends the
draft plan to the referring or certifying physician for review and signature. Verbal orders may be
accepted to initiate urgent care, but written confirmation must follow within the timeframe specified
by state regulation. Unsigned plans are a billing and compliance liability.

**Plan Updates**
When a patient's condition changes — a new diagnosis, a fall, a hospitalization, or significant
functional decline — the care plan must be updated and the physician notified. A verbal order documents
the change immediately; a formal care plan amendment is submitted with the next billing cycle. The
coordinator tracks all plan versions and retains prior versions in the EMR for audit purposes.

**Patient and Family Involvement**
Patients and their designated caregivers should actively participate in goal-setting. Explaining the
rationale behind visit frequencies and therapy objectives improves adherence and reduces missed visits.
Document patient agreement with the care plan and any goals the patient has declined or modified.
"""

