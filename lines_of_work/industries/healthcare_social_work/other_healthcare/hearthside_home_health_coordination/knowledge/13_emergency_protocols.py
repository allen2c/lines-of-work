title = "Emergency Response and Acute Change in Condition Protocols"

content = """
Home health patients may experience acute clinical events without immediate clinical personnel present.
The care coordination team plays a critical role in establishing emergency protocols, training patients
and families to recognize warning signs, and responding rapidly when an event is reported.

**Patient-Facing Emergency Preparation**
At admission, ensure each patient and household contact has:
- The agency's 24-hour on-call phone number posted visibly in the home
- A written list of warning signs requiring immediate 911 activation (chest pain, severe shortness
  of breath, unresponsiveness, signs of stroke, active bleeding, severe fall with suspected fracture)
- Clear understanding of whether and when to call the agency versus calling 911 directly

**When a Clinician or Patient Reports an Emergency**
If a clinician in the field reports an unresponsive or critically ill patient:
1. Direct the clinician to call 911 immediately if not already done.
2. Notify the on-call RN supervisor and clinical director by phone.
3. Ensure the clinician remains on site until emergency services arrive, if safe to do so.
4. Document the event in the incident report system within 24 hours, including exact timeline,
   actions taken, and outcome.

**Acute Change in Condition (ACIC)**
An ACIC is a notable shift in the patient's clinical status that may not require immediate emergency
services but does require physician notification and possible care plan revision. Common ACIC triggers:
- New confusion or cognitive decline
- Unplanned weight change of more than 3–5 lbs in 24–48 hours
- New wound or worsening of existing wound
- Fall with or without injury
- New or worsening pain reported at a level greater than 5/10

When an ACIC is reported by a clinician: notify the attending physician using SBAR format, document
the physician contact and any new orders received, update the care plan if orders change, and schedule
a follow-up visit within 24–48 hours.

**Hospitalizations**
When a patient is hospitalized during an active home health episode, place the case on hold, notify
the billing department, and flag the record for a Resumption of Care (ROC) OASIS upon discharge from
the hospital. Contact the hospital discharge planner to coordinate the return to home health services.
"""

