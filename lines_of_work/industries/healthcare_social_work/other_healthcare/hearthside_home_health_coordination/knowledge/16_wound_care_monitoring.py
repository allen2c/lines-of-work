title = "Wound Care Monitoring and Healing Progress Tracking"

content = """
Wound management is one of the most common skilled nursing services delivered in home health.
Effective coordination ensures that wound care orders are current, supplies are available, visits
are consistently scheduled, and healing progress is tracked and reported to the physician at
clinically meaningful intervals.

**Common Wound Types in Home Health**
- *Surgical wounds:* Post-operative incisions requiring monitoring for dehiscence, signs of infection,
  or seroma/hematoma formation.
- *Pressure injuries (pressure ulcers):* Classified by stage (1–4, unstageable, deep tissue injury).
  Stage 3 and 4 wounds are complex and require physician-directed wound care protocols.
- *Diabetic foot ulcers:* High risk for infection, osteomyelitis, and amputation. Require close
  interdisciplinary management, often with a wound care specialist involved.
- *Venous stasis ulcers:* Typically located on the lower leg; treated with compression therapy
  and moisture-retentive dressings.
- *Arterial ulcers:* Pale, well-demarcated wounds caused by poor circulation; compression therapy
  is contraindicated. Require vascular surgery involvement.

**Wound Documentation Standards**
At each wound care visit, the RN must document:
- Location (using anatomical landmarks or a body diagram)
- Dimensions (length × width in centimeters; depth if applicable)
- Wound bed characteristics (granulation, slough, eschar percentage)
- Periwound skin condition
- Exudate type, color, and volume
- Odor
- Pain at the wound site
- Treatment applied

Objective, measurement-based documentation enables the physician to assess healing trajectory
without being present.

**Coordination Responsibilities**
- Confirm that wound care supply orders (dressings, cleansing agents) are placed and delivered
  before the scheduled visit.
- Track wound care visit frequency against orders; a missed wound care visit for a Stage 3 or 4
  pressure injury is a patient safety event requiring immediate action.
- Alert the clinical supervisor if wound documentation shows no improvement or signs of worsening
  over two consecutive visits — a care plan update and physician notification are warranted.
- Coordinate referrals to wound care specialists or outpatient wound clinics when the home health
  team's scope is insufficient for the wound complexity.
"""

