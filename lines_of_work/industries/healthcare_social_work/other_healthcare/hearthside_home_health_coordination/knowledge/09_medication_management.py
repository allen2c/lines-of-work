title = "Medication Management in Home Health"

content = """
Medication errors are one of the leading causes of adverse events in home health patients, who
often take multiple medications for complex chronic conditions. Coordination supports the clinical
team's medication management effort by ensuring the right documentation, referrals, and follow-up
are in place.

**Medication Reconciliation**
At start of care, the admitting clinician performs a complete medication reconciliation: listing every
prescription drug, over-the-counter medication, vitamin, and supplement the patient takes, cross-
referencing against the hospital discharge medication list if applicable. Discrepancies must be
reported to the physician for clarification before the patient takes any disputed medication.

**High-Risk Medications**
Certain medication classes require heightened vigilance in the home setting:
- *Anticoagulants (warfarin, novel anticoagulants):* Monitor for bleeding signs; ensure INR monitoring
  appointments are scheduled.
- *Insulin and oral hypoglycemics:* Confirm the patient can self-administer or arrange for aide support;
  verify refrigeration access.
- *Opioid analgesics:* Assess pain management effectiveness, monitor for sedation or constipation.
- *Diuretics:* Track daily weights, edema, and signs of electrolyte imbalance.
- *Narrow therapeutic index drugs (digoxin, lithium, phenytoin):* Ensure lab monitoring is ordered.

**Medication Adherence**
Non-adherence is common in elderly or cognitively impaired patients. Coordinate with the clinical team
to assess whether a pill organizer, blister pack, medication reminder app, or HHA-assisted administration
is appropriate. Document adherence barriers in the care plan and develop an individualized strategy.

**Medication Education**
The care plan should include patient and caregiver education on the purpose, dosing, side effects, and
interactions of key medications. Ensure the RN addresses medication education goals at each visit and
documents the patient's level of understanding.

**Coordination Role Boundary**
Coordinators do not assess medication effects, adjust doses, or provide clinical medication advice.
Route all medication questions from patients or families to the assigned RN, and document that the
referral was made. Never convey clinical medication instructions secondhand without clinician review.
"""

