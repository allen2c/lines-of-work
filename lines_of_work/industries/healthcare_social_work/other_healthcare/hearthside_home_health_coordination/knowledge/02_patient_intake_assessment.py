title = "Patient Intake and Case Opening"

content = """
The intake process converts a physician referral into an active home health case with a funded,
clinically grounded plan of care. A well-executed intake prevents delays, establishes the agency's
legal and clinical obligations, and creates the first impression that shapes the patient-agency
relationship throughout the episode.

**Receiving the Referral**
Referrals arrive from hospital discharge planners, physicians' offices, outpatient clinics, or family
members. At intake, collect: patient demographics, primary and secondary diagnoses, referring physician
name and NPI, insurance information, emergency contact, and any known safety or access concerns
(e.g., pets, locked buildings, fall history). Log the referral timestamp — CMS requires the start-of-care
visit to occur within a defined window of the referral date for billing compliance.

**Insurance Verification**
Before scheduling the first visit, verify the patient's Medicare, Medicaid, or private insurance
eligibility. Confirm that home health benefits are active, that the agency is an approved provider,
and that any required pre-authorization has been obtained. Document the verification outcome and the
name of the insurance representative spoken with.

**Scheduling the Initial Visit**
Assign the initial visit to an RN (or PT if the primary need is rehabilitative). Communicate the
patient's address, access information, preferred contact method, and any known communication barriers
(hearing impairment, language preference) to the visiting clinician. The clinician must bring the
agency's intake packet, consent forms, and OASIS assessment tools.

**Consent and Rights**
At the first visit, the clinician presents and obtains signed consent for treatment, privacy notices
(HIPAA), and the Patient Rights and Responsibilities document. The patient must be verbally informed
of their right to accept or refuse services and how to file a complaint.

**Case Opening in the EMR**
After the first visit, create the patient record in the electronic medical record system. Enter all
demographic, insurance, diagnosis, and contact data. Link the referring physician for electronic
order transmission. Assign the care coordination primary contact. A complete case opening record is
the foundation for all subsequent documentation, billing, and compliance tracking.
"""

