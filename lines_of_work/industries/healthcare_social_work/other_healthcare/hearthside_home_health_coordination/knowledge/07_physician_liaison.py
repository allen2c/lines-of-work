title = "Physician Liaison: Orders, Communication, and Plan Approval"

content = """
The care coordinator serves as the primary communication conduit between the home health agency and
the patient's certifying physician. Timely, accurate physician communication is a regulatory requirement,
a clinical safety imperative, and a key driver of care plan compliance.

**Obtaining Physician Orders**
All skilled home health services require a physician order. At the start of care, the coordinator routes
the draft Plan of Care (CMS Form 485) to the certifying physician for review and signature. The 485 must
be signed before or within a short period after care begins — state-specific rules apply, but CMS
requires the signed plan to be on file before the claim is submitted.

For interim orders — changes in visit frequency, new medications, wound care protocol changes, or
additional discipline referrals — obtain a verbal order from the physician immediately, document it
in the EMR with the date, time, physician name, and content, and then route a written order for
signature within the required state timeframe (typically 30 days).

**Communicating Clinical Updates**
Notify the physician promptly when:
- A patient is hospitalized or visits an emergency department
- A significant change in condition (SCIC) is identified by the clinician
- A patient declines treatment or is non-adherent
- A wound is not healing as expected, or there are signs of infection
- Laboratory values fall outside safe ranges (if the agency receives lab results)

Use the SBAR format (Situation, Background, Assessment, Recommendation) for all urgent clinical
communications. Document the conversation in the EMR within 24 hours, including any orders received.

**Managing Physician Relationships**
Some physicians are unfamiliar with the home health billing and documentation requirements. If a
physician's order lacks required specificity (e.g., a vague "PT as needed" without a frequency), contact
the physician's office to clarify before scheduling. Educate physicians' office staff on the 485 signature
process and offer fax, portal, or EHR-direct signature options to minimize delays.

**Order Tracking**
Maintain an open-order log in the EMR or a coordination tracking tool. Flag any order that has been
outstanding for more than 7 days without signature. Escalate to the clinical supervisor if a physician
repeatedly fails to return signed orders, as this creates billing risk and compliance exposure.
"""

