title = "Documentation Standards and Electronic Medical Record Use"

content = """
In home health, documentation is simultaneously a clinical tool, a billing instrument, and a
legal record. Incomplete or inaccurate documentation creates compliance risk, delays payment,
and can undermine care quality if the clinical picture is unclear to incoming staff.

**Core Documentation Principles**
- *Timeliness:* Visit notes must be completed and signed by the clinician within 24 hours of the visit.
  OASIS assessments must be locked within the CMS-mandated window.
- *Specificity:* Vague documentation such as "patient doing well" or "wounds improving" is insufficient.
  Clinical notes must describe specific findings, measurements, and objective observations.
- *Accuracy:* Document what was observed and performed, not what was planned. If a planned intervention
  was not completed, document why and what alternative action was taken.
- *Signature and authentication:* All entries must be signed by the clinician who performed the service,
  with the full credential and timestamp. Co-signatures by supervisors must be added when required.

**Electronic Medical Record (EMR) Use**
All clinical, scheduling, and communication activities must be entered into the agency's EMR. Common
documentation tasks for coordinators include: opening and updating patient records, logging physician
contacts and verbal orders, entering scheduling changes, tracking consent forms, and monitoring
documentation completion rates by clinician.

**Verbal Order Documentation**
When a verbal order is received by phone, document it immediately: date, time, physician name and
callback number, content of the order, and the name of the clinician who received it. Flag the order
for written confirmation tracking. Never implement a verbal order without first documenting it.

**Audit Readiness**
The agency is subject to CMS, state, and accreditation audits at any time. All documentation must be
complete, retrievable, and consistent across the care record. Common audit targets include OASIS
timeliness, 485 signatures, HHA supervisory visit frequency, and patient rights acknowledgment.
Coordinators should run regular compliance summary reports and address gaps before they accumulate.

**Correction Procedures**
Never delete or overwrite an incorrect entry. Use the EMR's amendment or correction function, which
preserves the original entry alongside the correction, and requires a reason code and signature.
"""

