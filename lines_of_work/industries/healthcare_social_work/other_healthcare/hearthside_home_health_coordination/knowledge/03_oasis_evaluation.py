title = "OASIS Assessment: Purpose, Timing, and Coordination"

content = """
The Outcome and Assessment Information Set (OASIS) is the standardized assessment instrument mandated
by the Centers for Medicare and Medicaid Services (CMS) for all adult Medicare and Medicaid home health
patients receiving skilled care. OASIS data drives care planning, payment classification, quality
reporting, and agency performance benchmarking.

**Who Completes OASIS**
OASIS must be completed by a registered nurse, physical therapist, speech-language pathologist, or
occupational therapist — the same skilled clinician conducting the clinical assessment. Care coordinators
do not complete OASIS items directly but are responsible for ensuring the assessment is scheduled,
submitted, and coded within required timeframes.

**Key Assessment Time Points**
CMS requires OASIS completion at five time points:
- *Start of Care (SOC):* Within 5 calendar days of the first billable skilled visit.
- *Resumption of Care (ROC):* After a patient returns home from an inpatient stay during an episode.
- *Recertification (REC):* Between days 56 and 60 of each 60-day episode.
- *Other Follow-Up (FU):* When a significant change in condition (SCIC) warrants reassessment.
- *Transfer and Discharge (TRN/DC):* When the patient is transferred to inpatient care or discharged.

**OASIS Data Items**
The assessment captures patient demographics, clinical diagnoses, functional status (bathing, dressing,
ambulation, transferring), cognitive status, medication management capability, wound status, pain
assessment, living situation, and caregiver availability. Each item uses standardized response options
that must be selected based on observed or reported patient performance at the time of assessment,
not the patient's best or worst performance.

**Coordination Role**
The care coordinator monitors the OASIS submission calendar. When a recertification window opens
(day 56 of the episode), schedule the recertification visit immediately and confirm the clinician
completes and submits the assessment before day 60. Late OASIS submissions result in billing holds
and CMS compliance flags. Correct any rejected OASIS records within the agency's review cycle with
input from the clinical supervisor.
"""

