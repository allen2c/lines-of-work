title = "Infection Control in the Home Setting"

content = """
Infection control in home health differs fundamentally from hospital-based infection control because
the care environment is uncontrolled: the home may have pets, inadequate ventilation, limited hand
hygiene facilities, and multiple household members with varying health status. Consistent application
of standard precautions by every clinician on every visit is the foundation of safe home health practice.

**Standard Precautions**
All clinicians apply standard precautions regardless of the patient's known diagnoses. This means:
- Hand hygiene with soap and water or alcohol-based sanitizer before and after every patient contact
- Use of gloves when contact with blood, body fluids, mucous membranes, or non-intact skin is possible
- Use of a mask and eye protection when splash or aerosol exposure is likely (wound irrigation,
  respiratory suctioning)
- Proper disposal of sharps and biohazardous waste using agency-supplied containers

Clinicians carry a standard supply kit with PPE, sterile supplies, and a small sharps container.

**Transmission-Based Precautions**
When a patient has a known or suspected transmissible infection (MRSA, VRE, active tuberculosis,
C. difficile, influenza, COVID-19), additional precautions apply based on the transmission route:
- *Contact:* Gown and gloves; dedicate reusable equipment to the patient.
- *Droplet:* Surgical mask; avoid prolonged close face-to-face contact.
- *Airborne:* N95 respirator; perform fit testing and training before assigning clinician.

**Wound Care and Aseptic Technique**
Wound care visits require strict aseptic technique to prevent introducing pathogens into the wound
bed. Clinicians must use sterile dressings, follow the ordered wound care protocol precisely, and
document wound characteristics (size, depth, exudate, tissue type, odor) at each visit.

**Patient and Household Education**
Educate patients and family members on hand hygiene before and after handling dressings or
medical equipment, proper handling of soiled linens, disposal of used supplies, and signs of
local infection (redness, warmth, purulent drainage, fever). Document education in the visit note.

**Coordination Role**
Ensure new clinical staff receive infection control orientation before their first home visit.
Flag any patient whose infection status changes so that the visiting clinician is briefed before
entering the home. Coordinate with the clinical supervisor if an outbreak or cluster of infections
is identified across the agency's patient population.
"""

