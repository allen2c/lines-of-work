title = "Home Health Discipline Roles and Scope of Practice"

content = """
Understanding the distinct scope of each clinical discipline is essential for building appropriate
care plans, scheduling the right clinician for each task, and recognizing when a referral to an
additional discipline is warranted. Coordinators do not direct clinical practice but must understand
enough to route work correctly.

**Registered Nurse (RN)**
The RN is the primary clinical lead in most home health cases. Core RN functions include comprehensive
assessment, medication management and education, wound assessment and dressing changes, IV therapy,
disease management teaching, coordination with the physician, and supervision of LPNs and HHAs. The RN
completes the OASIS assessment and is responsible for the overall clinical integrity of the care plan.

**Licensed Practical Nurse (LPN)**
LPNs perform routine skilled nursing visits under RN supervision — vital signs monitoring, routine wound
care, medication administration, and patient observation. LPNs cannot independently complete OASIS
assessments or certify patients. Each LPN visit must align with an active RN supervisory visit schedule.

**Physical Therapist (PT)**
PTs evaluate and treat impairments in mobility, strength, balance, and functional movement. In home
health, PT goals typically target safe ambulation, fall risk reduction, transfer training, and stair
navigation. PTs may also initiate care when the primary skilled need is rehabilitative rather than nursing.

**Occupational Therapist (OT)**
OTs address the patient's ability to perform activities of daily living (ADLs) — bathing, dressing,
meal preparation, and home management — often through adaptive equipment and compensatory strategies.
OT referrals are appropriate when functional limitations affect independent living.

**Speech-Language Pathologist (SLP)**
SLPs evaluate and treat swallowing disorders (dysphagia), communication impairments, and cognitive
deficits affecting daily function. A physician order specifying the need is required before an SLP visit.

**Medical Social Worker (MSW)**
MSWs assess psychosocial needs, connect patients with community resources, support crisis intervention,
and assist with discharge planning. An MSW referral is appropriate for patients facing housing instability,
caregiver burnout, mental health concerns, or complex discharge needs.

**Home Health Aide (HHA)**
HHAs provide personal care (bathing, grooming, light housekeeping, meal preparation) under the supervision
of a skilled clinician. HHA services are only billable to Medicare when a patient is also receiving skilled
nursing or therapy services. HHA visits require documented supervisory visits by the RN or PT.
"""

