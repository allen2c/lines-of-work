title = "Regulatory Compliance: CMS, HIPAA, and Accreditation"

content = """
Home health agencies operate within a dense regulatory environment that spans federal CMS requirements,
HIPAA privacy and security rules, state licensure requirements, and optional accreditation standards.
Non-compliance can result in payment suspension, exclusion from Medicare, civil monetary penalties,
or loss of licensure.

**CMS Conditions of Participation (CoPs)**
The CoPs define minimum standards for patient rights, care planning, clinical record management,
skilled services, HHA services, and quality assessment. Key CoP obligations for coordinators include:
- Ensuring each patient receives a written notice of their rights at admission
- Maintaining an active grievance process and logging all formal complaints
- Completing and transmitting OASIS data within required windows
- Ensuring HHAs receive supervisory visits at required intervals (typically every 14 days)

**HIPAA Privacy Rule**
All patient health information (PHI) — including name, diagnosis, treatment details, and contact
information — is protected under HIPAA and may only be shared with individuals the patient has
authorized or with covered entities involved in treatment. Coordinators must:
- Obtain and document patient authorization before sharing information with family members
- Use secure transmission methods (encrypted email, fax with cover sheets, secure portal) for PHI
- Report suspected breaches immediately to the privacy officer
- Never discuss patient information in public spaces or unsecured digital environments

**State Licensure Requirements**
Home health licensure is state-administered and varies significantly. Coordinators should be familiar
with state-specific requirements for: minimum staffing ratios, staff credential verification, criminal
background checks, mandatory incident reporting, and advance directive documentation.

**Accreditation**
Hearthside Home Health holds Joint Commission accreditation, which surveys against National Patient
Safety Goals and home health-specific standards. During survey periods, coordinators may be asked to
demonstrate: care plan documentation, patient rights acknowledgment, medication safety procedures,
infection control protocols, and emergency preparedness plans. Maintain audit-ready documentation
at all times, not just during survey windows.
"""

