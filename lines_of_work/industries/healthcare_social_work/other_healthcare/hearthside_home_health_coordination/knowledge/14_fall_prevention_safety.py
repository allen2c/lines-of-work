title = "Fall Prevention and Home Safety Assessment"

content = """
Falls are among the most common and consequential adverse events in home health. Approximately one-third
of adults over 65 fall each year, and falls in the home setting can result in hip fractures, head injury,
and prolonged hospitalization. A systematic fall prevention approach is embedded across every episode of care.

**Fall Risk Stratification**
At start of care and at each OASIS assessment point, the clinical team scores the patient's fall risk
using a validated tool such as the Morse Fall Scale or the STEADI (Stopping Elderly Accidents, Deaths,
and Injuries) algorithm. Key risk factors include: history of prior falls, gait and balance impairment,
polypharmacy (especially sedatives, antihypertensives, diuretics), cognitive impairment, orthostatic
hypotension, vision impairment, and unsafe footwear.

**Home Safety Assessment**
PTs and OTs conduct a structured home safety assessment early in the episode. They evaluate:
- Trip hazards: loose rugs, clutter, poor lighting, uneven thresholds
- Bathroom safety: availability of grab bars, non-slip mats, raised toilet seat
- Stair safety: handrail integrity, lighting, stair condition
- Bedroom layout: bed height, path to bathroom, telephone access

The clinician documents specific recommendations and ensures the patient or family understands how
to implement them. Durable medical equipment (DME) referrals for grab bars, shower chairs, or
bedside commodes should be ordered with physician authorization.

**Coordination Responsibilities**
- Track patients flagged as high fall risk and confirm that the PT has been assigned or consulted.
- Confirm DME orders have been placed and delivered before the patient is expected to use them.
- Ensure fall incidents are documented in the incident reporting system within 24 hours.
- Notify the physician of any fall, with or without injury, using the SBAR format.

**Post-Fall Protocol**
A fall during an active episode requires immediate clinical follow-up:
1. A nursing or PT visit within 24 hours to assess for injury and review precipitating factors.
2. Physician notification with documentation of the call and any new orders.
3. Care plan review to determine whether visit frequency, PT referral, or DME needs to be revised.
"""

