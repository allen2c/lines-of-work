content = """
El trabajo grupal es una modalidad de intervención donde el grupo mismo se convierte en agente
de cambio: los participantes aprenden unos de otros, se apoyan mutuamente y desarrollan sentido
de pertenencia y solidaridad. Es complementario al trabajo individual de gestión de casos y,
en muchos contextos, más eficaz para producir cambios sostenibles.

**Tipos de Grupos en Sendero Comunitario**

- **Grupos psicoeducativos**: transmiten información y habilidades en un tema específico (pautas
  de crianza, manejo del estrés, derechos de las mujeres). Su estructura es más didáctica.
- **Grupos de apoyo mutuo**: el protagonismo lo tienen los participantes; el profesional facilita
  pero no dirige. Son especialmente eficaces en situaciones de duelo, enfermedad crónica,
  adicciones o experiencias comunes de trauma.
- **Grupos de habilidades sociales**: trabajan competencias específicas (comunicación asertiva,
  resolución de conflictos, búsqueda de empleo) a través de prácticas estructuradas y role-play.
- **Grupos comunitarios**: orientados a la acción colectiva: identificar problemas del barrio,
  organizarse para demandar servicios, promover iniciativas de mejora del entorno.

**Fases del Proceso Grupal**

Todo grupo atraviesa fases reconocibles: formación (los miembros se conocen y establecen normas),
conflicto (emergen diferencias y se ponen a prueba los límites del grupo), normatividad (se
establecen reglas de funcionamiento compartidas), rendimiento (el grupo trabaja eficazmente hacia
sus objetivos) y disolución (cierre y evaluación). El facilitador ajusta su rol según la fase.

**Composición y Criterios de Inclusión**

La conformación del grupo influye en su eficacia. Grupos muy heterogéneos (en edad, problemática
o nivel de vulnerabilidad) pueden dificultar la cohesión. En Sendero Comunitario, los grupos se
conforman con criterios de homogeneidad mínima: compartir una problemática común o un objetivo
común, aunque los participantes provengan de contextos distintos.

**Manejo de Conflictos y Situaciones Difíciles**

En el trabajo grupal emergen dinámicas complejas: miembros que monopolizan el espacio, silencio
colectivo ante temas difíciles, conflictos entre participantes, revelaciones de situaciones de
riesgo que requieren atención individual urgente. El facilitador debe estar preparado para manejar
estas situaciones con calma, sin perder el rumbo del grupo ni ignorar las necesidades individuales.
"""

