content = """
La evaluación es el proceso sistemático de recopilación y análisis de información para valorar
si un programa social logra sus objetivos, a qué costo y con qué efectos no previstos. En Sendero
Comunitario, la evaluación es una práctica integrada al trabajo cotidiano, no un ejercicio puntual
realizado solo ante donantes.

**Tipos de Evaluación**

- **Evaluación de proceso o implementación**: ¿el programa se ejecuta como fue diseñado? ¿Cuántas
  personas atendidas, cuántas sesiones realizadas, qué recursos utilizados?
- **Evaluación de resultados**: ¿los usuarios experimentaron cambios en su situación gracias al
  programa? Se mide comparando indicadores antes y después de la intervención.
- **Evaluación de impacto**: ¿los cambios observados son atribuibles al programa y no a otros
  factores? Requiere diseños más robustos (grupos de comparación, seguimiento longitudinal).
- **Evaluación de costo-efectividad**: ¿los resultados obtenidos justifican los recursos
  invertidos? ¿Existen alternativas más eficientes para lograr los mismos objetivos?

**Indicadores de Resultado en Trabajo Social**

Medir el impacto del trabajo social es complejo porque los cambios son frecuentemente subjetivos
y de largo plazo. Los indicadores utilizados en Sendero Comunitario combinan: indicadores
cuantitativos (porcentaje de casos que lograron acceso estable a servicio de salud, número de
usuarios que completaron el plan de intervención, tasa de reingresos al servicio), e indicadores
cualitativos (narrativas de cambio recogidas mediante entrevistas de seguimiento, escalas de
autopercepción del bienestar).

**Uso de Datos para la Mejora Continua**

Los datos de evaluación no son solo para los informes a donantes. Son una herramienta de gestión
interna: si un porcentaje elevado de casos se cierra antes de completar el PII, eso indica un
problema de adherencia que debe analizarse. Si un tipo de derivación tiene baja tasa de éxito,
hay que revisar el protocolo. La evaluación alimenta el ciclo de mejora continua del programa.

**Participación de los Usuarios en la Evaluación**

Las personas más expertas en la calidad del servicio son quienes lo reciben. Sendero Comunitario
aplica encuestas de satisfacción al cierre de cada caso, organiza grupos focales anuales con
ex usuarios, y mantiene un comité consultivo con representantes de la comunidad atendida.
Esta participación no es solo una buena práctica: es una expresión del enfoque de derechos y
del compromiso con la rendición de cuentas.
"""

