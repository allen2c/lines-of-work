content = """
La violencia familiar es una de las problemáticas más prevalentes en las comunidades que atiende
Sendero Comunitario. Su detección temprana, la contención adecuada y la coordinación con las
instancias de protección son competencias esenciales del trabajador social.

**Tipos de Violencia Familiar**

La Ley General de Acceso de las Mujeres a una Vida Libre de Violencia reconoce: violencia física
(golpes, lesiones), psicológica (insultos, control, humillación, aislamiento), sexual (actos
sexuales sin consentimiento), económica (control del dinero, impedimento de trabajar) y patrimonial
(destrucción de bienes). En la práctica, los tipos coexisten y se refuerzan mutuamente.

**Detección y Valoración del Riesgo**

El trabajador social puede aplicar instrumentos de valoración del riesgo como la Escala de Peligro
(Danger Assessment adaptada al contexto mexicano), que evalúa factores como: uso de armas por
parte del agresor, amenazas de muerte, violencia durante el embarazo, uso de sustancias por el
agresor y aislamiento de la víctima. Un puntaje alto de riesgo activa una respuesta de protección
inmediata.

**Atención a la Víctima**

La entrevista a una posible víctima de violencia se realiza siempre en privado, sin el presunto
agresor presente. Se inicia con una pregunta abierta y no directiva ("¿Cómo está la situación en
tu hogar?") antes de preguntar directamente sobre violencia. Se valida la experiencia de la
víctima sin minimizar ni dramatizar. Se informa claramente sobre sus derechos y las opciones de
protección disponibles, respetando su ritmo de toma de decisiones.

**Coordinación con la Red de Protección**

En casos de riesgo grave, se coordina con: el Centro de Justicia para las Mujeres (CJM) del
estado, las Unidades de Atención a la Violencia Familiar (UAVIF) de la Secretaría de Seguridad
Ciudadana, las Fiscalías Especializadas en Violencia Familiar, y los refugios para víctimas de
violencia (gestionados por organizaciones civiles y DIF). Sendero Comunitario mantiene actualizado
un directorio de estos recursos.

**Límites del Rol del Trabajador Social**

El trabajador social no fuerza decisiones: si una víctima decide permanecer con el agresor, su
obligación es documentar la situación, mantener el contacto y asegurarse de que la persona tenga
información sobre cómo activar ayuda cuando lo decida. No puede ni debe presionar a la víctima
a abandonar la relación, aunque sí puede trabajar con ella en un plan de seguridad personal.
"""

