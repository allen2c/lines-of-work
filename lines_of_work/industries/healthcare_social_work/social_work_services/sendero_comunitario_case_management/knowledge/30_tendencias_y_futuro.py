title = "Tendencias y Retos del Trabajo Social Contemporáneo"

content = """
El trabajo social enfrenta en el siglo XXI un conjunto de retos globales y locales que demandan
actualización constante, pensamiento crítico y capacidad de adaptación. Conocer estas tendencias
permite anticipar cambios y posicionar a Sendero Comunitario como una organización capaz de
responder a las necesidades del futuro.

**Cambio Climático y Vulnerabilidad Social**

Los efectos del cambio climático —inundaciones, sequías, olas de calor— impactan de forma
desproporcionada a las personas en situación de vulnerabilidad. Las familias que viven en zonas
de riesgo geológico o hídrico en la Ciudad de México (barrancas, cañadas, zonas de hundimiento)
son especialmente vulnerables. El trabajo social debe incorporar la dimensión ecológica en la
evaluación de riesgos y articularse con la gestión de riesgo de desastres.

**Envejecimiento Demográfico**

México está en proceso de transición demográfica acelerada. Para 2050, más del 20% de la
población tendrá más de 60 años. Esto implica una demanda creciente de servicios sociales para
adultos mayores, especialmente en materia de cuidados de larga duración, en un contexto donde
el Estado aún no ha desarrollado un sistema universal de cuidados. El trabajo social tendrá un
papel central en este campo.

**Migración y Desplazamiento Forzado**

Los flujos migratorios globales están aumentando, impulsados por violencia, pobreza, cambio
climático y persecución. Ciudad de México recibirá una proporción creciente de personas
migrantes y solicitantes de asilo. Esto demanda mayor especialización en trabajo social con
población migrante y mayor coordinación con organismos internacionales como el ACNUR y la OIM.

**Digitalización y Nuevas Formas de Exclusión**

La digitalización de los trámites y servicios públicos abre oportunidades pero también crea
nuevas barreras para personas sin acceso o habilidades digitales. El trabajo social tiene
responsabilidad en la alfabetización digital básica de sus usuarios y en la denuncia de modelos
de servicio público que excluyen a los más vulnerables.

**Salud Mental como Prioridad Social**

La pandemia de COVID-19 generó una crisis global de salud mental que afectó especialmente a
jóvenes, mujeres y personas en situación de pobreza. La demanda de servicios de salud mental
superó con creces la capacidad instalada. El trabajo social debe fortalecer su rol de puerta de
entrada y gestor de acceso a la salud mental, y participar activamente en la defensa de mayor
inversión pública en este campo.

**Trabajo Social y Justicia Social**

La profesión tiene una tradición histórica de compromiso con la justicia social. En un contexto
de creciente desigualdad, polarización política y erosión de derechos, este compromiso es más
necesario que nunca. Los trabajadores sociales no son técnicos neutros: son profesionales con
una posición ética respecto al tipo de sociedad en que quieren vivir y trabajar.
"""

