title = "Fundamentos del Trabajo Social Comunitario"

content = """
El trabajo social es una profesión basada en la práctica y una disciplina académica que promueve
el cambio y el desarrollo social, la cohesión social, el empoderamiento y la liberación de las
personas. Los principios de justicia social, derechos humanos, responsabilidad colectiva y respeto
por la diversidad son fundamentales para el trabajo social.

**Perspectiva Biopsicosocial**

El trabajo social comunitario parte de una comprensión integral de la persona: considera sus
condiciones físicas de salud, su estado emocional y psicológico, y el entorno social, familiar
y económico en el que vive. Esta perspectiva impide reducir los problemas sociales a causas únicas
y obliga a buscar soluciones multidimensionales.

**Enfoque de Derechos**

Toda intervención en Sendero Comunitario se asienta en el enfoque de derechos humanos. Los
usuarios no son receptores pasivos de ayuda, sino titulares de derechos que el Estado y las
instituciones tienen la obligación de garantizar. El trabajador social actúa como facilitador
del ejercicio efectivo de esos derechos, no como benefactor.

**Intervención Basada en Fortalezas**

En lugar de centrar la evaluación en déficits y carencias, el modelo de fortalezas identifica
los recursos, habilidades y redes de apoyo que ya posee el usuario. Esto fortalece la autoestima,
promueve la agencia personal y produce resultados más sostenibles que los enfoques centrados
exclusivamente en problemas.

**Ética de la No Directividad**

El trabajador social no impone soluciones. Su papel es ampliar el abanico de opciones, informar
sobre consecuencias y acompañar la toma de decisiones. El respeto a la autodeterminación del
usuario es un valor irrenunciable, incluso cuando sus decisiones no coincidan con la opinión
profesional, siempre que no exista riesgo de daño para sí mismo o terceros.
"""

