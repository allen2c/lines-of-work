content = """
Ciudad de México es un destino y punto de tránsito para migrantes internacionales, principalmente
de Centroamérica y Sudamérica, así como para migrantes internos desplazados por violencia o
pobreza. La atención a esta población requiere conocimiento específico del marco migratorio y
sensibilidad cultural.

**Marco Legal: Ley de Migración y Ley sobre Refugiados**

La Ley de Migración (2011) reconoce los derechos de los migrantes independientemente de su
estatus migratorio. La Ley sobre Refugiados, Protección Complementaria y Asilo Político (2011)
regula el proceso de reconocimiento de la condición de refugiado. La Comisión Mexicana de Ayuda
a Refugiados (COMAR) es la autoridad competente en materia de asilo. Los migrantes en situación
irregular tienen derecho a servicios de salud de emergencia y a la atención de sus menores hijos
en el sistema educativo, independientemente de su estatus.

**Proceso de Solicitud de Asilo**

Una persona que huyó de su país por persecución fundada en razón de raza, religión, nacionalidad,
pertenencia a determinado grupo social u opiniones políticas puede solicitar la condición de
refugiado ante la COMAR. El plazo para presentar la solicitud es de 30 días hábiles desde la
entrada al país o desde que surgieron las condiciones que impidieron solicitarla antes. El
trabajador social puede acompañar al usuario en este proceso y orientarle sobre la documentación
necesaria.

**Necesidades Prioritarias de la Población Migrante**

Las necesidades más urgentes incluyen: alojamiento temporal, alimentos, atención médica, acceso
a la justicia en caso de haber sido víctima de delitos durante el tránsito, y apoyo psicosocial
para quienes han vivido experiencias traumáticas. El trauma por tránsito migratorio (violencia
de grupos delictivos, extorsión, separación familiar) es frecuente y requiere evaluación cuidadosa.

**Red de Apoyo a Migrantes en CDMX**

Los principales recursos incluyen: la Casa de Acogida, Formación y Empoderamiento (CAFE) para
mujeres migrantes, el Programa Sembrando Vida y comedores comunitarios con acceso para migrantes,
Sin Fronteras IAP para orientación jurídica migratoria, el ACNUR (Agencia de la ONU para los
Refugiados) con sede en México, y Casa Refugiados para personas con reconocimiento de la condición
de refugiado.

**Sensibilidad Cultural**

El trabajo con migrantes requiere conciencia de las diferencias culturales en la expresión del
malestar, los roles de género, la religiosidad y la concepción de la familia. Las barreras
lingüísticas (dialectos indígenas centroamericanos, idiomas no hispanohablantes) deben abordarse
con el apoyo de intérpretes cuando sea necesario. El etnocentrismo y los estereotipos sobre
población migrante son obstáculos que el profesional debe examinar críticamente en su propia
práctica.
"""

