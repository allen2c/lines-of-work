title = "Técnicas de Entrevista en Trabajo Social"

content = """
La entrevista es el instrumento técnico más utilizado en el trabajo social. Su calidad determina
la profundidad de la evaluación, la calidad de la relación profesional y la efectividad de la
intervención.

**Escucha Activa**

La escucha activa implica prestar atención plena al contenido verbal, al tono emocional y al
lenguaje corporal del usuario, suspendiendo el juicio y la formulación prematura de soluciones.
Se expresa mediante el contacto visual, los gestos de asentimiento, las respuestas breves que
indican comprensión ("entiendo", "sí") y el silencio tolerado, que permite al usuario profundizar
sin presión.

**Reformulación y Paráfrasis**

Reformular consiste en devolver al usuario lo que acaba de expresar con palabras diferentes pero
sin cambiar el sentido. Tiene tres funciones: verificar que el profesional comprendió
correctamente, demostrar que fue escuchado y ayudar al usuario a escucharse a sí mismo.
Ejemplo: "Si entiendo bien, lo que me estás diciendo es que aunque quieres mejorar la situación
con tu pareja, sientes que no tienes fuerzas para iniciar ese proceso. ¿Es así?"

**Preguntas Abiertas y Cerradas**

Las preguntas abiertas ("¿Cómo describirías tu situación en casa?") favorecen la narrativa libre
y la exploración. Las preguntas cerradas ("¿Tienes trabajo actualmente?") obtienen datos concretos
rápidamente. Una entrevista efectiva combina ambos tipos, comenzando generalmente con abiertas y
usando cerradas para clarificar detalles.

**Preguntas Circulares**

Técnica proveniente de la terapia sistémica: en lugar de preguntar directamente al usuario cómo
se siente, se le pregunta cómo cree que lo perciben los demás. Ejemplo: "¿Qué crees que piensan
tus hijos de esta situación?" Estas preguntas activan perspectivas nuevas y revelan dinámicas
relacionales que el usuario no expresaría directamente.

**Manejo del Silencio**

El silencio no es ausencia de comunicación: frecuentemente indica que el usuario está procesando
emocionalmente algo importante. El profesional debe tolerar los silencios sin apresurarse a
rellenarlos con preguntas o comentarios. Un silencio de 10-20 segundos puede ser muy productivo.

**Señales de Alerta en la Entrevista**

El trabajador social debe atender indicadores que pueden pasar desapercibidos: contradicciones en
el relato, minimización excesiva de problemas graves, signos de miedo o vergüenza cuando se
tocan ciertos temas, o presencia de un acompañante que responde por el usuario y lo interrumpe.
Estos patrones pueden indicar situaciones de control o violencia no declarada.
"""

