title = "Recepción y Acogida de Usuarios"

content = """
La recepción es el primer punto de contacto entre el usuario y Sendero Comunitario. Una acogida
adecuada establece la confianza necesaria para el trabajo posterior y puede determinar si el
usuario continúa con el proceso de intervención o lo abandona.

**Entorno Físico y Emocional**

El espacio de recepción debe ser privado, cómodo y libre de interrupciones. Si la entrevista
inicial ocurre en terreno (visita domiciliaria, espacio público), el profesional debe buscar
un lugar discreto y tranquilo. El lenguaje no verbal del trabajador social —postura abierta,
contacto visual respetuoso, tono de voz calmado— comunica disponibilidad y calidez antes de
que se pronuncie cualquier palabra.

**Protocolo de Bienvenida**

1. Presentarse con nombre y función dentro de la organización.
2. Explicar brevemente en qué consiste Sendero Comunitario y qué servicios ofrece.
3. Informar sobre la confidencialidad y sus límites.
4. Invitar al usuario a compartir el motivo de su consulta sin interrupciones durante los primeros
   minutos (escucha activa no directiva).

**Registro Inicial**

Durante o al término de la entrevista de acogida, se completa el Formulario de Registro de
Usuario: datos de identificación, motivo de consulta, situación familiar básica, datos de
contacto y fuente de derivación. Este registro activa la apertura del expediente y la asignación
a un trabajador social responsable del caso.

**Valoración Preliminar de Riesgo**

Ya en la primera entrevista, el profesional identifica señales de alerta que requieren atención
urgente: indicios de violencia activa, menores en situación de desamparo, riesgo suicida o
conducta autolesiva, o necesidades básicas no cubiertas (sin vivienda, sin alimento). Ante
cualquiera de estas señales, se activa el protocolo de atención prioritaria antes de concluir
la sesión de acogida.

**Cierre de la Sesión de Acogida**

Al finalizar, se informa al usuario sobre los pasos siguientes: cuándo será contactado, qué
documentos puede traer a la próxima cita y qué puede esperar del proceso. Se le proporciona
un número de contacto de la organización para consultas urgentes entre sesiones.
"""

