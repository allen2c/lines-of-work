title = "Gestión de Casos: Modelo y Fases"

content = """
La gestión de casos (case management) es el método central de trabajo en Sendero Comunitario.
Consiste en coordinar de manera planificada y continua todos los servicios y apoyos que requiere
un usuario, actuando como punto de articulación entre la persona y la red de recursos disponibles.

**Principios del Modelo**

El modelo adoptado por Sendero Comunitario integra elementos del enfoque centrado en la persona,
el modelo de fortalezas y la gestión orientada a resultados. Cada caso tiene un gestor responsable
que mantiene la continuidad del vínculo profesional con el usuario, evitando la fragmentación que
surge cuando múltiples profesionales atienden sin coordinación.

**Fase 1 — Apertura del Caso**

Se abre el expediente, se asigna el gestor de caso y se realiza la evaluación biopsicosocial
inicial. Se define el nivel de intensidad del caso: básico (orientación puntual), estándar
(seguimiento mensual) o intensivo (contacto semanal o visitas domiciliarias frecuentes).

**Fase 2 — Diseño del Plan de Intervención**

El plan de intervención individual (PII) se elabora en co-construcción con el usuario. Incluye:
objetivos a corto, mediano y largo plazo; acciones concretas a realizar por el usuario, el
gestor y terceras instituciones; recursos a movilizar; plazos estimados; e indicadores de
avance. El usuario firma el PII como acto de compromiso y apropiación.

**Fase 3 — Implementación y Seguimiento**

El gestor coordina el acceso a los servicios acordados, media cuando surgen obstáculos y
mantiene contacto periódico con el usuario. El seguimiento incluye entrevistas presenciales,
llamadas telefónicas, visitas domiciliarias y coordinación con otros prestadores de servicios.
Los avances y obstáculos se registran en el expediente.

**Fase 4 — Revisión y Ajuste**

Cada tres meses se realiza una revisión formal del PII. Si los objetivos fueron alcanzados, se
actualizan; si persisten barreras, se analizan y se ajustan las estrategias. La revisión se
realiza siempre con el usuario presente.

**Fase 5 — Cierre del Caso**

El cierre ocurre cuando los objetivos del PII fueron alcanzados, el usuario ya no requiere apoyo
activo, o decide voluntariamente discontinuar el servicio. El cierre incluye una sesión de balance
con el usuario, el registro de resultados en el expediente y la evaluación de satisfacción. Los
casos cerrados pueden reabrirse si el usuario regresa con nuevas necesidades.
"""

