content = """
El trabajo social con poblaciones en situación de alta vulnerabilidad conlleva un riesgo
significativo de desgaste emocional, fatiga por compasión y burnout. El autocuidado no es un
lujo opcional: es una responsabilidad ética del profesional y una condición para ofrecer un
servicio de calidad.

**Fatiga por Compasión y Trauma Vicario**

La fatiga por compasión es el agotamiento emocional que resulta de la exposición continua al
sufrimiento ajeno. El trauma vicario ocurre cuando el profesional internaliza aspectos traumáticos
de las experiencias de sus usuarios, desarrollando síntomas similares a los del trastorno por
estrés postraumático: intrusiones, hipervigilancia, evitación. Son riesgos reales en el trabajo
social, especialmente en contextos de violencia, abuso infantil y pobreza extrema.

**Síntomas de Desgaste Profesional**

Señales de alerta que el trabajador social debe reconocer en sí mismo: dificultad para desconectar
del trabajo al salir de la jornada, pesadillas o pensamientos intrusivos sobre casos de usuarios,
irritabilidad o impaciencia con usuarios que antes no generaban esa reacción, cinismo creciente
respecto a la posibilidad de cambio, sensación de vacío o falta de sentido en el trabajo,
problemas de sueño, y uso de alcohol u otras sustancias como mecanismo de afrontamiento.

**Estrategias de Autocuidado Individual**

Las prácticas de autocuidado deben ser regulares y sostenidas, no solo ante una crisis. Incluyen:
establecer límites claros entre la vida laboral y personal (horarios, no llevar trabajo a casa
cuando es posible), prácticas de ejercicio físico regular, técnicas de relajación o mindfulness,
inversión en relaciones sociales y actividades de ocio no relacionadas con el trabajo, y buscar
apoyo psicológico propio cuando se detectan señales de desgaste.

**Supervisión Clínica**

La supervisión clínica —reuniones periódicas con un profesional más experimentado para revisar
casos y reflexionar sobre la práctica— es el mecanismo institucional más importante de apoyo al
trabajador social. En Sendero Comunitario, la supervisión individual es mensual y la supervisión
de equipo es quincenal. La supervisión no es un espacio de evaluación punitiva, sino de apoyo
al aprendizaje y al bienestar profesional.

**Responsabilidad Institucional**

El autocuidado no puede recaer exclusivamente en el trabajador social individual. La institución
también tiene responsabilidades: cargas de caso razonables (no más de 30-35 casos activos por
gestor en el modelo de Sendero Comunitario), acceso a supervisión clínica de calidad, formación
continua, condiciones laborales dignas (contrato, prestaciones, salario justo) y cultura
organizacional que normalice hablar del impacto emocional del trabajo.
"""

