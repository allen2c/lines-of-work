content = """
El conocimiento profundo de los recursos disponibles en la comunidad es uno de los activos más
valiosos del trabajador social. Sin ese conocimiento, la evaluación biopsicosocial más precisa
resulta estéril: no puede traducirse en apoyo real para el usuario.

**Tipos de Recursos Comunitarios**

Los recursos se clasifican en: institucionales formales (servicios gubernamentales: DIF, IMSS,
Secretaría de Bienestar, Secretaría de Salud), organizaciones de la sociedad civil (ONG, iglesias
con programas sociales, grupos de autoayuda), recursos informales (redes familiares, vecinales,
redes religiosas o de barrio), y recursos económicos (subsidios, becas, apoyos en especie).

**Directorio de Recursos de Sendero Comunitario**

Sendero Comunitario mantiene un directorio actualizado semestralmente de recursos clasificados
por: tipo de servicio, población objetivo, zona geográfica de cobertura, requisitos de acceso,
costos, datos de contacto y persona de referencia. Este directorio es una herramienta de trabajo
cotidiana, no un documento estático. Cada trabajador social es responsable de reportar cambios
(cierre de servicios, modificación de requisitos) al equipo de coordinación.

**Derivación Efectiva**

La derivación no es simplemente entregar un papel con una dirección. Una derivación efectiva
incluye: explicar al usuario en qué consiste el servicio y qué puede esperar, verificar que cumple
los requisitos de acceso, ayudarle a obtener la documentación necesaria, comunicarse previamente
con el servicio receptor cuando el caso es complejo, y hacer seguimiento posterior para verificar
que el usuario llegó y fue atendido.

**Barreras de Acceso más Frecuentes**

Las barreras que impiden a los usuarios acceder a recursos incluyen: falta de documentos de
identidad, distancias geográficas y costos de transporte, horarios incompatibles con el trabajo,
desconocimiento de los derechos y los procedimientos, estigma asociado a ciertos servicios
(salud mental, adicciones), y experiencias previas negativas con instituciones.

**Construcción de Redes Interinstitucionales**

Más allá del directorio, el trabajador social cultiva relaciones directas con profesionales de
referencia en otras instituciones. Saber con quién hablar en el DIF local, en el centro de salud
del barrio o en la escuela donde van los hijos de un usuario permite resolver obstáculos que
ningún reglamento contempla. Estas redes se construyen con el tiempo, mediante el trato correcto
y la reciprocidad profesional.
"""

