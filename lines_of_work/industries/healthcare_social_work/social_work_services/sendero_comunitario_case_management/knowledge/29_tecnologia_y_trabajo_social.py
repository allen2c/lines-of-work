title = "Tecnología e Innovación en el Trabajo Social"

content = """
La tecnología está transformando la práctica del trabajo social: desde la gestión digital de
expedientes hasta el uso de plataformas de atención remota. La adopción crítica de estas
herramientas —aprovechando sus beneficios sin ignorar sus riesgos— es una competencia creciente
del profesional.

**Sistemas de Gestión de Casos**

Sendero Comunitario opera con un sistema de gestión de casos digitalizado que permite: registro
de usuarios, apertura y seguimiento de expedientes, generación de alertas de seguimiento, reportes
de actividad por trabajador social, y estadísticas agregadas para la evaluación de programas.
La adopción de estos sistemas reduce el tiempo administrativo y mejora la calidad de la información,
pero requiere formación y disciplina en el uso.

**Atención Remota y Telecomunicaciones Sociales**

La pandemia de COVID-19 aceleró la adopción de atención remota en trabajo social: llamadas
telefónicas, videollamadas y mensajería instantánea como canales de seguimiento. La atención
remota amplía el alcance del servicio (usuarios con dificultades de movilidad o transporte)
pero tiene limitaciones: no permite observar el entorno del usuario, reduce las señales no
verbales disponibles y puede excluir a personas sin acceso a tecnología o habilidades digitales.

**Brecha Digital y Exclusión Tecnológica**

Las poblaciones más vulnerables son frecuentemente las que tienen menor acceso a tecnología:
personas mayores, personas en situación de calle, familias en pobreza extrema. El uso de
tecnología como único canal de atención profundiza la exclusión. Sendero Comunitario mantiene
la atención presencial como opción siempre disponible y ha equipado sus sedes con computadoras
de acceso libre para que los usuarios puedan realizar trámites en línea con asistencia del
trabajador social.

**Redes Sociales y Comunicación Comunitaria**

Las redes sociales (WhatsApp, Facebook) son herramientas reales de comunicación comunitaria.
Sendero Comunitario las usa para: difundir información sobre derechos y servicios, comunicarse
con usuarios que no tienen teléfono fijo, y mantener el contacto con grupos y líderes comunitarios.
Sin embargo, la comunicación por redes sociales con usuarios activos debe encuadrarse en límites
claros: horarios, contenido y nivel de formalidad, para evitar la difuminación de los límites
del rol profesional.

**Consideraciones Éticas del Uso de Tecnología**

El uso de tecnología plantea cuestiones éticas específicas: ¿cómo se protegen los datos del
usuario en sistemas digitales? ¿Qué pasa si el sistema es hackeado? ¿Es ético grabar una sesión
de atención sin consentimiento del usuario? ¿Pueden usarse redes sociales para verificar
información del usuario sin su conocimiento? El trabajador social debe conocer los principios
de privacidad digital y las políticas de seguridad de la información de la organización.
"""

