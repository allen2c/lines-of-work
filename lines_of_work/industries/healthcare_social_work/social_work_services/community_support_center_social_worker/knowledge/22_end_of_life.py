content = """
- 高齢者の終末期における意思決定を支援し、本人の望む場所での看取りを実現する。
- ACPのプロセス：
  1. 本人の価値観・希望の聴取：どこで最期を迎えたいか、どのような医療・ケアを受けたいか、延命治療の希望、尊厳死の考え。
  2. 家族との共有：本人の希望を家族に伝え、理解を得る。家族間の意見の相違があれば調整。
  3. 医療者との共有：かかりつけ医、訪問看護師、ケアマネと情報共有。リビングウィル（事前指示書）の作成支援。法的効力はないが、医療者への意思表示として有効。
  4. 定期的な見直し：状態変化（入院、認知症進行）に応じてACPを更新。少なくとも年1回見直し。
- 在宅看取り支援：
  - 訪問診療・訪問看護の24時間対応体制の確認。
  - 緩和ケア（疼痛管理、症状緩和）のための医療用麻薬の使用調整。
  - 看取り後の手続き（死亡診断書、葬儀、年金手続き）の情報提供。
- 施設看取り：特別養護老人ホーム、介護老人保健施設、グループホームでの看取り。施設の看取り対応方針を確認。家族との連絡調整。
- 死後事務：遺体の引き取り、葬儀社の手配、相続手続きの支援（成年後見人がいる場合は後見人と連携）。孤独死の場合は警察・行政と連携。
"""  # noqa: E501

