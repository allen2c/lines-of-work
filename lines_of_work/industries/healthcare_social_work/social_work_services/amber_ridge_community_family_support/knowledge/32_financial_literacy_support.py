title = "Financial Literacy and Budget Support"

content = """
Many families seeking support face financial stress. Helping them build basic financial
literacy and connect to income supports can reduce anxiety and create stability for other
goals.

**Budget Basics**

Support families to track income and expenses, distinguish needs from wants, and prioritise
essential costs. Use simple tools: spreadsheets, apps, or paper ledgers. Avoid judgment;
many families have never had formal financial education.

**Income Supports**

Know the eligibility criteria and application processes for: tax credits, child benefit,
housing benefit, disability benefits, and emergency grants. Help families gather documents
and complete applications. Follow up to ensure claims are processed.

**Debt and Creditors**

Families in debt often feel shame. Normalise seeking help. Explain options: debt advice
services, payment plans, and in some cases insolvency. Never advise on complex debt
structures; refer to specialist agencies.

**Savings and Resilience**

Even small savings buffers reduce crisis vulnerability. Encourage goal-based saving where
possible. Connect families to credit unions or savings schemes that avoid predatory
lending.
"""

