"""Program evaluation and outcome measurement in family support."""

title = "Program Evaluation and Outcome Measurement"

content = """
Evaluation ensures that services are effective and accountable. Family support programs
must collect data on outputs (activities delivered) and outcomes (changes in family
wellbeing) and use this information to improve practice.

**Outcome Domains**

Relevant outcomes for family support include: improved parenting skills, reduced stress,
increased social connectedness, stable housing, improved child development, and reduced
risk of maltreatment. Select measures that align with program goals and are feasible
to collect.

**Data Collection Methods**

Use a mix of methods: standardised instruments (e.g. parenting stress index), client
satisfaction surveys, qualitative interviews, and administrative data. Ensure that
data collection does not burden families or detract from service delivery. Obtain
informed consent for evaluation activities.

**Using Data for Improvement**

Review data regularly with the team. Identify trends, successes, and areas for improvement.
Share findings with stakeholders. Use evaluation results to refine interventions, advocate
for resources, and demonstrate impact to funders.

**Ethical Considerations**

Protect participant confidentiality. Report findings in aggregate form. Avoid using
evaluation to penalise individual workers or families. Balance accountability with
the primacy of the helping relationship.
"""

