title = "Child Protection"

content = """
Family support practitioners have a duty to protect children from harm. This includes
mandatory reporting, risk assessment, and collaboration with child protection authorities.

**When to Report**

Report when there is reasonable cause to believe a child has been abused or neglected.
Reasonable cause means a prudent person would suspect abuse based on the information
available. When in doubt, consult with a supervisor; in many jurisdictions, consultation
does not substitute for reporting when thresholds are met.

**Working with Child Protection**

When a report is made, child protection may investigate. Maintain professional communication
with child protection workers. Share relevant information with consent or as permitted by
law. Support the family through the process while upholding the child's safety.

**Supporting Families After a Report**

Families may feel betrayed or angry. Acknowledge their feelings, explain your legal
obligation, and continue to offer support where appropriate. The goal is child safety
and family preservation when both can be achieved.
"""

