"""Inter-agency coordination and referral networks."""

title = "Inter-Agency Coordination"

content = """
Families often need support from multiple systems: health, education, housing, employment,
and child welfare. Effective coordination ensures that services are complementary, gaps
are filled, and families are not overwhelmed by fragmented or conflicting interventions.

**Building and Maintaining Networks**

Develop relationships with key personnel in other agencies: schools, hospitals, housing
authorities, employment services, and child protection. Know their eligibility criteria,
application processes, and contact points. Attend inter-agency meetings and participate
in community planning tables.

**Warm Referrals**

A warm referral means more than handing a family a phone number. Contact the receiving
agency with the family's consent, provide a brief summary of needs, and facilitate the
introduction. Follow up to confirm the family was seen and to address any barriers.

**Information Sharing**

Respect confidentiality while sharing information necessary for coordinated care. Obtain
written consent before releasing information to other agencies. Document what was shared,
with whom, and when. Use release forms that specify the purpose and scope of disclosure.

**Resolving Conflicts**

When agencies have conflicting mandates or recommendations, advocate for the family's
best interest. Convene case conferences when needed. Escalate to supervisors when
coordination breaks down or when systemic barriers persist.
"""

