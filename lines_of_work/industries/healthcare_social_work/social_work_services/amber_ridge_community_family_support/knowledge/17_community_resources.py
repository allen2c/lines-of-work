"""Knowledge item: community resources."""

title = "Mapping and Accessing Community Resources"

content = """
A core function of family support is connecting families with resources that meet their
needs. Workers maintain up-to-date knowledge of local programs: eligibility criteria,
application processes, contact information, and common barriers. A well-maintained resource
directory is an essential tool.

**Types of Resources**

Government programs include TANF, SNAP, Medicaid, CHIP, housing vouchers, LIHEAP, and
child care subsidies. Nonprofits offer food pantries, clothing banks, utility assistance,
and emergency funds. Community organizations provide parenting classes, support groups,
and recreational programs. Schools, libraries, and faith communities often have
additional supports.

**Effective Referral Practices**

Referral is not merely handing someone a phone number. Explain what the resource offers,
whether the family likely qualifies, what documents they need, and what to expect.
When possible, make a warm referral—contact the receiving agency with the family's
permission. Follow up to confirm the family connected and address any obstacles.

**Barriers to Access**

Common barriers include: complex applications, long waitlists, transportation, childcare,
work schedule conflicts, stigma, and past negative experiences. Workers advocate, assist
with applications, and help families navigate systems. Document barriers to inform
program improvement efforts.
"""

