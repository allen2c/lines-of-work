title = "Safety Planning in Domestic Violence"

content = """
Safety planning is a collaborative process to reduce risk for victims and children when
leaving or remaining in an abusive relationship. It is not a one-off document but an
ongoing, dynamic process.

**Risk Assessment**

Use validated tools (e.g. DASH, SARA) where appropriate. Consider: escalation of
violence, access to weapons, separation assault, child abuse, and perpetrator's
history. High-risk cases require multi-agency response (MARAC).

**Leaving Safely**

Plan for: documents, keys, money, medications, and essential belongings. Identify safe
accommodation. Consider timing—leaving can be the most dangerous period. Never pressure
a victim to leave before they are ready; support their autonomy.

**Staying Safely**

Some victims choose to remain. Plan for: safe rooms, code words with children, emergency
contacts, and escape routes. Document incidents. Maintain regular contact.

**Children**

Include children in age-appropriate safety planning. Ensure they know how to call for
help. Consider impact of trauma and need for therapeutic support.
"""

