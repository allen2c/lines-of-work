title = "Professional Boundaries"

content = """
Clear boundaries protect both workers and families. They enable consistent, ethical
practice and prevent burnout or inappropriate relationships.

**Time and Availability**

Set clear expectations for contact hours, response times, and out-of-hours availability.
Avoid creating dependency through over-availability. Protect your own rest and
recovery time.

**Personal Information**

Share only what is professionally relevant. Avoid over-disclosure of your own life.
Maintain appropriate emotional distance while remaining warm and human. Do not seek
emotional support from families.

**Gifts and Favours**

Most organisations have policies on gifts. Small tokens of thanks may be acceptable;
substantial gifts or favours create obligation. Decline offers that could compromise
objectivity. Document any exceptions.

**Dual Relationships**

Avoid working with families where you have a personal connection. If unavoidable,
declare the conflict and seek supervision. Never exploit the power imbalance for
personal gain.
"""

