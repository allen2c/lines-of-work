title = "Working with Families"

content = """
Effective family support requires understanding family dynamics, roles, and communication
patterns. One-size-fits-all approaches fail.

**Family Systems Perspective**

Families function as systems: change in one member affects others. Consider how interventions
impact the whole family. Avoid triangulation (being drawn into family conflicts as an ally
of one member against another). Work to strengthen the family unit while respecting
individual needs.

**Engaging Reluctant or Resistant Families**

Some families are mandated to services or come under pressure. Acknowledge their feelings,
explore what they want (even if limited), and find small areas of agreement. Coercion
rarely produces lasting change; building voluntary engagement takes time.

**Involving All Relevant Members**

When appropriate, involve both parents, extended family, or other caregivers. Ensure
communication is inclusive and that no one is excluded without good reason.
"""

