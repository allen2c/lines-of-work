title = "Interview Techniques"

content = """
Skilled interviewing builds rapport, gathers accurate information, and supports family
engagement. Technique matters as much as content.

**Active Listening**

Give full attention: maintain eye contact (when culturally appropriate), avoid interrupting,
and use minimal encouragers (nodding, brief affirmations). Reflect back what you hear to
confirm understanding and show that the family has been heard.

**Open-Ended Questions**

Use open-ended questions to invite elaboration: "What has that been like for you?" rather
than "Was that difficult?" Avoid leading questions that suggest a preferred answer.

**Sensitivity to Trauma**

Many families have experienced trauma. Avoid re-traumatizing by pacing questions, offering
breaks, and not pushing for details before the family is ready. Notice signs of distress
and respond with empathy and choice.
"""

