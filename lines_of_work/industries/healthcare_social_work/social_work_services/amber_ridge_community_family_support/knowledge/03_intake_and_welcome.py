title = "Intake and Welcome Procedures"

content = """
The intake process sets the tone for the entire family support relationship. A welcoming,
clear, and respectful intake builds trust and ensures accurate information gathering.

**Initial Contact**

When a family first contacts Amber Ridge, staff should respond promptly, introduce themselves
and the organization, and explain what to expect. Offer options for how to proceed: in-person,
phone, or video. Accommodate language needs and accessibility requirements.

**Information Gathering**

Collect essential information: household composition, contact details, primary concerns,
referral source if applicable, and any urgent needs. Use open-ended questions and allow
families to tell their story in their own words. Avoid interrogating; frame questions as
part of understanding how best to help.

**Explaining Services and Limits**

Clearly explain what Amber Ridge can and cannot provide, expected timelines, and the family's
role in the process. Set realistic expectations to prevent disappointment and build a foundation
for collaborative work.
"""

