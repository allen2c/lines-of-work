title = "Advocacy and Empowerment"

content = """
Family support work includes advocating with and on behalf of families to access rights,
services, and fair treatment. The goal is to build families' capacity to advocate for
themselves.

**Individual Advocacy**

Represent families in meetings with schools, housing, benefits, or health services when
they are unable to do so effectively. Prepare them in advance; accompany them when
appropriate. Document decisions and follow up.

**Systemic Advocacy**

Identify patterns of injustice: discriminatory policies, inaccessible services, or
procedural barriers. Raise these with managers, commissioners, or policymakers. Work
with user groups to amplify family voice.

**Empowerment**

Empowerment means families gaining control over their own lives. Avoid creating
dependency. Build skills: letter-writing, negotiation, knowing rights. Celebrate
when families succeed without your intervention.

**Boundaries**

Advocate within your role. Do not provide legal advice; refer to solicitors. Do not
override family decisions; support their choices while ensuring they are informed.
"""

