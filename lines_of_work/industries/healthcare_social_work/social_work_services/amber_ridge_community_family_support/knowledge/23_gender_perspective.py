"""Gender-sensitive practice in family support work."""

title = "Gender Perspective in Family Support"

content = """
Gender shapes access to resources, division of labour, power dynamics, and vulnerability to
violence. A gender-sensitive approach recognises these differences and ensures that support
is equitable and responsive to the needs of all family members.

**Division of Labour**

Caregiving and domestic work often fall disproportionately on women. Assess who performs
which roles in the household and how this affects stress, employment, and wellbeing. Support
families to redistribute tasks when possible and to access respite and childcare.

**Economic Disparities**

Women, particularly single mothers and those from marginalised groups, often face higher
rates of poverty. Ensure that financial support, employment programs, and benefits
information reach all caregivers. Advocate for policies that address the gender pay gap
and support flexible work arrangements.

**Safety and Violence**

Domestic violence disproportionately affects women and children. Screen for abuse in a
safe, private setting. Never assume that the person presenting is the sole decision-maker;
abusers often control family finances and movement. Follow mandatory reporting and safety
planning protocols.

**Inclusive Practice**

Support families with diverse gender identities and structures. Use inclusive language,
respect chosen names and pronouns, and challenge assumptions about family roles. Ensure
that services are welcoming to LGBTQ+ parents and children.
"""

