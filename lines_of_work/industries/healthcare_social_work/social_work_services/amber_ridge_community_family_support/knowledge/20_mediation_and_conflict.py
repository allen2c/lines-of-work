content = """
Families often seek support amid conflict—between parents, between parents and children,
or with extended family. Family support workers may facilitate conversations to de-escalate
tension, clarify interests, and help parties reach mutually acceptable solutions. This is
distinct from formal mediation; workers operate within their scope and refer when
neutral mediation or legal intervention is needed.

**Principles of Conflict Resolution**

Focus on interests, not positions. Help parties identify what they need rather than
digging into fixed demands. Encourage active listening and reflection. Separate the
person from the problem. Generate options before evaluating. Ensure voluntary
participation—no one should feel coerced into agreement.

**When to Refer**

Refer to formal mediation when: parties have equal power and need a binding agreement;
legal matters (custody, divorce) are involved; or conflict involves violence or
imbalance of power. Refer to domestic violence services when safety is a concern.
Workers do not provide legal advice.

**Documentation**

Document the process, agreements reached, and any referrals made. Note if parties
declined to participate or if the worker determined that facilitation was not
appropriate. Maintain neutrality in documentation.
"""

