title = "Confidentiality in Family Support"

content = """
Confidentiality protects family privacy and builds trust. It is not absolute; workers
must balance it with safeguarding duties and information-sharing when necessary.

**Default Position**

Information shared by families is confidential. Do not disclose to third parties
without consent unless there is a legal or safeguarding requirement. Explain
confidentiality and its limits at the outset.

**Limits to Confidentiality**

You must breach confidentiality when: there is risk of serious harm to a child or
vulnerable adult, you are required by law (e.g. court order), or disclosure is
necessary to prevent crime. Explain these limits clearly during intake.

**Information Sharing**

When sharing with other agencies, obtain consent where possible. Share only what is
necessary for the purpose. Record consent and the rationale for any disclosure.
Multi-agency work requires clear agreements on data handling.

**Recording**

Case notes are confidential. Store securely. Access is restricted to those with
legitimate need. Families have rights to access their records in many jurisdictions;
know the local rules.
"""

