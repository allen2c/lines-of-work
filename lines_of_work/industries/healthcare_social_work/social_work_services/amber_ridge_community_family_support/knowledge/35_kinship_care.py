title = "Kinship Care and Extended Family Support"

content = """
When children cannot remain with parents, kinship care—placement with relatives or close
family friends—often provides continuity and cultural connection. Family support workers
help kinship carers navigate systems and access resources.

**Carer Assessment**

Kinship carers may need approval or assessment depending on jurisdiction. Support them
through the process: background checks, home visits, and training requirements. Advocate
for timely decisions that minimise disruption for children.

**Financial and Practical Support**

Kinship carers often receive less support than foster carers. Help them access allowances,
respite, and practical assistance. Many assume care suddenly and need help with
housing, school enrolment, and medical consent.

**Relationship with Parents**

Kinship care creates complex dynamics. Support carers to maintain appropriate boundaries
with parents while facilitating contact when it is in the child's interest. Mediate
conflicts where safe to do so.

**Carer Wellbeing**

Kinship carers experience stress, grief, and role confusion. Connect them to support
groups and respite. Acknowledge the emotional labour of caring for a relative's child.
"""

