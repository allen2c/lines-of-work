"""Methods for assessing family needs and strengths."""

title = "Family Assessment"

content = """
A thorough family assessment informs the support plan and ensures services match actual needs.
Assessment is ongoing, not a one-time event.

**Domains of Assessment**

Consider multiple domains: basic needs (housing, food, income), physical and mental health,
parenting capacity, child development, safety and risk, social supports, and cultural and
linguistic factors. No single domain tells the full story; patterns across domains matter.

**Tools and Instruments**

Use validated tools where appropriate (e.g., developmental screens, depression screens) but
do not rely on checklists alone. Combine structured instruments with conversational assessment
and observation. Document both quantitative results and qualitative observations.

**Engaging Families in Assessment**

Families are experts on their own situation. Invite them to identify strengths, concerns,
and priorities. Co-create the assessment narrative rather than imposing an external frame.
"""

