title = "Case Documentation and Record-Keeping"

content = """
Accurate, timely documentation protects clients, supports continuity of care, and fulfills
legal and funding requirements. Family support workers document assessments, plans,
contacts, and significant events in accordance with agency policies and professional
standards.

**What to Document**

Record: dates and nature of contacts, who was present, key content of conversations,
decisions made, referrals provided, follow-up needed, and any safety concerns. Use objective,
factual language. Avoid speculation, value judgments, or diagnostic language unless
qualified. Note when information is reported by the client versus observed directly.

**Confidentiality**

Client information is confidential. Share only with authorized parties and only as much
as necessary. Obtain written consent for releases of information. Be aware of exceptions:
mandatory reporting of child abuse, elder abuse, or imminent harm; court orders; and
certain funding requirements. Never discuss cases in public or on unsecured platforms.

**Quality Standards**

Documentation should be legible, dated, and signed. Corrections are made by drawing a
line through the error, initialing, and adding the correction—never erasing. Electronic
records require secure access and proper log-off. Retain records according to agency
retention schedules and legal requirements.
"""

