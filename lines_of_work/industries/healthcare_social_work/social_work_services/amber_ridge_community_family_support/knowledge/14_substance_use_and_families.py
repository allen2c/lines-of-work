"""Knowledge item: substance use and families."""

title = "Substance Use and Family Impact"

content = """
Substance use disorders affect millions of families. Family support workers encounter
families where one or more members struggle with alcohol, opioids, or other drugs. A
non-judgmental, harm-reduction-informed approach supports engagement and positive outcomes.

**Understanding the Impact**

Substance use can lead to financial instability, neglect, domestic violence, and child
welfare involvement. Children may experience trauma, role reversal (parentification), or
developmental delays. Families often feel shame and isolation. Workers acknowledge these
realities while emphasizing that recovery is possible and that families can be a source
of strength.

**Harm Reduction Principles**

Harm reduction prioritizes meeting people where they are. Goals may include reduced use,
safer use, or abstinence—depending on the individual's readiness. Workers connect families
with treatment options (outpatient, inpatient, medication-assisted treatment), support
groups (Al-Anon, SMART Recovery Family), and overdose prevention resources.

**Child Safety**

When substance use poses risk to children, workers must balance support with mandatory
reporting obligations. Document concerns, consult supervision, and follow child welfare
protocols. Supporting the family's engagement in treatment can improve outcomes and reduce
the likelihood of removal.
"""

