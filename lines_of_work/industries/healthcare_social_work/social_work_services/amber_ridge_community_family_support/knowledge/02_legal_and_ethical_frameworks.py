title = "Legal and Ethical Frameworks"

content = """
Family support practitioners operate within a framework of laws, regulations, and professional
ethics that protect both families and practitioners.

**Mandatory Reporting**

In most jurisdictions, practitioners are legally required to report suspected child abuse or
neglect to child protection authorities. Amber Ridge staff must know the exact thresholds
and procedures for their region. Failure to report can result in legal liability; over-reporting
without reasonable cause can harm families. Document the basis for any report.

**Confidentiality and Consent**

Information shared by families is confidential unless disclosure is required by law (e.g.,
mandatory reporting) or the family gives informed consent. Explain limits of confidentiality
at intake. When sharing information with other agencies, obtain written consent unless a
legal exception applies.

**Professional Boundaries**

Maintain clear professional boundaries: no dual relationships, no personal favors that create
dependency, no social media connections with clients. Boundaries protect both the family and
the practitioner from blurred roles and potential exploitation.
"""

