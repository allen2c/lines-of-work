title = "Support Planning"

content = """
A family support plan is a living document that outlines goals, actions, and responsibilities.
It should be developed collaboratively and reviewed regularly.

**Goal Setting**

Goals should be specific, measurable, achievable, relevant, and time-bound (SMART) where
appropriate, but also meaningful to the family. Avoid imposing professional goals that
families do not own. Start with what the family wants to achieve.

**Action Steps**

Break goals into concrete action steps. Specify who does what, by when, and what resources
or supports are needed. Assign responsibilities clearly; avoid vague commitments.

**Review and Adjustment**

Plans are not static. Schedule regular reviews to assess progress, celebrate successes,
and adjust when circumstances change or barriers emerge. Document changes and the rationale.
"""

