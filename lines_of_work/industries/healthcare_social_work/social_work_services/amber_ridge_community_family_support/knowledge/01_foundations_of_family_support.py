"""Foundational concepts of family support work."""

title = "Foundations of Family Support Work"

content = """
Family support work is a practice-based profession that promotes family well-being, strengthens
parenting capacity, and connects families to community resources. The principles of family
centeredness, strengths-based practice, and cultural humility are central to effective family
support.

**Family-Centered Approach**

Family support recognizes the family as the primary unit of care and decision-making. Practitioners
work alongside families rather than prescribing solutions. Families define their own goals;
the worker facilitates access to resources, builds skills, and removes barriers. This approach
honors parental expertise and promotes sustainable outcomes.

**Strengths-Based Practice**

Instead of focusing solely on deficits and risks, the strengths-based model identifies existing
family resources, coping strategies, and past successes. Building on what works increases
engagement and produces more durable change than deficit-focused interventions.

**Cultural Humility**

Family support practitioners acknowledge that families come from diverse cultural backgrounds
with distinct values, communication styles, and definitions of well-being. Cultural humility
means ongoing self-reflection, avoiding assumptions, and adapting practices to fit each family's
context rather than imposing a single model.
"""

