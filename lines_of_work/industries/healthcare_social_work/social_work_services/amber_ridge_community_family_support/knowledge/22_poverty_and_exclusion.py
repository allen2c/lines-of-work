"""Poverty and social exclusion in family support practice."""

title = "Poverty and Social Exclusion"

content = """
Poverty is not only a lack of income; it encompasses limited access to education, healthcare,
housing, and social participation. Family support workers must understand how material
deprivation intersects with stigma, isolation, and structural barriers.

**Material Deprivation**

Families in poverty often face impossible trade-offs: paying rent versus buying food, seeking
employment versus caring for a child with special needs. Assess the full picture of household
resources, debts, and recurring costs before recommending interventions. Refer to emergency
assistance, food banks, and utility support programs when immediate needs are unmet.

**Stigma and Shame**

Poverty is often accompanied by shame and fear of judgment. Families may underreport needs
or avoid seeking help. Create a non-judgmental environment. Use language that normalises
seeking support and emphasises strengths. Never imply that poverty is a moral failing.

**Structural Barriers**

Systems designed to help can also exclude. Complex application processes, strict eligibility
rules, and lack of childcare during appointments create barriers. Advocate for simplified
procedures and accompany families through bureaucratic processes when needed.

**Intersectionality**

Poverty compounds with discrimination based on race, immigration status, disability, and
gender. Recognise how multiple forms of disadvantage interact and tailor support accordingly.
"""

