title = "Cultural Competence in Family Support"

content = """
Cultural competence is the capacity to work effectively across cultural differences while
honouring the values, beliefs, and practices of the families you serve. Amber Ridge serves
a diverse community; workers must continually reflect on their own assumptions and adapt
their practice.

**Self-Awareness**

Examine your own cultural background, biases, and blind spots. Recognise how your upbringing
and professional training shape your expectations of family structure, parenting, and help-seeking.
Avoid imposing dominant-culture norms on families whose practices differ but are equally valid.

**Respectful Curiosity**

Ask open-ended questions about family customs, decision-making processes, and preferred
communication styles. Do not assume. Learn about significant cultural events, dietary
restrictions, and religious observances that may affect scheduling and service delivery.

**Language and Interpretation**

When families prefer a language other than English, arrange qualified interpreters. Never use
family members, especially children, as interpreters for sensitive matters. Confirm that
interpreters understand social work terminology and maintain confidentiality.

**Intersectionality**

Families experience multiple identities simultaneously: race, ethnicity, religion, class,
disability, sexual orientation, immigration status. Consider how these intersect and how
structural barriers compound disadvantage. Tailor support plans accordingly.
"""

