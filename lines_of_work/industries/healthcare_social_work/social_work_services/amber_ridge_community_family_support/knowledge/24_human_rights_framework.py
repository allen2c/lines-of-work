"""Human rights as a foundation for family support practice."""

title = "Human Rights Framework"

content = """
Family support work is grounded in the recognition that every person has inherent dignity
and inalienable rights. The Universal Declaration of Human Rights and subsequent treaties
provide a normative framework for practice.

**Rights-Based Approach**

Families are not passive recipients of charity; they are rights-holders. The state and
institutions have obligations to respect, protect, and fulfil these rights. The worker's
role is to facilitate the realisation of rights, not to bestow favours. Frame support as
entitlement, not benevolence.

**Key Rights in Family Support**

Relevant rights include: the right to an adequate standard of living, including housing
and food; the right to health and social security; the right to education; the right to
family life and protection from arbitrary interference; the right to non-discrimination;
and the rights of the child, including protection from violence and the right to be heard.

**Accountability**

When rights are violated or services are denied, families have the right to seek redress.
Support families to file complaints, access legal aid, and engage in advocacy. Document
systemic barriers and contribute to policy reform efforts.

**Dignity and Respect**

Treat every family member with dignity regardless of circumstances. Avoid paternalism.
Support self-determination and informed choice. Uphold confidentiality and privacy as
fundamental to human dignity.
"""

