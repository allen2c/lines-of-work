"""Principles and protocols for crisis intervention."""

title = "Crisis Intervention"

content = """
Crisis intervention aims to stabilize immediate danger, reduce acute distress, and connect
families to appropriate ongoing support. Speed and clarity matter.

**Safety First**

If there is imminent risk to life or safety (suicide, violence, child in danger), prioritize
safety over process. Activate emergency services when needed. Document actions taken and
the rationale.

**De-escalation**

When a family member is highly distressed or agitated, use calm tone, simple language,
and non-threatening body language. Offer choices where possible. Avoid arguing or
minimizing feelings.

**Follow-Up**

Crisis intervention is not complete until the immediate risk is addressed and a plan for
follow-up is in place. Ensure handoff to appropriate services (mental health, domestic
violence shelter, child protection) and confirm the family knows next steps.
"""

