"""Recognizing and responding to domestic violence in family support."""

title = "Domestic Violence"

content = """
Domestic violence is common among families seeking support. Practitioners must recognize
signs, conduct safe assessment, and connect survivors to specialized services.

**Recognizing Signs**

Signs may include: injuries explained away, partner controlling finances or contacts,
fear of partner, children showing anxiety or aggression, isolation from family and friends.
Survivors may not disclose immediately; build trust over time.

**Safety-First Assessment**

When domestic violence is suspected, assess safety privately (without the abusive partner
present). Use validated tools (e.g., Danger Assessment) when available. Never recommend
couples counseling when violence is present; it can increase risk.

**Referral and Support**

Connect survivors to domestic violence agencies, shelters, and legal advocacy. Provide
information on safety planning. Do not pressure disclosure; offer resources and let the
survivor decide when and how to act.
"""

