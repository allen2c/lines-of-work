"""Knowledge item: referral protocols."""

title = "Referral Protocols and Warm Handoffs"

content = """
Effective referrals connect families to the right services at the right time. A warm handoff—
where the practitioner actively facilitates the connection rather than simply providing a phone
number—significantly increases the likelihood that the family will follow through.

**When to Refer**

Refer when the family's needs exceed your scope (specialized mental health, legal aid, medical
care), when another agency has better resources for the situation, or when the family requests
a specific service. Do not refer to avoid difficult work; refer when it serves the family's
best interest.

**Pre-Referral Steps**

Verify that the receiving service exists, accepts referrals, and matches the family's eligibility
(e.g., income, geography, age). Obtain the family's consent to share relevant information.
Prepare a brief summary of the family's situation and what they need. Contact the receiving
agency in advance when the case is complex or urgent.

**Warm Handoff Process**

Introduce the family to the receiving service by phone, video, or in-person accompaniment when
possible. Provide the receiving practitioner with essential context. Ensure the family
understands what to expect and what documents to bring. Schedule a follow-up to confirm the
family attended and was served.

**Documentation**

Record the referral date, receiving agency, contact person, and reason for referral. Note
whether the handoff was warm or cold. Document follow-up attempts and outcomes. If the family
does not attend, explore barriers and offer alternative supports.
"""

