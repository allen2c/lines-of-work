content = """
Community empowerment is the process by which families and neighbourhoods gain greater control
over the conditions that affect their lives. Family support workers at Amber Ridge facilitate
this by identifying and strengthening existing assets rather than treating communities as
passive recipients of services.

**Asset-Based Community Development**

Every community has assets: skills, knowledge, social networks, physical spaces, and local
institutions. Mapping these assets helps families see their own strengths and connect with
neighbours who can offer mutual support. The worker's role is to surface and connect these
resources, not to import solutions from outside.

**Participatory Approaches**

Empowerment requires that families participate in defining their needs and designing
responses. Avoid top-down planning. Use methods such as community meetings, focus groups,
and participatory needs assessments so that families have a genuine voice in program design
and evaluation.

**Building Leadership**

Identify and nurture informal leaders within families and neighbourhoods. Support parents
who show initiative to become peer mentors, group facilitators, or advocates. Leadership
development creates sustainable change beyond the lifespan of any single intervention.

**Reducing Dependency**

The goal of family support is to build capacity, not create long-term dependency. Gradually
reduce direct intervention as families develop skills, networks, and confidence. Document
progress toward self-sufficiency and celebrate milestones.
"""

