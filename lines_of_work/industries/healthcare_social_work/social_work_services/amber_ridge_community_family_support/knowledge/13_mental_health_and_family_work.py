title = "Mental Health and Family Support Work"

content = """
Family support workers routinely encounter families affected by mental health conditions.
While workers do not provide clinical treatment, they must recognize signs of distress,
understand when to refer, and support families in accessing appropriate care without stigma.

**Recognition and Referral**

Signs that warrant referral to mental health professionals include: persistent sadness or
hopelessness, severe anxiety, thoughts of self-harm or harm to others, psychosis, substance
use disorders, and trauma reactions. Workers should maintain a list of local mental health
providers, crisis lines, and emergency services. Explain to families that seeking help is a
strength, not a weakness.

**Supporting the Family System**

Mental illness affects the whole family. Children may feel confused, scared, or responsible.
Spouses may experience caregiver burden. Workers can normalize these experiences, connect
families with family education programs (e.g., NAMI Family-to-Family), and help families
establish routines that support stability.

**Boundaries**

Workers support and refer; they do not diagnose or treat. Avoid giving clinical advice.
When a family member is in crisis, follow agency protocols for safety assessment and
emergency referral. Document referrals and follow up to ensure the family connected with
services.
"""

