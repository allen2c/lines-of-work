title = "Elder Care and Aging Support"

content = """
Family support workers frequently assist families navigating the challenges of aging parents or
relatives. Understanding the physical, cognitive, and emotional changes associated with aging
enables workers to connect families with appropriate resources and support systems.

**Common Needs**

Older adults may require assistance with activities of daily living, medication management,
medical appointments, housing transitions, financial planning, and end-of-life decisions. Family
members often serve as primary caregivers and may experience burnout, stress, or conflict
among siblings regarding care responsibilities.

**Key Resources**

Connect families with senior centers, meal programs, home health services, respite care,
adult day programs, and Area Agencies on Aging. Medicare, Medicaid, and veterans' benefits
may cover certain services. Advance directives and power of attorney documentation support
dignified decision-making when capacity declines.

**Cultural Sensitivity**

Attitudes toward aging, filial responsibility, and institutional care vary across cultures.
Some families prefer multigenerational living; others seek professional care. Respect these
preferences while ensuring safety and well-being. Recognize that elder abuse and neglect
require mandatory reporting in all jurisdictions.
"""

