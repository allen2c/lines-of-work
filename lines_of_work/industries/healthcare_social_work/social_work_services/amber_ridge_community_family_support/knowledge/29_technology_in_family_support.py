"""Technology use in family support practice."""

title = "Technology in Family Support"

content = """
Technology can extend reach, improve efficiency, and enhance communication. It also raises
questions of access, privacy, and the nature of the helping relationship. Use technology
thoughtfully and in accordance with organisational policy.

**Virtual and Hybrid Service Delivery**

Video calls and phone support can reach families who cannot attend in person due to
transportation, childcare, or mobility barriers. Ensure that virtual options do not
exclude those without reliable internet or devices. Provide alternatives when technology
is not accessible. Maintain the same standards of confidentiality and professionalism
online as in person.

**Case Management Systems**

Electronic records improve consistency, support supervision, and enable data analysis.
Enter information promptly and accurately. Use secure systems and strong passwords.
Never access client information on personal devices or unsecured networks.

**Communication Boundaries**

Establish clear boundaries for digital communication. Use work email and phone numbers.
Set expectations for response times. Do not friend clients on personal social media.
Document all substantive client communications in the case record.

**Digital Literacy**

Support families to access online resources, benefits portals, and virtual services.
Assist with digital literacy when needed. Be aware of the digital divide and advocate
for equitable access to technology.
"""

