title = "Supporting Immigrant and Refugee Families"

content = """
Immigrant and refugee families face unique challenges: language barriers, acculturation
stress, uncertain legal status, trauma from migration or persecution, and discrimination.
Family support workers provide culturally responsive services and connect families with
specialized resources.

**Legal Considerations**

Immigration status affects eligibility for benefits. Some programs are available regardless
of status (emergency Medicaid, school enrollment, WIC for children). Others require lawful
presence. Workers must know agency policies on serving undocumented families and never
request or record immigration status unless required for a specific benefit. Refer to
immigration attorneys for legal advice.

**Cultural Responsiveness**

Learn about the cultures and languages of families served. Use professional interpreters;
never use family members, especially children, for interpretation in sensitive matters.
Respect differing views on family roles, discipline, gender, and help-seeking. Build trust
through consistency, confidentiality, and demonstrated respect.

**Trauma-Informed Care**

Many refugees have experienced violence, loss, or persecution. Symptoms may include
hypervigilance, nightmares, or difficulty trusting. Avoid retraumatizing questions.
Provide information about trauma reactions and mental health services. Connect families
with refugee resettlement agencies and ethnic community organizations.
"""

