title = "Outcome Measurement and Impact"

content = """
Measuring outcomes helps Amber Ridge Community demonstrate impact, improve services, and secure
funding. Family support outcomes are often multidimensional: changes in stability, well-being,
skills, or access to resources. Choose measures that align with the family's goals and the
program's mission.

**Types of Outcomes**

Outputs are countable activities (visits conducted, referrals made). Outcomes are changes in
the family's condition (housing stabilized, parenting skills improved). Impact refers to
longer-term effects (children's school attendance, reduced re-entry to crisis services).
Measure at the level appropriate to your program's scope and timeline.

**Data Collection Methods**

Use standardized tools when available (e.g., validated scales for stress, depression, or
family functioning). Supplement with qualitative narratives and goal-attainment scaling.
Collect data at intake, at key milestones, and at case closure. Ensure families understand
why data is collected and how it is used.

**Ethical Considerations**

Do not burden families with excessive paperwork. Integrate assessment into natural
conversation when possible. Protect confidentiality in all reporting. Aggregate data for
reports; never identify individual families without consent. Use data to improve practice,
not only to satisfy funders.
"""

