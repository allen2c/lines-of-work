title = "Custodia y Régimen de Visitas"

content = """
La custodia determina con quién conviven habitualmente los menores; el régimen de
visitas regula el tiempo y la forma de relación con el otro progenitor. Ambos deben
priorizar el interés superior del menor.

**Custodia compartida:** Alternancia de la convivencia según el acuerdo o la sentencia
(por semanas, quincenas, etc.). Requiere cierta capacidad de cooperación entre
progenitores. No implica reparto exacto 50-50 en todos los casos.

**Custodia exclusiva:** Un progenitor tiene la custodia y el otro tiene derecho de
visitas. Las visitas pueden ser fines de semana alternos, midweek, vacaciones
repartidas, etc.

**Conflictos:** Cuando el incumplimiento de visitas o la manipulación del menor son
graves, la familia puede necesitar mediación, punto de encuentro familiar o asesoramiento
legal. Puente Verde orienta sobre recursos y deriva cuando corresponde.
"""

