content = """
Las familias monoparentales (un solo progenitor a cargo de los hijos) son diversas en origen
(viudedad, separación, elección) y en composición. El apoyo debe centrarse en las
necesidades concretas sin estigmatizar.

**Retos frecuentes:** Conciliación trabajo-crianza, carga económica, posible aislamiento
y sobrecarga emocional. La red de apoyo (familia extensa, amistades, asociaciones) es un
recurso clave.

**Recursos y derechos:** En Cataluña y España existen ayudas fiscales, becas y programas
de conciliación. Orientar sobre requisitos y plazos forma parte del apoyo familiar. Los
puntos de encuentro o el apoyo de la otra figura parental, cuando existe, pueden aliviar
la carga.

**No patologizar:** Una familia monoparental bien apoyada puede ofrecer un entorno
perfectamente sano. El foco está en fortalecer recursos y no en asumir carencias por
el solo hecho de ser monoparental.
"""

