title = "Nuevas Parejas y Menores"

content = """
La incorporación de una nueva pareja del progenitor (después de separación o viudedad)
puede generar reacciones en los menores: celos, rechazo, lealtad al otro progenitor.
El ritmo y la forma de presentar la nueva pareja influyen.

**Ritmo:** No forzar la relación. Permitir que el menor conozca a la pareja de forma
gradual y en contextos neutros o agradables. No sustituir el tiempo a solas con el
progenitor por tiempo en trío de inmediato.

**Límites del rol:** La nueva pareja no debe imponer disciplina ni sustituir al otro
progenitor. Puede participar en normas acordadas con el progenitor y en actividades
de ocio. La comunicación entre el progenitor y su pareja sobre cómo actuar ante
conductas del menor es clave.

**Conflictos:** Si el menor rechaza de forma persistente o hay alianzas que generan
sufrimiento, valorar derivación a terapia familiar o mediación.
"""

