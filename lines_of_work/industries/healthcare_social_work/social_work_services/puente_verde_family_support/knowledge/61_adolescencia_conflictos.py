title = "Conflictos Típicos en la Adolescencia"

content = """
Los conflictos entre progenitores y adolescentes suelen girar en torno a horarios,
pantallas, estudios, aspecto físico, amistades y privacidad. Gestionarlos sin
escalada requiere negociación, límites claros en lo no negociable y mantener el
vínculo.

**Horarios y salidas:** Negociar según la edad y la madurez. Establecer qué es
innegociable (avisar dónde están, hora de vuelta) y qué puede flexibilizarse.
Consecuencias coherentes si no se cumplen los acuerdos.

**Privacidad:** Respetar su espacio (llamar antes de entrar, no revisar móvil sin
motivo grave) favorece la confianza. La supervisión puede ser más indirecta que en
la niñez; el objetivo es que interioricen criterios, no solo obedecer por miedo.

**Cuándo derivar:** Si el conflicto es constante y destructivo, si hay conductas
de riesgo (consumo, absentismo, autolesiones) o si el adolescente o los padres
están en un callejón sin salida, derivar a terapia familiar o CSMIJ.
"""

