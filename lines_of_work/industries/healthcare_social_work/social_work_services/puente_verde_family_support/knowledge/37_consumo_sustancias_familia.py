title = "Consumo de Sustancias en el Ámbito Familiar"

content = """
El consumo problemático de alcohol u otras sustancias por parte de un progenitor o de
un adolescente afecta al clima familiar. Puente Verde puede orientar sobre recursos
de tratamiento y sobre cómo hablar del tema con los menores, pero no realiza
intervención en adicciones.

**Orientación a la familia:** Informar sobre recursos de atención a adicciones
(centros de salud mental, unidades de conductas adictivas, asociaciones). Animar a
quien consume a pedir ayuda y a la familia a cuidarse.

**Menores y sustancias:** Si el consumo es del adolescente, valorar derivación a
CSMIJ o a programas juveniles de prevención y tratamiento. No criminalizar; sí
dejar claro el riesgo y la opción de apoyo profesional.

**Protección de menores:** Si el consumo del adulto implica negligencia o riesgo
(conducción bajo los efectos, violencia), valorar derivación a servicios sociales.
"""

