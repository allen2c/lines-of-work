title = "Conciliación de la Vida Laboral y Familiar"

content = """
La conciliación es un factor de estrés frecuente para las familias. Conocer los derechos
laborales (reducción de jornada, excedencias, permisos por nacimiento o cuidado) y los
recursos comunitarios (escuelas, actividades extraescolares, campamentos) ayuda a
orientar.

**Derechos laborales:** Permisos por maternidad y paternidad; reducción de jornada por
cuidado de menores; excedencia por cuidado de familiar. La normativa cambia; en caso de
duda, derivar a sindicatos, oficinas de trabajo o asesoría laboral.

**Recursos de cuidado:** Guarderías públicas y concertadas, actividades extraescolares,
ludotecas, casales de verano. Los servicios sociales pueden informar sobre plazas y
criterios de prioridad.

**Organización en casa:** Rutinas compartidas entre progenitores, reparto de tareas y
apoyo de la red (abuelos, amigos) pueden aliviar la sobrecarga. El apoyo familiar
puede trabajar con la familia en identificar opciones realistas.
"""

