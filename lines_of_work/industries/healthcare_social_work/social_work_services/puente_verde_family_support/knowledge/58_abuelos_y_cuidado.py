title = "Abuelos y Cuidado de los Menores"

content = """
Los abuelos pueden ser una fuente de apoyo importante para la crianza; en algunas
familias asumen un papel muy activo. También pueden surgir tensiones por diferencias
en normas, horarios o estilos educativos.

**Recursos:** Validar el papel de los abuelos cuando es positivo. Orientar sobre
cómo comunicar acuerdos básicos (normas en casa de los abuelos cuando los menores
pasan tiempo allí) sin desautorizar. Los abuelos no tienen que hacer exactamente
igual que los padres; sí respetar límites esenciales (seguridad, salud, respeto).

**Conflictos:** Cuando las discrepancias generan conflicto abierto, trabajar con la
familia en mensajes claros y en qué es negociable. En casos de abuelos que desautorizan
o interfieren de forma grave, puede ser necesario establecer límites más firmes o
reducir el contacto si no hay cambio.

**Cuidado de abuelos:** Cuando los abuelos son cuidadores principales (familia
tridimensional), pueden necesitar apoyo y reconocimiento. Orientar sobre recursos
de respiro si procede.
"""

