title = "Gestión de Emociones en Progenitores"

content = """
Los progenitores también tienen emociones intensas (frustración, miedo, culpa, ira).
Gestionarlas de forma sana evita que las descarguen sobre los hijos y modela
autorregulación para los menores.

**Reconocimiento:** Validar que es normal sentirse desbordado a veces. No se trata
de no tener emociones, sino de no actuar impulsivamente (gritar, castigar con ira).
Hacer una pausa, respirar o retirarse un momento antes de responder puede ayudar.

**Autocuidado:** Dormir suficiente, tener espacios propios y apoyo social reduce la
vulnerabilidad al estrés. Cuando el progenitor está bien, responde mejor.

**Culpa:** Muchos padres y madres cargan con culpa. Trabajar la autocompasión y el
enfoque en «qué puedo hacer a partir de ahora» en lugar de rumiar errores pasados.
Si la culpa o la ansiedad son incapacitantes, derivar a salud mental.
"""

