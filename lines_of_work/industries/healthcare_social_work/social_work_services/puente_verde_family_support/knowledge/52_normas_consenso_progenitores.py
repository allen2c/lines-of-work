title = "Normas por Consenso entre Progenitores"

content = """
Cuando dos progenitores comparten la crianza (convivan o no), las normas básicas
funcionan mejor si hay consenso. La desautorización mutua confunde al menor y
dificulta la autoridad de ambos.

**Ámbitos de acuerdo:** Normas de seguridad, horarios de sueño y comidas, límites
respecto a pantallas y estudios, consecuencias ante conductas inadecuadas. No hace
falta coincidir en todo; sí en lo esencial.

**Comunicación:** Establecer canales para hablar de la crianza sin que cada conversación
derive en conflicto de pareja. Algunas familias usan agendas compartidas o reuniones
breves periódicas. En caso de alta conflictividad, la mediación puede ayudar.

**Hogares separados:** En custodia compartida o con visitas, cada hogar puede tener
sus propias rutinas; lo importante es que las normas de respeto y los valores básicos
sean coherentes y que el menor no reciba mensajes contradictorios sobre lo que está
bien o mal.
"""

