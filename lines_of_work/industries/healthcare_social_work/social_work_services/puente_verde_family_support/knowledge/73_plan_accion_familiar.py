title = "Plan de Acción Familiar"

content = """
El plan de acción es el conjunto de objetivos y pasos acordados con la familia para
abordar la situación que les preocupa. Debe ser realista, medible en la medida de lo
posible y revisable.

**Elementos:** Objetivos (qué quieren lograr); tareas o pasos concretos (quién hace
qué, cuándo); recursos a movilizar (red, servicios); criterios de revisión (cuándo
nos volvemos a ver o a valorar).

**Participación:** El plan se construye con la familia, no se impone. Si la familia
no se implica en las tareas, explorar los motivos (resistencia, falta de claridad,
objetivos no compartidos) y ajustar.

**Revisión:** En cada sesión o contacto, revisar qué se ha hecho, qué ha funcionado
y qué obstáculos han surgido. Celebrar los avances por pequeños que sean; reforzar
la capacidad de la familia.
"""

