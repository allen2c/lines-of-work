title = "Desarrollo en la Niñez Media"

content = """
La niñez media (aproximadamente 6-12 años) se caracteriza por la consolidación de la
autonomía, el desarrollo del razonamiento lógico y la ampliación del mundo social más allá
de la familia (amistades, escuela, actividades extraescolares).

**Desarrollo social y moral:** Los niños interiorizan normas y empiezan a distinguir lo que
está bien y lo que está mal. Las amistades cobran importancia; pueden surgir conflictos
entre iguales que requieren apoyo para la resolución pacífica.

**Escuela y aprendizaje:** El rendimiento escolar y la actitud hacia el aprendizaje se ven
influidos por el clima familiar, las expectativas razonables y el apoyo ante las dificultades.
La comunicación familia-escuela es clave.

**Identidad y autoestima:** El autoconcepto se construye a partir de las experiencias en
casa y en el colegio. El refuerzo positivo, el reconocimiento del esfuerzo y la aceptación
de los errores como parte del aprendizaje favorecen una autoestima sana.
"""

