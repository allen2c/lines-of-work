title = "Cambio de Escuela o Domicilio"

content = """
Los cambios de colegio o de vivienda suponen pérdida de referentes (amigos, profesores,
entorno) y pueden generar inseguridad o resistencia en los menores. La preparación
y el acompañamiento emocional facilitan la adaptación.

**Antes del cambio:** Explicar con antelación el motivo (trabajo, economía, nueva
familia) de forma adaptada a la edad. Permitir que expresen lo que sienten (tristeza,
enfado) y responder a sus preguntas. Visitar el nuevo centro o el nuevo barrio si
es posible.

**Después:** Mantener contacto con amigos anteriores si el menor lo desea (videollamadas,
visitas). Estar atento a señales de dificultad (rechazo escolar, somatizaciones,
tristeza persistente). Dar tiempo; la adaptación puede llevar semanas o meses.

**Coordinación:** Si el cambio es por separación o custodia, coordinar con el otro
progenitor y con el centro educativo para una transición ordenada.
"""

