title = "Límites y Normas en el Hogar"

content = """
Los límites y las normas aportan seguridad y previsibilidad a los menores. Deben ser
claros, coherentes entre progenitores y adaptados a la edad; su ausencia o su aplicación
inconsistente generan confusión y más conflicto.

**Criterios de diseño:** Pocas normas prioritarias (seguridad, respeto, rutinas básicas);
formulación en positivo cuando sea posible («hablamos con calma» en lugar de «no grites»);
consecuencias lógicas y proporcionales, conocidas de antemano.

**Coherencia entre progenitores:** Cuando dos adultos comparten la crianza, es importante
ponerse de acuerdo en las reglas básicas y en las consecuencias. Las desautorizaciones
mutuas delante del niño restan eficacia y generan alianzas que dificultan la autoridad.

**Revisión:** Las normas pueden revisarse en familia cuando los hijos tienen edad para
participar, explicando el sentido de cada una y escuchando su opinión dentro de lo
razonable.
"""

