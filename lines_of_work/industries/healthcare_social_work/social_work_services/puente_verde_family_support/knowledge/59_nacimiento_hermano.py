title = "Llegada de un Hermano"

content = """
El nacimiento de un hermano es una transición importante para el mayor. Puede
experimentar celos, regresión (control de esfínteres, sueño) o conductas de llamada
de atención. La preparación y el trato después del nacimiento pueden facilitar la
adaptación.

**Antes del nacimiento:** Explicar que va a llegar un hermano, qué implicará (más
tiempo de los padres para el bebé al principio) y qué no cambiará (le seguiremos
queriendo). Algunas familias leen cuentos sobre hermanos o visitan a amigos con
bebés.

**Después:** Incluir al mayor en tareas adaptadas a su edad (traer el pañal, elegir
la ropa del bebé). Reservar tiempo exclusivo con el mayor. No culpabilizar por
celos o rabietas; son normales. Reforzar su rol de hermano mayor sin cargarle de
responsabilidades que no corresponden a su edad.

**Si la adaptación es muy difícil:** Valorar si hay otros factores de estrés y, si
persiste, derivar a atención temprana o salud mental infanto-juvenil.
"""

