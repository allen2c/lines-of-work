title = "Comunicación No Violenta"

content = """
La comunicación no violenta (CNV) es un modelo que propone expresar observaciones,
sentimientos, necesidades y peticiones de forma clara y respetuosa, y escuchar al
otro con empatía. Puede aplicarse en la pareja y con los hijos para reducir
escaladas y malentendidos.

**Estructura:** Observación (hecho concreto, sin juicio) → Sentimiento → Necesidad
→ Petición. Ejemplo: «Cuando llegas tarde sin avisar (observación), me siento
preocupada (sentimiento) porque necesito saber que estás bien (necesidad). ¿Puedes
avisarme la próxima vez? (petición)».

**Con menores:** Adaptar el lenguaje a la edad. Los niños también pueden aprender a
nombrar lo que sienten y lo que necesitan. No se trata de ser perfectos; sí de
reducir críticas globales y gritos.

**Límites:** La CNV no implica permitir todo. Se pueden poner límites con claridad
y respeto («Entiendo que quieres más tiempo de pantalla; la norma es una hora entre
semana. Cuando cumples la norma, hablamos de qué hacer después»).
"""

