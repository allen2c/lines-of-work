content = """
El trabajo en red con otros profesionales (servicios sociales, salud, educación,
mediación, entidades) mejora la atención a la familia. Requiere consentimiento,
claridad en los objetivos de cada contacto y respeto a la confidencialidad.

**Cuándo activar la red:** Cuando un caso requiere recursos que Puente Verde no
ofrece, cuando la coordinación evita duplicar intervenciones o cuando la familia
se beneficia de una atención integrada. No contactar con otros sin autorización
salvo obligación legal.

**Comunicación:** Transmitir solo la información necesaria para el objetivo acordado.
Usar canales seguros. Dejar constancia de los contactos y de lo acordado.

**Reuniones de red:** En casos complejos pueden convocarse reuniones con varios
profesionales y la familia. Definir un coordinador y un plan compartido evita
fragmentación.
"""

