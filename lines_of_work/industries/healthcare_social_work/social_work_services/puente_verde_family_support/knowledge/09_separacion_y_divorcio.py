title = "Separación y Divorcio: Acompañamiento Familiar"

content = """
La separación o el divorcio suponen una reorganización importante para la familia. El
objetivo del apoyo es favorecer que los menores mantengan vínculos estables con ambos
progenitores y que la conflictividad no les dañe.

**Información a los hijos:** Adaptar el mensaje a la edad; evitar detalles de la pareja
que no les incumben; transmitir que la decisión es de los adultos y que ellos no son
responsables; asegurar que seguirán queriendo y viendo a ambos.

**Régimen de visitas y custodia:** Las fórmulas (custodia compartida, alterna, exclusiva)
dependen de la legislación y de cada caso. Lo prioritario es la estabilidad y el respeto
al tiempo de cada progenitor con los hijos. Los cambios bruscos o el incumplimiento
reiterado de acuerdos afectan negativamente.

**Recursos:** Puente Verde puede orientar sobre mediación familiar, puntos de encuentro
y servicios de apoyo a la parentalidad post-separación. Los aspectos legales corresponden
a abogados y juzgados.
"""

