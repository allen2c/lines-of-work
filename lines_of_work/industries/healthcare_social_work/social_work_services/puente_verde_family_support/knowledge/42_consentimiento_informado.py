title = "Consentimiento Informado"

content = """
El consentimiento informado es la aceptación libre y documentada de la familia (o del
menor cuando tenga capacidad suficiente) para recibir el servicio, para que se
registren los datos necesarios y para que se realicen derivaciones o coordinaciones
con otros recursos cuando proceda.

**Elementos:** Información clara sobre el servicio, sus límites, los profesionales
que intervienen, la confidencialidad y sus excepciones (obligación de derivar en
casos de riesgo). Oportunidad de preguntar. Documentación escrita (firma o equivalente).

**Menores:** Los titulares de la patria potestad dan el consentimiento para la
intervención con menores. A partir de cierta edad (según madurez y normativa), el
menor puede ser escuchado y su opinión tenida en cuenta; en algunos actos el menor
debe consentir.

**Excepciones:** En situaciones de riesgo para el menor, la ley puede permitir o
obligar a actuar sin consentimiento (derivación a servicios de protección). Dejar
constancia del motivo.
"""

