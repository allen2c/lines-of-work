title = "Apoyo Escolar desde la Familia"

content = """
La familia puede apoyar el aprendizaje sin sustituir al profesor: creando condiciones
(espacio, horario, descanso), mostrando interés por lo que aprende el hijo y manteniendo
comunicación con el centro. La presión excesiva suele ser contraproducente.

**Rutina de deberes:** Un momento y lugar fijos ayudan. Estar disponible para dudas sin
hacer los deberes por el niño. Si las tareas generan conflicto constante, valorar con el
centro si hay desajuste de nivel o necesidad de refuerzo.

**Comunicación con la escuela:** Asistir a reuniones, leer las circulares y responder
cuando el centro pide información. Ante dificultades de aprendizaje o conducta, pedir
entrevista y trabajar en equipo (familia, tutor, orientación).

**Expectativas:** Ajustar las expectativas a las capacidades y al momento del hijo.
Reforzar el esfuerzo más que la nota; transmitir que equivocarse forma parte del
aprendizaje.
"""

