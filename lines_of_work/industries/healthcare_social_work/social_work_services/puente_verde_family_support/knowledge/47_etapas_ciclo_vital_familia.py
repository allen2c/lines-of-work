title = "Etapas del Ciclo Vital Familiar"

content = """
Las familias atraviesan etapas (formación de pareja, nacimiento de hijos, escolarización,
adolescencia, emancipación, nido vacío, vejez) que conllevan reajustes y posibles
crisis normativas. Conocer estas etapas ayuda a contextualizar las demandas.

**Transiciones:** Cada transición implica cambios de roles y de reglas. Algunas familias
experimentan más estrés en momentos concretos (llegada del primer hijo, entrada en
adolescencia, separación). Normalizar la dificultad y trabajar los recursos disponibles
facilita el tránsito.

**No linealidad:** Las familias reconstituidas, monoparentales o con estructuras diversas
pueden no seguir la secuencia «clásica»; el ciclo vital se adapta al modelo familiar.

**Recursos:** En cada etapa hay recursos específicos (preparación al nacimiento,
programas de parentalidad adolescente, apoyo a cuidadores de mayores). Orientar según
la etapa en que se encuentra la familia.
"""

