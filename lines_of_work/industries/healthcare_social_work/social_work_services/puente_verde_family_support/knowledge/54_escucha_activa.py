title = "Escucha Activa"

content = """
La escucha activa consiste en prestar atención plena al otro, reflejar lo que dice
o lo que parece sentir, y no interrumpir con consejos o juicios de inmediato. Favorece
la confianza y la clarificación de lo que la persona necesita.

**Técnicas:** Parafrasear («Si te he entendido bien, lo que te preocupa es…»),
reflejar emociones («Parece que te sientes muy frustrado»), preguntas abiertas («¿Qué
fue lo que más te costó?»). Evitar «ya deberías haber…» o «no es para tanto».

**Con menores:** Dar tiempo para que terminen de hablar, no descalificar sus emociones
(«no llores por eso») y mostrar interés por su relato. A veces solo necesitan ser
escuchados antes de buscar soluciones.

**En la familia:** Cuando los progenitores practican la escucha activa entre ellos
y con los hijos, el clima mejora. Puente Verde puede modelar y proponer ejercicios
sencillos en sesión.
"""

