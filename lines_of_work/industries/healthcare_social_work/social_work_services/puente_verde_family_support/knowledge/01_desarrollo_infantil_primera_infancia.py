title = "Desarrollo Infantil en la Primera Infancia"

content = """
La primera infancia (0-6 años) es una etapa fundamental para el desarrollo físico, emocional y
social. El cerebro del niño presenta una gran plasticidad; las experiencias de apego, seguridad
y estimulación adecuada sentarán las bases de su bienestar futuro.

**Apego y vínculo:** La relación estable y sensible con al menos una figura de referencia
(progenitor, cuidador principal) permite al niño explorar el mundo con confianza. La
disponibilidad emocional y la respuesta coherente a las necesidades del bebé favorecen un
apego seguro.

**Hitos evolutivos:** Cada niño tiene su ritmo, pero conviene conocer las etapas habituales:
control cefálico, sonrisa social, gateo, primeros pasos, lenguaje comprensivo y expresivo.
Las desviaciones significativas pueden indicar la necesidad de valoración por pediatría o
atención temprana.

**Rutinas y límites:** En esta etapa las rutinas (sueño, alimentación, juego) aportan
seguridad. Los límites deben ser claros, coherentes y adaptados a la edad; la constancia
ayuda más que la dureza.
"""

