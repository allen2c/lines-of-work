title = "Duelo en la Familia"

content = """
La muerte de un ser querido afecta a cada miembro de la familia y a la dinámica común.
Los menores procesan el duelo según su edad; necesitan información clara, espacio para
preguntas y la posibilidad de participar en rituales si lo desean.

**Comunicación:** Explicar lo ocurrido con palabras sencillas y honestas, evitando
eufemismos que generen confusión («se ha ido», «está dormido»). Responder a las
preguntas repetidas con paciencia. Los niños pueden manifestar tristeza, enfado o
conductas regresivas; es normal.

**Rituales:** Permitir que asistan al funeral o al entierro si quieren, explicando antes
qué verán. Crear rituales propios (encender una vela, visitar el cementerio, recordar
anécdotas) ayuda a integrar la pérdida.

**Cuándo derivar:** Si el duelo se complica (duración muy prolongada, aislamiento
extremo, ideación de muerte en el menor), derivar a salud mental o a recursos de
duelo infantil.
"""

