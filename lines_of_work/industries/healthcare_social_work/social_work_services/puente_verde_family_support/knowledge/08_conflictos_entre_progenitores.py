title = "Conflictos entre Progenitores"

content = """
El desacuerdo entre progenitores es normal; lo que marca la diferencia es cómo se gestiona.
Cuando los conflictos son frecuentes, intensos o se ventilan delante de los hijos, el
impacto en el bienestar infantil puede ser importante.

**Impacto en los menores:** Los niños pueden sentirse culpables, divididos o inseguros.
Es recomendable no descalificar al otro progenitor delante del hijo, no usar al menor como
mensajero o como aliado en el conflicto, y mantener acuerdos básicos sobre normas y
rutinas aunque la relación de pareja haya terminado.

**Espacios de acuerdo:** Identificar qué aspectos exigen consenso (normas en casa de cada
uno, horarios, temas escolares o de salud) y cuáles pueden gestionarse por separado reduce
la tensión. La mediación familiar puede ayudar a alcanzar acuerdos.

**Cuándo derivar:** Si existe violencia, manipulación o riesgo para el menor, se debe
derivar a servicios sociales o recursos especializados. El apoyo familiar de Puente Verde
no sustituye la intervención en situaciones de alto conflicto.
"""

