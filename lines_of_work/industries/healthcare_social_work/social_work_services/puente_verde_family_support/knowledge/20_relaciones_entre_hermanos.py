content = """
La rivalidad entre hermanos es habitual; no toda competición o conflicto es patológica.
El objetivo es favorecer la resolución pacífica y el respeto, no eliminar el conflicto.

**Evitar comparaciones:** Etiquetar («el listo», «el revoltoso») o comparar en público
aumenta la rivalidad. Reconocer a cada uno por sus logros y cualidades sin contraponer.

**Normas claras:** No permitir agresiones físicas o verbales; intervenir con calma y
aplicar consecuencias coherentes. En peleas, no siempre hace falta buscar «quién empezó»;
a veces basta con separar y pedir que cada uno se calme en su espacio.

**Tiempo individual:** Cada hijo necesita momentos a solas con un progenitor. Programar
ese tiempo reduce la competencia por atención y refuerza el vínculo.

**Llegada de un hermano:** Preparar al mayor, involucrarlo en tareas adaptadas a su edad
y mantener rutinas con él ayuda a la adaptación. La regresión temporal (control de
esfínteres, sueño) puede ser normal.
"""

