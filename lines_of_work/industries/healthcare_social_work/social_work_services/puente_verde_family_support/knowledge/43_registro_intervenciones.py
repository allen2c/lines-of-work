content = """
El registro de las intervenciones (entrevistas, contactos, derivaciones, acuerdos) es
necesario para la continuidad del trabajo, la supervisión y la rendición de cuentas.
Debe ser objetivo, relevante y conforme a la normativa de protección de datos.

**Contenido:** Fecha, tipo de contacto, personas presentes, resumen de lo tratado,
acuerdos o tareas, derivaciones realizadas, incidencias relevantes. Evitar juicios
valorativos innecesarios; describir hechos y conductas observables.

**Almacenamiento:** Los expedientes se conservan en soporte seguro y solo son
accesibles por el personal autorizado. Los plazos de conservación se ajustan a la
normativa aplicable.

**Uso:** El registro sirve para el seguimiento del caso y para la coordinación con
otros profesionales cuando la familia lo autorice. No se comparte con terceros sin
consentimiento o sin base legal.
"""

