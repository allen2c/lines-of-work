title = "Diversidad Funcional en la Familia"

content = """
El término «diversidad funcional» enfatiza la diversidad de capacidades en lugar del
déficit. Las familias con miembros con diversidad funcional pueden necesitar apoyo
en recursos, en la organización del día a día y en el bienestar de todos (incluidos
los cuidadores y los hermanos).

**Lenguaje:** Usar el lenguaje que la familia prefiera (persona con discapacidad,
con diversidad funcional, etc.). Evitar términos peyorativos o que reduzcan a la
persona a su condición.

**Recursos:** Prestaciones (ley de dependencia, pensiones), educación inclusiva,
centros de día, respiro, asociaciones. Orientar y derivar según la necesidad.

**No patologizar la familia:** Tener un miembro con diversidad funcional no define
a la familia como «problemática». El apoyo se centra en fortalecer capacidades y
conectar con recursos, no en etiquetar.
"""

