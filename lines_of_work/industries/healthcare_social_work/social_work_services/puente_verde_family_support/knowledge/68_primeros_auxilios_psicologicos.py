content = """
Los primeros auxilios psicológicos (PAP) son una respuesta de apoyo inmediato ante
una persona en crisis (accidente, muerte inesperada, noticia traumática). No son
terapia; son escucha, contención y orientación hacia recursos. En el ámbito familiar,
pueden aplicarse cuando la familia acude tras un evento impactante.

**Principios:** Asegurar la seguridad física; escuchar sin presionar; validar
emociones; no minimizar ni dramatizar; ofrecer información práctica (qué puede
hacer ahora); conectar con red de apoyo y con servicios si procede.

**Límites:** Si la persona está en shock severo o hay riesgo autolítico, priorizar
la derivación a urgencias o a salud mental. Los PAP no sustituyen la atención
especializada cuando hace falta.

**Niños:** Con menores, usar lenguaje adaptado, mantener rutinas en la medida de lo
posible y dar espacio para que pregunten. No mentir sobre lo ocurrido; sí dosificar
la información según la edad.
"""

