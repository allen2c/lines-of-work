title = "Protocolo de Derivación a Servicios Sociales"

content = """
Cuando un caso supera el ámbito del apoyo familiar (posible desamparo, necesidad de
medidas de protección, ayudas económicas o vivienda que requieren valoración social),
se debe derivar a los servicios sociales de referencia.

**Pasos:** Informar a la familia de que el caso será derivado y por qué. Recoger
consentimiento cuando sea posible; en situaciones de riesgo, la derivación puede
hacerse sin consentimiento previo si la ley lo permite. Contactar con el servicio
social de base del municipio o distrito correspondiente al domicilio de la familia.
Transmitir los datos necesarios (sin vulnerar confidencialidad más allá de lo
estrictamente necesario) y dejar constancia en el expediente.

**Seguimiento:** Si la familia lo autoriza, se puede hacer un seguimiento de si
acudió a la cita. Puente Verde no asume la gestión del caso una vez derivado.
"""

