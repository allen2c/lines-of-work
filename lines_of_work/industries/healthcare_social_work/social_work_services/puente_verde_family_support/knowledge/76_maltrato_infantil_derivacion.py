title = "Maltrato Infantil: Derivación Obligatoria"

content = """
Ante la sospecha fundada de maltrato infantil (físico, emocional, sexual o negligencia
grave), el profesional tiene la obligación de ponerlo en conocimiento de las
autoridades competentes (servicios sociales, EAIA, fiscalía según el caso). La
legislación española y catalana establece el deber de comunicación.

**No valorar en solitario:** No corresponde a Puente Verde realizar la valoración
de si existe maltrato; sí identificar indicadores y derivar. La valoración la realizan
los servicios de protección.

**Comunicación a la familia:** Según el caso y el criterio del equipo, se puede
informar a la familia de que se va a realizar la derivación y por qué, o no hacerlo
si ello pudiera poner en mayor riesgo al menor. Documentar la decisión.

**Seguimiento:** Una vez derivado, el caso pasa a los servicios correspondientes.
Puente Verde puede mantener contacto si los servicios lo autorizan y la familia
consiente, sin asumir funciones de protección.
"""

