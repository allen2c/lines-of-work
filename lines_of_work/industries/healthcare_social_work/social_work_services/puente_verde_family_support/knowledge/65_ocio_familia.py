content = """
El tiempo de ocio compartido (juegos, salidas, deporte, cultura) fortalece el
vínculo y ofrece oportunidades para la comunicación informal. Muchas familias
piden ideas para actividades que se adapten a distintas edades y presupuestos.

**Ideas:** Parques, rutas a pie, bibliotecas, museos con programas familiares,
deportes en equipo, cocinar juntos, juegos de mesa. Lo importante es la calidad
del tiempo, no el gasto. Adaptar a los intereses de los hijos y a la disponibilidad
real.

**Pantallas:** El ocio puede incluir películas o videojuegos compartidos; establecer
límites de tiempo y contenido. Alternar con actividades sin pantalla.

**Adolescentes:** Pueden preferir más tiempo con iguales; respetar esa necesidad y
mantener algunas actividades familiares acordadas (una comida semanal, una salida
al mes) para no perder el contacto.
"""

