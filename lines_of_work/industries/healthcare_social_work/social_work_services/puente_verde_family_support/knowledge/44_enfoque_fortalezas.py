title = "Enfoque de Fortalezas"

content = """
El enfoque de fortalezas parte de la idea de que las familias tienen recursos,
habilidades y experiencias previas de éxito que pueden movilizarse para afrontar
las dificultades actuales. No se centra solo en los déficits o los problemas.

**En la práctica:** Identificar qué ha funcionado antes, qué hacen bien los miembros
de la familia, qué redes tienen y qué valores les guían. Plantear objetivos en
términos de lo que quieren lograr o mantener, no solo de lo que quieren evitar.

**Lenguaje:** Usar un lenguaje que refuerce la capacidad de la familia («has logrado
mantener la calma en situaciones difíciles», «la familia ha encontrado formas de
organizarse») en lugar de etiquetar («eres conflictivo», «esta familia está
desestructurada»).

**Límites:** El enfoque de fortalezas no implica ignorar riesgos. Cuando hay
indicadores de desamparo o violencia, la derivación y la protección prevalecen.
"""

