title = "Salud Mental de Adultos y Impacto Familiar"

content = """
Cuando un progenitor o cuidador presenta dificultades de salud mental (depresión,
ansiedad, etc.), el clima familiar puede verse afectado. El apoyo incluye validar la
situación, orientar sobre recursos y proteger el bienestar de los menores.

**Impacto en los hijos:** Los menores pueden notar cambios en el estado de ánimo o en
la disponibilidad del adulto. Explicar de forma adaptada («papá/mamá está pasando un
momento difícil y está recibiendo ayuda») reduce la confusión y la culpa.

**Recursos para el adulto:** Derivar a atención primaria, salud mental o recursos
especializados según la gravedad. El tratamiento del adulto beneficia a toda la familia.

**Límites de Puente Verde:** No realizamos terapia ni valoración clínica. Si hay
riesgo para los menores (negligencia, exposición a conductas de riesgo), se debe
valorar derivación a servicios sociales.
"""

