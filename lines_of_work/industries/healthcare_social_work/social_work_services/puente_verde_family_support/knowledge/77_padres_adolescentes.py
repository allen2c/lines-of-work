title = "Padres y Madres Adolescentes"

content = """
Los progenitores muy jóvenes (adolescentes o poco más) pueden necesitar apoyo
específico en crianza, continuidad educativa y acceso a recursos. El estigma y la
falta de red agravan a veces la situación; el enfoque ha de ser de apoyo, no de
juicio.

**Necesidades frecuentes:** Información sobre desarrollo infantil y parentalidad;
apoyo para continuar estudios o formación; acceso a vivienda, ingresos y cuidado
del bebé; relación con la familia de origen (que a veces asume mucho cuidado).
Orientar sobre recursos de apoyo a la parentalidad joven (programas públicos,
asociaciones).

**Inclusión del otro progenitor:** Favorecer la corresponsabilidad cuando sea
posible; no asumir que la madre adolescente es la única responsable. Trabajar
con ambos si están presentes.

**Derivación:** Si hay riesgo de desamparo o la madre/padre adolescente está en
situación de vulnerabilidad grave, coordinar con servicios sociales.
"""

