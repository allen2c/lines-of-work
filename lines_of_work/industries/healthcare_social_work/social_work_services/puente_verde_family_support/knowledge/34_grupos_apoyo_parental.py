title = "Grupos de Apoyo Parental"

content = """
Los grupos de apoyo parental reúnen a familias que comparten situaciones similares
(crianza, separación, adopción, discapacidad, etc.) para compartir experiencias y
recursos en un marco facilitado por profesionales o por pares formados.

**Beneficios:** Reducen el aislamiento, normalizan experiencias y ofrecen ideas
prácticas. No sustituyen la intervención individual cuando hay necesidades específicas.

**Tipos:** Algunos están impulsados por servicios públicos (centros de salud, servicios
sociales); otros por asociaciones o entidades. Puente Verde puede derivar a grupos
existentes en el territorio según el perfil de la familia.

**Criterios de derivación:** Cuando la familia expresa necesidad de sentirse acompañada
por otras familias o cuando el profesional valora que el grupo puede complementar el
apoyo individual. Respetar la voluntad de la familia; no todos desean participar en
grupos.
"""

