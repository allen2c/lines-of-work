title = "Coordinación con Centros Educativos"

content = """
La coordinación entre la familia, Puente Verde y el centro educativo (tutor, orientación,
equipo directivo) puede ser necesaria cuando hay dificultades escolares, conductuales o
de integración. Se realiza con el consentimiento de la familia y dentro del marco de
protección de datos.

**Cuándo contactar:** Cuando la familia autoriza y hay un objetivo claro (compartir
información relevante para el bienestar del menor, unificar criterios de límites,
coordinar una derivación). No se contacta con el centro sin autorización explícita.

**Qué transmitir:** Solo la información necesaria para el objetivo acordado. No se
comparten detalles clínicos o familiares que no incumban al ámbito educativo.

**Reuniones:** Pueden ser presenciales o por vía telemática. Dejar constancia de lo
acordado y de los pasos siguientes. El centro educativo es un aliado; la comunicación
respetuosa y clara favorece el interés del menor.
"""

