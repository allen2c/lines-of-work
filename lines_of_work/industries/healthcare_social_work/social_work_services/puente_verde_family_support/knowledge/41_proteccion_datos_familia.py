title = "Protección de Datos en la Atención Familiar"

content = """
El tratamiento de datos personales en el ámbito del apoyo familiar está sujeto al
Reglamento General de Protección de Datos (RGPD) y a la Ley Orgánica de Protección
de Datos española. Solo se recogen y comparten los datos necesarios para la finalidad
del servicio, con consentimiento o base legal adecuada.

**Consentimiento:** Informar a la familia sobre qué datos se recogen, para qué y con
quiénes se pueden compartir (otros profesionales, administración). Recoger el
consentimiento por escrito cuando proceda. En menores, el consentimiento lo dan los
titulares de la patria potestad, salvo en supuestos que la ley reserve al menor.

**Comunicaciones:** No enviar datos personales por correo no seguro; usar canales
oficiales o acordados. No dejar expedientes o pantallas visibles a personas no
autorizadas.

**Derechos:** La familia tiene derecho a acceder, rectificar, suprimir y limitar el
tratamiento. Informar de estos derechos y de la posibilidad de reclamar ante la
Autoridad de Control.
"""

