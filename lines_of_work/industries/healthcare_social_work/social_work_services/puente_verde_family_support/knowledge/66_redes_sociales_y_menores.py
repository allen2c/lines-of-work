content = """
El uso de redes sociales por parte de menores genera preocupaciones sobre privacidad,
contactos no deseados, ciberacoso y tiempo de uso. La educación digital y la
supervisión adaptada a la edad reducen riesgos sin prohibir por sistema.

**Edad:** Muchas redes tienen edad mínima (por ejemplo, 13-14 años). Respetar esas
edades y explicar el porqué. Antes de esa edad, supervisar más y limitar el acceso.

**Conversación:** Hablar de qué publican, con quién hablan y qué hacer si reciben
mensajes inapropiados o acoso. Transmitir que pueden acudir a un adulto sin miedo
a que les quiten el móvil por castigo.

**Modelo:** El uso que hacen los progenitores de las redes influye. Pactar momentos
sin pantallas en familia y no publicar fotos de los hijos sin su consentimiento
cuando tengan edad para decidir.
"""

