content = """
Las familias que acogen o adoptan a menores necesitan apoyo en la integración del niño
o adolescente, que puede llegar con historias de abandono, maltrato o institucionalización.
La adaptación es bidireccional y puede llevar tiempo.

**Vinculación:** El menor puede mostrar desconfianza, conductas de prueba o rechazo
inicial. La paciencia, la consistencia afectiva y las rutinas ayudan. No forzar el
contacto físico ni el uso de términos («mamá», «papá») antes de que el menor esté
preparado.

**Orígenes:** En adopción, el derecho del niño a conocer su historia está reconocido.
Hablar de los orígenes con honestidad adaptada a la edad favorece la identidad. Los
recursos de postadopción y los grupos de familias adoptivas son un apoyo valioso.

**Derivación:** Los casos con trauma complejo o trastornos del apego pueden requerir
terapia especializada. Puente Verde orienta y deriva cuando el acompañamiento breve
no basta.
"""

