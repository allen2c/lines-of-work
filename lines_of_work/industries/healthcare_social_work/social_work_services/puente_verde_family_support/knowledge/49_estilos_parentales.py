content = """
Los estilos parentales describen formas de relacionarse con los hijos en términos de
calidez y de exigencia/control. La investigación distingue habitualmente entre
autoritativo (calidez y límites claros), autoritario (poco afecto, mucho control),
permisivo (mucho afecto, pocos límites) y negligente (poco de ambos).

**Estilo autoritativo:** Asociado a mejores resultados en bienestar y rendimiento.
Combina afecto, comunicación y normas coherentes. Es el que se promueve desde la
parentalidad positiva.

**No etiquetar:** Evitar colgar etiquetas («eres un padre autoritario»). En su lugar,
describir conductas y proponer alternativas (« cuando gritas, el niño se bloquea;
podemos probar a bajar el tono y explicar la consecuencia»).

**Cambio:** Los estilos pueden modificarse con conciencia y práctica. El apoyo
familiar puede proponer pequeños pasos y reforzar los avances.
"""

