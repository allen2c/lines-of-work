title = "Sueño en la Infancia"

content = """
Los problemas de sueño (insomnio de conciliación, despertares frecuentes, resistencia
a acostarse) son muy habituales y afectan al bienestar del menor y de la familia.
Las rutinas y la coherencia suelen ser más útiles que las soluciones mágicas.

**Higiene del sueño:** Horarios regulares, ambiente tranquilo y oscuro, evitar pantallas
y actividad intensa antes de dormir. En niños mayores, una rutina corta y previsible
(ducha, cuento, apagar luz) facilita la transición.

**Despertares:** En la primera infancia los despertares pueden ser normales. Valorar si
el niño sabe volver a dormirse solo o depende de la presencia del adulto; en este
segundo caso, se pueden trabajar paulatinamente estrategias de retirada o de
acompañamiento mínimo.

**Cuándo derivar:** Si hay ronquidos intensos, apnea, terrores nocturnos muy frecuentes
o somnolencia diurna, derivar a pediatría o a unidad de sueño.
"""

