content = """
En situaciones de riesgo inminente (violencia, desamparo, crisis de salud mental con
riesgo autolítico), la familia o el profesional deben conocer los recursos de
urgencia disponibles.

**Teléfonos:** 112 (emergencias generales); 016 (violencia de género, no deja rastro
en factura); teléfonos de atención a la infancia y de salud mental de crisis según
territorio. Tener los números actualizados en el centro.

**Servicios sociales de guardia:** Algunas zonas tienen servicios sociales de urgencia
o turnos de guardia. Informar a la familia de cómo acceder.

**Protocolo interno:** En Puente Verde, ante una situación de urgencia, el profesional
informa a la familia del recurso, acompaña la derivación si es necesario y deja
constancia. No se abandona a la persona en crisis sin indicar adónde acudir.
"""

