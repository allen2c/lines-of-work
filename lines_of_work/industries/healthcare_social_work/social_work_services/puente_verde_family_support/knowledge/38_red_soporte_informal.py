content = """
La red de soporte informal (familia extensa, amistades, vecindad, comunidad religiosa
o cultural) es un recurso fundamental para muchas familias. Fortalecerla y conectar
a la familia con ella forma parte del apoyo.

**Identificación:** En la valoración inicial, explorar quién forma parte de la red,
qué tipo de apoyo ofrecen o podrían ofrecer y si hay conflictos o barreras que
dificultan el contacto.

**No sustituir:** El objetivo no es que los profesionales sustituyan la red, sino que
la familia pueda apoyarse en ella de forma más efectiva. A veces basta con recordar
que pedir ayuda no es debilidad y sugerir formas concretas de pedirla.

**Precauciones:** En casos de violencia o de riesgo para menores, valorar si algún
miembro de la red puede ser un apoyo seguro o si hay que tener cuidado con
confidencias que pongan en riesgo a la persona.
"""

