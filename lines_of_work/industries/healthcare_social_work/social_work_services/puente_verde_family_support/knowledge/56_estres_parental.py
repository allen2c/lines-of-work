title = "Estrés Parental"

content = """
El estrés parental es la tensión que experimentan los progenitores ante las demandas
de la crianza. Puede deberse a características del menor (temperamento difícil,
necesidades especiales), a la falta de apoyo o a factores externos (económicos,
laborales, de salud).

**Señales:** Irritabilidad, cansancio crónico, sensación de no poder más, conflictos
frecuentes con la pareja o con los hijos, evitación de situaciones (no querer llegar
a casa, delegar en exceso).

**Intervención:** Validar el malestar, explorar qué está en la base (sueño, apoyo,
expectativas) y proponer pequeños cambios. Reforzar el autocuidado y la petición de
ayuda. Conectar con grupos de apoyo o con recursos de respiro si existen.

**Derivación:** Si hay indicadores de depresión, ansiedad o riesgo para los menores,
derivar a atención primaria o salud mental. El estrés parental elevado puede
afectar la calidad del cuidado.
"""

