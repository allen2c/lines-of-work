title = "Mediación Familiar"

content = """
La mediación familiar es un proceso voluntario en el que un mediador neutral ayuda a
dos o más personas a alcanzar acuerdos sobre cuestiones que les afectan (custodia,
visitas, comunicación, reparto de bienes). No impone soluciones; facilita el diálogo.

**Cuándo derivar:** Conflictos entre progenitores que impiden acuerdos básicos;
separaciones o divorcios en que se quiere evitar la vía judicial o complementarla;
desacuerdos con hijos adolescentes o con familia extensa cuando proceda.

**Recursos:** Servicios de mediación familiar dependientes de la Generalitat o de
ayuntamientos; mediadores privados colegiados. En algunos casos la mediación es
obligatoria antes de acudir al juzgado (consultar normativa vigente).

**Límites:** La mediación no es adecuada cuando hay violencia de género o maltrato
infantil. En esos casos se deriva a servicios de protección y recursos especializados.
"""

