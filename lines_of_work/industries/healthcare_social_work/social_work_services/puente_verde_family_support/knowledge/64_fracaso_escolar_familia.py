title = "Fracaso Escolar y Apoyo Familiar"

content = """
El bajo rendimiento o el abandono escolar pueden deberse a múltiples factores:
dificultades de aprendizaje, desmotivación, problemas emocionales, acoso o
situación familiar. La familia puede apoyar sin sustituir el diagnóstico
educativo o psicológico.

**Exploración:** Recoger la visión del menor, de la familia y, con autorización,
del centro. Identificar si hay dificultades específicas (dislexia, TDAH, ansiedad)
que requieran evaluación. La coordinación con orientación educativa es clave.

**Apoyo en casa:** Crear condiciones para el estudio (espacio, horario, límite de
distracciones). Reforzar el esfuerzo más que la nota. Evitar la presión excesiva
que aumenta la ansiedad.

**Alternativas:** No todos los jóvenes siguen la vía académica estándar. Informar
sobre FP, programas de segunda oportunidad y otras rutas. El objetivo es que el
menor encuentre un proyecto viable, no solo «aprobar».
"""

