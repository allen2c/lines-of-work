title = "Seguimiento y Cierre del Caso"

content = """
El seguimiento consiste en valorar periódicamente si los objetivos acordados con la
familia se están cumpliendo y si el apoyo sigue siendo necesario. El cierre se
acuerda cuando la familia ha alcanzado los objetivos o cuando se deriva a otro
recurso; debe quedar documentado.

**Seguimiento:** En cada contacto, revisar los acuerdos previos, los avances y las
dificultades. Ajustar el plan si la situación ha cambiado. No alargar la intervención
sin motivo; el apoyo familiar es de carácter limitado en el tiempo.

**Cierre:** Informar a la familia de que se propone el cierre (o que ellos lo solicitan).
Recoger su valoración si procede. Dejar constancia de los motivos del cierre, de los
recursos a los que se ha derivado (si aplica) y de la posibilidad de volver a contactar
si surgen nuevas necesidades.

**Reapertura:** Si la familia vuelve tiempo después, valorar de nuevo la demanda y
el recurso más adecuado; no asumir que debe reabrirse el mismo tipo de intervención.
"""

