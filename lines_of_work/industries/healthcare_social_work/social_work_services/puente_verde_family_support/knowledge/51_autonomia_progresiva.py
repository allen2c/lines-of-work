title = "Autonomía Progresiva"

content = """
La autonomía progresiva es el principio por el cual el menor va asumiendo decisiones
y responsabilidades según su madurez. Favorecerla implica ir soltando control en
ámbitos adecuados a la edad y mantener el acompañamiento emocional.

**Por edades:** En la primera infancia, las decisiones son muy acotadas (elegir entre
dos opciones). En la niñez media, se pueden delegar más tareas (ordenar la mochila,
gestionar el tiempo de deberes con supervisión). En la adolescencia, negociar horarios,
uso de pantallas y participación en decisiones familiares.

**Equilibrio:** Ni sobreproteger ni abandonar. Ofrecer apoyo cuando lo pidan y dejar
espacio para que asuman consecuencias naturales cuando el riesgo es bajo.

**Marco legal:** La legislación reconoce el derecho del menor a ser escuchado y a que
su opinión sea tenida en cuenta según su edad y madurez. En determinados actos (por
ejemplo, en procesos judiciales que les afecten), su consentimiento puede ser
necesario.
"""

