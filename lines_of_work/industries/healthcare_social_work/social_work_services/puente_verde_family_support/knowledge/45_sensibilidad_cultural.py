title = "Sensibilidad Cultural en el Apoyo Familiar"

content = """
Las familias pueden tener orígenes, creencias y prácticas diversas. La sensibilidad
cultural implica respetar esas diferencias, evitar estereotipos y adaptar la
intervención sin imponer un único modelo de familia o de crianza.

**Exploración:** Preguntar con respeto sobre la composición familiar, los roles,
las expectativas sobre los hijos y las creencias que influyen en las decisiones.
No asumir que todas las familias comparten el mismo modelo nuclear o las mismas
prioridades.

**Recursos:** Conocer recursos específicos para familias migrantes, minorías
culturales o religiosas (asociaciones, mediadores interculturales) cuando puedan
ser de ayuda.

**Límites:** El respeto cultural no justifica prácticas que vulneren derechos
(por ejemplo, maltrato o desigualdad que ponga en riesgo a menores). En esos
casos se actúa con claridad sobre los derechos aplicables y se deriva si procede.
"""

