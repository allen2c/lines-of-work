content = """
Las rutinas estructuran el día y reducen la incertidumbre, especialmente en la infancia.
Ayudan a regular el sueño, la alimentación y el tiempo de estudio o juego, y facilitan
la conciliación cuando hay varios miembros en el hogar.

**Rutinas básicas:** Despertar, comidas, deberes, tiempo libre, preparación para dormir.
La regularidad en horarios aproximados (no rígidos al minuto) suele mejorar el comportamiento
y el descanso.

**Flexibilidad:** En fines de semana, vacaciones o situaciones excepcionales las rutinas
pueden relajarse. Explicar los cambios a los menores («hoy cenamos más tarde porque
hay visita») evita que lo vivan como arbitrariedad.

**Participación:** A medida que crecen, los hijos pueden asumir tareas dentro de la rutina
(recoger la mesa, preparar la mochila). La participación refuerza la sensación de
pertenencia y responsabilidad.
"""

