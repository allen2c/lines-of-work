title = "Indicadores de Riesgo de Desamparo"

content = """
El desamparo es la situación en que un menor no recibe los cuidados necesarios para su
desarrollo por parte de sus responsables legales, o está expuesto a situaciones de
riesgo grave. Conocer los indicadores permite derivar a tiempo a servicios de protección.

**Indicadores de posible negligencia:** Desnutrición o higiene muy deficiente; ausencia
de supervisión adecuada a la edad; abandono escolar reiterado sin justificación;
enfermedades no tratadas; menores solos en casa de forma habitual e inadecuada a su edad.

**Indicadores de maltrato:** Lesiones no explicadas o explicaciones incoherentes;
conductas sexualizadas no acordes a la edad; miedo intenso a un adulto; revelación
por parte del menor de abusos. La violencia entre progenitores con presencia de
menores también es un indicador de riesgo.

**Actuación:** Ante sospecha fundada, derivar a servicios sociales de base o al equipo
de atención a la infancia (EAIA). No es función de Puente Verde realizar la valoración
de protección; sí identificar y canalizar.
"""

