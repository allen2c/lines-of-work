title = "Espacios Seguros para Menores"

content = """
Un espacio seguro es aquel en el que el menor puede expresarse sin miedo a ser
juzgado, castigado o expuesto. En las intervenciones de apoyo familiar, es importante
que los menores se sientan escuchados y que no se les use como «informantes» contra
el otro progenitor.

**En la entrevista:** Explicar al menor por qué está ahí y qué vamos a hablar. No
prometer secretos absolutos si hay obligación de derivar (por ejemplo, en caso de
riesgo); sí explicar que lo que diga es importante y que se tratará con respeto.
No forzar a que hable si no quiere.

**En casa:** Orientar a los progenitores para que los menores tengan un espacio
donde expresar emociones y opiniones sin que eso genere represalias. No usar al
menor como mensajero ni como aliado en conflictos de pareja.
"""

