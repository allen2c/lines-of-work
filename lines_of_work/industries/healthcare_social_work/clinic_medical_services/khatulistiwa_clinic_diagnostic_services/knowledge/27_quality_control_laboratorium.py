title = "Quality Control Laboratorium"

content = """
Quality control (QC) memastikan bahwa alat dan prosedur laboratorium menghasilkan hasil yang
akurat dan presisi. QC dijalankan secara rutin oleh petugas lab; unit layanan diagnostik tidak
langsung mengerjakan QC tetapi harus memahami pentingnya.

**QC Harian:** Sebelum memproses pasien, lab menjalankan kontrol dengan bahan referensi known
concentration. Hasil harus dalam range yang ditetapkan. Bila gagal, alat tidak digunakan hingga
perbaikan.

**Kalibrasi:** Analyzer dikalibrasi berkala sesuai protokol pabrik dan standar laboratorium.
Tanpa kalibrasi yang tepat, drift hasil dapat terjadi.

**Dampak ke Pasien:** Jika QC gagal, hasil pasien yang diproses dalam periode tersebut dapat
tidak valid. Lab akan menahan rilis atau meminta pengulangan. Unit layanan diagnostik mengkomunikasikan
keterlambatan atau pengulangan ke pasien dan dokter bila diperlukan.
"""  # noqa: E501

