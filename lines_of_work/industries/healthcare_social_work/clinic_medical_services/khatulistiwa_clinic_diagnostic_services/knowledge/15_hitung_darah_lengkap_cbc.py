title = "Hitung Darah Lengkap (CBC)"

content = """
CBC mengukur hemoglobin, hematokrit, jumlah eritrosit, leukosit (dan differential), dan trombosit.
Tes rutin untuk skrining anemia, infeksi, dan kelainan darah.

**Tidak Perlu Puasa:** CBC dapat dilakukan kapan saja. Puasa tidak mempengaruhi hasil secara bermakna.

**Komponen Utama:** Hemoglobin rendah = anemia. Leukosit tinggi dapat menandakan infeksi; rendah dapat
imunosupresi. Trombosit untuk fungsi pembekuan. Differential count (neutrofil, limfosit, dll) membantu
membedakan jenis infeksi atau gangguan.

**Spesimen:** Tabung EDTA (ungu). Harus dicampur segera setelah pengambilan untuk mencegah pembekuan.
Analisis idealnya dalam beberapa jam; dapat disimpan lebih lama bila didinginkan tergantung lab.

**Interferensi:** Hemolisis dapat menurunkan hemoglobin dan mengacaukan hitungan. Lipemia berat
dapat mengganggu beberapa analyzer. Bila sampel tidak memenuhi syarat, ulangi pengambilan.
"""  # noqa: E501

