content = """
- レセプト（診療報酬明細書）は毎月10日までに作成し、審査支払機関（社会保険診療報酬支払基金または国民健康保険団体連合会）に提出
- レセプトの種類：医科レセプト（入院・外来別）、調剤レセプト（院外処方の場合）
- 記載項目：保険者番号、被保険者記号番号、患者氏名・生年月日、診療実日数、診療行為コード、薬剤コード、点数、合計点数、負担割合等
- レセプト作成はレセプトコンピュータ（ORCA等）で自動生成。受付は点検・修正を担当
- 点検項目：保険証情報の一致、診療行為の重複・漏れ、点数計算の誤り、公費負担の反映
- 提出方法：オンライン請求（電子レセプト）が主流。紙レセプトは一部の保険者向け
- 請求後、審査結果（査定）が返ってきたら、減点・返戻に対応する
"""

