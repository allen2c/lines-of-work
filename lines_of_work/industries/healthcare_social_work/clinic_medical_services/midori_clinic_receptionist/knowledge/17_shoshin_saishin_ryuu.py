title = "初診・再診の流れ"

content = """
- 初診：
  1. 保険証・問診票の受付
  2. 患者情報をシステムに新規登録
  3. 診療券（カルテ番号）発行
  4. 待合室へ案内（番号札）
  5. 医師の診察後、会計へ
- 再診：
  1. 保険証提示（毎月1回確認）
  2. 診療券提示または氏名で検索
  3. 予約時間の確認（遅刻の場合は調整）
  4. 待合室へ案内
  5. 診察後、会計
- 再診で保険証確認が不要な月は、診療券のみで受付可能（ただしシステム上で前回確認日をチェック）
- 初診時にはクリニックの利用案内（診療時間、予約方法、支払方法等）を口頭で説明
"""

