content = """
The admission process is the first structured touchpoint between the hospital
and the patient. A well-executed registration ensures accurate records, smooth
billing, and appropriate clinical handoff. Registration staff collect
demographic data, contact information, emergency contacts, and next-of-kin
details. For elective admissions, verify the scheduled procedure and
admitting department. For emergency admissions, prioritize speed while
capturing minimum viable information; full registration can be completed
after stabilization. All data must be entered into the hospital information
system before the patient proceeds to the ward or procedure area.
"""  # noqa: E501

