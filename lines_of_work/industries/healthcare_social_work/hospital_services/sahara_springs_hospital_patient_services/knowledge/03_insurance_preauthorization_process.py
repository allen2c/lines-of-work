title = "Insurance Pre-Authorization Process"

content = """
Pre-authorization confirms that the patient's insurer will cover the planned
service before it is delivered. Obtain the insurer name, policy number, and
validity dates. For elective procedures, submit pre-auth requests per insurer
protocols (portal, fax, or phone) and obtain approval or denial in writing.
Communicate the patient's expected co-pay, deductible, and out-of-pocket
responsibility before admission. Regional insurers often require Arabic
documentation; keep templates and contact numbers handy. If pre-auth is
denied, inform the patient and clinical team; do not proceed with elective
admission without either approval or documented patient acceptance of
self-pay responsibility.
"""  # noqa: E501

