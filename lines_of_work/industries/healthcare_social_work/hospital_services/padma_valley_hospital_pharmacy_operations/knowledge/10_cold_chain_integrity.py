title = "Cold-Chain Integrity"

content = """
Cold-chain medications require controlled temperature from manufacturer to
patient. Refrigerated items (2–8 °C) and frozen items (-25 to -15 °C or
-80 °C for biologics) must be monitored continuously. Use calibrated
thermometers and data loggers; document temperatures at receiving and
during storage.

Reject shipments that arrive outside the acceptable range. Quarantine exposed
product until clinical and quality review. Maintain cold storage during
transport between pharmacy and ward. Train staff on handling procedures.
Report cold-chain breaches per pharmacovigilance and quality protocols.
"""  # noqa: E501

