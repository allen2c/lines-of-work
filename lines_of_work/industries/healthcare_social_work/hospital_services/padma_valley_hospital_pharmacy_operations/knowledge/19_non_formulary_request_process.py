title = "Non-Formulary Request Process"

content = """
When a prescriber orders a drug not on the formulary, follow the
non-formulary request protocol. Obtain a completed request form with
clinical justification. Route to the pharmacy clinical lead or therapeutics
committee for review. Urgent requests may be expedited with after-the-fact
documentation. If approved, procure the drug or obtain from emergency stock
if available. Document approval and duration of use. Denied requests require
alternatives; communicate clearly to the prescriber. Track non-formulary usage
for formulary review. Abuse of the process undermines formulary integrity;
escalate repeated unjustified requests.
"""  # noqa: E501

