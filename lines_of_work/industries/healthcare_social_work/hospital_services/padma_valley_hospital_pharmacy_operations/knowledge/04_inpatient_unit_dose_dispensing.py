title = "Inpatient Unit-Dose Dispensing"

content = """
Unit-dose dispensing delivers medications in single-dose packaging prepared for
individual administration. Each dose is labeled with patient name, drug, strength,
route, and administration time. Unit-dose reduces medication errors, supports
nursing efficiency, and facilitates accurate bedside administration.

Batch preparation follows the daily medication schedule. Verify each order against
the MAR before preparing; double-check high-alert medications. Return unused or
discontinued doses to the pharmacy for proper disposal or credit. Maintain
separation of look-alike and sound-alike drugs during preparation and storage.
"""  # noqa: E501

