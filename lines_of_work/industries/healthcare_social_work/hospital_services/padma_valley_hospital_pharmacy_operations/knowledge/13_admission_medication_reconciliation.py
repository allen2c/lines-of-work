title = "Admission Medication Reconciliation"

content = """
At admission, reconcile the patient's home medications with the inpatient
prescribing plan. Obtain a complete list of medications the patient was taking
before admission: prescription, OTC, and herbal products. Resolve discrepancies
with the admitting physician: continue, hold, or substitute. Document the
reconciliation in the EHR. Clarify unclear or incomplete information with the
patient or family. Prioritize high-risk medications: anticoagulants,
antidiabetics, cardiovascular drugs. Incomplete reconciliation increases the
risk of omissions, duplications, or harmful interactions during the stay and
at discharge.
"""  # noqa: E501

