title = "Stock Level Monitoring"

content = """
Maintain adequate stock to meet patient needs without excessive overstock.
Define minimum and maximum levels (reorder points) for each item based on
usage patterns and lead time. Monitor stock levels routinely; generate
reorder reports when items fall below the reorder point. Consider seasonal
demand, epidemic outbreaks, and formulary changes when adjusting levels.
Critical and short-supply items may require daily monitoring. Coordinate with
procurement for replenishment; escalate stock-outs affecting patient care
immediately. Document stock discrepancies and investigate causes. Avoid
hoarding; excess stock increases expiry and storage costs.
"""  # noqa: E501

