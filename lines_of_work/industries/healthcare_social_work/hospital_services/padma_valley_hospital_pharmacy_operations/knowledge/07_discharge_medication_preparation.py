title = "Discharge Medication Preparation"

content = """
Discharge medications must be ready before the patient leaves the hospital to
ensure continuity of care and reduce readmission risk. Coordinate with nursing
and discharge planning to receive the discharge prescription and reconciled
medication list in advance. Verify all prescribed drugs against the discharge
summary and reconciliation form. Prepare medications with clear instructions
in the patient's preferred language. Complete dispensing and counseling before
the patient departs. Document completion and hand off any pending or
unavailable medications to the outpatient pharmacy or follow-up instructions.
Prioritize discharge prescriptions over routine refills when capacity is limited.
"""  # noqa: E501

