title = "Allergy and Contraindication Checking"

content = """
Before dispensing any medication, verify the patient has no documented allergies
or contraindications to the prescribed agent. Check the pharmacy system, EHR,
or medication reconciliation record for allergy alerts. Cross-reference the
active ingredient, excipients, and cross-reactive compounds (e.g., penicillin
and cephalosporins). Document the allergy check and outcome. If an allergy is
flagged, do not dispense until the prescriber confirms or provides an
alternative. For new or undocumented allergies reported by the patient,
document immediately and escalate to the prescriber before proceeding.
"""  # noqa: E501

