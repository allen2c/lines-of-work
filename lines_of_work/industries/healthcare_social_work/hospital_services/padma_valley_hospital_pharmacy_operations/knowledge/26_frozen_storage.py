title = "Frozen Storage (-20°C or Below)"

content = """
Some vaccines and biologics require frozen storage at -20°C or -70°C.
Use dedicated pharmaceutical freezers with temperature monitoring and alarms.
Store in original packaging; protect from light if indicated. Do not
re-freeze thawed products unless manufacturer specifies. Document thaw dates
and expiry after thawing. Transport frozen items in validated cold-chain
containers. Certain live vaccines may require frozen storage; others are
refrigerated only. Consult product inserts and WHO guidance. Freezer
failures require immediate action; relocate stock if possible and document.
"""  # noqa: E501

