title = "Refrigerated Storage (2–8°C)"

content = """
Drugs requiring refrigeration must be stored between 2°C and 8°C. Use
pharmacy-grade refrigerators with temperature logging and alarms. Do not
store in domestic refrigerators unless validated. Place items away from
freezer compartment and door to avoid temperature fluctuation. Do not
overcrowd; ensure air circulation. Monitor temperature at least twice daily;
document readings. Calibrate thermometers regularly. In case of power
failure, note duration and consult product literature or manufacturer.
Discard products that have been out of range unless manufacturer advises
otherwise. Vaccines, biologics, and some insulin products are
refrigeration-sensitive.
"""  # noqa: E501

