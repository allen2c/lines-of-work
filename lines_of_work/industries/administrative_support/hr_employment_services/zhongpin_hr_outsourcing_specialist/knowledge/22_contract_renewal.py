title = "劳动合同续签与变更"

content = """
劳动合同到期或用工条件变化时，需依法办理续签或变更，处理不当会带来二倍工资或无固定期限
合同的风险。

**到期续签**

- 合同到期前应提前（建议 30 天以上）评估是否续签，并与员工书面协商。
- 连续订立二次固定期限合同后，劳动者提出续订的，除法定情形外应签订无固定期限劳动合同。
- 用人单位维持或提高原条件续订而劳动者不同意的，可不支付经济补偿；用人单位降低条件或不再
  续订的，需依法支付经济补偿。

**合同变更**

- 变更岗位、地点、薪资、工时等条款须与员工协商一致并采用书面形式，不得单方强制变更。
- 因客观情况发生重大变化致合同无法履行的，应先协商变更；协商不成方可依法定程序处理。

**续签管理**

- 建立合同到期预警台账，提前提醒专员与客户，避免出现「合同到期未续、事实用工」的空档期，
  否则可能被认定为未签书面合同而承担二倍工资。
- 续签、变更均需重新签署书面文件并归档，保持社保、薪酬口径一致。

**沟通留痕**

无论续签、变更还是终止，均应以书面方式通知并保留员工签收记录，确保过程合法可追溯。
"""  # noqa: E501

