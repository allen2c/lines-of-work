"""Skills assessment methods for identifying training gaps."""

title = "Skills Assessment and Gap Analysis"

content = """
Skills assessment is the systematic process of measuring employee competencies against
role requirements and organizational goals. It provides the evidence base for training
needs analysis and ensures that development investments target real gaps.

**Assessment Methods**

- **Self-assessment:** Employees rate their own proficiency. Useful for engagement but
  subject to bias; combine with manager or peer input for balance.
- **Manager assessment:** Supervisors evaluate direct reports. Provides operational
  perspective but may miss cross-functional or emerging skills.
- **360-degree feedback:** Multi-rater feedback from peers, reports, and managers.
  Best for leadership and soft skills; requires careful rollout to build trust.
- **Competency tests:** Standardized tests or simulations measuring specific knowledge
  or skills. Objective but may not capture contextual application.
- **Performance data:** KPIs, project outcomes, and work samples. Reflects real-world
  application but can be influenced by factors beyond skill level.

**Gap Analysis Process**

1. Define the target competency model for each role or career level.
2. Assess current state using one or more methods above.
3. Compare current vs. target to identify gaps.
4. Prioritize gaps by business impact and feasibility of training.
5. Map gaps to training solutions or other interventions (e.g., hiring, job redesign).
"""

