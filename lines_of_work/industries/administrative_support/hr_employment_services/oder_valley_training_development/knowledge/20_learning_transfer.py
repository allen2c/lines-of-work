"""Ensuring learning transfers from training to workplace application."""

title = "Learning Transfer and Application Support"

content = """
Learning transfer is the extent to which participants apply newly acquired
knowledge and skills on the job. Without deliberate support, a large share
of training impact is lost within weeks of completion.

**Barriers to Transfer**

- Lack of opportunity to practice (e.g., infrequent tasks).
- Competing priorities and time pressure.
- Insufficient manager support or reinforcement.
- Work environment that does not reward or enable new behaviors.
- Forgetting curve; skills decay without spaced practice.

**Transfer Support Strategies**

- **Pre-training:** Align with manager on expectations; identify real
  applications; prepare participants for active participation.
- **During training:** Use realistic scenarios and practice; build in
  action planning for post-training application.
- **Post-training:** Follow-up sessions, coaching, or peer check-ins;
  manager reinforcement and feedback; job aids and reminders.
- **Systemic:** Align performance management, incentives, and workflows
  with desired behaviors so the environment supports rather than blocks transfer.
"""

