"""Design and operation of workplace mentoring programs."""

title = "Mentoring Programs"

content = """
Mentoring pairs experienced employees (mentors) with less experienced ones
(mentees) for guidance, knowledge transfer, and career development. When
structured well, mentoring complements formal training and strengthens
organizational culture.

**Program Design**

- Define objectives: onboarding acceleration, leadership pipeline, diversity
  advancement, or technical knowledge transfer.
- Establish eligibility criteria for mentors and mentees.
- Match pairs based on goals, compatibility, and diversity considerations;
  avoid direct reporting relationships to ensure psychological safety.
- Set duration (e.g., 6–12 months) and expected meeting frequency.
- Provide mentor training on listening, feedback, and boundary-setting.

**Operations**

- Kick off with orientation for both parties; clarify roles and expectations.
- Offer optional discussion guides or topic prompts without over-structuring.
- Check in periodically; address mismatches or disengagement early.
- Celebrate completions and capture feedback for program improvement.
"""

