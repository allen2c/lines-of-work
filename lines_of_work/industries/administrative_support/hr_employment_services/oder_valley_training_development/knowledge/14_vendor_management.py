"""Selecting and managing external training providers."""

title = "Vendor Management for Training"

content = """
External training vendors extend organizational capability when internal resources
are insufficient or specialized expertise is required. Effective vendor management
ensures quality, consistency, and value for money.

**Vendor Selection**

- Define clear requirements: learning objectives, audience, format, timeline, budget.
- Issue RFPs or conduct structured interviews with shortlisted providers.
- Evaluate on expertise, methodology, references, and cultural fit.
- Pilot with a small cohort before full rollout when possible.
- Include contract terms for IP, confidentiality, and performance guarantees.

**Ongoing Management**

- Assign a single point of contact for each vendor relationship.
- Establish SLAs for delivery quality, responsiveness, and reporting.
- Conduct regular reviews; share feedback and adjust scope as needed.
- Maintain a vendor register with performance history for future sourcing.
- Diversify providers to avoid over-reliance and encourage competition.
"""

