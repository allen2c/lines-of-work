title = "Training Documentation and Records"

content = """
Accurate training documentation supports compliance, audit readiness, and
evidence-based decision-making. Records should be consistent, retrievable,
and retained according to legal and organizational requirements.

**What to Document**

- Course catalogs, syllabi, and learning objectives.
- Attendance and completion records per participant.
- Assessment results (e.g., test scores, certifications).
- Facilitator and vendor qualifications where required.
- Evaluation data (reaction, learning, behavior, results).
- Changes to content or delivery and version history.

**Systems and Practices**

- Use an LMS or dedicated HR system for centralized record storage.
- Define retention periods (e.g., 3–7 years for compliance training).
- Ensure GDPR and data protection compliance for personal data.
- Provide employees access to their own training history.
- Export and archive records before system changes or vendor transitions.
"""

