content = """
Onboarding-Schulungen führen neue Mitarbeiter in das Unternehmen, die Abteilung und
ihre Rolle ein. Sie reduzieren Einarbeitungszeit und Fluktuation und fördern die
Integration.

**Inhalte**

- Unternehmensgeschichte, Werte, Organisationsstruktur
- Relevante Richtlinien (Compliance, Datenschutz, Arbeitssicherheit)
- IT-Systeme, Zugänge, Kommunikationskanäle
- Abläufe und Ansprechpartner im eigenen Bereich
- Erwartungen an die Rolle und erste Ziele

**Formate**

Kombination aus zentralen Einführungstagen, Abteilungsspezifika und begleitetem
Learning-by-doing. Checklisten und Zeitpläne geben Struktur. Paten oder Mentoren
unterstützen die ersten Wochen.

**Evaluation**

Kurze Befragungen nach der Einführungswoche und nach 90 Tagen: Verständnis der
Rolle, Zufriedenheit mit der Einarbeitung, offene Fragen. Ergebnisse fließen in
die kontinuierliche Verbesserung des Onboarding-Prozesses ein.
"""  # noqa: E501

