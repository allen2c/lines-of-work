"""Protocole de vérification et de gestion des passeports de service."""

title = "Protocole passeport de service"

content = """
Les passeports de service (ou officiels) sont émis par les autorités nationales aux
fonctionnaires et diplomates. Avant toute réservation, vérifier la validité : au moins
six mois après la date de retour, et suffisamment de pages vierges.

**Vérifications** : Numéro, date d'émission, date d'expiration, nom et photo conformes
au billet. Certaines compagnies exigent une validité de 6 mois minimum.

**Renouvellement** : Si le passeport arrive à expiration pendant le voyage, le voyageur
doit le renouveler avant le départ. Coordonner avec la mission pour les délais.

**Copies** : Conserver une copie scannée dans le dossier voyage pour les urgences et
la réémission de documents en cas de perte.
"""  # noqa: E501

