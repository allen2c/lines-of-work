"""Knowledge item 04: reservas de salas de reunião."""

title: str = "Reservas de Salas de Reunião"

content: str = """
As salas de reunião são um recurso compartilhado e devem ser reservadas com antecedência.
O sistema de agendamento evita conflitos e garante uso equitativo entre os usuários.

**Procedimento:** Registrar reserva com nome do solicitante, data, horário de início e fim,
e número de participantes. Verificar disponibilidade antes de confirmar. Reservas
recorrentes podem ser cadastradas com validade definida.

**Regras de Uso:** Respeitar o horário reservado; encerrar a reunião no prazo para não
atrasar o próximo usuário. Deixar a sala em ordem: apagar quadros, recolher materiais e
desligar equipamentos.

**Cancelamento:** Cancelar com antecedência mínima de 2 horas para liberar o horário.
Faltas repetidas sem aviso podem resultar em restrição de reservas.
"""  # noqa: E501

