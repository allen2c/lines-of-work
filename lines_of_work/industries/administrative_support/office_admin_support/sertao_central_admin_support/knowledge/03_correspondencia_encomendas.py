"""Knowledge item 03: tratamento de correspondência e encomendas."""

title: str = "Tratamento de Correspondência e Encomendas"

content: str = """
A administração central recebe correspondência e encomendas em nome dos usuários. O processo
deve garantir registro, armazenamento seguro e distribuição ou notificação em tempo hábil.

**Recepção:** Registrar cada item recebido com data, remetente, destinatário e tipo (carta,
encomenda, documento). Encomendas volumosas ou perecíveis devem ser armazenadas em área
adequada e o destinatário notificado no mesmo dia.

**Distribuição:** Colocar correspondência nas caixas dos usuários ou entregar em mãos quando
solicitado. Documentos sigilosos ou registrados exigem assinatura de recebimento.

**Envios:** Auxiliar usuários com postagem de cartas e pacotes. Verificar horários dos
correios e transportadoras locais. Manter registro de rastreamento quando aplicável.
"""  # noqa: E501

