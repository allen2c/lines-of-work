title: str = "Atendimento Telefônico"

content: str = """
O atendimento telefônico é a porta de entrada para muitas demandas. Uma postura profissional
e objetiva transmite credibilidade e facilita a resolução das solicitações.

**Saudação:** Atender com identificação do centro e oferta de ajuda. Exemplo: "Sertão
Central, bom dia. Como posso ajudar?"

**Triagem:** Identificar a natureza da ligação (visita, reserva, correspondência, reclamação)
e direcionar para o setor ou pessoa adequada. Anotar recados com nome, telefone, assunto
e horário da ligação.

**Recados:** Garantir que a mensagem seja repassada no mesmo dia. Em casos urgentes, tentar
contato alternativo ou deixar aviso visível para o destinatário.
"""  # noqa: E501

