title: str = "Estoque de Materiais de Escritório"

content: str = """
O controle de estoque evita rupturas e desperdícios. Materiais como papel, toners,
canetas e grampeadores são de uso comum e devem ser monitorados regularmente.

**Inventário:** Manter lista de itens essenciais e quantidades mínimas. Realizar contagem
semanal ou quinzenal conforme o volume de uso.

**Reposição:** Acionar pedido de compra quando o estoque atingir o limite definido.
Registrar data do pedido e previsão de entrega. Em regiões com logística limitada, antecipar
compras para evitar falta.

**Distribuição:** Controlar a retirada de materiais pelos usuários. Itens de alto custo
(toners, cartuchos) podem exigir registro ou aprovação do supervisor.
"""  # noqa: E501

