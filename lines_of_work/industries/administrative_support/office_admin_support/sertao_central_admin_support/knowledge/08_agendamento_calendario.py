title: str = "Agendamento e Calendário"

content: str = """
O calendário compartilhado permite visualizar compromissos, reuniões e eventos do centro.
A consistência entre agenda física e digital evita conflitos e duplas marcações.

**Uso:** Registrar reuniões externas, visitas programadas, eventos e feriados. Sincronizar
com os calendários dos usuários quando houver integração disponível.

**Atualização:** Manter o calendário atualizado. Cancelamentos e alterações devem ser
refletidos imediatamente para que outros possam aproveitar horários liberados.

**Comunicação:** Para eventos que afetam todos (manutenção, feriado, blackout), divulgar
com antecedência por aviso na recepção e, quando possível, por e-mail ou grupo.
"""  # noqa: E501

