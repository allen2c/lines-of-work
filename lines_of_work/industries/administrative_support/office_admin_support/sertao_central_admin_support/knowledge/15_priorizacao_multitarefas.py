title: str = "Priorização e Multitarefas"

content: str = """
O administrativo recebe demandas simultâneas de múltiplos usuários. Priorizar corretamente
evita atrasos críticos e frustração.

**Critérios:** Urgência (prazo imediato), impacto (quantas pessoas afetadas) e dependência
(outras tarefas bloqueadas). Segurança e saúde sempre em primeiro lugar.

**Práticas:** Manter lista de tarefas com prazos. Comunicar quando o prazo solicitado não for
viável. Para demandas complexas, quebrar em etapas e informar o progresso.

**Limites:** Não assumir mais do que pode entregar. Em picos de demanda, escalonar ou
negociar prazos com os solicitantes. Registrar sobrecarga para discussão com o supervisor.
"""  # noqa: E501

