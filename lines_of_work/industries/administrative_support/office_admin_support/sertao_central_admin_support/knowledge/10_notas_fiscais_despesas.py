title: str = "Notas Fiscais e Despesas Básicas"

content: str = """
A administração central pode receber e organizar notas fiscais e comprovantes de despesas
em nome dos usuários. O processo deve seguir as regras contábeis e fiscais aplicáveis.

**Recepção:** Registrar cada documento com data, valor, fornecedor e finalidade. Guardar
original ou cópia autenticada conforme exigência do setor de contabilidade.

**Organização:** Agrupar por período (mensal) e por tipo de despesa. Manter pasta ou
arquivo digital acessível para o responsável pela prestação de contas.

**Limites:** A administração central não aprova despesas nem emite pagamentos. Questões
sobre reembolso, aprovação ou classificação contábil devem ser encaminhadas ao setor
financeiro ou ao supervisor.
"""  # noqa: E501

