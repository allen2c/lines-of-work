"""Knowledge item 19: comunicação remota. Atendimento por telefone, e-mail e mensagens."""

title: str = "Comunicação Remota"

content: str = """
Muitos usuários do Sertão Central estão em municípios distantes e se comunicam por telefone,
e-mail ou mensagens. O atendimento remoto deve ser tão eficiente quanto o presencial.

**Telefone:** Atender com cortesia, identificar-se e anotar nome, contato e demanda. Se não
puder resolver na hora, informar prazo de retorno. Evitar colocar em espera por tempo excessivo.

**E-mail:** Responder em até um dia útil. Usar assunto claro e corpo objetivo. Para demandas
complexas, agendar ligação ou reunião.

**Mensagens:** Confirmar recebimento. Não tratar assuntos sensíveis por canais inseguros.
Manter tom profissional mesmo em aplicativos informais.
"""  # noqa: E501

