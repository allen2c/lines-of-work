"""Knowledge item 07: arquivamento de documentos."""

title: str = "Arquivamento de Documentos"

content: str = """
A organização de arquivos físicos e digitais facilita a localização e preserva a integridade
das informações. Cada usuário ou setor pode ter regras específicas; a administração central
apoia a padronização e o cumprimento de prazos de retenção.

**Classificação:** Agrupar documentos por tipo (contratos, fiscais, RH) e cronologia.
Usar nomenclatura consistente para pastas e arquivos digitais.

**Retenção:** Conhecer os prazos legais de guarda (ex.: 5 anos para documentos fiscais).
Documentos fora do prazo devem ser descartados conforme procedimento de eliminação segura.

**Acesso:** Garantir que apenas pessoas autorizadas acessem documentos confidenciais.
Arquivos físicos em armários devem ter controle de chaves; arquivos digitais, permissões
adequadas.
"""  # noqa: E501

