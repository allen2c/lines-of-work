"""Knowledge item 12: coordenação com fornecedores. Contato, pedidos e acompanhamento."""

title: str = "Coordenação com Fornecedores"

content: str = """
A administração central mantém contato com fornecedores de materiais, limpeza, manutenção e
serviços gerais. A coordenação deve ser clara e documentada.

**Pedidos:** Registrar pedidos com data, descrição, quantidade e valor estimado. Encaminhar
para aprovação quando ultrapassar limites definidos. Confirmar prazos de entrega com o fornecedor.

**Acompanhamento:** Acompanhar entregas e verificar conformidade. Em caso de atraso ou
divergência, comunicar ao responsável e ao fornecedor. Manter histórico de fornecedores
confiáveis e problemáticos.

**Pagamentos:** Encaminhar notas fiscais e comprovantes ao setor financeiro no prazo.
Não realizar pagamentos diretos sem autorização.
"""  # noqa: E501

