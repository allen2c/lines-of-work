title: str = "Precisão em Lançamentos e Dados"

content: str = """
Erros em registros administrativos geram retrabalho, inconsistências e podem afetar
decisões. A atenção aos detalhes e a conferência antes de finalizar são fundamentais.

**Boas Práticas:** Digitar dados com calma; conferir nomes, datas e valores antes de salvar.
Em planilhas, usar validação de dados quando possível para reduzir erros de digitação.

**Correção:** Ao identificar um erro, corrigir e documentar brevemente a alteração quando
relevante. Não apagar registros sem autorização; preferir anulação ou estorno conforme
procedimento.

**Revisão:** Para documentos ou relatórios importantes, fazer uma segunda leitura ou
pedir conferência de outro colega antes de enviar ou arquivar.
"""  # noqa: E501

