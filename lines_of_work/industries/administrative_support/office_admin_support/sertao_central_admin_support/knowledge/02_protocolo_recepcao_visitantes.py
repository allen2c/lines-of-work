"""Knowledge item 02: protocolo de recepção e visitantes."""

title: str = "Protocolo de Recepção e Visitantes"

content: str = """
O processo de recepção é o primeiro contato entre visitantes e o Sertão Central. Um
atendimento cordial e eficiente transmite profissionalismo e segurança. Todos os visitantes
devem ser cumprimentados, identificados e direcionados adequadamente.

**Identificação:** Solicitar documento com foto (RG, CNH ou passaporte) e registrar nome,
empresa ou instituição e destino no livro de visitantes. Visitantes sem identificação
devem aguardar confirmação do anfitrião antes de liberar o acesso.

**Registro de Entrada:** Anotar horário de entrada e emitir crachá de visitante quando
aplicável. O crachá deve ser devolvido na saída. Para visitantes frequentes, verificar
cadastro pré-aprovado para agilizar o processo.

**Direcionamento:** Confirmar com o anfitrião antes de liberar o visitante para as salas.
Em caso de atraso do anfitrião, oferecer espera na recepção com água ou café.
"""  # noqa: E501

