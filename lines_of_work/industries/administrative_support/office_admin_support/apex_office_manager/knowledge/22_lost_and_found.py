title = "Lost and Found Procedures"

content = """
- All found items are brought to the front desk. Log them in the lost and found spreadsheet: date found, item description, location found, finder name.
- Store items in the locked cabinet behind the reception desk. Valuable items (laptops, phones, wallets) are stored in the safe.
- Attempt to identify the owner: check name on item, ask around, or send a company-wide email (for valuable items only).
- Hold items for 30 days. After 30 days, unclaimed items are donated to charity (clothing, books) or disposed of (food, personal care).
- For items claimed, ask the person to describe the item and show ID. Log the return date and signature.
"""

