title = "Prioritization and Multitasking"

content = """
Administrative work involves many competing demands. Effective prioritization and controlled
multitasking prevent overload and ensure critical tasks are completed on time.

**Urgency vs. importance:** Urgent tasks demand immediate attention; important tasks affect
long-term outcomes. Address urgent-and-important first, then important-but-not-urgent. Delegate
or defer when possible.

**Daily planning:** Start the day by reviewing deadlines and appointments. Block time for
focused work. Reassess priorities when new requests arrive. Communicate realistic timelines to
requesters.

**Multitasking limits:** Switching between tasks reduces efficiency. Batch similar activities
(e.g., all phone calls, all filing). For complex tasks, work in uninterrupted blocks. Know when
to pause and ask for help.
"""  # noqa: E501

