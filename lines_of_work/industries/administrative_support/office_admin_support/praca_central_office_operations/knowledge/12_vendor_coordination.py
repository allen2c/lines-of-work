title = "Vendor and Supplier Coordination"

content = """
Office administration involves coordinating with vendors for supplies, maintenance, and services.
Effective vendor coordination ensures timely delivery, quality, and cost control.

**Supplier management:** Maintain contact lists for office supplies, catering, cleaning, and
equipment. Track delivery schedules and reorder points. Verify invoices against purchase orders
before processing payment.

**Communication:** Confirm orders in writing (email or PO). Specify quantities, delivery address,
and requested dates. Follow up on late deliveries and document any issues for future reference.

**Problem resolution:** For damaged or incorrect shipments, document the issue with photos and
reference numbers. Contact the vendor promptly. Escalate recurring problems to management.
"""  # noqa: E501

