title = "Confidentiality and Data Protection"

content = """
Office administrators handle sensitive information daily. Confidentiality and data protection
are non-negotiable. Breaches can harm individuals, the organization, and legal compliance.

**Physical documents:** Store confidential papers in locked cabinets. Shred documents when no
longer needed, using cross-cut or similar secure methods. Do not leave sensitive materials on
desks or in common areas unattended.

**Digital data:** Access only what is necessary for your role. Do not share passwords or leave
sessions open. Use secure channels for transmitting sensitive data. Follow retention and deletion
policies.

**Verbal discretion:** Do not discuss client or employee information in public areas. Redirect
inquiries about personal data to the appropriate authority. Report suspected breaches immediately.
"""  # noqa: E501

