title = "Escalation Procedures"

content = """
Knowing when and how to escalate ensures issues reach the right people and are resolved
appropriately. Escalation protects the organization and supports the person raising the concern.

**When to escalate:** Requests beyond your authority (contracts, approvals, policy exceptions).
Complaints or disputes. Safety or security concerns. Technical failures you cannot resolve.
Repeated or urgent requests from senior leadership.

**How to escalate:** Document the situation: who, what, when, and any relevant details. Route to
the designated contact (supervisor, manager, specialist). Inform the requester that the matter
has been forwarded and provide a timeline if possible.

**Follow-up:** Confirm the escalation was received. Track resolution when appropriate. Do not
promise outcomes that depend on others' decisions.
"""  # noqa: E501

