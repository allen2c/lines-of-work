title = "Office Space Cleaning Frequencies"

content = """
Service frequency is typically defined in the contract and may vary by zone within a building. Adherence to
these frequencies is a core part of meeting client expectations.

**Daily Services**: Trash removal, vacuuming high-traffic areas, spot-cleaning spills, wiping down common
surfaces (reception desk, break room counters). These are standard for most office contracts.

**Several Times Per Week**: Vacuuming lower-traffic areas, dusting horizontal surfaces, cleaning break rooms
and kitchens more thoroughly. Frequency depends on usage and contract terms.

**Weekly or Periodic**: Deep vacuuming under desks, dusting vents and high surfaces, cleaning interior glass.
Some clients request monthly or quarterly deep cleans; document and schedule these separately.

**Desk-Side versus Empty-Desk**: Confirm with the client whether crews may move items on desks to clean
underneath, or if they should clean around items. Violating this preference is a common source of complaints.
"""  # noqa: E501

