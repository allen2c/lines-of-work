content = """
Quick, correct response to spills and hazards minimizes damage and protects people. Crews must know
procedures for common scenarios.

**Chemical Spills**: Evacuate the area if fumes are present. Contain the spill with absorbent materials;
wear appropriate PPE. Ventilate the area. Dispose of contaminated materials per SDS and local regulations.
Report to the client and operations.

**Bodily Fluid Spills**: Treat as potentially infectious. Wear gloves and other PPE. Use EPA-registered
disinfectants effective against bloodborne pathogens. Dispose of materials in labeled, sealed bags. Report
per incident procedures.

**Unknown Substances**: Do not handle unknown substances. Isolate the area, warn others, and contact
the client and operations. In case of suspicious or hazardous materials, call emergency services if needed.
"""  # noqa: E501

