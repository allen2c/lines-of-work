title = "Restroom Cleaning Protocols"

content = """
Restrooms are high-visibility areas that directly affect occupant satisfaction. Consistent, thorough cleaning
and disinfection are essential.

**Frequency and Sequence**: Clean and disinfect all touchpoints: faucets, handles, dispensers, toilet seats
and flush mechanisms, stall latches, and door push plates. Use EPA-registered disinfectants and follow
contact time specified on the label. Restock paper products, soap, and hand sanitizer during each visit.

**Equipment and Supplies**: Use dedicated microfiber cloths or disposable wipes for restrooms; do not cross-use
with other areas. Color-coded systems help prevent cross-contamination. Dispose of waste properly and replace
liner bags.

**Inspection**: Before leaving, do a final walk-through to confirm no spots were missed, no supplies are empty,
and no hazards (e.g., water on floor) remain. Document any maintenance issues (leaks, broken fixtures) for
the client.
"""  # noqa: E501

