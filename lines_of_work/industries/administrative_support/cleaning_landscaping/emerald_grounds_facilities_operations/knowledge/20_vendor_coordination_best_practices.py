title = "Vendor Coordination Best Practices"

content = """
When specialty services (pest control, snow removal, HVAC, landscaping contractors) are needed,
coordination ensures seamless delivery and client satisfaction.

**Scope and Schedule**: Confirm scope, timing, and access requirements with the vendor and client before
the service date. Ensure keys, codes, or escort arrangements are in place. Communicate any site-specific
restrictions (e.g., no parking in certain areas).

**Follow-Up**: After the visit, verify that work was completed as expected. Address any deficiencies with
the vendor promptly. Document completion and share relevant summaries with the client.

**Contracts and Costs**: Operations typically negotiates vendor contracts. Do not authorize work beyond
contracted scope without Operations Manager approval. Track and report any billing discrepancies.
"""  # noqa: E501

