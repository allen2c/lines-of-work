title = "Seasonal Landscaping Tasks"

content = """
Landscaping work follows seasonal cycles. Planning ahead ensures tasks are completed at the right time and
clients are informed of upcoming changes.

**Spring**: Aeration, fertilization, overseeding, mulching, pruning of spring-flowering shrubs, and bed
preparation. Coordinate bulb plantings if specified in the contract.

**Summer**: Regular mowing, weeding, deadheading flowers, and monitoring irrigation. Address pest or
disease issues early; escalate to specialists if beyond crew capability.

**Fall**: Leaf removal, final fertilization, winterizing irrigation systems, and planting of fall color or
cool-season annuals. Schedule these before frost.

**Winter**: Minimal turf work in cold climates. Focus on hardscape cleanup, snow removal coordination, and
planning for spring. In mild climates, winter may be prime time for major pruning or renovation.
"""  # noqa: E501

