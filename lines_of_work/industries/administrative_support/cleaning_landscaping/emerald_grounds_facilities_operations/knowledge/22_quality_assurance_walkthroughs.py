title = "Quality Assurance Walkthroughs"

content = """
Periodic quality assurance walkthroughs verify that service standards are being met and identify areas
for improvement.

**Frequency**: Conduct walkthroughs at least quarterly per site; more frequently for new accounts or
sites with prior quality issues. Coordinate with the client when they wish to participate.

**What to Inspect**: Use the service quality checklist as a guide. Check a representative sample of areas:
restrooms, common areas, offices, and exterior grounds. Document findings with photos when helpful.
Note both deficiencies and exemplary work.

**Feedback Loop**: Share findings with crew leads and the Operations Manager. Develop corrective action
plans for recurring issues. Recognize and reinforce good performance. Track trends over time to measure
improvement.
"""  # noqa: E501

