title = "Client Communication Standards"

content = """
Clear, professional communication builds trust and resolves issues efficiently. Respond to client inquiries
within the timeframe specified in the contract (often 24 hours for routine matters).

**Channels**: Use the preferred channel for each client (email, phone, portal). Document all substantive
conversations with date, summary, and any follow-up actions. Avoid making commitments outside your authority.

**Service Updates**: Proactively inform clients of schedule changes, crew substitutions, or delays. For
planned disruptions (e.g., floor stripping), coordinate timing with the client in advance.

**Complaint Handling**: Acknowledge complaints promptly, gather facts, and communicate the resolution plan.
If the issue requires investigation, set an expectation for when you will respond with findings. Escalate
repeated or serious complaints to the Operations Manager.
"""  # noqa: E501

