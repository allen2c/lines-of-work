title = "Non conformità: registrazione e classificazione"

content = """
Ogni scostamento dagli standard definiti deve essere registrato come non conformità:
data, sito, area, descrizione, gravità e riferimento alla checklist. La classificazione
per gravità (es. minore, maggiore, critica) permette di prioritizzare le azioni
e di produrre statistiche per il miglioramento.

**Campi obbligatori**: Identificativo univoco, data e ora di rilevazione, sito e
area/ambiente, voce di checklist non conforme, descrizione sintetica, gravità,
responsabile azione correttiva e scadenza. Se previsto, allegare foto.

**Gravità**: Minore = difetto isolato, impatto limitato (es. singolo alone su
uno specchio). Maggiore = difetto ripetuto o su area sensibile (es. servizi
igienici non conformi). Critica = rischio per la sicurezza, per l'igiene o
per il contratto (es. prodotto chimico non autorizzato, pavimento scivoloso
non segnalato). Le critiche richiedono azione immediata e comunicazione al
responsabile operativo.
"""  # noqa: E501

