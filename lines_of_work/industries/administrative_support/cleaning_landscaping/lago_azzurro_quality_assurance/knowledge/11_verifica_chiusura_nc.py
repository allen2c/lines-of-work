"""Verifica dell'avvenuta correzione prima di chiudere una non conformità."""  # noqa: E501

title = "Verifica chiusura non conformità"

content = """
Una non conformità non può essere chiusa senza verifica che la correzione sia
stata effettivamente eseguita e che il risultato sia conforme. La verifica
può essere svolta in occasione di un'ispezione successiva o con un controllo
dedicato (sopralluogo o foto inviate dal responsabile dell'azione).

**Chi verifica**: Di norma il verificatore è l'ispettore qualità che ha
registrato la non conformità o un suo pari; in casi eccezionali può essere
delegato al responsabile operativo, purché documentato.

**Esito**: Se la verifica conferma la conformità, la non conformità viene
chiusa con data ed esito. Se la correzione è insufficiente, la non conformità
resta aperta, si aggiorna la descrizione o si registra una nuova azione
correttiva con nuova scadenza. Non chiudere mai una NC solo in base a
comunicazione verbale senza riscontro oggettivo.
"""  # noqa: E501

