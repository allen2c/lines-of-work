title = "Poda de arbustos y setos"

content = """
- Setos formales: podar 2-3 veces al año (primavera, verano, otoño) manteniendo forma trapezoidal (base más ancha) para que llegue luz. Altura máxima según contrato (normalmente 1.5-2 m).
- Arbustos ornamentales: podar después de floración. Eliminar ramas muertas, chupones y flores marchitas. No podar más del 20% del volumen total.
- Herramientas: tijeras de seto manuales o eléctricas. Para setos largos, usar desbrozadora con cabezal de cuchillas. Afilar cuchillas cada 10 horas de uso.
- Seguridad: usar guantes anti corte, gafas y protectores auditivos si es motorizado. Verificar que no haya cables eléctricos cerca. No podar setos en días de viento fuerte.
"""

