title = "Aireación y escarificación del césped"

content = """
- Aireación: perforar el suelo para mejorar oxigenación, drenaje y absorción de nutrientes. Realizar en primavera u otoño, cuando el césped está en crecimiento activo. Usar aireador de púas (profundidad 5-10 cm, separación 10-15 cm).
- Escarificación: eliminar la capa de fieltro (materia orgánica acumulada) con escarificador de cuchillas. Hacer una pasada en dirección perpendicular. Profundidad 3-5 mm. No escarificar en épocas de sequía o calor extremo.
- Después de airear/escarificar: recoger los tapones de tierra o residuos. Aplicar una capa fina de arena (1-2 mm) para rellenar agujeros. Regar abundantemente.
- Frecuencia: airear 1-2 veces al año (césped muy pisado: 2 veces). Escarificar una vez al año, preferiblemente en otoño.
"""

