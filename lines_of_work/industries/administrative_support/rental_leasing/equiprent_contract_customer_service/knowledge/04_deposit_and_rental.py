title = "押金与租金计算方式"

content = """
- 押金：通常为设备月租金的1-2倍，最低1000元，最高不超过设备价值的30%。长期租赁（>12个月）可协商押金减免。
- 租金计算：按日、周、月三种单位，月租=日租×30×折扣系数（长期租赁折扣0.7-0.9）。示例：叉车日租200元，月租=200×30×0.8=4800元。
- 付款方式：银行转账、微信/支付宝对公账户、支票。首期租金及押金需在设备交付前付清。
- 发票：租金可开具增值税专用发票（13%）或普通发票，需在付款后3个工作日内申请。
- 续租租金：按原合同租金上浮不超过5%，或按当时市场价重新协商。
"""

