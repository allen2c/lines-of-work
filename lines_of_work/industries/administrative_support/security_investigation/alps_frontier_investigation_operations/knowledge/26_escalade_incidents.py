content = """
L'escalade garantit que les incidents dépassant le périmètre ou les compétences du niveau
opérationnel sont traités par les personnes appropriées. Les critères et délais doivent être
clairement définis.

**Niveaux d'escalade**: Définir les seuils (gravité, impact client, implication de tiers) qui
déclenchent une remontée au superviseur, au responsable de compte ou à la direction.
Documenter la chaîne d'escalade et les contacts disponibles 24/7 si nécessaire.

**Délais et communication**: Pour les incidents critiques, l'escalade doit être immédiate.
Informer le client selon les termes du contrat. Tenir un registre des escalades et des
décisions prises.

**Clôture**: Chaque incident escaladé doit faire l'objet d'un retour et, si pertinent, d'une
mise à jour des procédures pour éviter une récurrence.
"""  # noqa: E501

