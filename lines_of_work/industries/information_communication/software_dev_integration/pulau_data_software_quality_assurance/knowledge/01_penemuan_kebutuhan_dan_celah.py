title = "Penemuan Kebutuhan dan Celah"

content = """
Penemuan kebutuhan dan identifikasi celah adalah fondasi strategi pengujian. Tanpa pemahaman
yang jelas tentang apa yang harus diverifikasi, pengujian menjadi reaktif dan tidak sistematis.

**Konteks:** Dalam pasar digital Indonesia, kebutuhan sering berkembang cepat. Klien dari
fintech, e-commerce, atau layanan kesehatan memiliki persyaratan regulasi dan fungsional
yang spesifik. Celah antara kebutuhan yang terdokumentasi dan implementasi aktual adalah
sumber utama cacat produksi.

**Langkah Utama:**
1) Kumpulkan dan validasi kebutuhan dari PRD, user story, dan spesifikasi teknis.
2) Identifikasi celah: kebutuhan yang ambigu, bertentangan, atau tidak terdokumentasi.
3) Prioritaskan celah berdasarkan dampak bisnis dan risiko kegagalan.
4) Dokumentasikan asumsi pengujian untuk kebutuhan yang belum lengkap.
5) Sinkronkan dengan pemangku kepentingan sebelum memulai siklus pengujian.

**Kriteria Penerimaan:** Proses penemuan dianggap selesai ketika kebutuhan kritis terdokumentasi,
celah teridentifikasi dengan owner yang jelas, dan asumsi pengujian disetujui.

**Kegagalan Umum:** Mengabaikan kebutuhan non-fungsional (kinerja, keamanan), mengasumsikan
kebutuhan tanpa konfirmasi, atau tidak melacak celah hingga tertutup.
"""

