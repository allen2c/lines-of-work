content = """
Wyrównanie interesariuszy oraz przejrzystość dokumentu wymagań produktu (PRD) są niezbędne,
aby zespół niezawodności mógł planować testy i kryteria jakości zgodnie z intencją biznesową.
Rozbieżności między PRD a oczekiwaniami prowadzą do niepotrzebnych testów lub pominięcia
krytycznych scenariuszy.

**Kontekst:** PRD powinien zawierać jasne definicje krytycznych ścieżek użytkownika, granic
akceptowalnej degradacji oraz priorytetów w przypadku konfliktów wymagań. Bez tego zespół
niezawodności nie może ustalić, co testować w pierwszej kolejności.

**Kluczowe działania:**
1) Przeprowadź sesje alignment z product ownerem i kluczowymi interesariuszami.
2) Udokumentuj niejasności i założenia w PRD lub w osobnym dokumencie.
3) Zdefiniuj kryteria akceptacji dla każdej funkcji krytycznej dla niezawodności.
4) Ustal proces aktualizacji PRD przy zmianie wymagań.
5) Komunikuj wpływ zmian wymagań na plan testów i harmonogram.
"""

