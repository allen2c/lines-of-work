title = "Proces analizy przyczyny głównej"

content = """
Analiza przyczyny głównej (RCA) to systematyczna metoda ustalania, dlaczego wystąpił
incydent. Różni się od postmortem tym, że może być stosowana także do defektów
i problemów jakościowych, nie tylko awarii produkcyjnych.

**Kontekst:** Skuteczna RCA wymaga dostępu do danych (logi, metryki, zmiany w kodzie),
współpracy zespołów oraz narzędzi (np. 5 Whys, diagram Ishikawy). Powierzchowna
analiza prowadzi do leczenia objawów zamiast przyczyn.

**Kluczowe działania:**
1) Zdefiniuj zakres: co dokładnie analizujemy (incydent, defekt, degradacja).
2) Zbierz dane przed formułowaniem hipotez.
3) Stosuj 5 Whys lub podobną metodę do dotarcia do przyczyny systemowej.
4) Rozróżniaj przyczynę główną od czynników współistniejących.
5) Udokumentuj wnioski i powiąż z action items oraz aktualizacją procesów.
"""

