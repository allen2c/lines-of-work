title = "Triage zgłoszeń testowych w backlogu"

content = """
Triage to proces sortowania i priorytetyzacji zgłoszeń (defekty, zadania testowe,
zgłoszenia z monitoringu) w backlogu. Bez skutecznego triage backlog staje się
nieprzejrzysty, a krytyczne elementy giną w szumie.

**Kontekst:** Triage powinien odbywać się regularnie (np. codziennie lub co sprint),
z udziałem testerów, deweloperów i product ownera. Kryteria obejmują severity,
wpływ na użytkownika, zależności i zasoby.

**Kluczowe działania:**
1) Ustal częstotliwość i uczestników triage.
2) Zdefiniuj kategorie: do naprawy w tym sprincie, do backlogu, do zamknięcia,
   wymaga więcej informacji.
3) Przypisuj właścicieli do zgłoszeń po triage.
4) Śledź metryki: czas od zgłoszenia do triage, czas do naprawy.
5) Przeglądaj zgłoszenia „wymaga więcej informacji” i zamykaj lub eskaluj.
"""

