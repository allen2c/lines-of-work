title = "Uczenie się z postmortemów incydentów"

content = """
Postmortem po incydencie to strukturyzowana analiza przyczyn, przebiegu i wniosków.
Celem jest poprawa procesów i systemów, a nie obwinianie osób. Bez skutecznych
postmortemów organizacje powtarzają te same błędy.

**Kontekst:** Postmortem powinien być blameless, skupiony na faktach i działaniach
naprawczych. Kluczowe pytania: co się stało, dlaczego, co zrobimy, aby to nie
powtórzyło się, oraz jakie action items z tego wynikają.

**Kluczowe działania:**
1) Przeprowadzaj postmortem w ciągu 48–72 h od zamknięcia incydentu.
2) Zbierz dane: logi, metryki, timeline, zeznania uczestników.
3) Zidentyfikuj przyczynę główną i przyczyny współistniejące (np. 5 Whys).
4) Sformułuj action items z właścicielami i terminami.
5) Udostępnij wnioski szerszej organizacji i zaktualizuj dokumentację/knowledge base.
"""

