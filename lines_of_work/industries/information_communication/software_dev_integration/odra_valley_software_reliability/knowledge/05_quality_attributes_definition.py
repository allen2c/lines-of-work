"""Knowledge item: quality attributes definition."""

title = "Definicja atrybutów jakości"

content = """
Atrybuty jakości to mierzalne właściwości systemu, które wpływają na niezawodność i doświadczenie
użytkownika. Należą do nich dostępność, czas odpowiedzi, throughput, spójność danych oraz
odporność na awarie.

**Kontekst:** Każdy atrybut powinien mieć zdefiniowane cele (np. 99,9% dostępności), metryki
pomiaru oraz kryteria akceptacji w testach. Bez jasnych definicji zespół nie może obiektywnie
ocenić, czy system spełnia wymagania.

**Kluczowe działania:**
1) Zdefiniuj atrybuty jakości istotne dla produktu i klientów.
2) Ustal cele liczbowe i progi degradacji.
3) Powiąż atrybuty z SLO/SLA i error budget.
4) Udokumentuj, jak każdy atrybut jest mierzony i testowany.
5) Przeglądaj definicje przy zmianie wymagań biznesowych.
"""

