title = "Bug Report Triage and Reproduction"

content = """
When a customer reports unexpected behavior, support
triages to determine if it is a bug and gather
enough information for Engineering.

**Initial Triage:** Reproduce if possible in a
test environment. Check for known issues or
user error. Gather environment and steps.

**Reproduction Steps:** Clear, repeatable steps
are critical. Include data state, user role,
and any prerequisites. Screenshots or screen
recordings help.

**Severity Assessment:** Impact (single user vs
many, workaround availability) and frequency
inform priority. Document reasoning in the
escalation.
"""  # noqa: E501

