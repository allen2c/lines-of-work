title = "Password Reset and Account Recovery Procedures"

content = """
Password reset requests are among the most frequent support tickets. Follow standardized
procedures to verify identity and reset credentials without compromising security.

**Identity Verification:** Confirm the requester's identity via registered email or
phone. For enterprise accounts, verify through the designated account administrator
or security contact. Never reset based on username alone.

**Reset Methods:** Use the self-service reset link when available. For manual resets,
generate a temporary password with expiry (e.g., 24 hours) and require immediate change.
Document the reset in the ticket and audit logs.

**Lockout Resolution:** If an account is locked due to failed attempts, verify identity
and clear the lockout. Advise the user on lockout policies to prevent recurrence.
"""  # noqa: E501

