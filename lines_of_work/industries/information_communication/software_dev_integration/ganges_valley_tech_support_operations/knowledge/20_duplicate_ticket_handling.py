title = "Duplicate and Cross-Referenced Ticket Handling"

content = """
Customers sometimes open multiple tickets for the same issue, or related
issues span several tickets. Consolidation and cross-referencing keep
work efficient and avoid conflicting responses.

**Detection:** Search by customer ID, email, or issue description before
creating or responding. Link related tickets in the system.

**Consolidation:** When duplicates are found, respond to the most recent
or comprehensive ticket and close others with a reference. Do not leave
customers without a response on any ticket.

**Cross-Reference:** For related but distinct issues, add a link in the
notes. This helps with trend analysis and escalations.
"""  # noqa: E501

