title = "Log Collection and Diagnostic Data Guidelines"

content = """
Logs and diagnostic data help Engineering diagnose issues. Support collects
them when escalation is needed, following privacy and retention policies.

**What to Request:** Application logs, browser console output, network
traces, or error screenshots. Specify the time range and actions
that preceded the issue.

**Privacy:** Ensure no PII (passwords, tokens, personal data) is included.
Redact or mask sensitive fields before submission. Follow data handling
procedures.

**Retention:** Inform customers how long logs are retained and that they
can request deletion. Store in the designated secure location.
"""  # noqa: E501

