title = "Queue Management and Workload Balancing"

content = """
Support queues distribute tickets across
agents. Effective queue management
prevents bottlenecks and SLA breaches.

**Queue Types:** Inbound, escalation,
follow-up, or specialized (e.g., billing).
Agents work from assigned queues based
on skills and capacity.

**Prioritization:** Within queues, sort
by SLA or priority. Urgent tickets
surface first. Avoid cherry-picking
easy tickets at the expense of
at-risk items.

**Overflow:** When queues are overloaded,
supervisors may reassign or add capacity.
Flag sustained overload for management.
"""  # noqa: E501

