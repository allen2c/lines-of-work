title = "api contract design"

content = """
API চুক্তি পরিষেবা-পরিষেবা যোগাযোগের ভিত্তি। ভালভাবে সংজ্ঞায়িত চুক্তি ইন্টিগ্রেশন
ব্যর্থতা হ্রাস করে এবং পরীক্ষার পুনরাবৃত্তি নিশ্চিত করে।

**প্রসঙ্গ:** OpenAPI, gRPC proto বা অন্যান্য চুক্তি স্পেসিফিকেশন ব্যবহার করে API
সীমানা সংজ্ঞায়িত করুন। চুক্তি সংস্করণ এবং backward compatibility নীতি স্থাপন করুন।

**মূল পদ্ধতি:**
1) চুক্তি-প্রথম নকশা অনুসরণ করুন: চুক্তি সংজ্ঞা কোডের আগে
2) সংস্করণ সেমান্টিক্স প্রয়োগ করুন (breaking vs non-breaking পরিবর্তন)
3) চুক্তি পরীক্ষা CI/CD-এ অন্তর্ভুক্ত করুন
4) consumer-driven contract testing বিবেচনা করুন
5) ডকুমেন্টেশন এবং উদাহরণ চুক্তির সাথে সিঙ্ক রাখুন

**গ্রহণযোগ্যতা মানদণ্ড:** সমস্ত API-এর জন্য চুক্তি বিদ্যমান, সংস্করণ নিয়ন্ত্রিত,
এবং contract tests CI-এ পাস করে।
"""

