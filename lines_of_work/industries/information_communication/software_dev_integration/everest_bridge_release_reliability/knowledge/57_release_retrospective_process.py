"""Release retrospective process."""

title = "Release Retrospective Process"

content = """
Retrospectives na elke release (of wekelijkse batch) verbeteren het release proces. Focus op
wat goed ging, wat beter kan, en actiepunten. Geen blame, wel leerpunten.

**Agenda:** Korte tijdslijn van de release. Wat verliep soepel? Waar liep het vast? Welke
risico's werden gemitigeerd? Wat zouden we anders doen?

**Actiepunten:** Concreet, toewijsbaar, met deadline. Bijvoorbeeld: "Automate rollback
verification by next sprint." Track actiepunten tot afronding.

**Delen:** Bevindingen delen met breder team. Patronen over meerdere releases informeren
procesverbetering. Documenteer in release playbook.
"""

