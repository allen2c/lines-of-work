title = "Release Risk Assessment"

content = """
Release risk assessment identifies potential failure modes before deployment. Conduct a structured
review of each release: scope complexity, dependency changes, database migrations, API changes,
and configuration updates. Assign risk levels (low, medium, high, critical) based on impact and
likelihood. High-risk releases require additional testing, phased rollout, or extended monitoring.
Document mitigations and rollback triggers. Reassess when scope changes mid-cycle.
"""

