"""Continuous improvement of the release process."""

title = "Release Continuous Improvement"

content = """
Release processes should improve over time based on data and feedback. Use metrics, retrospectives,
and incident learnings to refine release practices.

**Metrics to Track:** Deployment frequency, lead time, change failure rate, mean time to
recovery. Compare against industry benchmarks (e.g. DORA metrics). Track trends, not just
point-in-time values.

**Retrospectives:** Conduct release retrospectives after significant releases or incidents.
Capture what went well, what did not, and action items. Assign owners and deadlines.

**Process Updates:** Update runbooks, checklists, and automation based on learnings. Communicate
changes to the release community. Pilot improvements in low-risk contexts before broad rollout.

**Feedback Loops:** Gather input from development, operations, and support. Identify pain points
and automation opportunities. Prioritize improvements that reduce toil and risk.
"""

