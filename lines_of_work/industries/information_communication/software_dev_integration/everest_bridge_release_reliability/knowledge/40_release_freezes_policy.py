title = "Release Freeze Policy"

content = """
Release freezes suspend non-critical deployments during sensitive periods. Common triggers:
fiscal year-end, major holidays, critical business events. Define freeze calendar and
exceptions (security patches, critical fixes). Communicate freezes well in advance.
Establish approval process for emergency overrides. Document freeze rationale for
compliance. Resume normal cadence with a clear end date.
"""

