"""Release communication protocol knowledge item."""

title = "Release Communication Protocol"

content = """
Clear communication around releases keeps stakeholders informed and reduces confusion.
Define who is notified, when, and through which channels.

**Pre-release:** Notify relevant teams of the planned release window, scope, and expected
impact. Include support, operations, and customer-facing teams. Provide release notes
or a summary of changes.

**During Release:** Use a dedicated channel or status page for real-time updates. Report
deployment start, progress, and completion. If issues arise, communicate delays and
expected resolution.

**Post-release:** Confirm successful deployment and any follow-up actions. Share metrics
or health status. For significant releases, schedule a brief retrospective or feedback
session.
"""

