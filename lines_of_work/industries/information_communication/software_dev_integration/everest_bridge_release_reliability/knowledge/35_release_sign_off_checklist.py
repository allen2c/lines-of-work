title = "Release Sign-Off Checklist"

content = """
A release sign-off checklist formalizes approval before deployment. Items: all tests passed,
security scan clean, performance baseline met, documentation updated, rollback tested,
stakeholders notified. Require explicit sign-off from designated roles (e.g. tech lead,
release manager). Reject releases with unchecked critical items. Store checklist evidence
with release record for audit.
"""

