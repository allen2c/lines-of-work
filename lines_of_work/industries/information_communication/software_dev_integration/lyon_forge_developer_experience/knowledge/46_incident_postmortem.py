content = """
Un post-mortem est une analyse blameless d'un incident pour en tirer des apprentissages et des
actions correctives. Lyon Forge exige un post-mortem pour tout incident de sévérité élevée.

**Structure:** Chronologie des événements. Cause racine (5 pourquoi si utile). Impact (utilisateurs,
durée, données). Actions correctives avec owners et dates. Actions préventives pour éviter la
récurrence.

**Blameless:** L'objectif est d'améliorer le système, pas de désigner des coupables. Les erreurs
humaines révèlent des failles de processus ou de tooling. Un environnement blameless encourage
la transparence et l'apprentissage.

**Suivi:** Les actions sont tracées dans le système de suivi. Un responsable vérifie leur
complétion. Les post-mortems sont partagés en interne pour diffusion des apprentissages.
"""

