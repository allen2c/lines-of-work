content = """
Le mentorat technique accélère l'onboarding et la montée en compétence. Lyon Forge encourage
chaque développeur senior à accompagner au moins un pair.

**Rôle du mentor:** Répondre aux questions, faire des revues de code constructives, partager les
conventions et le contexte historique. Ne pas faire le travail à la place du mentoré. Poser des
questions pour guider la réflexion.

**Rôle du mentoré:** Préparer les questions, documenter les réponses pour référence future.
Être proactif dans la recherche avant de demander. Accepter les feedbacks comme des opportunités.

**Formalisation:** Des binômes peuvent être assignés lors de l'onboarding. Des sessions régulières
(hebdo ou bi-hebdo) assurent un rythme. Le mentorat est reconnu dans les évaluations de
performance.
"""

