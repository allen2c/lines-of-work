title = "Accessibilité du code"

content = """
Un code accessible est un code facile à comprendre, modifier et étendre par tout membre
de l'équipe. Lyon Forge valorise la lisibilité et la maintenabilité au-delà de la
concision ou de l'ingéniosité.

**Principes:** Nommer clairement les variables, fonctions et modules. Limiter la complexité
cyclomatique. Éviter les effets de bord implicites. Documenter les décisions non évidentes.

**Revue:** Lors des revues de code, l'accessibilité est un critère explicite. Un code
qui fonctionne mais qui est difficile à comprendre reçoit des demandes de clarification
ou de refactoring.

**Outils:** Les linters et analyseurs statiques détectent une partie des problèmes.
L'œil humain reste essentiel pour évaluer la clarté des noms et la cohérence des
abstractions.
"""

