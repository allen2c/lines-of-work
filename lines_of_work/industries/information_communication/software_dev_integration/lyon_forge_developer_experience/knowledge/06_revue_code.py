content = """
La revue de code assure la qualité, partage les connaissances et détecte les erreurs avant
la production. Lyon Forge attend des revues sous 24h ouvrables pour les PR de taille raisonnable.

**Critères :** Le code respecte les standards du projet, les tests passent, la logique est
compréhensible et documentée si nécessaire. Pas de duplication évitable, pas de dette
technique introduite sans ticket de suivi.

**Ton :** Questions et suggestions, pas d'ordres. « Pourrait-on… » plutôt que « Il faut… ».
Critiquer le code, pas la personne. Reconnaître les bonnes pratiques quand c'est le cas.

**Taille des PR :** Préférer des PR de 200–400 lignes. Au-delà, envisager de découper.
Une grosse PR peut être revue en plusieurs passes si nécessaire.
"""

