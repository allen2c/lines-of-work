content = """
L'onboarding des développeurs définit le rythme de productivité et l'engagement à long terme.
Lyon Forge structure l'intégration sur deux semaines avec des objectifs clairs par jour.

**Premier jour :** Accès aux systèmes (SSH, dépôts, outils internes), présentation de l'équipe,
parcours du wiki interne et des runbooks. Un mentor est assigné pour la première semaine.

**Semaine 1 :** Configuration de l'environnement local (voir 02_environnement_local), lecture du
guide d'architecture, premier commit guidé sur une tâche de warm-up. Revue de code obligatoire.

**Semaine 2 :** Tâche autonome avec support à la demande, participation à une démo d'équipe,
initiation aux processus de release et d'escalade. Bilan d'onboarding avec le responsable technique.

**Critères de succès :** Le développeur peut faire un déploiement en staging, ouvrir une PR
valide, et connaître les canaux de communication (Slack, tickets, revues).
"""

