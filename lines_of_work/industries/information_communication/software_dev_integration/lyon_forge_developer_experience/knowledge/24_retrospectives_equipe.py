title = "Rétrospectives d'équipe"

content = """
Les rétrospectives sont des moments dédiés à l'amélioration continue des pratiques et de
l'expérience développeur. Elles ont lieu à la fin de chaque itération ou sprint.

**Format:** Durée 1 heure maximum. Chaque participant partage ce qui a bien fonctionné,
ce qui pourrait être amélioré, et une action concrète pour la prochaine itération.
Un facilitateur anime et note les décisions.

**Actions:** Chaque rétrospective doit produire au moins une action assignée avec un
responsable. Les actions liées à l'expérience développeur (outils, documentation, processus)
sont prioritaires quand elles reviennent régulièrement.

**Suivi:** Les actions sont tracées dans le backlog. Leur statut est revu en début de
rétrospective suivante. Les actions non réalisées sont soit reportées soit abandonnées
avec justification.
"""

