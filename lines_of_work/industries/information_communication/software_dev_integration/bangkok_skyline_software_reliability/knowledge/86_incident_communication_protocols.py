content = """
Протоколы коммуникации при инцидентах обеспечивают своевременную и точную передачу
информации о сбоях заинтересованным сторонам, команде on-call и пострадавшим
пользователям. Без чётких правил коммуникация становится хаотичной, что усиливает
стресс и задерживает восстановление.

**Контекст:** В распределённых API-сервисах с высоким трафиком инцидент затрагивает
несколько команд: разработку, SRE, поддержку, продукт. Каждая сторона нуждается в
информации разного объёма и формата. Протокол должен определять каналы, частоту
обновлений, шаблоны сообщений и эскалацию при затяжных инцидентах.

**Основные процедуры:**
1) Определить роли: Incident Commander (координирует), Communications Lead
   (публикует обновления), Technical Lead (анализирует и исправляет). Роли не
   должны пересекаться во время активного инцидента.
2) Использовать единый канал для статус-обновлений (Slack, PagerDuty, внутренний
   war room). Обновления каждые 15–30 минут при активном инциденте; при стабилизации
   — реже, но с явным указанием следующего обновления.
3) Шаблон обновления: текущий статус (Investigating / Identified / Monitoring /
   Resolved), краткое описание воздействия, выполненные действия, следующие шаги,
   ETA следующего обновления.
4) Для внешних пользователей: статус-страница или уведомления только при подтверждённом
   воздействии. Избегать преждевременных или неточных заявлений.
5) Пост-инцидент: итоговое сообщение с причиной, воздействием, принятыми мерами и
   ссылкой на постмортем. Распространить в те же каналы, что и обновления.

**Критерии приёмки:** Протокол считается внедрённым, если роли назначены, шаблоны
документированы, команда прошла как минимум один drill, и обновления публикуются
в согласованные интервалы во время реальных инцидентов.

**Типичные ошибки:** Слишком частые или неинформативные обновления; обновления только
в технических каналах без информирования бизнеса; задержка с объявлением инцидента.
Смягчение: определить минимальный порог для объявления (например, >5% ошибок),
тренировать Communications Lead, хранить шаблоны в runbook.
"""  # noqa: E501

