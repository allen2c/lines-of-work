"""Knowledge item: Risikotaxonomie-Design."""

title = "Risikotaxonomie-Design"

content = """
Eine strukturierte Risikotaxonomie ermöglicht konsistente Bewertung und Priorisierung von
Risiken über Features und Releases hinweg. Ohne einheitliche Kategorien entstehen
Inkonsistenzen in der Risikokommunikation.

**Kontext:** Risiken werden nach Auswirkung (Impact) und Eintrittswahrscheinlichkeit
(Likelihood) klassifiziert. Zusätzliche Dimensionen wie Erkennbarkeit und Wiederherstellbarkeit
können die Bewertung verfeinern.

**Vorgehen:**
1) Kategorien für technische, betriebliche und datenbezogene Risiken definieren
2) Severity-Matrix (z. B. kritisch, hoch, mittel, niedrig) festlegen
3) Bewertungskriterien dokumentieren und mit Beispielen versehen
4) Risikoregister mit klaren Ownerships und Fälligkeiten pflegen
5) Regelmäßige Überprüfung und Anpassung der Taxonomie durchführen

**Akzeptanzkriterien:** Taxonomie ist dokumentiert, Team nutzt sie konsistent, und
Risiken sind nachvollziehbar priorisiert.
"""

