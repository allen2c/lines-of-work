"""Knowledge item: Stakeholder-Abstimmung und PRD-Klarheit."""

title = "Stakeholder-Abstimmung und PRD-Klarheit"

content = """
Eine klare Product Requirements Document (PRD) und abgestimmte Stakeholder sind Voraussetzung
für effektive Zuverlässigkeitsarbeit. Uneinigkeit über Ziele führt zu falschen Prioritäten
und unzureichender Testtiefe.

**Kontext:** Die PRD definiert, was gebaut wird und welche Qualitätsziele gelten. Ohne
geteilte Definition von „fertig“ und „zuverlässig“ können Tests nicht sinnvoll geplant werden.

**Vorgehen:**
1) PRD auf messbare Qualitätsziele (SLO, Verfügbarkeit, Latenz) prüfen
2) Stakeholder-Workshops zur Priorisierung von Zuverlässigkeitsanforderungen durchführen
3) Akzeptanzkriterien für Release-Readiness gemeinsam festlegen
4) Kommunikationskanäle für Änderungen und Eskalationen definieren
5) Regelmäßige Sync-Termine zur Abstimmung einplanen

**Akzeptanzkriterien:** PRD enthält explizite Zuverlässigkeitsziele, alle relevanten
Stakeholder sind einbezogen, und Änderungen werden nachvollziehbar kommuniziert.
"""

