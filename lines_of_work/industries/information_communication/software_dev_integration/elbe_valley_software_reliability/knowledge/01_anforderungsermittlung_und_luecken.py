"""Knowledge item: Anforderungsermittlung und Lücken."""

title = "Anforderungsermittlung und Lücken"

content = """
Die Anforderungsermittlung bildet die Grundlage für zuverlässige Software. Unklare oder fehlende
Anforderungen führen zu Lücken in der Testabdeckung und erhöhen das Risiko von Produktionsfehlern.

**Kontext:** Dieses Thema betrifft die Identifikation von Anforderungen, die für Stabilität,
Verfügbarkeit und Datenintegrität relevant sind. Dazu gehören funktionale und nicht-funktionale
Anforderungen sowie implizite Erwartungen von Stakeholdern.

**Vorgehen:**
1) Workshops und Interviews mit Product Owner, Entwicklern und Betrieb durchführen
2) PRD und technische Spezifikationen auf Vollständigkeit und Widersprüche prüfen
3) Risikorelevante Anforderungen explizit dokumentieren (z. B. Last, Fehlertoleranz)
4) Lücken zwischen Anforderungen und Testabdeckung identifizieren und priorisieren
5) Rückkopplung an Stakeholder mit konkreten Handlungsempfehlungen

**Akzeptanzkriterien:** Alle kritischen Anforderungen sind dokumentiert, Lücken sind mit
Severity versehen, und es existiert ein Plan zur Schließung priorisierter Lücken.
"""

