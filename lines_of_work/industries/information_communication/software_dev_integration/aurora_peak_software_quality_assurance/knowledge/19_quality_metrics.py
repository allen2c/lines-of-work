title = "Quality Metrics and Reporting"

content = """
We track quality metrics to guide improvements and communicate status.
Metrics include coverage, defect density, escape rate, and test execution
trends. Reports are shared with engineering and product leadership.

**Coverage Metrics:** Requirement coverage (percentage of requirements
with passed tests), code coverage (for unit tests), and scenario
coverage. We aim for high coverage of critical paths; raw percentages
alone can be misleading.

**Defect Metrics:** Defects found per phase (development, QA, UAT,
production), severity distribution, and time to resolution. We analyze
defect trends to identify weak areas and prevent escapes.

**Escape Rate:** Production defects per release, especially those
that could have been caught in testing. Escape analysis informs
process improvements and test design.
"""  # noqa: E501

