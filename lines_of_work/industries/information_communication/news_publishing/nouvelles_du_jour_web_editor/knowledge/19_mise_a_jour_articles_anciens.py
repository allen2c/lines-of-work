title = "Mise à jour d'articles anciens"

content = """
- Les articles de plus de 6 mois peuvent être mis à jour si l'actualité a évolué (ex. ajout d'un nouveau développement).
- Ajouter un encart en haut de l'article : « Cet article a été mis à jour le [date] avec de nouvelles informations. »
- Ne pas modifier la date de publication originale. La date de modification apparaît dans les métadonnées (dateModified).
- Si l'article est devenu obsolète (ex. résultat électoral), le marquer comme « Archivé » et ajouter un lien vers un article plus récent.
- Les mises à jour doivent être validées par le chef de rubrique si elles modifient le sens de l'article.
- Ne pas mettre à jour les articles d'opinion sans l'accord de l'auteur.
"""

