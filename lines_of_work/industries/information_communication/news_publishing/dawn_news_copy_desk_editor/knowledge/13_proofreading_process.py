title = "三校流程细则"

content = """
- 一校（编辑自校）：编辑在完成编审后，对排版后的清样进行第一次校对。重点检查标题、导语、图片说明、报眉、版号、日期。一校后签字，送责任编辑复审。
- 二校（专职校对）：由校对组专职校对员进行，逐字逐句通读全文，核对所有文字、标点、数字、格式。二校后填写《校对差错记录表》，退回编辑修改。编辑修改后需再次送二校确认。
- 三校（终校）：由资深校对员或校对组长进行，重点检查前两校遗漏的差错，并核对版面整体协调性（标题位置、图片与文字对应、广告分隔线等）。三校后签字，送终审主编。
- 每个校次之间需留出至少30分钟修改时间。特急稿件可压缩至15分钟，但需注明“特急”并跟踪修改。
- 三校完成后，所有修改必须体现在最终清样上，不得遗漏。终审主编签字后，任何修改需重新走三校流程。
"""

