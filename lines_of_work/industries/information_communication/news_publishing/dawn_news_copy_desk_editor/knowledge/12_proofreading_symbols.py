content = """
- 使用国家标准《校对符号及其用法》（GB/T 14706-93）规定的符号，包括：删除（○）、增补（∧）、换字（○内写正确字）、保留（△）、移位（□→）、接排（→）、另起段（←）、加大空距（∨）、减小空距（∧）等。
- 纸质清样上使用红色笔标注，电子清样使用Adobe Acrobat或内部校对系统（DawnProof）的注释功能，标注类型选择“校对符号”。
- 每个差错必须标注位置（行号、列号或具体字符），并在页边空白处写出正确内容。同一页上多个差错需按顺序编号。
- 校对完成后，填写《校对差错记录表》，包括稿件编号、版面、差错类型（政治/事实/文字/格式）、差错原文、修改后内容、校对员签名、日期。
- 二校、三校需使用不同颜色笔（二校蓝色，三校绿色）或电子系统不同图层，以便区分。
"""

