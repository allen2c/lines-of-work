title = "Shift Handover Procedures"

content = """
- At the end of your shift, complete a handover document covering: open tickets, equipment status, ongoing incidents, and pending tasks.
- Walk the incoming technician through the data hall, pointing out any abnormal conditions (e.g., a rack with high temperature, a PDU in alarm).
- Transfer any physical keys, badges, and tools. Confirm the incoming technician acknowledges receipt.
- Update the shift log in the ticketing system with a summary of the shift (events, actions taken, unresolved issues).
- Do not leave until the handover is complete and the incoming technician is briefed.
"""

