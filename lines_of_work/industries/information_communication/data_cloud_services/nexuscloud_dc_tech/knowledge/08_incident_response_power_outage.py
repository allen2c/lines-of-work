title = "Incident Response – Power Outage"

content = """
- If lights go out or UPS alarms sound, check the BMS for UPS status and generator transfer.
- Remain calm – UPS batteries will hold for 5–10 minutes. Do not open any electrical panels.
- If generator fails to start, call facilities immediately. If generator runs, monitor fuel level and runtime.
- After power returns, verify all PDU and rack PDUs are back online. Check for any equipment that did not restart.
- Log the incident with start/end time, cause (if known), and any affected customer equipment.
"""

