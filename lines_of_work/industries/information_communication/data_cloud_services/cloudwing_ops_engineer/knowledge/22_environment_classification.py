title = "环境分类与命名规范"

content = """\
- 生产环境（prod）：用于正式业务，所有资源标签`env=prod`。禁止直接修改，必须通过CI/CD。数据库实例规格≥8C32G，集群节点数≥3。
- 预发布环境（staging）：用于集成测试和灰度验证，标签`env=staging`。配置与生产一致（除数据量），使用脱敏数据。允许手动操作，但需记录。
- 开发环境（dev）：用于开发自测，标签`env=dev`。资源规格可缩小，无SLA要求。允许开发人员通过Rancher部署，但需遵守资源配额（每个项目≤4C8G）。
- 命名规范：主机名格式`<服务名>-<环境>-<序号>`，如`api-prod-01`。Kubernetes命名空间格式`<项目>-<环境>`，如`payment-prod`。DNS记录格式`<服务>.<环境>.cloudwing.cn`。
"""

