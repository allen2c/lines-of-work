title = "常见故障Runbook（四）：磁盘空间满"

content = """\
- 症状：磁盘使用率>95%，应用无法写入日志或数据。影响：服务不可用。
- 排查步骤：1）`df -h`查看各分区使用率。2）`du -sh /* | sort -rh`定位大目录。3）`lsof | grep deleted`查找已删除但未释放的文件（如日志文件被进程占用）。4）检查是否有core dump文件或临时文件。
- 缓解措施：若为日志文件，执行`truncate -s 0 <文件>`或配置日志轮转（logrotate）。若为已删除但未释放，重启对应进程。若为临时文件，清理`/tmp`。紧急情况下，扩容云盘（在线扩容需云厂商支持）。
- 预防：设置磁盘告警阈值（85% Warning，92% Critical）；配置logrotate按大小或时间轮转；定期清理过期备份。
"""

