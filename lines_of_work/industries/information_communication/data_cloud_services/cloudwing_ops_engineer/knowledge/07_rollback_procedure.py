content = """\
- 回滚触发条件：部署后监控指标异常（错误率上升>5%或延迟增加>20%）、冒烟测试失败、或人工发现明显问题。回滚无需等待审批，立即执行。
- 回滚步骤：1）登录GitLab，找到当前部署的commit，执行`git revert`或回退至上一稳定版本。2）重新运行CI/CD pipeline，使用回滚分支。3）验证回滚后监控指标恢复正常。4）在Jira变更工单中记录回滚原因、时间、影响。
- 若回滚失败（如数据库迁移不可逆），立即执行备用方案：切换至灾备环境（DR）或联系SRE Lead。回滚后需在24小时内提交事后分析，优化部署流程。
- 数据库回滚需特别谨慎：DDL变更必须使用`pt-online-schema-change`或gh-ost，并保留回滚SQL。数据回滚需从备份恢复，耗时较长，需提前评估。
"""

