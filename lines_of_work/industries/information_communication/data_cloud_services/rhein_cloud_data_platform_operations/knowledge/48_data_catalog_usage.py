title = "Nutzung des Data Catalogs"

content = """
Ein Data Catalog zentralisiert Metadaten: Tabellen, Spalten, Herkunft,
Datenqualität, Nutzung. Tools wie Apache Atlas, AWS Glue Data Catalog oder
Databricks Unity Catalog ermöglichen Discovery, Governance und Zugriffskontrolle.
Rhein Cloud pflegt den Catalog als Single Source of Truth für Datenbestände.
Neue Assets müssen registriert werden. Spaltenbeschreibungen und
Klassifizierung (PII, vertraulich) sind verpflichtend. Der Catalog wird für
Lineage, Impact-Analysen und Compliance-Anfragen genutzt.
"""

