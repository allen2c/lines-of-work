title = "Spaltenebene-Sicherheit (Column-Level Security)"

content = """
Column-Level Security (CLS) beschränkt den Zugriff auf bestimmte Spalten
basierend auf Benutzer oder Rolle. Sensible Spalten (z.B. Gehalt, Gesundheitsdaten)
werden nur autorisierten Nutzern angezeigt. Snowflake, Databricks und andere
Plattformen unterstützen CLS über Masking Policies oder dynamische
Berechtigungen. Die Implementierung erfordert eine klare Klassifizierung
der Spalten und ein Berechtigungsmodell. Rhein Cloud setzt CLS für
DSGVO-relevante und vertrauliche Daten ein.
"""

