content = """
**Pendeteksian**
Penggunaan tanpa lisensi dapat ditemukan melalui monitoring tayang, laporan dari komunitas,
atau audit internal. Tim pemantauan dapat melacak program iklan dan siaran untuk trek katalog.

**Respons Awal**
Jika penggunaan tanpa lisensi terdeteksi, jangan langsung konfrontatif. Beberapa produksi
mungkin tidak menyadari kewajiban lisensi. Hubungi secara profesional, informasikan
kewajiban, dan tawarkan penyelesaian (lisensi retroaktif dengan tarif standar atau
penyesuaian).

**Eskalasi**
Jika klien menolak atau mengabaikan, eskalasi ke tim legal. Tuntutan hukum adalah opsi
terakhir. Dokumentasikan semua komunikasi.
"""

