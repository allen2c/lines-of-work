title = "Lisensi Blanket vs. Lisensi Individual"

content = """
**Lisensi Individual**
Satu lisensi per trek, per proyek. Standar bagi kebanyakan produksi. Jelas, terdokumentasi,
dan mudah dilacak.

**Lisensi Blanket**
Satu perjanjian mencakup penggunaan beberapa trek atau seluruh katalog dalam parameter tertentu
(misalnya "semua trek untuk iklan dalam negeri, 1 tahun"). Lebih cocok untuk broadcaster besar
atau produser dengan volume tinggi.

**Praktik Nusantara**
Kita terutama menawarkan lisensi individual. Blanket dapat dipertimbangkan untuk klien strategis
setelah diskusi dengan manajemen. Blanket memerlukan perhitungan tarif yang lebih kompleks dan
monitoring penggunaan yang ketat.
"""

