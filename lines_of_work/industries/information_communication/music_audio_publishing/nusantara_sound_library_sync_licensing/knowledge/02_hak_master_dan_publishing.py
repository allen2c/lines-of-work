content = """
**Hak Master (Master Use Right)**
Hak master adalah hak untuk menggunakan rekaman audio tertentu (fixed performance). Pemegang hak master
biasanya label rekaman atau pemilik master. Lisensi master diperlukan untuk setiap sinkronisasi rekaman.

**Hak Penerbitan (Publishing / Sync Right)**
Hak penerbitan melekat pada komposisi musik (lagu sebagai karya). Pemegang hak adalah penerbit atau
komposer. Lisensi sinkronisasi penerbitan diperlukan terlepas dari siapa yang merekam lagu.

**Lisensi Ganda**
Untuk sinkronisasi, produksi umumnya membutuhkan keduanya: lisensi master dari pemilik rekaman dan
lisensi sinkronisasi dari pemilik hak penerbitan. Perpustakaan musik sering menggabungkan keduanya
dalam satu penawaran jika katalognya memiliki hak penuh.
"""

