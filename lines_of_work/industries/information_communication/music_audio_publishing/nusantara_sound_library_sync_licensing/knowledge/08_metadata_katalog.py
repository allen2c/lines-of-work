title = "Standar Metadata Katalog untuk Identifikasi Trek"

content = """
Metadata katalog memungkinkan identifikasi trek yang akurat dan memfasilitasi pelaporan royalti.
Setiap trek dalam Nusantara Sound Library harus memiliki metadata lengkap.

**Bidang Wajib**
- Judul trek (title)
- Nama komposer/arranger
- Penerbit dan kode penerbit
- Durasi
- ID internal (track ID)
- Genre/mood untuk pencarian

**Bidang Tambahan**
- ISRC (jika tersedia)
- Informasi hak (wilayah, batasan)
- Kata kunci untuk pencarian produksi

**Konsistensi**
Metadata harus konsisten di seluruh sistem. Kesalahan metadata dapat menyebabkan royalti salah
alokasi dan sengketa hak.
"""

