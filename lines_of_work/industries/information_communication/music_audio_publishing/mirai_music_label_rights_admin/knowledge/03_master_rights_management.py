title = "原盤権（マスター権）の管理"

content = """
- 原盤権はレーベルが保有する場合とアーティストが保有する場合がある。契約書で明確に定義。
- レーベル保有の場合、ライセンス収入（他社へのサブライセンス、CM使用等）の70～90%をレーベルが取得。残りをアーティストに分配。
- 原盤権の譲渡・買戻しオプション：契約期間終了後、アーティストが一定額で買い戻せる条項が一般的（例：5年経過後、500万円）。
- 原盤権の管理台帳：楽曲ごとに原盤権者、権利割合、契約期間、ライセンス先を記録。更新はリリース時および契約変更時。
- 海外での原盤権管理は現地サブパブリッシャーに委託。契約書に準拠法（日本法または現地法）を明記。
"""

