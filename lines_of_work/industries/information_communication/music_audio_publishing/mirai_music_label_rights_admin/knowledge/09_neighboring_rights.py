title = "隣接権（実演家・レコード製作者の権利）"

content = """
- 日本では実演家（歌手・演奏家）とレコード製作者（レーベル）に隣接権が認められる。
- レコード製作者の権利：複製権、送信可能化権、譲渡権。放送・有線放送には報酬請求権（JASRACではなく日本レコード協会RIAJが管理）。
- 実演家の権利：同様の権利に加え、放送用レコードの二次使用料（RIAJ経由で分配）。
- 隣接権の管理：レーベルはRIAJに会員登録し、使用料の分配を受ける。分配率は演奏時間・放送回数に基づく。
- 海外の隣接権：各国の制度が異なるため、現地の権利管理団体（例：米国SoundExchange）と契約が必要。
"""

