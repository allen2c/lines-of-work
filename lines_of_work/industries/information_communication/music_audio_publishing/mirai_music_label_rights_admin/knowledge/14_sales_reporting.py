title = "売上レポートの照合"

content = """
- 配信会社から届く売上レポート（CSVまたはExcel）を、自社の売上予測・過去データと照合。
- 照合項目：売上数量、売上金額、返品・キャンセル、通貨、期間。異常値（前月比±50%超、平均単価の大幅変動）をチェック。
- 不一致が見つかった場合：配信会社のポータルで詳細データを確認。それでも解決しない場合はサポート窓口に問い合わせ。
- レポートの受領期限：配信会社により異なるが、多くは月次で翌月15日まで。遅延が続く場合は契約条件を確認。
- 照合後、印税計算システムにデータを取り込み。手動修正は最小限にし、修正履歴を記録。
"""

