content = """
- 使用驻波比测试仪（如Site Master S331L）进行测试。测试前需校准仪器：连接开路器、短路器、负载进行校准，确保精度。
- 测试时断开天线与馈线的连接，将测试仪连接到馈线末端。设置测试频率范围（如800-2700MHz，根据基站制式）。读取驻波比（VSWR）值，要求小于1.5。若大于1.5，需排查馈线接头、跳线、天线本身。
- 对于多频段天线，需分别测试每个频段。记录每个频段的驻波比峰值。若发现驻波比异常，需使用时域反射（TDR）功能定位故障点距离。
- 测试完成后，恢复天线连接，并确保接头拧紧（扭矩30-40N·m），涂抹防水胶。
"""

