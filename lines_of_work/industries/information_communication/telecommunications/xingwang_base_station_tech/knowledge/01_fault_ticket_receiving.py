content = """
- 网管中心通过派单系统（如U2000或iManager）下发故障工单，技术员手机端APP（星网运维助手）会收到推送通知。工单包含：基站名称、地址、告警类型、优先级、派发时间。
- 优先级定义：紧急（整站退服、大面积用户投诉）需15分钟内响应；高（单小区退服、重要告警）30分钟；中（次要告警）1小时；低（巡检类）2小时。响应指确认收到并出发前往现场。
- 到达现场后，首先在APP上点击“到达”并拍照签到。然后根据告警类型选择对应的故障排查流程。处理完成后，在APP上填写处理结果、更换备件型号、测试数据，并上传至少3张现场照片（故障点、修复后、环境全景）。最后点击“完成”并等待网管中心确认关闭工单。
"""

