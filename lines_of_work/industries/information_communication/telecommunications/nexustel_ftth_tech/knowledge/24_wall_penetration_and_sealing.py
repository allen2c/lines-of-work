title = "Wall Penetration and Sealing"

content = """
- Drill hole from outside to inside at slight upward angle (5–10°) to prevent water entry. Use a 3/4” masonry bit for brick or concrete; 1/2” for wood siding.
- Insert a weatherproof entry grommet or plastic bushing to protect cable from sharp edges. Pull cable through, leaving 6–12 inches slack inside.
- Seal exterior hole with silicone caulk or expanding foam (for larger gaps). Ensure seal is watertight; smooth with finger.
- Inside, install a wall plate or cable clip to secure cable. Do not leave exposed fiber; use a surface-mount box if needed.
- If drilling through fire-rated wall (e.g., garage to house), use firestop putty or intumescent sealant to maintain fire rating.
- For multiple cables, use a single larger hole with a multi-cable grommet. Never drill through electrical conduit or water pipes.
"""  # noqa: E501

