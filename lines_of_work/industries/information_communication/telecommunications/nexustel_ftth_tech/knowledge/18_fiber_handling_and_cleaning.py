title = "Fiber Handling and Cleaning"

content = """
- Always handle bare fiber with clean, dry hands or gloves. Avoid touching the cleaved end; oil from skin degrades splice quality.
- Clean fiber before cleaving: use lint-free wipe soaked in 99% isopropyl alcohol, wipe from coated section to bare fiber in one direction. Do not reuse wipes.
- Clean connector endfaces with dry lint-free wipe first; if still dirty, use alcohol and dry immediately. Never use compressed air (can deposit oil).
- Inspect endface after cleaning; if contamination persists, use a connector cleaning pen (e.g., Cletop) with wet-dry method.
- Store unused connectors in sealed bags; avoid dust. Replace dust caps immediately after disconnecting.
- Do not bend fiber sharply; minimum bend radius for cable is 10x diameter, for bare fiber is 30 mm. Use bend limiters at entry points.
"""  # noqa: E501

