title = "Connector Termination"

content = """
- Use pre-polished SC/APC connectors (field-installable) for drop cable termination at customer premises. Follow manufacturer instructions for stripping and crimping.
- Strip cable jacket 2 inches, remove buffer tube, clean bare fiber with alcohol wipe. Insert fiber into connector ferrule until it stops; crimp the connector body.
- Cleave excess fiber flush with ferrule end using special cleaver; polish with 0.3 µm lapping film if required (pre-polished connectors may not need polish).
- Inspect endface with 400x microscope: no scratches, pits, cracks, or contamination. Acceptable if all zones (core, cladding, contact) are clean.
- If endface is dirty, clean with dry lint-free wipe; if still dirty, use alcohol and dry. Never reuse a contaminated connector; replace if damaged.
"""  # noqa: E501

