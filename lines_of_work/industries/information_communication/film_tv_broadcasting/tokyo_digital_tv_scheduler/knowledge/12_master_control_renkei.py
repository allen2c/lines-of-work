title = "マスターコントロールとの連携"

content = """
- マスターコントロール（MC）は送出の最終実行部門。スケジューラーはMCと常時連絡を取り、スケジュールの投入・変更・トラブル対応を行う。
- 連絡手段：専用インカム（ヘッドセット）とチャットシステム（Slack相当）。緊急時は電話も使用。
- スケジュール投入後、MCは自動チェック（尺・CM順序・フォーマット）を実行。エラーがあればスケジューラーに通知。スケジューラーは修正後、再投入。
- 放送中、MCはスケジューラーの指示に従い、手動操作を実行。スケジューラーはMCに「次のCM枠開始」「番組終了」「ニュース割り込み」などのタイミングを指示。
- トラブル発生時、MCは即座にスケジューラーに報告。スケジューラーは状況を判断し、編成部・技術部にエスカレーションするか決定。
"""

