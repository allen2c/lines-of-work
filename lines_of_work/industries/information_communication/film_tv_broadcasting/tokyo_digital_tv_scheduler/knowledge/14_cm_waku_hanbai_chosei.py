title = "CM枠の販売と調整"

content = """
- CM枠の販売は営業部が担当。スケジューラーは販売情報（枠ID、放送日時、秒数、クライアント名、素材ID）を営業部から受領し、タイムテーブルに反映。
- 販売枠の種類：
  - タイムCM：番組提供枠。番組内に固定配置。放送順は提供クライアントの指定に従う。
  - スポットCM：番組間枠。放送順は営業部の指定順序を基本とするが、同一枠内での順序変更はスケジューラーの裁量。
- 販売枠の超過・不足が発生した場合の調整：
  - 超過：営業部に連絡し、優先順位の低いCMを翌日以降に振替。振替先は同時間帯の枠を優先。
  - 不足：営業部に空き枠情報を提供。追加販売が可能か確認。空き枠がない場合は、SBを短縮して枠を捻出（ただしSBは最低5秒必要）。
- 販売枠のキャンセルは放送前日18:00までに営業部から通知。当日キャンセルは原則不可だが、緊急時は編成部の承認が必要。
"""

