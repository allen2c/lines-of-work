title = "CMの順序と制約"

content = """
- CMの放送順序は以下の制約に従う。
  - 同一業種（例：自動車、飲料、化粧品）のCMは連続して放送しない。最低1本の異業種CMを挟む。
  - 競合他社のCMは同一枠内で隣接しない。営業部が指定する場合を除く。
  - 公共広告（ACジャパンなど）は枠の最後に配置するのが慣例。
  - 15秒CMと30秒CMの混在は可能だが、枠の総秒数に収まるよう配置。
- 順序変更が必要な場合（素材未着など）、スケジューラーは営業部に連絡し、承認を得る。緊急時は事後報告でも可。
- CM枠内でCM本数が奇数になる場合、最後にフィラー（局内CM）を挿入して偶数に調整。フィラーは15秒または30秒。
"""

