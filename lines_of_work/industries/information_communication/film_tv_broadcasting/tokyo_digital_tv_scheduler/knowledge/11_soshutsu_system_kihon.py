title = "送出システムの基本操作"

content = """
- 送出システムは「Master Control System（MCS）」を使用。MCSは番組・CMの再生、切り替え、時刻同期を自動制御する。
- スケジューラーはMCSにタイムテーブルを投入。システムは自動で再生リストを生成し、指定時刻に素材を再生する。
- 手動操作が必要なケース：
  - 緊急ニュース挿入時：MCSの「ニュース割り込み」ボタンで自動切り替え。その後、手動で通常スケジュールに復帰。
  - CM差し替え：MCSの「CMスキップ」機能で特定CMを飛ばし、代替CMを手動で再生。
  - 番組尺調整：MCSの「タイムシフト」機能で番組の再生速度を微調整（±0.5%以内）。ただし、音声ピッチ補正が必要。
- システム障害時は、バックアップの「手動送出パネル」を使用。パネル操作はマスターコントロール担当者が行い、スケジューラーは指示を出す。
"""

