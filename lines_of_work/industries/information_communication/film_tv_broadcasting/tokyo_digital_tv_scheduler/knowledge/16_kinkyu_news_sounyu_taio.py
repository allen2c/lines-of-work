content = """
- 緊急ニュース（地震・津波・重大事件）の挿入は編成部の指示で行う。スケジューラーは以下の手順を実行。
  1. 編成部から「ニュース割り込み」の指示を受ける。割り込みの尺（5分、10分、15分など）を確認。
  2. 現在放送中の番組を中断。中断位置を記録（後日再放送のため）。
  3. CM枠を一時停止。中断されたCMは振替対象として記録。
  4. ニュース素材（生放送または録画）をMCに指示して送出開始。
  5. ニュース終了後、通常スケジュールに復帰。復帰タイミングは次の番組開始時刻に合わせる。
  6. 復帰後、中断された番組の残り尺を調整。残り尺が短い場合は、CM枠を削減して放送。長い場合は、次回放送に持ち越す。
- 緊急ニュース挿入に伴うCM枠の振替は、放送終了後に営業部と調整。振替先は原則として翌週の同時間帯。
"""

