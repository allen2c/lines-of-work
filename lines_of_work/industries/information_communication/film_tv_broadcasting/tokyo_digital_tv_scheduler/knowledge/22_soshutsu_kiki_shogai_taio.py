title = "送出機器の障害対応"

content = """
- 送出機器（再生サーバー、スイッチャー、エンコーダー）の障害が発生した場合、以下の手順で対応。
  1. MCが障害を検知し、スケジューラーに報告。障害の種類（再生停止、映像乱れ、音声途切れ）を確認。
  2. スケジューラーは即座に編成部と技術部に連絡。技術部が復旧作業を開始。
  3. 復旧までの間、手動送出パネルで代替番組（フィラー）を送出。フィラーは事前に用意された5分間のループ映像。
  4. 復旧後、スケジュールを再開。遅れた時間はCM枠を削減して調整。削減できない場合は、番組を短縮して放送。
- 障害が30分以上続く場合、放送を中断し、編成部の指示で代替番組（映画など）に切り替える。CM枠はすべて振替対象。
"""

