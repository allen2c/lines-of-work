title = "番組尺変更時の調整"

content = """
- 番組尺の変更は、制作部からの依頼または編成部の指示で発生。変更理由は「番組内容の充実」「CM枠の増減」「放送時間枠の変更」など。
- 尺変更の手順：
  1. 変更内容（新尺、変更理由、希望放送日）を編成部から受領。
  2. 影響範囲を分析：前後の番組、CM枠、SBの調整が必要か。
  3. 調整案を作成：CM枠の増減、SBの長さ変更、前後番組の尺調整。調整案は編成部と営業部に提示。
  4. 承認後、タイムテーブルを修正し、MCに再投入。変更が放送当日の場合は、放送開始30分前までに完了。
- 尺変更に伴うCM枠の増減は、営業部の承認が必要。増加する場合は販売可能な枠があるか確認。減少する場合は、削除されるCMの振替先を営業部と調整。
"""

