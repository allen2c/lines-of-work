title = "番組尺の調整方法"

content = """
- 番組尺は編成表で指定された「正味尺」と「総尺」の2種類。正味尺は番組本編の長さ、総尺はCM枠・SBを含めた全体の長さ。
- 実際の番組素材の尺が正味尺と異なる場合、以下の手順で調整する。
  - 差が±3秒以内：CM枠内のCM本数は変えず、SBの長さを微調整（最小0.5秒単位）。
  - 差が±3秒超～±15秒：CM枠内のCMを1本追加または削除（15秒単位）。ただし、CM枠の総秒数が営業部の許容範囲内であることを確認。
  - 差が±15秒超：編成部に尺変更を申請。番組のカットまたは延長を制作部に依頼する。
- 調整後も総尺は編成表の放送時間枠（例：19:00～19:54）に収まらなければならない。収まらない場合は、前後の番組の尺を調整するか、編成部に時間枠の変更を求める。
"""

