title = "定時番組と特番の調整"

content = """
- 定時番組（毎週同じ時間に放送）は編成表で固定。特番（特別番組）が入る場合、定時番組の放送を休止または時間移動する。
- 特番の調整手順：
  1. 編成部から特番の放送日時・尺・内容の通知を受ける。
  2. 影響を受ける定時番組の休止・移動を決定。移動先の時間枠を編成部と協議。
  3. 特番のCM枠を営業部と調整。特番用のCM枠が別途販売される場合、通常のCM枠と重複しないよう注意。
  4. タイムテーブルを修正。特番の前後でSBの長さを調整し、全体の放送時間枠に収める。
- 特番が生放送の場合、尺の変動が大きいため、余裕を持ったCM枠設定（通常より5秒長め）を行う。放送中に尺が超過した場合、CM枠を削減して対応。
- 特番のリハーサルは通常より長め（1時間）に設定。生放送の場合は、放送開始30分前までに全リハーサルを完了。
"""

