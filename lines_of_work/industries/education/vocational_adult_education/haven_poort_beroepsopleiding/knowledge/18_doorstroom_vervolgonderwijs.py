title = "Doorstroom naar vervolgonderwijs"

content = """
Na een MBO-opleiding kunnen studenten doorstromen naar een hoger MBO-niveau, het
HBO of een andere vervolgopleiding. Haven Poort informeert over toelatingsvoorwaarden
en kan doorverwijzen naar decaan of loopbaanadviseur.

**MBO-niveaus**
Met een diploma niveau 2 kan worden doorgegaan naar niveau 3, met niveau 3 naar
niveau 4. Soms zijn aanvullende eisen van toepassing (bijv. voor bepaalde
keuzedelen of vakken).

**Doorstroom naar HBO**
Met een MBO-niveau-4-diploma is toelating tot het HBO in veel gevallen mogelijk.
Sommige HBO-opleidingen stellen aanvullende eisen (bijv. specifieke vakken of
een toelatingsonderzoek). De decaan kan algemene informatie geven; voor
opleidingsspecifieke vragen wordt verwezen naar de HBO-instelling.

**Volwassenen**
Volwassenen die via EVC of een 21+-toets toelating willen tot HBO of WO, kunnen
bij de betreffende instelling informeren. Haven Poort kan uitleg geven over
algemene routes maar niet over toelating bij andere instellingen.
"""

