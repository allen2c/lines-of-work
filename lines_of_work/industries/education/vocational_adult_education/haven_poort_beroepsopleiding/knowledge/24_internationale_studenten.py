title = "Internationale studenten"

content = """
Studenten van buiten Nederland die een opleiding of cursus bij Haven Poort willen
volgen, moeten voldoen aan toelatings- en eventueel verblijfsvoorwaarden. Voor
hen kunnen extra eisen gelden voor taal en vooropleiding.

**Toelating**
Voor Nederlandstalige opleidingen is meestal een bepaald niveau Nederlands (NT2)
vereist. Diploma’s van buitenlandse vooropleidingen worden beoordeeld op
gelijkwaardigheid; dit kan via IDW (international credential evaluation) of door
de instelling. De adviseur legt uit welke documenten nodig zijn en verwijst
door naar de verantwoordelijke voor diplomawaardering.

**Verblijf en visum**
Vragen over verblijfsvergunning of studievoorwaarden voor niet-EER-studenten
vallen onder de Immigratie- en Naturalisatiedienst (IND). Haven Poort kan een
inschrijvingsbevestiging verstrekken voor een visumaanvraag wanneer de student
daadwerkelijk is ingeschreven.

**Begeleiding**
Internationale studenten kunnen dezelfde studieloopbaanbegeleiding en
voorzieningen gebruiken als andere studenten; bij taalspecifieke problemen kan
extra ondersteuning worden ingezet.
"""

