title = "Studiefinanciering voor volwassenen"

content = """
Volwassenen die een MBO-opleiding of een andere erkende opleiding volgen, kunnen
onder voorwaarden in aanmerking komen voor studiefinanciering of een lening via
DUO. De voorwaarden wijzigen regelmatig; actuele informatie is leidend.

**Soorten**
Afhankelijk van leeftijd en opleiding: prestatiebeurs, lening, of alleen
een reisproduct. Voor deeltijd of cursussen gelden vaak andere regels dan voor
voltijd MBO. De adviseur kan in grote lijnen uitleggen wat er bestaat en verwijst
voor bedragen en aanvraag naar DUO.

**Aanvraag**
Aanvragen lopen via DUO (duo.nl). De student heeft een DigiD nodig. Haven Poort
bevestigt inschrijving en studieduur aan DUO wanneer daartoe wordt verzocht; de
student blijft zelf verantwoordelijk voor het aanvragen en doorgeven van wijzigingen.

**Subsidies naast DUO**
Naast studiefinanciering kunnen regelingen van gemeente, UWV of werkgever van
toepassing zijn. De adviseur noemt deze mogelijkheden en verwijst door naar de
relevante instantie.
"""

