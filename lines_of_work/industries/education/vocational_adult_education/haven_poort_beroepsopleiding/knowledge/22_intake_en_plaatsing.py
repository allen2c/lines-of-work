title = "Intake en plaatsing"

content = """
De intake is het eerste gesprek na aanmelding. Doel is vast te stellen of de
opleiding of het traject past bij de kandidaat en of hij of zij voldoet aan de
toelatingsvoorwaarden. Soms volgt een plaatsingstoets.

**Verloop intake**
De adviseur of intaker bespreekt vooropleiding, werkervaring, beschikbaarheid en
doelen. Op basis daarvan wordt een advies gegeven over een passend traject. Bij
twijfel over toelating kan een toets of EVC-traject worden voorgesteld.

**Plaatsingstoets**
Sommige opleidingen vereisen een toets voor taal, rekenen of vakinhoud. De toets
bepaalt of de kandidaat direct kan starten of eerst een voorbereidend traject
moet volgen. Data en locatie worden bij de aanmelding of na intake meegedeeld.

**Plaatsing in een groep**
Na definitieve inschrijving wordt de student in een groep of cohort geplaatst.
Wensen voor dag, locatie of type (BOL/BBL) worden zoveel mogelijk meegenomen;
beschikbaarheid kan beperkt zijn.
"""

