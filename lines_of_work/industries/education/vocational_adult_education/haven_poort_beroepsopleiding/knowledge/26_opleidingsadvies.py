content = """
Opleidingsadvies helpt kandidaten een passend traject te kiezen op basis van
doelen, vooropleiding, ervaring en omstandigheden. De adviseur voert dit gesprek
tijdens of na de intake.

**Inhoud gesprek**
Doelen (bijv. ander werk, startkwalificatie, bijscholing), vooropleiding en
diploma’s, werkervaring en EVC-mogelijkheden, beschikbare tijd (voltijd/deeltijd,
avond), en eventuele belemmeringen (taal, financiering, zorg). Op basis daarvan
wordt een advies gegeven: een concrete opleiding, cursus of een voorbereidend
traject.

**Grenzen**
De adviseur geeft geen garanties over baankansen of salaris. Bij complexe
loopbaanvragen kan worden doorverwezen naar een loopbaancoach of het UWV. Bij
juridische of financiële vragen wordt verwezen naar de desbetreffende afdeling
of instantie.

**Vervolg**
Na advies kan de kandidaat zich inschrijven of eerst nadenken. Bij inschrijving
volgt de formele toelatings- en plaatsingsprocedure.
"""

