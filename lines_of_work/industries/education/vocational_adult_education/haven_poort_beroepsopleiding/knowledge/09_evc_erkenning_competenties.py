title = "Erkenning van eerder verworven competenties (EVC)"

content = """
EVC staat voor het erkennen van kennis en vaardigheden die iemand buiten het
formeel onderwijs heeft opgedaan. Door EVC kan een traject worden verkort of kan
iemand worden toegelaten zonder de standaard vooropleiding.

**Procedures**
De kandidaat dient een portfolio of bewijzen in; een assessor beoordeelt of dit
voldoet aan de eisen van het kwalificatiedossier of onderdelen daarvan. Bij
positieve beoordeling kunnen vrijstellingen worden verleend.

**Rol van de adviseur**
De adviseur legt uit wat EVC inhoudt, welke onderdelen in aanmerking kunnen komen
en hoe de procedure verloopt. De daadwerkelijke beoordeling en vrijstelling worden
gedaan door de examencommissie of een erkend EVC-instelling; de adviseur verwijst
door naar de juiste contactpersoon.

**Documentatie**
Bewijzen kunnen zijn: werkgeversverklaringen, certificaten, producten, of een
assessment. Wat nodig is, hangt af van het onderdeel waarvoor EVC wordt aangevraagd.
"""

