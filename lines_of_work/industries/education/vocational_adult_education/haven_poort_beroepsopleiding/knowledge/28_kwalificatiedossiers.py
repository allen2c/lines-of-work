content = """
Een kwalificatiedossier beschrijft wat een beginnend beroepsbeoefenaar moet
kennen en kunnen voor een bepaalde kwalificatie. De dossiers worden door SBB
(Samenwerkingsorganisatie Beroepsonderwijs Bedrijfsleven) vastgesteld en door
de minister vastgelegd. Opleidingen sluiten hierop aan.

**Opbouw**
Een dossier bevat kerntaken, werkprocessen en bijbehorende kennis en vaardigheden,
en keuzedelen. Keuzedelen laten ruimte voor verbreding of verdieping. Examens
en BPV zijn op deze onderdelen gebaseerd.

**Actualisatie**
Kwalificatiedossiers worden periodiek herzien. Opleidingen passen hun programma
hierop aan. Studenten vallen onder het dossier dat gold bij hun inschrijving,
tenzij anders is overeengekomen.

**Voor de adviseur**
De adviseur kan uitleggen dat opleidingen zijn gebaseerd op landelijke dossiers
en dat daardoor diploma’s landelijk erkend zijn. Voor inhoudelijke details van
een dossier wordt verwezen naar de opleidingscoördinator of naar de SBB-website.
"""

