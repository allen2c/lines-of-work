title = "Processus d'inscription standard"

content = """
- L'apprenant potentiel contacte le centre (téléphone, formulaire web, email). Le coordinateur recueille ses besoins, vérifie les prérequis et oriente vers la formation adaptée.
- Si la formation est éligible CPF, le coordinateur aide l'apprenant à créer ou utiliser son compte CPF (montant disponible, plafond). Il génère un devis personnalisé.
- L'inscription est confirmée après réception du devis signé et du paiement (ou accord de financement OPCO). Le coordinateur envoie la convention de formation et le programme détaillé.
- Délai : l'inscription doit être finalisée au plus tard 5 jours ouvrés avant le début de la session. Passé ce délai, une demande de dérogation est nécessaire.
"""

