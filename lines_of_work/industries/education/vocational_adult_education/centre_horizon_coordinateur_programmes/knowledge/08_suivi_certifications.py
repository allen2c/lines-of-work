content = """
- Chaque formation certifiante est associée à un certificateur externe (ex. : TOSA, TOEIC, CCI, Pearson Vue). Le coordinateur connaît les modalités d'inscription et les dates limites.
- Pour chaque session, le coordinateur inscrit les apprenants auprès du certificateur au moins 15 jours avant l'examen. Il fournit la liste nominative et les pièces justificatives (photo d'identité, pièce d'identité).
- Les résultats sont reçus sous 2 à 4 semaines. Le coordinateur les télécharge, les vérifie et les enregistre dans le système de gestion des apprenants.
- En cas d'échec, le coordinateur informe l'apprenant et lui propose une session de rattrapage (gratuite dans la limite d'une fois, sinon payante). Il organise le nouveau passage selon les règles du certificateur.
"""

