title = "Coordination des formateurs"

content = """
- Les formateurs sont des prestataires externes ou des salariés du centre. Le coordinateur tient à jour un fichier des formateurs : coordonnées, diplômes, expérience, habilitations, domaines d'intervention.
- Avant chaque session, le coordinateur envoie au formateur le kit pédagogique (support de cours, grille d'évaluation, feuille de présence) et le planning détaillé.
- Le formateur doit remettre la feuille de présence signée par les apprenants à la fin de chaque journée. Le coordinateur vérifie et archive.
- Une évaluation du formateur est réalisée par les apprenants en fin de formation. Le coordinateur compile les résultats et les transmet au responsable pédagogique pour le suivi qualité.
"""

