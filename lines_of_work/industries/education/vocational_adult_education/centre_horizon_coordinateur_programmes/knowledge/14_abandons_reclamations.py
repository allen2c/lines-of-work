title = "Gestion des abandons et réclamations"

content = """
- Un abandon est déclaré lorsque l'apprenant cesse de suivre la formation sans motif valable. Le coordinateur contacte l'apprenant pour comprendre la raison et tenter de le réintégrer.
- Si l'abandon est confirmé, le coordinateur met à jour le dossier, annule l'inscription à l'examen (si possible) et informe le financeur (remboursement partiel selon les règles).
- Les réclamations écrites (email, courrier) sont enregistrées dans un registre dédié. Le coordinateur répond sous 10 jours ouvrés avec une proposition de solution.
- Pour les réclamations non résolues, le coordinateur transmet au responsable qualité qui peut saisir le médiateur de la formation professionnelle.
"""

