title = "**學員突發事件處理流程**"

content = """
- **事件分類**：
  - **健康突發**：學員在訓練期間突發疾病或意外（如暈倒、受傷）。
  - **情緒危機**：學員出現嚴重情緒問題（如自殘傾向、暴力行為）。
  - **法律糾紛**：學員涉及勞資爭議或刑事案件（如詐騙、盜竊）。
  - **家庭變故**：學員遭遇重大家庭變故（如親人過世、家暴）。
- **處理流程**：
  1. **立即應變**：
     - 健康突發：立即通知救護車（撥打 119），並通知家屬。
     - 情緒危機：隔離學員，並通知心理諮詢師或社工。
     - 法律糾紛：保持中立，不提供法律意見，立即通知中心主任。
  2. **事件紀錄**：
     - 填寫「突發事件報告表」，包含：
       - 事件時間、地點、經過。
       - 處理措施及結果。
       - 後續追蹤計畫（如「需持續心理輔導」）。
     - 紀錄需於 **24 小時內** 提交中心主任審核。
  3. **後續追蹤**：
     - 健康突發：確認學員康復後方可繼續訓練，必要時提供補課安排。
     - 情緒危機：安排心理諮詢，並定期追蹤情況（每週 1 次）。
     - 法律糾紛：協助學員尋求法律援助，並暫停訓練直至事件解決。
- **預防措施**：
  - **健康管理**：定期提醒學員注意身體狀況，並提供急救箱。
  - **情緒支持**：每月舉辦「壓力管理」講座，並提供心理諮詢熱線。
  - **法律宣導**：邀請律師進行法律知識講座，提升學員法律意識。
- **升級機制**：
  - 若事件嚴重（如學員自殺未遂），立即通報勞動局及社會局。
  - 若事件涉及刑事責任（如學員打傷他人），立即通報警方並配合調查。
"""  # noqa: E501

