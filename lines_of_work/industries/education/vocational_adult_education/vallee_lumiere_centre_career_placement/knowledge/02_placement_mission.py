content = """
Le service placement professionnel relie les diplômés et apprenants en fin de parcours
aux employeurs et aux opportunités de stage. Il ne garantit pas l'embauche mais facilite
l'accès au marché du travail.

**Objectifs**
- Diffuser les offres reçues des partenaires employeurs
- Coordonner les conventions de stage et le suivi en entreprise
- Conseiller les candidats sur les outils de recherche d'emploi
- Organiser et animer les événements de recrutement (forums, job dating)
- Collecter les retours pour améliorer les services

**Ce que le service ne fait pas**
- Négocier les salaires ou conditions avec les employeurs
- Garantir un poste ou un stage à quiconque
- Remplacer le travail de candidature du demandeur
- Gérer les conflits juridiques entre stagiaire et entreprise (orientation vers
  les services compétents)
"""

