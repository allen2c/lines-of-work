title = "Alternance et apprentissage"

content = """
L'alternance (contrat d'apprentissage ou de professionnalisation) combine formation
en centre et en entreprise. C'est un levier important d'insertion pour les jeunes
et les adultes en reconversion.

**Contrat d'apprentissage**
Principalement pour les 16–29 ans (ou plus dans certains cas). Formation en CFA
ou organisme agréé. Rémunération en pourcentage du Smic selon âge et niveau.
L'employeur bénéficie d'exonérations sous conditions.

**Contrat de professionnalisation**
Plus souple sur l'âge. Formation en organisme agréé ou en interne. Rémunération
selon âge et niveau de qualification.

**Rôle du service placement**
Diffuser les offres en alternance des partenaires, orienter les candidats vers
les bons interlocuteurs (entreprises, CFA, OPCO). Le centre peut lui-même proposer
des formations en alternance ; dans ce cas, le service coordination avec les
entreprises est clé.
"""

