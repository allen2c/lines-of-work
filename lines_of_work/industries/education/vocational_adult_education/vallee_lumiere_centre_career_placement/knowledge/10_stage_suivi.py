title = "Suivi des stages en entreprise"

content = """
Le centre assure un suivi pédagogique des stages via le tuteur école et le service
placement. L'objectif est de vérifier le bon déroulement et la conformité à la
convention.

**Visites et points intermédiaires**
Selon la durée du stage, des visites ou points téléphoniques peuvent être prévus.
Le service placement peut coordonner ces échanges avec le tuteur entreprise.

**Évaluations**
Les grilles d'évaluation (mi-parcours, fin de stage) sont transmises au tuteur
entreprise. Le stagiaire et le centre reçoivent une copie. Les difficultés
signalées sont traitées avec discrétion et diligence.

**Problèmes en stage**
En cas de conflit, de non-respect de la convention ou de situation à risque,
orienter immédiatement vers le tuteur pédagogique, la direction ou le service
juridique. Ne pas minimiser les signalements des stagiaires.
"""

