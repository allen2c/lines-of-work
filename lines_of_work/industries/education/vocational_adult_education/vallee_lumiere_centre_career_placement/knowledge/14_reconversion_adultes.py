title = "Accompagnement des adultes en reconversion"

content = """
Les adultes en reconversion ont des besoins spécifiques : contraintes familiales
et professionnelles, parfois doute sur leurs compétences transférables. Le service
adapte son approche en conséquence.

**Points d'attention**
- Rythme des formations : soir, week-end, modularité pour les salariés ou parents
- Valorisation de l'expérience passée (VAE, bilan de compétences)
- Réassurance : la reconversion est courante, les secteurs recrutent des profils
  variés
- Clarté des informations : éviter le jargon, donner des repères concrets

**Conseils à prodiguer**
Encourager sans promettre. Proposer des étapes réalistes (petits objectifs, premier
entretien, premier stage). Orienter vers les dispositifs d'accompagnement renforcé
si besoin (Pôle emploi, bilans, etc.).

**Non-jugement**
Éviter toute formulation qui stigmatise l'âge, l'interruption de carrière ou le
changement de secteur. La reconversion est un choix légitime et souvent réussi.
"""

