title = "Dispositifs d'aide à l'emploi"

content = """
Plusieurs dispositifs publics et partenaires peuvent accompagner les chercheurs d'emploi.
Le service placement oriente vers les acteurs compétents selon les situations.

**Pôle emploi**
Inscription obligatoire pour les demandeurs d'emploi. Accompagnement, offres, aides
à la mobilité ou à la création d'entreprise. Le centre peut travailler en partenariat
pour des actions ciblées.

**OPCO (Opérateurs de compétences)**
Financement de la formation professionnelle, conseil aux entreprises et aux
demandeurs. Utile pour comprendre les dispositifs de prise en charge (CPF, etc.).

**Missions locales (jeunes)**
Accompagnement des 16–25 ans dans l'insertion professionnelle et sociale.

**Cap Emploi**
Accompagnement spécifique des travailleurs handicapés.

Le service placement ne gère pas directement ces dispositifs ; il oriente et
coordonne avec eux lorsque des actions communes sont prévues.
"""

