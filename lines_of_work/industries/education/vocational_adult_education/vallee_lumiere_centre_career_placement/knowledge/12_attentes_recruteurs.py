content = """
Comprendre ce que recherchent les employeurs aide à mieux préparer les candidatures
et les entretiens. Ces attentes varient selon les secteurs et les postes.

**Compétences techniques**
Correspondance entre la formation suivie et les compétences requises. Mettre en avant
les réalisations concrètes (projets, stages) plutôt que la simple liste de diplômes.

**Comportement et soft skills**
Ponctualité, rigueur, capacité à travailler en équipe, adaptabilité. Les recruteurs
évaluent ces traits lors de l'entretien et des références.

**Motivation**
Compréhension du poste et de l'entreprise, clarté du projet professionnel. Les
candidats qui montrent de l'intérêt et posent des questions pertinentes se démarquent.

**Présentation**
Tenue adaptée au secteur, CV clair et sans fautes, respect des délais de candidature.
Un manque de soin donne une mauvaise image même si le profil technique est bon.
"""

