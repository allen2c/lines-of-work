content = """
La convention de stage encadre légalement le séjour d'un stagiaire en entreprise.
Elle est obligatoire et signée par le centre, l'entreprise et le stagiaire.

**Contenu type**
- Identité des trois parties
- Objectifs pédagogiques et missions confiées
- Durée, horaires, lieu
- Encadrement (tuteur école, tuteur entreprise)
- Gratification si applicable (obligatoire au-delà de 2 mois)
- Assurances et responsabilités

**Rôle du service placement**
Vérifier la conformité du document, faciliter la signature, collecter les exemplaires.
En cas de désaccord sur les clauses, orienter vers le service juridique ou la direction.
Ne pas modifier unilatéralement les termes convenus avec l'employeur.

**Durées maximales**
Variable selon le niveau de formation ; se référer à la réglementation en vigueur
(typiquement 6 mois par année d'enseignement, plafonds selon le cycle).
"""

