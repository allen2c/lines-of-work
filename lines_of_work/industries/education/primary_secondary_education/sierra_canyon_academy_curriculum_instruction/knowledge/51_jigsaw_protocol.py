title = "Jigsaw Protocol"

content = """
Jigsaw is a cooperative structure where each student learns one part of a topic and
teaches it to peers. It builds accountability and efficient coverage of content.

**Process:** Divide content into segments. Assign each student to an expert group
that studies one segment. Reconfigure into mixed groups where each expert teaches
their segment. Assess understanding of the whole.

**Design:** Ensure segments are meaningful and of similar complexity. Provide
materials and clear tasks. Build in processing time.

**Use cases:** Reading multiple texts, analyzing different cases, covering
multiple standards. Good for content that can be subdivided.
"""  # noqa: E501

