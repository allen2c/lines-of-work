title = "Essential Questions"

content = """
Essential questions are open-ended, thought-provoking inquiries that frame units and
drive inquiry. They have no single correct answer and invite exploration.

**Characteristics:** Open, provocative, require higher-order thinking, recur across
units, and connect to students' lives. Examples: "What makes a just society?" or "How
do scientists know what they know?"

**Use:** Pose at the start of a unit. Return throughout. Use to frame assessments and
discussions. Allow students to revise answers as they learn.

**Design:** Avoid yes/no or single-fact questions. Link to standards and enduring
understandings.
"""  # noqa: E501

