title = "Rubric Design for Assessments"

content = """
Rubrics communicate expectations and provide consistent criteria for grading. Sierra
Canyon uses analytic rubrics (dimensions with levels) for performance tasks and projects.

**Structure:** Define dimensions (e.g., content, organization, language). For each
dimension, describe performance at each level (e.g., 4–1 or meets/approaching/below).

**Clarity:** Use specific, observable descriptors. Avoid vague terms. Align descriptors
to standards and learning objectives.

**Use with students:** Share rubrics before the task. Model self-assessment. Use
rubrics for feedback during drafts, not only final grading.
"""  # noqa: E501

