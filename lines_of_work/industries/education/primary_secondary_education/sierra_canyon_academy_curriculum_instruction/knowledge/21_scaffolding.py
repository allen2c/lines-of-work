title = "Instructional Scaffolding"

content = """
Scaffolding provides temporary support so students can complete tasks they cannot yet
do independently. Remove scaffolds as learners gain competence.

**Types:** Verbal (hints, think-alouds), procedural (graphic organizers, checklists),
and material (sentence frames, word banks, adapted text). Match scaffold to need.

**Vygotsky’s ZPD:** Work within the zone of proximal development. Provide enough support
to succeed without doing the work for students.

**Fading:** Plan for gradual removal. Monitor readiness and reduce support over time.
Avoid over-scaffolding, which can hinder independence.
"""  # noqa: E501

