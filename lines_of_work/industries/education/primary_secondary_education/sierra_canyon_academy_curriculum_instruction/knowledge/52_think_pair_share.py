title = "Think-Pair-Share"

content = """
Think-pair-share (TPS) gives students time to think individually, discuss with a
partner, and then share with the class. It increases participation and depth of
response.

**Process:** Pose a question. Give think time (30 seconds to a few minutes).
Pairs discuss. Select pairs to share with the class. Can use random or voluntary
sharing.

**Benefits:** Reduces anxiety, allows rehearsal, surfaces diverse ideas. Useful
as a quick check or to launch deeper discussion.

**Variations:** Think-write-pair-share (add writing); pair-square (expand to
fours); snowball (escalating group size).
"""  # noqa: E501

