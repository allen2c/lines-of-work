title = "Differentiated Instruction Principles"

content = """
Differentiation addresses varying readiness levels, interests, and learning profiles.
Sierra Canyon expects curriculum and instruction to accommodate diverse learners within
general education settings.

**Content:** Vary the complexity, depth, or format of what students engage with.
Provide tiered assignments and flexible grouping.

**Process:** Offer multiple pathways to learn the same content (e.g., hands-on, visual,
text-based, collaborative).

**Product:** Allow students to demonstrate learning in different ways (written, oral,
multimedia, performance). All options must align to the same standards.
"""  # noqa: E501

