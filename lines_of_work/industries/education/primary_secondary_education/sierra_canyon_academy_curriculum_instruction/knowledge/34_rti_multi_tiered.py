title = "Multi-Tiered Systems of Support (MTSS)"

content = """
MTSS provides a framework for supporting all students through tiered instruction and
intervention. Curriculum design contributes to Tier 1 (core instruction) quality.

**Tier 1:** High-quality, differentiated core instruction for all. Strong Tier 1
reduces need for intervention. Curriculum team ensures alignment and quality.

**Tier 2:** Supplemental support for students who need more. May include small-group
instruction, additional practice, or targeted strategies. Align interventions to core.

**Tier 3:** Intensive support for students with significant gaps. Often
individualized. Coordinate with special education when appropriate.
"""  # noqa: E501

