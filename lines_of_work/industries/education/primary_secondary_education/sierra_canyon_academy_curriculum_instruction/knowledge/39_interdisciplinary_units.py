content = """
Interdisciplinary units connect two or more subjects around a central theme or
problem. Sierra Canyon uses these to deepen understanding and foster connections.

**Planning:** Choose a compelling theme or driving question. Identify standards
from each subject. Design tasks that require integration. Ensure each discipline
contributes meaningfully.

**Balance:** Do not sacrifice depth for breadth. Each subject should have clear
learning goals. Avoid superficial connections.

**Examples:** Sustainability (science, math, HSS), storytelling (ELA, HSS, arts),
data and society (math, HSS, ELA). Align with school calendar and team capacity.
"""  # noqa: E501

