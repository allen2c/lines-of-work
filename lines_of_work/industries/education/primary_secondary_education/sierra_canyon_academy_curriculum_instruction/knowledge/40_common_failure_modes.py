title = "Common Curriculum Failure Modes"

content = """
Curriculum design can fall short in predictable ways. Sierra Canyon anticipates
and mitigates these.

**Misalignment:** Objectives, instruction, and assessment do not match. Regularly
audit for alignment. Use backwards design.

**Overload:** Too much content, too fast. Prioritize. Build in time for mastery and
review. Say no to nice-to-haves.

**Under-scaffolding:** Tasks are too difficult for many students. Differentiate.
Use gradual release. Provide supports.

**Disconnection:** Content feels irrelevant. Build in relevance, choice, and
authentic applications. Connect to student experience.
"""  # noqa: E501

