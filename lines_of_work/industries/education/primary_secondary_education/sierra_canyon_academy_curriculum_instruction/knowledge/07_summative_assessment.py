title = "Summative Assessment Design"

content = """
Summative assessments evaluate learning at the end of a unit, course, or term. They
should align with learning objectives and standards.

**Types:** Unit tests, essays, projects, presentations, portfolios, and standardized
assessments. Choose formats that match what students were taught and the skills being
assessed.

**Alignment:** Ensure items or tasks map to objectives and cover the full range of
content and cognitive demand. Avoid over-testing narrow skills.

**Fairness:** Use clear rubrics, provide accommodations per IEP/504 plans, and avoid
construct-irrelevant variance (e.g., overly complex wording when assessing content).
"""  # noqa: E501

