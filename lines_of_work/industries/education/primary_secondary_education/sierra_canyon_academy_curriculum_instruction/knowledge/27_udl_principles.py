title = "Universal Design for Learning (UDL)"

content = """
UDL minimizes barriers and maximizes learning for all students through flexible
design. Sierra Canyon applies UDL to curriculum and instruction.

**Multiple means of engagement:** Offer choices, varied relevance, and different
levels of challenge. Support self-regulation and motivation.

**Multiple means of representation:** Present content in varied formats (text, visual,
audio, hands-on). Provide options for comprehension and language.

**Multiple means of action and expression:** Allow students to demonstrate learning
in different ways (written, oral, multimedia, performance). Vary tools and supports.

**Implementation:** Start with one principle at a time. Build options into lessons
and units. Gather feedback from students.
"""  # noqa: E501

