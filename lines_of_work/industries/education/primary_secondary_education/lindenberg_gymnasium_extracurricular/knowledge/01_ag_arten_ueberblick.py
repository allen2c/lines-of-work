content = """
Arbeitsgemeinschaften (AGs) am Lindenberg Gymnasium gliedern sich in feste Kategorien, die die
Planung und Ressourcenverteilung strukturieren. Eine klare Einordnung erleichtert die
Kommunikation mit Eltern, Schülern und Betreuern.

**Sport-AGs:** Fußball, Handball, Tischtennis, Leichtathletik, Schwimmen, Volleyball. Benötigen
Halle oder Außenanlagen, oft Betreuung durch externe Trainer oder Sportlehrkräfte. Hoher
Versicherungs- und Aufsichtsbedarf.

**Musik-AGs:** Chor, Orchester, Band, Bläsergruppe. Benötigen Probenräume, Instrumentenlager und
gelegentlich Aula für Auftritte. Koordination mit Musiklehrkräften und eventuell externen
Musikschulen.

**MINT-AGs:** Robotik, Jugend forscht, Naturwissenschaften, Programmierung. Nutzen Fachräume,
Werkstätten oder Computerräume. Material- und Gerätebedarf variiert stark.

**Sprach- und Kultur-AGs:** Theater, Debattierclub, Fremdsprachen, Schülerzeitung. Oft
kreativitätsorientiert, flexibler Raumbedarf.

**Allgemein:** Jede AG hat eine feste Kategorie für Raumzuweisung, Budgetzuordnung und
Compliance-Anforderungen. Kategorien werden zu Jahresbeginn mit der Schulleitung abgestimmt.
"""  # noqa: E501

