content = """
Once a family has decided that Sunny Meadow Preschool is the right fit, the next
step is the formal application. This process is designed to be straightforward
yet comprehensive, ensuring we have all the information needed to consider each
child.

**Online Application Portal:** All applications must be submitted through our
user-friendly online portal. This ensures that all information is captured
accurately and that parents can track the status of their application.

**Required Documentation:** To complete an application, parents must provide:
1. **Application Form:** A detailed form including basic biographical information
   about the child and their family.
2. **Immunization Records:** A current immunization record as required by state
   licensing regulations.
3. **Birth Certificate:** A copy of the child's birth certificate to verify age
   eligibility.
4. **Previous School Records (if applicable):** Records from any previous
   preschool or early childhood programs.
5. **Application Fee:** A non-refundable fee must be paid at the time of
   submission to cover administrative costs.

**Timeline:** Applications for the upcoming academic year typically open in
January. While we accept applications year-round, we encourage early submission,
as our programs often fill quickly.
"""

