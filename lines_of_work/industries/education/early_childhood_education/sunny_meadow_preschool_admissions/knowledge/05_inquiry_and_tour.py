content = """
The journey to joining Sunny Meadow Preschool begins with the initial inquiry. This
stage is crucial for establishing a connection with prospective families and
providing them with the information they need to decide if our school is the
right fit for their child.

**Inquiry Channels:** Parents typically reach out via our website's inquiry form,
email, or telephone. Every inquiry should be acknowledged within 24 business
hours with a personalized response and our digital parent information packet.

**The School Tour:** A tour is the best way for parents to experience our vibrant
community firsthand. Tours are scheduled during school hours so parents can see
the classrooms in action, observe the interactions between teachers and
children, and get a feel for the school's atmosphere.

**Key Tour Highlights:**
1. **The Classrooms:** Showcase our age-appropriate materials, student work
   displays, and the layout of the learning centers.
2. **Outdoor Play Space:** Emphasize the importance of physical activity and
   nature exploration in our curriculum.
3. **Staff Interactions:** Highlight the warmth and professionalism of our teaching
   staff.
4. **Safety Features:** Point out our secure entry systems, clean facilities, and
   adherence to safety protocols.

**Tour Preparation:** Before the tour, the admissions agent should review any
information provided in the inquiry form (e.g., child's age, specific interests)
to tailor the conversation and address any pre-identified concerns.
"""

