title = "Enrollment and Onboarding: The New Student Transition"

content = """
The enrollment process begins once an offer of admission has been extended and
accepted. Our goal is to make the transition into Sunny Meadow Preschool as
seamless and positive as possible for both the child and their family.

**Enrollment Packet:** Upon acceptance, families receive a comprehensive
enrollment packet. This packet includes:
1. **Enrollment Agreement:** A contract outlining the terms of enrollment,
   including tuition, fees, and school policies.
2. **Emergency Contact Form:** A form detailing emergency contacts and
   authorized pick-up persons.
3. **Medical Authorization Form:** Information about the child's pediatrician,
   known allergies, and authorization for emergency medical treatment.
4. **Student Profile:** A questionnaire about the child's interests, habits, and
   any specific needs or preferences.

**Onboarding Events:** We host several events to help new families integrate into
our community:
- **New Parent Orientation:** A virtual or in-person session covering school
  operations, communication channels, and what to expect during the first week.
- **Classroom Visit:** An opportunity for the child to meet their teachers and
  explore their new classroom before the first day of school.
- **Welcome Coffee:** A casual gathering for new parents to meet each other and
  established members of the school community.
"""

