title = "Program Overview: The Pre-K Program"

content = """
Our Pre-Kindergarten (Pre-K) Program is tailored for children aged 4 to 5 years
who are in their final year before entering elementary school. This program aims
to solidify the skills needed for a smooth transition to Kindergarten while
maintaining our commitment to play-based learning.

**Key Readiness Areas:**
1. **Advanced Literacy:** We focus on letter-sound correspondence, sight word
   recognition, and early writing skills. Children are encouraged to create their
   own stories and 'publish' their own books.
2. **Mathematical Proficiency:** Our Pre-K students explore addition and subtraction
   concepts through games, work with more complex patterns, and begin to understand
   the concept of time and money.
3. **Scientific Inquiry:** We encourage children to ask 'why' and 'how'. Simple
   experiments, gardening, and observing life cycles (like butterflies) help
   develop critical thinking and observation skills.
4. **Executive Function:** We help children develop the ability to plan, focus
   attention, and remember instructions through multi-step projects and
   increasingly complex games.

**Kindergarten Readiness:** Our teachers work closely with families to prepare
children for the expectations of Kindergarten. This includes developing
independence in self-care, the ability to follow a schedule, and the confidence
to navigate new social environments.
"""

