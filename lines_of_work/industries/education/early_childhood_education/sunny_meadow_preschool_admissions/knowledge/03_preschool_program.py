title = "Program Overview: The Preschool Program"

content = """
The Preschool Program is designed for children aged 3 to 4 years. At this stage,
children are becoming more social and their cognitive abilities are expanding
rapidly. Our curriculum shifts to include more complex project-based learning
and collaborative activities.

**Curriculum Highlights:**
1. **Thematic Units:** We explore various themes throughout the year, such as 'Our
   Community', 'The Natural World', and 'Space Exploration'. These themes integrate
   science, social studies, and the arts into a cohesive learning experience.
2. **Emerging Literacy:** We foster a print-rich environment where children are
   introduced to letter recognition, phonological awareness, and the joy of
   storytelling through daily read-alouds and journaling.
3. **Mathematical Thinking:** Concept of numbers, patterns, sorting, and measuring
   are introduced through hands-on activities using manipulatives like counters,
   scales, and measuring tapes.
4. **Collaborative Play:** Children are encouraged to work together on projects,
   negotiate roles in dramatic play, and solve social conflicts with teacher
   guidance.

**Social-Emotional Growth:** We use the 'Positive Discipline' approach to help
children develop self-control and problem-solving skills. We emphasize identifying
feelings and using words to resolve differences, fostering a respectful and
kind classroom community.
"""

