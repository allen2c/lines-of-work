title = "Waitlist Management: Policies and Communication"

content = """
Due to high demand, some of our programs may reach capacity before the start of the
academic year. In these cases, we maintain a waitlist for interested families. Our
waitlist management is guided by principles of fairness and transparency.

**Joining the Waitlist:** Families are automatically placed on the waitlist for
their desired program if a space is not currently available at the time of
application. There is no additional fee to join the waitlist beyond the initial
application fee.

**Priority Criteria:** While our waitlist is generally chronological, we do apply
certain priority criteria:
1. **Siblings:** Priority is given to siblings of currently enrolled students.
2. **Alumni:** We extend priority to the children of our alumni.
3. **Full-Time vs. Part-Time:** For certain programs, priority may be given to
   families seeking full-time enrollment over part-time.

**Communication:** We understand that being on a waitlist can be stressful for
families. We provide regular updates to waitlisted parents regarding their
status and the likelihood of a space becoming available. We encourage families
to remain in contact and update us on any changes in their enrollment needs.

**Offer of Admission:** When a space becomes available, we notify the first family
on the waitlist. They typically have 48 hours to accept the offer and pay the
enrollment deposit to secure the space.
"""

