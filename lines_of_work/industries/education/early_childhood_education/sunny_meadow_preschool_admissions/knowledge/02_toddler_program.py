title = "Program Overview: The Toddler Program"

content = """
The Toddler Program at Sunny Meadow Preschool is designed for our youngest learners,
typically aged 2 to 3 years. This program focuses on the critical transition from
parallel play to early social interaction and the development of foundational
independence.

**Environment:** The toddler classroom is a warm, sensory-rich environment with
furniture and materials appropriately scaled for small hands and bodies. We
emphasize safety and exploration, allowing toddlers to move freely and engage
with toys that stimulate their curious minds.

**Core Focus Areas:**
1. **Social-Emotional Development:** We support toddlers as they begin to recognize
   their own emotions and the feelings of others. Learning to share, take turns,
   and express needs verbally are key milestones.
2. **Language Acquisition:** Our teachers engage in constant conversation, singing,
   and reading to build vocabulary and encourage early communication skills.
3. **Self-Help Skills:** We encourage toddlers to take pride in 'doing it myself',
   from putting on their coats to helping clean up toys.
4. **Physical Development:** Both fine and gross motor skills are nurtured through
   activities like finger painting, puzzles, climbing, and outdoor play.

**Potty Training:** We work in close partnership with parents during the potty
training phase. Our approach is gentle and encouraging, following the child's
lead and maintaining consistency between home and school routines.
"""

