content = """
- 每年9月初，学院根据教务处下发的录取名单，在教务系统中批量导入新生学籍信息，包括姓名、身份证号、专业、班级、学号（规则：入学年份+学院代码+专业代码+序号，共12位）。
- 新生需在报到后2周内完成学信网图像采集，逾期未采集者暂停学籍注册资格。
- 注册状态分为"已注册""暂缓注册""未注册"。因学费未缴或材料不全者标记为"暂缓注册"，需在1个月内补齐，否则按自动退学处理。
- 注册完成后，生成学生证并加盖学院公章，学生证编号与学号一致。每学期开学第一周需进行学期注册盖章，未盖章者无法使用学生证购票优惠。
"""

