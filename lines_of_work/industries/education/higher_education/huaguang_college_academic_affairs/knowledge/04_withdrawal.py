title = "退学处理流程"

content = """
- 退学分为主动申请和强制退学。主动退学需学生本人及家长签字，提交《退学申请表》；强制退学情形包括：学业警示后仍不达标、未请假连续2周不参加教学活动、超过最长学习年限（本科6年、硕士5年）等。
- 学院收到退学申请后，由教学秘书核查学籍状态、成绩、欠费情况，形成报告提交学院党政联席会审议。审议通过后报教务处，教务处发文并注销学信网学籍。
- 退学学生可申请开具《退学证明》及已修课程成绩单，但不再颁发毕业证书或学位证书。学费按《华光大学学生收费管理办法》退还剩余学期部分。
- 强制退学前需履行告知程序，书面通知学生及家长，给予15天申诉期。申诉由学院教学委员会受理，最终决定由校长办公会做出。
"""

