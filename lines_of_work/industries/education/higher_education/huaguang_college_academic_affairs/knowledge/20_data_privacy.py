title = "学生数据隐私保护"

content = """
- 学生个人信息（身份证号、家庭住址、联系方式、成绩、处分记录）属于敏感数据，仅限教务工作需要时访问，不得用于其他用途。
- 成绩单、学籍证明等文件需加盖学院公章方可对外提供。学生本人可凭校园卡在自助打印机上打印，自助打印机数据直连教务系统，不经过人工。
- 向第三方（如用人单位、考研院校）提供成绩单，需学生本人书面授权，并核实对方身份。批量提供数据需经教学院长批准。
- 数据泄露应急：一旦发现数据泄露，立即报告学院办公室和网络信息中心，并在2小时内通知受影响学生。同时启动调查，追究责任人。
"""

