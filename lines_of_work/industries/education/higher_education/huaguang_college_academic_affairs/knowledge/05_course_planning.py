title = "培养方案与开课计划"

content = """
- 培养方案每四年修订一次，由学院教学委员会组织各系主任、骨干教师起草，经教务处审核、校教学指导委员会批准后执行。方案包含课程代码、学分、学时、开课学期、先修课程等。
- 每学期第10周，教学秘书根据培养方案生成下一学期开课计划，包括必修课、专业选修课、通识选修课。必修课按行政班开设，选修课需满足选课人数≥15人方可开课，不足则停开并通知学生改选。
- 开课计划需在教务系统中设置课程容量（一般不超过120人/班）、授课教师、上课时间（周次、节次）、教室类型（多媒体/机房/实验室）。排课冲突检测由系统自动完成，人工复核。
- 每学期第12周前，开课计划需经教学院长签字后提交教务处备案。若有调整，需在选课前完成，选课后原则上不再变更。
"""

