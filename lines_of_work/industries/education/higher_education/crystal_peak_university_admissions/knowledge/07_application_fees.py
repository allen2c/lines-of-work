title = "Application Fees and Fee Waiver Policies"

content = """
Application fees support the cost of holistic review processes, including reader
compensation, technology infrastructure, and applicant support services. Crystal
Peak maintains fee waiver programs to ensure financial circumstances never
prevent qualified students from applying.

**Standard Application Fee:** The undergraduate application fee is $65 for domestic
applicants and $75 for international applicants. The fee is non-refundable and
must be paid via credit card or electronic check at submission. Application fees
are waived automatically for students who qualify for fee waivers.

**Fee Waiver Eligibility:** U.S. citizens and permanent residents may request
fee waivers if they meet any of the following criteria: participation in the
Free and Reduced Price Lunch Program, enrollment in TRIO or GEAR UP programs,
qualification for SAT/ACT fee waivers, family receiving public assistance, or
experiencing family financial hardship. International applicants may request waivers
based on demonstrated financial hardship.

**Waiver Request Process:** Fee waiver requests are submitted through the Common
Application, where counselors attest to student eligibility. For applicants
unable to obtain counselor signatures, direct requests to the Admissions Office
with supporting documentation are accepted. Waiver decisions are typically
communicated within 3-5 business days.

**Automatic Waivers:** Crystal Peak automatically waives application fees for
students attending designated relationship high schools in historically underserved
communities, active-duty military applicants, and veterans using GI Bill benefits.

**Refund Policy:** Application fees are non-refundable once submitted, even if
the applicant withdraws the application or is denied admission. Applicants should
confirm all application materials are complete before submitting payment.

**Multiple Application Fees:** Students applying to multiple entry terms or
programs (e.g., both Fall and Spring) must submit separate applications and fees
for each. Internal program transfers after enrollment do not require additional
application fees.
"""

