title = "Graduate Admissions Overview"

content = """
Crystal Peak University offers 50+ graduate and professional programs across
disciplines, each with distinct admission requirements, timelines, and
evaluation criteria. Understanding graduate admissions processes supports
counselors and prospective students navigating post-baccalaureate education options.

**Program-Specific Requirements:** Unlike undergraduate admissions, graduate
admissions are managed by individual departments and schools. Each program
sets its own prerequisites, standardized test requirements, and application
materials. Some programs require GRE or GMAT scores; others have eliminated
testing requirements. Students must consult program-specific admissions pages
for accurate requirements.

**Application Components:** Typical graduate applications include a statement
of purpose, letters of recommendation (usually three), official transcripts from
all prior institutions, standardized test scores if required, and a resume or
curriculum vitae. Research-based programs may require writing samples or
research proposals.

**Application Timelines:** Graduate application deadlines vary widely by program.
Many doctoral programs have December or January deadlines for Fall enrollment.
Professional master's programs often have rolling admissions or multiple
entry points throughout the year. Some programs admit students only once
annually; others admit for Fall, Spring, and Summer terms.

**Funding and Assistantships:** Most doctoral programs and some master's
programs offer funding packages including tuition remission, health insurance,
and stipends in exchange for teaching or research assistantships. Funding
consideration often requires earlier application deadlines than unfunded admission.

**Interdisciplinary Programs:** Crystal Peak supports several interdisciplinary
graduate programs that draw faculty from multiple departments. These programs
often have flexible admission requirements but may require identifying faculty
advisors before application.

**Conditional Admission:** Some programs offer conditional admission to students
who demonstrate potential but lack specific prerequisites. Conditional admits must
complete identified coursework within their first year before advancing to
full standing.

**Non-Degree Enrollment:** Students may enroll in graduate courses as non-degree
students, either for professional development or to strengthen future applications.
Non-degree enrollment requires application but with reduced documentation.
Credit limits apply before formal degree program admission is required.

**Contact Information:** Graduate admissions questions should be directed to
individual program coordinators or the Graduate Admissions Office, not the
undergraduate admissions office, as requirements and processes differ substantially.
"""

