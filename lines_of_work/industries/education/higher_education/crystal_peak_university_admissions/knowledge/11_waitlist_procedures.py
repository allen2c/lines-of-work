title = "Waitlist Policies and Activation Procedures"

content = """
The waitlist provides a mechanism to manage enrollment uncertainty while
preserving opportunities for qualified applicants when space becomes available.
Crystal Peak maintains an active waitlist for each admission cycle with structured
procedures for waitlist management.

**Waitlist Placement:** Students are placed on the waitlist when the admissions
committee has determined they are admissible but enrollment capacity constraints
prevent immediate admission. Waitlisted students are not ranked; selection from
the waitlist considers enrollment needs across academic programs, geographic
diversity, and other institutional priorities.

**Waitlist Response:** Upon being waitlisted, applicants must confirm their
desire to remain on the waitlist through the applicant portal. Students who
do not confirm interest within two weeks are assumed to have declined the
waitlist position and are removed. Confirming waitlist status does not require
withdrawing applications at other institutions or declining other admission offers.

**Additional Materials:** Waitlisted students may submit one letter of continued
interest (500 words maximum) updating the admissions committee on recent
academic achievements, awards, or significant developments since the original
application. Additional recommendation letters or unsolicited updates are
strongly discouraged.

**Waitlist Timeline:** Waitlist decisions typically begin in early May, after
National College Decision Day (May 1) when admitted student enrollment
commitments are confirmed. Waitlist activity may continue through mid-July,
depending on summer melt and enrollment fluctuations.

**Admission from Waitlist:** If offered admission from the waitlist, students
receive notification via email and portal update. Admitted students from the
waitlist have 72 hours to accept or decline the offer. Upon acceptance, students
must submit an enrollment deposit and withdraw applications from other institutions.

**Financial Aid for Waitlist Admits:** Waitlisted students who are later admitted
receive financial aid packages with their acceptance notification. The university
meets full demonstrated need for all admitted students regardless of admission
timeline.
"""

