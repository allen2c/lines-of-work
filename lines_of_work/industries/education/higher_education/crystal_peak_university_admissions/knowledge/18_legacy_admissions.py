title = "Legacy and Alumni Affiliation Considerations"

content = """
Crystal Peak University acknowledges the multi-generational connections that
strengthen institutional community while maintaining that admission decisions
are based primarily on individual merit and institutional fit.

**Legacy Definition:** For admission purposes, legacy status applies to applicants
with a parent or grandparent who earned an undergraduate degree from Crystal Peak
University. Siblings, aunts, uncles, and cousins do not constitute legacy status.
The legacy connection must be verified through institutional records.

**Legacy in Holistic Review:** Legacy status is a minor positive factor in the
holistic review process, typically considered alongside other applicant
demonstrated interest indicators. Legacy applicants receive no automatic
preference or separate review tracks. Admission rates for legacy applicants
reflect correlation with family educational attainment, not causation.

**Development Considerations:** Applicants whose families have made significant
philanthropic contributions to Crystal Peak are not accorded admission preference.
Development staff and admissions staff operate independently, and no applicant
receives admission consideration based on donation history or potential.

**Alumni Interview Program:** Legacy status does not guarantee alumni interview
assignment. Interview availability depends on geographic coverage, application
timeline, and interviewer capacity. Legacy applicants request interviews through
the same process as all other applicants.

**Sibling Enrollment:** Current sibling enrollment at Crystal Peak does not
confer legacy status but may inform understanding of applicant fit and family
connection to the institution. Sibling relationships are noted but not
weighted heavily in review.

**Multiple Legacy Connections:** Applicants with multiple generations of Crystal
Peak alumni in their family receive no additional preference beyond the single
legacy factor. The depth of family connection is not weighted more heavily
than breadth.

**Transparency Commitment:** Crystal Peak publishes annual admissions statistics
showing legacy representation in the admitted class, maintaining accountability
to community expectations about access and opportunity.
"""

