content = """
Crystal Peak University competes in NCAA Division II athletics, fielding 18
varsity teams across men's and women's sports. Student-athlete recruitment
operates within NCAA compliance frameworks while supporting academic and athletic
excellence.

**Recruitment Process:** Prospective student-athletes should contact coaches
directly to express interest and provide athletic resumes, performance statistics,
and competition footage. Coaches evaluate athletic fit and may extend verbal
commitments contingent upon admission and NCAA eligibility center certification.

**NCAA Eligibility Center:** All prospective student-athletes must register with
the NCAA Eligibility Center and meet academic standards including minimum GPA,
specific core course requirements, and standardized test score thresholds (if
applying to Division I or II). Crystal Peak requires eligibility center
certification before athletic participation.

**Athletic Scholarships:** Division II programs offer partial athletic
scholarships that may be combined with academic merit awards and need-based
financial aid. Total aid packages cannot exceed institutional cost of attendance.
Coaches determine scholarship allocation based on athletic contribution potential.

**Academic Integration:** Student-athletes must meet the same admission standards
as all applicants. Athletic ability does not substitute for academic preparation.
The Faculty Athletics Representative reviews all admitted student-athlete files
to ensure academic readiness for rigorous coursework.

**Recruiting Rules and Regulations:** Coaches must adhere to NCAA recruiting
calendars that define permissible contact periods, evaluation windows, and quiet
periods. Prospective athletes and families should familiarize themselves with
these regulations to understand communication timing.

**Preferred Walk-On Opportunities:** For sports without available scholarship
funding, coaches may identify preferred walk-on candidates who receive admission
support letters but no athletic financial aid. These athletes participate in
tryouts upon enrollment and may earn roster spots through demonstrated performance.

**Transfer Athletes:** Transfer student-athletes must meet NCAA transfer rules
including residency requirements and academic standing thresholds. Athletic
eligibility is determined by NCAA guidelines independent of admission decisions.
"""

