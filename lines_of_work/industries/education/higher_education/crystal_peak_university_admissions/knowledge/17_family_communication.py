title = "Parent and Family Communication Guidelines"

content = """
Family involvement in the college application process requires careful navigation
of privacy regulations, developmental appropriateness, and institutional
communication protocols. Crystal Peak establishes clear boundaries that respect
applicant autonomy while supporting family engagement.

**FERPA and Applicant Privacy:** The Family Educational Rights and Privacy Act
(FERPA) protects student educational records, including application materials.
Parents do not have automatic access to application status, admission decisions,
or financial aid information unless the applicant grants explicit consent through
the portal authorization system.

**Applicant-Initiated Communication:** Crystal Peak primarily communicates with
applicants directly via email and portal notifications. This practice builds
student independence and ensures applicants own their admission process. Families
are encouraged to support applicants in monitoring communications rather than
seeking direct institutional contact.

**Parent-Specific Resources:** The university provides dedicated parent and
family newsletters, webinars, and a family portal with general information
about student services, campus safety, and family orientation programming.
These resources do not include application-specific information.

**Counselor Partnerships:** High school counselors serve as important intermediaries,
receiving application status updates and decision notifications alongside
applicants. Counselors can provide general guidance to families while respecting
applicant privacy preferences.

**Family Involvement Boundaries:** While family support is encouraged, parents
should not complete application materials, write essays, or contact admissions
on behalf of applicants. Excessive parent involvement may disadvantage
applications by signaling lack of student independence.

**Financial Aid Confidentiality:** Financial aid packages and family contribution
assessments are shared with both the student and parents listed on the FAFSA.
Separate financial aid counseling appointments are available for parents who
have questions about the aid package.

**Special Circumstances:** For applicants with documented circumstances
requiring parent involvement (medical conditions, legal guardianship), Crystal
Peak accommodates necessary parent communication with appropriate documentation.

**Parent Contact Protocol:** Admissions staff will redirect parent inquiries
about application status or decisions to the applicant, maintaining consistent
FERPA compliance while remaining courteous and helpful.
"""

