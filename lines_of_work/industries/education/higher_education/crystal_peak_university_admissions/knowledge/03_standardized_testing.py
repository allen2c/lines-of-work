title = "Standardized Testing Policies and Score Submission"

content = """
Crystal Peak University maintains a test-optional admission policy, recognizing
that standardized test scores represent only one dimension of academic preparation.
Understanding testing policies helps applicants make informed decisions about
whether to submit scores and how those scores factor into holistic review.

**Test-Optional Policy:** Applicants may choose whether to submit SAT or ACT
scores as part of their application. This policy applies to all undergraduate
programs, including competitive majors like Engineering and Nursing. The decision
to withhold scores will not disadvantage an applicant in the review process.

**Score Submission Methods:** Applicants self-report scores on the application
for initial review. Upon enrollment, admitted students who submitted scores must
provide official score reports from the College Board or ACT for verification.
Discrepancies may result in admission rescission.

**Score Consideration:** When submitted, scores are evaluated within the context
of the applicant's background and educational opportunities. Crystal Peak does
not maintain minimum score requirements; instead, scores inform understanding
of academic readiness alongside grades, coursework rigor, and other factors.

**Superscoring:** For applicants submitting scores, Crystal Peak superscores both
SAT and ACT, combining the highest section scores across multiple test dates.
Applicants should report all scores taken, and the admissions system automatically
calculates the highest composite.

**English Proficiency Testing:** International applicants must submit TOEFL,
IELTS, Duolingo, or PTE scores regardless of test-optional status for domestic
applicants. These scores ensure adequate English preparation for rigorous
academic coursework.

**Testing Fee Waivers:** U.S. citizens and permanent residents demonstrating
financial need may request application fee waivers and may also qualify for SAT/ACT
fee waivers through their high school counselors.
"""

