title = "Required Academic Documents and Transcript Policies"

content = """
Document verification forms the foundation of holistic application review. Crystal
Peak University requires specific academic records to assess applicant preparation
and fit. Understanding these requirements ensures timely processing and prevents
administrative delays.

**Official Transcripts:** First-year applicants must submit official high school
transcripts showing all coursework through junior year, with senior year courses
in progress. Transcripts must bear the school seal or registrar signature and
be sent directly from the issuing institution. Electronic transcripts through
Parchment, Naviance, or Slate are preferred and typically process within 48 hours.

**Self-Reported Academic Records:** Applicants may self-report grades and test
scores during the initial review phase. Upon admission, all students must submit
official documents for verification. Discrepancies between self-reported and
official records may result in admission rescission.

**College Coursework:** Dual enrollment or early college coursework requires
official transcripts from the awarding college or university, regardless of
whether grades appear on the high school transcript. These credits may transfer
if courses meet Crystal Peak's standards.

**Transfer Students:** Transfer applicants must submit official transcripts from
all previously attended post-secondary institutions, including dual enrollment
programs. Withholding academic history constitutes a violation of the application
affirmation and may result in denial or dismissal.

**Credential Evaluation:** International transcripts require evaluation by an
approved agency (WES, ECE, or SpanTran) showing U.S. equivalent grades and course
credit values. The evaluation must include a course-by-course analysis, not merely
a GPA conversion.

**Document Authentication:** Admissions reserves the right to verify document
authenticity with issuing institutions. Falsified or altered documents result in
immediate application withdrawal and notification to relevant authorities.
"""

