content = """
The enrollment confirmation process transforms admitted students into enrolled
members of the Crystal Peak community. Clear understanding of deposit requirements,
deadlines, and next steps ensures smooth transition from admission to enrollment.

**National College Decision Day:** All admitted first-year students must submit
their enrollment confirmation and non-refundable deposit by May 1, regardless
of admission plan (Early Decision, Early Action, or Regular Decision). This
national standard allows students to complete financial aid comparison and
consultation with family and counselors before committing.

**Enrollment Deposit:** The enrollment deposit is $500 for domestic students
and $750 for international students. The deposit secures the student's place
in the entering class and is applied toward first-semester charges. Deposits
are non-refundable except in documented cases of financial hardship or medical
emergency preventing enrollment.

**Confirmation Process:** Students confirm enrollment through the Crystal Peak
Applicant Portal by clicking the "Confirm Enrollment" button and submitting
payment via credit card or electronic check. Upon confirmation, students receive
enrollment credentials, housing application access, and registration timeline
information.

**Withdrawal After Confirmation:** Students who confirm enrollment but subsequently
decide to attend another institution should withdraw their Crystal Peak enrollment
through the portal. While deposits are not refunded, timely withdrawal opens
spaces for waitlisted students and allows accurate enrollment planning.

**Extended Deposit Deadlines:** In rare circumstances (significant financial
aid processing delays, visa processing for international students, or documented
family emergencies), deposit deadlines may be extended by one to two weeks. Extension
requests must be submitted before the original deadline.

**Housing Application Timeline:** The housing application opens in mid-May for
students who have confirmed enrollment. Housing is guaranteed for all first-year
students who apply by the June 15 priority deadline. Roommate matching and
housing preferences are collected through the housing portal.

**Orientation Registration:** New Student Orientation registration opens in
early June. All entering students must attend orientation before course
registration. Multiple orientation sessions are offered throughout the summer
to accommodate family vacation schedules.

**Final Transcript Submission:** Enrolled students must submit final official
transcripts showing high school graduation and any senior year coursework.
Transcripts are due by July 15. Admission is contingent upon satisfactory
completion of senior year work.
"""

