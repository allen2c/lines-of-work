title = "Transfer Admission Policies and Credit Evaluation"

content = """
Transfer students bring valuable perspectives from prior college experiences.
Crystal Peak welcomes applications from students currently enrolled at other
institutions or those who have previously earned college credit after high
school graduation.

**Transfer Eligibility:** Students who have graduated from high school and
completed at least one semester of full-time college coursework (12+ credits)
should apply as transfer students. Those with fewer credits may apply as
first-year applicants with college credit, depending on their circumstances.

**Application Requirements:** Transfer applicants submit the Common Application
for Transfer Students, including all college transcripts, high school transcripts,
one recommendation from a college instructor or advisor, and a personal statement
explaining transfer motivation and goals. Standardized test scores are optional
for students with 24+ completed college credits.

**Credit Evaluation:** Accepted transfer credits are evaluated on a course-by-course
basis. Courses with grades of C or better from regionally accredited institutions
typically transfer if they align with Crystal Peak curriculum. The university
accepts up to 60 transfer credits toward the 120-credit degree requirement.

**Transfer Pathway Programs:** Crystal Peak maintains articulation agreements
with community colleges and regional universities, creating guaranteed pathways
for students who complete specified coursework with minimum GPA requirements.
These agreements streamline credit transfer and provide clear roadmap guidance.

**Residency Requirements:** All students must complete at least 30 of their final
36 credits at Crystal Peak to earn a degree. This residency policy ensures that
the university experience includes substantial time within the Crystal Peak community.

**Transfer Application Deadlines:** March 1 for Fall semester enrollment and
October 1 for Spring semester enrollment. Rolling review continues until programs
reach capacity. Nursing and Engineering programs typically fill before deadline
dates, so early application is strongly encouraged.

**Transfer Student Support:** Admitted transfers participate in orientation
programs specifically designed for their needs, including academic advising
sessions that map remaining degree requirements and credit transfer confirmation.
"""

