title = "International Student Admissions Procedures"

content = """
International student admissions at Crystal Peak University require additional
documentation and evaluation steps beyond the standard domestic application.
Understanding these requirements ensures accurate counseling and proper file
processing for this important applicant cohort.

**English Proficiency Requirements:** All applicants whose native language is not
English must demonstrate proficiency through approved testing. Acceptable exams
include TOEFL iBT (minimum 79), IELTS Academic (minimum 6.5), Duolingo English
Test (minimum 105), or PTE Academic (minimum 53). Scores must be sent directly
from the testing agency and remain valid for two years from the test date.

**Credential Evaluation:** Academic transcripts from non-U.S. institutions require
certified translation and professional evaluation. Crystal Peak accepts evaluations
from WES, ECE, or SpanTran. Evaluations must include course-by-course analysis with
GPA calculation on the U.S. 4.0 scale. Some graduate programs may require additional
subject-specific evaluations.

**Financial Certification:** International applicants must submit a Financial
Certification Form with supporting bank statements or sponsor letters demonstrating
sufficient funds for tuition, fees, and living expenses for the first academic year.
The current required amount is $48,500 for undergraduates and varies by program for
graduate students. Documents must be in English or include certified translation.

**Visa Documentation:** Upon admission, the university issues Form I-20 (for F-1 visa
applicants) or Form DS-2019 (for J-1 exchange visitors). Processing requires a copy
of the passport biographical page and proof of financial support. Students must
pay the SEVIS fee and schedule a visa interview at the U.S. embassy or consulate
in their home country.

**Application Deadlines:** International applicants should apply earlier than domestic
students to allow time for credential evaluation and visa processing. Recommended
deadlines are December 1 for Fall semester and July 1 for Spring semester. Some
programs with rolling admission may extend these dates, but visa processing time
should always be considered.

**Special Considerations:** Transfer credits from international institutions are
evaluated individually; not all coursework may transfer. Students on temporary visas
are ineligible for federal financial aid but may qualify for institutional scholarships.
International athletes must meet NCAA eligibility requirements plus visa regulations.
"""

