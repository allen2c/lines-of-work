title = "Early Decision and Early Action Policies"

content = """
Early application plans allow students to demonstrate strong institutional
interest and receive earlier admission decisions. Crystal Peak offers both binding
Early Decision and non-binding Early Action to serve different applicant needs.

**Early Decision (Binding):** The November 1 Early Decision deadline requires
applicants to commit to enrolling if admitted. This binding agreement is
appropriate only when Crystal Peak is the applicant's clear first choice and
after thorough consideration of financial aid implications. Students applying
Early Decision may simultaneously apply to other institutions under non-binding
plans, but must withdraw those applications upon Crystal Peak acceptance.

**Early Decision Agreement:** The Common Application Early Decision Agreement
requires signatures from the applicant, a parent/guardian, and a school counselor,
all attesting understanding of the binding commitment. Applications lacking
proper signatures are automatically moved to Regular Decision review.

**Early Decision Financial Aid:** Admitted Early Decision students receive
financial aid packages with their acceptance letters, typically by mid-December.
Because the commitment is binding regardless of aid package, students requiring
significant financial support should carefully consider whether Early Decision
is appropriate. The university meets full demonstrated need for all admitted
students, but family contribution assessments vary.

**Early Action (Non-Binding):** The November 15 Early Action deadline provides
mid-January decisions without enrollment commitment. Early Action applicants
may apply to any other institutions and have until May 1 to confirm enrollment.
This plan suits well-prepared students seeking early notification without
restricting their options.

**Single-Choice Early Action:** Crystal Peak does not restrict Early Action
applicants from applying early elsewhere. Students may apply to any combination
of Early Decision and Early Action programs at other institutions while
maintaining their Crystal Peak Early Action application.

**Deferral to Regular Decision:** Early Decision and Early Action applicants
not admitted may be deferred to Regular Decision for additional review, denied,
or waitlisted. Deferral does not indicate likely denial; it signals the
committee desires additional context, such as first-semester senior grades.
"""

