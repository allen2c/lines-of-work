title = "Gap Year and Enrollment Deferral Policies"

content = """
Crystal Peak University recognizes that a gap year between high school and
college can provide valuable personal growth, work experience, or community
engagement. The university supports structured gap years through an enrollment
deferral process.

**Deferral Eligibility:** Admitted students may request a one-year enrollment
deferral for structured gap year experiences. Deferrals are granted for reasons
including travel, volunteer service, work experience, religious mission service,
or addressing personal health concerns. Deferrals are not granted for students
planning to enroll at other institutions during the gap year.

**Deferral Request Process:** Students must first confirm their admission by
submitting the enrollment deposit and then submit a written deferral request
by June 1. The request must include a detailed gap year plan with specific
activities, goals, and anticipated outcomes. Vague intentions to "travel and
figure things out" typically result in deferral denial.

**Approved Gap Year Activities:** Successful gap year plans include structured
programs such as AmeriCorps or Peace Corps service, organized travel programs,
full-time employment in career-related fields, or addressing significant
personal circumstances. Academic coursework at other institutions during a
gap year is permitted but may not exceed six credits.

**Deferral Approval:** The admissions committee reviews deferral requests and
communicates decisions within three weeks. Approved deferrals guarantee enrollment
the following fall semester upon confirmation that the gap year plan was
substantially completed. Students who significantly deviate from approved plans
may forfeit their guaranteed admission.

**Financial Aid Implications:** Merit scholarships are held for one year but
may be adjusted if institutional award levels change. Need-based financial aid
must be reevaluated based on updated family financial circumstances in the
deferral year.

**Housing and Registration:** Deferred students participate in standard housing
selection and course registration processes the year they enroll, not during
their original admission year. Orientation programming is required at time of
delayed enrollment.

**Denied Deferral Options:** If a deferral request is denied, students may
accept their original admission for the current year or decline and reapply
in a future cycle without penalty.
"""

