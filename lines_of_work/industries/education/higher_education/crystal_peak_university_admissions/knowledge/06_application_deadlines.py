title = "Application Deadlines and Decision Plans"

content = """
Crystal Peak University offers multiple admission plans to accommodate diverse
applicant timelines and decision preferences. Understanding these options enables
counselors to guide students toward the plan best suited to their preparation
level and institutional interest.

**Early Decision (Binding):** November 1 deadline with mid-December notification.
Early Decision is a binding agreement: accepted students must withdraw all other
applications and enroll at Crystal Peak. This plan suits students for whom Crystal
Peak is the clear first choice and who have thoroughly researched the financial
implications. The binding nature signals strongest institutional interest.

**Early Action (Non-Binding):** November 15 deadline with mid-January notification.
Early Action provides earlier decision notification without enrollment commitment.
Students may apply to other institutions and have until May 1 to confirm enrollment.
This plan suits prepared applicants seeking early peace of mind without restricting
options.

**Regular Decision:** January 15 deadline with mid-March notification. Regular
Decision accommodates students completing rigorous fall coursework, retaking
standardized tests, or refining application materials. All applicants receive equal
consideration regardless of plan selected.

**Rolling Admission:** Beginning November 1, applications are reviewed and
decisions rendered on a continuous basis until enrollment capacity is reached.
Rolling admission primarily serves transfer applicants and certain program-specific
applicants. Earlier submission within rolling admission improves consideration
for competitive programs nearing capacity.

**Decision Response Deadlines:** All admitted students, regardless of admission
plan, must submit enrollment confirmations by May 1 (National College Decision Day).
Extensions are rarely granted and require compelling circumstances.

**Deadline Extensions:** Crystal Peak does not typically extend deadlines except
in documented cases of natural disasters, school closures, or significant
personal/family emergencies. Extension requests must be submitted in writing
before the original deadline with supporting documentation.
"""

