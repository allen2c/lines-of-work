title = "Data Retention and Destruction"

content = """
- Student academic records are retained for 5 years after graduation or last attendance (per state law).
- Admissions records (applications, transcripts) are retained for 3 years after the application cycle.
- FERPA consent forms are retained for 3 years after the student’s last enrollment.
- Records are stored in Banner (digital) and in secure off-site storage (paper). Access is restricted to authorized personnel.
- Destruction: paper records are shredded; digital records are securely deleted after the retention period. A log of destruction is maintained.
"""

