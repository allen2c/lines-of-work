title = "FERPA Privacy Rules"

content = """
- FERPA (Family Educational Rights and Privacy Act) protects student education records.
- Directory information (name, address, phone, email, major, dates of attendance, degrees, honors, previous institutions) may be released without consent unless the student opts out.
- Non-directory information (grades, GPA, schedule, disciplinary records, social security number) requires written consent from the student.
- Consent forms are valid for one academic year; students can revoke at any time.
- Parents may access records only if the student is a dependent for tax purposes (documentation required) or with student’s written consent.
"""

