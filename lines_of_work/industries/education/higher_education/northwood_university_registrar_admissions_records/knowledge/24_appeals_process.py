title = "Appeals Process"

content = """
- Students may appeal decisions related to residency, transfer credit, academic standing, graduation requirements, and FERPA.
- Appeals must be submitted in writing to the Registrar’s Office within 30 days of the decision.
- The appeal should include: student name, ID, decision being appealed, reason for appeal, and supporting documentation.
- The Registrar reviews the appeal and responds within 10 business days. If denied, the student may appeal to the Academic Dean.
- Final appeal: to the Provost. Decisions are binding.
"""

