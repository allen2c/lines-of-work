title = "Enrollment Certification for Loans"

content = """
- For federal student loans (Direct Subsidized/Unsubsidized, PLUS), the Registrar certifies enrollment status to the National Student Clearinghouse.
- Certification includes: student’s name, SSN, enrollment status (full-time, half-time, less than half-time), expected graduation date, and program.
- Half-time enrollment: 6 credits (undergraduate), 5 credits (graduate). Below half-time may affect loan deferment.
- Certification is automatic for students who have signed a Master Promissory Note. Manual requests are processed within 3 business days.
- If a student drops below half-time, the Registrar notifies the loan servicer within 30 days.
"""

