title = "Academic Standing"

content = """
- Good standing: cumulative GPA ≥ 2.0.
- Academic probation: cumulative GPA < 2.0. Student is notified and must meet with an advisor. Probation lasts one semester; if GPA does not improve to 2.0, student is suspended.
- Academic suspension: cumulative GPA < 1.5 for two consecutive semesters. Suspension for one semester; reapplication required.
- Readmission after suspension: student must submit a petition and may be conditionally admitted with a reduced course load.
- Appeals of suspension are handled by the Academic Dean.
"""

