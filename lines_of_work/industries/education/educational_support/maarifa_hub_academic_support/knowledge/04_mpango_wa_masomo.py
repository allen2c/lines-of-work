content = """
Mpango wa masomo binafsi (Individual Learning Plan) ni hati rasmi inayoainisha
malengo ya mwanafunzi, mbinu za kufundishia, na ratiba ya vipindi. Ni mkataba wa
kielimu kati ya Maarifa Hub, mwanafunzi, na mzazi au mlezi.

**Muundo wa Mpango**
Mpango wa masomo wa kawaida unajumuisha:
- Taarifa ya msingi ya mwanafunzi (jina, darasa, shule ya msingi)
- Matokeo ya tathmini ya awali
- Malengo ya muda mfupi (ndani ya muhula mmoja)
- Malengo ya muda mrefu (mwaka mmoja au hadi mtihani)
- Mada na masomo ya kufunikwa
- Idadi ya vipindi kwa wiki na urefu wa kila kipindi
- Mbinu maalum za kufundishia (kwa mfano, matumizi ya picha, mchezo, au mazoezi ya ziada)

**Upangaji wa Vipindi**
Vipindi vinaweza kuwa:
- *Vipindi vya mtu mmoja mmoja:* Mwanafunzi mmoja na mwalimu mmoja — bora kwa
  wanafunzi wenye mapungufu makubwa au mahitaji maalum ya kielimu
- *Vipindi vya kikundi kidogo:* Wanafunzi 2–4 wenye kiwango kinachofanana — bora
  kwa masomo ya kawaida na kwa wanafunzi wanaofaidika na mazingira ya kijamii

**Ukaguzi na Marekebisho ya Mpango**
Mpango unakaguliwa kila mwezi mmoja. Ikiwa mwanafunzi anaendelea vizuri, malengo
yanaweza kuongezwa. Ikiwa kuna changamoto, mbinu au ratiba inaweza kubadilishwa.
Mzazi anapaswa kushiriki kikamilifu katika mchakato wa ukaguzi.

**Wajibu wa Mwanafunzi**
Mwanafunzi anatarajiwa kushiriki kikamilifu katika vipindi vyote, kukamilisha kazi
za nyumbani zilizoasigiwa, na kumjulisha mwalimu iwapo ana changamoto yoyote.
Kuhudhuria kwa mara kwa mara ni muhimu kwa mafanikio ya mpango.
"""

