title = "Communication radio et signalisation"

content = """
La radio est l'outil central de coordination entre opérateurs, maintenance, sécurité et
encadrement. Une communication disciplinée évite les malentendus dans les moments critiques.

**Discipline d'antenne :** Parler clairement, brièvement, une personne à la fois. Annoncer
son identité et son poste, énoncer le message, attendre l'accusé de réception. Ne pas
saturer le canal par des échanges superflus.

**Codes et procédures :** Utiliser les codes convenus du parc pour les situations courantes
et les urgences. Connaître le canal d'urgence et la façon de déclencher une alerte
prioritaire.

**Confirmation :** Reformuler les instructions importantes pour vérifier la bonne
compréhension, surtout lors d'une panne ou d'une évacuation. Un message non confirmé est un
message non reçu.

**Confidentialité :** Ne pas diffuser sur les ondes de données personnelles de visiteurs ni
d'informations sensibles sur un accident. Employer un langage neutre et factuel.

**Signalisation visuelle :** Compléter la radio par des gestes convenus entre équipiers,
utiles dans le bruit ou à distance : voie libre, arrêt, attention. Ces signaux doivent être
identiques pour toute l'équipe.

**Vérification du matériel :** Contrôler la charge et le fonctionnement de la radio en début
de poste, signaler tout défaut. Une radio silencieuse en cas d'urgence est un risque majeur.
"""  # noqa: E501

