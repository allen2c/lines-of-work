title = "Coordination avec la restauration et les boutiques"

content = """
Les points de restauration et les boutiques bordent souvent les attractions et partagent
leurs flux de visiteurs. L'opérateur coordonne l'exploitation avec ces services pour la
fluidité et la sécurité.

**Flux partagés :** Veiller à ce que les files des attractions n'entravent pas les accès
aux stands de restauration, aux boutiques ou aux allées de circulation, et inversement.

**Nourriture et boissons à bord :** Rappeler l'interdiction d'embarquer de la nourriture,
des boissons ou des objets encombrants lorsque l'attraction l'exige. Prévoir un espace de
dépôt ou orienter vers les casiers.

**Propreté et sécurité :** Signaler les déversements, déchets ou sols glissants aux équipes
d'entretien, notamment aux abords des zones d'embarquement où le risque de chute augmente.

**Affluence coordonnée :** Informer les services voisins des fermetures d'attraction ou des
pics d'affluence prévisibles, qui modifient la fréquentation des commerces alentour.

**Orientation des visiteurs :** Renseigner sur la localisation des restaurants, boutiques,
sanitaires et points d'eau, et sur leurs horaires, sans quitter son poste de sécurité.

**Objets achetés :** Rappeler de sécuriser les achats (peluches, ballons, souvenirs) avant
l'embarquement, ces objets étant une cause fréquente de perte et de gêne à bord.
"""  # noqa: E501

