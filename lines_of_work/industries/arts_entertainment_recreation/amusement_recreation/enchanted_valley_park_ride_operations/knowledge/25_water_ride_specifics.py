title = "Spécificités des attractions aquatiques"

content = """
Les attractions aquatiques du secteur des Rapides Bleus (bouées, descentes, bateaux)
présentent des risques particuliers liés à l'eau, aux surfaces glissantes et à la
profondeur. Elles appellent une vigilance renforcée.

**Sols glissants :** Les zones d'embarquement et de débarquement sont mouillées en
permanence. Signaler et surveiller les risques de chute, guider les visiteurs sur les
cheminements antidérapants, tenir compte des enfants qui courent.

**Consignes vestimentaires :** Rappeler les tenues et chaussures adaptées, l'interdiction
d'objets pouvant être perdus dans l'eau, et le fait que les visiteurs seront mouillés.

**Contrôle des embarcations :** Vérifier le nombre de passagers, la répartition du poids et
le bon positionnement dans la bouée ou le bateau, éléments qui influent sur la stabilité.

**Surveillance de l'eau :** Repérer tout visiteur en difficulté, en coordination avec la
surveillance aquatique lorsque l'attraction en comporte. Ne jamais laisser un passager
tenter de sortir seul dans une zone en eau.

**Qualité et niveau d'eau :** Signaler tout défaut apparent (niveau, débit, propreté) à la
maintenance ; l'opérateur ne traite pas l'eau mais alerte.

**Météo :** Les attractions aquatiques restent soumises aux seuils de vent et d'orage. Le
froid et la fatigue liés à l'eau accentuent la vigilance à porter aux visiteurs.
"""  # noqa: E501

