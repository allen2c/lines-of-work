title = "Évacuation d'urgence des attractions"

content = """
L'évacuation d'une attraction, notamment lorsqu'elle immobilise des passagers en hauteur,
est une opération délicate qui suit une procédure formée et répétée. La panique est le
principal danger.

**Déclencheurs :** Panne bloquant les nacelles, coupure d'énergie, incendie, alerte
générale, conditions dangereuses. La décision d'évacuer relève de la procédure et de
l'encadrement, pas de l'improvisation.

**Rassurer avant tout :** S'adresser aux passagers immobilisés d'une voix calme et audible,
expliquer que la situation est maîtrisée, qu'ils sont en sécurité et qu'il ne faut pas
tenter de sortir seuls des dispositifs de retenue.

**Ne pas improviser la descente :** L'évacuation en hauteur nécessite du personnel formé et
du matériel dédié. L'opérateur suit le rôle qui lui est assigné dans le plan et n'entame
aucune manœuvre non prévue.

**Coordination :** Communiquer par radio avec la maintenance, la sécurité et l'encadrement.
Suivre l'ordre d'évacuation nacelle par nacelle, aider les personnes fragiles en priorité.

**Regroupement :** Guider les personnes évacuées vers le point de rassemblement, vérifier
qu'aucun passager ne manque.

**Après l'évacuation :** Ne pas remettre l'attraction en service sans validation complète.
Rédiger un rapport détaillé et participer au débriefing.
"""  # noqa: E501

