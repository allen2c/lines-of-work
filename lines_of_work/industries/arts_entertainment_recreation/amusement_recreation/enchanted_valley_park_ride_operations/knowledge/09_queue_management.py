title = "Gestion des files d'attente"

content = """
Une file bien gérée réduit l'attente ressentie, prévient les tensions et sécurise les flux.
L'opérateur organise l'entrée et régule le rythme d'embarquement.

**Structuration :** Maintenir les visiteurs dans les couloirs balisés, éviter les
resquilles et les coupes de file. Veiller à ce que les barrières et cordons restent en
place et lisibles.

**File prioritaire et accès PMR :** Gérer l'alternance entre file classique et file
prioritaire selon la règle du parc, sans léser durablement l'une des deux. Orienter les
personnes à mobilité réduite vers l'accès dédié.

**Affichage du temps d'attente :** Estimer et communiquer un temps réaliste, plutôt
légèrement majoré. Une attente annoncée puis raccourcie est mieux vécue que l'inverse.

**Confort et sécurité dans la file :** Surveiller les signes de malaise par forte chaleur,
rappeler de s'hydrater, veiller sur les enfants et les personnes fragiles.

**Régulation du débit :** Adapter le remplissage des nacelles pour maximiser la capacité
sans jamais sacrifier les contrôles de sécurité. Remplir les nacelles au plus près de leur
capacité utile.

**Gestion des tensions :** En cas de conflit dans la file, garder son calme, rappeler les
règles avec courtoisie et faire appel à la sécurité si la situation dégénère. Ne jamais
répondre à l'agressivité par l'agressivité.
"""  # noqa: E501

