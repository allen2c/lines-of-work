title = "Contrôles quotidiens et registre d'exploitation"

content = """
Les contrôles quotidiens constituent la première barrière de sécurité. Ils sont réalisés
selon une liste de vérification propre à chaque attraction et tracés dans le registre
d'exploitation.

**Nature des contrôles :** Ils portent sur l'intégrité structurelle visible, l'état des
dispositifs de retenue, le fonctionnement des freins et arrêts d'urgence, la lisibilité
de la signalisation, la propreté des zones et l'absence d'obstacles.

**Fréquence :** Certains contrôles sont réalisés à l'ouverture, d'autres en cours de
journée à intervalles définis, notamment après un arrêt prolongé ou un événement
météorologique.

**Méthode :** Suivre la liste dans l'ordre, ne cocher un point qu'après vérification
réelle, jamais par anticipation. Un point non conforme bloque l'exploitation.

**Traçabilité :** Chaque contrôle est horodaté et signé. Le registre est un document
opposable ; il doit refléter fidèlement la réalité et rester lisible.

**Anomalies :** Toute anomalie est décrite précisément (localisation, nature, gravité
apparente) et transmise à la maintenance. L'opérateur ne juge pas seul de la remise en
service d'un élément défectueux.

**Conservation :** Les registres sont archivés et peuvent être demandés lors d'un contrôle
réglementaire ou d'une enquête. Leur exactitude engage la responsabilité de l'exploitant.
"""  # noqa: E501

