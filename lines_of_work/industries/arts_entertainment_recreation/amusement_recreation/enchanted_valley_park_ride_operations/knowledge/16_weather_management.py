title = "Gestion des conditions météorologiques"

content = """
La météo influe directement sur la sécurité des attractions de plein air. L'opérateur
surveille les conditions et applique les seuils d'exploitation définis par le parc et les
constructeurs.

**Vent :** Chaque attraction possède un seuil de vent au-delà duquel l'exploitation est
suspendue, en particulier les grandes structures et attractions en hauteur. Surveiller
l'anémomètre et respecter strictement les seuils, sans tolérance.

**Orage et foudre :** À l'approche d'un orage électrique, fermer les attractions exposées
et mettre les visiteurs à l'abri. La reprise n'a lieu qu'après la fin avérée du risque.

**Pluie :** La pluie rend certaines surfaces glissantes et peut affecter des systèmes.
Renforcer la vigilance sur les zones d'embarquement, appliquer les consignes propres à
chaque attraction.

**Chaleur :** Par forte chaleur, surveiller les visiteurs dans les files, rappeler
l'hydratation, repérer les signes d'insolation et adapter le rythme.

**Décision et escalade :** L'opérateur applique les seuils mais toute fermeture prolongée
ou reprise après épisode dangereux est validée par l'encadrement d'exploitation. En cas de
doute, on ferme et on attend.

**Communication :** Informer clairement les visiteurs des fermetures météorologiques,
sans dramatiser, en expliquant que la mesure vise leur sécurité.
"""  # noqa: E501

