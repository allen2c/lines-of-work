title = "Vérification des dispositifs de retenue"

content = """
Le contrôle des dispositifs de retenue est le geste de sécurité le plus critique de
l'opérateur. Il est réalisé pour chaque passager, à chaque cycle, sans exception.

**Types de retenue :** Barres de sécurité abaissables, harnais d'épaule, ceintures
ventrales, verrous secondaires. Chaque attraction possède son système et son mode de
vérification propre, indiqué par le constructeur.

**Méthode de contrôle :** Après abaissement, exercer une traction ou une pression ferme
pour confirmer le verrouillage. Vérifier qu'aucun vêtement, cheveu ou objet n'est coincé.
Contrôler la position du passager : dos calé, membres à l'intérieur.

**Cas des enfants :** Vérifier que la taille permet un maintien correct et que le
dispositif épouse le corps sans jeu excessif. En cas de doute sur le maintien, refuser
l'accès avec bienveillance.

**Voyants et redondance :** Les indicateurs électroniques complètent mais ne remplacent
jamais le contrôle manuel. Un voyant vert ne dispense pas de tester physiquement.

**Refus de départ :** Si une retenue ne se verrouille pas correctement, ne pas lancer le
cycle. Faire descendre le passager concerné, condamner la place et alerter la maintenance.

**Constance :** Ce contrôle se fait à l'identique du premier au dernier passager de la
journée, quelle que soit la longueur de la file. La fatigue ne justifie aucun relâchement.
"""  # noqa: E501

