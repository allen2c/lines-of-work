"""Slide and attraction safety rules for water park guests."""

title = "Zasady korzystania ze zjeżdżalni i atrakcji"

content = """
Każda zjeżdżalnia i atrakcja wodna w Parku Wodnym Brzeg Bałtyku posiada określone zasady
bezpieczeństwa wynikające z konstrukcji, przepływu wody i wymagań producenta. Goście muszą
zapoznać się z regulaminem przed wejściem na atrakcję.

**Wymagania wzrostowe:** Każda zjeżdżalnia ma minimalny wzrost uczestnika. Personel mierzy
dzieci przy wejściu. Goście poniżej wymaganego wzrostu nie mogą korzystać z atrakcji,
nawet w towarzystwie osoby dorosłej.

**Zakaz przedmiotów:** Na zjeżdżalnie nie wolno wnosić okularów (dozwolone tylko okulary
z paskiem), biżuterii, zegarków ani luźnych elementów ubioru. Goście przechowują je
w szafkach przed wejściem na atrakcję.

**Pozycja zjazdu:** Zjazd wyłącznie na plecach, nogami do przodu. Zakaz zjazdu na brzuchu,
głową w dół lub w pozycji stojącej. Nie wolno łączyć się w pary podczas zjazdu, chyba że
atrakcja wyraźnie na to zezwala.

**Odstępy:** Zachować bezpieczną odległość od poprzedniego zjeżdżającego. Wchodzić do
rynny dopiero po sygnale personelu. Nie blokować zjazdu przez zatrzymywanie się.

**Zakaz spożywania:** Przed wejściem na atrakcję nie spożywać alkoholu ani substancji
ograniczających zdolność oceny sytuacji. Personel ma prawo odmówić wstępu w przypadku
podejrzenia nietrzeźwości.

**Stan zdrowia:** Osoby z chorobami serca, kręgosłupa, w ciąży lub z otwartymi ranami
nie powinny korzystać z zjeżdżalni. W razie wątpliwości skonsultować się z personelem
medycznym parku.

**Zjeżdżalnie rodzinne:** Na wybranych atrakcjach dozwolony jest zjazd w pontonach
dwuoosobowych lub wieloosobowych. Dzieci muszą spełniać wymagania wzrostowe i siedzieć
między dorosłymi.

**Obsługa pontonów:** Personel pomaga w ustawieniu pontonu i wejściu do rynny. Goście
nie manipulują samodzielnie urządzeniami startowymi.
"""  # noqa: E501

