content = """
Secure cash handling protects revenue and staff. Procedures align with park policy and
financial controls.

**Till Management:** Designated float at shift start. Count and sign. No personal funds
in till. Breaks require till handover with count. End-of-day reconciliation and deposit.

**Transaction Limits:** Cash transactions above threshold require supervisor approval.
Large payments prefer card. Suspicious activity reported immediately. No acceptance of
damaged or counterfeit currency.

**Refund Authority:** Staff may process refunds within limit. Above limit requires
supervisor. Documentation required. Reason coded. Guest signature for cash refunds.

**Card Security:** PCI compliance for card data. No storage of full numbers. Contactless
preferred. Disputed charges escalated to finance.

**Incident Reporting:** Theft, shortage, or discrepancy reported immediately. Written
incident report. Investigation per policy. Insurance claims as applicable.
"""  # noqa: E501

