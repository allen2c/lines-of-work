title = "編目與登錄號規則"

content = """
- 登錄號格式採「年度.流水號.件號」三段式，如 2026.012.1 表示 2026 年第 12 案入藏的第 1 件；套件用連字號表範圍（.1-5）。
- 一物一號原則：每件文物一組唯一號，不重複、不跳號、不回收作廢號；作廢時於帳冊註記原因並保留號碼。
- 品名採「材質＋器形＋用途」描述式命名，如「木製漁具浮標」；年代不明者以「年代待考」註記，不臆測。
- 分類：依館藏架構分為文獻、器物、影像、標本、複製品等大類，再依主題（拓墾、產業、海港）細分。
- 標記：登錄號以可逆、不傷文物的方式標示（如隔離層加碳墨、或繫掛標籤），忌直接書寫於文物本體。
- 著錄欄位：登錄號、品名、類別、數量、尺寸、材質、年代、來源、狀況、庫位、影像檔名與著錄者。
- 校對：編目完成後由第二人複核號碼、品名與影像對應，確認無誤再鎖定紀錄，異動須留修改軌跡。
"""

