content = """
- 入藏來源：捐贈、購藏、採集、移撥或代管；均須經典藏審議通過並簽署入藏／捐贈同意書後受理。
- 點收：清點件數、核對清單與文物實體，確認品名、數量、附件（底座、囊匣、文件）是否齊全，當場拍照存證。
- 賦號：依《館藏分類編號原則》給予唯一登錄號，一物一號，套件另編子號（如 2026.005.1-3）。
- 登錄卡建檔：記錄登錄號、品名、類別、年代、材質、尺寸／重量、來源、入藏日期、暫置位置與權責人。
- 狀況檢查：完成入藏狀況檢查表與檢視照片，載明既有損傷、缺件與保存疑慮。
- 清潔與檢疫：新入藏品先於檢疫區隔離觀察（一般 2–4 週），確認無蟲害黴害後方可入庫上架。
- 系統建置：於典藏管理系統建立數位品項紀錄，連結影像與文件檔，完成後移交暫存或正式庫位並更新位置紀錄。
"""

