title = "Lost and Found Procedures"

content = """
Lost items create anxiety for visitors and administrative burden for staff.
Systematic lost and found procedures ensure items return to owners efficiently
while meeting legal and ethical obligations.

**Item Receipt:**

*Immediate Documentation:* When item turned in, record: date, time, location
found, description, and finder identity. Use standardized form or digital
system. Assign unique tracking number.

*Physical Handling:* Secure valuable items (phones, wallets, jewelry) in
locked drawer or safe. Other items stored in designated lost and found area.
Avoid commingling personal items with museum supplies.

*Description Detail:* Record specific identifying details: color, brand,
distinctive markings, contents of bags. Vague descriptions impede matching
with claims.

**Claim Processing:**

*Verification:* Before releasing item, require claimant to provide specific
description matching recorded details. Do not prompt with information;
require claimant to supply identifying characteristics.

*ID Verification:* For valuable items, require photo ID and contact
information. Document release with signature and date.

*Release Authorization:* Only visitor services supervisor or designated staff
may release items. Train all staff to direct claimants to authorized personnel.

**Retention Period:**

Items held minimum 30 days. After retention period, unclaimed items:

*Valuable Items:* Donated to charity or disposed of according to institutional
policy. Document disposal for audit trail.

*Personal Information:* Documents, ID cards, or items containing personal
data require secure destruction (shredding) rather than donation.

**Communication:**

*Social Media:* Post description of significant lost items on museum social
media platforms (without revealing details that would enable false claims).

*Inquiries:* When visitor inquires about lost item, check immediately rather
than deferring. Quick resolution demonstrates institutional care.

**Proactive Prevention:**

Remind groups to check belongings before departure. Announce lost and found
contact information during orientations. Post signage in restrooms and coat
check areas.
"""  # noqa: E501

