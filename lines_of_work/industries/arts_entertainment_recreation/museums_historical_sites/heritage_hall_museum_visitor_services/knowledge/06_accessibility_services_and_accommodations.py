title = "Accessibility Services and Visitor Accommodations"

content = """
Heritage Hall Museum commits to providing full access to all visitors regardless
of ability. Understanding available accommodations ensures effective service
delivery and dignified treatment of visitors with disabilities.

**Physical Accessibility:**

*Wheelchair Access:* All public areas accessible via elevator or ramp. Wheelchairs
available for loan at admission desk on first-come basis. Gallery pathways
maintain minimum 36-inch width throughout.

*Seating:* Benches and rest areas located in every gallery. Portable stools
available upon request for visitors who cannot stand for extended periods.

*Restrooms:* Accessible restrooms on every floor equipped with grab bars,
adequate turning radius, and accessible fixtures. Family restroom on first
floor accommodates companions and changing needs.

**Sensory Accommodations:**

*Visual Impairments:* Audio descriptions available for major exhibitions.
Tactile elements incorporated where conservation permits. Large-print guides
and braille signage at key locations.

*Hearing Impairments:* Written transcripts available for all audio components.
ASL interpretation available for tours with 48-hour advance request. Assisted
listening devices available at admission desk.

*Sensory Sensitivity:* Quiet hours offered first hour each Sunday with reduced
crowding and dimmed lighting. Sensory-friendly map identifies high-stimulation
areas. Noise-canceling headphones available for loan.

**Cognitive and Learning Accommodations:**

*Alternative Formats:* Exhibition content available in simplified language
versions and visual story guides. Multi-sensory learning stations support
diverse learning styles.

*Predictable Structure:* Social narratives available for visitors who benefit
from advance preparation. These describe what to expect during a museum visit.

**Service Animals:**

Trained service animals welcome throughout museum. Emotional support animals
not recognized under ADA; however, museum grants access on case-by-case basis
with advance notice and documentation.

**Program Accommodations:**

All public programs designed with accessibility as core requirement rather
than afterthought. Touch tours, verbal description tours, and sign-interpreted
events offered regularly. Private tours with accommodations available upon
request with advance booking.

**Communication Protocol:**

When interacting with visitors with disabilities, speak directly to the
individual rather than companions. Ask before assisting; respect refusals.
Use person-first language unless individual indicates preference otherwise.
"""  # noqa: E501

