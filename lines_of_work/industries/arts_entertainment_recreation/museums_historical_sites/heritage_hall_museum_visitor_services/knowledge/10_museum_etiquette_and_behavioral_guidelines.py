content = """
Maintaining appropriate visitor behavior protects collections and ensures
positive experiences for all guests. Visitor services staff must enforce
policies diplomatically while preserving welcoming atmosphere.

**Collection Protection Rules:**

*No Touching:* Visitors may not touch artifacts, artwork, or display cases.
Even clean hands leave oils and residues that damage objects over time. This
applies to all objects unless explicitly labeled as touchable.

*Appropriate Distance:* Maintain recommended viewing distances marked by floor
tape or barriers. Leaning on display cases risks artifact damage and personal
injury.

*Flash Photography:* Flash photography prohibited throughout museum. Flash
degrades sensitive materials and creates unsafe conditions for other visitors.
Non-flash photography permitted in most galleries for personal use.

**Visitor Conduct Standards:**

*Noise Levels:* Maintain conversational volume that respects other visitors'
experience. Audio devices require headphones. Phone calls should be taken
in lobby or outside.

*Food and Beverage:* No eating or drinking permitted in galleries. Covered
beverages allowed in lobby and cafe only. Water bottles permitted if kept
secured in bags.

*Running and Horseplay:* Children must remain under adult supervision.
Running, climbing on exhibits, or rough play endangers visitors and collections.

*Personal Belongings:* Large bags, backpacks, and umbrellas must be checked at
coat check or stored in lockers. These items risk accidental contact with
objects and block pathways.

**Enforcement Approach:**

Address violations with assumption of good intent. Most visitors who touch
objects or use flash photography simply don't know better. Approach with
friendly correction rather than accusation.

*First Approach:* "Welcome to Heritage Hall. Just a reminder that we ask visitors
not to touch the objects to help preserve them. Thank you for understanding."

*Second Approach:* If behavior continues, restate policy with slightly firmer
tone. "I need to ask that you not touch the artwork. This is an important rule
for protecting the collection."

*Third Approach:* If visitor refuses to comply, contact security. Document
interaction for incident report. Ejection is last resort but available for
flagrant disregard.

**Special Considerations:**

Children and visitors with developmental disabilities may have different
behavioral norms. Adjust expectations accordingly while maintaining safety
boundaries. Offer alternatives (touch tables, outdoor spaces) when appropriate.
"""  # noqa: E501

