content = """
Digital platforms extend museum reach and create opportunities for visitor
engagement beyond physical visits. Understanding digital strategy helps staff
support visitor expectations and institutional marketing goals.

**Platform Presence:**

*Instagram:* Visual platform emphasizing exhibition photography, behind-the-scenes
content, and visitor experiences. Primary audience: younger adults and art
enthusiasts. Daily posting schedule during active exhibitions.

*Facebook:* Broader demographic reach. Event promotion, community discussion,
and longer-form content. Primary channel for event marketing and older
demographic engagement.

*Twitter:* Real-time updates, cultural commentary, and professional networking.
Less visitor-focused; more industry and press engagement.

*TikTok:* Emerging platform for short-form educational content. Experimentation
phase with youth-oriented content and viral potential trends.

*Website:* Central information hub: hours, exhibitions, programming calendar,
ticket purchasing, and membership management. Mobile-responsive design
accommodates on-site usage.

**Visitor Services Integration:**

*Information Accuracy:* Stay current with website content to answer visitor
questions accurately. If website contains errors, report to Communications
Department immediately.

*Photo Policies:* Clarify photography rules for visitors wanting to capture
content for social media. Non-flash photography permitted in most galleries;
specific restrictions posted at exhibition entrances.

*Hashtag Promotion:* Encourage visitors to share experiences using official
museum hashtags. Display hashtags prominently on signage and admission materials.
Current official tag: #HeritageHallMuseum

*Geotagging:* Confirm museum location accuracy on mapping services. Report
incorrect information to management for correction.

**User-Generated Content:**

Visitor photos and reviews provide valuable social proof. Positive mentions
may be shared with permission. Negative reviews require monitoring and
professional response per Communications Department protocol.

Staff should never post about visitors without explicit consent. Respect
privacy expectations even in public spaces.

**Digital Accessibility:**

Website meets WCAG 2.1 AA standards for accessibility. Social media posts
should include alt text for images when possible. Video content includes
captions. Report accessibility barriers to IT and Communications teams.

**E-Ticketing and Digital Membership:**

Visitors increasingly present digital tickets on phones rather than printing.
Verify mobile tickets same as paper; scan barcodes or QR codes at admission.
Digital membership cards stored in Apple Wallet or Google Pay accepted.

**Technology Troubleshooting:**

Basic tech support falls within visitor services scope: connecting to WiFi,
accessing audio guides, or navigating website. Complex technical issues
escalate to IT Department.

Museum WiFi available free; password posted at admission desk and on signage.
"""  # noqa: E501

