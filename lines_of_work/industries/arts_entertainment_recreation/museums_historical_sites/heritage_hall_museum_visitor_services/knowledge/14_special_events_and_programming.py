title = "Special Events and Programming Support"

content = """
Special events extend museum reach beyond regular visitors and create memorable
experiences that generate word-of-mouth marketing. Visitor services staff play
critical role in event execution and guest experience.

**Event Types:**

*Exhibition Openings:* Celebrations marking debut of new exhibitions. Typically
include curator remarks, reception, and first access to galleries. Require
invitation management and capacity control.

*Lectures and Symposia:* Scholarly presentations on collection-related topics.
May feature visiting experts, authors, or artists. Require AV support,
seating management, and Q&A moderation.

*Family Days:* Themed events with hands-on activities, performances, and
interactive experiences. High energy, unpredictable attendance. Require
crowd management and child safety vigilance.

*Evening Events:* After-hours programs for adults including receptions,
concerts, or themed parties. May include alcohol service requiring age
verification and intoxication monitoring.

*Holiday Programming:* Special events tied to calendar observances. Popular
with families and casual visitors. Often highest attendance of year.

**Pre-Event Preparation:**

*Briefing:* Attend pre-event meeting reviewing schedule, capacity limits,
special requirements, and emergency protocols. Know who to contact for
various issues.

*Setup:* Assist with furniture arrangement, signage placement, and welcome
station preparation. Verify accessibility accommodations in place.

*Materials:* Prepare necessary supplies: name tags, programs, tickets,
giveaway items. Ensure sufficient quantities plus buffer.

**During Event Responsibilities:**

*Guest Services:* Greet arrivals, check invitations or tickets if required,
provide orientation to event layout and schedule.

*Wayfinding:* Direct attendees to registration, coat check, restrooms, and
program spaces. Monitor for confused or lost visitors.

*Capacity Monitoring:* Track attendance against room limits. Control entry
if approaching capacity per fire code. Coordinate with security for crowd
management.

*Issue Resolution:* Address spills, temperature complaints, or accessibility
needs immediately. Contact facilities or catering for technical problems.

**Post-Event:**

*Breakdown:* Assist with returning spaces to normal configuration. Collect
lost items and route to lost and found. Report any damage or supply needs.

*Feedback Collection:* Note observations about what worked and what didn't.
Visitor comments overheard during event provide valuable insights.
"""  # noqa: E501

