content = """
Baccarat attracts high-stakes players with its simple rules and low house edge.
Mini-baccarat offers the same game at lower limits with faster pace.

**Game Objective:**
Bet on which hand (Player or Banker) will have a point total closest to 9, or bet
on a Tie. Face cards and 10s count as 0; other cards at face value. Only the last
digit counts (15 = 5).

**Dealing Procedures:**

*Card Distribution:* Deal two cards each to Player and Banker positions, alternating
starting with Player. Cards are dealt face-down in big baccarat, face-up in mini-baccarat.

*Third Card Rules:* These are automatic, not player decisions.

*Player Hand:* Stands on 6 or 7. Draws on 0-5 (except when Banker has 8 or 9, which
are naturals).

*Banker Hand:* More complex rules based on Player's third card (if drawn):
- Banker 0-2: Always draws
- Banker 3: Draws unless Player drew 8
- Banker 4: Draws if Player drew 2-7
- Banker 5: Draws if Player drew 4-7
- Banker 6: Draws if Player drew 6-7
- Banker 7: Always stands
- Banker 8-9: Natural, both hands stand

**Payouts:**

*Player Bet:* 1:1 (even money). House edge approximately 1.24%.

*Banker Bet:* 1:1 minus 5% commission. House edge approximately 1.06% (best bet).

*Tie Bet:* 8:1 or 9:1 depending on house rules. House edge 14.4% (avoid).

**Commission Tracking:**
Mark commission owed on Banker wins using the commission box. Players may pay
commission periodically or at session end. Verify commission calculations carefully.
"""  # noqa: E501

