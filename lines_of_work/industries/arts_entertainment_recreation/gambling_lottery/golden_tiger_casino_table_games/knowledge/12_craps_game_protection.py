title = "Craps Game Protection and Dice Control"

content = """
Craps protection focuses on dice integrity, bet handling, and shooter mechanics in
the casino's most physically active table game.

**Dice Integrity:**

*Inspection:* New dice are examined for balance, sharp edges, and clear markings.
Marked or "loaded" dice (weighted to favor certain rolls) are the primary threat.

*Rotation:* Dice are regularly replaced during play. Suspect dice are immediately
retired and examined.

*Storage:* Dice remain in secure storage with controlled distribution to tables.
Unattended dice are a major security violation.

**Shooter Control:**

*Throw Requirements:* Both dice must hit the back wall and rebound. Short throws
(rolling short of the wall) are called "no roll." Die leaving the table requires
inspection or replacement.

*Switching:* Players attempting to substitute crooked dice for casino dice. Stickman
must maintain visual contact with dice at all times.

*Setting:* Players arranging dice to specific orientations before throwing. Not
cheating but may indicate dice control attempts. Allow setting within time limits.

**Bet Handling:**

*Booked Bets:* Verifying all bets are properly positioned before rolling. Unclear
bets must be clarified or returned.

*Call Bets:* In craps, many bets are announced rather than placed. Ensure call bets
are acknowledged and positioned correctly.

*Late Bets:* After "no more bets" or when shooter has dice, no new wagers. Strict
dealer enforcement required.

**Dispute Prevention:**

*Announcement:* Clear calls of all rolls, wins, and losses. Ambiguity leads to disputes.

*Payout Verification:* Verify all payouts with boxman, especially large wins.

*Dispute Resolution:* When players contest a roll or payout, consult boxman and
surveillance immediately. Never unilaterally reverse decisions.

**Controlled Shooting:**

While no proven method exists to consistently influence dice outcomes, players who
exhibit unusual throwing techniques or consistent winning streaks may draw scrutiny.
Monitor but don't harass unless clear cheating is observed.
"""  # noqa: E501

