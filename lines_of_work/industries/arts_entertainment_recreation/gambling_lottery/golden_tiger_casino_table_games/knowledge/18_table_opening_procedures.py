title = "Table Opening and Closing Procedures"

content = """
Proper table opening and closing ensures accurate inventory control, game integrity,
and regulatory compliance. These procedures are standardized across all table games.

**Opening Procedures:**

*Pre-Opening Checklist:*
- Verify table is clean and properly set up
- Check lighting and equipment functionality
- Ensure proper signage (limits, rules, side bet information)
- Confirm dealer bankroll from previous shift or cage

*Inventory Verification:*
- Count all chips in the table inventory
- Verify card decks (sealed, correct count, proper backs)
- Check dice (proper number, no damage, correct markings for craps)
- Verify shuffler operation if applicable
- Sign off on opening inventory sheet

*Game Setup:*
- Shuffle and prepare cards per game requirements
- Place cut cards properly
- Verify wheel balance and spin (roulette)
- Check layout for damage or marks

*Documentation:*
- Complete opening paperwork with floor supervisor
- Note any discrepancies immediately
- Obtain supervisor signature confirming accuracy

**Closing Procedures:**

*Final Play:*
- Announce "last call" for new players appropriately
- Complete all hands/spins in progress
- Do not begin new rounds near closing time

*Inventory Reconciliation:*
- Count all remaining chips
- Verify all cards accounted for (seal excess decks)
- Return dice to proper storage (craps)
- Complete closing inventory sheet

*Drop Procedures:*
- Secure all cash, chips, and paperwork in drop boxes
- Verify drop box contents with floor supervisor
- Sign documentation confirming accuracy

*Table Breakdown:*
- Secure or remove sensitive equipment
- Clean and prepare table for next shift
- Return to specified storage state

**Shift Changes:**

During active play, outgoing and incoming dealers must verify inventory together.
Never leave a table unattended during shift transition.
"""  # noqa: E501

