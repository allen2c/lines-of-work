title = "Incident Reporting and Documentation"

content = """
Proper incident documentation creates legal records, supports investigations, and
improves casino operations. All significant events require accurate written reports.

**When to Report:**

*Player Conduct:* Disputes, abusive behavior, suspected cheating, advantage play,
or intoxication incidents.

*Game Integrity:* Suspected equipment tampering, dealing errors affecting significant
money, or procedural violations.

*Security Events:* Thefts, altercations, medical emergencies, or unauthorized
persons in restricted areas.

*Regulatory Matters:* Suspected money laundering, underage gambling attempts, or
self-excluded player appearances.

**Report Components:**

*Date and Time:* Exact time of incident start and resolution.

*Location:* Specific table number, pit location, or area designation.

*Personnel:* All staff involved including dealers, supervisors, security, and
surveillance. Include badge numbers or identification.

*Subject Information:* Detailed description of involved players including name,
player's card number, physical description, and clothing. For unknown persons,
provide maximum identifying details.

*Incident Description:* Chronological account of what occurred. Be factual, objective,
and complete. Include direct quotes when relevant.

*Witnesses:* Names and contact information for any witnesses.

*Resolution:* How the incident was handled, final outcome, and any follow-up required.

*Signatures:* Reporting dealer, supervisor, and any reviewing managers.

**Writing Guidelines:**

*Objectivity:* Report facts, not opinions or assumptions. "Player raised voice"
not "player was angry."

*Completeness:* Include all relevant details. Incomplete reports create problems
later during investigations or legal proceedings.

*Timeliness:* Complete reports as soon as possible while memory is fresh. Delayed
reporting reduces accuracy and credibility.

*Clarity:* Use clear language. Avoid jargon unless necessary. If jargon is used,
ensure it's understood by non-gaming readers.

**Confidentiality:**

Incident reports contain sensitive information. Never discuss report contents with
unauthorized persons. Store reports securely per casino policy.
"""  # noqa: E501

