title = "Card Shuffling and Preparation Procedures"

content = """
Proper shuffling randomizes the deck and prevents predictability. Different games
require specific shuffling techniques to maintain game integrity.

**Manual Shuffling (for single/double deck games):**

*Riffle Shuffle:* Split deck into two halves, interleave cards by releasing from
thumbs. Minimum 3 riffles required per shuffle sequence.

*Boxing/Strip Shuffle:* Take small packets from the top and drop them onto the
deck. Randomizes card order and prevents clumping. Minimum 2 strips per sequence.

*Cut:* Split the deck and exchange halves. Place cut card approximately one deck from
bottom. Player may cut if offered; otherwise dealer cuts.

*Complete Sequence:* Standard manual shuffle requires: riffle, riffle, box, riffle,
cut. Some jurisdictions specify minimum shuffling requirements.

**Automatic Shufflers:**

*Continuous Shufflers (CSM):* Used in some blackjack games. Cards are continuously
reshuffled, eliminating card counting but increasing hands per hour significantly.

*Batch Shufflers:* Shuffle one or more decks as a unit. Dealer loads discards, machine
shuffles while current shoe plays, then swaps when current shoe ends.

*Operation:* Never open or attempt to repair shufflers. Report malfunctions
immediately. Follow specific loading procedures for your machine type.

**Shoe Games:**

*Loading:* Insert pre-shuffled decks into shoe. Verify seal on new decks before use.

*Cut Card Placement:* Insert cut card approximately one deck from the end of the
shoe. When cut card appears, complete current round and reshuffle.

*Burn Cards:* Some games require burning (discarding) top cards after shuffling.
Follow game-specific procedures.

**New Deck Procedures:**

Verify: correct number of cards, no visible defects, proper backs matching current
game. Fan new deck to show players cards are present before shuffling. Remove jokers
unless required for specific games (Pai Gow).

**Integrity Checks:**

Never perform false shuffles that maintain card order. Never expose shuffle
mechanics to players. Never shuffle in ways that could be predicted or influenced.
"""  # noqa: E501

