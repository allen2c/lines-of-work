title = "Game Protection for Card Games"

content = """
Game protection prevents cheating, ensures fair play, and protects casino assets.
Card games require specific vigilance against various advantage play techniques.

**Card Counting Detection:**

*Basic Principle:* Card counting tracks high/low card ratios to adjust bet sizes.
Not illegal but casinos may refuse service to counters.

*Indicators:* Dramatic bet spreading (minimum to maximum), playing deviations from
basic strategy correlating with count, head movement suggesting tracking, playing
only at positive counts, and team play signals.

*Response:* Notify floor supervisor when count is suspected. Supervisor monitors
and may instruct shuffle earlier, limit bet spread, or back-off the player.

**Marked Cards:**

*Detection:* Look for unusual wear patterns, bends, scratches, or marks that could
identify card values. Check for tiny pinpricks, edge sorting (manufacturing defect
exploitation), or luminous markings (requiring special glasses).

*Prevention:* Use high-quality cards, change decks regularly, inspect new decks,
and maintain controlled lighting to defeat luminous markings.

**Collusion:**

*Player-Player:* Multiple players sharing information about hands, using signals
to coordinate play, or chip dumping to transfer funds.

*Player-Dealer:* Dealer exposing hole cards, dealing seconds, or manipulating shuffles.
Extremely serious with immediate termination and criminal prosecution.

*Signals:* Watch for unusual hand gestures, chip placement patterns, or verbal
codes that might coordinate play.

**Mechanic Cheating:**

*Second Dealing:* Dealer delivers the second card instead of top card. Detect by
watching top card position and verifying it moves to expected location.

*Bottom Dealing:* Dealing from bottom of deck. Monitor deck grip and card delivery.

*False Shuffles:* Appearing to shuffle while maintaining card order. Verify shuffle
techniques are genuine and cards are randomized.
"""  # noqa: E501

