content = """
Standard terminology ensures clear communication among staff and with players.
Using correct terms projects professionalism and prevents misunderstandings.

**Common Table Games Terms:**

*Action:* Betting activity on a table. "Heavy action" means many bets or large bets.

*Bankroll:* Total funds a player has for gambling or the table's chip inventory.

*Boxman:* Craps supervisor seated between base dealers who oversees the game,
manages disputes, and guards the bank.

*Burn Card:* Card removed from play after shuffling or before dealing. Used in
some poker variants.

*Cage:* Casino cashier area where chips and cash are exchanged.

*Color-Up:* Exchanging lower denomination chips for higher denominations to
reduce chip volume.

*Cut:* Splitting the deck after shuffling. The cut card marks where play ends.

*Drop:* Total cash and chips collected from a table during a shift.

*Fill:* Adding chips to a table inventory from the cage.

*Flat Bet:* Betting the same amount consistently without varying bet size.

*Hold:* Percentage of buy-ins the casino retains as win.

*Marker:* Casino credit instrument; IOU signed by credit players.

*Pit:* Area containing multiple gaming tables with dedicated supervision.

*Rack:* Chip tray holding dealer working inventory.

*Shoe:* Device holding multiple decks for dealing.

*Stickman:* Craps dealer who handles dice with the stick and manages center bets.

*Toke:* Dealer tip (gratuity) from players.

*Tray:* Another term for chip rack; also the container holding dice in craps.

**Dealer Calls:**

Standard verbal announcements: "Card to player," "Dealer has seventeen," "Insurance
is open," "No more bets," "Paying line," "Taking the don't," "The point is eight."

**Radio/Phone Codes:**

Many casinos use coded language for security and efficiency: "Code Red" (fire),
"Code Blue" (medical), "Code Silver" (weapon), "21" (blackjack), etc. Know your
casino's specific codes.

**Player Communication:**

Explain terms to new players without condescension. "That's called a 'natural' —
an ace and ten-value card on your first two cards." Clear communication improves
player experience and reduces disputes.
"""  # noqa: E501

