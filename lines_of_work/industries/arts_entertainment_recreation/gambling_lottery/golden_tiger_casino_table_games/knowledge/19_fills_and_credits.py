title = "Fills and Credits Procedures"

content = """
Fills (adding chips to the table) and credits (removing excess chips) maintain
proper table inventory levels and ensure game continuity.

**When to Request Fills:**

*Low Denominations:* When common betting denominations run low, affecting game pace.
Typical thresholds: fewer than 20 $5 chips, fewer than 10 $25 chips.

*Excessive Breaking:* When dealers spend excessive time making change rather than
dealing, slowing the game.

*Expected High Traffic:* Before anticipated busy periods (weekend nights, special
events), proactively request additional inventory.

**When to Request Credits:**

*Excess Inventory:* When chip inventory exceeds operational needs, creating security
risk and inventory management burden.

*High Denominations:* Excessive high-value chips on the table increase theft risk
and may indicate insufficient play-to-inventory ratio.

*End of Shift:* Before closing or shift change, credits reduce counting burden and
improve accuracy.

**Fill/Credit Procedure:**

*Step 1 — Request:* Notify floor supervisor of need and requested amounts by
denomination. Supervisor approves or modifies request.

*Step 2 — Documentation:* Floor supervisor prepares fill/credit slip with table
number, date, time, amounts by denomination, and reason.

*Step 3 — Cage Coordination:* For fills, cage prepares requested chips. For credits,
cage prepares credit verification.

*Step 4 — Execution:*
- Security and floor supervisor transport fill/credit to/from table
- Dealer and supervisor count together, verifying amounts
- Dealer signs confirming receipt (fills) or surrender (credits)
- Supervisor signs as witness
- All parties retain copies per procedures

*Step 5 — Table Inventory Update:* Immediately update table inventory documentation
to reflect changes.

**Documentation Requirements:**

All fills and credits require: date, time, table number, dealer name, supervisor
name, security witness, amounts by denomination, signatures of all parties, and
reason for transaction. Never execute undocumented fills or credits.
"""  # noqa: E501

