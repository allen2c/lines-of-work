title = "Gaming Commission Regulations and Compliance"

content = """
Casino operations are heavily regulated. Understanding and adhering to gaming
commission requirements is essential for all table games personnel.

**Licensing Requirements:**

*Work Cards:* All table games employees must hold valid gaming licenses (work cards)
issued by the state gaming commission. Licenses require background checks and periodic
renewal.

*Display:* Work cards must be displayed visibly while on duty. Photocopies or
temporary documentation must be approved by gaming control.

*Reporting:* Changes in criminal history, address, or employment status must be
reported to the gaming commission within specified timeframes (usually 30 days).

**Game Integrity Requirements:**

*Game Rules:* Only approved games may be offered. Rule variations require commission
approval. Never modify game rules without written authorization.

*Equipment Standards:* Cards, dice, wheels, and shufflers must meet commission
specifications. Equipment modifications require approval.

*Minimum Staffing:* Tables must maintain minimum staffing as specified by game type.
Never operate understaffed games.

**Record Keeping:**

*Fill and Credit Slips:* All chip movements between cage and tables must be documented
with proper forms, signatures, and approvals.

*Incident Reports:* Significant incidents (disputes, suspected cheating, injuries)
require written documentation.

*Surveillance Requirements:* Gaming areas must maintain 100% coverage by surveillance
cameras with minimum retention periods (typically 7-30 days).

**Prohibited Activities:**

*Personal Gambling:* Gaming employees cannot gamble while on duty or in casino areas
where they work. Many jurisdictions prohibit casino employees from gambling anywhere
in the state.

*Tips/Gratuities:* Tipping policies vary by jurisdiction. Some prohibit all tipping;
others allow pooled tips. Know and follow local requirements.

*Information Disclosure:* Never share internal casino information with outside parties.
Never provide player information without proper authorization.

**Inspections and Investigations:**

Cooperate fully with gaming commission inspectors and investigators. Provide requested
documents and testimony. Never obstruct or mislead regulatory authorities.
"""  # noqa: E501

