content = """
Professional conduct at the gaming table maintains casino reputation, ensures
regulatory compliance, and creates positive guest experiences.

**Personal Presentation:**

*Uniform Standards:* Wear approved uniform in clean, pressed condition. Follow
specific grooming standards regarding hair, jewelry, and visible tattoos.

*Hygiene:* Maintain impeccable personal hygiene. Use breath fresheners regularly.
Casinos are close-quarters environments.

*Name Tags:* Wear name tags visibly as required. Some casinos use first names only
for security; others use full names or badge numbers.

**On-Duty Conduct:**

*Attentiveness:* Remain focused on the game. No eating, drinking, or personal phone
use while dealing. Never appear distracted or disinterested.

*Communication:* Use professional language at all times. No profanity, even if
players use it. Avoid controversial topics (politics, religion, personal problems).

*Player Interaction:* Be friendly but maintain professional boundaries. Don't share
personal information or ask overly personal questions.

**Cell Phone and Personal Electronics:**

Most casinos prohibit personal cell phones on the gaming floor. If permitted for
emergencies only, they must remain off and stored securely. Never photograph or
record on the gaming floor.

**Break and Meal Periods:**

*Punctuality:* Return from breaks exactly on time. Tables wait for no one; late
returns inconvenience colleagues and players.

*Break Areas:* Use designated break rooms, not gaming areas or player seating.

*Meal Cleanup:* Return from meals with clean uniform and hands. Wash hands after
eating and before resuming dealing.

**Off-Duty Conduct:**

*Gambling Restrictions:* Never gamble where you work. Many jurisdictions prohibit
casino employees from gambling anywhere in the state.

*Social Media:* Never post about specific casino operations, player information,
or internal procedures. Social media can violate privacy and confidentiality.

*Legal Compliance:* Gaming employees are held to higher legal standards. Criminal
conduct often results in license revocation and termination.

**Conflict of Interest:**

Never provide favors to friends or family. Never use position for personal gain.
Never accept gifts from players beyond standard tipping (where permitted).
"""  # noqa: E501

