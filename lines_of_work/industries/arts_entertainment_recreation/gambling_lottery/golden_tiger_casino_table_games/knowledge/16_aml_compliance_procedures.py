content = """
Table games are potential channels for money laundering. Strict AML procedures
protect the casino from regulatory violations and criminal exploitation.

**Currency Transaction Reports (CTRs):**

*Threshold:* Aggregate cash transactions exceeding $10,000 in a single gaming day
require CTR filing with FinCEN.

*Aggregation:* Track all transactions — buy-ins, chip exchanges, cash-outs, and
front money deposits — by the same person or entity within 24 hours.

*Identification:* Verify player identity for reportable transactions. Valid government
ID required: driver's license, passport, or state ID.

*Prohibition:* Never structure transactions to avoid reporting thresholds. This is
a federal crime. If a player asks to break a $10,000+ transaction into smaller
amounts, refuse and file a Suspicious Activity Report (SAR).

**Suspicious Activity Reports (SARs):**

*Triggers:* Unusual transaction patterns, attempts to avoid reporting, chip walking
(buying chips at one table, cashing at another without play), minimal play relative
to transaction volume, transactions inconsistent with stated income or occupation.

*Filing Timeline:* SARs must be filed within 30 days of identifying suspicious
activity. No delay permitted for internal investigation.

*Confidentiality:* Never inform the subject of SAR filing. Tipping off is a federal
crime.

**Red Flags:**

*Structuring:* Multiple transactions just under $10,000 by the same person.

*Minimal Play:* Buying large amounts of chips, placing minimal bets, then cashing out.

*Chip Walking:* Purchasing chips at one table/cage, immediately moving to cash out
elsewhere without meaningful play.

*Third-Party Transactions:* One person providing chips or cash to another without
clear relationship explanation.

*Inconsistent Behavior:* Transactions disproportionate to stated income or occupation.

**Dealer Responsibilities:**

Report all suspicious activity to the floor supervisor immediately. Complete
accurate documentation. Never confront suspected money launderers. Cooperate fully
with investigations.
"""  # noqa: E501

