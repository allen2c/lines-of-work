content = """
Understanding table layouts ensures proper game setup, accurate bet placement, and
clear communication with players about wagering options.

**Blackjack Layout:**

*Player Positions:* Typically 5-7 player spots (circles) across the table curve. Each
spot includes circles for main bets and optional side bet areas.

*Insurance Line:* Horizontal line above player positions marked "Insurance Pays 2 to 1."
Players place insurance bets on this line when dealer shows ace.

*Side Bet Areas:* Depending on game variants, may include Perfect Pairs, 21+3, or
other side bet locations. Know which side bets are active at your table.

*Dealer Area:* Includes chip rack, card discard tray, and dealing position. Layout
may display house rules: "Dealer Stands on Soft 17," "Blackjack Pays 3:2," etc.

**Roulette Layout:**

*Inside Bets:* Individual numbers 0/00 and 1-36 arranged in three columns. Additional
areas for splits, streets, corners, and six-lines.

*Outside Bets:* Even money bets (Red/Black, Odd/Even, 1-18/19-36) and 2:1 bets
(Dozens 1st/2nd/3rd, Columns).

*Wheel Position:* Physical wheel sits at one end of the table. Layout is mirror
image — numbers are arranged differently on layout versus wheel.

*Racetrack:* Some tables include oval racetrack for call bets (voisins, orphelins,
tiers). Dealer places these bets for players.

**Craps Layout:**

*Pass/Don't Pass:* Center line bet areas for the fundamental wagers.

*Come/Don't Come:* Similar to Pass line but for establishing points after come-out.

*Field:* Large area for one-roll field bets paying even or double on 2 and 12.

*Place Bets:* Numbered boxes (4, 5, 6, 8, 9, 10) for place and buy bets.

*Proposition Bets:* Center area for hardways, any craps, any seven, horn bets,
managed by stickman.

**Poker Table Layouts:**

*Community Card Games:* Designated areas for flop, turn, and river cards with
player betting positions around oval or octagonal table.

*Player vs. House Games:* Standardized betting circles for Ante, Play, and optional
side bets.
"""  # noqa: E501

