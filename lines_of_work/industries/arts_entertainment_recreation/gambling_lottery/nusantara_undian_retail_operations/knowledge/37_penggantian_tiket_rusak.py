"""Knowledge: Damaged ticket replacement procedure."""

title: str = "Penggantian Tiket Rusak"

content: str = """
Tiket yang rusak saat pembelian atau sebelum peserta menerima (cetakan blur, robek,
printer error) dapat diganti sesuai prosedur. Penggantian harus didokumentasikan.

**Saat pembelian.** Jika tiket rusak sebelum diserahkan, batalkan transaksi di sistem
dan terbitkan tiket baru. Jangan serahkan tiket yang tidak terbaca dengan jelas.

**Setelah pembelian.** Jika peserta mengembalikan tiket rusak segera setelah pembelian
dan tiket belum digosok (undian instan) atau hasil belum diumumkan, verifikasi
melalui sistem dan ikuti prosedur penggantian. Tiket lama dicap atau dimusnahkan.

**Hadiah.** Tiket rusak yang memuat hadiah memerlukan prosedur verifikasi khusus.
Rujuk ke atasan atau pusat klaim.
"""  # noqa: E501

