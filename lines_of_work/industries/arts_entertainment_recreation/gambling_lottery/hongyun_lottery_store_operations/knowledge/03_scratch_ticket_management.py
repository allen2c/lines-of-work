title = "刮刮樂品項與陳列管理"

content = """
- 刮刮樂屬立即型彩券，常見面額為新臺幣100元、200元、300元、500元、1,000元與2,000元不等，依檔期上市新款。
- 每本刮刮樂含固定張數（常見一本50張或100張），啟用前須以投注機或系統「激活（啟用）」該本，未激活之券不得販售與兌獎。
- 陳列原則：依面額分區、票號連續、正面朝外；高面額券置於顧客可見但店員可控範圍，避免失竊。
- 銷售時逐張撕取並確認券面完整、無刮開痕跡；已刮開或破損之券不得售出。
- 每日開店與交班需清點各本剩餘張數，與系統銷售紀錄核對，差異須即時查明。
- 一本售罄後於系統辦理「結本」，未售完之停售或回收檔期券依發行機構規定辦理退貨。
- 顧客兌領刮刮樂獎金時，先確認券別、期別未逾兌獎期限，再依金額判斷門市兌付或轉銀行兌領。
"""

