title = "兌獎期限與逾期處理"

content = """
- 電腦彩券與刮刮樂之兌獎期限為「自開獎日之次日起3個月內」，逾期未領視為放棄，不得兌付。
- 刮刮樂以該檔次公告之最後兌獎日為準，通常於停止銷售後仍保留一段兌獎期，須留意檔期公告。
- 對獎時系統對逾期彩券會顯示逾期或無法兌付，門市不得以任何理由通融兌付逾期券。
- 提醒顧客盡早對獎兌領，尤其連假、跨月期間避免因作業日錯算而逾期。
- 顧客持接近到期之彩券兌領時，優先協助確認期別與到期日，避免臨櫃時已逾期。
- 逾期未領獎金依規定繳回發行機構並用於公益用途，門市不得留存或私下處理。
- 對於顧客主張遺失或毀損而未能於期限內兌領者，說明規則並引導向發行機構查詢，不代為承諾補償。
"""

