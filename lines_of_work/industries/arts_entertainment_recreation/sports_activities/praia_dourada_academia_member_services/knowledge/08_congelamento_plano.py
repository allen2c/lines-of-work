title = "Congelamento de plano"

content = """
O congelamento permite ao sócio suspender temporariamente o plano sem perder a
condição de membro. É indicado para viagens, tratamentos de saúde, mudança temporária
ou outras situações que impeçam a frequência regular.

**Duração:** O congelamento pode ser solicitado por períodos de 15 a 90 dias, conforme
a política vigente. O número máximo de congelamentos por ano e a duração mínima e
máxima podem variar — consulte o regulamento atual.

**Solicitação:** Deve ser feita com antecedência mínima de 5 dias úteis antes do
início desejado. O sócio preenche formulário na recepção ou via aplicativo e
apresenta documentação quando exigida (por exemplo, atestado médico para períodos
longos).

**Taxa de congelamento:** Pode haver uma taxa administrativa única por período
congelado. O valor e as condições são informados no momento da solicitação.

**Retomada:** Ao final do período, o plano é reativado automaticamente. O sócio
recebe aviso por e-mail alguns dias antes. Se precisar estender o congelamento,
deve solicitar antes do vencimento do período atual.
"""  # noqa: E501

