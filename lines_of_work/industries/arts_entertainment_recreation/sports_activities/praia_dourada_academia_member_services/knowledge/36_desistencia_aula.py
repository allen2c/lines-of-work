"""Procedimentos para desistir de aula reservada."""

title = "Desistência de Aula Reservada"

content = """
A desistência de uma aula reservada deve ser feita com antecedência mínima
de 2 horas para liberar a vaga e evitar penalidades.

**Como desistir:** Pelo app (botão "Cancelar reserva") ou na recepção.
A desistência é confirmada por e-mail ou notificação.

**Sem penalidade:** Desistências com 2 ou mais horas de antecedência não
geram restrição. A vaga é oferecida à lista de espera.

**Com penalidade:** Desistências com menos de 2 horas ou ausência sem aviso
(no-show) geram restrição de reserva. Três no-shows em 30 dias resultam
em bloqueio de reservas por 30 dias.

**Exceções:** Casos de força maior (emergência médica, acidente) podem ser
analisados mediante documentação. Encaminhar ao Gerente de Atendimento.
"""  # noqa: E501

