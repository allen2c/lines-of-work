"""Normas de higiene nos vestiários."""

title = "Higiene nos Vestiários"

content = """
Os vestiários são espaços compartilhados e exigem cuidados de higiene de todos.
A Academia Praia Dourada mantém limpeza constante e espera colaboração dos sócios.

**Uso de toalhas:** Cada sócio deve trazer toalha pessoal. É proibido sentar
diretamente em bancos ou equipamentos sem proteção. Toalhas de rosto e corpo
podem ser alugadas na recepção em caso de esquecimento.

**Chuveiros:** Usar chinelos no chuveiro. Enxugar o piso após o uso para evitar
acidentes. Descartar adequadamente absorventes e materiais higiênicos.

**Armários:** Não deixar objetos de valor sem trancar. Retirar todos os pertences
ao final do uso. Armários são limpos diariamente; itens esquecidos vão para
o achados e perdidos.

**Ventilação:** Manter portas abertas quando indicado para circulação de ar.
Não usar perfumes em excesso em respeito a quem tem sensibilidade.

**Denúncias:** Problemas de limpeza ou mau uso devem ser reportados à recepção.
"""  # noqa: E501

