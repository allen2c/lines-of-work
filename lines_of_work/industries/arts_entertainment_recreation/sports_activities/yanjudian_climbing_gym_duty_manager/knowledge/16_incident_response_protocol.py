title = "意外事件處置五步驟"

content = """
- 事件處理口訣「停、看、護、報、錄」，適用墜落、擦撞、扭傷、暈眩、器材失效。
- 停：以哨音或口令停止傷者周邊 3 公尺內攀爬；若在上攀區，確保者穩住並緩降傷者。
- 看：評估意識、呼吸、外傷、疼痛部位、可否自主移動；疑頸椎或脊椎傷不移動傷者並固定頭頸。
- 護：施予對應急救（止血、冰敷、固定、保暖）；無反應無呼吸即 CPR 並取 AED。
- 報：依嚴重度通報，需送醫者撥 119 並派員到路口引導救護車。
- 錄：無論輕重，當班結束前填事件紀錄單，含時間、地點、當事人、經過、處置、證人、照片；器材失效須封存失效器材。
- 嚴重度分級：輕微（擦傷、輕扭傷）現場處置登錄；中度（需就醫但意識清楚）陪同就醫並電話通報總經理；重大（昏迷、大出血、頭部撞擊、無呼吸）立即 119、通報總經理與館主、封存現場。
- 事件後 24 小時內召開簡短檢討，找可預防因素並更新作業或警示標示。
"""

